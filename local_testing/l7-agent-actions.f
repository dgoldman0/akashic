\ =====================================================================
\  L7 Agent action and access-policy preservation contracts
\ =====================================================================
\  Loaded after tui/applets/agent/agent.f and
\  tui/applets/desk/agent-access-policy.f.  The fixture invokes the real
\  Agent controller actions against a controlled provider/runtime and reads
\  app-shell's visible toast state.  It also proves runtime-owned candidate
\  staging before an injected Desk access policy can replace a live profile.
\ =====================================================================

PROVIDED akashic-l7-agent-actions

VARIABLE _L7AA-FAILS
VARIABLE _L7AA-CHECKS
VARIABLE _L7AA-DEPTH

: _L7AA-ASSERT  ( flag -- )
    1 _L7AA-CHECKS +!
    0= IF 1 _L7AA-FAILS +! ." L7 AGENT ASSERT " _L7AA-CHECKS @ . CR THEN ;

: _L7AA-STACK  ( -- )
    DEPTH DUP _L7AA-DEPTH @ <> IF
        ." L7 AGENT STACK " _L7AA-DEPTH @ . ." -> " DUP . CR .S CR
    THEN
    _L7AA-DEPTH @ = _L7AA-ASSERT ;

: _L7AA-TOAST=  ( expected-a expected-u -- flag )
    _ASHELL-TOAST-MSG 2@ 2SWAP STR-STR= ;

CREATE _L7AA-AGENT-STATE _AG-STATE-SIZE ALLOT
CREATE _L7AA-PROVIDER AGENT-PROVIDER-SIZE ALLOT
CREATE _L7AA-SETTINGS AGENT-RUN-SETTINGS-SIZE ALLOT

VARIABLE _L7AA-RUNTIME
VARIABLE _L7AA-CONNECT-STATUS
VARIABLE _L7AA-REFRESH-STATUS
VARIABLE _L7AA-CONNECT-CALLS
VARIABLE _L7AA-REFRESH-CALLS

: _L7AA-CONNECT  ( queue context -- ior )
    2DROP 1 _L7AA-CONNECT-CALLS +! _L7AA-CONNECT-STATUS @ ;

: _L7AA-DISCONNECT  ( queue context -- ) 2DROP ;

: _L7AA-REFRESH  ( context -- status )
    DROP 1 _L7AA-REFRESH-CALLS +!
    _L7AA-REFRESH-STATUS @ DUP ARSET-S-PENDING = IF
        ARSET-STATE-LOADING _L7AA-SETTINGS ARSET.STATE !
    ELSE DUP ARSET-S-OK = IF
        ARSET-STATE-READY _L7AA-SETTINGS ARSET.STATE !
    ELSE
        ARSET-STATE-ERROR _L7AA-SETTINGS ARSET.STATE !
    THEN THEN ;

: _L7AA-PROVIDER-INIT  ( -- )
    _L7AA-PROVIDER APROV-INIT
    S" org.akashic.agent.testing.l7-actions"
        _L7AA-PROVIDER APROV.ID-U ! _L7AA-PROVIDER APROV.ID-A !
    ['] _L7AA-CONNECT _L7AA-PROVIDER APROV.CONNECT-XT !
    ['] _L7AA-DISCONNECT _L7AA-PROVIDER APROV.DISCONNECT-XT !
    _L7AA-SETTINGS ARSET-INIT
    _L7AA-SETTINGS _L7AA-SETTINGS ARSET.CONTEXT !
    ['] _L7AA-REFRESH _L7AA-SETTINGS ARSET.REFRESH-XT !
    ARSET-STATE-READY _L7AA-SETTINGS ARSET.STATE !
    _L7AA-SETTINGS _L7AA-PROVIDER APROV.RUN-SETTINGS ! ;

: _L7AA-UIDL-INIT  ( -- )
    S" <uidl><region id=agent-body/><label id=state text=Unset/></uidl>"
        UIDL-PARSE _L7AA-ASSERT
    S" agent-body" UIDL-BY-ID DUP 0<> _L7AA-ASSERT _AG-E-BODY !
    S" state" UIDL-BY-ID DUP 0<> _L7AA-ASSERT _AG-E-STATE ! ;

: _L7AA-STATE=  ( expected-a expected-u -- flag )
    _AG-E-STATE @ S" text" UIDL-ATTR 0= IF 2DROP 2DROP 0 EXIT THEN
    2SWAP STR-STR= ;

: _L7AA-RENDER-BEGIN  ( -- )
    _AG-E-BODY @ UIDL-CLEAN!
    _AG-E-STATE @ UIDL-CLEAN! ;

: _L7AA-RENDERED?  ( -- flag )
    _AG-E-BODY @ UIDL-DIRTY?
    _AG-E-STATE @ UIDL-DIRTY? AND ;

: _L7AA-ACTIONS  ( -- )
    _L7AA-AGENT-STATE _AG-STATE-SIZE 0 FILL
    _L7AA-AGENT-STATE _AG-CURRENT-STATE !
    _L7AA-UIDL-INIT
    _L7AA-PROVIDER-INIT
    0 _L7AA-CONNECT-STATUS !
    ARSET-S-OK _L7AA-REFRESH-STATUS !
    _L7AA-PROVIDER ARUNTIME-NEW
    DUP 0= _L7AA-ASSERT DROP DUP _L7AA-RUNTIME ! _AG-RUNTIME !
    0 _L7AA-CONNECT-CALLS ! 0 _L7AA-REFRESH-CALLS !

    _L7AA-RUNTIME @ ARUNTIME.REVISION @ >R
    _L7AA-RENDER-BEGIN
    0 _AG-DO-CLEAR
    S" Conversation cleared" _L7AA-TOAST= _L7AA-ASSERT
    S" Offline" _L7AA-STATE= _L7AA-ASSERT
    _L7AA-RENDERED? _L7AA-ASSERT
    _L7AA-RUNTIME @ ARUNTIME.REVISION @ R> 1+ = _L7AA-ASSERT

    7001 _L7AA-RUNTIME @ ARUNTIME.ACTIVE-RUN !
    ARUN-S-RUNNING _L7AA-RUNTIME @ ARUNTIME.STATUS !
    _L7AA-RUNTIME @ ARUNTIME.REVISION @ >R
    _L7AA-RENDER-BEGIN
    0 _AG-DO-CLEAR
    S" Finish or cancel the active run before clearing"
        _L7AA-TOAST= _L7AA-ASSERT
    S" Streaming" _L7AA-STATE= _L7AA-ASSERT
    _L7AA-RENDERED? _L7AA-ASSERT
    _L7AA-RUNTIME @ ARUNTIME.REVISION @ R> = _L7AA-ASSERT
    0 _L7AA-RUNTIME @ ARUNTIME.ACTIVE-RUN !
    ARUN-S-OFFLINE _L7AA-RUNTIME @ ARUNTIME.STATUS !

    0 _L7AA-CONNECT-STATUS !
    _L7AA-RENDER-BEGIN
    0 _AG-DO-RECONNECT
    S" Reconnect requested" _L7AA-TOAST= _L7AA-ASSERT
    S" Offline" _L7AA-STATE= _L7AA-ASSERT
    _L7AA-RENDERED? _L7AA-ASSERT
    _L7AA-CONNECT-CALLS @ 1 = _L7AA-ASSERT
    71 _L7AA-CONNECT-STATUS !
    _L7AA-RENDER-BEGIN
    0 _AG-DO-RECONNECT
    S" Agent reconnect failed" _L7AA-TOAST= _L7AA-ASSERT
    S" Error" _L7AA-STATE= _L7AA-ASSERT
    _L7AA-RENDERED? _L7AA-ASSERT
    _L7AA-CONNECT-CALLS @ 2 = _L7AA-ASSERT

    0 _L7AA-CONNECT-STATUS !
    ARSET-S-PENDING _L7AA-REFRESH-STATUS !
    _L7AA-RENDER-BEGIN
    0 _AG-DO-REFRESH-MODELS
    S" Model catalog refresh started" _L7AA-TOAST= _L7AA-ASSERT
    S" Loading models" _L7AA-STATE= _L7AA-ASSERT
    _L7AA-RENDERED? _L7AA-ASSERT
    _L7AA-REFRESH-CALLS @ 1 = _L7AA-ASSERT
    ARSET-S-TRANSPORT _L7AA-REFRESH-STATUS !
    _L7AA-RENDER-BEGIN
    0 _AG-DO-REFRESH-MODELS
    S" Model catalog refresh failed" _L7AA-TOAST= _L7AA-ASSERT
    S" Model catalog error" _L7AA-STATE= _L7AA-ASSERT
    _L7AA-RENDERED? _L7AA-ASSERT
    _L7AA-REFRESH-CALLS @ 2 = _L7AA-ASSERT
    _L7AA-STACK ;

VARIABLE _L7AA-SELECT-MODE
VARIABLE _L7AA-SELECT-PRESET
VARIABLE _L7AA-SELECT-PROFILE

: _L7AA-SELECT  ( preset profile data -- status )
    DROP _L7AA-SELECT-PROFILE ! _L7AA-SELECT-PRESET !
    _L7AA-SELECT-MODE @ CASE
        0 OF
            _L7AA-SELECT-PRESET @ _L7AA-SELECT-PROFILE @ DAP-PRESET!
        ENDOF
        1 OF
            _L7AA-SELECT-PROFILE @ AAP-INIT
            99 _L7AA-SELECT-PROFILE @ AAP.PRESET !
            AAP-S-INVALID
        ENDOF
        2 OF
            _L7AA-SELECT-PRESET @ _L7AA-SELECT-PROFILE @ DAP-PRESET!
            DUP IF EXIT THEN DROP
            S" forged.profile"
                _L7AA-SELECT-PROFILE @ AAP.ID-U !
                _L7AA-SELECT-PROFILE @ AAP.ID-A !
            AAP-S-OK
        ENDOF
        AAP-S-INVALID SWAP
    ENDCASE ;

: _L7AA-MANDATE-FACTORY  ( run-id data -- mandate-run status )
    2DROP 0 AMRUN-S-DENIED ;

VARIABLE _L7AA-REVISION

: _L7AA-LIVE-CHAT?  ( -- flag )
    _L7AA-RUNTIME @ ARUNTIME-ACCESS-PROFILE ?DUP IF
        DUP DAP-PROFILE-VALID? SWAP AAP.PRESET @ AAP-PRESET-CHAT-ONLY = AND
    ELSE
        0
    THEN ;

: _L7AA-POLICY  ( -- )
    1 _L7AA-RUNTIME @ ARUNTIME.TOOL-GATEWAY !
    ['] _L7AA-MANDATE-FACTORY 0 _L7AA-RUNTIME @
        ARUNTIME-MANDATE-FACTORY!
    ['] _L7AA-SELECT ['] DAP-RUNTIME-VALID? 0 _L7AA-RUNTIME @
        ARUNTIME-ACCESS-POLICY! AAP-S-OK = _L7AA-ASSERT

    0 _L7AA-SELECT-MODE !
    AAP-PRESET-CHAT-ONLY _L7AA-RUNTIME @ ARUNTIME-ACCESS-PRESET!
        AAP-S-OK = _L7AA-ASSERT
    _L7AA-LIVE-CHAT? _L7AA-ASSERT
    7 _L7AA-RUNTIME @ ARUNTIME.LAST-DISCLOSURE-ITEMS !
    99 _L7AA-RUNTIME @ ARUNTIME.LAST-DISCLOSURE-BYTES !
    _L7AA-RUNTIME @ ARUNTIME.REVISION @ _L7AA-REVISION !

    1 _L7AA-SELECT-MODE !
    AAP-PRESET-PRACTICE-READ _L7AA-RUNTIME @ ARUNTIME-ACCESS-PRESET!
        AAP-S-INVALID = _L7AA-ASSERT
    _L7AA-LIVE-CHAT? _L7AA-ASSERT
    _L7AA-RUNTIME @ ARUNTIME.REVISION @ _L7AA-REVISION @ = _L7AA-ASSERT
    _L7AA-RUNTIME @ ARUNTIME.LAST-DISCLOSURE-ITEMS @ 7 = _L7AA-ASSERT
    _L7AA-RUNTIME @ ARUNTIME.LAST-DISCLOSURE-BYTES @ 99 = _L7AA-ASSERT

    2 _L7AA-SELECT-MODE !
    AAP-PRESET-PRACTICE-ASSIST _L7AA-RUNTIME @ ARUNTIME-ACCESS-PRESET!
        AAP-S-INVALID = _L7AA-ASSERT
    _L7AA-LIVE-CHAT? _L7AA-ASSERT
    _L7AA-RUNTIME @ ARUNTIME.REVISION @ _L7AA-REVISION @ = _L7AA-ASSERT
    _L7AA-RUNTIME @ ARUNTIME.LAST-DISCLOSURE-ITEMS @ 7 = _L7AA-ASSERT
    _L7AA-RUNTIME @ ARUNTIME.LAST-DISCLOSURE-BYTES @ 99 = _L7AA-ASSERT

    \ A structurally valid edit to a stored desk.* profile cannot bypass the
    \ host's exact validator at runtime or at Desk mandate consumption.
    CAP-E-MUTATE _L7AA-RUNTIME @ ARUNTIME.ACCESS-PROFILE AAP.EFFECTS !
    _L7AA-RUNTIME @ ARUNTIME.ACCESS-PROFILE AAP-VALID? _L7AA-ASSERT
    _L7AA-RUNTIME @ ARUNTIME.ACCESS-PROFILE DAP-PROFILE-VALID? 0= _L7AA-ASSERT
    _L7AA-RUNTIME @ ARUNTIME-ACCESS-PROFILE 0= _L7AA-ASSERT

    0 _L7AA-RUNTIME @ ARUNTIME.TOOL-GATEWAY !
    _L7AA-STACK ;

: _L7AA-RUN  ( -- )
    0 _L7AA-FAILS ! 0 _L7AA-CHECKS ! DEPTH _L7AA-DEPTH !
    _L7AA-ACTIONS
    _L7AA-POLICY
    _L7AA-RUNTIME @ ARUNTIME-FREE
    0 _AG-RUNTIME ! 0 _AG-CURRENT-STATE ! 0 _L7AA-RUNTIME !
    _L7AA-STACK
    _L7AA-FAILS @ 0= IF
        ." L7 AGENT ACTIONS PASS " _L7AA-CHECKS @ .
    ELSE
        ." L7 AGENT ACTIONS FAIL " _L7AA-FAILS @ .
        ." / " _L7AA-CHECKS @ .
    THEN CR ;

_L7AA-RUN
