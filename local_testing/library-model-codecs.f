\ Independent resealing helpers let malformed semantic payloads retain
\ mechanically valid envelopes during negative tests.
: _LMC-FIXED-HCRC  ( record -- crc32 )
    CRC32-BEGIN
    DUP 48 CRC32-ADD
    DUP 56 + 8 CRC32-ADD
    DROP CRC32-END ;

VARIABLE _LMC-RS-REC
VARIABLE _LMC-RS-U

: _LMC-FIXED-RESEAL  ( record payload-u -- )
    _LMC-RS-U ! _LMC-RS-REC !
    _LMC-RS-REC @ LIB-FIXED-HEADER-SIZE + _LMC-RS-U @ CRC32
        _LMC-RS-REC @ 40 + !
    _LMC-RS-REC @ _LMC-FIXED-HCRC _LMC-RS-REC @ 48 + ! ;

: _LMC-CONTENT-HCRC  ( record -- crc32 )
    CRC32-BEGIN
    DUP 144 CRC32-ADD
    DUP 152 + 8 CRC32-ADD
    DROP CRC32-END ;

: _LMC-CONTENT-HRESEAL  ( record -- )
    DUP _LMC-CONTENT-HCRC SWAP 144 + ! ;

: _LMC-CONTENT-RESEAL  ( record -- )
    DUP _LMC-RS-REC ! 96 + @ _LMC-RS-U !
    _LMC-RS-REC @ LIB-CONTENT-HEADER-SIZE + _LMC-RS-U @ CRC32
        _LMC-RS-REC @ 136 + !
    _LMC-RS-REC @ LIB-CONTENT-HEADER-SIZE + _LMC-RS-U @
        _LMC-RS-REC @ 104 + SHA3-256-HASH
    _LMC-RS-REC @ _LMC-CONTENT-HRESEAL ;

