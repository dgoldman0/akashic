: _LMC-ASSERT  ( flag -- )
    1 _LMC-CHECKS +!
    0= IF 1 _LMC-FAILS +! ." LIBRARY MODEL CODECS ASSERT "
        _LMC-CHECKS @ . CR
    THEN ;

: _LMC-ZERO?  ( address length -- flag )
    0 ?DO DUP I + C@ IF DROP 0 UNLOOP EXIT THEN LOOP DROP -1 ;

: _LMC-FILLED?  ( address length byte -- flag )
    _LMC-BYTE ! 0 ?DO
        DUP I + C@ _LMC-BYTE @ <> IF DROP 0 UNLOOP EXIT THEN
    LOOP DROP -1 ;

: _LMC-ENTRY-VALID-STACK?  ( entry -- flag )
    DEPTH _LMC-DEPTH ! LIB-ENTRY-VALID? DROP
    DEPTH _LMC-DEPTH @ 1- = ;

