\ RAM-VFS contracts for neutral checked pages and their caller-owned cache.

PROVIDED akashic-persistence-page-contracts

VARIABLE _PSC-FAILS
VARIABLE _PSC-CHECKS
VARIABLE _PSC-DEPTH
VARIABLE _PSC-ARENA
VARIABLE _PSC-VFS
VARIABLE _PSC-IOR
VARIABLE _PSC-OLD-VFS
VARIABLE _PSC-STATS
VARIABLE _PSC-CACHE
VARIABLE _PSC-CACHE-MEMORY
VARIABLE _PSC-PAYLOAD
VARIABLE _PSC-FILE
VARIABLE _PSC-WORK
VARIABLE _PSC-OPS
VARIABLE _PSC-BINDING

: _PSC-ASSERT  ( flag -- )
    1 _PSC-CHECKS +!
    0= IF
        1 _PSC-FAILS +!
        ." PERSISTENCE PAGE ASSERT " _PSC-CHECKS @ . CR
    THEN ;

: _PSC-STACK  ( -- )
    DEPTH DUP _PSC-DEPTH @ <> IF
        ." PERSISTENCE PAGE STACK " _PSC-DEPTH @ . ."  -> " DUP . CR .S CR
    THEN
    _PSC-DEPTH @ = _PSC-ASSERT ;

: _PSC-ALLOC  ( bytes destination-cell -- )
    >R ALLOCATE ?DUP IF THROW THEN R> ! ;

: _PSC-THROW-WRITE  ( buffer length offset inode vfs -- actual ior )
    2DROP 2DROP DROP -910 THROW ;

: _PSC-THROW-SYNC  ( vfs -- ior )
    DROP -911 THROW ;

: _PSC-SETUP  ( -- )
    PERSIST-STATS-SIZE _PSC-STATS _PSC-ALLOC
    PERSIST-PAGE-CACHE-SIZE _PSC-CACHE _PSC-ALLOC
    PERSIST-PAGE-CACHE-FRAME-SIZE 2 * _PSC-CACHE-MEMORY _PSC-ALLOC
    PERSIST-PAGE-PAYLOAD-SIZE _PSC-PAYLOAD _PSC-ALLOC
    PERSIST-PAGE-FILE-SIZE _PSC-FILE _PSC-ALLOC
    PERSIST-PAGE-WORK-SIZE _PSC-WORK _PSC-ALLOC
    VFS-OPS-SIZE _PSC-OPS _PSC-ALLOC
    VFS-BINDING-DESC-SIZE _PSC-BINDING _PSC-ALLOC

    VFS-RAM-OPS _PSC-OPS @ VFS-OPS-SIZE MOVE
    VFS-RAM-BINDING _PSC-BINDING @ VFS-BINDING-DESC-SIZE MOVE
    _PSC-OPS @ _PSC-BINDING @ VB.OPS !
    1048576 A-XMEM ARENA-NEW
    DUP 0= _PSC-ASSERT DROP _PSC-ARENA !
    _PSC-ARENA @ _PSC-BINDING @ 0 VFS-NEW
    _PSC-IOR ! _PSC-VFS !
    _PSC-IOR @ 0= _PSC-ASSERT
    _PSC-VFS @ 0<> _PSC-ASSERT ;

: _PSC-CACHE-CONTRACTS  ( -- )
    2 PPAGE-CACHE-BYTES
        PERSIST-S-OK = _PSC-ASSERT
        PERSIST-PAGE-CACHE-FRAME-SIZE 2 * = _PSC-ASSERT
    _PSC-STACK
    _PSC-CACHE-MEMORY @ PERSIST-PAGE-CACHE-FRAME-SIZE 2 *
        2 _PSC-CACHE @ PPAGE-CACHE-INIT PERSIST-S-OK = _PSC-ASSERT
    _PSC-CACHE @ PPAGE-CACHE-VALID? _PSC-ASSERT
    _PSC-PAYLOAD @ PERSIST-PAGE-PAYLOAD-SIZE 90 FILL
    _PSC-PAYLOAD @ 7 _PSC-CACHE @ _PPAGE-CACHE-PUT
    7 _PSC-CACHE @ _PPAGE-CACHE-FIND DUP 0<> _PSC-ASSERT
        PERSIST-PAGE-PAYLOAD-SIZE
        _PSC-PAYLOAD @ PERSIST-PAGE-PAYLOAD-SIZE COMPARE 0= _PSC-ASSERT
    9 _PSC-CACHE @ _PPAGE-CACHE-FIND 0= _PSC-ASSERT
    _PSC-PAYLOAD @ 9 _PSC-CACHE @ _PPAGE-CACHE-PUT
    7 _PSC-CACHE @ _PPAGE-CACHE-FIND 0= _PSC-ASSERT
    9 _PSC-CACHE @ _PPAGE-CACHE-FIND 0<> _PSC-ASSERT
    _PSC-CACHE @ PPAGE-CACHE-INVALIDATE PERSIST-S-OK = _PSC-ASSERT
    9 _PSC-CACHE @ _PPAGE-CACHE-FIND 0= _PSC-ASSERT
    _PSC-STACK ;

: _PSC-PAGE-CONTRACTS  ( -- )
    _PSC-STATS @ PERSIST-STATS-INIT
    S" /pages.dat" _PSC-VFS @ _PSC-STATS @ _PSC-CACHE @ _PSC-FILE @
        PPAGE-FILE-INIT PERSIST-S-OK = _PSC-ASSERT
    _PSC-FILE @ PPAGE-FILE-VALID? _PSC-ASSERT
    _PSC-WORK @ PPAGE-WORK-INIT PERSIST-S-OK = _PSC-ASSERT
    _PSC-WORK @ PPAGE-PAYLOAD$ SWAP 0= SWAP 0= AND _PSC-ASSERT
    _PSC-WORK @ PPAGE-LAST-STATUS@ PERSIST-S-INVALID = _PSC-ASSERT
    _PSC-FILE @ _PSC-WORK @ PPAGE-ENSURE PERSIST-S-OK = _PSC-ASSERT
    _PSC-FILE @ _PSC-WORK @ PPAGE-FILE-SIZE?
        PERSIST-S-OK = _PSC-ASSERT 0= _PSC-ASSERT
    _PSC-STACK

    _PSC-PAYLOAD @ PERSIST-PAGE-PAYLOAD-SIZE 90 FILL
    0 _PSC-FILE @ _PSC-WORK @ PPAGE-TRUNCATE PERSIST-S-OK = _PSC-ASSERT
    _PSC-PAYLOAD @ PERSIST-PAGE-PAYLOAD-SIZE 0
        _PSC-FILE @ _PSC-WORK @ PPAGE-WRITE PERSIST-S-OK = _PSC-ASSERT
    0 1 _PSC-FILE @ _PSC-WORK @ PPAGE-VERIFY PERSIST-S-OK = _PSC-ASSERT
    _PSC-WORK @ PPAGE-PAYLOAD$
        _PSC-PAYLOAD @ PERSIST-PAGE-PAYLOAD-SIZE COMPARE 0= _PSC-ASSERT
    _PSC-PAYLOAD @ PERSIST-PAGE-PAYLOAD-SIZE 0
        _PSC-FILE @ _PSC-WORK @ PPAGE-WRITE
        PERSIST-S-CONFLICT = _PSC-ASSERT
    _PSC-STACK ;

: _PSC-THROW-CONTRACTS  ( -- )
    ['] _PSC-THROW-WRITE _PSC-OPS @ VFS-OP-WRITE CELLS + !
    0 VFS-USE
    _PSC-PAYLOAD @ PERSIST-PAGE-PAYLOAD-SIZE 1
        _PSC-FILE @ _PSC-WORK @ PPAGE-WRITE PERSIST-S-FAULT = _PSC-ASSERT
    _PSC-VFS @ V.OPEN-COUNT @ 0= _PSC-ASSERT
    _PSC-WORK @ _PPW.SCOPE VFA-SCOPE-FD@ 0= _PSC-ASSERT
    _PSC-WORK @ _PPW.BUSY @ 0= _PSC-ASSERT
    VFS-CUR 0= _PSC-ASSERT
    ['] _VFS-RAM-WRITE _PSC-OPS @ VFS-OP-WRITE CELLS + !
    _PSC-STACK

    _PSC-PAYLOAD @ PERSIST-PAGE-PAYLOAD-SIZE 1
        _PSC-FILE @ _PSC-WORK @ PPAGE-WRITE PERSIST-S-OK = _PSC-ASSERT
    1 _PSC-FILE @ _PSC-WORK @ PPAGE-TRUNCATE PERSIST-S-OK = _PSC-ASSERT
    _PSC-FILE @ _PSC-WORK @ PPAGE-FILE-SIZE?
        PERSIST-S-OK = _PSC-ASSERT
        PERSIST-PAGE-SIZE = _PSC-ASSERT
    1 2 _PSC-FILE @ _PSC-WORK @ PPAGE-READ
        PERSIST-S-CORRUPT = _PSC-ASSERT
    0 1 _PSC-FILE @ _PSC-WORK @ PPAGE-READ PERSIST-S-OK = _PSC-ASSERT

    ['] _PSC-THROW-SYNC _PSC-OPS @ VFS-OP-SYNCFS CELLS + !
    _PSC-FILE @ PPAGE-SYNC PERSIST-S-FAULT = _PSC-ASSERT
    ['] _VFS-RAM-SYNCFS _PSC-OPS @ VFS-OP-SYNCFS CELLS + !
    _PSC-FILE @ PPAGE-SYNC PERSIST-S-OK = _PSC-ASSERT
    _PSC-STACK ;

: _PSC-RUN  ( -- )
    0 _PSC-FAILS ! 0 _PSC-CHECKS ! DEPTH _PSC-DEPTH !
    VFS-CUR _PSC-OLD-VFS !
    _PSC-SETUP
    _PSC-CACHE-CONTRACTS
    _PSC-PAGE-CONTRACTS
    _PSC-THROW-CONTRACTS
    _PSC-OLD-VFS @ VFS-USE
    _PSC-VFS @ VFS-DESTROY
    _PSC-STACK
    _PSC-FAILS @ 0= IF
        ." PERSISTENCE PAGE PASS " _PSC-CHECKS @ . CR
    ELSE
        ." PERSISTENCE PAGE FAIL " _PSC-FAILS @ .
        ."  / " _PSC-CHECKS @ . CR
    THEN ;
