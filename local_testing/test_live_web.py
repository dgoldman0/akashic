def http_get(path: str, timeout: float = 5.0) -> tuple[int, dict, str]:
    """Send a raw HTTP GET to the emulator and return (status, headers, body)."""
    s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    s.settimeout(timeout)
    try:
        s.connect((EMU_IP, PORT))
        req = f"GET {path} HTTP/1.1\r\nHost: {EMU_IP}:{PORT}\r\nConnection: close\r\n\r\n"
        s.sendall(req.encode())

        # Read response
        chunks = []
        while True:
            try:
                data = s.recv(4096)
                if not data:
                    break
                chunks.append(data)
            except socket.timeout:
                break

        raw = b''.join(chunks)
        text = raw.decode('utf-8', errors='replace')

        # Parse status
        first_line = text.split('\r\n', 1)[0]
        status = int(first_line.split(' ', 2)[1]) if ' ' in first_line else 0

        # Parse headers + body
        if '\r\n\r\n' in text:
            hdr_part, body = text.split('\r\n\r\n', 1)
        else:
            hdr_part, body = text, ''

        headers = {}
        for line in hdr_part.split('\r\n')[1:]:
            if ':' in line:
                k, v = line.split(':', 1)
                headers[k.strip().lower()] = v.strip()

        return (status, headers, body)

    except Exception as e:
        return (0, {}, f"ERROR: {e}")
    finally:
        s.close()


