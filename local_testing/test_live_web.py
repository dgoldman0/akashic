def run_auto_tests():
    """Run automated HTTP tests against the live server."""
    # Give the server a moment to settle
    time.sleep(1.0)

    print("=" * 60)
    print("  Automated Integration Tests")
    print("=" * 60)

    passed = 0
    failed = 0
    tests = []

    def check(name, condition, detail=""):
        nonlocal passed, failed
        if condition:
            passed += 1
            tests.append(("PASS", name))
            print(f"  [PASS] {name}")
        else:
            failed += 1
            tests.append(("FAIL", name))
            print(f"  [FAIL] {name}{' — ' + detail if detail else ''}")

    # --- Test 1: GET / ---
    print("\n  Testing GET / ...")
    status, hdrs, body = http_get("/")
    check("GET / — status 200", status == 200, f"got {status}")
    check("GET / — has HTML",
          "<h1>" in body.lower() or "welcome" in body.lower(),
          f"body[:200]={body[:200]}")
    check("GET / — content-type",
          "text/html" in hdrs.get("content-type", ""),
          f"got {hdrs.get('content-type', 'missing')}")

    # --- Test 2: GET /about ---
    time.sleep(0.3)
    print("\n  Testing GET /about ...")
    status, hdrs, body = http_get("/about")
    check("GET /about — status 200", status == 200, f"got {status}")
    check("GET /about — has About",
          "about" in body.lower(),
          f"body[:200]={body[:200]}")

    # --- Test 3: GET /api/status ---
    time.sleep(0.3)
    print("\n  Testing GET /api/status ...")
    status, hdrs, body = http_get("/api/status")
    check("GET /api/status — status 200", status == 200, f"got {status}")
    check("GET /api/status — JSON body",
          '"status"' in body or '"ok"' in body,
          f"body={body[:200]}")
    check("GET /api/status — content-type json",
          "application/json" in hdrs.get("content-type", ""),
          f"got {hdrs.get('content-type', 'missing')}")

    # --- Test 4: POST /api/echo ---
    time.sleep(0.3)
    print("\n  Testing POST /api/echo ...")
    echo_body = '{"message":"hello from python"}'
    status, hdrs, body = http_post("/api/echo", echo_body)
    check("POST /api/echo — status 200", status == 200, f"got {status}")
    check("POST /api/echo — echoes body",
          "hello from python" in body,
          f"body={body[:200]}")

    # --- Test 5: GET /hello/:name ---
    time.sleep(0.3)
    print("\n  Testing GET /hello/World ...")
    status, hdrs, body = http_get("/hello/World")
    check("GET /hello/World — status 200", status == 200, f"got {status}")
    check("GET /hello/World — has greeting",
          "World" in body,
          f"body={body[:200]}")

    # --- Test 6: CORS headers ---
    time.sleep(0.3)
    print("\n  Testing CORS headers ...")
    status, hdrs, body = http_get("/api/status")
    has_cors = ("access-control-allow-origin" in hdrs
                or "access-control" in str(hdrs).lower())
    check("CORS headers present", has_cors,
          f"headers={list(hdrs.keys())}")

    # --- Test 7: 404 for unknown route ---
    time.sleep(0.3)
    print("\n  Testing GET /nonexistent ...")
    status, hdrs, body = http_get("/nonexistent")
    check("GET /nonexistent — status 404", status == 404, f"got {status}")

    # --- Summary ---
    print(f"\n{'=' * 60}")
    print(f"  Results: {passed} passed, {failed} failed, {passed + failed} total")
    print(f"{'=' * 60}\n")

    if failed > 0:
        print("  Failed tests:")
        for result, name in tests:
            if result == "FAIL":
                print(f"    - {name}")
        print()


