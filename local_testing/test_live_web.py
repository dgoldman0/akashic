def _forth_create_string(name: str, s: str) -> list[str]:
    """Generate Forth code: CREATE <name> <bytes...> and a -LEN variable."""
    # Build compact C, line (max ~80 chars per line)
    chunks = []
    cur = []
    for ch in s:
        cur.append(f"{ord(ch)} C,")
        if len(" ".join(cur)) > 70:
            chunks.append("  " + " ".join(cur))
            cur = []
    if cur:
        chunks.append("  " + " ".join(cur))
    return [f"CREATE {name}"] + chunks + [f"{len(s)} CONSTANT {name}-LEN"]


