    _ICB-CAP _ICB-CAP-BEFORE CAP-DESC CMOVE
    _ICB-CB CAPB-INIT CBUILD-S-OK = _ICB-ASSERT
    _ICB-CB CAPB-BEGIN CBUILD-S-OK = _ICB-ASSERT
    _ICB-CB CAPB-BEGIN CBUILD-S-BUSY = _ICB-ASSERT
