        CBUILD-S-SCHEMA = _ICB-ASSERT
    _ICB-SCB CSB-ABORT CBUILD-S-OK = _ICB-ASSERT ;

: _ICB-BUILD-NESTED  ( builder -- status )
    >R
    1 R@ CVB-BEGIN-MAP ?DUP IF R> DROP EXIT THEN
    S" outer" 0 R@ CVB-CANDIDATE R@ CVB-MAP-SLOT!
    ?DUP IF NIP R@ CVB-ABORT DROP R> DROP EXIT THEN
    DUP _ICB-CHILD !
    2 SWAP R@ CVB-LIST! ?DUP IF R@ CVB-ABORT DROP R> DROP EXIT THEN
    0 _ICB-CHILD @ R@ CVB-LIST-NTH
    ?DUP IF NIP R@ CVB-ABORT DROP R> DROP EXIT THEN
    S" alpha" ROT R@ CVB-STRING! ?DUP IF
        R@ CVB-ABORT DROP R> DROP EXIT
    THEN
    1 _ICB-CHILD @ R@ CVB-LIST-NTH
    ?DUP IF NIP R@ CVB-ABORT DROP R> DROP EXIT THEN
    S" beta" ROT R@ CVB-BYTES! ?DUP IF
        R@ CVB-ABORT DROP R> DROP EXIT
    THEN
    0 R@ CVB-SEAL
    R> DROP ;

: _ICB-VALUE-FAILURES  ( -- )
    XMEM? _ICB-ASSERT
    _ICB-VALUE-SENTINEL!
    _ICB-VB CVB-INIT CBUILD-S-OK = _ICB-ASSERT

    \ Root allocation failure latches NOMEM and never touches a destination.
    _ICB-FAULT-BEGIN
    1 _ICB-VB CVB-BEGIN-MAP CBUILD-S-NOMEM = _ICB-ASSERT
    _ICB-FAULT-END
    _ICB-VB CVB-STATUS@ CBUILD-S-NOMEM = _ICB-ASSERT
    1 _ICB-VB CVB-BEGIN-MAP CBUILD-S-BUSY = _ICB-ASSERT
    _ICB-VALUE-SENTINEL? _ICB-ASSERT
    _ICB-VB CVB-ABORT CBUILD-S-OK = _ICB-ASSERT

    \ Key allocation failure preserves the map and the first error is sticky.
    _ICB-AVAILABLE _ICB-AVAILABLE-BEFORE !
    1 _ICB-VB CVB-BEGIN-MAP CBUILD-S-OK = _ICB-ASSERT
    _ICB-FAULT-BEGIN
    S" key" 0 _ICB-VB CVB-CANDIDATE _ICB-VB CVB-MAP-SLOT!
    CBUILD-S-NOMEM = SWAP 0= AND _ICB-ASSERT
    _ICB-FAULT-END
    S" key" 0 _ICB-VB CVB-CANDIDATE _ICB-VB CVB-MAP-SLOT!
    CBUILD-S-NOMEM = SWAP 0= AND _ICB-ASSERT
    _ICB-VB CVB-ABORT CBUILD-S-OK = _ICB-ASSERT
    _ICB-AVAILABLE _ICB-AVAILABLE-BEFORE @ = _ICB-ASSERT

    \ Nested container allocation failure also rolls back recursively.
    _ICB-AVAILABLE _ICB-AVAILABLE-BEFORE !
    1 _ICB-VB CVB-BEGIN-MAP CBUILD-S-OK = _ICB-ASSERT
    S" nested" 0 _ICB-VB CVB-CANDIDATE _ICB-VB CVB-MAP-SLOT!
        _ICB-TAKE-CHILD _ICB-CHILD !
    _ICB-FAULT-BEGIN
    2 _ICB-CHILD @ _ICB-VB CVB-LIST! CBUILD-S-NOMEM = _ICB-ASSERT
    _ICB-FAULT-END
    _ICB-VB CVB-ABORT CBUILD-S-OK = _ICB-ASSERT
    _ICB-AVAILABLE _ICB-AVAILABLE-BEFORE @ = _ICB-ASSERT

    \ A failed deep seal and premature publication preserve destination bytes.
    1 _ICB-VB CVB-BEGIN-MAP CBUILD-S-OK = _ICB-ASSERT
    _ICB-VALUE _ICB-VB CVB-PUBLISH CBUILD-S-INVALID = _ICB-ASSERT
    _ICB-VALUE-SENTINEL? _ICB-ASSERT
    S" count" 0 _ICB-VB CVB-CANDIDATE _ICB-VB CVB-MAP-SLOT!
        _ICB-TAKE-CHILD _ICB-CHILD !
    S" wrong type" _ICB-CHILD @ _ICB-VB CVB-STRING!
        CBUILD-S-OK = _ICB-ASSERT
    _ICB-MAP-SCHEMA _ICB-VB CVB-SEAL CBUILD-S-VALUE = _ICB-ASSERT
    _ICB-VALUE-SENTINEL? _ICB-ASSERT
    _ICB-VB CVB-ABORT CBUILD-S-OK = _ICB-ASSERT

    \ Recursive abort restores every root, key, list, string, and byte block.
    _ICB-AVAILABLE _ICB-AVAILABLE-BEFORE !
    _ICB-VB _ICB-BUILD-NESTED CBUILD-S-OK = _ICB-ASSERT
    _ICB-VB CVB-ABORT CBUILD-S-OK = _ICB-ASSERT
    _ICB-AVAILABLE _ICB-AVAILABLE-BEFORE @ = _ICB-ASSERT ;

: _ICB-PUBLICATION-AND-INDEPENDENCE  ( -- )
    _ICB-VALUE CV-FREE _ICB-VALUE2 CV-INIT
    _ICB-VB CVB-INIT CBUILD-S-OK = _ICB-ASSERT
    _ICB-VB2 CVB-INIT CBUILD-S-OK = _ICB-ASSERT
    1 _ICB-VB CVB-BEGIN-LIST CBUILD-S-OK = _ICB-ASSERT
    1 _ICB-VB2 CVB-BEGIN-LIST CBUILD-S-OK = _ICB-ASSERT
    0 _ICB-VB CVB-CANDIDATE _ICB-VB CVB-LIST-NTH
        _ICB-TAKE-CHILD _ICB-CHILD !
    0 _ICB-VB2 CVB-CANDIDATE _ICB-VB2 CVB-LIST-NTH
        _ICB-TAKE-CHILD _ICB-CHILD2 !
    11 _ICB-CHILD @ _ICB-VB CVB-INT! CBUILD-S-OK = _ICB-ASSERT
    22 _ICB-CHILD2 @ _ICB-VB2 CVB-INT! CBUILD-S-OK = _ICB-ASSERT
    0 _ICB-VB CVB-SEAL CBUILD-S-OK = _ICB-ASSERT
    0 _ICB-VB2 CVB-SEAL CBUILD-S-OK = _ICB-ASSERT
    _ICB-VB CVB-CANDIDATE CV-DATA@ _ICB-OWNED-DATA !
    _ICB-VALUE _ICB-VB CVB-PUBLISH CBUILD-S-OK = _ICB-ASSERT
    _ICB-VALUE CV-DATA@ _ICB-OWNED-DATA @ = _ICB-ASSERT
    _ICB-VB CVB-CANDIDATE 0= _ICB-ASSERT
    _ICB-VALUE2 _ICB-VB2 CVB-PUBLISH CBUILD-S-OK = _ICB-ASSERT
    0 _ICB-VALUE CV-LIST-NTH CV-DATA@ 11 = _ICB-ASSERT
    0 _ICB-VALUE2 CV-LIST-NTH CV-DATA@ 22 = _ICB-ASSERT

    \ A destination inside the candidate is refused without freeing the tree.
    1 _ICB-VB CVB-BEGIN-LIST CBUILD-S-OK = _ICB-ASSERT
    0 _ICB-VB CVB-CANDIDATE _ICB-VB CVB-LIST-NTH
        _ICB-TAKE-CHILD _ICB-CHILD !
    33 _ICB-CHILD @ _ICB-VB CVB-INT! CBUILD-S-OK = _ICB-ASSERT
    0 _ICB-VB CVB-SEAL CBUILD-S-OK = _ICB-ASSERT
    _ICB-CHILD @ _ICB-VB CVB-PUBLISH CBUILD-S-INVALID = _ICB-ASSERT
    _ICB-VB CVB-CANDIDATE CV-TYPE@ CV-T-LIST = _ICB-ASSERT
    _ICB-VB CVB-ABORT CBUILD-S-OK = _ICB-ASSERT
    _ICB-VALUE CV-FREE _ICB-VALUE2 CV-FREE ;

