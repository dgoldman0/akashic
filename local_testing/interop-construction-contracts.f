    CV-T-INT _ICB-INT-SCHEMA CS-ALLOW!
    _ICB-TEXT-SCHEMA CS-INIT
    CV-T-STRING _ICB-TEXT-SCHEMA CS-ALLOW!

    _ICB-SCB CSB-INIT CBUILD-S-OK = _ICB-ASSERT
    _ICB-MAP-FIELDS 2 _ICB-SCB CSB-BEGIN
        CBUILD-S-OK = _ICB-ASSERT
    S" count" _ICB-INT-SCHEMA _ICB-SCB CSB-REQUIRED
