: _ICB-RUN  ( -- )
    0 _ICB-FAILS ! 0 _ICB-CHECKS ! DEPTH _ICB-DEPTH !
    ." [akashic] construction schema phase" CR
    _ICB-SCHEMAS _ICB-STACK
    ." [akashic] construction value phase" CR
    _ICB-VALUES _ICB-STACK
    ." [akashic] construction capability phase" CR
    _ICB-CAPS _ICB-STACK
    ." [akashic] construction failure phase" CR
    _ICB-SCHEMA-FAILURES _ICB-STACK
    _ICB-VALUE-FAILURES _ICB-STACK
    _ICB-PUBLICATION-AND-INDEPENDENCE _ICB-STACK
    _ICB-CAPABILITY-FAILURES _ICB-STACK
    _ICB-VALUE CV-FREE
    _ICB-STACK
    _ICB-FAILS @ 0= IF
        ." INTEROP CONSTRUCTION CONTRACTS PASS " _ICB-CHECKS @ . CR
    ELSE
        ." INTEROP CONSTRUCTION CONTRACTS FAIL " _ICB-FAILS @ .
        ."  / " _ICB-CHECKS @ . CR
    THEN ;

." [akashic] running interoperability construction fixture" CR
_ICB-RUN
