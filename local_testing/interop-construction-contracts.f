VARIABLE _ICB-FAILS
VARIABLE _ICB-CHECKS
VARIABLE _ICB-DEPTH

: _ICB-ASSERT  ( flag -- )
    1 _ICB-CHECKS +!
    0= IF
        1 _ICB-FAILS +!
        ." INTEROP CONSTRUCTION ASSERT " _ICB-CHECKS @ . CR
    THEN ;

: _ICB-STACK  ( -- )
    DEPTH DUP _ICB-DEPTH @ <> IF
        ." INTEROP CONSTRUCTION STACK " _ICB-DEPTH @ . ."  -> " DUP . CR
        .S CR
    THEN
    _ICB-DEPTH @ = _ICB-ASSERT ;

