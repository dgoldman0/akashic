        CBUILD-S-OK = _ICB-ASSERT
    _ICB-MAP-SCHEMA CS-SCHEMA-VALIDATE 0= _ICB-ASSERT ;

: _ICB-TAKE-CHILD  ( child status -- child )
    CBUILD-S-OK = _ICB-ASSERT
    DUP 0<> _ICB-ASSERT ;

