: _ICB-AVAILABLE  ( -- bytes )
    XMEM? IF
        XMEM-FREE XMEM-FL @
        BEGIN DUP WHILE
            DUP @ ROT + SWAP 8 + @
        REPEAT
        DROP
    ELSE
        HEAP-FREE-BYTES
    THEN ;

: _ICB-FAULT-BEGIN  ( -- )
    XMEM-LIMIT @ _ICB-XMEM-LIMIT !
    XMEM-FL @ _ICB-XMEM-FL !
    0 XMEM-FL !
    XMEM-HERE @ XMEM-LIMIT ! ;

: _ICB-FAULT-END  ( -- )
    _ICB-XMEM-LIMIT @ XMEM-LIMIT !
    _ICB-XMEM-FL @ XMEM-FL ! ;

: _ICB-VALUE-SENTINEL!  ( -- )
    _ICB-VALUE CV-FREE
    S" destination sentinel" _ICB-VALUE CV-STRING! 0= _ICB-ASSERT ;

: _ICB-VALUE-SENTINEL?  ( -- flag )
    _ICB-VALUE DUP CV-TYPE@ CV-T-STRING = >R
    DUP CV-DATA@ SWAP CV-LEN@
    S" destination sentinel" STR-STR= R> AND ;

: _ICB-ZERO?  ( address length -- flag )
    0 ?DO
        DUP I + C@ IF DROP 0 UNLOOP EXIT THEN
    LOOP
    DROP -1 ;

