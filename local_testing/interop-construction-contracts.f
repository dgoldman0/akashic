    _ICB-VB CVB-INIT CBUILD-S-OK = _ICB-ASSERT
    2 _ICB-VB CVB-BEGIN-MAP CBUILD-S-OK = _ICB-ASSERT
    S" count" 0 _ICB-VB CVB-CANDIDATE _ICB-VB CVB-MAP-SLOT!
        _ICB-TAKE-CHILD _ICB-CHILD !
    S" count" 0 _ICB-VB CVB-CANDIDATE _ICB-VB CVB-MAP-SLOT!
        _ICB-TAKE-CHILD _ICB-CHILD2 !
    _ICB-CHILD2 @ _ICB-CHILD @ = _ICB-ASSERT
    7 _ICB-CHILD @ _ICB-VB CVB-INT! CBUILD-S-OK = _ICB-ASSERT
    S" label" 1 _ICB-VB CVB-CANDIDATE _ICB-VB CVB-MAP-SLOT!
        _ICB-TAKE-CHILD _ICB-CHILD !
    S" seven" _ICB-CHILD @ _ICB-VB CVB-STRING!
        CBUILD-S-OK = _ICB-ASSERT
    _ICB-MAP-SCHEMA _ICB-VB CVB-SEAL CBUILD-S-OK = _ICB-ASSERT
    _ICB-VALUE _ICB-VB CVB-PUBLISH CBUILD-S-OK = _ICB-ASSERT
