\ Deterministic Gate 2A contracts for policy-neutral span mechanics and
\ caller-owned scalar/locator schema initializers.

PROVIDED akashic-gate2a-contracts

VARIABLE _G2A-FAILS
VARIABLE _G2A-CHECKS
VARIABLE _G2A-DEPTH

: _G2A-ASSERT  ( flag -- )
    1 _G2A-CHECKS +!
    0= IF 1 _G2A-FAILS +! ." GATE2A ASSERT " _G2A-CHECKS @ . CR THEN ;

: _G2A-STACK  ( -- )
    DEPTH DUP _G2A-DEPTH @ <> IF
        ." GATE2A STACK " _G2A-DEPTH @ . ." -> " DUP . CR .S CR
    THEN
    _G2A-DEPTH @ = _G2A-ASSERT ;

CREATE _G2A-SCHEMA CS-SIZE ALLOT
CREATE _G2A-EXPECTED CS-SIZE ALLOT
CREATE _G2A-OTHER CS-SIZE ALLOT
CREATE _G2A-VALUE CV-SIZE ALLOT
CREATE _G2A-VALUE2 CV-SIZE ALLOT
CREATE _G2A-REF RREF-SIZE ALLOT
CREATE _G2A-TEXT 517 ALLOT
CREATE _G2A-BAD-UTF8 1 ALLOT

: _G2A-SCHEMA=  ( -- flag )
    _G2A-SCHEMA CS-SIZE _G2A-EXPECTED CS-SIZE STR-STR= ;

: _G2A-SPAN-CONTRACTS  ( -- )
    1000 16 MSPAN-NONWRAPPING? _G2A-ASSERT
    1000 0 MSPAN-NONWRAPPING? _G2A-ASSERT
    0 0 MSPAN-NONWRAPPING? _G2A-ASSERT
    0 1 MSPAN-NONWRAPPING? _G2A-ASSERT
    1000 -1 MSPAN-NONWRAPPING? 0= _G2A-ASSERT
    -1 0 MSPAN-NONWRAPPING? _G2A-ASSERT
    -8 7 MSPAN-NONWRAPPING? _G2A-ASSERT
    -8 8 MSPAN-NONWRAPPING? 0= _G2A-ASSERT

    1000 10 1010 5 MSPAN-OVERLAP? 0= _G2A-ASSERT
    1010 5 1000 10 MSPAN-OVERLAP? 0= _G2A-ASSERT
    1000 11 1010 5 MSPAN-OVERLAP? _G2A-ASSERT
    1010 5 1000 11 MSPAN-OVERLAP? _G2A-ASSERT
    1000 20 1005 5 MSPAN-OVERLAP? _G2A-ASSERT
    1000 20 1000 20 MSPAN-OVERLAP? _G2A-ASSERT
    1000 0 1000 1 MSPAN-OVERLAP? 0= _G2A-ASSERT
    0 4 2 2 MSPAN-OVERLAP? _G2A-ASSERT
    -8 8 -4 1 MSPAN-OVERLAP? 0= _G2A-ASSERT
    -16 8 -12 4 MSPAN-OVERLAP? _G2A-ASSERT
    _G2A-STACK ;

: _G2A-SCHEMA-BYTE-CONTRACTS  ( -- )
    _G2A-SCHEMA CSC-NULL!
    _G2A-EXPECTED CS-INIT CV-T-NULL _G2A-EXPECTED CS-ALLOW!
    _G2A-SCHEMA= _G2A-ASSERT

    _G2A-SCHEMA CSC-BOOL!
    _G2A-EXPECTED CS-INIT CV-T-BOOL _G2A-EXPECTED CS-ALLOW!
    _G2A-SCHEMA= _G2A-ASSERT

    _G2A-SCHEMA CSC-NONNEG-INT!
    _G2A-EXPECTED CS-INIT CV-T-INT _G2A-EXPECTED CS-ALLOW!
    0 _G2A-EXPECTED CS-MIN!
    _G2A-SCHEMA= _G2A-ASSERT

    _G2A-SCHEMA CSC-POSITIVE-REVISION!
    _G2A-EXPECTED CS-INIT CV-T-INT _G2A-EXPECTED CS-ALLOW!
    1 _G2A-EXPECTED CS-MIN!
    _G2A-SCHEMA= _G2A-ASSERT

    7 _G2A-SCHEMA CSC-UTF8!
    _G2A-EXPECTED CS-INIT CV-T-STRING _G2A-EXPECTED CS-ALLOW!
    7 _G2A-EXPECTED CS-MAX-LEN!
    _G2A-SCHEMA= _G2A-ASSERT

    _G2A-SCHEMA CSC-SEMANTIC-RREF!
    _G2A-EXPECTED CS-INIT CV-T-RESOURCE _G2A-EXPECTED CS-ALLOW!
    IRES-RREF-URI-MAX _G2A-EXPECTED CS-MAX-LEN!
    _G2A-SCHEMA= _G2A-ASSERT

    _G2A-SCHEMA CSC-OPTIONAL-SEMANTIC-RREF!
    _G2A-EXPECTED CS-INIT
    CV-T-NULL CS-TYPE-BIT CV-T-RESOURCE CS-TYPE-BIT OR
        _G2A-EXPECTED CS-ALLOW-MASK!
    IRES-RREF-URI-MAX _G2A-EXPECTED CS-MAX-LEN!
    _G2A-SCHEMA= _G2A-ASSERT

    _G2A-SCHEMA CSC-RREF-TEXT!
    _G2A-EXPECTED CS-INIT CV-T-STRING _G2A-EXPECTED CS-ALLOW!
    IRES-RREF-URI-MAX _G2A-EXPECTED CS-MAX-LEN!
    _G2A-SCHEMA= _G2A-ASSERT

    _G2A-SCHEMA CSC-VFS-LOCATOR-TEXT!
    _G2A-EXPECTED CS-INIT CV-T-STRING _G2A-EXPECTED CS-ALLOW!
    516 _G2A-EXPECTED CS-MAX-LEN!
    _G2A-SCHEMA= _G2A-ASSERT
    CSC-RREF-TEXT-MAX IRES-RREF-URI-MAX = _G2A-ASSERT
    CSC-VFS-LOCATOR-TEXT-MAX 516 = _G2A-ASSERT

    \ Initializing another caller's allocation must not mutate the first.
    _G2A-OTHER CSC-BOOL!
    _G2A-SCHEMA CSC-NULL!
    _G2A-SCHEMA CS.TYPE-MASK @ CV-T-NULL CS-TYPE-BIT = _G2A-ASSERT
    _G2A-OTHER CS.TYPE-MASK @ CV-T-BOOL CS-TYPE-BIT = _G2A-ASSERT
    _G2A-STACK ;

: _G2A-SCALAR-VALUE-CONTRACTS  ( -- )
    _G2A-SCHEMA CSC-NULL!
    _G2A-VALUE CV-NULL!
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE 0= _G2A-ASSERT
    -1 _G2A-VALUE CV-BOOL!
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE CS-E-TYPE = _G2A-ASSERT

    _G2A-SCHEMA CSC-BOOL!
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE 0= _G2A-ASSERT
    0 _G2A-VALUE CV-INT!
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE CS-E-TYPE = _G2A-ASSERT

    _G2A-SCHEMA CSC-NONNEG-INT!
    0 _G2A-VALUE CV-INT!
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE 0= _G2A-ASSERT
    -1 _G2A-VALUE CV-INT!
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE CS-E-MIN = _G2A-ASSERT

    _G2A-SCHEMA CSC-POSITIVE-REVISION!
    1 _G2A-VALUE CV-INT!
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE 0= _G2A-ASSERT
    0 _G2A-VALUE CV-INT!
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE CS-E-MIN = _G2A-ASSERT

    _G2A-TEXT 517 [CHAR] x FILL
    4 _G2A-SCHEMA CSC-UTF8!
    _G2A-TEXT 4 _G2A-VALUE CV-STRING! 0= _G2A-ASSERT
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE 0= _G2A-ASSERT
    _G2A-TEXT 5 _G2A-VALUE CV-STRING! 0= _G2A-ASSERT
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE CS-E-LENGTH = _G2A-ASSERT
    255 _G2A-BAD-UTF8 C!
    _G2A-BAD-UTF8 1 _G2A-VALUE CV-STRING! 0= _G2A-ASSERT
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE CS-E-TYPE = _G2A-ASSERT
    _G2A-STACK ;

: _G2A-LOCATOR-VALUE-CONTRACTS  ( -- )
    _G2A-REF RREF-INIT
    1 _G2A-REF RREF.ID C!
    7 _G2A-REF RREF.REVISION !
    _G2A-REF _G2A-VALUE IRES-RREF! IRES-S-OK = _G2A-ASSERT

    _G2A-SCHEMA CSC-SEMANTIC-RREF!
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE 0= _G2A-ASSERT
    _G2A-VALUE CV-DATA@ _G2A-VALUE CV-LEN@
        _G2A-VALUE2 CV-STRING! 0= _G2A-ASSERT
    _G2A-VALUE2 _G2A-SCHEMA CS-VALIDATE CS-E-TYPE = _G2A-ASSERT
    _G2A-TEXT 111 _G2A-VALUE CV-RESOURCE! 0= _G2A-ASSERT
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE CS-E-LENGTH = _G2A-ASSERT

    _G2A-SCHEMA CSC-OPTIONAL-SEMANTIC-RREF!
    _G2A-VALUE CV-NULL!
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE 0= _G2A-ASSERT
    _G2A-REF _G2A-VALUE IRES-RREF! IRES-S-OK = _G2A-ASSERT
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE 0= _G2A-ASSERT

    _G2A-SCHEMA CSC-RREF-TEXT!
    _G2A-VALUE CV-DATA@ _G2A-VALUE CV-LEN@
        _G2A-VALUE2 CV-STRING! 0= _G2A-ASSERT
    _G2A-VALUE2 _G2A-SCHEMA CS-VALIDATE 0= _G2A-ASSERT
    _G2A-TEXT 111 _G2A-VALUE2 CV-STRING! 0= _G2A-ASSERT
    _G2A-VALUE2 _G2A-SCHEMA CS-VALIDATE CS-E-LENGTH = _G2A-ASSERT

    _G2A-SCHEMA CSC-VFS-LOCATOR-TEXT!
    _G2A-TEXT 516 _G2A-VALUE CV-STRING! 0= _G2A-ASSERT
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE 0= _G2A-ASSERT
    _G2A-TEXT 517 _G2A-VALUE CV-STRING! 0= _G2A-ASSERT
    _G2A-VALUE _G2A-SCHEMA CS-VALIDATE CS-E-LENGTH = _G2A-ASSERT
    _G2A-STACK ;

: _G2A-RUN  ( -- )
    0 _G2A-FAILS ! 0 _G2A-CHECKS ! DEPTH _G2A-DEPTH !
    _G2A-VALUE CV-INIT _G2A-VALUE2 CV-INIT
    _G2A-SPAN-CONTRACTS
    _G2A-SCHEMA-BYTE-CONTRACTS
    _G2A-SCALAR-VALUE-CONTRACTS
    _G2A-LOCATOR-VALUE-CONTRACTS
    _G2A-VALUE CV-FREE _G2A-VALUE2 CV-FREE
    _G2A-STACK
    _G2A-FAILS @ 0= IF
        ." GATE2A CONTRACTS PASS " _G2A-CHECKS @ .
    ELSE
        ." GATE2A CONTRACTS FAIL " _G2A-FAILS @ . ." / " _G2A-CHECKS @ .
    THEN CR ;

_G2A-RUN
