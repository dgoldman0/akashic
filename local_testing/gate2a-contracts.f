\ Deterministic contracts for policy-neutral span mechanics, caller-owned
\ bounded span sets and output writers, and scalar/locator initializers.

PROVIDED akashic-gate2a-contracts

VARIABLE _G2A-FAILS
VARIABLE _G2A-CHECKS
VARIABLE _G2A-DEPTH

: _G2A-ASSERT  ( flag -- )
    1 _G2A-CHECKS +!
    0= IF 1 _G2A-FAILS +! ." GATE2A ASSERT " _G2A-CHECKS @ . CR THEN ;

: _G2A-STACK  ( -- )
    DEPTH DUP _G2A-DEPTH @ <> IF
        ." GATE2A STACK " _G2A-DEPTH @ . ." -> " DUP . CR .S CR
    THEN
    _G2A-DEPTH @ = _G2A-ASSERT ;

