: _G2A-SCHEMA=  ( -- flag )
    _G2A-SCHEMA CS-SIZE _G2A-EXPECTED CS-SIZE STR-STR= ;

: _G2A-SPAN-CONTRACTS  ( -- )
    1000 16 MSPAN-NONWRAPPING? _G2A-ASSERT
    1000 0 MSPAN-NONWRAPPING? _G2A-ASSERT
    0 0 MSPAN-NONWRAPPING? _G2A-ASSERT
    0 1 MSPAN-NONWRAPPING? _G2A-ASSERT
    1000 -1 MSPAN-NONWRAPPING? 0= _G2A-ASSERT
    -1 0 MSPAN-NONWRAPPING? _G2A-ASSERT
    -8 7 MSPAN-NONWRAPPING? _G2A-ASSERT
    -8 8 MSPAN-NONWRAPPING? 0= _G2A-ASSERT

    1000 10 1010 5 MSPAN-OVERLAP? 0= _G2A-ASSERT
    1010 5 1000 10 MSPAN-OVERLAP? 0= _G2A-ASSERT
    1000 11 1010 5 MSPAN-OVERLAP? _G2A-ASSERT
    1010 5 1000 11 MSPAN-OVERLAP? _G2A-ASSERT
    1000 20 1005 5 MSPAN-OVERLAP? _G2A-ASSERT
    1000 20 1000 20 MSPAN-OVERLAP? _G2A-ASSERT
    1000 0 1000 1 MSPAN-OVERLAP? 0= _G2A-ASSERT
    0 4 2 2 MSPAN-OVERLAP? _G2A-ASSERT
    -8 8 -4 1 MSPAN-OVERLAP? 0= _G2A-ASSERT
    -16 8 -12 4 MSPAN-OVERLAP? _G2A-ASSERT
    _G2A-STACK ;

: _G2A-ALL-CHAR?  ( address length char -- flag )
    _G2A-CHAR ! 0 ?DO
        DUP I + C@ _G2A-CHAR @ <> IF DROP 0 UNLOOP EXIT THEN
    LOOP
    DROP -1 ;

: _G2A-SPAN-SET-CONTRACTS  ( -- )
    4 MSPAN-SET-BYTES 80 = _G2A-ASSERT
    -1 MSPAN-SET-BYTES 0= _G2A-ASSERT
    -1 5 RSHIFT 1- MSPAN-SET-BYTES 0> _G2A-ASSERT
    -1 5 RSHIFT MSPAN-SET-BYTES 0= _G2A-ASSERT
    4 _G2A-SPAN-SET-A MSPAN-SET-INIT MSPAN-SET-S-OK = _G2A-ASSERT
    3 _G2A-SPAN-SET-B MSPAN-SET-INIT MSPAN-SET-S-OK = _G2A-ASSERT
    _G2A-SPAN-SET-A MSPAN-SET-VALID? _G2A-ASSERT
    _G2A-SPAN-SET-A MSPAN-SET-COUNT@ 0= _G2A-ASSERT
    _G2A-SPAN-SET-A MSPAN-SET-CAPACITY@ 4 = _G2A-ASSERT

    1000 10 _G2A-SPAN-SET-A MSPAN-SET-ADD
        MSPAN-SET-S-OK = _G2A-ASSERT
    1010 5 _G2A-SPAN-SET-A MSPAN-SET-ADD
        MSPAN-SET-S-OK = _G2A-ASSERT
    1005 2 _G2A-SPAN-SET-A MSPAN-SET-ADD
        MSPAN-SET-S-OVERLAP = _G2A-ASSERT
    _G2A-SPAN-SET-A MSPAN-SET-COUNT@ 2 = _G2A-ASSERT
    1009 2 _G2A-SPAN-SET-A MSPAN-SET-OVERLAP? _G2A-ASSERT
    1015 1 _G2A-SPAN-SET-A MSPAN-SET-OVERLAP? 0= _G2A-ASSERT
    0 0 _G2A-SPAN-SET-A MSPAN-SET-ADD
        MSPAN-SET-S-OK = _G2A-ASSERT
    1000 0 _G2A-SPAN-SET-A MSPAN-SET-OVERLAP? 0= _G2A-ASSERT
    -8 8 _G2A-SPAN-SET-A MSPAN-SET-ADD
        MSPAN-SET-S-INVALID = _G2A-ASSERT
    1000 -1 _G2A-SPAN-SET-A MSPAN-SET-PUSH
        MSPAN-SET-S-INVALID = _G2A-ASSERT
    _G2A-SPAN-SET-A MSPAN-SET-COUNT@ 3 = _G2A-ASSERT
    2000 1 _G2A-SPAN-SET-A MSPAN-SET-ADD
        MSPAN-SET-S-OK = _G2A-ASSERT
    3000 1 _G2A-SPAN-SET-A MSPAN-SET-PUSH
        MSPAN-SET-S-CAPACITY = _G2A-ASSERT
    _G2A-SPAN-SET-A MSPAN-SET-COUNT@ 4 = _G2A-ASSERT

    \ PUSH deliberately admits overlap in a borrowed object graph; ADD is
    \ the disjoint insertion policy used by private-region registries.
    1000 10 _G2A-SPAN-SET-B MSPAN-SET-PUSH
        MSPAN-SET-S-OK = _G2A-ASSERT
    1005 10 _G2A-SPAN-SET-B MSPAN-SET-PUSH
        MSPAN-SET-S-OK = _G2A-ASSERT
    _G2A-SPAN-SET-B MSPAN-SET-COUNT@ 2 = _G2A-ASSERT
    _G2A-SPAN-SET-A MSPAN-SET-COUNT@ 4 = _G2A-ASSERT

    \ Entries borrow geometry only.  Mutating the named object and clearing
    \ the set neither dereferences nor clears the borrowed bytes.
    _G2A-SPAN-SET-B MSPAN-SET-CLEAR MSPAN-SET-S-OK = _G2A-ASSERT
    _G2A-BORROWED 16 [CHAR] a FILL
    _G2A-BORROWED 16 _G2A-SPAN-SET-B MSPAN-SET-ADD
        MSPAN-SET-S-OK = _G2A-ASSERT
    _G2A-BORROWED 16 [CHAR] z FILL
    _G2A-BORROWED 1 _G2A-SPAN-SET-B MSPAN-SET-OVERLAP? _G2A-ASSERT
    _G2A-SPAN-SET-B MSPAN-SET-CLEAR MSPAN-SET-S-OK = _G2A-ASSERT
    _G2A-BORROWED 16 [CHAR] z _G2A-ALL-CHAR? _G2A-ASSERT
    _G2A-SPAN-SET-B MSPAN-SET-COUNT@ 0= _G2A-ASSERT

    \ A structurally valid header does not hide corrupt active geometry.
    1000 10 _G2A-SPAN-SET-B MSPAN-SET-ADD
        MSPAN-SET-S-OK = _G2A-ASSERT
    -1 0 _G2A-SPAN-SET-B _MSPAN-SET-NTH 8 + !
    _G2A-SPAN-SET-B MSPAN-SET-VALID? 0= _G2A-ASSERT
    1000 1 _G2A-SPAN-SET-B MSPAN-SET-OVERLAP? 0= _G2A-ASSERT
    2000 1 _G2A-SPAN-SET-B MSPAN-SET-ADD
        MSPAN-SET-S-INVALID = _G2A-ASSERT
    _G2A-SPAN-SET-B MSPAN-SET-COUNT@ 1 = _G2A-ASSERT
    10 0 _G2A-SPAN-SET-B _MSPAN-SET-NTH 8 + !
    _G2A-SPAN-SET-B MSPAN-SET-VALID? _G2A-ASSERT
    _G2A-STACK ;

: _G2A-CHECK-NUMBER  ( n expected-a expected-u -- )
    _G2A-NUM-U ! _G2A-NUM-A ! _G2A-NUM-N !
    _G2A-WRITER-A CBW-RESET CBW-S-OK = _G2A-ASSERT
    _G2A-NUM-N @ _G2A-WRITER-A CBW-NUMBER CBW-S-OK = _G2A-ASSERT
    _G2A-WRITER-BUF-A _G2A-WRITER-A CBW-LENGTH@
        _G2A-NUM-A @ _G2A-NUM-U @ STR-STR= _G2A-ASSERT ;

: _G2A-BUFFER-WRITER-CONTRACTS  ( -- )
    _G2A-WRITER-BUF-A 32 [CHAR] ? FILL
    _G2A-WRITER-BUF-B 32 [CHAR] ! FILL
    _G2A-WRITER-BUF-A 4 _G2A-WRITER-A CBW-INIT
        CBW-S-OK = _G2A-ASSERT
    _G2A-WRITER-BUF-B 4 _G2A-WRITER-B CBW-INIT
        CBW-S-OK = _G2A-ASSERT
    S" ab" _G2A-WRITER-A CBW-APPEND CBW-S-OK = _G2A-ASSERT
    S" XY" _G2A-WRITER-B CBW-APPEND CBW-S-OK = _G2A-ASSERT
    [CHAR] c _G2A-WRITER-A CBW-CHAR CBW-S-OK = _G2A-ASSERT
    [CHAR] Z _G2A-WRITER-B CBW-CHAR CBW-S-OK = _G2A-ASSERT
    S" abc" _G2A-WRITER-BUF-A 3 STR-STR= _G2A-ASSERT
    S" XYZ" _G2A-WRITER-BUF-B 3 STR-STR= _G2A-ASSERT

    [CHAR] d _G2A-WRITER-A CBW-CHAR CBW-S-OK = _G2A-ASSERT
    _G2A-WRITER-A CBW-LENGTH@ 4 = _G2A-ASSERT
    [CHAR] e _G2A-WRITER-A CBW-CHAR CBW-S-CAPACITY = _G2A-ASSERT
    _G2A-WRITER-A CBW-LENGTH@ 4 = _G2A-ASSERT
    S" ignored" _G2A-WRITER-A CBW-APPEND
        CBW-S-CAPACITY = _G2A-ASSERT
    S" abcd" _G2A-WRITER-BUF-A 4 STR-STR= _G2A-ASSERT
    _G2A-WRITER-A CBW-RESULT
        CBW-S-CAPACITY = _G2A-ASSERT
        4 = _G2A-ASSERT
        _G2A-WRITER-BUF-A = _G2A-ASSERT
    \ One writer's sticky failure cannot poison another caller's state.
    [CHAR] W _G2A-WRITER-B CBW-CHAR CBW-S-OK = _G2A-ASSERT
    S" XYZW" _G2A-WRITER-BUF-B 4 STR-STR= _G2A-ASSERT
    _G2A-WRITER-B CBW-STATUS@ CBW-S-OK = _G2A-ASSERT
    _G2A-WRITER-A CBW-RESET CBW-S-OK = _G2A-ASSERT
    _G2A-WRITER-A CBW-LENGTH@ 0= _G2A-ASSERT
    _G2A-WRITER-A CBW-STATUS@ CBW-S-OK = _G2A-ASSERT
    S" abcd" _G2A-WRITER-BUF-A 4 STR-STR= _G2A-ASSERT

    _G2A-WRITER-BUF-A 4 [CHAR] ? FILL
    S" ab" _G2A-WRITER-A CBW-APPEND CBW-S-OK = _G2A-ASSERT
    S" cde" _G2A-WRITER-A CBW-APPEND
        CBW-S-CAPACITY = _G2A-ASSERT
    _G2A-WRITER-A CBW-LENGTH@ 2 = _G2A-ASSERT
    S" ab??" _G2A-WRITER-BUF-A 4 STR-STR= _G2A-ASSERT

    _G2A-WRITER-A CBW-RESET DROP
    0 0 _G2A-WRITER-A CBW-APPEND CBW-S-OK = _G2A-ASSERT
    0 1 _G2A-WRITER-A CBW-APPEND CBW-S-INVALID = _G2A-ASSERT
    _G2A-WRITER-A CBW-LENGTH@ 0= _G2A-ASSERT
    _G2A-WRITER-A CBW-STATUS@ CBW-S-INVALID = _G2A-ASSERT
    [CHAR] x _G2A-WRITER-A CBW-CHAR CBW-S-INVALID = _G2A-ASSERT
    _G2A-WRITER-A CBW-LENGTH@ 0= _G2A-ASSERT
    S" ab??" _G2A-WRITER-BUF-A 4 STR-STR= _G2A-ASSERT
    _G2A-WRITER-A CBW-RESET DROP
    -8 8 _G2A-WRITER-A CBW-APPEND CBW-S-INVALID = _G2A-ASSERT
    _G2A-WRITER-A CBW-RESET DROP
    _G2A-WRITER-A 1 _G2A-WRITER-A CBW-APPEND
        CBW-S-INVALID = _G2A-ASSERT
    _G2A-WRITER-A CBW-RESET DROP

    \ INIT preflights descriptor/target aliasing and is nonmutating on fail.
    _G2A-WRITER-A 32 _G2A-WRITER-A CBW-INIT
        CBW-S-INVALID = _G2A-ASSERT
    _G2A-WRITER-A CBW-VALID? _G2A-ASSERT
    0 0 _G2A-WRITER-B CBW-INIT CBW-S-OK = _G2A-ASSERT
    [CHAR] x _G2A-WRITER-B CBW-CHAR
        CBW-S-CAPACITY = _G2A-ASSERT

    \ Source and target may overlap; APPEND therefore promises MOVE,
    \ including the right-shift case that a forward byte copy corrupts.
    S" abcd" _G2A-WRITER-BUF-A SWAP MOVE
    _G2A-WRITER-BUF-A 8 _G2A-WRITER-A CBW-INIT DROP
    _G2A-WRITER-BUF-A 2 _G2A-WRITER-A CBW-APPEND
        CBW-S-OK = _G2A-ASSERT
    _G2A-WRITER-BUF-A 4 _G2A-WRITER-A CBW-APPEND
        CBW-S-OK = _G2A-ASSERT
    S" ababcd" _G2A-WRITER-BUF-A 6 STR-STR= _G2A-ASSERT
    _G2A-WRITER-A CBW-RESULT
        CBW-S-OK = _G2A-ASSERT
        6 = _G2A-ASSERT
        _G2A-WRITER-BUF-A = _G2A-ASSERT

    _G2A-WRITER-BUF-A 32 _G2A-WRITER-A CBW-INIT DROP
    0 S" 0" _G2A-CHECK-NUMBER
    42 S" 42" _G2A-CHECK-NUMBER
    -123 S" -123" _G2A-CHECK-NUMBER
    0x7FFFFFFFFFFFFFFF S" 9223372036854775807" _G2A-CHECK-NUMBER
    0x8000000000000000 S" -9223372036854775808" _G2A-CHECK-NUMBER
    BASE @ >R HEX
    42 S" 42" _G2A-CHECK-NUMBER
    R> BASE !

    _G2A-WRITER-BUF-B 32 [CHAR] q FILL
    _G2A-WRITER-BUF-B 19 _G2A-WRITER-B CBW-INIT DROP
    0x8000000000000000 _G2A-WRITER-B CBW-NUMBER
        CBW-S-CAPACITY = _G2A-ASSERT
    _G2A-WRITER-B CBW-LENGTH@ 0= _G2A-ASSERT
    _G2A-WRITER-BUF-B 32 [CHAR] q _G2A-ALL-CHAR? _G2A-ASSERT
    _G2A-STACK ;

: _G2A-SCHEMA-BYTE-CONTRACTS  ( -- )
    _G2A-SCHEMA CSC-NULL!
    _G2A-EXPECTED CS-INIT CV-T-NULL _G2A-EXPECTED CS-ALLOW!
    _G2A-SCHEMA= _G2A-ASSERT

    _G2A-SCHEMA CSC-BOOL!
    _G2A-EXPECTED CS-INIT CV-T-BOOL _G2A-EXPECTED CS-ALLOW!
    _G2A-SCHEMA= _G2A-ASSERT

    _G2A-SCHEMA CSC-NONNEG-INT!
    _G2A-EXPECTED CS-INIT CV-T-INT _G2A-EXPECTED CS-ALLOW!
    0 _G2A-EXPECTED CS-MIN!
    _G2A-SCHEMA= _G2A-ASSERT

    _G2A-SCHEMA CSC-POSITIVE-REVISION!
    _G2A-EXPECTED CS-INIT CV-T-INT _G2A-EXPECTED CS-ALLOW!
    1 _G2A-EXPECTED CS-MIN!
    _G2A-SCHEMA= _G2A-ASSERT

    7 _G2A-SCHEMA CSC-UTF8!
    _G2A-EXPECTED CS-INIT CV-T-STRING _G2A-EXPECTED CS-ALLOW!
    7 _G2A-EXPECTED CS-MAX-LEN!
    _G2A-SCHEMA= _G2A-ASSERT

    _G2A-SCHEMA CSC-SEMANTIC-RREF!
    _G2A-EXPECTED CS-INIT CV-T-RESOURCE _G2A-EXPECTED CS-ALLOW!
    IRES-RREF-URI-MAX _G2A-EXPECTED CS-MAX-LEN!
    _G2A-SCHEMA= _G2A-ASSERT

    _G2A-SCHEMA CSC-OPTIONAL-SEMANTIC-RREF!
    _G2A-EXPECTED CS-INIT
    CV-T-NULL CS-TYPE-BIT CV-T-RESOURCE CS-TYPE-BIT OR
        _G2A-EXPECTED CS-ALLOW-MASK!
    IRES-RREF-URI-MAX _G2A-EXPECTED CS-MAX-LEN!
    _G2A-SCHEMA= _G2A-ASSERT

    _G2A-SCHEMA CSC-RREF-TEXT!
    _G2A-EXPECTED CS-INIT CV-T-STRING _G2A-EXPECTED CS-ALLOW!
    IRES-RREF-URI-MAX _G2A-EXPECTED CS-MAX-LEN!
    _G2A-SCHEMA= _G2A-ASSERT

    _G2A-SCHEMA CSC-VFS-LOCATOR-TEXT!
    _G2A-EXPECTED CS-INIT CV-T-STRING _G2A-EXPECTED CS-ALLOW!
    516 _G2A-EXPECTED CS-MAX-LEN!
    _G2A-SCHEMA= _G2A-ASSERT
    CSC-RREF-TEXT-MAX IRES-RREF-URI-MAX = _G2A-ASSERT
    CSC-VFS-LOCATOR-TEXT-MAX 516 = _G2A-ASSERT

    \ Initializing another caller's allocation must not mutate the first.
    _G2A-OTHER CSC-BOOL!
    _G2A-SCHEMA CSC-NULL!
    _G2A-SCHEMA CS.TYPE-MASK @ CV-T-NULL CS-TYPE-BIT = _G2A-ASSERT
    _G2A-OTHER CS.TYPE-MASK @ CV-T-BOOL CS-TYPE-BIT = _G2A-ASSERT
    _G2A-STACK ;

