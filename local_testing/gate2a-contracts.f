: _G2A-RUN  ( -- )
    0 _G2A-FAILS ! 0 _G2A-CHECKS ! DEPTH _G2A-DEPTH !
    _G2A-VALUE CV-INIT _G2A-VALUE2 CV-INIT
    _G2A-SPAN-CONTRACTS
    _G2A-SPAN-SET-CONTRACTS
    _G2A-BUFFER-WRITER-CONTRACTS
    _G2A-SCHEMA-BYTE-CONTRACTS
    _G2A-SCALAR-VALUE-CONTRACTS
    _G2A-LOCATOR-VALUE-CONTRACTS
    _G2A-VALUE CV-FREE _G2A-VALUE2 CV-FREE
    _G2A-STACK
    _G2A-FAILS @ 0= IF
        ." GATE2A CONTRACTS PASS " _G2A-CHECKS @ .
    ELSE
        ." GATE2A CONTRACTS FAIL " _G2A-FAILS @ . ." / " _G2A-CHECKS @ .
    THEN CR ;

_G2A-RUN
