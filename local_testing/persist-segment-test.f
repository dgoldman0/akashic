\ RAM-VFS contracts for neutral aligned variable-record segments.

PROVIDED akashic-persistence-segment-contracts

VARIABLE _PSCT-FAILS
VARIABLE _PSCT-CHECKS
VARIABLE _PSCT-DEPTH
VARIABLE _PSCT-ARENA
VARIABLE _PSCT-VFS
VARIABLE _PSCT-OLD-VFS
VARIABLE _PSCT-OLD-WRITE-XT
VARIABLE _PSCT-OLD-SYNC-XT

CREATE _PSCT-OPS VFS-OPS-SIZE ALLOT
CREATE _PSCT-BINDING VFS-BINDING-DESC-SIZE ALLOT
CREATE _PSCT-STATS PERSIST-STATS-SIZE ALLOT
CREATE _PSCT-FILE PSEG-FILE-SIZE ALLOT
CREATE _PSCT-FILE-ABSENT PSEG-FILE-SIZE ALLOT
CREATE _PSCT-FILE-INVALID PSEG-FILE-SIZE ALLOT
CREATE _PSCT-WORK PSEG-WORK-SIZE ALLOT
CREATE _PSCT-WORK-INVALID PSEG-WORK-SIZE ALLOT
CREATE _PSCT-WORK-ALIAS PSEG-WORK-SIZE ALLOT
CREATE _PSCT-BUFFER 256 ALLOT
CREATE _PSCT-REF-A PERSIST-REF-SIZE ALLOT
CREATE _PSCT-REF-B PERSIST-REF-SIZE ALLOT
CREATE _PSCT-REF-C PERSIST-REF-SIZE ALLOT
CREATE _PSCT-REF-D PERSIST-REF-SIZE ALLOT
CREATE _PSCT-PAYLOAD 160 ALLOT

: _PSCT-WRITE-THROW  ( buffer length offset inode vfs -- actual ior )
    2DROP 2DROP DROP -9201 THROW ;

: _PSCT-SYNC-THROW  ( vfs -- ior )
    DROP -9202 THROW ;

: _PSCT-ASSERT  ( flag -- )
    1 _PSCT-CHECKS +!
    0= IF
        1 _PSCT-FAILS +!
        ." PERSISTENCE SEGMENT ASSERT " _PSCT-CHECKS @ . CR
    THEN ;

: _PSCT-STACK  ( -- )
    DEPTH DUP _PSCT-DEPTH @ <> IF
        ." PERSISTENCE SEGMENT STACK " _PSCT-DEPTH @ .
        ."  -> " DUP . CR .S CR
    THEN
    _PSCT-DEPTH @ = _PSCT-ASSERT ;

: _PSCT-STATUS=  ( actual expected -- )
    2DUP <> IF
        ." PERSISTENCE SEGMENT STATUS actual/expected " 2DUP . . CR
    THEN
    = _PSCT-ASSERT _PSCT-STACK ;

: _PSCT-SETUP  ( -- )
    VFS-CUR _PSCT-OLD-VFS !
    VFS-RAM-OPS _PSCT-OPS VFS-OPS-SIZE MOVE
    VFS-RAM-BINDING _PSCT-BINDING VFS-BINDING-DESC-SIZE MOVE
    _PSCT-OPS _PSCT-BINDING VB.OPS !
    _PSCT-OPS VFS-OP-WRITE CELLS + @ _PSCT-OLD-WRITE-XT !
    _PSCT-OPS VFS-OP-SYNCFS CELLS + @ _PSCT-OLD-SYNC-XT !
    524288 A-XMEM ARENA-NEW
    DUP 0= _PSCT-ASSERT DROP _PSCT-ARENA !
    _PSCT-ARENA @ _PSCT-BINDING 0 VFS-NEW
    DUP 0= _PSCT-ASSERT DROP _PSCT-VFS !
    _PSCT-STATS PERSIST-STATS-INIT
    _PSCT-FILE-INVALID PSEG-FILE-SIZE 0 FILL
    _PSCT-WORK-INVALID PSEG-WORK-SIZE 0 FILL
    _PSCT-REF-A PERSIST-REF-INIT
    _PSCT-REF-B PERSIST-REF-INIT
    _PSCT-REF-C PERSIST-REF-INIT
    _PSCT-REF-D PERSIST-REF-INIT
    160 0 ?DO I 31 + _PSCT-PAYLOAD I + C! LOOP ;

: _PSCT-INIT-CONTRACTS  ( -- )
    S" relative.bin" 128 _PSCT-VFS @ _PSCT-STATS _PSCT-FILE-INVALID
        PSEG-FILE-INIT PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-FILE-INVALID PSEG-FILE-VALID? 0= _PSCT-ASSERT
    _PSCT-STACK

    S" /same-owner.bin" 128 _PSCT-VFS @ _PSCT-VFS @
        _PSCT-FILE-INVALID PSEG-FILE-INIT
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-FILE-INVALID PSEG-FILE-VALID? 0= _PSCT-ASSERT
    S" /partial-owner.bin" 128 _PSCT-VFS @ _PSCT-VFS @ 8 +
        _PSCT-FILE-INVALID PSEG-FILE-INIT
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-FILE-INVALID PSEG-FILE-VALID? 0= _PSCT-ASSERT
    _PSCT-STACK

    S" /segment.bin" 128 _PSCT-VFS @ _PSCT-STATS _PSCT-FILE
        PSEG-FILE-INIT PERSIST-S-OK _PSCT-STATUS=
    _PSCT-FILE PSEG-FILE-VALID? _PSCT-ASSERT
    _PSCT-VFS @ _PSCT-FILE _PSEG-F.STATS !
    _PSCT-FILE PSEG-FILE-VALID? 0= _PSCT-ASSERT
    _PSCT-VFS @ 8 + _PSCT-FILE _PSEG-F.STATS !
    _PSCT-FILE PSEG-FILE-VALID? 0= _PSCT-ASSERT
    _PSCT-STATS _PSCT-FILE _PSEG-F.STATS !
    _PSCT-FILE PSEG-FILE-VALID? _PSCT-ASSERT
    _PSCT-FILE PSEG-FILE-PATH$ S" /segment.bin" COMPARE 0= _PSCT-ASSERT
    _PSCT-FILE PSEG-MAX-PAYLOAD@ 128 = _PSCT-ASSERT
    17 _PSCT-FILE PSEG-MEASURE
        PERSIST-S-OK = SWAP 88 = AND _PSCT-ASSERT
    129 _PSCT-FILE PSEG-MEASURE
        PERSIST-S-CAPACITY = SWAP 0= AND _PSCT-ASSERT
    17 _PSCT-FILE-INVALID PSEG-MEASURE
        PERSIST-S-INVALID = SWAP 0= AND _PSCT-ASSERT
    _PSCT-STACK

    _PSCT-BUFFER 63 _PSCT-WORK-INVALID PSEG-WORK-INIT
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-WORK-INVALID PSEG-WORK-VALID? 0= _PSCT-ASSERT
    _PSCT-BUFFER 256 _PSCT-WORK PSEG-WORK-INIT
        PERSIST-S-OK _PSCT-STATUS=
    _PSCT-WORK PSEG-WORK-VALID? _PSCT-ASSERT
    _PSCT-FILE PSEG-FILE-SIZE _PSCT-WORK-ALIAS PSEG-WORK-INIT
        PERSIST-S-OK _PSCT-STATUS=
    _PSCT-FILE _PSCT-WORK-ALIAS PSEG-ENSURE
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-STACK

    S" /absent.bin" 128 _PSCT-VFS @ _PSCT-STATS _PSCT-FILE-ABSENT
        PSEG-FILE-INIT PERSIST-S-OK _PSCT-STATUS=
    _PSCT-STACK ;

: _PSCT-PROVISION-CONTRACTS  ( -- )
    _PSCT-FILE 0 PSEG-ENSURE PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-FILE-INVALID _PSCT-WORK PSEG-ENSURE
        PERSIST-S-INVALID _PSCT-STATUS=
    1 _PSCT-WORK _PSEG-W.BUSY !
    _PSCT-FILE _PSCT-WORK PSEG-ENSURE PERSIST-S-BUSY _PSCT-STATUS=
    0 _PSCT-WORK _PSEG-W.BUSY !

    _PSCT-FILE-ABSENT _PSCT-WORK PSEG-FILE-SIZE?
        PERSIST-S-ABSENT = SWAP 0= AND _PSCT-ASSERT _PSCT-STACK
    _PSCT-FILE _PSCT-WORK PSEG-ENSURE PERSIST-S-OK _PSCT-STATUS=
    _PSCT-FILE _PSCT-WORK PSEG-ENSURE PERSIST-S-OK _PSCT-STATUS=
    _PSCT-FILE _PSCT-WORK PSEG-FILE-SIZE?
        PERSIST-S-OK = SWAP 0= AND _PSCT-ASSERT _PSCT-STACK

    _PSCT-FILE 0 PSEG-FILE-SIZE?
        PERSIST-S-INVALID = SWAP 0= AND _PSCT-ASSERT _PSCT-STACK
    _PSCT-FILE-INVALID _PSCT-WORK PSEG-FILE-SIZE?
        PERSIST-S-INVALID = SWAP 0= AND _PSCT-ASSERT _PSCT-STACK
    1 _PSCT-WORK _PSEG-W.BUSY !
    _PSCT-FILE _PSCT-WORK PSEG-FILE-SIZE?
        PERSIST-S-BUSY = SWAP 0= AND _PSCT-ASSERT _PSCT-STACK
    0 _PSCT-WORK _PSEG-W.BUSY !
    _PSCT-STACK ;

: _PSCT-WRITE-CONTRACTS  ( -- )
    _PSCT-PAYLOAD 17 1 0 _PSCT-REF-A _PSCT-FILE 0 PSEG-WRITE
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-PAYLOAD 17 1 0 _PSCT-REF-A _PSCT-FILE-INVALID _PSCT-WORK
        PSEG-WRITE PERSIST-S-INVALID _PSCT-STATUS=
    1 _PSCT-WORK _PSEG-W.BUSY !
    _PSCT-PAYLOAD 17 1 0 _PSCT-REF-A _PSCT-FILE _PSCT-WORK PSEG-WRITE
        PERSIST-S-BUSY _PSCT-STATUS=
    0 _PSCT-WORK _PSEG-W.BUSY !
    _PSCT-PAYLOAD 17 0 0 _PSCT-REF-A _PSCT-FILE _PSCT-WORK PSEG-WRITE
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-PAYLOAD 17 1 1 _PSCT-REF-A _PSCT-FILE _PSCT-WORK PSEG-WRITE
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-PAYLOAD 17 1 0 0 _PSCT-FILE _PSCT-WORK PSEG-WRITE
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-PAYLOAD 129 1 0 _PSCT-REF-A _PSCT-FILE _PSCT-WORK PSEG-WRITE
        PERSIST-S-CAPACITY _PSCT-STATUS=
    _PSCT-WORK 8 1 0 _PSCT-REF-D _PSCT-FILE _PSCT-WORK PSEG-WRITE
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-PAYLOAD 17 1 0 _PSCT-BUFFER _PSCT-FILE _PSCT-WORK PSEG-WRITE
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-PAYLOAD 17 1 0 _PSCT-PAYLOAD _PSCT-FILE _PSCT-WORK PSEG-WRITE
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-PAYLOAD 17 1 8 _PSCT-REF-A _PSCT-FILE _PSCT-WORK PSEG-WRITE
        PERSIST-S-CORRUPT _PSCT-STATUS=

    _PSCT-PAYLOAD 17 1 0 _PSCT-REF-A _PSCT-FILE _PSCT-WORK PSEG-WRITE
        PERSIST-S-OK _PSCT-STATUS=
    _PSCT-REF-A PERSIST-REF-VALID? _PSCT-ASSERT
    _PSCT-REF-A PREF.OFFSET @ 0= _PSCT-ASSERT
    _PSCT-REF-A PREF.SPAN @ 88 = _PSCT-ASSERT
    _PSCT-REF-A PREF.ORDINAL @ 1 = _PSCT-ASSERT
    _PSCT-FILE _PSCT-WORK PSEG-FILE-SIZE?
        PERSIST-S-OK = SWAP 88 = AND _PSCT-ASSERT _PSCT-STACK

    _PSCT-PAYLOAD 17 + 31 2 88 _PSCT-REF-B _PSCT-FILE _PSCT-WORK
        PSEG-WRITE PERSIST-S-OK _PSCT-STATUS=
    _PSCT-REF-B PREF.SPAN @ 96 = _PSCT-ASSERT
    _PSCT-FILE _PSCT-WORK PSEG-FILE-SIZE?
        PERSIST-S-OK = SWAP 184 = AND _PSCT-ASSERT _PSCT-STACK

    ['] _PSCT-WRITE-THROW _PSCT-OPS VFS-OP-WRITE CELLS + !
    _PSCT-PAYLOAD 9 3 184 _PSCT-REF-C _PSCT-FILE _PSCT-WORK PSEG-WRITE
        PERSIST-S-FAULT _PSCT-STATUS=
    _PSCT-OLD-WRITE-XT @ _PSCT-OPS VFS-OP-WRITE CELLS + !
    _PSCT-WORK _PSEG-W.BUSY @ 0= _PSCT-ASSERT
    _PSCT-VFS @ V.OPEN-COUNT @ 0= _PSCT-ASSERT

    _PSCT-PAYLOAD 9 3 184 _PSCT-REF-C _PSCT-FILE _PSCT-WORK PSEG-WRITE
        PERSIST-S-OK _PSCT-STATUS=
    _PSCT-PAYLOAD 9 4 184 _PSCT-REF-D _PSCT-FILE _PSCT-WORK PSEG-WRITE
        PERSIST-S-CONFLICT _PSCT-STATUS=
    184 _PSCT-FILE 0 PSEG-TRUNCATE PERSIST-S-INVALID _PSCT-STATUS=
    184 _PSCT-FILE-INVALID _PSCT-WORK PSEG-TRUNCATE
        PERSIST-S-INVALID _PSCT-STATUS=
    1 _PSCT-WORK _PSEG-W.BUSY !
    184 _PSCT-FILE _PSCT-WORK PSEG-TRUNCATE PERSIST-S-BUSY _PSCT-STATUS=
    0 _PSCT-WORK _PSEG-W.BUSY !
    183 _PSCT-FILE _PSCT-WORK PSEG-TRUNCATE PERSIST-S-INVALID _PSCT-STATUS=
    400 _PSCT-FILE _PSCT-WORK PSEG-TRUNCATE PERSIST-S-CORRUPT _PSCT-STATUS=
    0 _PSCT-FILE-ABSENT _PSCT-WORK PSEG-TRUNCATE
        PERSIST-S-ABSENT _PSCT-STATUS=
    184 _PSCT-FILE _PSCT-WORK PSEG-TRUNCATE PERSIST-S-OK _PSCT-STATUS=
    184 _PSCT-FILE _PSCT-WORK PSEG-TRUNCATE PERSIST-S-OK _PSCT-STATUS=
    _PSCT-FILE _PSCT-WORK PSEG-FILE-SIZE?
        PERSIST-S-OK = SWAP 184 = AND _PSCT-ASSERT _PSCT-STACK
    _PSCT-STACK ;

: _PSCT-READ-CONTRACTS  ( -- )
    _PSCT-REF-A 184 _PSCT-FILE 0 PSEG-READ
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-REF-A 184 _PSCT-FILE-INVALID _PSCT-WORK PSEG-READ
        PERSIST-S-INVALID _PSCT-STATUS=
    1 _PSCT-WORK _PSEG-W.BUSY !
    _PSCT-REF-A 184 _PSCT-FILE _PSCT-WORK PSEG-READ
        PERSIST-S-BUSY _PSCT-STATUS=
    0 _PSCT-WORK _PSEG-W.BUSY !
    0 184 _PSCT-FILE _PSCT-WORK PSEG-READ
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-REF-A _PSCT-BUFFER PERSIST-REF-SIZE MOVE
    _PSCT-BUFFER 184 _PSCT-FILE _PSCT-WORK PSEG-READ
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-REF-A 80 _PSCT-FILE _PSCT-WORK PSEG-READ
        PERSIST-S-INVALID _PSCT-STATUS=
    _PSCT-REF-A 184 _PSCT-FILE-ABSENT _PSCT-WORK PSEG-READ
        PERSIST-S-ABSENT _PSCT-STATUS=

    _PSCT-REF-A 184 _PSCT-FILE _PSCT-WORK PSEG-READ
        PERSIST-S-OK _PSCT-STATUS=
    _PSCT-WORK PSEG-PAYLOAD$
        _PSCT-PAYLOAD 17 COMPARE 0= _PSCT-ASSERT _PSCT-STACK
    _PSCT-REF-B 184 _PSCT-FILE _PSCT-WORK PSEG-VERIFY
        PERSIST-S-OK _PSCT-STATUS=
    _PSCT-WORK PSEG-PAYLOAD$
        _PSCT-PAYLOAD 17 + 31 COMPARE 0= _PSCT-ASSERT _PSCT-STACK

    99 _PSCT-REF-A PREF.ORDINAL !
    _PSCT-REF-A 184 _PSCT-FILE _PSCT-WORK PSEG-READ
        PERSIST-S-CORRUPT _PSCT-STATUS=
    _PSCT-WORK PSEG-PAYLOAD$ OR 0= _PSCT-ASSERT _PSCT-STACK
    1 _PSCT-REF-A PREF.ORDINAL !
    _PSCT-STACK ;

: _PSCT-SYNC-CONTRACTS  ( -- )
    _PSCT-FILE-INVALID PSEG-SYNC PERSIST-S-INVALID _PSCT-STATUS=
    ['] _PSCT-SYNC-THROW _PSCT-OPS VFS-OP-SYNCFS CELLS + !
    _PSCT-FILE PSEG-SYNC PERSIST-S-FAULT _PSCT-STATUS=
    _PSCT-OLD-SYNC-XT @ _PSCT-OPS VFS-OP-SYNCFS CELLS + !
    _PSCT-FILE PSEG-SYNC PERSIST-S-OK _PSCT-STATUS=
    _PSCT-STATS PSTAT.SEGMENT-WRITES @ 3 = _PSCT-ASSERT
    _PSCT-STATS PSTAT.SEGMENT-READS @ 2 = _PSCT-ASSERT
    _PSCT-STATS PSTAT.VERIFICATIONS @ 2 = _PSCT-ASSERT
    _PSCT-STATS PSTAT.SYNCS @ 1 = _PSCT-ASSERT
    _PSCT-STACK ;

: _PSCT-RUN  ( -- )
    0 _PSCT-FAILS ! 0 _PSCT-CHECKS ! DEPTH _PSCT-DEPTH !
    _PSCT-SETUP
    _PSCT-INIT-CONTRACTS
    _PSCT-PROVISION-CONTRACTS
    _PSCT-WRITE-CONTRACTS
    _PSCT-READ-CONTRACTS
    _PSCT-SYNC-CONTRACTS
    _PSCT-OLD-VFS @ VFS-USE
    _PSCT-VFS @ VFS-DESTROY
    _PSCT-STACK
    _PSCT-FAILS @ 0= IF
        ." PERSISTENCE SEGMENT PASS " _PSCT-CHECKS @ . CR
    ELSE
        ." PERSISTENCE SEGMENT FAIL " _PSCT-FAILS @ .
        ."  / " _PSCT-CHECKS @ . CR
    THEN ;
