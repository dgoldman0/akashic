VARIABLE _G3A-FAILS
VARIABLE _G3A-CHECKS
VARIABLE _G3A-DEPTH

: _G3A-ASSERT  ( flag -- )
    1 _G3A-CHECKS +!
    0= IF 1 _G3A-FAILS +! ." GATE3A ASSERT " _G3A-CHECKS @ . CR THEN ;

: _G3A-STACK  ( -- )
    DEPTH DUP _G3A-DEPTH @ <> IF
        ." GATE3A STACK " _G3A-DEPTH @ . ."  -> " DUP . CR .S CR
    THEN
    _G3A-DEPTH @ = _G3A-ASSERT ;

