: _G3A-ROOT-EXACT?  ( -- status )
    _G3A-ROOT-QUALIFIED @ 0= IF RACQ-S-UNQUALIFIED EXIT THEN
    _G3A-RA-L @ QLOC.DOMAIN-REVISION @ 2 = IF RACQ-S-PRUNED EXIT THEN
    _G3A-RA-L @ QLOC.DOMAIN-REVISION @ _G3A-DOMAIN-REV @ <> IF
        RACQ-S-REVISION-MISMATCH EXIT
    THEN
    _G3A-RA-L @ QLOC.DIGEST-KIND @ QLOC-DK-PROJECTION-CONTENT <> IF
        RACQ-S-UNQUALIFIED EXIT
    THEN
    _G3A-RA-L @ QLOC-PROJECTION$ S" utf8-text.v1" STR-STR= 0= IF
        RACQ-S-UNQUALIFIED EXIT
    THEN
    _G3A-RA-L @ QLOC.STATE-DIGEST _G3A-DIGEST SHA3-256-COMPARE 0= IF
        RACQ-S-DIGEST-MISMATCH EXIT
    THEN
    RACQ-S-OK ;

: _G3A-ROOT-ACQUIRE  ( locator result root -- status )
    _G3A-RA-R ! _G3A-RA-O ! _G3A-RA-L !
    1 _G3A-ACQUIRE-CALLS +!
    _G3A-ROOT-LIVE @ 0= IF RACQ-S-UNAVAILABLE EXIT THEN
    _G3A-RA-L @ QLOC.REF RREF.ID _G3A-RID RID= 0= IF
        RACQ-S-RID-MISMATCH EXIT
    THEN
    _G3A-TOMBSTONED @ IF RACQ-S-TOMBSTONED EXIT THEN
    _G3A-RA-L @ QLOC.MODE @ QLOC-M-EXACT-DOMAIN = IF
        _G3A-ROOT-EXACT? ?DUP IF EXIT THEN
    THEN
    _G3A-RETAINED @ IF RACQ-S-CAPACITY EXIT THEN
    _G3A-REF RREF-INIT
    _G3A-RID _G3A-REF RREF.ID RID-COPY
    _G3A-REF _G3A-RA-O @ RACQ-RESULT-REF! RACQ-S-OK <> IF
        RACQ-S-INVALID EXIT
    THEN
    _G3A-SLOT-GEN @ 1+ DUP _G3A-SLOT-GEN !
    100 + DUP _G3A-SLOT-COOKIE !
    _G3A-SLOT-GEN @ _G3A-RA-R @
        _G3A-RA-O @ RACQ.RESULT-TOKEN RACQ-TOKEN-ACTIVATE!
        RACQ-S-OK <> IF RACQ-S-INVALID EXIT THEN
    _G3A-RA-O @ RACQ.RESULT-TOKEN _G3A-SLOT-TOKEN !
    -1 _G3A-RETAINED !
    _G3A-ACQUIRE-POST-STATUS @ ?DUP IF EXIT THEN
    RACQ-S-OK ;

VARIABLE _G3A-RT-T
