    _G3A-HQ ! _G3A-HQ @ CBR.RESULT CV-MAP! 0= ;

: _G3A-HSLOT  ( key-a key-u index -- flag )
    _G3A-HQ @ CBR.RESULT CV-MAP-SLOT! DUP IF
