    S" Fixture" S" title" 3 _G3A-HSTRING 0= IF CBUS-S-FAILED EXIT THEN
    S" text/plain" S" media_type" 4 _G3A-HSTRING 0= IF CBUS-S-FAILED EXIT THEN
