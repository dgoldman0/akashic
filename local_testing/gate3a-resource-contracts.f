    ELSE
        DROP _G3A-HC ! -1
    THEN ;

: _G3A-HRESOURCE  ( index -- flag )
    >R S" resource" R> _G3A-HSLOT 0= IF 0 EXIT THEN
    _G3A-HREF RREF-INIT _G3A-RID _G3A-HREF RREF.ID RID-COPY
