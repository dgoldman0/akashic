: _G3A-ROOT-TOKEN?  ( token root -- flag )
    _G3A-RT-R ! _G3A-RT-T !
    _G3A-RT-T @ _G3A-RT-R @ RACQ-TOKEN-FOR-ROOT? 0= IF 0 EXIT THEN
    _G3A-RT-T @ _G3A-SLOT-TOKEN @ =
    _G3A-RT-T @ RACQ.TOKEN-COOKIE @ _G3A-SLOT-COOKIE @ = AND
    _G3A-RT-T @ RACQ.TOKEN-GENERATION @ _G3A-SLOT-GEN @ = AND
    _G3A-RETAINED @ AND ;

: _G3A-ROOT-QUIESCENT  ( token root -- status )
    1 _G3A-QUIET-CALLS +!
    _G3A-ROOT-TOKEN? 0= IF RACQ-S-STALE-TOKEN EXIT THEN
    _G3A-QUIET-BUSY @ IF
        -1 _G3A-QUIET-BUSY +! RACQ-S-BUSY
    ELSE
        RACQ-S-OK
    THEN ;

: _G3A-ROOT-RELEASE  ( token root -- status )
    _G3A-ROOT-TOKEN? 0= IF RACQ-S-STALE-TOKEN EXIT THEN
    _G3A-RELEASE-FAILS @ IF
        -1 _G3A-RELEASE-FAILS +! RACQ-S-RELEASE-FAILED EXIT
    THEN
    1 _G3A-RELEASE-CALLS +!
    0 _G3A-RETAINED ! 0 _G3A-SLOT-TOKEN !
    RACQ-S-OK ;

