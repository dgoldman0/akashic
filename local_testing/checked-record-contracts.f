: _CRECC-ASSERT  ( flag -- )
    1 _CRECC-CHECKS +!
    0= IF
        1 _CRECC-FAILS +!
        ." CRECC ASSERT " _CRECC-CHECKS @ . CR
    THEN ;

: _CRECC-STACK  ( -- )
    DEPTH DUP _CRECC-DEPTH @ <> IF
        ." CRECC STACK " _CRECC-DEPTH @ . ." -> " DUP . CR .S CR
    THEN
    _CRECC-DEPTH @ = _CRECC-ASSERT ;

: _CRECC-FILLED?  ( address length byte -- flag )
    _CRECC-BYTE !
    0 ?DO
        DUP I + C@ _CRECC-BYTE @ <> IF DROP 0 UNLOOP EXIT THEN
    LOOP
    DROP -1 ;

: _CRECC-ZERO?  ( address length -- flag ) 0 _CRECC-FILLED? ;

: _CRECC-HEADER-CRC  ( record -- crc32 )
    CRC32-BEGIN
    DUP CREC-H-HEADER-CRC CRC32-ADD
    CREC-H-FLAGS + 8 CRC32-ADD
    CRC32-END ;

: _CRECC-RESEAL-HEADER  ( record -- )
    DUP _CRECC-HEADER-CRC SWAP CREC-H-HEADER-CRC + ! ;

: _CRECC-CB-CAPTURE  ( ca cu pa pu tag -- )
    _CRECC-CB-TAG ! _CRECC-CB-PU ! _CRECC-CB-PA !
    _CRECC-CB-CU ! _CRECC-CB-CA ! ;

: _CRECC-ENCODE-CB  ( context-a context-u payload-a payload-u tag -- status )
    _CRECC-CB-CAPTURE
    1 _CRECC-CB-ENCODE-N +!
    _CRECC-CB-ENCODE-THROW @ IF -811 THROW THEN
    _CRECC-CB-ENCODE-STATUS @ ?DUP IF EXIT THEN
    _CRECC-CB-CU @ _CRECC-CB-PU @ <> IF CREC-S-SEMANTIC EXIT THEN
    _CRECC-CB-PU @ IF
        _CRECC-CB-CA @ 0= IF CREC-S-SEMANTIC EXIT THEN
        _CRECC-CB-CA @ _CRECC-CB-PA @ _CRECC-CB-PU @ MOVE
    THEN
    _CRECC-CB-REENTER @ 1 = IF
        0 0 _CRECC-SPEC-FIXED _CRECC-CB-WORK @
            CREC-INSPECT-HEADER _CRECC-CB-NESTED-STATUS !
    THEN
    _CRECC-CB-REENTER @ 2 = IF
        0 _CRECC-CB-REENTER !
        _CRECC-PAYLOAD-B 16 16 9 _CRECC-RECORD-B 96
            _CRECC-SPEC-FIXED _CRECC-WORK-B CREC-ENCODE
        _CRECC-CB-NESTED-STATUS ! _CRECC-CB-NESTED-U !
    THEN
    CREC-S-OK ;

: _CRECC-VALIDATE-CB
  ( context-a context-u payload-a payload-u tag -- status )
    _CRECC-CB-CAPTURE
    1 _CRECC-CB-VALIDATE-N +!
    _CRECC-CB-VALIDATE-THROW @ IF -812 THROW THEN
    _CRECC-CB-VALIDATE-STATUS @ ?DUP IF EXIT THEN
    _CRECC-CB-CU @ _CRECC-CB-PU @ <> IF CREC-S-SEMANTIC EXIT THEN
    _CRECC-CB-PU @ IF
        _CRECC-CB-CA @ 0= IF CREC-S-SEMANTIC EXIT THEN
        _CRECC-CB-CA @ _CRECC-CB-CU @
            _CRECC-CB-PA @ _CRECC-CB-PU @ COMPARE IF
            CREC-S-SEMANTIC EXIT
        THEN
    THEN
    CREC-S-OK ;

