\ =====================================================================
\  token-set.f - Bounded, zeroizing OAuth 2 token ownership
\ =====================================================================

PROVIDED akashic-oauth2-token-set

REQUIRE ../../text/utf8.f

0 CONSTANT O2TOK-S-OK
1 CONSTANT O2TOK-S-INVALID
2 CONSTANT O2TOK-S-CAPACITY
3 CONSTANT O2TOK-S-ABSENT
4 CONSTANT O2TOK-S-BUSY
5 CONSTANT O2TOK-S-CALLBACK

8192 CONSTANT O2TOK-ACCESS-CAPACITY
4096 CONSTANT O2TOK-REFRESH-CAPACITY
8192 CONSTANT O2TOK-ID-CAPACITY

 0 CONSTANT _O2T-ACCESS-U
 8 CONSTANT _O2T-REFRESH-U
16 CONSTANT _O2T-ID-U
24 CONSTANT _O2T-EXPIRES-MS
32 CONSTANT _O2T-GENERATION
40 CONSTANT _O2T-USES
48 CONSTANT _O2T-BORROWED
56 CONSTANT _O2T-ACCESS
_O2T-ACCESS O2TOK-ACCESS-CAPACITY + CONSTANT _O2T-REFRESH
_O2T-REFRESH O2TOK-REFRESH-CAPACITY + CONSTANT _O2T-ID
_O2T-ID O2TOK-ID-CAPACITY + CONSTANT OAUTH2-TOKEN-SET-SIZE

: O2TOK.ACCESS-U    ( tokens -- a ) _O2T-ACCESS-U + ;
: O2TOK.REFRESH-U   ( tokens -- a ) _O2T-REFRESH-U + ;
: O2TOK.ID-U        ( tokens -- a ) _O2T-ID-U + ;
: O2TOK.EXPIRES-MS  ( tokens -- a ) _O2T-EXPIRES-MS + ;
: O2TOK.GENERATION  ( tokens -- a ) _O2T-GENERATION + ;
: O2TOK.USES        ( tokens -- a ) _O2T-USES + ;
: O2TOK.BORROWED    ( tokens -- a ) _O2T-BORROWED + ;
: O2TOK.ACCESS      ( tokens -- a ) _O2T-ACCESS + ;
: O2TOK.REFRESH     ( tokens -- a ) _O2T-REFRESH + ;
: O2TOK.ID          ( tokens -- a ) _O2T-ID + ;

: O2TOK-INIT  ( tokens -- ) OAUTH2-TOKEN-SET-SIZE 0 FILL ;

: O2TOK-PRESENT?  ( tokens -- flag )
    DUP O2TOK.ACCESS-U @ 0> SWAP O2TOK.REFRESH-U @ 0> AND ;

: O2TOK-CLEAR  ( tokens -- )
    DUP O2TOK.BORROWED @ IF DROP EXIT THEN
    DUP O2TOK.ACCESS O2TOK-ACCESS-CAPACITY 0 FILL
    DUP O2TOK.REFRESH O2TOK-REFRESH-CAPACITY 0 FILL
    DUP O2TOK.ID O2TOK-ID-CAPACITY 0 FILL
    DUP 0 SWAP O2TOK.ACCESS-U !
    DUP 0 SWAP O2TOK.REFRESH-U !
    DUP 0 SWAP O2TOK.ID-U !
    DUP 0 SWAP O2TOK.EXPIRES-MS !
    1 SWAP O2TOK.GENERATION +! ;

VARIABLE _O2TS-IA
VARIABLE _O2TS-IU
VARIABLE _O2TS-AA
VARIABLE _O2TS-AU
VARIABLE _O2TS-RA
VARIABLE _O2TS-RU
VARIABLE _O2TS-EXPIRES
VARIABLE _O2TS-T

: _O2TOK-TEXT?  ( addr len capacity -- flag )
    >R DUP 0= OVER R@ > OR IF R> DROP 2DROP 0 EXIT THEN
    OVER 0= IF R> DROP 2DROP 0 EXIT THEN
    UTF8-VALID? R> DROP ;

: O2TOK-SET  ( id-a id-u access-a access-u refresh-a refresh-u expires-ms tokens -- status )
    _O2TS-T ! _O2TS-EXPIRES ! _O2TS-RU ! _O2TS-RA !
    _O2TS-AU ! _O2TS-AA ! _O2TS-IU ! _O2TS-IA !
    _O2TS-T @ 0= IF O2TOK-S-INVALID EXIT THEN
    _O2TS-T @ O2TOK.BORROWED @ IF O2TOK-S-BUSY EXIT THEN
    _O2TS-IA @ _O2TS-IU @ O2TOK-ID-CAPACITY _O2TOK-TEXT? 0= IF
        O2TOK-S-CAPACITY EXIT
    THEN
    _O2TS-AA @ _O2TS-AU @ O2TOK-ACCESS-CAPACITY _O2TOK-TEXT? 0= IF
        O2TOK-S-CAPACITY EXIT
    THEN
    _O2TS-RA @ _O2TS-RU @ O2TOK-REFRESH-CAPACITY _O2TOK-TEXT? 0= IF
        O2TOK-S-CAPACITY EXIT
    THEN
    _O2TS-T @ O2TOK-CLEAR
    _O2TS-IA @ _O2TS-T @ O2TOK.ID _O2TS-IU @ CMOVE
    _O2TS-IU @ _O2TS-T @ O2TOK.ID-U !
    _O2TS-AA @ _O2TS-T @ O2TOK.ACCESS _O2TS-AU @ CMOVE
    _O2TS-AU @ _O2TS-T @ O2TOK.ACCESS-U !
    _O2TS-RA @ _O2TS-T @ O2TOK.REFRESH _O2TS-RU @ CMOVE
    _O2TS-RU @ _O2TS-T @ O2TOK.REFRESH-U !
    _O2TS-EXPIRES @ _O2TS-T @ O2TOK.EXPIRES-MS !
    O2TOK-S-OK ;

: O2TOK-UPDATE  ( id-a id-u access-a access-u refresh-a refresh-u expires-ms tokens -- status )
    _O2TS-T ! _O2TS-EXPIRES ! _O2TS-RU ! _O2TS-RA !
    _O2TS-AU ! _O2TS-AA ! _O2TS-IU ! _O2TS-IA !
    _O2TS-T @ 0= IF O2TOK-S-INVALID EXIT THEN
    _O2TS-T @ O2TOK.BORROWED @ IF O2TOK-S-BUSY EXIT THEN
    _O2TS-IU @ IF
        _O2TS-IA @ _O2TS-IU @ O2TOK-ID-CAPACITY _O2TOK-TEXT? 0= IF
            O2TOK-S-CAPACITY EXIT
        THEN
    THEN
    _O2TS-AU @ IF
        _O2TS-AA @ _O2TS-AU @ O2TOK-ACCESS-CAPACITY _O2TOK-TEXT? 0= IF
            O2TOK-S-CAPACITY EXIT
        THEN
    THEN
    _O2TS-RU @ IF
        _O2TS-RA @ _O2TS-RU @ O2TOK-REFRESH-CAPACITY _O2TOK-TEXT? 0= IF
            O2TOK-S-CAPACITY EXIT
        THEN
    THEN
    _O2TS-IU @ IF
        _O2TS-T @ O2TOK.ID O2TOK-ID-CAPACITY 0 FILL
        _O2TS-IA @ _O2TS-T @ O2TOK.ID _O2TS-IU @ CMOVE
        _O2TS-IU @ _O2TS-T @ O2TOK.ID-U !
    THEN
    _O2TS-AU @ IF
        _O2TS-T @ O2TOK.ACCESS O2TOK-ACCESS-CAPACITY 0 FILL
        _O2TS-AA @ _O2TS-T @ O2TOK.ACCESS _O2TS-AU @ CMOVE
        _O2TS-AU @ _O2TS-T @ O2TOK.ACCESS-U !
    THEN
    _O2TS-RU @ IF
        _O2TS-T @ O2TOK.REFRESH O2TOK-REFRESH-CAPACITY 0 FILL
        _O2TS-RA @ _O2TS-T @ O2TOK.REFRESH _O2TS-RU @ CMOVE
        _O2TS-RU @ _O2TS-T @ O2TOK.REFRESH-U !
    THEN
    _O2TS-EXPIRES @ IF
        _O2TS-EXPIRES @ _O2TS-T @ O2TOK.EXPIRES-MS !
    THEN
    1 _O2TS-T @ O2TOK.GENERATION +!
    O2TOK-S-OK ;

VARIABLE _O2TW-CB
VARIABLE _O2TW-CONTEXT
VARIABLE _O2TW-T
VARIABLE _O2TW-A
VARIABLE _O2TW-U
VARIABLE _O2TW-STATUS

: _O2TOK-WITH-INNER  ( -- )
    _O2TW-A @ _O2TW-U @ _O2TW-CONTEXT @ _O2TW-CB @ EXECUTE
    _O2TW-STATUS ! ;

: _O2TOK-WITH  ( callback callback-context tokens buffer-offset length-offset -- status )
    >R >R _O2TW-T ! _O2TW-CONTEXT ! _O2TW-CB !
    _O2TW-T @ 0= IF R> DROP R> DROP O2TOK-S-INVALID EXIT THEN
    _O2TW-T @ O2TOK.BORROWED @ IF R> DROP R> DROP O2TOK-S-BUSY EXIT THEN
    _O2TW-T @ R> + _O2TW-A !
    _O2TW-T @ R> + @ DUP _O2TW-U ! 0= IF O2TOK-S-ABSENT EXIT THEN
    -1 _O2TW-T @ O2TOK.BORROWED !
    0 _O2TW-STATUS !
    ['] _O2TOK-WITH-INNER CATCH IF O2TOK-S-CALLBACK _O2TW-STATUS ! THEN
    0 _O2TW-T @ O2TOK.BORROWED !
    1 _O2TW-T @ O2TOK.USES +!
    _O2TW-STATUS @ ;

: O2TOK-WITH-ACCESS  ( callback callback-context tokens -- status )
    _O2T-ACCESS _O2T-ACCESS-U _O2TOK-WITH ;

: O2TOK-WITH-REFRESH  ( callback callback-context tokens -- status )
    _O2T-REFRESH _O2T-REFRESH-U _O2TOK-WITH ;

: O2TOK-WITH-ID  ( callback callback-context tokens -- status )
    _O2T-ID _O2T-ID-U _O2TOK-WITH ;
