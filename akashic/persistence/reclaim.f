: _RCW-ACTIVE?  ( reclaim-work store pstore-work -- flag )
    >R
    OVER RECLAIM-WORK-VALID? 0= IF 2DROP R> DROP 0 EXIT THEN
    OVER _RCW.ACTIVE @ 0= IF 2DROP R> DROP 0 EXIT THEN
    OVER _RCW.FINALIZED @ IF 2DROP R> DROP 0 EXIT THEN
    OVER _RCW.STATUS @ PERSIST-S-OK <> IF 2DROP R> DROP 0 EXIT THEN
    OVER _RCW.RECLAIM @ _RCL.ACTIVE-WORK @ 2 PICK <> IF
        2DROP R> DROP 0 EXIT
    THEN
    OVER _RCW.STORE @ OVER <> IF 2DROP R> DROP 0 EXIT THEN
    OVER _RCW.PSTORE-WORK @ R@ <> IF 2DROP R> DROP 0 EXIT THEN
    DUP R@ PSTORE-TX-READY? >R 2DROP R> R> DROP ;

\ Once an operation has passed the active-owner gate, any local failure makes
\ the shared PSTORE proposal uncommittable as well as stopping reclaim work.
\ This is a memory-only handoff; callers retain the original layered status
\ and must abort PSTORE before releasing the reclaim owner.
: _RCW-SET-STATUS  ( status work -- status )
    >R
    DUP IF
        DUP R@ _RCW.STORE @ R@ _RCW.PSTORE-WORK @
            PSTORE-TX-POISON DROP
    THEN
    DUP R@ _RCW.STATUS !
    R> DROP ;

: _RCW-PROPOSED-PAGE-COUNT  ( work -- count|-1 )
    _RCW.PSTORE-WORK @ PSTORE-PROPOSED-ROOT@
    DUP IF PROOTV.PAGE-COUNT @ ELSE DROP -1 THEN ;

: _RCW-READ-BUCKET  ( page-id work -- status )
    >R
    DUP R@ _RCW.STORE @ R@ _RCW.PSTORE-WORK @ PSTORE-READ-PAGE-TX
    DUP IF NIP R> DROP EXIT THEN DROP
    R@ _RCW.PSTORE-WORK @ PSTORE-PAGE-PAYLOAD$
    DUP PERSIST-PAGE-PAYLOAD-SIZE <> IF
        2DROP DROP R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    DROP R@ _RCW.BUCKET PERSIST-PAGE-PAYLOAD-SIZE MOVE
    R@ _RCW.BUCKET R@ _RCW-PROPOSED-PAGE-COUNT
        _RECLAIM-BUCKET-VALID? 0= IF
        DROP R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    DROP R> DROP PERSIST-S-OK ;

: _RCW-WRITE-OUTPUT  ( page-id work -- status )
    >R
    R@ _RCW.OUTPUT PERSIST-PAGE-PAYLOAD-SIZE ROT
    R@ _RCW.STORE @ R@ _RCW.PSTORE-WORK @ PSTORE-WRITE-PAGE-TX
    R> DROP ;

: _RCW-STAGED-CONTAINS?  ( page-id work -- flag )
