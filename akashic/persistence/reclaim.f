: _RECLAIM-STATE-EMPTY  ( state -- )
    DUP RECLAIM-STATE-SIZE 0 FILL
    _RECLAIM-STATE-MAGIC OVER _RCS.MAGIC !
    -1 OVER _RCS.IN-HEAD !
    -1 OVER _RCS.OUT-HEAD !
    -1 OVER _RCS.ROTATE-SOURCE !
