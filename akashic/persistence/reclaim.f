: RECLAIM-RETIRED-COUNT@  ( reclaim -- count|-1 )
    DUP RECLAIM-VALID? IF _RCL.STATE _RCS.RETIRED-COUNT @ ELSE DROP -1 THEN ;

: RECLAIM-REUSABLE-COUNT@  ( reclaim -- count|-1 )
    DUP RECLAIM-VALID? IF _RCL.STATE _RCS.REUSABLE-COUNT @ ELSE DROP -1 THEN ;

: _RECLAIM-STORE-FENCE  ( store -- generation|0 )
    DUP PSTORE-ROOT-FILE@ DUP 0= IF 2DROP 0 EXIT THEN
    >R DROP
    GPAIR-SLOT-A R@ PROOT-SLOT-GENERATION@
    GPAIR-SLOT-B R> PROOT-SLOT-GENERATION@
    2DUP 0= SWAP 0= OR IF 2DROP 0 EXIT THEN
    MIN ;

: RECLAIM-FENCE@  ( reclaim -- generation|0 )
    DUP RECLAIM-VALID? 0= IF DROP 0 EXIT THEN
    _RCL.STORE @ ?DUP IF _RECLAIM-STORE-FENCE ELSE 0 THEN ;

: _RECLAIM-TX-ALIASES?  ( store pstore-work reclaim reclaim-work -- flag )
    >R
    R@ RECLAIM-WORK-SIZE 4 PICK PSTORE-SPAN-DISJOINT? 0= IF
        _RECLAIM-DROP3 R> DROP 0 EXIT
    THEN
    DUP RECLAIM-SIZE 4 PICK PSTORE-SPAN-DISJOINT? 0= IF
        _RECLAIM-DROP3 R> DROP 0 EXIT
    THEN
    1 PICK PSTORE-WORK-SIZE R@ RECLAIM-WORK-SIZE MSPAN-OVERLAP? IF
        _RECLAIM-DROP3 R> DROP 0 EXIT
    THEN
    DUP RECLAIM-SIZE R@ RECLAIM-WORK-SIZE MSPAN-OVERLAP? IF
        _RECLAIM-DROP3 R> DROP 0 EXIT
    THEN
    1 PICK PSTORE-WORK-SIZE 2 PICK RECLAIM-SIZE MSPAN-OVERLAP? IF
        _RECLAIM-DROP3 R> DROP 0 EXIT
    THEN
    _RECLAIM-DROP3 R> DROP -1 ;

: _RECLAIM-TX-ISSUED-COUNT  ( store pstore-work -- count status )
    >R
    DUP PSTORE-CURRENT-ROOT@ DUP 0= IF
        2DROP R> DROP -1 PERSIST-S-CONFLICT EXIT
    THEN
    PROOTV.PAGE-COUNT @
    R@ PSTORE-PROPOSED-ROOT@ DUP 0= IF
        DROP 2DROP R> DROP -1 PERSIST-S-BUSY EXIT
    THEN
    PROOTV.PAGE-COUNT @
    R> DROP ROT DROP
    2DUP > IF 2DROP -1 PERSIST-S-CONFLICT EXIT THEN
    SWAP -
    DUP RECLAIM-ALLOCATED-MAX > IF
        DROP -1 PERSIST-S-CAPACITY EXIT
    THEN
    PERSIST-S-OK ;

: _RCW-SEED-ISSUED  ( base count work -- )
    >R
    DUP R@ _RCW.ALLOCATED-COUNT !
    0 ?DO
        DUP I + I R@ _RCW.ALLOCATED-ENTRY !
    LOOP
    DROP R> DROP ;

: RECLAIM-TX-BEGIN  ( store pstore-work reclaim reclaim-work -- status )
    >R
    2 PICK 2 PICK 2 PICK R@ _RECLAIM-TX-ALIASES? 0= IF
        _RECLAIM-DROP3 R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ RECLAIM-WORK-VALID? 0= IF
        _RECLAIM-DROP3 R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _RCW.ACTIVE @ IF _RECLAIM-DROP3 R> DROP PERSIST-S-BUSY EXIT THEN
    DUP RECLAIM-VALID? 0= IF _RECLAIM-DROP3 R> DROP PERSIST-S-INVALID EXIT THEN
    DUP _RCL.ACTIVE-WORK @ IF
        _RECLAIM-DROP3 R> DROP PERSIST-S-BUSY EXIT
    THEN
    2 PICK PSTORE-VALID? 0= IF _RECLAIM-DROP3 R> DROP PERSIST-S-INVALID EXIT THEN
    1 PICK PSTORE-WORK-VALID? 0= IF
        _RECLAIM-DROP3 R> DROP PERSIST-S-INVALID EXIT
    THEN
    2 PICK OVER _RCL.STORE @ <> IF
        _RECLAIM-DROP3 R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    2 PICK PSTORE-GENERATION@ OVER _RCL.GENERATION @ <> IF
        _RECLAIM-DROP3 R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    2 PICK 2 PICK PSTORE-TX-READY? 0= IF
        _RECLAIM-DROP3 R> DROP PERSIST-S-BUSY EXIT
    THEN
    2 PICK PSTORE-GENERATION@ 1 _RECLAIM-ADD? 0= IF
        DROP _RECLAIM-DROP3 R> DROP PERSIST-S-CAPACITY EXIT
    THEN DROP
    2 PICK 2 PICK _RECLAIM-TX-ISSUED-COUNT
    DUP IF
        >R DROP _RECLAIM-DROP3 R> R> DROP EXIT
    THEN
    2DROP
    DUP R@ _RCW.RECLAIM !
    2 PICK R@ _RCW.STORE !
    1 PICK R@ _RCW.PSTORE-WORK !
    DUP _RCL.STATE R@ _RCW.STATE RECLAIM-STATE-SIZE MOVE
    2 PICK PSTORE-GENERATION@ 1 _RECLAIM-ADD? DROP
    R@ _RCW.NEXT-GENERATION !
    2 PICK PSTORE-CURRENT-ROOT@ PROOTV.PAGE-COUNT @ DUP
        R@ _RCW.BASE-PAGE-COUNT !
    2 PICK PSTORE-PROPOSED-ROOT@ PROOTV.PAGE-COUNT @ OVER -
        R@ _RCW-SEED-ISSUED
    0 R@ _RCW.STAGED-COUNT !
    0 R@ _RCW.DISCARD-COUNT !
    0 R@ _RCW.FINALIZED !
    -1 R@ _RCW.ACTIVE !
    PERSIST-S-OK R@ _RCW.STATUS !
    R@ OVER _RCL.ACTIVE-WORK !
    _RECLAIM-DROP3 R> DROP PERSIST-S-OK ;

