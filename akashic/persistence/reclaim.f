: _RCW-OUTPUT-CONTAINS?  ( page-id work -- flag )
    DUP _RCW.OUTPUT _RCB.COUNT @ 0 ?DO
        OVER I 2 PICK _RCW.OUTPUT _RCB.ENTRY @ = IF
            2DROP -1 UNLOOP EXIT
        THEN
    LOOP
    2DROP 0 ;

: _RCW-START-ROTATION  ( work -- )
    DUP _RCW.STATE _RCS.IN-HEAD @ OVER _RCW.STATE _RCS.ROTATE-SOURCE !
    -1 OVER _RCW.STATE _RCS.IN-HEAD !
    -1 OVER _RCW.STATE _RCS.ROTATE-BUILD !
    -1 SWAP _RCW.STATE _RCS.ROTATING ! ;

: _RCW-ROTATE-ONE  ( work -- moved status )
    >R
    R@ _RCW.STATE _RCS.ROTATE-SOURCE @ DUP 0< IF
        DROP R> DROP 0 PERSIST-S-CORRUPT EXIT
    THEN
    DUP R@ _RCW.SOURCE !
    R@ _RCW-READ-BUCKET DUP IF
        >R 0 R> R> DROP EXIT
    THEN DROP
    R@ _RCW.BUCKET _RCB.KIND @ _RECLAIM-BUCKET-PENDING <> IF
        R> DROP 0 PERSIST-S-CORRUPT EXIT
    THEN
    \ The allocator also uses BUCKET to inspect a reusable-id bucket.  Save
    \ the pending source before allocating so ready-backed metadata reuse
    \ cannot replace the payload that rotation is meant to copy.
    R@ _RCW.BUCKET R@ _RCW.OUTPUT PERSIST-PAGE-PAYLOAD-SIZE MOVE
    R@ _RCW.BUCKET _RCB.NEXT @ R@ _RCW.MOVED !
    R@ _RCW.SOURCE @ R@ _RCW-OUTPUT-CONTAINS? IF
        R> DROP 0 PERSIST-S-CORRUPT EXIT
    THEN
    1 R@ _RCW-ALLOCATE-ID
    DUP IF >R DROP 0 R> R> DROP EXIT THEN DROP
    DUP R@ _RCW.RESULT ! DROP
    R@ _RCW.RESULT @ R@ _RCW.SOURCE @ = IF
        R> DROP 0 PERSIST-S-CORRUPT EXIT
    THEN
    R@ _RCW.RESULT @ R@ _RCW-OUTPUT-CONTAINS? IF
        R> DROP 0 PERSIST-S-CORRUPT EXIT
    THEN
    R@ _RCW.STATE _RCS.ROTATE-BUILD @
        R@ _RCW.OUTPUT _RCB.NEXT !
    R@ _RCW.RESULT @ R@ _RCW-WRITE-OUTPUT DUP IF
        R> DROP 0 SWAP EXIT
    THEN DROP
    R@ _RCW.RESULT @ R@ _RCW.STATE _RCS.ROTATE-BUILD !
    R@ _RCW.MOVED @
        R@ _RCW.STATE _RCS.ROTATE-SOURCE !
    R@ _RCW.SOURCE @ R@ _RCW-STAGE-ID DUP IF
        R> DROP 0 SWAP EXIT
    THEN DROP
    R@ _RCW.STATE _RCS.ROTATE-SOURCE @ -1 = IF
        R@ _RCW.STATE _RCS.ROTATE-BUILD @
            R@ _RCW.STATE _RCS.OUT-HEAD !
        0 R@ _RCW.STATE _RCS.OUT-INDEX !
        -1 R@ _RCW.STATE _RCS.ROTATE-SOURCE !
        -1 R@ _RCW.STATE _RCS.ROTATE-BUILD !
        0 R@ _RCW.STATE _RCS.ROTATING !
    THEN
    R> DROP 0 PERSIST-S-OK ;

: _RCW-COPY-PROMOTION  ( work -- )
    DUP _RCW.MOVED @ 0 ?DO
        DUP _RCW.STATE _RCS.OUT-INDEX @ I +
        OVER _RCW.BUCKET _RCB.ENTRY @
        I 2 PICK _RCW.MOVE-ENTRY !
    LOOP
    DROP ;

: _RCW-PROMOTE  ( requested work -- moved status )
    >R
    R@ _RCW.STATE _RCS.READY-HEAD @ -1 = IF
        R@ _RCW.STATE _RCS.READY-INDEX @ IF
            DROP R> DROP 0 PERSIST-S-CORRUPT EXIT
        THEN
        R@ _RCW.STATE _RCS.REUSABLE-COUNT @ IF
            DROP R> DROP 0 PERSIST-S-CORRUPT EXIT
        THEN
    ELSE
        R@ _RCW.STATE _RCS.REUSABLE-COUNT @ 0> 0= IF
            DROP R> DROP 0 PERSIST-S-CORRUPT EXIT
        THEN
    THEN
    \ Treat requested as a ready-population low-water as well as the maximum
    \ number of ids moved by this call.  A partial ready bucket must not stall
    \ maintenance while one new pending bucket is finalized per transaction.
    R@ _RCW.STATE _RCS.REUSABLE-COUNT @ OVER >= IF
        DROP R> DROP 0 PERSIST-S-OK EXIT
    THEN
    R@ _RCW.STATE _RCS.OUT-HEAD @ DUP 0< IF
        DROP DROP R> DROP 0 PERSIST-S-OK EXIT
    THEN
    DUP R@ _RCW.SOURCE !
    R@ _RCW-READ-BUCKET DUP IF
        >R DROP 0 R> R> DROP EXIT
    THEN DROP
    R@ _RCW.BUCKET _RCB.KIND @ _RECLAIM-BUCKET-PENDING <> IF
        DROP R> DROP 0 PERSIST-S-CORRUPT EXIT
    THEN
    R@ _RCW.STORE @ _RECLAIM-STORE-FENCE DUP 0= IF
        2DROP R> DROP 0 PERSIST-S-OK EXIT
    THEN
    R@ _RCW.BUCKET _RCB.GENERATION @ < IF
        DROP R> DROP 0 PERSIST-S-OK EXIT
    THEN
    R@ _RCW.STATE _RCS.OUT-INDEX @ DUP
        R@ _RCW.BUCKET _RCB.COUNT @ >= IF
        DROP DROP R> DROP 0 PERSIST-S-CORRUPT EXIT
    THEN
    R@ _RCW.BUCKET _RCB.COUNT @ SWAP - MIN
    DUP R@ _RCW.MOVED !
    R@ _RCW.STATE _RCS.RETIRED-COUNT @ OVER < IF
        DROP R> DROP 0 PERSIST-S-CORRUPT EXIT
    THEN
    DROP
    R@ _RCW-COPY-PROMOTION
    \ Allocation may inspect a ready bucket in the shared BUCKET scratch.
    \ Preserve the pending source until its count and next link are captured.
    R@ _RCW.BUCKET R@ _RCW.OUTPUT PERSIST-PAGE-PAYLOAD-SIZE MOVE
    R@ _RCW.SOURCE @ R@ _RCW-OUTPUT-CONTAINS? IF
        R> DROP 0 PERSIST-S-CORRUPT EXIT
    THEN
    R@ _RCW.STATE _RCS.OUT-INDEX @ R@ _RCW.MOVED @ +
        R@ _RCW.OUTPUT _RCB.COUNT @ = IF 1 ELSE 0 THEN
    R@ _RCW-ALLOCATE-ID
    DUP IF >R DROP 0 R> R> DROP EXIT THEN DROP
    DUP R@ _RCW.RESULT ! DROP
    R@ _RCW.RESULT @ R@ _RCW.SOURCE @ = IF
        R> DROP 0 PERSIST-S-CORRUPT EXIT
    THEN
    R@ _RCW.RESULT @ R@ _RCW-OUTPUT-CONTAINS? IF
        R> DROP 0 PERSIST-S-CORRUPT EXIT
    THEN
    R@ _RCW.OUTPUT _RCB.COUNT @ R@ _RCW.TEMP !
    R@ _RCW.OUTPUT _RCB.NEXT @ R@ _RCW.REQUESTED !
    _RECLAIM-BUCKET-READY 0 R@ _RCW.MOVED @
        R@ _RCW.STATE _RCS.READY-HEAD @ R@
        _RCW-OUTPUT-HEADER
    R@ _RCW.STATE _RCS.READY-INDEX @
        R@ _RCW.OUTPUT _RCB.NEXT-INDEX !
    R@ _RCW.MOVED @ R@ _RCW-MOVE>OUTPUT
    R@ _RCW.RESULT @ R@ _RCW-WRITE-OUTPUT DUP IF
        R> DROP 0 SWAP EXIT
    THEN DROP
    R@ _RCW.MOVED @ R@ _RCW.STATE _RCS.REUSABLE-COUNT @
        _RECLAIM-ADD? 0= IF
        DROP R> DROP 0 PERSIST-S-CAPACITY EXIT
    THEN
    R@ _RCW.STATE _RCS.REUSABLE-COUNT !
    R@ _RCW.MOVED @ NEGATE R@ _RCW.STATE _RCS.RETIRED-COUNT +!
    R@ _RCW.RESULT @ R@ _RCW.STATE _RCS.READY-HEAD !
    0 R@ _RCW.STATE _RCS.READY-INDEX !
    R@ _RCW.STATE _RCS.OUT-INDEX @ R@ _RCW.MOVED @ +
        R@ _RCW.TEMP @ = IF
        R@ _RCW.REQUESTED @ R@ _RCW.STATE _RCS.OUT-HEAD !
        0 R@ _RCW.STATE _RCS.OUT-INDEX !
        R@ _RCW.SOURCE @ R@ _RCW-STAGE-ID DUP IF
            R> DROP 0 SWAP EXIT
        THEN DROP
    ELSE
        R@ _RCW.MOVED @ R@ _RCW.STATE _RCS.OUT-INDEX +!
    THEN
    R@ _RCW.MOVED @ PERSIST-S-OK R> DROP ;

: _RCW-STEP-END  ( max-pages work moved status -- moved status )
    >R OVER R> SWAP _RCW-SET-STATUS
    2SWAP 2DROP ;

: RECLAIM-STEP  ( max-pages reclaim-work -- moved status )
    DUP RECLAIM-WORK-VALID? 0= IF
        2DROP 0 PERSIST-S-INVALID EXIT
    THEN
    DUP DUP _RCW.STORE @ 2 PICK _RCW.PSTORE-WORK @
        _RCW-ACTIVE? 0= IF
        2DROP 0 PERSIST-S-BUSY EXIT
    THEN
    OVER DUP 0< SWAP RECLAIM-MAX-BATCH > OR IF
        0 PERSIST-S-INVALID _RCW-STEP-END EXIT
    THEN
    OVER 0= IF 0 PERSIST-S-OK _RCW-STEP-END EXIT THEN
    DUP _RCW.STATE _RCS.OUT-HEAD @ -1 = IF
        DUP _RCW.STATE _RCS.ROTATING @ 0= IF
            DUP _RCW.STATE _RCS.IN-HEAD @ -1 = IF
                0 PERSIST-S-OK _RCW-STEP-END EXIT
            THEN
            DUP _RCW-START-ROTATION
        THEN
        DUP _RCW-ROTATE-ONE
        _RCW-STEP-END EXIT
    THEN
    OVER OVER _RCW-PROMOTE
    _RCW-STEP-END ;

\ ---------------------------------------------------------------------
\ Proposal finalization, state export, adoption, and abort
\ ---------------------------------------------------------------------

: _RCW-FINAL-END  ( work status -- status )
    SWAP _RCW-SET-STATUS ;

: _RCW-RESTORE-STATE  ( work -- )
    DUP _RCW.SAVED-STATE OVER _RCW.STATE RECLAIM-STATE-SIZE MOVE DROP ;

: _RCW-METADATA-ID  ( reserve work -- page-id status )
    >R
    DUP R@ _RCW-ALLOCATE-ID
    DUP PERSIST-S-CAPACITY <> IF
        >R NIP R> R> DROP EXIT
    THEN
    2DROP DROP
    R@ _RCW-PROPOSED-PAGE-COUNT DUP 0< IF
        DROP R> DROP -1 PERSIST-S-CONFLICT EXIT
    THEN
    DUP R@ _RCW-ALLOCATED-CONTAINS? IF
        DROP R> DROP -1 PERSIST-S-CONFLICT EXIT
    THEN
    PERSIST-S-OK R> DROP ;

: _RCW-DISCARD-CHUNKS  ( work -- count )
    _RCW.DISCARD-COUNT @ DUP 0= IF EXIT THEN
    RECLAIM-MAX-BATCH 1- + RECLAIM-MAX-BATCH / ;

: _RCW-DISCARD>OUTPUT  ( start count work -- )
    1 PICK 0 ?DO
        I 3 PICK + 1 PICK _RCW.DISCARDED-ENTRY @
        I 2 PICK _RCW.OUTPUT _RCB.ENTRY !
    LOOP
    _RECLAIM-DROP3 ;

: _RCW-BUILD-DISCARD-CHUNK  ( chunk work -- )
    >R
    RECLAIM-MAX-BATCH * DUP R@ _RCW.SOURCE !
    R@ _RCW.DISCARD-COUNT @ SWAP - RECLAIM-MAX-BATCH MIN
    DUP R@ _RCW.MOVED !
    DROP
    _RECLAIM-BUCKET-READY 0 R@ _RCW.MOVED @ -1 R@
        _RCW-OUTPUT-HEADER
    R@ _RCW.SOURCE @ R@ _RCW.MOVED @ R@ _RCW-DISCARD>OUTPUT
    R> DROP ;

: _RCW-PREPARE-DISCARDS  ( work -- status )
    DUP _RCW-DISCARD-CHUNKS 0 ?DO
        0 OVER _RCW-METADATA-ID
        DUP IF >R DROP DROP R> UNLOOP EXIT THEN DROP
        DUP I 3 PICK _RCW.DISCARD-META-ENTRY ! DROP
        I OVER _RCW-BUILD-DISCARD-CHUNK
        I OVER _RCW.DISCARD-META-ENTRY @ OVER _RCW-WRITE-OUTPUT
        DUP IF NIP UNLOOP EXIT THEN DROP
    LOOP
    DROP PERSIST-S-OK ;

: _RCW-STAGED-RANGE>OUTPUT  ( start count work -- )
    1 PICK 0 ?DO
        I 3 PICK + 1 PICK _RCW.STAGED-ENTRY @
        I 2 PICK _RCW.OUTPUT _RCB.ENTRY !
    LOOP
    _RECLAIM-DROP3 ;

: _RCW-BUILD-PENDING-CHUNK  ( chunk work -- )
    >R
    RECLAIM-MAX-BATCH * DUP R@ _RCW.SOURCE !
    R@ _RCW.STAGED-COUNT @ SWAP - RECLAIM-MAX-BATCH MIN
    DUP R@ _RCW.MOVED ! DROP
    _RECLAIM-BUCKET-PENDING R@ _RCW.NEXT-GENERATION @
        R@ _RCW.MOVED @ -1 R@ _RCW-OUTPUT-HEADER
    R@ _RCW.SOURCE @ R@ _RCW.MOVED @ R@ _RCW-STAGED-RANGE>OUTPUT
    R> DROP ;

: _RCW-PREPARE-PENDING  ( work -- status )
    DUP _RCW.STAGED-COUNT @ 0= IF DROP PERSIST-S-OK EXIT THEN
    0 OVER _RCW-METADATA-ID
    DUP IF >R DROP R> NIP EXIT THEN DROP
    DUP 0 3 PICK _RCW.PENDING-META-ENTRY ! DROP
    0 OVER _RCW-BUILD-PENDING-CHUNK
    0 OVER _RCW.PENDING-META-ENTRY @ OVER _RCW-WRITE-OUTPUT
    DUP IF NIP EXIT THEN DROP
    DUP _RCW.STAGED-COUNT @ RECLAIM-MAX-BATCH > IF
        0 OVER _RCW-METADATA-ID
        DUP IF >R DROP R> NIP EXIT THEN DROP
        DUP 1 3 PICK _RCW.PENDING-META-ENTRY ! DROP
        1 OVER _RCW-BUILD-PENDING-CHUNK
        1 OVER _RCW.PENDING-META-ENTRY @ OVER _RCW-WRITE-OUTPUT
        DUP IF NIP EXIT THEN DROP
    THEN
    DROP PERSIST-S-OK ;

: _RCW-LINK-PENDING  ( work -- status )
    DUP _RCW.STAGED-COUNT @ 0= IF DROP PERSIST-S-OK EXIT THEN
    DUP _RCW.STATE _RCS.RETIRED-COUNT @
        OVER _RCW.STAGED-COUNT @ _RECLAIM-ADD? 0= IF
        2DROP PERSIST-S-CAPACITY EXIT
    THEN
    OVER _RCW.TEMP !
    DUP _RCW.STATE _RCS.IN-HEAD @ OVER _RCW.REQUESTED !
    DUP _RCW.STAGED-COUNT @ RECLAIM-MAX-BATCH > IF
        1 OVER _RCW-BUILD-PENDING-CHUNK
        DUP _RCW.REQUESTED @ OVER _RCW.OUTPUT _RCB.NEXT !
        1 OVER _RCW.PENDING-META-ENTRY @ OVER _RCW-WRITE-OUTPUT
        DUP IF NIP EXIT THEN DROP
        0 OVER _RCW-BUILD-PENDING-CHUNK
        1 OVER _RCW.PENDING-META-ENTRY @ OVER _RCW.OUTPUT _RCB.NEXT !
    ELSE
        0 OVER _RCW-BUILD-PENDING-CHUNK
        DUP _RCW.REQUESTED @ OVER _RCW.OUTPUT _RCB.NEXT !
    THEN
    0 OVER _RCW.PENDING-META-ENTRY @ OVER _RCW-WRITE-OUTPUT
    DUP IF NIP EXIT THEN DROP
    0 OVER _RCW.PENDING-META-ENTRY @ OVER _RCW.STATE _RCS.IN-HEAD !
    DUP _RCW.TEMP @ OVER _RCW.STATE _RCS.RETIRED-COUNT !
    0 SWAP _RCW.STAGED-COUNT !
    PERSIST-S-OK ;

: _RCW-FINALIZE-PENDING  ( work -- status )
    DUP _RCW-PREPARE-PENDING
    DUP IF NIP EXIT THEN DROP
    _RCW-LINK-PENDING ;

: _RCW-LINK-DISCARDS  ( work -- status )
    DUP _RCW.DISCARD-COUNT @ 0= IF DROP PERSIST-S-OK EXIT THEN
    DUP _RCW.STATE _RCS.READY-HEAD @ OVER _RCW.REQUESTED !
    DUP _RCW.STATE _RCS.READY-INDEX @ OVER _RCW.RESULT !
    DUP _RCW-DISCARD-CHUNKS 2 = IF
        1 OVER _RCW-BUILD-DISCARD-CHUNK
        DUP _RCW.REQUESTED @ OVER _RCW.OUTPUT _RCB.NEXT !
        DUP _RCW.RESULT @ OVER _RCW.OUTPUT _RCB.NEXT-INDEX !
        1 OVER _RCW.DISCARD-META-ENTRY @ OVER _RCW-WRITE-OUTPUT
        DUP IF NIP EXIT THEN DROP
        0 OVER _RCW-BUILD-DISCARD-CHUNK
        1 OVER _RCW.DISCARD-META-ENTRY @ OVER _RCW.OUTPUT _RCB.NEXT !
        0 OVER _RCW.OUTPUT _RCB.NEXT-INDEX !
    ELSE
        0 OVER _RCW-BUILD-DISCARD-CHUNK
        DUP _RCW.REQUESTED @ OVER _RCW.OUTPUT _RCB.NEXT !
        DUP _RCW.RESULT @ OVER _RCW.OUTPUT _RCB.NEXT-INDEX !
    THEN
    0 OVER _RCW.DISCARD-META-ENTRY @ OVER _RCW-WRITE-OUTPUT
    DUP IF NIP EXIT THEN DROP
    0 OVER _RCW.DISCARD-META-ENTRY @ OVER _RCW.STATE _RCS.READY-HEAD !
    0 OVER _RCW.STATE _RCS.READY-INDEX !
    DUP _RCW.STATE _RCS.REUSABLE-COUNT @
        OVER _RCW.DISCARD-COUNT @ _RECLAIM-ADD? 0= IF
        2DROP PERSIST-S-CAPACITY EXIT
    THEN
    OVER _RCW.STATE _RCS.REUSABLE-COUNT !
    0 SWAP _RCW.DISCARD-COUNT !
    PERSIST-S-OK ;

: RECLAIM-FINALIZE  ( reclaim-work -- status )
    DUP RECLAIM-WORK-VALID? 0= IF
        DROP PERSIST-S-INVALID EXIT
    THEN
    DUP DUP _RCW.STORE @ 2 PICK _RCW.PSTORE-WORK @
        _RCW-ACTIVE? 0= IF
        DROP PERSIST-S-BUSY EXIT
    THEN
    DUP _RCW.STATE OVER _RCW.SAVED-STATE RECLAIM-STATE-SIZE MOVE
    DUP _RCW-PREPARE-DISCARDS
    DUP IF OVER _RCW-RESTORE-STATE _RCW-FINAL-END EXIT THEN DROP
    DUP _RCW-FINALIZE-PENDING
    DUP IF OVER _RCW-RESTORE-STATE _RCW-FINAL-END EXIT THEN DROP
    DUP _RCW-LINK-DISCARDS
    DUP IF OVER _RCW-RESTORE-STATE _RCW-FINAL-END EXIT THEN DROP
    -1 OVER _RCW.FINALIZED !
    PERSIST-S-OK _RCW-FINAL-END ;

: RECLAIM-PROPOSED-STATE$  ( reclaim-work -- state-a state-u )
    DUP RECLAIM-WORK-VALID? 0= IF DROP 0 0 EXIT THEN
    DUP _RCW.ACTIVE @ OVER _RCW.FINALIZED @ AND 0= IF DROP 0 0 EXIT THEN
    DUP _RCW.STATUS @ PERSIST-S-OK <> IF DROP 0 0 EXIT THEN
    _RCW.STATE RECLAIM-STATE-SIZE ;

: RECLAIM-STATE$  ( reclaim -- state-a state-u )
    DUP RECLAIM-VALID? IF _RCL.STATE RECLAIM-STATE-SIZE ELSE DROP 0 0 THEN ;

: RECLAIM-STATE!  ( destination-a destination-u reclaim-work -- status )
    >R
    R@ RECLAIM-WORK-VALID? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _RCW.ACTIVE @ R@ _RCW.FINALIZED @ AND 0= IF
        2DROP R> DROP PERSIST-S-BUSY EXIT
    THEN
    R@ _RCW.STATUS @ PERSIST-S-OK <> IF
        2DROP R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    DUP RECLAIM-STATE-SIZE <> IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    OVER DUP 0= IF DROP 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    RECLAIM-STATE-SIZE MSPAN-NONWRAPPING? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    OVER RECLAIM-STATE-SIZE R@ RECLAIM-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    OVER RECLAIM-STATE-SIZE R@ _RCW.RECLAIM @ RECLAIM-SIZE
        MSPAN-OVERLAP? IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    OVER RECLAIM-STATE-SIZE R@ _RCW.STORE @
        PSTORE-SPAN-DISJOINT? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DROP R@ _RCW.STATE SWAP RECLAIM-STATE-SIZE MOVE
    R> DROP PERSIST-S-OK ;

: _RCW-CLEAR  ( work -- )
    0 OVER _RCW.ACTIVE !
    0 OVER _RCW.FINALIZED !
    0 OVER _RCW.RECLAIM !
    0 OVER _RCW.STORE !
    0 OVER _RCW.PSTORE-WORK !
    0 OVER _RCW.NEXT-GENERATION !
    0 OVER _RCW.BASE-PAGE-COUNT !
    0 OVER _RCW.STAGED-COUNT !
    0 OVER _RCW.ALLOCATED-COUNT !
    0 OVER _RCW.DISCARD-COUNT !
    PERSIST-S-OK SWAP _RCW.STATUS ! ;

: RECLAIM-ADOPT  ( reclaim-work reclaim -- status )
    >R
    DUP RECLAIM-WORK-VALID? 0= IF
        DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ RECLAIM-VALID? 0= IF DROP R> DROP PERSIST-S-INVALID EXIT THEN
    DUP _RCW.ACTIVE @ OVER _RCW.FINALIZED @ AND 0= IF
        DROP R> DROP PERSIST-S-BUSY EXIT
    THEN
    DUP _RCW.STATUS @ PERSIST-S-OK <> IF
        DROP R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    DUP _RCW.RECLAIM @ R@ <> IF
        DROP R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    R@ _RCL.ACTIVE-WORK @ OVER <> IF
        DROP R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    DUP _RCW.STORE @ R@ _RCL.STORE @ <> IF
        DROP R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    DUP _RCW.PSTORE-WORK @ PSTORE-PROPOSED-ROOT@ IF
        DROP R> DROP PERSIST-S-BUSY EXIT
    THEN
    DUP _RCW.STORE @ PSTORE-GENERATION@
        OVER _RCW.NEXT-GENERATION @ <> IF
        DROP R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    DUP _RCW.STATE OVER _RCW.STORE @ PSTORE-CURRENT-ROOT@
        PROOTV.PAGE-COUNT @ _RECLAIM-STATE-BOUNDS? 0= IF
        DROP R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    DUP _RCW.STATE R@ _RCL.STATE RECLAIM-STATE-SIZE MOVE
    DUP _RCW.NEXT-GENERATION @ R@ _RCL.GENERATION !
    PERSIST-S-OK R@ _RCL.STATUS !
    0 R@ _RCL.ACTIVE-WORK !
    _RCW-CLEAR R> DROP PERSIST-S-OK ;

: RECLAIM-ABORT  ( reclaim-work -- status )
    DUP RECLAIM-WORK-VALID? 0= IF DROP PERSIST-S-INVALID EXIT THEN
    DUP _RCW.ACTIVE @ 0= IF DROP PERSIST-S-BUSY EXIT THEN
    DUP _RCW.RECLAIM @ _RCL.ACTIVE-WORK @ OVER <> IF
        DROP PERSIST-S-CONFLICT EXIT
    THEN
    DUP _RCW.PSTORE-WORK @ PSTORE-PROPOSED-ROOT@ IF
        DROP PERSIST-S-BUSY EXIT
    THEN
    DUP _RCW.RECLAIM @ 0 SWAP _RCL.ACTIVE-WORK !
    _RCW-CLEAR PERSIST-S-OK ;
