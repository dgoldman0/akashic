        OVER I 2 PICK _RCW.STAGED-ENTRY @ = IF
            2DROP -1 UNLOOP EXIT
        THEN
    LOOP
    2DROP 0 ;

: _RCW-ALLOCATED-CONTAINS?  ( page-id work -- flag )
    DUP _RCW.ALLOCATED-COUNT @ 0 ?DO
        OVER I 2 PICK _RCW.ALLOCATED-ENTRY @ = IF
            2DROP -1 UNLOOP EXIT
        THEN
    LOOP
    2DROP 0 ;

: _RCW-DISCARDED-CONTAINS?  ( page-id work -- flag )
    DUP _RCW.DISCARD-COUNT @ 0 ?DO
        OVER I 2 PICK _RCW.DISCARDED-ENTRY @ = IF
            2DROP -1 UNLOOP EXIT
        THEN
    LOOP
    2DROP 0 ;

: _RCW-STAGE-ID  ( page-id work -- status )
    >R
    DUP 0< IF DROP R> DROP PERSIST-S-INVALID EXIT THEN
    R@ _RCW.STAGED-COUNT @ RECLAIM-RETIRED-MAX >= IF
        DROP R> DROP PERSIST-S-CAPACITY EXIT
    THEN
    DUP R@ _RCW-STAGED-CONTAINS? IF
        DROP R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    DUP R@ _RCW.TEMP ! DROP
    R@ _RCW.STAGED-COUNT @ R@ _RCW.RESULT !
    R>                                      ( work )
    DUP _RCW.STAGED-COUNT @ 0 ?DO
        DUP _RCW.RESULT @ OVER _RCW.STAGED-COUNT @ = IF
            I OVER _RCW.STAGED-ENTRY @ OVER _RCW.TEMP @ > IF
                I OVER _RCW.RESULT !
            THEN
        THEN
    LOOP
    DUP _RCW.RESULT @ OVER _RCW.STAGED-ENTRY
    DUP CELL+
    2 PICK _RCW.STAGED-COUNT @ 3 PICK _RCW.RESULT @ - CELLS MOVE
    DUP _RCW.TEMP @ OVER _RCW.RESULT @ 2 PICK _RCW.STAGED-ENTRY !
    1 SWAP _RCW.STAGED-COUNT +!
    PERSIST-S-OK ;

: _RCW-RECORD-ALLOCATION  ( page-id work -- status )
    >R
    R@ _RCW.ALLOCATED-COUNT @ RECLAIM-ALLOCATED-MAX >= IF
        DROP R> DROP PERSIST-S-CAPACITY EXIT
    THEN
    DUP R@ _RCW-ALLOCATED-CONTAINS? IF
        DROP R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    R@ _RCW.ALLOCATED-COUNT @ R@ _RCW.ALLOCATED-ENTRY !
    1 R@ _RCW.ALLOCATED-COUNT +!
    R> DROP PERSIST-S-OK ;

: _RCW-DISCARD-ID  ( page-id work -- status )
    >R
    R@ _RCW.DISCARD-COUNT @ RECLAIM-DISCARD-MAX >= IF
        DROP R> DROP PERSIST-S-CAPACITY EXIT
    THEN
    DUP R@ _RCW-DISCARDED-CONTAINS? IF
        DROP R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    DUP R@ _RCW.TEMP ! DROP
    R@ _RCW.DISCARD-COUNT @ R@ _RCW.RESULT !
    R>
    DUP _RCW.DISCARD-COUNT @ 0 ?DO
        DUP _RCW.RESULT @ OVER _RCW.DISCARD-COUNT @ = IF
            I OVER _RCW.DISCARDED-ENTRY @ OVER _RCW.TEMP @ > IF
                I OVER _RCW.RESULT !
            THEN
        THEN
    LOOP
    DUP _RCW.RESULT @ OVER _RCW.DISCARDED-ENTRY
    DUP CELL+
    2 PICK _RCW.DISCARD-COUNT @ 3 PICK _RCW.RESULT @ - CELLS MOVE
    DUP _RCW.TEMP @ OVER _RCW.RESULT @ 2 PICK _RCW.DISCARDED-ENTRY !
    1 SWAP _RCW.DISCARD-COUNT +!
    PERSIST-S-OK ;

: _RCW-OUTPUT-HEADER  ( kind generation count next work -- )
    >R
    R@ _RCW.OUTPUT PERSIST-PAGE-PAYLOAD-SIZE 0 FILL
    _RECLAIM-BUCKET-MAGIC R@ _RCW.OUTPUT _RCB.MAGIC !
    3 PICK R@ _RCW.OUTPUT _RCB.KIND !
    2 PICK R@ _RCW.OUTPUT _RCB.GENERATION !
    1 PICK R@ _RCW.OUTPUT _RCB.COUNT !
    DUP R@ _RCW.OUTPUT _RCB.NEXT !
    _RECLAIM-DROP4 R> DROP ;

: _RCW-MOVE>OUTPUT  ( count work -- )
    OVER 0 ?DO
        I OVER _RCW.MOVE-ENTRY @ I 2 PICK _RCW.OUTPUT _RCB.ENTRY !
    LOOP
    2DROP ;

\ Select the next reusable id.  Reserve is the number of additional retire
\ slots the enclosing metadata operation still needs after this allocation.
: _RCW-ALLOCATE-ID  ( reserve work -- page-id status )
    >R
    DUP 0< IF DROP R> DROP -1 PERSIST-S-INVALID EXIT THEN
    DUP R@ _RCW.REQUESTED !
    R@ _RCW.STAGED-COUNT @ SWAP _RECLAIM-ADD? 0= IF
        DROP R> DROP -1 PERSIST-S-CAPACITY EXIT
    THEN
    DUP RECLAIM-RETIRED-MAX > IF
        DROP R> DROP -1 PERSIST-S-CAPACITY EXIT
    THEN
    DROP
    R@ _RCW.STATE _RCS.READY-HEAD @ DUP -1 = IF
        DROP
        R@ _RCW.STATE _RCS.READY-INDEX @ IF
            R> DROP -1 PERSIST-S-CORRUPT EXIT
        THEN
        R@ _RCW.STATE _RCS.REUSABLE-COUNT @ IF
            R> DROP -1 PERSIST-S-CORRUPT EXIT
        THEN
        R@ _RCW-PROPOSED-PAGE-COUNT
        DUP 0< IF DROP R> DROP -1 PERSIST-S-CONFLICT EXIT THEN
        \ A high-water id is only physically claimed when its consumer writes
        \ it and advances PSTORE's proposed page count.  Until then it remains
        \ present in the issued ledger.  Refuse to hand that same reservation
        \ to another consumer or to an internal metadata/maintenance path.
        DUP R@ _RCW-ALLOCATED-CONTAINS? IF
            DROP R> DROP -1 PERSIST-S-CONFLICT EXIT
        THEN
        PERSIST-S-OK R> DROP EXIT
    THEN
    DUP R@ _RCW.TEMP !
    R@ _RCW-READ-BUCKET DUP IF
        >R DROP -1 R> R> DROP EXIT
    THEN DROP
    R@ _RCW.BUCKET _RCB.KIND @ _RECLAIM-BUCKET-READY <> IF
        R> DROP -1 PERSIST-S-CORRUPT EXIT
    THEN
    R@ _RCW.STATE _RCS.READY-INDEX @ DUP
    R@ _RCW.BUCKET _RCB.COUNT @ >= IF
        DROP R> DROP -1 PERSIST-S-CORRUPT EXIT
    THEN
    DUP R@ _RCW.BUCKET _RCB.ENTRY @ R@ _RCW.RESULT !
    1+ R@ _RCW.BUCKET _RCB.COUNT @ = IF
        R@ _RCW.STAGED-COUNT @ R@ _RCW.REQUESTED @ _RECLAIM-ADD?
        0= IF DROP R> DROP -1 PERSIST-S-CAPACITY EXIT THEN
        1 _RECLAIM-ADD? 0= IF
            DROP R> DROP -1 PERSIST-S-CAPACITY EXIT
        THEN
        RECLAIM-RETIRED-MAX > IF R> DROP -1 PERSIST-S-CAPACITY EXIT THEN
        R@ _RCW.TEMP @ R@ _RCW-STAGED-CONTAINS? IF
            R> DROP -1 PERSIST-S-CORRUPT EXIT
        THEN
    THEN
    R@ _RCW.RESULT @ R@ _RCW.TEMP @ = IF
        R> DROP -1 PERSIST-S-CORRUPT EXIT
    THEN
    R@ _RCW.RESULT @ R@ _RCW.BASE-PAGE-COUNT @ >= IF
        R> DROP -1 PERSIST-S-CORRUPT EXIT
    THEN
    R@ _RCW.RESULT @ R@ _RCW-STAGED-CONTAINS? IF
        R> DROP -1 PERSIST-S-CONFLICT EXIT
    THEN
    R@ _RCW.RESULT @ R@ _RCW-ALLOCATED-CONTAINS? IF
        R> DROP -1 PERSIST-S-CONFLICT EXIT
    THEN
    R@ _RCW.STATE _RCS.REUSABLE-COUNT @ 0> 0= IF
        R> DROP -1 PERSIST-S-CORRUPT EXIT
    THEN
    -1 R@ _RCW.STATE _RCS.REUSABLE-COUNT +!
    R@ _RCW.STATE _RCS.READY-INDEX @ 1+
        R@ _RCW.BUCKET _RCB.COUNT @ = IF
        R@ _RCW.BUCKET _RCB.NEXT @ R@ _RCW.STATE _RCS.READY-HEAD !
        R@ _RCW.BUCKET _RCB.NEXT-INDEX @
            R@ _RCW.STATE _RCS.READY-INDEX !
        R@ _RCW.RESULT @
        R@ _RCW.TEMP @ R@ _RCW-STAGE-ID DUP IF
            >R DROP -1 R> R> DROP EXIT
        THEN DROP
    ELSE
        1 R@ _RCW.STATE _RCS.READY-INDEX +!
        R@ _RCW.RESULT @
    THEN
    PERSIST-S-OK R> DROP ;

: RECLAIM-ALLOCATE
  ( reclaim-context store pstore-work -- page-id status )
    >R
    OVER OVER R@ _RCW-ACTIVE? 0= IF
        2DROP R> DROP -1 PERSIST-S-BUSY EXIT
    THEN
    OVER _RCW.ALLOCATED-COUNT @ RECLAIM-ALLOCATED-MAX >= IF
        PERSIST-S-CAPACITY 2 PICK _RCW-SET-STATUS DROP
        2DROP R> DROP -1 PERSIST-S-CAPACITY EXIT
    THEN
    0 2 PICK _RCW-ALLOCATE-ID
    DUP 0= IF
        DROP DUP 3 PICK _RCW-RECORD-ALLOCATION
    THEN
    DUP 4 PICK _RCW-SET-STATUS DROP
    2SWAP 2DROP R> DROP ;

: _RCW-RETIRE-SPAN?  ( page-ids-a count work -- flag )
    >R
    DUP 0= IF
        DROP 0= R> DROP EXIT
    THEN
    DUP 0< IF 2DROP R> DROP 0 EXIT THEN
    CELLS
    2DUP MSPAN-NONWRAPPING? 0= IF 2DROP R> DROP 0 EXIT THEN
    2DUP R@ RECLAIM-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP 0 EXIT
    THEN
    2DUP R@ _RCW.RECLAIM @ RECLAIM-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP 0 EXIT
    THEN
    R@ _RCW.STORE @ PSTORE-SPAN-DISJOINT? R> DROP ;

: _RCW-RETIRE-END  ( page-ids-a count work status -- status )
    >R DUP R> SWAP _RCW-SET-STATUS
    >R _RECLAIM-DROP3 R> ;

: RECLAIM-RETIRE-BATCH  ( page-ids-a count reclaim-work -- status )
    DUP RECLAIM-WORK-VALID? 0= IF
        _RECLAIM-DROP3 PERSIST-S-INVALID EXIT
    THEN
    DUP DUP _RCW.STORE @ 2 PICK _RCW.PSTORE-WORK @
        _RCW-ACTIVE? 0= IF
        _RECLAIM-DROP3 PERSIST-S-BUSY EXIT
    THEN
    1 PICK DUP 0< SWAP RECLAIM-MAX-BATCH > OR IF
        PERSIST-S-INVALID _RCW-RETIRE-END EXIT
    THEN
    2 PICK 2 PICK 2 PICK _RCW-RETIRE-SPAN? 0= IF
        PERSIST-S-INVALID _RCW-RETIRE-END EXIT
    THEN
    DUP _RCW.STAGED-COUNT @ 2 PICK _RECLAIM-ADD? 0= IF
        DROP PERSIST-S-CAPACITY _RCW-RETIRE-END EXIT
    THEN
    RECLAIM-RETIRED-MAX > IF
        PERSIST-S-CAPACITY _RCW-RETIRE-END EXIT
    THEN
    1 PICK 0 ?DO
        I CELLS 3 PICK + @
        DUP 0< IF
            DROP PERSIST-S-INVALID _RCW-RETIRE-END UNLOOP EXIT
        THEN
        DUP 2 PICK _RCW.BASE-PAGE-COUNT @ >= IF
            DROP PERSIST-S-CONFLICT _RCW-RETIRE-END UNLOOP EXIT
        THEN
        DUP 2 PICK _RCW-ALLOCATED-CONTAINS? IF
            DROP PERSIST-S-CONFLICT _RCW-RETIRE-END UNLOOP EXIT
        THEN
        DUP 2 PICK _RCW-STAGED-CONTAINS? IF
            DROP PERSIST-S-CONFLICT _RCW-RETIRE-END UNLOOP EXIT
        THEN
        I 0 ?DO
            I CELLS 4 PICK + @ OVER = IF
                DROP PERSIST-S-CONFLICT _RCW-RETIRE-END
                UNLOOP UNLOOP EXIT
            THEN
        LOOP
        DROP
    LOOP
    1 PICK 0 ?DO
        I CELLS 3 PICK + @ 1 PICK _RCW-STAGE-ID
        DUP IF _RCW-RETIRE-END UNLOOP EXIT THEN DROP
    LOOP
    PERSIST-S-OK _RCW-RETIRE-END ;

: RECLAIM-DISCARD-BATCH  ( page-ids-a count reclaim-work -- status )
    DUP RECLAIM-WORK-VALID? 0= IF
        _RECLAIM-DROP3 PERSIST-S-INVALID EXIT
    THEN
    DUP DUP _RCW.STORE @ 2 PICK _RCW.PSTORE-WORK @
        _RCW-ACTIVE? 0= IF
        _RECLAIM-DROP3 PERSIST-S-BUSY EXIT
    THEN
    1 PICK DUP 0< SWAP RECLAIM-MAX-BATCH > OR IF
        PERSIST-S-INVALID _RCW-RETIRE-END EXIT
    THEN
    2 PICK 2 PICK 2 PICK _RCW-RETIRE-SPAN? 0= IF
        PERSIST-S-INVALID _RCW-RETIRE-END EXIT
    THEN
    DUP _RCW.DISCARD-COUNT @ 2 PICK _RECLAIM-ADD? 0= IF
        DROP PERSIST-S-CAPACITY _RCW-RETIRE-END EXIT
    THEN
    RECLAIM-DISCARD-MAX > IF
        PERSIST-S-CAPACITY _RCW-RETIRE-END EXIT
    THEN
    1 PICK 0 ?DO
        I CELLS 3 PICK + @
        DUP 0< IF
            DROP PERSIST-S-INVALID _RCW-RETIRE-END UNLOOP EXIT
        THEN
        DUP 2 PICK _RCW-PROPOSED-PAGE-COUNT >= IF
            DROP PERSIST-S-CONFLICT _RCW-RETIRE-END UNLOOP EXIT
        THEN
        DUP 2 PICK _RCW-ALLOCATED-CONTAINS? 0= IF
            DROP PERSIST-S-CONFLICT _RCW-RETIRE-END UNLOOP EXIT
        THEN
        DUP 2 PICK _RCW-DISCARDED-CONTAINS? IF
            DROP PERSIST-S-CONFLICT _RCW-RETIRE-END UNLOOP EXIT
        THEN
        I 0 ?DO
            I CELLS 4 PICK + @ OVER = IF
                DROP PERSIST-S-CONFLICT _RCW-RETIRE-END
                UNLOOP UNLOOP EXIT
            THEN
        LOOP
        DROP
    LOOP
    1 PICK 0 ?DO
        I CELLS 3 PICK + @ 1 PICK _RCW-DISCARD-ID
        DUP IF _RCW-RETIRE-END UNLOOP EXIT THEN DROP
    LOOP
    PERSIST-S-OK _RCW-RETIRE-END ;

\ Atomically classify an unreachable mixed path.  Pages issued by this
\ proposal become immediate discards; all other accepted ids must be below
\ the transaction's committed base and become two-root-fenced retirements.
: RECLAIM-RELEASE-BATCH  ( page-ids-a count reclaim-work -- status )
    DUP RECLAIM-WORK-VALID? 0= IF
        _RECLAIM-DROP3 PERSIST-S-INVALID EXIT
    THEN
    DUP DUP _RCW.STORE @ 2 PICK _RCW.PSTORE-WORK @
        _RCW-ACTIVE? 0= IF
        _RECLAIM-DROP3 PERSIST-S-BUSY EXIT
    THEN
    1 PICK DUP 0< SWAP RECLAIM-MAX-BATCH > OR IF
        PERSIST-S-INVALID _RCW-RETIRE-END EXIT
    THEN
    2 PICK 2 PICK 2 PICK _RCW-RETIRE-SPAN? 0= IF
        PERSIST-S-INVALID _RCW-RETIRE-END EXIT
    THEN
    0 OVER _RCW.SOURCE !
    0 OVER _RCW.MOVED !
    1 PICK 0 ?DO
        I CELLS 3 PICK + @
        DUP 0< IF
            DROP PERSIST-S-INVALID _RCW-RETIRE-END UNLOOP EXIT
        THEN
        DUP 2 PICK _RCW-PROPOSED-PAGE-COUNT >= IF
            DROP PERSIST-S-CONFLICT _RCW-RETIRE-END UNLOOP EXIT
        THEN
        DUP 2 PICK _RCW-ALLOCATED-CONTAINS? IF
            DUP 2 PICK _RCW-DISCARDED-CONTAINS? IF
                DROP PERSIST-S-CONFLICT _RCW-RETIRE-END UNLOOP EXIT
            THEN
            1 2 PICK _RCW.MOVED +!
        ELSE
            DUP 2 PICK _RCW.BASE-PAGE-COUNT @ >= IF
                DROP PERSIST-S-CONFLICT _RCW-RETIRE-END UNLOOP EXIT
            THEN
            DUP 2 PICK _RCW-STAGED-CONTAINS? IF
                DROP PERSIST-S-CONFLICT _RCW-RETIRE-END UNLOOP EXIT
            THEN
            1 2 PICK _RCW.SOURCE +!
        THEN
        I 0 ?DO
            I CELLS 4 PICK + @ OVER = IF
                DROP PERSIST-S-CONFLICT _RCW-RETIRE-END
                UNLOOP UNLOOP EXIT
            THEN
        LOOP
        DROP
    LOOP
    DUP _RCW.STAGED-COUNT @ OVER _RCW.SOURCE @ _RECLAIM-ADD? 0= IF
        DROP PERSIST-S-CAPACITY _RCW-RETIRE-END EXIT
    THEN
    RECLAIM-RETIRED-MAX > IF
        PERSIST-S-CAPACITY _RCW-RETIRE-END EXIT
    THEN
    DUP _RCW.DISCARD-COUNT @ OVER _RCW.MOVED @ _RECLAIM-ADD? 0= IF
        DROP PERSIST-S-CAPACITY _RCW-RETIRE-END EXIT
    THEN
    RECLAIM-DISCARD-MAX > IF
        PERSIST-S-CAPACITY _RCW-RETIRE-END EXIT
    THEN
    1 PICK 0 ?DO
        I CELLS 3 PICK + @
        DUP 2 PICK _RCW-ALLOCATED-CONTAINS? IF
            1 PICK _RCW-DISCARD-ID
        ELSE
            1 PICK _RCW-STAGE-ID
        THEN
        DUP IF _RCW-RETIRE-END UNLOOP EXIT THEN DROP
    LOOP
    PERSIST-S-OK _RCW-RETIRE-END ;

