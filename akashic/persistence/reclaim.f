: _RECLAIM-ADD?  ( nonnegative-a nonnegative-b -- sum flag )
    OVER 0< OVER 0< OR IF 2DROP 0 0 EXIT THEN
    2DUP + DUP 0< IF DROP 2DROP 0 0 EXIT THEN
    DUP 3 PICK U< IF DROP 2DROP 0 0 EXIT THEN
    >R 2DROP R> -1 ;

