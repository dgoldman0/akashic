: RECLAIM-VALID?  ( reclaim -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP RECLAIM-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _RCL.MAGIC @ _RECLAIM-MAGIC <> IF DROP 0 EXIT THEN
    DUP _RCL.SELF @ OVER <> IF DROP 0 EXIT THEN
    DUP _RCL.STATUS @ DUP PERSIST-S-OK <
        SWAP PERSIST-S-FAULT > OR IF DROP 0 EXIT THEN
    DUP _RCL.STATE RECLAIM-STATE-SIZE RECLAIM-STATE-VALID? 0= IF
        DROP 0 EXIT
    THEN
    DUP _RCL.GENERATION @ 0< IF DROP 0 EXIT THEN
    DUP _RCL.STORE @ ?DUP IF
        PSTORE-VALID? 0= IF DROP 0 EXIT THEN
    ELSE
        DUP _RCL.GENERATION @ IF DROP 0 EXIT THEN
    THEN
    DUP _RCL.ACTIVE-WORK @ ?DUP IF
        DUP RECLAIM-WORK-SIZE MSPAN-NONWRAPPING? 0= IF
            2DROP 0 EXIT
        THEN
        DUP _RCW.MAGIC @ _RECLAIM-WORK-MAGIC <> IF 2DROP 0 EXIT THEN
        DUP _RCW.SELF @ OVER <> IF 2DROP 0 EXIT THEN
        DUP _RCW.ACTIVE @ -1 <> IF 2DROP 0 EXIT THEN
        DUP _RCW.RECLAIM @ 2 PICK <> IF 2DROP 0 EXIT THEN
        DROP
    THEN
    DROP -1 ;

: RECLAIM-WORK-VALID?  ( work -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP RECLAIM-WORK-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _RCW.MAGIC @ _RECLAIM-WORK-MAGIC <> IF DROP 0 EXIT THEN
    DUP _RCW.SELF @ OVER <> IF DROP 0 EXIT THEN
    DUP _RCW.ACTIVE @ DUP 0= SWAP -1 = OR 0= IF DROP 0 EXIT THEN
    DUP _RCW.FINALIZED @ DUP 0= SWAP -1 = OR 0= IF DROP 0 EXIT THEN
    DUP _RCW.STATUS @ DUP PERSIST-S-OK <
        SWAP PERSIST-S-FAULT > OR IF DROP 0 EXIT THEN
    DUP _RCW.STAGED-COUNT @ DUP 0< SWAP RECLAIM-RETIRED-MAX > OR IF
        DROP 0 EXIT
    THEN
    DUP _RCW.ALLOCATED-COUNT @ DUP 0< SWAP RECLAIM-ALLOCATED-MAX > OR IF
        DROP 0 EXIT
    THEN
    DUP _RCW.DISCARD-COUNT @ DUP 0< SWAP RECLAIM-DISCARD-MAX > OR IF
        DROP 0 EXIT
    THEN
    DUP _RCW.ACTIVE @ IF
        DUP _RCW.RECLAIM @ DUP RECLAIM-VALID? 0= IF 2DROP 0 EXIT THEN DROP
        DUP _RCW.STORE @ DUP PSTORE-VALID? 0= IF 2DROP 0 EXIT THEN DROP
        DUP _RCW.PSTORE-WORK @ DUP PSTORE-WORK-VALID? 0= IF
            2DROP 0 EXIT
        THEN DROP
        DUP _RCW.STATE RECLAIM-STATE-SIZE RECLAIM-STATE-VALID? 0= IF
            DROP 0 EXIT
        THEN
        DUP _RCW.NEXT-GENERATION @ 0> 0= IF DROP 0 EXIT THEN
        DUP _RCW.BASE-PAGE-COUNT @ 0< IF DROP 0 EXIT THEN
    THEN
    DROP -1 ;

: RECLAIM-INIT  ( reclaim -- status )
    DUP 0= IF DROP PERSIST-S-INVALID EXIT THEN
    DUP RECLAIM-SIZE MSPAN-NONWRAPPING? 0= IF
        DROP PERSIST-S-INVALID EXIT
    THEN
    DUP RECLAIM-SIZE 0 FILL
    _RECLAIM-MAGIC OVER _RCL.MAGIC !
    DUP OVER _RCL.SELF !
    PERSIST-S-OK OVER _RCL.STATUS !
    _RCL.STATE _RECLAIM-STATE-EMPTY
    PERSIST-S-OK ;

: RECLAIM-WORK-INIT  ( work -- status )
    DUP 0= IF DROP PERSIST-S-INVALID EXIT THEN
    DUP RECLAIM-WORK-SIZE MSPAN-NONWRAPPING? 0= IF
        DROP PERSIST-S-INVALID EXIT
    THEN
    DUP RECLAIM-WORK-SIZE 0 FILL
    _RECLAIM-WORK-MAGIC OVER _RCW.MAGIC !
    DUP OVER _RCW.SELF !
    PERSIST-S-INVALID SWAP _RCW.STATUS !
    PERSIST-S-OK ;

: _RECLAIM-STATE-BOUNDS?  ( state proposed-count -- flag )
    >R
    DUP _RCS.IN-HEAD @ DUP 0>= IF R@ >= IF DROP R> DROP 0 EXIT THEN ELSE DROP THEN
    DUP _RCS.OUT-HEAD @ DUP 0>= IF R@ >= IF DROP R> DROP 0 EXIT THEN ELSE DROP THEN
    DUP _RCS.ROTATE-SOURCE @ DUP 0>= IF
        R@ >= IF DROP R> DROP 0 EXIT THEN
    ELSE DROP THEN
    DUP _RCS.ROTATE-BUILD @ DUP 0>= IF
        R@ >= IF DROP R> DROP 0 EXIT THEN
    ELSE DROP THEN
    _RCS.READY-HEAD @ DUP 0>= IF R@ < ELSE DROP -1 THEN
    R> DROP ;

: RECLAIM-OPEN  ( state-a state-u store reclaim -- status )
    >R
    R@ RECLAIM-VALID? 0= IF _RECLAIM-DROP3 R> DROP PERSIST-S-INVALID EXIT THEN
    R@ _RCL.ACTIVE-WORK @ IF
        _RECLAIM-DROP3 R> DROP PERSIST-S-BUSY EXIT
    THEN
    DUP PSTORE-VALID? 0= IF _RECLAIM-DROP3 R> DROP PERSIST-S-INVALID EXIT THEN
    2 PICK 2 PICK RECLAIM-STATE-VALID? 0= IF
        _RECLAIM-DROP3 R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    DUP PSTORE-CURRENT-ROOT@ PROOTV.PAGE-COUNT @
        3 PICK SWAP _RECLAIM-STATE-BOUNDS? 0= IF
        _RECLAIM-DROP3 R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    2 PICK 2 PICK R@ RECLAIM-SIZE MSPAN-OVERLAP? IF
        _RECLAIM-DROP3 R> DROP PERSIST-S-INVALID EXIT
    THEN
    2 PICK 2 PICK 2 PICK PSTORE-SPAN-DISJOINT? 0= IF
        _RECLAIM-DROP3 R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ RECLAIM-SIZE 2 PICK PSTORE-SPAN-DISJOINT? 0= IF
        _RECLAIM-DROP3 R> DROP PERSIST-S-INVALID EXIT
    THEN
    2 PICK R@ _RCL.STATE RECLAIM-STATE-SIZE MOVE
    DUP R@ _RCL.STORE !
    DUP PSTORE-GENERATION@ R@ _RCL.GENERATION !
    PERSIST-S-OK R@ _RCL.STATUS !
    _RECLAIM-DROP3 R> DROP PERSIST-S-OK ;

: RECLAIM-STORE@  ( reclaim -- store|0 )
    DUP RECLAIM-VALID? IF _RCL.STORE @ ELSE DROP 0 THEN ;

: RECLAIM-GENERATION@  ( reclaim -- generation|-1 )
