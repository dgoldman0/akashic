    -1 SWAP _RCS.READY-HEAD ! ;

: RECLAIM-STATE-VALID?  ( state-a state-u -- flag )
    RECLAIM-STATE-SIZE <> IF DROP 0 EXIT THEN
    DUP 0= IF DROP 0 EXIT THEN
    DUP RECLAIM-STATE-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _RCS.MAGIC @ _RECLAIM-STATE-MAGIC <> IF DROP 0 EXIT THEN
    DUP _RCS.IN-HEAD @ _RECLAIM-HEAD? 0= IF DROP 0 EXIT THEN
    DUP _RCS.OUT-HEAD @ _RECLAIM-HEAD? 0= IF DROP 0 EXIT THEN
    DUP _RCS.ROTATE-SOURCE @ _RECLAIM-HEAD? 0= IF DROP 0 EXIT THEN
    DUP _RCS.ROTATE-BUILD @ _RECLAIM-HEAD? 0= IF DROP 0 EXIT THEN
    DUP _RCS.READY-HEAD @ _RECLAIM-HEAD? 0= IF DROP 0 EXIT THEN
    DUP _RCS.OUT-INDEX @ DUP 0< SWAP RECLAIM-MAX-BATCH >= OR IF
        DROP 0 EXIT
    THEN
    DUP _RCS.READY-INDEX @ DUP 0< SWAP RECLAIM-MAX-BATCH >= OR IF
        DROP 0 EXIT
    THEN
    DUP _RCS.RETIRED-COUNT @ 0< IF DROP 0 EXIT THEN
    DUP _RCS.REUSABLE-COUNT @ 0< IF DROP 0 EXIT THEN
    DUP _RCS.ROTATING @ DUP 0= SWAP -1 = OR 0= IF DROP 0 EXIT THEN
    DUP _RCS.OUT-HEAD @ -1 = IF
        DUP _RCS.OUT-INDEX @ IF DROP 0 EXIT THEN
    THEN
    DUP _RCS.READY-HEAD @ -1 = IF
        DUP _RCS.READY-INDEX @ IF DROP 0 EXIT THEN
        DUP _RCS.REUSABLE-COUNT @ IF DROP 0 EXIT THEN
    ELSE
        DUP _RCS.REUSABLE-COUNT @ 0> 0= IF DROP 0 EXIT THEN
    THEN
    DUP _RCS.ROTATING @ IF
        DUP _RCS.OUT-HEAD @ -1 <> IF DROP 0 EXIT THEN
        DUP _RCS.ROTATE-SOURCE @ 0< IF DROP 0 EXIT THEN
    ELSE
        DUP _RCS.ROTATE-SOURCE @ -1 <> IF DROP 0 EXIT THEN
        DUP _RCS.ROTATE-BUILD @ -1 <> IF DROP 0 EXIT THEN
    THEN
    _RCS.RESERVED 40 _PERSIST-ZERO? ;

: RECLAIM-STATE-INIT  ( state-a state-u -- status )
    RECLAIM-STATE-SIZE <> IF DROP PERSIST-S-INVALID EXIT THEN
    DUP 0= IF DROP PERSIST-S-INVALID EXIT THEN
    DUP RECLAIM-STATE-SIZE MSPAN-NONWRAPPING? 0= IF
        DROP PERSIST-S-INVALID EXIT
    THEN
    _RECLAIM-STATE-EMPTY PERSIST-S-OK ;

