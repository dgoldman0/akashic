: _RECLAIM-BUCKET-VALID?  ( bucket proposed-count -- flag )
    DUP 0< IF 2DROP 0 EXIT THEN
    OVER 0= IF 2DROP 0 EXIT THEN
    OVER PERSIST-PAGE-PAYLOAD-SIZE MSPAN-NONWRAPPING? 0= IF
        2DROP 0 EXIT
    THEN
    OVER _RCB.MAGIC @ _RECLAIM-BUCKET-MAGIC <> IF 2DROP 0 EXIT THEN
    OVER _RCB.KIND @ DUP _RECLAIM-BUCKET-PENDING =
        SWAP _RECLAIM-BUCKET-READY = OR 0= IF 2DROP 0 EXIT THEN
    OVER _RCB.COUNT @ DUP 0> SWAP RECLAIM-MAX-BATCH <= AND 0= IF
        2DROP 0 EXIT
    THEN
    OVER _RCB.NEXT @ DUP -1 < IF DROP 2DROP 0 EXIT THEN
    DUP 0>= IF OVER >= IF 2DROP 0 EXIT THEN ELSE DROP THEN
    OVER _RCB.NEXT-INDEX @ DUP 0< SWAP RECLAIM-MAX-BATCH >= OR IF
        2DROP 0 EXIT
    THEN
    OVER _RCB.NEXT @ -1 = IF
        OVER _RCB.NEXT-INDEX @ IF 2DROP 0 EXIT THEN
    THEN
    OVER _RCB.KIND @ _RECLAIM-BUCKET-PENDING = IF
        OVER _RCB.GENERATION @ 0> 0= IF 2DROP 0 EXIT THEN
        OVER _RCB.NEXT-INDEX @ IF 2DROP 0 EXIT THEN
    ELSE
        OVER _RCB.GENERATION @ IF 2DROP 0 EXIT THEN
    THEN
    OVER _RCB.RESERVED 16 _PERSIST-ZERO? 0= IF 2DROP 0 EXIT THEN
    OVER _RCB.COUNT @ 0 ?DO
        I 2 PICK _RCB.ENTRY @ DUP 0< IF
            DROP 2DROP 0 UNLOOP EXIT
        THEN
        DUP 2 PICK >= IF DROP 2DROP 0 UNLOOP EXIT THEN
        I IF
            I 1- 3 PICK _RCB.ENTRY @ <= IF
                2DROP 0 UNLOOP EXIT
            THEN
        ELSE
            DROP
        THEN
    LOOP
    OVER _RCB.COUNT @ CELLS _RCB-ENTRIES +
    DUP >R 2 PICK + PERSIST-PAGE-PAYLOAD-SIZE R> - _PERSIST-ZERO?
    >R 2DROP R> ;

