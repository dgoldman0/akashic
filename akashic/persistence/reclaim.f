\ =====================================================================
\  reclaim.f - Bounded two-root-fenced physical page reclamation
\ =====================================================================
\  The caller embeds the 128-byte state header in its application root.
\  Retired and reusable populations live in checked page buckets of at most
\  32 ids.  A persistent two-stack queue rotates one bucket per step, so no
\  operation scans or copies a corpus-proportional population.
\
\  A page retired by generation G becomes reusable only when both observed
\  atomic-root slots are nonzero and at least G.  Generation zero therefore
\  blocks reuse.  Descriptor and operation memory are entirely caller-owned.
\ =====================================================================

PROVIDED akashic-persist-reclaim

