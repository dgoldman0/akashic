\ =====================================================================
\  core.f - Common caller-owned persistence types and accounting
\ =====================================================================
\  This module defines values shared by the neutral page, segment, root,
\  and store layers.  It performs no I/O and owns no mutable storage.
\  Every descriptor, counter set, record reference, and root value is
\  allocated by the caller.
\ =====================================================================

PROVIDED akashic-persist-core

REQUIRE ../utils/memory-span.f

