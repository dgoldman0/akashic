\ ---------------------------------------------------------------------
\ Immutable segment-record reference
\ ---------------------------------------------------------------------

 0 CONSTANT _PREF-OFFSET
 8 CONSTANT _PREF-SPAN
16 CONSTANT _PREF-ORDINAL
24 CONSTANT PERSIST-REF-SIZE

: PREF.OFFSET   ( ref -- a ) _PREF-OFFSET + ;
: PREF.SPAN     ( ref -- a ) _PREF-SPAN + ;
: PREF.ORDINAL  ( ref -- a ) _PREF-ORDINAL + ;

: PERSIST-REF-INIT  ( ref -- )
    DUP 0= IF DROP EXIT THEN
    DUP PERSIST-REF-SIZE MSPAN-NONWRAPPING? 0= IF DROP EXIT THEN
    PERSIST-REF-SIZE 0 FILL ;

: PERSIST-REF-COPY  ( source destination -- )
    2DUP 0= SWAP 0= OR IF 2DROP EXIT THEN
    2DUP PERSIST-REF-SIZE MSPAN-NONWRAPPING? SWAP
        PERSIST-REF-SIZE MSPAN-NONWRAPPING? AND 0= IF 2DROP EXIT THEN
    PERSIST-REF-SIZE MOVE ;

: PERSIST-REF-VALID?  ( ref -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP PERSIST-REF-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP PREF.OFFSET @ 0< IF DROP 0 EXIT THEN
    DUP PREF.SPAN @ PERSIST-RECORD-HEADER-SIZE < IF DROP 0 EXIT THEN
    DUP PREF.ORDINAL @ 0> 0= IF DROP 0 EXIT THEN
    DUP PREF.OFFSET @ OVER PREF.SPAN @ +
    DUP 0< IF 2DROP 0 EXIT THEN
    SWAP PREF.OFFSET @ U< 0= ;

\ ---------------------------------------------------------------------
\ Atomic root value
\ ---------------------------------------------------------------------
\  The root knows only committed physical bounds and one opaque application
\  page.  Index, blob, applet, schema, and path policy live above it.

 0 CONSTANT _PROOTV-IDENTITY
32 CONSTANT _PROOTV-PAGE-COUNT
40 CONSTANT _PROOTV-SEGMENT-TAIL
48 CONSTANT _PROOTV-RECORD-COUNT
56 CONSTANT _PROOTV-APPLICATION-ROOT
64 CONSTANT _PROOTV-FLAGS
72 CONSTANT _PROOTV-RESERVED
96 CONSTANT PERSIST-ROOT-VALUE-SIZE

: PROOTV.IDENTITY          ( root -- a ) _PROOTV-IDENTITY + ;
: PROOTV.PAGE-COUNT        ( root -- a ) _PROOTV-PAGE-COUNT + ;
: PROOTV.SEGMENT-TAIL      ( root -- a ) _PROOTV-SEGMENT-TAIL + ;
: PROOTV.RECORD-COUNT      ( root -- a ) _PROOTV-RECORD-COUNT + ;
: PROOTV.APPLICATION-ROOT  ( root -- a ) _PROOTV-APPLICATION-ROOT + ;
: PROOTV.FLAGS             ( root -- a ) _PROOTV-FLAGS + ;
: PROOTV.RESERVED          ( root -- a ) _PROOTV-RESERVED + ;

: PERSIST-ROOT-VALUE-INIT  ( root -- )
    DUP 0= IF DROP EXIT THEN
    DUP PERSIST-ROOT-VALUE-SIZE MSPAN-NONWRAPPING? 0= IF DROP EXIT THEN
    DUP PERSIST-ROOT-VALUE-SIZE 0 FILL
    -1 SWAP PROOTV.APPLICATION-ROOT ! ;

: PERSIST-ROOT-VALUE-COPY  ( source destination -- )
    2DUP 0= SWAP 0= OR IF 2DROP EXIT THEN
    2DUP PERSIST-ROOT-VALUE-SIZE MSPAN-NONWRAPPING? SWAP
        PERSIST-ROOT-VALUE-SIZE MSPAN-NONWRAPPING? AND 0= IF 2DROP EXIT THEN
    PERSIST-ROOT-VALUE-SIZE MOVE ;

: _PERSIST-ZERO?  ( a u -- flag )
    0 ?DO DUP I + C@ IF DROP 0 UNLOOP EXIT THEN LOOP DROP -1 ;

: _PERSIST-IDENTITY-VALID?  ( identity -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    PERSIST-IDENTITY-SIZE _PERSIST-ZERO? 0= ;

: PERSIST-ROOT-VALUE-VALID?  ( root -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP PERSIST-ROOT-VALUE-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP PROOTV.IDENTITY _PERSIST-IDENTITY-VALID? 0= IF DROP 0 EXIT THEN
    DUP PROOTV.PAGE-COUNT @ 0< IF DROP 0 EXIT THEN
    DUP PROOTV.SEGMENT-TAIL @ 0< IF DROP 0 EXIT THEN
    DUP PROOTV.RECORD-COUNT @ 0< IF DROP 0 EXIT THEN
    DUP PROOTV.FLAGS @ IF DROP 0 EXIT THEN
    DUP PROOTV.RESERVED 24 _PERSIST-ZERO? 0= IF DROP 0 EXIT THEN
    DUP PROOTV.RECORD-COUNT @ 0= IF
        DUP PROOTV.SEGMENT-TAIL @ IF DROP 0 EXIT THEN
    ELSE
        DUP PROOTV.SEGMENT-TAIL @ 0> 0= IF DROP 0 EXIT THEN
        DUP PROOTV.RECORD-COUNT @
            OVER PROOTV.SEGMENT-TAIL @ PERSIST-RECORD-HEADER-SIZE /
            > IF DROP 0 EXIT THEN
    THEN
    DUP PROOTV.PAGE-COUNT @ 0= IF
        PROOTV.APPLICATION-ROOT @ -1 = EXIT
    THEN
    DUP PROOTV.APPLICATION-ROOT @ DUP 0< IF 2DROP 0 EXIT THEN
    SWAP PROOTV.PAGE-COUNT @ < ;

