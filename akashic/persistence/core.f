: PERSIST-STATS-INIT  ( stats -- )
    DUP 0= IF DROP EXIT THEN
    DUP PERSIST-STATS-SIZE MSPAN-NONWRAPPING? 0= IF DROP EXIT THEN
    PERSIST-STATS-SIZE 0 FILL ;

: PERSIST-STATS-VALID?  ( stats -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    PERSIST-STATS-SIZE MSPAN-NONWRAPPING? ;

: PERSIST-STATS-RESET  ( stats -- )
    PERSIST-STATS-INIT ;

: _PSTAT-ADD  ( u field -- )
    >R
    DUP 0< IF DROP PERSIST-MAX-SIGNED ELSE
        R@ @ DUP 0< IF DROP DROP PERSIST-MAX-SIGNED ELSE
            PERSIST-MAX-SIGNED OVER - 2 PICK < IF
                2DROP PERSIST-MAX-SIGNED
            ELSE
                +
            THEN
        THEN
    THEN
    R> ! ;

: _PSTAT-INC  ( field -- ) 1 SWAP _PSTAT-ADD ;

: _PSTAT-NOTE-PAGE-READ  ( bytes stats -- )
    DUP PSTAT.PAGE-READS _PSTAT-INC PSTAT.BYTES-READ _PSTAT-ADD ;
: _PSTAT-NOTE-PAGE-WRITE  ( bytes stats -- )
    DUP PSTAT.PAGE-WRITES _PSTAT-INC PSTAT.BYTES-WRITTEN _PSTAT-ADD ;
