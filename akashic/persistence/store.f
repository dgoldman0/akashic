    DUP PSTORE-WORK-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _PSW.MAGIC @ _PSTORE-WORK-MAGIC <> IF DROP 0 EXIT THEN
    DUP _PSW.SELF @ OVER <> IF DROP 0 EXIT THEN
    DUP _PSW.BUSY @ DUP 0= SWAP -1 = OR 0= IF DROP 0 EXIT THEN
    DUP _PSW.ACTIVE @ DUP 0= SWAP -1 = OR 0= IF DROP 0 EXIT THEN
    DUP _PSW.FAILED @ DUP 0= SWAP -1 = OR 0= IF DROP 0 EXIT THEN
    DUP _PSW.DIRTY @ DUP 0= SWAP -1 = OR 0= IF DROP 0 EXIT THEN
