: _PSTORE-INIT-FAIL  ( status store -- status )
    >R
    R@ _PST.PAGE-FILE PPAGE-FILE-VALID? IF
        R@ _PST.PAGE-FILE PPAGE.CACHE @ ?DUP IF
            0 OVER _PPC.OWNER !
            PPAGE-CACHE-INVALIDATE DROP
        THEN
    THEN
    R@ PSTORE-SIZE 0 FILL
    R> DROP ;

