: _PSTORE-READ-RECORD-RUN  ( work -- status )
    >R
    R@ _PSW.STORE @ _PST.FLAGS @ _PSTORE-F-LOADED AND 0= IF
        R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    R@ _PSTORE-ARG-REF? 0= IF R> DROP PERSIST-S-INVALID EXIT THEN
    R@ _PSW.ARG-REF @
    R@ _PSW.STORE @ _PST.CURRENT PROOTV.SEGMENT-TAIL @
    R@ _PSW.STORE @ _PST.SEGMENT-FILE R@ _PSW.SEGMENT-WORK
        PSEG-READ R> DROP ;

: PSTORE-READ-RECORD  ( ref store work -- status )
    >R
    DUP R@ _PSTORE-BOUND? 0= IF 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    1 PICK R@ _PSW.ARG-REF !
    NIP R@ _PSTORE-SCOPED-ENTER
    DUP IF _PSTORE-REJECT R> DROP EXIT THEN DROP
    DUP ['] _PSTORE-READ-RECORD-RUN _PSTORE-WORK-CATCH
    ROT ROT _PSTORE-SCOPED-LEAVE R> DROP ;

