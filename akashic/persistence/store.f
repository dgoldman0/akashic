    DUP 0= IF DROP 0 EXIT THEN
    DUP PSTORE-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _PST.MAGIC @ _PSTORE-MAGIC <> IF DROP 0 EXIT THEN
    DUP _PST.SELF @ OVER <> IF DROP 0 EXIT THEN
    DUP _PST.STATS @ ?DUP IF PERSIST-STATS-VALID? 0= IF DROP 0 EXIT THEN THEN
    DUP _PST.LOCK @ PSTORE-SPIN-GUARD-VALID? 0= IF DROP 0 EXIT THEN
    DUP _PST.GENERATION @ 0< IF DROP 0 EXIT THEN
    DUP _PST.STATUS @ DUP PERSIST-S-OK <
        SWAP PERSIST-S-FAULT > OR IF DROP 0 EXIT THEN
    DUP _PST.FLAGS @ _PSTORE-F-KNOWN INVERT AND IF DROP 0 EXIT THEN
    DUP _PST.IDENTITY _PERSIST-IDENTITY-VALID? 0= IF DROP 0 EXIT THEN
    DUP _PST.CURRENT PERSIST-ROOT-VALUE-VALID? 0= IF DROP 0 EXIT THEN
    DUP _PST.IDENTITY OVER _PST.CURRENT PROOTV.IDENTITY
        PERSIST-IDENTITY-SIZE _PSTORE-BYTES= 0= IF DROP 0 EXIT THEN
    DUP _PST.PAGE-FILE PPAGE-FILE-VALID? 0= IF DROP 0 EXIT THEN
    DUP _PST.SEGMENT-FILE PSEG-FILE-VALID? 0= IF DROP 0 EXIT THEN
    DUP _PST.ROOT-FILE PROOT-VALID? 0= IF DROP 0 EXIT THEN
    DUP _PST.FLAGS @ _PSTORE-F-LOADED AND IF
        DUP _PST.AUTHORITY-WORK @ DUP 0= IF 2DROP 0 EXIT THEN
        DUP PSTORE-WORK-VALID? 0= IF 2DROP 0 EXIT THEN
        DUP _PSW.ROOT-WORK PROOT-GENERATION@
            2 PICK _PST.GENERATION @ <> IF 2DROP 0 EXIT THEN
        DROP
    ELSE
        DUP _PST.AUTHORITY-WORK @ IF DROP 0 EXIT THEN
    THEN
    DUP _PST.TRANSACTION-WORK @ ?DUP IF
        DUP PSTORE-WORK-VALID? 0= IF 2DROP 0 EXIT THEN
        DUP _PSW.ACTIVE @ 0= IF 2DROP 0 EXIT THEN
        _PSW.STORE @ OVER <> IF DROP 0 EXIT THEN
    THEN
