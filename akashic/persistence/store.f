    DUP R@ _PSW.ARG-U @ <> IF 2DROP R> DROP PERSIST-S-CORRUPT EXIT THEN
    DROP R@ _PSW.ARG-A @ SWAP R@ _PSW.ARG-U @ _PSTORE-BYTES= 0= IF
        R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    R@ _PSW.STORE @ _PSTORE-NOTE-COMPARE
    PERSIST-FAULT-SEGMENT-VERIFIED R@ _PSW.RESULT @
        R@ _PSW.STORE @ _PSTORE-FAULT DUP IF R> DROP EXIT THEN DROP
    R@ _PSW.TEMP @ R@ _PSW.PROPOSED PROOTV.SEGMENT-TAIL !
    R@ _PSW.RESULT @ R@ _PSW.PROPOSED PROOTV.RECORD-COUNT !
    -1 R@ _PSW.DIRTY !
    R> DROP PERSIST-S-OK ;

