\ Configuration order: page path, segment path, root-A path, root-B path,
\ identity, maximum segment payload, VFS, stats, cache, spin guard, fault XT,
\ fault context, then the destination store descriptor.
: PSTORE-INIT  ( 16-config-values store -- status )
    16 PICK 16 PICK 16 PICK 16 PICK 16 PICK 16 PICK 16 PICK 16 PICK
    16 PICK 16 PICK 16 PICK 16 PICK 16 PICK 16 PICK 16 PICK 16 PICK
    16 PICK _PSTORE-INIT-ARGS? 0= IF
        _PSTORE-DROP16 DROP PERSIST-S-INVALID EXIT
    THEN
    >R
    R@ PSTORE-SIZE 0 FILL
    _PSTORE-MAGIC R@ _PST.MAGIC !
    R@ R@ _PST.SELF !
    4 PICK R@ _PST.STATS !
    2 PICK R@ _PST.LOCK !
    1 PICK R@ _PST.FAULT-XT !
    DUP R@ _PST.FAULT-CONTEXT !
    PERSIST-S-OK R@ _PST.STATUS !
    7 PICK R@ _PST.IDENTITY PERSIST-IDENTITY-SIZE MOVE
    R@ _PST.CURRENT PERSIST-ROOT-VALUE-INIT
    R@ _PST.IDENTITY R@ _PST.CURRENT PROOTV.IDENTITY
        PERSIST-IDENTITY-SIZE MOVE

    15 PICK 15 PICK 7 PICK 7 PICK 7 PICK R@ _PST.PAGE-FILE
        PPAGE-FILE-INIT DUP IF
        >R _PSTORE-DROP16 R> R> _PSTORE-INIT-FAIL EXIT
    THEN DROP
    13 PICK 13 PICK 8 PICK 8 PICK 8 PICK R@ _PST.SEGMENT-FILE
        PSEG-FILE-INIT DUP IF
        >R _PSTORE-DROP16 R> R> _PSTORE-INIT-FAIL EXIT
    THEN DROP
    11 PICK 11 PICK 11 PICK 11 PICK
        R@ _PST.PAGE-FILE R@ _PST.SEGMENT-FILE
        10 PICK 8 PICK 8 PICK R@ _PST.ROOT-FILE PROOT-INIT DUP IF
        >R _PSTORE-DROP16 R> R> _PSTORE-INIT-FAIL EXIT
    THEN DROP
    _PSTORE-DROP16 R> DROP PERSIST-S-OK ;

: PSTORE-WORK-INIT  ( segment-buffer-a segment-buffer-u work -- status )
    >R
    R@ 0= IF 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    R@ PSTORE-WORK-SIZE MSPAN-NONWRAPPING? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    2DUP _PSEG-BUFFER? 0= IF 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    2DUP R@ PSTORE-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ PSTORE-WORK-SIZE 0 FILL
    _PSTORE-WORK-MAGIC R@ _PSW.MAGIC !
    R@ R@ _PSW.SELF !
    PERSIST-S-INVALID R@ _PSW.STATUS !
    R@ _PSW.PAGE-WORK PPAGE-WORK-INIT DUP IF
        >R 2DROP R> R> DUP PSTORE-WORK-SIZE 0 FILL DROP EXIT
    THEN DROP
    1 PICK 1 PICK R@ _PSW.SEGMENT-WORK PSEG-WORK-INIT DUP IF
        >R 2DROP R> R> DUP PSTORE-WORK-SIZE 0 FILL DROP EXIT
    THEN DROP
    R@ _PSW.ROOT-WORK PROOT-WORK-INIT DUP IF
        >R 2DROP R> R> DUP PSTORE-WORK-SIZE 0 FILL DROP EXIT
    THEN DROP
    2DROP R> DROP PERSIST-S-OK ;

: _PSTORE-WORK-ALIASES?  ( temporarily-bound-work -- disjoint? )
    >R
    R@ PSTORE-WORK-SIZE
        R@ _PSW.STORE @ PSTORE-SIZE MSPAN-OVERLAP? IF
        R> DROP 0 EXIT
    THEN
    R@ _PSW.SEGMENT-WORK _PSEG-W.BUFFER @
        R@ _PSW.SEGMENT-WORK _PSEG-W.CAPACITY @
        R@ _PSW.STORE @ PSTORE-SIZE MSPAN-OVERLAP? IF
        R> DROP 0 EXIT
    THEN
    R@ _PSW.STORE @ _PST.LOCK @ PSTORE-SPIN-GUARD-SIZE
        R@ PSTORE-WORK-SIZE MSPAN-OVERLAP? IF R> DROP 0 EXIT THEN
    R@ _PSW.STORE @ _PST.LOCK @ PSTORE-SPIN-GUARD-SIZE
        R@ _PSW.SEGMENT-WORK _PSEG-W.BUFFER @
        R@ _PSW.SEGMENT-WORK _PSEG-W.CAPACITY @
        MSPAN-OVERLAP? IF R> DROP 0 EXIT THEN
    R@ _PSW.STORE @ _PST.PAGE-FILE PPAGE.VFS @ VFS-DESC-SIZE
        R@ PSTORE-WORK-SIZE MSPAN-OVERLAP? IF R> DROP 0 EXIT THEN
    R@ _PSW.STORE @ _PST.PAGE-FILE PPAGE.VFS @ VFS-DESC-SIZE
        R@ _PSW.SEGMENT-WORK _PSEG-W.BUFFER @
        R@ _PSW.SEGMENT-WORK _PSEG-W.CAPACITY @
        MSPAN-OVERLAP? IF R> DROP 0 EXIT THEN
    R@ _PSW.STORE @ _PST.STATS @ ?DUP IF
        DUP PERSIST-STATS-SIZE R@ PSTORE-WORK-SIZE
            MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
        DUP PERSIST-STATS-SIZE
            R@ _PSW.SEGMENT-WORK _PSEG-W.BUFFER @
            R@ _PSW.SEGMENT-WORK _PSEG-W.CAPACITY @
            MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
        DROP
    THEN
    R@ _PSW.STORE @ _PST.PAGE-FILE PPAGE.CACHE @ ?DUP IF
        DUP PERSIST-PAGE-CACHE-SIZE R@ PSTORE-WORK-SIZE
            MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
        DUP _PPC.MEMORY @ OVER _PPC.BYTES @
            R@ PSTORE-WORK-SIZE MSPAN-OVERLAP? IF
            DROP R> DROP 0 EXIT
        THEN
        DUP _PPC.MEMORY @ OVER _PPC.BYTES @
            R@ _PSW.SEGMENT-WORK _PSEG-W.BUFFER @
            R@ _PSW.SEGMENT-WORK _PSEG-W.CAPACITY @
            MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
        DROP
    THEN
    R> DROP -1 ;

: _PSTORE-BOUND?  ( store work -- flag )
    DUP PSTORE-WORK-VALID? 0= IF 2DROP 0 EXIT THEN
    OVER PSTORE-VALID? 0= IF 2DROP 0 EXIT THEN
    OVER _PST.SEGMENT-FILE PSEG-MAX-RECORD-U@
        OVER _PSW.SEGMENT-WORK _PSEG-W.CAPACITY @ > IF
        2DROP 0 EXIT
    THEN
    DUP _PSW.STORE @ ?DUP IF 2 PICK <> IF 2DROP 0 EXIT THEN THEN
    DUP _PSW.STORE @ >R
    OVER OVER _PSW.STORE !
    DUP _PSTORE-WORK-ALIASES?
    R> 2 PICK _PSW.STORE !
    0= IF 2DROP 0 EXIT THEN
    2DROP -1 ;

