\ Test one caller span against a live PSTORE work object and the segment
\ buffer borrowed by that work.  A null work pointer contributes no spans.
: _PSTORE-SPAN-DISJOINT-WORK?  ( address length work|0 -- flag )
    DUP 0= IF DROP 2DROP -1 EXIT THEN
    >R
    2DUP R@ PSTORE-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP 0 EXIT
    THEN
    2DUP
        R@ _PSW.SEGMENT-WORK _PSEG-W.BUFFER @
        R@ _PSW.SEGMENT-WORK _PSEG-W.CAPACITY @
        MSPAN-OVERLAP? IF
        2DROP R> DROP 0 EXIT
    THEN
    2DROP R> DROP -1 ;

\ Public alias boundary for storage layered above PSTORE.  It covers the
\ descriptor and its inline children, every explicitly borrowed configured
\ object, page-cache backing memory, and any currently bound authority or
\ transaction work plus their segment buffers.  The fault context is opaque
\ and has no size contract, so it is deliberately outside this predicate.
: PSTORE-SPAN-DISJOINT?  ( address length store -- flag )
    >R
    DUP IF OVER 0= IF 2DROP R> DROP 0 EXIT THEN THEN
    2DUP MSPAN-NONWRAPPING? 0= IF 2DROP R> DROP 0 EXIT THEN
    R@ PSTORE-VALID? 0= IF 2DROP R> DROP 0 EXIT THEN
    2DUP R@ PSTORE-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP 0 EXIT
    THEN
    2DUP R@ _PST.PAGE-FILE PPAGE.VFS @ VFS-DESC-SIZE
        MSPAN-OVERLAP? IF
        2DROP R> DROP 0 EXIT
    THEN
    R@ _PST.STATS @ DUP IF
        >R 2DUP R> PERSIST-STATS-SIZE MSPAN-OVERLAP? IF
            2DROP R> DROP 0 EXIT
        THEN
    ELSE
        DROP
    THEN
    R@ _PST.PAGE-FILE PPAGE.CACHE @ DUP IF
        >R
        2DUP R@ PERSIST-PAGE-CACHE-SIZE MSPAN-OVERLAP? IF
            2DROP R> DROP R> DROP 0 EXIT
        THEN
        2DUP R@ _PPC.MEMORY @ R@ _PPC.BYTES @ MSPAN-OVERLAP? IF
            2DROP R> DROP R> DROP 0 EXIT
        THEN
        R> DROP
    ELSE
        DROP
    THEN
    2DUP R@ _PST.LOCK @ PSTORE-SPIN-GUARD-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP 0 EXIT
    THEN
    2DUP R@ _PST.AUTHORITY-WORK @ _PSTORE-SPAN-DISJOINT-WORK? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    2DUP R@ _PST.TRANSACTION-WORK @ _PSTORE-SPAN-DISJOINT-WORK? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    2DROP R> DROP -1 ;

