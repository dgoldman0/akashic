: _PSTORE-ARG-SPAN?  ( work -- flag )
    >R
    R@ _PSW.ARG-U @ DUP 0< IF DROP R> DROP 0 EXIT THEN
    DUP 0= IF DROP R@ _PSW.ARG-A @ 0= R> DROP EXIT THEN
    R@ _PSW.ARG-A @ DUP 0= IF DROP DROP R> DROP 0 EXIT THEN
    OVER MSPAN-NONWRAPPING? 0= IF DROP R> DROP 0 EXIT THEN
    DROP
    R@ _PSW.ARG-A @ R@ _PSW.ARG-U @
        R@ PSTORE-WORK-SIZE MSPAN-OVERLAP? IF R> DROP 0 EXIT THEN
    R@ _PSW.ARG-A @ R@ _PSW.ARG-U @
        R@ _PSW.STORE @ PSTORE-SIZE MSPAN-OVERLAP? IF R> DROP 0 EXIT THEN
    R@ _PSW.ARG-A @ R@ _PSW.ARG-U @
        R@ _PSW.STORE @ _PST.PAGE-FILE PPAGE.VFS @ VFS-DESC-SIZE
        MSPAN-OVERLAP? IF R> DROP 0 EXIT THEN
    R@ _PSW.STORE @ _PST.STATS @ ?DUP IF
        PERSIST-STATS-SIZE R@ _PSW.ARG-A @ R@ _PSW.ARG-U @
            MSPAN-OVERLAP? IF R> DROP 0 EXIT THEN
    THEN
    R@ _PSW.STORE @ _PST.PAGE-FILE PPAGE.CACHE @ ?DUP IF
        DUP PERSIST-PAGE-CACHE-SIZE
            R@ _PSW.ARG-A @ R@ _PSW.ARG-U @ 2SWAP
            MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
        DUP _PPC.MEMORY @ SWAP _PPC.BYTES @
            R@ _PSW.ARG-A @ R@ _PSW.ARG-U @ 2SWAP
            MSPAN-OVERLAP? IF R> DROP 0 EXIT THEN
    THEN
    R@ _PSW.ARG-A @ R@ _PSW.ARG-U @
        R@ _PSW.SEGMENT-WORK _PSEG-W.BUFFER @
        R@ _PSW.SEGMENT-WORK _PSEG-W.CAPACITY @
        MSPAN-OVERLAP? IF R> DROP 0 EXIT THEN
    R@ _PSW.ARG-A @ R@ _PSW.ARG-U @
        R@ _PSW.STORE @ _PST.LOCK @ PSTORE-SPIN-GUARD-SIZE
        MSPAN-OVERLAP? 0= R> DROP ;

: _PSTORE-ARG-REF?  ( work -- flag )
    >R
    R@ _PSW.ARG-REF @ DUP 0= IF DROP R> DROP 0 EXIT THEN
    DUP PERSIST-REF-SIZE MSPAN-NONWRAPPING? 0= IF DROP R> DROP 0 EXIT THEN
    DUP PERSIST-REF-SIZE R@ PSTORE-WORK-SIZE
        MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    DUP PERSIST-REF-SIZE R@ _PSW.STORE @ PSTORE-SIZE
        MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    DUP PERSIST-REF-SIZE
        R@ _PSW.STORE @ _PST.PAGE-FILE PPAGE.VFS @ VFS-DESC-SIZE
        MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    R@ _PSW.STORE @ _PST.STATS @ ?DUP IF
        PERSIST-STATS-SIZE 2 PICK PERSIST-REF-SIZE
            MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    THEN
    R@ _PSW.STORE @ _PST.PAGE-FILE PPAGE.CACHE @ ?DUP IF
        DUP PERSIST-PAGE-CACHE-SIZE 3 PICK PERSIST-REF-SIZE
            MSPAN-OVERLAP? IF 2DROP R> DROP 0 EXIT THEN
        DUP _PPC.MEMORY @ SWAP _PPC.BYTES @
            2 PICK PERSIST-REF-SIZE MSPAN-OVERLAP? IF
            DROP R> DROP 0 EXIT
        THEN
    THEN
    DUP PERSIST-REF-SIZE
        R@ _PSW.SEGMENT-WORK _PSEG-W.BUFFER @
        R@ _PSW.SEGMENT-WORK _PSEG-W.CAPACITY @
        MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    DUP PERSIST-REF-SIZE
        R@ _PSW.STORE @ _PST.LOCK @ PSTORE-SPIN-GUARD-SIZE
        MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    DROP R> DROP -1 ;

