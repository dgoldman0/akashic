\ Provisioning, cold open, and transaction begin
\ =====================================================================

: _PSTORE-PROVISION-RUN  ( work -- status )
    >R
    R@ _PSW.STORE @ _PST.PAGE-FILE R@ _PSW.PAGE-WORK PPAGE-ENSURE
    DUP IF R> DROP EXIT THEN DROP
    R@ _PSW.STORE @ _PST.SEGMENT-FILE
        R@ _PSW.SEGMENT-WORK PSEG-ENSURE
    DUP IF R> DROP EXIT THEN DROP
    R@ _PSW.STORE @ _PST.FLAGS DUP @ _PSTORE-F-PROVISIONED OR SWAP !
    R> DROP PERSIST-S-OK ;

: PSTORE-PROVISION  ( store work -- status )
    _PSTORE-SCOPED-ENTER DUP IF _PSTORE-REJECT EXIT THEN DROP
    DUP ['] _PSTORE-PROVISION-RUN _PSTORE-WORK-CATCH
    ROT ROT _PSTORE-SCOPED-LEAVE ;

: _PSTORE-OPEN-RUN  ( work -- status )
    >R
    R@ _PSW.STORE @ _PST.FLAGS DUP @ _PSTORE-F-LOADED INVERT AND SWAP !
    0 R@ _PSW.STORE @ _PST.AUTHORITY-WORK !
    R@ _PSW.PROPOSED PERSIST-ROOT-VALUE-INIT
    R@ _PSW.PROPOSED R@ _PSW.STORE @ _PST.ROOT-FILE
        R@ _PSW.ROOT-WORK PROOT-LOAD             ( generation status )
    DUP PERSIST-S-ABSENT = IF
        2DROP
        R@ _PSW.STORE @ _PST.CURRENT PERSIST-ROOT-VALUE-INIT
        R@ _PSW.STORE @ _PST.IDENTITY
            R@ _PSW.STORE @ _PST.CURRENT PROOTV.IDENTITY
            PERSIST-IDENTITY-SIZE MOVE
        0 R@ _PSW.STORE @ _PST.GENERATION !
        R@ R@ _PSW.STORE @ _PST.AUTHORITY-WORK !
        R@ _PSW.STORE @ _PST.FLAGS DUP @ _PSTORE-F-LOADED OR SWAP !
        R> DROP PERSIST-S-ABSENT EXIT
    THEN
    DUP IF NIP R> DROP EXIT THEN DROP             ( generation )
    DUP 0> 0= IF DROP R> DROP PERSIST-S-CORRUPT EXIT THEN
    R@ _PSW.STORE @ _PST.IDENTITY
        R@ _PSW.PROPOSED PROOTV.IDENTITY
        PERSIST-IDENTITY-SIZE _PSTORE-BYTES= 0= IF
        DROP R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    DUP R@ _PSW.STORE @ _PST.GENERATION ! DROP
    R@ R@ _PSW.STORE @ _PST.AUTHORITY-WORK !
    R@ _PSW.PROPOSED R@ _PSW.STORE @ _PST.CURRENT
        PERSIST-ROOT-VALUE-COPY
    R@ _PSW.STORE @ _PST.FLAGS DUP @
        _PSTORE-F-LOADED _PSTORE-F-PROVISIONED OR OR SWAP !
    R> DROP PERSIST-S-OK ;

: PSTORE-OPEN  ( store work -- status )
    _PSTORE-SCOPED-ENTER DUP IF _PSTORE-REJECT EXIT THEN DROP
    DUP ['] _PSTORE-OPEN-RUN _PSTORE-WORK-CATCH
    ROT ROT _PSTORE-SCOPED-LEAVE ;

: _PSTORE-BEGIN-RUN  ( work -- status )
    >R
    R@ _PSW.STORE @ _PST.CURRENT PROOTV.PAGE-COUNT @
        R@ _PSW.STORE @ _PST.PAGE-FILE R@ _PSW.PAGE-WORK
        PPAGE-TRUNCATE DUP IF R> DROP EXIT THEN DROP
    R@ _PSW.STORE @ _PST.CURRENT PROOTV.SEGMENT-TAIL @
        R@ _PSW.STORE @ _PST.SEGMENT-FILE R@ _PSW.SEGMENT-WORK
        PSEG-TRUNCATE DUP IF R> DROP EXIT THEN DROP
    R@ _PSW.STORE @ _PST.CURRENT R@ _PSW.PROPOSED
        PERSIST-ROOT-VALUE-COPY
    R> DROP PERSIST-S-OK ;

: PSTORE-BEGIN  ( store work -- status )
    2DUP _PSTORE-BOUND? 0= IF PERSIST-S-INVALID _PSTORE-REJECT EXIT THEN
    OVER _PST.FLAGS @ _PSTORE-F-LOADED AND 0= IF
        PERSIST-S-CONFLICT _PSTORE-REJECT EXIT
    THEN
    OVER _PST.FLAGS @ _PSTORE-F-PROVISIONED AND 0= IF
        PERSIST-S-CONFLICT _PSTORE-REJECT EXIT
    THEN
    OVER _PST.AUTHORITY-WORK @ OVER <> IF
        PERSIST-S-CONFLICT _PSTORE-REJECT EXIT
    THEN
    _PSTORE-SCOPED-ENTER DUP IF _PSTORE-REJECT EXIT THEN DROP
    DUP ['] _PSTORE-BEGIN-RUN _PSTORE-WORK-CATCH DUP IF
        ROT ROT _PSTORE-SCOPED-LEAVE EXIT
    THEN DROP
    -1 OVER _PSW.ACTIVE !
    0 OVER _PSW.BUSY !
    0 OVER _PSW.FAILED !
    0 OVER _PSW.DIRTY !
    DUP 2 PICK _PST.TRANSACTION-WORK !
    PERSIST-S-OK OVER _PSW.STATUS !
    PERSIST-S-OK 2 PICK _PST.STATUS !
    2DROP PERSIST-S-OK ;

