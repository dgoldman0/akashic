: _PSTORE-GUARD-OBJECTS-DISJOINT?  ( guard vfs stats|0 cache|0 -- flag )
    >R >R                                      ( guard vfs ; R: cache stats )
    OVER PSTORE-SPIN-GUARD-SIZE 2 PICK VFS-DESC-SIZE
        MSPAN-OVERLAP? IF
        2DROP R> DROP R> DROP 0 EXIT
    THEN
    DROP                                       ( guard ; R: cache stats )
    R> ?DUP IF                                 ( guard stats ; R: cache )
        OVER PSTORE-SPIN-GUARD-SIZE 2 PICK PERSIST-STATS-SIZE
            MSPAN-OVERLAP? IF
            2DROP R> DROP 0 EXIT
        THEN
        DROP
    THEN
    R> ?DUP IF                                 ( guard cache )
        OVER PSTORE-SPIN-GUARD-SIZE 2 PICK PERSIST-PAGE-CACHE-SIZE
            MSPAN-OVERLAP? IF 2DROP 0 EXIT THEN
        DUP _PPC.MEMORY @ OVER _PPC.BYTES @
        3 PICK PSTORE-SPIN-GUARD-SIZE 2SWAP MSPAN-OVERLAP? IF
            2DROP 0 EXIT
        THEN
        DROP
    THEN
    DROP -1 ;

