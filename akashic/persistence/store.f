: PSTORE-CURRENT-ROOT@  ( store -- root-value|0 )
    DUP PSTORE-VALID? IF _PST.CURRENT ELSE DROP 0 THEN ;

: PSTORE-PROPOSED-ROOT@  ( work -- root-value|0 )
    DUP PSTORE-WORK-VALID? 0= IF DROP 0 EXIT THEN
    DUP _PSW.ACTIVE @ 0= IF DROP 0 EXIT THEN
    _PSW.PROPOSED ;

: PSTORE-PAGE-FILE@  ( store -- page-file|0 )
    DUP PSTORE-VALID? IF _PST.PAGE-FILE ELSE DROP 0 THEN ;

: PSTORE-SEGMENT-FILE@  ( store -- segment-file|0 )
    DUP PSTORE-VALID? IF _PST.SEGMENT-FILE ELSE DROP 0 THEN ;

: PSTORE-ROOT-FILE@  ( store -- root-file|0 )
    DUP PSTORE-VALID? IF _PST.ROOT-FILE ELSE DROP 0 THEN ;

: PSTORE-PAGE-PAYLOAD$  ( work -- payload-a payload-u )
    DUP PSTORE-WORK-VALID? 0= IF DROP 0 0 EXIT THEN
    _PSW.PAGE-WORK PPAGE-PAYLOAD$ ;

: PSTORE-RECORD-PAYLOAD$  ( work -- payload-a payload-u )
    DUP PSTORE-WORK-VALID? 0= IF DROP 0 0 EXIT THEN
    _PSW.SEGMENT-WORK PSEG-PAYLOAD$ ;

: PSTORE-WORK-RECORD-BUFFER$  ( work -- buffer-a buffer-u )
    DUP PSTORE-WORK-VALID? 0= IF DROP 0 0 EXIT THEN
    _PSW.SEGMENT-WORK DUP _PSEG-W.BUFFER @ SWAP _PSEG-W.CAPACITY @ ;

\ Public alias boundary for a specific PSTORE workspace and its caller-owned
\ record buffer.  Unlike PSTORE-SPAN-DISJOINT?, this remains complete before
\ the work object is bound as store authority or transaction state.
: PSTORE-WORK-SPAN-DISJOINT?  ( address length work -- flag )
    >R
    DUP IF OVER 0= IF 2DROP R> DROP 0 EXIT THEN THEN
    2DUP MSPAN-NONWRAPPING? 0= IF 2DROP R> DROP 0 EXIT THEN
    R@ PSTORE-WORK-VALID? 0= IF 2DROP R> DROP 0 EXIT THEN
    R> _PSTORE-SPAN-DISJOINT-WORK? ;

: PSTORE-UNCERTAIN?  ( store -- flag )
    DUP PSTORE-VALID? 0= IF DROP -1 EXIT THEN
    _PST.FLAGS @ _PSTORE-F-UNCERTAIN AND 0<> ;

\ =====================================================================
\ Guard, fault, and operation cleanup
\ =====================================================================

: _PSTORE-MARK-UNCERTAIN  ( store -- )
    DUP _PST.FLAGS DUP @ _PSTORE-F-UNCERTAIN OR SWAP !
    PERSIST-S-UNCERTAIN SWAP _PST.STATUS ! ;

\ Any nested persistence layer can discover that cleanup left durable state
\ ambiguous.  The outer store owns the operation boundary, so it must adopt
\ that uncertainty rather than merely copying the returned status.
: _PSTORE-ADOPT-UNCERTAIN  ( status store -- status )
    >R
    DUP PERSIST-S-UNCERTAIN = IF R@ _PSTORE-MARK-UNCERTAIN THEN
    R@ _PST.FLAGS @ _PSTORE-F-UNCERTAIN AND IF
        DROP PERSIST-S-UNCERTAIN
    THEN
    R> DROP ;

: _PSTORE-LOCK-TRY-RUN  ( store -- status )
    _PST.LOCK @ GUARD-TRY-ACQUIRE IF
        PERSIST-S-OK
    ELSE
        PERSIST-S-BUSY
    THEN ;

: _PSTORE-LOCK-TRY  ( store -- status )
    DUP >R ['] _PSTORE-LOCK-TRY-RUN CATCH
    DUP IF
        2DROP R@ _PSTORE-MARK-UNCERTAIN PERSIST-S-UNCERTAIN
    ELSE
        DROP
    THEN
    R> DROP ;

: _PSTORE-LOCK-RELEASE-RUN  ( store -- )
    _PST.LOCK @ GUARD-RELEASE ;

: _PSTORE-LOCK-RELEASE  ( primary-status store -- final-status )
    DUP >R ['] _PSTORE-LOCK-RELEASE-RUN CATCH
    DUP IF
        2DROP DROP
        R@ _PSTORE-MARK-UNCERTAIN
        PERSIST-S-UNCERTAIN
    ELSE
        DROP
    THEN
    R> DROP ;

: _PSTORE-FAULT-RUN  ( store -- status )
    DUP _PST.FAULT-XT @ ?DUP 0= IF DROP PERSIST-S-OK EXIT THEN
    >R
    DUP _PST.FAULT-POINT @
    OVER _PST.FAULT-ORDINAL @
    ROT _PST.FAULT-CONTEXT @
    R> EXECUTE
    PERSIST-S-OK = IF PERSIST-S-OK ELSE PERSIST-S-FAULT THEN ;

: _PSTORE-FAULT  ( point ordinal store -- status )
    >R
    R@ _PST.FAULT-ORDINAL !
    R@ _PST.FAULT-POINT !
    R@ ['] _PSTORE-FAULT-RUN CATCH
    DUP IF 2DROP PERSIST-S-FAULT ELSE DROP THEN
    DUP IF R@ _PST.STATS @ ?DUP IF _PSTAT-NOTE-FAULT THEN THEN
    0 R@ _PST.FAULT-POINT !
    0 R@ _PST.FAULT-ORDINAL !
    R> DROP ;

: _PSTORE-WORK-CATCH  ( work body-xt -- status )
    >R DUP R> CATCH
    DUP IF
        _PSTORE-DROP3 PERSIST-S-FAULT
    ELSE
        DROP NIP
    THEN ;

: _PSTORE-REJECT  ( store work status -- status )
    >R
    DUP PSTORE-WORK-VALID? IF R@ OVER _PSW.STATUS ! THEN
    OVER PSTORE-VALID? IF R@ 2 PICK _PST.STATUS ! THEN
    2DROP R> ;

: _PSTORE-SCOPED-ENTER  ( store work -- store work status )
    2DUP _PSTORE-BOUND? 0= IF PERSIST-S-INVALID EXIT THEN
    DUP _PSW.BUSY @ IF PERSIST-S-BUSY EXIT THEN
    DUP _PSW.ACTIVE @ IF PERSIST-S-BUSY EXIT THEN
    OVER _PST.FLAGS @ _PSTORE-F-UNCERTAIN AND IF
        PERSIST-S-UNCERTAIN EXIT
    THEN
    OVER _PST.TRANSACTION-WORK @ IF PERSIST-S-BUSY EXIT THEN
    OVER _PSTORE-LOCK-TRY DUP IF EXIT THEN DROP
    OVER _PST.TRANSACTION-WORK @ IF
        PERSIST-S-BUSY 2 PICK _PSTORE-LOCK-RELEASE EXIT
    THEN
    -1 OVER _PSW.BUSY !
    OVER OVER _PSW.STORE !
    PERSIST-S-INVALID OVER _PSW.STATUS !
    PERSIST-S-OK ;

: _PSTORE-SCOPED-LEAVE  ( status store work -- final-status )
    >R
    0 R@ _PSW.BUSY !
    0 R@ _PSW.STORE !
    DUP >R _PSTORE-LOCK-RELEASE
    R@ _PSTORE-ADOPT-UNCERTAIN
    DUP R@ _PST.STATUS !
    R> DROP
    DUP R@ _PSW.STATUS !
    R> DROP ;

: _PSTORE-TX-LEAVE  ( status store work -- final-status )
    >R
    0 R@ _PSW.BUSY !
    0 R@ _PSW.ACTIVE !
    0 R@ _PSW.FAILED !
    0 R@ _PSW.DIRTY !
    0 R@ _PSW.STORE !
    0 OVER _PST.TRANSACTION-WORK !
    DUP >R _PSTORE-LOCK-RELEASE
    R@ _PSTORE-ADOPT-UNCERTAIN
    DUP R@ _PST.STATUS !
    R> DROP
    DUP R@ _PSW.STATUS !
    R> DROP ;

: _PSTORE-TX-OWNED?  ( store work -- flag )
    2DUP _PSTORE-BOUND? 0= IF 2DROP 0 EXIT THEN
    DUP _PSW.ACTIVE @ 0= IF 2DROP 0 EXIT THEN
    DUP _PSW.STORE @ 2 PICK <> IF 2DROP 0 EXIT THEN
    OVER _PST.TRANSACTION-WORK @ OVER <> IF 2DROP 0 EXIT THEN
    OVER _PST.LOCK @ GUARD-MINE? 0= IF 2DROP 0 EXIT THEN
    2DROP -1 ;

: _PSTORE-TX-READY?  ( store work -- flag )
    2DUP _PSTORE-TX-OWNED? 0= IF 2DROP 0 EXIT THEN
    DUP _PSW.BUSY @ IF 2DROP 0 EXIT THEN
    DUP _PSW.FAILED @ IF 2DROP 0 EXIT THEN
    OVER _PST.FLAGS @ _PSTORE-F-UNCERTAIN AND 0= >R 2DROP R> ;

: PSTORE-TX-READY?  ( store work -- flag )
    _PSTORE-TX-READY? ;

\ Mark an owned active transaction unusable after a failure discovered by a
\ layer above PSTORE.  This is deliberately a memory-only state transition:
\ the layered operation keeps its original status while every subsequent
\ transaction operation and commit is rejected until explicit abort.
: PSTORE-TX-POISON  ( failure-status store work -- failure-status )
    2 PICK DUP PERSIST-S-OK <= SWAP PERSIST-S-FAULT > OR IF
        2DROP DROP PERSIST-S-INVALID EXIT
    THEN
    2DUP _PSTORE-TX-OWNED? 0= IF
        2DROP DROP PERSIST-S-BUSY EXIT
    THEN
    DUP _PSW.BUSY @ IF
        2DROP DROP PERSIST-S-BUSY EXIT
    THEN
    ROT >R
    R@ PERSIST-S-UNCERTAIN = IF OVER _PSTORE-MARK-UNCERTAIN THEN
    -1 OVER _PSW.FAILED !
    R@ OVER _PSW.STATUS !
    R@ 2 PICK _PST.STATUS !
    2DROP R> ;

: _PSTORE-TX-OP-END  ( status work -- status )
    >R
    R@ _PSW.STORE @ _PSTORE-ADOPT-UNCERTAIN
    DUP IF -1 R@ _PSW.FAILED ! THEN
    DUP R@ _PSW.STATUS !
    DUP R@ _PSW.STORE @ _PST.STATUS !
    0 R@ _PSW.BUSY !
    R> DROP ;

