: PSTORE-WRITE-PAGE-TX
  ( payload-a payload-u page-id store work -- status )
    >R
    DUP R@ _PSTORE-TX-READY? 0= IF
        _PSTORE-DROP4 R> DROP PERSIST-S-BUSY EXIT
    THEN
    1 PICK R@ _PSW.RESULT !
    2 PICK R@ _PSW.ARG-U !
    3 PICK R@ _PSW.ARG-A !
    -1 R@ _PSW.BUSY !
    R@ ['] _PSTORE-WRITE-PAGE-RUN _PSTORE-WORK-CATCH
    R@ _PSTORE-TX-OP-END
    >R _PSTORE-DROP4 R> R> DROP ;

: PSTORE-APPLICATION-ROOT!  ( page-id store work -- status )
    >R
    DUP R@ _PSTORE-TX-READY? 0= IF
        2DROP DROP R> DROP PERSIST-S-BUSY EXIT
    THEN
    -1 R@ _PSW.BUSY !
    1 PICK DUP 0< IF
        DROP PERSIST-S-INVALID
    ELSE
        R@ _PSW.PROPOSED PROOTV.PAGE-COUNT @ < IF
            1 PICK R@ _PSW.PROPOSED PROOTV.APPLICATION-ROOT !
            -1 R@ _PSW.DIRTY !
            PERSIST-S-OK
        ELSE
            PERSIST-S-NOT-FOUND
        THEN
    THEN
    R@ _PSTORE-TX-OP-END
    >R 2DROP R> R> DROP ;

\ =====================================================================
\ Commit and abort
\ =====================================================================

: _PSTORE-COMMIT-RUN  ( work -- status )
    >R
    R@ _PSW.FAILED @ IF R> DROP PERSIST-S-CONFLICT EXIT THEN
    R@ _PSW.DIRTY @ 0= IF R> DROP PERSIST-S-CONFLICT EXIT THEN
    R@ _PSW.PROPOSED PERSIST-ROOT-VALUE-VALID? 0= IF
        R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PSW.PROPOSED PROOTV.PAGE-COUNT @ 0> 0= IF
        R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PSW.STORE @ _PST.PAGE-FILE PPAGE-SYNC
    DUP IF R> DROP EXIT THEN DROP
    R@ _PSW.STORE @ _PST.SEGMENT-FILE PSEG-SYNC
    DUP IF R> DROP EXIT THEN DROP
    R@ _PSW.STORE @ _PST.GENERATION @ 1 _PSTORE-ADD?
    0= IF DROP R> DROP PERSIST-S-CAPACITY EXIT THEN
    PERSIST-FAULT-DATA-SYNCED SWAP R@ _PSW.STORE @ _PSTORE-FAULT
    DUP IF R> DROP EXIT THEN DROP
    R@ _PSW.PROPOSED R@ _PSW.STORE @ _PST.ROOT-FILE
        R@ _PSW.ROOT-WORK PROOT-PUBLISH
    R@ _PSW.ROOT-WORK PROOT-DURABLE? IF
        R@ _PSW.PROPOSED R@ _PSW.STORE @ _PST.CURRENT
            PERSIST-ROOT-VALUE-COPY
        R@ _PSW.ROOT-WORK PROOT-GENERATION@
            R@ _PSW.STORE @ _PST.GENERATION !
        R@ _PSW.STORE @ _PST.FLAGS DUP @
            _PSTORE-F-PROVISIONED _PSTORE-F-LOADED OR OR SWAP !
    ELSE
        R@ _PSW.ROOT-WORK PROOT-LAST-OUTCOME@ GPAIR-W-MAYBE =
        OVER PERSIST-S-OK = OR IF
            DROP
            R@ _PSW.STORE @ _PSTORE-MARK-UNCERTAIN
            PERSIST-S-UNCERTAIN
        THEN
    THEN
    R> DROP ;

: PSTORE-COMMIT  ( store work -- status )
    2DUP _PSTORE-TX-OWNED? 0= IF
        PERSIST-S-BUSY _PSTORE-REJECT EXIT
    THEN
    DUP _PSW.FAILED @ IF
        2DROP PERSIST-S-CONFLICT EXIT
    THEN
    -1 OVER _PSW.BUSY !
    DUP ['] _PSTORE-COMMIT-RUN _PSTORE-WORK-CATCH
    ROT ROT _PSTORE-TX-LEAVE ;

: PSTORE-ABORT  ( store work -- status )
    2DUP _PSTORE-TX-OWNED? 0= IF
        PERSIST-S-BUSY _PSTORE-REJECT EXIT
    THEN
    -1 OVER _PSW.BUSY !
    PERSIST-S-OK ROT ROT _PSTORE-TX-LEAVE ;

\ Read directly through the workspace that owns an active transaction.  This
\ deliberately does not reacquire the store guard: it verifies against the
\ proposed page bound, including pages appended earlier in the same proposal.
: _PSTORE-READ-PAGE-TX-RUN  ( work -- status )
    >R
    R@ _PSW.RESULT @ DUP 0< IF
        DROP R> DROP PERSIST-S-NOT-FOUND EXIT
    THEN
    R@ _PSW.PROPOSED PROOTV.PAGE-COUNT @ 2DUP >= IF
        2DROP R> DROP PERSIST-S-NOT-FOUND EXIT
    THEN
    R@ _PSW.STORE @ _PST.PAGE-FILE R@ _PSW.PAGE-WORK
        PPAGE-VERIFY R> DROP ;

: PSTORE-READ-PAGE-TX  ( page-id store work -- status )
    >R
    DUP R@ _PSTORE-TX-READY? 0= IF
        2DROP R> DROP PERSIST-S-BUSY EXIT
    THEN
    1 PICK R@ _PSW.RESULT !
    -1 R@ _PSW.BUSY !
    R@ ['] _PSTORE-READ-PAGE-TX-RUN _PSTORE-WORK-CATCH
    R@ _PSTORE-TX-OP-END
    >R 2DROP R> R> DROP ;

