: _PSTORE-APPEND-RECORD-RUN  ( work -- status )
    >R
    R@ _PSTORE-ARG-SPAN? R@ _PSTORE-ARG-REF? AND 0= IF
        R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PSW.PROPOSED PROOTV.RECORD-COUNT @ 1 _PSTORE-ADD?
    0= IF DROP R> DROP PERSIST-S-CAPACITY EXIT THEN
    DUP 0> 0= IF DROP R> DROP PERSIST-S-CAPACITY EXIT THEN
    DUP R@ _PSW.RESULT !
    R@ _PSW.ARG-A @ R@ _PSW.ARG-U @ ROT
    R@ _PSW.PROPOSED PROOTV.SEGMENT-TAIL @
    R@ _PSW.ARG-REF @
    R@ _PSW.STORE @ _PST.SEGMENT-FILE R@ _PSW.SEGMENT-WORK
        PSEG-WRITE DUP IF R> DROP EXIT THEN DROP

    PERSIST-FAULT-SEGMENT-WRITTEN R@ _PSW.RESULT @
        R@ _PSW.STORE @ _PSTORE-FAULT DUP IF R> DROP EXIT THEN DROP
    R@ _PSW.ARG-REF @ PREF.OFFSET @
        R@ _PSW.PROPOSED PROOTV.SEGMENT-TAIL @ <> IF
        R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    R@ _PSW.ARG-REF @ PREF.ORDINAL @ R@ _PSW.RESULT @ <> IF
        R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    R@ _PSW.ARG-REF @ PREF.OFFSET @
        R@ _PSW.ARG-REF @ PREF.SPAN @ _PSTORE-ADD?
    0= IF DROP R> DROP PERSIST-S-CAPACITY EXIT THEN
    DUP R@ _PSW.TEMP ! DROP

    R@ _PSW.ARG-REF @ R@ _PSW.TEMP @
        R@ _PSW.STORE @ _PST.SEGMENT-FILE R@ _PSW.SEGMENT-WORK
