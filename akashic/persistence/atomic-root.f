        DROP PERSIST-S-CORRUPT EXIT
    THEN
    GPAIR-CANDIDATE-VALUE@
    OVER _PROOT-W.VALUE @
    PERSIST-ROOT-VALUE-SIZE MOVE
    DROP PERSIST-S-OK ;

