: PROOT-PATH-A$  ( root -- path-a path-u )
    DUP _PROOT-F.PATH-A SWAP _PROOT-F.PATH-A-U @ ;

: PROOT-PATH-B$  ( root -- path-a path-u )
    DUP _PROOT-F.PATH-B SWAP _PROOT-F.PATH-B-U @ ;

: _PROOT-SLOT-PATH$  ( slot root -- path-a path-u )
    >R
    GPAIR-SLOT-A = IF R> PROOT-PATH-A$ ELSE R> PROOT-PATH-B$ THEN ;

: _PROOT-PATHS-DISTINCT?  ( root -- flag )
    DUP PROOT-PATH-A$ 2 PICK PROOT-PATH-B$
        _PROOT-BYTES-EQUAL? IF DROP 0 EXIT THEN
    DUP PROOT-PATH-A$ 2 PICK _PROOT-F.PAGE-FILE @ PPAGE-FILE-PATH$
        _PROOT-BYTES-EQUAL? IF DROP 0 EXIT THEN
    DUP PROOT-PATH-B$ 2 PICK _PROOT-F.PAGE-FILE @ PPAGE-FILE-PATH$
        _PROOT-BYTES-EQUAL? IF DROP 0 EXIT THEN
    DUP PROOT-PATH-A$ 2 PICK _PROOT-F.SEGMENT @ PSEG-FILE-PATH$
        _PROOT-BYTES-EQUAL? IF DROP 0 EXIT THEN
    DUP PROOT-PATH-B$ 2 PICK _PROOT-F.SEGMENT @ PSEG-FILE-PATH$
        _PROOT-BYTES-EQUAL? IF DROP 0 EXIT THEN
    DUP _PROOT-F.PAGE-FILE @ PPAGE-FILE-PATH$
    2 PICK _PROOT-F.SEGMENT @ PSEG-FILE-PATH$
        _PROOT-BYTES-EQUAL? 0= NIP ;

: PROOT-VALID?  ( root -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP PROOT-FILE-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _PROOT-F.MAGIC @ _PROOT-FILE-MAGIC <> IF DROP 0 EXIT THEN
    DUP _PROOT-F.SELF @ OVER <> IF DROP 0 EXIT THEN
    DUP _PROOT-F.PAGE-FILE @ DUP PPAGE-FILE-VALID? 0= IF
        2DROP 0 EXIT
    THEN DROP
    DUP _PROOT-F.SEGMENT @ DUP PSEG-FILE-VALID? 0= IF
        2DROP 0 EXIT
    THEN DROP
    DUP _PROOT-F.VFS @ DUP 0= IF 2DROP 0 EXIT THEN
    VFS-DESC-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _PROOT-F.PAGE-FILE @ PPAGE.VFS @
        OVER _PROOT-F.VFS @ <> IF DROP 0 EXIT THEN
    DUP _PROOT-F.SEGMENT @ PSEG-FILE-VFS@
        OVER _PROOT-F.VFS @ <> IF DROP 0 EXIT THEN
    DUP _PROOT-F.STATS @ ?DUP IF
        PERSIST-STATS-VALID? 0= IF DROP 0 EXIT THEN
    THEN
    DUP _PROOT-F.FAULT-XT @ 0= IF
        DUP _PROOT-F.FAULT-CTX @ IF DROP 0 EXIT THEN
    THEN
    DUP PROOT-PATH-A$ _PERSIST-ABSOLUTE-PATH? 0= IF DROP 0 EXIT THEN
    DUP PROOT-PATH-B$ _PERSIST-ABSOLUTE-PATH? 0= IF DROP 0 EXIT THEN
    DUP _PROOT-F.SLOT-A-GEN @ 0< IF DROP 0 EXIT THEN
    DUP _PROOT-F.SLOT-B-GEN @ 0< IF DROP 0 EXIT THEN
    DUP _PROOT-F.FLAGS @ IF DROP 0 EXIT THEN
    DUP _PROOT-F.RESERVED @ IF DROP 0 EXIT THEN
    DUP _PROOT-F.SPEC CREC-SPEC-VALID? 0= IF DROP 0 EXIT THEN
    DUP _PROOT-F.SPEC CREC-SPEC-MODE@ CREC-MODE-FIXED <> IF
        DROP 0 EXIT
    THEN
    DUP _PROOT-F.SPEC CREC-SPEC-FORMAT@ _PROOT-RECORD-FORMAT <> IF
        DROP 0 EXIT
    THEN
    DUP _PROOT-F.SPEC CREC-SPEC-PAYLOAD-MIN@
        PERSIST-ROOT-VALUE-SIZE <> IF DROP 0 EXIT THEN
    DUP _PROOT-F.SPEC CREC-SPEC-PAYLOAD-MAX@
        PERSIST-ROOT-VALUE-SIZE <> IF DROP 0 EXIT THEN
    DUP _PROOT-F.SPEC CREC-SPEC-TAG-POLICY@
        CREC-TAG-POSITIVE <> IF DROP 0 EXIT THEN
    _PROOT-PATHS-DISTINCT? ;

: PROOT-SLOT-GENERATION@  ( slot root -- generation|0 )
    DUP PROOT-VALID? 0= IF 2DROP 0 EXIT THEN
    SWAP DUP GPAIR-SLOT-A = IF
        DROP _PROOT-F.SLOT-A-GEN @
    ELSE
        GPAIR-SLOT-B = IF _PROOT-F.SLOT-B-GEN @ ELSE DROP 0 THEN
    THEN ;

\ Arguments are two path pairs, page/segment descriptors, optional stats
\ and fault callback/context, then the destination root descriptor.
: _PROOT-INIT-ARGS?  ( ten-arguments -- flag )
    >R
    R@ 0= IF _PROOT-DROP9 R> DROP 0 EXIT THEN
    R@ PROOT-FILE-SIZE MSPAN-NONWRAPPING? 0= IF
        _PROOT-DROP9 R> DROP 0 EXIT
    THEN
    8 PICK 8 PICK _PERSIST-ABSOLUTE-PATH? 0= IF
        _PROOT-DROP9 R> DROP 0 EXIT
    THEN
    6 PICK 6 PICK _PERSIST-ABSOLUTE-PATH? 0= IF
        _PROOT-DROP9 R> DROP 0 EXIT
    THEN
    4 PICK PPAGE-FILE-VALID? 0= IF _PROOT-DROP9 R> DROP 0 EXIT THEN
    3 PICK PSEG-FILE-VALID? 0= IF _PROOT-DROP9 R> DROP 0 EXIT THEN
    4 PICK PPAGE.VFS @ 4 PICK PSEG-FILE-VFS@ <> IF
        _PROOT-DROP9 R> DROP 0 EXIT
    THEN
    2 PICK ?DUP IF PERSIST-STATS-VALID? 0= IF
        _PROOT-DROP9 R> DROP 0 EXIT
    THEN THEN
    1 PICK 0= IF DUP IF _PROOT-DROP9 R> DROP 0 EXIT THEN THEN
    8 PICK 8 PICK R@ PROOT-FILE-SIZE MSPAN-OVERLAP? IF
        _PROOT-DROP9 R> DROP 0 EXIT
    THEN
    6 PICK 6 PICK R@ PROOT-FILE-SIZE MSPAN-OVERLAP? IF
        _PROOT-DROP9 R> DROP 0 EXIT
    THEN
    4 PICK PERSIST-PAGE-FILE-SIZE R@ PROOT-FILE-SIZE
        MSPAN-OVERLAP? IF _PROOT-DROP9 R> DROP 0 EXIT THEN
    3 PICK PSEG-FILE-SIZE R@ PROOT-FILE-SIZE
        MSPAN-OVERLAP? IF _PROOT-DROP9 R> DROP 0 EXIT THEN
    4 PICK PPAGE.VFS @ VFS-DESC-SIZE R@ PROOT-FILE-SIZE
        MSPAN-OVERLAP? IF _PROOT-DROP9 R> DROP 0 EXIT THEN
    R@ PROOT-FILE-SIZE 6 PICK _PROOT-PAGE-CACHE-DISJOINT? 0= IF
        _PROOT-DROP9 R> DROP 0 EXIT
    THEN
    2 PICK ?DUP IF
        PERSIST-STATS-SIZE R@ PROOT-FILE-SIZE MSPAN-OVERLAP? IF
            _PROOT-DROP9 R> DROP 0 EXIT
        THEN
    THEN
    4 PICK PERSIST-PAGE-FILE-SIZE 5 PICK PSEG-FILE-SIZE
        MSPAN-OVERLAP? IF _PROOT-DROP9 R> DROP 0 EXIT THEN
    _PROOT-DROP9 R> DROP -1 ;

\ ( path-a-a path-a-u path-b-a path-b-u page-file segment-file
\   stats|0 fault-xt|0 fault-context root -- persist-status )
: PROOT-INIT  ( ten-arguments -- persist-status )
    9 PICK 9 PICK 9 PICK 9 PICK 9 PICK 9 PICK 9 PICK 9 PICK 9 PICK
    9 PICK _PROOT-INIT-ARGS? 0= IF
        _PROOT-DROP9 DROP PERSIST-S-INVALID EXIT
    THEN
    >R
    R@ PROOT-FILE-SIZE 0 FILL
    _PROOT-FILE-MAGIC R@ _PROOT-F.MAGIC !
    R@ R@ _PROOT-F.SELF !
    4 PICK R@ _PROOT-F.PAGE-FILE !
    3 PICK R@ _PROOT-F.SEGMENT !
    4 PICK PPAGE.VFS @ R@ _PROOT-F.VFS !
    2 PICK R@ _PROOT-F.STATS !
    1 PICK R@ _PROOT-F.FAULT-XT !
    DUP R@ _PROOT-F.FAULT-CTX !
    8 PICK 8 PICK R@ _PROOT-F.PATH-A SWAP MOVE
    7 PICK R@ _PROOT-F.PATH-A-U !
    6 PICK 6 PICK R@ _PROOT-F.PATH-B SWAP MOVE
    5 PICK R@ _PROOT-F.PATH-B-U !
    _PROOT-RECORD-MAGIC 8 _PROOT-RECORD-FORMAT CREC-TAG-POSITIVE
        ['] _PROOT-ENCODE ['] _PROOT-VALIDATE R@ _PROOT-F.SPEC
        CREC-SPEC-INIT
    DUP IF
        _PROOT-CREC>STATUS R> DUP PROOT-FILE-SIZE 0 FILL DROP
        >R _PROOT-DROP9 R> EXIT
    THEN DROP
    PERSIST-ROOT-VALUE-SIZE PROOT-RECORD-SIZE R@ _PROOT-F.SPEC
        CREC-SPEC-FIXED!
    DUP IF
        _PROOT-CREC>STATUS R> DUP PROOT-FILE-SIZE 0 FILL DROP
        >R _PROOT-DROP9 R> EXIT
    THEN DROP
    R@ _PROOT-F.SPEC CREC-SPEC-SEAL
    DUP IF
        _PROOT-CREC>STATUS R> DUP PROOT-FILE-SIZE 0 FILL DROP
        >R _PROOT-DROP9 R> EXIT
    THEN DROP
    R@ PROOT-VALID? 0= IF
        R> DUP PROOT-FILE-SIZE 0 FILL DROP
        _PROOT-DROP9 PERSIST-S-INVALID EXIT
    THEN
    R> DROP _PROOT-DROP9 PERSIST-S-OK ;

