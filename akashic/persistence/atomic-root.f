    R@ _PROOT-W.RECORD-U !
    R@ _PROOT-W.ROOT @ _PROOT-F.SEGMENT @
    R@ _PROOT-W.SEG-WORK PSEG-FILE-SIZE?
    DUP IF
        NIP _PROOT-DATA-STATUS R> DROP EXIT
    THEN DROP
    R@ _PROOT-W.RECORD-U @ < IF
        R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    R> DROP PERSIST-S-OK ;

