: PROOT-WORK-VALID?  ( work -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP PROOT-WORK-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _PROOT-W.MAGIC @ _PROOT-WORK-MAGIC <> IF DROP 0 EXIT THEN
    DUP _PROOT-W.SELF @ OVER <> IF DROP 0 EXIT THEN
    DUP _PROOT-W.BUSY @ DUP 0= SWAP 1 = OR 0= IF DROP 0 EXIT THEN
    DUP _PROOT-W.STATUS @ _PROOT-STATUS? 0= IF DROP 0 EXIT THEN
    DUP _PROOT-W.SLOT @ DUP -1 < SWAP GPAIR-SLOT-B > OR IF
        DROP 0 EXIT
    THEN
    DUP _PROOT-W.GENERATION @ 0< IF DROP 0 EXIT THEN
    DUP _PROOT-W.LOADED @ DUP 0= SWAP 1 = OR 0= IF DROP 0 EXIT THEN
    DUP _PROOT-W.RESERVED @ IF DROP 0 EXIT THEN
    DUP _PROOT-W.CREC CREC-WORK-VALID? 0= IF DROP 0 EXIT THEN
    DUP _PROOT-W.CANDIDATE-A GPAIR-CANDIDATE-VALID? 0= IF
        DROP 0 EXIT
    THEN
    DUP _PROOT-W.CANDIDATE-B GPAIR-CANDIDATE-VALID? 0= IF
        DROP 0 EXIT
    THEN
    DUP _PROOT-W.GPAIR DUP GPAIR-VALID? 0= IF 2DROP 0 EXIT THEN
    GPAIR-CONTEXT@ OVER <> IF DROP 0 EXIT THEN
    DUP _PROOT-W.PAGE-WORK PPAGE-WORK-VALID? 0= IF DROP 0 EXIT THEN
    DUP _PROOT-W.SEG-WORK PSEG-WORK-VALID? 0= IF DROP 0 EXIT THEN
    DROP -1 ;

: PROOT-GENERATION@  ( work -- generation|0 )
    DUP PROOT-WORK-VALID? 0= IF DROP 0 EXIT THEN
    _PROOT-W.GPAIR GPAIR-GENERATION@ ;

: PROOT-ACTIVE-SLOT@  ( work -- slot|-1 )
    DUP PROOT-WORK-VALID? 0= IF DROP -1 EXIT THEN
    _PROOT-W.GPAIR GPAIR-ACTIVE-SLOT@ ;

: PROOT-LAST-OUTCOME@  ( work -- outcome )
    DUP PROOT-WORK-VALID? 0= IF DROP GPAIR-W-NO-EFFECT EXIT THEN
    _PROOT-W.GPAIR GPAIR-OUTCOME@ ;

: PROOT-LAST-STATUS@  ( work -- persist-status )
    DUP PROOT-WORK-VALID? 0= IF DROP PERSIST-S-INVALID EXIT THEN
    _PROOT-W.STATUS @ ;

: PROOT-FALLBACK?  ( work -- flag )
    DUP PROOT-WORK-VALID? 0= IF DROP 0 EXIT THEN
    _PROOT-W.GPAIR GPAIR-RESULT@ GPAIR-R-FALLBACK = ;

: PROOT-DURABLE?  ( work -- flag )
    PROOT-LAST-OUTCOME@ GPAIR-W-DURABLE = ;

: _PROOT-WORK-END  ( persist-status work -- persist-status )
    >R
    DUP R@ _PROOT-W.STATUS !
    0 R@ _PROOT-W.FD !
    0 R@ _PROOT-W.BUSY !
    R> DROP ;

: _PROOT-CLOSE  ( primary-status work -- final-status )
    >R
    R@ _PROOT-W.SCOPE VFA-SCOPE-CLOSE?
    IF DROP PERSIST-S-UNCERTAIN THEN
    R> DROP ;

: _PROOT-OPEN  ( flags work -- fd persist-status )
    >R
    R@ _PROOT-W.ROOT @ _PROOT-F.VFS @ R@ _PROOT-W.SCOPE
        VFA-SCOPE-INIT
    DUP IF DROP R> DROP 0 PERSIST-S-IO EXIT THEN DROP
    R@ _PROOT-W.SLOT @ R@ _PROOT-W.ROOT @ _PROOT-SLOT-PATH$
    ROT R@ _PROOT-W.SCOPE VFA-SCOPE-OPEN?
    DUP IF NIP _PROOT-IOR>STATUS R> DROP 0 SWAP EXIT THEN
    DROP DUP R@ _PROOT-W.FD ! R> DROP PERSIST-S-OK ;

: _PROOT-RUN-CATCH  ( work body-xt -- persist-status )
    >R DUP R> CATCH
    DUP IF
        DROP DROP
        DUP _PROOT-W.SCOPE VFA-SCOPE-VALID? IF
            PERSIST-S-FAULT SWAP _PROOT-CLOSE
        ELSE
            DROP PERSIST-S-FAULT
        THEN
    ELSE
        DROP NIP
    THEN ;

: _PROOT-NOTE-READ  ( bytes work -- )
