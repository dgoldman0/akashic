\ =====================================================================
\  atomic-root.f - Checked two-slot publication for persistence roots
\ =====================================================================
\  The root files are the only authority boundary in the neutral store.
\  Each exact 160-byte snapshot contains a checked-record envelope, a
\  positive generation tag, and one 96-byte PERSIST root value.  Paths,
\  VFS ownership, statistics, fault injection, and all operation storage
\  are supplied by the caller.
\ =====================================================================

