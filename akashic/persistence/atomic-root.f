1 CONSTANT _PROOT-RECORD-FORMAT
PERSIST-RECORD-HEADER-SIZE PERSIST-ROOT-VALUE-SIZE +
    CONSTANT PROOT-RECORD-SIZE

\ Serialized identity only; this is not mutable operation scratch.
CREATE _PROOT-RECORD-MAGIC
    0x50 C, 0x52 C, 0x4F C, 0x4F C, 0x54 C, 0x52 C, 0x31 C, 0x30 C,

: _PROOT-DROP3  ( x1 x2 x3 -- ) 2DROP DROP ;
: _PROOT-DROP4  ( x1 x2 x3 x4 -- ) 2DROP 2DROP ;
: _PROOT-DROP9  ( x1 x2 x3 x4 x5 x6 x7 x8 x9 -- )
    2DROP 2DROP 2DROP 2DROP DROP ;

: _PROOT-BUFFER?  ( address length -- flag )
    DUP 0< IF 2DROP 0 EXIT THEN
    DUP 0= IF DROP DROP -1 EXIT THEN
    OVER 0= IF 2DROP 0 EXIT THEN
    MSPAN-NONWRAPPING? ;

: _PROOT-STATUS?  ( status -- flag )
    DUP PERSIST-S-OK >= SWAP PERSIST-S-FAULT <= AND ;

: _PROOT-BYTES-EQUAL?  ( a1 u1 a2 u2 -- flag )
    2 PICK OVER <> IF 2DROP 2DROP 0 EXIT THEN
    DUP 0 ?DO
        3 PICK I + C@ 2 PICK I + C@ <> IF
            2DROP 2DROP 0 UNLOOP EXIT
        THEN
    LOOP
    2DROP 2DROP -1 ;

: _PROOT-PAGE-CACHE-DISJOINT?  ( address length page-file -- flag )
    PPAGE.CACHE @ ?DUP 0= IF 2DROP -1 EXIT THEN
    >R
    2DUP R@ PERSIST-PAGE-CACHE-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP 0 EXIT
    THEN
    R@ _PPC.MEMORY @ R@ _PPC.BYTES @ MSPAN-OVERLAP? 0=
    R> DROP ;

: _PROOT-CREC>STATUS  ( crec-status -- persist-status )
    DUP CREC-S-OK = IF DROP PERSIST-S-OK EXIT THEN
    DUP CREC-S-INVALID = IF DROP PERSIST-S-INVALID EXIT THEN
    DUP CREC-S-CAPACITY = IF DROP PERSIST-S-CAPACITY EXIT THEN
    DUP CREC-S-BUSY = IF DROP PERSIST-S-BUSY EXIT THEN
    DUP CREC-S-CALLBACK = OVER CREC-S-FAULT = OR IF
        DROP PERSIST-S-FAULT EXIT
    THEN
    DROP PERSIST-S-CORRUPT ;

: _PROOT-IOR>STATUS  ( ior -- persist-status )
    DUP 0= IF DROP PERSIST-S-OK EXIT THEN
    VFS-IOR-REASON VFS-R-NOENT = IF
        PERSIST-S-ABSENT
    ELSE
        PERSIST-S-IO
    THEN ;

\ =====================================================================
\  Checked snapshot callbacks
\ =====================================================================

: _PROOT-ENCODE
  ( source source-u payload payload-u generation -- crec-status )
    >R
    DUP PERSIST-ROOT-VALUE-SIZE <> IF
        R> DROP 2DROP 2DROP CREC-S-SEMANTIC EXIT
    THEN DROP
    1 PICK PERSIST-ROOT-VALUE-SIZE <> IF
        R> DROP 2DROP DROP CREC-S-SEMANTIC EXIT
    THEN
    2 PICK PERSIST-ROOT-VALUE-VALID? 0= IF
        R> DROP 2DROP DROP CREC-S-SEMANTIC EXIT
    THEN
    R@ 0> 0= IF R> DROP 2DROP DROP CREC-S-SEMANTIC EXIT THEN
    >R DROP R>
    PERSIST-ROOT-VALUE-SIZE MOVE
    R> DROP CREC-S-OK ;

: _PROOT-VALIDATE
  ( context context-u payload payload-u generation -- crec-status )
    >R
    DUP PERSIST-ROOT-VALUE-SIZE <> IF
        R> DROP 2DROP 2DROP CREC-S-SEMANTIC EXIT
    THEN DROP
    DUP PERSIST-ROOT-VALUE-VALID? 0= IF
        R> DROP _PROOT-DROP3 CREC-S-SEMANTIC EXIT
    THEN
    DROP
    OR IF R> DROP CREC-S-INVALID EXIT THEN
    R> 0> IF CREC-S-OK ELSE CREC-S-SEMANTIC THEN ;

