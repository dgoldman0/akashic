: _PROOT-OPEN-WRITABLE  ( work -- persist-status )
    >R
    VFS-FF-READ VFS-FF-WRITE OR R@ _PROOT-OPEN
    DUP PERSIST-S-ABSENT = IF
        2DROP
        R@ _PROOT-W.SLOT @ R@ _PROOT-W.ROOT @ _PROOT-SLOT-PATH$
        R@ _PROOT-W.ROOT @ _PROOT-F.VFS @ VFS-CREATE
        DUP 0= IF
            DROP R> DROP PERSIST-S-IO EXIT
        THEN DROP
        VFS-FF-READ VFS-FF-WRITE OR R@ _PROOT-OPEN
    THEN
    DUP IF NIP R> DROP EXIT THEN
    2DROP R> DROP PERSIST-S-OK ;

: _PROOT-FAULT-WITH-CLOSE  ( point work -- persist-status )
    >R R@ _PROOT-FAULT
    DUP IF R> _PROOT-CLOSE ELSE R> DROP THEN ;

: _PROOT-WRITE-SNAPSHOT  ( work -- persist-status )
    >R
    R@ _PROOT-OPEN-WRITABLE DUP IF R> DROP EXIT THEN DROP
    0 R@ _PROOT-W.FD @ VFS-TRUNCATE IF
        PERSIST-S-IO R> _PROOT-CLOSE EXIT
    THEN
    0 R@ _PROOT-W.FD @ VFS-SEEK? IF
        PERSIST-S-IO R> _PROOT-CLOSE EXIT
    THEN
    R@ _PROOT-W.RECORD R@ _PROOT-W.RECORD-U @ R@ _PROOT-W.FD @
        VFS-WRITE-EXACT IF
        PERSIST-S-IO R> _PROOT-CLOSE EXIT
    THEN
    R@ _PROOT-W.RECORD-U @ R@ _PROOT-NOTE-WRITE
    PERSIST-FAULT-ROOT-WRITTEN R@ _PROOT-FAULT-WITH-CLOSE
    DUP IF R> DROP EXIT THEN DROP
    R@ _PROOT-W.FD @ VFA-FILE-SIZE?
    DUP IF 2DROP PERSIST-S-IO R> _PROOT-CLOSE EXIT THEN
    DROP R@ _PROOT-W.RECORD-U @ <> IF
        PERSIST-S-CORRUPT R> _PROOT-CLOSE EXIT
    THEN
    PERSIST-FAULT-ROOT-SIZED R@ _PROOT-FAULT-WITH-CLOSE
    DUP IF R> DROP EXIT THEN DROP
    PERSIST-S-OK R> _PROOT-CLOSE ;

: _PROOT-SYNC-SNAPSHOT  ( work -- persist-status )
    >R
    R@ _PROOT-W.ROOT @ _PROOT-F.VFS @ VFS-SYNC IF
        R> DROP PERSIST-S-IO EXIT
    THEN
    R@ _PROOT-NOTE-SYNC
    PERSIST-FAULT-ROOT-SYNCED R@ _PROOT-FAULT
    R> DROP ;

: _PROOT-VERIFY-SNAPSHOT  ( work -- persist-status )
    >R
    VFS-FF-READ R@ _PROOT-OPEN
    DUP IF NIP R> DROP EXIT THEN DROP
    VFA-FILE-SIZE?
    DUP IF 2DROP PERSIST-S-IO R> _PROOT-CLOSE EXIT THEN
    DROP PROOT-RECORD-SIZE <> IF
        PERSIST-S-CORRUPT R> _PROOT-CLOSE EXIT
    THEN
    R@ _PROOT-W.RECORD PROOT-RECORD-SIZE 0 R@ _PROOT-W.FD @
        VFA-READ-RANGE? IF
        PERSIST-S-IO R> _PROOT-CLOSE EXIT
    THEN
    PROOT-RECORD-SIZE R@ _PROOT-NOTE-READ
    0 0 R@ _PROOT-W.RECORD PROOT-RECORD-SIZE
        R@ _PROOT-W.ROOT @ _PROOT-F.SPEC R@ _PROOT-W.CREC
        CREC-VALIDATE
    DUP IF _PROOT-CREC>STATUS R> _PROOT-CLOSE EXIT THEN DROP
    R@ _PROOT-W.CREC CREC-TAG@ R@ _PROOT-W.GENERATION @ <> IF
        PERSIST-S-CORRUPT R> _PROOT-CLOSE EXIT
    THEN
    R@ _PROOT-W.CREC CREC-PAYLOAD$
    R@ _PROOT-W.VALUE @ PERSIST-ROOT-VALUE-SIZE
    _PROOT-BYTES-EQUAL? 0= IF
        PERSIST-S-CORRUPT R> _PROOT-CLOSE EXIT
    THEN
    R@ _PROOT-NOTE-COMPARE
    PERSIST-S-OK R@ _PROOT-CLOSE
    DUP IF R> DROP EXIT THEN DROP
    PERSIST-FAULT-ROOT-VERIFIED R@ _PROOT-FAULT
    R> DROP ;

: _PROOT-PUBLISH-RUN  ( work -- persist-status )
    >R
    R@ _PROOT-VALIDATE-PROPOSED DUP IF R> DROP EXIT THEN DROP
    R@ _PROOT-ENCODE-SNAPSHOT DUP IF R> DROP EXIT THEN DROP
    R@ _PROOT-W.GPAIR GPAIR-SAVE-MAYBE!
    DUP IF _PROOT-GPAIR>STATUS R> DROP EXIT THEN DROP
    R@ _PROOT-WRITE-SNAPSHOT DUP IF R> DROP EXIT THEN DROP
    R@ _PROOT-SYNC-SNAPSHOT DUP IF R> DROP EXIT THEN DROP
    R@ _PROOT-VERIFY-SNAPSHOT DUP IF R> DROP EXIT THEN DROP
    R@ _PROOT-W.GPAIR GPAIR-SAVE-DURABLE!
    DUP IF _PROOT-GPAIR>STATUS R> DROP EXIT THEN DROP
    PERSIST-FAULT-ROOT-PUBLISHED R@ _PROOT-FAULT
    R> DROP ;

: _PROOT-GPAIR-SAVE
  ( root-value target-slot next-generation pair work -- detail )
    >R
    DUP R@ _PROOT-W.GPAIR <> IF
        _PROOT-DROP4 R> DROP PERSIST-S-INVALID EXIT
    THEN
    3 PICK R@ _PROOT-W.VALUE !
    2 PICK R@ _PROOT-W.SLOT !
    1 PICK R@ _PROOT-W.GENERATION !
    _PROOT-DROP4
    R@ ['] _PROOT-PUBLISH-RUN _PROOT-RUN-CATCH
    R> DROP ;

