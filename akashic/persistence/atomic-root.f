: PROOT-WORK-INIT  ( work -- persist-status )
    DUP 0= IF DROP PERSIST-S-INVALID EXIT THEN
    DUP PROOT-WORK-SIZE MSPAN-NONWRAPPING? 0= IF
        DROP PERSIST-S-INVALID EXIT
    THEN
    >R
    R@ PROOT-WORK-SIZE 0 FILL
    _PROOT-WORK-MAGIC R@ _PROOT-W.MAGIC !
    R@ R@ _PROOT-W.SELF !
    PERSIST-S-INVALID R@ _PROOT-W.STATUS !
    -1 R@ _PROOT-W.SLOT !
    GPAIR-R-NONE R@ _PROOT-W.RESULT !
    R@ _PROOT-W.CREC CREC-WORK-INIT
    DUP IF _PROOT-CREC>STATUS R> DROP EXIT THEN DROP
    R@ _PROOT-W.CANDIDATE-A GPAIR-CANDIDATE-INIT
    DUP IF DROP R> DROP PERSIST-S-FAULT EXIT THEN DROP
    R@ _PROOT-W.CANDIDATE-B GPAIR-CANDIDATE-INIT
    DUP IF DROP R> DROP PERSIST-S-FAULT EXIT THEN DROP
    R@ _PROOT-W.PAGE-WORK PPAGE-WORK-INIT
    DUP IF R> DROP EXIT THEN DROP
    R@ _PROOT-W.SEG-BUFFER PERSIST-RECORD-HEADER-SIZE
        R@ _PROOT-W.SEG-WORK PSEG-WORK-INIT
    DUP IF R> DROP EXIT THEN DROP
    ['] _PROOT-GPAIR-EQUAL ['] _PROOT-GPAIR-SAVE R@
        R@ _PROOT-W.GPAIR GPAIR-INIT
    DUP IF _PROOT-GPAIR>STATUS R> DROP EXIT THEN DROP
    R@ PROOT-WORK-VALID? 0= IF R> DROP PERSIST-S-INVALID EXIT THEN
    R> DROP PERSIST-S-OK ;

: _PROOT-ROOT-WORK?  ( root work -- flag )
    DUP PROOT-WORK-VALID? 0= IF 2DROP 0 EXIT THEN
    OVER PROOT-VALID? 0= IF 2DROP 0 EXIT THEN
    2DUP PROOT-FILE-SIZE SWAP PROOT-WORK-SIZE
        MSPAN-OVERLAP? IF 2DROP 0 EXIT THEN
    OVER _PROOT-F.VFS @ VFS-DESC-SIZE
        2 PICK PROOT-WORK-SIZE MSPAN-OVERLAP? IF 2DROP 0 EXIT THEN
    OVER _PROOT-F.PAGE-FILE @ PERSIST-PAGE-FILE-SIZE
        2 PICK PROOT-WORK-SIZE MSPAN-OVERLAP? IF 2DROP 0 EXIT THEN
    DUP PROOT-WORK-SIZE 3 PICK _PROOT-F.PAGE-FILE @
        _PROOT-PAGE-CACHE-DISJOINT? 0= IF 2DROP 0 EXIT THEN
    OVER _PROOT-F.SEGMENT @ PSEG-FILE-SIZE
        2 PICK PROOT-WORK-SIZE MSPAN-OVERLAP? IF 2DROP 0 EXIT THEN
    OVER _PROOT-F.STATS @ ?DUP IF
        PERSIST-STATS-SIZE 2 PICK PROOT-WORK-SIZE
            MSPAN-OVERLAP? IF 2DROP 0 EXIT THEN
    THEN
    2DROP -1 ;

: _PROOT-VALUE-DISJOINT?  ( root-value root work -- flag )
    >R >R
    DUP 0= IF DROP R> DROP R> DROP 0 EXIT THEN
    DUP PERSIST-ROOT-VALUE-SIZE MSPAN-NONWRAPPING? 0= IF
        DROP R> DROP R> DROP 0 EXIT
    THEN
    DUP PERSIST-ROOT-VALUE-SIZE R@ PROOT-FILE-SIZE
        MSPAN-OVERLAP? IF DROP R> DROP R> DROP 0 EXIT THEN
    DUP PERSIST-ROOT-VALUE-SIZE
        R@ _PROOT-F.VFS @ VFS-DESC-SIZE MSPAN-OVERLAP? IF
        DROP R> DROP R> DROP 0 EXIT
    THEN
    DUP PERSIST-ROOT-VALUE-SIZE
        R@ _PROOT-F.PAGE-FILE @ PERSIST-PAGE-FILE-SIZE
        MSPAN-OVERLAP? IF DROP R> DROP R> DROP 0 EXIT THEN
    DUP PERSIST-ROOT-VALUE-SIZE R@ _PROOT-F.PAGE-FILE @
        _PROOT-PAGE-CACHE-DISJOINT? 0= IF
        DROP R> DROP R> DROP 0 EXIT
    THEN
    DUP PERSIST-ROOT-VALUE-SIZE
        R@ _PROOT-F.SEGMENT @ PSEG-FILE-SIZE
        MSPAN-OVERLAP? IF DROP R> DROP R> DROP 0 EXIT THEN
    R@ _PROOT-F.STATS @ ?DUP IF
        DUP PERSIST-STATS-SIZE 3 PICK PERSIST-ROOT-VALUE-SIZE
            MSPAN-OVERLAP? IF 2DROP R> DROP R> DROP 0 EXIT THEN
        DROP
    THEN
    R> DROP
    DUP PERSIST-ROOT-VALUE-SIZE R@ PROOT-WORK-SIZE
        MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    DROP R> DROP -1 ;

: PROOT-LOAD  ( destination-root-value root work -- generation status )
    >R
    DUP R@ _PROOT-ROOT-WORK? 0= IF
        2DROP R> DROP 0 PERSIST-S-INVALID EXIT
    THEN
    R@ _PROOT-W.BUSY @ IF
        2DROP R> DROP 0 PERSIST-S-BUSY EXIT
    THEN
    2DUP R@ _PROOT-VALUE-DISJOINT? 0= IF
        2DROP R> DROP 0 PERSIST-S-INVALID EXIT
    THEN
    OVER PERSIST-ROOT-VALUE-SIZE 0 FILL
    R@ _PROOT-W.ROOT !
    R@ _PROOT-W.VALUE !
    1 R@ _PROOT-W.BUSY !
    0 R@ _PROOT-W.LOADED !
    -1 R@ _PROOT-W.SLOT !
    0 R@ _PROOT-W.GENERATION !
    PERSIST-S-INVALID R@ _PROOT-W.STATUS !
    R@ ['] _PROOT-LOAD-RUN _PROOT-RUN-CATCH
    R@ _PROOT-WORK-END
    DUP PERSIST-S-OK = IF
        R@ _PROOT-W.GPAIR GPAIR-GENERATION@ SWAP
    ELSE
        0 SWAP
    THEN
    R> DROP ;

: _PROOT-PUBLISH-READY?  ( root work -- flag )
    2DUP _PROOT-ROOT-WORK? 0= IF 2DROP 0 EXIT THEN
    DUP _PROOT-W.LOADED @ 0= IF 2DROP 0 EXIT THEN
    DUP _PROOT-W.ROOT @ 2 PICK <> IF 2DROP 0 EXIT THEN
    _PROOT-W.GPAIR GPAIR-RESULT@
    DUP GPAIR-R-ABSENT =
    OVER GPAIR-R-FALLBACK = OR
    OVER GPAIR-R-NEWEST = OR
    SWAP GPAIR-R-EQUAL = OR
    NIP ;

: _PROOT-ADOPT-PUBLISHED-SLOT  ( work -- )
    >R
    R@ _PROOT-W.GPAIR GPAIR-OUTCOME@ GPAIR-W-DURABLE <> IF
        R> DROP EXIT
    THEN
    R@ _PROOT-W.GPAIR GPAIR-TARGET-SLOT@
    R@ _PROOT-W.GPAIR GPAIR-NEXT-GENERATION@
    SWAP GPAIR-SLOT-A = IF
        R@ _PROOT-W.ROOT @ _PROOT-F.SLOT-A-GEN !
    ELSE
        R@ _PROOT-W.ROOT @ _PROOT-F.SLOT-B-GEN !
    THEN
    R> DROP ;

: PROOT-PUBLISH  ( root-value root work -- persist-status )
    >R
    DUP R@ _PROOT-PUBLISH-READY? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PROOT-W.BUSY @ IF 2DROP R> DROP PERSIST-S-BUSY EXIT THEN
    2DUP R@ _PROOT-VALUE-DISJOINT? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    OVER PERSIST-ROOT-VALUE-VALID? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PROOT-W.ROOT !
    1 R@ _PROOT-W.BUSY !
    PERSIST-S-INVALID R@ _PROOT-W.STATUS !
    R@ _PROOT-W.GPAIR GPAIR-SAVE
    DUP IF
        _PROOT-GPAIR>STATUS NIP
    ELSE
        DROP
        R@ _PROOT-W.GPAIR GPAIR-DETAIL@
        DUP _PROOT-STATUS? 0= IF DROP PERSIST-S-FAULT THEN
        NIP
    THEN
    R@ _PROOT-ADOPT-PUBLISHED-SLOT
    R> _PROOT-WORK-END ;
