    >R
    R@ _PROOT-W.CANDIDATE-A DUP GPAIR-CANDIDATE-CLASS@
        GPAIR-C-VALID = IF GPAIR-CANDIDATE-GENERATION@ ELSE DROP 0 THEN
    R@ _PROOT-W.ROOT @ _PROOT-F.SLOT-A-GEN !
    R@ _PROOT-W.CANDIDATE-B DUP GPAIR-CANDIDATE-CLASS@
        GPAIR-C-VALID = IF GPAIR-CANDIDATE-GENERATION@ ELSE DROP 0 THEN
    R@ _PROOT-W.ROOT @ _PROOT-F.SLOT-B-GEN !
    R> DROP ;

: _PROOT-LOAD-RUN  ( work -- persist-status )
    >R
    R@ _PROOT-W.GPAIR GPAIR-RESET
    DUP IF _PROOT-GPAIR>STATUS R> DROP EXIT THEN DROP
    GPAIR-SLOT-A R@ _PROOT-READ-CANDIDATE
    DUP IF R> DROP EXIT THEN DROP
    GPAIR-SLOT-B R@ _PROOT-READ-CANDIDATE
    DUP IF R> DROP EXIT THEN DROP
    R@ _PROOT-W.CANDIDATE-A
    R@ _PROOT-W.CANDIDATE-B
    R@ _PROOT-W.GPAIR GPAIR-SELECT
    DUP IF
        _PROOT-GPAIR>STATUS NIP R> DROP EXIT
    THEN DROP
    DUP R@ _PROOT-W.RESULT !
    DUP GPAIR-R-ABSENT = IF
        DROP R@ _PROOT-ADOPT-SLOT-GENERATIONS
        1 R@ _PROOT-W.LOADED !
        R> DROP PERSIST-S-ABSENT EXIT
    THEN
    DUP GPAIR-R-CORRUPT = IF
        DROP R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    GPAIR-R-AMBIGUOUS = IF
        R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    R@ _PROOT-COPY-SELECTED DUP IF R> DROP EXIT THEN DROP
    R@ _PROOT-ADOPT-SLOT-GENERATIONS
    1 R@ _PROOT-W.LOADED !
    R> DROP PERSIST-S-OK ;

