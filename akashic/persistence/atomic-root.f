        _PSTAT-NOTE-ROOT-READ
    ELSE DROP THEN ;

: _PROOT-NOTE-WRITE  ( bytes work -- )
    _PROOT-W.ROOT @ _PROOT-F.STATS @ ?DUP IF
        _PSTAT-NOTE-ROOT-WRITE
    ELSE DROP THEN ;

: _PROOT-NOTE-COMPARE  ( work -- )
    _PROOT-W.ROOT @ _PROOT-F.STATS @ ?DUP IF _PSTAT-NOTE-COMPARE THEN ;

: _PROOT-NOTE-SYNC  ( work -- )
    _PROOT-W.ROOT @ _PROOT-F.STATS @ ?DUP IF _PSTAT-NOTE-SYNC THEN ;

: _PROOT-NOTE-FAULT  ( work -- )
    _PROOT-W.ROOT @ _PROOT-F.STATS @ ?DUP IF _PSTAT-NOTE-FAULT THEN ;

: _PROOT-FAULT-CALL  ( work -- persist-status )
    >R
    R@ _PROOT-W.ROOT @ _PROOT-F.FAULT-XT @ ?DUP 0= IF
        R> DROP PERSIST-S-OK EXIT
    THEN
    DROP
    R@ _PROOT-W.DETAIL @
    R@ _PROOT-W.GENERATION @
    R@ _PROOT-W.ROOT @ _PROOT-F.FAULT-CTX @
    R@ _PROOT-W.ROOT @ _PROOT-F.FAULT-XT @ EXECUTE
    R> DROP ;

: _PROOT-FAULT  ( point work -- persist-status )
    >R R@ _PROOT-W.DETAIL !
    R@ ['] _PROOT-FAULT-CALL CATCH
    DUP IF
        2DROP PERSIST-S-FAULT
    ELSE
        DROP DUP _PROOT-STATUS? 0= IF DROP PERSIST-S-FAULT THEN
    THEN
    DUP IF R@ _PROOT-NOTE-FAULT THEN
    R> DROP ;

