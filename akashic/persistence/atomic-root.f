: _PROOT-CANDIDATE  ( work -- candidate )
    DUP _PROOT-W.SLOT @ GPAIR-SLOT-A = IF
        _PROOT-W.CANDIDATE-A
    ELSE
        _PROOT-W.CANDIDATE-B
    THEN ;

: _PROOT-CANDIDATE-VALUE  ( work -- root-value )
    DUP _PROOT-W.SLOT @ GPAIR-SLOT-A = IF
        _PROOT-W.VALUE-A
    ELSE
        _PROOT-W.VALUE-B
    THEN ;

: _PROOT-CANDIDATE-ABSENT!  ( work -- )
    _PROOT-CANDIDATE >R
    PERSIST-S-ABSENT R> GPAIR-CANDIDATE-ABSENT! DROP ;

: _PROOT-CANDIDATE-CORRUPT!  ( work -- )
    _PROOT-CANDIDATE >R
    PERSIST-S-CORRUPT R> GPAIR-CANDIDATE-CORRUPT! DROP ;

: _PROOT-CANDIDATE-MALFORMED  ( work -- persist-status )
    DUP _PROOT-CANDIDATE-CORRUPT!
    DROP PERSIST-S-OK ;

: _PROOT-READ-CANDIDATE-RUN  ( work -- persist-status )
    >R
    R@ _PROOT-CANDIDATE GPAIR-CANDIDATE-INIT DROP
    VFS-FF-READ R@ _PROOT-OPEN
    DUP IF
        NIP DUP PERSIST-S-ABSENT = IF
            DROP R@ _PROOT-CANDIDATE-ABSENT!
            R> DROP PERSIST-S-OK EXIT
        THEN
        R> DROP EXIT
    THEN DROP
    VFA-FILE-SIZE?
    DUP IF
        2DROP PERSIST-S-IO R> _PROOT-CLOSE EXIT
    THEN DROP
    PROOT-RECORD-SIZE <> IF
        PERSIST-S-OK R@ _PROOT-CLOSE
        DUP IF R> DROP EXIT THEN DROP
        R> _PROOT-CANDIDATE-MALFORMED EXIT
    THEN
    R@ _PROOT-W.RECORD PROOT-RECORD-SIZE 0 R@ _PROOT-W.FD @
        VFA-READ-RANGE? IF
        PERSIST-S-IO R> _PROOT-CLOSE EXIT
    THEN
    PROOT-RECORD-SIZE R@ _PROOT-NOTE-READ
    0 0 R@ _PROOT-W.RECORD PROOT-RECORD-SIZE
        R@ _PROOT-W.ROOT @ _PROOT-F.SPEC R@ _PROOT-W.CREC
        CREC-VALIDATE
    DUP IF
        _PROOT-CREC>STATUS
        DUP PERSIST-S-CORRUPT = IF
            DROP PERSIST-S-OK R@ _PROOT-CLOSE
            DUP IF R> DROP EXIT THEN DROP
            R> _PROOT-CANDIDATE-MALFORMED EXIT
        THEN
        R> _PROOT-CLOSE EXIT
    THEN DROP
    R@ _PROOT-W.CREC CREC-PAYLOAD$
    DUP PERSIST-ROOT-VALUE-SIZE <> IF
        2DROP PERSIST-S-OK R@ _PROOT-CLOSE
        DUP IF R> DROP EXIT THEN DROP
        R> _PROOT-CANDIDATE-MALFORMED EXIT
    THEN
    DROP R@ _PROOT-CANDIDATE-VALUE PERSIST-ROOT-VALUE-SIZE MOVE
    PERSIST-S-OK R@ _PROOT-CLOSE
    DUP IF R> DROP EXIT THEN DROP
    R@ _PROOT-CANDIDATE-VALUE
    R@ _PROOT-W.CREC CREC-TAG@
    PERSIST-S-OK
    R@ _PROOT-CANDIDATE GPAIR-CANDIDATE-VALUE!
    DUP IF DROP R> DROP PERSIST-S-FAULT EXIT THEN DROP
    R> DROP PERSIST-S-OK ;

: _PROOT-DATA-STATUS  ( persist-status -- persist-status )
