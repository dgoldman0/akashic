: _PBT-NODE-INIT  ( kind height node work -- )
    >R
    DUP PERSIST-PAGE-PAYLOAD-SIZE 0 FILL
    _PBTN-MAGIC OVER _PBTN.MAGIC !
    2 PICK OVER _PBTN.KIND !
    1 PICK OVER _PBTN.HEIGHT !
    R@ _PBTW.TREE @ _PBTD.SCOPE @ OVER _PBTN.SCOPE !
    DROP 2DROP R> DROP ;

: _PBT-LEAF-SET  ( key-a key-u value-a value-u index node -- )
    _PBTL-ENTRY >R
    R@ _PBTL-ENTRY-SIZE 0 FILL
    2 PICK R@ _PBTL-KEY-U-OFF + !
    DUP R@ _PBTL-VALUE-U-OFF + !
    3 PICK R@ _PBTL-KEY-OFF + 4 PICK MOVE
    1 PICK R@ _PBTL-VALUE-OFF + 2 PICK MOVE
    2DROP 2DROP R> DROP ;

: _PBT-LEAF-COPY  ( source-index source-node dest-index dest-node -- )
    _PBTL-ENTRY >R _PBTL-ENTRY R> _PBTL-ENTRY-SIZE MOVE ;

: _PBT-BRANCH-SET  ( page-id key-a key-u index node -- )
    _PBTB-ENTRY >R
    R@ _PBTB-ENTRY-SIZE 0 FILL
    DUP R@ _PBTB-KEY-U-OFF + !
    2 PICK R@ _PBTB-CHILD-OFF + !
    OVER R@ _PBTB-KEY-OFF + 2 PICK MOVE
    2DROP DROP R> DROP ;

: _PBT-BRANCH-COPY  ( source-index source-node dest-index dest-node -- )
    _PBTB-ENTRY >R _PBTB-ENTRY R> _PBTB-ENTRY-SIZE MOVE ;

: _PBT-LEAF-LOWER  ( node work -- index found? )
    OVER _PBTN.COUNT @ 0 ?DO
        DUP _PBTW.KEY OVER _PBTW.KEY-U @
        I 4 PICK _PBTL-KEY$ 4 PICK _PBTW.TREE @ _PBT-KEY-COMPARE
        DUP 0 > 0= IF
            0= ROT DROP SWAP DROP I SWAP UNLOOP EXIT
        THEN
        DROP
    LOOP
    OVER _PBTN.COUNT @ >R 2DROP R> 0 ;

: _PBT-BRANCH-CHOOSE  ( node work -- index )
    OVER _PBTN.COUNT @ 0 ?DO
        DUP _PBTW.KEY OVER _PBTW.KEY-U @
        I 4 PICK _PBTB-KEY$ 4 PICK _PBTW.TREE @ _PBT-KEY-COMPARE
        0 > 0= IF 2DROP I UNLOOP EXIT THEN
    LOOP
    OVER _PBTN.COUNT @ 1- >R 2DROP R> ;

: _PBT-COPY-TARGETS  ( key-a key-u value-a value-u work -- status )
    >R
    2 PICK _PBT-KEY-LENGTH? 0= IF
        2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DUP DUP 0< SWAP PBTREE-VALUE-MAX > OR IF
        2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    3 PICK 0= IF
        2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    3 PICK 3 PICK MSPAN-NONWRAPPING? 0= IF
        2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DUP IF
        1 PICK 0= IF 2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
        1 PICK 1 PICK MSPAN-NONWRAPPING? 0= IF
            2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT
        THEN
    THEN
    3 PICK 3 PICK R@ PBTREE-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DUP IF
        1 PICK 1 PICK R@ PBTREE-WORK-SIZE MSPAN-OVERLAP? IF
            2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT
        THEN
    THEN
    2 PICK R@ _PBTW.KEY-U !
    DUP R@ _PBTW.VALUE-U !
    3 PICK R@ _PBTW.KEY 4 PICK MOVE
    DUP IF 1 PICK R@ _PBTW.VALUE 2 PICK MOVE THEN
    2DROP 2DROP R> DROP PERSIST-S-OK ;

: _PBT-PUT-LOGICAL-LEAF  ( logical-index dest-index dest-node work -- )
    >R
    2 PICK R@ _PBTW.INDEX @ = IF
        ROT DROP
        R@ _PBTW.KEY R@ _PBTW.KEY-U @
        R@ _PBTW.VALUE R@ _PBTW.VALUE-U @ 2ROT _PBT-LEAF-SET
        R> DROP EXIT
    THEN
    2 PICK R@ _PBTW.INDEX @ > R@ _PBTW.TEMP0 @ AND IF
        2 PICK 1-
    ELSE
        2 PICK
    THEN
    R@ _PBTW.TEMP1 !
    ROT DROP
    R@ _PBTW.TEMP1 @ R@ _PBTW.BUF0 2SWAP _PBT-LEAF-COPY
    R> DROP ;

: _PBT-BUILD-LEAF-RANGE  ( logical-start count destination work -- )
    _PBTN-LEAF 1 3 PICK 3 PICK _PBT-NODE-INIT
    2 PICK 2 PICK _PBTN.COUNT !
    2 PICK 0 ?DO
        3 PICK I + I 3 PICK 3 PICK _PBT-PUT-LOGICAL-LEAF
    LOOP
    2DROP 2DROP ;

: _PBT-CAPTURE-MAX  ( node key-destination key-u-field -- status )
    >R
    OVER _PBTN.COUNT @ DUP 0= IF
        DROP 2DROP R> DROP PERSIST-S-NOT-FOUND EXIT
    THEN
    1- 2 PICK _PBT-NODE-KEY$
    DUP R@ !
    >R ROT DROP SWAP R> MOVE
    R> DROP PERSIST-S-OK ;

: _PBT-CAPTURE-AND-WRITE-CHILDREN  ( work -- status )
    >R
    R@ _PBTW.BUF1 R@ _PBTW.KEY R@ _PBTW.P1-KEY-U _PBT-CAPTURE-MAX
    DUP IF R> DROP EXIT THEN DROP
    R@ _PBTW.SPLIT @ IF
        R@ _PBTW.BUF2 R@ _PBTW.KEY2 R@ _PBTW.P2-KEY-U _PBT-CAPTURE-MAX
        DUP IF R> DROP EXIT THEN DROP
    THEN
    R@ _PBTW.BUF1 R@ _PBT-WRITE
    DUP IF NIP R> DROP EXIT THEN DROP R@ _PBTW.P1-PAGE !
    R@ _PBTW.SPLIT @ IF
        R@ _PBTW.BUF2 R@ _PBT-WRITE
        DUP IF NIP R> DROP EXIT THEN DROP R@ _PBTW.P2-PAGE !
    THEN
    R> DROP PERSIST-S-OK ;

\ ---------------------------------------------------------------------
\ Path descent and copy-on-write construction
\ ---------------------------------------------------------------------

: _PBT-ROOT-CURRENT?  ( root tree -- flag )
    _PBTD.STORE @ PSTORE-GENERATION@ SWAP _PBTR.GENERATION @ = ;

: PBTREE-ROOT-ADVANCE  ( root out-root tree -- status )
    >R
    R@ PBTREE-VALID? 0= IF 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    OVER R@ PBTREE-ROOT-VALID? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    OVER R@ _PBT-ROOT-CURRENT? 0= IF
        2DROP R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    DUP 0= IF 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    DUP PBTREE-ROOT-SIZE MSPAN-NONWRAPPING? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DUP PBTREE-ROOT-SIZE 3 PICK PBTREE-ROOT-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DUP PBTREE-ROOT-SIZE R@ PBTREE-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DUP PBTREE-ROOT-SIZE R@ _PBTD.STORE @ PSTORE-SPAN-DISJOINT? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PBTD.STORE @ PSTORE-GENERATION@ PERSIST-MAX-SIGNED >= IF
        2DROP R> DROP PERSIST-S-CAPACITY EXIT
    THEN
    2DUP PBTREE-ROOT-SIZE MOVE
    R@ _PBTD.STORE @ PSTORE-GENERATION@ 1+ OVER _PBTR.GENERATION !
    2DROP R> DROP PERSIST-S-OK ;

: _PBT-ROOT-MUTABLE?  ( root tree -- flag )
    >R _PBTR.GENERATION @
    R@ _PBTD.STORE @ PSTORE-GENERATION@ 2DUP = IF
        2DROP R> DROP -1 EXIT
    THEN
    1+ = R> DROP ;

: _PBT-DESCEND-TX  ( work -- found? status )
    >R
    R@ _PBTW.ROOT @ _PBTR.HEIGHT @ R@ _PBTW.DEPTH !
    R@ _PBTW.ROOT @ _PBTR.PAGE @ R@ _PBTW.CURRENT-PAGE !
    0 R@ _PBTW.LEVEL !
    BEGIN R@ _PBTW.LEVEL @ R@ _PBTW.DEPTH @ 1- < WHILE
        R@ _PBTW.CURRENT-PAGE @ R@ _PBT-READ-TX
        DUP IF 0 SWAP R> DROP EXIT THEN DROP
        R@ _PBTW.BUF0
        R@ _PBTW.DEPTH @ R@ _PBTW.LEVEL @ -
        R@ _PBT-NODE-VALID? 0= IF 0 PERSIST-S-CORRUPT R> DROP EXIT THEN
        R@ _PBTW.BUF0 R@ _PBT-BRANCH-CHOOSE DUP R@ _PBTW.INDEX !
        R@ _PBTW.LEVEL @ R@ _PBTW-PATH-INDEX !
        R@ _PBTW.CURRENT-PAGE @
            R@ _PBTW.LEVEL @ R@ _PBTW-PATH-PAGE !
        R@ _PBTW.INDEX @ R@ _PBTW.BUF0 _PBTB-CHILD@
            R@ _PBTW.CURRENT-PAGE !
        1 R@ _PBTW.LEVEL +!
    REPEAT
    R@ _PBTW.CURRENT-PAGE @ R@ _PBT-READ-TX
    DUP IF 0 SWAP R> DROP EXIT THEN DROP
    R@ _PBTW.BUF0 1 R@ _PBT-NODE-VALID? 0= IF
        0 PERSIST-S-CORRUPT R> DROP EXIT
    THEN
    R@ _PBTW.BUF0 R@ _PBT-LEAF-LOWER
    OVER R@ _PBTW.INDEX !
    R@ _PBTW.INDEX @ R@ _PBTW.LEVEL @ R@ _PBTW-PATH-INDEX !
    R@ _PBTW.CURRENT-PAGE @ R@ _PBTW.LEVEL @ R@ _PBTW-PATH-PAGE !
    NIP PERSIST-S-OK R> DROP ;

: _PBT-NODE-MINIMUM  ( node -- count )
    _PBTN.KIND @ _PBTN-LEAF = IF
        PBTREE-LEAF-CAPACITY 1+ 2 /
    ELSE
        PBTREE-BRANCH-CAPACITY 2 /
    THEN ;

: _PBT-NODE-CAPACITY  ( node -- count )
    _PBTN.KIND @ _PBTN-LEAF = IF
        PBTREE-LEAF-CAPACITY
    ELSE
        PBTREE-BRANCH-CAPACITY
    THEN ;

: _PBT-NODE-UNDERFULL?  ( node -- flag )
    DUP _PBTN.COUNT @ SWAP _PBT-NODE-MINIMUM < ;

: _PBT-PUT-LEAF  ( found? work -- status )
    >R
    0 R@ _PBTW.DEFERRED !
    0 R@ _PBTW.REMOVE !
    0 R@ _PBTW.COLLAPSE !
    DUP IF 0 ELSE 1 THEN R@ _PBTW.DELTA !
    DUP 0= IF -1 ELSE 0 THEN R@ _PBTW.TEMP0 !       \ insertion flag
    R@ _PBTW.BUF0 _PBTN.COUNT @ R@ _PBTW.DELTA @ + DUP
    PBTREE-LEAF-CAPACITY <= IF
        0 R@ _PBTW.SPLIT !
        0 OVER R@ _PBTW.BUF1 R@ _PBT-BUILD-LEAF-RANGE
    ELSE
        -1 R@ _PBTW.SPLIT !
        0 6 R@ _PBTW.BUF1 R@ _PBT-BUILD-LEAF-RANGE
        6 OVER 6 - R@ _PBTW.BUF2 R@ _PBT-BUILD-LEAF-RANGE
    THEN
    DROP DROP
    R@ _PBT-CAPTURE-AND-WRITE-CHILDREN R> DROP ;

: _PBT-BUILD-DELETE-LEAF  ( work -- )
    DUP _PBTN-LEAF 1 3 PICK _PBTW.BUF1 3 PICK _PBT-NODE-INIT
    DUP _PBTW.BUF0 _PBTN.COUNT @ 1-
    DUP 2 PICK _PBTW.BUF1 _PBTN.COUNT !
    0 ?DO
        I 2 PICK _PBTW.INDEX @ >= IF I 1+ ELSE I THEN
        OVER _PBTW.BUF0 I 3 PICK _PBTW.BUF1 _PBT-LEAF-COPY
    LOOP
    2DROP ;

: _PBT-DELETE-LEAF  ( work -- status )
    >R
    0 R@ _PBTW.DEFERRED !
    -1 R@ _PBTW.DELTA !
    0 R@ _PBTW.SPLIT !
    0 R@ _PBTW.COLLAPSE !
    R@ _PBT-BUILD-DELETE-LEAF
    R@ _PBTW.BUF1 _PBTN.COUNT @ 0= IF
        -1 R@ _PBTW.REMOVE !
        R> DROP PERSIST-S-OK EXIT
    THEN
    0 R@ _PBTW.REMOVE !
    R@ _PBTW.DEPTH @ 1 >
    R@ _PBTW.BUF1 _PBT-NODE-UNDERFULL? AND IF
        -1 R@ _PBTW.DEFERRED !
        R@ _PBTW.BUF1 R@ _PBTW.KEY R@ _PBTW.P1-KEY-U _PBT-CAPTURE-MAX
        R> DROP EXIT
    THEN
    R@ _PBT-CAPTURE-AND-WRITE-CHILDREN R> DROP ;

0 CONSTANT _PBT-CHILD-REPLACE
1 CONSTANT _PBT-CHILD-SPLIT
2 CONSTANT _PBT-CHILD-REMOVE
3 CONSTANT _PBT-CHILD-MERGE
4 CONSTANT _PBT-CHILD-REDISTRIBUTE

: _PBT-BUILD-P1  ( work -- )
    >R
    R@ _PBTW.P1-PAGE @ R@ _PBTW.KEY R@ _PBTW.P1-KEY-U @
        R@ _PBTW.TEMP1 @ R@ _PBTW.CURRENT-PAGE @ _PBT-BRANCH-SET
    R> DROP ;

: _PBT-BUILD-P2  ( work -- )
    >R
    R@ _PBTW.P2-PAGE @ R@ _PBTW.KEY2 R@ _PBTW.P2-KEY-U @
        R@ _PBTW.TEMP1 @ R@ _PBTW.CURRENT-PAGE @ _PBT-BRANCH-SET
    R> DROP ;

: _PBT-BUILD-OLD  ( source-index work -- )
    >R R@ _PBTW.BUF0 R@ _PBTW.TEMP1 @ R@ _PBTW.CURRENT-PAGE @
        _PBT-BRANCH-COPY R> DROP ;

: _PBT-PUT-LOGICAL-BRANCH  ( logical-index dest-index dest-node work -- )
    >R
    R@ _PBTW.CURRENT-PAGE !
    DUP R@ _PBTW.TEMP1 ! DROP
    R@ _PBTW.TEMP0 !
    R@ _PBTW.CHILD-MODE @ _PBT-CHILD-REPLACE = IF
        R@ _PBTW.TEMP0 @ R@ _PBTW.INDEX @ = IF
            R@ _PBT-BUILD-P1
        ELSE
            R@ _PBTW.TEMP0 @ R@ _PBT-BUILD-OLD
        THEN
        R> DROP EXIT
    THEN
    R@ _PBTW.CHILD-MODE @ _PBT-CHILD-SPLIT = IF
        R@ _PBTW.TEMP0 @ R@ _PBTW.INDEX @ = IF
            R@ _PBT-BUILD-P1 R> DROP EXIT
        THEN
        R@ _PBTW.TEMP0 @ R@ _PBTW.INDEX @ 1+ = IF
            R@ _PBT-BUILD-P2 R> DROP EXIT
        THEN
        R@ _PBTW.TEMP0 @ DUP R@ _PBTW.INDEX @ > IF 1- THEN
            R@ _PBT-BUILD-OLD
        R> DROP EXIT
    THEN
    R@ _PBTW.CHILD-MODE @ _PBT-CHILD-REMOVE = IF
        R@ _PBTW.TEMP0 @ DUP R@ _PBTW.INDEX @ >= IF 1+ THEN
            R@ _PBT-BUILD-OLD
        R> DROP EXIT
    THEN
    R@ _PBTW.CHILD-MODE @ _PBT-CHILD-MERGE = IF
        R@ _PBTW.TEMP0 @ R@ _PBTW.INDEX @ = IF
            R@ _PBT-BUILD-P1
        ELSE
            R@ _PBTW.TEMP0 @ DUP R@ _PBTW.INDEX @ > IF 1+ THEN
                R@ _PBT-BUILD-OLD
        THEN
        R> DROP EXIT
    THEN
    R@ _PBTW.CHILD-MODE @ _PBT-CHILD-REDISTRIBUTE = IF
        R@ _PBTW.TEMP0 @ R@ _PBTW.INDEX @ = IF
            R@ _PBT-BUILD-P1 R> DROP EXIT
        THEN
        R@ _PBTW.TEMP0 @ R@ _PBTW.INDEX @ 1+ = IF
            R@ _PBT-BUILD-P2 R> DROP EXIT
        THEN
    THEN
    R@ _PBTW.TEMP0 @ R@ _PBT-BUILD-OLD
    R> DROP ;

: _PBT-COPY-COMBINED  ( logical-index dest-index dest-node work -- )
    >R
    2 PICK R@ _PBTW.CURRENT-PAGE @ < IF
        2 PICK R@ _PBTW.TEMP0 @
    ELSE
        2 PICK R@ _PBTW.CURRENT-PAGE @ - R@ _PBTW.TEMP1 @
    THEN
    R@ _PBTW.TEMP0 @ _PBTN.KIND @ _PBTN-LEAF = IF
        2SWAP _PBT-LEAF-COPY
    ELSE
        2SWAP _PBT-BRANCH-COPY
    THEN
    DROP R> DROP ;

: _PBT-NODE-INIT-LIKE  ( source destination work -- )
    >R
    OVER _PBTN.KIND @ 2 PICK _PBTN.HEIGHT @ 2 PICK R@ _PBT-NODE-INIT
    2DROP R> DROP ;

: _PBT-BUILD-COMBINED-RANGE
  ( logical-start count destination work -- )
    DUP _PBTW.TEMP0 @ 2 PICK 2 PICK _PBT-NODE-INIT-LIKE
    2 PICK 2 PICK _PBTN.COUNT !
    2 PICK 0 ?DO
        3 PICK I + I 3 PICK 3 PICK _PBT-COPY-COMBINED
    LOOP
    2DROP 2DROP ;

: _PBT-COMBINED-ORDERED?  ( work -- flag )
    >R
    R@ _PBTW.TEMP0 @ _PBTN.COUNT @ 1- R@ _PBTW.TEMP0 @ _PBT-NODE-KEY$
    0 R@ _PBTW.TEMP1 @ _PBT-NODE-KEY$
    R@ _PBTW.TREE @ _PBT-KEY-COMPARE 0<
    R> DROP ;

: _PBT-REBALANCE-CHILD  ( work -- status )
    >R
    R@ _PBTW.INDEX @ 1+ R@ _PBTW.BUF0 _PBTN.COUNT @ < IF
        -1 R@ _PBTW.CHILD-SPLIT !             \ modified child is left
        R@ _PBTW.INDEX @ 1+ R@ _PBTW.BUF0 _PBTB-CHILD@
    ELSE
        0 R@ _PBTW.CHILD-SPLIT !              \ modified child is right
        R@ _PBTW.INDEX @ 1- DUP 0< IF
            DROP R> DROP PERSIST-S-CORRUPT EXIT
        THEN
        DUP R@ _PBTW.INDEX !
        R@ _PBTW.BUF0 _PBTB-CHILD@
    THEN
    DUP R@ _PBTW.P2-PAGE !
    DUP R@ _PBT-RECORD-RETIREMENT
    DUP IF NIP R> DROP EXIT THEN DROP
    R@ _PBT-READ-TX DUP IF R> DROP EXIT THEN DROP
    R@ _PBTW.BUF0 R@ _PBTW.BUF1 _PBTN.HEIGHT @ R@ _PBT-NODE-VALID? 0= IF
        R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    R@ _PBTW.BUF0 _PBT-NODE-UNDERFULL? IF
        R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    R@ _PBTW.CHILD-SPLIT @ IF
        R@ _PBTW.BUF1 R@ _PBTW.TEMP0 !
        R@ _PBTW.BUF0 R@ _PBTW.TEMP1 !
    ELSE
        R@ _PBTW.BUF0 R@ _PBTW.TEMP0 !
        R@ _PBTW.BUF1 R@ _PBTW.TEMP1 !
    THEN
    R@ _PBTW.TEMP0 @ _PBTN.COUNT @ R@ _PBTW.CURRENT-PAGE !
    R@ _PBT-COMBINED-ORDERED? 0= IF
        R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    R@ _PBTW.TEMP0 @ _PBTN.COUNT @
        R@ _PBTW.TEMP1 @ _PBTN.COUNT @ +
    DUP R@ _PBTW.TEMP0 @ _PBT-NODE-CAPACITY <= IF
        0 SWAP R@ _PBTW.BUF2 R@ _PBT-BUILD-COMBINED-RANGE
        R@ _PBTW.BUF2 R@ _PBTW.BUF1 PERSIST-PAGE-PAYLOAD-SIZE MOVE
        0 R@ _PBTW.SPLIT !
        R@ _PBT-CAPTURE-AND-WRITE-CHILDREN
        DUP IF R> DROP EXIT THEN DROP
        _PBT-CHILD-MERGE R@ _PBTW.CHILD-MODE !
    ELSE
        DUP 2 / DUP R@ _PBTW.COLLAPSE !
        0 OVER R@ _PBTW.BUF2 R@ _PBT-BUILD-COMBINED-RANGE
        DUP 2 PICK SWAP - R@ _PBTW.BUF3 R@
            _PBT-BUILD-COMBINED-RANGE
        DROP
        R@ _PBTW.BUF2 R@ _PBTW.BUF1 PERSIST-PAGE-PAYLOAD-SIZE MOVE
        R@ _PBTW.BUF3 R@ _PBTW.BUF2 PERSIST-PAGE-PAYLOAD-SIZE MOVE
        -1 R@ _PBTW.SPLIT !
        R@ _PBT-CAPTURE-AND-WRITE-CHILDREN
        DUP IF R> DROP EXIT THEN DROP
        _PBT-CHILD-REDISTRIBUTE R@ _PBTW.CHILD-MODE !
    THEN
    0 R@ _PBTW.DEFERRED !
    R@ _PBTW.LEVEL @ R@ _PBTW-PATH-PAGE @ R@ _PBT-READ-TX
    DUP IF R> DROP EXIT THEN DROP
    R@ _PBTW.BUF0
        R@ _PBTW.DEPTH @ R@ _PBTW.LEVEL @ - R@ _PBT-NODE-VALID? 0= IF
        R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    R> DROP PERSIST-S-OK ;

: _PBT-BRANCH-NODE-INIT  ( height node work -- )
    >R >R _PBTN-BRANCH SWAP R> R> _PBT-NODE-INIT ;

: _PBT-BUILD-BRANCH-RANGE
  ( logical-start count height destination work -- )
    2 PICK 2 PICK 2 PICK _PBT-BRANCH-NODE-INIT
    3 PICK 2 PICK _PBTN.COUNT !
    3 PICK 0 ?DO
        4 PICK I + I 3 PICK 3 PICK _PBT-PUT-LOGICAL-BRANCH
    LOOP
    2DROP 2DROP DROP ;

: _PBT-CHILD-COUNT-DELTA  ( mode -- n )
    DUP _PBT-CHILD-SPLIT = IF DROP 1 EXIT THEN
    DUP _PBT-CHILD-REMOVE = SWAP _PBT-CHILD-MERGE = OR IF -1 ELSE 0 THEN ;

: _PBT-REWRITE-PARENT  ( parent-height work -- status )
    >R
    R@ _PBTW.DEFERRED @ IF
        R@ _PBT-REBALANCE-CHILD
        DUP IF NIP R> DROP EXIT THEN DROP
    ELSE
        R@ _PBTW.SPLIT @ IF
            _PBT-CHILD-SPLIT
        ELSE
            R@ _PBTW.REMOVE @ IF
                _PBT-CHILD-REMOVE
            ELSE
                _PBT-CHILD-REPLACE
            THEN
        THEN
        R@ _PBTW.CHILD-MODE !
    THEN
    R@ _PBTW.CHILD-MODE @ _PBT-CHILD-SPLIT = R@ _PBTW.CHILD-SPLIT !
    R@ _PBTW.CHILD-MODE @ _PBT-CHILD-REMOVE = R@ _PBTW.CHILD-REMOVE !
    R@ _PBTW.BUF0 _PBTN.COUNT @
        R@ _PBTW.CHILD-MODE @ _PBT-CHILD-COUNT-DELTA +
    DUP 0= IF
        2DROP
        -1 R@ _PBTW.REMOVE !
        0 R@ _PBTW.SPLIT !
        0 R@ _PBTW.DEFERRED !
        R> DROP PERSIST-S-OK EXIT
    THEN
    DUP 1 = R@ _PBTW.LEVEL @ 0= AND IF
        2DROP
        R@ _PBTW.CHILD-MODE @ _PBT-CHILD-REMOVE = IF
            R@ _PBTW.INDEX @ 0= IF 1 ELSE 0 THEN DUP
            R@ _PBTW.BUF0 _PBTB-CHILD@ R@ _PBTW.P1-PAGE !
            R@ _PBTW.BUF0 _PBTB-KEY$
            DUP R@ _PBTW.P1-KEY-U !
            R@ _PBTW.KEY SWAP MOVE
        THEN
        0 R@ _PBTW.REMOVE !
        0 R@ _PBTW.SPLIT !
        0 R@ _PBTW.DEFERRED !
        -1 R@ _PBTW.COLLAPSE !
        R> DROP PERSIST-S-OK EXIT
    THEN
    0 R@ _PBTW.REMOVE !
    0 R@ _PBTW.COLLAPSE !
    DUP PBTREE-BRANCH-CAPACITY <= IF
        0 R@ _PBTW.SPLIT !
        0 1 PICK 3 PICK R@ _PBTW.BUF1 R@ _PBT-BUILD-BRANCH-RANGE
    ELSE
        -1 R@ _PBTW.SPLIT !
        0 7 3 PICK R@ _PBTW.BUF1 R@ _PBT-BUILD-BRANCH-RANGE
        7 OVER 7 - 3 PICK R@ _PBTW.BUF2 R@ _PBT-BUILD-BRANCH-RANGE
    THEN
    2DROP
    R@ _PBTW.LEVEL @ 0>
    R@ _PBTW.BUF1 _PBT-NODE-UNDERFULL? AND
    R@ _PBTW.SPLIT @ 0= AND IF
        -1 R@ _PBTW.DEFERRED !
        R@ _PBTW.BUF1 R@ _PBTW.KEY R@ _PBTW.P1-KEY-U _PBT-CAPTURE-MAX
        R> DROP EXIT
    THEN
    0 R@ _PBTW.DEFERRED !
    R@ _PBT-CAPTURE-AND-WRITE-CHILDREN R> DROP ;

: _PBT-UNWIND  ( work -- status )
    >R
    R@ _PBTW.DEPTH @ 2 - R@ _PBTW.LEVEL !
    BEGIN R@ _PBTW.LEVEL @ 0< 0= WHILE
        R@ _PBTW.LEVEL @ R@ _PBTW-PATH-PAGE @ R@ _PBT-READ-TX
        DUP IF R> DROP EXIT THEN DROP
        R@ _PBTW.DEPTH @ R@ _PBTW.LEVEL @ - DUP
        R@ _PBTW.BUF0 SWAP R@ _PBT-NODE-VALID? 0= IF
            DROP R> DROP PERSIST-S-CORRUPT EXIT
        THEN
        R@ _PBTW.LEVEL @ R@ _PBTW-PATH-INDEX @ R@ _PBTW.INDEX !
        R@ _PBT-REWRITE-PARENT DUP IF R> DROP EXIT THEN DROP
        -1 R@ _PBTW.LEVEL +!
    REPEAT
    R> DROP PERSIST-S-OK ;

: _PBT-SET-P1  ( index node work -- )
    >R
    OVER R@ _PBTW.TEMP0 ! DUP R@ _PBTW.TEMP1 ! 2DROP
    R@ _PBTW.P1-PAGE @ R@ _PBTW.KEY R@ _PBTW.P1-KEY-U @
        R@ _PBTW.TEMP0 @ R@ _PBTW.TEMP1 @ _PBT-BRANCH-SET
    R> DROP ;

: _PBT-SET-P2  ( index node work -- )
    >R
    OVER R@ _PBTW.TEMP0 ! DUP R@ _PBTW.TEMP1 ! 2DROP
    R@ _PBTW.P2-PAGE @ R@ _PBTW.KEY2 R@ _PBTW.P2-KEY-U @
        R@ _PBTW.TEMP0 @ R@ _PBTW.TEMP1 @ _PBT-BRANCH-SET
    R> DROP ;

: _PBT-PUBLISH-ROOT-OUTPUT  ( work -- status )
    DUP _PBTW.BUF3 OVER _PBTW.OUT-ROOT @ PBTREE-ROOT-SIZE MOVE
    DROP PERSIST-S-OK ;

: _PBT-WRITE-ROOT  ( work -- status )
    >R
    R@ _PBTW.ROOT @ _PBTR.CARDINALITY @ R@ _PBTW.DELTA @ +
        R@ _PBTW.CURRENT-PAGE !
    R@ _PBTW.BUF3 DUP PBTREE-ROOT-SIZE 0 FILL
    _PBTR-MAGIC OVER _PBTR.MAGIC !
    R@ _PBTW.TREE @ _PBTD.SCOPE @ OVER _PBTR.SCOPE !
    R@ _PBTW.TREE @ _PBTD.STORE @ PSTORE-GENERATION@ 1+
        OVER _PBTR.GENERATION !
    R@ _PBTW.CURRENT-PAGE @ OVER _PBTR.CARDINALITY !
    DROP
    R@ _PBTW.CURRENT-PAGE @ 0= R@ _PBTW.REMOVE @ OR IF
        -1 R@ _PBTW.BUF3 _PBTR.PAGE !
        0 R@ _PBTW.BUF3 _PBTR.HEIGHT !
        R@ _PBT-PUBLISH-ROOT-OUTPUT R> DROP EXIT
    THEN
    R@ _PBTW.SPLIT @ IF
        R@ _PBTW.DEPTH @ PBTREE-HEIGHT-MAX >= IF
            R> DROP PERSIST-S-CAPACITY EXIT
        THEN
        _PBTN-BRANCH R@ _PBTW.DEPTH @ 1+
            R@ _PBTW.BUF0 R@ _PBT-NODE-INIT
        2 R@ _PBTW.BUF0 _PBTN.COUNT !
        0 R@ _PBTW.BUF0 R@ _PBT-SET-P1
        1 R@ _PBTW.BUF0 R@ _PBT-SET-P2
        R@ _PBTW.BUF0 R@ _PBT-WRITE
        DUP IF NIP R> DROP EXIT THEN DROP
        R@ _PBTW.BUF3 _PBTR.PAGE !
        R@ _PBTW.DEPTH @ 1+ R@ _PBTW.BUF3 _PBTR.HEIGHT !
        R@ _PBT-PUBLISH-ROOT-OUTPUT R> DROP EXIT
    THEN
    R@ _PBTW.P1-PAGE @ R@ _PBTW.BUF3 _PBTR.PAGE !
    R@ _PBTW.DEPTH @ R@ _PBTW.COLLAPSE @ IF 1- THEN
        R@ _PBTW.BUF3 _PBTR.HEIGHT !
    R@ _PBT-PUBLISH-ROOT-OUTPUT R> DROP ;

: _PBT-PUBLISH-RETIRED  ( work -- status )
    DUP _PBTW.ROOT @ _PBTR.PAGE @ -1 = IF
        DROP PERSIST-S-OK EXIT
    THEN
    DUP _PBTW.DEPTH @ 0 ?DO
        I OVER _PBTW-PATH-PAGE @ OVER _PBT-RECORD-RETIREMENT
        DUP IF NIP UNLOOP EXIT THEN DROP
    LOOP
    DROP PERSIST-S-OK ;

