\ =====================================================================
\  btree.f - Caller-owned immutable ordered index
\ =====================================================================
\  Neutral copy-on-write B+tree over PSTORE checked pages.  This module
\  owns no mutable storage, path, schema, applet, reclamation, or publication
\  policy.  Mutations return a root which the caller stages in its own
\  application-root page before committing the surrounding transaction.
\
\  Leaves are deliberately not linked.  Ordered continuation crosses page
\  boundaries with a sealed cursor path.  Fixed-width page slots exchange a
\  little fanout for exact validation, canonical padding, and fixed workspace.
\ =====================================================================

