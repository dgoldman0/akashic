\ Theoretical full-node capacity used as a root corruption ceiling and by
\ architecture models.  The balanced split/merge bound below is lower.
: PBTREE-THEORETICAL-CAPACITY-FOR-HEIGHT  ( height -- cardinality )
    DUP 1 < OVER PBTREE-HEIGHT-MAX > OR IF DROP 0 EXIT THEN
    PBTREE-LEAF-CAPACITY SWAP 1 ?DO
        DUP PERSIST-MAX-SIGNED PBTREE-BRANCH-CAPACITY / > IF
            DROP PERSIST-MAX-SIGNED UNLOOP EXIT
        THEN
        PBTREE-BRANCH-CAPACITY *
    LOOP ;

\ Monotonic-build capacity under the deterministic half-split insertion policy.
\ Values are one less than the next root-split thresholds: 11, 89, 635, 4457,
\ ... through bounded height 9.  Deletion may retain a taller balanced tree at
\ a lower cardinality, so this is not a post-churn resident-height ceiling.
: PBTREE-BALANCED-CAPACITY-FOR-HEIGHT  ( height -- cardinality )
    DUP 1 < OVER PBTREE-HEIGHT-MAX > OR IF DROP 0 EXIT THEN
    12 SWAP 1 ?DO 7 * 6 + LOOP 1- ;

\ Minimum height required by those monotonic-build thresholds.  A persisted
\ root remains the authority for its actual height after balanced churn.
: PBTREE-HEIGHT-FOR  ( cardinality -- monotonic-build-height|0 )
    DUP 0> 0= IF DROP 0 EXIT THEN
    PBTREE-HEIGHT-MAX 1+ 1 ?DO
        DUP I PBTREE-BALANCED-CAPACITY-FOR-HEIGHT <= IF
            DROP I UNLOOP EXIT
        THEN
    LOOP
    DROP 0 ;

: PBTREE-ROOT-VALID?  ( root tree -- flag )
    >R
    R@ PBTREE-VALID? 0= IF DROP R> DROP 0 EXIT THEN
    DUP 0= IF DROP R> DROP 0 EXIT THEN
    DUP PBTREE-ROOT-SIZE MSPAN-NONWRAPPING? 0= IF DROP R> DROP 0 EXIT THEN
    DUP _PBTR.MAGIC @ _PBTR-MAGIC <> IF DROP R> DROP 0 EXIT THEN
    DUP _PBTR.SCOPE @ R@ _PBTD.SCOPE @ <> IF DROP R> DROP 0 EXIT THEN
    DUP _PBTR.GENERATION @ 0< IF DROP R> DROP 0 EXIT THEN
    DUP _PBTR.CARDINALITY @ 0< IF DROP R> DROP 0 EXIT THEN
    DUP _PBTR.HEIGHT @ DUP 0< SWAP PBTREE-HEIGHT-MAX > OR IF
        DROP R> DROP 0 EXIT
    THEN
    DUP _PBTR.PAGE @ -1 = IF
        DUP _PBTR.HEIGHT @ 0=
        OVER _PBTR.CARDINALITY @ 0= AND
        OVER _PBTR.RESERVED 16 _PBT-ZERO? AND
        SWAP DROP R> DROP EXIT
    THEN
    DUP _PBTR.PAGE @ 0< IF DROP R> DROP 0 EXIT THEN
    DUP _PBTR.HEIGHT @ 0> 0= IF DROP R> DROP 0 EXIT THEN
    DUP _PBTR.CARDINALITY @ 0> 0= IF DROP R> DROP 0 EXIT THEN
    DUP _PBTR.CARDINALITY @ OVER _PBTR.HEIGHT @
        PBTREE-THEORETICAL-CAPACITY-FOR-HEIGHT > IF
        DROP R> DROP 0 EXIT
    THEN
    _PBTR.RESERVED 16 _PBT-ZERO? R> DROP ;

