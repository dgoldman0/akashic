: PBTREE-VALID?  ( tree -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP PBTREE-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _PBTD.MAGIC @ _PBTREE-MAGIC <> IF DROP 0 EXIT THEN
    DUP _PBTD.STORE @ PSTORE-VALID? 0= IF DROP 0 EXIT THEN
    DUP _PBTD.SCOPE @ 0> 0= IF DROP 0 EXIT THEN
    DUP _PBTD.ALLOCATOR-XT @ 0= IF DROP 0 EXIT THEN
    DUP _PBTD.STATUS @ DUP PERSIST-S-FAULT > SWAP 0< OR IF DROP 0 EXIT THEN
    DUP _PBTD.PAGE-READS @ 0< IF DROP 0 EXIT THEN
    DUP _PBTD.PAGE-WRITES @ 0< IF DROP 0 EXIT THEN
    DUP _PBTD.COMPARISONS @ 0< IF DROP 0 EXIT THEN
    _PBTD.WORKING-BYTES @ 0< 0= ;

: PBTREE-INIT  ( scope allocator-xt allocator-context store tree -- status )
    >R
    DUP PSTORE-VALID? 0= IF 2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    3 PICK 0> 0= IF 2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    2 PICK 0= IF 2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    R@ PBTREE-SIZE MSPAN-NONWRAPPING? 0= IF
        2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ PBTREE-SIZE 2 PICK PSTORE-SPAN-DISJOINT? 0= IF
        2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ PBTREE-SIZE 0 FILL
    _PBTREE-MAGIC R@ _PBTD.MAGIC !
    DUP R@ _PBTD.STORE !
    3 PICK R@ _PBTD.SCOPE !
    2 PICK R@ _PBTD.ALLOCATOR-XT !
    1 PICK R@ _PBTD.ALLOCATOR-CONTEXT !
    PERSIST-S-OK R@ _PBTD.STATUS !
    2DROP 2DROP R> DROP PERSIST-S-OK ;

