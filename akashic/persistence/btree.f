: _PBT-GET-RUN  ( work -- value-a value-u status )
    >R
    R@ _PBTW.ROOT @ R@ _PBTW.TREE @ _PBT-ROOT-CURRENT? 0= IF
        0 0 PERSIST-S-CONFLICT R> DROP EXIT
    THEN
    R@ _PBTW.ROOT @ _PBTR.PAGE @ -1 = IF
        0 0 PERSIST-S-NOT-FOUND R> DROP EXIT
    THEN
    R@ _PBTW.ROOT @ _PBTR.PAGE @ R@ _PBTW.CURRENT-PAGE !
    R@ _PBTW.ROOT @ _PBTR.HEIGHT @ R@ _PBTW.DEPTH !
    0 R@ _PBTW.LEVEL !
    BEGIN R@ _PBTW.LEVEL @ R@ _PBTW.DEPTH @ 1- < WHILE
        R@ _PBTW.CURRENT-PAGE @ R@ _PBT-READ
        DUP IF 0 0 ROT R> DROP EXIT THEN DROP
        R@ _PBTW.BUF0
        R@ _PBTW.DEPTH @ R@ _PBTW.LEVEL @ -
        R@ _PBT-NODE-VALID? 0= IF
            0 0 PERSIST-S-CORRUPT R> DROP EXIT
        THEN
        R@ _PBTW.BUF0 R@ _PBT-BRANCH-CHOOSE
            R@ _PBTW.BUF0 _PBTB-CHILD@ R@ _PBTW.CURRENT-PAGE !
        1 R@ _PBTW.LEVEL +!
    REPEAT
    R@ _PBTW.CURRENT-PAGE @ R@ _PBT-READ
    DUP IF 0 0 ROT R> DROP EXIT THEN DROP
    R@ _PBTW.BUF0 1 R@ _PBT-NODE-VALID? 0= IF
        0 0 PERSIST-S-CORRUPT R> DROP EXIT
    THEN
    R@ _PBTW.BUF0 R@ _PBT-LEAF-LOWER 0= IF
        DROP 0 0 PERSIST-S-NOT-FOUND R> DROP EXIT
    THEN
    R@ _PBTW.BUF0 _PBTL-VALUE$
    DUP R@ _PBTW.VALUE-U !
    OVER R@ _PBTW.VALUE 2 PICK MOVE
    2DROP R@ _PBTW.VALUE R@ _PBTW.VALUE-U @ PERSIST-S-OK R> DROP ;

: _PBT-GET-RUN-TX  ( work -- value-a value-u status )
    >R
    R@ _PBTW.TREE @ _PBTD.STORE @ R@ _PBTW.PSTORE-WORK @
        PSTORE-TX-READY? 0= IF
        0 0 PERSIST-S-BUSY R> DROP EXIT
    THEN
    R@ _PBTW.ROOT @ R@ _PBTW.TREE @ _PBT-ROOT-MUTABLE? 0= IF
        0 0 PERSIST-S-CONFLICT R> DROP EXIT
    THEN
    R@ _PBTW.ROOT @ _PBTR.PAGE @ -1 = IF
        0 0 PERSIST-S-NOT-FOUND R> DROP EXIT
    THEN
    R@ _PBTW.ROOT @ _PBTR.PAGE @ R@ _PBTW.CURRENT-PAGE !
    R@ _PBTW.ROOT @ _PBTR.HEIGHT @ R@ _PBTW.DEPTH !
    0 R@ _PBTW.LEVEL !
    BEGIN R@ _PBTW.LEVEL @ R@ _PBTW.DEPTH @ 1- < WHILE
        R@ _PBTW.CURRENT-PAGE @ R@ _PBT-READ-TX
        DUP IF 0 0 ROT R> DROP EXIT THEN DROP
        R@ _PBTW.BUF0
        R@ _PBTW.DEPTH @ R@ _PBTW.LEVEL @ -
        R@ _PBT-NODE-VALID? 0= IF
            0 0 PERSIST-S-CORRUPT R> DROP EXIT
        THEN
        R@ _PBTW.BUF0 R@ _PBT-BRANCH-CHOOSE
            R@ _PBTW.BUF0 _PBTB-CHILD@ R@ _PBTW.CURRENT-PAGE !
        1 R@ _PBTW.LEVEL +!
    REPEAT
    R@ _PBTW.CURRENT-PAGE @ R@ _PBT-READ-TX
    DUP IF 0 0 ROT R> DROP EXIT THEN DROP
    R@ _PBTW.BUF0 1 R@ _PBT-NODE-VALID? 0= IF
        0 0 PERSIST-S-CORRUPT R> DROP EXIT
    THEN
    R@ _PBTW.BUF0 R@ _PBT-LEAF-LOWER 0= IF
        DROP 0 0 PERSIST-S-NOT-FOUND R> DROP EXIT
    THEN
    R@ _PBTW.BUF0 _PBTL-VALUE$
    DUP R@ _PBTW.VALUE-U !
    OVER R@ _PBTW.VALUE 2 PICK MOVE
    2DROP R@ _PBTW.VALUE R@ _PBTW.VALUE-U @ PERSIST-S-OK R> DROP ;

: _PBT-PREPARE-GET  ( key-a key-u root tree work -- status )
    >R
    2DUP R@ _PBT-BEGIN-OP DUP IF
        >R 2DROP 2DROP R> R> DROP EXIT
    THEN DROP
    3 PICK 3 PICK 0 0 R@ _PBT-COPY-TARGETS
    >R 2DROP 2DROP R> R> DROP ;

: PBTREE-GET  ( key-a key-u root tree work -- value-a value-u status )
    DUP _PBT-WORK-ENTRY-STATUS DUP IF
        >R 2DROP 2DROP DROP 0 0 R> EXIT
    THEN DROP
    DUP >R _PBT-PREPARE-GET
    DUP IF R@ _PBT-END-OP >R 0 0 R> R> DROP EXIT THEN DROP
    R@ _PBT-GET-RUN
    DUP R@ _PBT-END-OP >R DROP R> R> DROP ;

: PBTREE-GET-TX  ( key-a key-u root tree work -- value-a value-u status )
    DUP _PBT-WORK-ENTRY-STATUS DUP IF
        >R 2DROP 2DROP DROP 0 0 R> EXIT
    THEN DROP
    DUP >R _PBT-PREPARE-GET
    DUP IF R@ _PBT-END-OP >R 0 0 R> R> DROP EXIT THEN DROP
    R@ _PBT-GET-RUN-TX
    DUP R@ _PBT-END-OP >R DROP R> R> DROP ;

: _PBT-PREPARE-MUTATION
  ( key-a key-u value-a value-u root out-root tree work -- status )
    >R
    2 PICK OVER R@ _PBT-BEGIN-OP DUP IF
        >R 2DROP 2DROP 2DROP DROP R> R> DROP EXIT
    THEN DROP
    R@ _PBTW.TREE @ _PBTD.STORE @ PSTORE-GENERATION@
        PERSIST-MAX-SIGNED >= IF
        2DROP 2DROP 2DROP DROP R> DROP PERSIST-S-CAPACITY EXIT
    THEN
    1 PICK DUP 0= IF
        DROP 2DROP 2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DUP PBTREE-ROOT-SIZE MSPAN-NONWRAPPING? 0= IF
        DROP 2DROP 2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DUP PBTREE-ROOT-SIZE R@ PBTREE-WORK-SIZE MSPAN-OVERLAP? IF
        DROP 2DROP 2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PBTW.OUT-ROOT !
    2 PICK PBTREE-ROOT-SIZE 3 PICK PBTREE-ROOT-SIZE MSPAN-OVERLAP? IF
        2DROP 2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PBTW.OUT-ROOT @ PBTREE-ROOT-SIZE 2 PICK PBTREE-SIZE
        MSPAN-OVERLAP? IF
        2DROP 2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PBTW.OUT-ROOT @ PBTREE-ROOT-SIZE
        2 PICK _PBTD.STORE @ PSTORE-SPAN-DISJOINT? 0= IF
        2DROP 2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PBTW.OUT-ROOT @ PBTREE-ROOT-SIZE R@ _PBTW.PSTORE-WORK @
        PSTORE-WORK-SPAN-DISJOINT? 0= IF
        2DROP 2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    6 PICK 6 PICK 6 PICK 6 PICK R@ _PBT-COPY-TARGETS
    >R 2DROP 2DROP 2DROP DROP R> R> DROP ;

: _PBT-PUT-RUN  ( work -- status )
    >R
    R@ _PBTW.ROOT @ R@ _PBTW.TREE @ _PBT-ROOT-MUTABLE? 0= IF
        R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    R@ _PBTW.ROOT @ _PBTR.PAGE @ -1 = IF
        1 R@ _PBTW.DEPTH !
        1 R@ _PBTW.DELTA !
        0 R@ _PBTW.SPLIT ! 0 R@ _PBTW.REMOVE ! 0 R@ _PBTW.COLLAPSE !
        _PBTN-LEAF 1 R@ _PBTW.BUF1 R@ _PBT-NODE-INIT
        1 R@ _PBTW.BUF1 _PBTN.COUNT !
        R@ _PBTW.KEY R@ _PBTW.KEY-U @
        R@ _PBTW.VALUE R@ _PBTW.VALUE-U @
        0 R@ _PBTW.BUF1 _PBT-LEAF-SET
        R@ _PBT-CAPTURE-AND-WRITE-CHILDREN DUP IF R> DROP EXIT THEN DROP
        R@ _PBT-WRITE-ROOT DUP IF R> DROP EXIT THEN DROP
        R@ _PBT-PUBLISH-RETIRED R> DROP EXIT
    THEN
    R@ _PBT-DESCEND-TX DUP IF NIP R> DROP EXIT THEN DROP
    R@ _PBT-PUT-LEAF DUP IF R> DROP EXIT THEN DROP
    R@ _PBT-UNWIND DUP IF R> DROP EXIT THEN DROP
    R@ _PBT-WRITE-ROOT DUP IF R> DROP EXIT THEN DROP
    R@ _PBT-PUBLISH-RETIRED R> DROP ;

: _PBT-MUTATION-COMPLETE  ( status work -- status )
    >R
    DUP IF
        0 R@ _PBTW.RETIRED-COUNT !
        DUP R@ _PBTW.TREE @ _PBTD.STORE @
            R@ _PBTW.PSTORE-WORK @ PSTORE-TX-POISON DROP
    THEN
    R> DROP ;

\ A missing delete is the one prepared mutation failure that is known to have
\ made no proposal writes.  A NOT-FOUND observed after an allocation is not
\ that ordinary case and still poisons the surrounding transaction.
: _PBT-DELETE-COMPLETE  ( status work -- status )
    >R
    DUP IF
        0 R@ _PBTW.RETIRED-COUNT !
        DUP PERSIST-S-NOT-FOUND <>
        R@ _PBTW.ALLOC-COUNT @ 0<> OR IF
            DUP R@ _PBTW.TREE @ _PBTD.STORE @
                R@ _PBTW.PSTORE-WORK @ PSTORE-TX-POISON DROP
        THEN
    THEN
    R> DROP ;

: PBTREE-PUT
  ( key-a key-u value-a value-u root out-root tree work -- status )
    DUP _PBT-WORK-ENTRY-STATUS DUP IF
        >R 2DROP 2DROP 2DROP 2DROP R> EXIT
    THEN DROP
    DUP >R _PBT-PREPARE-MUTATION
    DUP IF R@ _PBT-END-OP R> DROP EXIT THEN DROP
    0 R@ _PBTW.RETIRED-COUNT !
    R@ _PBT-PUT-RUN R@ _PBT-MUTATION-COMPLETE
    R@ _PBT-END-OP R> DROP ;

: _PBT-PREPARE-DELETE  ( key-a key-u root out-root tree work -- status )
    >R
    2 PICK OVER R@ _PBT-BEGIN-OP DUP IF
        >R 2DROP 2DROP DROP R> R> DROP EXIT
    THEN DROP
    R@ _PBTW.TREE @ _PBTD.STORE @ PSTORE-GENERATION@
        PERSIST-MAX-SIGNED >= IF
        2DROP 2DROP DROP R> DROP PERSIST-S-CAPACITY EXIT
    THEN
    1 PICK DUP 0= IF
        DROP 2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DUP PBTREE-ROOT-SIZE MSPAN-NONWRAPPING? 0= IF
        DROP 2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DUP PBTREE-ROOT-SIZE R@ PBTREE-WORK-SIZE MSPAN-OVERLAP? IF
        DROP 2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PBTW.OUT-ROOT !
    2 PICK PBTREE-ROOT-SIZE 3 PICK PBTREE-ROOT-SIZE MSPAN-OVERLAP? IF
        2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PBTW.OUT-ROOT @ PBTREE-ROOT-SIZE 2 PICK PBTREE-SIZE
        MSPAN-OVERLAP? IF
        2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PBTW.OUT-ROOT @ PBTREE-ROOT-SIZE
        2 PICK _PBTD.STORE @ PSTORE-SPAN-DISJOINT? 0= IF
        2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PBTW.OUT-ROOT @ PBTREE-ROOT-SIZE R@ _PBTW.PSTORE-WORK @
        PSTORE-WORK-SPAN-DISJOINT? 0= IF
        2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    4 PICK 4 PICK 0 0 R@ _PBT-COPY-TARGETS
    >R 2DROP 2DROP DROP R> R> DROP ;

: _PBT-DELETE-RUN  ( work -- status )
    >R
    R@ _PBTW.ROOT @ R@ _PBTW.TREE @ _PBT-ROOT-MUTABLE? 0= IF
        R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    R@ _PBTW.ROOT @ _PBTR.PAGE @ -1 = IF
        R> DROP PERSIST-S-NOT-FOUND EXIT
    THEN
    R@ _PBT-DESCEND-TX DUP IF NIP R> DROP EXIT THEN DROP
    0= IF R> DROP PERSIST-S-NOT-FOUND EXIT THEN
    R@ _PBT-DELETE-LEAF DUP IF R> DROP EXIT THEN DROP
    R@ _PBT-UNWIND DUP IF R> DROP EXIT THEN DROP
    R@ _PBT-WRITE-ROOT DUP IF R> DROP EXIT THEN DROP
    R@ _PBT-PUBLISH-RETIRED R> DROP ;

: PBTREE-DELETE  ( key-a key-u root out-root tree work -- status )
    DUP _PBT-WORK-ENTRY-STATUS DUP IF
        >R 2DROP 2DROP 2DROP R> EXIT
    THEN DROP
    DUP >R _PBT-PREPARE-DELETE
    DUP IF R@ _PBT-END-OP R> DROP EXIT THEN DROP
    0 R@ _PBTW.RETIRED-COUNT !
    R@ _PBT-DELETE-RUN R@ _PBT-DELETE-COMPLETE
    R@ _PBT-END-OP R> DROP ;

\ ---------------------------------------------------------------------
\ Opaque stable-key cursor
\ ---------------------------------------------------------------------
\ The cursor pins one exact root identity, generation, and scope.  Its path
\ is a navigation hint bounded by PBTREE-HEIGHT-MAX; the last key is the
\ semantic continuation.  NEXT walks the path rather than seeking from the
\ root for every result.  RESUME performs one exclusive stable-key seek.

  0 CONSTANT _PBTCR-MAGIC-OFF
  8 CONSTANT _PBTCR-SCOPE-OFF
 16 CONSTANT _PBTCR-GENERATION-OFF
 24 CONSTANT _PBTCR-ROOT-PAGE-OFF
 32 CONSTANT _PBTCR-HEIGHT-OFF
 40 CONSTANT _PBTCR-STARTED-OFF
 48 CONSTANT _PBTCR-EOF-OFF
 56 CONSTANT _PBTCR-LAST-U-OFF
 64 CONSTANT _PBTCR-LAST-KEY-OFF
320 CONSTANT _PBTCR-SEAL-OFF
328 CONSTANT _PBTCR-PATH-OFF
 16 CONSTANT _PBTCR-PATH-ENTRY-SIZE
_PBTCR-PATH-OFF PBTREE-HEIGHT-MAX _PBTCR-PATH-ENTRY-SIZE * +
    CONSTANT PBTREE-CURSOR-SIZE

: _PBTCR.MAGIC      ( cursor -- a ) _PBTCR-MAGIC-OFF + ;
: _PBTCR.SCOPE      ( cursor -- a ) _PBTCR-SCOPE-OFF + ;
: _PBTCR.GENERATION ( cursor -- a ) _PBTCR-GENERATION-OFF + ;
: _PBTCR.ROOT-PAGE  ( cursor -- a ) _PBTCR-ROOT-PAGE-OFF + ;
: _PBTCR.HEIGHT     ( cursor -- a ) _PBTCR-HEIGHT-OFF + ;
: _PBTCR.STARTED    ( cursor -- a ) _PBTCR-STARTED-OFF + ;
: _PBTCR.EOF        ( cursor -- a ) _PBTCR-EOF-OFF + ;
: _PBTCR.LAST-U     ( cursor -- a ) _PBTCR-LAST-U-OFF + ;
: _PBTCR.LAST-KEY   ( cursor -- a ) _PBTCR-LAST-KEY-OFF + ;
: _PBTCR.SEAL       ( cursor -- a ) _PBTCR-SEAL-OFF + ;

: _PBTCR-PATH-ENTRY  ( level cursor -- entry )
    SWAP _PBTCR-PATH-ENTRY-SIZE * _PBTCR-PATH-OFF + + ;
: _PBTCR-PATH-PAGE   ( level cursor -- a ) _PBTCR-PATH-ENTRY ;
: _PBTCR-PATH-INDEX  ( level cursor -- a ) _PBTCR-PATH-ENTRY 8 + ;

: _PBTCR-SEAL-CALCULATE  ( cursor -- seal )
    DUP _PBTCR.SCOPE @ OVER _PBTCR.GENERATION @ XOR
    OVER _PBTCR.ROOT-PAGE @ XOR OVER _PBTCR.HEIGHT @ XOR
    OVER _PBTCR.STARTED @ XOR OVER _PBTCR.EOF @ XOR
    OVER _PBTCR.LAST-U @ XOR
    OVER _PBTCR.LAST-U @ 0 ?DO
        OVER _PBTCR.LAST-KEY I + C@ XOR 33 * PERSIST-MAX-SIGNED AND
    LOOP
    PBTREE-HEIGHT-MAX 0 ?DO
        I 2 PICK _PBTCR-PATH-PAGE @ XOR 33 * PERSIST-MAX-SIGNED AND
        I 2 PICK _PBTCR-PATH-INDEX @ XOR 33 * PERSIST-MAX-SIGNED AND
    LOOP
    NIP ;

: _PBTCR-SEAL!  ( cursor -- )
    DUP _PBTCR-SEAL-CALCULATE SWAP _PBTCR.SEAL ! ;

: _PBTCR-FLAG?  ( x -- flag ) DUP 0= SWAP -1 = OR ;

: _PBTCR-PATH-CANONICAL?  ( cursor -- flag )
    DUP _PBTCR.STARTED @ 0= IF
        _PBTCR-PATH-OFF + PBTREE-HEIGHT-MAX _PBTCR-PATH-ENTRY-SIZE *
            _PBT-ZERO? EXIT
    THEN
    DUP _PBTCR.HEIGHT @ 0 ?DO
        I OVER _PBTCR-PATH-PAGE @ 0< IF DROP 0 UNLOOP EXIT THEN
        I OVER _PBTCR-PATH-INDEX @ 0< IF DROP 0 UNLOOP EXIT THEN
    LOOP
    PBTREE-HEIGHT-MAX OVER _PBTCR.HEIGHT @ ?DO
        I OVER _PBTCR-PATH-ENTRY _PBTCR-PATH-ENTRY-SIZE _PBT-ZERO? 0= IF
            DROP 0 UNLOOP EXIT
        THEN
    LOOP
    DROP -1 ;

: PBTREE-CURSOR-VALID?  ( cursor -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP PBTREE-CURSOR-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _PBTCR.MAGIC @ _PBTC-MAGIC <> IF DROP 0 EXIT THEN
    DUP _PBTCR.SCOPE @ 0> 0= IF DROP 0 EXIT THEN
    DUP _PBTCR.GENERATION @ 0< IF DROP 0 EXIT THEN
    DUP _PBTCR.ROOT-PAGE @ -1 < IF DROP 0 EXIT THEN
    DUP _PBTCR.HEIGHT @ DUP 0< SWAP PBTREE-HEIGHT-MAX > OR IF DROP 0 EXIT THEN
    DUP _PBTCR.ROOT-PAGE @ -1 = OVER _PBTCR.HEIGHT @ 0= <> IF DROP 0 EXIT THEN
    DUP _PBTCR.STARTED @ _PBTCR-FLAG? 0= IF DROP 0 EXIT THEN
    DUP _PBTCR.EOF @ _PBTCR-FLAG? 0= IF DROP 0 EXIT THEN
    DUP _PBTCR.LAST-U @ DUP 0< SWAP PBTREE-KEY-MAX > OR IF DROP 0 EXIT THEN
    DUP _PBTCR.STARTED @ IF
        DUP _PBTCR.LAST-U @ 0> 0= IF DROP 0 EXIT THEN
    ELSE
        DUP _PBTCR.LAST-U @ IF DROP 0 EXIT THEN
    THEN
    DUP _PBTCR.LAST-KEY OVER _PBTCR.LAST-U @ PBTREE-KEY-MAX
        _PBT-PAD-ZERO? 0= IF DROP 0 EXIT THEN
    DUP _PBTCR-PATH-CANONICAL? 0= IF DROP 0 EXIT THEN
    DUP _PBTCR.SEAL @ OVER _PBTCR-SEAL-CALCULATE = SWAP DROP ;

: _PBTCR-RESET  ( root tree cursor -- status )
    >R
    2DUP PBTREE-ROOT-VALID? 0= IF 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    2DUP _PBT-ROOT-CURRENT? 0= IF 2DROP R> DROP PERSIST-S-CONFLICT EXIT THEN
    R@ PBTREE-CURSOR-SIZE MSPAN-NONWRAPPING? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ PBTREE-CURSOR-SIZE 3 PICK PBTREE-ROOT-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ PBTREE-CURSOR-SIZE 2 PICK PBTREE-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ PBTREE-CURSOR-SIZE 2 PICK _PBTD.STORE @ PSTORE-SPAN-DISJOINT? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ PBTREE-CURSOR-SIZE 0 FILL
    _PBTC-MAGIC R@ _PBTCR.MAGIC !
    DUP _PBTD.SCOPE @ R@ _PBTCR.SCOPE !
    OVER _PBTR.GENERATION @ R@ _PBTCR.GENERATION !
    OVER _PBTR.PAGE @ R@ _PBTCR.ROOT-PAGE !
    OVER _PBTR.HEIGHT @ R@ _PBTCR.HEIGHT !
    R@ _PBTCR-SEAL!
    2DROP R> DROP PERSIST-S-OK ;

: PBTREE-CURSOR-INIT  ( root tree cursor -- status )
    _PBTCR-RESET ;

: _PBTCR-MATCH  ( root tree cursor -- status )
    >R
    2DUP PBTREE-ROOT-VALID? 0= IF 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    R@ PBTREE-CURSOR-SIZE MSPAN-NONWRAPPING? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PBTCR.MAGIC @ _PBTC-MAGIC <> IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PBTCR.SCOPE @ OVER _PBTD.SCOPE @ <> IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PBTCR.GENERATION @ 2 PICK _PBTR.GENERATION @ <>
    R@ _PBTCR.GENERATION @ 2 PICK _PBTD.STORE @ PSTORE-GENERATION@ <> OR IF
        2DROP R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    R@ _PBTCR.ROOT-PAGE @ 2 PICK _PBTR.PAGE @ <>
    R@ _PBTCR.HEIGHT @ 3 PICK _PBTR.HEIGHT @ <> OR IF
        2DROP R> DROP PERSIST-S-CONFLICT EXIT
    THEN
    R@ PBTREE-CURSOR-VALID? 0= IF
        2DROP R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    2DROP R> DROP PERSIST-S-OK ;

: _PBTCR-EMIT  ( cursor work -- key-a key-u value-a value-u status )
    >R
    DUP _PBTCR.HEIGHT @ 1- OVER _PBTCR-PATH-PAGE @ R@ _PBT-READ
    DUP IF >R DROP 0 0 0 0 R> R> DROP EXIT THEN DROP
    R@ _PBTW.BUF0 1 R@ _PBT-NODE-VALID? 0= IF
        DROP 0 0 0 0 PERSIST-S-CORRUPT R> DROP EXIT
    THEN
    DUP _PBTCR.HEIGHT @ 1- OVER _PBTCR-PATH-INDEX @ DUP
    R@ _PBTW.BUF0 _PBTN.COUNT @ >= IF
        2DROP 0 0 0 0 PERSIST-S-CORRUPT R> DROP EXIT
    THEN
    R@ _PBTW.INDEX !
    DUP _PBTCR.LAST-KEY PBTREE-KEY-MAX 0 FILL
    R@ _PBTW.INDEX @ R@ _PBTW.BUF0 _PBTL-KEY$
    DUP 3 PICK _PBTCR.LAST-U !
    OVER 3 PICK _PBTCR.LAST-KEY 2 PICK MOVE
    2DROP
    R@ _PBTW.INDEX @ R@ _PBTW.BUF0 _PBTL-VALUE$
    DUP R@ _PBTW.VALUE-U !
    OVER R@ _PBTW.VALUE 2 PICK MOVE
    2DROP
    -1 OVER _PBTCR.STARTED !
    0 OVER _PBTCR.EOF !
    DUP _PBTCR-SEAL!
    DUP _PBTCR.LAST-KEY SWAP _PBTCR.LAST-U @
    R@ _PBTW.VALUE R@ _PBTW.VALUE-U @ PERSIST-S-OK
    R> DROP ;

: _PBTCR-DESCEND-LEFTMOST
  ( start-page start-level cursor work -- status )
    >R
    DUP R@ _PBTW.TEMP0 ! DROP
    SWAP R@ _PBTW.CURRENT-PAGE !
    R@ _PBTW.LEVEL !
    BEGIN
        R@ _PBTW.LEVEL @ R@ _PBTW.TEMP0 @ _PBTCR.HEIGHT @ 1- <
    WHILE
        R@ _PBTW.CURRENT-PAGE @ R@ _PBT-READ
        DUP IF R> DROP EXIT THEN DROP
        R@ _PBTW.BUF0
        R@ _PBTW.TEMP0 @ _PBTCR.HEIGHT @ R@ _PBTW.LEVEL @ -
        R@ _PBT-NODE-VALID? 0= IF R> DROP PERSIST-S-CORRUPT EXIT THEN
        R@ _PBTW.CURRENT-PAGE @
            R@ _PBTW.LEVEL @ R@ _PBTW.TEMP0 @ _PBTCR-PATH-PAGE !
        0 R@ _PBTW.LEVEL @ R@ _PBTW.TEMP0 @ _PBTCR-PATH-INDEX !
        0 R@ _PBTW.BUF0 _PBTB-CHILD@ R@ _PBTW.CURRENT-PAGE !
        1 R@ _PBTW.LEVEL +!
    REPEAT
    R@ _PBTW.CURRENT-PAGE @ R@ _PBT-READ
    DUP IF R> DROP EXIT THEN DROP
    R@ _PBTW.BUF0 1 R@ _PBT-NODE-VALID? 0= IF
        R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    R@ _PBTW.BUF0 _PBTN.COUNT @ 0= IF R> DROP PERSIST-S-CORRUPT EXIT THEN
    R@ _PBTW.CURRENT-PAGE @
        R@ _PBTW.LEVEL @ R@ _PBTW.TEMP0 @ _PBTCR-PATH-PAGE !
    0 R@ _PBTW.LEVEL @ R@ _PBTW.TEMP0 @ _PBTCR-PATH-INDEX !
    R> DROP PERSIST-S-OK ;

: _PBTCR-FIRST  ( cursor work -- key-a key-u value-a value-u status )
    >R
    DUP _PBTCR.ROOT-PAGE @ -1 = IF
        -1 OVER _PBTCR.EOF ! DUP _PBTCR-SEAL! DROP
        0 0 0 0 PERSIST-S-NOT-FOUND R> DROP EXIT
    THEN
    DUP _PBTCR.ROOT-PAGE @ 0 2 PICK R@ _PBTCR-DESCEND-LEFTMOST
    DUP IF >R DROP 0 0 0 0 R> R> DROP EXIT THEN DROP
    R@ _PBTCR-EMIT R> DROP ;

: _PBTCR-ADVANCE  ( cursor work -- key-a key-u value-a value-u status )
    >R
    DUP _PBTCR.EOF @ IF
        DROP 0 0 0 0 PERSIST-S-NOT-FOUND R> DROP EXIT
    THEN
    DUP _PBTCR.HEIGHT @ 1- OVER _PBTCR-PATH-PAGE @ R@ _PBT-READ
    DUP IF >R DROP 0 0 0 0 R> R> DROP EXIT THEN DROP
    R@ _PBTW.BUF0 1 R@ _PBT-NODE-VALID? 0= IF
        DROP 0 0 0 0 PERSIST-S-CORRUPT R> DROP EXIT
    THEN
    DUP _PBTCR.HEIGHT @ 1- OVER _PBTCR-PATH-INDEX DUP @ 1+
    R@ _PBTW.BUF0 _PBTN.COUNT @ < IF
        1 SWAP +!
        R@ _PBTCR-EMIT R> DROP EXIT
    THEN
    DROP
    DUP _PBTCR.HEIGHT @ 2 - R@ _PBTW.LEVEL !
    BEGIN R@ _PBTW.LEVEL @ 0< 0= WHILE
        R@ _PBTW.LEVEL @ OVER _PBTCR-PATH-PAGE @ R@ _PBT-READ
        DUP IF >R DROP 0 0 0 0 R> R> DROP EXIT THEN DROP
        R@ _PBTW.BUF0
        OVER _PBTCR.HEIGHT @ R@ _PBTW.LEVEL @ -
        R@ _PBT-NODE-VALID? 0= IF
            DROP 0 0 0 0 PERSIST-S-CORRUPT R> DROP EXIT
        THEN
        R@ _PBTW.LEVEL @ OVER _PBTCR-PATH-INDEX DUP @ 1+
        R@ _PBTW.BUF0 _PBTN.COUNT @ < IF
            1 SWAP +!
            R@ _PBTW.LEVEL @ OVER _PBTCR-PATH-INDEX @
                R@ _PBTW.BUF0 _PBTB-CHILD@
            R@ _PBTW.LEVEL @ 1+ 2 PICK R@ _PBTCR-DESCEND-LEFTMOST
            DUP IF >R DROP 0 0 0 0 R> R> DROP EXIT THEN DROP
            R@ _PBTCR-EMIT R> DROP EXIT
        THEN
        DROP
        -1 R@ _PBTW.LEVEL +!
    REPEAT
    -1 OVER _PBTCR.EOF ! DUP _PBTCR-SEAL! DROP
    0 0 0 0 PERSIST-S-NOT-FOUND R> DROP ;

: _PBTCR-SEEK-RUN  ( exclusive? cursor work -- key-a key-u value-a value-u status )
    >R
    OVER R@ _PBTW.TEMP1 !
    DUP _PBTCR.ROOT-PAGE @ -1 = IF
        -1 OVER _PBTCR.EOF ! DUP _PBTCR-SEAL! 2DROP
        0 0 0 0 PERSIST-S-NOT-FOUND R> DROP EXIT
    THEN
    DUP _PBTCR.ROOT-PAGE @ R@ _PBTW.CURRENT-PAGE !
    0 R@ _PBTW.LEVEL !
    BEGIN R@ _PBTW.LEVEL @ OVER _PBTCR.HEIGHT @ 1- < WHILE
        R@ _PBTW.CURRENT-PAGE @ R@ _PBT-READ
        DUP IF >R 2DROP 0 0 0 0 R> R> DROP EXIT THEN DROP
        R@ _PBTW.BUF0 OVER _PBTCR.HEIGHT @ R@ _PBTW.LEVEL @ -
            R@ _PBT-NODE-VALID? 0= IF
            2DROP 0 0 0 0 PERSIST-S-CORRUPT R> DROP EXIT
        THEN
        R@ _PBTW.BUF0 R@ _PBT-BRANCH-CHOOSE R@ _PBTW.INDEX !
        R@ _PBTW.CURRENT-PAGE @
            R@ _PBTW.LEVEL @ 2 PICK _PBTCR-PATH-PAGE !
        R@ _PBTW.INDEX @ R@ _PBTW.LEVEL @ 2 PICK _PBTCR-PATH-INDEX !
        R@ _PBTW.INDEX @ R@ _PBTW.BUF0 _PBTB-CHILD@
            R@ _PBTW.CURRENT-PAGE !
        1 R@ _PBTW.LEVEL +!
    REPEAT
    R@ _PBTW.CURRENT-PAGE @ R@ _PBT-READ
    DUP IF >R 2DROP 0 0 0 0 R> R> DROP EXIT THEN DROP
    R@ _PBTW.BUF0 1 R@ _PBT-NODE-VALID? 0= IF
        2DROP 0 0 0 0 PERSIST-S-CORRUPT R> DROP EXIT
    THEN
    R@ _PBTW.CURRENT-PAGE @ R@ _PBTW.LEVEL @ 2 PICK _PBTCR-PATH-PAGE !
    R@ _PBTW.BUF0 R@ _PBT-LEAF-LOWER                ( exclusive cursor index found )
    SWAP R@ _PBTW.INDEX !                            ( exclusive cursor found )
    DUP R@ _PBTW.TEMP1 @ AND IF
        DROP
        R@ _PBTW.INDEX @ R@ _PBTW.LEVEL @ 2 PICK _PBTCR-PATH-INDEX !
        NIP R@ _PBTCR-ADVANCE R> DROP EXIT
    THEN
    DROP R@ _PBTW.INDEX @ R@ _PBTW.BUF0 _PBTN.COUNT @ < IF
        R@ _PBTW.INDEX @ R@ _PBTW.LEVEL @ 2 PICK _PBTCR-PATH-INDEX !
        NIP R@ _PBTCR-EMIT R> DROP EXIT
    THEN
    R@ _PBTW.BUF0 _PBTN.COUNT @ 1-
        R@ _PBTW.LEVEL @ 2 PICK _PBTCR-PATH-INDEX !
    NIP R@ _PBTCR-ADVANCE R> DROP ;

: _PBTCR-PREPARE-SEEK  ( key-a key-u root tree cursor work -- status )
    >R
    2 PICK 2 PICK R@ _PBT-BEGIN-OP DUP IF
        >R 2DROP 2DROP DROP R> R> DROP EXIT
    THEN DROP
    DUP PBTREE-CURSOR-SIZE R@ PBTREE-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DUP PBTREE-CURSOR-SIZE R@ _PBTW.PSTORE-WORK @
        PSTORE-WORK-SPAN-DISJOINT? 0= IF
        2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    4 PICK 4 PICK 2 PICK PBTREE-CURSOR-SIZE MSPAN-OVERLAP? IF
        2DROP 2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    2 PICK 2 PICK 2 PICK _PBTCR-RESET DUP IF
        >R 2DROP 2DROP DROP R> R> DROP EXIT
    THEN DROP
    DUP R@ _PBTW.TEMP0 !
    4 PICK 4 PICK 0 0 R@ _PBT-COPY-TARGETS
    >R 2DROP 2DROP DROP R> R> DROP ;

: _PBTCR-SEEK-PUBLIC
  ( key-a key-u root tree cursor work exclusive? -- key-a key-u value-a value-u status )
    >R
    DUP >R _PBTCR-PREPARE-SEEK
    DUP IF
        R@ _PBT-END-OP >R 0 0 0 0 R> R> DROP R> DROP EXIT
    THEN DROP
    R> R> OVER _PBTW.TEMP0 @ ROT DUP >R _PBTCR-SEEK-RUN
    DUP R@ _PBT-END-OP >R DROP R> R> DROP ;

: PBTREE-SEEK
  ( key-a key-u root tree cursor work -- key-a key-u value-a value-u status )
    DUP _PBT-WORK-ENTRY-STATUS DUP IF
        >R 2DROP 2DROP 2DROP 0 0 0 0 R> EXIT
    THEN DROP
    0 _PBTCR-SEEK-PUBLIC ;

: PBTREE-RESUME
  ( last-key-a last-key-u root tree cursor work -- key-a key-u value-a value-u status )
    DUP _PBT-WORK-ENTRY-STATUS DUP IF
        >R 2DROP 2DROP 2DROP 0 0 0 0 R> EXIT
    THEN DROP
    -1 _PBTCR-SEEK-PUBLIC ;

: _PBTCR-PREPARE-NEXT  ( root tree cursor work -- status )
    >R
    2 PICK 2 PICK R@ _PBT-BEGIN-OP DUP IF
        >R 2DROP DROP R> R> DROP EXIT
    THEN DROP
    DUP PBTREE-CURSOR-SIZE R@ PBTREE-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DUP PBTREE-CURSOR-SIZE R@ _PBTW.PSTORE-WORK @
        PSTORE-WORK-SPAN-DISJOINT? 0= IF
        2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    2 PICK 2 PICK 2 PICK _PBTCR-MATCH DUP IF
        >R 2DROP DROP R> R> DROP EXIT
    THEN DROP
    DUP R@ _PBTW.TEMP0 !
    2DROP DROP R> DROP PERSIST-S-OK ;

: PBTREE-NEXT
  ( root tree cursor work -- key-a key-u value-a value-u status )
    DUP _PBT-WORK-ENTRY-STATUS DUP IF
        >R 2DROP 2DROP 0 0 0 0 R> EXIT
    THEN DROP
    DUP >R _PBTCR-PREPARE-NEXT
    DUP IF
        R@ _PBT-END-OP >R 0 0 0 0 R> R> DROP EXIT
    THEN DROP
    R@ _PBTW.TEMP0 @ DUP _PBTCR.STARTED @ IF
        R@ _PBTCR-ADVANCE
    ELSE
        R@ _PBTCR-FIRST
    THEN
    DUP R@ _PBT-END-OP >R DROP R> R> DROP ;
