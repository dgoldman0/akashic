: _PBT-WORK-BOUND?  ( tree work -- flag )
    2DUP PBTREE-WORK-VALID? SWAP PBTREE-VALID? AND 0= IF 2DROP 0 EXIT THEN
    DUP PBTREE-WORK-SIZE 3 PICK PBTREE-SIZE MSPAN-OVERLAP? IF
        2DROP 0 EXIT
    THEN
    DUP PBTREE-WORK-SIZE 3 PICK _PBTD.STORE @
        PSTORE-SPAN-DISJOINT? 0= IF 2DROP 0 EXIT THEN
    OVER PBTREE-SIZE 3 PICK _PBTD.STORE @
        PSTORE-SPAN-DISJOINT? 0= IF 2DROP 0 EXIT THEN
    DUP _PBTW.PSTORE-WORK @ >R
    DUP PBTREE-WORK-SIZE R@ PSTORE-WORK-SPAN-DISJOINT? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    OVER PBTREE-SIZE R@ PSTORE-WORK-SPAN-DISJOINT? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    2DROP R> DROP -1 ;

: _PBT-BEGIN-OP  ( root tree work -- status )
    >R
    DUP R@ _PBT-WORK-BOUND? 0= IF 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    R@ _PBTW.BUSY @ IF 2DROP R> DROP PERSIST-S-BUSY EXIT THEN
    2DUP PBTREE-ROOT-VALID? 0= IF 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    OVER PBTREE-ROOT-SIZE R@ PBTREE-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    OVER PBTREE-ROOT-SIZE 2 PICK PBTREE-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    OVER PBTREE-ROOT-SIZE 2 PICK _PBTD.STORE @
        PSTORE-SPAN-DISJOINT? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    OVER PBTREE-ROOT-SIZE R@ _PBTW.PSTORE-WORK @
        PSTORE-WORK-SPAN-DISJOINT? 0= IF
        2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DUP R@ _PBTW.TREE !
    OVER R@ _PBTW.ROOT !
    PBTREE-WORK-SIZE OVER _PBTD.WORKING-BYTES @ MAX
        OVER _PBTD.WORKING-BYTES !
    PERSIST-S-OK OVER _PBTD.STATUS !
    PERSIST-S-OK R@ _PBTW.STATUS !
    0 R@ _PBTW.CACHE-VALID !
    0 R@ _PBTW.ALLOC-COUNT !
    -1 R@ _PBTW.BUSY !
    2DROP R> DROP PERSIST-S-OK ;

: _PBT-END-OP  ( status work -- status )
    >R DUP R@ _PBTW.STATUS !
    DUP R@ _PBTW.TREE @ ?DUP IF
        DUP PBTREE-VALID? IF _PBTD.STATUS ! ELSE 2DROP THEN
    ELSE
        DROP
    THEN
    0 R@ _PBTW.BUSY !
    R> DROP ;

: _PBT-WORK-ENTRY-STATUS  ( work -- status )
    DUP PBTREE-WORK-VALID? 0= IF DROP PERSIST-S-INVALID EXIT THEN
    _PBTW.BUSY @ IF PERSIST-S-BUSY ELSE PERSIST-S-OK THEN ;

\ ---------------------------------------------------------------------
\ Structural validation and page I/O
\ ---------------------------------------------------------------------

: _PBT-KEY-COMPARE  ( a u b v tree -- n )
    >R COMPARE R> _PBT-NOTE-COMPARE ;

: _PBT-KEY-LENGTH?  ( u -- flag )
    DUP 0> SWAP PBTREE-KEY-MAX <= AND ;

: _PBT-NODE-KEY$  ( index node -- key-a key-u )
    DUP _PBTN.KIND @ _PBTN-LEAF = IF _PBTL-KEY$ ELSE _PBTB-KEY$ THEN ;

: _PBT-NODE-COUNT-VALID?  ( node -- flag )
    DUP _PBTN.COUNT @ DUP 0< IF 2DROP 0 EXIT THEN
    SWAP _PBTN.KIND @ _PBTN-LEAF = IF
        DUP 0> SWAP PBTREE-LEAF-CAPACITY <= AND
    ELSE
        DUP 0> SWAP PBTREE-BRANCH-CAPACITY <= AND
    THEN ;

: _PBT-PAD-ZERO?  ( base used capacity -- flag )
    >R
    DUP R@ > IF 2DROP R> DROP 0 EXIT THEN
    2DUP + R> ROT - _PBT-ZERO? NIP ;

: _PBT-LEAF-SLOT-CANONICAL?  ( entry -- flag )
    DUP _PBTL-KEY-OFF + OVER _PBTL-KEY-U-OFF + @ PBTREE-KEY-MAX
        _PBT-PAD-ZERO? >R
    DUP _PBTL-VALUE-OFF + SWAP _PBTL-VALUE-U-OFF + @ PBTREE-VALUE-MAX
        _PBT-PAD-ZERO?
    R> AND ;

: _PBT-BRANCH-SLOT-CANONICAL?  ( entry -- flag )
    DUP _PBTB-KEY-OFF + SWAP _PBTB-KEY-U-OFF + @ PBTREE-KEY-MAX
        _PBT-PAD-ZERO? ;

: _PBT-NODE-UNUSED-ZERO?  ( node -- flag )
    DUP _PBTN.KIND @ _PBTN-LEAF = IF
        DUP _PBTN.COUNT @ _PBTL-ENTRY-SIZE * _PBTN-ENTRIES-OFF + OVER +
        SWAP _PBTN.COUNT @ _PBTL-ENTRY-SIZE * _PBTN-ENTRIES-OFF +
        PERSIST-PAGE-PAYLOAD-SIZE SWAP -
    ELSE
        DUP _PBTN.COUNT @ _PBTB-ENTRY-SIZE * _PBTN-ENTRIES-OFF + OVER +
        SWAP _PBTN.COUNT @ _PBTB-ENTRY-SIZE * _PBTN-ENTRIES-OFF +
        PERSIST-PAGE-PAYLOAD-SIZE SWAP -
    THEN
    _PBT-ZERO? ;

: _PBT-NODE-SLOTS-VALID?  ( node work -- flag )
    OVER _PBTN.COUNT @ 0 ?DO
        I 2 PICK _PBT-NODE-KEY$ NIP _PBT-KEY-LENGTH? 0= IF
            2DROP 0 UNLOOP EXIT
        THEN
        OVER _PBTN.KIND @ _PBTN-LEAF = IF
            I 2 PICK _PBTL-ENTRY DUP _PBTL-VALUE-U-OFF + @
            DUP 0< SWAP PBTREE-VALUE-MAX > OR IF
                DROP 2DROP 0 UNLOOP EXIT
            THEN
            _PBT-LEAF-SLOT-CANONICAL? 0= IF 2DROP 0 UNLOOP EXIT THEN
        ELSE
            I 2 PICK _PBTB-CHILD@ 0< IF 2DROP 0 UNLOOP EXIT THEN
            I 2 PICK _PBTB-ENTRY _PBT-BRANCH-SLOT-CANONICAL? 0= IF
                2DROP 0 UNLOOP EXIT
            THEN
        THEN
        I 0> IF
            I 1- 2 PICK _PBT-NODE-KEY$
            I 4 PICK _PBT-NODE-KEY$
            4 PICK _PBTW.TREE @ _PBT-KEY-COMPARE 0< 0= IF
                2DROP 0 UNLOOP EXIT
            THEN
        THEN
    LOOP
    OVER _PBT-NODE-UNUSED-ZERO? >R 2DROP R> ;

: _PBT-NODE-VALID?  ( node expected-height work -- flag )
    >R
    OVER _PBTN.MAGIC @ _PBTN-MAGIC <> IF 2DROP R> DROP 0 EXIT THEN
    OVER _PBTN.HEIGHT @ OVER <> IF 2DROP R> DROP 0 EXIT THEN
    DUP DUP 0> SWAP PBTREE-HEIGHT-MAX <= AND 0= IF 2DROP R> DROP 0 EXIT THEN
    1 = IF
        DUP _PBTN.KIND @ _PBTN-LEAF <> IF DROP R> DROP 0 EXIT THEN
    ELSE
        DUP _PBTN.KIND @ _PBTN-BRANCH <> IF DROP R> DROP 0 EXIT THEN
    THEN
    DUP _PBTN.SCOPE @ R@ _PBTW.TREE @ _PBTD.SCOPE @ <> IF
        DROP R> DROP 0 EXIT
    THEN
    DUP _PBTN.RESERVED 24 _PBT-ZERO? 0= IF DROP R> DROP 0 EXIT THEN
    DUP _PBT-NODE-COUNT-VALID? 0= IF DROP R> DROP 0 EXIT THEN
    R@ _PBT-NODE-SLOTS-VALID? R> DROP ;

: _PBT-CACHE-MATCH?  ( page-id work -- flag )
    >R
    R@ _PBTW.CACHE-VALID @ 0= IF DROP R> DROP 0 EXIT THEN
    R@ _PBTW.CACHE-PAGE @ <> IF R> DROP 0 EXIT THEN
    R@ _PBTW.CACHE-SCOPE @ R@ _PBTW.TREE @ _PBTD.SCOPE @ =
    R@ _PBTW.CACHE-GENERATION @
        R@ _PBTW.TREE @ _PBTD.STORE @ PSTORE-GENERATION@ = AND
    R> DROP ;

: _PBT-CACHE-CURRENT  ( page-id work -- )
    >R
    R@ _PBTW.CACHE-PAGE !
    R@ _PBTW.TREE @ _PBTD.SCOPE @ R@ _PBTW.CACHE-SCOPE !
    R@ _PBTW.TREE @ _PBTD.STORE @ PSTORE-GENERATION@
        R@ _PBTW.CACHE-GENERATION !
    -1 R@ _PBTW.CACHE-VALID !
    R> DROP ;

: _PBT-COPY-PSTORE-PAGE  ( page-id work -- status )
    >R
    R@ _PBTW.PSTORE-WORK @ PSTORE-PAGE-PAYLOAD$
    DUP PERSIST-PAGE-PAYLOAD-SIZE <> IF
        2DROP DROP R> DROP PERSIST-S-CORRUPT EXIT
    THEN
    DROP R@ _PBTW.BUF0 PERSIST-PAGE-PAYLOAD-SIZE MOVE
    R@ _PBTW.TREE @ _PBT-NOTE-READ
    R@ _PBT-CACHE-CURRENT
    R> DROP PERSIST-S-OK ;

: _PBT-READ  ( page-id work -- status )
    2DUP _PBT-CACHE-MATCH? IF 2DROP PERSIST-S-OK EXIT THEN
    >R DUP R@ _PBTW.TREE @ _PBTD.STORE @
        R@ _PBTW.PSTORE-WORK @ PSTORE-READ-PAGE
    DUP IF NIP R> DROP EXIT THEN DROP
    R@ _PBT-COPY-PSTORE-PAGE R> DROP ;

: _PBT-READ-TX  ( page-id work -- status )
    2DUP _PBT-CACHE-MATCH? IF 2DROP PERSIST-S-OK EXIT THEN
    >R DUP R@ _PBTW.TREE @ _PBTD.STORE @
        R@ _PBTW.PSTORE-WORK @ PSTORE-READ-PAGE-TX
    DUP IF NIP R> DROP EXIT THEN DROP
    R@ _PBT-COPY-PSTORE-PAGE R> DROP ;

: PBTREE-HIGH-WATER-ALLOCATE
  ( allocator-context store pstore-work -- page-id status )
    PSTORE-PROPOSED-ROOT@ DUP 0= IF
        DROP 2DROP -1 PERSIST-S-CONFLICT EXIT
    THEN
    PROOTV.PAGE-COUNT @ >R 2DROP R> PERSIST-S-OK ;

: _PBT-ALLOCATE-RUN  ( work -- page-id status )
    >R
    R@ _PBTW.TREE @ DUP _PBTD.ALLOCATOR-XT @ R@ _PBTW.TEMP0 !
    DUP _PBTD.ALLOCATOR-CONTEXT @
    SWAP _PBTD.STORE @
    R@ _PBTW.PSTORE-WORK @
    R@ _PBTW.TEMP0 @ EXECUTE
    R> DROP ;

: _PBT-ALLOCATE-CALL  ( work -- page-id status )
    DUP ['] _PBT-ALLOCATE-RUN CATCH
    DUP IF
        2DROP DROP -1 PERSIST-S-FAULT
    ELSE
        DROP ROT DROP
    THEN ;

: _PBT-ALLOCATED?  ( page-id work -- flag )
    DUP _PBTW.ALLOC-COUNT @ 0 ?DO
        I 1 PICK _PBTW-ALLOCATED @ 2 PICK = IF
            2DROP -1 UNLOOP EXIT
        THEN
    LOOP
    2DROP 0 ;

: _PBT-RECORD-ALLOCATION  ( page-id work -- )
    >R
    R@ _PBTW.ALLOC-COUNT @
    R@ _PBTW-ALLOCATED !
    1 R@ _PBTW.ALLOC-COUNT +!
    R> DROP ;

: _PBT-RECORD-RETIREMENT  ( page-id work -- status )
    OVER 0< IF 2DROP PERSIST-S-INVALID EXIT THEN
    DUP _PBTW.RETIRED-COUNT @ 0 ?DO
        I 1 PICK _PBTW-RETIRED @ 2 PICK = IF
            2DROP PERSIST-S-OK UNLOOP EXIT
        THEN
    LOOP
    DUP _PBTW.RETIRED-COUNT @ PBTREE-RETIREMENT-MAX >= IF
        2DROP PERSIST-S-CAPACITY EXIT
    THEN
    DUP _PBTW.RETIRED-COUNT @ OVER _PBTW-RETIRED
    2 PICK SWAP !
    1 OVER _PBTW.RETIRED-COUNT +!
    2DROP PERSIST-S-OK ;

: _PBT-WRITE  ( node work -- page-id status )
    >R
    R@ _PBTW.ALLOC-COUNT @ PBTREE-ALLOCATION-MAX >= IF
        DROP -1 PERSIST-S-CAPACITY R> DROP EXIT
    THEN
    R@ _PBT-ALLOCATE-CALL
    DUP IF >R 2DROP -1 R> R> DROP EXIT THEN DROP
    DUP 0< IF 2DROP -1 PERSIST-S-INVALID R> DROP EXIT THEN
    DUP R@ _PBTW.PSTORE-WORK @ PSTORE-PROPOSED-ROOT@
        PROOTV.PAGE-COUNT @ > IF
        2DROP -1 PERSIST-S-NOT-FOUND R> DROP EXIT
    THEN
    DUP R@ _PBT-ALLOCATED? IF
        2DROP -1 PERSIST-S-CONFLICT R> DROP EXIT
    THEN
    DUP R@ _PBT-RECORD-ALLOCATION
    DUP R@ _PBTW.TEMP1 ! DROP
    PERSIST-PAGE-PAYLOAD-SIZE R@ _PBTW.TEMP1 @
        R@ _PBTW.TREE @ _PBTD.STORE @
        R@ _PBTW.PSTORE-WORK @ PSTORE-WRITE-PAGE-TX
    DUP IF -1 SWAP R> DROP EXIT THEN DROP
    R@ _PBTW.TREE @ _PBT-NOTE-WRITE
    0 R@ _PBTW.CACHE-VALID !
    R@ _PBTW.TEMP1 @ PERSIST-S-OK R> DROP ;

