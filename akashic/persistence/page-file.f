0x50504746494C4531 CONSTANT _PPAGE-FILE-MAGIC

  0 CONSTANT _PPG-MAGIC
  8 CONSTANT _PPG-SELF
 16 CONSTANT _PPG-VFS
 24 CONSTANT _PPG-STATS
 32 CONSTANT _PPG-CACHE
 40 CONSTANT _PPG-PATH-U
 48 CONSTANT _PPG-PATH
304 CONSTANT _PPG-SPEC
CREC-SPEC-SIZE _PPG-SPEC + CONSTANT PERSIST-PAGE-FILE-SIZE

: _PPG.MAGIC   ( file -- a ) _PPG-MAGIC + ;
: _PPG.SELF    ( file -- a ) _PPG-SELF + ;
: PPAGE.VFS    ( file -- a ) _PPG-VFS + ;
: PPAGE.STATS  ( file -- a ) _PPG-STATS + ;
: PPAGE.CACHE  ( file -- a ) _PPG-CACHE + ;
: _PPG.PATH-U  ( file -- a ) _PPG-PATH-U + ;
: _PPG.PATH    ( file -- a ) _PPG-PATH + ;
: _PPG.SPEC    ( file -- a ) _PPG-SPEC + ;

: _PERSIST-ABSOLUTE-PATH?  ( path-a path-u -- flag )
    DUP 0> 0= IF 2DROP 0 EXIT THEN
    DUP PERSIST-PATH-MAX > IF 2DROP 0 EXIT THEN
    OVER 0= IF 2DROP 0 EXIT THEN
    2DUP MSPAN-NONWRAPPING? 0= IF 2DROP 0 EXIT THEN
    DROP C@ [CHAR] / = ;

: PPAGE-FILE-PATH$  ( file -- path-a path-u )
    DUP _PPG.PATH SWAP _PPG.PATH-U @ ;

: _PPAGE-ENCODER  ( context-a context-u payload-a payload-u tag -- status )
    >R
    DUP PERSIST-PAGE-PAYLOAD-SIZE <> IF
        R> DROP 2DROP 2DROP CREC-S-INVALID EXIT
    THEN DROP
    1 PICK PERSIST-PAGE-PAYLOAD-SIZE <> IF
        R> DROP 2DROP DROP CREC-S-INVALID EXIT
    THEN
    2 PICK 0= IF R> DROP 2DROP DROP CREC-S-INVALID EXIT THEN
    >R DROP R>
    PERSIST-PAGE-PAYLOAD-SIZE MOVE
    R> DROP CREC-S-OK ;

: _PPAGE-VALIDATOR  ( context-a context-u payload-a payload-u tag -- status )
    >R
    DUP PERSIST-PAGE-PAYLOAD-SIZE <> IF
        R> DROP 2DROP 2DROP CREC-S-SEMANTIC EXIT
    THEN
    2DROP OR IF R> DROP CREC-S-INVALID EXIT THEN
    R> 0> IF CREC-S-OK ELSE CREC-S-SEMANTIC THEN ;

: _PPAGE-CREC>STATUS  ( crec-status -- status )
    DUP CREC-S-OK = IF DROP PERSIST-S-OK EXIT THEN
    DUP CREC-S-CAPACITY = IF DROP PERSIST-S-CAPACITY EXIT THEN
    DUP CREC-S-BUSY = IF DROP PERSIST-S-BUSY EXIT THEN
    DUP CREC-S-INVALID = IF DROP PERSIST-S-INVALID EXIT THEN
    DUP CREC-S-CALLBACK = IF DROP PERSIST-S-FAULT EXIT THEN
    DUP CREC-S-FAULT = IF DROP PERSIST-S-FAULT EXIT THEN
    DROP PERSIST-S-CORRUPT ;

: PPAGE-FILE-VALID?  ( file -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP PERSIST-PAGE-FILE-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _PPG.MAGIC @ _PPAGE-FILE-MAGIC <> IF DROP 0 EXIT THEN
    DUP _PPG.SELF @ OVER <> IF DROP 0 EXIT THEN
    DUP PPAGE.VFS @ DUP 0= IF 2DROP 0 EXIT THEN
    VFS-DESC-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP PPAGE.STATS @ ?DUP IF PERSIST-STATS-VALID? 0= IF DROP 0 EXIT THEN THEN
    DUP PPAGE.CACHE @ ?DUP IF
        DUP PPAGE-CACHE-VALID? 0= IF 2DROP 0 EXIT THEN
        _PPC.OWNER @ OVER <> IF DROP 0 EXIT THEN
    THEN
    DUP PPAGE-FILE-PATH$ _PERSIST-ABSOLUTE-PATH? 0= IF DROP 0 EXIT THEN
    _PPG.SPEC CREC-SPEC-VALID? ;

: PPAGE-FILE-INIT  ( path-a path-u vfs stats cache file -- status )
    >R
    R@ 0= IF _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT THEN
    R@ PERSIST-PAGE-FILE-SIZE MSPAN-NONWRAPPING? 0= IF
        _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
    THEN
    4 PICK 4 PICK _PERSIST-ABSOLUTE-PATH? 0= IF
        _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
    THEN
    2 PICK 0= IF _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT THEN
    2 PICK VFS-DESC-SIZE MSPAN-NONWRAPPING? 0= IF
        _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
    THEN
    1 PICK ?DUP IF PERSIST-STATS-VALID? 0= IF
        _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
    THEN THEN
    DUP ?DUP IF PPAGE-CACHE-VALID? 0= IF
        _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
    THEN THEN
    4 PICK 4 PICK R@ PERSIST-PAGE-FILE-SIZE MSPAN-OVERLAP? IF
        _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
    THEN
    2 PICK VFS-DESC-SIZE R@ PERSIST-PAGE-FILE-SIZE MSPAN-OVERLAP? IF
        _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
    THEN
    1 PICK ?DUP IF
        PERSIST-STATS-SIZE R@ PERSIST-PAGE-FILE-SIZE MSPAN-OVERLAP? IF
            _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
        THEN
    THEN
    1 PICK IF
        2 PICK VFS-DESC-SIZE 3 PICK PERSIST-STATS-SIZE
            MSPAN-OVERLAP? IF
            _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
        THEN
    THEN
    DUP IF
        2 PICK VFS-DESC-SIZE 2 PICK PERSIST-PAGE-CACHE-SIZE
            MSPAN-OVERLAP? IF
            _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
        THEN
        1 PICK IF
            1 PICK PERSIST-STATS-SIZE 2 PICK PERSIST-PAGE-CACHE-SIZE
                MSPAN-OVERLAP? IF
                _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
            THEN
        THEN
        DUP _PPC.OWNER @ ?DUP IF R@ <> IF
            _PPAGE-DROP5 R> DROP PERSIST-S-CONFLICT EXIT
        THEN THEN
        DUP PERSIST-PAGE-CACHE-SIZE R@ PERSIST-PAGE-FILE-SIZE
            MSPAN-OVERLAP? IF
            _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
        THEN
        DUP _PPC.MEMORY @ OVER _PPC.BYTES @
            R@ PERSIST-PAGE-FILE-SIZE MSPAN-OVERLAP? IF
            _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
        THEN
        DUP _PPC.MEMORY @ OVER _PPC.BYTES @
            4 PICK VFS-DESC-SIZE MSPAN-OVERLAP? IF
            _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
        THEN
        1 PICK IF
            1 PICK PERSIST-STATS-SIZE 2 PICK _PPC.MEMORY @
                3 PICK _PPC.BYTES @ MSPAN-OVERLAP? IF
                _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
            THEN
        THEN
    THEN
    R@ PERSIST-PAGE-FILE-SIZE 0 FILL
    _PPAGE-FILE-MAGIC R@ _PPG.MAGIC !
    R@ R@ _PPG.SELF !
    2 PICK R@ PPAGE.VFS !
    1 PICK R@ PPAGE.STATS !
    DUP R@ PPAGE.CACHE !
    4 PICK 4 PICK R@ _PPG.PATH SWAP MOVE
    3 PICK R@ _PPG.PATH-U !
    _PPAGE-RECORD-MAGIC 8 PERSIST-PAGE-FORMAT CREC-TAG-POSITIVE
        ['] _PPAGE-ENCODER ['] _PPAGE-VALIDATOR R@ _PPG.SPEC
        CREC-SPEC-INIT DUP IF
        _PPAGE-CREC>STATUS >R _PPAGE-DROP5 R> R> DROP EXIT
    THEN DROP
    PERSIST-PAGE-PAYLOAD-SIZE PERSIST-PAGE-SIZE R@ _PPG.SPEC
        CREC-SPEC-FIXED! DUP IF
        _PPAGE-CREC>STATUS >R _PPAGE-DROP5 R> R> DROP EXIT
    THEN DROP
    R@ _PPG.SPEC CREC-SPEC-SEAL _PPAGE-CREC>STATUS
    DUP PERSIST-S-OK = IF
        1 PICK ?DUP IF
            DUP PPAGE-CACHE-INVALIDATE DROP
            R@ SWAP _PPC.OWNER !
        THEN
    THEN
    >R _PPAGE-DROP5 R> R> DROP ;

