: _PPAGE-DROP4  ( x1 x2 x3 x4 -- ) 2DROP 2DROP ;
: _PPAGE-DROP5  ( x1 x2 x3 x4 x5 -- ) 2DROP 2DROP DROP ;
: _PPAGE-DROP3  ( x1 x2 x3 -- ) 2DROP DROP ;

