0x5050434143484531 CONSTANT _PPAGE-CACHE-MAGIC

 0 CONSTANT _PPC-MAGIC
 8 CONSTANT _PPC-SELF
16 CONSTANT _PPC-MEMORY
24 CONSTANT _PPC-BYTES
32 CONSTANT _PPC-SLOTS
40 CONSTANT _PPC-OWNER
48 CONSTANT PERSIST-PAGE-CACHE-SIZE

 0 CONSTANT _PPF-VALID
 8 CONSTANT _PPF-PAGE-ID
16 CONSTANT _PPF-PAYLOAD
PERSIST-PAGE-PAYLOAD-SIZE _PPF-PAYLOAD +
    CONSTANT PERSIST-PAGE-CACHE-FRAME-SIZE

: _PPC.MAGIC   ( cache -- a ) _PPC-MAGIC + ;
: _PPC.SELF    ( cache -- a ) _PPC-SELF + ;
: _PPC.MEMORY  ( cache -- a ) _PPC-MEMORY + ;
: _PPC.BYTES   ( cache -- a ) _PPC-BYTES + ;
: _PPC.SLOTS   ( cache -- a ) _PPC-SLOTS + ;
: _PPC.OWNER   ( cache -- a ) _PPC-OWNER + ;

: PPAGE-CACHE-BYTES  ( slots -- bytes status )
    DUP 0> 0= IF DROP 0 PERSIST-S-INVALID EXIT THEN
    DUP PERSIST-MAX-SIGNED PERSIST-PAGE-CACHE-FRAME-SIZE / > IF
        DROP 0 PERSIST-S-CAPACITY EXIT
    THEN
    PERSIST-PAGE-CACHE-FRAME-SIZE * PERSIST-S-OK ;

: PPAGE-CACHE-VALID?  ( cache -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP PERSIST-PAGE-CACHE-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _PPC.MAGIC @ _PPAGE-CACHE-MAGIC <> IF DROP 0 EXIT THEN
    DUP _PPC.SELF @ OVER <> IF DROP 0 EXIT THEN
    DUP _PPC.MEMORY @ DUP 0= IF 2DROP 0 EXIT THEN
    OVER _PPC.BYTES @ MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _PPC.SLOTS @ PPAGE-CACHE-BYTES DUP IF
        DROP 2DROP 0 EXIT
    THEN
    DROP SWAP _PPC.BYTES @ = ;

: PPAGE-CACHE-INIT  ( memory bytes slots cache -- status )
    >R
    R@ 0= IF 2DROP DROP R> DROP PERSIST-S-INVALID EXIT THEN
    R@ PERSIST-PAGE-CACHE-SIZE MSPAN-NONWRAPPING? 0= IF
        2DROP DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    DUP PPAGE-CACHE-BYTES DUP IF
        >R DROP 2DROP DROP R> R> DROP EXIT
    THEN
    DROP                         ( memory bytes slots required ; R: cache )
    2 PICK OVER < IF
        2DROP 2DROP R> DROP PERSIST-S-CAPACITY EXIT
    THEN
    3 PICK 0= IF 2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    3 PICK 3 PICK MSPAN-NONWRAPPING? 0= IF
        2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    3 PICK OVER R@ PERSIST-PAGE-CACHE-SIZE MSPAN-OVERLAP? IF
        2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ PERSIST-PAGE-CACHE-SIZE 0 FILL
    _PPAGE-CACHE-MAGIC R@ _PPC.MAGIC !
    R@ R@ _PPC.SELF !
    3 PICK R@ _PPC.MEMORY !
    DUP R@ _PPC.BYTES !
    1 PICK R@ _PPC.SLOTS !
    3 PICK SWAP 0 FILL
    2DROP DROP R> DROP PERSIST-S-OK ;

: PPAGE-CACHE-INVALIDATE  ( cache -- status )
    DUP PPAGE-CACHE-VALID? 0= IF DROP PERSIST-S-INVALID EXIT THEN
    DUP _PPC.MEMORY @ SWAP _PPC.BYTES @ 0 FILL PERSIST-S-OK ;

: _PPAGE-CACHE-FRAME  ( page-id cache -- frame )
    DUP _PPC.SLOTS @ ROT SWAP MOD
    PERSIST-PAGE-CACHE-FRAME-SIZE * SWAP _PPC.MEMORY @ + ;

: _PPAGE-CACHE-FIND  ( page-id cache -- payload|0 )
    OVER >R _PPAGE-CACHE-FRAME
    DUP _PPF-VALID + @ 0= IF DROP R> DROP 0 EXIT THEN
    DUP _PPF-PAGE-ID + @ R> <> IF DROP 0 EXIT THEN
    _PPF-PAYLOAD + ;

: _PPAGE-CACHE-PUT  ( payload page-id cache -- )
    OVER >R _PPAGE-CACHE-FRAME >R
    R@ _PPF-PAYLOAD + PERSIST-PAGE-PAYLOAD-SIZE MOVE
    R> R> OVER _PPF-PAGE-ID + !
    -1 SWAP _PPF-VALID + ! ;

