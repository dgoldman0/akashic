: _PPAGE-ENSURE-RUN  ( work -- status )
    >R
    VFS-FF-READ VFS-FF-WRITE OR R@ _PPAGE-OPEN
    DUP PERSIST-S-OK = IF
        DROP DROP PERSIST-S-OK R@ _PPAGE-CLOSE
        R> DROP EXIT
    THEN
    DUP PERSIST-S-ABSENT <> IF
        NIP R> DROP EXIT
    THEN
    2DROP
    R@ _PPW.FILE @ PPAGE-FILE-PATH$
        R@ _PPW.FILE @ PPAGE.VFS @ VFS-CREATE 0= IF
        R> DROP PERSIST-S-IO EXIT
    THEN
    VFS-FF-READ VFS-FF-WRITE OR R@ _PPAGE-OPEN DUP IF
        NIP R> DROP EXIT
    THEN DROP DROP
    PERSIST-S-OK R@ _PPAGE-CLOSE R> DROP ;

: PPAGE-ENSURE  ( file work -- status )
    >R
    DUP R@ _PPAGE-FILE-WORK? 0= IF DROP R> DROP PERSIST-S-INVALID EXIT THEN
    R@ _PPW.BUSY @ IF DROP R> DROP PERSIST-S-BUSY EXIT THEN
    R@ _PPW.FILE !
    -1 R@ _PPW.BUSY ! PERSIST-S-INVALID R@ _PPW.STATUS !
    R@ ['] _PPAGE-ENSURE-RUN _PPAGE-RUN-CATCH
    DUP R@ _PPW.STATUS ! 0 R@ _PPW.BUSY ! R> DROP ;

: _PPAGE-SIZE-RUN  ( work -- status )
    >R
    VFS-FF-READ R@ _PPAGE-OPEN DUP IF
        NIP R> DROP EXIT
    THEN
    DROP VFA-FILE-SIZE? DUP IF
        DROP DROP PERSIST-S-IO R@ _PPAGE-CLOSE
        R> DROP EXIT
    THEN DROP
    R@ _PPW.RECORD-U !
    PERSIST-S-OK R@ _PPAGE-CLOSE
    R> DROP ;

: PPAGE-FILE-SIZE?  ( file work -- bytes status )
    >R
    DUP R@ _PPAGE-FILE-WORK? 0= IF DROP R> DROP 0 PERSIST-S-INVALID EXIT THEN
    R@ _PPW.BUSY @ IF DROP R> DROP 0 PERSIST-S-BUSY EXIT THEN
    R@ _PPW.FILE !
    -1 R@ _PPW.BUSY ! PERSIST-S-INVALID R@ _PPW.STATUS !
    0 R@ _PPW.RECORD-U !
    R@ ['] _PPAGE-SIZE-RUN _PPAGE-RUN-CATCH
    DUP R@ _PPW.STATUS ! 0 R@ _PPW.BUSY !
    DUP IF 0 SWAP ELSE R@ _PPW.RECORD-U @ SWAP THEN
    R> DROP ;

: _PPAGE-TRUNCATE-RUN  ( work -- status )
    >R
    R@ _PPW.COMMITTED @ _PPAGE-COUNT>SIZE? 0= IF
        DROP R> DROP PERSIST-S-CAPACITY EXIT
    THEN R@ _PPW.RECORD-U !
    VFS-FF-READ VFS-FF-WRITE OR R@ _PPAGE-OPEN DUP IF
        NIP R> DROP EXIT
    THEN DROP
    VFA-FILE-SIZE? DUP IF
        DROP DROP PERSIST-S-IO R@ _PPAGE-CLOSE
        R> DROP EXIT
    THEN DROP
    DUP PERSIST-PAGE-SIZE MOD IF
        DROP PERSIST-S-CORRUPT R@ _PPAGE-CLOSE
        R> DROP EXIT
    THEN
    R@ _PPW.RECORD-U @ OVER > IF
        DROP PERSIST-S-CORRUPT R@ _PPAGE-CLOSE
        R> DROP EXIT
    THEN DROP
    R@ _PPW.RECORD-U @ R@ _PPW.FD @ VFS-TRUNCATE IF
        PERSIST-S-IO R@ _PPAGE-CLOSE
        R> DROP EXIT
    THEN
    R@ _PPW.FD @ VFA-FILE-SIZE? DUP IF
        2DROP PERSIST-S-IO R@ _PPAGE-CLOSE R> DROP EXIT
    THEN
    DROP R@ _PPW.RECORD-U @ <> IF
        PERSIST-S-IO R@ _PPAGE-CLOSE R> DROP EXIT
    THEN
    PERSIST-S-OK R@ _PPAGE-CLOSE
    DUP PERSIST-S-OK = IF
        R@ _PPW.FILE @ PPAGE.CACHE @ ?DUP IF
            PPAGE-CACHE-INVALIDATE DROP
        THEN
    THEN
    R> DROP ;

: PPAGE-TRUNCATE  ( committed-count file work -- status )
    >R
    DUP R@ _PPAGE-FILE-WORK? 0= IF 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    R@ _PPW.BUSY @ IF 2DROP R> DROP PERSIST-S-BUSY EXIT THEN
    R@ _PPW.FILE ! R@ _PPW.COMMITTED !
    -1 R@ _PPW.BUSY ! PERSIST-S-INVALID R@ _PPW.STATUS !
    R@ ['] _PPAGE-TRUNCATE-RUN _PPAGE-RUN-CATCH
    DUP R@ _PPW.STATUS ! 0 R@ _PPW.BUSY ! R> DROP ;

: _PPAGE-SYNC-RUN  ( file -- status )
    DUP PPAGE.VFS @ VFS-SYNC IF DROP PERSIST-S-IO EXIT THEN
    PPAGE.STATS @ ?DUP IF _PSTAT-NOTE-SYNC THEN
    PERSIST-S-OK ;

: PPAGE-SYNC  ( file -- status )
    DUP PPAGE-FILE-VALID? 0= IF DROP PERSIST-S-INVALID EXIT THEN
    ['] _PPAGE-SYNC-RUN CATCH
    DUP IF 2DROP PERSIST-S-FAULT ELSE DROP THEN ;
