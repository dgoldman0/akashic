: PPAGE-WORK-VALID?  ( work -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP PERSIST-PAGE-WORK-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _PPW.MAGIC @ _PPAGE-WORK-MAGIC <> IF DROP 0 EXIT THEN
    DUP _PPW.SELF @ OVER <> IF DROP 0 EXIT THEN
    DUP _PPW.BUSY @ DUP 0= SWAP -1 = OR 0= IF DROP 0 EXIT THEN
    DUP _PPW.STATUS @ DUP PERSIST-S-OK <
        SWAP PERSIST-S-FAULT > OR IF DROP 0 EXIT THEN
    _PPW.CREC-WORK CREC-WORK-VALID? ;

: PPAGE-WORK-INIT  ( work -- status )
    DUP 0= IF DROP PERSIST-S-INVALID EXIT THEN
    DUP PERSIST-PAGE-WORK-SIZE MSPAN-NONWRAPPING? 0= IF
        DROP PERSIST-S-INVALID EXIT
    THEN
    DUP PERSIST-PAGE-WORK-SIZE 0 FILL
    _PPAGE-WORK-MAGIC OVER _PPW.MAGIC !
    DUP OVER _PPW.SELF !
    DUP _PPW.CREC-WORK CREC-WORK-INIT _PPAGE-CREC>STATUS
    DUP PERSIST-S-OK = IF PERSIST-S-INVALID 2 PICK _PPW.STATUS ! THEN
    NIP ;

: _PPAGE-FILE-WORK?  ( file work -- flag )
    DUP PPAGE-WORK-VALID? 0= IF 2DROP 0 EXIT THEN
    OVER PPAGE-FILE-VALID? 0= IF 2DROP 0 EXIT THEN
    >R
    DUP PERSIST-PAGE-FILE-SIZE R@ PERSIST-PAGE-WORK-SIZE
        MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    DUP PPAGE.VFS @ VFS-DESC-SIZE R@ PERSIST-PAGE-WORK-SIZE
        MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    DUP PPAGE.STATS @ ?DUP IF
        PERSIST-STATS-SIZE R@ PERSIST-PAGE-WORK-SIZE
            MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    THEN
    DUP PPAGE.CACHE @ ?DUP IF
        DUP PERSIST-PAGE-CACHE-SIZE R@ PERSIST-PAGE-WORK-SIZE
            MSPAN-OVERLAP? IF 2DROP R> DROP 0 EXIT THEN
        DUP _PPC.MEMORY @ SWAP _PPC.BYTES @
            R@ PERSIST-PAGE-WORK-SIZE MSPAN-OVERLAP? IF
            DROP R> DROP 0 EXIT
        THEN
    THEN
    DROP R> DROP -1 ;

: PPAGE-PAYLOAD$  ( work -- payload-a payload-u )
    DUP PPAGE-WORK-VALID? 0= IF DROP 0 0 EXIT THEN
    DUP _PPW.STATUS @ PERSIST-S-OK <> IF DROP 0 0 EXIT THEN
    _PPW.RECORD PERSIST-RECORD-HEADER-SIZE +
    PERSIST-PAGE-PAYLOAD-SIZE ;

: PPAGE-LAST-STATUS@  ( work -- status )
    DUP PPAGE-WORK-VALID? 0= IF DROP PERSIST-S-INVALID EXIT THEN
    _PPW.STATUS @ ;

: _PPAGE-ID>OFFSET?  ( page-id -- offset flag )
    DUP 0< IF DROP 0 0 EXIT THEN
    DUP PERSIST-MAX-SIGNED PERSIST-PAGE-SIZE -
        PERSIST-PAGE-SIZE / > IF DROP 0 0 EXIT THEN
    PERSIST-PAGE-SIZE * -1 ;

: _PPAGE-COUNT>SIZE?  ( page-count -- bytes flag )
    DUP 0< IF DROP 0 0 EXIT THEN
    DUP PERSIST-MAX-SIGNED PERSIST-PAGE-SIZE / > IF DROP 0 0 EXIT THEN
    PERSIST-PAGE-SIZE * -1 ;

: _PPAGE-CLOSE  ( status work -- status )
    >R R@ _PPW.SCOPE VFA-SCOPE-CLOSE? DUP IF
        DROP DROP PERSIST-S-UNCERTAIN
    ELSE DROP THEN
    R> DROP ;

: _PPAGE-OPEN  ( flags work -- fd status )
    >R
    R@ _PPW.FILE @ PPAGE.VFS @ R@ _PPW.SCOPE VFA-SCOPE-INIT DUP IF
        2DROP R> DROP 0 PERSIST-S-IO EXIT
    THEN DROP
    R@ _PPW.FILE @ PPAGE-FILE-PATH$ ROT R@ _PPW.SCOPE VFA-SCOPE-OPEN?
    DUP IF
        R@ _PPW.SCOPE VFA-SCOPE-CLEANUP@ IF
            2DROP R> DROP 0 PERSIST-S-UNCERTAIN EXIT
        THEN
        DUP VFS-IOR-REASON VFS-R-NOENT = >R
        2DROP R> IF PERSIST-S-ABSENT ELSE PERSIST-S-IO THEN
        R> DROP 0 SWAP EXIT
    THEN
    DROP DUP R@ _PPW.FD ! PERSIST-S-OK R> DROP ;

\ Keep a work pointer below the CATCH frame.  If a binding throws after an
\ open or partial effect, the restored pointer lets us retire the exact owned
\ scope before the public wrapper clears BUSY.
: _PPAGE-RUN-CATCH  ( work body-xt -- status )
    >R DUP R> CATCH DUP IF
        DROP DROP
        DUP _PPW.SCOPE VFA-SCOPE-VALID? IF
            PERSIST-S-FAULT SWAP _PPAGE-CLOSE
        ELSE
            DROP PERSIST-S-FAULT
        THEN
    ELSE
        DROP NIP
    THEN ;

: _PPAGE-STATS  ( work -- stats|0 )
    _PPW.FILE @ PPAGE.STATS @ ;

: _PPAGE-VALIDATE-RECORD  ( work -- status )
    >R
    0 0 R@ _PPW.RECORD PERSIST-PAGE-SIZE
        R@ _PPW.FILE @ _PPG.SPEC R@ _PPW.CREC-WORK CREC-VALIDATE
        _PPAGE-CREC>STATUS DUP IF R> DROP EXIT THEN DROP
    R@ _PPW.CREC-WORK CREC-TAG@
        R@ _PPW.PAGE-ID @ 1+ <> IF R> DROP PERSIST-S-CORRUPT EXIT THEN
    R@ _PPAGE-STATS ?DUP IF _PSTAT-NOTE-VERIFY THEN
    R> DROP PERSIST-S-OK ;

: _PPAGE-READ-DISK  ( work -- status )
    >R
    VFS-FF-READ R@ _PPAGE-OPEN DUP IF
        NIP DUP PERSIST-S-ABSENT = IF DROP PERSIST-S-CORRUPT THEN
        R> DROP EXIT
    THEN
    DROP                              ( fd ; R: work )
    DUP VFA-FILE-SIZE? DUP IF
        DROP 2DROP PERSIST-S-IO R@ _PPAGE-CLOSE R> DROP EXIT
    THEN DROP                          ( fd size ; R: work )
    DUP PERSIST-PAGE-SIZE MOD IF
        2DROP PERSIST-S-CORRUPT R@ _PPAGE-CLOSE R> DROP EXIT
    THEN
    R@ _PPW.PAGE-ID @ _PPAGE-ID>OFFSET? 0= IF
        DROP 2DROP PERSIST-S-INVALID R@ _PPAGE-CLOSE R> DROP EXIT
    THEN
    DUP PERSIST-PAGE-SIZE + 2 PICK > IF
        _PPAGE-DROP3 PERSIST-S-CORRUPT R@ _PPAGE-CLOSE R> DROP EXIT
    THEN
    ROT >R NIP R>                     ( offset fd ; R: work )
    R@ _PPW.RECORD PERSIST-PAGE-SIZE 2SWAP VFA-READ-RANGE?
    DUP IF
        DROP PERSIST-S-IO R@ _PPAGE-CLOSE R> DROP EXIT
    THEN DROP
    PERSIST-PAGE-SIZE R@ _PPAGE-STATS ?DUP IF
        _PSTAT-NOTE-PAGE-READ
    ELSE DROP THEN
    PERSIST-S-OK R@ _PPAGE-CLOSE DUP IF R> DROP EXIT THEN DROP
    R@ _PPAGE-VALIDATE-RECORD R> DROP ;

: _PPAGE-CACHE-LOAD?  ( work -- hit? )
    >R
    R@ _PPW.FILE @ PPAGE.CACHE @ ?DUP 0= IF R> DROP 0 EXIT THEN
    R@ _PPW.PAGE-ID @ SWAP _PPAGE-CACHE-FIND ?DUP IF
        R@ _PPW.RECORD PERSIST-RECORD-HEADER-SIZE +
            PERSIST-PAGE-PAYLOAD-SIZE MOVE
        R@ _PPAGE-STATS ?DUP IF _PSTAT-NOTE-HIT THEN
        R> DROP -1 EXIT
    THEN
    R@ _PPAGE-STATS ?DUP IF _PSTAT-NOTE-MISS THEN
    R> DROP 0 ;

: _PPAGE-CACHE-INSTALL  ( work -- )
    >R
    R@ _PPW.FILE @ PPAGE.CACHE @ ?DUP IF
        R@ _PPW.RECORD PERSIST-RECORD-HEADER-SIZE +
        R@ _PPW.PAGE-ID @ ROT _PPAGE-CACHE-PUT
    THEN
    R> DROP ;

: _PPAGE-READ-RUN  ( work -- status )
    >R
    R@ _PPW.PAGE-ID @ DUP 0< IF DROP R> DROP PERSIST-S-INVALID EXIT THEN
    R@ _PPW.COMMITTED @ DUP 0< IF DROP DROP R> DROP PERSIST-S-INVALID EXIT THEN
    >= IF R> DROP PERSIST-S-NOT-FOUND EXIT THEN
    R@ _PPW.MODE @ 0= IF
        R@ _PPAGE-CACHE-LOAD? IF R> DROP PERSIST-S-OK EXIT THEN
    THEN
    R@ _PPAGE-READ-DISK DUP PERSIST-S-OK = IF R@ _PPAGE-CACHE-INSTALL THEN
    R> DROP ;

: _PPAGE-READ-COMMON  ( page-id committed mode file work -- status )
    >R
    DUP R@ _PPAGE-FILE-WORK? 0= IF 2DROP 2DROP R> DROP PERSIST-S-INVALID EXIT THEN
    R@ _PPW.BUSY @ IF 2DROP 2DROP R> DROP PERSIST-S-BUSY EXIT THEN
    R@ _PPW.FILE !
    R@ _PPW.MODE !
    R@ _PPW.COMMITTED !
    R@ _PPW.PAGE-ID !
    -1 R@ _PPW.BUSY !
    PERSIST-S-INVALID R@ _PPW.STATUS !
    R@ ['] _PPAGE-READ-RUN _PPAGE-RUN-CATCH
    DUP R@ _PPW.STATUS !
    0 R@ _PPW.BUSY !
    R> DROP ;

: PPAGE-READ  ( page-id committed-count file work -- status )
    >R 0 SWAP R> _PPAGE-READ-COMMON ;

: PPAGE-VERIFY  ( page-id committed-count file work -- status )
    >R 1 SWAP R> _PPAGE-READ-COMMON ;

: _PPAGE-WRITE-RUN  ( work -- status )
    >R
    R@ _PPW.SOURCE-U @ PERSIST-PAGE-PAYLOAD-SIZE <> IF
        R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PPW.SOURCE-A @ R@ _PPW.SOURCE-U @ MSPAN-NONWRAPPING? 0= IF
        R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PPW.SOURCE-A @ R@ _PPW.SOURCE-U @
        R@ PERSIST-PAGE-WORK-SIZE MSPAN-OVERLAP? IF
        R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PPW.SOURCE-A @ R@ _PPW.SOURCE-U @
        R@ _PPW.FILE @ PERSIST-PAGE-FILE-SIZE MSPAN-OVERLAP? IF
        R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PPW.SOURCE-A @ R@ _PPW.SOURCE-U @
        R@ _PPW.FILE @ PPAGE.VFS @ VFS-DESC-SIZE MSPAN-OVERLAP? IF
        R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PPW.FILE @ PPAGE.STATS @ ?DUP IF
        R@ _PPW.SOURCE-A @ R@ _PPW.SOURCE-U @ ROT
            PERSIST-STATS-SIZE MSPAN-OVERLAP? IF
            R> DROP PERSIST-S-INVALID EXIT
        THEN
    THEN
    R@ _PPW.FILE @ PPAGE.CACHE @ ?DUP IF
        DUP PERSIST-PAGE-CACHE-SIZE
            R@ _PPW.SOURCE-A @ R@ _PPW.SOURCE-U @ 2SWAP
            MSPAN-OVERLAP? IF DROP R> DROP PERSIST-S-INVALID EXIT THEN
        DUP _PPC.MEMORY @ SWAP _PPC.BYTES @
            R@ _PPW.SOURCE-A @ R@ _PPW.SOURCE-U @ 2SWAP
            MSPAN-OVERLAP? IF R> DROP PERSIST-S-INVALID EXIT THEN
    THEN
    R@ _PPW.PAGE-ID @ _PPAGE-ID>OFFSET? 0= IF
        DROP R> DROP PERSIST-S-CAPACITY EXIT
    THEN DROP
    R@ _PPW.MODE @ IF
        R@ _PPW.COMMITTED @ DUP 0< IF
            DROP R> DROP PERSIST-S-INVALID EXIT
        THEN
        R@ _PPW.PAGE-ID @ < IF R> DROP PERSIST-S-NOT-FOUND EXIT THEN
    THEN
    R@ _PPW.SOURCE-A @ R@ _PPW.SOURCE-U @
        PERSIST-PAGE-PAYLOAD-SIZE R@ _PPW.PAGE-ID @ 1+
        R@ _PPW.RECORD PERSIST-PAGE-SIZE R@ _PPW.FILE @ _PPG.SPEC
        R@ _PPW.CREC-WORK CREC-ENCODE
    DUP IF NIP _PPAGE-CREC>STATUS R> DROP EXIT THEN
    DROP R@ _PPW.RECORD-U !
    VFS-FF-READ VFS-FF-WRITE OR R@ _PPAGE-OPEN DUP IF
        NIP R> DROP EXIT
    THEN DROP                            ( fd ; R: work )
    DUP VFA-FILE-SIZE? DUP IF
        DROP 2DROP PERSIST-S-IO R@ _PPAGE-CLOSE R> DROP EXIT
    THEN DROP                            ( fd size )
    DUP PERSIST-PAGE-SIZE MOD IF
        2DROP PERSIST-S-CORRUPT R@ _PPAGE-CLOSE R> DROP EXIT
    THEN
    R@ _PPW.MODE @ IF
        R@ _PPW.COMMITTED @ _PPAGE-COUNT>SIZE?
    ELSE
        R@ _PPW.PAGE-ID @ _PPAGE-ID>OFFSET?
    THEN
    0= IF
        _PPAGE-DROP3 PERSIST-S-CAPACITY R@ _PPAGE-CLOSE R> DROP EXIT
    THEN
    <> IF
        DROP PERSIST-S-CONFLICT R@ _PPAGE-CLOSE R> DROP EXIT
    THEN                                 ( fd )
    R@ _PPW.PAGE-ID @ _PPAGE-ID>OFFSET? 0= IF
        2DROP PERSIST-S-CAPACITY R@ _PPAGE-CLOSE R> DROP EXIT
    THEN                                 ( fd offset )
    SWAP                                 ( offset fd )
    2DUP VFS-SEEK? DUP IF
        DROP 2DROP PERSIST-S-IO R@ _PPAGE-CLOSE R> DROP EXIT
    THEN DROP NIP                        ( fd )
    \ A rewrite can have partial effect even when exact-write later reports
    \ I/O failure.  Drop any pre-write cache entry before touching the slot.
    R@ _PPW.MODE @ IF
        R@ _PPW.PAGE-ID @ R@ _PPW.COMMITTED @ < IF
            R@ _PPW.FILE @ PPAGE.CACHE @ ?DUP IF
                PPAGE-CACHE-INVALIDATE DROP
            THEN
        THEN
    THEN
    R@ _PPW.RECORD PERSIST-PAGE-SIZE ROT VFS-WRITE-EXACT DUP IF
        DROP PERSIST-S-IO R@ _PPAGE-CLOSE R> DROP EXIT
    THEN DROP
    PERSIST-PAGE-SIZE R@ _PPAGE-STATS ?DUP IF
        _PSTAT-NOTE-PAGE-WRITE
    ELSE DROP THEN
    PERSIST-S-OK R@ _PPAGE-CLOSE
    R> DROP ;

: PPAGE-WRITE  ( payload-a payload-u page-id file work -- status )
    >R
    DUP R@ _PPAGE-FILE-WORK? 0= IF _PPAGE-DROP4 R> DROP PERSIST-S-INVALID EXIT THEN
    R@ _PPW.BUSY @ IF _PPAGE-DROP4 R> DROP PERSIST-S-BUSY EXIT THEN
    R@ _PPW.FILE !
    R@ _PPW.PAGE-ID !
    R@ _PPW.SOURCE-U !
    R@ _PPW.SOURCE-A !
    0 R@ _PPW.COMMITTED !
    0 R@ _PPW.MODE !
    -1 R@ _PPW.BUSY !
    PERSIST-S-INVALID R@ _PPW.STATUS !
    R@ ['] _PPAGE-WRITE-RUN _PPAGE-RUN-CATCH
    DUP R@ _PPW.STATUS !
    0 R@ _PPW.BUSY !
    R> DROP ;

\ Write either an existing slot below proposed-count or the exact append slot
\ equal to proposed-count.  The physical file must contain exactly that many
\ complete pages before the write; gaps and suffix disagreement are rejected.
: PPAGE-WRITE-AT
  ( payload-a payload-u page-id proposed-count file work -- status )
    >R
    DUP R@ _PPAGE-FILE-WORK? 0= IF
        _PPAGE-DROP5 R> DROP PERSIST-S-INVALID EXIT
    THEN
    R@ _PPW.BUSY @ IF _PPAGE-DROP5 R> DROP PERSIST-S-BUSY EXIT THEN
    R@ _PPW.FILE !
    R@ _PPW.COMMITTED !
    R@ _PPW.PAGE-ID !
    R@ _PPW.SOURCE-U !
    R@ _PPW.SOURCE-A !
    1 R@ _PPW.MODE !
    -1 R@ _PPW.BUSY !
    PERSIST-S-INVALID R@ _PPW.STATUS !
    R@ ['] _PPAGE-WRITE-RUN _PPAGE-RUN-CATCH
    DUP R@ _PPW.STATUS !
    0 R@ _PPW.BUSY !
    R> DROP ;

