_BFF-ITEMS BFM-MAX-ITEMS BFM-ITEM-SIZE * + CONSTANT BFM-FEED-SIZE

: BFM.FEED.COUNT  ( feed -- a ) _BFF-COUNT + ;
: BFM.FEED.CURSOR-U  ( feed -- a ) _BFF-CURSOR-U + ;
: BFM.FEED.CURSOR  ( feed -- a ) _BFF-CURSOR + ;
