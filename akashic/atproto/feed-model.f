: BFM.ITEM.CREATED  ( item -- a u ) DUP _BFI-CREATED + SWAP _BFI-CREATED-U + @ ;

: BFM-FEED-INIT  ( feed -- ) BFM-FEED-SIZE 0 FILL ;

VARIABLE _BFM-OA
VARIABLE _BFM-OU
VARIABLE _BFM-KA
VARIABLE _BFM-KU
VARIABLE _BFM-VA
VARIABLE _BFM-VU
VARIABLE _BFM-FOUND
VARIABLE _BFM-IOR

: _BFM-FIELD  ( object-a object-u key-a key-u -- value-a value-u found status )
    _BFM-KU ! _BFM-KA ! _BFM-OU ! _BFM-OA !
    _BFM-OA @ _BFM-OU @ _BFM-KA @ _BFM-KU @ JSON-FIELD
    _BFM-IOR ! _BFM-FOUND ! _BFM-VU ! _BFM-VA !
    _BFM-IOR @ IF 0 0 0 BFM-S-INVALID EXIT THEN
    _BFM-VA @ _BFM-VU @ _BFM-FOUND @ BFM-S-OK ;

VARIABLE _BFM-SA
VARIABLE _BFM-SU
VARIABLE _BFM-SD
VARIABLE _BFM-SMAX
VARIABLE _BFM-SLEN

: _BFM-COPY-STRING  ( value-a value-u destination max length-cell -- status )
    _BFM-SLEN ! _BFM-SMAX ! _BFM-SD ! _BFM-SU ! _BFM-SA !
    _BFM-SA @ _BFM-SU @ JSON-STRING? 0= IF BFM-S-TYPE EXIT THEN
    _BFM-SA @ _BFM-SU @ JSON-GET-STRING
    _BFM-SD @ _BFM-SMAX @ JSON-UNESCAPE
    JSON-OK? 0= IF DROP BFM-S-CAPACITY EXIT THEN
    DUP _BFM-SMAX @ > IF DROP BFM-S-CAPACITY EXIT THEN
    _BFM-SLEN @ ! BFM-S-OK ;

VARIABLE _BFM-ROA
VARIABLE _BFM-ROU
VARIABLE _BFM-RKA
VARIABLE _BFM-RKU
VARIABLE _BFM-RD
VARIABLE _BFM-RMAX
VARIABLE _BFM-RLEN

: _BFM-REQUIRED-STRING
    ( object-a object-u key-a key-u destination max length-cell -- status )
    _BFM-RLEN ! _BFM-RMAX ! _BFM-RD !
    _BFM-RKU ! _BFM-RKA ! _BFM-ROU ! _BFM-ROA !
    _BFM-ROA @ _BFM-ROU @ _BFM-RKA @ _BFM-RKU @ _BFM-FIELD
    DUP IF >R 2DROP DROP R> EXIT THEN DROP
    0= IF 2DROP BFM-S-MISSING EXIT THEN
    _BFM-RD @ _BFM-RMAX @ _BFM-RLEN @ _BFM-COPY-STRING ;

VARIABLE _BFM-OOA
VARIABLE _BFM-OOU
VARIABLE _BFM-OKA
VARIABLE _BFM-OKU
VARIABLE _BFM-OD
VARIABLE _BFM-OMAX
VARIABLE _BFM-OLEN

: _BFM-OPTIONAL-STRING
    ( object-a object-u key-a key-u destination max length-cell -- status )
    _BFM-OLEN ! _BFM-OMAX ! _BFM-OD !
    _BFM-OKU ! _BFM-OKA ! _BFM-OOU ! _BFM-OOA !
    0 _BFM-OLEN @ !
    _BFM-OOA @ _BFM-OOU @ _BFM-OKA @ _BFM-OKU @ _BFM-FIELD
    DUP IF >R 2DROP DROP R> EXIT THEN DROP
    0= IF 2DROP BFM-S-OK EXIT THEN
    _BFM-OD @ _BFM-OMAX @ _BFM-OLEN @ _BFM-COPY-STRING ;

VARIABLE _BFM-NOA
VARIABLE _BFM-NOU
VARIABLE _BFM-NKA
VARIABLE _BFM-NKU
VARIABLE _BFM-ND

: _BFM-OPTIONAL-NUMBER  ( object-a object-u key-a key-u destination -- status )
    _BFM-ND ! _BFM-NKU ! _BFM-NKA ! _BFM-NOU ! _BFM-NOA !
    0 _BFM-ND @ !
    _BFM-NOA @ _BFM-NOU @ _BFM-NKA @ _BFM-NKU @ _BFM-FIELD
    DUP IF >R 2DROP DROP R> EXIT THEN DROP
    0= IF 2DROP BFM-S-OK EXIT THEN
    2DUP JSON-NUMBER? 0= IF 2DROP BFM-S-TYPE EXIT THEN
    JSON-GET-NUMBER DUP 0< IF DROP BFM-S-INVALID EXIT THEN
    _BFM-ND @ ! BFM-S-OK ;

VARIABLE _BFM-IA
VARIABLE _BFM-IU
VARIABLE _BFM-ITEM
VARIABLE _BFM-POST-A
VARIABLE _BFM-POST-U
VARIABLE _BFM-AUTH-A
VARIABLE _BFM-AUTH-U
VARIABLE _BFM-REC-A
VARIABLE _BFM-REC-U

: _BFM-OBJECT-FIELD  ( object-a object-u key-a key-u -- value-a value-u status )
    _BFM-FIELD DUP IF >R 2DROP DROP 0 0 R> EXIT THEN DROP
    0= IF 2DROP 0 0 BFM-S-MISSING EXIT THEN
    2DUP JSON-OBJECT? 0= IF 2DROP 0 0 BFM-S-TYPE EXIT THEN
    BFM-S-OK ;

: _BFM-DECODE-ITEM  ( item-json-a item-json-u item -- status )
    _BFM-ITEM ! _BFM-IU ! _BFM-IA !
    _BFM-ITEM @ BFM-ITEM-SIZE 0 FILL
    _BFM-IA @ _BFM-IU @ JSON-OBJECT? 0= IF BFM-S-TYPE EXIT THEN
    _BFM-IA @ _BFM-IU @ S" post" _BFM-OBJECT-FIELD
    DUP IF >R 2DROP R> EXIT THEN DROP _BFM-POST-U ! _BFM-POST-A !
    _BFM-POST-A @ _BFM-POST-U @ S" author" _BFM-OBJECT-FIELD
    DUP IF >R 2DROP R> EXIT THEN DROP _BFM-AUTH-U ! _BFM-AUTH-A !
    _BFM-POST-A @ _BFM-POST-U @ S" record" _BFM-OBJECT-FIELD
    DUP IF >R 2DROP R> EXIT THEN DROP _BFM-REC-U ! _BFM-REC-A !

    _BFM-POST-A @ _BFM-POST-U @ S" uri"
        _BFM-ITEM @ _BFI-URI + BFM-URI-CAP _BFM-ITEM @ _BFI-URI-U +
        _BFM-REQUIRED-STRING ?DUP IF EXIT THEN
    _BFM-POST-A @ _BFM-POST-U @ S" cid"
        _BFM-ITEM @ _BFI-CID + BFM-CID-CAP _BFM-ITEM @ _BFI-CID-U +
        _BFM-REQUIRED-STRING ?DUP IF EXIT THEN
    _BFM-AUTH-A @ _BFM-AUTH-U @ S" did"
        _BFM-ITEM @ _BFI-DID + BFM-DID-CAP _BFM-ITEM @ _BFI-DID-U +
        _BFM-REQUIRED-STRING ?DUP IF EXIT THEN
    _BFM-AUTH-A @ _BFM-AUTH-U @ S" handle"
        _BFM-ITEM @ _BFI-HANDLE + BFM-HANDLE-CAP _BFM-ITEM @ _BFI-HANDLE-U +
        _BFM-REQUIRED-STRING ?DUP IF EXIT THEN
    _BFM-AUTH-A @ _BFM-AUTH-U @ S" displayName"
        _BFM-ITEM @ _BFI-DISPLAY + BFM-DISPLAY-CAP _BFM-ITEM @ _BFI-DISPLAY-U +
        _BFM-OPTIONAL-STRING ?DUP IF EXIT THEN
    _BFM-REC-A @ _BFM-REC-U @ S" text"
        _BFM-ITEM @ _BFI-TEXT + BFM-TEXT-CAP _BFM-ITEM @ _BFI-TEXT-U +
        _BFM-REQUIRED-STRING ?DUP IF EXIT THEN
    _BFM-REC-A @ _BFM-REC-U @ S" createdAt"
        _BFM-ITEM @ _BFI-CREATED + BFM-CREATED-CAP _BFM-ITEM @ _BFI-CREATED-U +
        _BFM-REQUIRED-STRING ?DUP IF EXIT THEN
    _BFM-POST-A @ _BFM-POST-U @ S" likeCount"
        _BFM-ITEM @ BFM.ITEM.LIKES _BFM-OPTIONAL-NUMBER ?DUP IF EXIT THEN
    _BFM-POST-A @ _BFM-POST-U @ S" repostCount"
        _BFM-ITEM @ BFM.ITEM.REPOSTS _BFM-OPTIONAL-NUMBER ?DUP IF EXIT THEN
    _BFM-POST-A @ _BFM-POST-U @ S" replyCount"
        _BFM-ITEM @ BFM.ITEM.REPLIES _BFM-OPTIONAL-NUMBER ?DUP IF EXIT THEN

    _BFM-IA @ _BFM-IU @ S" reply" _BFM-FIELD
    DUP IF >R 2DROP DROP R> EXIT THEN DROP
    IF 2DROP BFM-F-REPLY _BFM-ITEM @ BFM.ITEM.FLAGS +! ELSE 2DROP THEN
    _BFM-IA @ _BFM-IU @ S" reason" _BFM-FIELD
    DUP IF >R 2DROP DROP R> EXIT THEN DROP
    IF 2DROP BFM-F-REPOST _BFM-ITEM @ BFM.ITEM.FLAGS +! ELSE 2DROP THEN
    BFM-S-OK ;

VARIABLE _BFM-DA
VARIABLE _BFM-DU
VARIABLE _BFM-DEST
VARIABLE _BFM-FEED-A
VARIABLE _BFM-FEED-U
VARIABLE _BFM-CUR-A
VARIABLE _BFM-CUR-U
VARIABLE _BFM-COUNT
VARIABLE _BFM-STATUS

: _BFM-DECODE-INTO  ( json-a json-u feed -- status )
    _BFM-DEST ! _BFM-DU ! _BFM-DA !
    _BFM-DA @ _BFM-DU @ JSON-VALID? 0= IF BFM-S-INVALID EXIT THEN
    _BFM-DA @ _BFM-DU @ JSON-OBJECT? 0= IF BFM-S-TYPE EXIT THEN
    _BFM-DA @ _BFM-DU @ S" feed" _BFM-FIELD
    DUP IF >R 2DROP DROP R> EXIT THEN DROP
    0= IF 2DROP BFM-S-MISSING EXIT THEN
    2DUP JSON-ARRAY? 0= IF 2DROP BFM-S-TYPE EXIT THEN
    JSON-ENTER _BFM-FEED-U ! _BFM-FEED-A !
    0 _BFM-COUNT !
    _BFM-FEED-A @ _BFM-FEED-U @ JSON-SKIP-WS _BFM-CUR-U ! _BFM-CUR-A !
    BEGIN
        _BFM-CUR-U @ 0> IF _BFM-CUR-A @ C@ 93 <> ELSE 0 THEN
    WHILE
        _BFM-COUNT @ BFM-MAX-ITEMS >= IF BFM-S-CAPACITY EXIT THEN
        _BFM-CUR-A @ _BFM-CUR-U @ JSON-VALUE-SPAN
        _BFM-COUNT @ _BFM-DEST @ BFM.FEED.ITEM _BFM-DECODE-ITEM
        DUP IF EXIT THEN DROP
        1 _BFM-COUNT +!
        _BFM-CUR-A @ _BFM-CUR-U @ JSON-NEXT DROP
        _BFM-CUR-U ! _BFM-CUR-A !
    REPEAT
    _BFM-COUNT @ _BFM-DEST @ BFM.FEED.COUNT !
    _BFM-DA @ _BFM-DU @ S" cursor" _BFM-DEST @ BFM.FEED.CURSOR
        BFM-CURSOR-CAP _BFM-DEST @ BFM.FEED.CURSOR-U _BFM-OPTIONAL-STRING ;

VARIABLE _BFM-TEMP
VARIABLE _BFM-TEMP-IOR

: BFM-DECODE-FEED  ( json-a json-u feed -- status )
    >R BFM-FEED-SIZE ALLOCATE DUP IF
        SWAP DROP R> DROP BFM-S-CAPACITY EXIT
    THEN DROP _BFM-TEMP !
    _BFM-TEMP @ BFM-FEED-INIT
    _BFM-TEMP @ _BFM-DECODE-INTO DUP _BFM-TEMP-IOR !
    _BFM-TEMP-IOR @ BFM-S-OK = IF
        _BFM-TEMP @ R@ BFM-FEED-SIZE CMOVE
    THEN
    _BFM-TEMP @ BFM-FEED-SIZE 0 FILL _BFM-TEMP @ FREE
    R> DROP _BFM-TEMP-IOR @ ;

