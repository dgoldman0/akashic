: _PAF-ADMIT-CONTENT-TYPE  ( provider -- xerr | 0 )
    DUP _PAFA-P ! DROP
    S" content-type" _PAFA-P @ PAF.EXCHANGE HBUF.PARSER
    HSTR-HEADER-COUNT DUP _PAFA-COUNT !
    0= IF
        PAF-ERR-MEDIA-TYPE PAF-CODE-CONTENT-TYPE-MISSING _PAFA-P @ _PAF-ERROR!
        PAF-XERR-MEDIA-TYPE EXIT
    THEN
    _PAFA-COUNT @ 1 <> IF
        PAF-ERR-MEDIA-TYPE PAF-CODE-CONTENT-TYPE-DUPLICATE _PAFA-P @ _PAF-ERROR!
        PAF-XERR-MEDIA-TYPE EXIT
    THEN
    S" content-type" _PAFA-P @ PAF.EXCHANGE HBUF.PARSER HSTR-HEADER IF
        _PAF-APPLICATION-JSON? IF 0 EXIT THEN
    ELSE
        2DROP
    THEN
    PAF-ERR-MEDIA-TYPE PAF-CODE-CONTENT-TYPE-MISMATCH _PAFA-P @ _PAF-ERROR!
    PAF-XERR-MEDIA-TYPE ;

: _PAF-ADMIT-CONTENT-ENCODING  ( provider -- xerr | 0 )
    DUP _PAFA-P ! DROP
    S" content-encoding" _PAFA-P @ PAF.EXCHANGE HBUF.PARSER
    HSTR-HEADER-COUNT DUP _PAFA-COUNT !
    0= IF 0 EXIT THEN
    _PAFA-COUNT @ 1 <> IF
        PAF-ERR-CONTENT-ENCODING PAF-CODE-CONTENT-ENCODING-DUPLICATE
        _PAFA-P @ _PAF-ERROR! PAF-XERR-CONTENT-ENCODING EXIT
    THEN
    S" content-encoding" _PAFA-P @ PAF.EXCHANGE HBUF.PARSER HSTR-HEADER IF
        S" identity" STR-STRI= IF 0 EXIT THEN
    ELSE
        2DROP
    THEN
    PAF-ERR-CONTENT-ENCODING PAF-CODE-CONTENT-ENCODING-MISMATCH
    _PAFA-P @ _PAF-ERROR! PAF-XERR-CONTENT-ENCODING ;

: _PAF-ADMIT  ( operation provider -- step-status )
    _PAFS-P ! _PAFS-OP !
    _PAFS-P @ PAF.EXCHANGE HBUF.HTTP-CODE @ DUP
    _PAFS-P @ PAF.HTTP-CODE ! 200 <> IF
        PAF-ERR-HTTP _PAFS-P @ PAF.HTTP-CODE @ PAF-XERR-HTTP
        _PAFS-OP @ _PAFS-P @ _PAF-STEP-FAIL EXIT
    THEN
    _PAFS-P @ _PAF-ADMIT-CONTENT-TYPE ?DUP IF
        DUP _PAFS-OP @ XIOO.ERROR ! DROP
        PAF-STATE-FAILED _PAFS-P @ PAF.STATE ! XIO-STEP-FAILED EXIT
    THEN
    _PAFS-P @ _PAF-ADMIT-CONTENT-ENCODING ?DUP IF
        DUP _PAFS-OP @ XIOO.ERROR ! DROP
        PAF-STATE-FAILED _PAFS-P @ PAF.STATE ! XIO-STEP-FAILED EXIT
    THEN
    _PAFS-P @ PAF.EXCHANGE HBUF.BODY-U @ _PAFS-P @ PAF.BODY-U !
    _PAFS-P @ _PAFS-OP @ XIOO.RESULT !
    PAF-STATE-READY _PAFS-P @ PAF.STATE ! XIO-STEP-SUCCEEDED ;

: PAF-XIO-POLL  ( operation provider -- step-status )
    _PAFS-P ! _PAFS-OP !
    _PAFS-P @ PAF-VALID? 0= IF
        PAF-XERR-INVALID _PAFS-OP @ XIOO.ERROR ! XIO-STEP-FAILED EXIT
    THEN
    _PAFS-P @ PAF.STATE @ PAF-STATE-ACTIVE <> IF
        PAF-ERR-STATE PAF-S-BUSY PAF-XERR-STATE
        _PAFS-OP @ _PAFS-P @ _PAF-STEP-FAIL EXIT
    THEN
    _PAFS-P @ PAF.EXCHANGE HBUF-POLL DUP _PAFS-STATUS !
    DUP HBUF-S-PENDING = IF DROP XIO-STEP-PENDING EXIT THEN
    HBUF-S-OK = IF _PAFS-OP @ _PAFS-P @ _PAF-ADMIT EXIT THEN
    _PAFS-P @ PAF.EXCHANGE HBUF.PARSER HSTR.STATUS @
    HSTR-S-BODY-OVERFLOW = IF
        PAF-ERR-CAPACITY PAF-CODE-BODY-OVERFLOW PAF-XERR-CAPACITY
    ELSE
        PAF-ERR-TRANSPORT _PAFS-STATUS @ PAF-XERR-TRANSPORT
    THEN
    _PAFS-OP @ _PAFS-P @ _PAF-STEP-FAIL ;

