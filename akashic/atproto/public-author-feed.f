: _PAF-DID-CHARS?  ( addr len -- flag )
    _PAFVA-U ! _PAFVA-A !
    _PAFVA-U @ 9 < IF 0 EXIT THEN
    _PAFVA-U @ 8 ?DO
        _PAFVA-A @ I + C@ DUP 37 = IF
            DROP I 2 + _PAFVA-U @ >= IF 0 UNLOOP EXIT THEN
            _PAFVA-A @ I + 1+ C@ _PAF-HEX-CHAR? 0= IF
                0 UNLOOP EXIT
            THEN
            _PAFVA-A @ I + 2 + C@ _PAF-HEX-CHAR? 0= IF
                0 UNLOOP EXIT
            THEN
        ELSE
            _PAF-DID-ID-CHAR? 0= IF 0 UNLOOP EXIT THEN
        THEN
    LOOP -1 ;

