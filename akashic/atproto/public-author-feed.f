: PAF-INIT  ( provider -- status )
    DUP 0= IF DROP PAF-S-INVALID EXIT THEN
    DUP _PAFI-P ! PUBLIC-AUTHOR-FEED-SIZE 0 FILL
    PAF-MAGIC _PAFI-P @ PAF.MAGIC !
    _PAFI-P @ PAF.TRANSPORT KDOSTLS-INIT
    PAF-PUBLIC-HOST 443 _PAFI-P @ PAF.TRANSPORT KDOSTLS-CONFIGURE
    DUP _PAFI-STATUS ! KDOSTLS-E-OK <> IF
        PAF-ERR-CONFIG _PAFI-STATUS @ _PAFI-P @ _PAF-ERROR!
        PAF-STATE-FAILED _PAFI-P @ PAF.STATE ! PAF-S-TRANSPORT EXIT
    THEN
    _PAFI-P @ PAF.WIRE PAF-REQUEST-CAPACITY
    _PAFI-P @ PAF.REQUEST HREQ-INIT DUP IF
        PAF-ERR-CONFIG SWAP _PAFI-P @ _PAF-ERROR!
        PAF-STATE-FAILED _PAFI-P @ PAF.STATE ! PAF-S-INVALID EXIT
    THEN DROP
    PAF-STATE-IDLE _PAFI-P @ PAF.STATE ! PAF-S-OK ;

VARIABLE _PAFCFG-A
VARIABLE _PAFCFG-U
VARIABLE _PAFCFG-P

: PAF-CONFIGURE  ( actor-a actor-u provider -- status )
    _PAFCFG-P ! _PAFCFG-U ! _PAFCFG-A !
    _PAFCFG-P @ PAF-VALID? 0= IF PAF-S-INVALID EXIT THEN
    _PAFCFG-P @ PAF.CLEANUP-ERROR @ IF PAF-S-TRANSPORT EXIT THEN
    _PAFCFG-P @ PAF.STATE @ DUP PAF-STATE-IDLE =
    OVER PAF-STATE-CONFIGURED = OR
    SWAP PAF-STATE-RELEASED = OR 0= IF PAF-S-BUSY EXIT THEN
    _PAFCFG-P @ PAF.BODY-A @ IF PAF-S-BUSY EXIT THEN
    _PAFCFG-A @ _PAFCFG-U @ _PAF-ACTOR-VALID? 0= IF
        PAF-ERR-CONFIG PAF-CODE-ACTOR _PAFCFG-P @ _PAF-ERROR!
        PAF-S-INVALID EXIT
    THEN
    _PAFCFG-P @ PAF.ACTOR PAF-ACTOR-CAPACITY 0 FILL
    _PAFCFG-A @ _PAFCFG-P @ PAF.ACTOR _PAFCFG-U @ CMOVE
    _PAFCFG-U @ _PAFCFG-P @ PAF.ACTOR-U !
    0 _PAFCFG-P @ PAF.ERROR-KIND ! 0 _PAFCFG-P @ PAF.ERROR-CODE !
    PAF-STATE-CONFIGURED _PAFCFG-P @ PAF.STATE ! PAF-S-OK ;
