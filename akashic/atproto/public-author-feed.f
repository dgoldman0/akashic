    SWAP NIO.CANCEL-XT @ 0<> AND ;

: _PAF-ALLOCATE-BODY  ( provider -- status )
    DUP PAF.BODY-A @ IF DROP PAF-S-BUSY EXIT THEN
    PAF-BODY-CAPACITY ALLOCATE DUP IF
        >R 2DROP R> DROP PAF-S-CAPACITY EXIT
    THEN DROP
    DUP PAF-BODY-CAPACITY 0 FILL
    OVER PAF.BODY-A !
    0 OVER PAF.BODY-U !
    DUP PAF.FLAGS DUP @ _PAF-F-BODY-OWNED OR SWAP !
    DROP PAF-S-OK ;

: _PAF-CLEAR-PRIMARY  ( provider -- )
    DUP 0 SWAP PAF.ERROR-KIND !
    DUP 0 SWAP PAF.ERROR-CODE !
    DUP 0 SWAP PAF.HTTP-CODE !
    DUP 0 SWAP PAF.BODY-U !
    DUP PAF.FLAGS DUP @
    _PAF-F-CANCEL-CALLED _PAF-F-WIPE-CALLED OR INVERT AND SWAP !
    DROP ;

