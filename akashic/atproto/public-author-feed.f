: _PAF-UNRESERVED?  ( c -- flag )
    DUP 65 >= OVER 90 <= AND IF DROP -1 EXIT THEN
    DUP 97 >= OVER 122 <= AND IF DROP -1 EXIT THEN
    DUP 48 >= OVER 57 <= AND IF DROP -1 EXIT THEN
    DUP 45 = OVER 46 = OR OVER 95 = OR SWAP 126 = OR ;

: _PAF-DID-ID-CHAR?  ( c -- flag )
    DUP 65 >= OVER 90 <= AND >R
    DUP 97 >= OVER 122 <= AND R> OR >R
    DUP 48 >= OVER 57 <= AND R> OR >R
    DUP 46 = OVER 95 = OR OVER 58 = OR
    OVER 37 = OR SWAP 45 = OR R> OR ;

: _PAF-DID-END-CHAR?  ( c -- flag )
    DUP 65 >= OVER 90 <= AND >R
    DUP 97 >= OVER 122 <= AND R> OR >R
    DUP 48 >= OVER 57 <= AND R> OR >R
    DUP 46 = OVER 95 = OR SWAP 45 = OR R> OR ;

: _PAF-HEX-CHAR?  ( c -- flag )
    DUP 48 >= OVER 57 <= AND IF DROP -1 EXIT THEN
    DUP 65 >= OVER 70 <= AND IF DROP -1 EXIT THEN
    DUP 97 >= SWAP 102 <= AND ;

