    DUP 0= OVER PAF-ACTOR-CAPACITY > OR IF 2DROP 0 EXIT THEN
    2DUP DID-VALID? IF
        2DUP _PAF-DID-CHARS? 0= IF 2DROP 0 EXIT THEN
        1- + C@ _PAF-DID-END-CHAR? EXIT
    THEN
    FALSE DNS-NAME-VALID? ;

