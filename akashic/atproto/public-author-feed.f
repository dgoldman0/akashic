: _PAF-ACTOR-ENCODE  ( -- flag )
    _PAFB-P @ PAF-ACTOR@ 0 ?DO
        DUP I + C@ DUP _PAF-UNRESERVED? IF
            _PAF-PATH-C! 0= IF DROP 0 UNLOOP EXIT THEN
        ELSE
            37 _PAF-PATH-C! 0= IF 2DROP 0 UNLOOP EXIT THEN
            DUP 4 RSHIFT _PAF-HEX _PAF-PATH-C! 0= IF
                2DROP 0 UNLOOP EXIT
            THEN
            15 AND _PAF-HEX _PAF-PATH-C! 0= IF DROP 0 UNLOOP EXIT THEN
        THEN
    LOOP DROP -1 ;

