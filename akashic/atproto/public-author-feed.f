: PAF-XIO-START  ( operation provider -- step-status )
    _PAFS-P ! _PAFS-OP !
    _PAFS-P @ PAF-VALID? 0= IF
        PAF-XERR-INVALID _PAFS-OP @ XIOO.ERROR ! XIO-STEP-FAILED EXIT
    THEN
    _PAFS-P @ PAF.CLEANUP-ERROR @ IF
        PAF-ERR-CLEANUP PAF-CODE-CLEANUP PAF-XERR-CLEANUP
        _PAFS-OP @ _PAFS-P @ _PAF-STEP-FAIL EXIT
    THEN
    _PAFS-P @ PAF.STATE @ DUP PAF-STATE-CONFIGURED =
    SWAP PAF-STATE-RELEASED = OR 0= IF
        PAF-ERR-STATE PAF-S-BUSY PAF-XERR-STATE
        _PAFS-OP @ _PAFS-P @ _PAF-STEP-FAIL EXIT
    THEN
    _PAFS-P @ _PAF-COOPERATIVE-PORT? 0= IF
        PAF-ERR-UNSUPPORTED PAF-CODE-NONCOOPERATIVE PAF-XERR-UNSUPPORTED
        _PAFS-OP @ _PAFS-P @ _PAF-STEP-FAIL EXIT
    THEN
    _PAFS-P @ _PAF-CLEAR-PRIMARY
    _PAFS-P @ _PAF-ALLOCATE-BODY DUP PAF-S-OK <> IF
        DROP PAF-ERR-CAPACITY PAF-CODE-ALLOCATE PAF-XERR-CAPACITY
        _PAFS-OP @ _PAFS-P @ _PAF-STEP-FAIL EXIT
    THEN DROP
    _PAFS-P @ _PAF-BUILD-REQUEST DUP _PAFS-STATUS ! IF
        PAF-ERR-CAPACITY _PAFS-STATUS @ PAF-XERR-CAPACITY
        _PAFS-OP @ _PAFS-P @ _PAF-STEP-FAIL EXIT
    THEN
    _PAFS-P @ PAF.BODY-A @ PAF-BODY-CAPACITY
    _PAFS-P @ PAF.EXCHANGE HBUF-INIT HBUF-S-OK <> IF
        PAF-ERR-CAPACITY PAF-CODE-ALLOCATE PAF-XERR-CAPACITY
        _PAFS-OP @ _PAFS-P @ _PAF-STEP-FAIL EXIT
    THEN
    PAF-BODY-CAPACITY _PAFS-P @ PAF.EXCHANGE HBUF.PARSER HSTR-BODY-LIMIT!
    _PAFS-P @ PAF.REQUEST _PAFS-P @ PAF.PORT
    _PAFS-P @ PAF.EXCHANGE HBUF-START DUP _PAFS-STATUS !
    HBUF-S-PENDING = IF
        PAF-STATE-ACTIVE _PAFS-P @ PAF.STATE ! XIO-STEP-PENDING EXIT
    THEN
    PAF-ERR-TRANSPORT _PAFS-STATUS @ PAF-XERR-TRANSPORT
    _PAFS-OP @ _PAFS-P @ _PAF-STEP-FAIL ;

