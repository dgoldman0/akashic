: _PAF-FREE-BODY  ( provider -- )
    DUP PAF.BODY-A @ ?DUP IF
        DUP PAF-BODY-CAPACITY 0 FILL FREE
        0 OVER PAF.BODY-A !
    THEN
    DUP 0 SWAP PAF.BODY-U !
    DUP PAF.FLAGS DUP @ _PAF-F-BODY-OWNED INVERT AND SWAP ! DROP ;

: PAF-XIO-WIPE  ( operation provider -- )
    _PAFW-P ! _PAFW-OP !
    _PAFW-P @ PAF-VALID? 0= IF EXIT THEN
    _PAF-F-WIPE-CALLED _PAFW-P @ _PAF-FLAG? IF
        _PAFW-P @ PAF.CLEANUP-ERROR @ ?DUP IF THROW THEN EXIT
    THEN
    _PAF-F-WIPE-CALLED _PAFW-P @ _PAF-FLAG!
    0 _PAFW-CLEANUP !
    _PAFW-P @ PAF.EXCHANGE HBUF-RESET
    _PAFW-P @ PAF.EXCHANGE HBUF.PORT @ IF
        PAF-XERR-CLEANUP _PAFW-CLEANUP !
    THEN
    _PAFW-P @ _PAF-FREE-BODY
    0 _PAFW-P @ PAF.EXCHANGE HBUF.BODY-A !
    0 _PAFW-P @ PAF.EXCHANGE HBUF.BODY-CAP !
    0 _PAFW-P @ PAF.EXCHANGE HBUF.BODY-U !
    _PAFW-P @ PAF.EXCHANGE HBUF.RECV HBUF-RECV-CAPACITY 0 FILL
    _PAFW-P @ PAF.REQUEST HREQ-CLEAR
    _PAFW-P @ PAF.PATH PAF-PATH-CAPACITY 0 FILL
    0 _PAFW-P @ PAF.PATH-U !
    _PAFW-CLEANUP @ ?DUP IF
        DUP _PAFW-P @ PAF.CLEANUP-ERROR !
        _PAFW-P @ PAF.ERROR-KIND @ 0= IF
            PAF-ERR-CLEANUP PAF-CODE-CLEANUP _PAFW-P @ _PAF-ERROR!
        THEN
        PAF-STATE-FAILED _PAFW-P @ PAF.STATE ! THROW
    THEN
    _PAFW-P @ PAF.CLEANUP-ERROR @ IF
        PAF-STATE-FAILED
    ELSE
        PAF-STATE-RELEASED
    THEN _PAFW-P @ PAF.STATE ! ;

