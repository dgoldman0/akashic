: CFACET-INIT  ( facet -- )
    DUP CFACET-SIZE 0 FILL
    CFACET-MAGIC OVER CFACET.MAGIC !
    CFACET-ABI-VERSION OVER CFACET.ABI !
    CFACET-SIZE SWAP CFACET.SIZE ! ;

: CFACET-NTH  ( index facet -- entry | 0 )
    >R DUP 0< OVER R@ CFACET.COUNT @ >= OR IF
        DROP R> DROP 0 EXIT
    THEN
    CFENTRY-SIZE * R> CFACET.ENTRIES + ;

VARIABLE _CFV-FACET

: CFACET-VALID?  ( facet -- flag )
    DUP 0= IF DROP 0 EXIT THEN _CFV-FACET !
    _CFV-FACET @ CFACET.MAGIC @ CFACET-MAGIC <> IF 0 EXIT THEN
    _CFV-FACET @ CFACET.ABI @ CFACET-ABI-VERSION <> IF 0 EXIT THEN
    _CFV-FACET @ CFACET.SIZE @ CFACET-SIZE < IF 0 EXIT THEN
    _CFV-FACET @ CFACET.ID RID-PRESENT? 0= IF 0 EXIT THEN
    _CFV-FACET @ CFACET.PRACTICE-ID RID-PRESENT? 0= IF 0 EXIT THEN
    _CFV-FACET @ CFACET.EPOCH @ 0> 0= IF 0 EXIT THEN
    _CFV-FACET @ CFACET.CONTEXT-ID @ 0> 0= IF 0 EXIT THEN
    _CFV-FACET @ CFACET.CONTEXT-GEN @ 0> 0= IF 0 EXIT THEN
    _CFV-FACET @ CFACET.REVISION @ 0> 0= IF 0 EXIT THEN
    _CFV-FACET @ CFACET.COUNT @ DUP 0< IF DROP 0 EXIT THEN
    CFACET-MAX-ENTRIES > IF 0 EXIT THEN
    _CFV-FACET @ CFACET.FLAGS @ IF 0 EXIT THEN
    _CFV-FACET @ CFACET.COUNT @ 0 ?DO
        I _CFV-FACET @ CFACET-NTH CFENTRY-VALID? 0= IF
            0 UNLOOP EXIT
        THEN
    LOOP
    -1 ;

