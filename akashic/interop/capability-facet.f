VARIABLE _CFEV-ENTRY

: CFENTRY-VALID?  ( entry -- flag )
    DUP 0= IF DROP 0 EXIT THEN _CFEV-ENTRY !
    _CFEV-ENTRY @ CFENTRY.TARGET-ID @ 0> 0= IF 0 EXIT THEN
    _CFEV-ENTRY @ CFENTRY.TARGET-GEN @ 0> 0= IF 0 EXIT THEN
    _CFEV-ENTRY @ CFENTRY.EFFECTS @ DUP 0< IF DROP 0 EXIT THEN
    CAP-E-OBSERVE CAP-E-NAVIGATE OR CAP-E-MUTATE OR CAP-E-PERSIST OR
        CAP-E-DESTRUCTIVE OR CAP-E-EXTERNAL OR INVERT AND IF 0 EXIT THEN
    _CFEV-ENTRY @ CFENTRY.FLAGS @ DUP 0< IF DROP 0 EXIT THEN
    CFENTRY-FLAGS-MASK INVERT AND IF 0 EXIT THEN
    _CFEV-ENTRY @ CFENTRY.MAX-RESULT @ 0< IF 0 EXIT THEN
    _CFEV-ENTRY @ CFENTRY.OP-U @ DUP 0> SWAP CFENTRY-OP-ID-MAX <= AND
        0= IF 0 EXIT THEN
    _CFEV-ENTRY @ CFENTRY-OP@ UTF8-VALID? ;

