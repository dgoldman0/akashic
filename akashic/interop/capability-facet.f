\ =====================================================================
\  capability-facet.f - Exact attenuated operation selections
\ =====================================================================
\  A capability facet is a value description of which operation on which
\  activation-local target may be named.  It carries no handler pointer,
\  grant, or invocation authority.  Authority remains a separate owner-side
\  decision and every selected operation is bound to target generation and
\  an exact declared effect set.
\ =====================================================================

