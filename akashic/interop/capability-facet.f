: CFACET-ADD
  ( target-id target-generation effects flags max-result op-a op-u facet -- status )
    _CFA-FACET ! _CFA-U ! _CFA-A ! _CFA-MAX-RESULT !
    _CFA-FLAGS ! _CFA-EFFECTS !
    _CFA-GEN ! _CFA-ID !
    _CFA-FACET @ CFACET-VALID? 0= IF CFACET-S-INVALID EXIT THEN
    _CFA-ID @ 0> 0= _CFA-GEN @ 0> 0= OR IF CFACET-S-INVALID EXIT THEN
    _CFA-U @ DUP 0> SWAP CFENTRY-OP-ID-MAX <= AND 0= IF
        CFACET-S-INVALID EXIT
    THEN
    _CFA-A @ _CFA-U @ UTF8-VALID? 0= IF CFACET-S-INVALID EXIT THEN
    _CFA-EFFECTS @ DUP 0< IF DROP CFACET-S-INVALID EXIT THEN
    CAP-E-OBSERVE CAP-E-NAVIGATE OR CAP-E-MUTATE OR CAP-E-PERSIST OR
    CAP-E-DESTRUCTIVE OR CAP-E-EXTERNAL OR INVERT AND IF
        CFACET-S-INVALID EXIT
    THEN
    _CFA-FLAGS @ DUP 0< IF DROP CFACET-S-INVALID EXIT THEN
    CFENTRY-FLAGS-MASK INVERT AND IF CFACET-S-INVALID EXIT THEN
    _CFA-MAX-RESULT @ 0< IF CFACET-S-INVALID EXIT THEN
    _CFA-FLAGS @ CFENTRY-F-AUTO-OBSERVE AND IF
        _CFA-EFFECTS @ CAP-E-OBSERVE <> IF CFACET-S-INVALID EXIT THEN
        _CFA-FLAGS @ CFENTRY-F-REVIEW-COMMIT AND IF
            CFACET-S-INVALID EXIT
        THEN
    THEN
    _CFA-ID @ _CFA-GEN @ _CFA-A @ _CFA-U @ _CFA-FACET @ CFACET-FIND
        IF CFACET-S-DUPLICATE EXIT THEN
    _CFA-FACET @ CFACET.COUNT @ CFACET-MAX-ENTRIES >= IF
        CFACET-S-FULL EXIT
    THEN
    _CFA-FACET @ CFACET.COUNT @ CFENTRY-SIZE *
        _CFA-FACET @ CFACET.ENTRIES + DUP _CFA-ENTRY !
    CFENTRY-SIZE 0 FILL
    _CFA-ID @ _CFA-ENTRY @ CFENTRY.TARGET-ID !
    _CFA-GEN @ _CFA-ENTRY @ CFENTRY.TARGET-GEN !
    _CFA-EFFECTS @ _CFA-ENTRY @ CFENTRY.EFFECTS !
    _CFA-FLAGS @ _CFA-ENTRY @ CFENTRY.FLAGS !
    _CFA-MAX-RESULT @ _CFA-ENTRY @ CFENTRY.MAX-RESULT !
    _CFA-U @ _CFA-ENTRY @ CFENTRY.OP-U !
    _CFA-A @ _CFA-ENTRY @ CFENTRY.OP _CFA-U @ MOVE
    1 _CFA-FACET @ CFACET.COUNT +!
    CFACET-S-OK ;

