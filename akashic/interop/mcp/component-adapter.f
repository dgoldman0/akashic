: MCPA-REFRESH  ( adapter -- status )
    DUP _MCAREF-A ! MCPA-CATALOG-CLEAR
    _MCAREF-A @ MCPA.REGISTRY @ CREG.INST-N @ 0 ?DO
        I _MCAREF-A @ MCPA.REGISTRY @ CREG-INST-NTH DUP _MCAREF-INST !
        CINST-DESC _MCAREF-DESC !
        _MCAREF-DESC @ COMP.CAPS-N @ 0 ?DO
            I _MCAREF-DESC @ COMP-CAP-NTH
            _MCAREF-INST @ SWAP _MCAREF-A @ _MCPA-ADD-CAPABILITY
            ?DUP IF
                _MCAREF-A @ MCPA-CATALOG-CLEAR UNLOOP UNLOOP EXIT
            THEN
        LOOP
    LOOP
    MCP-S-OK ;

