: MCPA-NEW  ( registry bus server -- adapter ior )
    _MCAN-S ! _MCAN-B ! _MCAN-R !
    _MCAN-R @ 0= _MCAN-B @ 0= OR _MCAN-S @ 0= OR IF
        0 MCP-S-INVALID EXIT
    THEN
    MCPA-SIZE ALLOCATE
    DUP IF SWAP DROP 0 SWAP EXIT THEN
    DROP DUP MCPA-SIZE 0 FILL
    _MCAN-R @ OVER MCPA.REGISTRY !
    _MCAN-B @ OVER MCPA.BUS !
    _MCAN-S @ OVER MCPA.SERVER !
    CPRINC-AGENT OVER MCPA.PRINCIPAL !
    0 ;

: MCPA-BINDING-NTH  ( index adapter -- binding | 0 )
    >R DUP 0< OVER R@ MCPA.BINDING-N @ >= OR IF
        DROP R> DROP 0 EXIT
    THEN
    8 * R> MCPA.BINDINGS + @ ;

: MCPA-CATALOG-CLEAR  ( adapter -- )
    DUP MCPA.SERVER @ MCP-SERVER-CATALOG-CLEAR
    DUP MCPA.BINDING-N @ 0 ?DO
        I OVER MCPA-BINDING-NTH ?DUP IF FREE THEN
    LOOP
    DUP MCPA.BINDINGS MCPA-MAX-BINDINGS 8 * 0 FILL
    0 SWAP MCPA.BINDING-N ! ;

: MCPA-FREE  ( adapter -- )
    DUP 0= IF DROP EXIT THEN
    DUP MCPA-CATALOG-CLEAR FREE ;

