: _MCPA-BUS-STATUS  ( bus-status -- mcp-status )
    CASE
        CBUS-S-OK OF MCP-S-OK ENDOF
        CBUS-S-INVALID OF MCP-S-INVALID ENDOF
        CBUS-S-NOT-FOUND OF MCP-S-NOT-FOUND ENDOF
        CBUS-S-STALE-INSTANCE OF MCP-S-NOT-FOUND ENDOF
        CBUS-S-STALE-REVISION OF MCP-S-NOT-FOUND ENDOF
        CBUS-S-DENIED OF MCP-S-DENIED ENDOF
        CBUS-S-NEEDS-APPROVAL OF MCP-S-APPROVAL ENDOF
        CBUS-S-BUSY OF MCP-S-BUSY ENDOF
        CBUS-S-TIMEOUT OF MCP-S-TIMEOUT ENDOF
        CBUS-S-CANCELLED OF MCP-S-CANCELLED ENDOF
        DROP MCP-S-FAILED
    ENDCASE ;

VARIABLE _MCADV-A
VARIABLE _MCADV-XT
VARIABLE _MCADV-DECISION

: _MCPA-REVIEW-CALL  ( -- )
    _MCAD-REQ @ _MCADV-A @ MCPA.REVIEW-CONTEXT @
    _MCADV-XT @ EXECUTE _MCADV-DECISION ! ;

: _MCPA-DISPATCH  ( binding args-value result-value -- status )
    _MCAD-RESULT ! _MCAD-ARGS ! _MCAD-BIND !
    0 _MCAD-REQ ! 0 _MCAD-ERR-A ! 0 _MCAD-ERR-U !
    _MCAD-BIND @ MCPB.ADAPTER @ _MCADV-A !
    _MCAD-BIND @ MCPB.INST-ID @ _MCAD-BIND @ MCPB.INST-GEN @
    _MCADV-A @ MCPA.REGISTRY @ CREG-INST-FIND DUP 0= IF
        DROP MCP-S-NOT-FOUND EXIT
    THEN
    _MCAD-INST !
    CBR-NEW DUP IF 2DROP MCP-S-CAPACITY EXIT THEN DROP _MCAD-REQ !
    _MCAD-ARGS @ _MCAD-REQ @ CBR.ARGS CV-COPY ?DUP IF
        DROP _MCAD-REQ @ CBR-FREE MCP-S-INVALID EXIT
    THEN
    _MCADV-A @ MCPA.PRINCIPAL @ _MCAD-REQ @ CBR.PRINCIPAL !
    _MCAD-BIND @ MCPB.INST-ID @ _MCAD-REQ @ CBR.TARGET-ID !
    _MCAD-BIND @ MCPB.INST-GEN @ _MCAD-REQ @ CBR.TARGET-GEN !
    _MCAD-BIND @ MCPB.CAP @ _MCAD-REQ @ CBR.CAP !
    _MCAD-BIND @ MCPB.CAP @ CAP.MAX-MS @ ?DUP IF
        MS@ + _MCAD-REQ @ CBR.DEADLINE !
    THEN
    _MCAD-REQ @ _MCADV-A @ MCPA.BUS @ CBUS-DISPATCH _MCAD-STATUS !
    _MCAD-STATUS @ CBUS-S-NEEDS-APPROVAL = IF
        _MCADV-A @ MCPA.REVIEW-XT @ DUP _MCADV-XT ! IF
            MCPA-REVIEW-DENY _MCADV-DECISION !
            ['] _MCPA-REVIEW-CALL CATCH IF
                MCPA-REVIEW-DENY _MCADV-DECISION !
            THEN
            _MCADV-DECISION @ MCPA-REVIEW-ALLOW = IF
                _MCAD-REQ @ CBR-APPROVE
                _MCAD-REQ @ _MCADV-A @ MCPA.BUS @ CBUS-DISPATCH
                _MCAD-STATUS !
            ELSE
                _MCADV-DECISION @ MCPA-REVIEW-CANCEL = IF
                    CBUS-S-CANCELLED _MCAD-STATUS !
                ELSE
                    CBUS-S-DENIED _MCAD-STATUS !
                THEN
            THEN
        THEN
    THEN
    _MCAD-REQ @ CBR.ERROR-A @ _MCAD-ERR-A !
    _MCAD-REQ @ CBR.ERROR-U @ _MCAD-ERR-U !
    _MCAD-STATUS @ CBUS-S-OK = IF
        _MCAD-REQ @ CBR.RESULT _MCAD-RESULT @ CV-COPY ?DUP IF
            DROP CBUS-S-FAILED _MCAD-STATUS !
        THEN
    THEN
    _MCAD-REQ @ CBR-FREE
    _MCAD-STATUS @ _MCPA-BUS-STATUS ;

