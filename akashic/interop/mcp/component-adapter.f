: _MCPA-BUILD-NAME  ( binding -- status )
    _MCBN-B !
    _MCBN-B @ MCPB.CAP @ CAP-ID _MCBN-U ! _MCBN-A !
    _MCBN-B @ MCPB.INST-ID @ NUM>STR _MCBN-N ! _MCBN-I !
    _MCBN-U @ 2 + _MCBN-N @ + MCP-MAX-NAME > IF MCP-S-CAPACITY EXIT THEN
    _MCBN-A @ _MCBN-B @ MCPB.NAME-BUF _MCBN-U @ CMOVE
    46 _MCBN-B @ MCPB.NAME-BUF _MCBN-U @ + C!
    105 _MCBN-B @ MCPB.NAME-BUF _MCBN-U @ 1+ + C!
    _MCBN-I @ _MCBN-B @ MCPB.NAME-BUF _MCBN-U @ 2 + + _MCBN-N @ CMOVE
    _MCBN-U @ 2 + _MCBN-N @ + DUP _MCBN-B @ MCPB.NAME-U !
    _MCBN-B @ MCPB.NAME-BUF SWAP MCP-TOOL-NAME-VALID? 0= IF
        MCP-S-INVALID
    ELSE
        MCP-S-OK
    THEN ;

: _MCPA-BUILD-URI  ( binding -- status )
    _MCBN-B !
    S" akashic://instance/" _MCBN-U ! _MCBN-A !
    _MCBN-B @ MCPB.INST-ID @ NUM>STR _MCBN-N ! _MCBN-I !
    _MCBN-B @ MCPB.CAP @ CAP-ID _MCBN-CAP-U ! _MCBN-CAP-A !
    _MCBN-U @ _MCBN-N @ + 1+ _MCBN-CAP-U @ + MCP-MAX-URI > IF
        MCP-S-CAPACITY EXIT
    THEN
    _MCBN-A @ _MCBN-B @ MCPB.URI-BUF _MCBN-U @ CMOVE
    _MCBN-I @ _MCBN-B @ MCPB.URI-BUF _MCBN-U @ + _MCBN-N @ CMOVE
    47 _MCBN-B @ MCPB.URI-BUF _MCBN-U @ _MCBN-N @ + + C!
    _MCBN-CAP-A @ _MCBN-B @ MCPB.URI-BUF
    _MCBN-U @ _MCBN-N @ + 1+ + _MCBN-CAP-U @ CMOVE
    _MCBN-U @ _MCBN-N @ + 1+ _MCBN-CAP-U @ +
    DUP _MCBN-B @ MCPB.URI-U !
    _MCBN-B @ MCPB.URI-BUF SWAP MCP-URI-VALID? 0= IF
        MCP-S-INVALID
    ELSE
        MCP-S-OK
    THEN ;

: _MCPA-TOOL-FLAGS  ( cap -- flags )
    0 SWAP
    DUP CAP.EFFECTS @ CAP-E-OBSERVE = IF
        SWAP MCP-TOOL-F-READ-ONLY OR SWAP
    THEN
    DUP CAP.EFFECTS @ CAP-E-DESTRUCTIVE AND IF
        SWAP MCP-TOOL-F-DESTRUCTIVE OR SWAP
    THEN
    DUP CAP.FLAGS @ CAP-F-IDEMPOTENT AND IF
        SWAP MCP-TOOL-F-IDEMPOTENT OR SWAP
    THEN
    CAP.EFFECTS @ CAP-E-EXTERNAL AND IF
        MCP-TOOL-F-OPEN-WORLD OR
    THEN ;

: _MCPA-MIME  ( schema -- addr len )
    DUP 0= IF DROP S" text/plain" EXIT THEN
    CS.TYPE-MASK @
    DUP CV-T-BYTES CS-TYPE-BIT AND IF DROP S" application/octet-stream" EXIT THEN
    DUP CV-T-RESOURCE CS-TYPE-BIT AND IF DROP S" text/uri-list" EXIT THEN
    DUP CV-T-MAP CS-TYPE-BIT AND IF DROP S" application/json" EXIT THEN
    CV-T-LIST CS-TYPE-BIT AND IF S" application/json" ELSE S" text/plain" THEN ;

