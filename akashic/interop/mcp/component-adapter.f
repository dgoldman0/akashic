: _MCPA-ADD-CAPABILITY  ( instance cap adapter -- status )
    _MCAF-A ! _MCAF-CAP ! _MCAF-INST !
    _MCAF-CAP @ CAP.HANDLER-XT @ 0= IF MCP-S-OK EXIT THEN
    _MCAF-CAP @ CAP.KIND @ DUP CAP-K-COMMAND < SWAP CAP-K-RESOURCE > OR IF
        MCP-S-OK EXIT
    THEN
    _MCAF-A @ MCPA.BINDING-N @ MCPA-MAX-BINDINGS >= IF
        MCP-S-CAPACITY EXIT
    THEN
    _MCAF-INST @ _MCAF-CAP @ _MCAF-A @ _MCPA-BINDING-NEW
    DUP IF NIP EXIT THEN DROP _MCAF-BIND !
    _MCAF-BIND @
    _MCAF-A @ MCPA.BINDING-N @ 8 * _MCAF-A @ MCPA.BINDINGS + !
    1 _MCAF-A @ MCPA.BINDING-N +!
    _MCAF-BIND @ MCPB.TOOL _MCAF-A @ MCPA.SERVER @ MCP-SERVER-TOOL+
    ?DUP IF EXIT THEN
    _MCAF-CAP @ CAP.KIND @ CAP-K-RESOURCE = IF
        _MCAF-BIND @ MCPB.RESOURCE
        _MCAF-A @ MCPA.SERVER @ MCP-SERVER-RESOURCE+ ?DUP IF EXIT THEN
    THEN
    MCP-S-OK ;

