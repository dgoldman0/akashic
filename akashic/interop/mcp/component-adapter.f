: _MCPA-BINDING-NEW  ( instance cap adapter -- binding status )
    _MCBI-A ! _MCBI-CAP ! _MCBI-INST !
    MCPA-BINDING-SIZE ALLOCATE
    DUP IF 2DROP 0 MCP-S-CAPACITY EXIT THEN
    DROP DUP _MCBI-B ! MCPA-BINDING-SIZE 0 FILL
    _MCBI-A @ _MCBI-B @ MCPB.ADAPTER !
    _MCBI-INST @ CINST.ID @ _MCBI-B @ MCPB.INST-ID !
    _MCBI-INST @ CINST.GENERATION @ _MCBI-B @ MCPB.INST-GEN !
    _MCBI-CAP @ _MCBI-B @ MCPB.CAP !
    _MCBI-B @ _MCPA-BUILD-NAME ?DUP IF
        _MCBI-B @ FREE 0 SWAP EXIT
    THEN
    _MCBI-B @ _MCPA-BUILD-URI ?DUP IF
        _MCBI-B @ FREE 0 SWAP EXIT
    THEN

    _MCBI-B @ MCPB.TOOL DUP MCP-TOOL-INIT
    _MCBI-B @ MCPB.NAME-BUF OVER MTOOL.NAME-A !
    _MCBI-B @ MCPB.NAME-U @ OVER MTOOL.NAME-U !
    _MCBI-CAP @ CAP.TITLE-A @ OVER MTOOL.TITLE-A !
    _MCBI-CAP @ CAP.TITLE-U @ OVER MTOOL.TITLE-U !
    _MCBI-CAP @ CAP.DESC-A @ OVER MTOOL.DESC-A !
    _MCBI-CAP @ CAP.DESC-U @ OVER MTOOL.DESC-U !
    _MCBI-CAP @ CAP.IN-SCHEMA @ OVER MTOOL.IN-SCHEMA !
    _MCBI-CAP @ CAP.OUT-SCHEMA @ OVER MTOOL.OUT-SCHEMA !
    _MCBI-CAP @ _MCPA-TOOL-FLAGS OVER MTOOL.FLAGS !
    _MCBI-CAP @ CAP.EFFECTS @ OVER MTOOL.EFFECTS !
    ['] _MCPA-TOOL-CALL OVER MTOOL.CALL-XT !
    _MCBI-B @ SWAP MTOOL.CONTEXT !

    _MCBI-CAP @ CAP.KIND @ CAP-K-RESOURCE = IF
        _MCBI-B @ MCPB.RESOURCE DUP MCP-RESOURCE-INIT
        _MCBI-B @ MCPB.URI-BUF OVER MRES.URI-A !
        _MCBI-B @ MCPB.URI-U @ OVER MRES.URI-U !
        _MCBI-CAP @ CAP.ID-A @ OVER MRES.NAME-A !
        _MCBI-CAP @ CAP.ID-U @ OVER MRES.NAME-U !
        _MCBI-CAP @ CAP.TITLE-A @ OVER MRES.TITLE-A !
        _MCBI-CAP @ CAP.TITLE-U @ OVER MRES.TITLE-U !
        _MCBI-CAP @ CAP.DESC-A @ OVER MRES.DESC-A !
        _MCBI-CAP @ CAP.DESC-U @ OVER MRES.DESC-U !
        _MCBI-CAP @ CAP.OUT-SCHEMA @ _MCPA-MIME
        2 PICK MRES.MIME-U ! OVER MRES.MIME-A !
        ['] _MCPA-RESOURCE-READ OVER MRES.READ-XT !
        _MCBI-B @ SWAP MRES.CONTEXT !
    THEN
    _MCBI-B @ MCP-S-OK ;

