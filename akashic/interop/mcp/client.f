: MCP-CLIENT-RESOURCE-READ  ( uri-a uri-u client -- result-a result-u status )
    _MCR-C ! _MCR-UU ! _MCR-UA !
    _MCR-C @ _MC-READY? 0= IF 0 0 MCP-S-INVALID EXIT THEN
    _MCR-UA @ _MCR-UU @ MCP-URI-VALID? 0= IF 0 0 MCP-S-INVALID EXIT THEN
    _MCR-C @ _MC-PARAM-BEGIN JSON-{
    S" uri" _MCR-UA @ _MCR-UU @ JSON-KV-ESTR JSON-}
    _MC-PARAM-END DUP IF >R 2DROP R> 0 0 ROT EXIT THEN DROP
    S" resources/read" 2SWAP _MCR-C @ _MC-REQUEST ;

VARIABLE _MCCN-ID
VARIABLE _MCCN-RA
VARIABLE _MCCN-RU
VARIABLE _MCCN-C

: MCP-CLIENT-CANCEL  ( request-id reason-a reason-u client -- status )
    _MCCN-C ! _MCCN-RU ! _MCCN-RA ! _MCCN-ID !
    _MCCN-C @ _MC-READY? 0= IF MCP-S-INVALID EXIT THEN
    _MCCN-C @ _MC-PARAM-BEGIN JSON-{
    S" requestId" _MCCN-ID @ JSON-KV-NUM
    _MCCN-RU @ IF S" reason" _MCCN-RA @ _MCCN-RU @ JSON-KV-ESTR THEN
    JSON-}
    _MC-PARAM-END DUP IF >R 2DROP R> EXIT THEN DROP
    S" notifications/cancelled" 2SWAP _MCCN-C @ _MC-NOTIFY ;

: MCP-CLIENT-CLOSE  ( client -- )
    DUP MCLIENT.STATE @ MCP-STATE-CLOSED = IF DROP EXIT THEN
    DUP MCLIENT.TRANSPORT @ MCP-TRANSPORT-CLOSE
    MCP-STATE-CLOSED SWAP MCLIENT.STATE ! ;
