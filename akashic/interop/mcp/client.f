\ =====================================================================
\  client.f - Synchronous bounded native MCP client
\ =====================================================================
\  Results are borrowed spans into the client's receive buffer and remain
\  valid until the next receive. The transport owns framing and I/O.
\ =====================================================================

PROVIDED akashic-mcp-client-core

REQUIRE transport.f

16384 CONSTANT MCP-CLIENT-TX-CAP
32768 CONSTANT MCP-CLIENT-RX-CAP
8192 CONSTANT MCP-CLIENT-PARAM-CAP

 0 CONSTANT _MC-STATE
 8 CONSTANT _MC-TRANSPORT
16 CONSTANT _MC-NEXT-ID
24 CONSTANT _MC-LAST-ID
32 CONSTANT _MC-SERVER-CAPS
40 CONSTANT _MC-NAME-A
48 CONSTANT _MC-NAME-U
56 CONSTANT _MC-TITLE-A
64 CONSTANT _MC-TITLE-U
72 CONSTANT _MC-VERSION-A
80 CONSTANT _MC-VERSION-U
88 CONSTANT _MC-RX-U
96 CONSTANT _MC-MESSAGE
_MC-MESSAGE JRPC-MESSAGE-SIZE + CONSTANT _MC-TX-BUF
_MC-TX-BUF MCP-CLIENT-TX-CAP + CONSTANT _MC-RX-BUF
_MC-RX-BUF MCP-CLIENT-RX-CAP + CONSTANT _MC-PARAM-BUF
_MC-PARAM-BUF MCP-CLIENT-PARAM-CAP + CONSTANT MCP-CLIENT-SIZE

: MCLIENT.STATE        ( client -- a ) _MC-STATE + ;
: MCLIENT.TRANSPORT    ( client -- a ) _MC-TRANSPORT + ;
: MCLIENT.NEXT-ID      ( client -- a ) _MC-NEXT-ID + ;
: MCLIENT.LAST-ID      ( client -- a ) _MC-LAST-ID + ;
: MCLIENT.SERVER-CAPS  ( client -- a ) _MC-SERVER-CAPS + ;
: MCLIENT.NAME-A       ( client -- a ) _MC-NAME-A + ;
: MCLIENT.NAME-U       ( client -- a ) _MC-NAME-U + ;
: MCLIENT.TITLE-A      ( client -- a ) _MC-TITLE-A + ;
: MCLIENT.TITLE-U      ( client -- a ) _MC-TITLE-U + ;
: MCLIENT.VERSION-A    ( client -- a ) _MC-VERSION-A + ;
: MCLIENT.VERSION-U    ( client -- a ) _MC-VERSION-U + ;
: MCLIENT.RX-U         ( client -- a ) _MC-RX-U + ;
: MCLIENT.MESSAGE      ( client -- message ) _MC-MESSAGE + ;
: MCLIENT.TX-BUF       ( client -- buffer ) _MC-TX-BUF + ;
: MCLIENT.RX-BUF       ( client -- buffer ) _MC-RX-BUF + ;
: MCLIENT.PARAM-BUF    ( client -- buffer ) _MC-PARAM-BUF + ;

VARIABLE _MCN-T
VARIABLE _MCN-NA
VARIABLE _MCN-NU
VARIABLE _MCN-VA
VARIABLE _MCN-VU

: MCP-CLIENT-NEW  ( transport name-a name-u version-a version-u -- client ior )
    _MCN-VU ! _MCN-VA ! _MCN-NU ! _MCN-NA ! _MCN-T !
    _MCN-T @ 0= _MCN-NU @ 0= OR _MCN-VU @ 0= OR IF
        0 MCP-S-INVALID EXIT
    THEN
    MCP-CLIENT-SIZE ALLOCATE
    DUP IF SWAP DROP 0 SWAP EXIT THEN
    DROP DUP MCP-CLIENT-SIZE 0 FILL
    MCP-STATE-NEW OVER MCLIENT.STATE !
    1 OVER MCLIENT.NEXT-ID !
    _MCN-T @ OVER MCLIENT.TRANSPORT !
    _MCN-NA @ OVER MCLIENT.NAME-A !
    _MCN-NU @ OVER MCLIENT.NAME-U !
    _MCN-VA @ OVER MCLIENT.VERSION-A !
    _MCN-VU @ OVER MCLIENT.VERSION-U !
    0 ;

: MCP-CLIENT-FREE  ( client -- ) ?DUP IF FREE THEN ;

VARIABLE _MCT-A
VARIABLE _MCT-U
VARIABLE _MCT-C

: MCP-CLIENT-TITLE!  ( title-a title-u client -- )
    _MCT-C ! _MCT-U ! _MCT-A !
    _MCT-A @ _MCT-C @ MCLIENT.TITLE-A !
    _MCT-U @ _MCT-C @ MCLIENT.TITLE-U ! ;

: MCP-CLIENT-LAST-ERROR  ( client -- code message-a message-u )
    MCLIENT.MESSAGE DUP JRPC.ERROR-CODE @
    OVER JRPC.ERROR-MESSAGE-A @ ROT JRPC.ERROR-MESSAGE-U @ ;

VARIABLE _MCQ-MA
VARIABLE _MCQ-MU
VARIABLE _MCQ-PA
VARIABLE _MCQ-PU
VARIABLE _MCQ-C
VARIABLE _MCQ-ID
VARIABLE _MCQ-TX-U
VARIABLE _MCQ-RX-U

: _MC-REQUEST  ( method-a method-u params-a params-u client -- result-a result-u status )
    _MCQ-C ! _MCQ-PU ! _MCQ-PA ! _MCQ-MU ! _MCQ-MA !
    _MCQ-C @ MCLIENT.NEXT-ID @ DUP _MCQ-ID !
    DUP _MCQ-C @ MCLIENT.LAST-ID !
    1 _MCQ-C @ MCLIENT.NEXT-ID +!
    _MCQ-MA @ _MCQ-MU @ _MCQ-PA @ _MCQ-PU @
    _MCQ-C @ MCLIENT.TX-BUF MCP-CLIENT-TX-CAP JRPC-BUILD-REQUEST
    DUP IF 2DROP 0 0 MCP-S-CAPACITY EXIT THEN
    DROP _MCQ-TX-U !
    _MCQ-C @ MCLIENT.TX-BUF _MCQ-TX-U @
    _MCQ-C @ MCLIENT.TRANSPORT @ MCP-TRANSPORT-SEND
    ?DUP IF DROP 0 0 MCP-S-TRANSPORT EXIT THEN
    _MCQ-C @ MCLIENT.RX-BUF MCP-CLIENT-RX-CAP
    _MCQ-C @ MCLIENT.TRANSPORT @ MCP-TRANSPORT-RECV
    DUP IF 2DROP 0 0 MCP-S-TRANSPORT EXIT THEN
    DROP DUP _MCQ-RX-U ! _MCQ-C @ MCLIENT.RX-U !
    _MCQ-RX-U @ 0= IF 0 0 MCP-S-PROTOCOL EXIT THEN
    _MCQ-C @ MCLIENT.RX-BUF _MCQ-RX-U @
    _MCQ-C @ MCLIENT.MESSAGE JRPC-PARSE IF 0 0 MCP-S-PROTOCOL EXIT THEN
    _MCQ-C @ MCLIENT.MESSAGE DUP JRPC.ID-KIND @ JRPC-ID-INT <> IF
        DROP 0 0 MCP-S-PROTOCOL EXIT
    THEN
    DUP JRPC.ID-INT @ _MCQ-ID @ <> IF DROP 0 0 MCP-S-PROTOCOL EXIT THEN
    DUP JRPC.KIND @ JRPC-K-ERROR = IF DROP 0 0 MCP-S-PROTOCOL EXIT THEN
    DUP JRPC.KIND @ JRPC-K-RESULT <> IF DROP 0 0 MCP-S-PROTOCOL EXIT THEN
    DUP JRPC.RESULT-A @ SWAP JRPC.RESULT-U @ MCP-S-OK ;

: _MC-NOTIFY  ( method-a method-u params-a params-u client -- status )
    _MCQ-C ! _MCQ-PU ! _MCQ-PA ! _MCQ-MU ! _MCQ-MA !
    _MCQ-MA @ _MCQ-MU @ _MCQ-PA @ _MCQ-PU @
    _MCQ-C @ MCLIENT.TX-BUF MCP-CLIENT-TX-CAP JRPC-BUILD-NOTIFICATION
    DUP IF 2DROP MCP-S-CAPACITY EXIT THEN
    DROP _MCQ-TX-U !
    _MCQ-C @ MCLIENT.TX-BUF _MCQ-TX-U @
    _MCQ-C @ MCLIENT.TRANSPORT @ MCP-TRANSPORT-SEND ;

VARIABLE _MCPB-C
VARIABLE _MCPB-U

: _MC-PARAM-BEGIN  ( client -- )
    DUP _MCPB-C !
    JSON-BUILD-RESET MCLIENT.PARAM-BUF MCP-CLIENT-PARAM-CAP JSON-SET-OUTPUT ;

: _MC-PARAM-END  ( -- addr len status )
    JSON-OUTPUT-OK? 0= IF 0 0 MCP-S-CAPACITY EXIT THEN
    JSON-OUTPUT-RESULT NIP DUP _MCPB-U !
    _MCPB-C @ MCLIENT.PARAM-BUF SWAP MCP-S-OK ;

VARIABLE _MCIF-A
VARIABLE _MCIF-U

: _MC-RESULT-FIELD  ( client key-a key-u -- value-a value-u found ior )
    _MCIF-U ! _MCIF-A !
    MCLIENT.MESSAGE DUP JRPC.RESULT-A @ SWAP JRPC.RESULT-U @
    _MCIF-A @ _MCIF-U @ JRPC-FIELD ;

CREATE _MC-VERSION-BUF 32 ALLOT

VARIABLE _MCI-C
VARIABLE _MCI-CAPS-A
VARIABLE _MCI-CAPS-U

: _MC-INITIALIZE-RESULT  ( client -- status )
    _MCI-C !
    _MCI-C @ S" protocolVersion" _MC-RESULT-FIELD
    DUP IF >R DROP 2DROP R> DROP MCP-S-PROTOCOL EXIT THEN DROP
    0= IF 2DROP MCP-S-PROTOCOL EXIT THEN
    _MC-VERSION-BUF 32 JRPC-DECODE-STRING DUP IF
        2DROP MCP-S-PROTOCOL EXIT
    THEN
    DROP _MC-VERSION-BUF SWAP MCP-PROTOCOL-VERSION STR-STR= 0= IF
        MCP-S-PROTOCOL EXIT
    THEN
    _MCI-C @ S" capabilities" _MC-RESULT-FIELD
    DUP IF >R DROP 2DROP R> DROP MCP-S-PROTOCOL EXIT THEN DROP
    0= IF 2DROP MCP-S-PROTOCOL EXIT THEN
    2DUP JSON-OBJECT? 0= IF 2DROP MCP-S-PROTOCOL EXIT THEN
    _MCI-CAPS-U ! _MCI-CAPS-A !
    0 _MCI-C @ MCLIENT.SERVER-CAPS !
    _MCI-CAPS-A @ _MCI-CAPS-U @ S" tools" JRPC-FIELD
    DUP IF >R DROP 2DROP R> DROP MCP-S-PROTOCOL EXIT THEN DROP
    IF
        2DROP _MCI-C @ MCLIENT.SERVER-CAPS DUP @ MCP-CAP-TOOLS OR SWAP !
    ELSE
        2DROP
    THEN
    _MCI-CAPS-A @ _MCI-CAPS-U @ S" resources" JRPC-FIELD
    DUP IF >R DROP 2DROP R> DROP MCP-S-PROTOCOL EXIT THEN DROP
    IF
        2DROP _MCI-C @ MCLIENT.SERVER-CAPS DUP @ MCP-CAP-RESOURCES OR SWAP !
    ELSE
        2DROP
    THEN
    _MCI-C @ S" serverInfo" _MC-RESULT-FIELD
    DUP IF >R DROP 2DROP R> DROP MCP-S-PROTOCOL EXIT THEN DROP
    0= IF 2DROP MCP-S-PROTOCOL EXIT THEN
    JSON-OBJECT? 0= IF MCP-S-PROTOCOL EXIT THEN
    MCP-S-OK ;

: MCP-CLIENT-INITIALIZE  ( client -- status )
    DUP MCLIENT.STATE @ MCP-STATE-NEW <> IF DROP MCP-S-INVALID EXIT THEN
    DUP >R _MC-PARAM-BEGIN JSON-{
    S" protocolVersion" MCP-PROTOCOL-VERSION JSON-KV-ESTR
    S" capabilities" JSON-KEY: JSON-{ JSON-}
    S" clientInfo" JSON-KEY: JSON-{
        S" name" R@ DUP MCLIENT.NAME-A @ SWAP MCLIENT.NAME-U @ JSON-KV-ESTR
        R@ MCLIENT.TITLE-U @ IF
            S" title" R@ DUP MCLIENT.TITLE-A @ SWAP MCLIENT.TITLE-U @
            JSON-KV-ESTR
        THEN
        S" version" R@ DUP MCLIENT.VERSION-A @ SWAP MCLIENT.VERSION-U @
        JSON-KV-ESTR
    JSON-}
    JSON-}
    _MC-PARAM-END DUP IF
        >R 2DROP R> R> DROP EXIT
    THEN
    DROP
    MCP-STATE-INITIALIZING R@ MCLIENT.STATE !
    S" initialize" 2SWAP R@ _MC-REQUEST
    DUP IF >R 2DROP R> MCP-STATE-FAILED R@ MCLIENT.STATE ! R> DROP EXIT THEN
    DROP 2DROP
    R@ _MC-INITIALIZE-RESULT ?DUP IF
        MCP-STATE-FAILED R@ MCLIENT.STATE ! R> DROP EXIT
    THEN
    S" notifications/initialized" 0 0 R@ _MC-NOTIFY ?DUP IF
        MCP-STATE-FAILED R@ MCLIENT.STATE ! R> DROP EXIT
    THEN
    MCP-STATE-READY R@ MCLIENT.STATE !
    R> DROP MCP-S-OK ;

: _MC-READY?  ( client -- flag )
    MCLIENT.STATE @ MCP-STATE-READY = ;

: MCP-CLIENT-PING  ( client -- result-a result-u status )
    DUP _MC-READY? 0= IF DROP 0 0 MCP-S-INVALID EXIT THEN
    >R S" ping" S" {}" R> _MC-REQUEST ;

VARIABLE _MCL-CA
VARIABLE _MCL-CU
VARIABLE _MCL-C

: _MC-LIST  ( cursor-a cursor-u method-a method-u client -- result-a result-u status )
    _MCL-C ! _MCQ-MU ! _MCQ-MA ! _MCL-CU ! _MCL-CA !
    _MCL-C @ _MC-READY? 0= IF 0 0 MCP-S-INVALID EXIT THEN
    _MCL-C @ _MC-PARAM-BEGIN JSON-{
    _MCL-CU @ IF S" cursor" _MCL-CA @ _MCL-CU @ JSON-KV-ESTR THEN
    JSON-}
    _MC-PARAM-END DUP IF >R 2DROP R> 0 0 ROT EXIT THEN DROP
    _MCQ-MA @ _MCQ-MU @ 2SWAP _MCL-C @ _MC-REQUEST ;

: MCP-CLIENT-TOOLS-LIST  ( cursor-a cursor-u client -- result-a result-u status )
    S" tools/list" ROT _MC-LIST ;

: MCP-CLIENT-RESOURCES-LIST  ( cursor-a cursor-u client -- result-a result-u status )
    S" resources/list" ROT _MC-LIST ;

: MCP-CLIENT-TEMPLATES-LIST  ( cursor-a cursor-u client -- result-a result-u status )
    S" resources/templates/list" ROT _MC-LIST ;

VARIABLE _MCC-NA
VARIABLE _MCC-NU
VARIABLE _MCC-AA
VARIABLE _MCC-AU
VARIABLE _MCC-C

: MCP-CLIENT-TOOLS-CALL  ( name-a name-u args-a args-u client -- result-a result-u status )
    _MCC-C ! _MCC-AU ! _MCC-AA ! _MCC-NU ! _MCC-NA !
    _MCC-C @ _MC-READY? 0= IF 0 0 MCP-S-INVALID EXIT THEN
    _MCC-NA @ _MCC-NU @ MCP-TOOL-NAME-VALID? 0= IF
        0 0 MCP-S-INVALID EXIT
    THEN
    _MCC-AA @ _MCC-AU @ JSON-VALID? 0= IF 0 0 MCP-S-INVALID EXIT THEN
    _MCC-AA @ _MCC-AU @ JSON-OBJECT? 0= IF 0 0 MCP-S-INVALID EXIT THEN
    _MCC-C @ _MC-PARAM-BEGIN JSON-{
    S" name" _MCC-NA @ _MCC-NU @ JSON-KV-ESTR
    S" arguments" _MCC-AA @ _MCC-AU @ JSON-KV-RAW
    JSON-}
    _MC-PARAM-END DUP IF >R 2DROP R> 0 0 ROT EXIT THEN DROP
    S" tools/call" 2SWAP _MCC-C @ _MC-REQUEST ;

VARIABLE _MCR-UA
VARIABLE _MCR-UU
