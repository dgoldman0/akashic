: _RCON-FIELD!  ( fields key-a key-u schema index -- )
    _RCSF-I ! _RCSF-S ! _RCSF-KU ! _RCSF-KA ! _RCSF-FA !
    _RCSF-FA @ _RCSF-I @ CS-FIELD-SIZE * + DUP CS-FIELD-SIZE 0 FILL
    _RCSF-KA @ OVER CSF.KEY-A !
    _RCSF-KU @ OVER CSF.KEY-U !
    _RCSF-S @ OVER CSF.SCHEMA !
    CSF-F-REQUIRED SWAP CSF.FLAGS ! ;

: _RCON-MAP-SCHEMA!  ( fields count schema -- )
    >R R@ CS-INIT CV-T-MAP R@ CS-ALLOW!
    DUP R@ CS-MAX-LEN!
    OVER R@ CS.FIELDS !
    R> CS.FIELD-N ! DROP ;

