    DUP >R _RCONE-FIELD DUP 0= IF
        DROP R> DROP RCON-S-TYPE EXIT
    THEN
    RCON-DIGEST-KIND@ 0= IF
        DROP R> DROP RCON-S-TYPE EXIT
    THEN
    R> _RCONE.DIGEST-KIND ! RCON-S-OK ;

: _RCONE-DIGEST!  ( key-a key-u evidence -- status )
    DUP >R _RCONE-FIELD DUP _RCON-DIGEST32? 0= IF
        DROP R> DROP RCON-S-TYPE EXIT
    THEN
    CV-DATA@ R> _RCONE.STATE-DIGEST ! RCON-S-OK ;

: _RCONE-PROJECTION!  ( key-a key-u evidence -- status )
    DUP >R _RCONE-FIELD DUP 0= IF
        DROP R> DROP RCON-S-TYPE EXIT
    THEN
    DUP CV-TYPE@ CV-T-STRING <> IF
        DROP R> DROP RCON-S-TYPE EXIT
    THEN
    DUP CV-DATA@ SWAP CV-LEN@
    2DUP QLOC-PROJECTION-MAX _RCON-NONEMPTY-UTF8-SPAN? 0= IF
        2DROP R> DROP RCON-S-TYPE EXIT
    THEN
    DUP R@ _RCONE.PROJECTION-U !
    OVER R@ _RCONE.PROJECTION-A !
    2DROP R> DROP RCON-S-OK ;

