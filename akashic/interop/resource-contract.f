        SWAP _RCONRO-FAIL EXIT
    THEN
    DUP _RCONRO-CONTENT! ?DUP IF
        SWAP _RCONRO-FAIL EXIT
    THEN
    _RCONRO-SCRUB DROP RCON-S-OK ;

