    THEN
    DUP CV-TYPE@ CV-T-BYTES <> IF
        DROP R> DROP RCON-S-TYPE EXIT
    THEN
    DUP CV-DATA@ SWAP CV-LEN@
    2DUP RCON-CONTENT-MAX _RCON-BYTES-SPAN? 0= IF
        2DROP R> DROP RCON-S-TYPE EXIT
    THEN
    DUP R@ RCONRO.CONTENT-U !
    OVER R@ RCONRO.CONTENT-A !
    2DUP R@ RCONRO.CONTENT-DIGEST SHA3-256-HASH
    2DROP
    S" content_digest" R@ _RCONRO-FIELD DUP _RCON-DIGEST32? 0= IF
        DROP R> DROP RCON-S-TYPE EXIT
