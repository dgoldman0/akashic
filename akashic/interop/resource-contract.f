\ =====================================================================
\  resource-contract.f - Portable exact bounded resource value contracts
\ =====================================================================
\  These closed schemas describe domain state.  Their domain_revision is
\  never CINST.REVISION/LBIND.REVISION.  Snapshot and replace operands carry
\  exact domain evidence explicitly; the request envelope's EXPECT-REV
\  remains the independent activation-local component concurrency guard.
\ =====================================================================

