    >R DUP >R DROP
    R@ _RCONE.OWNER-A @ R@ _RCONE.OWNER-U @ R@ _RCONE.REF @
    R@ _RCONE.REVISION @ R@ _RCONE.STATE-DIGEST @
    R@ _RCONE.DIGEST-KIND @
    R@ _RCONE.PROJECTION-A @ R@ _RCONE.PROJECTION-U @
    R> DROP R> QLOC-EXACT! _RCON-QLOC>STATUS ;

: _RCONSO-FAIL  ( status operands -- status )
