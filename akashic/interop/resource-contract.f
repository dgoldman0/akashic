: _RCON-UTF8-SPAN?  ( addr len maximum -- flag )
    >R
    DUP 0< OVER R@ > OR IF 2DROP R> DROP 0 EXIT THEN
    DUP 0= IF 2DROP R> DROP -1 EXIT THEN
    OVER 0= IF 2DROP R> DROP 0 EXIT THEN
    UTF8-VALID? R> DROP ;

: _RCON-NONEMPTY-UTF8-SPAN?  ( addr len maximum -- flag )
    >R DUP 0> IF R> _RCON-UTF8-SPAN? ELSE
        2DROP R> DROP 0
    THEN ;

: _RCON-BYTES-SPAN?  ( addr len maximum -- flag )
    >R
    DUP 0< OVER R@ > OR IF 2DROP R> DROP 0 EXIT THEN
    DUP 0= IF 2DROP R> DROP -1 EXIT THEN
    MSPAN-NONWRAPPING? R> DROP ;

: _RCON-SEMANTIC-REF?  ( reference -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP RREF-VALID? 0= IF DROP 0 EXIT THEN
    RREF.REVISION @ 0= ;

