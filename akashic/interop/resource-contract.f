        QLOC-OWNER-MAX _RCON-NONEMPTY-UTF8-SPAN? 0= IF DROP 0 EXIT THEN
    _RCONE.REF @ _RCON-SEMANTIC-REF? ;

: _RCONE-REVISION!  ( key-a key-u evidence -- status )
    DUP >R _RCONE-FIELD DUP 0= IF
        DROP R> DROP RCON-S-TYPE EXIT
    THEN
    DUP CV-TYPE@ CV-T-INT <> IF
        DROP R> DROP RCON-S-TYPE EXIT
    THEN
    CV-DATA@ DUP 0> 0= IF
        DROP R> DROP RCON-S-TYPE EXIT
    THEN
    R> _RCONE.REVISION ! RCON-S-OK ;

