: RCON-CAP-CONFIG-VALID?  ( config -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP RCON-CAP-CONFIG-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP RCONCC.HANDLER @ 0= IF DROP 0 EXIT THEN
    DUP RCONCC.FLAGS @ DUP 0< IF 2DROP 0 EXIT THEN
    CAP-FLAGS-MASK INVERT AND IF DROP 0 EXIT THEN
    DUP RCONCC-TITLE$ CV-MAX-STRING-LEN _RCON-UTF8-SPAN? 0= IF
        DROP 0 EXIT
    THEN
    RCONCC-DESC$ CV-MAX-STRING-LEN _RCON-UTF8-SPAN? ;

: _RCON-CAP-WORKSPACE-VALID?  ( workspace -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP RCON-CAP-WORKSPACE-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    RCONCW.BUILDER CAPB-VALID? ;

: _RCON-CAP-DESTINATION?  ( cap workspace -- flag )
    >R
    DUP 0= IF DROP R> DROP 0 EXIT THEN
    DUP CAP-DESC MSPAN-NONWRAPPING? 0= IF DROP R> DROP 0 EXIT THEN
    DUP CAP-DESC R@ RCON-CAP-WORKSPACE-SIZE MSPAN-OVERLAP? 0=
    NIP R> DROP ;

: _RCON-CAPB-FAIL  ( construction-status workspace -- status )
    >R DUP CBUILD-S-BUSY = IF
        _RCON-CBUILD>STATUS R> DROP EXIT
    THEN
    _RCON-CBUILD>STATUS R@ RCONCW.BUILDER CAPB-ABORT DROP R> DROP ;

: _RCON-CAP-OPEN  ( config cap workspace -- status )
    DUP _RCON-CAP-WORKSPACE-VALID? 0= IF
        2DROP DROP RCON-S-INVALID EXIT
    THEN
    2 PICK RCON-CAP-CONFIG-VALID? 0= IF
        2DROP DROP RCON-S-INVALID EXIT
    THEN
    OVER OVER _RCON-CAP-DESTINATION? 0= IF
        2DROP DROP RCON-S-INVALID EXIT
    THEN
    >R
    R@ _RCONCW.DESTINATION !
    R@ _RCONCW.CONFIG !
    R@ RCONCW.BUILDER CAPB-BEGIN ?DUP IF
        R@ _RCON-CAPB-FAIL R> DROP EXIT
    THEN
    R> DROP RCON-S-OK ;

: _RCON-CAP-META  ( kind id-a id-u workspace -- construction-status )
    >R
    R@ _RCONCW.CONFIG @ RCONCC-TITLE$
    R@ _RCONCW.CONFIG @ RCONCC-DESC$
    R@ RCONCW.BUILDER CAPB-META R> DROP ;

: _RCON-CAP-CONTRACT
  ( input output effects workspace -- construction-status )
    >R
    R@ _RCONCW.CONFIG @ RCONCC.FLAGS @
    0 CCLASS-OWNER-COMMIT R@ RCONCW.BUILDER CAPB-CONTRACT
    R> DROP ;

: _RCON-CAP-CALLBACKS  ( workspace -- construction-status )
    >R
    R@ _RCONCW.CONFIG @ RCONCC.HANDLER @
    0 0 R@ RCONCW.BUILDER CAPB-CALLBACKS
    R> DROP ;

: _RCON-CAP-FINISH  ( workspace -- status )
    >R
    R@ _RCONCW.DESTINATION @ R@ RCONCW.BUILDER CAPB-SEAL ?DUP IF
        R@ _RCON-CAPB-FAIL R> DROP EXIT
    THEN
    R> DROP RCON-S-OK ;

: RCON-DESCRIBE-CAP!  ( config cap workspace -- status )
    DUP >R _RCON-CAP-OPEN ?DUP IF R> DROP EXIT THEN
    CAP-K-RESOURCE S" resource.describe" R@ _RCON-CAP-META ?DUP IF
        R@ _RCON-CAPB-FAIL R> DROP EXIT
    THEN
    RCON-DESCRIBE-IN-SCHEMA RCON-DESCRIBE-OUT-SCHEMA CAP-E-OBSERVE
        R@ _RCON-CAP-CONTRACT ?DUP IF
        R@ _RCON-CAPB-FAIL R> DROP EXIT
    THEN
    R@ _RCON-CAP-CALLBACKS ?DUP IF
        R@ _RCON-CAPB-FAIL R> DROP EXIT
    THEN
    R> _RCON-CAP-FINISH ;

: RCON-SNAPSHOT-CAP!  ( config cap workspace -- status )
    DUP >R _RCON-CAP-OPEN ?DUP IF R> DROP EXIT THEN
    CAP-K-RESOURCE S" resource.snapshot" R@ _RCON-CAP-META ?DUP IF
        R@ _RCON-CAPB-FAIL R> DROP EXIT
    THEN
    RCON-SNAPSHOT-IN-SCHEMA RCON-SNAPSHOT-OUT-SCHEMA CAP-E-OBSERVE
        R@ _RCON-CAP-CONTRACT ?DUP IF
        R@ _RCON-CAPB-FAIL R> DROP EXIT
    THEN
    R@ _RCON-CAP-CALLBACKS ?DUP IF
        R@ _RCON-CAPB-FAIL R> DROP EXIT
    THEN
    R> _RCON-CAP-FINISH ;

: RCON-REPLACE-CAP!  ( config cap workspace -- status )
    DUP >R _RCON-CAP-OPEN ?DUP IF R> DROP EXIT THEN
    CAP-K-COMMAND S" resource.replace" R@ _RCON-CAP-META ?DUP IF
        R@ _RCON-CAPB-FAIL R> DROP EXIT
    THEN
    RCON-REPLACE-IN-SCHEMA RCON-REPLACE-OUT-SCHEMA
        CAP-E-MUTATE CAP-E-PERSIST OR
        R@ _RCON-CAP-CONTRACT ?DUP IF
        R@ _RCON-CAPB-FAIL R> DROP EXIT
    THEN
    R@ _RCON-CAP-CALLBACKS ?DUP IF
        R@ _RCON-CAPB-FAIL R> DROP EXIT
    THEN
    R> _RCON-CAP-FINISH ;
