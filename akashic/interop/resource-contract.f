        IRES-S-TYPE  OF RCON-S-TYPE ENDOF
        IRES-S-NOMEM OF RCON-S-NOMEM ENDOF
        RCON-S-INVALID SWAP
    ENDCASE ;

: RCON-DESCRIBE-VIEW-VALID?  ( view -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP RCON-DESCRIBE-VIEW-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP RCONDV.RESOURCE @ _RCON-SEMANTIC-REF? 0= IF DROP 0 EXIT THEN
    DUP RCONDV.DOMAIN-REVISION @ 0< IF DROP 0 EXIT THEN
    DUP RCONDV-KIND$ RCON-KIND-MAX
        _RCON-NONEMPTY-UTF8-SPAN? 0= IF DROP 0 EXIT THEN
    DUP RCONDV-TITLE$ RCON-TITLE-MAX
        _RCON-UTF8-SPAN? 0= IF DROP 0 EXIT THEN
    DUP RCONDV-MEDIA$ RCON-MEDIA-TYPE-MAX
        _RCON-NONEMPTY-UTF8-SPAN? 0= IF DROP 0 EXIT THEN
    DUP RCONDV-OWNER$ QLOC-OWNER-MAX
        _RCON-NONEMPTY-UTF8-SPAN? 0= IF DROP 0 EXIT THEN
    RCONDV.SIZE @ DUP 0< SWAP RCON-CONTENT-MAX > OR 0= ;

: _RCON-RESULT-WORKSPACE-VALID?  ( workspace -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP RCON-RESULT-WORKSPACE-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    RCONRW.BUILDER CVB-VALID? ;

: _RCON-RESULT-DESTINATION?  ( result workspace -- flag )
    >R
    DUP 0= IF DROP R> DROP 0 EXIT THEN
    DUP CV-SIZE MSPAN-NONWRAPPING? 0= IF DROP R> DROP 0 EXIT THEN
    DUP CV-SIZE R@ RCON-RESULT-WORKSPACE-SIZE MSPAN-OVERLAP? 0=
    NIP R> DROP ;

: _RCON-SNAPSHOT-VIEW-STATUS  ( view workspace -- status )
    >R
    DUP 0= IF DROP R> DROP RCON-S-INVALID EXIT THEN
    DUP RCON-SNAPSHOT-VIEW-SIZE MSPAN-NONWRAPPING? 0= IF
        DROP R> DROP RCON-S-INVALID EXIT
    THEN
    DUP RCONSV.LOCATOR @ DUP 0= IF
        DROP DROP R> DROP RCON-S-INVALID EXIT
    THEN
    DUP QLOC-VALID? 0= SWAP QLOC.MODE @ QLOC-M-EXACT-DOMAIN <> OR IF
        DROP R> DROP RCON-S-INVALID EXIT
    THEN
    DUP RCONSV-MEDIA$ RCON-MEDIA-TYPE-MAX
        _RCON-NONEMPTY-UTF8-SPAN? 0= IF
        DROP R> DROP RCON-S-INVALID EXIT
    THEN
    DUP RCONSV-CONTENT$ 2DUP RCON-CONTENT-MAX
        _RCON-BYTES-SPAN? 0= IF
        2DROP DROP R> DROP RCON-S-INVALID EXIT
    THEN
    R@ RCONRW.DIGEST SHA3-256-HASH
    DUP RCONSV.CONTENT-DIGEST @ DUP 0= IF
        DROP DROP R> DROP RCON-S-INVALID EXIT
    THEN
    DUP SHA3-256-LEN SHA3-256-LEN _RCON-BYTES-SPAN? 0= IF
        2DROP R> DROP RCON-S-INVALID EXIT
    THEN
    R@ RCONRW.DIGEST SHA3-256-COMPARE 0= IF
        DROP R> DROP RCON-S-MISMATCH EXIT
    THEN
    DUP RCONSV.LOCATOR @ DUP QLOC.DIGEST-KIND @
        QLOC-DK-PROJECTION-CONTENT = IF
        QLOC.STATE-DIGEST R@ RCONRW.DIGEST SHA3-256-COMPARE 0= IF
            DROP R> DROP RCON-S-MISMATCH EXIT
        THEN
    ELSE
        DROP
    THEN
    DROP R> DROP RCON-S-OK ;

: _RCON-REPLACE-RELATION?  ( old-locator new-locator -- flag )
    >R
    DUP QLOC.REF RREF.ID R@ QLOC.REF RREF.ID RID= 0= IF
        DROP R> DROP 0 EXIT
    THEN
    DUP QLOC-OWNER$ R@ QLOC-OWNER$ STR-STR= 0= IF
        DROP R> DROP 0 EXIT
    THEN
    DUP QLOC.DOMAIN-REVISION @ R@ QLOC.DOMAIN-REVISION @ < 0= IF
        DROP R> DROP 0 EXIT
    THEN
    DUP QLOC.DIGEST-KIND @ R@ QLOC.DIGEST-KIND @ <> IF
        DROP R> DROP 0 EXIT
    THEN
    QLOC-PROJECTION$ R@ QLOC-PROJECTION$ STR-STR=
    R> DROP ;

: _RCON-REPLACE-VIEW-STATUS  ( view -- status )
    DUP 0= IF DROP RCON-S-INVALID EXIT THEN
    DUP RCON-REPLACE-VIEW-SIZE MSPAN-NONWRAPPING? 0= IF
        DROP RCON-S-INVALID EXIT
    THEN
    DUP RCONRV.OLD-LOCATOR @ DUP 0= IF
        DROP DROP RCON-S-INVALID EXIT
    THEN
    DUP QLOC-VALID? 0= SWAP QLOC.MODE @ QLOC-M-EXACT-DOMAIN <> OR IF
        DROP RCON-S-INVALID EXIT
    THEN
    DUP RCONRV.NEW-LOCATOR @ DUP 0= IF
        DROP DROP RCON-S-INVALID EXIT
    THEN
    DUP QLOC-VALID? 0= SWAP QLOC.MODE @ QLOC-M-EXACT-DOMAIN <> OR IF
        DROP RCON-S-INVALID EXIT
    THEN
    DUP RCONRV.OLD-LOCATOR @ OVER RCONRV.NEW-LOCATOR @
        _RCON-REPLACE-RELATION? 0= IF
        DROP RCON-S-MISMATCH EXIT
    THEN
    DUP RCONRV.CONTENT-DIGEST @ DUP 0= IF
        DROP DROP RCON-S-INVALID EXIT
    THEN
    DUP SHA3-256-LEN SHA3-256-LEN _RCON-BYTES-SPAN? 0= IF
        2DROP RCON-S-INVALID EXIT
    THEN
    DROP
    DUP RCONRV.NEW-LOCATOR @ DUP QLOC.DIGEST-KIND @
        QLOC-DK-PROJECTION-CONTENT = IF
        QLOC.STATE-DIGEST OVER RCONRV.CONTENT-DIGEST @
            SHA3-256-COMPARE 0= IF
            DROP RCON-S-MISMATCH EXIT
        THEN
    ELSE
        DROP
    THEN
    DROP RCON-S-OK ;

: _RCON-CVB-CANCEL  ( status workspace -- status )
    >R R@ RCONRW.BUILDER CVB-ABORT DROP R> DROP ;

: _RCON-CVB-FAIL  ( construction-status workspace -- status )
    >R DUP CBUILD-S-BUSY = IF
        _RCON-CBUILD>STATUS R> DROP EXIT
    THEN
    _RCON-CBUILD>STATUS R@ RCONRW.BUILDER CVB-ABORT DROP R> DROP ;

: _RCON-RESULT-BEGIN  ( count view result workspace -- construction-status )
    >R
    R@ _RCONRW.RESULT !
    R@ _RCONRW.VIEW !
    R@ RCONRW.BUILDER CVB-BEGIN-MAP
    R> DROP ;

: _RCON-RESULT-SLOT  ( key-a key-u index workspace -- construction-status )
    >R
    R@ RCONRW.BUILDER CVB-CANDIDATE
    R@ RCONRW.BUILDER CVB-MAP-SLOT! DUP IF
        NIP R> DROP EXIT
    THEN
    DROP R@ _RCONRW.CHILD ! R> DROP CBUILD-S-OK ;

: _RCON-RESULT-INT!  ( n key-a key-u index workspace -- status )
    >R R@ _RCON-RESULT-SLOT ?DUP IF
        NIP R> DROP EXIT
    THEN
    R@ _RCONRW.CHILD @ R@ RCONRW.BUILDER CVB-INT! R> DROP ;

: _RCON-RESULT-BOOL!  ( flag key-a key-u index workspace -- status )
    >R R@ _RCON-RESULT-SLOT ?DUP IF
        NIP R> DROP EXIT
    THEN
    R@ _RCONRW.CHILD @ R@ RCONRW.BUILDER CVB-BOOL! R> DROP ;

: _RCON-RESULT-STRING!  ( a u key-a key-u index workspace -- status )
    >R R@ _RCON-RESULT-SLOT ?DUP IF
        >R 2DROP R> R> DROP EXIT
    THEN
    R@ _RCONRW.CHILD @ R@ RCONRW.BUILDER CVB-STRING! R> DROP ;

: _RCON-RESULT-BYTES!  ( a u key-a key-u index workspace -- status )
    >R R@ _RCON-RESULT-SLOT ?DUP IF
        >R 2DROP R> R> DROP EXIT
    THEN
    R@ _RCONRW.CHILD @ R@ RCONRW.BUILDER CVB-BYTES! R> DROP ;

: _RCON-RESULT-RESOURCE!  ( reference workspace -- status )
    >R
    S" resource" 0 R@ _RCON-RESULT-SLOT ?DUP IF
        NIP _RCON-CBUILD>STATUS R> DROP EXIT
    THEN
    R@ _RCONRW.CHILD @ IRES-RREF! _RCON-IRES>STATUS R> DROP ;

: _RCON-RESULT-FINISH  ( schema workspace -- status )
    >R
    R@ RCONRW.BUILDER CVB-SEAL ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.RESULT @ R@ RCONRW.BUILDER CVB-PUBLISH ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R> DROP RCON-S-OK ;

: _RCON-DIGEST-KIND-FIELD!  ( locator key-a key-u index workspace -- status )
    >R >R >R >R
    QLOC.DIGEST-KIND @ RCON-DIGEST-KIND$ 0= IF
        2DROP R> DROP R> DROP R> DROP R> DROP
        CBUILD-S-INVALID EXIT
    THEN
    R> R> R> R> _RCON-RESULT-STRING! ;

: RCON-DESCRIBE-RESULT!  ( view result workspace -- status )
    DUP _RCON-RESULT-WORKSPACE-VALID? 0= IF
        2DROP DROP RCON-S-INVALID EXIT
    THEN
    2 PICK RCON-DESCRIBE-VIEW-VALID? 0= IF
        2DROP DROP RCON-S-INVALID EXIT
    THEN
    OVER OVER _RCON-RESULT-DESTINATION? 0= IF
        2DROP DROP RCON-S-INVALID EXIT
    THEN
    DUP >R >R 8 -ROT R> _RCON-RESULT-BEGIN ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONDV.RESOURCE @ R@ _RCON-RESULT-RESOURCE!
        ?DUP IF R@ _RCON-CVB-CANCEL R> DROP EXIT THEN
    R@ _RCONRW.VIEW @ RCONDV.DOMAIN-REVISION @
        S" domain_revision" 1 R@ _RCON-RESULT-INT! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONDV-KIND$
        S" kind" 2 R@ _RCON-RESULT-STRING! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONDV-TITLE$
        S" title" 3 R@ _RCON-RESULT-STRING! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONDV-MEDIA$
        S" media_type" 4 R@ _RCON-RESULT-STRING! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONDV.MUTABLE @
        S" mutable" 5 R@ _RCON-RESULT-BOOL! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONDV.SIZE @
        S" size" 6 R@ _RCON-RESULT-INT! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONDV-OWNER$
        S" owner" 7 R@ _RCON-RESULT-STRING! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    RCON-DESCRIBE-OUT-SCHEMA R@ _RCON-RESULT-FINISH
    R> DROP ;

: RCON-SNAPSHOT-RESULT!  ( view result workspace -- status )
    DUP _RCON-RESULT-WORKSPACE-VALID? 0= IF
        2DROP DROP RCON-S-INVALID EXIT
    THEN
    2 PICK OVER _RCON-SNAPSHOT-VIEW-STATUS ?DUP IF
        >R 2DROP DROP R> EXIT
    THEN
    OVER OVER _RCON-RESULT-DESTINATION? 0= IF
        2DROP DROP RCON-S-INVALID EXIT
    THEN
    DUP >R >R 9 -ROT R> _RCON-RESULT-BEGIN ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONSV.LOCATOR @ QLOC.REF
        R@ _RCON-RESULT-RESOURCE! ?DUP IF
        R@ _RCON-CVB-CANCEL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONSV.LOCATOR @ QLOC.DOMAIN-REVISION @
        S" domain_revision" 1 R@ _RCON-RESULT-INT! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONSV.LOCATOR @
        S" digest_kind" 2 R@ _RCON-DIGEST-KIND-FIELD! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONSV.LOCATOR @ QLOC.STATE-DIGEST
        SHA3-256-LEN S" state_digest" 3
        R@ _RCON-RESULT-BYTES! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONSV.LOCATOR @ QLOC-PROJECTION$
        S" projection" 4 R@ _RCON-RESULT-STRING! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONSV-MEDIA$
        S" media_type" 5 R@ _RCON-RESULT-STRING! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONSV-CONTENT$
        S" content" 6 R@ _RCON-RESULT-BYTES! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONRW.VIEW @ RCONSV.CONTENT-U @
        S" size" 7 R@ _RCON-RESULT-INT! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    R@ RCONRW.DIGEST SHA3-256-LEN
        S" content_digest" 8 R@ _RCON-RESULT-BYTES! ?DUP IF
        R@ _RCON-CVB-FAIL R> DROP EXIT
    THEN
    RCON-SNAPSHOT-OUT-SCHEMA R@ _RCON-RESULT-FINISH
    R> DROP ;

: RCON-REPLACE-RESULT!  ( view result workspace -- status )
    DUP _RCON-RESULT-WORKSPACE-VALID? 0= IF
