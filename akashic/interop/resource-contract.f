_RCON-SCHEMAS-SETUP

: RCON-DIGEST-KIND$  ( kind -- addr len flag )
    DUP QLOC-DK-SEMANTIC-STATE = IF
        DROP S" semantic-state" -1 EXIT
    THEN
    QLOC-DK-PROJECTION-CONTENT = IF
        S" projection-content" -1
    ELSE
        0 0 0
    THEN ;

: RCON-DIGEST-KIND@  ( value -- kind flag )
    DUP 0= IF DROP 0 0 EXIT THEN
    DUP CV-TYPE@ CV-T-STRING <> IF DROP 0 0 EXIT THEN
    DUP CV-DATA@ SWAP CV-LEN@
    2DUP S" semantic-state" STR-STR= IF
        2DROP QLOC-DK-SEMANTIC-STATE -1 EXIT
    THEN
    S" projection-content" STR-STR= IF
        QLOC-DK-PROJECTION-CONTENT -1
    ELSE
        0 0
    THEN ;

VARIABLE _RCB-V
VARIABLE _RCB-C

: _RCON-SLOT  ( key-a key-u index -- status )
    _RCB-V @ CV-MAP-SLOT! DUP IF NIP ELSE DROP _RCB-C ! 0 THEN ;

: _RCON-BUILD-FAIL  ( -- status ) _RCB-V @ CV-FREE RCON-S-NOMEM ;

VARIABLE _RCSB-L

: RCON-SNAPSHOT-ARGS!  ( locator value -- status )
    _RCB-V ! _RCSB-L !
    _RCB-V @ 0= _RCSB-L @ QLOC-VALID? 0= OR IF RCON-S-INVALID EXIT THEN
    _RCSB-L @ QLOC.MODE @ QLOC-M-EXACT-DOMAIN <> IF RCON-S-INVALID EXIT THEN
    4 _RCB-V @ CV-MAP! IF RCON-S-NOMEM EXIT THEN
    S" domain_revision" 0 _RCON-SLOT IF _RCON-BUILD-FAIL EXIT THEN
    _RCSB-L @ QLOC.DOMAIN-REVISION @ _RCB-C @ CV-INT!
    S" digest_kind" 1 _RCON-SLOT IF _RCON-BUILD-FAIL EXIT THEN
    _RCSB-L @ QLOC.DIGEST-KIND @ RCON-DIGEST-KIND$ 0= IF
        2DROP _RCON-BUILD-FAIL EXIT
    THEN
    _RCB-C @ CV-STRING! IF _RCON-BUILD-FAIL EXIT THEN
    S" state_digest" 2 _RCON-SLOT IF _RCON-BUILD-FAIL EXIT THEN
    _RCSB-L @ QLOC.STATE-DIGEST SHA3-256-LEN _RCB-C @ CV-BYTES! IF
        _RCON-BUILD-FAIL EXIT
    THEN
    S" projection" 3 _RCON-SLOT IF _RCON-BUILD-FAIL EXIT THEN
    _RCSB-L @ QLOC-PROJECTION$ _RCB-C @ CV-STRING! IF
        _RCON-BUILD-FAIL EXIT
    THEN
    RCON-S-OK ;

VARIABLE _RCRB-L
VARIABLE _RCRB-A
VARIABLE _RCRB-U
VARIABLE _RCRB-D

: RCON-REPLACE-ARGS!  ( locator content-a content-u digest value -- status )
    _RCB-V ! _RCRB-D ! _RCRB-U ! _RCRB-A ! _RCRB-L !
    _RCB-V @ 0= _RCRB-L @ QLOC-VALID? 0= OR IF RCON-S-INVALID EXIT THEN
    _RCRB-L @ QLOC.MODE @ QLOC-M-EXACT-DOMAIN <> IF RCON-S-INVALID EXIT THEN
    _RCRB-U @ 0< _RCRB-U @ RCON-CONTENT-MAX > OR IF RCON-S-INVALID EXIT THEN
    _RCRB-U @ 0> _RCRB-A @ 0= AND _RCRB-D @ 0= OR IF RCON-S-INVALID EXIT THEN
    6 _RCB-V @ CV-MAP! IF RCON-S-NOMEM EXIT THEN
    S" expected_domain_revision" 0 _RCON-SLOT IF _RCON-BUILD-FAIL EXIT THEN
    _RCRB-L @ QLOC.DOMAIN-REVISION @ _RCB-C @ CV-INT!
    S" expected_digest_kind" 1 _RCON-SLOT IF _RCON-BUILD-FAIL EXIT THEN
    _RCRB-L @ QLOC.DIGEST-KIND @ RCON-DIGEST-KIND$ 0= IF
        2DROP _RCON-BUILD-FAIL EXIT
    THEN
    _RCB-C @ CV-STRING! IF _RCON-BUILD-FAIL EXIT THEN
    S" expected_state_digest" 2 _RCON-SLOT IF _RCON-BUILD-FAIL EXIT THEN
    _RCRB-L @ QLOC.STATE-DIGEST SHA3-256-LEN _RCB-C @ CV-BYTES! IF
        _RCON-BUILD-FAIL EXIT
    THEN
    S" projection" 3 _RCON-SLOT IF _RCON-BUILD-FAIL EXIT THEN
    _RCRB-L @ QLOC-PROJECTION$ _RCB-C @ CV-STRING! IF
        _RCON-BUILD-FAIL EXIT
    THEN
    S" content" 4 _RCON-SLOT IF _RCON-BUILD-FAIL EXIT THEN
    _RCRB-A @ _RCRB-U @ _RCB-C @ CV-BYTES! IF _RCON-BUILD-FAIL EXIT THEN
    S" content_digest" 5 _RCON-SLOT IF _RCON-BUILD-FAIL EXIT THEN
    _RCRB-D @ SHA3-256-LEN _RCB-C @ CV-BYTES! IF _RCON-BUILD-FAIL EXIT THEN
    RCON-S-OK ;

: _RCON-FIELD  ( key-a key-u value -- child | 0 ) CV-MAP-FIND ;

CREATE _RCV-REF RREF-SIZE ALLOT
CREATE _RCV-DIGEST SHA3-256-LEN ALLOT
VARIABLE _RCV-V
VARIABLE _RCV-L
VARIABLE _RCV-F
VARIABLE _RCV-K
VARIABLE _RCV-EXPECTED-DIGEST

: _RCON-NONEMPTY-TEXT?  ( value -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP CV-TYPE@ CV-T-STRING <> IF DROP 0 EXIT THEN
    DUP CV-LEN@ 0> SWAP DUP CV-DATA@ SWAP CV-LEN@ UTF8-VALID? AND ;

: _RCON-DIGEST32?  ( value -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP CV-TYPE@ CV-T-BYTES = SWAP CV-LEN@ SHA3-256-LEN = AND ;

: RCON-DESCRIBE-RESULT?  ( value -- flag )
    DUP _RCV-V ! RCON-DESCRIBE-OUT-SCHEMA CS-VALIDATE-DEEP IF 0 EXIT THEN
    S" resource" _RCV-V @ _RCON-FIELD _RCV-REF IRES-RREF@
        IRES-S-OK <> IF 0 EXIT THEN
    _RCV-REF RREF.REVISION @ IF 0 EXIT THEN
    S" kind" _RCV-V @ _RCON-FIELD _RCON-NONEMPTY-TEXT? 0= IF 0 EXIT THEN
    S" media_type" _RCV-V @ _RCON-FIELD _RCON-NONEMPTY-TEXT? 0= IF 0 EXIT THEN
    S" owner" _RCV-V @ _RCON-FIELD _RCON-NONEMPTY-TEXT? ;

: _RCON-EXACT-ECHO?  ( locator value -- flag )
    _RCV-V ! _RCV-L !
    _RCV-L @ QLOC-VALID? 0= IF 0 EXIT THEN
    _RCV-L @ QLOC.MODE @ QLOC-M-EXACT-DOMAIN <> IF 0 EXIT THEN
    S" resource" _RCV-V @ _RCON-FIELD _RCV-REF IRES-RREF@
        IRES-S-OK <> IF 0 EXIT THEN
    _RCV-REF RREF.REVISION @ IF 0 EXIT THEN
    _RCV-REF RREF.ID _RCV-L @ QLOC.REF RREF.ID RID= 0= IF 0 EXIT THEN
    S" domain_revision" _RCV-V @ _RCON-FIELD CV-DATA@
        _RCV-L @ QLOC.DOMAIN-REVISION @ <> IF 0 EXIT THEN
    S" digest_kind" _RCV-V @ _RCON-FIELD RCON-DIGEST-KIND@
        0= IF DROP 0 EXIT THEN
    _RCV-L @ QLOC.DIGEST-KIND @ <> IF 0 EXIT THEN
    S" state_digest" _RCV-V @ _RCON-FIELD DUP _RCON-DIGEST32? 0= IF
        DROP 0 EXIT
    THEN
    CV-DATA@ _RCV-L @ QLOC.STATE-DIGEST SHA3-256-COMPARE 0= IF 0 EXIT THEN
    S" projection" _RCV-V @ _RCON-FIELD DUP CV-DATA@ SWAP CV-LEN@
        _RCV-L @ QLOC-PROJECTION$ STR-STR= ;

: RCON-SNAPSHOT-RESULT?  ( locator value -- flag )
    DUP RCON-SNAPSHOT-OUT-SCHEMA CS-VALIDATE-DEEP IF 2DROP 0 EXIT THEN
    2DUP _RCON-EXACT-ECHO? 0= IF 2DROP 0 EXIT THEN
    NIP _RCV-V !
    S" content" _RCV-V @ _RCON-FIELD CV-LEN@ _RCV-F !
    S" size" _RCV-V @ _RCON-FIELD CV-DATA@ _RCV-F @ <> IF 0 EXIT THEN
    S" content" _RCV-V @ _RCON-FIELD DUP CV-DATA@ SWAP CV-LEN@
        _RCV-DIGEST SHA3-256-HASH
    S" content_digest" _RCV-V @ _RCON-FIELD DUP _RCON-DIGEST32? 0= IF
        DROP 0 EXIT
    THEN
    CV-DATA@ _RCV-DIGEST SHA3-256-COMPARE 0= IF 0 EXIT THEN
    _RCV-L @ QLOC.DIGEST-KIND @ QLOC-DK-PROJECTION-CONTENT = IF
        _RCV-DIGEST _RCV-L @ QLOC.STATE-DIGEST SHA3-256-COMPARE EXIT
    THEN
    -1 ;

: RCON-REPLACE-RESULT?  ( old-locator expected-content-digest value -- flag )
    DUP RCON-REPLACE-OUT-SCHEMA CS-VALIDATE-DEEP IF 2DROP DROP 0 EXIT THEN
    _RCV-V ! _RCV-EXPECTED-DIGEST ! _RCV-L !
    _RCV-EXPECTED-DIGEST @ 0= IF 0 EXIT THEN
    _RCV-L @ QLOC-VALID? 0= IF 0 EXIT THEN
    _RCV-L @ QLOC.MODE @ QLOC-M-EXACT-DOMAIN <> IF 0 EXIT THEN
    S" resource" _RCV-V @ _RCON-FIELD _RCV-REF IRES-RREF@
        IRES-S-OK <> IF 0 EXIT THEN
    _RCV-REF RREF.REVISION @ IF 0 EXIT THEN
    _RCV-REF RREF.ID _RCV-L @ QLOC.REF RREF.ID RID= 0= IF 0 EXIT THEN
    S" domain_revision" _RCV-V @ _RCON-FIELD CV-DATA@
        _RCV-L @ QLOC.DOMAIN-REVISION @ > 0= IF 0 EXIT THEN
    S" digest_kind" _RCV-V @ _RCON-FIELD RCON-DIGEST-KIND@
        0= IF DROP 0 EXIT THEN
    _RCV-L @ QLOC.DIGEST-KIND @ <> IF 0 EXIT THEN
    S" state_digest" _RCV-V @ _RCON-FIELD _RCON-DIGEST32? 0= IF 0 EXIT THEN
    S" projection" _RCV-V @ _RCON-FIELD DUP CV-DATA@ SWAP CV-LEN@
        _RCV-L @ QLOC-PROJECTION$ STR-STR= 0= IF 0 EXIT THEN
    S" content_digest" _RCV-V @ _RCON-FIELD DUP _RCV-F !
        _RCON-DIGEST32? 0= IF 0 EXIT THEN
    _RCV-F @ CV-DATA@ _RCV-EXPECTED-DIGEST @
        SHA3-256-COMPARE 0= IF 0 EXIT THEN
    _RCV-L @ QLOC.DIGEST-KIND @ QLOC-DK-PROJECTION-CONTENT = IF
        S" state_digest" _RCV-V @ _RCON-FIELD CV-DATA@
        _RCV-F @ CV-DATA@ SHA3-256-COMPARE EXIT
    THEN
    -1 ;

