        DROP RCON-S-INVALID SWAP _RCONSO-FAIL EXIT
    THEN
    DUP _RCONE.VALUE @ RCON-SNAPSHOT-IN-SCHEMA CS-VALIDATE-DEEP IF
        DROP RCON-S-TYPE SWAP _RCONSO-FAIL EXIT
    THEN
    S" domain_revision" 2 PICK _RCONE-REVISION! ?DUP IF
        NIP SWAP _RCONSO-FAIL EXIT
    THEN
    S" digest_kind" 2 PICK _RCONE-DIGEST-KIND! ?DUP IF
        NIP SWAP _RCONSO-FAIL EXIT
    THEN
    S" state_digest" 2 PICK _RCONE-DIGEST! ?DUP IF
        NIP SWAP _RCONSO-FAIL EXIT
    THEN
