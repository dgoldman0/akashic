        >R DROP 2DROP R> R> _RCON-ARGS-WORKSPACE-FREE EXIT
    THEN
    _RCON-SNAPSHOT-ARGS-BUILD! R> _RCON-ARGS-WORKSPACE-FREE ;

: RCON-REPLACE-ARGS!  ( locator content-a content-u digest value -- status )
    _RCON-ARGS-WORKSPACE-SIZE ALLOCATE DUP IF
        2DROP 2DROP 2DROP DROP RCON-S-NOMEM EXIT
    THEN
    DROP DUP >R
    DUP _RCON-ARGS-WORKSPACE-INIT ?DUP IF
        >R 2DROP 2DROP 2DROP R> R> _RCON-ARGS-WORKSPACE-FREE EXIT
