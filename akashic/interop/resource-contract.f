    R@ _RCONAW.BUILDER CVB-CANDIDATE
    R@ _RCONAW.BUILDER CVB-MAP-SLOT!
    R> DROP ;

: _RCON-ARGS-INT!  ( n key-a key-u index workspace -- status )
    >R R@ _RCON-ARGS-SLOT DUP IF
        >R 2DROP R> R> DROP EXIT
    THEN
    DROP R@ _RCONAW.BUILDER CVB-INT! R> DROP ;

: _RCON-ARGS-STRING!  ( a u key-a key-u index workspace -- status )
    >R R@ _RCON-ARGS-SLOT DUP IF
        >R DROP 2DROP R> R> DROP EXIT
    THEN
    DROP R@ _RCONAW.BUILDER CVB-STRING! R> DROP ;

: _RCON-ARGS-BYTES!  ( a u key-a key-u index workspace -- status )
    >R R@ _RCON-ARGS-SLOT DUP IF
        >R DROP 2DROP R> R> DROP EXIT
    THEN
    DROP R@ _RCONAW.BUILDER CVB-BYTES! R> DROP ;

: _RCON-ARGS-DIGEST-KIND!
  ( locator key-a key-u index workspace -- status )
    >R >R >R >R
    QLOC.DIGEST-KIND @ RCON-DIGEST-KIND$ 0= IF
        2DROP R> DROP R> DROP R> DROP R> DROP
        CBUILD-S-INVALID EXIT
    THEN
    R> R> R> R> _RCON-ARGS-STRING! ;

: _RCON-ARGS-FINISH  ( schema workspace -- status )
    >R
    R@ _RCONAW.BUILDER CVB-SEAL ?DUP IF
        R@ _RCON-ARGS-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONAW.DESTINATION @ R@ _RCONAW.BUILDER CVB-PUBLISH ?DUP IF
        R@ _RCON-ARGS-CVB-FAIL R> DROP EXIT
    THEN
    R> DROP RCON-S-OK ;

: _RCON-SNAPSHOT-ARGS-BUILD!
  ( locator value workspace -- status )
    DUP _RCON-ARGS-WORKSPACE-VALID? 0= IF
        2DROP DROP RCON-S-INVALID EXIT
    THEN
    2 PICK DUP QLOC-VALID? 0= SWAP QLOC.MODE @
        QLOC-M-EXACT-DOMAIN <> OR IF
        2DROP DROP RCON-S-INVALID EXIT
    THEN
    OVER OVER _RCON-ARGS-DESTINATION? 0= IF
        2DROP DROP RCON-S-INVALID EXIT
    THEN
    >R
    R@ _RCONAW.DESTINATION !
    R@ _RCONAW.LOCATOR !
    4 R@ _RCONAW.BUILDER CVB-BEGIN-MAP ?DUP IF
        R@ _RCON-ARGS-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONAW.LOCATOR @ QLOC.DOMAIN-REVISION @
        S" domain_revision" 0 R@ _RCON-ARGS-INT! ?DUP IF
        R@ _RCON-ARGS-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONAW.LOCATOR @ S" digest_kind" 1
        R@ _RCON-ARGS-DIGEST-KIND! ?DUP IF
        R@ _RCON-ARGS-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONAW.LOCATOR @ QLOC.STATE-DIGEST SHA3-256-LEN
        S" state_digest" 2 R@ _RCON-ARGS-BYTES! ?DUP IF
        R@ _RCON-ARGS-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONAW.LOCATOR @ QLOC-PROJECTION$
        S" projection" 3 R@ _RCON-ARGS-STRING! ?DUP IF
        R@ _RCON-ARGS-CVB-FAIL R> DROP EXIT
    THEN
    RCON-SNAPSHOT-IN-SCHEMA R@ _RCON-ARGS-FINISH
    R> DROP ;

: _RCON-REPLACE-ARGS-BUILD!
  ( locator content-a content-u digest value workspace -- status )
    DUP _RCON-ARGS-WORKSPACE-VALID? 0= IF
        2DROP 2DROP 2DROP RCON-S-INVALID EXIT
    THEN
    5 PICK DUP QLOC-VALID? 0= SWAP QLOC.MODE @
        QLOC-M-EXACT-DOMAIN <> OR IF
        2DROP 2DROP 2DROP RCON-S-INVALID EXIT
    THEN
    OVER OVER _RCON-ARGS-DESTINATION? 0= IF
        2DROP 2DROP 2DROP RCON-S-INVALID EXIT
    THEN
    4 PICK 4 PICK RCON-CONTENT-MAX _RCON-BYTES-SPAN? 0= IF
        2DROP 2DROP 2DROP RCON-S-INVALID EXIT
    THEN
    2 PICK DUP 0= IF
        DROP 2DROP 2DROP 2DROP RCON-S-INVALID EXIT
    THEN
    DUP SHA3-256-LEN SHA3-256-LEN _RCON-BYTES-SPAN? 0= IF
        DROP 2DROP 2DROP 2DROP RCON-S-INVALID EXIT
    THEN
    DROP
    >R
    R@ _RCONAW.DESTINATION !
    R@ _RCONAW.CONTENT-DIGEST !
    R@ _RCONAW.CONTENT-U !
    R@ _RCONAW.CONTENT-A !
    R@ _RCONAW.LOCATOR !
    R@ _RCONAW.CONTENT-A @ R@ _RCONAW.CONTENT-U @ R@ _RCONAW.HASH
        SHA3-256-HASH
    R@ _RCONAW.CONTENT-DIGEST @ R@ _RCONAW.HASH SHA3-256-COMPARE 0= IF
        R> DROP RCON-S-MISMATCH EXIT
    THEN
    6 R@ _RCONAW.BUILDER CVB-BEGIN-MAP ?DUP IF
        R@ _RCON-ARGS-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONAW.LOCATOR @ QLOC.DOMAIN-REVISION @
        S" expected_domain_revision" 0 R@ _RCON-ARGS-INT! ?DUP IF
        R@ _RCON-ARGS-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONAW.LOCATOR @ S" expected_digest_kind" 1
        R@ _RCON-ARGS-DIGEST-KIND! ?DUP IF
        R@ _RCON-ARGS-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONAW.LOCATOR @ QLOC.STATE-DIGEST SHA3-256-LEN
        S" expected_state_digest" 2 R@ _RCON-ARGS-BYTES! ?DUP IF
        R@ _RCON-ARGS-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONAW.LOCATOR @ QLOC-PROJECTION$
        S" projection" 3 R@ _RCON-ARGS-STRING! ?DUP IF
        R@ _RCON-ARGS-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONAW.CONTENT-A @ R@ _RCONAW.CONTENT-U @
        S" content" 4 R@ _RCON-ARGS-BYTES! ?DUP IF
        R@ _RCON-ARGS-CVB-FAIL R> DROP EXIT
    THEN
    R@ _RCONAW.HASH SHA3-256-LEN
        S" content_digest" 5 R@ _RCON-ARGS-BYTES! ?DUP IF
        R@ _RCON-ARGS-CVB-FAIL R> DROP EXIT
    THEN
    RCON-REPLACE-IN-SCHEMA R@ _RCON-ARGS-FINISH
    R> DROP ;

