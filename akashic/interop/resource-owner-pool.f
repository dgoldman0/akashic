: ROFFER.MAGIC  ( offer -- a ) _ROF-MAGIC + ;
: ROFFER.ABI    ( offer -- a ) _ROF-ABI + ;
: ROFFER.SIZE   ( offer -- a ) _ROF-SIZE + ;
: ROFFER.RID    ( offer -- rid ) _ROF-RID + ;
: ROFFER.POOL   ( offer -- a ) _ROF-POOL + ;
