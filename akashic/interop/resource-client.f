: RCLI-DESCRIBE  ( principal request client -- status )
    _RCIC-C ! _RCIC-Q ! _RCIC-P !
    _RCIC-P @ _RCIC-Q @ _RCIC-C @ RCLI-DESCRIBE-PREPARE
        ?DUP IF EXIT THEN
    _RCIC-Q @ _RCIC-C @ RCLI-DESCRIBE-DISPATCH ;

