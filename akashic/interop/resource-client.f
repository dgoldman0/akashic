    _RCIC-C ! _RCIC-Q ! _RCIC-P ! _RCIC-L !
    _RCIC-L @ _RCIC-P @ _RCIC-Q @ _RCIC-C @ RCLI-SNAPSHOT-PREPARE
        ?DUP IF EXIT THEN
    _RCIC-L @ _RCIC-Q @ _RCIC-C @ RCLI-SNAPSHOT-DISPATCH ;

: RCLI-REPLACE-CALL  ( locator content-a content-u digest principal request client -- status )
    \ Consequential/Agent callers normally use PREPARE, stamp authority,
    \ then DISPATCH.  This convenience path still traverses bus policy.
    _RCIC-C ! _RCIC-Q ! _RCIC-P ! _RCIC-D !
    _RCIC-U ! _RCIC-A ! _RCIC-L !
    _RCIC-L @ _RCIC-A @ _RCIC-U @ _RCIC-D @
        _RCIC-P @ _RCIC-Q @ _RCIC-C @ RCLI-REPLACE-PREPARE
        ?DUP IF EXIT THEN
    _RCIC-L @ _RCIC-Q @ _RCIC-C @ RCLI-REPLACE-DISPATCH ;

VARIABLE _RCIF-C
VARIABLE _RCIF-S

: RCLI-FINI  ( client -- status )
    DUP _RCIF-C ! RCLI-VALID? 0= IF RCLI-S-INVALID EXIT THEN
    _RCIF-C @ RCLI.BIND _RCIF-C @ RCLI.ACQUISITION @ RACQ-DETACH
        DUP _RCIF-S !
    _RCIF-S @ RACQ-S-OK = IF
        DROP _RCIF-C @ RCLI-CLEAR CBUS-S-OK
    ELSE
        DROP RCLI-S-RELEASE
    THEN ;

