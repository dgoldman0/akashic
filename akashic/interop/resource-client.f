    _RCIP-P @ _RCIP-Q @ _RCIP-C @ RCLI.REPLACE @ _RCIP-C @
        _RCLI-PREPARE DUP IF EXIT THEN DROP
    _RCIS-L @ _RCIR-A @ _RCIR-U @ _RCIR-D @ _RCIP-Q @ CBR.ARGS
        RCON-REPLACE-ARGS!
        RCON-S-OK = IF CBUS-S-OK ELSE RCLI-S-INVALID THEN ;

CREATE _RCID-REF RREF-SIZE ALLOT
