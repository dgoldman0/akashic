VARIABLE _RCID-S

: _RCLI-DISPATCH  ( request expected-cap client -- status )
    _RCID-C ! >R _RCID-Q !
    _RCID-C @ RCLI-VALID? 0= IF R> DROP RCLI-S-INVALID EXIT THEN
    _RCID-Q @ 0= IF R> DROP RCLI-S-INVALID EXIT THEN
    _RCID-Q @ CBR.CAP @ R> <> IF RCLI-S-INVALID EXIT THEN
    _RCID-Q @ _RCID-C @ RCLI.BUS @ CBUS-DISPATCH ;

: RCLI-DESCRIBE-DISPATCH  ( request client -- status )
    DUP _RCID-C ! RCLI.DESCRIBE @ _RCID-C @ _RCLI-DISPATCH
        DUP IF EXIT THEN DROP
    _RCID-Q @ CBR.RESULT RCON-DESCRIBE-RESULT? 0= IF
        RCLI-S-RESULT-TYPE EXIT
    THEN
    S" resource" _RCID-Q @ CBR.RESULT CV-MAP-FIND
        _RCID-REF IRES-RREF@ IRES-S-OK <> IF RCLI-S-RESULT-TYPE EXIT THEN
    _RCID-REF RREF.ID _RCID-C @ RCLI.BIND LBIND.RESOURCE-ID
        RID= 0= IF RCLI-S-RESULT-MISMATCH EXIT THEN
    S" owner" _RCID-Q @ CBR.RESULT CV-MAP-FIND DUP CV-DATA@ SWAP CV-LEN@
    _RCID-C @ RCLI.ACQUISITION @ RACQ.RESULT-TOKEN RACQ.TOKEN-ROOT @
        RACQ-ROOT-OWNER$ STR-STR= 0= IF RCLI-S-RESULT-MISMATCH EXIT THEN
    CBUS-S-OK ;

: RCLI-SNAPSHOT-DISPATCH  ( locator request client -- status )
    _RCID-C ! _RCID-Q ! _RCIS-L !
    _RCID-Q @ _RCID-C @ RCLI.SNAPSHOT @ _RCID-C @ _RCLI-DISPATCH
        DUP IF EXIT THEN DROP
    _RCIS-L @ _RCID-Q @ CBR.RESULT RCON-SNAPSHOT-RESULT? IF
        CBUS-S-OK
    ELSE
        RCLI-S-RESULT-MISMATCH
    THEN ;

: _RCLI-REPLACE-DIGEST!  ( -- flag )
    S" content_digest" _RCID-Q @ CBR.ARGS CV-MAP-FIND DUP 0= IF
        DROP 0 EXIT
    THEN
    DUP CV-TYPE@ CV-T-BYTES <> IF DROP 0 EXIT THEN
    DUP CV-LEN@ SHA3-256-LEN <> IF DROP 0 EXIT THEN
    CV-DATA@ DUP 0= IF DROP 0 EXIT THEN
    _RCID-CONTENT-DIGEST SHA3-256-LEN MOVE -1 ;

: RCLI-REPLACE-DISPATCH  ( old-locator request client -- status )
    _RCID-C ! _RCID-Q ! _RCIS-L !
    _RCID-C @ RCLI-REPLACE? 0= IF RCLI-S-READONLY EXIT THEN
    _RCLI-REPLACE-DIGEST! 0= IF RCLI-S-INVALID EXIT THEN
    _RCID-Q @ _RCID-C @ RCLI.REPLACE @ _RCID-C @ _RCLI-DISPATCH
        DUP IF EXIT THEN DROP
    _RCIS-L @ _RCID-CONTENT-DIGEST _RCID-Q @ CBR.RESULT
        RCON-REPLACE-RESULT? 0= IF
        RCLI-S-RESULT-MISMATCH EXIT
    THEN
    _RCID-Q @ _RCID-C @ RCLI.CONTEXT @ _RCID-C @ RCLI.BIND LBIND-ADVANCE
        LBIND-S-OK = IF CBUS-S-OK ELSE RCLI-S-STALE THEN ;

