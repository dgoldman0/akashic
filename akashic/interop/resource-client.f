VARIABLE _RCIV-R
VARIABLE _RCIV-I
VARIABLE _RCIV-D

: RCLI-VALID?  ( client -- flag )
    DUP 0= IF DROP 0 EXIT THEN _RCIV-C !
    _RCIV-C @ RCLI.MAGIC @ RCLI-MAGIC <> IF 0 EXIT THEN
    _RCIV-C @ RCLI.ABI @ RCLI-ABI-VERSION <> IF 0 EXIT THEN
    _RCIV-C @ RCLI.SIZE @ RCLI-SIZE <> IF 0 EXIT THEN
    _RCIV-C @ RCLI.FLAGS @ RCLI-F-REPLACE INVERT AND IF 0 EXIT THEN
    _RCIV-C @ _RCI-RESERVED + @ IF 0 EXIT THEN
    _RCIV-C @ RCLI.CONTEXT @ CTX-VALID? 0= IF 0 EXIT THEN
    _RCIV-C @ RCLI.CONTEXT @ _RCIV-C @ RCLI.BIND
        LBIND-CONTEXT? 0= IF 0 EXIT THEN
    _RCIV-C @ RCLI.BUS @ _RCIV-R !
    _RCIV-R @ 0= IF 0 EXIT THEN
    _RCIV-R @ CBUS.REGISTRY @ 0= IF 0 EXIT THEN
    _RCIV-C @ RCLI.CONTEXT @ CTX.QUEUE @ _RCIV-R @ <> IF 0 EXIT THEN
    _RCIV-C @ RCLI.ACQUISITION @ _RCIV-R !
    _RCIV-R @ RACQ-RESULT-VALID? 0= IF 0 EXIT THEN
    _RCIV-R @ RACQ.RESULT-REF RREF.ID
        _RCIV-C @ RCLI.BIND LBIND.RESOURCE-ID
        RID= 0= IF 0 EXIT THEN
    _RCIV-C @ RCLI.OWNER @ _RCIV-I !
    _RCIV-I @ 0= IF 0 EXIT THEN
    _RCIV-I @ CINST.ID @ _RCIV-C @ RCLI.BIND LBIND.TARGET-ID @ <> IF
        0 EXIT
    THEN
    _RCIV-I @ CINST.GENERATION @
        _RCIV-C @ RCLI.BIND LBIND.TARGET-GEN @ <> IF
        0 EXIT
    THEN
    _RCIV-I @ CINST-DESC DUP COMP-CAPS-VALID? 0= IF DROP 0 EXIT THEN
        _RCIV-D !
    S" resource.describe" _RCIV-D @ COMP-CAP-FIND
        _RCIV-C @ RCLI.DESCRIBE @ <> IF 0 EXIT THEN
    _RCIV-C @ RCLI.DESCRIBE @ DUP 0= IF DROP 0 EXIT THEN
        CAP-K-RESOURCE CAP-E-OBSERVE
        RCON-DESCRIBE-IN-SCHEMA RCON-DESCRIBE-OUT-SCHEMA
        _RCLI-CAP-EXACT? 0= IF 0 EXIT THEN
    S" resource.snapshot" _RCIV-D @ COMP-CAP-FIND
        _RCIV-C @ RCLI.SNAPSHOT @ <> IF 0 EXIT THEN
    _RCIV-C @ RCLI.SNAPSHOT @ DUP 0= IF DROP 0 EXIT THEN
        CAP-K-RESOURCE CAP-E-OBSERVE
        RCON-SNAPSHOT-IN-SCHEMA RCON-SNAPSHOT-OUT-SCHEMA
        _RCLI-CAP-EXACT? 0= IF 0 EXIT THEN
    _RCIV-C @ RCLI-REPLACE? IF
        S" resource.replace" _RCIV-D @ COMP-CAP-FIND
            _RCIV-C @ RCLI.REPLACE @ <> IF 0 EXIT THEN
        _RCIV-C @ RCLI.REPLACE @ DUP 0= IF DROP 0 EXIT THEN
            CAP-K-COMMAND CAP-E-MUTATE CAP-E-PERSIST OR
            RCON-REPLACE-IN-SCHEMA RCON-REPLACE-OUT-SCHEMA
            _RCLI-CAP-EXACT? 0= IF 0 EXIT THEN
    ELSE
        _RCIV-C @ RCLI.REPLACE @ IF 0 EXIT THEN
    THEN
    -1 ;

VARIABLE _RCII-O
VARIABLE _RCII-B
VARIABLE _RCII-C
VARIABLE _RCII-BUS
VARIABLE _RCII-I
VARIABLE _RCII-D
VARIABLE _RCII-CAP

: RCLI-INIT  ( acquisition binding context bus client -- status )
    _RCII-O ! _RCII-BUS ! _RCII-C ! _RCII-B ! _RCII-I !
    _RCII-O @ 0= IF RCLI-S-INVALID EXIT THEN
    _RCII-O @ RCLI-CLEAR
    _RCII-I @ RACQ-RESULT-VALID? 0= IF RCLI-S-INVALID EXIT THEN
    _RCII-B @ LBIND-VALID? 0= _RCII-C @ CTX-VALID? 0= OR IF
        RCLI-S-INVALID EXIT
    THEN
    _RCII-C @ _RCII-B @ LBIND-CONTEXT? 0= IF RCLI-S-INVALID EXIT THEN
    _RCII-BUS @ 0= IF RCLI-S-INVALID EXIT THEN
    _RCII-C @ CTX.QUEUE @ _RCII-BUS @ <> IF RCLI-S-INVALID EXIT THEN
    _RCII-I @ RACQ.RESULT-REF RREF.ID _RCII-B @ LBIND.RESOURCE-ID
        RID= 0= IF RCLI-S-INVALID EXIT THEN
    _RCII-B @ LBIND.TARGET-ID @ _RCII-B @ LBIND.TARGET-GEN @
        _RCII-BUS @ CBUS.REGISTRY @ CREG-INST-FIND DUP 0= IF
        DROP RCLI-S-INVALID EXIT
    THEN _RCII-D !
    _RCII-D @ CINST-DESC DUP COMP-CAPS-VALID? 0= IF
        DROP RCLI-S-INVALID EXIT
    THEN _RCII-CAP !
    S" resource.describe" _RCII-CAP @ COMP-CAP-FIND DUP 0= IF
        DROP RCLI-S-NO-DESCRIBE EXIT
    THEN
    DUP CAP-K-RESOURCE CAP-E-OBSERVE
        RCON-DESCRIBE-IN-SCHEMA RCON-DESCRIBE-OUT-SCHEMA
        _RCLI-CAP-EXACT? 0= IF
        DROP RCLI-S-NO-DESCRIBE EXIT
    THEN _RCII-O @ RCLI.DESCRIBE !
    S" resource.snapshot" _RCII-CAP @ COMP-CAP-FIND DUP 0= IF
        DROP RCLI-S-NO-SNAPSHOT EXIT
    THEN
    DUP CAP-K-RESOURCE CAP-E-OBSERVE
        RCON-SNAPSHOT-IN-SCHEMA RCON-SNAPSHOT-OUT-SCHEMA
        _RCLI-CAP-EXACT? 0= IF
        DROP RCLI-S-NO-SNAPSHOT EXIT
    THEN _RCII-O @ RCLI.SNAPSHOT !
    S" resource.replace" _RCII-CAP @ COMP-CAP-FIND ?DUP IF
        DUP CAP-K-COMMAND CAP-E-MUTATE CAP-E-PERSIST OR
            RCON-REPLACE-IN-SCHEMA RCON-REPLACE-OUT-SCHEMA
            _RCLI-CAP-EXACT? 0= IF DROP RCLI-S-INVALID EXIT THEN
        _RCII-O @ RCLI.REPLACE !
        RCLI-F-REPLACE _RCII-O @ RCLI.FLAGS !
    THEN
    _RCII-C @ _RCII-O @ RCLI.CONTEXT !
    _RCII-BUS @ _RCII-O @ RCLI.BUS !
    _RCII-I @ _RCII-O @ RCLI.ACQUISITION !
    _RCII-D @ _RCII-O @ RCLI.OWNER !
    _RCII-B @ _RCII-O @ RCLI.BIND LBIND-COPY
        LBIND-S-OK <> IF _RCII-O @ RCLI-CLEAR RCLI-S-INVALID EXIT THEN
    _RCII-O @ RCLI-VALID? IF CBUS-S-OK ELSE RCLI-S-INVALID THEN ;

VARIABLE _RCIP-P
VARIABLE _RCIP-Q
VARIABLE _RCIP-C
VARIABLE _RCIP-CAP

: _RCLI-PREPARE  ( principal request cap client -- status )
    _RCIP-C ! _RCIP-CAP ! _RCIP-Q ! _RCIP-P !
    _RCIP-C @ RCLI-VALID? 0= IF RCLI-S-INVALID EXIT THEN
    _RCIP-Q @ 0= IF RCLI-S-INVALID EXIT THEN
    _RCIP-P @ CPRINC-USER < _RCIP-P @ CPRINC-AGENT > OR IF
        RCLI-S-INVALID EXIT
    THEN
    _RCIP-C @ RCLI.BIND _RCIP-C @ RCLI.CONTEXT @ _RCIP-Q @
        LBIND-REQUEST! LBIND-S-OK <> IF RCLI-S-STALE EXIT THEN
    _RCIP-P @ _RCIP-Q @ CBR.PRINCIPAL !
    _RCIP-CAP @ _RCIP-Q @ CBR.CAP !
    CBUS-S-OK ;

: RCLI-DESCRIBE-PREPARE  ( principal request client -- status )
    DUP RCLI.DESCRIBE @ SWAP _RCLI-PREPARE DUP IF EXIT THEN
    DROP _RCIP-Q @ CBR.ARGS CV-NULL! CBUS-S-OK ;

VARIABLE _RCIS-L

: RCLI-SNAPSHOT-PREPARE  ( locator principal request client -- status )
    _RCIP-C ! _RCIP-Q ! _RCIP-P ! _RCIS-L !
    _RCIS-L @ QLOC-VALID? 0= IF RCLI-S-INVALID EXIT THEN
    _RCIS-L @ QLOC.REF RREF.ID _RCIP-C @ RCLI.BIND LBIND.RESOURCE-ID
        RID= 0= IF RCLI-S-RESULT-MISMATCH EXIT THEN
    _RCIP-P @ _RCIP-Q @ _RCIP-C @ RCLI.SNAPSHOT @ _RCIP-C @
        _RCLI-PREPARE DUP IF EXIT THEN DROP
    _RCIS-L @ _RCIP-Q @ CBR.ARGS RCON-SNAPSHOT-ARGS!
        RCON-S-OK = IF CBUS-S-OK ELSE RCLI-S-INVALID THEN ;

