    _RCIP-C ! _RCIP-Q ! _RCIP-P ! _RCIR-D ! _RCIR-U ! _RCIR-A ! _RCIS-L !
    _RCIP-C @ RCLI-REPLACE? 0= IF RCLI-S-READONLY EXIT THEN
