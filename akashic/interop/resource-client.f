\ =====================================================================
\  resource-client.f - Activation-local typed common-resource client
\ =====================================================================
\  Descriptor discovery caches no authority.  Every operation is stamped
\  from an LBIND and dispatched through the ordinary request bus, where
\  policy/approval/authority is enforced.  Advanced callers may PREPARE,
\  add a reviewed invocation handle/mandate, then call the typed DISPATCH.
\ =====================================================================

