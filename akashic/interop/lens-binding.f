REQUIRE ../runtime/practice-head.f
REQUIRE ../concurrency/guard.f
REQUIRE request-bus.f

0 CONSTANT LBIND-S-OK
1 CONSTANT LBIND-S-INVALID
2 CONSTANT LBIND-S-NOT-FOUND
3 CONSTANT LBIND-S-STALE-INSTANCE
4 CONSTANT LBIND-S-STALE-REVISION
5 CONSTANT LBIND-S-STALE-EPOCH
6 CONSTANT LBIND-S-MISMATCH
7 CONSTANT LBIND-S-BUSY

0x444E424C CONSTANT LBIND-MAGIC       \ "LBND"
1          CONSTANT LBIND-ABI-VERSION

\ Pointer-free version-1 lens binding, 19 cells / 152 bytes.
  0 CONSTANT _LBD-MAGIC
  8 CONSTANT _LBD-ABI
 16 CONSTANT _LBD-SIZE
 24 CONSTANT _LBD-EPOCH
 32 CONSTANT _LBD-CONTEXT-ID
 40 CONSTANT _LBD-CONTEXT-GEN
 48 CONSTANT _LBD-PRACTICE-ID          \ RID-SIZE bytes
 80 CONSTANT _LBD-RESOURCE-ID          \ RID-SIZE bytes
112 CONSTANT _LBD-TARGET-ID
120 CONSTANT _LBD-TARGET-GEN
128 CONSTANT _LBD-REVISION
136 CONSTANT _LBD-FLAGS
144 CONSTANT _LBD-RESERVED
152 CONSTANT LBIND-SIZE

: LBIND.MAGIC        ( bind -- a ) _LBD-MAGIC + ;
: LBIND.ABI          ( bind -- a ) _LBD-ABI + ;
: LBIND.SIZE         ( bind -- a ) _LBD-SIZE + ;
: LBIND.EPOCH        ( bind -- a ) _LBD-EPOCH + ;
: LBIND.CONTEXT-ID   ( bind -- a ) _LBD-CONTEXT-ID + ;
: LBIND.CONTEXT-GEN  ( bind -- a ) _LBD-CONTEXT-GEN + ;
: LBIND.PRACTICE-ID  ( bind -- id ) _LBD-PRACTICE-ID + ;
: LBIND.RESOURCE-ID  ( bind -- id ) _LBD-RESOURCE-ID + ;
: LBIND.TARGET-ID    ( bind -- a ) _LBD-TARGET-ID + ;
: LBIND.TARGET-GEN   ( bind -- a ) _LBD-TARGET-GEN + ;
: LBIND.REVISION     ( bind -- a ) _LBD-REVISION + ;
: LBIND.FLAGS        ( bind -- a ) _LBD-FLAGS + ;

: LBIND-INIT  ( binding -- )
    DUP LBIND-SIZE 0 FILL
    LBIND-MAGIC OVER LBIND.MAGIC !
    LBIND-ABI-VERSION OVER LBIND.ABI !
    LBIND-SIZE SWAP LBIND.SIZE ! ;

: LBIND-CLEAR  ( binding -- ) LBIND-INIT ;

: LBIND-VALID?  ( binding -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP LBIND.MAGIC @ LBIND-MAGIC =
    OVER LBIND.ABI @ LBIND-ABI-VERSION = AND
    OVER LBIND.SIZE @ LBIND-SIZE >= AND
    OVER LBIND.EPOCH @ 0> AND
    OVER LBIND.CONTEXT-ID @ 0> AND
    OVER LBIND.CONTEXT-GEN @ 0> AND
    OVER LBIND.PRACTICE-ID RID-PRESENT? AND
    OVER LBIND.RESOURCE-ID RID-PRESENT? AND
    OVER LBIND.TARGET-ID @ 0> AND
    OVER LBIND.TARGET-GEN @ 0> AND
    OVER LBIND.REVISION @ 0> AND
    SWAP LBIND.FLAGS @ 0= AND ;

: LBIND-COPY  ( source destination -- status )
    DUP 0= IF 2DROP LBIND-S-INVALID EXIT THEN
    OVER LBIND-VALID? 0= IF NIP LBIND-INIT LBIND-S-INVALID EXIT THEN
    2DUP = IF 2DROP LBIND-S-OK EXIT THEN
    LBIND-SIZE CMOVE LBIND-S-OK ;

VARIABLE _LBCM-C
VARIABLE _LBCM-B
VARIABLE _LBCM-H

: LBIND-CONTEXT?  ( context binding -- flag )
    _LBCM-B ! _LBCM-C !
    _LBCM-B @ LBIND-VALID? 0= IF 0 EXIT THEN
    _LBCM-C @ CTX-VALID? 0= IF 0 EXIT THEN
    _LBCM-C @ CTX.PRACTICE @ DUP 0= IF DROP 0 EXIT THEN _LBCM-H !
    _LBCM-H @ PHEAD-VALID? 0= IF 0 EXIT THEN
    _LBCM-C @ CTX.EPOCH @ _LBCM-B @ LBIND.EPOCH @ =
    _LBCM-C @ CTX.ID @ _LBCM-B @ LBIND.CONTEXT-ID @ = AND
    _LBCM-C @ CTX.GENERATION @ _LBCM-B @ LBIND.CONTEXT-GEN @ = AND
    _LBCM-H @ PHEAD.ID _LBCM-B @ LBIND.PRACTICE-ID RID= AND ;

: _LBIND-RREG-STATUS  ( rreg-status -- lbind-status )
    CASE
        RREG-S-OK OF LBIND-S-OK ENDOF
        RREG-S-NOT-FOUND OF LBIND-S-NOT-FOUND ENDOF
        RREG-S-STALE-INSTANCE OF LBIND-S-STALE-INSTANCE ENDOF
        RREG-S-STALE-REVISION OF LBIND-S-STALE-REVISION ENDOF
        RREG-S-STALE-EPOCH OF LBIND-S-STALE-EPOCH ENDOF
        LBIND-S-INVALID SWAP
    ENDCASE ;

VARIABLE _LBA-R
VARIABLE _LBA-C
VARIABLE _LBA-RG
VARIABLE _LBA-B
VARIABLE _LBA-H
VARIABLE _LBA-I
VARIABLE _LBA-S

: LBIND-ATTACH  ( reference context resource-registry binding -- status )
    _LBA-B ! _LBA-RG ! _LBA-C ! _LBA-R !
    _LBA-B @ 0= IF LBIND-S-INVALID EXIT THEN
    _LBA-B @ LBIND-INIT
    _LBA-R @ RREF-VALID? 0= _LBA-C @ CTX-VALID? 0= OR IF
        LBIND-S-INVALID EXIT
    THEN
    _LBA-C @ CTX.PRACTICE @ DUP 0= IF DROP LBIND-S-INVALID EXIT THEN
        _LBA-H !
    _LBA-H @ PHEAD-VALID? 0= IF LBIND-S-INVALID EXIT THEN
    _LBA-R @ _LBA-C @ _LBA-RG @ RREG-RESOLVE DUP _LBA-S !
    DUP IF
        2DROP _LBA-S @ _LBIND-RREG-STATUS EXIT
    THEN
    DROP _LBA-I !
    _LBA-C @ CTX.EPOCH @ _LBA-B @ LBIND.EPOCH !
    _LBA-C @ CTX.ID @ _LBA-B @ LBIND.CONTEXT-ID !
    _LBA-C @ CTX.GENERATION @ _LBA-B @ LBIND.CONTEXT-GEN !
    _LBA-H @ PHEAD.ID _LBA-B @ LBIND.PRACTICE-ID RID-COPY
    _LBA-R @ RREF.ID _LBA-B @ LBIND.RESOURCE-ID RID-COPY
    _LBA-I @ CINST.ID @ _LBA-B @ LBIND.TARGET-ID !
    _LBA-I @ CINST.GENERATION @ _LBA-B @ LBIND.TARGET-GEN !
    _LBA-I @ CINST.REVISION @ _LBA-B @ LBIND.REVISION !
    _LBA-B @ LBIND-VALID? IF LBIND-S-OK ELSE LBIND-S-INVALID THEN ;

VARIABLE _LBRF-B
VARIABLE _LBRF-R

: LBIND-REF  ( binding reference -- status )
    _LBRF-R ! _LBRF-B !
    _LBRF-R @ 0= IF LBIND-S-INVALID EXIT THEN
    _LBRF-R @ RREF-INIT
    _LBRF-B @ LBIND-VALID? 0= IF LBIND-S-INVALID EXIT THEN
    _LBRF-B @ LBIND.RESOURCE-ID _LBRF-R @ RREF.ID RID-COPY
    _LBRF-B @ LBIND.REVISION @ _LBRF-R @ RREF.REVISION !
    LBIND-S-OK ;

VARIABLE _LBRQ-B
VARIABLE _LBRQ-C
VARIABLE _LBRQ-Q

: _LBIND-REQUEST-RESET  ( request -- flag )
    DUP _CBR-LIFECYCLE-RESET 0= IF DROP 0 EXIT THEN
    \ Reuse always begins from a non-terminal, non-authorized envelope.  In
    \ particular, stale STATUS/ACTUAL-REV must never make ADVANCE succeed.
    DUP CBR.RESULT CV-FREE
    DUP CBR-ERROR-CLEAR
    DUP CBR.HANDLE IH-INIT
    DUP CBR.TURN @ ?DUP IF PTURN-INIT THEN
    DUP CBR.INVOCATION-ID RID-CLEAR
    DUP CBR.MANDATE-ID RID-CLEAR
    DUP CBR.PRACTICE-ID RID-CLEAR
    DUP CBR.RESOURCE-ID RID-CLEAR
    DUP CBR-ARGS-SEAL-CLEAR
    DUP 0 SWAP CBR.ID !
    DUP 0 SWAP CBR.CONTEXT-ID !
    DUP 0 SWAP CBR.CONTEXT-GEN !
    DUP 0 SWAP CBR.EPOCH !
    DUP 0 SWAP CBR.TARGET-ID !
    DUP 0 SWAP CBR.TARGET-GEN !
    DUP 0 SWAP CBR.EXPECT-REV !
    DUP CBUS-S-INVALID SWAP CBR.STATUS !
    DUP 0 SWAP CBR.ACTUAL-REV !
    DUP 0 SWAP CBR.START-MS !
    0 SWAP CBR.END-MS !
    -1 ;

: _LBIND-REQUEST-LOCKED  ( -- status )
    _LBRQ-Q @ _LBIND-REQUEST-RESET 0= IF LBIND-S-BUSY EXIT THEN
    _LBRQ-C @ _LBRQ-B @ LBIND-CONTEXT? 0= IF
        LBIND-S-STALE-EPOCH EXIT
    THEN
    _LBRQ-B @ LBIND.CONTEXT-ID @ _LBRQ-Q @ CBR.CONTEXT-ID !
    _LBRQ-B @ LBIND.CONTEXT-GEN @ _LBRQ-Q @ CBR.CONTEXT-GEN !
    _LBRQ-B @ LBIND.EPOCH @ _LBRQ-Q @ CBR.EPOCH !
    _LBRQ-B @ LBIND.PRACTICE-ID _LBRQ-Q @ CBR.PRACTICE-ID RID-COPY
    _LBRQ-B @ LBIND.RESOURCE-ID _LBRQ-Q @ CBR.RESOURCE-ID RID-COPY
    _LBRQ-B @ LBIND.TARGET-ID @ _LBRQ-Q @ CBR.TARGET-ID !
    _LBRQ-B @ LBIND.TARGET-GEN @ _LBRQ-Q @ CBR.TARGET-GEN !
    _LBRQ-B @ LBIND.REVISION @ _LBRQ-Q @ CBR.EXPECT-REV !
    LBIND-S-OK ;

: LBIND-REQUEST!  ( binding context request -- status )
    _LBRQ-Q ! _LBRQ-C ! _LBRQ-B !
    _LBRQ-Q @ 0= IF LBIND-S-INVALID EXIT THEN
    ['] _LBIND-REQUEST-LOCKED WITH-CBR-LIFECYCLE ;

VARIABLE _LBAD-Q
VARIABLE _LBAD-C
VARIABLE _LBAD-B

: LBIND-ADVANCE  ( request context binding -- status )
    _LBAD-B ! _LBAD-C ! _LBAD-Q !
    _LBAD-Q @ 0= IF LBIND-S-INVALID EXIT THEN
    _LBAD-C @ _LBAD-B @ LBIND-CONTEXT? 0= IF
        LBIND-S-STALE-EPOCH EXIT
    THEN
    _LBAD-Q @ CBR-LIFECYCLE-BUSY? IF LBIND-S-BUSY EXIT THEN
    _LBAD-Q @ CBR.STATUS @ CBUS-S-OK <> IF LBIND-S-MISMATCH EXIT THEN
    _LBAD-Q @ CBR.CONTEXT-ID @ _LBAD-B @ LBIND.CONTEXT-ID @ <>
    _LBAD-Q @ CBR.CONTEXT-GEN @ _LBAD-B @ LBIND.CONTEXT-GEN @ <> OR
    _LBAD-Q @ CBR.EPOCH @ _LBAD-B @ LBIND.EPOCH @ <> OR IF
        LBIND-S-MISMATCH EXIT
    THEN
    _LBAD-Q @ CBR.PRACTICE-ID _LBAD-B @ LBIND.PRACTICE-ID RID= 0= IF
        LBIND-S-MISMATCH EXIT
    THEN
    _LBAD-Q @ CBR.RESOURCE-ID _LBAD-B @ LBIND.RESOURCE-ID RID= 0= IF
        LBIND-S-MISMATCH EXIT
    THEN
    _LBAD-Q @ CBR.TARGET-ID @ _LBAD-B @ LBIND.TARGET-ID @ <>
    _LBAD-Q @ CBR.TARGET-GEN @ _LBAD-B @ LBIND.TARGET-GEN @ <> OR IF
        LBIND-S-MISMATCH EXIT
    THEN
    _LBAD-Q @ CBR.EXPECT-REV @ _LBAD-B @ LBIND.REVISION @ <> IF
        LBIND-S-STALE-REVISION EXIT
    THEN
    _LBAD-Q @ CBR.ACTUAL-REV @ DUP 0> 0= IF
        DROP LBIND-S-MISMATCH EXIT
    THEN
    DUP _LBAD-B @ LBIND.REVISION @ < IF
        DROP LBIND-S-STALE-REVISION EXIT
    THEN
    _LBAD-B @ LBIND.REVISION !
    LBIND-S-OK ;

