VARIABLE _AHR-GRANT

: _AHT-HANDLE-ENTRY  ( handle table -- grant status )
    _AHR-TABLE ! _AHR-TOKEN !
    _AHR-TOKEN @ IH-VALID? 0= IF 0 AUTH-S-INVALID EXIT THEN
    _AHR-TOKEN @ IH.EPOCH @ _AHR-TABLE @ AHT.EPOCH @ <>
    _AHR-TOKEN @ IH.TABLE-ID @ _AHR-TABLE @ AHT.TABLE-ID @ <> OR
    IF 0 AUTH-S-STALE-HANDLE EXIT THEN
    _AHR-TOKEN @ IH.SLOT @ _AHR-TABLE @ AHT-GRANT-NTH DUP 0= IF
        AUTH-S-STALE-HANDLE EXIT
    THEN _AHR-GRANT !
    _AHR-TOKEN @ IH.GENERATION @ _AHR-GRANT @ AGR.GENERATION @ <>
    IF 0 AUTH-S-STALE-HANDLE EXIT THEN
    _AHR-TOKEN @ IH.EPOCH @ _AHR-TOKEN @ IH.TABLE-ID @
    _AHR-TOKEN @ IH.SLOT @ _AHR-TOKEN @ IH.GENERATION @
    _AHS-CHECK _AHR-TABLE @ _AHT-SEAL!
    _AHS-CHECK _AHR-TOKEN @ IH.SEAL SHA3-256-COMPARE 0=
    IF 0 AUTH-S-STALE-HANDLE EXIT THEN
    _AHR-GRANT @ AUTH-S-OK ;

