VARIABLE _AHV-GRANT

: AHT-RESOLVE  ( now binding handle table -- grant status )
    _AHV-TABLE ! _AHV-TOKEN ! _AHV-BIND ! _AHV-NOW !
    _AHV-BIND @ ABIND-VALID? 0= IF 0 AUTH-S-INVALID EXIT THEN
    _AHV-TOKEN @ _AHV-TABLE @ _AHT-HANDLE-ENTRY
    DUP IF EXIT THEN DROP _AHV-GRANT !
    _AHV-GRANT @ AGR.STATE @ CASE
        AGR-S-REVOKED OF 0 AUTH-S-REVOKED EXIT ENDOF
        AGR-S-CONSUMED OF 0 AUTH-S-CONSUMED EXIT ENDOF
        AGR-S-ACTIVE OF ENDOF
        0 AUTH-S-STALE-HANDLE EXIT
    ENDCASE
    _AHV-GRANT @ AGR.REMAINING @ 0> 0= IF
        0 AUTH-S-CONSUMED EXIT
    THEN
    _AHV-GRANT @ AGR.NOT-BEFORE @ ?DUP IF
        _AHV-NOW @ > IF 0 AUTH-S-DENIED EXIT THEN
    THEN
    _AHV-GRANT @ AGR.EXPIRES @ ?DUP IF
        _AHV-NOW @ < IF 0 AUTH-S-EXPIRED EXIT THEN
    THEN
    _AHV-BIND @ _AHV-GRANT @ _AGR-BINDING-MATCH? 0= IF
        0 AUTH-S-MISMATCH EXIT
    THEN
    _AHV-GRANT @ AUTH-S-OK ;

: AHT-CONSUME  ( now binding handle table -- grant status )
    AHT-RESOLVE DUP IF EXIT THEN
    DROP DUP -1 SWAP AGR.REMAINING +!
    DUP AGR.REMAINING @ 0= IF
        AGR-S-CONSUMED OVER AGR.STATE !
        -1 _AHV-TABLE @ AHT.COUNT +!
    THEN
    AUTH-S-OK ;

VARIABLE _AHRV-TOKEN
VARIABLE _AHRV-TABLE

: AHT-REVOKE  ( handle table -- status )
    _AHRV-TABLE ! _AHRV-TOKEN !
    _AHRV-TOKEN @ _AHRV-TABLE @ _AHT-HANDLE-ENTRY
    DUP IF NIP EXIT THEN DROP
    DUP AGR.STATE @ AGR-S-ACTIVE = IF
        AGR-S-REVOKED SWAP AGR.STATE !
        -1 _AHRV-TABLE @ AHT.COUNT +!
        AUTH-S-OK
    ELSE
        DROP AUTH-S-REVOKED
    THEN ;

