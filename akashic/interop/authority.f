: ABIND-OP!  ( addr len binding -- status )
    >R DUP AUTH-OP-ID-MAX > IF 2DROP R> DROP AUTH-S-INVALID EXIT THEN
    DUP 0< IF 2DROP R> DROP AUTH-S-INVALID EXIT THEN
    DUP R@ ABIND.OP-U !
    R@ ABIND.OP AUTH-OP-ID-MAX 0 FILL
    R@ ABIND.OP SWAP MOVE
    R> DROP AUTH-S-OK ;

: ABIND-OP@  ( binding -- addr len )
    DUP ABIND.OP SWAP ABIND.OP-U @ ;

: ABIND-VALID?  ( binding -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP ABIND.EPOCH @ 0<>
    OVER ABIND.PRINCIPAL @ 0<> AND
    OVER ABIND.TARGET-ID @ 0<> AND
    OVER ABIND.TARGET-GEN @ 0<> AND
    OVER ABIND.OP-U @ DUP 0> SWAP AUTH-OP-ID-MAX <= AND AND
    OVER ABIND.INVOCATION-ID RID-ZERO? 0= AND
    SWAP DROP ;

