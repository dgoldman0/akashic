\ =====================================================================
\  authority.f - Activation-local grants and opaque invocation handles
\ =====================================================================
\  Operation descriptors describe what can be called.  This module is
\  deliberately separate: an active grant in a protected owner table is
\  what authorizes one particular invocation.
\
\  Tables are owner-serialized.  Tokens are safe to pass through less
\  trusted Contexts, but the table and its secret must remain behind the
\  restricted wordset/owner boundary.  Portable delegation needs a signed
\  grant format; these activation-local records are never persisted.
\ =====================================================================

