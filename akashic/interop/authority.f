: _AHT-FIND-FREE  ( table -- slot | -1 )
    AUTH-TABLE-CAPACITY 0 DO
        I OVER AHT-GRANT-NTH AGR.STATE @ AGR-S-ACTIVE <> IF
            DROP I UNLOOP EXIT
        THEN
    LOOP
    DROP -1 ;

: _AHT-GRANT-ID!  ( grant table -- )
    >R
    DUP AGR.ID RID-CLEAR
    R@ AHT.TABLE-ID @ OVER AGR.ID !
    R@ AHT.NEXT-ID @ OVER AGR.ID 8 + !
    R@ AHT.EPOCH @ OVER AGR.ID 16 + !
    DUP AGR.GENERATION @ SWAP AGR.ID 24 + !
    1 R> AHT.NEXT-ID +! ;

: AHT-ISSUE  ( grant handle table -- status )
    _AHI-TABLE ! _AHI-TOKEN ! _AHI-GRANT !
    _AHI-GRANT @ AGR-VALID? 0= IF AUTH-S-INVALID EXIT THEN
    _AHI-GRANT @ AGR.EPOCH @ _AHI-TABLE @ AHT.EPOCH @ <>
    IF AUTH-S-MISMATCH EXIT THEN
    _AHI-TABLE @ _AHT-FIND-FREE DUP 0< IF
        DROP AUTH-S-FULL EXIT
    THEN _AHI-SLOT !
    _AHI-SLOT @ _AHI-TABLE @ AHT-GRANT-NTH DUP AGR.GENERATION @
    1+ DUP 0= IF DROP 1 THEN _AHI-GEN !
    _AHI-GRANT @ OVER AUTH-GRANT-SIZE MOVE
    _AHI-GEN @ OVER AGR.GENERATION !
    AGR-S-ACTIVE OVER AGR.STATE !
    DUP _AHI-TABLE @ _AHT-GRANT-ID!
    DROP
    _AHI-TOKEN @ IH-INIT
    _AHI-TABLE @ AHT.EPOCH @ _AHI-TOKEN @ IH.EPOCH !
    _AHI-TABLE @ AHT.TABLE-ID @ _AHI-TOKEN @ IH.TABLE-ID !
    _AHI-SLOT @ _AHI-TOKEN @ IH.SLOT !
    _AHI-GEN @ _AHI-TOKEN @ IH.GENERATION !
    _AHI-TABLE @ AHT.EPOCH @
    _AHI-TABLE @ AHT.TABLE-ID @
    _AHI-SLOT @ _AHI-GEN @ _AHI-TOKEN @ IH.SEAL
    _AHI-TABLE @ _AHT-SEAL!
    1 _AHI-TABLE @ AHT.COUNT +!
    AUTH-S-OK ;

