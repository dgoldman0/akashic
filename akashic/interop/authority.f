    _AHTR-EPOCH @ _AHTR-TABLE @ AHT.EPOCH !
    _AHTR-SECRET @ _AHTR-TABLE @ _AHT-SECRET!
    0 _AHTR-TABLE @ AHT.COUNT !
    1 _AHTR-TABLE @ AHT.NEXT-ID ! ;
