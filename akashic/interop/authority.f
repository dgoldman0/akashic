CREATE _AHT-SEED 8 ALLOT

: _AHT-SECRET!  ( secret table -- )
    >R _AHT-SEED !
    _AHT-SEED 8 R> _AHT.SECRET SHA3-256-HASH ;

: AHT-NEW  ( epoch secret -- table ior )
    >R >R
    AUTH-TABLE-SIZE ALLOCATE
    DUP IF R> DROP R> DROP EXIT THEN
    DROP DUP AUTH-TABLE-SIZE 0 FILL
    R> OVER AHT.EPOCH !
    _AHT-NEXT-TABLE-ID @ OVER AHT.TABLE-ID !
    1 _AHT-NEXT-TABLE-ID +!
    R> OVER _AHT-SECRET!
    1 OVER AHT.NEXT-ID !
    0 ;

: AHT-FREE  ( table -- ) ?DUP IF
    DUP AUTH-TABLE-SIZE 0 FILL FREE
THEN ;

