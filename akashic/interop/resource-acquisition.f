: _RACQ-RELEASE  ( token -- status )
    DUP _RREL-T ! RACQ-TOKEN-VALID? 0= IF RACQ-S-STALE-TOKEN EXIT THEN
    _RREL-T @ RACQ.TOKEN-STATE @ RACQ-TOKEN-S-EMPTY = IF RACQ-S-OK EXIT THEN
    _RREL-T @ RACQ.TOKEN-STATE @ RACQ-TOKEN-S-ACTIVE <> IF
        RACQ-S-BUSY EXIT
    THEN
    _RREL-T @ RACQ.TOKEN-ROOT @ DUP RACQ-ROOT-VALID? 0= IF
        DROP RACQ-S-RELEASE-FAILED EXIT
    THEN _RREL-R !
    RACQ-TOKEN-S-RELEASING _RREL-T @ RACQ.TOKEN-STATE !
    BEGIN
        _RREL-T @ _RREL-R @
        _RREL-R @ RACQ.ROOT-QUIESCENT-XT @ EXECUTE DUP _RREL-S !
        _RREL-S @ RACQ-S-BUSY =
    WHILE
        DROP YIELD?
    REPEAT
    _RREL-S @ RACQ-S-OK <> IF
        DROP RACQ-TOKEN-S-ACTIVE _RREL-T @ RACQ.TOKEN-STATE !
        _RREL-S @ RACQ-STATUS-VALID? IF _RREL-S @ ELSE RACQ-S-RELEASE-FAILED THEN
        EXIT
    THEN DROP
    _RREL-T @ _RREL-R @
    _RREL-R @ RACQ.ROOT-RELEASE-XT @ EXECUTE DUP _RREL-S !
    _RREL-S @ RACQ-S-OK = IF
        DROP _RREL-T @ RACQ-TOKEN-INIT RACQ-S-OK EXIT
    THEN
    DROP RACQ-TOKEN-S-ACTIVE _RREL-T @ RACQ.TOKEN-STATE !
    _RREL-S @ RACQ-STATUS-VALID? IF _RREL-S @ ELSE RACQ-S-RELEASE-FAILED THEN ;

