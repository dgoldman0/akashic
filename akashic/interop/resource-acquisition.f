: _RACQ-OWNER-FAIL  ( primary -- status )
    _RAA-PRIMARY !
    \ Owners must not retain on a failed acquire, but fail closed around a
    \ partial callback that activated its token before returning an error.
    \ Cleanup failure then has the same active, retryable-token semantics as
    \ failed LBIND attachment cleanup.  DETAIL preserves the terminal owner
    \ reason while RELEASE-FAILED is the retryable primary status.
    _RAA-O @ RACQ.RESULT-TOKEN _RAA-R @ RACQ-TOKEN-FOR-ROOT? IF
        _RAA-O @ RACQ.RESULT-TOKEN _RACQ-RELEASE DUP RACQ-S-OK = IF
            DROP _RAA-PRIMARY @ 0 _RACQ-RESULT-STATUS! EXIT
        THEN
        DROP RACQ-S-RELEASE-FAILED _RAA-PRIMARY @
            _RACQ-RESULT-STATUS!
    ELSE
        _RAA-PRIMARY @ 0 _RACQ-RESULT-STATUS!
    THEN ;

: RACQ-ATTACH  ( locator root context rreg binding result -- status )
    _RAA-O ! _RAA-B ! _RAA-RG ! _RAA-C ! _RAA-R ! _RAA-L !
    _RACQ-ATTACH-SPANS-SAFE? 0= IF RACQ-S-INVALID EXIT THEN
    _RAA-O @ RACQ-RESULT-INIT
    _RAA-L @ QLOC-VALID? 0= _RAA-R @ RACQ-ROOT-VALID? 0= OR IF
        RACQ-S-INVALID 0 _RACQ-RESULT-STATUS! EXIT
    THEN
    _RAA-L @ QLOC-OWNER$ _RAA-R @ RACQ-ROOT-OWNER$ STR-STR= 0= IF
        RACQ-S-OWNER-MISMATCH 0 _RACQ-RESULT-STATUS! EXIT
    THEN
    _RAA-C @ 0= _RAA-RG @ 0= OR _RAA-B @ 0= OR IF
        RACQ-S-INVALID 0 _RACQ-RESULT-STATUS! EXIT
    THEN
    \ Always retain through the owner root, even if the RID is published.
    _RAA-L @ _RAA-O @ _RAA-R @
    _RAA-R @ RACQ.ROOT-ACQUIRE-XT @ EXECUTE DUP _RAA-S !
    _RAA-S @ RACQ-STATUS-VALID? 0= IF
        DROP RACQ-S-INVALID _RACQ-OWNER-FAIL EXIT
    THEN
    _RAA-S @ RACQ-S-OK <> IF
        DROP _RAA-S @ _RACQ-OWNER-FAIL EXIT
    THEN DROP
    _RAA-O @ RACQ.RESULT-TOKEN _RAA-R @ RACQ-TOKEN-FOR-ROOT? 0= IF
        RACQ-S-INVALID 0 _RACQ-ROLLBACK EXIT
    THEN
    _RAA-O @ RACQ.RESULT-REF RREF-VALID? 0= IF
        RACQ-S-INVALID 0 _RACQ-ROLLBACK EXIT
    THEN
    _RAA-O @ RACQ.RESULT-REF RREF.ID _RAA-L @ QLOC.REF RREF.ID RID= 0= IF
        RACQ-S-RID-MISMATCH 0 _RACQ-ROLLBACK EXIT
    THEN
    _RAA-O @ RACQ.RESULT-REF _RAA-C @ _RAA-RG @ _RAA-B @ LBIND-ATTACH
        DUP _RAA-LS !
    _RAA-LS @ LBIND-S-OK <> IF
        DROP RACQ-S-ATTACH-FAILED _RAA-LS @ _RACQ-ROLLBACK EXIT
    THEN DROP
    RACQ-S-OK 0 _RACQ-RESULT-STATUS! ;

