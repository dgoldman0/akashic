\ =====================================================================
\  resource-acquisition.f - Owner-root retain and lens attachment
\ =====================================================================
\  Resolution always calls the live semantic owner's root.  Registry
\  presence is never used as a retain shortcut.  A successful owner call
\  returns one semantic RREF plus a self-bound activation-local token; only
\  then may the caller attach an LBIND.
\
\  Tokens deliberately contain live pointers and MUST NOT be persisted.
\  SELF makes a byte-copy invalid, while the owner callback additionally
\  checks its private slot/cookie/generation.  Failed cleanup leaves the
\  original token active and retryable.  Successful release clears it only
\  after the owner has accepted exactly one release.
\ =====================================================================

