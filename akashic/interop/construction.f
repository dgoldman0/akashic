  ( input-schema output-schema effects flags max-ms concurrency builder -- status )
    >R
    R@ _CAPB-READY ?DUP IF
        R> _CAPB-CONTRACT-FAIL EXIT
    THEN
    5 PICK ?DUP IF CS-SCHEMA-VALIDATE IF
