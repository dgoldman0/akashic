: _QLOC-READ-FIELD  ( key-a key-u -- value | 0 )
    _QLR-V @ CV-MAP-FIND ;

: _QLOC-TEXT-VALUE$  ( value -- addr len flag )
    DUP 0= IF DROP 0 0 0 EXIT THEN
    DUP CV-TYPE@ CV-T-STRING <> IF DROP 0 0 0 EXIT THEN
    DUP CV-DATA@ SWAP CV-LEN@ -1 ;

: _QLOC-DIGEST-KIND@  ( value -- kind flag )
    _QLOC-TEXT-VALUE$ 0= IF 2DROP 0 0 EXIT THEN
    2DUP S" none" STR-STR= IF 2DROP QLOC-DK-NONE -1 EXIT THEN
    2DUP S" semantic-state" STR-STR= IF
        2DROP QLOC-DK-SEMANTIC-STATE -1 EXIT
    THEN
    S" projection-content" STR-STR= IF
        QLOC-DK-PROJECTION-CONTENT -1
    ELSE
        0 0
    THEN ;

: QLOC-VALUE@  ( value locator -- status )
    _QLR-L ! _QLR-V !
    _QLR-L @ 0= IF QLOC-S-INVALID EXIT THEN
    _QLR-L @ QLOC-INIT
    _QLR-V @ QLOC-VALUE-SCHEMA CS-VALIDATE-DEEP IF QLOC-S-TYPE EXIT THEN
    S" owner" _QLOC-READ-FIELD DUP _QLR-OWNER ! 0= IF QLOC-S-TYPE EXIT THEN
    S" resource" _QLOC-READ-FIELD DUP _QLR-RESOURCE ! 0= IF QLOC-S-TYPE EXIT THEN
    S" mode" _QLOC-READ-FIELD DUP _QLR-MODE ! 0= IF QLOC-S-TYPE EXIT THEN
    S" domain_revision" _QLOC-READ-FIELD DUP _QLR-REV ! 0= IF QLOC-S-TYPE EXIT THEN
    S" digest_kind" _QLOC-READ-FIELD DUP _QLR-DK ! 0= IF QLOC-S-TYPE EXIT THEN
    S" state_digest" _QLOC-READ-FIELD DUP _QLR-DIGEST ! 0= IF QLOC-S-TYPE EXIT THEN
    S" projection" _QLOC-READ-FIELD DUP _QLR-PROJECTION ! 0= IF QLOC-S-TYPE EXIT THEN
    _QLR-RESOURCE @ _QLR-REF IRES-RREF@ IRES-S-OK <> IF QLOC-S-TYPE EXIT THEN
    _QLR-REF RREF.REVISION @ IF QLOC-S-INVALID EXIT THEN
    _QLR-REV @ CV-TYPE@ CV-T-INT <> IF QLOC-S-TYPE EXIT THEN
    _QLR-DIGEST @ CV-TYPE@ CV-T-BYTES <> IF QLOC-S-TYPE EXIT THEN
    _QLR-DK @ _QLOC-DIGEST-KIND@ 0= IF DROP QLOC-S-INVALID EXIT THEN _QLR-DK !
    _QLR-OWNER @ _QLOC-TEXT-VALUE$ 0= IF 2DROP QLOC-S-TYPE EXIT THEN
    _QLR-OU ! _QLR-OA !
    _QLR-MODE @ _QLOC-TEXT-VALUE$ 0= IF 2DROP QLOC-S-TYPE EXIT THEN
    _QLR-MU ! _QLR-MA !
    _QLR-PROJECTION @ _QLOC-TEXT-VALUE$ 0= IF 2DROP QLOC-S-TYPE EXIT THEN
    _QLR-PU ! _QLR-PA !
    _QLR-MA @ _QLR-MU @ S" identity" STR-STR= IF
        _QLR-DK @ QLOC-DK-NONE <> IF QLOC-S-INVALID EXIT THEN
        _QLR-DIGEST @ CV-LEN@ IF QLOC-S-INVALID EXIT THEN
        _QLR-REV @ CV-DATA@ IF QLOC-S-INVALID EXIT THEN
        _QLR-PU @ IF QLOC-S-INVALID EXIT THEN
        _QLR-OA @ _QLR-OU @ _QLR-REF _QLR-L @ QLOC-IDENTITY! EXIT
    THEN
    _QLR-MA @ _QLR-MU @ S" exact-domain-state" STR-STR= 0= IF
        QLOC-S-INVALID EXIT
    THEN
    _QLR-DIGEST @ CV-LEN@ SHA3-256-LEN <> IF QLOC-S-INVALID EXIT THEN
    _QLR-OA @ _QLR-OU @ _QLR-REF _QLR-REV @ CV-DATA@
    _QLR-DIGEST @ CV-DATA@ _QLR-DK @
    _QLR-PA @ _QLR-PU @ _QLR-L @ QLOC-EXACT! ;

