\ =====================================================================
\  qualified-locator.f - Closed durable semantic resource locators
\ =====================================================================
\  QLOC is pointer-free durable identity/evidence.  It deliberately keeps
\  the embedded RREF at revision zero: positive RREF revisions come from a
\  live component and are not durable domain-history evidence unless an
\  owner separately qualifies that relationship.
\
\  Exact-domain-state locators carry a positive domain revision, one typed
\  SHA3-256 digest, and a projection-contract ID.  They never contain an
\  LBIND, component handle, acquisition token, path, capability, or grant.
\ =====================================================================

