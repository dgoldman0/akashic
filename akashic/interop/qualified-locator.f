: QLOC-EXACT!  ( oa ou ref rev digest kind pa pu locator -- status )
    _QLE-L ! _QLE-PU ! _QLE-PA ! _QLE-DK ! _QLE-DIGEST !
    _QLE-REV ! _QLE-R ! _QLE-OU ! _QLE-OA !
    _QLE-OA @ _QLE-OU @ _QLE-R @ _QLE-L @ _QLOC-BEGIN ?DUP IF EXIT THEN
    _QLE-REV @ 0> 0= _QLE-DIGEST @ 0= OR IF QLOC-S-INVALID EXIT THEN
    _QLE-DK @ QLOC-DK-SEMANTIC-STATE =
    _QLE-DK @ QLOC-DK-PROJECTION-CONTENT = OR 0= IF
        QLOC-S-INVALID EXIT
    THEN
    _QLE-PA @ 0= _QLE-PU @ 1 < OR IF QLOC-S-INVALID EXIT THEN
    _QLE-PU @ QLOC-PROJECTION-MAX > IF QLOC-S-CAPACITY EXIT THEN
    _QLE-PA @ _QLE-PU @ UTF8-VALID? 0= IF QLOC-S-INVALID EXIT THEN
    QLOC-M-EXACT-DOMAIN _QLE-L @ QLOC.MODE !
    _QLE-DK @ _QLE-L @ QLOC.DIGEST-KIND !
    _QLE-REV @ _QLE-L @ QLOC.DOMAIN-REVISION !
    _QLE-DIGEST @ _QLE-L @ QLOC.STATE-DIGEST SHA3-256-LEN CMOVE
    _QLE-PU @ _QLE-L @ QLOC.PROJECTION-U !
    _QLE-PA @ _QLE-L @ QLOC.PROJECTION _QLE-PU @ CMOVE
    _QLE-L @ QLOC-VALID? IF QLOC-S-OK ELSE QLOC-S-INVALID THEN ;

: QLOC-COPY  ( source destination -- status )
    DUP 0= IF 2DROP QLOC-S-INVALID EXIT THEN
    OVER QLOC-VALID? 0= IF NIP QLOC-INIT QLOC-S-INVALID EXIT THEN
    2DUP = IF 2DROP QLOC-S-OK EXIT THEN
    QLOC-SIZE MOVE QLOC-S-OK ;

: QLOC=  ( a b -- flag )
    DUP QLOC-VALID? 0= IF 2DROP 0 EXIT THEN
    OVER QLOC-VALID? 0= IF 2DROP 0 EXIT THEN
    SWAP QLOC-SIZE ROT QLOC-SIZE STR-STR= ;

