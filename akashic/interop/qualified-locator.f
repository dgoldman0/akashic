: QLOC-INIT  ( locator -- )
    DUP QLOC-SIZE 0 FILL
    QLOC-MAGIC OVER QLOC.MAGIC !
    QLOC-ABI-VERSION OVER QLOC.ABI !
    QLOC-SIZE SWAP QLOC.SIZE ! ;

VARIABLE _QLZ-A
VARIABLE _QLZ-U
VARIABLE _QLZ-C

: _QLOC-ZERO?  ( addr len -- flag )
    _QLZ-U ! _QLZ-A !
    _QLZ-U @ 0 ?DO
        _QLZ-A @ I + C@ IF 0 UNLOOP EXIT THEN
    LOOP
    -1 ;

: _QLOC-TAIL-ZERO?  ( addr used capacity -- flag )
    _QLZ-C ! _QLZ-U ! _QLZ-A !
    _QLZ-A @ _QLZ-U @ + _QLZ-C @ _QLZ-U @ - _QLOC-ZERO? ;

: _QLOC-TEXT-VALID?  ( addr len capacity -- flag )
    _QLZ-C ! _QLZ-U ! _QLZ-A !
    _QLZ-A @ 0= IF 0 EXIT THEN
    _QLZ-U @ 1 < _QLZ-U @ _QLZ-C @ > OR IF 0 EXIT THEN
    _QLZ-A @ _QLZ-U @ UTF8-VALID? 0= IF 0 EXIT THEN
    _QLZ-A @ _QLZ-U @ _QLZ-C @ _QLOC-TAIL-ZERO? ;

VARIABLE _QLV-L
VARIABLE _QLV-M
VARIABLE _QLV-DK

: QLOC-VALID?  ( locator -- flag )
    DUP 0= IF DROP 0 EXIT THEN _QLV-L !
    _QLV-L @ QLOC.MAGIC @ QLOC-MAGIC <> IF 0 EXIT THEN
    _QLV-L @ QLOC.ABI @ QLOC-ABI-VERSION <> IF 0 EXIT THEN
    _QLV-L @ QLOC.SIZE @ QLOC-SIZE <> IF 0 EXIT THEN
    _QLV-L @ QLOC.FLAGS @ IF 0 EXIT THEN
    _QLV-L @ _QL-RESERVED + @ IF 0 EXIT THEN
    _QLV-L @ QLOC-OWNER$ QLOC-OWNER-MAX _QLOC-TEXT-VALID? 0= IF
        0 EXIT
    THEN
    _QLV-L @ QLOC.REF RREF-VALID? 0= IF 0 EXIT THEN
    _QLV-L @ QLOC.REF RREF.SIZE @ RREF-SIZE <> IF 0 EXIT THEN
    _QLV-L @ QLOC.REF 72 + @ IF 0 EXIT THEN
    _QLV-L @ QLOC.REF RREF.REVISION @ IF 0 EXIT THEN
    _QLV-L @ QLOC.MODE @ DUP _QLV-M !
    DUP QLOC-M-IDENTITY = SWAP QLOC-M-EXACT-DOMAIN = OR 0= IF
        0 EXIT
    THEN
    _QLV-L @ QLOC.DIGEST-KIND @ _QLV-DK !
    _QLV-M @ QLOC-M-IDENTITY = IF
        _QLV-DK @ QLOC-DK-NONE <> IF 0 EXIT THEN
        _QLV-L @ QLOC.DOMAIN-REVISION @ IF 0 EXIT THEN
        _QLV-L @ QLOC.STATE-DIGEST SHA3-256-LEN _QLOC-ZERO? 0= IF
            0 EXIT
        THEN
        _QLV-L @ QLOC.PROJECTION-U @ IF 0 EXIT THEN
        _QLV-L @ QLOC.PROJECTION QLOC-PROJECTION-MAX _QLOC-ZERO?
        EXIT
    THEN
    _QLV-DK @ QLOC-DK-SEMANTIC-STATE =
    _QLV-DK @ QLOC-DK-PROJECTION-CONTENT = OR 0= IF 0 EXIT THEN
    _QLV-L @ QLOC.DOMAIN-REVISION @ 0> 0= IF 0 EXIT THEN
    _QLV-L @ QLOC-PROJECTION$ QLOC-PROJECTION-MAX
        _QLOC-TEXT-VALID? ;

VARIABLE _QLB-OA
VARIABLE _QLB-OU
VARIABLE _QLB-R
VARIABLE _QLB-L

: _QLOC-BEGIN  ( owner-a owner-u reference locator -- status )
    _QLB-L ! _QLB-R ! _QLB-OU ! _QLB-OA !
    _QLB-L @ 0= IF QLOC-S-INVALID EXIT THEN
    _QLB-L @ QLOC-INIT
    _QLB-R @ RREF-VALID? 0= IF QLOC-S-INVALID EXIT THEN
    _QLB-R @ RREF.REVISION @ IF QLOC-S-INVALID EXIT THEN
    _QLB-OA @ 0= _QLB-OU @ 1 < OR IF QLOC-S-INVALID EXIT THEN
    _QLB-OU @ QLOC-OWNER-MAX > IF QLOC-S-CAPACITY EXIT THEN
    _QLB-OA @ _QLB-OU @ UTF8-VALID? 0= IF QLOC-S-INVALID EXIT THEN
    _QLB-OU @ _QLB-L @ QLOC.OWNER-U !
    _QLB-OA @ _QLB-L @ QLOC.OWNER _QLB-OU @ CMOVE
    _QLB-R @ _QLB-L @ QLOC.REF RREF-COPY RREF-S-OK <> IF
        QLOC-S-INVALID EXIT
    THEN
    QLOC-S-OK ;

: QLOC-IDENTITY!  ( owner-a owner-u reference locator -- status )
    _QLB-L ! _QLB-R ! _QLB-OU ! _QLB-OA !
    _QLB-OA @ _QLB-OU @ _QLB-R @ _QLB-L @ _QLOC-BEGIN
        ?DUP IF EXIT THEN
    QLOC-M-IDENTITY _QLB-L @ QLOC.MODE !
    _QLB-L @ QLOC-VALID? IF QLOC-S-OK ELSE QLOC-S-INVALID THEN ;

