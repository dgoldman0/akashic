VARIABLE _QLW-L
VARIABLE _QLW-V
VARIABLE _QLW-C
VARIABLE _QLW-S

: _QLOC-WRITE-SLOT  ( key-a key-u index -- child status )
    _QLW-V @ CV-MAP-SLOT! DUP IF
        NIP
    ELSE
        DROP _QLW-C ! 0
    THEN ;

: _QLOC-WRITE-FAIL  ( -- status )
    _QLW-V @ CV-FREE QLOC-S-NOMEM ;

: QLOC-VALUE!  ( locator value -- status )
    _QLW-V ! _QLW-L !
    _QLW-V @ 0= _QLW-L @ QLOC-VALID? 0= OR IF QLOC-S-INVALID EXIT THEN
    7 _QLW-V @ CV-MAP! ?DUP IF DROP QLOC-S-NOMEM EXIT THEN
    S" owner" 0 _QLOC-WRITE-SLOT IF _QLOC-WRITE-FAIL EXIT THEN
    _QLW-L @ QLOC-OWNER$ _QLW-C @ CV-STRING! IF _QLOC-WRITE-FAIL EXIT THEN
    S" resource" 1 _QLOC-WRITE-SLOT IF _QLOC-WRITE-FAIL EXIT THEN
    _QLW-L @ QLOC.REF _QLW-C @ IRES-RREF! IRES-S-OK <> IF
        _QLOC-WRITE-FAIL EXIT
    THEN
    S" mode" 2 _QLOC-WRITE-SLOT IF _QLOC-WRITE-FAIL EXIT THEN
    _QLW-L @ QLOC.MODE @ QLOC-M-IDENTITY = IF
        S" identity"
    ELSE
        S" exact-domain-state"
    THEN _QLW-C @ CV-STRING! IF _QLOC-WRITE-FAIL EXIT THEN
    S" domain_revision" 3 _QLOC-WRITE-SLOT IF _QLOC-WRITE-FAIL EXIT THEN
    _QLW-L @ QLOC.DOMAIN-REVISION @ _QLW-C @ CV-INT!
    S" digest_kind" 4 _QLOC-WRITE-SLOT IF _QLOC-WRITE-FAIL EXIT THEN
    _QLW-L @ QLOC.DIGEST-KIND @ CASE
        QLOC-DK-NONE OF S" none" ENDOF
        QLOC-DK-SEMANTIC-STATE OF S" semantic-state" ENDOF
        QLOC-DK-PROJECTION-CONTENT OF S" projection-content" ENDOF
    ENDCASE
    _QLW-C @ CV-STRING! IF _QLOC-WRITE-FAIL EXIT THEN
    S" state_digest" 5 _QLOC-WRITE-SLOT IF _QLOC-WRITE-FAIL EXIT THEN
    _QLW-L @ QLOC.STATE-DIGEST
    _QLW-L @ QLOC.MODE @ QLOC-M-IDENTITY = IF 0 ELSE SHA3-256-LEN THEN
    _QLW-C @ CV-BYTES! IF _QLOC-WRITE-FAIL EXIT THEN
    S" projection" 6 _QLOC-WRITE-SLOT IF _QLOC-WRITE-FAIL EXIT THEN
    _QLW-L @ QLOC-PROJECTION$ _QLW-C @ CV-STRING! IF
        _QLOC-WRITE-FAIL EXIT
    THEN
    QLOC-S-OK ;

