: _CREC-HEADER-CRC  ( record -- crc32 )
    CRC32-BEGIN
    DUP CREC-H-HEADER-CRC CRC32-ADD
    CREC-H-FLAGS + 8 CRC32-ADD
    CRC32-END ;

: _CREC-INSPECT-BODY  ( work -- status )
    >R
    R@ _CREC-WORK.EXTENT-U @ CREC-HEADER-SIZE < IF
        R> DROP CREC-S-CORRUPT EXIT
    THEN
    R@ _CREC-WORK.RECORD-A @ CREC-H-MAGIC + 8
        R@ _CREC-WORK.SPEC @ _CREC-SPEC.RECORD-MAGIC 8 COMPARE IF
        R> DROP CREC-S-CORRUPT EXIT
    THEN
    R@ _CREC-WORK.RECORD-A @ _CREC-HEADER-CRC
        R@ _CREC-WORK.RECORD-A @ CREC-H-HEADER-CRC + @ <> IF
        R> DROP CREC-S-CHECKSUM EXIT
    THEN
    R@ _CREC-WORK.RECORD-A @ CREC-H-FORMAT + @
        R@ _CREC-WORK.SPEC @ _CREC-SPEC.FORMAT @ 2DUP <> IF
        > IF R> DROP CREC-S-UNSUPPORTED
          ELSE R> DROP CREC-S-CORRUPT THEN
        EXIT
    THEN
    2DROP
    R@ _CREC-WORK.RECORD-A @ CREC-H-HEADER-U + @
        CREC-HEADER-SIZE <> IF R> DROP CREC-S-CORRUPT EXIT THEN
    R@ _CREC-WORK.RECORD-A @ CREC-H-FLAGS + @ IF
        R> DROP CREC-S-CORRUPT EXIT
    THEN
    R@ _CREC-WORK.RECORD-A @ CREC-H-TAG + @
        DUP R@ _CREC-WORK.TAG !
        R@ _CREC-WORK.SPEC @ _CREC-TAG-VALID? 0= IF
        R> DROP CREC-S-CORRUPT EXIT
    THEN
    R@ _CREC-WORK.RECORD-A @ CREC-H-PAYLOAD-U + @
        DUP R@ _CREC-WORK.PAYLOAD-U !
        R@ _CREC-WORK.SPEC @ CREC-MEASURE
    DUP IF
        2DROP R> DROP CREC-S-CORRUPT EXIT
    THEN
    DROP R@ _CREC-WORK.RESULT-RECORD-U !
    R@ _CREC-WORK.PAYLOAD-U @ R@ _CREC-WORK.RESULT-PAYLOAD-U !
    R@ _CREC-WORK.TAG @ R@ _CREC-WORK.RESULT-TAG !
    CREC-RESULT-INSPECTED R@ _CREC-WORK.KIND !
    R> DROP CREC-S-OK ;

: _CREC-VALIDATOR-INVOKE  ( work -- callback-status )
    >R
    R@ _CREC-WORK.CONTEXT-A @
    R@ _CREC-WORK.CONTEXT-U @
    R@ _CREC-WORK.RECORD-A @ CREC-HEADER-SIZE +
    R@ _CREC-WORK.RESULT-PAYLOAD-U @
    R@ _CREC-WORK.RESULT-TAG @
    R@ _CREC-WORK.SPEC @ _CREC-SPEC.VALIDATE-XT @ EXECUTE
    R> DROP ;

: _CREC-CALL-VALIDATOR  ( work -- status )
    ['] _CREC-VALIDATOR-INVOKE CATCH
    DUP IF 2DROP CREC-S-CALLBACK EXIT THEN
    DROP
    DUP CREC-STATUS-VALID? 0= IF DROP CREC-S-CALLBACK THEN ;

: _CREC-VALIDATE-BODY  ( work -- status )
    DUP _CREC-INSPECT-BODY DUP IF NIP EXIT THEN DROP
    DUP _CREC-WORK.EXTENT-U @
        OVER _CREC-WORK.RESULT-RECORD-U @ <> IF
        DROP CREC-S-CORRUPT EXIT
    THEN
    DUP _CREC-WORK.RECORD-A @ CREC-HEADER-SIZE +
        OVER _CREC-WORK.RESULT-PAYLOAD-U @ CRC32
        OVER _CREC-WORK.RECORD-A @ CREC-H-PAYLOAD-CRC + @ <> IF
        DROP CREC-S-CHECKSUM EXIT
    THEN
    DUP _CREC-WORK.RECORD-A @ CREC-HEADER-SIZE +
        OVER _CREC-WORK.RESULT-PAYLOAD-U @ +
    OVER _CREC-WORK.RESULT-RECORD-U @ CREC-HEADER-SIZE -
        2 PICK _CREC-WORK.RESULT-PAYLOAD-U @ -
        _CREC-ZERO? 0= IF DROP CREC-S-CORRUPT EXIT THEN
    DUP _CREC-CALL-VALIDATOR DUP IF NIP EXIT THEN DROP
    DUP _CREC-WORK.RECORD-A @ CREC-HEADER-SIZE +
        OVER _CREC-WORK.RESULT-PAYLOAD-A !
    CREC-RESULT-VALIDATED SWAP _CREC-WORK.KIND !
    CREC-S-OK ;

: CREC-INSPECT-HEADER  ( record-a available-u spec work -- status )
    2DUP _CREC-WORK-READY DUP IF
        >R 2DROP 2DROP R> EXIT
    THEN DROP
    3 PICK 3 PICK _CREC-READ-SPAN? 0= IF
        2DROP 2DROP CREC-S-INVALID EXIT
    THEN
    3 PICK 3 PICK 3 PICK CREC-SPEC-SIZE MSPAN-OVERLAP? IF
        2DROP 2DROP CREC-S-INVALID EXIT
    THEN
    3 PICK 3 PICK 2 PICK CREC-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP 2DROP CREC-S-INVALID EXIT
    THEN
    DUP _CREC-WORK-BEGIN
    3 PICK OVER _CREC-WORK.RECORD-A !
    2 PICK OVER _CREC-WORK.EXTENT-U !
    1 PICK OVER _CREC-WORK.SPEC !
    >R 2DROP DROP
    R@ ['] _CREC-INSPECT-BODY CATCH
    DUP IF 2DROP CREC-S-FAULT ELSE DROP THEN
    R> _CREC-WORK-END ;

: CREC-VALIDATE
  ( context-a context-u record-a record-u spec work -- status )
    2DUP _CREC-WORK-READY DUP IF
        >R 2DROP 2DROP 2DROP R> EXIT
    THEN DROP
    5 PICK 5 PICK _CREC-CONTEXT-SPAN? 0= IF
        2DROP 2DROP 2DROP CREC-S-INVALID EXIT
    THEN
    3 PICK 3 PICK _CREC-READ-SPAN? 0= IF
        2DROP 2DROP 2DROP CREC-S-INVALID EXIT
    THEN
    5 PICK 5 PICK 5 PICK 5 PICK MSPAN-OVERLAP? IF
        2DROP 2DROP 2DROP CREC-S-INVALID EXIT
    THEN
    5 PICK 5 PICK 3 PICK CREC-SPEC-SIZE MSPAN-OVERLAP? IF
        2DROP 2DROP 2DROP CREC-S-INVALID EXIT
    THEN
    5 PICK 5 PICK 2 PICK CREC-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP 2DROP 2DROP CREC-S-INVALID EXIT
    THEN
    3 PICK 3 PICK 3 PICK CREC-SPEC-SIZE MSPAN-OVERLAP? IF
        2DROP 2DROP 2DROP CREC-S-INVALID EXIT
    THEN
    3 PICK 3 PICK 2 PICK CREC-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP 2DROP 2DROP CREC-S-INVALID EXIT
    THEN
    DUP _CREC-WORK-BEGIN
    5 PICK OVER _CREC-WORK.CONTEXT-A !
    4 PICK OVER _CREC-WORK.CONTEXT-U !
    3 PICK OVER _CREC-WORK.RECORD-A !
    2 PICK OVER _CREC-WORK.EXTENT-U !
    1 PICK OVER _CREC-WORK.SPEC !
    >R 2DROP 2DROP DROP
    R@ ['] _CREC-VALIDATE-BODY CATCH
    DUP IF 2DROP CREC-S-FAULT ELSE DROP THEN
    R> _CREC-WORK-END ;

