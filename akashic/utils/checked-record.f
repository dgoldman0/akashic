\ =====================================================================
\  checked-record.f - Neutral checked fixed and framed records
\ =====================================================================
\  One canonical 64-byte envelope surrounds a caller-defined payload.
\  This module owns geometry, CRCs, canonical zero padding, callback
\  containment, and result publication.  It owns no path, VFS handle,
\  replacement protocol, generation ordering, applet schema, or storage.
\
\  Specifications and operation workspaces are caller-owned.  The module
\  has no mutable private scratch or process-global current operation.
\ =====================================================================

PROVIDED akashic-checked-record

REQUIRE memory-span.f
REQUIRE ../math/crc.f

