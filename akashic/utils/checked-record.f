: _CREC-ENCODER-INVOKE  ( work -- callback-status )
    >R
    R@ _CREC-WORK.CONTEXT-A @
    R@ _CREC-WORK.CONTEXT-U @
    R@ _CREC-WORK.RECORD-A @ CREC-HEADER-SIZE +
    R@ _CREC-WORK.PAYLOAD-U @
    R@ _CREC-WORK.TAG @
    R@ _CREC-WORK.SPEC @ _CREC-SPEC.ENCODE-XT @ EXECUTE
    R> DROP ;

: _CREC-CALL-ENCODER  ( work -- status )
    ['] _CREC-ENCODER-INVOKE CATCH
    DUP IF 2DROP CREC-S-CALLBACK EXIT THEN
    DROP
    DUP CREC-STATUS-VALID? 0= IF DROP CREC-S-CALLBACK THEN ;

: _CREC-WIPE-RECORD  ( work -- )
    DUP _CREC-WORK.RECORD-A @
    SWAP _CREC-WORK.RESULT-RECORD-U @ 0 FILL ;

: _CREC-ENCODE-BODY  ( work -- status )
    DUP _CREC-WIPE-RECORD
    DUP _CREC-CALL-ENCODER DUP IF NIP EXIT THEN DROP

    \ Canonicalize every byte outside the callback-owned payload after the
    \ callback returns, then seal the two CRC domains.
    DUP _CREC-WORK.RECORD-A @ CREC-HEADER-SIZE 0 FILL
    DUP _CREC-WORK.RECORD-A @ CREC-HEADER-SIZE +
        OVER _CREC-WORK.PAYLOAD-U @ +
    OVER _CREC-WORK.RESULT-RECORD-U @ CREC-HEADER-SIZE -
        2 PICK _CREC-WORK.PAYLOAD-U @ - 0 FILL

    DUP _CREC-WORK.SPEC @ _CREC-SPEC.RECORD-MAGIC
        OVER _CREC-WORK.RECORD-A @ CREC-H-MAGIC + 8 MOVE
    DUP _CREC-WORK.SPEC @ _CREC-SPEC.FORMAT @
        OVER _CREC-WORK.RECORD-A @ CREC-H-FORMAT + !
    CREC-HEADER-SIZE OVER _CREC-WORK.RECORD-A @ CREC-H-HEADER-U + !
    DUP _CREC-WORK.TAG @
        OVER _CREC-WORK.RECORD-A @ CREC-H-TAG + !
    DUP _CREC-WORK.PAYLOAD-U @
        OVER _CREC-WORK.RECORD-A @ CREC-H-PAYLOAD-U + !
    DUP _CREC-WORK.RECORD-A @ CREC-HEADER-SIZE +
        OVER _CREC-WORK.PAYLOAD-U @ CRC32
        OVER _CREC-WORK.RECORD-A @ CREC-H-PAYLOAD-CRC + !
    DUP _CREC-WORK.RECORD-A @ _CREC-HEADER-CRC
        OVER _CREC-WORK.RECORD-A @ CREC-H-HEADER-CRC + !

    DUP _CREC-WORK.RECORD-A @ CREC-HEADER-SIZE +
        OVER _CREC-WORK.RESULT-PAYLOAD-A !
    DUP _CREC-WORK.PAYLOAD-U @
        OVER _CREC-WORK.RESULT-PAYLOAD-U !
    DUP _CREC-WORK.TAG @ OVER _CREC-WORK.RESULT-TAG !
    CREC-RESULT-ENCODED SWAP _CREC-WORK.KIND !
    CREC-S-OK ;

: CREC-ENCODE  ( ca cu pu tag record cap spec work -- record-u status )
    2DUP _CREC-WORK-READY DUP IF
        >R _CREC-DROP8 0 R> EXIT
    THEN DROP
    5 PICK 2 PICK CREC-MEASURE
    DUP IF
        >R DROP _CREC-DROP8 0 R> EXIT
    THEN
    DROP >R
    7 PICK 7 PICK _CREC-CONTEXT-SPAN? 0= IF
        R> DROP _CREC-DROP8 0 CREC-S-INVALID EXIT
    THEN
    2 PICK 0< IF R> DROP _CREC-DROP8 0 CREC-S-INVALID EXIT THEN
    2 PICK R@ < IF R> DROP _CREC-DROP8 0 CREC-S-CAPACITY EXIT THEN
    3 PICK R@ _CREC-READ-SPAN? 0= IF
        R> DROP _CREC-DROP8 0 CREC-S-INVALID EXIT
    THEN
    4 PICK 2 PICK _CREC-TAG-VALID? 0= IF
        R> DROP _CREC-DROP8 0 CREC-S-INVALID EXIT
    THEN
    7 PICK 7 PICK 5 PICK R@ MSPAN-OVERLAP? IF
        R> DROP _CREC-DROP8 0 CREC-S-INVALID EXIT
    THEN
    7 PICK 7 PICK 3 PICK CREC-SPEC-SIZE MSPAN-OVERLAP? IF
        R> DROP _CREC-DROP8 0 CREC-S-INVALID EXIT
    THEN
    7 PICK 7 PICK 2 PICK CREC-WORK-SIZE MSPAN-OVERLAP? IF
        R> DROP _CREC-DROP8 0 CREC-S-INVALID EXIT
    THEN
    3 PICK R@ 3 PICK CREC-SPEC-SIZE MSPAN-OVERLAP? IF
        R> DROP _CREC-DROP8 0 CREC-S-INVALID EXIT
    THEN
    3 PICK R@ 2 PICK CREC-WORK-SIZE MSPAN-OVERLAP? IF
        R> DROP _CREC-DROP8 0 CREC-S-INVALID EXIT
    THEN

    DUP _CREC-WORK-BEGIN
    R@ OVER _CREC-WORK.RESULT-RECORD-U !
    7 PICK OVER _CREC-WORK.CONTEXT-A !
    6 PICK OVER _CREC-WORK.CONTEXT-U !
    5 PICK OVER _CREC-WORK.PAYLOAD-U !
    4 PICK OVER _CREC-WORK.TAG !
    3 PICK OVER _CREC-WORK.RECORD-A !
    1 PICK OVER _CREC-WORK.SPEC !
    R> DROP
    >R _CREC-DROP7
    R@ ['] _CREC-ENCODE-BODY CATCH
    DUP IF 2DROP CREC-S-FAULT ELSE DROP THEN
    DUP IF R@ _CREC-WIPE-RECORD THEN
    R@ _CREC-WORK-END
    DUP CREC-S-OK = IF
        R@ CREC-RECORD-U@ SWAP
    ELSE
        0 SWAP
    THEN
    R> DROP ;
