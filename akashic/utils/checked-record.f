: CREC-WORK-VALID?  ( work -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP CREC-WORK-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _CREC-WORK.MAGIC @ _CREC-WORK-MAGIC <> IF DROP 0 EXIT THEN
    DUP _CREC-WORK.SELF @ OVER <> IF DROP 0 EXIT THEN
    DUP _CREC-WORK.STATE @ DUP _CREC-WORK-IDLE =
        SWAP _CREC-WORK-ACTIVE = OR 0= IF DROP 0 EXIT THEN
    DUP _CREC-WORK.STATUS @ CREC-STATUS-VALID? 0= IF DROP 0 EXIT THEN
    DUP _CREC-WORK.KIND @ DUP CREC-RESULT-NONE <
        SWAP CREC-RESULT-ENCODED > OR IF DROP 0 EXIT THEN
    _CREC-WORK.RESERVED @ 0= ;

: CREC-WORK-INIT  ( work -- status )
    DUP 0= IF DROP CREC-S-INVALID EXIT THEN
    DUP CREC-WORK-SIZE MSPAN-NONWRAPPING? 0= IF
        DROP CREC-S-INVALID EXIT
    THEN
    DUP CREC-WORK-SIZE 0 FILL
    _CREC-WORK-MAGIC OVER _CREC-WORK.MAGIC !
    DUP DUP _CREC-WORK.SELF !
    CREC-S-INVALID SWAP _CREC-WORK.STATUS !
    CREC-S-OK ;

: _CREC-WORK-READY  ( spec work -- status )
    DUP CREC-WORK-VALID? 0= IF 2DROP CREC-S-INVALID EXIT THEN
    DUP _CREC-WORK.STATE @ _CREC-WORK-ACTIVE = IF
        2DROP CREC-S-BUSY EXIT
    THEN
    OVER CREC-SPEC-VALID? 0= IF 2DROP CREC-S-INVALID EXIT THEN
    OVER CREC-SPEC-SIZE 2 PICK CREC-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP CREC-S-INVALID EXIT
    THEN
    2DROP CREC-S-OK ;

: _CREC-WORK-CLEAR-RESULT  ( work -- )
    CREC-S-INVALID OVER _CREC-WORK.STATUS !
    CREC-RESULT-NONE OVER _CREC-WORK.KIND !
    _CRW-RESULT-RECORD-U + 32 0 FILL ;

: _CREC-WORK-BEGIN  ( work -- )
    DUP _CRW-RECORD-A + 56 0 FILL
    DUP _CREC-WORK-CLEAR-RESULT
    _CREC-WORK-ACTIVE SWAP _CREC-WORK.STATE ! ;

: _CREC-WORK-END  ( status work -- status )
    >R
    DUP IF R@ _CREC-WORK-CLEAR-RESULT THEN
    DUP R@ _CREC-WORK.STATUS !
    R@ _CRW-RECORD-A + 56 0 FILL
    _CREC-WORK-IDLE R@ _CREC-WORK.STATE !
    R> DROP ;

: _CREC-WORK-SUCCEEDED?  ( work -- flag )
    DUP CREC-WORK-VALID? 0= IF DROP 0 EXIT THEN
    DUP _CREC-WORK.STATE @ _CREC-WORK-IDLE <>
        IF DROP 0 EXIT THEN
    DUP _CREC-WORK.STATUS @ CREC-S-OK <>
        IF DROP 0 EXIT THEN
    _CREC-WORK.KIND @ CREC-RESULT-NONE <> ;

: CREC-WORK-STATUS@  ( work -- status )
    DUP CREC-WORK-VALID? IF _CREC-WORK.STATUS @
    ELSE DROP CREC-S-INVALID THEN ;

: CREC-RESULT-KIND@  ( work -- kind )
    DUP _CREC-WORK-SUCCEEDED? IF _CREC-WORK.KIND @ ELSE DROP 0 THEN ;

: CREC-RECORD-U@  ( work -- record-u|0 )
    DUP _CREC-WORK-SUCCEEDED? IF
        _CREC-WORK.RESULT-RECORD-U @
    ELSE DROP 0 THEN ;

: CREC-PAYLOAD-U@  ( work -- payload-u|0 )
    DUP _CREC-WORK-SUCCEEDED? IF
        _CREC-WORK.RESULT-PAYLOAD-U @
    ELSE DROP 0 THEN ;

: CREC-TAG@  ( work -- tag|0 )
    DUP _CREC-WORK-SUCCEEDED? IF
        _CREC-WORK.RESULT-TAG @
    ELSE DROP 0 THEN ;

: CREC-PAYLOAD$  ( work -- payload-a payload-u )
    DUP _CREC-WORK-SUCCEEDED? 0= IF DROP 0 0 EXIT THEN
    DUP _CREC-WORK.KIND @ DUP CREC-RESULT-VALIDATED =
        SWAP CREC-RESULT-ENCODED = OR 0= IF DROP 0 0 EXIT THEN
    DUP _CREC-WORK.RESULT-PAYLOAD-A @
    SWAP _CREC-WORK.RESULT-PAYLOAD-U @ ;

