: _CREC-SPEC-BASE-VALID?  ( spec -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP CREC-SPEC-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _CREC-SPEC.MAGIC @ _CREC-SPEC-MAGIC <> IF DROP 0 EXIT THEN
    DUP _CREC-SPEC.SELF @ OVER <> IF DROP 0 EXIT THEN
    DUP _CREC-SPEC.FLAGS @ DUP 0<>
        SWAP _CREC-SPEC-F-SEALED <> AND IF DROP 0 EXIT THEN
    DUP _CREC-SPEC.MODE @ DUP 0<
        OVER CREC-MODE-FRAMED > OR IF 2DROP 0 EXIT THEN DROP
    DUP _CREC-SPEC.FORMAT @ 0> 0= IF DROP 0 EXIT THEN
    DUP _CREC-SPEC.TAG-POLICY @ _CREC-TAG-POLICY? 0= IF DROP 0 EXIT THEN
    DUP _CREC-SPEC.ENCODE-XT @ 0= IF DROP 0 EXIT THEN
    DUP _CREC-SPEC.VALIDATE-XT @ 0= IF DROP 0 EXIT THEN
    _CREC-SPEC.RESERVED @ 0= ;

: _CREC-SPEC-GEOMETRY-VALID?  ( spec -- flag )
    DUP _CREC-SPEC.MODE @ CREC-MODE-FIXED = IF
        DUP _CREC-SPEC.PAYLOAD-MIN @ DUP 0< IF 2DROP 0 EXIT THEN DROP
        DUP _CREC-SPEC.PAYLOAD-MIN @
            OVER _CREC-SPEC.PAYLOAD-MAX @ <> IF DROP 0 EXIT THEN
        DUP _CREC-SPEC.ALIGNMENT @ 1 <> IF DROP 0 EXIT THEN
        DUP _CREC-SPEC.RECORD-U @ DUP 0< IF 2DROP 0 EXIT THEN DROP
        DUP _CREC-SPEC.PAYLOAD-MIN @
            _CREC-LENGTH-MAX CREC-HEADER-SIZE - U> IF DROP 0 EXIT THEN
        DUP _CREC-SPEC.PAYLOAD-MIN @ CREC-HEADER-SIZE +
            OVER _CREC-SPEC.RECORD-U @ <=
        NIP EXIT
    THEN
    DUP _CREC-SPEC.MODE @ CREC-MODE-FRAMED <> IF DROP 0 EXIT THEN
    DUP _CREC-SPEC.RECORD-U @ 0<> IF DROP 0 EXIT THEN
    DUP _CREC-SPEC.PAYLOAD-MIN @ DUP 0< IF 2DROP 0 EXIT THEN DROP
    DUP _CREC-SPEC.PAYLOAD-MAX @ DUP 0< IF 2DROP 0 EXIT THEN DROP
    DUP _CREC-SPEC.PAYLOAD-MIN @
        OVER _CREC-SPEC.PAYLOAD-MAX @ > IF DROP 0 EXIT THEN
    DUP _CREC-SPEC.ALIGNMENT @ _CREC-POWER-OF-TWO? 0= IF DROP 0 EXIT THEN
    DUP _CREC-SPEC.PAYLOAD-MAX @
        OVER _CREC-SPEC.ALIGNMENT @ _CREC-FRAMED-RECORD-U
    CREC-S-OK = SWAP 0> AND NIP ;

: CREC-SPEC-VALID?  ( spec -- flag )
    DUP _CREC-SPEC-BASE-VALID? 0= IF DROP 0 EXIT THEN
    DUP _CREC-SPEC.FLAGS @ _CREC-SPEC-F-SEALED <>
        IF DROP 0 EXIT THEN
    _CREC-SPEC-GEOMETRY-VALID? ;

: CREC-SPEC-SEALED?  ( spec -- flag ) CREC-SPEC-VALID? ;

\ Establish immutable common policy.  Geometry is selected separately and
\ the descriptor is admitted to operations only after CREC-SPEC-SEAL.
: CREC-SPEC-INIT
  ( magic-a magic-u format tag-policy encode-xt validate-xt spec -- status )
    DUP 0= IF _CREC-DROP7 CREC-S-INVALID EXIT THEN
    DUP CREC-SPEC-SIZE MSPAN-NONWRAPPING? 0= IF
        _CREC-DROP7 CREC-S-INVALID EXIT
    THEN
    5 PICK 8 <> IF _CREC-DROP7 CREC-S-INVALID EXIT THEN
    6 PICK 8 _CREC-READ-SPAN? 0= IF
        _CREC-DROP7 CREC-S-INVALID EXIT
    THEN
    6 PICK 8 2 PICK CREC-SPEC-SIZE MSPAN-OVERLAP? IF
        _CREC-DROP7 CREC-S-INVALID EXIT
    THEN
    4 PICK 0> 0= IF _CREC-DROP7 CREC-S-INVALID EXIT THEN
    3 PICK _CREC-TAG-POLICY? 0= IF
        _CREC-DROP7 CREC-S-INVALID EXIT
    THEN
    2 PICK 0= 1 PICK 0= OR IF _CREC-DROP7 CREC-S-INVALID EXIT THEN

    DUP CREC-SPEC-SIZE 0 FILL
    _CREC-SPEC-MAGIC OVER _CREC-SPEC.MAGIC !
    DUP DUP _CREC-SPEC.SELF !
    6 PICK OVER _CREC-SPEC.RECORD-MAGIC 8 MOVE
    4 PICK OVER _CREC-SPEC.FORMAT !
    3 PICK OVER _CREC-SPEC.TAG-POLICY !
    2 PICK OVER _CREC-SPEC.ENCODE-XT !
    1 PICK OVER _CREC-SPEC.VALIDATE-XT !
    _CREC-DROP7 CREC-S-OK ;

: CREC-SPEC-FIXED!
  ( payload-u record-u spec -- status )
    DUP _CREC-SPEC-BASE-VALID? 0= IF
        2DROP DROP CREC-S-INVALID EXIT
    THEN
    DUP _CREC-SPEC.FLAGS @ 0<>
    OVER _CREC-SPEC.MODE @ 0<> OR IF
        2DROP DROP CREC-S-BUSY EXIT
    THEN
    2 PICK 0< 1 PICK 0< OR IF
        2DROP DROP CREC-S-INVALID EXIT
    THEN
    2 PICK _CREC-LENGTH-MAX CREC-HEADER-SIZE - U> IF
        2DROP DROP CREC-S-CAPACITY EXIT
    THEN
    2 PICK CREC-HEADER-SIZE + 1 PICK > IF
        2DROP DROP CREC-S-INVALID EXIT
    THEN
    2 PICK OVER _CREC-SPEC.PAYLOAD-MIN !
    2 PICK OVER _CREC-SPEC.PAYLOAD-MAX !
    1 PICK OVER _CREC-SPEC.RECORD-U !
    1 OVER _CREC-SPEC.ALIGNMENT !
    CREC-MODE-FIXED OVER _CREC-SPEC.MODE !
    2DROP DROP CREC-S-OK ;

: CREC-SPEC-FRAMED!
  ( payload-min payload-max alignment spec -- status )
    DUP _CREC-SPEC-BASE-VALID? 0= IF
        2DROP 2DROP CREC-S-INVALID EXIT
    THEN
    DUP _CREC-SPEC.FLAGS @ 0<>
    OVER _CREC-SPEC.MODE @ 0<> OR IF
        2DROP 2DROP CREC-S-BUSY EXIT
    THEN
    3 PICK 0< 3 PICK 0< OR IF
        2DROP 2DROP CREC-S-INVALID EXIT
    THEN
    \ The first PICK leaves an extra cell on the stack, so the original
    \ maximum moves from depth two to depth three for this comparison.
    3 PICK 3 PICK > IF 2DROP 2DROP CREC-S-INVALID EXIT THEN
    1 PICK _CREC-POWER-OF-TWO? 0= IF
        2DROP 2DROP CREC-S-INVALID EXIT
    THEN
    2 PICK 2 PICK _CREC-FRAMED-RECORD-U
    DUP IF
        >R DROP 2DROP 2DROP R> EXIT
    THEN
    2DROP
    3 PICK OVER _CREC-SPEC.PAYLOAD-MIN !
    2 PICK OVER _CREC-SPEC.PAYLOAD-MAX !
    1 PICK OVER _CREC-SPEC.ALIGNMENT !
    CREC-MODE-FRAMED OVER _CREC-SPEC.MODE !
    2DROP 2DROP CREC-S-OK ;

: CREC-SPEC-SEAL  ( spec -- status )
    DUP _CREC-SPEC-BASE-VALID? 0= IF DROP CREC-S-INVALID EXIT THEN
    DUP _CREC-SPEC.FLAGS @ IF DROP CREC-S-BUSY EXIT THEN
    DUP _CREC-SPEC-GEOMETRY-VALID? 0= IF DROP CREC-S-INVALID EXIT THEN
    _CREC-SPEC-F-SEALED SWAP _CREC-SPEC.FLAGS ! CREC-S-OK ;

: CREC-MEASURE  ( payload-u spec -- record-u status )
    DUP CREC-SPEC-VALID? 0= IF 2DROP 0 CREC-S-INVALID EXIT THEN
    >R
    DUP 0< IF DROP R> DROP 0 CREC-S-INVALID EXIT THEN
    DUP R@ _CREC-SPEC.PAYLOAD-MIN @ < IF
        DROP R> DROP 0 CREC-S-INVALID EXIT
    THEN
    DUP R@ _CREC-SPEC.PAYLOAD-MAX @ > IF
        DROP R> DROP 0 CREC-S-CAPACITY EXIT
    THEN
    R@ _CREC-SPEC.MODE @ CREC-MODE-FIXED = IF
        DROP R@ _CREC-SPEC.RECORD-U @ R> DROP CREC-S-OK EXIT
    THEN
    R@ _CREC-SPEC.ALIGNMENT @ _CREC-FRAMED-RECORD-U
    R> DROP ;

: _CREC-TAG-VALID?  ( tag spec -- flag )
    _CREC-SPEC.TAG-POLICY @
    DUP CREC-TAG-ANY = IF 2DROP -1 EXIT THEN
    CREC-TAG-ZERO = IF 0= ELSE 0> THEN ;

