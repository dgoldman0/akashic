: CREC-STATUS-VALID?  ( status -- flag )
    DUP CREC-S-OK >= SWAP CREC-S-FAULT <= AND ;

: _CREC-TAG-POLICY?  ( policy -- flag )
    DUP CREC-TAG-ANY =
    OVER CREC-TAG-ZERO = OR
    SWAP CREC-TAG-POSITIVE = OR ;

: _CREC-POWER-OF-TWO?  ( u -- flag )
    DUP 0> SWAP DUP 1- AND 0= AND ;

: _CREC-READ-SPAN?  ( address length -- flag )
    DUP 0< IF 2DROP 0 EXIT THEN
    DUP 0= IF 2DROP -1 EXIT THEN
    OVER 0= IF 2DROP 0 EXIT THEN
    MSPAN-NONWRAPPING? ;

: _CREC-CONTEXT-SPAN?  ( address length -- flag )
    DUP 0< IF 2DROP 0 EXIT THEN
    DUP 0= IF DROP 0= EXIT THEN
    OVER 0= IF 2DROP 0 EXIT THEN
    MSPAN-NONWRAPPING? ;

: _CREC-ZERO?  ( address length -- flag )
    0 ?DO
        DUP I + C@ IF DROP 0 UNLOOP EXIT THEN
    LOOP
    DROP -1 ;

: _CREC-DROP7  ( x1 x2 x3 x4 x5 x6 x7 -- )
    2DROP 2DROP 2DROP DROP ;

: _CREC-DROP8  ( x1 x2 x3 x4 x5 x6 x7 x8 -- )
    2DROP 2DROP 2DROP 2DROP ;

\ Return a nonnegative, signed-length-safe aligned envelope size.
: _CREC-FRAMED-RECORD-U  ( payload-u alignment -- record-u status )
    DUP _CREC-POWER-OF-TWO? 0= IF
        2DROP 0 CREC-S-INVALID EXIT
    THEN
    >R
    DUP 0< IF DROP R> DROP 0 CREC-S-INVALID EXIT THEN
    DUP _CREC-LENGTH-MAX CREC-HEADER-SIZE - U> IF
        DROP R> DROP 0 CREC-S-CAPACITY EXIT
    THEN
    CREC-HEADER-SIZE +
    R@ 1- _CREC-LENGTH-MAX SWAP - OVER U< IF
        DROP R> DROP 0 CREC-S-CAPACITY EXIT
    THEN
    R@ 1- + R> NEGATE AND CREC-S-OK ;

