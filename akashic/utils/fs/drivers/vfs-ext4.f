\ =====================================================================
\  Private reflected CRC32C (ext4/e2fsprogs semantics)
\ =====================================================================
\
\ The platform CRC32C word is intentionally MSB-first.  ext4 uses the
\ reflected Castagnoli kernel with polynomial 0x82f63b78, seed 0xffffffff,
\ and no final xor.  Keep this implementation private to avoid conflating
\ the two contracts.

CREATE _EXT4-CRC-TABLE 256 CELLS ALLOT
VARIABLE _EXT4-CRC-WORK
VARIABLE _EXT4-CRC-STATE

: _EXT4-CRC-TABLE-INIT  ( -- )
    256 0 DO
        I _EXT4-CRC-WORK !
        8 0 DO
            _EXT4-CRC-WORK @ DUP 1 AND IF
                1 RSHIFT 0x82F63B78 XOR
            ELSE
                1 RSHIFT
            THEN
            0xFFFFFFFF AND _EXT4-CRC-WORK !
        LOOP
        _EXT4-CRC-WORK @ _EXT4-CRC-TABLE I CELLS + !
    LOOP ;

_EXT4-CRC-TABLE-INIT

: _EXT4-CRC-START  ( seed -- )  0xFFFFFFFF AND _EXT4-CRC-STATE ! ;
: _EXT4-CRC-ADD  ( addr len -- )
    0 ?DO
        DUP I + C@ _EXT4-CRC-STATE @ XOR 0xFF AND
        CELLS _EXT4-CRC-TABLE + @
        _EXT4-CRC-STATE @ 8 RSHIFT XOR 0xFFFFFFFF AND
        _EXT4-CRC-STATE !
    LOOP DROP ;
: _EXT4-CRC@  ( -- crc )  _EXT4-CRC-STATE @ ;

