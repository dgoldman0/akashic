        EXT4-D-BACKUP-SUPER _EXT4-CORRUPT EXIT
    THEN
    _EXT4-BS-BUF @ _EXT4-BS-CTX @ _EXT4-BACKUP-SUPER-FIELDS? 0= IF
        EXT4-D-BACKUP-SUPER _EXT4-CORRUPT EXIT
    THEN
