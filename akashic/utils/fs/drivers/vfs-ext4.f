    _EXT4-DX-CTX ! _EXT4-DX-ENTRY ! _EXT4-DX-BUF !
    _EXT4-DX-ENTRY @ _EXT4-DX-LIMIT @ 8 * + DUP _EXT4-DX-TAIL !
    DUP _EXT4-DX-CTX @ _EXT4-C.BSIZE + @ _EXT4-DX-BUF @ + 8 - <> IF
        DROP EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT
    THEN
    DUP L@ IF DROP EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT THEN
    4 + L@ _EXT4-DX-STORED !
    _EXT4-DX-DIRINO @ _EXT4-DX-CTX @ _EXT4-C.TMP + L!
    _EXT4-DX-GEN @ _EXT4-DX-CTX @ _EXT4-C.TMP 4 + + L!
    _EXT4-DX-CTX @ _EXT4-C.SEED + @ _EXT4-CRC-START
    _EXT4-DX-CTX @ _EXT4-C.TMP + 8 _EXT4-CRC-ADD
    _EXT4-DX-BUF @
    _EXT4-DX-ENTRY @ _EXT4-DX-BUF @ - _EXT4-DX-COUNT @ 8 * +
    _EXT4-CRC-ADD
    0 _EXT4-DX-CTX @ _EXT4-C.TMP 8 + + L!
    0 _EXT4-DX-CTX @ _EXT4-C.TMP 12 + + L!
    _EXT4-DX-CTX @ _EXT4-C.TMP 8 + + 8 _EXT4-CRC-ADD
    _EXT4-CRC@ _EXT4-DX-STORED @ <> IF
        EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT
    THEN
    0 ;

: _EXT4-VALIDATE-DX-ROOT  ( ctx dir-blocks -- ior )
    _EXT4-DX-DIR-BLOCKS ! DUP _EXT4-DX-CTX !
    _EXT4-C.DIR-BLOCK + DUP _EXT4-DX-BUF !
    DUP L@ _EXT4-DX-DIRINO @ <>
    OVER 4 + W@ 12 <> OR
    OVER 6 + C@ 1 <> OR
    OVER 7 + C@ 2 <> OR
    OVER 8 + C@ [CHAR] . <> OR IF
        DROP EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT
    THEN
    DUP 12 + L@ _EXT4-DX-PARINO @ <>
    OVER 16 + W@ _EXT4-DX-CTX @ _EXT4-C.BSIZE + @ 12 - <> OR
    OVER 18 + C@ 2 <> OR
    OVER 19 + C@ 2 <> OR
    OVER 20 + C@ [CHAR] . <> OR
    OVER 21 + C@ [CHAR] . <> OR IF
        DROP EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT
    THEN
    DUP 24 + L@ IF DROP EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT THEN
    DUP 28 + C@ 1 <>
    OVER 29 + C@ 8 <> OR
    OVER 31 + C@ 0<> OR IF
        DROP EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT
    THEN
    DUP 30 + C@ DUP _EXT4-DX-LEVEL ! 1 U> IF
        DROP EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT
    THEN
    32 +
    _EXT4-DX-CTX @ _EXT4-C.BSIZE + @ 40 - 8 /
    _EXT4-DX-DIR-BLOCKS @ _EXT4-DX-CTX @
    _EXT4-VALIDATE-DX-ENTRIES ?DUP IF EXIT THEN
    _EXT4-DX-BUF @ _EXT4-DX-ENTRY @ _EXT4-DX-CTX @
    _EXT4-VALIDATE-DX-CHECKSUM ;

: _EXT4-DX-NODE?  ( ctx -- flag )
    DUP _EXT4-DX-CTX ! _EXT4-C.DIR-BLOCK + _EXT4-DX-BUF !
    _EXT4-DX-BUF @ L@ 0=
    _EXT4-DX-BUF @ 4 + W@ _EXT4-DX-CTX @ _EXT4-C.BSIZE + @ = AND
    _EXT4-DX-BUF @ 6 + C@ 0= AND
    _EXT4-DX-BUF @ 7 + C@ 0= AND ;

: _EXT4-VALIDATE-DX-NODE  ( ctx dir-blocks -- ior )
    _EXT4-DX-DIR-BLOCKS ! DUP _EXT4-DX-CTX !
    _EXT4-C.DIR-BLOCK + DUP _EXT4-DX-BUF ! 8 +
    _EXT4-DX-CTX @ _EXT4-C.BSIZE + @ 16 - 8 /
    _EXT4-DX-DIR-BLOCKS @ _EXT4-DX-CTX @
    _EXT4-VALIDATE-DX-ENTRIES ?DUP IF EXIT THEN
    _EXT4-DX-BUF @ _EXT4-DX-ENTRY @ _EXT4-DX-CTX @
    _EXT4-VALIDATE-DX-CHECKSUM ;

