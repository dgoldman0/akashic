VARIABLE _EXT4-BM-INODE-BITMAP
VARIABLE _EXT4-BM-INODE-TABLE
VARIABLE _EXT4-BM-INODE-SPAN

: _EXT4-VALIDATE-BITMAPS  ( group ctx -- ior )
    _EXT4-BM-CTX ! _EXT4-BM-GROUP !
    _EXT4-BM-GROUP @ _EXT4-BM-CTX @ _EXT4-LOAD-DESC ?DUP IF EXIT THEN
    _EXT4-BM-CTX @ _EXT4-C.DESC + DUP
        _EXT4-GD.BLOCK-BITMAP-LO + L@ _EXT4-BM-BLOCK-BITMAP !
    DUP _EXT4-GD.INODE-BITMAP-LO + L@ _EXT4-BM-INODE-BITMAP !
    DUP _EXT4-GD.INODE-TABLE-LO + L@ _EXT4-BM-INODE-TABLE ! DROP
    _EXT4-BM-CTX @ _EXT4-C.IPG + @ _EXT4-BM-CTX @ _EXT4-C.ISIZE + @ *
    _EXT4-BM-CTX @ _EXT4-C.BSIZE + @ 1- +
    _EXT4-BM-CTX @ _EXT4-C.BSIZE + @ / _EXT4-BM-INODE-SPAN !
    _EXT4-GD-FLAGS @ _EXT4-BG-BLOCK-UNINIT AND 0= IF
        _EXT4-BM-CTX @ _EXT4-C.DESC + DUP
        _EXT4-GD.BLOCK-CSUM-LO + W@
        SWAP _EXT4-GD.BLOCK-CSUM-HI + W@ 16 LSHIFT OR
        _EXT4-BM-STORED !
        _EXT4-BM-CTX @ _EXT4-C.DESC + _EXT4-GD.BLOCK-BITMAP-LO + L@
        _EXT4-BM-CTX @ _EXT4-READ-BLOCK ?DUP IF EXIT THEN
        _EXT4-BM-CTX @ _EXT4-C.SEED + @ _EXT4-CRC-START
        _EXT4-BM-CTX @ _EXT4-C.BLOCK +
        _EXT4-BM-CTX @ _EXT4-C.BPG + @ 8 / _EXT4-CRC-ADD
        _EXT4-CRC@ _EXT4-BM-STORED @ <> IF
            EXT4-D-BITMAP-CHECKSUM _EXT4-CORRUPT EXIT
        THEN
    THEN
    _EXT4-GD-FLAGS @ _EXT4-BG-INODE-UNINIT AND 0= IF
        _EXT4-BM-CTX @ _EXT4-C.DESC + DUP
        _EXT4-GD.INODE-CSUM-LO + W@
        SWAP _EXT4-GD.INODE-CSUM-HI + W@ 16 LSHIFT OR
        _EXT4-BM-STORED !
        _EXT4-BM-CTX @ _EXT4-C.DESC + _EXT4-GD.INODE-BITMAP-LO + L@
        _EXT4-BM-CTX @ _EXT4-READ-BLOCK ?DUP IF EXIT THEN
        _EXT4-BM-CTX @ _EXT4-C.SEED + @ _EXT4-CRC-START
        _EXT4-BM-CTX @ _EXT4-C.BLOCK +
        _EXT4-BM-CTX @ _EXT4-C.IPG + @ 8 / _EXT4-CRC-ADD
        _EXT4-CRC@ _EXT4-BM-STORED @ <> IF
            EXT4-D-BITMAP-CHECKSUM _EXT4-CORRUPT EXIT
        THEN
    THEN
    \ An uninitialized block bitmap has no authoritative ownership bits yet.
    \ Descriptor validation already bounds its reserved metadata locations;
    \ ordinary inode pointers into such a group remain rejected by the shared
    \ allocation helper.
    _EXT4-GD-FLAGS @ _EXT4-BG-BLOCK-UNINIT AND IF 0 EXIT THEN
    _EXT4-BM-BLOCK-BITMAP @ EXT4-D-BITMAP-CHECKSUM _EXT4-BM-CTX @
    _EXT4-REQUIRE-ALLOCATED-BLOCK ?DUP IF EXIT THEN
    _EXT4-BM-INODE-BITMAP @ EXT4-D-BITMAP-CHECKSUM _EXT4-BM-CTX @
    _EXT4-REQUIRE-ALLOCATED-BLOCK ?DUP IF EXIT THEN
    _EXT4-BM-INODE-TABLE @ _EXT4-BM-INODE-SPAN @
    EXT4-D-BITMAP-CHECKSUM _EXT4-BM-CTX @
    _EXT4-REQUIRE-ALLOCATED-RANGE ;

VARIABLE _EXT4-VG-CTX
VARIABLE _EXT4-VG-FREE-BLOCKS
VARIABLE _EXT4-VG-FREE-INODES
VARIABLE _EXT4-VG-ADD
: _EXT4-VALIDATE-GROUPS  ( ctx -- ior )
    _EXT4-VG-CTX !
    0 _EXT4-VG-FREE-BLOCKS ! 0 _EXT4-VG-FREE-INODES !
    _EXT4-VG-CTX @ _EXT4-C.GROUPS + @ 0 DO
        I _EXT4-VG-CTX @ _EXT4-VALIDATE-BITMAPS ?DUP IF
            UNLOOP EXIT
        THEN
        \ Allocation ownership checks may load the descriptor for a different
        \ physical group under flex_bg; restore the counter source explicitly.
        I _EXT4-VG-CTX @ _EXT4-LOAD-DESC ?DUP IF UNLOOP EXIT THEN
        _EXT4-VG-CTX @ _EXT4-C.DESC + DUP
        _EXT4-GD.FREE-BLOCKS-LO + W@
        SWAP _EXT4-GD.FREE-BLOCKS-HI + W@ 16 LSHIFT OR
        DUP _EXT4-VG-ADD ! _EXT4-VG-FREE-BLOCKS @ +
        DUP _EXT4-VG-ADD @ U< IF
            DROP EXT4-D-GEOMETRY _EXT4-CORRUPT UNLOOP EXIT
        THEN _EXT4-VG-FREE-BLOCKS !
        _EXT4-VG-CTX @ _EXT4-C.DESC + DUP
        _EXT4-GD.FREE-INODES-LO + W@
        SWAP _EXT4-GD.FREE-INODES-HI + W@ 16 LSHIFT OR
        DUP _EXT4-VG-ADD ! _EXT4-VG-FREE-INODES @ +
        DUP _EXT4-VG-ADD @ U< IF
            DROP EXT4-D-GEOMETRY _EXT4-CORRUPT UNLOOP EXIT
        THEN _EXT4-VG-FREE-INODES !
    LOOP
    _EXT4-VG-FREE-BLOCKS @ _EXT4-VG-CTX @ _EXT4-C.FREE-BLOCKS + @ <>
    _EXT4-VG-FREE-INODES @ _EXT4-VG-CTX @ _EXT4-C.FREE-INODES + @ <> OR IF
        EXT4-D-GEOMETRY _EXT4-CORRUPT EXIT
    THEN
    0 ;

