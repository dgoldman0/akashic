            _EXT4-DX-HASH @ 1 AND 0= AND IF
                DROP EXT4-D-DIRECTORY _EXT4-CORRUPT UNLOOP EXIT
            THEN
            _EXT4-DX-PREV-HASH !
        THEN
        _EXT4-DX-ENTRY @ I 8 * + 4 + L@ DUP
        0xFF000000 AND IF
            DROP EXT4-D-DIRECTORY _EXT4-CORRUPT UNLOOP EXIT
        THEN
