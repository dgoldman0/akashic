: _EXT4-RD-ROLLBACK  ( -- )
    BEGIN
        _EXT4-RD-PARENT @ IN.CHILD @ DUP _EXT4-RD-OLD-CHILD @ <>
    WHILE
        DUP _EXT4-RD-PARENT @ _VFS-REMOVE-CHILD
        FALSE _EXT4-RD-V @ _VFS-DENTRY-RELEASE
    REPEAT DROP
    _EXT4-RD-OLD-COUNT @ _EXT4-RD-V @ V.ICOUNT !
    _EXT4-RD-OLD-STR @ _EXT4-RD-V @ V.STR-PTR ! ;

: _EXT4-RD-LOAD-LOGICAL  ( logical-block -- ior )
    _EXT4-RD-PARENT @ IN.BID @ _EXT4-RD-CTX @ _EXT4-LOAD-INODE
    ?DUP IF NIP EXIT THEN
    _EXT4-RD-CTX @ _EXT4-MAP-BLOCK
    _EXT4-RD-IOR ! _EXT4-RD-PRESENT ! _EXT4-RD-PHYS !
    _EXT4-RD-IOR @ ?DUP IF EXIT THEN
    _EXT4-RD-PRESENT @ 0= IF EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT THEN
    _EXT4-RD-PHYS @ _EXT4-RD-CTX @ _EXT4-READ-BLOCK ?DUP IF EXIT THEN
    _EXT4-RD-CTX @ _EXT4-C.BLOCK +
    _EXT4-RD-CTX @ _EXT4-C.DIR-BLOCK +
    _EXT4-RD-CTX @ _EXT4-C.BSIZE + @ CMOVE
    0 ;

: _EXT4-READDIR-LINEAR  ( -- ior )
    0 _EXT4-DS-DOT ! 0 _EXT4-DS-DOTDOT !
    _EXT4-RD-BLOCKS @ 0 ?DO
        I _EXT4-RD-LOAD-LOGICAL ?DUP IF UNLOOP EXIT THEN
        _EXT4-RD-PARENT @ _EXT4-RD-V @ _EXT4-RD-CTX @
        _EXT4-SCAN-DIR-BLOCK ?DUP IF UNLOOP EXIT THEN
    LOOP
    _EXT4-DS-DOT @ 0= _EXT4-DS-DOTDOT @ 0= OR IF
        EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT
    THEN
    0 ;

: _EXT4-READDIR-HTREE  ( -- ior )
    _EXT4-RD-PARENT @ IN.BID @ _EXT4-DX-DIRINO !
    _EXT4-RD-PARENT @ IN.PARENT @ ?DUP IF IN.BID @ ELSE
        _EXT4-DX-DIRINO @
    THEN
    _EXT4-DX-PARINO !
    _EXT4-RD-PARENT @ D.VNODE @ VN.GEN @ _EXT4-DX-GEN !
    0 _EXT4-RD-LOAD-LOGICAL ?DUP IF EXIT THEN
    _EXT4-RD-CTX @ _EXT4-RD-BLOCKS @ _EXT4-VALIDATE-DX-ROOT
    ?DUP IF EXIT THEN
    -1 _EXT4-DS-DOT ! -1 _EXT4-DS-DOTDOT !
    0 _EXT4-RD-NODE-SEEN ! 0 _EXT4-RD-LEAF-SEEN !
    _EXT4-DX-LEVEL @ IF
        _EXT4-DX-COUNT @ _EXT4-RD-NODE-EXPECTED !
        0 _EXT4-RD-LEAF-EXPECTED !
    ELSE
        0 _EXT4-RD-NODE-EXPECTED !
        _EXT4-DX-COUNT @ _EXT4-RD-LEAF-EXPECTED !
    THEN
    _EXT4-RD-BLOCKS @ 1 ?DO
        I _EXT4-RD-LOAD-LOGICAL ?DUP IF UNLOOP EXIT THEN
        _EXT4-RD-CTX @ _EXT4-DX-NODE? IF
            _EXT4-DX-LEVEL @ 0= IF
                EXT4-D-DIRECTORY _EXT4-CORRUPT UNLOOP EXIT
            THEN
            _EXT4-RD-CTX @ _EXT4-RD-BLOCKS @ _EXT4-VALIDATE-DX-NODE
            ?DUP IF UNLOOP EXIT THEN
            1 _EXT4-RD-NODE-SEEN +!
            _EXT4-RD-LEAF-EXPECTED @ _EXT4-DX-COUNT @ + DUP
            _EXT4-RD-LEAF-EXPECTED @ U< IF
                DROP EXT4-D-DIRECTORY _EXT4-CORRUPT UNLOOP EXIT
            THEN _EXT4-RD-LEAF-EXPECTED !
        ELSE
            1 _EXT4-RD-LEAF-SEEN +!
            _EXT4-RD-PARENT @ _EXT4-RD-V @ _EXT4-RD-CTX @
            _EXT4-SCAN-DIR-BLOCK ?DUP IF UNLOOP EXIT THEN
        THEN
    LOOP
    _EXT4-RD-NODE-SEEN @ _EXT4-RD-NODE-EXPECTED @ <>
    _EXT4-RD-LEAF-SEEN @ _EXT4-RD-LEAF-EXPECTED @ <> OR
    _EXT4-RD-NODE-SEEN @ _EXT4-RD-LEAF-SEEN @ + 1+
        _EXT4-RD-BLOCKS @ <> OR IF
        EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT
    THEN
    0 ;

: _EXT4-READDIR-WORK  ( -- ior )
    _EXT4-RD-PARENT @ IN.BID @ _EXT4-RD-CTX @ _EXT4-LOAD-INODE
    ?DUP IF EXIT THEN
    _EXT4-RD-CTX @ _EXT4-STAGE-CURRENT-INODE
    _EXT4-RD-IOR ! DUP VFS-T-DIR <> IF
        DROP _EXT4-RD-IOR @ ?DUP IF EXIT THEN VFS-E-NOTDIR EXIT
    THEN DROP
    _EXT4-RD-IOR @ ?DUP IF EXIT THEN
    _EXT4-RD-CTX @ _EXT4-C.R.SIZE + @ DUP _EXT4-RD-SIZE !
    DUP 0= IF DROP EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT THEN
    DUP 0x7FFFFFFF U> IF
        DROP EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT
    THEN
    DUP _EXT4-RD-CTX @ _EXT4-C.BSIZE + @ MOD IF
        DROP EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT
    THEN
    _EXT4-RD-CTX @ _EXT4-C.BSIZE + @ 1- +
    _EXT4-RD-CTX @ _EXT4-C.BSIZE + @ / DUP _EXT4-RD-BLOCKS !
    DROP
    _EXT4-RD-CTX @ _EXT4-C.INODE + _EXT4-I.FLAGS + L@
    _EXT4-INDEX-FL AND IF
        _EXT4-READDIR-HTREE
    ELSE
        _EXT4-READDIR-LINEAR
    THEN
    ;

: _EXT4-READDIR  ( directory vfs -- ior )
    DUP _EXT4-READY? 0= IF 2DROP VFS-E-BUSY EXIT THEN
    _EXT4-RD-V ! _EXT4-RD-PARENT !
    _EXT4-RD-V @ _EXT4-CTX _EXT4-RD-CTX !
    _EXT4-RD-PARENT @ IN.CHILD @ _EXT4-RD-OLD-CHILD !
    _EXT4-RD-V @ V.ICOUNT @ _EXT4-RD-OLD-COUNT !
    _EXT4-RD-V @ V.STR-PTR @ _EXT4-RD-OLD-STR !
    _EXT4-RD-V @ V.VOLUME @ _EXT4-IO-VOL !
    _EXT4-RD-V @ _EXT4-IO-VFS !
    _EXT4-READDIR-WORK DUP IF _EXT4-RD-ROLLBACK THEN ;

