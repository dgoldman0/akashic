: _EXT4-MOUNT  ( vfs -- ior )
    DUP _EXT4-M-V !
    DUP _EXT4-ATTACHED? 0= IF DROP EXT4-D-ATTACHMENT _EXT4-CORRUPT EXIT THEN
    DROP
    _EXT4-M-V @ V.VOLUME @ DUP VOL.SECTOR-SIZE _EXT4-SECTOR <> IF
        DROP 0 0 VFS-IOR-D-VOLUME VFS-R-UNSUPPORTED VFS-IOR-MAKE EXIT
    THEN
    _EXT4-IO-VOL ! _EXT4-M-V @ _EXT4-IO-VFS !
    _EXT4-M-V @ V.BCTX @ ?DUP IF
        _EXT4-M-CTX !
    ELSE
        _EXT4-M-V @ V.ARENA @ _EXT4-CTX-SIZE ARENA-ALLOT?
        IF DROP VFS-E-NOMEM EXIT THEN
        DUP _EXT4-M-CTX ! _EXT4-M-V @ V.BCTX !
    THEN
    _EXT4-M-CTX @ _EXT4-CTX-SIZE 0 FILL
    _EXT4-M-CTX @ _EXT4-C.SB + 2 2 _EXT4-READ-SECTORS
    DUP IF EXIT THEN DROP
    _EXT4-M-CTX @ _EXT4-M-V @ _EXT4-VALIDATE-SUPER
    DUP IF EXIT THEN DROP
    _EXT4-M-CTX @ _EXT4-VALIDATE-GROUPS
    DUP IF EXIT THEN DROP
    _EXT4-M-CTX @ _EXT4-VALIDATE-BACKUPS
    DUP IF EXIT THEN DROP
    _EXT4-M-CTX @ _EXT4-VALIDATE-JOURNAL
    DUP IF EXIT THEN DROP
    _EXT4-M-CTX @ _EXT4-VALIDATE-ORPHAN-FILE
    DUP IF EXIT THEN DROP
    2 _EXT4-M-CTX @ _EXT4-LOAD-INODE
    DUP IF EXIT THEN DROP
    _EXT4-M-CTX @ _EXT4-STAGE-ROOT
    DUP IF EXIT THEN DROP
    _EXT4-M-V @ _EXT4-ATTACHED? 0= IF
        EXT4-D-ATTACHMENT _EXT4-CORRUPT EXIT
    THEN
    _EXT4-M-CTX @ _EXT4-M-V @ _EXT4-PUBLISH-ROOT
    -1 _EXT4-M-CTX @ _EXT4-C.READY + !
    0 ;

