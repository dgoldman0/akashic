: _EXT4-READ-DATA-WORK  ( -- ior )
    _EXT4-DR-IN @ IN.BID @ _EXT4-DR-CTX @ _EXT4-LOAD-INODE
    ?DUP IF EXIT THEN
    _EXT4-DR-CTX @ _EXT4-STAGE-CURRENT-INODE
    _EXT4-DR-IOR ! _EXT4-DR-TYPE !
    _EXT4-DR-IOR @ ?DUP IF EXIT THEN
    _EXT4-DR-TYPE @ VFS-T-DIR = IF VFS-E-ISDIR EXIT THEN
    _EXT4-DR-TYPE @ VFS-T-SPECIAL = IF VFS-E-UNSUPPORTED EXIT THEN
    _EXT4-DR-OFF @ _EXT4-DR-CTX @ _EXT4-C.R.SIZE + @ >= IF 0 EXIT THEN
    _EXT4-DR-CTX @ _EXT4-C.R.SIZE + @ _EXT4-DR-OFF @ -
    _EXT4-DR-LEN @ MIN _EXT4-DR-REM !
    _EXT4-DR-TYPE @ VFS-T-SYMLINK =
    _EXT4-DR-CTX @ _EXT4-C.R.SIZE + @ 60 <= AND
    _EXT4-DR-CTX @ _EXT4-C.R.BLOCKS + @ 0= AND IF
        _EXT4-DR-CTX @ _EXT4-C.INODE + _EXT4-I.BLOCK +
        _EXT4-DR-OFF @ + _EXT4-DR-BUF @ _EXT4-DR-REM @ CMOVE
        _EXT4-DR-REM @ _EXT4-DR-ACT +!
        0 EXIT
    THEN
    BEGIN _EXT4-DR-REM @ 0> WHILE
        _EXT4-DR-OFF @ _EXT4-DR-CTX @ _EXT4-C.BSIZE + @ /MOD
        _EXT4-DR-LBLOCK ! _EXT4-DR-BOFF !
        _EXT4-DR-CTX @ _EXT4-C.BSIZE + @ _EXT4-DR-BOFF @ -
        _EXT4-DR-REM @ MIN _EXT4-DR-CHUNK !
        _EXT4-DR-LBLOCK @ _EXT4-DR-CTX @ _EXT4-MAP-BLOCK
        _EXT4-DR-IOR ! _EXT4-DR-PRESENT ! _EXT4-DR-PHYS !
        _EXT4-DR-IOR @ ?DUP IF EXIT THEN
        _EXT4-DR-PRESENT @ IF
            _EXT4-DR-PHYS @ _EXT4-DR-CTX @ _EXT4-READ-BLOCK
            ?DUP IF EXIT THEN
            _EXT4-DR-CTX @ _EXT4-C.BLOCK + _EXT4-DR-BOFF @ +
            _EXT4-DR-BUF @ _EXT4-DR-CHUNK @ CMOVE
        ELSE
            _EXT4-DR-BUF @ _EXT4-DR-CHUNK @ 0 FILL
        THEN
        _EXT4-DR-CHUNK @ DUP _EXT4-DR-ACT +!
        DUP _EXT4-DR-BUF +!
        DUP _EXT4-DR-OFF +!
        NEGATE _EXT4-DR-REM +!
    REPEAT
    0 ;

: _EXT4-READ  ( buffer len offset dentry vfs -- actual ior )
    DUP _EXT4-READY? 0= IF 2DROP 2DROP DROP 0 VFS-E-BUSY EXIT THEN
    _EXT4-DR-V ! _EXT4-DR-IN ! _EXT4-DR-OFF !
    _EXT4-DR-LEN ! _EXT4-DR-BUF !
    _EXT4-DR-OFF @ 0< _EXT4-DR-LEN @ 0< OR IF 0 VFS-E-INVALID EXIT THEN
    _EXT4-DR-V @ _EXT4-CTX _EXT4-DR-CTX !
    _EXT4-DR-V @ V.VOLUME @ _EXT4-IO-VOL !
    _EXT4-DR-V @ _EXT4-IO-VFS !
    0 _EXT4-DR-ACT !
    _EXT4-READ-DATA-WORK _EXT4-DR-IOR !
    _EXT4-DR-ACT @ _EXT4-DR-IOR @ _EXT4-PARTIAL-IOR ;

: _EXT4-OPEN  ( dentry vfs -- cookie ior )
    DUP _EXT4-READY? 0= IF 2DROP 0 VFS-E-BUSY EXIT THEN
    DROP DUP IN.TYPE @ VFS-T-SPECIAL = IF DROP 0 VFS-E-UNSUPPORTED EXIT THEN
    DUP D.VNODE @ VN.GEN @ 32 LSHIFT SWAP IN.BID @ OR 0 ;

: _EXT4-RELEASE  ( cookie dentry vfs -- ior )  DROP 2DROP 0 ;

