: _EXT4-SUPER-CHECKSUM?  ( sb -- flag )
    DUP _EXT4-SB.CHECKSUM + L@ _EXT4-SV-STORED !
    0xFFFFFFFF _EXT4-CRC-START
    _EXT4-SB.CHECKSUM _EXT4-CRC-ADD
    _EXT4-CRC@ _EXT4-SV-STORED @ = ;

: _EXT4-SUPER-SEED?  ( sb -- flag )
    DUP _EXT4-SB.CSUM-SEED + L@ >R
    0xFFFFFFFF _EXT4-CRC-START
    _EXT4-SB.UUID + 16 _EXT4-CRC-ADD
    _EXT4-CRC@ R> = ;

: _EXT4-VALIDATE-SUPER  ( ctx vfs -- ior )
    DROP _EXT4-SV-CTX !
    _EXT4-SV-CTX @ _EXT4-C.SB + _EXT4-SV-SB !
    _EXT4-SV-SB @ _EXT4-SB.MAGIC + W@ _EXT4-MAGIC <> IF
        EXT4-D-MAGIC _EXT4-UNSUPPORTED EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SUPER-CHECKSUM? 0= IF
        EXT4-D-SUPER-CHECKSUM _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SUPER-SEED? 0= IF
        EXT4-D-SUPER-CHECKSUM _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.REVISION + L@ 1 <
    _EXT4-SV-SB @ _EXT4-SB.CREATOR + L@ 0<> OR IF
        EXT4-D-GEOMETRY _EXT4-UNSUPPORTED EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.COMPAT + L@ _EXT4-COMPAT-V1 <> IF
        EXT4-D-FEATURE _EXT4-UNSUPPORTED EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.INCOMPAT + L@ DUP 4 AND IF
        DROP EXT4-D-RECOVERY _EXT4-UNSUPPORTED EXIT
    THEN
    _EXT4-INCOMPAT-V1 <> IF
        EXT4-D-FEATURE _EXT4-UNSUPPORTED EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.RO-COMPAT + L@ DUP 0x10000 AND IF
        DROP EXT4-D-RECOVERY _EXT4-UNSUPPORTED EXIT
    THEN
    DUP _EXT4-RO-REFUSED AND IF
        DROP EXT4-D-FEATURE _EXT4-UNSUPPORTED EXIT
    THEN
    _EXT4-RO-READONLY INVERT AND _EXT4-SV-RO !
    _EXT4-SV-SB @ _EXT4-SB.INODE-SIZE + W@ DUP
    128 = IF
        DROP _EXT4-SV-RO @ _EXT4-RO-I128-V1 AND
        _EXT4-RO-I128-V1 <>
    ELSE
        256 <> _EXT4-SV-RO @ _EXT4-RO-V1 AND
        _EXT4-RO-V1 <> OR
    THEN IF EXT4-D-FEATURE _EXT4-UNSUPPORTED EXIT THEN
    _EXT4-SV-SB @ _EXT4-SB.STATE + W@ 1 <>
    _EXT4-SV-SB @ _EXT4-SB.LAST-ORPHAN + L@ 0<> OR IF
        EXT4-D-RECOVERY _EXT4-UNSUPPORTED EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.ERRORS + W@ 2 <> IF
        EXT4-D-GEOMETRY _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.LOG-BLOCK + L@ DUP _EXT4-SV-LOG ! 2 > IF
        EXT4-D-GEOMETRY _EXT4-UNSUPPORTED EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.LOG-CLUSTER + L@ _EXT4-SV-LOG @ <> IF
        EXT4-D-GEOMETRY _EXT4-UNSUPPORTED EXIT
    THEN
    1024 _EXT4-SV-LOG @ LSHIFT DUP
    _EXT4-SV-CTX @ _EXT4-C.BSIZE + !
    512 / _EXT4-SV-CTX @ _EXT4-C.SPB + !
    _EXT4-SV-SB @ _EXT4-SB.BLOCKS-HI + L@ 0<> IF
        EXT4-D-BOUNDS _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.BLOCKS-LO + L@
    _EXT4-SV-CTX @ _EXT4-C.BLOCKS + !
    _EXT4-SV-SB @ _EXT4-SB.FIRST-DATA + L@ DUP
    _EXT4-SV-CTX @ _EXT4-C.FIRST + !
    _EXT4-SV-LOG @ 0= IF 1 <> ELSE 0<> THEN IF
        EXT4-D-GEOMETRY _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-CTX @ _EXT4-C.BLOCKS + @
    _EXT4-SV-CTX @ _EXT4-C.FIRST + @ <= IF
        EXT4-D-GEOMETRY _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-CTX @ _EXT4-C.BLOCKS + @
    _EXT4-SV-CTX @ _EXT4-C.SPB + @ *
    _EXT4-IO-VOL @ VOL.SECTORS U> IF
        EXT4-D-BOUNDS _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.BPG + L@ DUP
    _EXT4-SV-CTX @ _EXT4-C.BPG + !
    _EXT4-SV-CTX @ _EXT4-C.BSIZE + @ 8 * <> IF
        EXT4-D-GEOMETRY _EXT4-UNSUPPORTED EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.CPG + L@
    _EXT4-SV-CTX @ _EXT4-C.BPG + @ <> IF
        EXT4-D-GEOMETRY _EXT4-UNSUPPORTED EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.IPG + L@ DUP
    _EXT4-SV-CTX @ _EXT4-C.IPG + !
    _EXT4-SV-CTX @ _EXT4-C.BPG + @
    _EXT4-SV-CTX @ _EXT4-C.BSIZE + @ * 16384 / <> IF
        EXT4-D-GEOMETRY _EXT4-UNSUPPORTED EXIT
    THEN
    _EXT4-SV-CTX @ _EXT4-C.BLOCKS + @
    _EXT4-SV-CTX @ _EXT4-C.FIRST + @ -
    _EXT4-SV-CTX @ _EXT4-C.BPG + @ 1- +
    _EXT4-SV-CTX @ _EXT4-C.BPG + @ /
    DUP _EXT4-SV-GROUPS ! _EXT4-SV-CTX @ _EXT4-C.GROUPS + !
    _EXT4-SV-GROUPS @ 0= IF EXT4-D-GEOMETRY _EXT4-CORRUPT EXIT THEN
    _EXT4-SV-SB @ _EXT4-SB.INODES + L@ DUP
    _EXT4-SV-CTX @ _EXT4-C.INODES + !
    _EXT4-SV-GROUPS @ _EXT4-SV-CTX @ _EXT4-C.IPG + @ * <> IF
        EXT4-D-GEOMETRY _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.INODE-SIZE + W@ DUP
    _EXT4-SV-CTX @ _EXT4-C.ISIZE + !
    256 = IF
        _EXT4-SV-SB @ _EXT4-SB.MIN-EXTRA + W@ 32 <
        _EXT4-SV-SB @ _EXT4-SB.WANT-EXTRA + W@ 32 < OR IF
            EXT4-D-GEOMETRY _EXT4-CORRUPT EXIT
        THEN
    THEN
    _EXT4-SV-SB @ _EXT4-SB.DESC-SIZE + W@ DUP
    _EXT4-SV-CTX @ _EXT4-C.DSIZE + !
    _EXT4-DESC-SIZE <> IF EXT4-D-GEOMETRY _EXT4-CORRUPT EXIT THEN
    _EXT4-SV-SB @ _EXT4-SB.LOG-FLEX + C@ 4 <>
    _EXT4-SV-SB @ _EXT4-SB.CSUM-TYPE + C@ 1 <> OR IF
        EXT4-D-GEOMETRY _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.FIRST-INO + L@ 11 < IF
        EXT4-D-GEOMETRY _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.JOURNAL-INO + L@ 8 <> IF
        EXT4-D-JOURNAL _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.ORPHAN-INO + L@ DUP
    _EXT4-SV-CTX @ _EXT4-C.ORPHAN-INO + !
    DUP 0= SWAP _EXT4-SV-CTX @ _EXT4-C.INODES + @ > OR IF
        EXT4-D-ORPHAN-FILE _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.RBLOCKS-HI + L@ 0<>
    _EXT4-SV-SB @ _EXT4-SB.FREE-BLOCKS-HI + L@ 0<> OR IF
        EXT4-D-BOUNDS _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.RBLOCKS-LO + L@ DUP
    _EXT4-SV-CTX @ _EXT4-C.RESERVED-BLOCKS + !
    _EXT4-SV-CTX @ _EXT4-C.BLOCKS + @ U> IF
        EXT4-D-GEOMETRY _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.FREE-BLOCKS-LO + L@ DUP
    _EXT4-SV-CTX @ _EXT4-C.FREE-BLOCKS + !
    _EXT4-SV-CTX @ _EXT4-C.BLOCKS + @ U> IF
        EXT4-D-GEOMETRY _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.FREE-INODES + L@ DUP
    _EXT4-SV-CTX @ _EXT4-C.FREE-INODES + !
    _EXT4-SV-CTX @ _EXT4-C.INODES + @ U> IF
        EXT4-D-GEOMETRY _EXT4-CORRUPT EXIT
    THEN
    _EXT4-SV-SB @ _EXT4-SB.CSUM-SEED + L@
    _EXT4-SV-CTX @ _EXT4-C.SEED + !
    _EXT4-SV-SB @ _EXT4-SB.UUID + @
    _EXT4-SV-CTX @ _EXT4-C.UUID-LO + !
    _EXT4-SV-SB @ _EXT4-SB.UUID 8 + + @
    _EXT4-SV-CTX @ _EXT4-C.UUID-HI + !
    0 ;

