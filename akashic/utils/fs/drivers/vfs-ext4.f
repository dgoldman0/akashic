: _EXT4-MAP-IOR  ( backend-ior -- vfs-ior )
    DUP _EXT4-IO-BACKEND !
    0 _EXT4-IO-FLAGS !
    VFS-R-IO _EXT4-IO-REASON !
    DUP IF
        DUP IOR>FLAGS
        DUP IOR-F-RETRYABLE AND IF
            VFS-IOR-F-RETRYABLE _EXT4-IO-FLAGS +!
        THEN
        DUP IOR-F-PARTIAL AND IF
            VFS-IOR-F-PARTIAL _EXT4-IO-FLAGS +!
        THEN
        DUP IOR-F-CORRUPT AND IF
            VFS-IOR-F-CORRUPT _EXT4-IO-FLAGS +!
            VFS-R-CORRUPT _EXT4-IO-REASON !
        THEN
        DUP IOR-F-UNSUPPORTED AND IF
            VFS-R-UNSUPPORTED _EXT4-IO-REASON !
        THEN
        DUP IOR-F-READONLY AND IF
            VFS-IOR-F-READONLY _EXT4-IO-FLAGS +!
            VFS-R-READONLY _EXT4-IO-REASON !
        THEN
        IOR-F-STALE AND IF
            VFS-IOR-F-STALE _EXT4-IO-FLAGS +!
            VFS-R-STALE _EXT4-IO-REASON !
            _EXT4-LATCH-STALE
        THEN
    THEN
    _EXT4-IO-COMPLETED @ _EXT4-IO-EXPECTED @ <> IF
        VFS-IOR-F-PARTIAL _EXT4-IO-FLAGS DUP @ ROT OR SWAP !
    THEN
    _EXT4-IO-BACKEND @ 0=
    _EXT4-IO-COMPLETED @ _EXT4-IO-EXPECTED @ = AND IF
        DROP 0 EXIT
    THEN
    DROP
    _EXT4-IO-BACKEND @ 0xFFFFFFFF AND
    _EXT4-IO-FLAGS @ VFS-IOR-D-VOLUME _EXT4-IO-REASON @ VFS-IOR-MAKE ;

: _EXT4-READ-SECTORS  ( dma lba count -- ior )
    DUP _EXT4-IO-EXPECTED !
    _EXT4-IO-VOL @ VOL-READ
    SWAP _EXT4-IO-COMPLETED ! _EXT4-MAP-IOR ;

: _EXT4-CORRUPT  ( detail -- ior )
    VFS-IOR-F-CORRUPT VFS-IOR-D-FORMAT VFS-R-CORRUPT VFS-IOR-MAKE ;
: _EXT4-UNSUPPORTED  ( detail -- ior )
    0 VFS-IOR-D-FORMAT VFS-R-UNSUPPORTED VFS-IOR-MAKE ;

