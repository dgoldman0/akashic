: _EXT4-REQUIRE-ALLOCATED-BLOCK  ( physical-block detail ctx -- ior )
    _EXT4-RA-CTX ! _EXT4-RA-DETAIL ! DUP _EXT4-RA-BLOCK !
    _EXT4-RA-CTX @ _EXT4-BLOCK-ALLOCATED? ?DUP IF NIP EXIT THEN
    0= IF _EXT4-RA-DETAIL @ _EXT4-CORRUPT ELSE 0 THEN ;

VARIABLE _EXT4-RAR-FIRST
VARIABLE _EXT4-RAR-COUNT
VARIABLE _EXT4-RAR-DETAIL
VARIABLE _EXT4-RAR-CTX
VARIABLE _EXT4-RAR-CURRENT
VARIABLE _EXT4-RAR-REMAIN
VARIABLE _EXT4-RAR-GROUP
VARIABLE _EXT4-RAR-INDEX
VARIABLE _EXT4-RAR-CHUNK

: _EXT4-REQUIRE-ALLOCATED-RANGE  ( first count detail ctx -- ior )
    _EXT4-RAR-CTX ! _EXT4-RAR-DETAIL !
    _EXT4-RAR-COUNT ! _EXT4-RAR-FIRST !
    _EXT4-RAR-FIRST @ _EXT4-RAR-COUNT @
    _EXT4-RAR-CTX @ _EXT4-C.BLOCKS + @ BLOCK-RANGE? 0= IF
        EXT4-D-BOUNDS _EXT4-CORRUPT EXIT
    THEN
    _EXT4-RAR-FIRST @ _EXT4-RAR-CURRENT !
    _EXT4-RAR-COUNT @ _EXT4-RAR-REMAIN !
    BEGIN _EXT4-RAR-REMAIN @ WHILE
        _EXT4-RAR-CURRENT @ _EXT4-RAR-CTX @ _EXT4-C.FIRST + @ -
        _EXT4-RAR-CTX @ _EXT4-C.BPG + @ /MOD
        _EXT4-RAR-GROUP ! _EXT4-RAR-INDEX !
        _EXT4-RAR-GROUP @ _EXT4-RAR-CTX @ _EXT4-LOAD-DESC
        ?DUP IF EXIT THEN
        _EXT4-GD-FLAGS @ _EXT4-BG-BLOCK-UNINIT AND IF
            _EXT4-RAR-DETAIL @ _EXT4-CORRUPT EXIT
        THEN
        _EXT4-RAR-CTX @ _EXT4-C.DESC +
        _EXT4-GD.BLOCK-BITMAP-LO + L@
        _EXT4-RAR-CTX @ _EXT4-READ-BLOCK ?DUP IF EXIT THEN
        _EXT4-RAR-CTX @ _EXT4-C.BPG + @ _EXT4-RAR-INDEX @ -
        _EXT4-RAR-REMAIN @ MIN _EXT4-RAR-CHUNK !
        _EXT4-RAR-CHUNK @ 0 ?DO
            _EXT4-RAR-INDEX @ I + DUP 8 /
            _EXT4-RAR-CTX @ _EXT4-C.BLOCK + + C@
            1 ROT 8 MOD LSHIFT AND 0= IF
                _EXT4-RAR-DETAIL @ _EXT4-CORRUPT UNLOOP EXIT
            THEN
        LOOP
        _EXT4-RAR-CHUNK @ DUP _EXT4-RAR-CURRENT +!
        NEGATE _EXT4-RAR-REMAIN +!
    REPEAT 0 ;

