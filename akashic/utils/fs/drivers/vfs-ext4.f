: _EXT4-XA-SELECT-NS  ( index -- supported? )
    DUP _EXT4-XA-INDEX !
    DUP 1 = IF DROP S" user." _EXT4-XA-PREFIX-U ! _EXT4-XA-PREFIX-A !
        0 _EXT4-XA-FIXED ! TRUE EXIT THEN
    DUP 2 = IF DROP S" system.posix_acl_access"
        _EXT4-XA-PREFIX-U ! _EXT4-XA-PREFIX-A !
        -1 _EXT4-XA-FIXED ! TRUE EXIT THEN
    DUP 3 = IF DROP S" system.posix_acl_default"
        _EXT4-XA-PREFIX-U ! _EXT4-XA-PREFIX-A !
        -1 _EXT4-XA-FIXED ! TRUE EXIT THEN
    DUP 4 = IF DROP S" trusted." _EXT4-XA-PREFIX-U ! _EXT4-XA-PREFIX-A !
        0 _EXT4-XA-FIXED ! TRUE EXIT THEN
    6 = IF S" security." _EXT4-XA-PREFIX-U ! _EXT4-XA-PREFIX-A !
        0 _EXT4-XA-FIXED ! TRUE EXIT THEN
    FALSE ;

: _EXT4-XA-KEY=  ( first second -- flag )
    2DUP 1+ C@ SWAP 1+ C@ <> IF 2DROP FALSE EXIT THEN
    2DUP C@ SWAP C@ <> IF 2DROP FALSE EXIT THEN
    DUP C@ >R 16 + SWAP 16 + R> _EXT4-BYTES=? ;

: _EXT4-XA-ORDERED-AFTER?  ( previous current -- flag )
    _EXT4-XA-CMP-CURR ! _EXT4-XA-CMP-PREV !
    _EXT4-XA-CMP-PREV @ 1+ C@ _EXT4-XA-CMP-CURR @ 1+ C@
    2DUP U< IF 2DROP TRUE EXIT THEN
    U> IF FALSE EXIT THEN
    _EXT4-XA-CMP-PREV @ C@ _EXT4-XA-CMP-CURR @ C@
    2DUP U< IF 2DROP TRUE EXIT THEN
    U> IF FALSE EXIT THEN
    _EXT4-XA-CMP-PREV @ C@ 0 ?DO
        _EXT4-XA-CMP-PREV @ I 16 + + C@
        _EXT4-XA-CMP-CURR @ I 16 + + C@
        2DUP U< IF
            2DROP TRUE UNLOOP EXIT
        THEN
        U> IF FALSE UNLOOP EXIT THEN
    LOOP FALSE ;

: _EXT4-XA-NAME-MATCH?  ( entry -- flag )
    _EXT4-XA-FIXED @ IF
        C@ 0<> IF FALSE EXIT THEN
        _EXT4-XA-REQ-U @ _EXT4-XA-PREFIX-U @ =
        _EXT4-XA-REQ-A @ _EXT4-XA-PREFIX-A @ _EXT4-XA-PREFIX-U @
        _EXT4-BYTES=? AND EXIT
    THEN
    _EXT4-XA-REQ-U @ _EXT4-XA-PREFIX-U @ _EXT4-XA-NLEN @ + <> IF
        DROP FALSE EXIT
    THEN
    _EXT4-XA-REQ-A @ _EXT4-XA-PREFIX-A @ _EXT4-XA-PREFIX-U @
    _EXT4-BYTES=? 0= IF DROP FALSE EXIT THEN
    16 + _EXT4-XA-REQ-A @ _EXT4-XA-PREFIX-U @ +
    _EXT4-XA-NLEN @ _EXT4-BYTES=? ;

: _EXT4-XA-EMIT-NAME  ( entry -- )
    _EXT4-XA-PREFIX-A @ _EXT4-XA-OUT @ _EXT4-XA-PREFIX-U @ CMOVE
    _EXT4-XA-FIXED @ 0= IF
        16 + _EXT4-XA-OUT @ _EXT4-XA-PREFIX-U @ +
        _EXT4-XA-NLEN @ CMOVE
    ELSE DROP THEN
    0 _EXT4-XA-OUT @ _EXT4-XA-PREFIX-U @ _EXT4-XA-NLEN @ + + C!
    _EXT4-XA-PREFIX-U @ _EXT4-XA-NLEN @ + 1+ _EXT4-XA-OUT +! ;

: _EXT4-XA-OVERLAPS-PRIOR?  ( -- flag )
    _EXT4-XA-FIRST @ _EXT4-XA-SCAN !
    BEGIN _EXT4-XA-SCAN @ _EXT4-XA-ENTRY @ < WHILE
        _EXT4-XA-SCAN @ 8 + L@ ?DUP IF
            _EXT4-XA-VBASE @ _EXT4-XA-SCAN @ 2 + W@ + DUP
            ROT +
            _EXT4-XA-VSTART @ U>
            SWAP _EXT4-XA-VEND @ U< AND IF TRUE EXIT THEN
        THEN
        _EXT4-XA-SCAN @ C@ 16 + 3 + 3 INVERT AND _EXT4-XA-SCAN +!
    REPEAT FALSE ;

: _EXT4-XA-INLINE-DUP?  ( external-entry -- flag )
    _EXT4-XA-INLINE-FIRST @ DUP 0= IF 2DROP FALSE EXIT THEN
    _EXT4-XA-SCAN !
    BEGIN _EXT4-XA-SCAN @ 4 + _EXT4-XA-INLINE-LIMIT @ <= WHILE
        _EXT4-XA-SCAN @ L@ 0= IF DROP FALSE EXIT THEN
        DUP _EXT4-XA-SCAN @ _EXT4-XA-KEY= IF DROP TRUE EXIT THEN
        _EXT4-XA-SCAN @ C@ 16 + 3 + 3 INVERT AND _EXT4-XA-SCAN +!
    REPEAT DROP FALSE ;

\ The entry walker is split from the area setup so both LIST passes and GET
\ use one exact structural parser.  A zero u32 terminates the packed entries.
: _EXT4-XA-WALK  ( -- ior )
    _EXT4-XA-ENTRY @ _EXT4-XA-FIRST !
    0 _EXT4-XA-PREV !
    _EXT4-XA-VLIMIT @ _EXT4-XA-MIN-VALUE !
    BEGIN
        _EXT4-XA-ENTRY @ 4 + _EXT4-XA-LIMIT @ > IF
            EXT4-D-XATTR _EXT4-CORRUPT EXIT
        THEN
        _EXT4-XA-ENTRY @ L@ 0= IF
            _EXT4-XA-ENTRY @ 4 + _EXT4-XA-MIN-VALUE @ U> IF
                EXT4-D-XATTR _EXT4-CORRUPT EXIT
            THEN 0 EXIT
        THEN
        _EXT4-XA-ENTRY @ 16 + _EXT4-XA-LIMIT @ > IF
            EXT4-D-XATTR _EXT4-CORRUPT EXIT
        THEN
        _EXT4-XA-ENTRY @ C@ _EXT4-XA-NLEN !
        _EXT4-XA-ENTRY @ 1+ C@ _EXT4-XA-SELECT-NS 0= IF
            EXT4-D-XATTR _EXT4-UNSUPPORTED EXIT
        THEN
        _EXT4-XA-FIXED @ IF
            _EXT4-XA-NLEN @ IF EXT4-D-XATTR _EXT4-CORRUPT EXIT THEN
        ELSE _EXT4-XA-NLEN @ 0= IF EXT4-D-XATTR _EXT4-CORRUPT EXIT THEN THEN
        _EXT4-XA-PREV @ IF
            _EXT4-XA-PREV @ _EXT4-XA-ENTRY @ _EXT4-XA-ORDERED-AFTER? 0= IF
                EXT4-D-XATTR _EXT4-CORRUPT EXIT
            THEN
        THEN
        _EXT4-XA-CROSS @ IF
            _EXT4-XA-ENTRY @ _EXT4-XA-INLINE-DUP? IF
                EXT4-D-XATTR _EXT4-CORRUPT EXIT
            THEN
        THEN
        _EXT4-XA-NLEN @ 16 + 3 + 3 INVERT AND DUP _EXT4-XA-ESIZE !
        _EXT4-XA-ENTRY @ + _EXT4-XA-LIMIT @ > IF
            EXT4-D-XATTR _EXT4-CORRUPT EXIT
        THEN
        _EXT4-XA-ENTRY @ 4 + L@ IF
            EXT4-D-XATTR _EXT4-UNSUPPORTED EXIT
        THEN
        _EXT4-XA-ENTRY @ 2 + W@ _EXT4-XA-VOFF !
        _EXT4-XA-ENTRY @ 8 + L@ _EXT4-XA-VSIZE !
        _EXT4-XA-VSIZE @ IF
            _EXT4-XA-VOFF @ 3 AND IF EXT4-D-XATTR _EXT4-CORRUPT EXIT THEN
            _EXT4-XA-VBASE @ _EXT4-XA-VOFF @ + DUP _EXT4-XA-VSTART !
            DUP _EXT4-XA-VBASE @ U< IF
                DROP EXT4-D-XATTR _EXT4-CORRUPT EXIT
            THEN
            _EXT4-XA-VSIZE @ + DUP _EXT4-XA-VEND !
            DUP _EXT4-XA-VLIMIT @ U> IF
                DROP EXT4-D-XATTR _EXT4-CORRUPT EXIT
            THEN DROP
            _EXT4-XA-OVERLAPS-PRIOR? IF EXT4-D-XATTR _EXT4-CORRUPT EXIT THEN
            _EXT4-XA-VSTART @ _EXT4-XA-MIN-VALUE @ U< IF
                _EXT4-XA-VSTART @ _EXT4-XA-MIN-VALUE !
            THEN
        ELSE
            _EXT4-XA-VOFF @ IF EXT4-D-XATTR _EXT4-CORRUPT EXIT THEN
        THEN
        _EXT4-XA-OP @ _EXT4-XOP-LIST = IF
            _EXT4-XA-PREFIX-U @ _EXT4-XA-NLEN @ + 1+ _EXT4-XA-TOTAL +!
            _EXT4-XA-EMIT @ IF _EXT4-XA-ENTRY @ _EXT4-XA-EMIT-NAME THEN
        ELSE
            _EXT4-XA-ENTRY @ _EXT4-XA-NAME-MATCH? IF
                _EXT4-XA-FOUND @ IF EXT4-D-XATTR _EXT4-CORRUPT EXIT THEN
                -1 _EXT4-XA-FOUND !
                _EXT4-XA-VSIZE @ _EXT4-XA-FOUND-SIZE !
                _EXT4-XA-VSIZE @ _EXT4-XA-CAP @ > IF
                    _EXT4-XA-CAP @ IF VFS-E-OVERFLOW _EXT4-XA-IOR ! THEN
                ELSE _EXT4-XA-CAP @ IF
                    _EXT4-XA-VBASE @ _EXT4-XA-VOFF @ +
                    _EXT4-XA-OUT @ _EXT4-XA-VSIZE @ CMOVE
                THEN
                THEN
            THEN
        THEN
        _EXT4-XA-ENTRY @ _EXT4-XA-PREV !
        _EXT4-XA-ESIZE @ _EXT4-XA-ENTRY +!
    AGAIN ;

: _EXT4-XA-INLINE  ( ctx -- ior )
    DUP _EXT4-XA-CTX ! _EXT4-C.ISIZE + @ 128 <= IF 0 EXIT THEN
    _EXT4-XA-CTX @ _EXT4-C.INODE + _EXT4-I.EXTRA-SIZE + W@
    128 + _EXT4-XA-CTX @ _EXT4-C.INODE + +
    DUP 4 + _EXT4-XA-CTX @ _EXT4-C.INODE +
        _EXT4-XA-CTX @ _EXT4-C.ISIZE + @ + > IF
        DROP EXT4-D-XATTR _EXT4-CORRUPT EXIT
    THEN
    DUP L@ 0= IF DROP 0 EXIT THEN
    DUP L@ _EXT4-XATTR-MAGIC <> IF
        DROP EXT4-D-XATTR _EXT4-CORRUPT EXIT
    THEN
    4 + DUP _EXT4-XA-ENTRY ! DUP _EXT4-XA-VBASE !
    _EXT4-XA-INLINE-FIRST !
    _EXT4-XA-CTX @ _EXT4-C.INODE +
    _EXT4-XA-CTX @ _EXT4-C.ISIZE + @ +
    DUP _EXT4-XA-LIMIT ! DUP _EXT4-XA-VLIMIT !
    _EXT4-XA-INLINE-LIMIT !
    _EXT4-XA-WALK ;

: _EXT4-XA-EXTERNAL  ( ctx -- ior )
    DUP _EXT4-XA-CTX !
    _EXT4-C.INODE + DUP _EXT4-I.FILE-ACL-HI + W@ IF
        DROP EXT4-D-BOUNDS _EXT4-CORRUPT EXIT
    THEN
    _EXT4-I.FILE-ACL-LO + L@ DUP 0= IF DROP 0 EXIT THEN
    DUP EXT4-D-XATTR _EXT4-XA-CTX @ _EXT4-REQUIRE-ALLOCATED-BLOCK
    ?DUP IF NIP EXIT THEN
    _EXT4-XA-CTX @ _EXT4-LOAD-XATTR-BLOCK ?DUP IF EXIT THEN
    _EXT4-XA-CTX @ _EXT4-C.BLOCK + DUP
    32 + _EXT4-XA-ENTRY !
    DUP _EXT4-XA-VBASE !
    _EXT4-XA-CTX @ _EXT4-C.BSIZE + @ + DUP
    _EXT4-XA-LIMIT ! _EXT4-XA-VLIMIT !
    _EXT4-XA-WALK ;

: _EXT4-XA-SCAN-BOTH  ( ctx -- ior )
    0 _EXT4-XA-INLINE-FIRST ! 0 _EXT4-XA-INLINE-LIMIT !
    0 _EXT4-XA-CROSS !
    DUP _EXT4-XA-INLINE ?DUP IF NIP EXIT THEN
    -1 _EXT4-XA-CROSS !
    _EXT4-XA-EXTERNAL ;

