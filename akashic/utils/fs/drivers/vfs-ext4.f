: _EXT4-VALIDATE-DX-ENTRIES  ( entry-base expected-limit dir-blocks ctx -- ior )
    _EXT4-DX-CTX ! _EXT4-DX-DIR-BLOCKS !
    _EXT4-DX-EXPECTED-LIMIT ! DUP _EXT4-DX-ENTRY !
    DUP W@ DUP _EXT4-DX-LIMIT ! _EXT4-DX-EXPECTED-LIMIT @ <> IF
        DROP EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT
    THEN
    2 + W@ DUP _EXT4-DX-COUNT !
    DUP 0= SWAP _EXT4-DX-LIMIT @ U> OR IF
        EXT4-D-DIRECTORY _EXT4-CORRUPT EXIT
    THEN
    0 _EXT4-DX-PREV-HASH !
    _EXT4-DX-COUNT @ 0 ?DO
        I IF
            _EXT4-DX-ENTRY @ I 8 * + L@ DUP _EXT4-DX-HASH !
            0xFFFFFFFE AND DUP _EXT4-DX-PREV-HASH @ U< IF
                DROP EXT4-D-DIRECTORY _EXT4-CORRUPT UNLOOP EXIT
