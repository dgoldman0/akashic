90 CONSTANT EXT4-PROBE-SCORE
CREATE _EXT4-PROBE-BUF _EXT4-SB-SIZE ALLOT
VARIABLE _EXT4-PROBE-VOL

: _EXT4-PROBE  ( volume -- score ior )
    _EXT4-PROBE-VOL !
    _EXT4-PROBE-VOL @ VOL.SECTOR-SIZE _EXT4-SECTOR <> IF
        0 0 VFS-IOR-D-VOLUME VFS-R-UNSUPPORTED VFS-IOR-MAKE EXIT
    THEN
    0 _EXT4-IO-VFS !
    _EXT4-PROBE-VOL @ _EXT4-IO-VOL !
    _EXT4-PROBE-BUF 2 2 _EXT4-READ-SECTORS
    DUP IF 0 SWAP EXIT THEN DROP
    _EXT4-PROBE-BUF _EXT4-SB.MAGIC + W@ _EXT4-MAGIC = IF
        EXT4-PROBE-SCORE
    ELSE
        0
    THEN 0 ;

