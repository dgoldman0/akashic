VARIABLE _EXT4-IR-HI
VARIABLE _EXT4-IR-HAS-HI
VARIABLE _EXT4-IR-CALC
VARIABLE _EXT4-IR-BIT

: _EXT4-LOAD-INODE  ( inode-number ctx -- ior )
    _EXT4-IR-CTX ! DUP _EXT4-IR-INO !
    DUP 0= SWAP _EXT4-IR-CTX @ _EXT4-C.INODES + @ > OR IF
        EXT4-D-BOUNDS _EXT4-CORRUPT EXIT
    THEN
    _EXT4-IR-INO @ 1-
    _EXT4-IR-CTX @ _EXT4-C.IPG + @ /MOD
    _EXT4-IR-GROUP ! _EXT4-IR-INDEX !
    _EXT4-IR-GROUP @ _EXT4-IR-CTX @ _EXT4-LOAD-DESC ?DUP IF EXIT THEN
    _EXT4-GD-FLAGS @ _EXT4-BG-INODE-UNINIT AND IF
        EXT4-D-BOUNDS _EXT4-CORRUPT EXIT
    THEN
    _EXT4-IR-CTX @ _EXT4-C.DESC + _EXT4-GD.INODE-BITMAP-LO + L@
    _EXT4-IR-CTX @ _EXT4-READ-BLOCK ?DUP IF EXIT THEN
    _EXT4-IR-INDEX @ 8 / _EXT4-IR-CTX @ _EXT4-C.BLOCK + + C@
    1 _EXT4-IR-INDEX @ 8 MOD LSHIFT AND 0= IF
        EXT4-D-BOUNDS _EXT4-CORRUPT EXIT
    THEN
    _EXT4-IR-CTX @ _EXT4-C.DESC + _EXT4-GD.INODE-TABLE-LO + L@
    _EXT4-IR-INDEX @ _EXT4-IR-CTX @ _EXT4-C.ISIZE + @ *
    DUP _EXT4-IR-BYTE !
    _EXT4-IR-CTX @ _EXT4-C.BSIZE + @ / + DUP _EXT4-IR-BLOCK !
    _EXT4-IR-CTX @ _EXT4-READ-BLOCK ?DUP IF EXIT THEN
    _EXT4-IR-BYTE @ _EXT4-IR-CTX @ _EXT4-C.BSIZE + @ MOD
    DUP _EXT4-IR-OFF ! _EXT4-IR-CTX @ _EXT4-C.ISIZE + @ +
    _EXT4-IR-CTX @ _EXT4-C.BSIZE + @ > IF
        EXT4-D-BOUNDS _EXT4-CORRUPT EXIT
    THEN
    _EXT4-IR-CTX @ _EXT4-C.BLOCK + _EXT4-IR-OFF @ +
    _EXT4-IR-CTX @ _EXT4-C.INODE +
    _EXT4-IR-CTX @ _EXT4-C.ISIZE + @ CMOVE
    _EXT4-IR-CTX @ _EXT4-C.INODE + DUP
    _EXT4-I.CSUM-LO + W@ _EXT4-IR-LO !
    0 OVER _EXT4-I.CSUM-LO + W!
    0 _EXT4-IR-HAS-HI ! 0 _EXT4-IR-HI !
    _EXT4-IR-CTX @ _EXT4-C.ISIZE + @ 128 > IF
        DUP _EXT4-I.EXTRA-SIZE + W@ DUP 4 < IF
            DROP DROP EXT4-D-INODE-CHECKSUM _EXT4-CORRUPT EXIT
        THEN
        DUP 4 MOD IF DROP DROP EXT4-D-INODE-CHECKSUM _EXT4-CORRUPT EXIT THEN
        128 + _EXT4-IR-CTX @ _EXT4-C.ISIZE + @ > IF
            DROP EXT4-D-INODE-CHECKSUM _EXT4-CORRUPT EXIT
        THEN
        DUP _EXT4-I.CSUM-HI + W@ _EXT4-IR-HI !
        0 OVER _EXT4-I.CSUM-HI + W!
        -1 _EXT4-IR-HAS-HI !
    THEN
    _EXT4-IR-INO @ _EXT4-IR-CTX @ _EXT4-C.TMP + L!
    _EXT4-IR-CTX @ _EXT4-C.SEED + @ _EXT4-CRC-START
    _EXT4-IR-CTX @ _EXT4-C.TMP + 4 _EXT4-CRC-ADD
    DUP _EXT4-I.GENERATION + 4 _EXT4-CRC-ADD
    _EXT4-IR-CTX @ _EXT4-C.ISIZE + @ _EXT4-CRC-ADD
    _EXT4-CRC@ _EXT4-IR-CALC !
    _EXT4-IR-LO @ _EXT4-IR-CTX @ _EXT4-C.INODE +
        _EXT4-I.CSUM-LO + W!
    _EXT4-IR-HAS-HI @ IF
        _EXT4-IR-HI @ _EXT4-IR-CTX @ _EXT4-C.INODE +
            _EXT4-I.CSUM-HI + W!
        _EXT4-IR-HI @ 16 LSHIFT _EXT4-IR-LO @ OR
        _EXT4-IR-CALC @ <>
    ELSE
        _EXT4-IR-CALC @ 0xFFFF AND _EXT4-IR-LO @ <>
    THEN IF EXT4-D-INODE-CHECKSUM _EXT4-CORRUPT EXIT THEN
    _EXT4-IR-CTX @ _EXT4-C.INODE + _EXT4-I.DTIME + L@ IF
        EXT4-D-ROOT-INODE _EXT4-CORRUPT EXIT
    THEN
    0 ;

