: _EXT4-STATFS  ( buffer bytes vfs -- ior )
    _EXT4-SF-V ! _EXT4-SF-SIZE ! _EXT4-SF-BUF !
    _EXT4-SF-V @ _EXT4-CTX DUP _EXT4-SF-CTX !
    _EXT4-C.STAT + DUP _EXT4-SF-OUT ! VFS-STATFS-SIZE 0 FILL
    _EXT4-SF-CTX @ _EXT4-C.BSIZE + @ DUP
    _EXT4-SF-OUT @ VSF.BSIZE !
    _EXT4-SF-OUT @ VSF.FRSIZE !
    _EXT4-SF-CTX @ _EXT4-C.BLOCKS + @ _EXT4-SF-OUT @ VSF.BLOCKS !
    _EXT4-SF-CTX @ _EXT4-C.FREE-BLOCKS + @ DUP
    _EXT4-SF-OUT @ VSF.BFREE !
    _EXT4-SF-CTX @ _EXT4-C.RESERVED-BLOCKS + @
    2DUP U< IF 2DROP 0 ELSE - THEN DUP _EXT4-SF-AVAIL !
    _EXT4-SF-OUT @ VSF.BAVAIL !
    _EXT4-SF-CTX @ _EXT4-C.INODES + @ _EXT4-SF-OUT @ VSF.FILES !
    _EXT4-SF-CTX @ _EXT4-C.FREE-INODES + @ _EXT4-SF-OUT @ VSF.FFREE !
    255 _EXT4-SF-OUT @ VSF.NAMEMAX !
    0 _EXT4-SF-OUT @ VSF.FLAGS !
    _EXT4-SF-CTX @ _EXT4-C.UUID-LO + @ _EXT4-SF-OUT @ VSF.FSID-LO !
    _EXT4-SF-CTX @ _EXT4-C.UUID-HI + @ _EXT4-SF-OUT @ VSF.FSID-HI !
    0 _EXT4-SF-OUT @ VSF.RESERVED !
    _EXT4-SF-OUT @ _EXT4-SF-BUF @ VFS-STATFS-SIZE CMOVE
    0 ;

