        2DUP I + C@ SWAP I + C@ <> IF 2DROP FALSE UNLOOP EXIT THEN
    LOOP 2DROP TRUE ;

VARIABLE _EXT4-PO-N
VARIABLE _EXT4-PO-B
: _EXT4-POWER-OF?  ( n base -- flag )
    _EXT4-PO-B ! _EXT4-PO-N !
    BEGIN _EXT4-PO-N @ 1 > WHILE
        _EXT4-PO-N @ _EXT4-PO-B @ MOD IF FALSE EXIT THEN
        _EXT4-PO-N @ _EXT4-PO-B @ / _EXT4-PO-N !
    REPEAT
    _EXT4-PO-N @ 1 = ;

: _EXT4-SPARSE-GROUP?  ( group -- flag )
    DUP 0= OVER 1 = OR IF DROP TRUE EXIT THEN
    DUP 3 _EXT4-POWER-OF? IF DROP TRUE EXIT THEN
    DUP 5 _EXT4-POWER-OF? IF DROP TRUE EXIT THEN
    7 _EXT4-POWER-OF? ;

