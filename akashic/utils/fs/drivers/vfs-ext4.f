            EXT4-D-DIRECTORY _EXT4-CORRUPT UNLOOP EXIT
        THEN
        0 _EXT4-DX-J !
        BEGIN _EXT4-DX-J @ I < WHILE
            _EXT4-DX-ENTRY @ I 8 * + 4 + L@ 0x00FFFFFF AND
            _EXT4-DX-ENTRY @ _EXT4-DX-J @ 8 * + 4 + L@
            0x00FFFFFF AND = IF
                EXT4-D-DIRECTORY _EXT4-CORRUPT UNLOOP EXIT
            THEN
            1 _EXT4-DX-J +!
        REPEAT
    LOOP 0 ;

