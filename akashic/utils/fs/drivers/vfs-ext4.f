: _EXT4-EXTENT-LEN@  ( extent -- blocks )
    4 + W@ DUP 32768 > IF 32768 - THEN ;

: _EXT4-VALIDATE-EXTENT-INDEX-ENTRY  ( index -- ior )
    _EXT4-EV-INDEX !
    _EXT4-EV-LOGICAL @ _EXT4-EV-PREV !
    _EXT4-EV-ENTRY @ 8 + W@ IF EXT4-D-BOUNDS _EXT4-CORRUPT EXIT THEN
    _EXT4-EV-ENTRY @ 10 + W@ IF EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT THEN
    _EXT4-EV-ENTRY @ 4 + L@ DUP _EXT4-EV-PHYS ! 0= IF
        EXT4-D-BOUNDS _EXT4-CORRUPT EXIT
    THEN
    _EXT4-EV-MAX @ 4 <> _EXT4-EV-PHYS @ _EXT4-EV-BLOCK @ = AND IF
        EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT
    THEN
    _EXT4-EV-PHYS @ EXT4-D-DATA-MAP _EXT4-EV-CTX @
    _EXT4-REQUIRE-ALLOCATED-BLOCK ?DUP IF EXIT THEN
    0 _EXT4-EV-J !
    BEGIN _EXT4-EV-J @ _EXT4-EV-INDEX @ < WHILE
        _EXT4-EV-IN @ 12 _EXT4-EV-J @ 12 * + + 4 + L@
        _EXT4-EV-PHYS @ = IF EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT THEN
        1 _EXT4-EV-J +!
    REPEAT 0 ;

: _EXT4-VALIDATE-EXTENT-LEAF-ENTRY  ( index -- ior )
    _EXT4-EV-INDEX !
    _EXT4-EV-ENTRY @ 4 + W@ DUP 0= IF
        DROP EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT
    THEN
    DUP 32768 > IF 32768 - THEN DUP _EXT4-EV-LEN ! 0= IF
        EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT
    THEN
    _EXT4-EV-LOGICAL @ _EXT4-EV-LEN @ + DUP
    _EXT4-EV-LOGICAL @ U< IF
        DROP EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT
    THEN _EXT4-EV-PREV !
    _EXT4-EV-ENTRY @ 6 + W@ IF EXT4-D-BOUNDS _EXT4-CORRUPT EXIT THEN
    _EXT4-EV-ENTRY @ 8 + L@ DUP _EXT4-EV-PHYS ! 0= IF
        EXT4-D-BOUNDS _EXT4-CORRUPT EXIT
    THEN
    _EXT4-EV-PHYS @ _EXT4-EV-LEN @ EXT4-D-DATA-MAP
    _EXT4-EV-CTX @ _EXT4-REQUIRE-ALLOCATED-RANGE ?DUP IF EXIT THEN
    0 _EXT4-EV-J !
    BEGIN _EXT4-EV-J @ _EXT4-EV-INDEX @ < WHILE
        _EXT4-EV-IN @ 12 _EXT4-EV-J @ 12 * + + DUP
        8 + L@ _EXT4-EV-OTHER !
        _EXT4-EXTENT-LEN@ _EXT4-EV-OTHER-LEN !
        _EXT4-EV-OTHER @ _EXT4-EV-OTHER-LEN @ + _EXT4-EV-PHYS @ U>
        _EXT4-EV-PHYS @ _EXT4-EV-LEN @ + _EXT4-EV-OTHER @ U> AND IF
            EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT
        THEN
        1 _EXT4-EV-J +!
    REPEAT 0 ;

\ Validate one resident extent header and every entry it owns.  External
\ children and every data block in a leaf must agree with the allocation
\ bitmap before the pointer can participate in traversal.
: _EXT4-VALIDATE-EXTENT-NODE  ( header expected-max expected-depth ctx -- ior )
    _EXT4-EV-CTX ! _EXT4-EV-DEPTH ! _EXT4-EV-MAX ! _EXT4-EV-IN !
    _EXT4-EV-IN @ W@ _EXT4-EXTENT-MAGIC <> IF
        EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT
    THEN
    _EXT4-EV-IN @ 2 + W@ _EXT4-EV-COUNT !
    _EXT4-EV-IN @ 4 + W@ _EXT4-EV-MAX @ <>
    _EXT4-EV-COUNT @ _EXT4-EV-MAX @ U> OR IF
        EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT
    THEN
    _EXT4-EV-IN @ 6 + W@ DUP _EXT4-EV-DEPTH @ <>
    SWAP _EXT4-MAX-EXTENT-DEPTH U> OR IF
        EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT
    THEN
    _EXT4-EV-COUNT @ 0=
    _EXT4-EV-DEPTH @ 0<> _EXT4-EV-MAX @ 4 <> OR AND IF
        EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT
    THEN
    0 _EXT4-EV-PREV !
    _EXT4-EV-COUNT @ 0 ?DO
        _EXT4-EV-IN @ 12 I 12 * + + _EXT4-EV-ENTRY !
        _EXT4-EV-ENTRY @ L@ _EXT4-EV-LOGICAL !
        I IF
            _EXT4-EV-PREV @ _EXT4-EV-LOGICAL @ U> IF
                EXT4-D-DATA-MAP _EXT4-CORRUPT UNLOOP EXIT
            THEN
            _EXT4-EV-DEPTH @
            _EXT4-EV-PREV @ _EXT4-EV-LOGICAL @ = AND IF
                EXT4-D-DATA-MAP _EXT4-CORRUPT UNLOOP EXIT
            THEN
        THEN
        _EXT4-EV-DEPTH @ IF
            I _EXT4-VALIDATE-EXTENT-INDEX-ENTRY
        ELSE
            I _EXT4-VALIDATE-EXTENT-LEAF-ENTRY
        THEN ?DUP IF UNLOOP EXIT THEN
    LOOP 0 ;

: _EXT4-VALIDATE-INLINE-EXTENTS  ( ctx -- ior )
    DUP _EXT4-EV-CTX ! _EXT4-C.INODE + _EXT4-I.BLOCK + _EXT4-EV-IN !
    _EXT4-EV-IN @ 6 + W@ DUP _EXT4-MAX-EXTENT-DEPTH U> IF
        DROP EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT
    THEN
    _EXT4-EV-IN @ 4 ROT
    _EXT4-EV-CTX @ _EXT4-VALIDATE-EXTENT-NODE ;

\ Read and authenticate one external extent-tree node.  Its checksum covers
\ inode number/generation plus the node bytes before the four-byte tail.
\ The verified node is copied to DIR-BLOCK so allocation checks may reuse the
\ ordinary BLOCK cache without invalidating the parser's input.
: _EXT4-LOAD-EXTENT-NODE  ( physical-block expected-depth ctx -- ior )
    _EXT4-EV-CTX ! _EXT4-EV-DEPTH ! DUP _EXT4-EV-BLOCK !
    EXT4-D-DATA-MAP _EXT4-EV-CTX @ _EXT4-REQUIRE-ALLOCATED-BLOCK
    ?DUP IF EXIT THEN
    _EXT4-EV-BLOCK @ _EXT4-EV-CTX @ _EXT4-READ-BLOCK ?DUP IF EXIT THEN
    _EXT4-EV-CTX @ _EXT4-C.BLOCK +
    _EXT4-EV-CTX @ _EXT4-C.TREE-BLOCK +
    _EXT4-EV-CTX @ _EXT4-C.BSIZE + @ CMOVE
    _EXT4-EV-CTX @ _EXT4-C.TREE-BLOCK + _EXT4-EV-IN !
    _EXT4-EV-CTX @ _EXT4-C.BSIZE + @ 12 - 12 / _EXT4-EV-MAX !
    _EXT4-EV-MAX @ 12 * 12 + _EXT4-EV-IN @ + L@ _EXT4-EV-STORED !
    _EXT4-IR-INO @ _EXT4-EV-CTX @ _EXT4-C.TMP + L!
    _EXT4-EV-CTX @ _EXT4-C.INODE + _EXT4-I.GENERATION + L@
    _EXT4-EV-CTX @ _EXT4-C.TMP 4 + + L!
    _EXT4-EV-CTX @ _EXT4-C.SEED + @ _EXT4-CRC-START
    _EXT4-EV-CTX @ _EXT4-C.TMP + 8 _EXT4-CRC-ADD
    _EXT4-EV-IN @ _EXT4-EV-MAX @ 12 * 12 + _EXT4-CRC-ADD
    _EXT4-CRC@ _EXT4-EV-STORED @ <> IF
        EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT
    THEN
    _EXT4-EV-IN @ _EXT4-EV-MAX @ _EXT4-EV-DEPTH @ _EXT4-EV-CTX @
    _EXT4-VALIDATE-EXTENT-NODE ;

VARIABLE _EXT4-ET-CTX
CREATE _EXT4-ET-LOWER 6 CELLS ALLOT
CREATE _EXT4-ET-UPPER 6 CELLS ALLOT
CREATE _EXT4-ET-HAS-UPPER 6 CELLS ALLOT
VARIABLE _EXT4-ET-DEPTH
VARIABLE _EXT4-ET-LAST
VARIABLE _EXT4-ET-ROOT
VARIABLE _EXT4-ET-ROOT-DEPTH
VARIABLE _EXT4-ET-ROOT-COUNT

: _EXT4-ET-LOWER!  ( value depth -- )  CELLS _EXT4-ET-LOWER + ! ;
: _EXT4-ET-LOWER@  ( depth -- value )  CELLS _EXT4-ET-LOWER + @ ;
: _EXT4-ET-UPPER!  ( value depth -- )  CELLS _EXT4-ET-UPPER + ! ;
: _EXT4-ET-UPPER@  ( depth -- value )  CELLS _EXT4-ET-UPPER + @ ;
: _EXT4-ET-HAS!    ( flag depth -- )   CELLS _EXT4-ET-HAS-UPPER + ! ;
: _EXT4-ET-HAS@    ( depth -- flag )   CELLS _EXT4-ET-HAS-UPPER + @ ;

\ Every index key is the exact first logical block in its child.  An upper
\ bound comes from the next sibling key (or is inherited by the last child),
\ preventing a checksummed child from escaping or overlapping its branch.
: _EXT4-ET-CHECK-BOUNDS  ( depth -- ior )
    DUP _EXT4-ET-DEPTH !
    _EXT4-EV-IN @ 12 + L@ SWAP _EXT4-ET-LOWER@ <> IF
        EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT
    THEN
    _EXT4-ET-DEPTH @ _EXT4-ET-HAS@ 0= IF 0 EXIT THEN
    _EXT4-EV-IN @ 2 + W@ 1- 12 * 12 + _EXT4-EV-IN @ +
    DUP _EXT4-ET-LAST !
    _EXT4-ET-DEPTH @ 0= IF
        DUP L@ SWAP _EXT4-EXTENT-LEN@ +
        _EXT4-ET-DEPTH @ _EXT4-ET-UPPER@ U> IF
            EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT
        THEN
    ELSE
        DROP _EXT4-ET-LAST @ L@
        _EXT4-ET-DEPTH @ _EXT4-ET-UPPER@ U< 0= IF
            EXT4-D-DATA-MAP _EXT4-CORRUPT EXIT
        THEN
    THEN 0 ;

: _EXT4-VALIDATE-EXTENT-SUBTREE  ( physical-block depth -- ior )
    2DUP _EXT4-ET-CTX @ _EXT4-LOAD-EXTENT-NODE ?DUP IF
        >R 2DROP R> EXIT
    THEN
    DUP _EXT4-ET-CHECK-BOUNDS ?DUP IF
        >R 2DROP R> EXIT
    THEN
    DUP 0= IF 2DROP 0 EXIT THEN
    _EXT4-ET-CTX @ _EXT4-C.TREE-BLOCK + 2 + W@ 0 ?DO
        2DUP _EXT4-ET-CTX @ _EXT4-LOAD-EXTENT-NODE ?DUP IF
            >R 2DROP R> UNLOOP EXIT
        THEN
        _EXT4-ET-CTX @ _EXT4-C.TREE-BLOCK + 12 I 12 * + + L@
        OVER 1- _EXT4-ET-LOWER!
        I 1+ _EXT4-EV-COUNT @ < IF
            _EXT4-ET-CTX @ _EXT4-C.TREE-BLOCK +
            12 I 1+ 12 * + + L@
            OVER 1- _EXT4-ET-UPPER!
            TRUE OVER 1- _EXT4-ET-HAS!
        ELSE
            DUP _EXT4-ET-UPPER@ OVER 1- _EXT4-ET-UPPER!
            DUP _EXT4-ET-HAS@ OVER 1- _EXT4-ET-HAS!
        THEN
        _EXT4-ET-CTX @ _EXT4-C.TREE-BLOCK + 12 I 12 * + + 4 + L@
        >R DUP 1- R> SWAP RECURSE ?DUP IF
            >R 2DROP R> UNLOOP EXIT
        THEN
    LOOP 2DROP 0 ;

: _EXT4-VALIDATE-EXTENT-TREE  ( ctx -- ior )
    DUP _EXT4-ET-CTX ! _EXT4-VALIDATE-INLINE-EXTENTS ?DUP IF EXIT THEN
    _EXT4-ET-CTX @ _EXT4-C.INODE + _EXT4-I.BLOCK + DUP _EXT4-ET-ROOT !
    6 + W@ DUP _EXT4-ET-ROOT-DEPTH ! 0= IF
        0 EXIT
    THEN
    _EXT4-ET-ROOT @ 2 + W@ DUP _EXT4-ET-ROOT-COUNT ! 0 ?DO
        _EXT4-ET-ROOT @ 12 I 12 * + + L@
        _EXT4-ET-ROOT-DEPTH @ 1- _EXT4-ET-LOWER!
        I 1+ _EXT4-ET-ROOT-COUNT @ < IF
            _EXT4-ET-ROOT @ 12 I 1+ 12 * + + L@
            _EXT4-ET-ROOT-DEPTH @ 1- _EXT4-ET-UPPER!
            TRUE _EXT4-ET-ROOT-DEPTH @ 1- _EXT4-ET-HAS!
        ELSE
            FALSE _EXT4-ET-ROOT-DEPTH @ 1- _EXT4-ET-HAS!
        THEN
        _EXT4-ET-ROOT @ 12 I 12 * + + 4 + L@
        _EXT4-ET-ROOT-DEPTH @ 1- _EXT4-VALIDATE-EXTENT-SUBTREE
        ?DUP IF UNLOOP EXIT THEN
    LOOP 0 ;

VARIABLE _EXT4-MAP-LBLOCK
VARIABLE _EXT4-MAP-CTX
VARIABLE _EXT4-MAP-IN
VARIABLE _EXT4-MAP-ENTRY
VARIABLE _EXT4-MAP-START
VARIABLE _EXT4-MAP-LEN
VARIABLE _EXT4-MAP-RAWLEN
VARIABLE _EXT4-MAP-DEPTH
VARIABLE _EXT4-MAP-CHILD

: _EXT4-MAP-EXTENT-LEAF  ( header -- physical present? ior )
    DUP 2 + W@ 0 ?DO
        DUP 12 I 12 * + + _EXT4-MAP-ENTRY !
        _EXT4-MAP-ENTRY @ L@ _EXT4-MAP-START !
        _EXT4-MAP-ENTRY @ 4 + W@ DUP _EXT4-MAP-RAWLEN !
        DUP 32768 > IF 32768 - THEN _EXT4-MAP-LEN !
        _EXT4-MAP-LBLOCK @ _EXT4-MAP-START @ >=
        _EXT4-MAP-LBLOCK @ _EXT4-MAP-START @ _EXT4-MAP-LEN @ + < AND IF
            DROP
            _EXT4-MAP-RAWLEN @ 32768 > IF 0 FALSE 0 UNLOOP EXIT THEN
            _EXT4-MAP-ENTRY @ 8 + L@
            _EXT4-MAP-LBLOCK @ _EXT4-MAP-START @ - + TRUE 0
            UNLOOP EXIT
        THEN
    LOOP DROP 0 FALSE 0 ;

: _EXT4-MAP-EXTENT  ( logical-block ctx -- physical present? ior )
    _EXT4-MAP-CTX ! _EXT4-MAP-LBLOCK !
    _EXT4-MAP-CTX @ _EXT4-VALIDATE-INLINE-EXTENTS ?DUP IF
        0 FALSE ROT EXIT
    THEN
    _EXT4-MAP-CTX @ _EXT4-C.INODE + _EXT4-I.BLOCK + DUP _EXT4-MAP-IN !
    6 + W@ _EXT4-MAP-DEPTH !
    BEGIN _EXT4-MAP-DEPTH @ WHILE
        0 _EXT4-MAP-CHILD !
        _EXT4-MAP-IN @ 2 + W@ 0 ?DO
            _EXT4-MAP-IN @ 12 I 12 * + + DUP L@
            _EXT4-MAP-LBLOCK @ U> IF DROP LEAVE THEN
            4 + L@ _EXT4-MAP-CHILD !
        LOOP
        _EXT4-MAP-CHILD @ 0= IF 0 FALSE 0 EXIT THEN
        _EXT4-MAP-DEPTH @ 1- DUP _EXT4-MAP-DEPTH !
        _EXT4-MAP-CHILD @ SWAP _EXT4-MAP-CTX @
        _EXT4-LOAD-EXTENT-NODE ?DUP IF 0 FALSE ROT EXIT THEN
        _EXT4-MAP-CTX @ _EXT4-C.TREE-BLOCK + _EXT4-MAP-IN !
    REPEAT
    _EXT4-MAP-IN @ _EXT4-MAP-EXTENT-LEAF ;

\ Legacy mappings remain an admitted read format even when the filesystem
\ advertises extents globally.  Validate the complete direct/indirect tree
\ with a depth bound of three and then use the same checked map interface.
VARIABLE _EXT4-LV-CTX
VARIABLE _EXT4-LV-PTRS

: _EXT4-LOAD-LEGACY-POINTER-BLOCK  ( physical-block ctx -- ior )
    DUP _EXT4-LV-CTX ! SWAP DUP >R EXT4-D-DATA-MAP ROT
    _EXT4-REQUIRE-ALLOCATED-BLOCK ?DUP IF R> DROP EXIT THEN
    R> _EXT4-LV-CTX @ _EXT4-READ-BLOCK ?DUP IF EXIT THEN
    _EXT4-LV-CTX @ _EXT4-C.BLOCK +
    _EXT4-LV-CTX @ _EXT4-C.TREE-BLOCK +
    _EXT4-LV-CTX @ _EXT4-C.BSIZE + @ CMOVE
    0 ;

: _EXT4-VALIDATE-LEGACY-SUBTREE  ( physical-block level -- ior )
    OVER _EXT4-LV-CTX @ _EXT4-LOAD-LEGACY-POINTER-BLOCK ?DUP IF
        >R 2DROP R> EXIT
    THEN
    _EXT4-LV-CTX @ _EXT4-C.BSIZE + @ 4 / 0 ?DO
        _EXT4-LV-CTX @ _EXT4-C.TREE-BLOCK + I 4 * + L@ ?DUP IF
            OVER 1 > IF
                >R DUP 1- R> SWAP RECURSE ?DUP IF
                    >R 2DROP R> UNLOOP EXIT
                THEN
                \ Recursion owns the shared tree buffer; restore this parent
                \ only after an actual child traversal, not once per null slot.
                OVER _EXT4-LV-CTX @ _EXT4-LOAD-LEGACY-POINTER-BLOCK
                ?DUP IF >R 2DROP R> UNLOOP EXIT THEN
            ELSE
                EXT4-D-DATA-MAP _EXT4-LV-CTX @
                _EXT4-REQUIRE-ALLOCATED-BLOCK ?DUP IF
                    >R 2DROP R> UNLOOP EXIT
                THEN
            THEN
        THEN
    LOOP 2DROP 0 ;

: _EXT4-VALIDATE-LEGACY-MAP  ( ctx -- ior )
    DUP _EXT4-LV-CTX ! _EXT4-C.INODE + _EXT4-I.BLOCK +
    _EXT4-NDIRECT 0 DO
        DUP I 4 * + L@ ?DUP IF
            EXT4-D-DATA-MAP _EXT4-LV-CTX @
            _EXT4-REQUIRE-ALLOCATED-BLOCK ?DUP IF
                NIP UNLOOP EXIT
            THEN
        THEN
    LOOP
    3 0 DO
        DUP _EXT4-NDIRECT I + 4 * + L@ ?DUP IF
            I 1+ _EXT4-VALIDATE-LEGACY-SUBTREE ?DUP IF
                NIP UNLOOP EXIT
            THEN
        THEN
    LOOP DROP 0 ;

VARIABLE _EXT4-LM-INDEX
VARIABLE _EXT4-LM-PTR
VARIABLE _EXT4-LM-LEVEL
VARIABLE _EXT4-LM-DIV

: _EXT4-U-POWER  ( base exponent -- value )
    1 SWAP 0 ?DO OVER * LOOP NIP ;

: _EXT4-MAP-LEGACY-INDIRECT  ( index root level ctx -- physical present? ior )
    _EXT4-MAP-CTX ! _EXT4-LM-LEVEL ! _EXT4-LM-PTR ! _EXT4-LM-INDEX !
    BEGIN _EXT4-LM-LEVEL @ WHILE
        _EXT4-LM-PTR @ 0= IF 0 FALSE 0 EXIT THEN
        _EXT4-LM-PTR @ _EXT4-MAP-CTX @
        _EXT4-LOAD-LEGACY-POINTER-BLOCK ?DUP IF 0 FALSE ROT EXIT THEN
        _EXT4-MAP-CTX @ _EXT4-C.BSIZE + @ 4 /
        _EXT4-LM-LEVEL @ 1- _EXT4-U-POWER DUP _EXT4-LM-DIV !
        _EXT4-LM-INDEX @ SWAP /MOD
        SWAP _EXT4-LM-INDEX !
        4 * _EXT4-MAP-CTX @ _EXT4-C.TREE-BLOCK + + L@ _EXT4-LM-PTR !
        -1 _EXT4-LM-LEVEL +!
    REPEAT
    _EXT4-LM-PTR @ DUP 0= IF DROP 0 FALSE 0 EXIT THEN
    DUP EXT4-D-DATA-MAP _EXT4-MAP-CTX @
    _EXT4-REQUIRE-ALLOCATED-BLOCK ?DUP IF NIP 0 FALSE ROT EXIT THEN
    TRUE 0 ;

: _EXT4-MAP-LEGACY  ( logical-block ctx -- physical present? ior )
    _EXT4-MAP-CTX ! _EXT4-MAP-LBLOCK !
    _EXT4-MAP-LBLOCK @ _EXT4-NDIRECT U< IF
        _EXT4-MAP-CTX @ _EXT4-C.INODE + _EXT4-I.BLOCK +
        _EXT4-MAP-LBLOCK @ 4 * + L@ DUP 0= IF DROP 0 FALSE 0 EXIT THEN
        DUP EXT4-D-DATA-MAP _EXT4-MAP-CTX @
        _EXT4-REQUIRE-ALLOCATED-BLOCK ?DUP IF NIP 0 FALSE ROT EXIT THEN
        TRUE 0 EXIT
    THEN
    _EXT4-MAP-LBLOCK @ _EXT4-NDIRECT - _EXT4-LM-INDEX !
    _EXT4-MAP-CTX @ _EXT4-C.BSIZE + @ 4 / _EXT4-LV-PTRS !
    3 0 DO
        _EXT4-LV-PTRS @ I 1+ _EXT4-U-POWER DUP _EXT4-LM-DIV !
        _EXT4-LM-INDEX @ OVER U< IF
            DROP
            _EXT4-LM-INDEX @
            _EXT4-MAP-CTX @ _EXT4-C.INODE + _EXT4-I.BLOCK +
            _EXT4-NDIRECT I + 4 * + L@
            I 1+ _EXT4-MAP-CTX @ _EXT4-MAP-LEGACY-INDIRECT
            UNLOOP EXIT
        THEN
        _EXT4-LM-INDEX @ SWAP - _EXT4-LM-INDEX !
    LOOP
