: _EXT4-VALIDATE-JOURNAL  ( ctx -- ior )
    DUP _EXT4-JV-CTX ! 8 SWAP _EXT4-LOAD-INODE ?DUP IF EXIT THEN
    _EXT4-JV-CTX @ _EXT4-C.INODE + DUP _EXT4-JV-IN !
    _EXT4-I.MODE + W@ 0xF000 AND 0x8000 <> IF
        EXT4-D-JOURNAL _EXT4-CORRUPT EXIT
    THEN
    _EXT4-JV-IN @ _EXT4-I.SIZE-HI + L@ 32 LSHIFT
    _EXT4-JV-IN @ _EXT4-I.SIZE-LO + L@ OR DUP _EXT4-JV-SIZE !
    4194304 <> IF EXT4-D-JOURNAL _EXT4-CORRUPT EXIT THEN
    _EXT4-JV-IN @ _EXT4-I.FLAGS + L@ _EXT4-EXTENTS-FL AND IF
        _EXT4-JV-CTX @ _EXT4-VALIDATE-EXTENT-TREE
    ELSE
        _EXT4-JV-CTX @ _EXT4-VALIDATE-LEGACY-MAP
    THEN ?DUP IF EXIT THEN
    0 _EXT4-JV-CTX @ _EXT4-MAP-BLOCK
    _EXT4-JV-IOR ! _EXT4-JV-PRESENT ! _EXT4-JV-PHYS !
    _EXT4-JV-IOR @ ?DUP IF EXIT THEN
    _EXT4-JV-PRESENT @ 0= IF EXT4-D-JOURNAL _EXT4-CORRUPT EXIT THEN
    _EXT4-JV-PHYS @ _EXT4-JV-CTX @ _EXT4-READ-BLOCK ?DUP IF EXIT THEN
    _EXT4-JV-CTX @ _EXT4-C.BLOCK + DUP _EXT4-JV-JSB !
    _EXT4-BE32@ _EXT4-JBD2-MAGIC =
    _EXT4-JV-JSB @ 4 + _EXT4-BE32@ _EXT4-JBD2-SUPER-V2 = AND
    _EXT4-JV-JSB @ 12 + _EXT4-BE32@
        _EXT4-JV-CTX @ _EXT4-C.BSIZE + @ = AND
    _EXT4-JV-JSB @ 16 + _EXT4-BE32@
        _EXT4-JV-SIZE @ _EXT4-JV-CTX @ _EXT4-C.BSIZE + @ / = AND
    _EXT4-JV-JSB @ 20 + _EXT4-BE32@ DUP 0>
    SWAP _EXT4-JV-JSB @ 16 + _EXT4-BE32@ < AND AND
    _EXT4-JV-JSB @ 28 + _EXT4-BE32@ 0= AND
    _EXT4-JV-JSB @ 32 + _EXT4-BE32@ 0= AND
    _EXT4-JV-JSB @ 48 +
        _EXT4-JV-CTX @ _EXT4-C.SB + _EXT4-SB.UUID +
        16 _EXT4-BYTES=? AND
    _EXT4-JV-JSB @ 64 + _EXT4-BE32@ 1 = AND
    _EXT4-JV-JSB @ 68 + _EXT4-BE32@ 0= AND 0= IF
        EXT4-D-JOURNAL _EXT4-CORRUPT EXIT
    THEN
    \ The first read-only landing admits only the clean mke2fs journal
    \ profile.  Other JBD2 feature states are format variants requiring
    \ replay/checksum support, not evidence of structural corruption.
    _EXT4-JV-JSB @ 36 + _EXT4-BE32@
    _EXT4-JV-JSB @ 40 + _EXT4-BE32@ OR
    _EXT4-JV-JSB @ 44 + _EXT4-BE32@ OR IF
        EXT4-D-JOURNAL _EXT4-UNSUPPORTED EXIT
    THEN
    \ With all feature masks clear, checksum_v1/v2/v3 and fast-commit
    \ metadata are absent.  Require their type, padding, count, and stored
    \ checksum fields to remain in that exact unchecksummed state.
    _EXT4-JV-JSB @ 0x50 + C@ 0<>
    _EXT4-JV-JSB @ 0x51 + C@ 0<> OR
    _EXT4-JV-JSB @ 0x52 + C@ 0<> OR
    _EXT4-JV-JSB @ 0x53 + C@ 0<> OR
    _EXT4-JV-JSB @ 0x54 + _EXT4-BE32@ 0<> OR
    _EXT4-JV-JSB @ 0xFC + _EXT4-BE32@ 0<> OR IF
        EXT4-D-JOURNAL _EXT4-CORRUPT EXIT
    THEN
    0 ;

