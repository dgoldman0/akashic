    ['] VFS-USE _VFSNI-STORE @ VFSNAP.RESTORE-XT !
    VFSNAP-S-ABSENT _VFSNI-STORE @ VFSNAP.LAST-STATUS !
    VFSNAP-S-OK ;

: _VFSNAP-INIT-SPANS-SAFE?
  ( path-a path-u scratch-a scratch-u spec store -- flag )
    >R
    4 PICK 4 PICK _VFSNAP-SPAN-VALID? 0= IF
        2DROP 2DROP DROP R> DROP 0 EXIT
    THEN
    2 PICK 2 PICK _VFSNAP-SPAN-VALID? 0= IF
        2DROP 2DROP DROP R> DROP 0 EXIT
    THEN
    DUP VFSNAP-SPEC-SEALED? 0= IF
        2DROP 2DROP DROP R> DROP 0 EXIT
    THEN
    DUP VFSNAP-SPEC.PAYLOAD-U @ VFSNAP-HEADER-SIZE +
        2 PICK <> IF 2DROP 2DROP DROP R> DROP 0 EXIT THEN
    R@ VFSNAP-STORE-SIZE _VFSNAP-SPAN-VALID? 0= IF
        2DROP 2DROP DROP R> DROP 0 EXIT
    THEN
    4 PICK 4 PICK 4 PICK 4 PICK _VFSNAP-RANGES-OVERLAP? IF
        2DROP 2DROP DROP R> DROP 0 EXIT
    THEN
    4 PICK 4 PICK R@ VFSNAP-STORE-SIZE
        _VFSNAP-RANGES-OVERLAP? IF
        2DROP 2DROP DROP R> DROP 0 EXIT
    THEN
    2 PICK 2 PICK R@ VFSNAP-STORE-SIZE
        _VFSNAP-RANGES-OVERLAP? IF
        2DROP 2DROP DROP R> DROP 0 EXIT
    THEN
    DUP VFSNAP-SPEC-SIZE R@ VFSNAP-STORE-SIZE
        _VFSNAP-RANGES-OVERLAP? IF
        2DROP 2DROP DROP R> DROP 0 EXIT
    THEN
    4 PICK 4 PICK 2 PICK VFSNAP-SPEC-SIZE
        _VFSNAP-RANGES-OVERLAP? IF
        2DROP 2DROP DROP R> DROP 0 EXIT
    THEN
    2 PICK 2 PICK 2 PICK VFSNAP-SPEC-SIZE
        _VFSNAP-RANGES-OVERLAP? IF
        2DROP 2DROP DROP R> DROP 0 EXIT
    THEN
    4 PICK 4 PICK 2 PICK _VFSNAP-ALL-PRIVATE-ALIASES? IF
        2DROP 2DROP DROP R> DROP 0 EXIT
    THEN
    2 PICK 2 PICK 2 PICK _VFSNAP-ALL-PRIVATE-ALIASES? IF
        2DROP 2DROP DROP R> DROP 0 EXIT
    THEN
    R@ VFSNAP-STORE-SIZE 2 PICK _VFSNAP-ALL-PRIVATE-ALIASES? IF
        2DROP 2DROP DROP R> DROP 0 EXIT
    THEN
    2DROP 2DROP DROP R> DROP -1 ;

