        VREPL-S-IO OF VFSNAP-S-IO ENDOF
        VREPL-S-RECOVERY OF VFSNAP-S-RECOVERY ENDOF
        VREPL-S-MARKER-CORRUPT OF VFSNAP-S-RECOVERY ENDOF
        VREPL-S-UNCERTAIN OF VFSNAP-S-RECOVERY ENDOF
