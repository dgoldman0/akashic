    THEN
    _VFSNI-PATH-A @ _VFSNI-PATH-U @
        _VFSNI-STORE @ VFSNAP.REPLACE VREPL-DERIVE-PATHS!
        DUP _VFSNI-STATUS ! VREPL-S-OK <> IF
        _VFSNI-STORE @ VFSNAP-STORE-SIZE 0 FILL
        _VFSNI-STATUS @ _VFSNAP-VREPL>STATUS EXIT
    THEN
