VARIABLE _VFSR-VREPL
VARIABLE _VFSR-STATUS

: _VFSNAP-RECOVER-BODY  ( -- status )
    _VFSOP-STORE @ VFSNAP-BLOCKED? IF
        _VFSOP-STORE @ VFSNAP-LAST-STATUS@ EXIT
    THEN
    _VFSOP-STORE @ VFSNAP.REPLACE VREPL-RECOVER DUP _VFSR-VREPL !
    _VFSOP-STORE @ VFSNAP.LAST-VREPL !
    _VFSR-VREPL @ _VFSNAP-VREPL>STATUS DUP _VFSR-STATUS ! DROP
    _VFSR-STATUS @ VFSNAP-S-OK = IF
        _VFSR-STATUS @ _VFSOP-STORE @ _VFSNAP-STATUS! EXIT
    THEN
    _VFSR-STATUS @ _VFSNAP-BLOCKING-STATUS? IF
        _VFSR-STATUS @ _VFSOP-STORE @ _VFSNAP-BLOCK! EXIT
    THEN
    _VFSR-STATUS @ _VFSOP-STORE @ _VFSNAP-STATUS! ;

VARIABLE _VFSL-DESTINATION
VARIABLE _VFSL-CAPACITY
VARIABLE _VFSL-GENERATION-DST
VARIABLE _VFSL-STATUS

: _VFSNAP-LOAD-BODY  ( -- status )
    _VFSOP-STORE @ VFSNAP-BLOCKED? IF
        _VFSOP-STORE @ VFSNAP-LAST-STATUS@ EXIT
    THEN
    _VFSNAP-RECOVER-BODY DUP IF EXIT THEN DROP
    _VFSNAP-READ DUP _VFSL-STATUS ! DROP
    _VFSL-STATUS @ VFSNAP-S-OK = IF
        _VFSNAP-RECORD VFSNAP-HEADER-SIZE + _VFSL-DESTINATION @
            _VFSNAP-SPEC VFSNAP-SPEC.PAYLOAD-U @ CMOVE
        _VFSRD-GENERATION @ _VFSL-GENERATION-DST @ !
        _VFSL-STATUS @ _VFSOP-STORE @ _VFSNAP-STATUS! EXIT
    THEN
    _VFSL-STATUS @ VFSNAP-S-ABSENT = IF
        _VFSL-STATUS @ _VFSOP-STORE @ _VFSNAP-STATUS! EXIT
    THEN
    _VFSL-STATUS @ _VFSNAP-BLOCKING-STATUS? IF
        _VFSL-STATUS @ _VFSOP-STORE @ _VFSNAP-BLOCK! EXIT
    THEN
    _VFSL-STATUS @ _VFSOP-STORE @ _VFSNAP-STATUS! ;

VARIABLE _VFSS-CONTEXT
VARIABLE _VFSS-EXPECTED
VARIABLE _VFSS-NEXT
VARIABLE _VFSS-STATUS
VARIABLE _VFSS-VREPL

: _VFSNAP-SAVE-FAIL  ( status -- status )
    DUP _VFSNAP-BLOCKING-STATUS? IF
        _VFSOP-STORE @ _VFSNAP-BLOCK! EXIT
    THEN
    _VFSOP-STORE @ _VFSNAP-STATUS! ;

: _VFSNAP-ENCODE  ( -- status )
    _VFSNAP-RECORD _VFSOP-STORE @ VFSNAP.SCRATCH-U @ 0 FILL
    _VFSS-CONTEXT @
    _VFSNAP-RECORD VFSNAP-HEADER-SIZE +
    _VFSNAP-SPEC VFSNAP-SPEC.PAYLOAD-U @
    _VFSS-NEXT @ _VFSNAP-SPEC VFSNAP-SPEC.ENCODE-XT @ EXECUTE
    DUP IF EXIT THEN DROP
    _VFSNAP-SPEC VFSNAP-SPEC.RECORD-MAGIC
        _VFSNAP-RECORD _VFSN-H-MAGIC + 8 CMOVE
    _VFSNAP-SPEC VFSNAP-SPEC.FORMAT @
        _VFSNAP-RECORD _VFSN-H-FORMAT + !
    VFSNAP-HEADER-SIZE _VFSNAP-RECORD _VFSN-H-HEADER-SIZE + !
    _VFSS-NEXT @ _VFSNAP-RECORD _VFSN-H-GENERATION + !
    _VFSNAP-SPEC VFSNAP-SPEC.PAYLOAD-U @
        _VFSNAP-RECORD _VFSN-H-PAYLOAD-SIZE + !
    _VFSNAP-RECORD VFSNAP-HEADER-SIZE +
        _VFSNAP-SPEC VFSNAP-SPEC.PAYLOAD-U @ _VFSNAP-PAYLOAD-CRC
        _VFSNAP-RECORD _VFSN-H-PAYLOAD-CRC + !
    _VFSNAP-RECORD _VFSNAP-HEADER-CRC
        _VFSNAP-RECORD _VFSN-H-HEADER-CRC + !
    VFSNAP-S-OK ;

: _VFSNAP-SAVE-BODY  ( -- status )
    _VFSS-EXPECTED @ 0< IF VFSNAP-S-INVALID _VFSNAP-SAVE-FAIL EXIT THEN
    _VFSS-EXPECTED @ 1+ DUP _VFSS-NEXT ! 0> 0= IF
        VFSNAP-S-CAPACITY _VFSNAP-SAVE-FAIL EXIT
    THEN
    _VFSOP-STORE @ VFSNAP-BLOCKED? IF
        _VFSOP-STORE @ VFSNAP-LAST-STATUS@ EXIT
    THEN
    _VFSNAP-RECOVER-BODY DUP IF _VFSNAP-SAVE-FAIL EXIT THEN DROP
    _VFSNAP-READ DUP _VFSS-STATUS ! DROP
    _VFSS-STATUS @ VFSNAP-S-ABSENT = IF
        _VFSS-EXPECTED @ IF VFSNAP-S-CONFLICT _VFSNAP-SAVE-FAIL EXIT THEN
    ELSE
        _VFSS-STATUS @ VFSNAP-S-OK <> IF
            _VFSS-STATUS @ _VFSNAP-SAVE-FAIL EXIT
        THEN
        _VFSRD-GENERATION @ _VFSS-EXPECTED @ <> IF
            VFSNAP-S-CONFLICT _VFSNAP-SAVE-FAIL EXIT
        THEN
    THEN
    _VFSNAP-ENCODE DUP _VFSS-STATUS ! IF
        _VFSS-STATUS @ _VFSNAP-SAVE-FAIL EXIT
    THEN
    _VFSNAP-RECORD _VFSOP-STORE @ VFSNAP.SCRATCH-U @
        _VFSOP-STORE @ VFSNAP.REPLACE VREPL-REPLACE DUP _VFSS-VREPL !
    _VFSOP-STORE @ VFSNAP.LAST-VREPL !
    _VFSS-VREPL @ _VFSNAP-VREPL>STATUS DUP _VFSS-STATUS ! DROP
    _VFSS-STATUS @ VFSNAP-S-OK = IF
        _VFSS-STATUS @ _VFSOP-STORE @ _VFSNAP-STATUS! EXIT
    THEN
    _VFSS-STATUS @ _VFSNAP-SAVE-FAIL ;

