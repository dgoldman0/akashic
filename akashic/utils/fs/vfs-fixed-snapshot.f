: _VFSNAP-SPEC-PRIVATE-ADD  ( private-a private-u spec -- status )
    _VSPA-SPEC ! _VSPA-U ! _VSPA-A !
    _VSPA-SPEC @ VFSNAP-SPEC-VALID? 0= IF VFSNAP-S-INVALID EXIT THEN
    _VSPA-SPEC @ VFSNAP-SPEC.FLAGS @ _VFSNAP-SPEC-F-SEALED AND IF
        VFSNAP-S-BUSY EXIT
    THEN
    _VSPA-SPEC @ VFSNAP-SPEC.PRIVATE-N @
        VFSNAP-SPEC-PRIVATE-MAX >= IF VFSNAP-S-CAPACITY EXIT THEN
    _VSPA-A @ _VSPA-U @ _VFSNAP-SPAN-VALID? 0= IF VFSNAP-S-INVALID EXIT THEN
    _VSPA-A @ _VSPA-U @ _VSPA-SPEC @ VFSNAP-SPEC-SIZE
        _VFSNAP-RANGES-OVERLAP? IF VFSNAP-S-INVALID EXIT THEN
    _VSPA-A @ _VSPA-U @ _VFSNAP-DEPENDENCY-ALIASES? IF
        VFSNAP-S-INVALID EXIT
    THEN
    _VSPA-A @ _VSPA-U @ _VSPA-SPEC @
        _VFSNAP-SPEC-PRIVATE-ALIASES? IF VFSNAP-S-INVALID EXIT THEN
    _VSPA-SPEC @ VFSNAP-SPEC.PRIVATE-N @ _VSPA-SPEC @
        _VFSNAP-SPEC-PRIVATE-NTH _VSPA-RANGE !
    _VSPA-A @ _VSPA-RANGE @ !
    _VSPA-U @ _VSPA-RANGE @ 8 + !
    1 _VSPA-SPEC @ VFSNAP-SPEC.PRIVATE-N +!
    VFSNAP-S-OK ;

' _VFSNAP-SPEC-PRIVATE-ADD CONSTANT _vfsnap-spec-private-add-xt

: _VFSNAP-SPEC-PRIVATE-ADD-SAFE?  ( private-a private-u spec -- flag )
    DUP _VFSNAP-SPEC-SPAN-SAFE? 0= IF 2DROP DROP 0 EXIT THEN
    2 PICK 2 PICK _VFSNAP-SPAN-VALID? 0= IF 2DROP DROP 0 EXIT THEN
    2 PICK 2 PICK 2 PICK VFSNAP-SPEC-SIZE
        _VFSNAP-RANGES-OVERLAP? IF 2DROP DROP 0 EXIT THEN
    2 PICK 2 PICK _VFSNAP-DEPENDENCY-ALIASES? IF
        2DROP DROP 0 EXIT
    THEN
    2 PICK 2 PICK 2 PICK _VFSNAP-SPEC-PRIVATE-ALIASES? IF
        2DROP DROP 0 EXIT
    THEN
    2DROP DROP -1 ;

: VFSNAP-SPEC-PRIVATE-ADD  ( private-a private-u spec -- status )
    2 PICK 2 PICK 2 PICK _VFSNAP-SPEC-PRIVATE-ADD-SAFE? 0= IF
        2DROP DROP VFSNAP-S-INVALID EXIT
    THEN
    _vfsnap-operation-active @ IF
        2DROP DROP VFSNAP-S-BUSY EXIT
    THEN
    _vfsnap-spec-private-add-xt _vfsnap-guard WITH-GUARD ;

: _VFSNAP-SPEC-SEAL  ( spec -- status )
    DUP VFSNAP-SPEC-VALID? 0= IF DROP VFSNAP-S-INVALID EXIT THEN
    _VFSNAP-SPEC-F-SEALED OVER VFSNAP-SPEC.FLAGS DUP @ ROT OR SWAP !
    DROP VFSNAP-S-OK ;

' _VFSNAP-SPEC-SEAL CONSTANT _vfsnap-spec-seal-xt
: VFSNAP-SPEC-SEAL  ( spec -- status )
    DUP _VFSNAP-SPEC-SPAN-SAFE? 0= IF DROP VFSNAP-S-INVALID EXIT THEN
    _vfsnap-operation-active @ IF DROP VFSNAP-S-BUSY EXIT THEN
    _vfsnap-spec-seal-xt _vfsnap-guard WITH-GUARD ;

