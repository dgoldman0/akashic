VARIABLE _VFSOP-STORE
VARIABLE _VFSRD-FD
VARIABLE _VFSRD-OLD-VFS
VARIABLE _VFSRD-HAVE-OLD-VFS
VARIABLE _VFSRD-STATUS
VARIABLE _VFSRD-FILE-U
VARIABLE _VFSRD-GENERATION
VARIABLE _VFSRD-CLEAN-FD
VARIABLE _VFSRD-CLEAN-FAILED

: _VFSNAP-RECORD  ( -- record ) _VFSOP-STORE @ VFSNAP.SCRATCH-A @ ;
: _VFSNAP-SPEC    ( -- spec ) _VFSOP-STORE @ VFSNAP.SPEC @ ;

: _VFSNAP-HEADER-CRC  ( record -- crc32 )
    CRC32-BEGIN
    DUP _VFSN-H-HEADER-CRC CRC32-ADD
    DUP _VFSN-H-FLAGS + 8 CRC32-ADD
    DROP CRC32-END ;

: _VFSNAP-PAYLOAD-CRC  ( a u -- crc32 )
    CRC32-BEGIN CRC32-ADD CRC32-END ;

: _VFSRD-CLOSE-CALL  ( -- )
    _VFSRD-CLEAN-FD @ _VFSOP-STORE @ VFSNAP.CLOSE-XT @ EXECUTE ;
: _VFSRD-RESTORE-CALL  ( -- )
    _VFSRD-OLD-VFS @ _VFSOP-STORE @ VFSNAP.RESTORE-XT @ EXECUTE ;

: _VFSRD-CLEANUP  ( -- failed? )
    0 _VFSRD-CLEAN-FAILED !
    _VFSRD-FD @ ?DUP IF
        _VFSRD-CLEAN-FD ! 0 _VFSRD-FD !
        ['] _VFSRD-CLOSE-CALL CATCH IF -1 _VFSRD-CLEAN-FAILED ! THEN
    THEN
    _VFSRD-HAVE-OLD-VFS @ IF
        0 _VFSRD-HAVE-OLD-VFS !
        ['] _VFSRD-RESTORE-CALL CATCH IF -1 _VFSRD-CLEAN-FAILED ! THEN
    THEN
    _VFSRD-CLEAN-FAILED @ ;

: _VFSRD-PARENT-LEN  ( base-offset -- length )
    DUP 1 = IF DROP 1 ELSE 1- THEN ;

: _VFSRD-NAMESPACE-STATUS  ( -- status )
    _VFSOP-STORE @ VFSNAP.REPLACE DUP VREPL.TARGET
    SWAP VREPL.TARGET-BASE @ _VFSRD-PARENT-LEN
    VFS-RP-FOLLOW-FINAL _VFSOP-STORE @ VFSNAP.VFS @
        VFS-RESOLVE-POLICY?
    IF DROP VFSNAP-S-RECOVERY EXIT THEN
    DUP 0= IF DROP VFSNAP-S-RECOVERY EXIT THEN
    IN.TYPE @ VFS-T-DIR <> IF VFSNAP-S-RECOVERY EXIT THEN

    _VFSOP-STORE @ VFSNAP-PATH$
    VFS-RP-NOFOLLOW-FINAL _VFSOP-STORE @ VFSNAP.VFS @
        VFS-RESOLVE-POLICY?
    DUP IF
        DUP VFS-IOR-REASON VFS-R-NOENT = >R
        2DROP R> IF VFSNAP-S-ABSENT ELSE VFSNAP-S-IO THEN EXIT
    THEN DROP
    DUP 0= IF DROP VFSNAP-S-IO EXIT THEN
    IN.TYPE @ VFS-T-FILE <> IF VFSNAP-S-RECOVERY EXIT THEN
    VFSNAP-S-OK ;

: _VFSRD-READ-BODY  ( -- status )
    _VFSRD-NAMESPACE-STATUS DUP IF EXIT THEN DROP
    _VFSOP-STORE @ VFSNAP-PATH$ VFS-OPEN DUP 0= IF
        DROP VFSNAP-S-IO EXIT
    THEN _VFSRD-FD !
    _VFSRD-FD @ VFS-SIZE DUP _VFSRD-FILE-U !
    DUP 0< IF DROP VFSNAP-S-IO EXIT THEN
    VFSNAP-HEADER-SIZE < IF VFSNAP-S-CORRUPT EXIT THEN
    _VFSNAP-RECORD VFSNAP-HEADER-SIZE _VFSRD-FD @
        VFS-READ-EXACT IF VFSNAP-S-IO EXIT THEN
    _VFSNAP-RECORD _VFSN-H-MAGIC + 8
        _VFSNAP-SPEC VFSNAP-SPEC.RECORD-MAGIC 8 COMPARE 0<> IF
        VFSNAP-S-CORRUPT EXIT
    THEN
    _VFSNAP-RECORD _VFSNAP-HEADER-CRC
        _VFSNAP-RECORD _VFSN-H-HEADER-CRC + @ <> IF
        VFSNAP-S-CORRUPT EXIT
    THEN
    _VFSNAP-RECORD _VFSN-H-FORMAT + @
        _VFSNAP-SPEC VFSNAP-SPEC.FORMAT @ <> IF
        VFSNAP-S-UNSUPPORTED EXIT
    THEN
    _VFSNAP-RECORD _VFSN-H-HEADER-SIZE + @ VFSNAP-HEADER-SIZE <>
    _VFSNAP-RECORD _VFSN-H-FLAGS + @ 0<> OR
    _VFSNAP-RECORD _VFSN-H-GENERATION + @ DUP _VFSRD-GENERATION !
        0> 0= OR
    _VFSNAP-RECORD _VFSN-H-PAYLOAD-SIZE + @
        _VFSNAP-SPEC VFSNAP-SPEC.PAYLOAD-U @ <> OR IF
        VFSNAP-S-CORRUPT EXIT
    THEN
    _VFSRD-FILE-U @ _VFSOP-STORE @ VFSNAP.SCRATCH-U @ <> IF
        VFSNAP-S-CORRUPT EXIT
    THEN
    _VFSNAP-RECORD VFSNAP-HEADER-SIZE +
        _VFSNAP-SPEC VFSNAP-SPEC.PAYLOAD-U @
        _VFSRD-FD @ VFS-READ-EXACT IF VFSNAP-S-IO EXIT THEN
    _VFSNAP-RECORD VFSNAP-HEADER-SIZE +
        _VFSNAP-SPEC VFSNAP-SPEC.PAYLOAD-U @ _VFSNAP-PAYLOAD-CRC
        _VFSNAP-RECORD _VFSN-H-PAYLOAD-CRC + @ <> IF
        VFSNAP-S-CORRUPT EXIT
    THEN
    _VFSNAP-RECORD VFSNAP-HEADER-SIZE +
        _VFSNAP-SPEC VFSNAP-SPEC.PAYLOAD-U @
        _VFSRD-GENERATION @
        _VFSNAP-SPEC VFSNAP-SPEC.VALIDATE-XT @ EXECUTE ;

: _VFSRD-READ-OP  ( -- )
    VFS-CUR _VFSRD-OLD-VFS ! -1 _VFSRD-HAVE-OLD-VFS !
    _VFSOP-STORE @ VFSNAP.VFS @ VFS-USE
    _VFSRD-READ-BODY _VFSRD-STATUS ! ;

: _VFSRD-READ-TRANSACTION  ( -- status )
    0 _VFSRD-FD ! 0 _VFSRD-HAVE-OLD-VFS !
    VFSNAP-S-IO _VFSRD-STATUS !
    ['] _VFSRD-READ-OP CATCH IF VFSNAP-S-IO _VFSRD-STATUS ! THEN
    _VFSRD-CLEANUP IF VFSNAP-S-IO EXIT THEN
    _VFSRD-STATUS @ ;

: _VFSRD-READ-TRANSACTION-CALL  ( -- status )
    ['] _VFSRD-READ-TRANSACTION VFS-TRANSACTION ;

: _VFSNAP-READ  ( -- status )
    ['] _VFSRD-READ-TRANSACTION-CALL CATCH ?DUP IF
        DROP VFSNAP-S-IO
    THEN ;

