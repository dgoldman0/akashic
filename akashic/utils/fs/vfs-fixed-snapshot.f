    DUP VFSNAP.FLAGS @ _VFSNAP-STORE-F-ACTIVE AND IF
        DROP VFSNAP-S-BUSY EXIT
    THEN
    _VFSNF-STORE !
    _VFSNF-STORE @ VFSNAP-SCRATCH$ 0 FILL
    _VFSNF-STORE @ VFSNAP-STORE-SIZE 0 FILL
    VFSNAP-S-OK ;

