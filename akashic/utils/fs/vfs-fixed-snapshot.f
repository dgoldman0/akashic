\ =====================================================================
\  Overflow-safe span and hostile-alias predicates
\ =====================================================================

: _VFSNAP-SPAN-VALID?  ( address length -- flag )
    DUP 0= IF 2DROP -1 EXIT THEN
    OVER 0= IF 2DROP 0 EXIT THEN
    MSPAN-NONWRAPPING? ;

: _VFSNAP-RANGES-OVERLAP?  ( a1 u1 a2 u2 -- flag )
    MSPAN-OVERLAP? ;

CREATE _VFSNAP-PRIVATE-BEGIN 0 ALLOT
VARIABLE _VFSNAP-PRIVATE-LIMIT
GUARD _vfsnap-guard
VARIABLE _vfsnap-operation-active

: _VFSNAP-DEPENDENCY-ALIASES?  ( address length -- flag )
    2DUP _VFSNAP-PRIVATE-BEGIN _VFSNAP-PRIVATE-LIMIT @
        _VFSNAP-PRIVATE-BEGIN - _VFSNAP-RANGES-OVERLAP? IF
        2DROP -1 EXIT
    THEN
    2DUP _vrepl-guard _VRCL-REPLACE-R 8 + _vrepl-guard -
        _VFSNAP-RANGES-OVERLAP? IF 2DROP -1 EXIT THEN
    2DUP _VFS-CUR
    [DEFINED] _vfs-guard [IF]
        _vfs-guard _GRD-SIZE-SPIN +
    [ELSE]
        _VIP-POS 8 +
    [THEN]
    _VFS-CUR - _VFSNAP-RANGES-OVERLAP? IF 2DROP -1 EXIT THEN
    2DUP _CRC-HDST
    [DEFINED] _crc-guard [IF]
        _CRC-BEGIN-MODE 8 +
    [ELSE]
        _CRC-HDST 8 +
    [THEN]
    _CRC-HDST - _VFSNAP-RANGES-OVERLAP? IF 2DROP -1 EXIT THEN
    2DUP _SHA3-IPAD
    [DEFINED] _sha3-guard [IF]
        _sha3-guard _GRD-SIZE-SPIN +
    [ELSE]
        _SHA3-HDST 8 +
    [THEN]
    _SHA3-IPAD - _VFSNAP-RANGES-OVERLAP? IF 2DROP -1 EXIT THEN
    2DROP 0 ;

: _VFSNAP-SPEC-SPAN-SAFE?  ( spec -- flag )
    DUP VFSNAP-SPEC-SIZE _VFSNAP-SPAN-VALID? 0= IF DROP 0 EXIT THEN
    DUP VFSNAP-SPEC-SIZE _VFSNAP-DEPENDENCY-ALIASES? 0= NIP ;

: VFSNAP-SPEC-VALID?  ( spec -- flag )
    DUP _VFSNAP-SPEC-SPAN-SAFE? 0= IF DROP 0 EXIT THEN
    DUP VFSNAP-SPEC.MAGIC @ _VFSNAP-SPEC-MAGIC =
    OVER VFSNAP-SPEC.PAYLOAD-U @ DUP 0> SWAP VFSNAP-HEADER-SIZE +
        VFSNAP-HEADER-SIZE U> AND AND
    OVER VFSNAP-SPEC.ENCODE-XT @ 0<> AND
    OVER VFSNAP-SPEC.VALIDATE-XT @ 0<> AND
    OVER VFSNAP-SPEC.PRIVATE-N @ DUP 0>=
        SWAP VFSNAP-SPEC-PRIVATE-MAX <= AND AND
    SWAP DROP ;

: VFSNAP-SPEC-SEALED?  ( spec -- flag )
    DUP VFSNAP-SPEC-VALID? 0= IF DROP 0 EXIT THEN
    VFSNAP-SPEC.FLAGS @ _VFSNAP-SPEC-F-SEALED AND 0<> ;

: _VFSNAP-SPEC-PRIVATE-ALIASES?  ( address length spec -- flag )
    DUP VFSNAP-SPEC.PRIVATE-N @ 0 ?DO
        2 PICK 2 PICK I 3 PICK _VFSNAP-SPEC-PRIVATE-NTH
        DUP @ SWAP 8 + @ _VFSNAP-RANGES-OVERLAP? IF
            2DROP DROP -1 UNLOOP EXIT
        THEN
    LOOP
    2DROP DROP 0 ;

: _VFSNAP-ALL-PRIVATE-ALIASES?  ( address length spec -- flag )
    >R 2DUP _VFSNAP-DEPENDENCY-ALIASES? >R
    R> IF 2DROP R> DROP -1 EXIT THEN
    R> _VFSNAP-SPEC-PRIVATE-ALIASES? ;

: _VFSNAP-STORE-SPAN-SAFE?  ( store -- flag )
    DUP VFSNAP-STORE-SIZE _VFSNAP-SPAN-VALID? 0= IF DROP 0 EXIT THEN
    DUP VFSNAP-STORE-SIZE _VFSNAP-DEPENDENCY-ALIASES? 0= NIP ;

: VFSNAP-VALID?  ( store -- flag )
    DUP _VFSNAP-STORE-SPAN-SAFE? 0= IF DROP 0 EXIT THEN
    DUP VFSNAP.MAGIC @ _VFSNAP-STORE-MAGIC <> IF DROP 0 EXIT THEN
    DUP VFSNAP.VFS @ 0= IF DROP 0 EXIT THEN
    DUP VFSNAP.CLOSE-XT @ 0= IF DROP 0 EXIT THEN
    DUP VFSNAP.RESTORE-XT @ 0= IF DROP 0 EXIT THEN
    DUP VFSNAP.SPEC @ VFSNAP-SPEC-SEALED? 0= IF DROP 0 EXIT THEN
    DUP VFSNAP.REPLACE VREPL-CONFIGURED? 0= IF DROP 0 EXIT THEN
    DUP VFSNAP.VFS @ OVER VFSNAP.REPLACE VREPL.VFS @ <> IF
        DROP 0 EXIT
    THEN
    DUP VFSNAP.SCRATCH-A @ OVER VFSNAP.SCRATCH-U @
        _VFSNAP-SPAN-VALID? 0= IF DROP 0 EXIT THEN
    DUP VFSNAP.SCRATCH-U @ OVER VFSNAP.SPEC @
        VFSNAP-SPEC.PAYLOAD-U @ VFSNAP-HEADER-SIZE + <> IF
        DROP 0 EXIT
    THEN
    DUP VFSNAP.SCRATCH-A @ OVER VFSNAP.SCRATCH-U @
        2 PICK VFSNAP-STORE-SIZE _VFSNAP-RANGES-OVERLAP? IF
        DROP 0 EXIT
    THEN
    DUP VFSNAP.SCRATCH-A @ OVER VFSNAP.SCRATCH-U @
        2 PICK VFSNAP.SPEC @ _VFSNAP-ALL-PRIVATE-ALIASES? IF
        DROP 0 EXIT
    THEN
    DUP VFSNAP.SPEC @ VFSNAP-SPEC-SIZE
        2 PICK VFSNAP-STORE-SIZE _VFSNAP-RANGES-OVERLAP? IF
        DROP 0 EXIT
    THEN
    DUP VFSNAP.SPEC @ VFSNAP-SPEC-SIZE
        2 PICK VFSNAP.SCRATCH-A @ 3 PICK VFSNAP.SCRATCH-U @
        _VFSNAP-RANGES-OVERLAP? IF DROP 0 EXIT THEN
    DROP -1 ;

