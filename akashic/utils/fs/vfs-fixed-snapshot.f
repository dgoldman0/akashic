: VFSNAP-SPEC-INIT
  ( magic-a magic-u format payload-u encode-xt validate-xt spec -- status )
    DUP _VFSNAP-SPEC-SPAN-SAFE? 0= IF
        6 0 DO DROP LOOP VFSNAP-S-INVALID EXIT
    THEN
    6 PICK 6 PICK _VFSNAP-SPAN-VALID? 0= IF
        7 0 DO DROP LOOP VFSNAP-S-INVALID EXIT
    THEN
    5 PICK 8 <> IF 7 0 DO DROP LOOP VFSNAP-S-INVALID EXIT THEN
    6 PICK 8 2 PICK VFSNAP-SPEC-SIZE _VFSNAP-RANGES-OVERLAP? IF
        7 0 DO DROP LOOP VFSNAP-S-INVALID EXIT
    THEN
    3 PICK DUP 0> SWAP VFSNAP-HEADER-SIZE + VFSNAP-HEADER-SIZE U> AND 0= IF
        7 0 DO DROP LOOP VFSNAP-S-INVALID EXIT
    THEN
    2 PICK 0= 2 PICK 0= OR IF
        7 0 DO DROP LOOP VFSNAP-S-INVALID EXIT
    THEN
    _vfsnap-operation-active @ IF
        7 0 DO DROP LOOP VFSNAP-S-BUSY EXIT
    THEN
    _vfsnap-spec-init-xt _vfsnap-guard WITH-GUARD ;

