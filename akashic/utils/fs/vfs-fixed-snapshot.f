\ =====================================================================
\  vfs-fixed-snapshot.f - Fixed-capacity checked VFS snapshots
\ =====================================================================
\  The core owns a 64-byte envelope around one exact fixed-size payload.
\  Callers own payload encoding/validation, domain status translation, and
\  an exclusive record-sized scratch span for the complete store lifetime.
\
\  Callback ABI:
\    encode   ( context payload-a payload-u next-generation -- status )
\    validate ( payload-a payload-u envelope-generation -- status )
\
\  The payload passed to encode is exactly PAYLOAD-U zero bytes.  Neither
\  callback can address the envelope through this ABI.  Recursive entry is
\  rejected before callback-visible scratch is touched.  Distinct stores
\  own disjoint scratch and state; actual replacement/VFS work retains the
\  existing process-wide VREPL/VFS serialization boundary.
\ =====================================================================

