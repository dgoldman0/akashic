: _VFSNAP-IO-SPANS-SAFE?
  ( destination capacity generation-dst store -- flag )
    >R
    2 PICK 0= IF 2DROP DROP R> DROP 0 EXIT THEN
    2 PICK 2 PICK _VFSNAP-SPAN-VALID? 0= IF
        2DROP DROP R> DROP 0 EXIT
    THEN
    DUP 8 _VFSNAP-SPAN-VALID? 0= IF 2DROP DROP R> DROP 0 EXIT THEN
    R@ VFSNAP-VALID? 0= IF 2DROP DROP R> DROP 0 EXIT THEN
    2 PICK 2 PICK R@ VFSNAP-STORE-SIZE
        _VFSNAP-RANGES-OVERLAP? IF 2DROP DROP R> DROP 0 EXIT THEN
    2 PICK 2 PICK R@ VFSNAP-SCRATCH$
        _VFSNAP-RANGES-OVERLAP? IF 2DROP DROP R> DROP 0 EXIT THEN
    2 PICK 2 PICK R@ VFSNAP.SPEC @ VFSNAP-SPEC-SIZE
        _VFSNAP-RANGES-OVERLAP? IF 2DROP DROP R> DROP 0 EXIT THEN
    2 PICK 2 PICK R@ VFSNAP.SPEC @ _VFSNAP-ALL-PRIVATE-ALIASES? IF
        2DROP DROP R> DROP 0 EXIT
    THEN
    DUP 8 R@ VFSNAP-STORE-SIZE _VFSNAP-RANGES-OVERLAP? IF
        2DROP DROP R> DROP 0 EXIT
    THEN
    DUP 8 R@ VFSNAP-SCRATCH$ _VFSNAP-RANGES-OVERLAP? IF
        2DROP DROP R> DROP 0 EXIT
    THEN
    DUP 8 R@ VFSNAP.SPEC @ VFSNAP-SPEC-SIZE
        _VFSNAP-RANGES-OVERLAP? IF 2DROP DROP R> DROP 0 EXIT THEN
    DUP 8 R@ VFSNAP.SPEC @ _VFSNAP-ALL-PRIVATE-ALIASES? IF
        2DROP DROP R> DROP 0 EXIT
    THEN
    2 PICK 2 PICK 2 PICK 8 _VFSNAP-RANGES-OVERLAP? IF
        2DROP DROP R> DROP 0 EXIT
    THEN
    2DROP DROP R> DROP -1 ;

