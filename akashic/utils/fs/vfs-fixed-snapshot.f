    CASE
        VREPL-S-OK OF VFSNAP-S-OK ENDOF
        VREPL-S-ROLLED-BACK OF VFSNAP-S-OK ENDOF
        VREPL-S-COMMITTED-CLEANUP OF VFSNAP-S-OK ENDOF
        VREPL-S-INVALID OF VFSNAP-S-INVALID ENDOF
        VREPL-S-CONFLICT OF VFSNAP-S-CONFLICT ENDOF
