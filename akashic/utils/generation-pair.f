0x475043414E443031 CONSTANT _GPAIR-CANDIDATE-MAGIC  \ "GPCAND01"

 0 CONSTANT _GPC-MAGIC
 8 CONSTANT _GPC-CLASS
16 CONSTANT _GPC-GENERATION
24 CONSTANT _GPC-VALUE
