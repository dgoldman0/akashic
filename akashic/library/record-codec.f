CREATE _LIBRC-PRIVATE-BEGIN 0 ALLOT
VARIABLE _LIBRC-SOURCE
VARIABLE _LIBRC-DEST
VARIABLE _LIBRC-CAP
VARIABLE _LIBRC-REC
VARIABLE _LIBRC-U
VARIABLE _LIBRC-MAGIC
VARIABLE _LIBRC-RECORD-U
VARIABLE _LIBRC-PAYLOAD-U
VARIABLE _LIBRC-VALIDATE-XT
VARIABLE _LIBRC-CONTENT-U
CREATE _LIBRC-DIGEST LIB-DIGEST-SIZE ALLOT
CREATE _LIBRC-CONTENT LIB-CONTENT-SIZE ALLOT
CREATE _LIBRC-PRIVATE-END 0 ALLOT

: _LIBRC-ZERO?  ( address length -- flag )
    0 ?DO DUP I + C@ IF DROP 0 UNLOOP EXIT THEN LOOP DROP -1 ;

: _LIBRC-ALIGN8  ( u -- aligned-u ) 7 + -8 AND ;

: _LIBRC-FIXED-HEADER-CRC  ( record -- crc32 )
    CRC32-BEGIN
    DUP _LIBRF-H-HEADER-CRC CRC32-ADD
    DUP _LIBRF-H-HEADER-CRC + 8 +
        LIB-FIXED-HEADER-SIZE _LIBRF-H-HEADER-CRC - 8 - CRC32-ADD
    DROP CRC32-END ;

: _LIBRC-CONTENT-HEADER-CRC  ( record -- crc32 )
    CRC32-BEGIN
    DUP _LIBRT-H-HEADER-CRC CRC32-ADD
    DUP _LIBRT-H-HEADER-CRC + 8 +
        LIB-CONTENT-HEADER-SIZE _LIBRT-H-HEADER-CRC - 8 - CRC32-ADD
    DROP CRC32-END ;

: _LIBRC-FIXED-INPUT?  ( -- flag )
    _LIBRC-REC @ 0= IF 0 EXIT THEN
    _LIBRC-U @ LIB-FIXED-HEADER-SIZE < IF 0 EXIT THEN
    _LIBRC-REC @ _LIBRC-U @ MSPAN-NONWRAPPING? ;

: _LIBRC-CONTENT-INPUT?  ( -- flag )
    _LIBRC-REC @ 0= IF 0 EXIT THEN
    _LIBRC-U @ LIB-CONTENT-HEADER-SIZE < IF 0 EXIT THEN
    _LIBRC-REC @ _LIBRC-U @ MSPAN-NONWRAPPING? ;

: _LIBRC-CONTENT-MEASURE  ( header-a header-u -- record-u status )
    _LIBRC-U ! _LIBRC-REC !
    _LIBRC-CONTENT-INPUT? 0= IF 0 LIB-S-INVALID EXIT THEN
    _LIBRC-REC @ _LIBRT-H-MAGIC + 8 _LIBRC-CONTENT-MAGIC 8 COMPARE IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBRC-REC @ _LIBRC-CONTENT-HEADER-CRC
        _LIBRC-REC @ _LIBRT-H-HEADER-CRC + @ <> IF
        0 LIB-S-CHECKSUM EXIT
    THEN
    _LIBRC-REC @ _LIBRT-H-FORMAT + @ DUP LIB-RECORD-FORMAT-V1 <> IF
        LIB-RECORD-FORMAT-V1 > IF LIB-S-UNSUPPORTED ELSE LIB-S-INVALID THEN
        0 SWAP EXIT
    THEN DROP
    _LIBRC-REC @ _LIBRT-H-HEADER-U + @ LIB-CONTENT-HEADER-SIZE <>
    _LIBRC-REC @ _LIBRT-H-FLAGS + @ 0<> OR IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBRC-REC @ _LIBRT-H-CONTENT-U + @ LIB-CONTENT-RECORD-SIZE
        DUP -1 = IF DROP 0 LIB-S-INVALID EXIT THEN
    DUP _LIBRC-REC @ _LIBRT-H-RECORD-U + @ <> IF
        DROP 0 LIB-S-INVALID EXIT
    THEN
    LIB-S-OK ;

\ Header recognition and shape validation shared by the two fixed records.
: _LIBRC-FIXED-ENVELOPE  ( -- status )
    _LIBRC-FIXED-INPUT? 0= IF LIB-S-INVALID EXIT THEN
    _LIBRC-REC @ _LIBRF-H-MAGIC + 8 _LIBRC-MAGIC @ 8 COMPARE IF
        LIB-S-INVALID EXIT
    THEN
    _LIBRC-REC @ _LIBRC-FIXED-HEADER-CRC
        _LIBRC-REC @ _LIBRF-H-HEADER-CRC + @ <> IF
        LIB-S-CHECKSUM EXIT
    THEN
    _LIBRC-REC @ _LIBRF-H-FORMAT + @ DUP LIB-RECORD-FORMAT-V1 <> IF
        LIB-RECORD-FORMAT-V1 > IF LIB-S-UNSUPPORTED ELSE LIB-S-INVALID THEN
        EXIT
    THEN DROP
    _LIBRC-REC @ _LIBRF-H-HEADER-U + @ LIB-FIXED-HEADER-SIZE <>
    _LIBRC-REC @ _LIBRF-H-RECORD-U + @ _LIBRC-RECORD-U @ <> OR
    _LIBRC-REC @ _LIBRF-H-PAYLOAD-U + @ _LIBRC-PAYLOAD-U @ <> OR
    _LIBRC-REC @ _LIBRF-H-FLAGS + @ 0<> OR IF
        LIB-S-INVALID EXIT
    THEN
    _LIBRC-U @ _LIBRC-RECORD-U @ <> IF LIB-S-INVALID EXIT THEN
    _LIBRC-REC @ LIB-FIXED-HEADER-SIZE + _LIBRC-PAYLOAD-U @ CRC32
        _LIBRC-REC @ _LIBRF-H-PAYLOAD-CRC + @ <> IF
        LIB-S-CHECKSUM EXIT
    THEN
    _LIBRC-REC @ LIB-FIXED-HEADER-SIZE + _LIBRC-PAYLOAD-U @ +
        _LIBRC-RECORD-U @ LIB-FIXED-HEADER-SIZE - _LIBRC-PAYLOAD-U @ -
        _LIBRC-ZERO? 0= IF LIB-S-INVALID EXIT THEN
    _LIBRC-REC @ LIB-FIXED-HEADER-SIZE +
        _LIBRC-VALIDATE-XT @ EXECUTE 0= IF LIB-S-INVALID EXIT THEN
    LIB-S-OK ;

: _LIBRC-FIXED-VALIDATE  ( record length magic record-u payload-u validator -- status )
    _LIBRC-VALIDATE-XT ! _LIBRC-PAYLOAD-U ! _LIBRC-RECORD-U !
    _LIBRC-MAGIC ! _LIBRC-U ! _LIBRC-REC !
    _LIBRC-FIXED-ENVELOPE ;

: _LIBRC-FIXED-ENCODE  ( source dest cap magic record-u payload-u validator -- length status )
    _LIBRC-VALIDATE-XT ! _LIBRC-PAYLOAD-U ! _LIBRC-RECORD-U !
    _LIBRC-MAGIC ! _LIBRC-CAP ! _LIBRC-DEST ! _LIBRC-SOURCE !
    _LIBRC-SOURCE @ 0= IF 0 LIB-S-INVALID EXIT THEN
    _LIBRC-SOURCE @ _LIBRC-PAYLOAD-U @ MSPAN-NONWRAPPING? 0= IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBRC-SOURCE @ _LIBRC-VALIDATE-XT @ EXECUTE 0= IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBRC-DEST @ 0= _LIBRC-CAP @ 0< OR IF 0 LIB-S-INVALID EXIT THEN
    _LIBRC-CAP @ _LIBRC-RECORD-U @ < IF 0 LIB-S-CAPACITY EXIT THEN
    _LIBRC-DEST @ _LIBRC-RECORD-U @ MSPAN-NONWRAPPING? 0= IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBRC-SOURCE @ _LIBRC-PAYLOAD-U @
        _LIBRC-DEST @ _LIBRC-RECORD-U @ MSPAN-OVERLAP? IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBRC-DEST @ _LIBRC-RECORD-U @ 0 FILL
    _LIBRC-MAGIC @ _LIBRC-DEST @ _LIBRF-H-MAGIC + 8 CMOVE
    LIB-RECORD-FORMAT-V1 _LIBRC-DEST @ _LIBRF-H-FORMAT + !
    LIB-FIXED-HEADER-SIZE _LIBRC-DEST @ _LIBRF-H-HEADER-U + !
    _LIBRC-RECORD-U @ _LIBRC-DEST @ _LIBRF-H-RECORD-U + !
    _LIBRC-PAYLOAD-U @ _LIBRC-DEST @ _LIBRF-H-PAYLOAD-U + !
    _LIBRC-SOURCE @ _LIBRC-DEST @ LIB-FIXED-HEADER-SIZE +
        _LIBRC-PAYLOAD-U @ MOVE
    _LIBRC-DEST @ LIB-FIXED-HEADER-SIZE + _LIBRC-PAYLOAD-U @ CRC32
        _LIBRC-DEST @ _LIBRF-H-PAYLOAD-CRC + !
    _LIBRC-DEST @ _LIBRC-FIXED-HEADER-CRC
        _LIBRC-DEST @ _LIBRF-H-HEADER-CRC + !
    _LIBRC-RECORD-U @ LIB-S-OK ;

: _LIBRC-FIXED-DECODE  ( record length dest magic record-u payload-u validator -- status )
    _LIBRC-VALIDATE-XT ! _LIBRC-PAYLOAD-U ! _LIBRC-RECORD-U !
    _LIBRC-MAGIC ! _LIBRC-DEST ! _LIBRC-U ! _LIBRC-REC !
    _LIBRC-DEST @ 0= IF LIB-S-INVALID EXIT THEN
    _LIBRC-DEST @ _LIBRC-PAYLOAD-U @ MSPAN-NONWRAPPING? 0= IF
        LIB-S-INVALID EXIT
    THEN
    _LIBRC-FIXED-INPUT? 0= IF
        _LIBRC-DEST @ _LIBRC-PAYLOAD-U @ 0 FILL LIB-S-INVALID EXIT
    THEN
    _LIBRC-REC @ _LIBRC-U @ _LIBRC-DEST @ _LIBRC-PAYLOAD-U @
        MSPAN-OVERLAP? IF LIB-S-INVALID EXIT THEN
    _LIBRC-DEST @ _LIBRC-PAYLOAD-U @ 0 FILL
    _LIBRC-FIXED-ENVELOPE ?DUP IF EXIT THEN
    _LIBRC-REC @ LIB-FIXED-HEADER-SIZE + _LIBRC-DEST @
        _LIBRC-PAYLOAD-U @ MOVE
    LIB-S-OK ;

: _LIBRC-CATALOG-VALIDATE  ( record length -- status )
    _LIBRC-CATALOG-MAGIC LIB-CATALOG-RECORD-SIZE
        LIB-CATALOG-PAYLOAD-SIZE ['] LIB-ENTRY-VALID?
        _LIBRC-FIXED-VALIDATE ;

: _LIBRC-COLLECTION-VALIDATE  ( record length -- status )
    _LIBRC-COLLECTION-MAGIC LIB-COLLECTION-RECORD-SIZE
        LIB-COLLECTION-PAYLOAD-SIZE ['] LIB-COLLECTION-VALID?
        _LIBRC-FIXED-VALIDATE ;

: _LIBRC-CATALOG-ENCODE  ( entry destination capacity -- length status )
    _LIBRC-CATALOG-MAGIC LIB-CATALOG-RECORD-SIZE
        LIB-CATALOG-PAYLOAD-SIZE ['] LIB-ENTRY-VALID?
        _LIBRC-FIXED-ENCODE ;

: _LIBRC-COLLECTION-ENCODE  ( collection destination capacity -- length status )
    _LIBRC-COLLECTION-MAGIC LIB-COLLECTION-RECORD-SIZE
        LIB-COLLECTION-PAYLOAD-SIZE ['] LIB-COLLECTION-VALID?
        _LIBRC-FIXED-ENCODE ;

: _LIBRC-CATALOG-DECODE  ( record length destination -- status )
    _LIBRC-CATALOG-MAGIC LIB-CATALOG-RECORD-SIZE
        LIB-CATALOG-PAYLOAD-SIZE ['] LIB-ENTRY-VALID?
        _LIBRC-FIXED-DECODE ;

: _LIBRC-COLLECTION-DECODE  ( record length destination -- status )
    _LIBRC-COLLECTION-MAGIC LIB-COLLECTION-RECORD-SIZE
        LIB-COLLECTION-PAYLOAD-SIZE ['] LIB-COLLECTION-VALID?
        _LIBRC-FIXED-DECODE ;

\ ---------------------------------------------------------------------
\ Variable-size content-revision record
\ ---------------------------------------------------------------------

: _LIBRC-CONTENT-SOURCE?  ( -- flag )
    _LIBRC-SOURCE @ 0= IF 0 EXIT THEN
    _LIBRC-SOURCE @ LIB-CONTENT-SIZE MSPAN-NONWRAPPING? 0= IF 0 EXIT THEN
    _LIBRC-SOURCE @ LIBCT.DATA-U @ DUP 0< IF DROP 0 EXIT THEN
    DUP LIB-CONTENT-MAX > IF DROP 0 EXIT THEN
    DUP 0= IF DROP -1 EXIT THEN
    _LIBRC-SOURCE @ LIBCT.DATA-A @ DUP 0= IF DROP DROP 0 EXIT THEN
    SWAP MSPAN-NONWRAPPING? ;

: _LIBRC-CONTENT-ENVELOPE  ( -- status )
    _LIBRC-CONTENT-INPUT? 0= IF LIB-S-INVALID EXIT THEN
    _LIBRC-REC @ _LIBRT-H-MAGIC + 8 _LIBRC-CONTENT-MAGIC 8 COMPARE IF
        LIB-S-INVALID EXIT
    THEN
    _LIBRC-REC @ _LIBRC-CONTENT-HEADER-CRC
        _LIBRC-REC @ _LIBRT-H-HEADER-CRC + @ <> IF
        LIB-S-CHECKSUM EXIT
    THEN
    _LIBRC-REC @ _LIBRT-H-FORMAT + @ DUP LIB-RECORD-FORMAT-V1 <> IF
        LIB-RECORD-FORMAT-V1 > IF LIB-S-UNSUPPORTED ELSE LIB-S-INVALID THEN
        EXIT
    THEN DROP
    _LIBRC-REC @ _LIBRT-H-HEADER-U + @ LIB-CONTENT-HEADER-SIZE <>
    _LIBRC-REC @ _LIBRT-H-FLAGS + @ 0<> OR IF LIB-S-INVALID EXIT THEN
    _LIBRC-REC @ _LIBRT-H-CONTENT-U + @ DUP 0< IF
        DROP LIB-S-INVALID EXIT
    THEN
    DUP LIB-CONTENT-MAX > IF DROP LIB-S-INVALID EXIT THEN
    DUP _LIBRC-CONTENT-U ! _LIBRC-ALIGN8 LIB-CONTENT-HEADER-SIZE +
        DUP _LIBRC-RECORD-U !
    _LIBRC-REC @ _LIBRT-H-RECORD-U + @ <>
    _LIBRC-U @ _LIBRC-RECORD-U @ <> OR IF LIB-S-INVALID EXIT THEN
    _LIBRC-REC @ LIB-CONTENT-HEADER-SIZE + _LIBRC-CONTENT-U @ CRC32
        _LIBRC-REC @ _LIBRT-H-PAYLOAD-CRC + @ <> IF
        LIB-S-CHECKSUM EXIT
    THEN
    _LIBRC-REC @ LIB-CONTENT-HEADER-SIZE + _LIBRC-CONTENT-U @
        _LIBRC-DIGEST SHA3-256-HASH
    _LIBRC-DIGEST _LIBRC-REC @ _LIBRT-H-DIGEST + SHA3-256-COMPARE 0= IF
        LIB-S-INTEGRITY EXIT
    THEN
    _LIBRC-REC @ LIB-CONTENT-HEADER-SIZE + _LIBRC-CONTENT-U @ +
        _LIBRC-RECORD-U @ LIB-CONTENT-HEADER-SIZE - _LIBRC-CONTENT-U @ -
        _LIBRC-ZERO? 0= IF LIB-S-INVALID EXIT THEN
    _LIBRC-CONTENT LIB-CONTENT-INIT
    _LIBRC-REC @ _LIBRT-H-ID + _LIBRC-CONTENT LIBCT.ID RID-COPY
    _LIBRC-REC @ _LIBRT-H-DOMAIN-REVISION + @
        _LIBRC-CONTENT LIBCT.DOMAIN-REVISION !
    _LIBRC-REC @ _LIBRT-H-CONTENT-REVISION + @
        _LIBRC-CONTENT LIBCT.CONTENT-REVISION !
    _LIBRC-REC @ _LIBRT-H-KIND + @ _LIBRC-CONTENT LIBCT.KIND !
    _LIBRC-REC @ _LIBRT-H-MEDIA + @ _LIBRC-CONTENT LIBCT.MEDIA !
    _LIBRC-REC @ LIB-CONTENT-HEADER-SIZE + _LIBRC-CONTENT LIBCT.DATA-A !
    _LIBRC-CONTENT-U @ _LIBRC-CONTENT LIBCT.DATA-U !
    _LIBRC-REC @ _LIBRT-H-DIGEST + _LIBRC-CONTENT LIBCT.DIGEST
        LIB-DIGEST-SIZE CMOVE
    _LIBRC-CONTENT LIB-CONTENT-VALID? 0= IF LIB-S-INVALID EXIT THEN
    LIB-S-OK ;

: _LIBRC-CONTENT-VALIDATE  ( record length -- status )
    _LIBRC-U ! _LIBRC-REC ! _LIBRC-CONTENT-ENVELOPE ;

: _LIBRC-CONTENT-ENCODE  ( content destination capacity -- length status )
    _LIBRC-CAP ! _LIBRC-DEST ! _LIBRC-SOURCE !
    _LIBRC-CONTENT-SOURCE? 0= IF 0 LIB-S-INVALID EXIT THEN
    _LIBRC-SOURCE @ LIB-CONTENT-VALID? 0= IF 0 LIB-S-INVALID EXIT THEN
    _LIBRC-SOURCE @ LIBCT.DATA-U @ DUP _LIBRC-CONTENT-U !
        _LIBRC-ALIGN8 LIB-CONTENT-HEADER-SIZE + _LIBRC-RECORD-U !
    _LIBRC-DEST @ 0= _LIBRC-CAP @ 0< OR IF 0 LIB-S-INVALID EXIT THEN
    _LIBRC-CAP @ _LIBRC-RECORD-U @ < IF 0 LIB-S-CAPACITY EXIT THEN
    _LIBRC-DEST @ _LIBRC-RECORD-U @ MSPAN-NONWRAPPING? 0= IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBRC-SOURCE @ LIB-CONTENT-SIZE
        _LIBRC-DEST @ _LIBRC-RECORD-U @ MSPAN-OVERLAP? IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBRC-SOURCE @ LIBCT-DATA$
        _LIBRC-DEST @ _LIBRC-RECORD-U @ MSPAN-OVERLAP? IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBRC-DEST @ _LIBRC-RECORD-U @ 0 FILL
    _LIBRC-CONTENT-MAGIC _LIBRC-DEST @ _LIBRT-H-MAGIC + 8 CMOVE
    LIB-RECORD-FORMAT-V1 _LIBRC-DEST @ _LIBRT-H-FORMAT + !
    LIB-CONTENT-HEADER-SIZE _LIBRC-DEST @ _LIBRT-H-HEADER-U + !
    _LIBRC-RECORD-U @ _LIBRC-DEST @ _LIBRT-H-RECORD-U + !
    _LIBRC-SOURCE @ LIBCT.ID _LIBRC-DEST @ _LIBRT-H-ID + RID-COPY
    _LIBRC-SOURCE @ LIBCT.DOMAIN-REVISION @
        _LIBRC-DEST @ _LIBRT-H-DOMAIN-REVISION + !
    _LIBRC-SOURCE @ LIBCT.CONTENT-REVISION @
        _LIBRC-DEST @ _LIBRT-H-CONTENT-REVISION + !
    _LIBRC-SOURCE @ LIBCT.KIND @ _LIBRC-DEST @ _LIBRT-H-KIND + !
    _LIBRC-SOURCE @ LIBCT.MEDIA @ _LIBRC-DEST @ _LIBRT-H-MEDIA + !
    _LIBRC-CONTENT-U @ _LIBRC-DEST @ _LIBRT-H-CONTENT-U + !
    _LIBRC-SOURCE @ LIBCT.DIGEST _LIBRC-DEST @ _LIBRT-H-DIGEST +
        LIB-DIGEST-SIZE CMOVE
    _LIBRC-SOURCE @ LIBCT-DATA$ _LIBRC-DEST @ LIB-CONTENT-HEADER-SIZE +
        SWAP MOVE
    _LIBRC-DEST @ LIB-CONTENT-HEADER-SIZE + _LIBRC-CONTENT-U @ CRC32
        _LIBRC-DEST @ _LIBRT-H-PAYLOAD-CRC + !
    _LIBRC-DEST @ _LIBRC-CONTENT-HEADER-CRC
        _LIBRC-DEST @ _LIBRT-H-HEADER-CRC + !
    _LIBRC-RECORD-U @ LIB-S-OK ;

: _LIBRC-CONTENT-DECODE  ( record length destination -- status )
    _LIBRC-DEST ! _LIBRC-U ! _LIBRC-REC !
    _LIBRC-DEST @ 0= IF LIB-S-INVALID EXIT THEN
    _LIBRC-DEST @ LIB-CONTENT-SIZE MSPAN-NONWRAPPING? 0= IF
        LIB-S-INVALID EXIT
    THEN
    _LIBRC-CONTENT-INPUT? 0= IF
        _LIBRC-DEST @ LIB-CONTENT-INIT LIB-S-INVALID EXIT
    THEN
    _LIBRC-REC @ _LIBRC-U @ _LIBRC-DEST @ LIB-CONTENT-SIZE
        MSPAN-OVERLAP? IF LIB-S-INVALID EXIT THEN
    _LIBRC-DEST @ LIB-CONTENT-INIT
    _LIBRC-CONTENT-ENVELOPE ?DUP IF EXIT THEN
    _LIBRC-CONTENT _LIBRC-DEST @ LIB-CONTENT-SIZE MOVE
    LIB-S-OK ;

