\ =====================================================================
\  record-codec.f - Deterministic Library record envelopes
\ =====================================================================
\  Pure, caller-buffer codecs for catalog entries, collections, and one
\  immutable content revision.  These words select no VFS path, generation
\  scheme, commit order, recovery policy, index, owner, or projection.
\
\  V1 is native little-endian MegaPad-64.  Fixed records carry canonical
\  zero padding.  Content records are eight-byte aligned and return a
\  borrowed payload view on decode.  Header CRC is checked before format
\  dispatch; a checksummed format newer than V1 is reported unsupported.
\ =====================================================================

