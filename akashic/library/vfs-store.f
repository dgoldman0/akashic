    _LIBVP-HAVE-OLD-VFS @ IF
        \ The selector restore is likewise exact-once under THROW.
        0 _LIBVP-HAVE-OLD-VFS !
        ['] _LIBVP-RESTORE-VFS-CALL CATCH IF
            -1 _LIBVP-CLEANUP-EVIDENCE !
            -1 _LIBVP-CLEAN-FAILED !
        THEN
    THEN
    _LIBVP-CLEAN-FAILED @ ;

