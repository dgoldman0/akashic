    _LIBVP-VFS @ _LIBVFS-RESOLVE ?DUP 0= IF
        LIBSTORE-S-OK EXIT
    THEN
    IN.TYPE @ VFS-T-FILE <> IF LIBSTORE-S-RECOVERY EXIT THEN
    1 _LIBVP-PRESENT-N +!
    LIBSTORE-S-OK ;

