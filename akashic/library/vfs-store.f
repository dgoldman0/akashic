    THEN
    _LIBVFS-BANK-A-PATH$ _LIBVP-NOTE-TARGET DUP IF EXIT THEN DROP
    _LIBVFS-BANK-B-PATH$ _LIBVP-NOTE-TARGET DUP IF EXIT THEN DROP
