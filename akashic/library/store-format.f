\ Round one exact content record up to its sector-padded arena frame.
: LIB-CONTENT-FRAME-SIZE  ( record-u -- frame-u|-1 )
    DUP LIB-CONTENT-HEADER-SIZE < IF DROP -1 EXIT THEN
    DUP LIB-CONTENT-RECORD-MAX > IF DROP -1 EXIT THEN
    DUP 7 AND IF DROP -1 EXIT THEN
    LIB-STORE-SECTOR-SIZE 1- +
    LIB-STORE-SECTOR-SIZE 1- INVERT AND ;

