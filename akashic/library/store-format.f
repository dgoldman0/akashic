: _LIBSF-ARENA-ENVELOPE  ( -- status )
    _LIBSF-SOURCE @ LIB-ARENA-HEADER-SIZE _LIBSF-PUBLIC-SPAN? 0= IF
        LIB-S-INVALID EXIT
    THEN
    _LIBSF-U @ LIB-ARENA-HEADER-SIZE <> IF LIB-S-INVALID EXIT THEN
    _LIBSF-EXPECTED @ LIB-DIGEST-SIZE _LIBSF-PUBLIC-SPAN? 0= IF
        LIB-S-INVALID EXIT
    THEN
    _LIBSF-EXPECTED @ LIB-DIGEST-SIZE _LIBSF-NONZERO? 0= IF
        LIB-S-INVALID EXIT
    THEN
    _LIBSF-SOURCE @ LIB-ARENA-HEADER-SIZE
    _LIBSF-EXPECTED @ LIB-DIGEST-SIZE MSPAN-OVERLAP? IF
        LIB-S-INVALID EXIT
    THEN
    _LIBSF-SOURCE @ _LIBSA-MAGIC + 8 _LIBSF-ARENA-MAGIC 8 COMPARE IF
        LIB-S-INVALID EXIT
    THEN
    _LIBSF-SOURCE @ _LIBSF-ARENA-CRC
    _LIBSF-SOURCE @ _LIBSA-HEADER-CRC + @ <> IF
        LIB-S-CHECKSUM EXIT
    THEN
    _LIBSF-SOURCE @ _LIBSA-FORMAT + @ DUP LIB-STORE-FORMAT-V1 <> IF
        LIB-STORE-FORMAT-V1 > IF LIB-S-UNSUPPORTED ELSE LIB-S-INVALID THEN
        EXIT
    THEN DROP
    _LIBSF-SOURCE @ _LIBSA-HEADER-U + @ LIB-ARENA-HEADER-SIZE <>
    _LIBSF-SOURCE @ _LIBSA-ARENA-U + @ LIB-ARENA-SIZE <> OR
    _LIBSF-SOURCE @ _LIBSA-SECTOR-U + @ LIB-STORE-SECTOR-SIZE <> OR
    _LIBSF-SOURCE @ _LIBSA-DATA-OFFSET + @ LIB-ARENA-HEADER-SIZE <> OR
    _LIBSF-SOURCE @ _LIBSA-DATA-U + @ LIB-ARENA-DATA-SIZE <> OR
    _LIBSF-SOURCE @ _LIBSA-RECORD-FORMAT + @ LIB-RECORD-FORMAT-V1 <> OR
    _LIBSF-SOURCE @ _LIBSA-RECORD-HEADER-U + @ LIB-CONTENT-HEADER-SIZE <> OR
    _LIBSF-SOURCE @ _LIBSA-RECORD-MAX + @ LIB-CONTENT-RECORD-MAX <> OR
    _LIBSF-SOURCE @ _LIBSA-FRAME-MAX + @ LIB-CONTENT-FRAME-MAX <> OR
    _LIBSF-SOURCE @ _LIBSA-CHAIN-ALGORITHM + @
        LIB-CONTENT-CHAIN-ALGORITHM-V1 <> OR
    _LIBSF-SOURCE @ _LIBSA-FLAGS + @ 0<> OR IF
        LIB-S-INVALID EXIT
    THEN
    _LIBSF-SOURCE @ _LIBSA-RESERVED +
        LIB-ARENA-HEADER-SIZE _LIBSA-RESERVED - _LIBSF-ZERO? 0= IF
        LIB-S-INVALID EXIT
    THEN
    _LIBSF-SOURCE @ _LIBSA-ARENA-ID + LIB-DIGEST-SIZE
        _LIBSF-NONZERO? 0= IF LIB-S-INVALID EXIT THEN
    _LIBSF-SOURCE @ _LIBSA-ARENA-ID + _LIBSF-EXPECTED @
        SHA3-256-COMPARE 0= IF LIB-S-INTEGRITY EXIT THEN
    LIB-S-OK ;

: _LIBSF-ARENA-ENCODE  ( arena-fact destination capacity -- length status )
    _LIBSF-CAP ! _LIBSF-DEST ! _LIBSF-FACT !
    _LIBSF-FACT @ _LIBSF-ARENA-FACT? 0= IF 0 LIB-S-INVALID EXIT THEN
    _LIBSF-CAP @ 0< IF 0 LIB-S-INVALID EXIT THEN
    _LIBSF-CAP @ LIB-ARENA-HEADER-SIZE < IF 0 LIB-S-CAPACITY EXIT THEN
    _LIBSF-DEST @ LIB-ARENA-HEADER-SIZE _LIBSF-PUBLIC-SPAN? 0= IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBSF-FACT @ LIB-ARENA-FACT-SIZE
    _LIBSF-DEST @ LIB-ARENA-HEADER-SIZE MSPAN-OVERLAP? IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBSF-DEST @ LIB-ARENA-HEADER-SIZE 0 FILL
    _LIBSF-ARENA-MAGIC _LIBSF-DEST @ _LIBSA-MAGIC + 8 CMOVE
    LIB-STORE-FORMAT-V1 _LIBSF-DEST @ _LIBSA-FORMAT + !
    LIB-ARENA-HEADER-SIZE _LIBSF-DEST @ _LIBSA-HEADER-U + !
    LIB-ARENA-SIZE _LIBSF-DEST @ _LIBSA-ARENA-U + !
    LIB-STORE-SECTOR-SIZE _LIBSF-DEST @ _LIBSA-SECTOR-U + !
    LIB-ARENA-HEADER-SIZE _LIBSF-DEST @ _LIBSA-DATA-OFFSET + !
    LIB-ARENA-DATA-SIZE _LIBSF-DEST @ _LIBSA-DATA-U + !
    LIB-RECORD-FORMAT-V1 _LIBSF-DEST @ _LIBSA-RECORD-FORMAT + !
    LIB-CONTENT-HEADER-SIZE _LIBSF-DEST @ _LIBSA-RECORD-HEADER-U + !
    LIB-CONTENT-RECORD-MAX _LIBSF-DEST @ _LIBSA-RECORD-MAX + !
    LIB-CONTENT-FRAME-MAX _LIBSF-DEST @ _LIBSA-FRAME-MAX + !
    LIB-CONTENT-CHAIN-ALGORITHM-V1
        _LIBSF-DEST @ _LIBSA-CHAIN-ALGORITHM + !
    _LIBSF-FACT @ LIBAF.ARENA-ID
        _LIBSF-DEST @ _LIBSA-ARENA-ID + LIB-DIGEST-SIZE CMOVE
    _LIBSF-DEST @ _LIBSF-ARENA-CRC
        _LIBSF-DEST @ _LIBSA-HEADER-CRC + !
    LIB-ARENA-HEADER-SIZE LIB-S-OK ;

: _LIBSF-ARENA-VALIDATE  ( header length expected-arena-id -- status )
    _LIBSF-EXPECTED ! _LIBSF-U ! _LIBSF-SOURCE !
    _LIBSF-ARENA-ENVELOPE ;

: _LIBSF-ARENA-DECODE  ( header length expected-arena-id fact -- status )
    _LIBSF-FACT ! _LIBSF-EXPECTED ! _LIBSF-U ! _LIBSF-SOURCE !
    _LIBSF-FACT @ LIB-ARENA-FACT-SIZE _LIBSF-PUBLIC-SPAN? 0= IF
        LIB-S-INVALID EXIT
    THEN
    _LIBSF-FACT @ LIB-ARENA-FACT-SIZE
    _LIBSF-SOURCE @ _LIBSF-U @ MSPAN-OVERLAP? IF LIB-S-INVALID EXIT THEN
    _LIBSF-FACT @ LIB-ARENA-FACT-SIZE
    _LIBSF-EXPECTED @ LIB-DIGEST-SIZE MSPAN-OVERLAP? IF
        LIB-S-INVALID EXIT
    THEN
    _LIBSF-ARENA-ENVELOPE ?DUP IF EXIT THEN
    _LIBSF-FACT @ LIB-ARENA-FACT-SIZE 0 FILL
    _LIBSF-SOURCE @ _LIBSA-ARENA-ID + _LIBSF-FACT @ LIBAF.ARENA-ID
        LIB-DIGEST-SIZE CMOVE
    LIB-S-OK ;

