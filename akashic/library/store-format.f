: _LIBSF-ZERO?  ( a u -- flag )
    0 ?DO DUP I + C@ IF DROP 0 UNLOOP EXIT THEN LOOP DROP -1 ;

: _LIBSF-NONZERO?  ( a u -- flag ) _LIBSF-ZERO? 0= ;

: _LIBSF-PUBLIC-SPAN?  ( a u -- flag )
    OVER 0= IF 2DROP 0 EXIT THEN
    2DUP MSPAN-NONWRAPPING? 0= IF 2DROP 0 EXIT THEN
    _LIBSF-PRIVATE-BEGIN
    _LIBSF-PRIVATE-END _LIBSF-PRIVATE-BEGIN - MSPAN-OVERLAP? 0= ;

\ Initializers preserve the conventional no-result API.  A hostile or
\ private alias is rejected by leaving it untouched.
: LIB-ARENA-FACT-INIT  ( arena-fact -- )
    DUP LIB-ARENA-FACT-SIZE _LIBSF-PUBLIC-SPAN? IF
        LIB-ARENA-FACT-SIZE 0 FILL
    ELSE
        DROP
    THEN ;

: LIB-BANK-FACT-INIT  ( bank-fact -- )
    DUP LIB-BANK-FACT-SIZE _LIBSF-PUBLIC-SPAN? IF
        LIB-BANK-FACT-SIZE 0 FILL
    ELSE
        DROP
    THEN ;

: LIB-HEAD-FACT-INIT  ( head-fact -- )
    DUP LIB-HEAD-FACT-SIZE _LIBSF-PUBLIC-SPAN? IF
        LIB-HEAD-FACT-SIZE 0 FILL
    ELSE
        DROP
    THEN ;

: _LIBSF-CRC-EXCLUDING  ( a total-u crc-offset -- crc )
    _LIBSF-CRC-OFFSET ! _LIBSF-TOTAL ! _LIBSF-SOURCE !
    CRC32-BEGIN
    _LIBSF-SOURCE @ _LIBSF-CRC-OFFSET @ CRC32-ADD
    _LIBSF-SOURCE @ _LIBSF-CRC-OFFSET @ + 8 +
    _LIBSF-TOTAL @ _LIBSF-CRC-OFFSET @ - 8 - CRC32-ADD
    CRC32-END ;

