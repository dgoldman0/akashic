: _LIBSF-BANK-ENVELOPE  ( -- status )
    _LIBSF-SOURCE @ LIB-BANK-HEADER-SIZE _LIBSF-PUBLIC-SPAN? 0= IF
        LIB-S-INVALID EXIT
    THEN
    _LIBSF-U @ LIB-BANK-HEADER-SIZE <> IF LIB-S-INVALID EXIT THEN
    _LIBSF-SOURCE @ _LIBSB-MAGIC + 8 _LIBSF-BANK-MAGIC 8 COMPARE IF
        LIB-S-INVALID EXIT
    THEN
    _LIBSF-SOURCE @ _LIBSF-BANK-CRC
    _LIBSF-SOURCE @ _LIBSB-HEADER-CRC + @ <> IF
        LIB-S-CHECKSUM EXIT
    THEN
    _LIBSF-SOURCE @ _LIBSB-FORMAT + @ DUP LIB-STORE-FORMAT-V1 <> IF
        LIB-STORE-FORMAT-V1 > IF LIB-S-UNSUPPORTED ELSE LIB-S-INVALID THEN
        EXIT
    THEN DROP
    _LIBSF-SOURCE @ _LIBSB-HEADER-U + @ LIB-BANK-HEADER-SIZE <>
    _LIBSF-SOURCE @ _LIBSB-BANK-U + @ LIB-BANK-SIZE <> OR
    _LIBSF-SOURCE @ _LIBSB-CATALOG-OFFSET + @ LIB-BANK-CATALOG-OFFSET <> OR
    _LIBSF-SOURCE @ _LIBSB-CATALOG-RECORD-U + @
        LIB-CATALOG-RECORD-SIZE <> OR
    _LIBSF-SOURCE @ _LIBSB-CATALOG-CAPACITY + @ LIB-CATALOG-MAX <> OR
    _LIBSF-SOURCE @ _LIBSB-COLLECTION-OFFSET + @
        LIB-BANK-COLLECTION-OFFSET <> OR
    _LIBSF-SOURCE @ _LIBSB-COLLECTION-RECORD-U + @
        LIB-COLLECTION-RECORD-SIZE <> OR
    _LIBSF-SOURCE @ _LIBSB-COLLECTION-CAPACITY + @
        LIB-COLLECTION-MAX <> OR
    _LIBSF-SOURCE @ _LIBSB-FLAGS + @ 0<> OR IF
