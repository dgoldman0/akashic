    LIB-HEAD-PAYLOAD-SIZE _LIBSH-PAYLOAD-CRC _LIBSF-CRC-EXCLUDING ;

: _LIBSF-GENESIS-INTO  ( digest -- )
    >R SHA3-256-BEGIN
    S" org.akashic.library.content-chain.genesis.v1" SHA3-256-ADD
    R> SHA3-256-END ;

: _LIBSF-CONTENT-FACTS?  ( arena-id tail count chain -- flag )
    >R >R _LIBSF-SPAN ! _LIBSF-OFFSET !
    _LIBSF-OFFSET @ LIB-DIGEST-SIZE _LIBSF-NONZERO? 0= IF
        R> DROP R> DROP 0 EXIT
    THEN
    _LIBSF-SPAN @ LIB-ARENA-HEADER-SIZE < IF
        R> DROP R> DROP 0 EXIT
    THEN
    _LIBSF-SPAN @ LIB-ARENA-SIZE > IF
        R> DROP R> DROP 0 EXIT
    THEN
    _LIBSF-SPAN @ LIB-STORE-SECTOR-SIZE 1- AND IF
        R> DROP R> DROP 0 EXIT
    THEN
    R> DUP 0< IF DROP R> DROP 0 EXIT THEN
    DUP LIB-ARENA-DATA-SIZE LIB-STORE-SECTOR-SIZE / > IF
        DROP R> DROP 0 EXIT
    THEN
    DUP _LIBSF-SPAN @ LIB-ARENA-HEADER-SIZE -
        LIB-STORE-SECTOR-SIZE / > IF DROP R> DROP 0 EXIT THEN
    DUP 0= IF
        DROP _LIBSF-SPAN @ LIB-ARENA-HEADER-SIZE <> IF R> DROP 0 EXIT THEN
        _LIBSF-DIGEST _LIBSF-GENESIS-INTO
        R> _LIBSF-DIGEST SHA3-256-COMPARE
    ELSE
        DROP _LIBSF-SPAN @ LIB-ARENA-HEADER-SIZE <= IF R> DROP 0 EXIT THEN
        R> LIB-DIGEST-SIZE _LIBSF-NONZERO?
    THEN ;

: _LIBSF-ARENA-FACT?  ( fact -- flag )
    DUP LIB-ARENA-FACT-SIZE _LIBSF-PUBLIC-SPAN? 0= IF DROP 0 EXIT THEN
    LIBAF.ARENA-ID LIB-DIGEST-SIZE _LIBSF-NONZERO? ;

: _LIBSF-BANK-FACT?  ( fact -- flag )
    DUP LIB-BANK-FACT-SIZE _LIBSF-PUBLIC-SPAN? 0= IF DROP 0 EXIT THEN
    DUP LIBBF.GENERATION @ 1 < IF DROP 0 EXIT THEN
    DUP LIBBF.CATALOG-COUNT @ DUP 0< SWAP LIB-CATALOG-MAX > OR IF
        DROP 0 EXIT
    THEN
    DUP LIBBF.COLLECTION-COUNT @ DUP 0< SWAP LIB-COLLECTION-MAX > OR IF
        DROP 0 EXIT
    THEN
    DUP LIBBF.MUTATION-SEQUENCE @ 0< IF DROP 0 EXIT THEN
    DUP LIBBF.CATALOG-COUNT @ 0=
    OVER LIBBF.COLLECTION-COUNT @ 0= AND IF
        DUP LIBBF.MUTATION-SEQUENCE @ IF DROP 0 EXIT THEN
    ELSE
        DUP LIBBF.MUTATION-SEQUENCE @ 0= IF DROP 0 EXIT THEN
    THEN
    DUP LIBBF.ARENA-ID
    OVER LIBBF.CONTENT-TAIL @
    2 PICK LIBBF.CONTENT-RECORD-COUNT @
    3 PICK LIBBF.CONTENT-CHAIN _LIBSF-CONTENT-FACTS? 0= IF DROP 0 EXIT THEN
    DUP LIBBF.BODY-CRC @ DUP 0xFFFFFFFF AND <> IF DROP 0 EXIT THEN
    LIBBF.BODY-SHA LIB-DIGEST-SIZE _LIBSF-NONZERO? ;

: _LIBSF-HEAD-FACT?  ( fact -- flag )
    DUP LIB-HEAD-FACT-SIZE _LIBSF-PUBLIC-SPAN? 0= IF DROP 0 EXIT THEN
    DUP LIBHF.GENERATION @ 1 < IF DROP 0 EXIT THEN
    DUP LIBHF.BANK-GENERATION @ OVER LIBHF.GENERATION @ <> IF
        DROP 0 EXIT
    THEN
    DUP LIBHF.BANK-SELECTOR @ DUP 0< SWAP 1 > OR IF DROP 0 EXIT THEN
    DUP LIBHF.CATALOG-COUNT @ DUP 0< SWAP LIB-CATALOG-MAX > OR IF
        DROP 0 EXIT
    THEN
    DUP LIBHF.COLLECTION-COUNT @ DUP 0< SWAP LIB-COLLECTION-MAX > OR IF
        DROP 0 EXIT
    THEN
    DUP LIBHF.MUTATION-SEQUENCE @ 0< IF DROP 0 EXIT THEN
    DUP LIBHF.CATALOG-COUNT @ 0=
    OVER LIBHF.COLLECTION-COUNT @ 0= AND IF
        DUP LIBHF.MUTATION-SEQUENCE @ IF DROP 0 EXIT THEN
    ELSE
        DUP LIBHF.MUTATION-SEQUENCE @ 0= IF DROP 0 EXIT THEN
    THEN
    DUP LIBHF.BANK-SHA LIB-DIGEST-SIZE _LIBSF-NONZERO? 0= IF
        DROP 0 EXIT
    THEN
    DUP LIBHF.ARENA-ID
    OVER LIBHF.CONTENT-TAIL @
    2 PICK LIBHF.CONTENT-RECORD-COUNT @
    3 PICK LIBHF.CONTENT-CHAIN _LIBSF-CONTENT-FACTS? SWAP DROP ;

