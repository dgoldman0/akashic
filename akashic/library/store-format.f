\ =====================================================================
\  store-format.f - Deterministic bounded Library store formats
\ =====================================================================
\  VFS-free codecs for the immutable content-arena header, one complete
\  catalog-bank header, and the VFSNAP head payload.  The VFS owner alone
\  chooses paths and commit order; this module only seals bytes and facts.
\
\  V1 is native little-endian MegaPad-64.  Every serialized tail and flag
\  is canonical zero.  Header CRC is checked before format dispatch, so a
\  checksummed future format is distinguishable from corrupt evidence.
\ =====================================================================

