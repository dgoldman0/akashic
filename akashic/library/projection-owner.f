: _LPR-SLOT-FIND-RAW  ( rid root -- index|-1 )
    _LPR-ROOT ! _LPR-A !
    _LPR-A @ RID-PRESENT? 0= IF -1 EXIT THEN
    LIBRARY-PROJECTION-OWNER-MAX 0 ?DO
        I _LPR-ROOT @ _LPR-SLOT DUP LPS.ACTIVE @ IF
            LPS.RID _LPR-A @ RID= IF I UNLOOP EXIT THEN
        ELSE
            DROP
        THEN
    LOOP
    -1 ;

: _LPR-SLOT-FREE-RAW  ( root -- index|-1 )
    LIBRARY-PROJECTION-OWNER-MAX 0 ?DO
        I OVER _LPR-SLOT LPS.ACTIVE @ 0= IF DROP I UNLOOP EXIT THEN
    LOOP
    DROP -1 ;

: _LPR-LEASE-FREE-RAW  ( root -- index|-1 )
    LIBRARY-PROJECTION-LEASE-MAX 0 ?DO
        I OVER _LPR-LEASE LPL.TOKEN @ 0= IF DROP I UNLOOP EXIT THEN
    LOOP
    DROP -1 ;

