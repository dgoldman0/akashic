        _LPH-STATE !
    _LPH-STATE @ LPST.MAGIC @ _LPR-STATE-MAGIC <>
        IF 0 0 EXIT THEN
    _LPH-STATE @ LPST.FLAGS @ IF 0 0 EXIT THEN
    _LPH-STATE @ LPST.ROOT @ DUP _LPR-ROOT-HEADER? 0= IF
        DROP 0 0 EXIT
    THEN _LPR-ROOT !
    _LPH-STATE @ LPST.SLOT-I @ DUP _LPR-SLOT-INDEX-VALID? 0= IF
        DROP 0 0 EXIT
    THEN _LPR-I !
    _LPR-I @ _LPR-ROOT @ _LPR-SLOT DUP _LPR-SLOT-PTR !
    LPS.ACTIVE @ 0= IF 0 0 EXIT THEN
    _LPR-SLOT-PTR @ LPS.INSTANCE @ _LPR-INSTANCE @ <> IF 0 0 EXIT THEN
    _LPR-SLOT-PTR @ LPS.GENERATION @
        _LPH-STATE @ LPST.GENERATION @ <> IF 0 0 EXIT THEN
    _LPR-SLOT-PTR @ LPS.KIND @
        _LPH-STATE @ LPST.KIND @ <> IF 0 0 EXIT THEN
    _LPH-STATE @ -1 ;

