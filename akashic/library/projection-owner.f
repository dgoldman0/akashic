: _LPR-ROOT-HEADER?  ( root -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP LIBRARY-PROJECTION-ROOT-SIZE MSPAN-NONWRAPPING? 0= IF
        DROP 0 EXIT
    THEN
    DUP LPR.MAGIC @ LIBRARY-PROJECTION-ROOT-MAGIC =
    OVER LPR.ABI @ LIBRARY-PROJECTION-ROOT-ABI-VERSION = AND
    OVER LPR.SIZE @ LIBRARY-PROJECTION-ROOT-SIZE = AND
    OVER LPR.FLAGS @ 0= AND
    OVER RACQ.ROOT-MAGIC @ RACQ-ROOT-MAGIC = AND
    OVER RACQ.ROOT-ABI @ RACQ-ROOT-ABI-VERSION = AND
    OVER RACQ.ROOT-SIZE @ RACQ-ROOT-SIZE = AND
    OVER RACQ.ROOT-OWNER-A @ 0<> AND
    OVER RACQ.ROOT-OWNER-U @ DUP 0> SWAP RACQ-OWNER-MAX <= AND AND
    OVER RACQ.ROOT-CONTEXT @ 0<> AND
    OVER RACQ.ROOT-ACQUIRE-XT @ 0<> AND
    OVER RACQ.ROOT-QUIESCENT-XT @ 0<> AND
    OVER RACQ.ROOT-RELEASE-XT @ 0<> AND
    OVER RACQ.ROOT-FLAGS @ 0= AND
    SWAP _RAR-RESERVED + @ 0= AND ;

