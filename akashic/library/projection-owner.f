: _LPR-INIT-REACHABLE-SAFE?  ( -- flag )
    _LPI-CONTEXT @ CTX.PRACTICE @ ?DUP IF
        PHEAD-SIZE _LPI-ROOT-SPAN-UNSAFE? IF 0 EXIT THEN
    THEN
    _LPI-CREG @ CREG.TYPE-N @ 0 ?DO
        I _LPI-CREG @ CREG-TYPE-NTH DUP _LPI-PTR !
        0= IF 0 UNLOOP EXIT THEN
        _LPI-PTR @ COMP-DESC _LPI-ROOT-SPAN-UNSAFE? IF
            0 UNLOOP EXIT
        THEN
        _LPI-PTR @ COMP-DESC-VALID? 0= IF 0 UNLOOP EXIT THEN
    LOOP
    _LPI-CREG @ CREG.INST-N @ 0 ?DO
        I _LPI-CREG @ CREG-INST-NTH DUP _LPI-PTR !
        0= IF 0 UNLOOP EXIT THEN
        _LPI-PTR @ COMP-INST _LPI-ROOT-SPAN-UNSAFE? IF
            0 UNLOOP EXIT
        THEN
        _LPI-PTR @ CINST.ID @ 0> 0= IF 0 UNLOOP EXIT THEN
        _LPI-PTR @ CINST.GENERATION @ 0> 0= IF 0 UNLOOP EXIT THEN
        _LPI-PTR @ CINST-DESC DUP _LPI-DESC !
        0= IF 0 UNLOOP EXIT THEN
        _LPI-DESC @ COMP-DESC _LPI-ROOT-SPAN-UNSAFE? IF
            0 UNLOOP EXIT
        THEN
        _LPI-DESC @ COMP-DESC-VALID? 0= IF 0 UNLOOP EXIT THEN
        _LPI-DESC @ COMP.STATE-SIZE @ DUP 0< IF
            DROP 0 UNLOOP EXIT
        THEN
        DUP _LPI-SPAN-U ! IF
            _LPI-PTR @ CINST-STATE _LPI-PTR !
            _LPI-PTR @ _LPI-SPAN-U @ _LPI-ROOT-SPAN-UNSAFE? IF
                0 UNLOOP EXIT
            THEN
        ELSE
            _LPI-PTR @ CINST-STATE IF 0 UNLOOP EXIT THEN
        THEN
    LOOP
    -1 ;

