: _LPR-QUALIFY>CBUS  ( racq-status -- cbus-status )
    DUP _LPR-STATUS ! CASE
        RACQ-S-BUSY OF CBUS-S-BUSY ENDOF
        RACQ-S-REVISION-MISMATCH OF CBUS-S-STALE-REVISION ENDOF
        RACQ-S-DIGEST-MISMATCH OF CBUS-S-STALE-REVISION ENDOF
        RACQ-S-GONE OF CBUS-S-STALE-REVISION ENDOF
        RACQ-S-PRUNED OF CBUS-S-STALE-REVISION ENDOF
        S" Library projection is no longer qualified" _LPR-STATUS @
            _LPR-REQUEST @ CBR-ERROR!
        CBUS-S-FAILED SWAP
    ENDCASE ;

: _LPR-DESCRIBE-BODY  ( -- status )
    _LPR-SLOT-PTR @ LPS.RID _LPR-ENTRY
        _LPR-ROOT @ LPR.STORE @ LIBRARY-VFS-STORE-READ-IDENTITY
    DUP LIBSTORE-S-OK <> IF _LPR-HSTORE-ERROR EXIT THEN DROP
    _LPR-ENTRY LIBE.ID _LPR-SLOT-PTR @ LPS.RID RID= 0= IF
        CBUS-S-INVALID EXIT
    THEN
    _LPR-ENTRY LIBE.KIND @ _LPR-SLOT-PTR @ LPS.KIND @ <> IF
        CBUS-S-FAILED EXIT
    THEN
    _LPR-ENTRY LIBE.LIFECYCLE @ DUP LIB-LIFECYCLE-ACTIVE =
        SWAP LIB-LIFECYCLE-ARCHIVED = OR 0= IF
        CBUS-S-FAILED EXIT
    THEN
    _LPR-ENTRY LIBE.MEDIA @ _LPR-MEDIA$ 0= IF
        2DROP CBUS-S-FAILED EXIT
    THEN
    _LPH-PROJECTION-U ! _LPH-PROJECTION-A !
    _LPR-ENTRY LIBE.KIND @ _LPR-KIND$ 0= IF
        2DROP CBUS-S-FAILED EXIT
    THEN
    _LPH-DIGEST-KIND ! _LPH-DIGEST !
    8 _LPR-HBEGIN 0= IF _LPR-HNOMEM EXIT THEN
    0 _LPR-HRESOURCE 0= IF _LPR-HNOMEM EXIT THEN
    _LPR-ENTRY LIBE.DOMAIN-REVISION @
        S" domain_revision" 1 _LPR-HINT 0= IF _LPR-HNOMEM EXIT THEN
    _LPH-DIGEST @ _LPH-DIGEST-KIND @
        S" kind" 2 _LPR-HSTRING 0= IF _LPR-HNOMEM EXIT THEN
    _LPR-ENTRY LIBE-TITLE$
        S" title" 3 _LPR-HSTRING 0= IF _LPR-HNOMEM EXIT THEN
    _LPH-PROJECTION-A @ _LPH-PROJECTION-U @
        S" media_type" 4 _LPR-HSTRING 0= IF _LPR-HNOMEM EXIT THEN
    _LPR-SLOT-PTR @ LPS.KIND @ LIB-KIND-MANAGED-DOCUMENT =
    _LPR-ROOT @ LPR.CONTEXT @ CTX-READONLY? 0= AND
    _LPR-ENTRY LIBE.LIFECYCLE @ LIB-LIFECYCLE-ACTIVE = AND
        S" mutable" 5 _LPR-HBOOL 0= IF _LPR-HNOMEM EXIT THEN
    _LPR-ENTRY LIBE.CONTENT-U @
        S" size" 6 _LPR-HINT 0= IF _LPR-HNOMEM EXIT THEN
    LIBRARY-PROJECTION-OWNER$
        S" owner" 7 _LPR-HSTRING 0= IF _LPR-HNOMEM EXIT THEN
    CBUS-S-OK ;

: _LPR-SNAPSHOT-READ  ( -- status )
    \ Exact-current qualification already loaded and digest-validated these
    \ bytes.  A historical qualification instead proved retained metadata;
    \ go directly to the retained reader rather than issuing a known-conflict
    \ current read first.
    _LPR-QUALIFIED-CONTENT @ IF RACQ-S-OK EXIT THEN
    _LPR-HLOC QLOC.DOMAIN-REVISION @
        _LPR-ENTRY LIBE.DOMAIN-REVISION @ < IF
        _LPR-SLOT-PTR @ LPS.RID
            _LPR-HLOC QLOC.DOMAIN-REVISION @
            _LPR-BYTES LIB-CONTENT-MAX _LPR-CONTENT
            _LPR-ROOT @ LPR.STORE @
            LIBRARY-VFS-STORE-READ-RETAINED-EXACT
        _LPR-STATUS ! _LPR-N !
        _LPR-STATUS @ _LPR-STORE>RACQ EXIT
    THEN
    _LPR-SLOT-PTR @ LPS.RID
        _LPR-HLOC QLOC.DOMAIN-REVISION @
        _LPR-BYTES LIB-CONTENT-MAX _LPR-RESULT-ENTRY _LPR-CONTENT
        _LPR-ROOT @ LPR.STORE @ LIBRARY-VFS-STORE-READ-EXACT
    _LPR-STATUS ! _LPR-N !
    _LPR-STATUS @ _LPR-STORE>RACQ ;

: _LPR-SNAPSHOT-BODY  ( -- status )
    _LPR-SNAPSHOT-LOCATOR! 0= IF CBUS-S-INVALID EXIT THEN
    _LPR-HLOC _LPR-ROOT @ _LPR-QUALIFY
    DUP RACQ-S-OK <> IF NIP _LPR-QUALIFY>CBUS EXIT THEN
    DROP _LPR-SLOT-PTR @ LPS.KIND @ <> IF CBUS-S-FAILED EXIT THEN
    _LPR-SNAPSHOT-READ DUP RACQ-S-OK <> IF
        _LPR-QUALIFY>CBUS EXIT
    THEN DROP
    _LPR-CONTENT LIBCT.DIGEST
        _LPR-HLOC QLOC.STATE-DIGEST SHA3-256-COMPARE 0= IF
        CBUS-S-STALE-REVISION EXIT
    THEN
    _LPR-CONTENT LIBCT.MEDIA @ _LPR-MEDIA$ 0= IF
        2DROP CBUS-S-FAILED EXIT
    THEN
    _LPH-PROJECTION-U ! _LPH-PROJECTION-A !
    9 _LPR-HBEGIN 0= IF _LPR-HNOMEM EXIT THEN
    0 _LPR-HRESOURCE 0= IF _LPR-HNOMEM EXIT THEN
    _LPR-HLOC QLOC.DOMAIN-REVISION @
        S" domain_revision" 1 _LPR-HINT 0= IF _LPR-HNOMEM EXIT THEN
    S" projection-content" S" digest_kind" 2 _LPR-HSTRING
        0= IF _LPR-HNOMEM EXIT THEN
    _LPR-HLOC QLOC.STATE-DIGEST LIB-DIGEST-SIZE
        S" state_digest" 3 _LPR-HBYTES 0= IF _LPR-HNOMEM EXIT THEN
    LIBRARY-PROJECTION-CONTRACT$
        S" projection" 4 _LPR-HSTRING 0= IF _LPR-HNOMEM EXIT THEN
    _LPH-PROJECTION-A @ _LPH-PROJECTION-U @
        S" media_type" 5 _LPR-HSTRING 0= IF _LPR-HNOMEM EXIT THEN
    _LPR-CONTENT LIBCT-DATA$
        S" content" 6 _LPR-HBYTES 0= IF _LPR-HNOMEM EXIT THEN
    _LPR-CONTENT LIBCT.DATA-U @
        S" size" 7 _LPR-HINT 0= IF _LPR-HNOMEM EXIT THEN
    _LPR-CONTENT LIBCT.DIGEST LIB-DIGEST-SIZE
        S" content_digest" 8 _LPR-HBYTES 0= IF _LPR-HNOMEM EXIT THEN
    CBUS-S-OK ;

: _LPR-REPLACE-BODY  ( -- status )
    _LPR-SLOT-PTR @ LPS.KIND @
        LIB-KIND-MANAGED-DOCUMENT <> IF CBUS-S-DENIED EXIT THEN
    _LPR-ROOT @ LPR.CONTEXT @ CTX-READONLY? IF
        S" Library context is read-only" RACQ-S-UNAVAILABLE
            _LPR-REQUEST @ CBR-ERROR!
        CBUS-S-DENIED EXIT
    THEN
    _LPR-REPLACE-LOCATOR! 0= IF CBUS-S-INVALID EXIT THEN
    _LPR-HLOC _LPR-ROOT @ _LPR-QUALIFY
    DUP RACQ-S-OK <> IF NIP _LPR-QUALIFY>CBUS EXIT THEN
    DROP LIB-KIND-MANAGED-DOCUMENT <> IF CBUS-S-FAILED EXIT THEN
    _LPR-ENTRY LIBE.LIFECYCLE @ LIB-LIFECYCLE-ACTIVE <> IF
        CBUS-S-DENIED EXIT
    THEN
    _LPR-ENTRY LIBE.DOMAIN-REVISION @
        _LPR-HLOC QLOC.DOMAIN-REVISION @ <> IF
        CBUS-S-STALE-REVISION EXIT
    THEN
    S" content" _LPR-HFIELD DUP 0= IF DROP CBUS-S-INVALID EXIT THEN
        DUP CV-DATA@ SWAP CV-LEN@ _LPR-B ! _LPR-A !
    _LPR-B @ DUP 0< SWAP LIB-CONTENT-MAX > OR IF
        CBUS-S-INVALID EXIT
    THEN
    _LPR-B @ IF
        _LPR-A @ _LPR-B @ UTF8-VALID? 0= IF CBUS-S-INVALID EXIT THEN
    THEN
    _LPR-A @ _LPR-B @ _LPR-DIGEST SHA3-256-HASH
    S" content_digest" _LPR-HFIELD DUP 0= IF DROP CBUS-S-INVALID EXIT THEN
        CV-DATA@ _LPR-DIGEST SHA3-256-COMPARE 0= IF
        CBUS-S-INVALID EXIT
    THEN
    _LPR-ENTRY LIBE.DOMAIN-REVISION @ 1+ _LPR-C !
    \ Allocate and populate the complete result before the durable commit.
    \ Once REPLACE-MANAGED succeeds, no fallible result allocation remains.
    6 _LPR-HBEGIN 0= IF _LPR-HNOMEM EXIT THEN
    0 _LPR-HRESOURCE 0= IF _LPR-HNOMEM EXIT THEN
    _LPR-C @ S" domain_revision" 1 _LPR-HINT
        0= IF _LPR-HNOMEM EXIT THEN
    S" projection-content" S" digest_kind" 2 _LPR-HSTRING
        0= IF _LPR-HNOMEM EXIT THEN
    _LPR-DIGEST LIB-DIGEST-SIZE
        S" state_digest" 3 _LPR-HBYTES 0= IF _LPR-HNOMEM EXIT THEN
    LIBRARY-PROJECTION-CONTRACT$
        S" projection" 4 _LPR-HSTRING 0= IF _LPR-HNOMEM EXIT THEN
    _LPR-DIGEST LIB-DIGEST-SIZE
        S" content_digest" 5 _LPR-HBYTES 0= IF _LPR-HNOMEM EXIT THEN
    _LPR-SLOT-PTR @ LPS.RID _LPR-ENTRY LIBE.DOMAIN-REVISION @
        _LPR-A @ _LPR-B @ _LPR-RESULT-ENTRY
        _LPR-ROOT @ LPR.STORE @ LIBRARY-VFS-STORE-REPLACE-MANAGED
    DUP LIBSTORE-S-OK <> IF _LPR-HSTORE-ERROR EXIT THEN DROP
    _LPR-RESULT-ENTRY LIBE.DOMAIN-REVISION @ _LPR-C @ <>
    _LPR-RESULT-ENTRY LIBE.CONTENT-DIGEST
        _LPR-DIGEST SHA3-256-COMPARE 0= OR IF
        CBUS-S-FAILED EXIT
    THEN
    CBUS-S-OK ;

