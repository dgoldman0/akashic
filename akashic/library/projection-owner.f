        I _LPV-ROOT @ _LPR-SLOT DUP _LPV-SLOT ! LPS.ACTIVE @ IF
            1 _LPV-LIVE +!
            _LPV-SLOT @ LPS.KIND @ _LPR-KIND-VALID? 0= IF
                0 UNLOOP EXIT
            THEN
            _LPV-SLOT @ LPS.GENERATION @ 0> 0= IF 0 UNLOOP EXIT THEN
            _LPV-SLOT @ LPS.REF-N @ DUP 0> 0= IF
                DROP 0 UNLOOP EXIT
            THEN
            DUP _LPV-REFS +!
            I _LPV-ROOT @ _LPR-SLOT-LEASE-N <> IF 0 UNLOOP EXIT THEN
            _LPV-SLOT @ LPS.INFLIGHT @ 0< IF 0 UNLOOP EXIT THEN
            _LPV-SLOT @ LPS.INSTANCE @ 0= IF 0 UNLOOP EXIT THEN
            _LPV-SLOT @ LPS.RID RID-PRESENT? 0= IF 0 UNLOOP EXIT THEN
            _LPV-SLOT @ LPS.RESERVED @ IF 0 UNLOOP EXIT THEN
            _LPV-SLOT @ LPS.FLAGS @
                _LPS-F-CLOSING INVERT AND IF 0 UNLOOP EXIT THEN
            _LPV-SLOT @ LPS.FLAGS @ _LPS-F-CLOSING AND IF
                _LPV-SLOT @ LPS.REF-N @ 1 <> IF 0 UNLOOP EXIT THEN
