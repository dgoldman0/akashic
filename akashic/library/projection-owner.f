                _LPR-TOKEN-LEASE? 0= IF 2DROP 0 UNLOOP EXIT THEN
            DROP _LPV-LEASE @ <> IF 0 UNLOOP EXIT THEN
        ELSE
            _LPV-LEASE @ LIBRARY-PROJECTION-LEASE-SIZE
                _LPR-ZERO? 0= IF 0 UNLOOP EXIT THEN
        THEN
    LOOP
    _LPV-LEASES @ _LPV-ROOT @ LPR.LEASE-N @ =
    _LPV-REFS @ _LPV-LEASES @ = AND ;

