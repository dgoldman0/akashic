            _LPV-SLOT @ _LPV-ROOT @ _LPR-SLOT-PUBLISHED? 0= IF
                0 UNLOOP EXIT
            THEN
            I _LPV-SLOT @ _LPV-ROOT @
                _LPR-SLOT-INSTANCE-MATCH? 0= IF 0 UNLOOP EXIT THEN
            I _LPV-SLOT @ _LPV-ROOT @
