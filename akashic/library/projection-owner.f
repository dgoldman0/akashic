\ =====================================================================
\  Typed, full-root attachment boundary
\ =====================================================================
\  This is the supported Library attachment surface.  It has RACQ-ATTACH's
\  six logical inputs, but ROOT denotes the complete Library root rather
\  than merely promising an 88-byte RACQ prefix:
\
\    ( locator library-root context rreg binding result -- status )
\
\  BINDING and RESULT must be independent caller-owned storage.  The checks
\  below cover every bounded root/registry/store span exposed by the runtime
\  metadata.  They cannot enumerate arbitrary dependency-private globals;
\  callers must keep all six argument objects fully disjoint from such
\  storage.  The Library guard is released before entering RACQ, preserving
\  RACQ -> Library as the only callback lock order.

VARIABLE _LPAT-LOCATOR
VARIABLE _LPAT-ROOT
VARIABLE _LPAT-CONTEXT
VARIABLE _LPAT-RREG
VARIABLE _LPAT-BINDING
VARIABLE _LPAT-RESULT
VARIABLE _LPAT-SPAN-A
VARIABLE _LPAT-SPAN-U
VARIABLE _LPAT-PTR
VARIABLE _LPAT-DESC
VARIABLE _LPAT-SIZE

: _LPR-ATTACH-PUBLIC-SPAN?  ( a u -- flag )
    OVER 0= IF 2DROP 0 EXIT THEN
    2DUP MSPAN-NONWRAPPING? 0= IF 2DROP 0 EXIT THEN
    2DUP _LPR-PRIVATE-BEGIN
        _LPR-PRIVATE-LIMIT @ _LPR-PRIVATE-BEGIN -
        MSPAN-OVERLAP? IF 2DROP 0 EXIT THEN
    _LPR-BYTES LIB-CONTENT-MAX MSPAN-OVERLAP? 0= ;

: _LPR-ATTACH-PUBLIC-OUTPUTS?  ( binding result -- flag )
    >R
    DUP LBIND-SIZE _LPR-ATTACH-PUBLIC-SPAN? 0= IF
        DROP R> DROP 0 EXIT
    THEN
    R@ RACQ-RESULT-SIZE _LPR-ATTACH-PUBLIC-SPAN? 0= IF
        DROP R> DROP 0 EXIT
    THEN
    DUP LBIND-SIZE R@ RACQ-RESULT-SIZE MSPAN-OVERLAP? IF
        DROP R> DROP 0 EXIT
    THEN
    DROP R> DROP -1 ;

: _LPAT-OUTPUTS-ALIAS?  ( a u -- flag )
    _LPAT-SPAN-U ! _LPAT-SPAN-A !
    _LPAT-BINDING @ LBIND-SIZE
        _LPAT-SPAN-A @ _LPAT-SPAN-U @ MSPAN-OVERLAP? IF -1 EXIT THEN
    _LPAT-RESULT @ RACQ-RESULT-SIZE
        _LPAT-SPAN-A @ _LPAT-SPAN-U @ MSPAN-OVERLAP? ;

: _LPAT-PROTECTED?  ( a u -- unsafe? )
    DUP 0> IF OVER 0= IF 2DROP -1 EXIT THEN THEN
    2DUP MSPAN-NONWRAPPING? 0= IF 2DROP -1 EXIT THEN
    _LPAT-OUTPUTS-ALIAS? ;

: _LPAT-ROOT-SHALLOW?  ( -- flag )
    \ The span walk below needs trusted, bounded registry counts and the
    \ exact borrowed runtime graph, but not the publication/lease rescan in
    \ _LPR-ROOT-DEEP?.  RACQ enters the same runtime-checked owner callback
    \ after this guard is released.
    _LPAT-ROOT @ _LPR-ROOT-RUNTIME? 0= IF 0 EXIT THEN
    _LPAT-ROOT @ _LPR-ROOT-CALLBACKS? 0= IF 0 EXIT THEN
    _LPAT-ROOT @ LPR.NEXT-GENERATION @ 0> 0= IF 0 EXIT THEN
    _LPAT-ROOT @ LPR.NEXT-COOKIE @ 0> 0= IF 0 EXIT THEN
    _LPAT-ROOT @ LPR.ACQUIRE-CALLS @ 0< IF 0 EXIT THEN
    _LPAT-ROOT @ LPR.RELEASE-CALLS @ 0< IF 0 EXIT THEN
    _LPAT-ROOT @ LPR.QUIESCENT-CALLS @ 0< IF 0 EXIT THEN
    _LPAT-ROOT @ LPR.TEST-BUSY @ 0< IF 0 EXIT THEN
    _LPAT-ROOT @ LPR.TEST-RELEASE-FAIL @ 0< IF 0 EXIT THEN
    -1 ;

: _LPAT-CREG-SAFE?  ( -- flag )
    _LPAT-ROOT @ LPR.CREG @ CREG.TYPE-N @ 0 ?DO
        I _LPAT-ROOT @ LPR.CREG @ CREG-TYPE-NTH DUP _LPAT-PTR !
        0= IF 0 UNLOOP EXIT THEN
        _LPAT-PTR @ COMP-DESC _LPAT-PROTECTED? IF 0 UNLOOP EXIT THEN
        _LPAT-PTR @ COMP-DESC-VALID? 0= IF 0 UNLOOP EXIT THEN
    LOOP
    _LPAT-ROOT @ LPR.CREG @ CREG.INST-N @ 0 ?DO
        I _LPAT-ROOT @ LPR.CREG @ CREG-INST-NTH DUP _LPAT-PTR !
        0= IF 0 UNLOOP EXIT THEN
        _LPAT-PTR @ COMP-INST _LPAT-PROTECTED? IF 0 UNLOOP EXIT THEN
        _LPAT-PTR @ CINST.ID @ 0> 0= IF 0 UNLOOP EXIT THEN
        _LPAT-PTR @ CINST.GENERATION @ 0> 0= IF 0 UNLOOP EXIT THEN
        _LPAT-PTR @ CINST-DESC DUP _LPAT-DESC !
        0= IF 0 UNLOOP EXIT THEN
        _LPAT-DESC @ COMP-DESC _LPAT-PROTECTED? IF 0 UNLOOP EXIT THEN
        _LPAT-DESC @ COMP-DESC-VALID? 0= IF 0 UNLOOP EXIT THEN
        _LPAT-DESC @ COMP.STATE-SIZE @ DUP 0< IF
            DROP 0 UNLOOP EXIT
        THEN
        DUP _LPAT-SIZE ! IF
            _LPAT-PTR @ CINST-STATE _LPAT-PTR !
            _LPAT-PTR @ _LPAT-SIZE @ _LPAT-PROTECTED? IF
                0 UNLOOP EXIT
            THEN
        ELSE
            _LPAT-PTR @ CINST-STATE IF 0 UNLOOP EXIT THEN
        THEN
    LOOP
    -1 ;

: _LPAT-TOKENS-SAFE?  ( -- flag )
    LIBRARY-PROJECTION-LEASE-MAX 0 ?DO
        I _LPAT-ROOT @ _LPR-LEASE LPL.TOKEN @ ?DUP IF
            RACQ-TOKEN-SIZE _LPAT-PROTECTED? IF 0 UNLOOP EXIT THEN
        THEN
    LOOP
    -1 ;

: _LPR-ATTACH-PREFLIGHT
  ( locator library-root context rreg binding result -- flag )
    _LPAT-RESULT ! _LPAT-BINDING ! _LPAT-RREG !
    _LPAT-CONTEXT ! _LPAT-ROOT ! _LPAT-LOCATOR !
    _LPAT-ROOT-SHALLOW? 0= IF 0 EXIT THEN
    _LPAT-CONTEXT @ CTX-VALID? 0= IF 0 EXIT THEN
    _LPAT-CONTEXT @ CTX.FLAGS @ CTX-F-ACTIVE AND 0= IF 0 EXIT THEN
    _LPAT-RREG @ _LPAT-ROOT @ LPR.RREG @ <> IF 0 EXIT THEN
    _LPAT-LOCATOR @ QLOC-VALID? 0= IF 0 EXIT THEN
    _LPAT-LOCATOR @ QLOC-SIZE _LPAT-PROTECTED? IF 0 EXIT THEN
    _LPAT-ROOT @ LIBRARY-PROJECTION-ROOT-SIZE
        _LPAT-PROTECTED? IF 0 EXIT THEN
    _LPAT-ROOT @ LPR.STORE @ LIBRARY-VFS-STORE-SIZE
        _LPAT-PROTECTED? IF 0 EXIT THEN
    _LPAT-ROOT @ LPR.CONTEXT @ CTX-SIZE
        _LPAT-PROTECTED? IF 0 EXIT THEN
    _LPAT-CONTEXT @ CTX-SIZE _LPAT-PROTECTED? IF 0 EXIT THEN
    _LPAT-ROOT @ LPR.CREG @ CREG-SIZE
        _LPAT-PROTECTED? IF 0 EXIT THEN
    _LPAT-ROOT @ LPR.RREG @ RREG-SIZE
        _LPAT-PROTECTED? IF 0 EXIT THEN
    _LPAT-ROOT @ LPR.BUS @ CBUS-SIZE
        _LPAT-PROTECTED? IF 0 EXIT THEN
    _LPAT-ROOT @ LPR.STORE @ LIBRARY-VFS-STORE.VFS @ DUP _LPAT-PTR !
        VFS-DESC-SIZE _LPAT-PROTECTED? IF 0 EXIT THEN
    _LPAT-PTR @ V.ARENA @ DUP _LPAT-PTR !
        32 _LPAT-PROTECTED? IF 0 EXIT THEN
    _LPAT-PTR @ DUP A.BASE @ SWAP A.SIZE @
        _LPAT-PROTECTED? IF 0 EXIT THEN
    _LPAT-ROOT @ LPR.CONTEXT @ CTX.PRACTICE @ ?DUP IF
        PHEAD-SIZE _LPAT-PROTECTED? IF 0 EXIT THEN
    THEN
    _LPAT-CONTEXT @ CTX.PRACTICE @ ?DUP IF
        PHEAD-SIZE _LPAT-PROTECTED? IF 0 EXIT THEN
    THEN
    _LPAT-CREG-SAFE? 0= IF 0 EXIT THEN
    _LPAT-TOKENS-SAFE? ;

: LIBRARY-PROJECTION-ATTACH
  ( locator library-root context rreg binding result -- status )
    5 PICK QLOC-SIZE _LPR-ATTACH-PUBLIC-SPAN? 0= IF
        2DROP 2DROP 2DROP RACQ-S-INVALID EXIT
    THEN
    4 PICK _LPR-ROOT-PUBLIC-SAFE? 0= IF
        2DROP 2DROP 2DROP RACQ-S-INVALID EXIT
    THEN
    3 PICK CTX-SIZE _LPR-ATTACH-PUBLIC-SPAN? 0= IF
        2DROP 2DROP 2DROP RACQ-S-INVALID EXIT
    THEN
    2 PICK RREG-SIZE _LPR-ATTACH-PUBLIC-SPAN? 0= IF
        2DROP 2DROP 2DROP RACQ-S-INVALID EXIT
    THEN
    1 PICK 1 PICK _LPR-ATTACH-PUBLIC-OUTPUTS? 0= IF
        2DROP 2DROP 2DROP RACQ-S-INVALID EXIT
    THEN
    5 PICK 5 PICK 5 PICK 5 PICK 5 PICK 5 PICK
    ['] _LPR-ATTACH-PREFLIGHT _LPR-GUARD WITH-GUARD 0= IF
        2DROP 2DROP 2DROP RACQ-S-INVALID EXIT
    THEN
    RACQ-ATTACH ;

