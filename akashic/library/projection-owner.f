    _LPR-INSTANCE @ _LPR-INSTANCE-STATE? 0= IF DROP 0 EXIT THEN
    DROP
    _LPR-REQUEST @ CBR.RESOURCE-ID
        _LPR-SLOT-PTR @ LPS.RID RID= 0= IF 0 EXIT THEN
    _LPR-SLOT-PTR @ LPS.FLAGS @ _LPS-F-CLOSING AND IF 0 EXIT THEN
    1 _LPR-SLOT-PTR @ LPS.INFLIGHT +!
    -1 ;

: _LPR-HANDLER-END  ( -- )
    _LPR-SLOT-PTR @ ?DUP IF -1 SWAP LPS.INFLIGHT +! THEN ;

: _LPR-HBEGIN  ( count -- flag )
    _LPR-REQUEST @ CBR.RESULT CV-MAP! 0= ;

: _LPR-HSLOT  ( key-a key-u index -- flag )
    _LPR-REQUEST @ CBR.RESULT CV-MAP-SLOT! DUP IF
        NIP DROP 0
    ELSE
        DROP _LPH-CHILD ! -1
    THEN ;

: _LPR-HRESOURCE  ( index -- flag )
    >R S" resource" R> _LPR-HSLOT 0= IF 0 EXIT THEN
    _LPR-REF RREF-INIT
    _LPR-SLOT-PTR @ LPS.RID _LPR-REF RREF.ID RID-COPY
    _LPR-REF _LPH-CHILD @ IRES-RREF! IRES-S-OK = ;

: _LPR-HINT  ( n key-a key-u index -- flag )
    _LPR-HSLOT 0= IF DROP 0 EXIT THEN
    _LPH-CHILD @ CV-INT! -1 ;

: _LPR-HSTRING  ( value-a value-u key-a key-u index -- flag )
    _LPR-HSLOT 0= IF 2DROP 0 EXIT THEN
    _LPH-CHILD @ CV-STRING! 0= ;

: _LPR-HBYTES  ( value-a value-u key-a key-u index -- flag )
    _LPR-HSLOT 0= IF 2DROP 0 EXIT THEN
    _LPH-CHILD @ CV-BYTES! 0= ;

: _LPR-HBOOL  ( flag key-a key-u index -- flag )
    _LPR-HSLOT 0= IF DROP 0 EXIT THEN
    _LPH-CHILD @ CV-BOOL! -1 ;

: _LPR-HFIELD  ( key-a key-u -- value|0 )
    _LPR-REQUEST @ CBR.ARGS CV-MAP-FIND ;

: _LPR-HNOMEM  ( -- status )
    _LPR-REQUEST @ CBR.RESULT CV-FREE
    S" Library projection result allocation failed" RACQ-S-CAPACITY
        _LPR-REQUEST @ CBR-ERROR!
    CBUS-S-FAILED ;

: _LPR-HSTORE-ERROR  ( store-status -- status )
    DUP LIBSTORE-S-BUSY = IF
        DROP CBUS-S-BUSY EXIT
    THEN
    DUP LIBSTORE-S-CONFLICT = OVER LIBSTORE-S-GONE = OR IF
        DROP CBUS-S-STALE-REVISION EXIT
    THEN
    S" Library projection store operation failed" ROT
        _LPR-REQUEST @ CBR-ERROR!
    CBUS-S-FAILED ;

