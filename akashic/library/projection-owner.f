    _LPE-ROOT @ _LPR-ROOT-HEADER? 0= IF RACQ-S-INVALID EXIT THEN
    1 _LPE-ROOT @ LPR.RELEASE-CALLS +!
    _LPE-TOKEN @ _LPE-ROOT @ _LPR-TOKEN-LEASE? 0= IF
