: _LPR-ROOT-ACQUIRE-LOCKED  ( locator result root -- status )
    _LPA-ROOT ! _LPA-RESULT ! _LPA-LOCATOR !
    0 _LPA-CREATED !
    _LPA-ROOT @ _LPR-ROOT-HEADER? 0= IF RACQ-S-INVALID EXIT THEN
    1 _LPA-ROOT @ LPR.ACQUIRE-CALLS +!
    _LPA-ROOT @ _LPR-ROOT-RUNTIME? 0= IF RACQ-S-UNAVAILABLE EXIT THEN
    _LPA-LOCATOR @ _LPA-ROOT @ _LPR-QUALIFY
    DUP RACQ-S-OK <> IF NIP _LPR-SCRATCH-WIPE EXIT THEN
    DROP _LPA-KIND !
    _LPA-ROOT @ _LPR-LEASE-FREE-RAW DUP _LPA-LEASE-I !
        0< IF _LPR-SCRATCH-WIPE RACQ-S-CAPACITY EXIT THEN
    _LPA-LOCATOR @ QLOC.REF RREF.ID _LPA-ROOT @
        _LPR-SLOT-FIND-RAW DUP _LPA-SLOT-I !
    0< IF
        _LPA-ROOT @ LPR.LIVE-N @ LIBRARY-PROJECTION-OWNER-MAX >= IF
            _LPR-SCRATCH-WIPE RACQ-S-CAPACITY EXIT
        THEN
        _LPA-ROOT @ _LPR-SLOT-FREE-RAW DUP _LPA-SLOT-I !
            0< IF _LPR-SCRATCH-WIPE RACQ-S-CAPACITY EXIT THEN
        _LPA-LOCATOR @ QLOC.REF RREF.ID _LPA-KIND @
            _LPA-SLOT-I @ _LPA-ROOT @ _LPR-CREATE-SLOT
        DUP RACQ-S-OK <> IF _LPR-SCRATCH-WIPE EXIT THEN DROP
        -1 _LPA-CREATED !
    ELSE
        _LPA-SLOT-I @ _LPA-ROOT @ _LPR-SLOT DUP _LPR-SLOT-PTR !
        DUP LPS.FLAGS @ _LPS-F-CLOSING AND IF
            DROP _LPR-SCRATCH-WIPE RACQ-S-BUSY EXIT
        THEN
        LPS.KIND @ _LPA-KIND @ <> IF
            _LPR-SCRATCH-WIPE RACQ-S-UNQUALIFIED EXIT
        THEN
        _LPR-SLOT-PTR @ _LPA-ROOT @ _LPR-SLOT-PUBLISHED? 0= IF
            _LPR-SCRATCH-WIPE RACQ-S-UNAVAILABLE EXIT
        THEN
    THEN
    _LPA-RESULT @ _LPA-SLOT-I @ _LPA-LEASE-I @ _LPA-ROOT @
        _LPR-COMMIT-LEASE DUP _LPR-STATUS !
    _LPR-STATUS @ RACQ-S-OK <> _LPA-CREATED @ AND IF
        _LPA-SLOT-I @ _LPA-ROOT @ _LPR-DESTROY-SLOT
        RACQ-S-OK <> IF
            DROP _LPR-SCRATCH-WIPE RACQ-S-RELEASE-FAILED EXIT
        THEN
    THEN
    DROP _LPR-SCRATCH-WIPE _LPR-STATUS @ ;

: _LPR-ROOT-ACQUIRE  ( locator result root -- status )
    ['] _LPR-ROOT-ACQUIRE-LOCKED _LPR-GUARD WITH-GUARD ;

