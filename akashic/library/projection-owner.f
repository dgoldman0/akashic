    DUP _LPV-ROOT ! _LPR-ROOT-RUNTIME? 0= IF 0 EXIT THEN
    _LPV-ROOT @ _LPR-ROOT-CALLBACKS? 0= IF 0 EXIT THEN
    _LPV-ROOT @ LPR.NEXT-GENERATION @ 0> 0= IF 0 EXIT THEN
    _LPV-ROOT @ LPR.NEXT-COOKIE @ 0> 0= IF 0 EXIT THEN
    _LPV-ROOT @ LPR.ACQUIRE-CALLS @ 0< IF 0 EXIT THEN
    _LPV-ROOT @ LPR.RELEASE-CALLS @ 0< IF 0 EXIT THEN
    _LPV-ROOT @ LPR.QUIESCENT-CALLS @ 0< IF 0 EXIT THEN
    _LPV-ROOT @ LPR.TEST-BUSY @ 0< IF 0 EXIT THEN
    _LPV-ROOT @ LPR.TEST-RELEASE-FAIL @ 0< IF 0 EXIT THEN
