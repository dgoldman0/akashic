: _LPR-QUALIFY-EXACT  ( -- status )
    _LPR-LOCATOR @ QLOC.DIGEST-KIND @
        QLOC-DK-PROJECTION-CONTENT <> IF
        RACQ-S-UNQUALIFIED EXIT
    THEN
    _LPR-LOCATOR @ QLOC-PROJECTION$
        LIBRARY-PROJECTION-CONTRACT$ STR-STR= 0= IF
        RACQ-S-UNQUALIFIED EXIT
    THEN
    _LPR-LOCATOR @ QLOC.DOMAIN-REVISION @ _LPR-A !
    _LPR-ENTRY LIBE.DOMAIN-REVISION @ _LPR-B !
    _LPR-A @ _LPR-B @ = IF
        _LPR-LOCATOR @ QLOC.REF RREF.ID _LPR-A @
            _LPR-BYTES LIB-CONTENT-MAX _LPR-RESULT-ENTRY _LPR-CONTENT
            _LPR-ROOT @ LPR.STORE @ LIBRARY-VFS-STORE-READ-EXACT
        _LPR-STATUS ! _LPR-N !
        _LPR-STATUS @ LIBSTORE-S-OK <> IF
            _LPR-STATUS @ _LPR-STORE>RACQ EXIT
        THEN
        _LPR-RESULT-ENTRY LIBE.ID
            _LPR-LOCATOR @ QLOC.REF RREF.ID RID= 0= IF
            RACQ-S-RID-MISMATCH EXIT
        THEN
        _LPR-CONTENT LIB-CONTENT-VALID? 0= IF
            RACQ-S-UNAVAILABLE EXIT
        THEN
        _LPR-CONTENT LIBCT.ID
            _LPR-LOCATOR @ QLOC.REF RREF.ID RID= 0= IF
            RACQ-S-RID-MISMATCH EXIT
        THEN
        _LPR-CONTENT LIBCT.CONTENT-REVISION @
            _LPR-RESULT-ENTRY LIBE.CURRENT-CONTENT-REVISION @ <> IF
            RACQ-S-UNAVAILABLE EXIT
        THEN
        _LPR-CONTENT LIBCT.DOMAIN-REVISION @ _LPR-A @ > IF
            RACQ-S-UNAVAILABLE EXIT
        THEN
        _LPR-CONTENT LIBCT.KIND @ _LPR-ENTRY LIBE.KIND @ <> IF
            RACQ-S-UNQUALIFIED EXIT
        THEN
        _LPR-CONTENT LIBCT.DIGEST
            _LPR-ENTRY LIBE.CONTENT-DIGEST SHA3-256-COMPARE 0= IF
            RACQ-S-UNAVAILABLE EXIT
        THEN
        _LPR-LOCATOR @ QLOC.STATE-DIGEST
        _LPR-CONTENT LIBCT.DIGEST SHA3-256-COMPARE IF
            -1 _LPR-QUALIFIED-CONTENT !
            RACQ-S-OK
        ELSE
            RACQ-S-DIGEST-MISMATCH
        THEN
        EXIT
    THEN
    _LPR-A @ _LPR-B @ > IF RACQ-S-REVISION-MISMATCH EXIT THEN
    _LPR-LOCATOR @ QLOC.REF RREF.ID _LPR-A @
        _LPR-BYTES LIB-CONTENT-MAX _LPR-CONTENT
        _LPR-ROOT @ LPR.STORE @
        LIBRARY-VFS-STORE-READ-RETAINED-EXACT
    _LPR-STATUS ! _LPR-N !
    _LPR-STATUS @ LIBSTORE-S-OK = IF
        _LPR-CONTENT LIB-CONTENT-VALID? 0= IF
            RACQ-S-UNAVAILABLE EXIT
        THEN
        _LPR-CONTENT LIBCT.ID
            _LPR-LOCATOR @ QLOC.REF RREF.ID RID= 0= IF
            RACQ-S-RID-MISMATCH EXIT
        THEN
        _LPR-CONTENT LIBCT.DOMAIN-REVISION @ _LPR-A @ <> IF
            RACQ-S-REVISION-MISMATCH EXIT
        THEN
        _LPR-CONTENT LIBCT.KIND @ _LPR-ENTRY LIBE.KIND @ <> IF
            RACQ-S-UNQUALIFIED EXIT
        THEN
        _LPR-CONTENT LIBCT.DIGEST
            _LPR-LOCATOR @ QLOC.STATE-DIGEST SHA3-256-COMPARE 0= IF
            RACQ-S-DIGEST-MISMATCH EXIT
        THEN
        -1 _LPR-QUALIFIED-CONTENT !
        RACQ-S-OK EXIT
    THEN
    _LPR-STATUS @ LIBSTORE-S-GONE <> IF
        _LPR-STATUS @ _LPR-STORE>RACQ EXIT
    THEN
    _LPR-LOCATOR @ QLOC.REF RREF.ID
        _LPR-B @ _LPR-HISTORY LIB-RETAINED-REVISION-MAX
        _LPR-ROOT @ LPR.STORE @
        LIBRARY-VFS-STORE-LIST-RETAINED-REVISIONS
    _LPR-STATUS ! _LPR-N !
    _LPR-STATUS @ LIBSTORE-S-OK <> IF
        _LPR-STATUS @ _LPR-STORE>RACQ EXIT
    THEN
    _LPR-B @ _LPR-C !
    _LPR-N @ 0 ?DO
        I _LPR-HISTORY-SUMMARY DUP LIBRS.DOMAIN-REVISION @
            DUP _LPR-C @ < IF DUP _LPR-C ! THEN
            _LPR-A @ = IF
            LIBRS.DIGEST _LPR-LOCATOR @ QLOC.STATE-DIGEST
                SHA3-256-COMPARE IF
                RACQ-S-OK
            ELSE
                RACQ-S-DIGEST-MISMATCH
            THEN
            UNLOOP EXIT
        ELSE
            DROP
        THEN
    LOOP
    _LPR-A @ _LPR-C @ < IF RACQ-S-PRUNED ELSE RACQ-S-GONE THEN ;

: _LPR-QUALIFY  ( locator root -- kind status )
    _LPR-ROOT ! _LPR-LOCATOR !
    0 _LPR-QUALIFIED-CONTENT !
    _LPR-LOCATOR @ QLOC-VALID? 0= IF 0 RACQ-S-INVALID EXIT THEN
    _LPR-LOCATOR @ QLOC-OWNER$
        LIBRARY-PROJECTION-OWNER$ STR-STR= 0= IF
        0 RACQ-S-OWNER-MISMATCH EXIT
    THEN
    _LPR-LOCATOR @ QLOC.REF RREF.ID _LPR-ENTRY
        _LPR-ROOT @ LPR.STORE @ LIBRARY-VFS-STORE-READ-IDENTITY
    DUP _LPR-STATUS ! LIBSTORE-S-OK <> IF
        0 _LPR-STATUS @ _LPR-STORE>RACQ EXIT
    THEN
    _LPR-ENTRY LIBE.ID _LPR-LOCATOR @ QLOC.REF RREF.ID RID= 0= IF
        0 RACQ-S-RID-MISMATCH EXIT
    THEN
    _LPR-ENTRY LIBE.KIND @ _LPR-KIND-VALID? 0= IF
        0 RACQ-S-UNQUALIFIED EXIT
    THEN
    _LPR-ENTRY LIBE.LIFECYCLE @ LIB-LIFECYCLE-ACTIVE <> IF
        _LPR-ENTRY LIBE.LIFECYCLE @ LIB-LIFECYCLE-ARCHIVED =
        _LPR-LOCATOR @ QLOC.MODE @ QLOC-M-IDENTITY = AND IF
            0 RACQ-S-UNAVAILABLE EXIT
        THEN
        _LPR-ENTRY LIBE.LIFECYCLE @ LIB-LIFECYCLE-ARCHIVED <> IF
            0 RACQ-S-GONE EXIT
        THEN
    THEN
    _LPR-LOCATOR @ QLOC.MODE @ QLOC-M-EXACT-DOMAIN = IF
        _LPR-QUALIFY-EXACT DUP RACQ-S-OK <> IF 0 SWAP EXIT THEN
        DROP
    THEN
    _LPR-ENTRY LIBE.KIND @ RACQ-S-OK ;

