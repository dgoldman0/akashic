    _LPC-ROOT @ LPR.NEXT-GENERATION _LPR-NEXT-COUNTER
        _LPC-SLOT @ LPS.GENERATION !
    _LPC-KIND @ _LPC-SLOT @ LPS.KIND !
    _LPC-RID @ _LPC-SLOT @ LPS.RID RID-COPY
    _LPC-INSTANCE @ _LPC-SLOT @ LPS.INSTANCE !
    _LPC-INSTANCE @ CINST-STATE DUP _LPC-STATE !
        LIBRARY-PROJECTION-STATE-SIZE 0 FILL
    _LPR-STATE-MAGIC _LPC-STATE @ LPST.MAGIC !
    _LPC-ROOT @ _LPC-STATE @ LPST.ROOT !
    _LPC-INDEX @ _LPC-STATE @ LPST.SLOT-I !
    _LPC-SLOT @ LPS.GENERATION @ _LPC-STATE @ LPST.GENERATION !
    _LPC-KIND @ _LPC-STATE @ LPST.KIND !
    _LPC-INSTANCE @ _LPC-ROOT @ LPR.CREG @ CREG-INST+
    DUP IF DROP 0 _LPR-CREATE-ROLLBACK RACQ-S-CAPACITY EXIT THEN DROP
    _LPC-RID @ _LPC-INSTANCE @ _LPC-ROOT @ LPR.CONTEXT @
        _LPC-ROOT @ LPR.RREG @ RREG-PUBLISH DUP _LPC-STATUS !
    DUP RREG-S-OK <> IF
        DROP -1 _LPR-CREATE-ROLLBACK
        _LPC-STATUS @ RREG-S-FULL = IF RACQ-S-CAPACITY
        ELSE RACQ-S-UNAVAILABLE THEN
        EXIT
    THEN DROP
    -1 _LPC-SLOT @ LPS.ACTIVE !
    1 _LPC-ROOT @ LPR.LIVE-N +!
    RACQ-S-OK ;

