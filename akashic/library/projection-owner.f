: _LPR-ROOT-INIT-LOCKED
  ( store context creg rreg bus root -- status )
    _LPI-ROOT ! _LPI-BUS ! _LPI-RREG ! _LPI-CREG !
    _LPI-CONTEXT ! _LPI-STORE !
    _LPI-ROOT @ 0= IF RACQ-S-INVALID EXIT THEN
    _LPI-ROOT @ LIBRARY-PROJECTION-ROOT-SIZE
        MSPAN-NONWRAPPING? 0= IF RACQ-S-INVALID EXIT THEN
    _LPI-ROOT @ _LPR-ROOT-HEADER? IF RACQ-S-BUSY EXIT THEN
    _LPR-INIT-INPUTS? 0= IF RACQ-S-INVALID EXIT THEN
    _LPR-MANAGED-DESC COMP-CAPS-VALID? 0= IF RACQ-S-INVALID EXIT THEN
    _LPR-CAPTURE-DESC COMP-CAPS-VALID? 0= IF RACQ-S-INVALID EXIT THEN
    0 _LPI-MISSING !
    _LPR-MANAGED-DESC DUP COMP.ID-A @ SWAP COMP.ID-U @
        _LPI-CREG @ CREG-TYPE-FIND DUP IF
            _LPR-MANAGED-DESC <> IF RACQ-S-INVALID EXIT THEN
        ELSE
            DROP 1 _LPI-MISSING +!
        THEN
    _LPR-CAPTURE-DESC DUP COMP.ID-A @ SWAP COMP.ID-U @
        _LPI-CREG @ CREG-TYPE-FIND DUP IF
            _LPR-CAPTURE-DESC <> IF RACQ-S-INVALID EXIT THEN
        ELSE
            DROP 1 _LPI-MISSING +!
        THEN
    _LPI-CREG @ CREG.TYPE-N @ _LPI-MISSING @ +
        CREG-MAX-TYPES > IF RACQ-S-CAPACITY EXIT THEN
    _LPR-MANAGED-DESC _LPI-CREG @ CREG-TYPE-ENSURE IF
        RACQ-S-CAPACITY EXIT
    THEN
    _LPR-CAPTURE-DESC _LPI-CREG @ CREG-TYPE-ENSURE IF
        RACQ-S-CAPACITY EXIT
    THEN
    _LPR-MANAGED-DESC DUP COMP.ID-A @ SWAP COMP.ID-U @
        _LPI-CREG @ CREG-TYPE-FIND
        _LPR-MANAGED-DESC <> IF RACQ-S-INVALID EXIT THEN
    _LPR-CAPTURE-DESC DUP COMP.ID-A @ SWAP COMP.ID-U @
        _LPI-CREG @ CREG-TYPE-FIND
        _LPR-CAPTURE-DESC <> IF RACQ-S-INVALID EXIT THEN
    _LPI-ROOT @ LIBRARY-PROJECTION-ROOT-SIZE 0 FILL
    LIBRARY-PROJECTION-ROOT-MAGIC _LPI-ROOT @ LPR.MAGIC !
    LIBRARY-PROJECTION-ROOT-ABI-VERSION _LPI-ROOT @ LPR.ABI !
    LIBRARY-PROJECTION-ROOT-SIZE _LPI-ROOT @ LPR.SIZE !
    _LPI-STORE @ _LPI-ROOT @ LPR.STORE !
    _LPI-CONTEXT @ _LPI-ROOT @ LPR.CONTEXT !
    _LPI-CREG @ _LPI-ROOT @ LPR.CREG !
    _LPI-RREG @ _LPI-ROOT @ LPR.RREG !
    _LPI-BUS @ _LPI-ROOT @ LPR.BUS !
    1 _LPI-ROOT @ LPR.NEXT-GENERATION !
    1 _LPI-ROOT @ LPR.NEXT-COOKIE !
    LIBRARY-PROJECTION-OWNER$ _LPI-ROOT @
        ['] _LPR-ROOT-ACQUIRE ['] _LPR-ROOT-QUIESCENT
        ['] _LPR-ROOT-RELEASE _LPI-ROOT @ LPR.RACQ RACQ-ROOT-INIT
    DUP RACQ-S-OK <> IF
        DROP _LPI-ROOT @ LIBRARY-PROJECTION-ROOT-SIZE 0 FILL
        RACQ-S-INVALID EXIT
    THEN DROP
    _LPI-ROOT @ LIBRARY-PROJECTION-ROOT-VALID? 0= IF
        _LPI-ROOT @ LIBRARY-PROJECTION-ROOT-SIZE 0 FILL
        RACQ-S-INVALID EXIT
    THEN
    RACQ-S-OK ;

