: _LPR-QUIESCENT-CHECK  ( -- status )
    ['] _LPR-QUIESCENT-CHECK-LOCKED _LPR-GUARD WITH-GUARD ;

\ RACQ invokes owner callbacks while holding its global guard.  Never wait
\ for the dispatch guard from that lock order: an unrelated dispatch may be
\ waiting to enter RACQ.  Contention is a retryable release failure, and the
\ scoped form still releases the bus guard if the body throws.
: _LPR-CBUS-TRY-STATUS  ( xt -- status )
    _CBUS-DISPATCH-GUARD GUARD-TRY-ACQUIRE 0= IF
        DROP RACQ-S-RELEASE-FAILED EXIT
    THEN
    CATCH
    _CBUS-DISPATCH-GUARD GUARD-RELEASE
    ?DUP IF DROP RACQ-S-RELEASE-FAILED THEN ;

: _LPR-ROOT-QUIESCENT  ( token root -- status )
    _LPQ-ROOT ! _LPQ-TOKEN !
    CBUS-DISPATCHING? IF RACQ-S-RELEASE-FAILED EXIT THEN
    ['] _LPR-QUIESCENT-PREP-LOCKED _LPR-GUARD WITH-GUARD
    DUP RACQ-S-OK <> IF EXIT THEN DROP
    _LPQ-FINAL @ 0= IF RACQ-S-OK EXIT THEN
    ['] _LPR-QUIESCENT-CHECK _LPR-CBUS-TRY-STATUS ;

