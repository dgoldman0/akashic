: MPTLS-HOST  ( adapter -- addr len )
    DUP MPTLS.HOST-BUF SWAP MPTLS.HOST-U @ ;

: _MPTLS-DNS-DEFAULT  ( host-a host-u adapter -- ip | 0 )
    DROP DNS-RESOLVE ;

: _MPTLS-CONNECT-DEFAULT  ( ip remote-port local-port adapter -- ctx | 0 )
    DROP TLS-ALPN-HTTP11 TLS-CONNECT-ALPN ;

: _MPTLS-SEND-DEFAULT  ( ctx buffer length adapter -- count )
    DROP TLS-SEND ;

: _MPTLS-RECV-DEFAULT  ( ctx buffer capacity adapter -- count | -1 )
    DROP TLS-RECV ;

: _MPTLS-CLOSE-DEFAULT  ( ctx adapter -- )
    DROP TLS-CLOSE ;

: _MPTLS-POLL-DEFAULT  ( adapter -- )
    DROP TCP-POLL NET-IDLE ;

: _MPTLS-STATUS-DEFAULT  ( ctx adapter -- link-status )
    DROP
    DUP 0= IF DROP MPTLS-LINK-FAILED EXIT THEN
    DUP TLS-CTX.ERROR @ TLS-E-OK <> IF DROP MPTLS-LINK-FAILED EXIT THEN
    DUP TLS-CTX.STATE @ TLSS-ESTABLISHED =
    OVER TLS-CTX.PEER-AUTH @ 1 = AND IF
        DROP MPTLS-LINK-OPEN EXIT
    THEN
    TLS-CTX.STATE @ DUP TLSS-CLOSING = SWAP TLSS-NONE = OR IF
        MPTLS-LINK-CLOSED
    ELSE
        MPTLS-LINK-FAILED
    THEN ;

VARIABLE _MPTLS-OWNER
VARIABLE _MPTLS-NEXT-PORT
0 _MPTLS-OWNER !
RANDOM32 16383 AND 49152 + _MPTLS-NEXT-PORT !

: _MPTLS-NEXT-LOCAL-PORT  ( -- port )
    _MPTLS-NEXT-PORT @
    DUP 1+ DUP 65535 > IF DROP 49152 THEN _MPTLS-NEXT-PORT ! ;

: _MPTLS-MARK-ERROR  ( error adapter -- )
    >R R@ MPTLS.LAST-ERROR ! MPTLS-STATE-ERROR R> MPTLS.STATE ! ;

: MPTLS-LINK@  ( adapter -- link-status )
    DUP MPTLS.CONTEXT @ SWAP DUP MPTLS.STATUS-XT @ EXECUTE ;

VARIABLE _MPTC-A

: _MPTLS-CLOSE-INNER  ( -- )
    _MPTC-A @ MPTLS.CONTEXT @ ?DUP IF
        _MPTC-A @ _MPTC-A @ MPTLS.CLOSE-XT @ EXECUTE
    THEN ;

: _MPTLS-RELEASE  ( adapter -- )
    DUP _MPTC-A !
    ['] _MPTLS-CLOSE-INNER CATCH DROP
    0 OVER MPTLS.CONTEXT !
    0 OVER MPTLS.LOCAL-PORT !
    MPTLS-STATE-CLOSED OVER MPTLS.STATE !
    _MPTLS-OWNER @ OVER = IF 0 _MPTLS-OWNER ! THEN
    DROP ;

VARIABLE _MPTO-A
VARIABLE _MPTO-IP
VARIABLE _MPTO-CTX

: _MPTLS-OPEN-FAIL  ( error -- nio-status )
    _MPTO-A @ _MPTLS-RELEASE
    _MPTO-A @ _MPTLS-MARK-ERROR
    NIO-S-FAILED ;

: _MPTLS-OPEN-INNER  ( -- nio-status )
    _MPTO-A @ MPTLS.STATE @ MPTLS-STATE-OPEN = IF NIO-S-OK EXIT THEN
    _MPTO-A @ MPTLS.HOST-U @ 0= IF MPTLS-E-INVALID _MPTLS-OPEN-FAIL EXIT THEN
    _MPTLS-OWNER @ ?DUP IF
        _MPTO-A @ <> IF MPTLS-E-BUSY _MPTLS-OPEN-FAIL EXIT THEN
    THEN
    TLS-TRUST-COUNT @ 0= IF MPTLS-E-NO-TRUST _MPTLS-OPEN-FAIL EXIT THEN
    _MPTO-A @ _MPTLS-OWNER !
    MPTLS-STATE-OPENING _MPTO-A @ MPTLS.STATE !
    MPTLS-E-OK _MPTO-A @ MPTLS.LAST-ERROR !

    TLS-SNI-HOST MPTLS-HOST-CAPACITY 0 FILL
    _MPTO-A @ MPTLS.HOST-U @ DUP TLS-SNI-LEN !
    _MPTO-A @ MPTLS.HOST-BUF TLS-SNI-HOST ROT CMOVE

    _MPTO-A @ MPTLS-HOST _MPTO-A @
    _MPTO-A @ MPTLS.DNS-XT @ EXECUTE DUP _MPTO-IP !
    0= IF MPTLS-E-DNS _MPTLS-OPEN-FAIL EXIT THEN

    _MPTLS-NEXT-LOCAL-PORT DUP _MPTO-A @ MPTLS.LOCAL-PORT !
    _MPTO-IP @ _MPTO-A @ MPTLS.REMOTE-PORT @ ROT _MPTO-A @
    _MPTO-A @ MPTLS.CONNECT-XT @ EXECUTE DUP _MPTO-CTX !
    0= IF MPTLS-E-CONNECT _MPTLS-OPEN-FAIL EXIT THEN
    _MPTO-CTX @ _MPTO-A @ MPTLS.CONTEXT !
    _MPTO-A @ MPTLS-LINK@ MPTLS-LINK-OPEN <> IF
        MPTLS-E-AUTH _MPTLS-OPEN-FAIL EXIT
    THEN
    MPTLS-STATE-OPEN _MPTO-A @ MPTLS.STATE !
    NIO-S-OK ;

: _MPTLS-NIO-OPEN  ( adapter -- nio-status )
    _MPTO-A !
    ['] _MPTLS-OPEN-INNER CATCH ?DUP IF
        DROP _MPTO-A @ _MPTLS-RELEASE
        MPTLS-E-FAULT _MPTO-A @ _MPTLS-MARK-ERROR
        NIO-S-FAILED
    THEN ;

VARIABLE _MPTS-A
VARIABLE _MPTS-B
VARIABLE _MPTS-U
VARIABLE _MPTS-N

: _MPTLS-NIO-SEND  ( buffer length adapter -- count nio-status )
    _MPTS-A ! _MPTS-U ! _MPTS-B !
    _MPTS-A @ MPTLS.STATE @ MPTLS-STATE-OPEN <> IF
        0 NIO-S-FAILED EXIT
    THEN
    _MPTS-U @ 0< _MPTS-B @ 0= _MPTS-U @ 0> AND OR IF
        MPTLS-E-INVALID _MPTS-A @ _MPTLS-MARK-ERROR
        0 NIO-S-FAILED EXIT
    THEN
    _MPTS-U @ 0= IF 0 NIO-S-OK EXIT THEN
    _MPTS-A @ MPTLS.CONTEXT @ _MPTS-B @ _MPTS-U @ _MPTS-A @
    _MPTS-A @ MPTLS.SEND-XT @ EXECUTE DUP _MPTS-N !
    0< _MPTS-N @ _MPTS-U @ > OR IF
        MPTLS-E-IO _MPTS-A @ _MPTLS-MARK-ERROR
        0 NIO-S-FAILED EXIT
    THEN
    _MPTS-N @ 0= IF
        _MPTS-A @ MPTLS-LINK@ MPTLS-LINK-OPEN <> IF
            MPTLS-E-IO _MPTS-A @ _MPTLS-MARK-ERROR
            0 NIO-S-FAILED EXIT
        THEN
    THEN
    _MPTS-N @ NIO-S-OK ;

VARIABLE _MPTR-A
VARIABLE _MPTR-B
VARIABLE _MPTR-U
VARIABLE _MPTR-N
VARIABLE _MPTR-LINK

: _MPTLS-NIO-RECV  ( buffer capacity adapter -- count nio-status )
    _MPTR-A ! _MPTR-U ! _MPTR-B !
    _MPTR-A @ MPTLS.STATE @ MPTLS-STATE-OPEN <> IF
        0 NIO-S-FAILED EXIT
    THEN
    _MPTR-U @ 0> 0= _MPTR-B @ 0= OR IF
        MPTLS-E-INVALID _MPTR-A @ _MPTLS-MARK-ERROR
        0 NIO-S-FAILED EXIT
    THEN
    _MPTR-A @ MPTLS.CONTEXT @ _MPTR-B @ _MPTR-U @ _MPTR-A @
    _MPTR-A @ MPTLS.RECV-XT @ EXECUTE DUP _MPTR-N !
    0< IF
        MPTLS-E-IO _MPTR-A @ _MPTLS-MARK-ERROR
        0 NIO-S-FAILED EXIT
    THEN
    _MPTR-N @ _MPTR-U @ > IF
        MPTLS-E-IO _MPTR-A @ _MPTLS-MARK-ERROR
        0 NIO-S-FAILED EXIT
    THEN
    _MPTR-N @ IF _MPTR-N @ NIO-S-OK EXIT THEN
    _MPTR-A @ MPTLS-LINK@ DUP _MPTR-LINK !
    MPTLS-LINK-OPEN = IF 0 NIO-S-OK EXIT THEN
    _MPTR-LINK @ MPTLS-LINK-CLOSED = IF
        MPTLS-STATE-CLOSED _MPTR-A @ MPTLS.STATE !
        0 NIO-S-EOF EXIT
    THEN
    MPTLS-E-IO _MPTR-A @ _MPTLS-MARK-ERROR
    0 NIO-S-FAILED ;

: _MPTLS-NIO-POLL  ( adapter -- )
    DUP MPTLS.POLL-XT @ EXECUTE ;

: _MPTLS-NIO-CLOSE  ( adapter -- )
    _MPTLS-RELEASE ;

: MPTLS-INIT  ( adapter -- )
    DUP MPTLS-SIZE 0 FILL
    DUP MPTLS.PORT NIO-INIT
    DUP OVER MPTLS.PORT NIO.CONTEXT !
    ['] _MPTLS-NIO-OPEN OVER MPTLS.PORT NIO.OPEN-XT !
    ['] _MPTLS-NIO-SEND OVER MPTLS.PORT NIO.SEND-XT !
    ['] _MPTLS-NIO-RECV OVER MPTLS.PORT NIO.RECV-XT !
    ['] _MPTLS-NIO-POLL OVER MPTLS.PORT NIO.POLL-XT !
    ['] _MPTLS-NIO-CLOSE OVER MPTLS.PORT NIO.CLOSE-XT !
    ['] _MPTLS-DNS-DEFAULT OVER MPTLS.DNS-XT !
    ['] _MPTLS-CONNECT-DEFAULT OVER MPTLS.CONNECT-XT !
    ['] _MPTLS-SEND-DEFAULT OVER MPTLS.SEND-XT !
    ['] _MPTLS-RECV-DEFAULT OVER MPTLS.RECV-XT !
    ['] _MPTLS-CLOSE-DEFAULT OVER MPTLS.CLOSE-XT !
    ['] _MPTLS-POLL-DEFAULT OVER MPTLS.POLL-XT !
    ['] _MPTLS-STATUS-DEFAULT SWAP MPTLS.STATUS-XT ! ;

VARIABLE _MPTCFG-A
VARIABLE _MPTCFG-U
VARIABLE _MPTCFG-PORT
VARIABLE _MPTCFG-T

: MPTLS-CONFIGURE  ( host-a host-u remote-port adapter -- status )
    _MPTCFG-T ! _MPTCFG-PORT ! _MPTCFG-U ! _MPTCFG-A !
    _MPTCFG-T @ MPTLS.STATE @ MPTLS-STATE-CLOSED <> IF
        MPTLS-E-BUSY EXIT
    THEN
    _MPTCFG-U @ 0= _MPTCFG-U @ MPTLS-HOST-CAPACITY > OR
    _MPTCFG-A @ 0= OR IF MPTLS-E-INVALID EXIT THEN
    _MPTCFG-A @ _MPTCFG-U @ FALSE DNS-NAME-VALID? 0= IF
        MPTLS-E-INVALID EXIT
    THEN
    _MPTCFG-PORT @ 1 < _MPTCFG-PORT @ 65535 > OR IF
        MPTLS-E-INVALID EXIT
    THEN
    _MPTCFG-T @ MPTLS.HOST-BUF MPTLS-HOST-CAPACITY 0 FILL
    _MPTCFG-A @ _MPTCFG-T @ MPTLS.HOST-BUF _MPTCFG-U @ CMOVE
    _MPTCFG-U @ _MPTCFG-T @ MPTLS.HOST-U !
    _MPTCFG-PORT @ _MPTCFG-T @ MPTLS.REMOTE-PORT !
    MPTLS-E-OK _MPTCFG-T @ MPTLS.LAST-ERROR !
    MPTLS-E-OK ;

VARIABLE _MPTNEW-T
VARIABLE _MPTNEW-STATUS

: MPTLS-NEW  ( host-a host-u remote-port -- adapter status )
    >R MPTLS-SIZE ALLOCATE
    DUP IF >R DROP 2DROP R> DROP R> DROP 0 MPTLS-E-INVALID EXIT THEN
    DROP DUP _MPTNEW-T ! MPTLS-INIT
    R> _MPTNEW-T @ MPTLS-CONFIGURE DUP _MPTNEW-STATUS !
    IF _MPTNEW-T @ FREE 0 _MPTNEW-STATUS @ EXIT THEN
    _MPTNEW-T @ MPTLS-E-OK ;

: MPTLS-FREE  ( adapter -- )
    DUP 0= IF DROP EXIT THEN
    DUP MPTLS.PORT NIO-CLOSE
    DUP MPTLS-SIZE 0 FILL
    FREE ;

