: KDOSTLS-CONFIGURE  ( host-a host-u remote-port adapter -- status )
    _KDTCFG-T ! _KDTCFG-PORT ! _KDTCFG-U ! _KDTCFG-A !
    _KDTCFG-T @ KDOSTLS.STATE @ KDOSTLS-STATE-CLOSED <> IF
        KDOSTLS-E-BUSY EXIT
    THEN
    _KDTCFG-U @ 0= _KDTCFG-U @ KDOSTLS-HOST-CAPACITY > OR
    _KDTCFG-A @ 0= OR IF KDOSTLS-E-INVALID EXIT THEN
    _KDTCFG-A @ _KDTCFG-U @ FALSE DNS-NAME-VALID? 0= IF
        KDOSTLS-E-INVALID EXIT
    THEN
    _KDTCFG-PORT @ 1 < _KDTCFG-PORT @ 65535 > OR IF
        KDOSTLS-E-INVALID EXIT
    THEN
    _KDTCFG-T @ KDOSTLS.HOST-BUF KDOSTLS-HOST-CAPACITY 0 FILL
    _KDTCFG-A @ _KDTCFG-T @ KDOSTLS.HOST-BUF _KDTCFG-U @ CMOVE
    _KDTCFG-U @ _KDTCFG-T @ KDOSTLS.HOST-U !
    _KDTCFG-PORT @ _KDTCFG-T @ KDOSTLS.REMOTE-PORT !
    KDOSTLS-E-OK _KDTCFG-T @ KDOSTLS.LAST-ERROR !
    KDOSTLS-E-OK ;

