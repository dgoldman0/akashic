    _KDCP-A @ KDOSTLS.CONTEXT @ _KDOSTLS-HS-PROCESS-ONE
    DUP _KDOSTLS-HS-S-FAILED = IF
        DROP TLS-CONNECT-E-HANDSHAKE-PROCESS
            _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
