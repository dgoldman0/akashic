VARIABLE _KDTC-POLLS

: _KDOSTLS-NIO-CLOSE-BOUNDED  ( adapter poll-max -- )
    0 MAX _KDTC-POLLS !
    DUP _KDTC-A ! _KDOSTLS-NIO-CLOSE-START
    DUP NIO-S-OK = IF DROP EXIT THEN
    DUP NIO-S-PENDING <> IF DROP KDOSTLS-E-CLEANUP NEGATE THROW THEN DROP
    \ Each close poll already admits one frame.  NET-IDLE is not safe here:
    \ compatibility callers may have no enabled wake IRQ, defeating the cap.
    _KDTC-POLLS @ 0 DO
        _KDTC-A @ _KDOSTLS-NIO-CLOSE-POLL
        DUP NIO-S-OK = IF DROP UNLOOP EXIT THEN
        DUP NIO-S-PENDING <> IF
            DROP UNLOOP KDOSTLS-E-CLEANUP NEGATE THROW
        THEN DROP
    LOOP
    _KDTC-A @ _KDCL-A !
    _KDOSTLS-CLOSE-ABORT NIO-S-OK <> IF
        KDOSTLS-E-CLEANUP NEGATE THROW
    THEN ;

: _KDOSTLS-NIO-CLOSE  ( adapter -- )
    KDOSTLS-CLOSE-POLL-MAX _KDOSTLS-NIO-CLOSE-BOUNDED ;

