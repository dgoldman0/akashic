\ ---------------------------------------------------------------------
\ Cooperative connector
\ ---------------------------------------------------------------------
\ One open poll invokes one phase callback exactly once.  Network-wait phases
\ admit at most one frame; TLS handshake messages are reassembled globally but
\ processed one at a time while this adapter owns KDOS's shared TLS scratch.
\ Tests may replace the default callback with the same
\ ( phase adapter -- prep-status ) contract.

: _KDOSTLS-NOW-DEFAULT  ( adapter -- ms ) DROP MS@ ;

: _KDOSTLS-NOW@  ( adapter -- ms )
    DUP KDOSTLS.NOW-XT @ EXECUTE ;

: _KDOSTLS-TIME-REACHED?  ( now deadline -- flag )
    - 0>= ;

