    _KDRV-A @ _KDOSTLS-TCB@ DUP 0= IF DROP 0 EXIT THEN
    TCB.STATE @ DUP TCPS-ESTABLISHED =
    SWAP TCPS-CLOSE-WAIT = OR
    _KDRV-A @ KDOSTLS.CONTEXT @ TLS-CTX.STATE @ TLSS-ESTABLISHED = AND
    _KDRV-A @ KDOSTLS.CONTEXT @ TLS-CTX.PEER-AUTH @ 1 = AND ;

: _KDOSTLS-CLEAN-EOF?  ( adapter -- flag )
    KDOSTLS.CONTEXT @ DUP 0= IF DROP 0 EXIT THEN
    DUP TLS-CTX.STATE @ TLSS-CLOSING =
    OVER TLS-CTX.ERROR @ TLS-E-OK = AND
    SWAP TLS-CTX.PEER-AUTH @ 0= AND ;

