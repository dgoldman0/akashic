1 CONSTANT _KDOSTLS-HS-S-PROCESSED
2 CONSTANT _KDOSTLS-HS-S-FAILED

VARIABLE _KDHM-TOTAL
VARIABLE _KDHM-REMAINING

: _KDOSTLS-HS-PROCESS-ONE  ( ctx -- hs-status )
    TLS-HS-RBUF-ERROR @ IF DROP _KDOSTLS-HS-S-FAILED EXIT THEN
    TLS-HS-RBUF-LEN @ 4 < IF DROP _KDOSTLS-HS-S-INCOMPLETE EXIT THEN
    TLS-HS-RBUF 1+ _BE24@ 4 + DUP _KDHM-TOTAL !
    TLS-HS-RBUF-MAX > IF
        DROP -1 TLS-HS-RBUF-ERROR ! _KDOSTLS-HS-S-FAILED EXIT
    THEN
    TLS-HS-RBUF-LEN @ _KDHM-TOTAL @ < IF
        DROP _KDOSTLS-HS-S-INCOMPLETE EXIT
    THEN
    TLS-HS-RBUF _KDHM-TOTAL @ TLS-PROCESS-HS-MSG IF
        -1 TLS-HS-RBUF-ERROR ! _KDOSTLS-HS-S-FAILED EXIT
    THEN
    TLS-HS-RBUF-LEN @ _KDHM-TOTAL @ - _KDHM-REMAINING !
    TLS-HS-RBUF _KDHM-TOTAL @ + TLS-HS-RBUF
        _KDHM-REMAINING @ CMOVE
    _KDHM-REMAINING @ TLS-HS-RBUF-LEN !
    _KDOSTLS-HS-S-PROCESSED ;

VARIABLE _KDRL-U

: _KDOSTLS-STEP-SERVER-RECORD  ( -- prep-status )
    _KDCP-A @ _KDOSTLS-REMOTE-CACHED? 0= IF
        KDOSTLS-PHASE-SERVER-RECORD _KDOSTLS-REMOTE-ARP-FOR EXIT
    THEN
    _KDCP-A @ _KDOSTLS-TCB@ DUP 0= IF
        DROP TLS-CONNECT-E-SERVER-WAIT _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN _KDTC-TCB !
    _KDCP-A @ _KDOSTLS-TCP-RECEIVE-ONE
    _KDTC-TCB @ TLS-READ-RECORD-NB DUP 0= IF
        DROP TLS-RBUF-ERROR @ IF
            TLS-CONNECT-E-SERVER-RECORD _KDCP-A @ KDOSTLS.NATIVE-ERROR !
            KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
            KDOSTLS-PREP-S-FAILED EXIT
        THEN
        _KDTC-TCB @ TCB.STATE @ TCPS-ESTABLISHED = IF
            KDOSTLS-PREP-S-PENDING EXIT
        THEN
        TLS-CONNECT-E-SERVER-WAIT _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    _KDRL-U !
    TLS-RECV-REC C@ TLS-CT-HANDSHAKE <> IF
        TLS-CONNECT-E-SERVER-RECORD _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    TLS-RECV-REC 5 + _KDRL-U @ 5 - _KDOSTLS-HS-APPEND 0= IF
        TLS-CONNECT-E-SERVER-PROCESS _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    _KDRL-U @ TLS-RBUF-CONSUME
    KDOSTLS-PHASE-SERVER-MESSAGE _KDOSTLS-PHASE!
    KDOSTLS-PREP-S-PENDING ;

