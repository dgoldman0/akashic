: _KDOSTLS-TCB-POINTER?  ( tcb -- flag )
    _KDTM-TCB !
    TCP-TCBS @ DUP _KDTM-BASE ! 0= IF 0 EXIT THEN
    _KDTM-TCB @ _KDTM-BASE @ U< IF 0 EXIT THEN
    _KDTM-TCB @ _KDTM-BASE @ -
    DUP /TCB MOD 0= SWAP /TCB /TCP-MAX-CONN * U< AND ;

: _KDOSTLS-TCB-MATCH?  ( adapter -- flag )
    DUP _KDTM-A ! KDOSTLS.CONTEXT @ DUP 0= IF DROP 0 EXIT THEN
    DUP _KDTM-CTX ! TLS-CTX.TCB @ DUP 0= IF DROP 0 EXIT THEN
    DUP _KDOSTLS-TCB-POINTER? 0= IF DROP 0 EXIT THEN
    _KDTM-TCB !
    _KDTM-TCB @ TCB.LOCAL-PORT @ _KDTM-A @ KDOSTLS.LOCAL-PORT @ <>
    _KDTM-TCB @ TCB.REMOTE-PORT @
        _KDTM-A @ KDOSTLS.REMOTE-PORT @ <> OR
    _KDTM-TCB @ TCB.REMOTE-IP _KDTM-A @ KDOSTLS.REMOTE-IP IP= 0= OR
    _KDTM-TCB @ TCB.ISS @ _KDTM-A @ KDOSTLS.TCB-ISS @ <> OR 0= ;

: _KDOSTLS-ABORT-INNER  ( -- )
    _KDAD-A @ KDOSTLS.CONTEXT @ ?DUP IF
        _KDAD-A @ _KDOSTLS-TCB-MATCH? 0= IF
            0 OVER TLS-CTX.TCB !
        THEN
        TLS-ABORT _KDAD-A @ KDOSTLS.ABORT-STATUS !
    THEN ;

: _KDOSTLS-PREP-DETACH  ( adapter -- detached? )
    DUP _KDAD-A !
    ['] _KDOSTLS-ABORT-INNER CATCH ?DUP IF
        SWAP KDOSTLS.CLEANUP-ERROR ! 0 EXIT
    THEN
    DUP 0 SWAP KDOSTLS.CONTEXT !
    DUP 0 SWAP KDOSTLS.LOCAL-PORT !
    DUP _KDOSTLS-PREP-CLEAR-SCRATCH
    _KDOSTLS-OWNER @ OVER = IF 0 _KDOSTLS-OWNER ! THEN
    DROP -1 ;

