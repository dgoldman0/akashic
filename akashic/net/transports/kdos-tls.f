: _KDOSTLS-DNS-RECEIVE-ONE  ( adapter -- ip | 0 )
    _KDCP-A !
    IP-RECV DUP 0= IF 2DROP 0 EXIT THEN
    _KDCP-IP-LEN ! _KDCP-HDR !
    _KDCP-HDR @ IP-H.PROTO C@ IP-PROTO-UDP <> IF 0 EXIT THEN
    _KDCP-IP-LEN @ /IP-HDR - DUP /UDP-HDR < IF DROP 0 EXIT THEN
    _KDCP-HDR @ IP-H.DATA DUP _KDCP-UDP !
    UDP-H.LEN NW16@ DUP _KDCP-UDP-LEN ! <> IF 0 EXIT THEN
    _KDCP-HDR @ IP-H.SRC _KDCP-A @ KDOSTLS.DNS-SERVER IP= 0= IF
        0 EXIT
    THEN
    _KDCP-UDP @ UDP-H.SPORT NW16@ 53 <> IF 0 EXIT THEN
    _KDCP-UDP @ UDP-H.DPORT NW16@
        _KDCP-A @ KDOSTLS.DNS-PORT @ <> IF 0 EXIT THEN
    _KDCP-HDR @ IP-H.SRC _KDCP-HDR @ IP-H.DST
    _KDCP-UDP @ _KDCP-UDP-LEN @ UDP-VERIFY-CKSUM 0= IF 0 EXIT THEN
    _KDCP-UDP @ UDP-H.DATA
    _KDCP-UDP-LEN @ /UDP-HDR -
    2DUP _KDCP-A @ _KDOSTLS-DNS-MATCH? 0= IF 2DROP 0 EXIT THEN
    DNS-PARSE-RESPONSE ;

: _KDOSTLS-STEP-TLS-PREP  ( -- prep-status )
    TLS-SNI-HOST KDOSTLS-HOST-CAPACITY 0 FILL
    _KDCP-A @ KDOSTLS.HOST-U @ DUP TLS-SNI-LEN !
    _KDCP-A @ KDOSTLS.HOST-BUF TLS-SNI-HOST ROT CMOVE
    TLS-CTX-ALLOC DUP 0= IF
        DROP TLS-CONNECT-E-ALLOC _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    DUP /TLS-CTX 0 FILL DUP _KDCP-A @ KDOSTLS.CONTEXT !
    TLS-ALPN-HTTP11 OVER TLS-CTX.ALPN-PROFILE !
    TLS-HELLO-STANDARD SWAP TLS-CTX.HELLO-PROFILE !
    _KDCP-A @ KDOSTLS.CONTEXT @ TLS-BUILD-CLIENT-HELLO
    _KDCP-A @ KDOSTLS.CLIENT-HELLO-U !
    _KDCP-A @ KDOSTLS.CLIENT-HELLO-A !
    _KDCP-A @ KDOSTLS.CLIENT-HELLO-U @ 0= IF
        TLS-CONNECT-E-CLIENT-HELLO _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    KDOSTLS-PHASE-DNS-ARP-CHECK _KDOSTLS-PHASE!
    KDOSTLS-PREP-S-PENDING ;

: _KDOSTLS-STEP-DNS-ARP-CHECK  ( -- prep-status )
    _KDCP-A @ KDOSTLS.DNS-SERVER NEXT-HOP
        _KDCP-A @ KDOSTLS.ARP-TARGET 4 CMOVE
    _KDCP-A @ _KDOSTLS-ARP-READY? IF
        KDOSTLS-PHASE-DNS-BUILD
    ELSE
        0 _KDCP-A @ KDOSTLS.RETRIES !
        KDOSTLS-PHASE-DNS-ARP-SEND
    THEN _KDOSTLS-PHASE!
    KDOSTLS-PREP-S-PENDING ;

: _KDOSTLS-STEP-ARP-SEND  ( wait-phase -- prep-status )
    _KDCP-A @ KDOSTLS.RETRIES @ KDOSTLS-PREP-RETRY-MAX >= IF
        DROP KDOSTLS-E-TIMEOUT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    _KDCP-A @ KDOSTLS.ARP-TARGET ARP-SEND-REQUEST
    1 _KDCP-A @ KDOSTLS.RETRIES +!
    KDOSTLS-ARP-RETRY-MS _KDCP-A @ _KDOSTLS-RETRY-AFTER!
    _KDOSTLS-PHASE! KDOSTLS-PREP-S-PENDING ;

: _KDOSTLS-STEP-DNS-ARP-WAIT  ( -- prep-status )
    _KDOSTLS-ARP-RECEIVE-ONE
    _KDCP-A @ _KDOSTLS-ARP-READY? IF
        KDOSTLS-PHASE-DNS-BUILD _KDOSTLS-PHASE!
    ELSE
        _KDCP-A @ _KDOSTLS-RETRY-DUE? IF
            KDOSTLS-PHASE-DNS-ARP-SEND _KDOSTLS-PHASE!
        THEN
    THEN KDOSTLS-PREP-S-PENDING ;

: _KDOSTLS-STEP-DNS-BUILD  ( -- prep-status )
    _KDCP-A @ KDOSTLS-HOST DNS-BUILD-QUERY
    DUP 0= IF
        2DROP KDOSTLS-E-DNS _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    DUP KDOSTLS-DNS-QUERY-CAPACITY > IF
        2DROP KDOSTLS-E-DNS _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    DUP _KDCP-A @ KDOSTLS.DNS-QUERY-U !
    _KDCP-A @ KDOSTLS.DNS-QUERY SWAP CMOVE
    _KDOSTLS-NEXT-LOCAL-PORT _KDCP-A @ KDOSTLS.DNS-PORT !
    0 _KDCP-A @ KDOSTLS.RETRIES !
    KDOSTLS-PHASE-DNS-SEND _KDOSTLS-PHASE!
    KDOSTLS-PREP-S-PENDING ;

: _KDOSTLS-STEP-DNS-SEND  ( -- prep-status )
    \ UDP-SEND reaches IP-SEND's blocking ARP fallback.  The connector owns
    \ cache warming, so revalidate immediately before entering that leaf.
    _KDCP-A @ KDOSTLS.DNS-SERVER NEXT-HOP ARP-LOOKUP 0= IF
        KDOSTLS-PHASE-DNS-ARP-CHECK _KDOSTLS-PHASE!
        KDOSTLS-PREP-S-PENDING EXIT
    THEN
    _KDCP-A @ KDOSTLS.RETRIES @ KDOSTLS-PREP-RETRY-MAX >= IF
        KDOSTLS-E-TIMEOUT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    _KDCP-A @ KDOSTLS.DNS-SERVER 53
    _KDCP-A @ KDOSTLS.DNS-PORT @
    _KDCP-A @ KDOSTLS.DNS-QUERY
    _KDCP-A @ KDOSTLS.DNS-QUERY-U @ UDP-SEND IF
        KDOSTLS-E-DNS _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    1 _KDCP-A @ KDOSTLS.RETRIES +!
    KDOSTLS-DNS-RETRY-MS _KDCP-A @ _KDOSTLS-RETRY-AFTER!
    KDOSTLS-PHASE-DNS-WAIT _KDOSTLS-PHASE!
    KDOSTLS-PREP-S-PENDING ;

: _KDOSTLS-STEP-DNS-WAIT  ( -- prep-status )
    _KDCP-A @ _KDOSTLS-DNS-RECEIVE-ONE ?DUP IF
        _KDCP-A @ KDOSTLS.REMOTE-IP 4 CMOVE
        KDOSTLS-PHASE-DNS-ADMIT _KDOSTLS-PHASE!
    ELSE
        _KDCP-A @ _KDOSTLS-RETRY-DUE? IF
            KDOSTLS-PHASE-DNS-SEND _KDOSTLS-PHASE!
        THEN
    THEN KDOSTLS-PREP-S-PENDING ;

: _KDOSTLS-STEP-DNS-ADMIT  ( -- prep-status )
    _KDCP-A @ _KDOSTLS-ADDRESS-ADMITTED? 0= IF
        KDOSTLS-E-ADDRESS _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    KDOSTLS-PHASE-TCP-OPEN _KDCP-A @ KDOSTLS.AFTER-ARP-PHASE !
    KDOSTLS-PHASE-REMOTE-ARP-CHECK _KDOSTLS-PHASE!
    KDOSTLS-PREP-S-PENDING ;

: _KDOSTLS-STEP-REMOTE-ARP-CHECK  ( -- prep-status )
    _KDCP-A @ KDOSTLS.REMOTE-IP NEXT-HOP
        _KDCP-A @ KDOSTLS.ARP-TARGET 4 CMOVE
    _KDCP-A @ _KDOSTLS-ARP-READY? IF
        KDOSTLS-PHASE-REMOTE-ARP-READY _KDOSTLS-PHASE!
        KDOSTLS-PREP-S-PENDING
    ELSE
        0 _KDCP-A @ KDOSTLS.RETRIES !
        KDOSTLS-PHASE-REMOTE-ARP-SEND _KDOSTLS-PHASE!
        KDOSTLS-PREP-S-PENDING
    THEN ;

: _KDOSTLS-STEP-REMOTE-ARP-WAIT  ( -- prep-status )
    _KDOSTLS-ARP-RECEIVE-ONE
    _KDCP-A @ _KDOSTLS-ARP-READY? IF
        KDOSTLS-PHASE-REMOTE-ARP-READY _KDOSTLS-PHASE!
        KDOSTLS-PREP-S-PENDING
    ELSE
        _KDCP-A @ _KDOSTLS-RETRY-DUE? IF
            KDOSTLS-PHASE-REMOTE-ARP-SEND _KDOSTLS-PHASE!
        THEN
        KDOSTLS-PREP-S-PENDING
    THEN ;

: _KDOSTLS-STEP-REMOTE-ARP-READY  ( -- prep-status )
    _KDCP-A @ KDOSTLS.AFTER-ARP-PHASE @ DUP 0= IF
        DROP KDOSTLS-E-INVALID _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    _KDOSTLS-PHASE! KDOSTLS-PREP-S-PENDING ;

VARIABLE _KDTCP-A
VARIABLE _KDTCP-HDR
VARIABLE _KDTCP-LEN

: _KDOSTLS-REMOTE-CACHED?  ( adapter -- flag )
    KDOSTLS.REMOTE-IP NEXT-HOP ARP-LOOKUP 0<> ;

: _KDOSTLS-TCP-RECEIVE-ONE  ( adapter -- )
    _KDTCP-A !
    \ TCP-INPUT may emit an ACK through IP-SEND.  Never enter it unless this
    \ adapter's remote route is still on the cached, nonblocking path.
    _KDTCP-A @ _KDOSTLS-REMOTE-CACHED? 0= IF EXIT THEN
    IP-RECV DUP 0= IF 2DROP EXIT THEN
    _KDTCP-LEN ! _KDTCP-HDR !
    _KDTCP-HDR @ IP-H.PROTO C@ IP-PROTO-TCP = IF
        \ An unmatched segment can make TCP-INPUT emit a RST to its source.
        \ Prove that source route is cached before entering any reply path.
        _KDTCP-HDR @ IP-H.SRC NEXT-HOP ARP-LOOKUP 0= IF EXIT THEN
        _KDTCP-HDR @ _KDTCP-LEN @ TCP-INPUT
    THEN ;

\ Cooperative port polling must return before the caller's send/receive step.
\ The source-route guard above keeps every TCP-INPUT reply nonblocking.
: _KDOSTLS-POLL-DEFAULT  ( adapter -- )
    _KDOSTLS-TCP-RECEIVE-ONE ;

: _KDOSTLS-TCB@  ( adapter -- tcb | 0 )
    DUP _KDOSTLS-TCB-MATCH? 0= IF DROP 0 EXIT THEN
    KDOSTLS.CONTEXT @ TLS-CTX.TCB @ ;

: _KDOSTLS-REMOTE-ARP-FOR  ( resume-phase -- prep-status )
    DUP _KDCP-A @ KDOSTLS.AFTER-ARP-PHASE !
    _KDCP-A @ KDOSTLS.REMOTE-IP NEXT-HOP
        _KDCP-A @ KDOSTLS.ARP-TARGET 4 CMOVE
    _KDCP-A @ _KDOSTLS-ARP-READY? IF
        _KDOSTLS-PHASE!
    ELSE
        DROP 0 _KDCP-A @ KDOSTLS.RETRIES !
        KDOSTLS-PHASE-REMOTE-ARP-SEND _KDOSTLS-PHASE!
    THEN
    KDOSTLS-PREP-S-PENDING ;

VARIABLE _KDTC-TCB

: _KDOSTLS-STEP-TCP-OPEN  ( -- prep-status )
    _KDCP-A @ KDOSTLS.REMOTE-IP NEXT-HOP ARP-LOOKUP DUP 0= IF
        DROP KDOSTLS-PHASE-TCP-OPEN _KDOSTLS-REMOTE-ARP-FOR EXIT
    THEN DROP
    _KDOSTLS-NEXT-LOCAL-PORT _KDCP-A @ KDOSTLS.LOCAL-PORT !
    _KDCP-A @ KDOSTLS.REMOTE-IP
    _KDCP-A @ KDOSTLS.REMOTE-PORT @
    _KDCP-A @ KDOSTLS.LOCAL-PORT @ TCP-CONNECT DUP 0= IF
        DROP TLS-CONNECT-E-TCP-OPEN _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    DUP _KDTC-TCB ! _KDCP-A @ KDOSTLS.CONTEXT @ TLS-CTX.TCB !
    _KDTC-TCB @ TCB.ISS @ _KDCP-A @ KDOSTLS.TCB-ISS !
    KDOSTLS-PHASE-TCP-WAIT _KDOSTLS-PHASE!
    KDOSTLS-PREP-S-PENDING ;

: _KDOSTLS-STEP-TCP-WAIT  ( -- prep-status )
    _KDCP-A @ _KDOSTLS-REMOTE-CACHED? 0= IF
        KDOSTLS-PHASE-TCP-WAIT _KDOSTLS-REMOTE-ARP-FOR EXIT
    THEN
    _KDCP-A @ _KDOSTLS-TCP-RECEIVE-ONE
    _KDCP-A @ _KDOSTLS-TCB@ DUP 0= IF
        DROP TLS-CONNECT-E-TCP-ESTABLISH _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    TCB.STATE @ DUP TCPS-ESTABLISHED = IF
        DROP KDOSTLS-PHASE-CLIENT-HELLO-SEND _KDOSTLS-PHASE!
        KDOSTLS-PREP-S-PENDING EXIT
    THEN
    TCPS-SYN-SENT = IF KDOSTLS-PREP-S-PENDING EXIT THEN
    TLS-CONNECT-E-TCP-ESTABLISH _KDCP-A @ KDOSTLS.NATIVE-ERROR !
    KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
    KDOSTLS-PREP-S-FAILED ;

: _KDOSTLS-STEP-CLIENT-HELLO-SEND  ( -- prep-status )
    _KDCP-A @ KDOSTLS.REMOTE-IP NEXT-HOP ARP-LOOKUP DUP 0= IF
        DROP KDOSTLS-PHASE-CLIENT-HELLO-SEND
            _KDOSTLS-REMOTE-ARP-FOR EXIT
    THEN DROP
    _KDCP-A @ _KDOSTLS-TCB@ DUP 0= IF
        DROP TLS-CONNECT-E-CLIENT-SEND _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    DUP TCP-SEND-READY? 0= IF
        TCB.STATE @ TCPS-ESTABLISHED = IF
            _KDCP-A @ _KDOSTLS-TCP-RECEIVE-ONE
            KDOSTLS-PREP-S-PENDING EXIT
        THEN
        TLS-CONNECT-E-CLIENT-SEND _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    _KDCP-A @ KDOSTLS.CLIENT-HELLO-A @
    _KDCP-A @ KDOSTLS.CLIENT-HELLO-U @ TCP-SEND
    _KDCP-A @ KDOSTLS.CLIENT-HELLO-U @ <> IF
        TLS-CONNECT-E-CLIENT-SEND _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    TLS-RBUF-RESET
    KDOSTLS-PHASE-SERVER-RECORD _KDOSTLS-PHASE!
    KDOSTLS-PREP-S-PENDING ;

