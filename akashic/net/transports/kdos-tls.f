VARIABLE _KDPS-A

: KDOSTLS-PREP-START  ( adapter -- prep-status )
    _KDPS-A !
    _KDPS-A @ KDOSTLS.STATE @ KDOSTLS-STATE-CLOSED <> IF
        KDOSTLS-E-BUSY _KDPS-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    _KDPS-A @ KDOSTLS.HOST-U @ 0= IF
        KDOSTLS-E-INVALID _KDPS-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    _KDOSTLS-OWNER @ ?DUP IF
        _KDPS-A @ <> IF
            KDOSTLS-E-BUSY _KDPS-A @ KDOSTLS.LAST-ERROR !
            KDOSTLS-PREP-S-FAILED EXIT
        THEN
    THEN
    TLS-TRUST-COUNT @ 0= IF
        KDOSTLS-E-NO-TRUST _KDPS-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    _KDPS-A @ _KDOSTLS-PREP-CLEAR-SCRATCH
    0 _KDPS-A @ KDOSTLS.CLEANUP-ERROR !
    0 _KDPS-A @ KDOSTLS.STEP-COUNT !
    0 _KDPS-A @ KDOSTLS.LAST-STEP-CYCLES !
    0 _KDPS-A @ KDOSTLS.MAX-STEP-CYCLES !
    TLS-ABORT-S-NONE _KDPS-A @ KDOSTLS.ABORT-STATUS !
    _KDPS-A @ KDOSTLS.GENERATION @ 1+ DUP 0= IF DROP 1 THEN
        _KDPS-A @ KDOSTLS.GENERATION !
    TLS-TRUST-GENERATION @ _KDPS-A @ KDOSTLS.TRUST-GENERATION !
    DNS-SERVER-IP _KDPS-A @ KDOSTLS.DNS-SERVER 4 CMOVE
    _KDPS-A @ _KDOSTLS-NOW@ KDOSTLS-PREP-TIMEOUT-MS +
        _KDPS-A @ KDOSTLS.DEADLINE-MS !
    KDOSTLS-E-OK _KDPS-A @ KDOSTLS.LAST-ERROR !
    TLS-CONNECT-E-OK _KDPS-A @ KDOSTLS.NATIVE-ERROR !
    _KDPS-A @ _KDOSTLS-OWNER !
    KDOSTLS-STATE-OPENING _KDPS-A @ KDOSTLS.STATE !
    KDOSTLS-PHASE-TLS-PREP _KDPS-A @ KDOSTLS.PHASE !
    KDOSTLS-PREP-S-PENDING ;

: KDOSTLS-PREP-CANCEL  ( adapter -- prep-status )
    DUP KDOSTLS.STATE @ KDOSTLS-STATE-CLOSED =
    OVER KDOSTLS.CONTEXT @ 0= AND
    _KDOSTLS-OWNER @ 2 PICK <> AND IF
        DROP KDOSTLS-PREP-S-CANCELLED EXIT
    THEN
    DUP _KDOSTLS-PREP-DETACH 0= IF
        DUP KDOSTLS-E-CLEANUP SWAP KDOSTLS.LAST-ERROR !
        DUP KDOSTLS-PHASE-FAILED SWAP KDOSTLS.PHASE !
        KDOSTLS-STATE-ERROR SWAP KDOSTLS.STATE !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    DUP KDOSTLS-E-CANCELLED SWAP KDOSTLS.LAST-ERROR !
    DUP KDOSTLS-PHASE-CANCELLED SWAP KDOSTLS.PHASE !
    KDOSTLS-STATE-CLOSED SWAP KDOSTLS.STATE !
    KDOSTLS-PREP-S-CANCELLED ;

VARIABLE _KDPP-A

: _KDOSTLS-PREP-POLL-INNER  ( -- prep-status )
    _KDPP-A @ KDOSTLS.PHASE @ _KDPP-A @
    _KDPP-A @ KDOSTLS.COOP-STEP-XT @ EXECUTE ;

: _KDOSTLS-PREP-STEP-RECORD  ( cycles adapter -- )
    >R DUP R@ KDOSTLS.LAST-STEP-CYCLES !
    DUP R@ KDOSTLS.MAX-STEP-CYCLES @ > IF
        R@ KDOSTLS.MAX-STEP-CYCLES !
    ELSE
        DROP
    THEN
    1 R> KDOSTLS.STEP-COUNT +! ;

: KDOSTLS-PREP-POLL  ( adapter -- prep-status )
    DUP _KDPP-A !
    DUP KDOSTLS.STATE @ KDOSTLS-STATE-OPENING <>
    _KDOSTLS-OWNER @ 2 PICK <> OR IF
        DROP KDOSTLS-E-INVALID _KDPP-A @ _KDOSTLS-PREP-FAIL EXIT
    THEN DROP
    TLS-TRUST-GENERATION @ _KDPP-A @ KDOSTLS.TRUST-GENERATION @ <> IF
        KDOSTLS-E-TRUST-CHANGED _KDPP-A @ _KDOSTLS-PREP-FAIL EXIT
    THEN
    _KDPP-A @ _KDOSTLS-NOW@
    _KDPP-A @ KDOSTLS.DEADLINE-MS @ _KDOSTLS-TIME-REACHED? IF
        KDOSTLS-E-TIMEOUT _KDPP-A @ _KDOSTLS-PREP-FAIL EXIT
    THEN
    PERF-CYCLES _KDCP-STEP-START !
    ['] _KDOSTLS-PREP-POLL-INNER CATCH
    PERF-CYCLES _KDCP-STEP-START @ - _KDPP-A @ _KDOSTLS-PREP-STEP-RECORD
    ?DUP IF
        DROP KDOSTLS-E-FAULT _KDPP-A @ _KDOSTLS-PREP-FAIL EXIT
    THEN
    DUP _KDCP-STEP-STATUS !
    DUP KDOSTLS-PREP-S-PENDING = IF EXIT THEN
    DUP KDOSTLS-PREP-S-READY = IF
        _KDPP-A @ KDOSTLS.PHASE @ KDOSTLS-PHASE-OPEN =
        _KDPP-A @ _KDOSTLS-OPEN-VALID? AND IF
            KDOSTLS-STATE-OPEN _KDPP-A @ KDOSTLS.STATE !
            KDOSTLS-E-OK _KDPP-A @ KDOSTLS.LAST-ERROR !
            EXIT
        THEN
        DROP KDOSTLS-E-INVALID _KDPP-A @ _KDOSTLS-PREP-FAIL EXIT
    THEN
    DUP KDOSTLS-PREP-S-CANCELLED = IF
        DROP _KDPP-A @ KDOSTLS-PREP-CANCEL EXIT
    THEN
    KDOSTLS-PREP-S-FAILED = IF
        _KDPP-A @ KDOSTLS.LAST-ERROR @ DUP KDOSTLS-E-OK = IF
            DROP KDOSTLS-E-FAULT
        THEN
        _KDPP-A @ _KDOSTLS-PREP-FAIL EXIT
    THEN
    KDOSTLS-E-INVALID _KDPP-A @ _KDOSTLS-PREP-FAIL ;

: _KDOSTLS-NIO-OPEN-START  ( adapter -- nio-status )
    KDOSTLS-PREP-START
    DUP KDOSTLS-PREP-S-PENDING = IF DROP NIO-S-PENDING EXIT THEN
    DUP KDOSTLS-PREP-S-READY = IF DROP NIO-S-OK EXIT THEN
    DUP KDOSTLS-PREP-S-CANCELLED = IF DROP NIO-S-CANCELLED EXIT THEN
    DROP NIO-S-FAILED ;

: _KDOSTLS-NIO-OPEN-POLL  ( adapter -- nio-status )
    KDOSTLS-PREP-POLL
    DUP KDOSTLS-PREP-S-PENDING = IF DROP NIO-S-PENDING EXIT THEN
    DUP KDOSTLS-PREP-S-READY = IF DROP NIO-S-OK EXIT THEN
    DUP KDOSTLS-PREP-S-CANCELLED = IF DROP NIO-S-CANCELLED EXIT THEN
    DROP NIO-S-FAILED ;

: _KDOSTLS-NIO-CANCEL  ( adapter -- )
    KDOSTLS-PREP-CANCEL KDOSTLS-PREP-S-FAILED = IF
        KDOSTLS-E-CLEANUP NEGATE THROW
    THEN ;

: _KDOSTLS-MARK-ERROR  ( error adapter -- )
    >R R@ KDOSTLS.LAST-ERROR ! KDOSTLS-STATE-ERROR R> KDOSTLS.STATE ! ;

: _KDOSTLS-LATCH-NATIVE-TLS-ERROR  ( adapter -- )
    \ Custom callbacks may use an opaque non-TLS context.  Inspect the context
    \ only when both receive and link-status are the native KDOS TLS pair.
    DUP KDOSTLS.RECV-XT @ ['] _KDOSTLS-RECV-DEFAULT <> IF DROP EXIT THEN
    DUP KDOSTLS.STATUS-XT @ ['] _KDOSTLS-STATUS-DEFAULT <> IF DROP EXIT THEN
    DUP KDOSTLS.CONTEXT @ ?DUP IF
        TLS-CTX.ERROR @ ?DUP IF
            SWAP KDOSTLS.NATIVE-ERROR !
        ELSE
            DROP
        THEN
    ELSE
        DROP
    THEN ;

: KDOSTLS-LINK@  ( adapter -- link-status )
    DUP _KDOSTLS-TCB@ 0= IF DROP KDOSTLS-LINK-CLOSED EXIT THEN
    DUP KDOSTLS.CONTEXT @ SWAP DUP KDOSTLS.STATUS-XT @ EXECUTE ;

VARIABLE _KDTC-A

: _KDOSTLS-RELEASE  ( adapter -- )
    DUP _KDTC-A ! _KDOSTLS-PREP-DETACH 0= IF
        KDOSTLS-E-CLEANUP _KDTC-A @ KDOSTLS.CLEANUP-ERROR !
    THEN
    KDOSTLS-STATE-CLOSED _KDTC-A @ KDOSTLS.STATE ! ;

VARIABLE _KDCL-A
VARIABLE _KDCL-CTX
VARIABLE _KDCL-TCB
VARIABLE _KDCL-PRE-STATE
VARIABLE _KDCN-CTX
VARIABLE _KDCN-TCB
VARIABLE _KDCN-NXT

: _KDOSTLS-CLOSE-NOTIFY-DEFAULT  ( ctx adapter -- sent? )
    DROP DUP _KDCN-CTX ! TLS-CTX.TCB @ DUP 0= IF DROP 0 EXIT THEN
    DUP _KDCN-TCB ! TCP-SEND-READY? 0= IF 0 EXIT THEN
    _KDCN-TCB @ TCB.SND-NXT @ _KDCN-NXT !
    _KDCN-CTX @ 1 0 TLS-SEND-ALERT
    _KDCN-TCB @ TCB.SND-NXT @ _KDCN-NXT @ <> ;

: _KDOSTLS-CLOSE-NOTIFY-TLS?  ( ctx -- flag )
    DUP TLS-CTX.STATE @ TLSS-ESTABLISHED =
    OVER TLS-CTX.PEER-AUTH @ 1 = AND
    OVER TLS-CTX.STATE @ TLSS-CLOSING =
    2 PICK TLS-CTX.ERROR @ TLS-E-OK = AND OR
    SWAP DROP ;

: _KDOSTLS-CLOSE-NOTIFY-TCP?  ( tcb -- flag )
    TCB.STATE @ DUP TCPS-ESTABLISHED =
    SWAP TCPS-CLOSE-WAIT = OR ;

: _KDOSTLS-GRACEFUL-DETACH  ( adapter -- )
    DUP KDOSTLS.CONTEXT @ ?DUP IF /TLS-CTX 0 FILL THEN
    DUP 0 SWAP KDOSTLS.CONTEXT !
    DUP 0 SWAP KDOSTLS.LOCAL-PORT !
    DUP _KDOSTLS-PREP-CLEAR-SCRATCH
    _KDOSTLS-OWNER @ OVER = IF 0 _KDOSTLS-OWNER ! THEN
    DUP KDOSTLS-PHASE-IDLE SWAP KDOSTLS.PHASE !
    KDOSTLS-STATE-CLOSED SWAP KDOSTLS.STATE ! ;

: _KDOSTLS-CLOSE-COMPLETE?  ( adapter -- flag )
    _KDOSTLS-TCB@ DUP 0= IF DROP 0 EXIT THEN
    TCB.STATE @ DUP TCPS-CLOSED = SWAP TCPS-TIME-WAIT = OR ;

: _KDOSTLS-CLOSE-TCB-MISMATCH?  ( adapter -- flag )
    DUP KDOSTLS.CONTEXT @ DUP 0= IF 2DROP 0 EXIT THEN
    TLS-CTX.TCB @ DUP 0= IF 2DROP 0 EXIT THEN
    DROP _KDOSTLS-TCB-MATCH? 0= ;

VARIABLE _KDZC-TCB

: _KDOSTLS-CLOSE-TCB-ZEROED?  ( adapter -- flag )
    KDOSTLS.CONTEXT @ DUP 0= IF DROP 0 EXIT THEN
    TLS-CTX.TCB @ DUP 0= IF DROP 0 EXIT THEN
    DUP _KDOSTLS-TCB-POINTER? 0= IF DROP 0 EXIT THEN
    _KDZC-TCB !
    _KDZC-TCB @ TCB.STATE @ TCPS-CLOSED =
    _KDZC-TCB @ TCB.LOCAL-PORT @ 0= AND
    _KDZC-TCB @ TCB.REMOTE-PORT @ 0= AND
    _KDZC-TCB @ TCB.ISS @ 0= AND
    _KDZC-TCB @ TCB.SND-NXT @ 0= AND
    _KDZC-TCB @ TCB.SND-UNA @ 0= AND ;

: _KDOSTLS-CLOSE-ABORT  ( -- nio-status )
    1 _KDCL-A @ KDOSTLS.CLOSE-FALLBACKS +!
    _KDCL-A @ _KDOSTLS-PREP-DETACH 0= IF NIO-S-FAILED EXIT THEN
    KDOSTLS-PHASE-IDLE _KDCL-A @ KDOSTLS.PHASE !
    KDOSTLS-STATE-CLOSED _KDCL-A @ KDOSTLS.STATE !
    NIO-S-OK ;

: _KDOSTLS-CLOSE-DEADLINE?  ( -- flag )
    _KDCL-A @ _KDOSTLS-NOW@
    _KDCL-A @ KDOSTLS.DEADLINE-MS @ _KDOSTLS-TIME-REACHED? ;

: _KDOSTLS-CLOSE-ARP-SEND  ( -- )
    _KDCL-A @ KDOSTLS.RETRIES @ KDOSTLS-PREP-RETRY-MAX >= IF EXIT THEN
    _KDCL-A @ _KDOSTLS-RETRY-DUE? 0= IF EXIT THEN
    _KDCL-A @ KDOSTLS.ARP-TARGET ARP-SEND-REQUEST
    1 _KDCL-A @ KDOSTLS.RETRIES +!
    KDOSTLS-ARP-RETRY-MS _KDCL-A @ _KDOSTLS-RETRY-AFTER! ;

: _KDOSTLS-CLOSE-ARP-POLL  ( -- )
    _KDCL-A @ _KDCP-A !
    _KDOSTLS-ARP-RECEIVE-ONE
    _KDCL-A @ _KDOSTLS-REMOTE-CACHED? 0= IF
        _KDOSTLS-CLOSE-ARP-SEND
    THEN ;

: _KDOSTLS-CLOSE-FIN-READY?  ( -- flag )
    _KDCL-TCB @ TCB.SND-NXT @ _KDCL-TCB @ TCB.SND-UNA @ = ;

: _KDOSTLS-CLOSE-ADVANCE  ( adapter -- nio-status )
    _KDCL-A !
    _KDCL-A @ KDOSTLS.CONTEXT @ DUP 0= IF
        DROP _KDCL-A @ _KDOSTLS-GRACEFUL-DETACH NIO-S-OK EXIT
    THEN _KDCL-CTX !
    _KDCL-CTX @ TLS-CTX.TCB @ DUP 0= IF
        DROP _KDCL-A @ _KDOSTLS-GRACEFUL-DETACH NIO-S-OK EXIT
    THEN _KDCL-TCB !
    _KDCL-A @ _KDOSTLS-TCB-MATCH? 0= IF
        _KDOSTLS-CLOSE-ABORT EXIT
    THEN
    _KDCL-A @ _KDOSTLS-CLOSE-COMPLETE? IF
        _KDCL-A @ _KDOSTLS-GRACEFUL-DETACH NIO-S-OK EXIT
    THEN
    _KDCL-A @ KDOSTLS.PHASE @
    CASE
        KDOSTLS-PHASE-CLOSE-NOTIFY OF
            _KDCL-TCB @ _KDOSTLS-CLOSE-NOTIFY-TCP? 0= IF
                KDOSTLS-PHASE-CLOSE-FIN _KDCL-A @ KDOSTLS.PHASE !
                NIO-S-PENDING EXIT
            THEN
            _KDCL-TCB @ TCP-SEND-READY? 0= IF NIO-S-PENDING EXIT THEN
            _KDCL-CTX @ _KDCL-A @
                _KDCL-A @ KDOSTLS.CLOSE-NOTIFY-XT @ EXECUTE 0= IF
                _KDOSTLS-CLOSE-ABORT EXIT
            THEN
            TLSS-CLOSING _KDCL-CTX @ TLS-CTX.STATE !
            KDOSTLS-PHASE-CLOSE-FIN _KDCL-A @ KDOSTLS.PHASE !
            NIO-S-PENDING
        ENDOF
        KDOSTLS-PHASE-CLOSE-FIN OF
            _KDCL-TCB @ TCB.STATE @ DUP TCPS-ESTABLISHED =
            OVER TCPS-CLOSE-WAIT = OR IF
                DROP _KDOSTLS-CLOSE-FIN-READY? 0= IF
                    NIO-S-PENDING EXIT
                THEN
                TLSS-CLOSING _KDCL-CTX @ TLS-CTX.STATE !
                _KDCL-TCB @ TCP-CLOSE
                KDOSTLS-PHASE-CLOSE-WAIT _KDCL-A @ KDOSTLS.PHASE !
                NIO-S-PENDING EXIT
            THEN
            DUP TCPS-FIN-WAIT-1 = OVER TCPS-FIN-WAIT-2 = OR
            OVER TCPS-CLOSING = OR OVER TCPS-LAST-ACK = OR IF
                DROP KDOSTLS-PHASE-CLOSE-WAIT
                    _KDCL-A @ KDOSTLS.PHASE !
                NIO-S-PENDING EXIT
            THEN
            DROP _KDOSTLS-CLOSE-ABORT
        ENDOF
        KDOSTLS-PHASE-CLOSE-WAIT OF NIO-S-PENDING ENDOF
        NIO-S-FAILED SWAP
    ENDCASE ;

: _KDOSTLS-NIO-CLOSE-START  ( adapter -- nio-status )
    DUP _KDCL-A ! DUP KDOSTLS.STATE @ KDOSTLS-STATE-CLOSED =
    OVER KDOSTLS.CONTEXT @ 0= AND
    _KDOSTLS-OWNER @ 2 PICK <> AND IF DROP NIO-S-OK EXIT THEN
    KDOSTLS.STATE @
    KDOSTLS-STATE-CLOSING = IF NIO-S-PENDING EXIT THEN
    _KDCL-A @ KDOSTLS.CONTEXT @ DUP 0= IF
        DROP _KDCL-A @ _KDOSTLS-GRACEFUL-DETACH NIO-S-OK EXIT
    THEN DUP _KDCL-CTX !
    TLS-CTX.TCB @ DUP 0= IF
        DROP _KDCL-A @ _KDOSTLS-GRACEFUL-DETACH NIO-S-OK EXIT
    THEN _KDCL-TCB !
    _KDCL-A @ _KDOSTLS-TCB-MATCH? 0= IF
        _KDOSTLS-CLOSE-ABORT EXIT
    THEN
    _KDCL-A @ _KDOSTLS-CLOSE-COMPLETE? IF
        _KDCL-A @ _KDOSTLS-GRACEFUL-DETACH NIO-S-OK EXIT
    THEN
    _KDCL-TCB @ TCB.REMOTE-IP NEXT-HOP
        _KDCL-A @ KDOSTLS.ARP-TARGET 4 CMOVE
    0 _KDCL-A @ KDOSTLS.RETRIES !
    0 _KDCL-A @ KDOSTLS.RETRY-AT-MS !
    _KDCL-A @ _KDOSTLS-NOW@ KDOSTLS-CLOSE-TIMEOUT-MS +
        _KDCL-A @ KDOSTLS.DEADLINE-MS !
    KDOSTLS-STATE-CLOSING _KDCL-A @ KDOSTLS.STATE !
    _KDCL-CTX @ _KDOSTLS-CLOSE-NOTIFY-TLS?
    _KDCL-TCB @ _KDOSTLS-CLOSE-NOTIFY-TCP? AND IF
        KDOSTLS-PHASE-CLOSE-NOTIFY
    ELSE
        KDOSTLS-PHASE-CLOSE-FIN
    THEN _KDCL-A @ KDOSTLS.PHASE !
    _KDCL-A @ _KDOSTLS-REMOTE-CACHED? 0= IF
        _KDOSTLS-CLOSE-ARP-SEND NIO-S-PENDING EXIT
    THEN
    _KDCL-A @ _KDOSTLS-CLOSE-ADVANCE ;

: _KDOSTLS-CLOSE-POST-RECEIVE  ( -- nio-status )
    \ TCB-INIT deliberately erases the tuple when the FIN-covering ACK
    \ arrives in LAST-ACK.  Accept that identity loss only when this poll
    \ observed LAST-ACK immediately before its single receive step.
    _KDCL-PRE-STATE @ TCPS-LAST-ACK =
    _KDCL-A @ _KDOSTLS-CLOSE-TCB-ZEROED? AND IF
        _KDCL-A @ _KDOSTLS-GRACEFUL-DETACH NIO-S-OK EXIT
    THEN
    _KDCL-A @ _KDOSTLS-CLOSE-TCB-MISMATCH? IF
        _KDOSTLS-CLOSE-ABORT EXIT
    THEN
    _KDCL-A @ _KDOSTLS-CLOSE-COMPLETE? IF
        _KDCL-A @ _KDOSTLS-GRACEFUL-DETACH NIO-S-OK EXIT
    THEN
    _KDCL-A @ _KDOSTLS-CLOSE-ADVANCE ;

: _KDOSTLS-NIO-CLOSE-POLL  ( adapter -- nio-status )
    DUP _KDCL-A ! DUP KDOSTLS.STATE @ KDOSTLS-STATE-CLOSED =
    OVER KDOSTLS.CONTEXT @ 0= AND
    _KDOSTLS-OWNER @ 2 PICK <> AND IF DROP NIO-S-OK EXIT THEN
    DUP KDOSTLS.STATE @ KDOSTLS-STATE-CLOSED = IF
        KDOSTLS-STATE-CLOSING OVER KDOSTLS.STATE !
    THEN
    KDOSTLS.STATE @ KDOSTLS-STATE-CLOSING <> IF NIO-S-FAILED EXIT THEN
    _KDCL-A @ _KDOSTLS-CLOSE-TCB-MISMATCH? IF
        _KDOSTLS-CLOSE-ABORT EXIT
    THEN
    _KDOSTLS-CLOSE-DEADLINE? IF _KDOSTLS-CLOSE-ABORT EXIT THEN
    _KDCL-A @ _KDOSTLS-CLOSE-COMPLETE? IF
        _KDCL-A @ _KDOSTLS-GRACEFUL-DETACH NIO-S-OK EXIT
    THEN
    _KDCL-A @ _KDOSTLS-REMOTE-CACHED? 0= IF
        _KDOSTLS-CLOSE-ARP-POLL NIO-S-PENDING EXIT
    THEN
    0 _KDCL-PRE-STATE !
    _KDCL-A @ _KDOSTLS-TCB@ ?DUP IF
        TCB.STATE @ _KDCL-PRE-STATE !
    THEN
    _KDCL-A @ _KDOSTLS-TCP-RECEIVE-ONE
    _KDOSTLS-CLOSE-POST-RECEIVE ;

VARIABLE _KDTO-A

: _KDOSTLS-NIO-OPEN  ( adapter -- nio-status )
    DUP _KDTO-A ! _KDOSTLS-NIO-OPEN-START
    DUP NIO-S-PENDING <> IF EXIT THEN DROP
    4096 0 DO
        _KDTO-A @ _KDOSTLS-NIO-OPEN-POLL
        DUP NIO-S-PENDING <> IF UNLOOP EXIT THEN DROP
        NET-IDLE
    LOOP
    _KDTO-A @ KDOSTLS-PREP-CANCEL KDOSTLS-PREP-S-FAILED = IF
        KDOSTLS-E-CLEANUP NEGATE THROW
    THEN
    NIO-S-FAILED ;

