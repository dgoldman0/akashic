VARIABLE _KDTCFG-A
VARIABLE _KDTCFG-U
VARIABLE _KDTCFG-PORT
VARIABLE _KDTCFG-T

: KDOSTLS-CONFIGURE  ( host-a host-u remote-port adapter -- status )
    _KDTCFG-T ! _KDTCFG-PORT ! _KDTCFG-U ! _KDTCFG-A !
    _KDTCFG-T @ KDOSTLS.STATE @ KDOSTLS-STATE-CLOSED <> IF
        KDOSTLS-E-BUSY EXIT
    THEN
    _KDTCFG-U @ 0= _KDTCFG-U @ KDOSTLS-HOST-CAPACITY > OR
    _KDTCFG-A @ 0= OR IF KDOSTLS-E-INVALID EXIT THEN
    _KDTCFG-A @ _KDTCFG-U @ FALSE DNS-NAME-VALID? 0= IF
        KDOSTLS-E-INVALID EXIT
    THEN
    _KDTCFG-PORT @ 1 < _KDTCFG-PORT @ 65535 > OR IF
        KDOSTLS-E-INVALID EXIT
    THEN
    _KDTCFG-T @ KDOSTLS.HOST-BUF KDOSTLS-HOST-CAPACITY 0 FILL
    _KDTCFG-A @ _KDTCFG-T @ KDOSTLS.HOST-BUF _KDTCFG-U @ CMOVE
    _KDTCFG-U @ _KDTCFG-T @ KDOSTLS.HOST-U !
    _KDTCFG-PORT @ _KDTCFG-T @ KDOSTLS.REMOTE-PORT !
    KDOSTLS-E-OK _KDTCFG-T @ KDOSTLS.LAST-ERROR !
    KDOSTLS-E-OK ;

VARIABLE _KDTNEW-T
VARIABLE _KDTNEW-STATUS

: KDOSTLS-NEW  ( host-a host-u remote-port -- adapter status )
    >R KDOSTLS-SIZE ALLOCATE
    DUP IF >R DROP 2DROP R> DROP R> DROP 0 KDOSTLS-E-INVALID EXIT THEN
    DROP DUP _KDTNEW-T ! KDOSTLS-INIT
    R> _KDTNEW-T @ KDOSTLS-CONFIGURE DUP _KDTNEW-STATUS !
    IF _KDTNEW-T @ FREE 0 _KDTNEW-STATUS @ EXIT THEN
    _KDTNEW-T @ KDOSTLS-E-OK ;

: KDOSTLS-FREE  ( adapter -- )
    DUP 0= IF DROP EXIT THEN
    DUP KDOSTLS.PORT NIO-CLOSE
    DUP KDOSTLS-SIZE 0 FILL
    FREE ;

\ KDOS TLS uses shared record, handshake, SNI, and trust-store scratch. The
\ owner gate deliberately permits one active KDOS TLS adapter at a time.
\ Multiple Agent runs remain cooperatively schedulable around that boundary;
\ true concurrent TLS requires a future descriptor-owned KDOS record layer.
