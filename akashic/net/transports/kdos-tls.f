    _KDTAP-A ! _KDTAP-XT ! _KDTAP-CONTEXT !
    _KDTAP-A @ 0= _KDTAP-XT @ 0= OR IF KDOSTLS-E-INVALID EXIT THEN
    _KDTAP-A @ KDOSTLS.STATE @ KDOSTLS-STATE-CLOSED <> IF
