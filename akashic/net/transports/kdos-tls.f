\ =====================================================================
\  kdos-tls.f - KDOS authenticated TLS transport for net/io-port
\ =====================================================================
\  This binds Akashic's byte-stream contract and KDOS DNS/TCP/TLS services.
\  The native compatibility entry remains blocking.  The NIO entry points use
\  an instrumented connector which advances one network frame or one bounded
\  TLS phase per poll, and retain lower ownership through cooperative close.
\  A caller-configured address policy runs after DNS has selected an address
\  and before any remote ARP or TCP work begins.  The default policy admits
\  only conservative public IPv4 destinations.  A different policy must be
\  installed explicitly while closed, for example by a reviewed local-network
\  composition.
\  This module owns no HTTP, credential, provider, Agent, Desk, TUI, emulator,
\  or host-bridge behavior.
\ =====================================================================

