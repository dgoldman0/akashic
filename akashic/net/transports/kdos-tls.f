    DUP _KDOSTLS-HS-S-FAILED = IF
        DROP TLS-CONNECT-E-SERVER-PROCESS _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    _KDOSTLS-HS-S-INCOMPLETE = IF
        KDOSTLS-PHASE-SERVER-RECORD _KDOSTLS-PHASE!
        KDOSTLS-PREP-S-PENDING EXIT
    THEN
    _KDCP-A @ KDOSTLS.CONTEXT @ TLS-CTX.HS-STATE @
        TLSH-SERVER-HELLO-RCVD <>
    TLS-HS-RBUF-LEN @ 0<> OR IF
        TLS-CONNECT-E-SERVER-PROCESS _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    KDOSTLS-PHASE-HANDSHAKE-RECORD _KDOSTLS-PHASE!
    KDOSTLS-PREP-S-PENDING ;

