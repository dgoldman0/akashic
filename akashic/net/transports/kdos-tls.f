        TLS-CONNECT-E-FINISHED _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    _KDCP-A @ KDOSTLS.CONTEXT @ DUP TLS-CTX.STATE @ TLSS-ESTABLISHED <>
    SWAP TLS-CTX.PEER-AUTH @ 1 <> OR IF
        TLS-CONNECT-E-AUTH _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-AUTH _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    KDOSTLS-PHASE-CLIENT-FINISHED-SEND _KDOSTLS-PHASE!
    KDOSTLS-PREP-S-PENDING ;

: _KDOSTLS-STEP-CLIENT-FINISHED-SEND  ( -- prep-status )
    _KDCP-A @ KDOSTLS.REMOTE-IP NEXT-HOP ARP-LOOKUP DUP 0= IF
        DROP KDOSTLS-PHASE-CLIENT-FINISHED-SEND
            _KDOSTLS-REMOTE-ARP-FOR EXIT
    THEN DROP
    _KDCP-A @ _KDOSTLS-TCB@ DUP 0= IF
        DROP TLS-CONNECT-E-FINISHED _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    DUP TCP-SEND-READY? 0= IF
        TCB.STATE @ TCPS-ESTABLISHED = IF
            _KDCP-A @ _KDOSTLS-TCP-RECEIVE-ONE
            KDOSTLS-PREP-S-PENDING EXIT
        THEN
        TLS-CONNECT-E-FINISHED _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    TLS-SEND-REC _KDCP-A @ KDOSTLS.FINISHED-U @ TCP-SEND
    _KDCP-A @ KDOSTLS.FINISHED-U @ <> IF
        TLS-CONNECT-E-FINISHED _KDCP-A @ KDOSTLS.NATIVE-ERROR !
        KDOSTLS-E-CONNECT _KDCP-A @ KDOSTLS.LAST-ERROR !
        KDOSTLS-PREP-S-FAILED EXIT
    THEN
    KDOSTLS-PHASE-OPEN _KDOSTLS-PHASE!
    KDOSTLS-PREP-S-PENDING ;

VARIABLE _KDOV-A

: _KDOSTLS-OPEN-VALID?  ( adapter -- flag )
    DUP _KDOV-A ! _KDOSTLS-OWNER @ <> IF 0 EXIT THEN
    _KDOV-A @ _KDOSTLS-TCB@ DUP 0= IF DROP 0 EXIT THEN
    TCB.STATE @ TCPS-ESTABLISHED <>
    _KDOV-A @ KDOSTLS.CONTEXT @ TLS-CTX.STATE @ TLSS-ESTABLISHED <> OR
    _KDOV-A @ KDOSTLS.CONTEXT @ TLS-CTX.PEER-AUTH @ 1 <> OR 0= ;

VARIABLE _KDRV-A

: _KDOSTLS-RECV-VALID?  ( adapter -- flag )
