    _MTRR-ID-A @ _MTRR-ID-U @ COMPARE 0= ;

\ Register one trusted preboot emitter.  Repeating the same ID, callback,
\ and context is idempotent; reusing an ID for different code is rejected.
\ emit-xt has stack effect ( builder context -- status ).
: MTRUST-REGISTER  ( id-a id-u emit-xt context -- status )
    _MTRR-CONTEXT ! _MTRR-EMIT-XT ! _MTRR-ID-U ! _MTRR-ID-A !
    COREID IF MTRUST-S-INVALID EXIT THEN
    _MTRUST-STATE @ MTRUST-STATE-OPEN <> IF
        _MTRUST-STATE @ MTRUST-STATE-FROZEN = IF
            MTRUST-S-FROZEN
        ELSE
            MTRUST-S-STATE
        THEN EXIT
    THEN
    _MTRR-ID-A @ 0= _MTRR-ID-U @ 0> 0= OR
    _MTRR-ID-U @ MTRUST-CONTRIBUTOR-ID-CAPACITY > OR
    _MTRR-EMIT-XT @ 0= OR IF MTRUST-S-INVALID EXIT THEN
    _MTRUST-CONTRIBUTOR-N @ 0 ?DO
        I _MTRUST-CONTRIBUTOR@ DUP _MTRR-ID-MATCH? IF
            DUP _MTC.EMIT-XT @ _MTRR-EMIT-XT @ =
            SWAP _MTC.CONTEXT @ _MTRR-CONTEXT @ = AND IF
                MTRUST-S-OK
            ELSE
                MTRUST-S-CONFLICT
            THEN
            UNLOOP EXIT
        THEN
        DROP
    LOOP
    _MTRUST-CONTRIBUTOR-N @ MTRUST-CONTRIBUTOR-MAX >= IF
        MTRUST-S-CAPACITY EXIT
    THEN
    _MTRUST-CONTRIBUTOR-N @ _MTRUST-CONTRIBUTOR@ >R
    _MTRR-ID-U @ R@ _MTC.ID-U !
    _MTRR-EMIT-XT @ R@ _MTC.EMIT-XT !
    _MTRR-CONTEXT @ R@ _MTC.CONTEXT !
    _MTRR-ID-A @ R@ _MTC.ID _MTRR-ID-U @ CMOVE
    R> DROP
    1 _MTRUST-CONTRIBUTOR-N +!
    MTRUST-S-OK ;

: _MTR-BE16@  ( addr -- u )
    DUP C@ 8 LSHIFT SWAP 1+ C@ OR ;

: _MTR-BE32@  ( addr -- u )
    DUP C@ 24 LSHIFT OVER 1+ C@ 16 LSHIFT OR
    OVER 2 + C@ 8 LSHIFT OR SWAP 3 + C@ OR ;

VARIABLE _MTR-BE64-A
: _MTR-BE64@  ( addr -- u )
    _MTR-BE64-A ! 0
    8 0 DO 8 LSHIFT _MTR-BE64-A @ I + C@ OR LOOP ;

: _MTB-INIT  ( buffer capacity builder -- )
    >R
    _MTRUST-BUILDER-MAGIC R@ _MTB.MAGIC !
    R@ _MTB.CAPACITY ! R@ _MTB.BUFFER !
    16 R@ _MTB.POSITION !
    0 R@ _MTB.COUNT !
    R@ _MTB.BUFFER @ R@ _MTB.CAPACITY @ 0 FILL
    R> DROP ;

: _MTB-FINI  ( builder -- )
    MTRUST-BUILDER-SIZE 0 FILL ;

: _MTB-C,  ( byte builder -- )
    DUP >R _MTB.BUFFER @ R@ _MTB.POSITION @ + C!
    1 R> _MTB.POSITION +! ;

: _MTB-U16,  ( value builder -- )
    >R
    DUP 8 RSHIFT 255 AND R@ _MTB-C,
    255 AND R> _MTB-C, ;

: _MTB-U32,  ( value builder -- )
    >R
    DUP 24 RSHIFT 255 AND R@ _MTB-C,
    DUP 16 RSHIFT 255 AND R@ _MTB-C,
    DUP 8 RSHIFT 255 AND R@ _MTB-C,
    255 AND R> _MTB-C, ;

