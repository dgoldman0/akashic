        _HRX-R @ _HRES-FAILED-EXCHANGE EXIT
    THEN
    HBUF-S-OK _HRX-R @ HRES.TRANSPORT-STATUS !
    _HRX-R @ HRES.EXCHANGE HBUF.PARSER HSTR.STATUS @
        _HRX-R @ HRES.PARSER-STATUS !
    _HRX-R @ _HRES-RELEASE-DETACHED DUP HRES-S-OK <> IF EXIT THEN DROP
    _HRX-R @ _HRES-ADMIT-RESPONSE ;

: HRES-POLL  ( resource -- status )
    DUP HRES-VALID? 0= IF DROP HRES-S-INVALID EXIT THEN
    DUP HRES.STATE @ HRES-STATE-RESULT = IF DROP HRES-S-OK EXIT THEN
    DUP HRES.STATE @ HRES-STATE-FAULT = IF
        DUP HRES.CLEANUP @ IF DROP HRES-S-CLEANUP ELSE DROP HRES-S-FAULT THEN
        EXIT
    THEN
    DUP HRES.STATE @ HRES-STATE-ACTIVE <> IF DROP HRES-S-STATE EXIT THEN
    DUP HRES.PHASE @ CASE
        HRES-PHASE-PREPARE OF _HRES-PREPARE ENDOF
        HRES-PHASE-EXCHANGE OF
            DUP HRES.EXCHANGE HBUF-POLL DUP _HRX-STATUS !
            DUP 2 PICK HRES.EXCHANGE-STATUS !
            HBUF-S-PENDING = IF DROP HRES-S-PENDING ELSE
                HRES-PHASE-ADMIT SWAP HRES.PHASE ! HRES-S-PENDING
            THEN
        ENDOF
        HRES-PHASE-ADMIT OF _HRES-ADMIT-PHASE ENDOF
        HRES-D-REQUEST SWAP _HRES-FAULT
    ENDCASE ;

