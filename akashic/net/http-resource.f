\ =====================================================================
\  http-resource.f - bounded transport-neutral HTTPS resource exchange
\ =====================================================================
\  This module owns HTTP request, redirect, response-admission, and result
\  state.  A sealed specification owns the exact configured target, Accept
\  value, redirect bound, target-to-port lease policy, and media-admission
\  callback.  The resource copies that specification and borrows one
\  caller-owned response buffer.
\
\  A bind callback receives the exact admitted target for every physical
\  hop and returns a closed, detached, cooperatively driven NIO port.  A
\  release callback runs only after detachment is proved by HBUF or by the
\  rejected-lease cancellation path.
\  Provider-contract violations fault the operation.  A safe ambient lease is
\  cancelled before release; an unsafe or uncertain lease poisons cleanup and
\  remains retained in a terminal, non-retrying quarantine rather than
\  releasing a possibly active connection.
\  Neither callback is allowed to perform network I/O.  This keeps KDOS,
\  trust, DNS, Desk, Streams, credentials, and payload decoding outside this
\  module.
\
\  Public HRES calls are single-threaded and non-reentrant because resources
\  share module scratch.  In particular, bind, release, and media callbacks
\  must not enter any HRES operation, even for another resource.  A bind
\  callback that throws must not have acquired a lease because no port handle
\  crossed the callback boundary for HRES to release.
\
\  The first version accepts one final 200 response and same-origin 301,
\  302, 303, 307, or 308 redirects.  It deliberately rejects 304 until a
\  retained-representation owner can bind conditional validators to an
\  exact prior effective target.  Redirect bodies count against one total
\  transfer-decoded body budget.  Chunked trailers and Trailer declarations
\  are rejected because HSTR does not expose trailer fields for admission.
\ =====================================================================

