\ =====================================================================
\  external-io.f - Owner-pumped machine-serialized external operations
\ =====================================================================
\  One service owns one active operation.  The owner core advances at most
\  one start/poll callback per tick.  Operations own their buffers and
\  semantic state; this service owns serialization, deadlines, cancellation,
\  cleanup, and terminal publication only.
\ =====================================================================

