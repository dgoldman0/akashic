\ The platform has one core-0 network/TLS owner.  Binding one canonical
\ descriptor keeps module scratch and the machine-global transport state from
\ becoming accidentally serial only per caller-created object.
VARIABLE _XIO-BOUND-SERVICE
0 _XIO-BOUND-SERVICE !

: XIOS.MAGIC      ( service -- a ) _XIOS-MAGIC + ;
: XIOS.ABI        ( service -- a ) _XIOS-ABI + ;
: XIOS.SIZE       ( service -- a ) _XIOS-SIZE + ;
: XIOS.ACTIVE     ( service -- a ) _XIOS-ACTIVE + ;
: XIOS.OWNER-CORE ( service -- a ) _XIOS-OWNER-CORE + ;
: XIOS.RETAINED   ( service -- a ) _XIOS-RETAINED + ;

: XIO-OP-INIT  ( operation -- )
    DUP XIO-OP-SIZE 0 FILL
    XIO-OP-MAGIC OVER XIOO.MAGIC !
    XIO-ABI-VERSION OVER XIOO.ABI !
    XIO-OP-SIZE OVER XIOO.SIZE !
    XIO-STATE-RESET SWAP XIOO.STATE ! ;

: XIO-SERVICE-INIT  ( service -- status )
    DUP 0= IF DROP XIO-S-INVALID EXIT THEN
    COREID 0<> IF DROP XIO-S-NOT-OWNER EXIT THEN
    _XIO-BOUND-SERVICE @ ?DUP IF
        2DUP <> IF 2DROP XIO-S-BUSY EXIT THEN
        DROP
        DUP XIOS.ACTIVE @
        OVER XIOS.RETAINED @ OR IF DROP XIO-S-BUSY EXIT THEN
    THEN
    DUP XIO-SERVICE-SIZE 0 FILL
    XIO-SERVICE-MAGIC OVER XIOS.MAGIC !
    XIO-ABI-VERSION OVER XIOS.ABI !
    XIO-SERVICE-SIZE OVER XIOS.SIZE !
    COREID OVER XIOS.OWNER-CORE !
    DUP _XIO-BOUND-SERVICE ! DROP XIO-S-OK ;

: XIO-OP-VALID?  ( operation -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP XIOO.MAGIC @ XIO-OP-MAGIC =
    OVER XIOO.ABI @ XIO-ABI-VERSION = AND
    OVER XIOO.SIZE @ XIO-OP-SIZE >= AND
    SWAP XIOO.STATE @ DUP XIO-STATE-RESET >=
    SWAP XIO-STATE-TIMED-OUT <= AND AND ;

: XIO-SERVICE-VALID?  ( service -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP XIOS.MAGIC @ XIO-SERVICE-MAGIC =
    OVER XIOS.ABI @ XIO-ABI-VERSION = AND
    SWAP XIOS.SIZE @ XIO-SERVICE-SIZE >= AND ;

: XIO-SERVICE-OWNER?  ( service -- flag )
    DUP XIO-SERVICE-VALID? 0= IF DROP 0 EXIT THEN
    XIOS.OWNER-CORE @ COREID = ;

: XIO-SERVICE-BOUND?  ( service -- flag )
    DUP XIO-SERVICE-VALID?
    SWAP _XIO-BOUND-SERVICE @ = AND ;

: XIO-ACTIVE?  ( service -- flag )
    XIOS.ACTIVE @ 0<> ;

: XIO-ACTIVE-OP  ( service -- operation | 0 )
    XIOS.ACTIVE @ ;

: XIO-SERVICE-FINI  ( service -- status )
    DUP XIO-SERVICE-BOUND? 0= IF DROP XIO-S-INVALID EXIT THEN
    DUP XIO-SERVICE-OWNER? 0= IF DROP XIO-S-NOT-OWNER EXIT THEN
    DUP XIO-ACTIVE?
    OVER XIOS.RETAINED @ 0<> OR IF DROP XIO-S-BUSY EXIT THEN
    0 _XIO-BOUND-SERVICE !
    XIO-SERVICE-SIZE 0 FILL XIO-S-OK ;

