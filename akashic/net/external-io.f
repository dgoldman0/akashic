\ service owner-id owner-generation request-generation deadline-ms context
\ start-xt poll-xt cancel-xt wipe-xt operation -- status
: XIO-OP-CONFIGURE
    10 PICK DUP XIO-SERVICE-BOUND? 0= IF
        DROP 11 0 DO DROP LOOP XIO-S-INVALID EXIT
    THEN
    XIO-SERVICE-OWNER? 0= IF
        11 0 DO DROP LOOP XIO-S-NOT-OWNER EXIT
    THEN
    _XIOCFG-OP ! _XIOCFG-WIPE ! _XIOCFG-CANCEL ! _XIOCFG-POLL !
    _XIOCFG-START ! _XIOCFG-CONTEXT ! _XIOCFG-DEADLINE !
    _XIOCFG-REQUEST-GEN ! _XIOCFG-OWNER-GEN ! _XIOCFG-OWNER ! DROP
    _XIOCFG-OP @ XIO-OP-VALID? 0= IF XIO-S-INVALID EXIT THEN
    _XIOCFG-OP @ XIOO.STATE @ XIO-STATE-RESET <> IF XIO-S-STATE EXIT THEN
    _XIOCFG-OWNER @ 0=
    _XIOCFG-OWNER-GEN @ 0> 0= OR
    _XIOCFG-REQUEST-GEN @ 0> 0= OR
    _XIOCFG-START @ 0= OR _XIOCFG-POLL @ 0= OR
    _XIOCFG-CANCEL @ 0= OR _XIOCFG-WIPE @ 0= OR IF
        XIO-S-INVALID EXIT
    THEN
    _XIOCFG-OWNER @ _XIOCFG-OP @ XIOO.OWNER-ID !
    _XIOCFG-OWNER-GEN @ _XIOCFG-OP @ XIOO.OWNER-GENERATION !
    _XIOCFG-REQUEST-GEN @ _XIOCFG-OP @ XIOO.REQUEST-GENERATION !
    _XIOCFG-DEADLINE @ _XIOCFG-OP @ XIOO.DEADLINE-MS !
    _XIOCFG-CONTEXT @ _XIOCFG-OP @ XIOO.CONTEXT !
    _XIOCFG-START @ _XIOCFG-OP @ XIOO.START-XT !
    _XIOCFG-POLL @ _XIOCFG-OP @ XIOO.POLL-XT !
    _XIOCFG-CANCEL @ _XIOCFG-OP @ XIOO.CANCEL-XT !
    _XIOCFG-WIPE @ _XIOCFG-OP @ XIOO.WIPE-XT !
    0 _XIOCFG-OP @ XIOO.RESULT !
    0 _XIOCFG-OP @ XIOO.ERROR !
    0 _XIOCFG-OP @ XIOO.CLEANUP-ERROR !
    0 _XIOCFG-OP @ XIOO.CANCEL-REQUESTED !
    0 _XIOCFG-OP @ XIOO.FLAGS !
    XIO-S-OK ;

: XIO-OP-CONFIGURED?  ( operation -- flag )
    DUP XIOO.OWNER-ID @ 0<>
    OVER XIOO.OWNER-GENERATION @ 0> AND
    OVER XIOO.REQUEST-GENERATION @ 0> AND
    OVER XIOO.START-XT @ 0<> AND
    OVER XIOO.POLL-XT @ 0<> AND
    OVER XIOO.CANCEL-XT @ 0<> AND
    SWAP XIOO.WIPE-XT @ 0<> AND ;

: XIO-OP-MATCH?  ( owner-id owner-generation request-generation operation -- flag )
    >R
    R@ XIOO.REQUEST-GENERATION @ =
    SWAP R@ XIOO.OWNER-GENERATION @ = AND
    SWAP R> XIOO.OWNER-ID @ = AND ;

VARIABLE _XIOSUB-SERVICE
VARIABLE _XIOSUB-OP

: XIO-SUBMIT  ( service operation -- status )
    OVER XIO-SERVICE-BOUND? 0= IF 2DROP XIO-S-INVALID EXIT THEN
    OVER XIO-SERVICE-OWNER? 0= IF 2DROP XIO-S-NOT-OWNER EXIT THEN
    _XIOSUB-OP ! _XIOSUB-SERVICE !
    _XIOSUB-SERVICE @ XIO-SERVICE-VALID? 0=
    _XIOSUB-OP @ XIO-OP-VALID? 0= OR IF XIO-S-INVALID EXIT THEN
    _XIOSUB-SERVICE @ XIO-ACTIVE?
    _XIOSUB-SERVICE @ XIOS.RETAINED @ 0<> OR IF XIO-S-BUSY EXIT THEN
    _XIOSUB-OP @ XIOO.STATE @ XIO-STATE-RESET <> IF XIO-S-STATE EXIT THEN
    _XIOSUB-OP @ XIO-OP-CONFIGURED? 0= IF XIO-S-INVALID EXIT THEN
    0 _XIOSUB-OP @ XIOO.RESULT !
    0 _XIOSUB-OP @ XIOO.ERROR !
    0 _XIOSUB-OP @ XIOO.CLEANUP-ERROR !
    0 _XIOSUB-OP @ XIOO.CANCEL-REQUESTED !
    0 _XIOSUB-OP @ XIOO.FLAGS !
    XIO-STATE-ACTIVE _XIOSUB-OP @ XIOO.STATE !
    _XIOSUB-OP @ _XIOSUB-SERVICE @ XIOS.ACTIVE !
    XIO-S-OK ;

VARIABLE _XIOCALL-OP
VARIABLE _XIOCALL-XT
VARIABLE _XIOCALL-STEP

: _XIO-CALL-STEP-INNER  ( -- )
    _XIOCALL-OP @ DUP XIOO.CONTEXT @ _XIOCALL-XT @ EXECUTE
    _XIOCALL-STEP ! ;

: _XIO-CALL-STEP  ( xt operation -- step-status ior )
    _XIOCALL-OP ! _XIOCALL-XT !
    XIO-STEP-FAILED _XIOCALL-STEP !
    ['] _XIO-CALL-STEP-INNER CATCH
    _XIOCALL-STEP @ SWAP ;

: _XIO-CALL-VOID-INNER  ( -- )
    _XIOCALL-OP @ DUP XIOO.CONTEXT @ _XIOCALL-XT @ EXECUTE ;

: _XIO-CALL-VOID  ( xt operation -- ior )
    _XIOCALL-OP ! _XIOCALL-XT !
    ['] _XIO-CALL-VOID-INNER CATCH ;

: _XIO-FLAG?  ( flag operation -- flag )
    XIOO.FLAGS @ AND 0<> ;

: _XIO-FLAG!  ( flag operation -- )
    XIOO.FLAGS DUP @ ROT OR SWAP ! ;

: _XIO-CANCEL-LOWER-ONCE  ( operation -- ior )
    _XIO-F-CANCEL-CALLED OVER _XIO-FLAG? IF DROP 0 EXIT THEN
    _XIO-F-CANCEL-CALLED OVER _XIO-FLAG!
    DUP XIOO.CANCEL-XT @ SWAP _XIO-CALL-VOID ;

: _XIO-WIPE-ONCE  ( operation -- ior )
    _XIO-F-WIPE-CALLED OVER _XIO-FLAG? IF DROP 0 EXIT THEN
    _XIO-F-WIPE-CALLED OVER _XIO-FLAG!
    DUP XIOO.WIPE-XT @ SWAP _XIO-CALL-VOID ;

: _XIO-DEADLINE-EXPIRED?  ( operation -- flag )
    XIOO.DEADLINE-MS @ ?DUP IF
        \ Signed modular comparison is valid for deadlines less than half
        \ the cell range away, which covers every practical monotonic timeout.
        MS@ SWAP - 0< 0=
    ELSE
        0
    THEN ;

VARIABLE _XIOT-SERVICE
VARIABLE _XIOT-OP
VARIABLE _XIOT-STATE
VARIABLE _XIOT-IOR

: _XIO-RECORD-CLEANUP-IOR  ( ior -- )
    ?DUP IF
        _XIOT-OP @ XIOO.CLEANUP-ERROR @ 0= IF
            _XIOT-OP @ XIOO.CLEANUP-ERROR !
        ELSE
            DROP
        THEN
        XIO-STATE-FAILED _XIOT-STATE !
    THEN ;

: _XIO-TERMINATE  ( terminal-state service operation -- )
    _XIOT-OP ! _XIOT-SERVICE ! _XIOT-STATE !
    _XIOT-OP @ _XIO-CANCEL-LOWER-ONCE _XIO-RECORD-CLEANUP-IOR
    _XIOT-OP @ _XIO-WIPE-ONCE _XIO-RECORD-CLEANUP-IOR
    0 _XIOT-SERVICE @ XIOS.ACTIVE !
    _XIOT-STATE @ _XIOT-OP @ XIOO.STATE ! ;

: _XIO-SUCCEED  ( service operation -- )
    >R
    R@ OVER XIOS.RETAINED !
    0 OVER XIOS.ACTIVE ! DROP
    XIO-STATE-SUCCEEDED R> XIOO.STATE ! ;

VARIABLE _XIOTICK-SERVICE
VARIABLE _XIOTICK-OP
VARIABLE _XIOTICK-STEP

: XIO-TICK  ( service -- )
    DUP XIO-SERVICE-BOUND? 0= IF DROP EXIT THEN
    DUP XIO-SERVICE-OWNER? 0= IF DROP EXIT THEN
    DUP _XIOTICK-SERVICE ! XIOS.ACTIVE @ DUP 0= IF DROP EXIT THEN
    DUP _XIOTICK-OP !
    DUP XIO-OP-VALID? 0= IF
        DROP 0 _XIOTICK-SERVICE @ XIOS.ACTIVE ! EXIT
    THEN
    XIOO.STATE @ XIO-STATE-ACTIVE <> IF
        0 _XIOTICK-SERVICE @ XIOS.ACTIVE ! EXIT
    THEN
    _XIOTICK-OP @ XIOO.CANCEL-REQUESTED @ IF
        _XIOTICK-OP @ XIOO.ERROR @ 0= IF
            XIO-E-CANCELLED _XIOTICK-OP @ XIOO.ERROR !
        THEN
        XIO-STATE-CANCELLED _XIOTICK-SERVICE @ _XIOTICK-OP @
        _XIO-TERMINATE EXIT
    THEN
    _XIOTICK-OP @ _XIO-DEADLINE-EXPIRED? IF
        XIO-E-DEADLINE _XIOTICK-OP @ XIOO.ERROR !
        XIO-STATE-TIMED-OUT _XIOTICK-SERVICE @ _XIOTICK-OP @
        _XIO-TERMINATE EXIT
    THEN
    _XIO-F-STARTED _XIOTICK-OP @ _XIO-FLAG? IF
        _XIOTICK-OP @ XIOO.POLL-XT @
    ELSE
        _XIO-F-STARTED _XIOTICK-OP @ _XIO-FLAG!
        _XIOTICK-OP @ XIOO.START-XT @
    THEN
    _XIOTICK-OP @ _XIO-CALL-STEP
    DUP IF
        NIP _XIOTICK-OP @ XIOO.ERROR !
        XIO-STATE-FAILED _XIOTICK-SERVICE @ _XIOTICK-OP @
        _XIO-TERMINATE EXIT
    THEN
    DROP _XIOTICK-STEP !
    _XIOTICK-OP @ XIOO.CANCEL-REQUESTED @ IF
        _XIOTICK-OP @ XIOO.ERROR @ 0= IF
            XIO-E-CANCELLED _XIOTICK-OP @ XIOO.ERROR !
        THEN
        XIO-STATE-CANCELLED _XIOTICK-SERVICE @ _XIOTICK-OP @
        _XIO-TERMINATE EXIT
    THEN
    _XIOTICK-OP @ _XIO-DEADLINE-EXPIRED? IF
        XIO-E-DEADLINE _XIOTICK-OP @ XIOO.ERROR !
        XIO-STATE-TIMED-OUT _XIOTICK-SERVICE @ _XIOTICK-OP @
        _XIO-TERMINATE EXIT
    THEN
    _XIOTICK-STEP @ XIO-STEP-PENDING = IF EXIT THEN
    _XIOTICK-STEP @ XIO-STEP-SUCCEEDED = IF
        _XIOTICK-SERVICE @ _XIOTICK-OP @ _XIO-SUCCEED EXIT
    THEN
    _XIOTICK-OP @ XIOO.ERROR @ 0= IF
        XIO-E-STEP _XIOTICK-OP @ XIOO.ERROR !
    THEN
    XIO-STATE-FAILED _XIOTICK-SERVICE @ _XIOTICK-OP @ _XIO-TERMINATE ;

VARIABLE _XIOC-SERVICE
VARIABLE _XIOC-OP

: XIO-CANCEL  ( service operation -- status )
    OVER XIO-SERVICE-BOUND? 0= IF 2DROP XIO-S-INVALID EXIT THEN
    OVER XIO-SERVICE-OWNER? 0= IF 2DROP XIO-S-NOT-OWNER EXIT THEN
    _XIOC-OP ! _XIOC-SERVICE !
    _XIOC-OP @ XIO-OP-VALID? 0= IF XIO-S-INVALID EXIT THEN
    _XIOC-OP @ XIOO.STATE @ XIO-STATE-CANCELLED = IF XIO-S-OK EXIT THEN
    _XIOC-OP @ XIOO.STATE @ XIO-STATE-ACTIVE <>
    _XIOC-SERVICE @ XIOS.ACTIVE @ _XIOC-OP @ <> OR IF XIO-S-STATE EXIT THEN
    -1 _XIOC-OP @ XIOO.CANCEL-REQUESTED !
    XIO-E-CANCELLED _XIOC-OP @ XIOO.ERROR !
    XIO-STATE-CANCELLED _XIOC-SERVICE @ _XIOC-OP @ _XIO-TERMINATE
    _XIOC-OP @ XIOO.STATE @ XIO-STATE-FAILED = IF
        XIO-S-CALLBACK
    ELSE
        XIO-S-OK
    THEN ;

VARIABLE _XIOR-SERVICE
VARIABLE _XIOR-OP

: XIO-RESET  ( service operation -- status )
    OVER XIO-SERVICE-BOUND? 0= IF 2DROP XIO-S-INVALID EXIT THEN
    OVER XIO-SERVICE-OWNER? 0= IF 2DROP XIO-S-NOT-OWNER EXIT THEN
    _XIOR-OP ! _XIOR-SERVICE !
    _XIOR-OP @ XIO-OP-VALID? 0= IF XIO-S-INVALID EXIT THEN
    _XIOR-OP @ XIOO.STATE @ DUP XIO-STATE-ACTIVE = IF
        DROP XIO-S-STATE EXIT
    THEN
    XIO-STATE-RESET = IF XIO-S-OK EXIT THEN
    _XIOR-SERVICE @ XIO-ACTIVE? IF XIO-S-BUSY EXIT THEN
    _XIOR-SERVICE @ XIOS.RETAINED @ ?DUP IF
        _XIOR-OP @ <> IF XIO-S-BUSY EXIT THEN
    THEN
    _XIOR-OP @ _XIO-WIPE-ONCE ?DUP IF
        DUP _XIOR-OP @ XIOO.ERROR !
        DUP _XIOR-OP @ XIOO.CLEANUP-ERROR ! DROP
        XIO-STATE-FAILED _XIOR-OP @ XIOO.STATE !
        XIO-S-CALLBACK EXIT
    THEN
    _XIOR-SERVICE @ XIOS.RETAINED @ _XIOR-OP @ = IF
        0 _XIOR-SERVICE @ XIOS.RETAINED !
    THEN
    0 _XIOR-OP @ XIOO.RESULT !
    0 _XIOR-OP @ XIOO.ERROR !
    0 _XIOR-OP @ XIOO.CLEANUP-ERROR !
    0 _XIOR-OP @ XIOO.CANCEL-REQUESTED !
    0 _XIOR-OP @ XIOO.FLAGS !
    XIO-STATE-RESET _XIOR-OP @ XIOO.STATE !
    XIO-S-OK ;
