\ =====================================================================
\  http-buffered.f - Cooperative bounded HTTP exchange over a native port
\ =====================================================================
\  The caller owns the request, port, and response-body buffer. This module
\  owns only parser state and a small receive scratch buffer.
\ =====================================================================

PROVIDED akashic-http-buffered

REQUIRE http-request.f
REQUIRE http-stream.f

0 CONSTANT HBUF-S-OK
1 CONSTANT HBUF-S-PENDING
2 CONSTANT HBUF-S-INVALID
3 CONSTANT HBUF-S-CAPACITY
4 CONSTANT HBUF-S-TRANSPORT
5 CONSTANT HBUF-S-PROTOCOL
6 CONSTANT HBUF-S-BUSY
7 CONSTANT HBUF-S-CANCELLED

1 CONSTANT HBUF-F-PERSISTENT

0 CONSTANT HBUF-STATE-IDLE
1 CONSTANT HBUF-STATE-SENDING
2 CONSTANT HBUF-STATE-RECEIVING
3 CONSTANT HBUF-STATE-DONE
4 CONSTANT HBUF-STATE-ERROR
5 CONSTANT HBUF-STATE-CANCELLED
6 CONSTANT HBUF-STATE-OPENING
7 CONSTANT HBUF-STATE-CLOSING

4096 CONSTANT HBUF-RECV-CAPACITY

  0 CONSTANT _HBUF-PORT
  8 CONSTANT _HBUF-REQUEST
 16 CONSTANT _HBUF-STATE
 24 CONSTANT _HBUF-LAST-STATUS
 32 CONSTANT _HBUF-BODY-A
 40 CONSTANT _HBUF-BODY-CAP
 48 CONSTANT _HBUF-BODY-U
 56 CONSTANT _HBUF-HTTP-CODE
 64 CONSTANT _HBUF-FLAGS
 72 CONSTANT _HBUF-PARSER
_HBUF-PARSER HSTR-PARSER-SIZE + CONSTANT _HBUF-RECV
_HBUF-RECV HBUF-RECV-CAPACITY + CONSTANT HTTP-BUFFERED-SIZE

: HBUF.PORT        ( exchange -- a ) _HBUF-PORT + ;
: HBUF.REQUEST     ( exchange -- a ) _HBUF-REQUEST + ;
: HBUF.STATE       ( exchange -- a ) _HBUF-STATE + ;
: HBUF.LAST-STATUS ( exchange -- a ) _HBUF-LAST-STATUS + ;
: HBUF.BODY-A      ( exchange -- a ) _HBUF-BODY-A + ;
: HBUF.BODY-CAP    ( exchange -- a ) _HBUF-BODY-CAP + ;
: HBUF.BODY-U      ( exchange -- a ) _HBUF-BODY-U + ;
: HBUF.HTTP-CODE   ( exchange -- a ) _HBUF-HTTP-CODE + ;
: HBUF.FLAGS       ( exchange -- a ) _HBUF-FLAGS + ;
: HBUF.PARSER      ( exchange -- parser ) _HBUF-PARSER + ;
: HBUF.RECV        ( exchange -- buffer ) _HBUF-RECV + ;

VARIABLE _HBUFH-P
VARIABLE _HBUFH-X

: _HBUF-HEADERS  ( parser exchange -- status )
    _HBUFH-X ! _HBUFH-P !
    _HBUFH-P @ HSTR.CODE @ _HBUFH-X @ HBUF.HTTP-CODE ! 0 ;

VARIABLE _HBUFB-P
VARIABLE _HBUFB-X
VARIABLE _HBUFB-A
VARIABLE _HBUFB-U

: _HBUF-BODY  ( parser exchange -- status )
    _HBUFB-X ! _HBUFB-P !
    _HBUFB-P @ HSTR-BODY-SLICE _HBUFB-U ! _HBUFB-A !
    _HBUFB-X @ HBUF.BODY-U @ _HBUFB-U @ +
    _HBUFB-X @ HBUF.BODY-CAP @ > IF 1 EXIT THEN
    _HBUFB-A @ _HBUFB-X @ HBUF.BODY-A @
    _HBUFB-X @ HBUF.BODY-U @ + _HBUFB-U @ CMOVE
    _HBUFB-U @ _HBUFB-X @ HBUF.BODY-U +! 0 ;

VARIABLE _HBUFI-X

: HBUF-INIT  ( body-a body-capacity exchange -- status )
    DUP _HBUFI-X ! HTTP-BUFFERED-SIZE 0 FILL
    OVER 0= OVER 0> 0= OR IF 2DROP HBUF-S-INVALID EXIT THEN
    _HBUFI-X @ HBUF.BODY-CAP ! _HBUFI-X @ HBUF.BODY-A !
    _HBUFI-X @ HBUF.PARSER HSTR-INIT
    ['] _HBUF-HEADERS _HBUFI-X @ HBUF.PARSER HSTR-ON-HEADERS!
    ['] _HBUF-BODY _HBUFI-X @ HBUF.PARSER HSTR-ON-BODY!
    _HBUFI-X @ _HBUFI-X @ HBUF.PARSER HSTR-CONTEXT!
    HBUF-STATE-IDLE _HBUFI-X @ HBUF.STATE !
    HBUF-S-OK ;

: HBUF-PERSISTENT!  ( flag exchange -- )
    SWAP IF
        HBUF.FLAGS DUP @ HBUF-F-PERSISTENT OR SWAP !
    ELSE
        HBUF.FLAGS DUP @ HBUF-F-PERSISTENT INVERT AND SWAP !
    THEN ;

: _HBUF-MESSAGE-RESET  ( exchange -- )
    DUP HBUF.PARSER HSTR-RESET
    DUP HBUF.BODY-A @ OVER HBUF.BODY-CAP @ 0 FILL
    DUP 0 SWAP HBUF.BODY-U !
    DUP 0 SWAP HBUF.HTTP-CODE !
    DUP 0 SWAP HBUF.REQUEST !
    DUP HBUF-S-OK SWAP HBUF.LAST-STATUS !
    HBUF-STATE-IDLE SWAP HBUF.STATE ! ;

: HBUF-RESET  ( exchange -- )
    DUP HBUF.PORT @ 0= IF _HBUF-MESSAGE-RESET EXIT THEN
    DUP HBUF.PORT @ NIO-CANCEL NIO-S-CANCELLED = IF
        DUP 0 SWAP HBUF.PORT !
        _HBUF-MESSAGE-RESET EXIT
    THEN
    DUP _HBUF-MESSAGE-RESET
    DUP HBUF-S-TRANSPORT SWAP HBUF.LAST-STATUS !
    HBUF-STATE-ERROR SWAP HBUF.STATE ! ;

VARIABLE _HBUFS-REQ
VARIABLE _HBUFS-PORT
VARIABLE _HBUFS-X

: HBUF-START  ( request port exchange -- status )
    _HBUFS-X ! _HBUFS-PORT ! _HBUFS-REQ !
    _HBUFS-X @ 0= _HBUFS-REQ @ 0= OR _HBUFS-PORT @ 0= OR IF
        HBUF-S-INVALID EXIT
    THEN
    _HBUFS-X @ HBUF.STATE @ DUP HBUF-STATE-OPENING =
    OVER HBUF-STATE-SENDING = OR
    OVER HBUF-STATE-RECEIVING = OR
    SWAP HBUF-STATE-CLOSING = OR IF HBUF-S-BUSY EXIT THEN
    _HBUFS-X @ HBUF.FLAGS @ HBUF-F-PERSISTENT AND
    _HBUFS-X @ HBUF.STATE @ HBUF-STATE-DONE = AND
    _HBUFS-X @ HBUF.PORT @ _HBUFS-PORT @ = AND
    _HBUFS-PORT @ NIO.OPEN-STATE @ NIO-OPEN-STATE-OPEN = AND IF
        _HBUFS-X @ _HBUF-MESSAGE-RESET
    ELSE
        _HBUFS-X @ HBUF-RESET
        _HBUFS-X @ HBUF.PORT @ IF HBUF-S-TRANSPORT EXIT THEN
        _HBUFS-PORT @ _HBUFS-X @ HBUF.PORT !
        _HBUFS-REQ @ _HBUFS-X @ HBUF.REQUEST !
        _HBUFS-PORT @ NIO-OPEN-START DUP NIO-S-PENDING = IF
            DROP HBUF-STATE-OPENING _HBUFS-X @ HBUF.STATE !
            HBUF-S-PENDING DUP _HBUFS-X @ HBUF.LAST-STATUS ! EXIT
        THEN
        DUP NIO-S-CANCELLED = IF
            DROP _HBUFS-PORT @ NIO-CANCEL NIO-S-CANCELLED = IF
                0 _HBUFS-X @ HBUF.PORT !
                HBUF-STATE-CANCELLED _HBUFS-X @ HBUF.STATE !
                HBUF-S-CANCELLED DUP _HBUFS-X @ HBUF.LAST-STATUS ! EXIT
            THEN
            HBUF-STATE-ERROR _HBUFS-X @ HBUF.STATE !
            HBUF-S-TRANSPORT DUP _HBUFS-X @ HBUF.LAST-STATUS ! EXIT
        THEN
        NIO-S-OK <> IF
            _HBUFS-PORT @ NIO-CANCEL NIO-S-CANCELLED = IF
                0 _HBUFS-X @ HBUF.PORT !
            THEN
            HBUF-STATE-ERROR _HBUFS-X @ HBUF.STATE !
            HBUF-S-TRANSPORT DUP _HBUFS-X @ HBUF.LAST-STATUS ! EXIT
        THEN
    THEN
    _HBUFS-REQ @ _HBUFS-X @ HBUF.REQUEST !
    HBUF-STATE-SENDING _HBUFS-X @ HBUF.STATE !
    HBUF-S-PENDING DUP _HBUFS-X @ HBUF.LAST-STATUS ! ;

VARIABLE _HBUFP-X
VARIABLE _HBUFP-PUMP
VARIABLE _HBUFP-REUSE-X

: _HBUF-PEER-REUSABLE?  ( exchange -- flag )
    DUP _HBUFP-REUSE-X !
    DUP HBUF.FLAGS @ HBUF-F-PERSISTENT AND 0= IF DROP 0 EXIT THEN
    DUP HBUF.REQUEST @ HREQ.FLAGS @ HREQ-F-CONNECTION-CLOSE AND IF DROP 0 EXIT THEN
    DUP HBUF.PORT @ NIO.OPEN-STATE @ NIO-OPEN-STATE-OPEN <> IF DROP 0 EXIT THEN
    DUP HBUF.PARSER HSTR.BODY-MODE @ HSTR-BODY-CLOSE = IF DROP 0 EXIT THEN
    HBUF.PARSER HSTR.VERSION @ 11 <> IF 0 EXIT THEN
    S" connection" _HBUFP-REUSE-X @ HBUF.PARSER HSTR-HEADER-COUNT
    DUP 1 > IF DROP 0 EXIT THEN
    0= IF -1 EXIT THEN
    S" connection" _HBUFP-REUSE-X @ HBUF.PARSER HSTR-HEADER IF
        S" close" HTTP-COMMA-TOKEN? 0=
    ELSE
        2DROP -1
    THEN ;

: _HBUF-CANCEL-PORT  ( exchange -- flag )
    DUP HBUF.PORT @ 0= IF DROP -1 EXIT THEN
    DUP HBUF.PORT @ NIO-CANCEL NIO-S-CANCELLED = IF
        DUP 0 SWAP HBUF.PORT ! DROP -1
    ELSE
        DROP 0
    THEN ;

: _HBUF-PUBLISH-CANCELLED  ( exchange -- status )
    DUP HBUF.PARSER HSTR-CANCEL
    DUP _HBUF-CANCEL-PORT IF
        DUP HBUF-STATE-CANCELLED SWAP HBUF.STATE !
        HBUF-S-CANCELLED DUP ROT HBUF.LAST-STATUS !
    ELSE
        DUP HBUF-STATE-ERROR SWAP HBUF.STATE !
        HBUF-S-TRANSPORT DUP ROT HBUF.LAST-STATUS !
    THEN ;

: _HBUF-FAIL  ( status exchange -- status )
    >R R@ _HBUF-CANCEL-PORT 0= IF DROP HBUF-S-TRANSPORT THEN
    DUP R@ HBUF.LAST-STATUS !
    HBUF-STATE-ERROR R@ HBUF.STATE !
    R> DROP ;

: _HBUF-PUBLISH-DONE  ( exchange -- status )
    DUP HBUF-STATE-DONE SWAP HBUF.STATE !
    HBUF-S-OK DUP ROT HBUF.LAST-STATUS ! ;

VARIABLE _HBUFC-X
VARIABLE _HBUFC-STATUS

: _HBUF-CLOSE-RESULT  ( nio-status exchange -- status )
    _HBUFC-X ! _HBUFC-STATUS !
    _HBUFC-STATUS @ NIO-S-PENDING = IF
        HBUF-STATE-CLOSING _HBUFC-X @ HBUF.STATE !
        HBUF-S-PENDING DUP _HBUFC-X @ HBUF.LAST-STATUS ! EXIT
    THEN
    _HBUFC-STATUS @ NIO-S-OK = IF
        0 _HBUFC-X @ HBUF.PORT !
        _HBUFC-X @ _HBUF-PUBLISH-DONE EXIT
    THEN
    _HBUFC-STATUS @ NIO-S-CANCELLED = IF
        0 _HBUFC-X @ HBUF.PORT !
        HBUF-STATE-CANCELLED _HBUFC-X @ HBUF.STATE !
        HBUF-S-CANCELLED DUP _HBUFC-X @ HBUF.LAST-STATUS ! EXIT
    THEN
    HBUF-S-TRANSPORT _HBUFC-X @ _HBUF-FAIL ;

: _HBUF-CLOSE-START  ( exchange -- status )
    DUP HBUF.PORT @ ?DUP IF
        NIO-CLOSE-START SWAP _HBUF-CLOSE-RESULT
    ELSE
        _HBUF-PUBLISH-DONE
    THEN ;

: _HBUF-CLOSE-POLL  ( exchange -- status )
    DUP HBUF.PORT @ ?DUP IF
        NIO-CLOSE-POLL SWAP _HBUF-CLOSE-RESULT
    ELSE
        HBUF-S-TRANSPORT SWAP _HBUF-FAIL
    THEN ;

: HBUF-POLL  ( exchange -- status )
    DUP _HBUFP-X ! HBUF.STATE @ HBUF-STATE-OPENING = IF
        _HBUFP-X @ HBUF.PORT @ NIO-OPEN-POLL
        DUP NIO-S-PENDING = IF DROP HBUF-S-PENDING EXIT THEN
        DUP NIO-S-OK = IF
            DROP HBUF-STATE-SENDING _HBUFP-X @ HBUF.STATE !
            HBUF-S-PENDING EXIT
        THEN
        NIO-S-CANCELLED = IF
            _HBUFP-X @ _HBUF-PUBLISH-CANCELLED EXIT
        THEN
        HBUF-S-TRANSPORT _HBUFP-X @ _HBUF-FAIL EXIT
    THEN
    _HBUFP-X @ HBUF.STATE @ HBUF-STATE-CLOSING = IF
        _HBUFP-X @ _HBUF-CLOSE-POLL EXIT
    THEN
    _HBUFP-X @ HBUF.STATE @ HBUF-STATE-SENDING = IF
        _HBUFP-X @ HBUF.PORT @ _HBUFP-X @ HBUF.REQUEST @ HREQ-SEND-STEP
        DUP _HBUFP-PUMP !
        DUP HREQ-PUMP-DONE = IF
            DROP HBUF-STATE-RECEIVING _HBUFP-X @ HBUF.STATE !
            HBUF-S-PENDING EXIT
        THEN
        DUP HREQ-PUMP-IDLE = SWAP HREQ-PUMP-PROGRESS = OR IF
            HBUF-S-PENDING EXIT
        THEN
        _HBUFP-PUMP @ HREQ-PUMP-CANCELLED = IF
            _HBUFP-X @ _HBUF-PUBLISH-CANCELLED EXIT
        THEN
        HBUF-S-TRANSPORT _HBUFP-X @ _HBUF-FAIL EXIT
    THEN
    _HBUFP-X @ HBUF.STATE @ HBUF-STATE-RECEIVING = IF
        _HBUFP-X @ HBUF.PARSER _HBUFP-X @ HBUF.PORT @
        _HBUFP-X @ HBUF.RECV HBUF-RECV-CAPACITY HSTR-PUMP
        DUP _HBUFP-PUMP !
        DUP HSTR-PUMP-IDLE = SWAP HSTR-PUMP-PROGRESS = OR IF
            HBUF-S-PENDING EXIT
        THEN
        _HBUFP-PUMP @ HSTR-PUMP-DONE = IF
            _HBUFP-X @ _HBUF-PEER-REUSABLE? 0= IF
                _HBUFP-X @ _HBUF-CLOSE-START EXIT
            THEN
            _HBUFP-X @ _HBUF-PUBLISH-DONE EXIT
        THEN
        _HBUFP-PUMP @ HSTR-PUMP-CANCELLED = IF
            _HBUFP-X @ _HBUF-PUBLISH-CANCELLED EXIT
        THEN
        _HBUFP-PUMP @ HSTR-PUMP-PARSER-ERROR = IF
            HBUF-S-PROTOCOL _HBUFP-X @ _HBUF-FAIL EXIT
        THEN
        HBUF-S-TRANSPORT _HBUFP-X @ _HBUF-FAIL EXIT
    THEN
    _HBUFP-X @ HBUF.STATE @ HBUF-STATE-DONE = IF HBUF-S-OK EXIT THEN
    _HBUFP-X @ HBUF.STATE @ HBUF-STATE-CANCELLED = IF
        HBUF-S-CANCELLED EXIT
    THEN
    _HBUFP-X @ HBUF.STATE @ HBUF-STATE-ERROR = IF
        _HBUFP-X @ HBUF.LAST-STATUS @ EXIT
    THEN
    HBUF-S-INVALID ;

: HBUF-CANCEL  ( exchange -- status )
    _HBUF-PUBLISH-CANCELLED HBUF-S-CANCELLED = IF
        HBUF-S-OK
    ELSE
        HBUF-S-TRANSPORT
    THEN ;
