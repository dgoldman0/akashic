\ =====================================================================
\  http-buffered.f - Cooperative bounded HTTP exchange over a native port
\ =====================================================================
\  The caller owns the request, port, and response-body buffer. This module
\  owns only parser state and a small receive scratch buffer.
\ =====================================================================

PROVIDED akashic-http-buffered

REQUIRE http-request.f
REQUIRE http-stream.f

0 CONSTANT HBUF-S-OK
1 CONSTANT HBUF-S-PENDING
2 CONSTANT HBUF-S-INVALID
3 CONSTANT HBUF-S-CAPACITY
4 CONSTANT HBUF-S-TRANSPORT
5 CONSTANT HBUF-S-PROTOCOL
6 CONSTANT HBUF-S-BUSY
7 CONSTANT HBUF-S-CANCELLED

0 CONSTANT HBUF-STATE-IDLE
1 CONSTANT HBUF-STATE-SENDING
2 CONSTANT HBUF-STATE-RECEIVING
3 CONSTANT HBUF-STATE-DONE
4 CONSTANT HBUF-STATE-ERROR
5 CONSTANT HBUF-STATE-CANCELLED

4096 CONSTANT HBUF-RECV-CAPACITY

  0 CONSTANT _HBUF-PORT
  8 CONSTANT _HBUF-REQUEST
 16 CONSTANT _HBUF-STATE
 24 CONSTANT _HBUF-LAST-STATUS
 32 CONSTANT _HBUF-BODY-A
 40 CONSTANT _HBUF-BODY-CAP
 48 CONSTANT _HBUF-BODY-U
 56 CONSTANT _HBUF-HTTP-CODE
 64 CONSTANT _HBUF-PARSER
_HBUF-PARSER HSTR-PARSER-SIZE + CONSTANT _HBUF-RECV
_HBUF-RECV HBUF-RECV-CAPACITY + CONSTANT HTTP-BUFFERED-SIZE

: HBUF.PORT        ( exchange -- a ) _HBUF-PORT + ;
: HBUF.REQUEST     ( exchange -- a ) _HBUF-REQUEST + ;
: HBUF.STATE       ( exchange -- a ) _HBUF-STATE + ;
: HBUF.LAST-STATUS ( exchange -- a ) _HBUF-LAST-STATUS + ;
: HBUF.BODY-A      ( exchange -- a ) _HBUF-BODY-A + ;
: HBUF.BODY-CAP    ( exchange -- a ) _HBUF-BODY-CAP + ;
: HBUF.BODY-U      ( exchange -- a ) _HBUF-BODY-U + ;
: HBUF.HTTP-CODE   ( exchange -- a ) _HBUF-HTTP-CODE + ;
: HBUF.PARSER      ( exchange -- parser ) _HBUF-PARSER + ;
: HBUF.RECV        ( exchange -- buffer ) _HBUF-RECV + ;

VARIABLE _HBUFH-P
VARIABLE _HBUFH-X

: _HBUF-HEADERS  ( parser exchange -- status )
    _HBUFH-X ! _HBUFH-P !
    _HBUFH-P @ HSTR.CODE @ _HBUFH-X @ HBUF.HTTP-CODE ! 0 ;

VARIABLE _HBUFB-P
VARIABLE _HBUFB-X
VARIABLE _HBUFB-A
VARIABLE _HBUFB-U

: _HBUF-BODY  ( parser exchange -- status )
    _HBUFB-X ! _HBUFB-P !
    _HBUFB-P @ HSTR-BODY-SLICE _HBUFB-U ! _HBUFB-A !
    _HBUFB-X @ HBUF.BODY-U @ _HBUFB-U @ +
    _HBUFB-X @ HBUF.BODY-CAP @ > IF 1 EXIT THEN
    _HBUFB-A @ _HBUFB-X @ HBUF.BODY-A @
    _HBUFB-X @ HBUF.BODY-U @ + _HBUFB-U @ CMOVE
    _HBUFB-U @ _HBUFB-X @ HBUF.BODY-U +! 0 ;

VARIABLE _HBUFI-X

: HBUF-INIT  ( body-a body-capacity exchange -- status )
    DUP _HBUFI-X ! HTTP-BUFFERED-SIZE 0 FILL
    OVER 0= OVER 0> 0= OR IF 2DROP HBUF-S-INVALID EXIT THEN
    _HBUFI-X @ HBUF.BODY-CAP ! _HBUFI-X @ HBUF.BODY-A !
    _HBUFI-X @ HBUF.PARSER HSTR-INIT
    ['] _HBUF-HEADERS _HBUFI-X @ HBUF.PARSER HSTR-ON-HEADERS!
    ['] _HBUF-BODY _HBUFI-X @ HBUF.PARSER HSTR-ON-BODY!
    _HBUFI-X @ _HBUFI-X @ HBUF.PARSER HSTR-CONTEXT!
    HBUF-STATE-IDLE _HBUFI-X @ HBUF.STATE !
    HBUF-S-OK ;

: HBUF-RESET  ( exchange -- )
    DUP HBUF.PORT @ ?DUP IF NIO-CLOSE THEN
    DUP HBUF.PARSER HSTR-RESET
    DUP HBUF.BODY-A @ OVER HBUF.BODY-CAP @ 0 FILL
    DUP 0 SWAP HBUF.BODY-U !
    DUP 0 SWAP HBUF.HTTP-CODE !
    DUP 0 SWAP HBUF.REQUEST !
    DUP 0 SWAP HBUF.PORT !
    DUP HBUF-S-OK SWAP HBUF.LAST-STATUS !
    HBUF-STATE-IDLE SWAP HBUF.STATE ! ;

VARIABLE _HBUFS-REQ
VARIABLE _HBUFS-PORT
VARIABLE _HBUFS-X

: HBUF-START  ( request port exchange -- status )
    _HBUFS-X ! _HBUFS-PORT ! _HBUFS-REQ !
    _HBUFS-X @ 0= _HBUFS-REQ @ 0= OR _HBUFS-PORT @ 0= OR IF
        HBUF-S-INVALID EXIT
    THEN
    _HBUFS-X @ HBUF.STATE @ DUP HBUF-STATE-SENDING =
    SWAP HBUF-STATE-RECEIVING = OR IF HBUF-S-BUSY EXIT THEN
    _HBUFS-X @ HBUF-RESET
    _HBUFS-PORT @ NIO-OPEN NIO-S-OK <> IF
        HBUF-STATE-ERROR _HBUFS-X @ HBUF.STATE !
        HBUF-S-TRANSPORT DUP _HBUFS-X @ HBUF.LAST-STATUS ! EXIT
    THEN
    _HBUFS-PORT @ _HBUFS-X @ HBUF.PORT !
    _HBUFS-REQ @ _HBUFS-X @ HBUF.REQUEST !
    HBUF-STATE-SENDING _HBUFS-X @ HBUF.STATE !
    HBUF-S-PENDING DUP _HBUFS-X @ HBUF.LAST-STATUS ! ;

VARIABLE _HBUFP-X
VARIABLE _HBUFP-PUMP

: _HBUF-FAIL  ( status exchange -- status )
    >R DUP R@ HBUF.LAST-STATUS !
    HBUF-STATE-ERROR R@ HBUF.STATE !
    R@ HBUF.PORT @ ?DUP IF NIO-CLOSE THEN
    R> DROP ;

: HBUF-POLL  ( exchange -- status )
    DUP _HBUFP-X ! HBUF.STATE @ HBUF-STATE-SENDING = IF
        _HBUFP-X @ HBUF.PORT @ _HBUFP-X @ HBUF.REQUEST @ HREQ-SEND-STEP
        DUP HREQ-PUMP-DONE = IF
            DROP HBUF-STATE-RECEIVING _HBUFP-X @ HBUF.STATE !
            HBUF-S-PENDING EXIT
        THEN
        DUP HREQ-PUMP-IDLE = SWAP HREQ-PUMP-PROGRESS = OR IF
            HBUF-S-PENDING EXIT
        THEN
        HBUF-S-TRANSPORT _HBUFP-X @ _HBUF-FAIL EXIT
    THEN
    _HBUFP-X @ HBUF.STATE @ HBUF-STATE-RECEIVING = IF
        _HBUFP-X @ HBUF.PARSER _HBUFP-X @ HBUF.PORT @
        _HBUFP-X @ HBUF.RECV HBUF-RECV-CAPACITY HSTR-PUMP
        DUP _HBUFP-PUMP !
        DUP HSTR-PUMP-IDLE = SWAP HSTR-PUMP-PROGRESS = OR IF
            HBUF-S-PENDING EXIT
        THEN
        _HBUFP-PUMP @ HSTR-PUMP-DONE = IF
            _HBUFP-X @ HBUF.PORT @ NIO-CLOSE
            0 _HBUFP-X @ HBUF.PORT !
            HBUF-STATE-DONE _HBUFP-X @ HBUF.STATE !
            HBUF-S-OK DUP _HBUFP-X @ HBUF.LAST-STATUS ! EXIT
        THEN
        _HBUFP-PUMP @ HSTR-PUMP-CANCELLED = IF
            HBUF-STATE-CANCELLED _HBUFP-X @ HBUF.STATE !
            HBUF-S-CANCELLED DUP _HBUFP-X @ HBUF.LAST-STATUS ! EXIT
        THEN
        _HBUFP-PUMP @ HSTR-PUMP-PARSER-ERROR = IF
            HBUF-S-PROTOCOL _HBUFP-X @ _HBUF-FAIL EXIT
        THEN
        HBUF-S-TRANSPORT _HBUFP-X @ _HBUF-FAIL EXIT
    THEN
    _HBUFP-X @ HBUF.STATE @ HBUF-STATE-DONE = IF HBUF-S-OK EXIT THEN
    _HBUFP-X @ HBUF.STATE @ HBUF-STATE-CANCELLED = IF
        HBUF-S-CANCELLED EXIT
    THEN
    _HBUFP-X @ HBUF.STATE @ HBUF-STATE-ERROR = IF
        _HBUFP-X @ HBUF.LAST-STATUS @ EXIT
    THEN
    HBUF-S-INVALID ;

: HBUF-CANCEL  ( exchange -- status )
    DUP HBUF.PARSER HSTR-CANCEL
    DUP HBUF.PORT @ ?DUP IF NIO-CLOSE THEN
    DUP 0 SWAP HBUF.PORT !
    DUP HBUF-STATE-CANCELLED SWAP HBUF.STATE !
    HBUF-S-CANCELLED SWAP HBUF.LAST-STATUS ! HBUF-S-OK ;
