: NIO.CLOSE-STATUS   ( port -- a ) _NIO-CLOSE-STATUS + ;

: NIO-INIT  ( port -- ) NET-IO-PORT-SIZE 0 FILL ;

: _NIO-CLEANUP-FLAG?  ( mask port -- flag )
    NIO.CLEANUP-FLAGS @ AND 0<> ;

: _NIO-CLEANUP-MARK  ( mask port -- )
    NIO.CLEANUP-FLAGS DUP @ ROT OR SWAP ! ;

: _NIO-CLEANUP-RESET  ( port -- )
    DUP 0 SWAP NIO.CLEANUP-FLAGS !
    DUP 0 SWAP NIO.CANCEL-ERROR !
    DUP 0 SWAP NIO.CLOSE-ERROR !
    DUP NIO-CLOSE-STATE-IDLE SWAP NIO.CLOSE-STATE !
    NIO-S-OK SWAP NIO.CLOSE-STATUS ! ;

: _NIO-CLEANUP-FAILED?  ( port -- flag )
    DUP NIO.CLOSE-STATE @ NIO-CLOSE-STATE-CANCELLED =
    OVER NIO.CANCEL-ERROR @ 0= AND IF DROP 0 EXIT THEN
    DUP NIO.CANCEL-ERROR @ OVER NIO.CLOSE-ERROR @ OR
    SWAP NIO.CLOSE-STATE @ NIO-CLOSE-STATE-FAILED = OR ;

: _NIO-CLOSE-ACTIVE?  ( port -- flag )
    NIO.CLOSE-STATE @ NIO-CLOSE-STATE-CLOSING = ;

: _NIO-CALL-STATUS  ( context xt -- io-status )
    CATCH ?DUP IF 2DROP NIO-S-FAILED THEN ;

: _NIO-CALL-CLEANUP  ( context xt -- exception )
    CATCH ?DUP IF NIP ELSE 0 THEN ;

: _NIO-CALL-CLOSE-STATUS  ( context xt port -- io-status )
    >R CATCH ?DUP IF
        DUP R@ NIO.CLOSE-ERROR !
        2DROP NIO-S-FAILED
    THEN
    R> DROP ;

: NIO-POLL  ( port -- )
    DUP 0= IF DROP EXIT THEN
    DUP NIO.POLL-XT @ ?DUP IF
        OVER >R SWAP NIO.CONTEXT @ SWAP _NIO-CALL-CLEANUP IF
            NIO-OPEN-STATE-FAILED R@ NIO.OPEN-STATE !
            NIO-S-FAILED R@ NIO.OPEN-STATUS !
        THEN
        R> DROP
    ELSE
        DROP
    THEN ;

: _NIO-BLOCKING-STATUS  ( io-status -- normalized-status )
    DUP NIO-S-OK = OVER NIO-S-FAILED = OR
    OVER NIO-S-CANCELLED = OR 0= IF DROP NIO-S-FAILED THEN ;

: _NIO-ASYNC-STATUS  ( io-status -- normalized-status )
    DUP NIO-S-OK = OVER NIO-S-PENDING = OR
    OVER NIO-S-FAILED = OR OVER NIO-S-CANCELLED = OR 0= IF
        DROP NIO-S-FAILED
    THEN ;

: _NIO-OPEN-PUBLISH  ( io-status port -- io-status )
    >R _NIO-ASYNC-STATUS
    DUP R@ NIO.OPEN-STATUS !
    DUP NIO-S-PENDING = IF NIO-OPEN-STATE-OPENING R@ NIO.OPEN-STATE ! THEN
    DUP NIO-S-OK = IF NIO-OPEN-STATE-OPEN R@ NIO.OPEN-STATE ! THEN
    DUP NIO-S-CANCELLED = IF NIO-OPEN-STATE-CANCELLED R@ NIO.OPEN-STATE ! THEN
    DUP NIO-S-FAILED = IF NIO-OPEN-STATE-FAILED R@ NIO.OPEN-STATE ! THEN
    R> DROP ;

: NIO-OPEN  ( port -- io-status )
    DUP 0= IF DROP NIO-S-FAILED EXIT THEN
    DUP _NIO-CLOSE-ACTIVE? IF DROP NIO-S-FAILED EXIT THEN
    DUP _NIO-CLEANUP-FAILED? IF DROP NIO-S-FAILED EXIT THEN
    DUP _NIO-CLEANUP-RESET
    DUP NIO.OPEN-XT @ ?DUP IF
        OVER >R SWAP NIO.CONTEXT @ SWAP _NIO-CALL-STATUS
        _NIO-BLOCKING-STATUS R> _NIO-OPEN-PUBLISH
    ELSE
        NIO-S-OK SWAP _NIO-OPEN-PUBLISH
    THEN
    ;

: NIO-OPEN-START  ( port -- io-status )
    DUP 0= IF DROP NIO-S-FAILED EXIT THEN
    DUP _NIO-CLOSE-ACTIVE? IF DROP NIO-S-FAILED EXIT THEN
    DUP _NIO-CLEANUP-FAILED? IF DROP NIO-S-FAILED EXIT THEN
    DUP NIO.OPEN-STATE @ DUP NIO-OPEN-STATE-OPEN = IF
        2DROP NIO-S-OK EXIT
    THEN
    NIO-OPEN-STATE-OPENING = IF DROP NIO-S-PENDING EXIT THEN
    DUP NIO.OPEN-START-XT @ ?DUP IF
        OVER _NIO-CLEANUP-RESET
        OVER >R SWAP NIO.CONTEXT @ SWAP _NIO-CALL-STATUS
        R> _NIO-OPEN-PUBLISH
    ELSE
        NIO-OPEN
    THEN ;

: NIO-OPEN-POLL  ( port -- io-status )
    DUP 0= IF DROP NIO-S-FAILED EXIT THEN
    DUP NIO.OPEN-STATE @ DUP NIO-OPEN-STATE-OPEN = IF
        2DROP NIO-S-OK EXIT
    THEN
    NIO-OPEN-STATE-OPENING <> IF DROP NIO-S-FAILED EXIT THEN
    DUP NIO.OPEN-POLL-XT @ ?DUP IF
        OVER >R SWAP NIO.CONTEXT @ SWAP _NIO-CALL-STATUS
        R> _NIO-OPEN-PUBLISH
    ELSE
        NIO-S-FAILED SWAP _NIO-OPEN-PUBLISH
    THEN ;

: _NIO-CLOSE-PUBLISH  ( io-status port -- io-status )
    >R _NIO-ASYNC-STATUS
    DUP R@ NIO.CLOSE-STATUS !
    DUP NIO-S-PENDING = IF
        NIO-CLOSE-STATE-CLOSING R@ NIO.CLOSE-STATE !
    THEN
    DUP NIO-S-OK = IF
        NIO-CLOSE-STATE-CLOSED R@ NIO.CLOSE-STATE !
        NIO-OPEN-STATE-CLOSED R@ NIO.OPEN-STATE !
        NIO-S-OK R@ NIO.OPEN-STATUS !
    THEN
    DUP NIO-S-CANCELLED = IF
        NIO-CLOSE-STATE-CANCELLED R@ NIO.CLOSE-STATE !
        NIO-OPEN-STATE-CANCELLED R@ NIO.OPEN-STATE !
        NIO-S-CANCELLED R@ NIO.OPEN-STATUS !
    THEN
    DUP NIO-S-FAILED = IF
        NIO-CLOSE-STATE-FAILED R@ NIO.CLOSE-STATE !
        NIO-OPEN-STATE-FAILED R@ NIO.OPEN-STATE !
        NIO-S-FAILED R@ NIO.OPEN-STATUS !
    THEN
    R> DROP ;

: NIO-CLOSE-STATUS  ( port -- io-status )
    DUP 0= IF DROP NIO-S-FAILED EXIT THEN
    DUP NIO.CLOSE-STATE @ CASE
        NIO-CLOSE-STATE-CLOSING OF DROP NIO-S-PENDING EXIT ENDOF
        NIO-CLOSE-STATE-CLOSED OF DROP NIO-S-OK EXIT ENDOF
        NIO-CLOSE-STATE-FAILED OF DROP NIO-S-FAILED EXIT ENDOF
        NIO-CLOSE-STATE-CANCELLED OF DROP NIO-S-CANCELLED EXIT ENDOF
    ENDCASE
    NIO-CLEANUP-F-CLOSE-ATTEMPTED OVER _NIO-CLEANUP-FLAG? IF
        DUP NIO.CLOSE-ERROR @ IF
            NIO-S-FAILED SWAP _NIO-CLOSE-PUBLISH
        ELSE
            NIO-S-OK SWAP _NIO-CLOSE-PUBLISH
        THEN
        EXIT
    THEN
    NIO-CLEANUP-F-CLOSE-ATTEMPTED OVER _NIO-CLEANUP-MARK
    DUP 0 SWAP NIO.CLOSE-ERROR !
    DUP NIO.CLOSE-XT @ ?DUP IF
        OVER >R SWAP NIO.CONTEXT @ SWAP _NIO-CALL-CLEANUP
        DUP IF
            DUP R@ NIO.CLOSE-ERROR !
            NIO-OPEN-STATE-FAILED R@ NIO.OPEN-STATE !
            NIO-S-FAILED R@ NIO.OPEN-STATUS !
            DROP R> NIO-S-FAILED SWAP _NIO-CLOSE-PUBLISH EXIT
        THEN
        DROP R>
    THEN
    NIO-S-OK SWAP _NIO-CLOSE-PUBLISH ;

: NIO-CLOSE-START  ( port -- io-status )
    DUP 0= IF DROP NIO-S-FAILED EXIT THEN
    DUP NIO.CLOSE-STATE @ CASE
        NIO-CLOSE-STATE-CLOSING OF DROP NIO-S-PENDING EXIT ENDOF
        NIO-CLOSE-STATE-CLOSED OF DROP NIO-S-OK EXIT ENDOF
        NIO-CLOSE-STATE-FAILED OF DROP NIO-S-FAILED EXIT ENDOF
        NIO-CLOSE-STATE-CANCELLED OF DROP NIO-S-CANCELLED EXIT ENDOF
    ENDCASE
    DUP NIO.CLOSE-START-XT @ ?DUP IF
        NIO-CLEANUP-F-CLOSE-ATTEMPTED 2 PICK _NIO-CLEANUP-MARK
        0 2 PICK NIO.CLOSE-ERROR !
        OVER >R SWAP NIO.CONTEXT @ SWAP R@ _NIO-CALL-CLOSE-STATUS
        R> _NIO-CLOSE-PUBLISH
    ELSE
        NIO-CLOSE-STATUS
    THEN ;

: NIO-CLOSE-POLL  ( port -- io-status )
    DUP 0= IF DROP NIO-S-FAILED EXIT THEN
    DUP NIO.CLOSE-STATE @ CASE
        NIO-CLOSE-STATE-CLOSED OF DROP NIO-S-OK EXIT ENDOF
        NIO-CLOSE-STATE-FAILED OF DROP NIO-S-FAILED EXIT ENDOF
        NIO-CLOSE-STATE-CANCELLED OF DROP NIO-S-CANCELLED EXIT ENDOF
        NIO-CLOSE-STATE-CLOSING OF
            DUP NIO.CLOSE-POLL-XT @ ?DUP IF
                OVER >R SWAP NIO.CONTEXT @ SWAP R@
                _NIO-CALL-CLOSE-STATUS
                R> _NIO-CLOSE-PUBLISH EXIT
            THEN
            NIO-S-FAILED SWAP _NIO-CLOSE-PUBLISH EXIT
        ENDOF
    ENDCASE
    DROP NIO-S-FAILED ;

: NIO-CLOSE  ( port -- ) NIO-CLOSE-STATUS DROP ;

: NIO-CANCEL  ( port -- io-status )
    DUP 0= IF DROP NIO-S-FAILED EXIT THEN
    NIO-CLEANUP-F-CANCEL-ATTEMPTED OVER _NIO-CLEANUP-FLAG? IF
        DUP _NIO-CLEANUP-FAILED? IF DROP NIO-S-FAILED EXIT THEN
        DROP NIO-S-CANCELLED EXIT
    THEN
    NIO-CLEANUP-F-CANCEL-ATTEMPTED OVER _NIO-CLEANUP-MARK
    DUP 0 SWAP NIO.CANCEL-ERROR !
    DUP NIO.CANCEL-XT @ ?DUP IF
        OVER >R SWAP NIO.CONTEXT @ SWAP _NIO-CALL-CLEANUP
        DUP IF
            DUP R@ NIO.CANCEL-ERROR ! DROP
            R> NIO-S-FAILED SWAP _NIO-CLOSE-PUBLISH EXIT
        THEN
        DROP R>
    ELSE
        DUP NIO-CLOSE-STATUS NIO-S-OK <> IF
            NIO-S-FAILED SWAP _NIO-CLOSE-PUBLISH EXIT
        THEN
        NIO-OPEN-STATE-CANCELLED OVER NIO.OPEN-STATE !
        NIO-S-CANCELLED OVER NIO.OPEN-STATUS !
        DROP NIO-S-CANCELLED EXIT
    THEN
    NIO-S-CANCELLED SWAP _NIO-CLOSE-PUBLISH ;

: _NIO-SEND-RESULT  ( count io-status limit -- count io-status )
    >R
    DUP NIO-S-OK = OVER NIO-S-FAILED = OR
    OVER NIO-S-CANCELLED = OR 0= IF
        2DROP R> DROP 0 NIO-S-FAILED EXIT
    THEN
    DUP NIO-S-OK <> IF
        OVER IF 2DROP R> DROP 0 NIO-S-FAILED ELSE R> DROP THEN
        EXIT
    THEN
    OVER 0< 2 PICK R@ > OR IF
        2DROP R> DROP 0 NIO-S-FAILED EXIT
    THEN
    R> DROP ;

: _NIO-RECV-RESULT  ( count io-status capacity -- count io-status )
    >R
    DUP NIO-S-OK = OVER NIO-S-EOF = OR
    OVER NIO-S-FAILED = OR OVER NIO-S-CANCELLED = OR 0= IF
        2DROP R> DROP 0 NIO-S-FAILED EXIT
    THEN
    DUP NIO-S-OK <> IF
        OVER IF 2DROP R> DROP 0 NIO-S-FAILED ELSE R> DROP THEN
        EXIT
    THEN
    OVER 0< 2 PICK R@ > OR IF
        2DROP R> DROP 0 NIO-S-FAILED EXIT
    THEN
    R> DROP ;

: NIO-RECV  ( buffer capacity port -- count io-status )
    DUP 0= IF DROP 2DROP 0 NIO-S-FAILED EXIT THEN
    OVER 0< IF DROP 2DROP 0 NIO-S-FAILED EXIT THEN
    2 PICK 0= 2 PICK 0> AND IF DROP 2DROP 0 NIO-S-FAILED EXIT THEN
    DUP NIO.RECV-XT @ ?DUP IF
        2 PICK >R OVER >R
        SWAP NIO.CONTEXT @ SWAP CATCH ?DUP IF
            2DROP 2DROP R> DROP R> DROP 0 NIO-S-FAILED EXIT
        THEN
        R> DROP R> _NIO-RECV-RESULT
    ELSE
        DROP 2DROP 0 NIO-S-FAILED
    THEN
    ;

: NIO-SEND  ( buffer length port -- count io-status )
    DUP 0= IF DROP 2DROP 0 NIO-S-FAILED EXIT THEN
    OVER 0< IF DROP 2DROP 0 NIO-S-FAILED EXIT THEN
    2 PICK 0= 2 PICK 0> AND IF DROP 2DROP 0 NIO-S-FAILED EXIT THEN
    DUP NIO.SEND-XT @ ?DUP IF
        2 PICK >R OVER >R
        SWAP NIO.CONTEXT @ SWAP CATCH ?DUP IF
            2DROP 2DROP R> DROP R> DROP 0 NIO-S-FAILED EXIT
        THEN
        R> DROP R> _NIO-SEND-RESULT
    ELSE
        DROP 2DROP 0 NIO-S-FAILED
    THEN
    ;
