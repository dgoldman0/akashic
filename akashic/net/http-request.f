\ =====================================================================
\  http-request.f - Caller-owned bounded HTTP/1.1 request writer
\ =====================================================================
\  The request owns no socket and allocates no memory. Its caller supplies a
\  buffer and a net/io-port transport. Clearing the descriptor zeroes the
\  complete buffer, including any Authorization header copied into it.
\ =====================================================================

PROVIDED akashic-http-request

REQUIRE io-port.f
REQUIRE ../utils/string.f

0 CONSTANT HREQ-STATE-EMPTY
1 CONSTANT HREQ-STATE-BUILDING
2 CONSTANT HREQ-STATE-SEALED
3 CONSTANT HREQ-STATE-SENDING
4 CONSTANT HREQ-STATE-SENT
5 CONSTANT HREQ-STATE-ERROR

0 CONSTANT HREQ-S-OK
1 CONSTANT HREQ-S-INVALID
2 CONSTANT HREQ-S-CAPACITY
3 CONSTANT HREQ-S-STATE
4 CONSTANT HREQ-S-TRANSPORT
5 CONSTANT HREQ-S-CANCELLED

0 CONSTANT HREQ-PUMP-IDLE
1 CONSTANT HREQ-PUMP-PROGRESS
2 CONSTANT HREQ-PUMP-DONE
3 CONSTANT HREQ-PUMP-ERROR
4 CONSTANT HREQ-PUMP-CANCELLED

 0 CONSTANT _HREQ-BUFFER
 8 CONSTANT _HREQ-CAPACITY
16 CONSTANT _HREQ-LENGTH
24 CONSTANT _HREQ-SENT
32 CONSTANT _HREQ-STATE
40 CONSTANT _HREQ-STATUS
48 CONSTANT _HREQ-FLAGS
56 CONSTANT _HREQ-RESERVED
64 CONSTANT HTTP-REQUEST-SIZE

: HREQ.BUFFER   ( request -- a ) _HREQ-BUFFER + ;
: HREQ.CAPACITY ( request -- a ) _HREQ-CAPACITY + ;
: HREQ.LENGTH   ( request -- a ) _HREQ-LENGTH + ;
: HREQ.SENT     ( request -- a ) _HREQ-SENT + ;
: HREQ.STATE    ( request -- a ) _HREQ-STATE + ;
: HREQ.STATUS   ( request -- a ) _HREQ-STATUS + ;
: HREQ.FLAGS    ( request -- a ) _HREQ-FLAGS + ;

CREATE _HREQ-CRLF 13 C, 10 C,

VARIABLE _HRI-BUFFER
VARIABLE _HRI-CAPACITY
VARIABLE _HRI-REQUEST

: HREQ-INIT  ( buffer capacity request -- status )
    _HRI-REQUEST ! _HRI-CAPACITY ! _HRI-BUFFER !
    _HRI-REQUEST @ 0= IF HREQ-S-INVALID EXIT THEN
    _HRI-REQUEST @ HTTP-REQUEST-SIZE 0 FILL
    _HRI-BUFFER @ 0= _HRI-CAPACITY @ 64 < OR IF
        HREQ-S-INVALID _HRI-REQUEST @ HREQ.STATUS !
        HREQ-STATE-ERROR _HRI-REQUEST @ HREQ.STATE !
        HREQ-S-INVALID EXIT
    THEN
    _HRI-BUFFER @ _HRI-CAPACITY @ 0 FILL
    _HRI-BUFFER @ _HRI-REQUEST @ HREQ.BUFFER !
    _HRI-CAPACITY @ _HRI-REQUEST @ HREQ.CAPACITY !
    HREQ-S-OK ;

VARIABLE _HRC-REQUEST
VARIABLE _HRC-BUFFER
VARIABLE _HRC-CAPACITY

: HREQ-CLEAR  ( request -- )
    DUP 0= IF DROP EXIT THEN
    DUP _HRC-REQUEST !
    DUP HREQ.BUFFER @ _HRC-BUFFER !
    HREQ.CAPACITY @ _HRC-CAPACITY !
    _HRC-BUFFER @ ?DUP IF _HRC-CAPACITY @ 0 FILL THEN
    _HRC-REQUEST @ HTTP-REQUEST-SIZE 0 FILL
    _HRC-BUFFER @ _HRC-REQUEST @ HREQ.BUFFER !
    _HRC-CAPACITY @ _HRC-REQUEST @ HREQ.CAPACITY ! ;

: _HREQ-FAIL  ( status request -- status )
    >R DUP R@ HREQ.STATUS ! HREQ-STATE-ERROR R> HREQ.STATE ! ;

VARIABLE _HRA-A
VARIABLE _HRA-U
VARIABLE _HRA-R

: _HREQ-APPEND  ( addr len request -- status )
    _HRA-R ! _HRA-U ! _HRA-A !
    _HRA-U @ 0< _HRA-A @ 0= _HRA-U @ 0> AND OR IF
        HREQ-S-INVALID _HRA-R @ _HREQ-FAIL EXIT
    THEN
    _HRA-R @ HREQ.LENGTH @ _HRA-U @ +
    _HRA-R @ HREQ.CAPACITY @ > IF
        HREQ-S-CAPACITY _HRA-R @ _HREQ-FAIL EXIT
    THEN
    _HRA-U @ IF
        _HRA-A @ _HRA-R @ HREQ.BUFFER @ _HRA-R @ HREQ.LENGTH @ +
        _HRA-U @ CMOVE
        _HRA-U @ _HRA-R @ HREQ.LENGTH +!
    THEN
    HREQ-S-OK ;

CREATE _HREQ-SEPARATORS
    40 C, 41 C, 60 C, 62 C, 64 C, 44 C, 59 C, 58 C, 92 C,
    34 C, 47 C, 91 C, 93 C, 63 C, 61 C, 123 C, 125 C,
17 CONSTANT _HREQ-SEPARATOR-COUNT

: _HREQ-TOKEN-CHAR?  ( c -- flag )
    DUP 33 < OVER 126 > OR IF DROP 0 EXIT THEN
    _HREQ-SEPARATORS _HREQ-SEPARATOR-COUNT ROT STR-INDEX 0< ;

: _HREQ-TOKEN?  ( addr len -- flag )
    DUP 0= IF 2DROP 0 EXIT THEN
    0 ?DO
        DUP I + C@ _HREQ-TOKEN-CHAR? 0= IF DROP 0 UNLOOP EXIT THEN
    LOOP
    DROP -1 ;

: _HREQ-PATH?  ( addr len -- flag )
    DUP 0= IF 2DROP 0 EXIT THEN
    OVER C@ 47 <> IF 2DROP 0 EXIT THEN
    0 ?DO
        DUP I + C@ DUP 33 < SWAP 126 > OR IF DROP 0 UNLOOP EXIT THEN
    LOOP
    DROP -1 ;

: _HREQ-VALUE?  ( addr len -- flag )
    0 ?DO
        DUP I + C@ DUP 9 = IF DROP ELSE
            DUP 32 < SWAP 127 = OR IF DROP 0 UNLOOP EXIT THEN
        THEN
    LOOP
    DROP -1 ;

: _HREQ-BEARER?  ( addr len -- flag )
    DUP 0= IF 2DROP 0 EXIT THEN
    0 ?DO
        DUP I + C@ DUP 33 < SWAP 126 > OR IF DROP 0 UNLOOP EXIT THEN
    LOOP
    DROP -1 ;

VARIABLE _HRB-MA
VARIABLE _HRB-MU
VARIABLE _HRB-PA
VARIABLE _HRB-PU
VARIABLE _HRB-R

: HREQ-BEGIN  ( method-a method-u path-a path-u request -- status )
    _HRB-R ! _HRB-PU ! _HRB-PA ! _HRB-MU ! _HRB-MA !
    _HRB-R @ 0= IF HREQ-S-INVALID EXIT THEN
    _HRB-R @ HREQ.STATE @ HREQ-STATE-EMPTY <> IF
        HREQ-S-STATE _HRB-R @ _HREQ-FAIL EXIT
    THEN
    _HRB-MA @ _HRB-MU @ _HREQ-TOKEN? 0=
    _HRB-PA @ _HRB-PU @ _HREQ-PATH? 0= OR IF
        HREQ-S-INVALID _HRB-R @ _HREQ-FAIL EXIT
    THEN
    HREQ-STATE-BUILDING _HRB-R @ HREQ.STATE !
    _HRB-MA @ _HRB-MU @ _HRB-R @ _HREQ-APPEND ?DUP IF EXIT THEN
    S"  " _HRB-R @ _HREQ-APPEND ?DUP IF EXIT THEN
    _HRB-PA @ _HRB-PU @ _HRB-R @ _HREQ-APPEND ?DUP IF EXIT THEN
    S"  HTTP/1.1" _HRB-R @ _HREQ-APPEND ?DUP IF EXIT THEN
    _HREQ-CRLF 2 _HRB-R @ _HREQ-APPEND ;

VARIABLE _HRH-NA
VARIABLE _HRH-NU
VARIABLE _HRH-VA
VARIABLE _HRH-VU
VARIABLE _HRH-R

: HREQ-HEADER  ( name-a name-u value-a value-u request -- status )
    _HRH-R ! _HRH-VU ! _HRH-VA ! _HRH-NU ! _HRH-NA !
    _HRH-R @ HREQ.STATE @ HREQ-STATE-BUILDING <> IF
        HREQ-S-STATE _HRH-R @ _HREQ-FAIL EXIT
    THEN
    _HRH-NA @ _HRH-NU @ _HREQ-TOKEN? 0=
    _HRH-VA @ _HRH-VU @ _HREQ-VALUE? 0= OR IF
        HREQ-S-INVALID _HRH-R @ _HREQ-FAIL EXIT
    THEN
    _HRH-NA @ _HRH-NU @ _HRH-R @ _HREQ-APPEND ?DUP IF EXIT THEN
    S" : " _HRH-R @ _HREQ-APPEND ?DUP IF EXIT THEN
    _HRH-VA @ _HRH-VU @ _HRH-R @ _HREQ-APPEND ?DUP IF EXIT THEN
    _HREQ-CRLF 2 _HRH-R @ _HREQ-APPEND ;

: HREQ-HOST  ( value-a value-u request -- status )
    >R S" Host" 2SWAP R> HREQ-HEADER ;

: HREQ-ACCEPT  ( value-a value-u request -- status )
    >R S" Accept" 2SWAP R> HREQ-HEADER ;

: HREQ-CONTENT-TYPE  ( value-a value-u request -- status )
    >R S" Content-Type" 2SWAP R> HREQ-HEADER ;

: HREQ-USER-AGENT  ( value-a value-u request -- status )
    >R S" User-Agent" 2SWAP R> HREQ-HEADER ;

: HREQ-CONNECTION-CLOSE  ( request -- status )
    >R S" Connection" S" close" R> HREQ-HEADER ;

VARIABLE _HRAB-A
VARIABLE _HRAB-U
VARIABLE _HRAB-R

: HREQ-AUTH-BEARER  ( token-a token-u request -- status )
    _HRAB-R ! _HRAB-U ! _HRAB-A !
    _HRAB-A @ _HRAB-U @ _HREQ-BEARER? 0= IF
        HREQ-S-INVALID _HRAB-R @ _HREQ-FAIL EXIT
    THEN
    _HRAB-R @ HREQ.STATE @ HREQ-STATE-BUILDING <> IF
        HREQ-S-STATE _HRAB-R @ _HREQ-FAIL EXIT
    THEN
    S" Authorization: Bearer " _HRAB-R @ _HREQ-APPEND ?DUP IF EXIT THEN
    _HRAB-A @ _HRAB-U @ _HRAB-R @ _HREQ-APPEND ?DUP IF EXIT THEN
    _HREQ-CRLF 2 _HRAB-R @ _HREQ-APPEND ;

: HREQ-CONTENT-LENGTH  ( length request -- status )
    >R DUP 0< IF DROP HREQ-S-INVALID R> _HREQ-FAIL EXIT THEN
    NUM>STR R> >R S" Content-Length" 2SWAP R> HREQ-HEADER ;

: HREQ-SEAL  ( request -- status )
    DUP HREQ.STATE @ HREQ-STATE-BUILDING <> IF
        HREQ-S-STATE SWAP _HREQ-FAIL EXIT
    THEN
    >R _HREQ-CRLF 2 R@ _HREQ-APPEND DUP 0= IF
        HREQ-STATE-SEALED R@ HREQ.STATE !
    THEN
    R> DROP ;

VARIABLE _HRE-A
VARIABLE _HRE-U
VARIABLE _HRE-R

: HREQ-BODY  ( body-a body-u request -- status )
    _HRE-R ! _HRE-U ! _HRE-A !
    _HRE-R @ HREQ.STATE @ HREQ-STATE-BUILDING <> IF
        HREQ-S-STATE _HRE-R @ _HREQ-FAIL EXIT
    THEN
    _HRE-A @ 0= _HRE-U @ 0> AND IF
        HREQ-S-INVALID _HRE-R @ _HREQ-FAIL EXIT
    THEN
    _HRE-U @ _HRE-R @ HREQ-CONTENT-LENGTH ?DUP IF EXIT THEN
    _HREQ-CRLF 2 _HRE-R @ _HREQ-APPEND ?DUP IF EXIT THEN
    _HRE-A @ _HRE-U @ _HRE-R @ _HREQ-APPEND ?DUP IF EXIT THEN
    HREQ-STATE-SEALED _HRE-R @ HREQ.STATE !
    HREQ-S-OK ;

VARIABLE _HRP-PORT
VARIABLE _HRP-R
VARIABLE _HRP-N
VARIABLE _HRP-IO
VARIABLE _HRP-REMAINING

: HREQ-SEND-STEP  ( port request -- pump-status )
    _HRP-R ! _HRP-PORT !
    _HRP-R @ HREQ.STATE @ DUP HREQ-STATE-SENT = IF
        DROP HREQ-PUMP-DONE EXIT
    THEN
    DUP HREQ-STATE-SEALED <> SWAP HREQ-STATE-SENDING <> AND IF
        HREQ-S-STATE _HRP-R @ _HREQ-FAIL DROP HREQ-PUMP-ERROR EXIT
    THEN
    _HRP-R @ HREQ.LENGTH @ _HRP-R @ HREQ.SENT @ - DUP _HRP-REMAINING !
    DUP 0< SWAP 0= OR IF
        HREQ-STATE-SENT _HRP-R @ HREQ.STATE ! HREQ-PUMP-DONE EXIT
    THEN
    HREQ-STATE-SENDING _HRP-R @ HREQ.STATE !
    _HRP-PORT @ NIO-POLL
    _HRP-R @ HREQ.BUFFER @ _HRP-R @ HREQ.SENT @ +
    _HRP-REMAINING @ _HRP-PORT @ NIO-SEND
    _HRP-IO ! _HRP-N !
    _HRP-IO @ NIO-S-CANCELLED = IF
        HREQ-S-CANCELLED _HRP-R @ _HREQ-FAIL DROP HREQ-PUMP-CANCELLED EXIT
    THEN
    _HRP-IO @ NIO-S-OK <> IF
        HREQ-S-TRANSPORT _HRP-R @ _HREQ-FAIL DROP HREQ-PUMP-ERROR EXIT
    THEN
    _HRP-N @ 0< _HRP-N @ _HRP-REMAINING @ > OR IF
        HREQ-S-TRANSPORT _HRP-R @ _HREQ-FAIL DROP HREQ-PUMP-ERROR EXIT
    THEN
    _HRP-N @ 0= IF HREQ-PUMP-IDLE EXIT THEN
    _HRP-N @ _HRP-R @ HREQ.SENT +!
    _HRP-R @ HREQ.SENT @ _HRP-R @ HREQ.LENGTH @ = IF
        HREQ-STATE-SENT _HRP-R @ HREQ.STATE ! HREQ-PUMP-DONE
    ELSE
        HREQ-PUMP-PROGRESS
    THEN ;
