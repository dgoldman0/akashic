            _OCHK-AP-CANDIDATE @ _OCHK-DIGEST _OCHK-SEMANTIC-DIGEST!
            _OCHK-AP-KEY @ OCK.LATEST-OBSERVATION @ _OCHK-AP-C @
                _OCHK-OBS-NTH-RAW OCO.SEMANTIC-DIGEST
                32 _OCHK-DIGEST 32 COMPARE 0= IF
                1 _OCHK-AP-UNCHANGED +!
            ELSE
                _OCHK-AP-KEY @ OCK.LATEST-REVISION @
                    OCHK-VERSIONS-PER-KEY >= IF OCHK-S-CAPACITY UNLOOP EXIT THEN
                1 _OCHK-AP-REVISED +!
                _OCHK-AP-CANDIDATE @ _OCHK-CANDIDATE-BLOB-NEED
                    _OCHK-AP-NEED +!
            THEN
        THEN
    LOOP
    _OCHK-AP-C @ OCHK.KEY-COUNT @ _OCHK-AP-NEW @ + OCHK-KEY-MAX > IF
        OCHK-S-CAPACITY EXIT
    THEN
    _OCHK-AP-NEW @ _OCHK-AP-REVISED @ + DUP >R
    _OCHK-AP-C @ OCHK.OBSERVATION-COUNT @ + OCHK-OBSERVATION-MAX >
    _OCHK-AP-SOURCE-OBS @ R> + OCHK-OBSERVATIONS-PER-SOURCE > OR IF
        OCHK-S-CAPACITY EXIT
    THEN
    _OCHK-AP-C @ OCHK.BLOB-USED @ _OCHK-AP-NEED @ +
        OCHK-BLOB-CAPACITY > IF OCHK-S-CAPACITY EXIT THEN
    OCHK-S-OK ;

VARIABLE _OCHK-BA
VARIABLE _OCHK-BU
VARIABLE _OCHK-BC
VARIABLE _OCHK-BO

: _OCHK-BLOB-APPEND  ( a u checkpoint -- offset )
    _OCHK-BC ! _OCHK-BU ! _OCHK-BA !
    _OCHK-BU @ 0= IF 0 EXIT THEN
    _OCHK-BC @ OCHK.BLOB-USED @ _OCHK-BO !
    _OCHK-BA @ _OCHK-BC @ OCHK.BLOB _OCHK-BO @ + _OCHK-BU @ CMOVE
    _OCHK-BU @ _OCHK-BC @ OCHK.BLOB-USED +!
    _OCHK-BO @ ;

VARIABLE _OCHK-COPY-CANDIDATE
VARIABLE _OCHK-COPY-OBS
VARIABLE _OCHK-COPY-C
VARIABLE _OCHK-OBS-STR-OFF-FIELD
VARIABLE _OCHK-OBS-STR-LEN-FIELD
