\ =====================================================================
\ observation-state.f - bounded durable Streams observation model
\ =====================================================================
\ The checkpoint is pointer-free and can be copied or replaced verbatim.
\ It retains the latest acquisition attempt for sixteen exact sources,
\ forty-eight immutable observation versions, sixty-four exact native-key
\ heads, and one canonical owned string blob.  Attempt history is bounded to
\ the latest attempt per source; immutable observation versions and each
\ key's last-seen attempt sequence preserve the useful evidence needed by the
\ first manual-refresh slice.
\
\ BEGIN durably establishes ACCEPTED before external I/O.  TERMINAL records a
\ non-success outcome without touching observations.  APPLY consumes one
\ already-decoded, pointer-bearing candidate batch and records new/revised/
\ unchanged results.  Callers perform APPLY against a checkpoint copy, save
\ that copy, and publish it only after replacement succeeds.
\
\ CRC and SHA3 values detect accidental corruption and stable equality.  They
\ are not content authentication, network authority, or rollback protection.
\ =====================================================================

