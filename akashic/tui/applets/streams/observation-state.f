: _OCHK-ZERO?  ( a u -- flag )
    DUP 0< IF 2DROP 0 EXIT THEN
    0 ?DO DUP I + C@ IF DROP 0 UNLOOP EXIT THEN LOOP DROP -1 ;

: _OCHK-SPAN?  ( a u -- flag )
    DUP 0< IF 2DROP 0 EXIT THEN
    OVER 0= IF 2DROP 0 EXIT THEN
    DUP 0= IF 2DROP -1 EXIT THEN
    >R DUP R@ + SWAP U< 0= R> DROP ;

