CREATE _OCHK-ZERO8 8 ALLOT

: _OCHK-CRC@  ( checkpoint -- crc )
    CRC32-BEGIN
    DUP _OCH-CRC CRC32-ADD
    _OCHK-ZERO8 8 CRC32-ADD
    DUP _OCH-CRC + 8 +
    STREAMS-OBSERVATION-CHECKPOINT-SIZE _OCH-CRC - 8 - CRC32-ADD
    DROP CRC32-END ;

: OCHK-SEAL  ( checkpoint -- )
    DUP 0 SWAP OCHK.CRC ! DUP _OCHK-CRC@ SWAP OCHK.CRC ! ;

: OCHK-INIT  ( checkpoint -- )
    DUP STREAMS-OBSERVATION-CHECKPOINT-SIZE 0 FILL
    OCHK-MAGIC OVER OCHK.MAGIC !
    OCHK-ABI OVER OCHK.ABI !
    STREAMS-OBSERVATION-CHECKPOINT-SIZE OVER OCHK.SIZE !
