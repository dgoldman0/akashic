: _OCHK-OBS-STR!  ( a u off-field len-field -- )
    _OCHK-OBS-STR-LEN-FIELD ! _OCHK-OBS-STR-OFF-FIELD !
    DUP _OCHK-OBS-STR-U ! _OCHK-COPY-C @ _OCHK-BLOB-APPEND
    _OCHK-COPY-OBS @ _OCHK-OBS-STR-OFF-FIELD @ + !
    _OCHK-OBS-STR-U @
    _OCHK-COPY-OBS @ _OCHK-OBS-STR-LEN-FIELD @ + ! ;

