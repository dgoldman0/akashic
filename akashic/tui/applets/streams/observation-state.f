    _OCHK-COPY-C ! _OCHK-COPY-OBS ! _OCHK-COPY-CANDIDATE !
    _OCHK-COPY-CANDIDATE @ OCC.TITLE-A @ _OCHK-COPY-CANDIDATE @ OCC.TITLE-U @
        _OCO-TITLE-O _OCO-TITLE-U _OCHK-OBS-STR!
    _OCHK-COPY-CANDIDATE @ OCC.URL-A @ _OCHK-COPY-CANDIDATE @ OCC.URL-U @
        _OCO-URL-O _OCO-URL-U _OCHK-OBS-STR!
    _OCHK-COPY-CANDIDATE @ OCC.SUMMARY-A @ _OCHK-COPY-CANDIDATE @ OCC.SUMMARY-U @
        _OCO-SUMMARY-O _OCO-SUMMARY-U _OCHK-OBS-STR!
    _OCHK-COPY-CANDIDATE @ OCC.CONTENT-A @ _OCHK-COPY-CANDIDATE @ OCC.CONTENT-U @
        _OCO-CONTENT-O _OCO-CONTENT-U _OCHK-OBS-STR!
    _OCHK-COPY-CANDIDATE @ OCC.PUBLISHED-A @ _OCHK-COPY-CANDIDATE @ OCC.PUBLISHED-U @
        _OCO-PUBLISHED-O _OCO-PUBLISHED-U _OCHK-OBS-STR!
    _OCHK-COPY-CANDIDATE @ OCC.MODIFIED-A @ _OCHK-COPY-CANDIDATE @ OCC.MODIFIED-U @
        _OCO-MODIFIED-O _OCO-MODIFIED-U _OCHK-OBS-STR! ;

: _OCHK-DERIVE-OBS-ID  ( source-head candidate destination -- )
    >R _OCHK-SEM-CANDIDATE ! SHA3-256-BEGIN
    S" akashic.streams.observation.identity.v1" _OCHK-SHA-$,
    DUP OCS.SOURCE-ID 32 SHA3-256-ADD
    DUP OCS.NAMESPACE 32 SHA3-256-ADD
    _OCHK-SEM-CANDIDATE @ OCC.FORMAT @ _OCHK-SHA-N,
    _OCHK-SEM-CANDIDATE @ OCC.NATIVE-KIND @ _OCHK-SHA-N,
    _OCHK-SEM-CANDIDATE @ OCC.NATIVE-A @
        _OCHK-SEM-CANDIDATE @ OCC.NATIVE-U @ _OCHK-SHA-$,
    DROP R> SHA3-256-END ;

: _OCHK-NEW-OBSERVATION  ( revision -- )
    _OCHK-AP-C @ OCHK.OBSERVATION-COUNT @ DUP _OCHK-AP-OBS-I !
    _OCHK-AP-C @ _OCHK-OBS-NTH-RAW DUP _OCHK-AP-OBS !
    OCHK-OBSERVATION-SIZE 0 FILL
    1 _OCHK-AP-OBS @ OCO.STATE !
    _OCHK-AP-HEAD @ OCS.ATTEMPT-SEQUENCE @ _OCHK-AP-OBS @ OCO.SEQUENCE !
    _OCHK-AP-OBS @ OCO.REVISION !
    _OCHK-AP-CANDIDATE @ OCC.FORMAT @ _OCHK-AP-OBS @ OCO.FORMAT !
    _OCHK-AP-HEAD @ OCS.SOURCE-ID _OCHK-AP-OBS @ OCO.SOURCE-ID RID-COPY
    _OCHK-AP-HEAD @ OCS.NAMESPACE _OCHK-AP-OBS @ OCO.NAMESPACE 32 CMOVE
    _OCHK-AP-KEY @ OCK.OBSERVATION-ID _OCHK-AP-OBS @ OCO.ID RID-COPY
    _OCHK-AP-HEAD @ OCS.ATTEMPT-ID _OCHK-AP-OBS @ OCO.ATTEMPT-ID RID-COPY
    _OCHK-AP-CANDIDATE @ _OCHK-AP-OBS @ OCO.SEMANTIC-DIGEST
        _OCHK-SEMANTIC-DIGEST!
    _OCHK-AP-KEY-I @ _OCHK-AP-OBS @ OCO.KEY-INDEX !
    _OCHK-AP-CANDIDATE @ _OCHK-AP-OBS @ _OCHK-AP-C @
        _OCHK-COPY-OBS-STRINGS
    1 _OCHK-AP-C @ OCHK.OBSERVATION-COUNT +!
    _OCHK-AP-OBS-I @ _OCHK-AP-KEY @ OCK.LATEST-OBSERVATION !
    _OCHK-AP-OBS @ OCO.REVISION @ _OCHK-AP-KEY @ OCK.LATEST-REVISION !
    _OCHK-AP-HEAD @ OCS.ATTEMPT-SEQUENCE @
        _OCHK-AP-KEY @ OCK.LAST-SEEN-SEQUENCE ! ;

: _OCHK-NEW-KEY  ( -- )
    _OCHK-AP-C @ OCHK.KEY-COUNT @ DUP _OCHK-AP-KEY-I !
    _OCHK-AP-C @ _OCHK-KEY-NTH-RAW DUP _OCHK-AP-KEY ! OCHK-KEY-SIZE 0 FILL
    1 _OCHK-AP-KEY @ OCK.STATE !
    _OCHK-AP-HEAD @ OCS.SOURCE-ID _OCHK-AP-KEY @ OCK.SOURCE-ID RID-COPY
    _OCHK-AP-HEAD @ OCS.NAMESPACE _OCHK-AP-KEY @ OCK.NAMESPACE 32 CMOVE
    _OCHK-AP-CANDIDATE @ OCC.FORMAT @ _OCHK-AP-KEY @ OCK.FORMAT !
    _OCHK-AP-CANDIDATE @ OCC.NATIVE-KIND @ _OCHK-AP-KEY @ OCK.NATIVE-KIND !
    _OCHK-AP-CANDIDATE @ OCC.NATIVE-A @ _OCHK-AP-CANDIDATE @ OCC.NATIVE-U @
        _OCHK-DIGEST SHA3-256-HASH
    _OCHK-DIGEST _OCHK-AP-KEY @ OCK.NATIVE-DIGEST 32 CMOVE
    _OCHK-AP-HEAD @ _OCHK-AP-CANDIDATE @
        _OCHK-AP-KEY @ OCK.OBSERVATION-ID _OCHK-DERIVE-OBS-ID
    _OCHK-AP-CANDIDATE @ OCC.NATIVE-A @ _OCHK-AP-CANDIDATE @ OCC.NATIVE-U @
        _OCHK-AP-C @ _OCHK-BLOB-APPEND _OCHK-AP-KEY @ OCK.NATIVE-O !
    _OCHK-AP-CANDIDATE @ OCC.NATIVE-U @ _OCHK-AP-KEY @ OCK.NATIVE-U !
    1 _OCHK-AP-C @ OCHK.KEY-COUNT +! ;

: _OCHK-APPLY-MUTATE  ( -- )
    _OCHK-AP-N @ 0 ?DO
        I _OCHK-AP-CANDIDATE@ _OCHK-AP-CANDIDATE !
        _OCHK-AP-HEAD @ _OCHK-AP-CANDIDATE @ _OCHK-AP-C @ _OCHK-KEY-FIND
        DUP -1 = IF
            2DROP _OCHK-NEW-KEY 1 _OCHK-NEW-OBSERVATION
        ELSE
            _OCHK-AP-KEY-I ! _OCHK-AP-KEY !
            _OCHK-AP-CANDIDATE @ _OCHK-DIGEST _OCHK-SEMANTIC-DIGEST!
            _OCHK-AP-KEY @ OCK.LATEST-OBSERVATION @ _OCHK-AP-C @
                _OCHK-OBS-NTH-RAW OCO.SEMANTIC-DIGEST
                32 _OCHK-DIGEST 32 COMPARE 0= IF
                _OCHK-AP-HEAD @ OCS.ATTEMPT-SEQUENCE @
                    _OCHK-AP-KEY @ OCK.LAST-SEEN-SEQUENCE !
            ELSE
                _OCHK-AP-KEY @ OCK.LATEST-REVISION @ 1+ _OCHK-NEW-OBSERVATION
            THEN
        THEN
    LOOP
    _OCHK-AP-EA @ _OCHK-AP-HEAD @ _OCS-EFFECTIVE + _OCHK-AP-EU @ CMOVE
    _OCHK-AP-EU @ _OCHK-AP-HEAD @ _OCS-EFFECTIVE-U + !
    OCHK-ATTEMPT-SUCCEEDED _OCHK-AP-HEAD @ OCS.STATE !
    OCHK-O-OK _OCHK-AP-HEAD @ OCS.OUTCOME !
    _OCHK-AP-DETAIL @ _OCHK-AP-HEAD @ OCS.DETAIL !
    _OCHK-AP-HTTP @ _OCHK-AP-HEAD @ OCS.HTTP-STATUS !
    _OCHK-AP-NEW @ _OCHK-AP-HEAD @ OCS.NEW-COUNT !
    _OCHK-AP-REVISED @ _OCHK-AP-HEAD @ OCS.REVISED-COUNT !
    _OCHK-AP-UNCHANGED @ _OCHK-AP-HEAD @ OCS.UNCHANGED-COUNT !
    MS@ _OCHK-AP-HEAD @ OCS.FINISHED-MS !
    1 _OCHK-AP-C @ OCHK.GENERATION +!
    _OCHK-AP-C @ OCHK-SEAL ;

: OCHK-APPLY
    ( source-rid source-revision effective-a effective-u detail http candidates count checkpoint -- status )
    _OCHK-AP-C ! _OCHK-AP-N ! _OCHK-AP-CANDIDATES !
    _OCHK-AP-HTTP ! _OCHK-AP-DETAIL !
    _OCHK-AP-EU ! _OCHK-AP-EA ! _OCHK-AP-REV ! _OCHK-AP-ID !
    _OCHK-AP-C @ OCHK-VALID? 0= IF OCHK-S-CORRUPT EXIT THEN
    _OCHK-AP-ID @ _OCHK-AP-C @ OCHK-SOURCE-FIND
    DUP _OCHK-AP-HEAD ! 0= IF OCHK-S-NOT-FOUND EXIT THEN
    _OCHK-AP-HEAD @ OCS.SOURCE-REVISION @ _OCHK-AP-REV @ <> IF
        OCHK-S-STALE EXIT
    THEN
    _OCHK-AP-HEAD @ OCS.STATE @ OCHK-ATTEMPT-ACCEPTED <> IF
        OCHK-S-STATE EXIT
    THEN
    _OCHK-APPLY-PREFLIGHT DUP IF EXIT THEN DROP
    _OCHK-APPLY-MUTATE
    _OCHK-AP-C @ OCHK-VALID? IF OCHK-S-OK ELSE OCHK-S-CORRUPT THEN ;
