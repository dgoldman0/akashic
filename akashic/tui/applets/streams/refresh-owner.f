: STREAMS-REFRESH-OWNER-RELEASE  ( owner -- status )
    DUP STREAMS-REFRESH-OWNER-VALID? 0= IF DROP SREF-S-INVALID EXIT THEN
    _SREF-RELEASE-OWNER ! SREF-S-OK _SREF-RELEASE-STATUS !
    _SREF-RELEASE-OWNER @ SREF.PHASE @ SREF-PHASE-ACTIVE = IF
        _SREF-RELEASE-OWNER @ SREF.XIO-OP XIOO.STATE @
            XIO-STATE-ACTIVE = IF
            _SREF-RELEASE-OWNER @ SREF.XIO-SERVICE @
                _SREF-RELEASE-OWNER @ SREF.XIO-OP XIO-CANCEL DROP
        THEN
        _SREF-RELEASE-OWNER @ SREF.XIO-OP XIOO.STATE @
        DUP XIO-STATE-SUCCEEDED = IF DROP XIO-STATE-CANCELLED THEN
        _SREF-TICK-XIO-STATE !
        _SREF-RELEASE-OWNER @ _SREF-TERMINATE-XIO
        DUP SREF-S-STORAGE = OVER SREF-S-CLEANUP = OR IF
            _SREF-RELEASE-STATUS !
        ELSE
            DROP SREF-S-OK _SREF-RELEASE-STATUS !
        THEN
    THEN
    _SREF-RELEASE-OWNER @ SREF.XIO-OP XIOO.STATE @ XIO-STATE-RESET <> IF
        _SREF-RELEASE-OWNER @ _SREF-RESET ?DUP IF
            DROP SREF-S-CLEANUP _SREF-RELEASE-STATUS !
        THEN
    THEN
    _SREF-RELEASE-OWNER @ SREF.PROVIDER @ ?DUP IF
        DUP SCONF-RELEASABLE? 0= IF DROP SREF-S-CLEANUP EXIT THEN
        SCONF-RELEASE SCONF-S-OK <> IF SREF-S-CLEANUP EXIT THEN
    THEN
    0 _SREF-RELEASE-OWNER @ SREF.PROVIDER !
    _SREF-RELEASE-OWNER @ SREF.DECODER @
        STREAMS-SYNDICATION-DECODER-FREE SYNDEC-S-OK <> IF
        SREF-S-CLEANUP EXIT
    THEN
    0 _SREF-RELEASE-OWNER @ SREF.DECODER !
    _SREF-RELEASE-OWNER @ SREF.CANDIDATE @ DUP
        STREAMS-OBSERVATION-CHECKPOINT-SIZE 0 FILL FREE
    0 _SREF-RELEASE-OWNER @ SREF.CANDIDATE !
    _SREF-RELEASE-OWNER @ SREF.LIVE @ DUP
        STREAMS-OBSERVATION-CHECKPOINT-SIZE 0 FILL FREE
    0 _SREF-RELEASE-OWNER @ SREF.LIVE !
    _SREF-RELEASE-OWNER @ STREAMS-REFRESH-OWNER-SIZE 0 FILL
    _SREF-RELEASE-STATUS @ ;
