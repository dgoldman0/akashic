: STREAMS-REFRESH-OWNER-START
  ( exact-source owner-id owner-generation owner -- status )
    _SREF-OWNER ! _SREF-START-OWNER-GEN !
    _SREF-START-OWNER-ID ! _SREF-START-SOURCE !
    _SREF-OWNER @ STREAMS-REFRESH-OWNER-VALID? 0= IF SREF-S-INVALID EXIT THEN
    _SREF-OWNER @ SREF.PHASE @ SREF-PHASE-BLOCKED = IF SREF-S-BLOCKED EXIT THEN
    _SREF-OWNER @ STREAMS-REFRESH-OWNER-AVAILABLE? 0= IF
        SREF-S-UNAVAILABLE EXIT
    THEN
    _SREF-START-OWNER-ID @ 0= _SREF-START-OWNER-GEN @ 0> 0= OR IF
        SREF-S-INVALID EXIT
    THEN
    _SREF-START-SOURCE @ _SREF-SOURCE-SUPPORTED? 0= IF
        SREF-S-INVALID EXIT
    THEN
    _SREF-START-SOURCE @ STREAMS-SOURCE-SIZE
        _SREF-OWNER @ STREAMS-REFRESH-OWNER-SIZE _SREF-SPANS-OVERLAP? IF
        SREF-S-INVALID EXIT
    THEN
    _SREF-OWNER @ SREF.XIO-OP XIOO.STATE @ XIO-STATE-RESET <> IF
        SREF-S-BUSY EXIT
    THEN
    _SREF-OWNER @ SREF.XIO-SERVICE @ XIO-ACTIVE?
    _SREF-OWNER @ SREF.XIO-SERVICE @ XIOS.RETAINED @ 0<> OR IF
        SREF-S-BUSY EXIT
    THEN
    _SREF-OWNER @ SREF.REQUEST-GENERATION @ _SREF-CELL-MAX >= IF
        SREF-S-CAPACITY EXIT
    THEN
