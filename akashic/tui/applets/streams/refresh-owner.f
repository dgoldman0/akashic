: _SREF-TERMINATE-STALE  ( owner -- status )
    _SREF-OWNER !
    _SREF-OWNER @ SREF.XIO-OP XIOO.ERROR @ _SREF-TERM-XIO !
    _SREF-OWNER @ SREF.XIO-OP XIOO.STATE @ XIO-STATE-ACTIVE = IF
        _SREF-OWNER @ SREF.XIO-SERVICE @
            _SREF-OWNER @ SREF.XIO-OP XIO-CANCEL DROP
    THEN
    _SREF-OWNER @ _SREF-RESET _SREF-TERM-CLEANUP !
    OCHK-ATTEMPT-FAILED _SREF-TERM-STATE !
    _SREF-TERM-CLEANUP @ IF OCHK-O-CLEANUP ELSE OCHK-O-STALE THEN
        _SREF-TERM-OUTCOME !
    0 _SREF-TERM-DETAIL ! 0 _SREF-TERM-HTTP ! 0 _SREF-TERM-DECODE !
    _SREF-TERM-CLEANUP @ IF
        SREF-PHASE-CLEANUP _SREF-TERM-PHASE !
        SREF-S-CLEANUP _SREF-TERM-STATUS !
    ELSE
        SREF-PHASE-STALE _SREF-TERM-PHASE !
        SREF-S-STALE _SREF-TERM-STATUS !
    THEN
    _SREF-OWNER @ _SREF-TERMINAL ;

: _SREF-TERMINATE-XIO  ( owner -- status )
    _SREF-OWNER !
    _SREF-OWNER @ _SREF-CAPTURE-PROVIDER
    _SREF-OWNER @ SREF.XIO-OP XIOO.ERROR @ _SREF-TERM-XIO !
    _SREF-OWNER @ SREF.XIO-OP XIOO.CLEANUP-ERROR @
        _SREF-TERM-CLEANUP !
    _SREF-OWNER @ _SREF-RESET ?DUP IF
        _SREF-TERM-CLEANUP @ 0= IF _SREF-TERM-CLEANUP ! ELSE DROP THEN
    THEN
    0 _SREF-TERM-DECODE !
    _SREF-TICK-XIO-STATE @ XIO-STATE-CANCELLED = IF
        OCHK-ATTEMPT-CANCELLED _SREF-TERM-STATE !
        OCHK-O-CANCELLED _SREF-TERM-OUTCOME !
        SREF-PHASE-CANCELLED _SREF-TERM-PHASE !
        SREF-S-CANCELLED _SREF-TERM-STATUS !
    ELSE
        OCHK-ATTEMPT-FAILED _SREF-TERM-STATE !
        _SREF-TICK-PROVIDER-O @ _SREF-PROVIDER>OUTCOME
            _SREF-TERM-OUTCOME !
        SREF-PHASE-FAILED _SREF-TERM-PHASE !
        SREF-S-EXTERNAL _SREF-TERM-STATUS !
    THEN
    _SREF-TERM-CLEANUP @ IF
        OCHK-O-CLEANUP _SREF-TERM-OUTCOME !
        SREF-PHASE-CLEANUP _SREF-TERM-PHASE !
        SREF-S-CLEANUP _SREF-TERM-STATUS !
    THEN
    _SREF-OWNER @ _SREF-TERMINAL ;

: _SREF-SUCCESS  ( owner -- status )
    _SREF-OWNER !
    _SREF-OWNER @ _SREF-CAPTURE-PROVIDER
    _SREF-TICK-PROVIDER-O @ SCONF-O-OK <>
    _SREF-OWNER @ SREF.PROVIDER @ SCONF-RESULT-VALID? 0= OR IF
        _SREF-OWNER @ _SREF-RESET _SREF-TERM-CLEANUP !
        OCHK-ATTEMPT-FAILED _SREF-TERM-STATE !
        _SREF-TERM-CLEANUP @ IF
            OCHK-O-CLEANUP
        ELSE
            _SREF-TICK-PROVIDER-O @ _SREF-PROVIDER>OUTCOME
        THEN _SREF-TERM-OUTCOME !
        0 _SREF-TERM-DECODE ! 0 _SREF-TERM-XIO !
        _SREF-TERM-CLEANUP @ IF
            SREF-PHASE-CLEANUP _SREF-TERM-PHASE !
            SREF-S-CLEANUP _SREF-TERM-STATUS !
        ELSE
            SREF-PHASE-FAILED _SREF-TERM-PHASE !
            SREF-S-EXTERNAL _SREF-TERM-STATUS !
        THEN
        _SREF-OWNER @ _SREF-TERMINAL EXIT
    THEN
    _SREF-OWNER @ SREF.PROVIDER @ SCONF-EFFECTIVE$
    DUP _SREF-OWNER @ SREF.EFFECTIVE-U !
    DUP 1 < OVER OCHK-URI-MAX > OR IF
        2DROP SYNDEC-S-CAPACITY _SREF-TICK-DECODE !
        0 _SREF-TICK-CLEANUP !
    ELSE
        _SREF-OWNER @ SREF.EFFECTIVE SWAP MOVE
        _SREF-OWNER @ SREF.SOURCE SSOURCE.FORMAT @
        _SREF-OWNER @ SREF.PROVIDER @ _SREF-OWNER @ SREF.DECODER @
            STREAMS-SYNDICATION-DECODE _SREF-TICK-DECODE !
    THEN
    _SREF-TICK-DECODE @ SYNDEC-S-OK <> IF
        _SREF-OWNER @ _SREF-RESET _SREF-TERM-CLEANUP !
        OCHK-ATTEMPT-FAILED _SREF-TERM-STATE !
        _SREF-TERM-CLEANUP @ IF OCHK-O-CLEANUP ELSE OCHK-O-DECODE THEN
            _SREF-TERM-OUTCOME !
        _SREF-TICK-DECODE @ _SREF-TERM-DECODE !
        0 _SREF-TERM-XIO !
        _SREF-TERM-CLEANUP @ IF
            SREF-PHASE-CLEANUP _SREF-TERM-PHASE !
            SREF-S-CLEANUP _SREF-TERM-STATUS !
        ELSE
            SREF-PHASE-FAILED _SREF-TERM-PHASE !
            SREF-S-DECODE _SREF-TERM-STATUS !
        THEN
        _SREF-OWNER @ _SREF-TERMINAL EXIT
    THEN
    _SREF-OWNER @ SREF.DECODER @ STREAMS-SYNDICATION-CANDIDATES
        _SREF-TICK-COUNT ! _SREF-TICK-CANDIDATES !
    _SREF-OWNER @ SREF.LIVE @ _SREF-OWNER @ SREF.CANDIDATE @
        STREAMS-OBSERVATION-CHECKPOINT-SIZE CMOVE
    _SREF-OWNER @ SREF.SOURCE SSOURCE.ID
    _SREF-OWNER @ SREF.SOURCE SSOURCE.REVISION @
    _SREF-OWNER @ SREF.EFFECTIVE _SREF-OWNER @ SREF.EFFECTIVE-U @
    _SREF-TERM-DETAIL @ _SREF-TERM-HTTP @
    _SREF-TICK-CANDIDATES @ _SREF-TICK-COUNT @
    _SREF-OWNER @ SREF.CANDIDATE @ OCHK-APPLY
    DUP _SREF-STATUS ! OCHK-S-OK <> IF
        _SREF-OWNER @ _SREF-RESET _SREF-TERM-CLEANUP !
        _SREF-STATUS @ OCHK-S-CAPACITY = IF SREF-S-CAPACITY ELSE SREF-S-DECODE THEN
        _SREF-STATUS !
        OCHK-ATTEMPT-FAILED _SREF-TERM-STATE !
        _SREF-TERM-CLEANUP @ IF
            OCHK-O-CLEANUP
        ELSE
            _SREF-STATUS @ SREF-S-CAPACITY = IF
                OCHK-O-CAPACITY
            ELSE
                OCHK-O-DECODE
            THEN
        THEN _SREF-TERM-OUTCOME !
        _SREF-STATUS @ _SREF-TERM-DECODE ! 0 _SREF-TERM-XIO !
        _SREF-TERM-CLEANUP @ IF
            SREF-PHASE-CLEANUP _SREF-TERM-PHASE !
            SREF-S-CLEANUP _SREF-TERM-STATUS !
        ELSE
            SREF-PHASE-FAILED _SREF-TERM-PHASE !
            _SREF-STATUS @ _SREF-TERM-STATUS !
        THEN
        _SREF-OWNER @ _SREF-TERMINAL EXIT
    THEN
    _SREF-OWNER @ _SREF-RESET DUP _SREF-TICK-CLEANUP ! IF
        OCHK-ATTEMPT-FAILED _SREF-TERM-STATE !
        OCHK-O-CLEANUP _SREF-TERM-OUTCOME !
        0 _SREF-TERM-DECODE ! 0 _SREF-TERM-XIO !
        _SREF-TICK-CLEANUP @ _SREF-TERM-CLEANUP !
        SREF-PHASE-CLEANUP _SREF-TERM-PHASE !
        SREF-S-CLEANUP _SREF-TERM-STATUS !
        _SREF-OWNER @ _SREF-TERMINAL EXIT
    THEN
    _SREF-OWNER @ _SREF-PERSIST DUP SREF-S-OK <> IF EXIT THEN DROP
    _SREF-OWNER @ SREF.DECODER @ STREAMS-SYNDICATION-DECODER-RESET DROP
    SREF-PHASE-SUCCEEDED SREF-S-OK 0 _SREF-OWNER @ _SREF-MARK ;

: STREAMS-REFRESH-OWNER-TICK
  ( current-source|0 owner-id owner-generation owner -- changed? status )
    _SREF-OWNER ! _SREF-TICK-OWNER-GEN !
    _SREF-TICK-OWNER-ID ! _SREF-TICK-CURRENT !
    _SREF-OWNER @ STREAMS-REFRESH-OWNER-VALID? 0= IF
        0 SREF-S-INVALID EXIT
    THEN
    _SREF-OWNER @ SREF.PHASE @ SREF-PHASE-ACTIVE <> IF
        0 SREF-S-OK EXIT
    THEN
    _SREF-TICK-OWNER-ID @ _SREF-OWNER @ SREF.OWNER-ID @ <> IF
        0 SREF-S-INVALID EXIT
    THEN
    _SREF-TICK-OWNER-GEN @ _SREF-OWNER @ SREF.OWNER-GENERATION @ <> IF
        _SREF-OWNER @ _SREF-TERMINATE-STALE -1 SWAP EXIT
    THEN
    _SREF-OWNER @ _SREF-SOURCE-EXACT? 0=
    _SREF-OWNER @ _SREF-XIO-EXACT? 0= OR IF
        _SREF-OWNER @ _SREF-TERMINATE-STALE -1 SWAP EXIT
    THEN
    _SREF-OWNER @ SREF.XIO-OP XIOO.STATE @ DUP _SREF-TICK-XIO-STATE !
    DUP XIO-STATE-ACTIVE = IF DROP 0 SREF-S-OK EXIT THEN
    DUP XIO-STATE-RESET = IF
        DROP _SREF-OWNER @ _SREF-TERMINATE-XIO -1 SWAP EXIT
    THEN
    XIO-STATE-SUCCEEDED = IF
        _SREF-OWNER @ SREF.XIO-SERVICE @ XIOS.RETAINED @
            _SREF-OWNER @ SREF.XIO-OP =
        _SREF-OWNER @ SREF.XIO-OP XIOO.RESULT @
            _SREF-OWNER @ SREF.PROVIDER @ = AND 0= IF
            _SREF-OWNER @ _SREF-TERMINATE-XIO -1 SWAP EXIT
        THEN
        _SREF-OWNER @ _SREF-SUCCESS -1 SWAP EXIT
    THEN
    _SREF-OWNER @ _SREF-TERMINATE-XIO -1 SWAP ;

: STREAMS-REFRESH-OWNER-CANCEL
  ( owner-id owner-generation owner -- changed? status )
    _SREF-OWNER ! _SREF-TICK-OWNER-GEN ! _SREF-TICK-OWNER-ID !
    _SREF-OWNER @ STREAMS-REFRESH-OWNER-VALID? 0= IF
        0 SREF-S-INVALID EXIT
    THEN
    _SREF-OWNER @ SREF.PHASE @ SREF-PHASE-ACTIVE <> IF
        0 SREF-S-INVALID EXIT
    THEN
    _SREF-TICK-OWNER-ID @ _SREF-OWNER @ SREF.OWNER-ID @ <>
    _SREF-TICK-OWNER-GEN @ _SREF-OWNER @ SREF.OWNER-GENERATION @ <> OR IF
        0 SREF-S-INVALID EXIT
    THEN
    _SREF-OWNER @ SREF.XIO-OP XIOO.STATE @ XIO-STATE-ACTIVE = IF
        _SREF-OWNER @ SREF.XIO-SERVICE @ _SREF-OWNER @ SREF.XIO-OP
            XIO-CANCEL DROP
    THEN
    _SREF-OWNER @ SREF.XIO-OP XIOO.STATE @
    DUP XIO-STATE-SUCCEEDED = IF DROP XIO-STATE-CANCELLED THEN
    _SREF-TICK-XIO-STATE !
    _SREF-OWNER @ _SREF-TERMINATE-XIO -1 SWAP ;

