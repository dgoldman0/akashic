: STREAMS-REFRESH-OWNER-INIT
  ( vfs xio-service configured-factory-xt owner -- status )
    _SREF-INIT-OWNER ! _SREF-INIT-FACTORY !
    _SREF-INIT-SERVICE ! _SREF-INIT-VFS !
    _SREF-INIT-OWNER @ 0= _SREF-INIT-VFS @ 0= OR IF
        SREF-S-INVALID EXIT
    THEN
    _SREF-INIT-OWNER @ STREAMS-REFRESH-OWNER-SIZE 0 FILL
    _SREF-INIT-FACTORY @ _SREF-INIT-OWNER @ SREF.FACTORY-XT !
    _SREF-INIT-SERVICE @ _SREF-INIT-OWNER @ SREF.XIO-SERVICE !
    _SREF-INIT-OWNER @ SREF.XIO-OP XIO-OP-INIT

    STREAMS-OBSERVATION-CHECKPOINT-SIZE _SREF-ALLOC DUP 0= IF
        _SREF-INIT-OWNER @ _SREF-PARTIAL-FREE SREF-S-CAPACITY EXIT
    THEN
    DUP OCHK-INIT _SREF-INIT-OWNER @ SREF.LIVE !
    STREAMS-OBSERVATION-CHECKPOINT-SIZE _SREF-ALLOC DUP 0= IF
        _SREF-INIT-OWNER @ _SREF-PARTIAL-FREE SREF-S-CAPACITY EXIT
    THEN
    DUP STREAMS-OBSERVATION-CHECKPOINT-SIZE 0 FILL
        _SREF-INIT-OWNER @ SREF.CANDIDATE !
    STREAMS-SYNDICATION-DECODER-NEW
    DUP _SREF-STATUS ! SYNDEC-S-OK <> IF
        DROP _SREF-INIT-OWNER @ _SREF-PARTIAL-FREE SREF-S-CAPACITY EXIT
    THEN
    _SREF-INIT-OWNER @ SREF.DECODER !

    _SREF-INIT-VFS @ _SREF-INIT-OWNER @ SREF.STORE
        STREAMS-OBSERVATION-STORE-INIT
    DUP _SREF-INIT-OWNER @ SREF.STORE-STATUS !
    OSTORE-S-OK <> IF
        _SREF-INIT-OWNER @ _SREF-PARTIAL-FREE SREF-S-STORAGE EXIT
    THEN
    _SREF-MAGIC _SREF-INIT-OWNER @ SREF.MAGIC !
    _SREF-INIT-OWNER @ SREF.LIVE @ STREAMS-OBSERVATION-CHECKPOINT-SIZE
        _SREF-INIT-OWNER @ SREF.STORE STREAMS-OBSERVATION-STORE-LOAD
    DUP _SREF-INIT-LOAD !
        _SREF-INIT-OWNER @ SREF.STORE-STATUS !
    _SREF-INIT-LOAD @ OSTORE-S-ABSENT = IF
        _SREF-INIT-OWNER @ SREF.LIVE @ OCHK-INIT
    ELSE
        _SREF-INIT-LOAD @ OSTORE-S-OK <> IF
            _SREF-INIT-LOAD @ _SREF-INIT-OWNER @ _SREF-BLOCK EXIT
        THEN
        _SREF-INIT-OWNER @ _SREF-INIT-RECOVERY SREF-S-OK <> IF
            _SREF-INIT-OWNER @ SREF.STORE-STATUS @
                _SREF-INIT-OWNER @ _SREF-BLOCK EXIT
        THEN
    THEN
    _SREF-INIT-OWNER @ SREF.LIVE @
        _SREF-INIT-OWNER @ SREF.CANDIDATE @
        STREAMS-OBSERVATION-CHECKPOINT-SIZE CMOVE

    _SREF-INIT-SERVICE @ 0= _SREF-INIT-FACTORY @ 0= OR IF
        SREF-PHASE-OFFLINE SREF-S-OK SREF-S-UNAVAILABLE
            _SREF-INIT-OWNER @ _SREF-MARK EXIT
    THEN
    _SREF-INIT-FACTORY @ EXECUTE
    _SREF-INIT-FACTORY-STATUS !
    DUP _SREF-INIT-OWNER @ SREF.PROVIDER !
    _SREF-INIT-FACTORY-STATUS @ SCONF-S-OK <>
    OVER SCONF-VALID? 0= OR IF
        DUP SCONF-VALID? IF SCONF-RELEASE DROP ELSE DROP THEN
        0 _SREF-INIT-OWNER @ SREF.PROVIDER !
        SREF-PHASE-OFFLINE SREF-S-OK _SREF-INIT-FACTORY-STATUS @
            _SREF-INIT-OWNER @ _SREF-MARK EXIT
    THEN DROP
    _SREF-INIT-OWNER @ SREF.PHASE @ SREF-PHASE-RECOVERED <> IF
        SREF-PHASE-IDLE _SREF-INIT-OWNER @ SREF.PHASE !
    THEN
    SREF-S-OK _SREF-INIT-OWNER @ SREF.LAST-STATUS !
    0 _SREF-INIT-OWNER @ SREF.LAST-ERROR !
    SREF-S-OK ;

