        SWAP SREF-PHASE-CLEANUP <> AND
    OVER SREF.XIO-SERVICE @ DUP 0<> IF XIO-SERVICE-BOUND? ELSE DROP 0 THEN AND
    OVER SREF.PROVIDER @ DUP 0<> IF SCONF-VALID? ELSE DROP 0 THEN AND
    OVER SREF.PROVIDER @ SCONF-CLEANUP-ERROR@ 0= AND NIP ;

