\ =====================================================================
\ refresh-owner.f - durable manual configured-source refresh owner
\ =====================================================================
\ One owner coordinates a source snapshot, configured provider, Desk XIO
\ operation, retained syndication decoder, and crash-recoverable observation
\ checkpoint.  CONFIGURE and authorization are pure.  BEGIN is saved and
\ published before XIO submission.  A successful decoded candidate is not
\ saved or published until XIO reset proves lower cleanup succeeded.
\
\ The ordinary Streams applet owns one descriptor and pumps it from its core-0
\ tick.  Daybook can later invoke the same typed Streams operation; there is
\ deliberately no scheduler or private network pump here.
\
\ Public API:
\   STREAMS-REFRESH-OWNER-INIT
\     ( vfs xio-service configured-factory-xt owner -- status )
\   STREAMS-REFRESH-OWNER-START
\     ( exact-source owner-id owner-generation owner -- status )
\   STREAMS-REFRESH-OWNER-TICK
\     ( current-source|0 owner-id owner-generation owner -- changed? status )
\   STREAMS-REFRESH-OWNER-CANCEL
\     ( owner-id owner-generation owner -- changed? status )
\   STREAMS-REFRESH-OWNER-RELEASE    ( owner -- status )
\
\ Accessors return borrowed state owned by the descriptor.  CHECKPOINT remains
\ valid until RELEASE.  SOURCE is nonzero only after an attempt was accepted.
\ =====================================================================

