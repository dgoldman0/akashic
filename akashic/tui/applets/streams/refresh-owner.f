        STREAMS-SOURCE-SIZE MOVE
    _SREF-OWNER @ SREF.SOURCE _SREF-SOURCE-SUPPORTED? 0= IF
        _SREF-OWNER @ SREF.SOURCE STREAMS-SOURCE-SIZE 0 FILL
        SREF-S-INVALID EXIT
    THEN
    _SREF-OWNER @ _SREF-NAMESPACE!
    _SREF-OWNER @ SREF.SOURCE _SREF-OWNER @ SREF.PROVIDER @ SCONF-CONFIGURE
    DUP _SREF-START-CALL ! SCONF-S-OK <> IF
        SREF-PHASE-FAILED SREF-S-INVALID _SREF-START-CALL @
            _SREF-OWNER @ _SREF-MARK EXIT
    THEN
    _SREF-OWNER @ SREF.PROVIDER @ SCONF-REQUESTED$
    _SREF-START-U ! _SREF-START-A !
    _SREF-START-U @ DUP 1 < SWAP OCHK-URI-MAX > OR IF
        SREF-S-INVALID EXIT
    THEN
    _SREF-OWNER @ SREF.LIVE @ _SREF-OWNER @ SREF.CANDIDATE @
        STREAMS-OBSERVATION-CHECKPOINT-SIZE CMOVE
    _SREF-OWNER @ SREF.SOURCE SSOURCE.ID
    _SREF-OWNER @ SREF.SOURCE SSOURCE.REVISION @
    _SREF-OWNER @ SREF.NAMESPACE SREF-PROVIDER-SYNDICATION
    _SREF-START-A @ _SREF-START-U @ _SREF-OWNER @ SREF.CANDIDATE @
        OCHK-BEGIN DUP _SREF-START-CALL !
    OCHK-S-OK <> IF
        _SREF-START-CALL @ OCHK-S-CAPACITY = IF
            SREF-S-CAPACITY
        ELSE
            SREF-S-INVALID
        THEN EXIT
    THEN
    _SREF-OWNER @ _SREF-PERSIST SREF-S-OK <> IF SREF-S-STORAGE EXIT THEN
    -1 _SREF-OWNER @ SREF.ACCEPTED !

    _SREF-START-OWNER-ID @ _SREF-OWNER @ SREF.OWNER-ID !
    _SREF-START-OWNER-GEN @ _SREF-OWNER @ SREF.OWNER-GENERATION !
    _SREF-START-NEXT @ _SREF-OWNER @ SREF.REQUEST-GENERATION !
    _SREF-OWNER @ SREF.XIO-SERVICE @
    _SREF-START-OWNER-ID @ _SREF-START-OWNER-GEN @ _SREF-START-NEXT @
    MS@ STREAMS-REFRESH-TIMEOUT-MS +
    _SREF-OWNER @ SREF.PROVIDER @
    ['] SCONF-XIO-START ['] SCONF-XIO-POLL
    ['] SCONF-XIO-CANCEL ['] SCONF-XIO-WIPE
    _SREF-OWNER @ SREF.XIO-OP XIO-OP-CONFIGURE
    DUP _SREF-START-CALL ! XIO-S-OK <> IF
        _SREF-OWNER @ SREF.XIO-OP XIO-OP-INIT
        _SREF-OWNER @ _SREF-START-FAIL EXIT
    THEN
    _SREF-OWNER @ SREF.XIO-SERVICE @ _SREF-OWNER @ SREF.XIO-OP XIO-SUBMIT
    DUP _SREF-START-CALL ! XIO-S-OK <> IF
        _SREF-OWNER @ SREF.XIO-OP XIO-OP-INIT
        _SREF-OWNER @ _SREF-START-FAIL EXIT
    THEN
    SREF-PHASE-ACTIVE SREF-S-OK 0 _SREF-OWNER @ _SREF-MARK ;

