: _STREAMS-OBSERVATION-STORE-INIT-AT
  ( target-a target-u vfs store -- status )
    _OSSI-STORE ! _OSSI-VFS ! _OSSI-TARGET-U ! _OSSI-TARGET-A !
    _OSSI-TARGET-A @ 0= _OSSI-TARGET-U @ 0< OR
    _OSSI-VFS @ 0= OR _OSSI-STORE @ 0= OR IF
        OSTORE-S-INVALID EXIT
    THEN
    _OSSI-TARGET-A @ _OSSI-TARGET-U @ _OSSI-STORE @
        _OSTORE-TARGET-SPAN-SAFE? 0= IF OSTORE-S-INVALID EXIT THEN
    _OSSI-STORE @ STREAMS-OBSERVATION-STORE-SIZE 0 FILL
    _OSTORE-MAGIC _OSSI-STORE @ STREAMS-OBSERVATION-STORE.MAGIC !
    _OSSI-VFS @ _OSSI-STORE @ STREAMS-OBSERVATION-STORE.VFS !
    OSTORE-S-ABSENT _OSSI-STORE @
        STREAMS-OBSERVATION-STORE.LAST-STATUS !
    _OSSI-TARGET-A @ _OSSI-TARGET-U @
        _OSSI-STORE @ STREAMS-OBSERVATION-STORE.SCRATCH
        STREAMS-OBSERVATION-STORE-RECORD-MAX
        _OSSI-VFS @ _OSTORE-SPEC
        _OSSI-STORE @ STREAMS-OBSERVATION-STORE.CORE
        VFSNAP-INIT-AT
        _OSSI-STORE @ _OSTORE-SYNC ;

' _STREAMS-OBSERVATION-STORE-INIT-AT CONSTANT _ostore-init-at-xt

: STREAMS-OBSERVATION-STORE-INIT-AT
  ( target-a target-u vfs store -- status )
    2OVER 2OVER SWAP DROP _OSTORE-TARGET-SPAN-SAFE? 0= IF
        2DROP 2DROP OSTORE-S-INVALID EXIT
    THEN
    _ostore-init-at-xt _streams-observation-store-guard WITH-GUARD ;

: STREAMS-OBSERVATION-STORE-INIT  ( vfs store -- status )
    STREAMS-OBSERVATION-STORE-TARGET$ 2SWAP
        STREAMS-OBSERVATION-STORE-INIT-AT ;

: _STREAMS-OBSERVATION-STORE-RECOVER  ( store -- status )
    DUP STREAMS-OBSERVATION-STORE-VALID? 0= IF
        DROP OSTORE-S-INVALID EXIT
    THEN
    DUP >R STREAMS-OBSERVATION-STORE.CORE VFSNAP-RECOVER
    R> _OSTORE-SYNC ;

' _STREAMS-OBSERVATION-STORE-RECOVER CONSTANT _ostore-recover-xt
: STREAMS-OBSERVATION-STORE-RECOVER  ( store -- status )
    DUP _OSTORE-STORE-SPAN-SAFE? 0= IF DROP OSTORE-S-INVALID EXIT THEN
    _ostore-recover-xt _streams-observation-store-guard WITH-GUARD ;

: _STREAMS-OBSERVATION-STORE-LOAD
  ( destination capacity store -- status )
    _OSSL-STORE ! _OSSL-CAPACITY ! _OSSL-DESTINATION !
    _OSSL-STORE @ STREAMS-OBSERVATION-STORE-VALID? 0= IF
        OSTORE-S-INVALID EXIT
    THEN
    _OSSL-DESTINATION @ _OSSL-CAPACITY @
        _OSSL-STORE @ STREAMS-OBSERVATION-STORE.LOAD-GENERATION
        _OSSL-STORE @ STREAMS-OBSERVATION-STORE.CORE VFSNAP-LOAD
        _OSSL-STORE @ _OSTORE-SYNC ;

' _STREAMS-OBSERVATION-STORE-LOAD CONSTANT _ostore-load-xt
: STREAMS-OBSERVATION-STORE-LOAD
  ( destination capacity store -- status )
    2 PICK 2 PICK 2 PICK _OSTORE-IO-SPAN-SAFE? 0= IF
        2DROP DROP OSTORE-S-INVALID EXIT
    THEN
    _ostore-load-xt _streams-observation-store-guard WITH-GUARD ;

: _STREAMS-OBSERVATION-STORE-SAVE
  ( checkpoint expected-generation store -- status )
    _OSSS-STORE ! _OSSS-EXPECTED ! _OSSS-CHECKPOINT !
    _OSSS-STORE @ STREAMS-OBSERVATION-STORE-VALID? 0= IF
        OSTORE-S-INVALID EXIT
    THEN
    _OSSS-EXPECTED @ 0< IF OSTORE-S-INVALID EXIT THEN
    _OSSS-STORE @ STREAMS-OBSERVATION-STORE-BLOCKED? IF
        _OSSS-STORE @ STREAMS-OBSERVATION-STORE.LAST-STATUS @ EXIT
    THEN
    _OSSS-CHECKPOINT @ OCHK-VALID? 0= IF OSTORE-S-INVALID EXIT THEN
    _OSSS-EXPECTED @ 1+ DUP 0> 0= IF
        DROP OSTORE-S-CAPACITY EXIT
    THEN
    _OSSS-CHECKPOINT @ OCHK.GENERATION @ <> IF
        OSTORE-S-INVALID EXIT
    THEN
    _OSSS-CHECKPOINT @ _OSSS-EXPECTED @
        _OSSS-STORE @ STREAMS-OBSERVATION-STORE.CORE VFSNAP-SAVE
        _OSSS-STORE @ _OSTORE-SYNC ;

\ Test/golden helper: exercise the shared encoder into this instance's
\ scratch without performing VFS publication.  It deliberately remains
\ private and delegates all envelope construction to the shared core.
: _STREAMS-OBSERVATION-STORE-ENCODE
  ( checkpoint store -- length status )
    _OSSS-STORE ! _OSSS-CHECKPOINT !
    _OSSS-STORE @ STREAMS-OBSERVATION-STORE-VALID? 0= IF
        0 OSTORE-S-INVALID EXIT
    THEN
    _OSSS-STORE @ STREAMS-OBSERVATION-STORE.CORE _VFSOP-STORE !
    _OSSS-CHECKPOINT @ _VFSS-CONTEXT !
    _OSSS-CHECKPOINT @ OCHK.GENERATION @ _VFSS-NEXT !
    _VFSNAP-ENCODE STREAMS-OBSERVATION-STORE-RECORD-MAX SWAP
        _OSTORE-VFSNAP>STATUS ;

' _STREAMS-OBSERVATION-STORE-SAVE CONSTANT _ostore-save-xt
: STREAMS-OBSERVATION-STORE-SAVE
  ( checkpoint expected-generation store -- status )
    2 PICK STREAMS-OBSERVATION-CHECKPOINT-SIZE 2 PICK
        _OSTORE-IO-SPAN-SAFE? 0= IF
        2DROP DROP OSTORE-S-INVALID EXIT
    THEN
    _ostore-save-xt _streams-observation-store-guard WITH-GUARD ;

: _STREAMS-OBSERVATION-STORE-FINI  ( store -- status )
    DUP STREAMS-OBSERVATION-STORE-VALID? 0= IF
        DROP OSTORE-S-INVALID EXIT
    THEN
    DUP STREAMS-OBSERVATION-STORE.CORE VFSNAP-FINI
    DUP VFSNAP-S-OK = IF
        DROP STREAMS-OBSERVATION-STORE-SIZE 0 FILL OSTORE-S-OK
    ELSE
        NIP _OSTORE-VFSNAP>STATUS
    THEN ;

' _STREAMS-OBSERVATION-STORE-FINI CONSTANT _ostore-fini-xt
: STREAMS-OBSERVATION-STORE-FINI  ( store -- status )
    DUP _OSTORE-STORE-SPAN-SAFE? 0= IF DROP OSTORE-S-INVALID EXIT THEN
    _ostore-fini-xt _streams-observation-store-guard WITH-GUARD ;

