: _OSTORE-SAVE-FAIL  ( status -- status )
    DUP OSTORE-S-CONFLICT =
    OVER OSTORE-S-INVALID = OR
    OVER OSTORE-S-CAPACITY = OR
    OVER OSTORE-S-BUSY = OR IF
        _OSSS-STORE @ _OSTORE-STATUS! EXIT
    THEN
    _OSSS-STORE @ _OSTORE-BLOCK! ;

: _STREAMS-OBSERVATION-STORE-SAVE
  ( checkpoint expected-generation store -- status )
    _OSSS-STORE ! _OSSS-EXPECTED ! _OSSS-CHECKPOINT !
    _OSSS-CHECKPOINT @ STREAMS-OBSERVATION-CHECKPOINT-SIZE _OSSS-STORE @
        _OSTORE-IO-SPAN-SAFE? 0= IF OSTORE-S-INVALID EXIT THEN
    _OSSS-STORE @ STREAMS-OBSERVATION-STORE-VALID? 0= IF
        OSTORE-S-INVALID EXIT
    THEN
    _OSSS-EXPECTED @ 0< IF OSTORE-S-INVALID EXIT THEN
    _OSSS-STORE @ STREAMS-OBSERVATION-STORE-BLOCKED? IF
        _OSSS-STORE @ STREAMS-OBSERVATION-STORE.LAST-STATUS @ EXIT
    THEN
    _OSSS-CHECKPOINT @ OCHK-VALID? 0= IF
        OSTORE-S-INVALID EXIT
    THEN
    _OSSS-EXPECTED @ 1+ DUP 0> 0= IF
        DROP OSTORE-S-CAPACITY EXIT
    THEN
    _OSSS-CHECKPOINT @ OCHK.GENERATION @ <> IF
        OSTORE-S-INVALID EXIT
    THEN
    _OSSS-STORE @ _STREAMS-OBSERVATION-STORE-RECOVER DUP IF
        _OSTORE-SAVE-FAIL EXIT
    THEN DROP
    _OSSS-STORE @ _OSRD-READ DUP _OSSS-STATUS ! DROP
    _OSSS-STATUS @ OSTORE-S-ABSENT = IF
        _OSSS-EXPECTED @ IF OSTORE-S-CONFLICT _OSTORE-SAVE-FAIL EXIT THEN
    ELSE
        _OSSS-STATUS @ OSTORE-S-OK <> IF
            _OSSS-STATUS @ _OSTORE-SAVE-FAIL EXIT
        THEN
        _OSRD-GENERATION @ _OSSS-EXPECTED @ <> IF
            OSTORE-S-CONFLICT _OSTORE-SAVE-FAIL EXIT
        THEN
    THEN
    _OSSS-CHECKPOINT @ _STREAMS-OBSERVATION-STORE-ENCODE
        _OSSS-STATUS ! _OSSS-LENGTH !
    _OSSS-STATUS @ IF _OSSS-STATUS @ _OSTORE-SAVE-FAIL EXIT THEN
    _OSTORE-RECORD _OSSS-LENGTH @
        _OSSS-STORE @ STREAMS-OBSERVATION-STORE.REPLACE VREPL-REPLACE
        DUP _OSSS-VREPL !
    _OSSS-STORE @
        STREAMS-OBSERVATION-STORE.LAST-VREPL !
    _OSSS-VREPL @ _OSTORE-VREPL>STATUS DUP _OSSS-STATUS !
    DROP
    _OSSS-STATUS @ OSTORE-S-OK = IF
        _OSSS-STATUS @ _OSSS-STORE @ _OSTORE-STATUS! EXIT
    THEN
    _OSSS-STATUS @ _OSTORE-SAVE-FAIL ;

