: _OSRD-READ-BODY  ( -- status )
    _OSRD-STORE @ STREAMS-OBSERVATION-STORE-PATH$
        _OSRD-STORE @ STREAMS-OBSERVATION-STORE.VFS @ VFS-RESOLVE 0= IF
        OSTORE-S-ABSENT EXIT
    THEN
    _OSRD-STORE @ STREAMS-OBSERVATION-STORE-PATH$ VFS-OPEN DUP 0= IF
        DROP OSTORE-S-IO EXIT
    THEN
    _OSRD-FD !
    _OSRD-FD @ VFS-SIZE DUP _OSRD-FILE-U !
    DUP 0< IF DROP OSTORE-S-IO EXIT THEN
    STREAMS-OBSERVATION-STORE-HEADER-SIZE < IF OSTORE-S-CORRUPT EXIT THEN
    _OSTORE-RECORD STREAMS-OBSERVATION-STORE-HEADER-SIZE
        _OSRD-FD @ VFS-READ-EXACT IF OSTORE-S-IO EXIT THEN
    _OSTORE-RECORD _OSS-H-MAGIC + 8
        _OSTORE-RECORD-MAGIC 8 COMPARE 0<> IF
        OSTORE-S-CORRUPT EXIT
    THEN
    _OSTORE-RECORD _STREAMS-OBSERVATION-STORE-HEADER-CRC
        _OSTORE-RECORD _OSS-H-HEADER-CRC + @ <> IF
        OSTORE-S-CORRUPT EXIT
    THEN
    _OSTORE-RECORD _OSS-H-FORMAT + @
        STREAMS-OBSERVATION-STORE-FORMAT-V1 <> IF
        OSTORE-S-UNSUPPORTED EXIT
    THEN
    _OSTORE-RECORD _OSS-H-HEADER-SIZE + @
        STREAMS-OBSERVATION-STORE-HEADER-SIZE <>
    _OSTORE-RECORD _OSS-H-FLAGS + @ 0<> OR
    _OSTORE-RECORD _OSS-H-GENERATION + @ DUP _OSRD-GENERATION !
        0> 0= OR
    _OSTORE-RECORD _OSS-H-PAYLOAD-SIZE + @
        STREAMS-OBSERVATION-CHECKPOINT-SIZE <> OR IF
        OSTORE-S-CORRUPT EXIT
    THEN
    _OSRD-FILE-U @ STREAMS-OBSERVATION-STORE-RECORD-MAX <> IF
        OSTORE-S-CORRUPT EXIT
    THEN
    _OSTORE-RECORD STREAMS-OBSERVATION-STORE-HEADER-SIZE +
        STREAMS-OBSERVATION-CHECKPOINT-SIZE _OSRD-FD @ VFS-READ-EXACT IF
        OSTORE-S-IO EXIT
    THEN
    _OSTORE-RECORD STREAMS-OBSERVATION-STORE-HEADER-SIZE +
        STREAMS-OBSERVATION-CHECKPOINT-SIZE
        _STREAMS-OBSERVATION-STORE-PAYLOAD-CRC
        _OSTORE-RECORD _OSS-H-PAYLOAD-CRC + @ <> IF
        OSTORE-S-CORRUPT EXIT
    THEN
    _OSTORE-RECORD STREAMS-OBSERVATION-STORE-HEADER-SIZE + DUP
        OCHK.MAGIC @ OCHK-MAGIC <> IF
        DROP OSTORE-S-CORRUPT EXIT
