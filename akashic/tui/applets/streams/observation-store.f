\ Preserve the historical null/empty policy at the typed boundary.
: _OSTORE-SPAN-VALID?  ( address length -- flag )
    DUP 0< IF 2DROP 0 EXIT THEN
    OVER 0= IF 2DROP 0 EXIT THEN
    DUP 0= IF 2DROP -1 EXIT THEN
    _VFSNAP-SPAN-VALID? ;
: _OSTORE-RANGES-OVERLAP?  ( a1 u1 a2 u2 -- flag )
    _VFSNAP-RANGES-OVERLAP? ;

: _OSTORE-PRIVATE-ALIASES?  ( address length -- flag )
    2DUP _OSTORE-SPEC _VFSNAP-ALL-PRIVATE-ALIASES? IF
        2DROP -1 EXIT
    THEN
    _OSTORE-SPEC VFSNAP-SPEC-SIZE _OSTORE-RANGES-OVERLAP? ;

: _OSTORE-STORE-SPAN-SAFE?  ( store -- flag )
    DUP STREAMS-OBSERVATION-STORE-SIZE _OSTORE-SPAN-VALID? 0= IF
        DROP 0 EXIT
    THEN
    DUP STREAMS-OBSERVATION-STORE-SIZE _OSTORE-PRIVATE-ALIASES? 0= NIP ;

: _OSTORE-IO-ALIASES?  ( address length store -- flag )
    >R
    R@ _OSTORE-STORE-SPAN-SAFE? 0= IF 2DROP R> DROP -1 EXIT THEN
    2DUP _OSTORE-SPAN-VALID? 0= IF 2DROP R> DROP 0 EXIT THEN
    2DUP R@ STREAMS-OBSERVATION-STORE-SIZE
        _OSTORE-RANGES-OVERLAP? >R
    _OSTORE-PRIVATE-ALIASES? R> OR R> DROP ;

: _OSTORE-IO-SPAN-SAFE?  ( address length store -- flag )
    >R
    2DUP _OSTORE-SPAN-VALID?
    R@ _OSTORE-STORE-SPAN-SAFE? AND 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    R> _OSTORE-IO-ALIASES? 0= ;

: _OSTORE-TARGET-SPAN-SAFE?  ( target-a target-u store -- flag )
    >R
    R@ _OSTORE-STORE-SPAN-SAFE? 0= IF 2DROP R> DROP 0 EXIT THEN
    2DUP _OSTORE-SPAN-VALID? 0= IF 2DROP R> DROP 0 EXIT THEN
    2DUP R@ STREAMS-OBSERVATION-STORE-SIZE
        _OSTORE-RANGES-OVERLAP? >R
    _OSTORE-PRIVATE-ALIASES? R> OR 0= R> DROP ;

: STREAMS-OBSERVATION-STORE-VALID?  ( store -- flag )
    DUP _OSTORE-STORE-SPAN-SAFE? 0= IF DROP 0 EXIT THEN
    DUP STREAMS-OBSERVATION-STORE.MAGIC @ _OSTORE-MAGIC <> IF
        DROP 0 EXIT
    THEN
    DUP STREAMS-OBSERVATION-STORE.VFS @ 0= IF DROP 0 EXIT THEN
    DUP STREAMS-OBSERVATION-STORE.CORE VFSNAP-VALID? 0= IF
        DROP 0 EXIT
    THEN
    DUP STREAMS-OBSERVATION-STORE.VFS @ SWAP
        STREAMS-OBSERVATION-STORE.CORE VFSNAP.VFS @ = ;

