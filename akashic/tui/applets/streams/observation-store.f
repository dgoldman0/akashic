        \ An alias rejection is a strictly non-mutating preflight.  In
        \ particular, do not wipe codec scratch when it is itself the
        \ caller-declared destination span.
        OSTORE-S-INVALID EXIT
    THEN
    ['] _STREAMS-OBSERVATION-STORE-LOAD-CALL CATCH
    _STREAMS-OBSERVATION-STORE-SCRATCH-WIPE
    DUP IF THROW THEN DROP ;

