\ ---------------------------------------------------------------------
\ Public span/alias preflight
\ ---------------------------------------------------------------------
\ Half-open byte spans make exact adjacency safe.  Validate both additions
\ before comparing endpoints so a wrapping address+length is never mistaken
\ for a disjoint range.  LOAD protects the complete caller-declared capacity;
\ SAVE protects the complete fixed checkpoint.

: _OSTORE-SPAN-VALID?  ( address length -- flag )
    DUP 0< IF 2DROP 0 EXIT THEN
    OVER 0= IF 2DROP 0 EXIT THEN
    DUP 0= IF 2DROP -1 EXIT THEN
    >R DUP R@ + SWAP U< 0= R> DROP ;

: _OSTORE-RANGES-OVERLAP?  ( a1 u1 a2 u2 -- flag )
    2OVER _OSTORE-SPAN-VALID? 0= IF
        2DROP 2DROP 0 EXIT
    THEN
    2DUP _OSTORE-SPAN-VALID? 0= IF
        2DROP 2DROP 0 EXIT
    THEN
    DUP 0= IF 2DROP 2DROP 0 EXIT THEN
    2OVER NIP 0= IF 2DROP 2DROP 0 EXIT THEN
    \ Preserve the four inputs only through the first comparison.  Reducing
    \ them in place keeps this predicate free of addressable module scratch,
    \ which matters when the adversarial span surrounds codec scratch itself.
    2OVER + >R OVER R> U< >R
    + >R DROP R> U< R> AND ;

: _OSTORE-PRIVATE-ALIASES?  ( address length -- flag )
    _OSTORE-PRIVATE-RANGE-N @ 0 ?DO
        2DUP
        _OSTORE-PRIVATE-RANGES I _OSTORE-PRIVATE-RANGE-SIZE * +
        DUP @ SWAP 8 + @ _OSTORE-RANGES-OVERLAP? IF
            2DROP -1 UNLOOP EXIT
        THEN
    LOOP
    2DROP 0 ;

: _OSTORE-STORE-SPAN-SAFE?  ( store -- flag )
    DUP STREAMS-OBSERVATION-STORE-SIZE _OSTORE-SPAN-VALID? 0= IF
        DROP 0 EXIT
    THEN
    DUP STREAMS-OBSERVATION-STORE-SIZE
        _OSTORE-PRIVATE-ALIASES? 0= NIP ;

: _OSTORE-IO-ALIASES?  ( address length store -- flag )
    >R
    R@ _OSTORE-STORE-SPAN-SAFE? 0= IF
        2DROP R> DROP -1 EXIT
    THEN
    2DUP _OSTORE-SPAN-VALID? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    2DUP R@ STREAMS-OBSERVATION-STORE-SIZE _OSTORE-RANGES-OVERLAP? >R
    _OSTORE-PRIVATE-ALIASES?
    R> OR R> DROP ;

: _OSTORE-IO-SPAN-SAFE?  ( address length store -- flag )
    >R
    2DUP _OSTORE-SPAN-VALID?
    R@ _OSTORE-STORE-SPAN-SAFE? AND
    0= IF 2DROP R> DROP 0 EXIT THEN
    R> _OSTORE-IO-ALIASES? 0= ;

: _OSTORE-TARGET-SPAN-SAFE?  ( target-a target-u store -- flag )
    >R
    R@ _OSTORE-STORE-SPAN-SAFE? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    2DUP _OSTORE-SPAN-VALID? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    2DUP R@ STREAMS-OBSERVATION-STORE-SIZE _OSTORE-RANGES-OVERLAP? >R
    _OSTORE-PRIVATE-ALIASES?
    R> OR 0= R> DROP ;

