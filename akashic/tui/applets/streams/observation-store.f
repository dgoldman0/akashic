: _OSTORE-ENCODE-PAYLOAD
  ( checkpoint payload-a payload-u next-generation -- status )
    _OSCB-GENERATION ! _OSCB-PAYLOAD-U !
    _OSCB-PAYLOAD ! _OSCB-CONTEXT !
    _OSCB-PAYLOAD-U @ STREAMS-OBSERVATION-CHECKPOINT-SIZE <> IF
        VFSNAP-S-CAPACITY EXIT
    THEN
    _OSCB-CONTEXT @ OCHK-VALID? 0= IF VFSNAP-S-INVALID EXIT THEN
    _OSCB-CONTEXT @ OCHK.GENERATION @ _OSCB-GENERATION @ <> IF
        VFSNAP-S-INVALID EXIT
    THEN
    \ OCHK-VALID? proves every fixed table/blob tail byte canonical.
    _OSCB-CONTEXT @ _OSCB-PAYLOAD @ STREAMS-OBSERVATION-CHECKPOINT-SIZE
        CMOVE
    VFSNAP-S-OK ;

: _OSTORE-VALIDATE-PAYLOAD
  ( payload-a payload-u envelope-generation -- status )
    _OSCB-GENERATION ! _OSCB-PAYLOAD-U ! _OSCB-PAYLOAD !
    _OSCB-PAYLOAD-U @ STREAMS-OBSERVATION-CHECKPOINT-SIZE <> IF
        VFSNAP-S-CORRUPT EXIT
    THEN
    _OSCB-PAYLOAD @ OCHK.MAGIC @ OCHK-MAGIC <> IF
        VFSNAP-S-CORRUPT EXIT
    THEN
    _OSCB-PAYLOAD @ OCHK.ABI @ OCHK-ABI <> IF
        VFSNAP-S-UNSUPPORTED EXIT
    THEN
    _OSCB-PAYLOAD @ OCHK-VALID? 0= IF VFSNAP-S-CORRUPT EXIT THEN
    _OSCB-PAYLOAD @ OCHK.GENERATION @ _OSCB-GENERATION @ <> IF
        VFSNAP-S-CORRUPT EXIT
    THEN
    VFSNAP-S-OK ;

\ Explicit adapter even though the current numeric domains coincide.
: _OSTORE-VFSNAP>STATUS  ( snapshot-status -- observation-status )
    CASE
        VFSNAP-S-OK OF OSTORE-S-OK ENDOF
        VFSNAP-S-ABSENT OF OSTORE-S-ABSENT ENDOF
        VFSNAP-S-CORRUPT OF OSTORE-S-CORRUPT ENDOF
        VFSNAP-S-UNSUPPORTED OF OSTORE-S-UNSUPPORTED ENDOF
        VFSNAP-S-INVALID OF OSTORE-S-INVALID ENDOF
        VFSNAP-S-CAPACITY OF OSTORE-S-CAPACITY ENDOF
        VFSNAP-S-IO OF OSTORE-S-IO ENDOF
        VFSNAP-S-RECOVERY OF OSTORE-S-RECOVERY ENDOF
        VFSNAP-S-BUSY OF OSTORE-S-BUSY ENDOF
        VFSNAP-S-CONFLICT OF OSTORE-S-CONFLICT ENDOF
        OSTORE-S-RECOVERY SWAP
    ENDCASE ;

: _OSTORE-SYNC  ( snapshot-status store -- observation-status )
    _OSAD-STORE ! _OSAD-STATUS !
    _OSAD-STORE @ STREAMS-OBSERVATION-STORE.CORE
        VFSNAP-LAST-STATUS@ _OSTORE-VFSNAP>STATUS
        _OSAD-STORE @ STREAMS-OBSERVATION-STORE.LAST-STATUS !
    _OSAD-STORE @ STREAMS-OBSERVATION-STORE.CORE VFSNAP-LAST-VREPL@
        _OSAD-STORE @ STREAMS-OBSERVATION-STORE.LAST-VREPL !
    _OSAD-STORE @ STREAMS-OBSERVATION-STORE.CORE VFSNAP-BLOCKED? IF
        _OSTORE-F-BLOCKED
    ELSE
        0
    THEN _OSAD-STORE @ STREAMS-OBSERVATION-STORE.FLAGS !
    _OSAD-STATUS @ _OSTORE-VFSNAP>STATUS ;

