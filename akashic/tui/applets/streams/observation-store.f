    _OSSL-STORE @ STREAMS-OBSERVATION-STORE-VALID? 0= OR IF
        OSTORE-S-INVALID EXIT
    THEN
    _OSSL-DESTINATION @ _OSSL-CAPACITY @ _OSSL-STORE @
        _OSTORE-IO-SPAN-SAFE? 0= IF OSTORE-S-INVALID EXIT THEN
    _OSSL-CAPACITY @ STREAMS-OBSERVATION-CHECKPOINT-SIZE < IF
        OSTORE-S-CAPACITY EXIT
    THEN
    _OSSL-STORE @ STREAMS-OBSERVATION-STORE-BLOCKED? IF
        _OSSL-STORE @ STREAMS-OBSERVATION-STORE.LAST-STATUS @ EXIT
    THEN
    _OSSL-STORE @ _STREAMS-OBSERVATION-STORE-RECOVER DUP IF EXIT THEN DROP
    _OSSL-STORE @ _OSRD-READ DUP _OSSL-STATUS ! DROP
    _OSSL-STATUS @ OSTORE-S-OK = IF
        _OSTORE-RECORD STREAMS-OBSERVATION-STORE-HEADER-SIZE +
            _OSSL-DESTINATION @ STREAMS-OBSERVATION-CHECKPOINT-SIZE CMOVE
        0 _OSSL-STORE @ STREAMS-OBSERVATION-STORE.FLAGS !
        OSTORE-S-OK _OSSL-STORE @ _OSTORE-STATUS! EXIT
    THEN
    _OSSL-STATUS @ OSTORE-S-ABSENT = IF
        _OSSL-STATUS @ _OSSL-STORE @ _OSTORE-STATUS! EXIT
    THEN
    _OSSL-STATUS @ OSTORE-S-CORRUPT =
    _OSSL-STATUS @ OSTORE-S-UNSUPPORTED = OR
    _OSSL-STATUS @ OSTORE-S-IO = OR
    _OSSL-STATUS @ OSTORE-S-RECOVERY = OR IF
        _OSSL-STATUS @ _OSSL-STORE @ _OSTORE-BLOCK! EXIT
    THEN
