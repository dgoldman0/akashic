        SCONF-S-CAPACITY EXIT
    THEN
    DUP _SCSNEW-C ! _SCSYN-CONTEXT-SIZE 0 FILL
    _SCSYN-CONTEXT-MAGIC _SCSNEW-C @ _SCS.C.MAGIC !
    _SCSNEW-AUTH-XT @ _SCSNEW-C @ _SCS.C.AUTH-XT !
    _SCSNEW-AUTH-CONTEXT @ _SCSNEW-C @ _SCS.C.AUTH-CONTEXT !

