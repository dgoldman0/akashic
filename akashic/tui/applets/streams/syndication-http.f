: _SCSYN-RELEASE  ( context -- status )
    DUP _SCSFREE-C ! _SCSYN-RELEASABLE? 0= IF SCONF-S-BUSY EXIT THEN
    _SCSFREE-C @ _SCS.C.RESOURCE @ DUP HRES-STATE@
        HRES-STATE-IDLE <> IF
        HRES-WIPE DUP _SCSFREE-STATUS ! HRES-S-OK <> IF
            _SCSFREE-STATUS @ _SCSFREE-C @ _SCS.C.CLEANUP !
            SCONF-S-TRANSPORT EXIT
        THEN
    ELSE
        DROP
    THEN
    _SCSFREE-C @ _SCS.C.RESOURCE @ HRES-DECONFIGURE
    DUP _SCSFREE-STATUS ! HRES-S-OK <> IF
        _SCSFREE-STATUS @ _SCSFREE-C @ _SCS.C.CLEANUP !
        SCONF-S-TRANSPORT EXIT
    THEN
    ['] _SCSYN-FREE-TLS CATCH ?DUP IF
        DUP _SCSFREE-C @ _SCS.C.CLEANUP ! DROP SCONF-S-TRANSPORT EXIT
    THEN
    _SCSFREE-C @ _SCS.C.BODY @ DUP HRES-BODY-MAX 0 FILL FREE
    _SCSFREE-C @ _SCS.C.RESOURCE @ DUP HTTP-RESOURCE-SIZE 0 FILL FREE
    _SCSFREE-C @ _SCS.C.SPEC @ DUP HRES-SPEC-SIZE 0 FILL FREE
    _SCSFREE-C @ DUP _SCSYN-CONTEXT-SIZE 0 FILL FREE
    SCONF-S-OK ;

