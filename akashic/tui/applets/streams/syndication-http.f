: _SCSYN-BIND  ( target context -- port provider-status )
    _SCSB-C ! _SCSB-TARGET !
    _SCSB-C @ _SCSYN-CONTEXT-VALID? 0= IF
        0 SCONF-S-INVALID EXIT
    THEN
    _SCSB-TARGET @ HTARGET-VALID? 0= IF 0 SCONF-S-INVALID EXIT THEN
    _SCSB-C @ _SCS.C.POISON @
    _SCSB-C @ _SCS.C.CLEANUP @ OR IF 0 SCONF-S-TRANSPORT EXIT THEN
    _SCSB-C @ _SCS.C.LEASED @ IF 0 SCONF-S-BUSY EXIT THEN
    _SCSB-C @ _SCS.C.SOURCE STREAMS-SOURCE-VALID? 0= IF
        0 SCONF-S-INVALID EXIT
    THEN
    _SCSB-C @ _SCS.C.SOURCE _SCSB-TARGET @ _SCSB-C @
        _SCSYN-AUTHORIZED? 0= IF 0 SCONF-S-INVALID EXIT THEN
    _SCSB-C @ _SCS.C.TLS @ DUP _SCSB-TLS !
    KDOSTLS.STATE @ KDOSTLS-STATE-CLOSED <> IF
        0 SCONF-S-BUSY EXIT
    THEN
    _SCSB-TARGET @ HTARGET-HOST$
    _SCSB-TARGET @ HTARGET-PORT@
    _SCSB-TLS @ KDOSTLS-CONFIGURE DUP _SCSB-STATUS !
    KDOSTLS-E-OK <> IF 0 _SCSB-STATUS @ EXIT THEN
    1 _SCSB-C @ _SCS.C.LEASED !
    _SCSB-TLS @ KDOSTLS.PORT SCONF-S-OK ;

VARIABLE _SCSR-PORT
VARIABLE _SCSR-C
VARIABLE _SCSR-TLS

: _SCSYN-PORT-DETACHED?  ( port -- flag )
    DUP NIO.OPEN-STATE @ DUP NIO-OPEN-STATE-CLOSED =
        SWAP NIO-OPEN-STATE-CANCELLED = OR
    OVER NIO.CLOSE-STATE @ DUP NIO-CLOSE-STATE-IDLE =
        OVER NIO-CLOSE-STATE-CLOSED = OR
        SWAP NIO-CLOSE-STATE-CANCELLED = OR AND
    OVER NIO.CANCEL-ERROR @ 0= AND
    SWAP NIO.CLOSE-ERROR @ 0= AND ;

: _SCSYN-LEASE-RELEASE  ( port context -- provider-status )
    _SCSR-C ! _SCSR-PORT !
    _SCSR-C @ _SCSYN-CONTEXT-VALID? 0= IF SCONF-S-INVALID EXIT THEN
    _SCSR-C @ _SCS.C.LEASED @ 0= IF SCONF-S-INVALID EXIT THEN
    _SCSR-C @ _SCS.C.TLS @ DUP _SCSR-TLS !
    KDOSTLS.PORT _SCSR-PORT @ <> IF SCONF-S-INVALID EXIT THEN
    _SCSR-PORT @ _SCSYN-PORT-DETACHED? 0= IF
        SCONF-E-INVALID _SCSR-C @ _SCS.C.CLEANUP !
        SCONF-S-TRANSPORT EXIT
    THEN
    _SCSR-TLS @ KDOSTLS.STATE @ KDOSTLS-STATE-CLOSED <> IF
        SCONF-E-INVALID _SCSR-C @ _SCS.C.CLEANUP !
        SCONF-S-TRANSPORT EXIT
    THEN
    _SCSR-TLS @ KDOSTLS.CLEANUP-ERROR @ ?DUP IF
        _SCSR-C @ _SCS.C.CLEANUP ! SCONF-S-TRANSPORT EXIT
    THEN
    0 _SCSR-C @ _SCS.C.LEASED ! SCONF-S-OK ;

