: _SCSYN-ALLOC  ( size -- address | 0 )
    ALLOCATE DUP IF 2DROP 0 ELSE DROP THEN ;

: _SCSYN-PARTIAL-FREE  ( -- )
    _SCSNEW-P @ ?DUP IF
        DUP STREAMS-CONFIGURED-PROVIDER-SIZE 0 FILL FREE 0 _SCSNEW-P !
    THEN
    _SCSNEW-C @ ?DUP 0= IF EXIT THEN
    DUP _SCS.C.TLS @ ?DUP IF DUP KDOSTLS-SIZE 0 FILL FREE THEN
    DUP _SCS.C.BODY @ ?DUP IF DUP HRES-BODY-MAX 0 FILL FREE THEN
    DUP _SCS.C.RESOURCE @ ?DUP IF DUP HTTP-RESOURCE-SIZE 0 FILL FREE THEN
    DUP _SCS.C.SPEC @ ?DUP IF DUP HRES-SPEC-SIZE 0 FILL FREE THEN
    DUP _SCSYN-CONTEXT-SIZE 0 FILL FREE 0 _SCSNEW-C ! ;

