: _SCSYN-SPAN-WRAPS?  ( address length -- flag )
    OVER + SWAP U< ;

: _SCSYN-SPANS-OVERLAP?  ( a1 u1 a2 u2 -- flag )
    DUP 0= IF 2DROP 2DROP 0 EXIT THEN
    2 PICK 0= IF 2DROP 2DROP 0 EXIT THEN
    2OVER _SCSYN-SPAN-WRAPS? IF 2DROP 2DROP -1 EXIT THEN
    2DUP _SCSYN-SPAN-WRAPS? IF 2DROP 2DROP -1 EXIT THEN
    2OVER + >R OVER R> U< >R
    + >R DROP R> U< R> AND ;

: _SCSYN-SOURCE?  ( source -- flag )
    DUP STREAMS-SOURCE-VALID? 0= IF DROP 0 EXIT THEN
    DUP SSOURCE.KIND @ SSOURCE-KIND-SYNDICATION =
    OVER SSOURCE.FLAGS @ SSOURCE-F-ENABLED AND 0<> AND
    OVER SSOURCE.REDIRECT-MAX @ HRES-REDIRECT-MAX <= AND
    OVER SSOURCE.RESPONSE-MAX @ DUP 0>
        SWAP HRES-BODY-MAX <= AND AND
    SWAP SSOURCE.FORMAT @ DUP SSOURCE-FORMAT-AUTO =
        OVER SSOURCE-FORMAT-RSS = OR
        OVER SSOURCE-FORMAT-ATOM = OR
        SWAP SSOURCE-FORMAT-JSON-FEED = OR AND ;

: _SCSYN-RECONFIGURE-READY  ( context -- status )
    DUP _SCS.C.RESOURCE @ DUP _SCSCFG-R ! HRES-STATE@
    DUP HRES-STATE-IDLE = IF 2DROP SCONF-S-OK EXIT THEN
    DUP HRES-STATE-CONFIGURED =
    OVER HRES-STATE-RELEASED = OR IF
        DROP DROP _SCSCFG-R @ HRES-DECONFIGURE _SCSYN-HRES>STATUS EXIT
    THEN
    2DROP SCONF-S-BUSY ;

: _SCSYN-CONFIGURE  ( source context -- status )
    _SCSCFG-C ! _SCSCFG-SOURCE !
    _SCSCFG-C @ _SCSYN-CONTEXT-VALID? 0= IF SCONF-S-INVALID EXIT THEN
    _SCSCFG-SOURCE @ STREAMS-SOURCE-SIZE
        _SCSCFG-C @ _SCSYN-CONTEXT-SIZE _SCSYN-SPANS-OVERLAP? IF
        SCONF-S-INVALID EXIT
    THEN
    _SCSCFG-SOURCE @ _SCSYN-SOURCE? 0= IF SCONF-S-INVALID EXIT THEN
    _SCSCFG-C @ _SCS.C.POISON @
    _SCSCFG-C @ _SCS.C.CLEANUP @ OR IF SCONF-S-TRANSPORT EXIT THEN
    _SCSCFG-C @ _SCS.C.LEASED @ IF SCONF-S-BUSY EXIT THEN
    _SCSCFG-C @ _SCSYN-RECONFIGURE-READY DUP IF EXIT THEN DROP

    _SCSCFG-C @ _SCS.C.SPEC @ DUP _SCSCFG-SPEC ! HRES-SPEC-INIT
    _SCSCFG-SOURCE @ STREAMS-SOURCE-ENDPOINT$
        _SCSCFG-SPEC @ HRES-SPEC-TARGET! DUP _SCSCFG-STATUS !
    HRES-S-OK <> IF _SCSCFG-STATUS @ _SCSYN-HRES>STATUS EXIT THEN
    _SCSCFG-SOURCE @ _SCSCFG-SPEC @ HRES-SPEC-TARGET@
        _SCSCFG-C @ _SCSYN-AUTHORIZED? 0= IF
        SCONF-S-INVALID EXIT
    THEN
    S" application/feed+json, application/json, application/atom+xml, application/rss+xml"
        _SCSCFG-SPEC @ HRES-SPEC-ACCEPT! DUP _SCSCFG-STATUS !
    HRES-S-OK <> IF _SCSCFG-STATUS @ _SCSYN-HRES>STATUS EXIT THEN
    _SCSCFG-SOURCE @ SSOURCE.REDIRECT-MAX @
        _SCSCFG-SPEC @ HRES-SPEC-REDIRECT-MAX! DUP _SCSCFG-STATUS !
    HRES-S-OK <> IF _SCSCFG-STATUS @ _SCSYN-HRES>STATUS EXIT THEN
    _SCSCFG-C @ ['] _SCSYN-BIND ['] _SCSYN-LEASE-RELEASE
        _SCSCFG-SPEC @ HRES-SPEC-BINDING! DUP _SCSCFG-STATUS !
    HRES-S-OK <> IF _SCSCFG-STATUS @ _SCSYN-HRES>STATUS EXIT THEN
    _SCSCFG-C @ ['] _SCSYN-MEDIA-POLICY
        _SCSCFG-SPEC @ HRES-SPEC-MEDIA! DUP _SCSCFG-STATUS !
    HRES-S-OK <> IF _SCSCFG-STATUS @ _SCSYN-HRES>STATUS EXIT THEN
    _SCSCFG-SPEC @ HRES-SPEC-SEAL DUP _SCSCFG-STATUS !
    HRES-S-OK <> IF _SCSCFG-STATUS @ _SCSYN-HRES>STATUS EXIT THEN

    _SCSCFG-C @ _SCS.C.RESOURCE @ DUP _SCSCFG-R ! HRES-INIT
    _SCSCFG-SPEC @ _SCSCFG-C @ _SCS.C.BODY @
    _SCSCFG-SOURCE @ SSOURCE.RESPONSE-MAX @ _SCSCFG-R @ HRES-CONFIGURE
    DUP _SCSCFG-STATUS ! HRES-S-OK <> IF
        _SCSCFG-STATUS @ _SCSYN-HRES>STATUS EXIT
    THEN
    _SCSCFG-SOURCE @ _SCSCFG-C @ _SCS.C.SOURCE
        STREAMS-SOURCE-SIZE MOVE
    _SCSCFG-C @ _SCS.C.SOURCE STREAMS-SOURCE-VALID? 0= IF
        _SCSCFG-R @ HRES-DECONFIGURE DROP
        _SCSCFG-C @ _SCS.C.SOURCE STREAMS-SOURCE-SIZE 0 FILL
        SCONF-S-INVALID EXIT
    THEN
    _SCSCFG-SOURCE @ SSOURCE.FORMAT @
        _SCSCFG-C @ _SCS.C.SOURCE-FORMAT !
    0 _SCSCFG-C @ _SCS.C.MEDIA-FORMAT !
    SCONF-S-OK ;

