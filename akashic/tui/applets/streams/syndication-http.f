\ Authorization is fail-closed on an exception.  The callback is a trusted
\ composition edge, but its failure must not escape into provider state or
\ accidentally authorize a request.
: _SCSYN-AUTHORIZED?  ( source target context -- flag )
    _SCSA-C ! _SCSA-TARGET ! _SCSA-SOURCE !
    0 _SCSA-ALLOWED !
    ['] _SCSYN-AUTH-CALL-INNER CATCH IF 0 EXIT THEN
    _SCSA-ALLOWED @ ;

: _SCSYN-HRES>STATUS  ( hres-status -- configured-status )
    DUP HRES-S-OK = IF DROP SCONF-S-OK EXIT THEN
    DUP HRES-S-INVALID = IF DROP SCONF-S-INVALID EXIT THEN
    DUP HRES-S-BUSY = OVER HRES-S-STATE = OR IF
        DROP SCONF-S-BUSY EXIT
    THEN
    DROP SCONF-S-TRANSPORT ;

