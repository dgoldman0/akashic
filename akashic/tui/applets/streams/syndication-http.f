: _SCSYN-MEDIA-FORMAT  ( media -- source-format | 0 )
    DUP MTYPE-TYPE$ S" application" STR-STRI= 0= IF DROP 0 EXIT THEN
    DUP MTYPE-SUBTYPE$ S" feed+json" STR-STRI= IF
        DROP SSOURCE-FORMAT-JSON-FEED EXIT
    THEN
    \ JSON Feed publishers commonly retain the generic registered JSON media
    \ type.  It is admitted only as the JSON Feed decoder kind; the configured
    \ source format and the decoder's required version member still close the
    \ semantic boundary before any observation is accepted.
    DUP MTYPE-SUBTYPE$ S" json" STR-STRI= IF
        DROP SSOURCE-FORMAT-JSON-FEED EXIT
    THEN
    DUP MTYPE-SUBTYPE$ S" atom+xml" STR-STRI= IF
        DROP SSOURCE-FORMAT-ATOM EXIT
    THEN
    MTYPE-SUBTYPE$ S" rss+xml" STR-STRI= IF
        SSOURCE-FORMAT-RSS
    ELSE
        0
    THEN ;

: _SCSYN-UTF8-PARAMS?  ( media -- flag )
    DUP MTYPE-PARAM-COUNT@ DUP 0= IF 2DROP -1 EXIT THEN
    1 <> IF DROP 0 EXIT THEN
    0 SWAP MTYPE-PARAM-NTH 0= IF 2DROP 2DROP 0 EXIT THEN
    _SCSM-VU ! _SCSM-VA ! _SCSM-NU ! _SCSM-NA !
    _SCSM-NA @ _SCSM-NU @ S" charset" STR-STRI=
    _SCSM-VA @ _SCSM-VU @ S" utf-8" STR-STRI= AND ;

: _SCSYN-FORMAT-ALLOWED?  ( actual configured -- flag )
    DUP SSOURCE-FORMAT-AUTO = IF 2DROP -1 EXIT THEN = ;

: _SCSYN-MEDIA-POLICY  ( media context -- media-status )
    _SCSM-C ! _SCSM-MEDIA !
    _SCSM-C @ _SCSYN-CONTEXT-VALID? 0= IF
        SCSYN-MEDIA-S-TYPE EXIT
    THEN
    _SCSM-MEDIA @ MTYPE-VALID? 0= IF SCSYN-MEDIA-S-TYPE EXIT THEN
    _SCSM-MEDIA @ _SCSYN-MEDIA-FORMAT DUP _SCSM-FORMAT ! 0= IF
        SCSYN-MEDIA-S-TYPE EXIT
    THEN
    _SCSM-MEDIA @ _SCSYN-UTF8-PARAMS? 0= IF
        SCSYN-MEDIA-S-CHARSET EXIT
    THEN
    _SCSM-FORMAT @ _SCSM-C @ _SCS.C.SOURCE-FORMAT @
        _SCSYN-FORMAT-ALLOWED? 0= IF SCSYN-MEDIA-S-FORMAT EXIT THEN
    _SCSM-C @ _SCS.C.RESOURCE @ HRES.EXCHANGE HBUF.BODY-U @
    _SCSM-C @ _SCS.C.BODY @ SWAP UTF8-VALID? 0= IF
        SCSYN-MEDIA-S-UTF8 EXIT
    THEN
    _SCSM-FORMAT @ _SCSM-C @ _SCS.C.MEDIA-FORMAT !
    SCSYN-MEDIA-S-OK ;

