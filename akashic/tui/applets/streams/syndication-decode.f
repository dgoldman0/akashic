: _SYNDEC-DECODE-BODY  ( -- codec-status )
    _SYNDEC-EXPECTED @ CASE
        SSOURCE-FORMAT-RSS OF
            _SYNDEC-BODY-A @ _SYNDEC-BODY-U @
            _SYNDEC-DECODER @ _SYNDEC.D.MODEL @
            _SYNDEC-DECODER @ _SYNDEC.D.SCRATCH @
            RSS-FEED-SCRATCH-SIZE RSS-FEED-DECODE
        ENDOF
        SSOURCE-FORMAT-ATOM OF
            _SYNDEC-BODY-A @ _SYNDEC-BODY-U @
            _SYNDEC-DECODER @ _SYNDEC.D.MODEL @
            _SYNDEC-DECODER @ _SYNDEC.D.SCRATCH @
            ATOM-FEED-SCRATCH-SIZE ATOM-FEED-DECODE
        ENDOF
        SSOURCE-FORMAT-JSON-FEED OF
            _SYNDEC-BODY-A @ _SYNDEC-BODY-U @
            _SYNDEC-DECODER @ _SYNDEC.D.MODEL @
            _SYNDEC-DECODER @ _SYNDEC.D.SCRATCH @
            JSON-FEED-SCRATCH-SIZE JSON-FEED-DECODE
        ENDOF
        SYN-S-UNSUPPORTED SWAP
    ENDCASE ;

: _SYNDEC-MODEL-COUNT  ( -- count )
    _SYNDEC-EXPECTED @ CASE
        SSOURCE-FORMAT-RSS OF
            _SYNDEC-DECODER @ _SYNDEC.D.MODEL @ RSS.FEED.COUNT @
        ENDOF
        SSOURCE-FORMAT-ATOM OF
            _SYNDEC-DECODER @ _SYNDEC.D.MODEL @ ATOM.FEED.COUNT @
        ENDOF
        SSOURCE-FORMAT-JSON-FEED OF
            _SYNDEC-DECODER @ _SYNDEC.D.MODEL @ SYN.FEED.COUNT @
        ENDOF
        0 SWAP
    ENDCASE ;

: _SYNDEC-MODEL-ITEM  ( index -- item | 0 )
    _SYNDEC-EXPECTED @ CASE
        SSOURCE-FORMAT-RSS OF
            _SYNDEC-DECODER @ _SYNDEC.D.MODEL @ RSS.FEED.ITEM
        ENDOF
        SSOURCE-FORMAT-ATOM OF
            _SYNDEC-DECODER @ _SYNDEC.D.MODEL @ ATOM.FEED.ENTRY
        ENDOF
        SSOURCE-FORMAT-JSON-FEED OF
            _SYNDEC-DECODER @ _SYNDEC.D.MODEL @ SYN.FEED.ENTRY
        ENDOF
        DROP 0 SWAP
    ENDCASE ;

: _SYNDEC-PROJECT-ITEM  ( item -- status )
    _SYNDEC-ITEM !
    _SYNDEC-EXPECTED @ CASE
        SSOURCE-FORMAT-RSS OF
            _SYNDEC-ITEM @ _SYNDEC-DECODER @ _SYNDEC.D.PROJECTION @
                RSS-FEED-PROJECT-ITEM
        ENDOF
        SSOURCE-FORMAT-ATOM OF
            _SYNDEC-ITEM @ _SYNDEC-DECODER @ _SYNDEC.D.PROJECTION @
                ATOM-FEED-PROJECT-ITEM
        ENDOF
        SSOURCE-FORMAT-JSON-FEED OF
            _SYNDEC-ITEM @ _SYNDEC-DECODER @ _SYNDEC.D.PROJECTION @
                JSON-FEED-PROJECT-ITEM
        ENDOF
        SYN-S-UNSUPPORTED SWAP
    ENDCASE ;

: _SYNDEC-PAIR!  ( a u address-cell length-cell -- )
    >R ROT SWAP ! R> ! ;

: _SYNDEC-PROJECTION>CANDIDATE  ( -- )
    _SYNDEC-CANDIDATE @ OCHK-CANDIDATE-INIT
    _SYNDEC-DECODER @ _SYNDEC.D.PROJECTION @ DUP
        SYN.PROJECTION.FORMAT _SYNDEC-CANDIDATE @ OCC.FORMAT !
    OCHK-NATIVE-PROVIDER-ID _SYNDEC-CANDIDATE @ OCC.NATIVE-KIND !
    DUP SYN.PROJECTION.NATIVE-ID
        _SYNDEC-CANDIDATE @ OCC.NATIVE-A
        _SYNDEC-CANDIDATE @ OCC.NATIVE-U _SYNDEC-PAIR!
    DUP SYN.PROJECTION.TITLE
        _SYNDEC-CANDIDATE @ OCC.TITLE-A
        _SYNDEC-CANDIDATE @ OCC.TITLE-U _SYNDEC-PAIR!
    DUP SYN.PROJECTION.URL
        _SYNDEC-CANDIDATE @ OCC.URL-A
        _SYNDEC-CANDIDATE @ OCC.URL-U _SYNDEC-PAIR!
    DUP SYN.PROJECTION.SUMMARY
        _SYNDEC-CANDIDATE @ OCC.SUMMARY-A
        _SYNDEC-CANDIDATE @ OCC.SUMMARY-U _SYNDEC-PAIR!
    DUP SYN.PROJECTION.CONTENT
        _SYNDEC-CANDIDATE @ OCC.CONTENT-A
        _SYNDEC-CANDIDATE @ OCC.CONTENT-U _SYNDEC-PAIR!
    DUP SYN.PROJECTION.PUBLISHED
        _SYNDEC-CANDIDATE @ OCC.PUBLISHED-A
        _SYNDEC-CANDIDATE @ OCC.PUBLISHED-U _SYNDEC-PAIR!
    SYN.PROJECTION.MODIFIED
        _SYNDEC-CANDIDATE @ OCC.MODIFIED-A
        _SYNDEC-CANDIDATE @ OCC.MODIFIED-U _SYNDEC-PAIR! ;

: _SYNDEC-JSON-AUTHOR$  ( item -- a u )
    DUP SYN.ENTRY.AUTHOR-COUNT @ IF
        0 SWAP SYN.ENTRY.AUTHOR
    ELSE
        DROP 0 _SYNDEC-DECODER @ _SYNDEC.D.MODEL @ SYN.FEED.AUTHOR
    THEN
    ?DUP IF SYN.AUTHOR.NAME ELSE 0 0 THEN ;

: _SYNDEC-ATOM-AUTHOR$  ( entry -- a u )
    DUP ATOM.ENTRY.PERSON-COUNT @ IF
        0 SWAP ATOM.ENTRY.PERSON
    ELSE
        DROP 0 _SYNDEC-DECODER @ _SYNDEC.D.MODEL @ ATOM.FEED.PERSON
    THEN
    ?DUP IF ATOM.PERSON.NAME ELSE 0 0 THEN ;

: _SYNDEC-ITEM-AUTHOR$  ( item -- a u )
    _SYNDEC-EXPECTED @ CASE
        SSOURCE-FORMAT-RSS OF RSS.ITEM.AUTHOR ENDOF
        SSOURCE-FORMAT-ATOM OF _SYNDEC-ATOM-AUTHOR$ ENDOF
        SSOURCE-FORMAT-JSON-FEED OF _SYNDEC-JSON-AUTHOR$ ENDOF
        DROP 0 0 ROT
    ENDCASE ;

: _SYNDEC-BUILD-CANDIDATES  ( -- status )
    _SYNDEC-MODEL-COUNT DUP _SYNDEC-COUNT !
    DUP 0< SWAP STREAMS-SYNDICATION-CANDIDATE-MAX > OR IF
        SYNDEC-S-CAPACITY EXIT
    THEN
    _SYNDEC-DECODER @ _SYNDEC.D.CANDIDATES @
        _SYNDEC-CANDIDATES-SIZE 0 FILL
    _SYNDEC-DECODER @ _SYNDEC.D.AUTHORS @ _SYNDEC-AUTHORS-SIZE 0 FILL
    0 _SYNDEC-I !
    BEGIN _SYNDEC-I @ _SYNDEC-COUNT @ < WHILE
        _SYNDEC-I @ _SYNDEC-MODEL-ITEM DUP _SYNDEC-ITEM ! 0= IF
            SYNDEC-S-INVALID EXIT
        THEN
        _SYNDEC-ITEM @ _SYNDEC-PROJECT-ITEM ?DUP IF EXIT THEN
        _SYNDEC-DECODER @ _SYNDEC.D.PROJECTION @ SYN.PROJECTION.FORMAT
        _SYNDEC-EXPECTED @ _SYNDEC-FORMAT>SYN <> IF
            SYNDEC-S-FORMAT EXIT
        THEN
        _SYNDEC-I @ _SYNDEC-DECODER @ _SYNDEC-CANDIDATE@
            _SYNDEC-CANDIDATE !
        _SYNDEC-PROJECTION>CANDIDATE
        _SYNDEC-I @ _SYNDEC-DECODER @ _SYNDEC-AUTHOR@
            _SYNDEC-AUTHOR-VIEW !
        _SYNDEC-ITEM @ _SYNDEC-ITEM-AUTHOR$
            _SYNDEC-AUTHOR-VIEW @ _SYNDEC-AUTHOR-A +
            _SYNDEC-AUTHOR-VIEW @ _SYNDEC-AUTHOR-U + _SYNDEC-PAIR!
        1 _SYNDEC-I +!
    REPEAT
    _SYNDEC-COUNT @ _SYNDEC-DECODER @ _SYNDEC.D.COUNT !
    _SYNDEC-EXPECTED @ _SYNDEC-DECODER @ _SYNDEC.D.FORMAT !
    _SYNDEC-STATE-DECODED _SYNDEC-DECODER @ _SYNDEC.D.STATE !
    SYNDEC-S-OK ;

: STREAMS-SYNDICATION-DECODE
    ( source-format provider decoder -- status )
    _SYNDEC-DECODER ! _SYNDEC-PROVIDER ! _SYNDEC-EXPECTED !
    _SYNDEC-DECODER @ STREAMS-SYNDICATION-DECODER-VALID? 0= IF
        SYNDEC-S-INVALID EXIT
    THEN
    _SYNDEC-PROVIDER @ SCONF-VALID? 0= IF SYNDEC-S-INVALID EXIT THEN
    _SYNDEC-EXPECTED @ DUP SSOURCE-FORMAT-AUTO =
        SWAP _SYNDEC-EXPLICIT-FORMAT? OR 0= IF
        SYNDEC-S-FORMAT EXIT
    THEN
    _SYNDEC-PROVIDER @ SCONF-RESULT-VALID? 0= IF SYNDEC-S-RESULT EXIT THEN
    _SYNDEC-PROVIDER @ SCONF-MEDIA-KIND DUP _SYNDEC-MEDIA !
        _SYNDEC-EXPLICIT-FORMAT? 0= IF SYNDEC-S-MEDIA EXIT THEN
    _SYNDEC-EXPECTED @ SSOURCE-FORMAT-AUTO = IF
        _SYNDEC-MEDIA @ _SYNDEC-EXPECTED !
    ELSE
        _SYNDEC-MEDIA @ _SYNDEC-EXPECTED @ <> IF SYNDEC-S-MEDIA EXIT THEN
    THEN
    _SYNDEC-PROVIDER @ SCONF-BODY$
        _SYNDEC-BODY-U ! _SYNDEC-BODY-A !
    _SYNDEC-BODY-A @ _SYNDEC-BODY-U @ SYN-SPAN-VALID? 0= IF
        SYNDEC-S-INVALID EXIT
    THEN
    _SYNDEC-BODY-U @ 0= IF SYNDEC-S-INVALID EXIT THEN

    _SYNDEC-DECODE-BODY DUP _SYNDEC-STATUS !
        _SYNDEC-DECODER @ _SYNDEC.D.CODEC-STATUS !
    _SYNDEC-STATUS @ SYN-S-OK <> IF _SYNDEC-STATUS @ EXIT THEN
    _SYNDEC-BUILD-CANDIDATES DUP IF
        _SYNDEC-DECODER @ _SYNDEC-RESULT-INVALIDATE
    THEN ;
