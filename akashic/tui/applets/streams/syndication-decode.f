: _SYNDEC-EXPLICIT-FORMAT?  ( source-format -- flag )
    DUP SSOURCE-FORMAT-RSS =
    OVER SSOURCE-FORMAT-ATOM = OR
    SWAP SSOURCE-FORMAT-JSON-FEED = OR ;

: _SYNDEC-FORMAT>SYN  ( source-format -- syndication-format | 0 )
    CASE
        SSOURCE-FORMAT-RSS OF SYN-FORMAT-RSS-2 ENDOF
        SSOURCE-FORMAT-ATOM OF SYN-FORMAT-ATOM-1 ENDOF
        SSOURCE-FORMAT-JSON-FEED OF SYN-FORMAT-JSON-FEED ENDOF
        0 SWAP
    ENDCASE ;

: STREAMS-SYNDICATION-DECODER-VALID?  ( decoder -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP _SYNDEC.D.MAGIC @ _SYNDEC-MAGIC <> IF DROP 0 EXIT THEN
    DUP _SYNDEC.D.ABI @ _SYNDEC-ABI <> IF DROP 0 EXIT THEN
    DUP _SYNDEC.D.MODEL @ 0= IF DROP 0 EXIT THEN
    DUP _SYNDEC.D.SCRATCH @ 0= IF DROP 0 EXIT THEN
    DUP _SYNDEC.D.CANDIDATES @ 0= IF DROP 0 EXIT THEN
    DUP _SYNDEC.D.AUTHORS @ 0= IF DROP 0 EXIT THEN
    DUP _SYNDEC.D.PROJECTION @ 0= IF DROP 0 EXIT THEN
    DUP _SYNDEC.D.STATE @ _SYNDEC-STATE-EMPTY = IF
        DUP _SYNDEC.D.FORMAT @ 0=
        SWAP _SYNDEC.D.COUNT @ 0= AND EXIT
    THEN
    DUP _SYNDEC.D.STATE @ _SYNDEC-STATE-DECODED <> IF DROP 0 EXIT THEN
    DUP _SYNDEC.D.FORMAT @ _SYNDEC-EXPLICIT-FORMAT? 0= IF
        DROP 0 EXIT
    THEN
    _SYNDEC.D.COUNT @ DUP 0< 0=
    SWAP STREAMS-SYNDICATION-CANDIDATE-MAX <= AND ;

: STREAMS-SYNDICATION-RESULT-VALID?  ( decoder -- flag )
    DUP STREAMS-SYNDICATION-DECODER-VALID? 0= IF DROP 0 EXIT THEN
    _SYNDEC.D.STATE @ _SYNDEC-STATE-DECODED = ;

: STREAMS-SYNDICATION-FORMAT@  ( decoder -- source-format | 0 )
    DUP STREAMS-SYNDICATION-RESULT-VALID? 0= IF DROP 0 EXIT THEN
    _SYNDEC.D.FORMAT @ ;

: STREAMS-SYNDICATION-CODEC-STATUS@  ( decoder -- codec-status )
    DUP STREAMS-SYNDICATION-DECODER-VALID? 0= IF
        DROP SYN-S-INVALID EXIT
    THEN
    _SYNDEC.D.CODEC-STATUS @ ;

: _SYNDEC-RESULT-INVALIDATE  ( decoder -- )
    DUP _SYNDEC.D.MODEL @ _SYNDEC-MODEL-SIZE 0 FILL
    DUP _SYNDEC.D.CANDIDATES @ _SYNDEC-CANDIDATES-SIZE 0 FILL
    DUP _SYNDEC.D.AUTHORS @ _SYNDEC-AUTHORS-SIZE 0 FILL
    DUP _SYNDEC.D.PROJECTION @ SYN-PROJECTION-SIZE 0 FILL
    0 OVER _SYNDEC.D.FORMAT !
    0 OVER _SYNDEC.D.COUNT !
    _SYNDEC-STATE-EMPTY SWAP _SYNDEC.D.STATE ! ;

: _SYNDEC-ALL-WIPE  ( decoder -- )
    DUP _SYNDEC-RESULT-INVALIDATE
    DUP _SYNDEC.D.SCRATCH @ _SYNDEC-MODEL-SIZE 0 FILL
    SYN-S-OK SWAP _SYNDEC.D.CODEC-STATUS ! ;

: STREAMS-SYNDICATION-DECODER-RESET  ( decoder -- status )
    DUP STREAMS-SYNDICATION-DECODER-VALID? 0= IF
        DROP SYNDEC-S-INVALID EXIT
    THEN
    _SYNDEC-ALL-WIPE SYNDEC-S-OK ;

VARIABLE _SYNDEC-NEW-D

: _SYNDEC-ALLOC  ( size -- address | 0 )
    ALLOCATE DUP IF 2DROP 0 ELSE DROP THEN ;

: _SYNDEC-PARTIAL-FREE  ( -- )
    _SYNDEC-NEW-D @ ?DUP 0= IF EXIT THEN
    DUP _SYNDEC.D.PROJECTION @ ?DUP IF
        DUP SYN-PROJECTION-SIZE 0 FILL FREE
    THEN
    DUP _SYNDEC.D.AUTHORS @ ?DUP IF
        DUP _SYNDEC-AUTHORS-SIZE 0 FILL FREE
    THEN
    DUP _SYNDEC.D.CANDIDATES @ ?DUP IF
        DUP _SYNDEC-CANDIDATES-SIZE 0 FILL FREE
    THEN
    DUP _SYNDEC.D.SCRATCH @ ?DUP IF
        DUP _SYNDEC-MODEL-SIZE 0 FILL FREE
    THEN
    DUP _SYNDEC.D.MODEL @ ?DUP IF
        DUP _SYNDEC-MODEL-SIZE 0 FILL FREE
    THEN
    DUP _SYNDEC-DESCRIPTOR-SIZE 0 FILL FREE
    0 _SYNDEC-NEW-D ! ;

: STREAMS-SYNDICATION-DECODER-NEW  ( -- decoder status )
    0 _SYNDEC-NEW-D !
    _SYNDEC-DESCRIPTOR-SIZE _SYNDEC-ALLOC DUP 0= IF
        SYNDEC-S-CAPACITY EXIT
    THEN
    DUP _SYNDEC-NEW-D ! _SYNDEC-DESCRIPTOR-SIZE 0 FILL

    _SYNDEC-MODEL-SIZE _SYNDEC-ALLOC DUP 0= IF
        _SYNDEC-PARTIAL-FREE SYNDEC-S-CAPACITY EXIT
    THEN
    DUP _SYNDEC-MODEL-SIZE 0 FILL _SYNDEC-NEW-D @ _SYNDEC.D.MODEL !
    _SYNDEC-MODEL-SIZE _SYNDEC-ALLOC DUP 0= IF
        _SYNDEC-PARTIAL-FREE SYNDEC-S-CAPACITY EXIT
    THEN
    DUP _SYNDEC-MODEL-SIZE 0 FILL _SYNDEC-NEW-D @ _SYNDEC.D.SCRATCH !
    _SYNDEC-CANDIDATES-SIZE _SYNDEC-ALLOC DUP 0= IF
        _SYNDEC-PARTIAL-FREE SYNDEC-S-CAPACITY EXIT
    THEN
    DUP _SYNDEC-CANDIDATES-SIZE 0 FILL
        _SYNDEC-NEW-D @ _SYNDEC.D.CANDIDATES !
    _SYNDEC-AUTHORS-SIZE _SYNDEC-ALLOC DUP 0= IF
        _SYNDEC-PARTIAL-FREE SYNDEC-S-CAPACITY EXIT
    THEN
    DUP _SYNDEC-AUTHORS-SIZE 0 FILL _SYNDEC-NEW-D @ _SYNDEC.D.AUTHORS !
    SYN-PROJECTION-SIZE _SYNDEC-ALLOC DUP 0= IF
        _SYNDEC-PARTIAL-FREE SYNDEC-S-CAPACITY EXIT
    THEN
    DUP SYN-PROJECTION-SIZE 0 FILL
        _SYNDEC-NEW-D @ _SYNDEC.D.PROJECTION !

    _SYNDEC-ABI _SYNDEC-NEW-D @ _SYNDEC.D.ABI !
    _SYNDEC-STATE-EMPTY _SYNDEC-NEW-D @ _SYNDEC.D.STATE !
    SYN-S-OK _SYNDEC-NEW-D @ _SYNDEC.D.CODEC-STATUS !
    _SYNDEC-MAGIC _SYNDEC-NEW-D @ _SYNDEC.D.MAGIC !
    _SYNDEC-NEW-D @ STREAMS-SYNDICATION-DECODER-VALID? 0= IF
        _SYNDEC-PARTIAL-FREE 0 SYNDEC-S-INVALID EXIT
    THEN
    _SYNDEC-NEW-D @ SYNDEC-S-OK ;

VARIABLE _SYNDEC-FREE-D

: STREAMS-SYNDICATION-DECODER-FREE  ( decoder -- status )
    DUP STREAMS-SYNDICATION-DECODER-VALID? 0= IF
        DROP SYNDEC-S-INVALID EXIT
    THEN
    DUP _SYNDEC-FREE-D ! _SYNDEC-ALL-WIPE
    _SYNDEC-FREE-D @ _SYNDEC.D.PROJECTION @
        DUP SYN-PROJECTION-SIZE 0 FILL FREE
    _SYNDEC-FREE-D @ _SYNDEC.D.AUTHORS @
        DUP _SYNDEC-AUTHORS-SIZE 0 FILL FREE
    _SYNDEC-FREE-D @ _SYNDEC.D.CANDIDATES @
        DUP _SYNDEC-CANDIDATES-SIZE 0 FILL FREE
    _SYNDEC-FREE-D @ _SYNDEC.D.SCRATCH @
        DUP _SYNDEC-MODEL-SIZE 0 FILL FREE
    _SYNDEC-FREE-D @ _SYNDEC.D.MODEL @
        DUP _SYNDEC-MODEL-SIZE 0 FILL FREE
    _SYNDEC-FREE-D @ DUP _SYNDEC-DESCRIPTOR-SIZE 0 FILL FREE
    SYNDEC-S-OK ;

: _SYNDEC-CANDIDATE@  ( index decoder -- candidate )
    _SYNDEC.D.CANDIDATES @ SWAP OCHK-CANDIDATE-SIZE * + ;

: _SYNDEC-AUTHOR@  ( index decoder -- author-view )
    _SYNDEC.D.AUTHORS @ SWAP _SYNDEC-AUTHOR-SIZE * + ;

: STREAMS-SYNDICATION-CANDIDATES  ( decoder -- candidates count )
    DUP STREAMS-SYNDICATION-RESULT-VALID? 0= IF DROP 0 0 EXIT THEN
    DUP _SYNDEC.D.CANDIDATES @ SWAP _SYNDEC.D.COUNT @ ;

: STREAMS-SYNDICATION-CANDIDATE  ( index decoder -- candidate | 0 )
    >R
    R@ STREAMS-SYNDICATION-RESULT-VALID? 0= IF
        DROP R> DROP 0 EXIT
    THEN
    DUP 0< OVER R@ _SYNDEC.D.COUNT @ >= OR IF
        DROP R> DROP 0 EXIT
    THEN
    R> _SYNDEC-CANDIDATE@ ;

: STREAMS-SYNDICATION-AUTHOR$  ( index decoder -- a u )
    >R
    R@ STREAMS-SYNDICATION-RESULT-VALID? 0= IF
        DROP R> DROP 0 0 EXIT
    THEN
    DUP 0< OVER R@ _SYNDEC.D.COUNT @ >= OR IF
        DROP R> DROP 0 0 EXIT
    THEN
    R> _SYNDEC-AUTHOR@ DUP _SYNDEC-AUTHOR-A + @
    SWAP _SYNDEC-AUTHOR-U + @ ;

