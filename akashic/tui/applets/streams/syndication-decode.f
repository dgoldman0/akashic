\ =====================================================================
\ syndication-decode.f - retained syndication result to OCHK candidates
\ =====================================================================
\ This owner-side adapter decodes one admitted configured-provider body as
\ RSS 2.0, Atom 1.0, or JSON Feed.  An AUTO source selects only the provider's
\ already-admitted explicit media kind; this layer never sniffs body bytes.
\ An explicit source format must match that admitted media kind exactly.  Its
\ sealed descriptor owns the typed model, codec scratch, shared projection,
\ and bounded candidate array.  Candidate strings therefore remain valid
\ after the provider body is wiped and through a synchronous OCHK-APPLY call.
\ They remain borrowed from the decoder and are invalidated by RESET, FREE, or
\ the next successful DECODE.
\ DECODE is an owner-context operation and, like the underlying codecs, is
\ non-reentrant; callers serialize decoder use on the owning applet thread.
\
\ OCHK's fixed candidate ABI has no author slot.  AUTHOR$ exposes the exact
\ first applicable author name in a parallel borrowed view: entry/item first,
\ then the format's feed author where that format defines inheritance.  RSS
\ has no feed-author fallback.  An author object without a name is represented
\ by an empty name rather than a synthesized URL or address.
\
\ Public ABI:
\   STREAMS-SYNDICATION-DECODER-NEW       ( -- decoder status )
\   STREAMS-SYNDICATION-DECODER-FREE      ( decoder -- status )
\   STREAMS-SYNDICATION-DECODER-VALID?    ( decoder -- flag )
\   STREAMS-SYNDICATION-DECODER-RESET     ( decoder -- status )
\   STREAMS-SYNDICATION-DECODE
\     ( source-format provider decoder -- status )
\   STREAMS-SYNDICATION-RESULT-VALID?     ( decoder -- flag )
\   STREAMS-SYNDICATION-FORMAT@           ( decoder -- source-format | 0 )
\   STREAMS-SYNDICATION-CODEC-STATUS@     ( decoder -- codec-status )
\   STREAMS-SYNDICATION-CANDIDATES        ( decoder -- candidates count )
\   STREAMS-SYNDICATION-CANDIDATE         ( index decoder -- candidate | 0 )
\   STREAMS-SYNDICATION-AUTHOR$           ( index decoder -- a u )
\
\ Codec failures return the exact SYN-S-* value.  A failed codec attempt is
\ transactional and leaves the preceding successful result available; a
\ caller must nevertheless apply only after a fresh SYNDEC-S-OK return.
\ =====================================================================

\ Keep the module identity within KDOS's 23-byte bound.
