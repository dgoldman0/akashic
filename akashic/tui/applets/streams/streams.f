: _STM-DRAFT-PERSIST  ( addr len revision -- flag )
    _STM-DRAFT-SAVE-REV ! _STM-DRAFT-SAVE-U ! _STM-DRAFT-SAVE-A !
    _STM-DRAFT-PERSIST-MODE @ _STM-DP-VOLATILE = IF -1 EXIT THEN
    _STM-DRAFT-PERSIST-MODE @ _STM-DP-READY <> IF
        _STM-DRAFT-PERSIST-MODE @ _STM-DP-BLOCKED <> IF
            SDSTORE-S-INVALID _STM-DRAFT-BLOCK!
        THEN
        0 EXIT
    THEN
    SDSTORE-S-IO _STM-DRAFT-STORE-STATUS !
    ['] _STM-DRAFT-SAVE-CALL CATCH ?DUP IF
        DROP SDSTORE-S-IO _STM-DRAFT-STORE-STATUS !
    THEN
    _STM-DRAFT-STORE-STATUS @ IF
        _STM-DP-BLOCKED _STM-DRAFT-PERSIST-MODE ! 0 EXIT
    THEN
    -1 ;

