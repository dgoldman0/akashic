    _STM-SOC-INSTANCE @ _STM-ACTIVATE
    _STM-SOC-CANDIDATE @ STREAMS-SOURCE-SIZE _STM-SOC-INSTANCE @
        _STM-SOURCE-OWNER-OUTPUT-SAFE? 0= IF
        STREAMS-SOURCE-S-INVALID EXIT
    THEN
