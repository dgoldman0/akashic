\ Bind and recover the singleton draft before enabling any mutation.  The
\ helper is intentionally non-fatal: an uncertain or unreadable store leaves
\ Streams usable for reading while draft overwrites remain blocked.
: _STM-DRAFT-STORAGE-INIT  ( -- status )
    _STM-DRAFT-MEMORY-CLEAR
    SDSTORE-S-INVALID _STM-DRAFT-BLOCK!
    ['] _STM-DRAFT-STORAGE-INIT-BODY CATCH ?DUP IF
        DROP _STM-DRAFT-MEMORY-CLEAR
        SDSTORE-S-IO _STM-DRAFT-BLOCK!
    THEN
    _STM-DRAFT-STORE-STATUS @ ;

\ ---------------------------------------------------------------------
\ Durable source-registry owner seam
\ ---------------------------------------------------------------------
\ The live registry is never mutated in place.  Every owner mutation copies
\ it to the instance-local candidate, applies one checked domain operation,
\ persists expected generation N -> N+1, and publishes that candidate to
\ live state only after the replacement store commits.
\ The physical target is singleton per VFS.  If two live instances share one
\ VFS, the later writer receives CONFLICT and retains its old live snapshot;
\ this seam does not silently reload or merge configuration after conflict.

: _STM-SOURCE-CANDIDATES-WIPE  ( -- )
    _STM-SOURCE-CANDIDATE-REGISTRY STREAMS-SOURCE-REGISTRY-SIZE 0 FILL
    _STM-SOURCE-CANDIDATE STREAMS-SOURCE-SIZE 0 FILL
    _STM-SOURCE-RREF RREF-SIZE 0 FILL ;

: _STM-SOURCE-BLOCK!  ( store-status -- )
    _STM-SOURCE-STORE-STATUS !
    _STM-SP-BLOCKED _STM-SOURCE-PERSIST-MODE ! ;

VARIABLE _STM-SSI-STATUS

: _STM-SOURCE-STORAGE-INIT-BODY  ( -- )
    VFS-CUR DUP 0= IF
        DROP SSSTORE-S-INVALID _STM-SOURCE-BLOCK! EXIT
    THEN
    _STM-SOURCE-STORE STREAMS-SOURCE-STORE-INIT
        DUP _STM-SSI-STATUS !
    SSSTORE-S-OK <> IF
        _STM-SSI-STATUS @ _STM-SOURCE-BLOCK! EXIT
    THEN
    _STM-SOURCE-REGISTRY STREAMS-SOURCE-REGISTRY-SIZE
        _STM-SOURCE-STORE STREAMS-SOURCE-STORE-LOAD
        DUP _STM-SSI-STATUS !
    SSSTORE-S-OK = IF
        _STM-SOURCE-REGISTRY STREAMS-SOURCE-REGISTRY-VALID? 0= IF
            SSSTORE-S-CORRUPT _STM-SOURCE-BLOCK! EXIT
        THEN
        SSSTORE-S-OK _STM-SOURCE-STORE-STATUS !
        _STM-SP-READY _STM-SOURCE-PERSIST-MODE ! EXIT
    THEN
    _STM-SSI-STATUS @ SSSTORE-S-ABSENT = IF
        \ Only a proven-absent store authorizes creation of a fresh
        \ generation-zero registry.  Every uncertain state remains invalid.
        _STM-SOURCE-REGISTRY STREAMS-SOURCE-REGISTRY-INIT
        SSSTORE-S-ABSENT _STM-SOURCE-STORE-STATUS !
        _STM-SP-READY _STM-SOURCE-PERSIST-MODE ! EXIT
    THEN
    _STM-SSI-STATUS @ _STM-SOURCE-BLOCK! ;

: _STM-SOURCE-STORAGE-INIT  ( -- status )
    _STM-SOURCE-REGISTRY STREAMS-SOURCE-REGISTRY-SIZE 0 FILL
    _STM-SOURCE-STORE STREAMS-SOURCE-STORE-SIZE 0 FILL
    _STM-SOURCE-CANDIDATES-WIPE
    SSSTORE-S-INVALID _STM-SOURCE-BLOCK!
    ['] _STM-SOURCE-STORAGE-INIT-BODY CATCH ?DUP IF
        DROP
        \ LOAD is transactional, so an exception cannot justify publishing
        \ either the scratch destination or a manufactured empty registry.
        _STM-SOURCE-REGISTRY STREAMS-SOURCE-REGISTRY-SIZE 0 FILL
        _STM-SOURCE-CANDIDATES-WIPE
        SSSTORE-S-IO _STM-SOURCE-BLOCK!
    THEN
    _STM-SOURCE-STORE-STATUS @ ;

: _STM-SOURCE-READY?  ( -- flag )
