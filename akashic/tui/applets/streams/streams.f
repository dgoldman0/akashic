    _STM-SOURCE-CANDIDATE SSOURCE.ID RID-CLEAR
    _STM-SOURCE-GENERATE-RID 0= IF
        _STM-SOURCE-CANDIDATES-WIPE STREAMS-SOURCE-S-CAPACITY EXIT
