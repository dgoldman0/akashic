\ Bind and recover the singleton draft before enabling any mutation.  The
\ helper is intentionally non-fatal: an uncertain or unreadable store leaves
\ Streams usable for reading while draft overwrites remain blocked.
: _STM-DRAFT-STORAGE-INIT  ( -- status )
    _STM-DRAFT-MEMORY-CLEAR
    SDSTORE-S-INVALID _STM-DRAFT-BLOCK!
    ['] _STM-DRAFT-STORAGE-INIT-BODY CATCH ?DUP IF
        DROP _STM-DRAFT-MEMORY-CLEAR
        SDSTORE-S-IO _STM-DRAFT-BLOCK!
    THEN
    _STM-DRAFT-STORE-STATUS @ ;

: _STM-DRAFT-BLOCKED-TEXT  ( -- addr len )
    _STM-DRAFT-STORE-STATUS @ SDSTORE-S-CORRUPT = IF
        S" Draft storage corrupt; editing disabled" EXIT
    THEN
    _STM-DRAFT-STORE-STATUS @ SDSTORE-S-UNSUPPORTED = IF
        S" Draft format unsupported; editing disabled" EXIT
    THEN
    _STM-DRAFT-STORE-STATUS @ SDSTORE-S-RECOVERY = IF
        S" Draft recovery uncertain; editing disabled" EXIT
    THEN
    _STM-DRAFT-STORE-STATUS @ SDSTORE-S-BUSY = IF
        S" Draft storage busy; editing disabled" EXIT
    THEN
    S" Draft storage unavailable; editing disabled" ;

