    _STM-LIVE-AVAILABLE? 0= IF CBUS-S-NOT-FOUND EXIT THEN
    _STM-REFRESH-U @ 0=
    _STM-REFRESH-U @ 0> _STM-REFRESH-A @ 0= AND OR IF
        CBUS-S-INVALID EXIT
    THEN
    _STM-XIO-OP XIO-OP-VALID? 0= IF CBUS-S-FAILED EXIT THEN
    _STM-XIO-OP XIOO.STATE @ XIO-STATE-RESET <> IF
        CBUS-S-BUSY EXIT
    THEN
    _STM-XIO-SERVICE @ XIO-ACTIVE?
    _STM-XIO-SERVICE @ XIOS.RETAINED @ 0<> OR IF
        _STM-LIVE-BUSY XIO-S-BUSY _STM-LIVE-MARK CBUS-S-BUSY EXIT
    THEN
    _STM-REQUEST-GENERATION @ CV-CELL-MAX >= IF
        _STM-LIVE-FAILED XIO-S-STATE _STM-LIVE-MARK CBUS-S-FAILED EXIT
    THEN
    _STM-REQUEST-GENERATION @ 1+ _STM-REFRESH-NEXT-GENERATION !
    _STM-XIO-SERVICE @
    _STM-CURRENT-INSTANCE @ CINST.ID @
    _STM-CURRENT-INSTANCE @ CINST.GENERATION @
    _STM-REFRESH-NEXT-GENERATION @
    MS@ _STM-REFRESH-TIMEOUT-MS +
    _STM-PUBLIC-PROVIDER @
    ['] SPUB-XIO-START ['] SPUB-XIO-POLL
    ['] SPUB-XIO-CANCEL ['] SPUB-XIO-WIPE
    _STM-XIO-OP XIO-OP-CONFIGURE
    DUP _STM-LIVE-CALL-STATUS ! XIO-S-OK <> IF
        _STM-XIO-OP XIO-OP-INIT
        _STM-LIVE-FAILED _STM-LIVE-CALL-STATUS @ _STM-LIVE-MARK
        CBUS-S-FAILED EXIT
    THEN
    _STM-REFRESH-A @ _STM-REFRESH-U @
        _STM-PUBLIC-PROVIDER @ SPUB-CONFIGURE
    DUP _STM-LIVE-CALL-STATUS ! SPUB-S-OK <> IF
        _STM-REFRESH-ROLLBACK SPUB-S-OK <> IF
            _STM-REFRESH-POISON
            _STM-LIVE-FAILED _STM-E-CLEANUP _STM-LIVE-MARK
            CBUS-S-FAILED EXIT
        THEN
        _STM-REFRESH-SELECT-ACTOR @ 0= IF
            _STM-LIVE-FAILED _STM-LIVE-CALL-STATUS @ _STM-LIVE-MARK
        THEN
        _STM-LIVE-CALL-STATUS @ SPUB-S-BUSY = IF
            CBUS-S-BUSY
        ELSE
            CBUS-S-INVALID
        THEN EXIT
    THEN
    _STM-XIO-SERVICE @ _STM-XIO-OP XIO-SUBMIT
    DUP _STM-LIVE-CALL-STATUS ! XIO-S-OK <> IF
        _STM-REFRESH-ROLLBACK SPUB-S-OK <> IF
            _STM-REFRESH-POISON
            _STM-LIVE-FAILED _STM-E-CLEANUP _STM-LIVE-MARK
            CBUS-S-FAILED EXIT
        THEN
        _STM-LIVE-CALL-STATUS @ XIO-S-BUSY = IF
            _STM-LIVE-BUSY _STM-LIVE-CALL-STATUS @ _STM-LIVE-MARK
            CBUS-S-BUSY
        ELSE
            _STM-LIVE-FAILED _STM-LIVE-CALL-STATUS @ _STM-LIVE-MARK
            CBUS-S-FAILED
        THEN EXIT
    THEN
    \ The capability's proposed actor becomes selected only after Desk has
    \ accepted the operation; failed configure/submit paths preserve it.
    _STM-REFRESH-SELECT-ACTOR @ IF
        _STM-REFRESH-A @ _STM-ACTOR-BUF _STM-REFRESH-U @ CMOVE
        _STM-ACTOR-BUF _STM-REFRESH-U @ +
            STREAMS-PUBLIC-ACTOR-CAPACITY _STM-REFRESH-U @ - 0 FILL
        _STM-REFRESH-U @ _STM-ACTOR-U !
    THEN
    _STM-REFRESH-NEXT-GENERATION @ _STM-REQUEST-GENERATION !
    _STM-LIVE-ACTIVE 0 _STM-LIVE-MARK CBUS-S-OK ;

