: _STM-CAP-SOURCE-SET-ENABLED-H  ( request instance -- status )
    _STM-ACTIVATE DUP _STM-CAP-REQ ! CBR.ARGS _STM-CAP-ARGS !
    _STM-CAP-SOURCE-MUTATION-CLEAN
    _STM-SOURCE-READY? 0= IF
        CBUS-S-FAILED _STM-CAP-SOURCE-FAIL EXIT
    THEN
    _STM-CAP-ARGS @ DUP CV-TYPE@ CV-T-MAP <> IF
        DROP CBUS-S-INVALID _STM-CAP-SOURCE-FAIL EXIT
    THEN DROP
    S" resource" _STM-CAP-ARGS @ CV-MAP-FIND DUP 0= IF
        DROP CBUS-S-INVALID _STM-CAP-SOURCE-FAIL EXIT
    THEN
    _STM-SOURCE-RREF IRES-RREF@ IRES-S-OK <> IF
        CBUS-S-INVALID _STM-CAP-SOURCE-FAIL EXIT
    THEN
    _STM-SOURCE-RREF RREF.REVISION @ 0> 0= IF
        CBUS-S-INVALID _STM-CAP-SOURCE-FAIL EXIT
    THEN
    _STM-SOURCE-RREF RREF.ID _STM-SOURCE-REGISTRY STREAMS-SOURCE-FIND
        DUP 0= IF
        DROP CBUS-S-NOT-FOUND _STM-CAP-SOURCE-FAIL EXIT
    THEN
    _STM-CAP-SOURCE !
    _STM-CAP-SOURCE @ SSOURCE.REVISION @
        _STM-SOURCE-RREF RREF.REVISION @ <> IF
        CBUS-S-STALE-REVISION _STM-CAP-SOURCE-FAIL EXIT
    THEN
    S" enabled" _STM-CAP-ARGS @ CV-MAP-FIND DUP 0= IF
        DROP CBUS-S-INVALID _STM-CAP-SOURCE-FAIL EXIT
    THEN
    DUP CV-TYPE@ CV-T-BOOL <> IF
        DROP CBUS-S-INVALID _STM-CAP-SOURCE-FAIL EXIT
    THEN
    CV-DATA@ 0<> _STM-CAP-SOURCE-ENABLED !
    \ A successful command must represent one semantic source change.  The
    \ direct owner ENABLE remains idempotent, but the typed command rejects a
    \ redundant request before building or persisting a candidate so the bus
    \ never advances the component revision for a no-op.
    _STM-CAP-SOURCE @ SSOURCE.FLAGS @ SSOURCE-F-ENABLED AND 0<>
        _STM-CAP-SOURCE-ENABLED @ = IF
        CBUS-S-INVALID _STM-CAP-SOURCE-FAIL EXIT
    THEN
    _STM-SOURCE-BEGIN-CANDIDATE DUP IF
        DROP CBUS-S-FAILED _STM-CAP-SOURCE-FAIL EXIT
    THEN DROP
    _STM-SOURCE-RREF RREF.ID
        _STM-SOURCE-RREF RREF.REVISION @
        _STM-CAP-SOURCE-ENABLED @
        _STM-SOURCE-CANDIDATE-REGISTRY STREAMS-SOURCE-ENABLE
        DUP _STM-CAP-SOURCE-STATUS !
    SSREG-S-OK <> IF
        _STM-CAP-SOURCE-STATUS @ _STM-SSREG>CBUS
            _STM-CAP-SOURCE-FAIL EXIT
    THEN
    _STM-SOURCE-RREF RREF.ID
        _STM-SOURCE-CANDIDATE-REGISTRY STREAMS-SOURCE-FIND
        DUP 0= IF
        DROP CBUS-S-FAILED _STM-CAP-SOURCE-FAIL EXIT
    THEN
    _STM-CAP-SOURCE !
    _STM-CAP-SOURCE @ _STM-SOURCE-RESULT-BUILD ?DUP IF
        _STM-CAP-SOURCE-FAIL EXIT
    THEN
    _STM-SOURCE-COMMIT-CANDIDATE
        DUP _STM-CAP-SOURCE-STATUS !
    STREAMS-SOURCE-S-OK <> IF
        CBUS-S-FAILED _STM-CAP-SOURCE-FAIL EXIT
    THEN
    \ The no-op path was rejected above.  Treat a future domain regression
    \ that reports success without a store/live transition as an invalid
    \ command rather than letting the bus manufacture a revision change.
    _STM-SOURCE-COMMIT-CHANGED @ 0= IF
        CBUS-S-INVALID _STM-CAP-SOURCE-FAIL EXIT
    THEN
    _STM-CAP-REQ @ CBR.RESULT _STM-SOURCE-RESULT-PUBLISH
    _STM-CAP-SOURCE-MUTATION-CLEAN
    \ Request-bus owner commit advances the component revision, but it does
    \ not know which applet surface projects the changed resource.  Dirty the
    \ Sources view only after persistence and caller-result publication.
    _STM-INVALIDATE
    CBUS-S-OK ;

