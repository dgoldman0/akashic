    _STM-SOURCE-REGISTRY STREAMS-SOURCE-REGISTRY-VALID? AND
    _STM-SOURCE-STORE STREAMS-SOURCE-STORE-VALID? AND
    _STM-SOURCE-STORE STREAMS-SOURCE-STORE-BLOCKED? 0= AND ;

