\ Bind and recover the singleton draft before enabling any mutation.  The
\ helper is intentionally non-fatal: an uncertain or unreadable store leaves
\ Streams usable for reading while draft overwrites remain blocked.
: _STM-DRAFT-STORAGE-INIT  ( -- status )
    _STM-DRAFT-MEMORY-CLEAR
    SDSTORE-S-INVALID _STM-DRAFT-BLOCK!
    ['] _STM-DRAFT-STORAGE-INIT-BODY CATCH ?DUP IF
        DROP _STM-DRAFT-MEMORY-CLEAR
        SDSTORE-S-IO _STM-DRAFT-BLOCK!
    THEN
    _STM-DRAFT-STORE-STATUS @ ;

