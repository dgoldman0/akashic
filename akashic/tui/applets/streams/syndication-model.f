: _SYN-ENTRY-AUTHOR  ( index entry -- author )
    SWAP SYN-AUTHOR-SIZE * _SYN-ENTRY-AUTHORS + + ;

: SYN.ENTRY.AUTHOR  ( index entry -- author | 0 )
    >R
    DUP 0< OVER SYN-MAX-AUTHORS >= OR IF DROP R> DROP 0 EXIT THEN
    R@ SYN.ENTRY.AUTHOR-COUNT @ DUP 0< OVER SYN-MAX-AUTHORS > OR IF
        DROP DROP R> DROP 0 EXIT
    THEN
    OVER <= IF DROP R> DROP 0 EXIT THEN
    R> _SYN-ENTRY-AUTHOR ;

: _SYN-ENTRY-TAG  ( index entry -- tag )
    SWAP SYN-TAG-SIZE * _SYN-ENTRY-TAGS + + ;

: SYN.ENTRY.TAG  ( index entry -- a u )
    >R
    DUP 0< OVER SYN-MAX-TAGS >= OR IF DROP R> DROP 0 0 EXIT THEN
    R@ SYN.ENTRY.TAG-COUNT @ DUP 0< OVER SYN-MAX-TAGS > OR IF
        DROP DROP R> DROP 0 0 EXIT
    THEN
    OVER <= IF DROP R> DROP 0 0 EXIT THEN
    R> _SYN-ENTRY-TAG DUP 8 + SWAP @ ;

: _SYN-ENTRY-ATTACHMENT  ( index entry -- attachment )
    SWAP SYN-ATTACHMENT-SIZE * _SYN-ENTRY-ATTACHMENTS + + ;

: SYN.ENTRY.ATTACHMENT  ( index entry -- attachment | 0 )
    >R
    DUP 0< OVER SYN-MAX-ATTACHMENTS >= OR IF DROP R> DROP 0 EXIT THEN
    R@ SYN.ENTRY.ATTACHMENT-COUNT @
    DUP 0< OVER SYN-MAX-ATTACHMENTS > OR IF
        DROP DROP R> DROP 0 EXIT
    THEN
    OVER <= IF DROP R> DROP 0 EXIT THEN
    R> _SYN-ENTRY-ATTACHMENT ;

