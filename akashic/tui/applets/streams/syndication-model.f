\ ---------------------------------------------------------------------
\ Author: name, site URL, and avatar URL are the JSON Feed author shape.
\ ---------------------------------------------------------------------

0 CONSTANT _SYN-AUTHOR-NAME-U
8 CONSTANT _SYN-AUTHOR-NAME
_SYN-AUTHOR-NAME SYN-AUTHOR-NAME-CAP + CONSTANT _SYN-AUTHOR-URL-U
_SYN-AUTHOR-URL-U 8 + CONSTANT _SYN-AUTHOR-URL
_SYN-AUTHOR-URL SYN-URL-CAP + CONSTANT _SYN-AUTHOR-AVATAR-U
_SYN-AUTHOR-AVATAR-U 8 + CONSTANT _SYN-AUTHOR-AVATAR
_SYN-AUTHOR-AVATAR SYN-URL-CAP + CONSTANT SYN-AUTHOR-SIZE

: SYN.AUTHOR.NAME  ( author -- a u )
    DUP _SYN-AUTHOR-NAME + SWAP _SYN-AUTHOR-NAME-U + @ ;
: SYN.AUTHOR.URL  ( author -- a u )
    DUP _SYN-AUTHOR-URL + SWAP _SYN-AUTHOR-URL-U + @ ;
: SYN.AUTHOR.AVATAR  ( author -- a u )
    DUP _SYN-AUTHOR-AVATAR + SWAP _SYN-AUTHOR-AVATAR-U + @ ;

