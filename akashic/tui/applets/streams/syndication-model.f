: SYN.ENTRY.CONTENT-REP  ( entry -- a ) _SYN-ENTRY-CONTENT-REP + ;
: SYN.ENTRY.AUTHOR-COUNT  ( entry -- a ) _SYN-ENTRY-AUTHOR-COUNT + ;
