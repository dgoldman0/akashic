    _SSRD-CLEANUP IF SSSTORE-S-IO EXIT THEN
    _SSRD-STATUS @ ;

: _SSRD-READ-TRANSACTION-CALL  ( -- status )
    ['] _SSRD-READ-TRANSACTION VFS-TRANSACTION ;

: _SSRD-READ  ( store -- status )
    _SSRD-STORE !
    ['] _SSRD-READ-TRANSACTION-CALL CATCH ?DUP IF
        DROP SSSTORE-S-IO
    THEN ;

