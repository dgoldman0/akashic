    _SSSS-STORE @ STREAMS-SOURCE-STORE.CORE _VFSOP-STORE !
    _SSSS-REGISTRY @ _VFSS-CONTEXT !
    _SSSS-REGISTRY @ SSREG.GENERATION @ _VFSS-NEXT !
    _VFSNAP-ENCODE STREAMS-SOURCE-STORE-RECORD-MAX SWAP
        _SSSTORE-VFSNAP>STATUS ;

