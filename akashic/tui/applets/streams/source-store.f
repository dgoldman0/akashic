: _SSSTORE-UNUSED-SLOTS-ZERO?  ( registry -- flag )
    DUP SSREG.COUNT @ >R
    SSREG.RECORDS R@ STREAMS-SOURCE-SIZE * +
    STREAMS-SOURCE-MAX R> - STREAMS-SOURCE-SIZE * _SSOURCE-ZERO? ;

: _SSSTORE-ENCODE-PAYLOAD
  ( registry payload-a payload-u next-generation -- status )
    _SSCB-GENERATION ! _SSCB-PAYLOAD-U !
    _SSCB-PAYLOAD ! _SSCB-CONTEXT !
    _SSCB-PAYLOAD-U @ STREAMS-SOURCE-REGISTRY-SIZE <> IF
        VFSNAP-S-CAPACITY EXIT
    THEN
    _SSCB-CONTEXT @ STREAMS-SOURCE-REGISTRY-VALID? 0= IF
        VFSNAP-S-INVALID EXIT
    THEN
    _SSCB-CONTEXT @ SSREG.GENERATION @ _SSCB-GENERATION @ <> IF
        VFSNAP-S-INVALID EXIT
    THEN
    \ The core supplied an all-zero payload.  Copy the registry header and
    \ live prefix only so unused fixed slots remain canonical zero bytes.
    _SSCB-CONTEXT @ _SSCB-PAYLOAD @ _SSR-RECORDS CMOVE
    _SSCB-CONTEXT @ SSREG.RECORDS
        _SSCB-PAYLOAD @ _SSR-RECORDS +
        _SSCB-CONTEXT @ SSREG.COUNT @ STREAMS-SOURCE-SIZE * CMOVE
    VFSNAP-S-OK ;

: _SSSTORE-VALIDATE-PAYLOAD
  ( payload-a payload-u envelope-generation -- status )
    _SSCB-GENERATION ! _SSCB-PAYLOAD-U ! _SSCB-PAYLOAD !
    _SSCB-PAYLOAD-U @ STREAMS-SOURCE-REGISTRY-SIZE <> IF
        VFSNAP-S-CORRUPT EXIT
    THEN
    _SSCB-PAYLOAD @ SSREG.MAGIC @ _SSREG-MAGIC <> IF
        VFSNAP-S-CORRUPT EXIT
    THEN
    _SSCB-PAYLOAD @ SSREG.ABI @ STREAMS-SOURCE-REGISTRY-ABI <> IF
        VFSNAP-S-UNSUPPORTED EXIT
    THEN
    _SSCB-PAYLOAD @ STREAMS-SOURCE-REGISTRY-VALID? 0= IF
        VFSNAP-S-CORRUPT EXIT
    THEN
    _SSCB-PAYLOAD @ _SSSTORE-UNUSED-SLOTS-ZERO? 0= IF
        VFSNAP-S-CORRUPT EXIT
    THEN
    _SSCB-PAYLOAD @ SSREG.GENERATION @ _SSCB-GENERATION @ <> IF
        VFSNAP-S-CORRUPT EXIT
    THEN
    VFSNAP-S-OK ;

: _SSSTORE-VFSNAP>STATUS  ( snapshot-status -- source-status )
    CASE
        VFSNAP-S-OK OF SSSTORE-S-OK ENDOF
        VFSNAP-S-ABSENT OF SSSTORE-S-ABSENT ENDOF
        VFSNAP-S-CORRUPT OF SSSTORE-S-CORRUPT ENDOF
        VFSNAP-S-UNSUPPORTED OF SSSTORE-S-UNSUPPORTED ENDOF
        VFSNAP-S-INVALID OF SSSTORE-S-INVALID ENDOF
        VFSNAP-S-CAPACITY OF SSSTORE-S-CAPACITY ENDOF
        VFSNAP-S-IO OF SSSTORE-S-IO ENDOF
        VFSNAP-S-RECOVERY OF SSSTORE-S-RECOVERY ENDOF
        VFSNAP-S-BUSY OF SSSTORE-S-BUSY ENDOF
        VFSNAP-S-CONFLICT OF SSSTORE-S-CONFLICT ENDOF
        SSSTORE-S-RECOVERY SWAP
    ENDCASE ;

: _SSSTORE-SYNC  ( snapshot-status store -- source-status )
    DROP _SSSTORE-VFSNAP>STATUS ;

\ =====================================================================
\  Hostile-alias preflight
\ =====================================================================

: _SSSTORE-SPAN-VALID?  ( address length -- flag )
    DUP 0< IF 2DROP 0 EXIT THEN
    OVER 0= IF 2DROP 0 EXIT THEN
    DUP 0= IF 2DROP -1 EXIT THEN
    _VFSNAP-SPAN-VALID? ;
: _SSSTORE-RANGES-OVERLAP?  ( a1 u1 a2 u2 -- flag )
    _VFSNAP-RANGES-OVERLAP? ;

: _SSSTORE-PRIVATE-ALIASES?  ( address length -- flag )
    2DUP _SSSTORE-SPEC _VFSNAP-ALL-PRIVATE-ALIASES? IF
        2DROP -1 EXIT
    THEN
    _SSSTORE-SPEC VFSNAP-SPEC-SIZE _SSSTORE-RANGES-OVERLAP? ;

: _SSSTORE-STORE-SPAN-SAFE?  ( store -- flag )
    DUP STREAMS-SOURCE-STORE-SIZE _SSSTORE-SPAN-VALID? 0= IF
        DROP 0 EXIT
    THEN
    DUP STREAMS-SOURCE-STORE-SIZE _SSSTORE-PRIVATE-ALIASES? 0= NIP ;

: _SSSTORE-IO-ALIASES?  ( address length store -- flag )
    >R
    R@ _SSSTORE-STORE-SPAN-SAFE? 0= IF 2DROP R> DROP -1 EXIT THEN
    2DUP _SSSTORE-SPAN-VALID? 0= IF 2DROP R> DROP 0 EXIT THEN
    2DUP R@ STREAMS-SOURCE-STORE-SIZE _SSSTORE-RANGES-OVERLAP? >R
    _SSSTORE-PRIVATE-ALIASES? R> OR R> DROP ;

: _SSSTORE-IO-SPAN-SAFE?  ( address length store -- flag )
    >R
    2DUP _SSSTORE-SPAN-VALID? R@ _SSSTORE-STORE-SPAN-SAFE? AND 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    R> _SSSTORE-IO-ALIASES? 0= ;

: _SSSTORE-TARGET-SPAN-SAFE?  ( target-a target-u store -- flag )
    >R
    R@ _SSSTORE-STORE-SPAN-SAFE? 0= IF 2DROP R> DROP 0 EXIT THEN
    2DUP _SSSTORE-SPAN-VALID? 0= IF 2DROP R> DROP 0 EXIT THEN
    2DUP R@ STREAMS-SOURCE-STORE-SIZE _SSSTORE-RANGES-OVERLAP? >R
    _SSSTORE-PRIVATE-ALIASES? R> OR 0= R> DROP ;

: STREAMS-SOURCE-STORE-VALID?  ( store -- flag )
    DUP _SSSTORE-STORE-SPAN-SAFE? 0= IF DROP 0 EXIT THEN
    STREAMS-SOURCE-STORE.CORE VFSNAP-VALID? ;

