VARIABLE _SSSL-DESTINATION
VARIABLE _SSSL-CAPACITY
VARIABLE _SSSL-STORE
VARIABLE _SSSL-STATUS

: _STREAMS-SOURCE-STORE-LOAD
  ( destination capacity store -- status )
    _SSSL-STORE ! _SSSL-CAPACITY ! _SSSL-DESTINATION !
    _SSSL-DESTINATION @ 0= _SSSL-CAPACITY @ 0< OR
    _SSSL-STORE @ STREAMS-SOURCE-STORE-VALID? 0= OR IF
        SSSTORE-S-INVALID EXIT
    THEN
    _SSSL-DESTINATION @ _SSSL-CAPACITY @ _SSSL-STORE @
        _SSSTORE-IO-SPAN-SAFE? 0= IF SSSTORE-S-INVALID EXIT THEN
    _SSSL-CAPACITY @ STREAMS-SOURCE-REGISTRY-SIZE < IF
        SSSTORE-S-CAPACITY EXIT
    THEN
    _SSSL-STORE @ STREAMS-SOURCE-STORE-BLOCKED? IF
        _SSSL-STORE @ STREAMS-SOURCE-STORE.LAST-STATUS @ EXIT
    THEN
    _SSSL-STORE @ _STREAMS-SOURCE-STORE-RECOVER DUP IF EXIT THEN DROP
    _SSSL-STORE @ _SSRD-READ DUP _SSSL-STATUS ! DROP
    _SSSL-STATUS @ SSSTORE-S-OK = IF
        _SSSTORE-RECORD STREAMS-SOURCE-STORE-HEADER-SIZE +
            _SSSL-DESTINATION @ STREAMS-SOURCE-REGISTRY-SIZE CMOVE
        0 _SSSL-STORE @ STREAMS-SOURCE-STORE.FLAGS !
        SSSTORE-S-OK _SSSL-STORE @ _SSSTORE-STATUS! EXIT
    THEN
    _SSSL-STATUS @ SSSTORE-S-ABSENT = IF
        _SSSL-STATUS @ _SSSL-STORE @ _SSSTORE-STATUS! EXIT
    THEN
    _SSSL-STATUS @ SSSTORE-S-CORRUPT =
    _SSSL-STATUS @ SSSTORE-S-UNSUPPORTED = OR
    _SSSL-STATUS @ SSSTORE-S-IO = OR
    _SSSL-STATUS @ SSSTORE-S-RECOVERY = OR IF
        _SSSL-STATUS @ _SSSL-STORE @ _SSSTORE-BLOCK! EXIT
    THEN
    _SSSL-STATUS @ _SSSL-STORE @ _SSSTORE-STATUS! ;

