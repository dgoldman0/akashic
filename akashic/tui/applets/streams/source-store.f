    _SSSS-STORE @
        STREAMS-SOURCE-STORE.LAST-VREPL !
    _SSSS-VREPL @ _SSSTORE-VREPL>STATUS DUP _SSSS-STATUS !
    DROP
    _SSSS-STATUS @ SSSTORE-S-OK = IF
        _SSSS-STATUS @ _SSSS-STORE @ _SSSTORE-STATUS! EXIT
    THEN
    _SSSS-STATUS @ _SSSTORE-SAVE-FAIL ;

