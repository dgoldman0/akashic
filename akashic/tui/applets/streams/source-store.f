: _SSRD-RESTORE-CALL  ( -- ) _SSRD-OLD-VFS @ VFS-USE ;

: _SSRD-CLEANUP  ( -- failed? )
    0 _SSRD-CLEAN-FAILED !
    _SSRD-FD @ ?DUP IF
        _SSRD-CLEAN-FD ! 0 _SSRD-FD !
        ['] _SSRD-CLOSE-CALL CATCH IF -1 _SSRD-CLEAN-FAILED ! THEN
    THEN
    _SSRD-HAVE-OLD-VFS @ IF
        0 _SSRD-HAVE-OLD-VFS !
        ['] _SSRD-RESTORE-CALL CATCH IF -1 _SSRD-CLEAN-FAILED ! THEN
    THEN
    _SSRD-CLEAN-FAILED @ ;

: _SSRD-READ-BODY  ( -- status )
    _SSRD-STORE @ STREAMS-SOURCE-STORE-PATH$
        _SSRD-STORE @ STREAMS-SOURCE-STORE.VFS @ VFS-RESOLVE 0= IF
        SSSTORE-S-ABSENT EXIT
    THEN
    _SSRD-STORE @ STREAMS-SOURCE-STORE-PATH$ VFS-OPEN DUP 0= IF
        DROP SSSTORE-S-IO EXIT
    THEN
    _SSRD-FD !
    _SSRD-FD @ VFS-SIZE DUP _SSRD-FILE-U !
    DUP 0< IF DROP SSSTORE-S-IO EXIT THEN
    STREAMS-SOURCE-STORE-HEADER-SIZE < IF SSSTORE-S-CORRUPT EXIT THEN
    _SSSTORE-RECORD STREAMS-SOURCE-STORE-HEADER-SIZE
        _SSRD-FD @ VFS-READ-EXACT IF SSSTORE-S-IO EXIT THEN
    _SSSTORE-RECORD _SSS-H-MAGIC + 8
        _SSSTORE-RECORD-MAGIC 8 COMPARE 0<> IF
        SSSTORE-S-CORRUPT EXIT
    THEN
    _SSSTORE-RECORD _STREAMS-SOURCE-STORE-HEADER-CRC
        _SSSTORE-RECORD _SSS-H-HEADER-CRC + @ <> IF
        SSSTORE-S-CORRUPT EXIT
    THEN
    _SSSTORE-RECORD _SSS-H-FORMAT + @
        STREAMS-SOURCE-STORE-FORMAT-V1 <> IF
        SSSTORE-S-UNSUPPORTED EXIT
    THEN
    _SSSTORE-RECORD _SSS-H-HEADER-SIZE + @
        STREAMS-SOURCE-STORE-HEADER-SIZE <>
    _SSSTORE-RECORD _SSS-H-FLAGS + @ 0<> OR
    _SSSTORE-RECORD _SSS-H-GENERATION + @ DUP _SSRD-GENERATION !
        0> 0= OR
    _SSSTORE-RECORD _SSS-H-PAYLOAD-SIZE + @
        STREAMS-SOURCE-REGISTRY-SIZE <> OR IF
        SSSTORE-S-CORRUPT EXIT
    THEN
    _SSRD-FILE-U @ STREAMS-SOURCE-STORE-RECORD-MAX <> IF
        SSSTORE-S-CORRUPT EXIT
    THEN
    _SSSTORE-RECORD STREAMS-SOURCE-STORE-HEADER-SIZE +
        STREAMS-SOURCE-REGISTRY-SIZE _SSRD-FD @ VFS-READ-EXACT IF
        SSSTORE-S-IO EXIT
    THEN
    _SSSTORE-RECORD STREAMS-SOURCE-STORE-HEADER-SIZE +
        STREAMS-SOURCE-REGISTRY-SIZE
        _STREAMS-SOURCE-STORE-PAYLOAD-CRC
        _SSSTORE-RECORD _SSS-H-PAYLOAD-CRC + @ <> IF
        SSSTORE-S-CORRUPT EXIT
    THEN
    _SSSTORE-RECORD STREAMS-SOURCE-STORE-HEADER-SIZE + DUP
        SSREG.MAGIC @ _SSREG-MAGIC <> IF
        DROP SSSTORE-S-CORRUPT EXIT
    THEN
    DUP SSREG.ABI @ STREAMS-SOURCE-REGISTRY-ABI <> IF
        DROP SSSTORE-S-UNSUPPORTED EXIT
    THEN
    DUP STREAMS-SOURCE-REGISTRY-VALID? 0= IF
        DROP SSSTORE-S-CORRUPT EXIT
    THEN
    DUP _SSSTORE-UNUSED-SLOTS-ZERO? 0= IF
        DROP SSSTORE-S-CORRUPT EXIT
    THEN
    SSREG.GENERATION @ _SSRD-GENERATION @ <> IF
        SSSTORE-S-CORRUPT EXIT
    THEN
    SSSTORE-S-OK ;

