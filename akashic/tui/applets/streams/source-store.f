: _STREAMS-SOURCE-STORE-LOAD-TERMINAL
  ( destination capacity store -- status )
    _SSLT-STORE ! _SSLT-CAPACITY ! _SSLT-DESTINATION !
    ['] _SSLT-CALL CATCH
    _SSLT-STORE @ DUP STREAMS-SOURCE-STORE-VALID? IF
        STREAMS-SOURCE-STORE-SCRATCH-WIPE
    ELSE DROP THEN
    DUP IF THROW THEN DROP ;

' _STREAMS-SOURCE-STORE-LOAD-TERMINAL CONSTANT _ssstore-load-xt
: STREAMS-SOURCE-STORE-LOAD  ( destination capacity store -- status )
    2 PICK 2 PICK 2 PICK _SSSTORE-IO-SPAN-SAFE? 0= IF
        2DROP DROP SSSTORE-S-INVALID EXIT
    THEN
    _ssstore-load-xt _streams-source-store-guard WITH-GUARD ;

: _STREAMS-SOURCE-STORE-SAVE  ( registry expected-generation store -- status )
    _SSSS-STORE ! _SSSS-EXPECTED ! _SSSS-REGISTRY !
    _SSSS-STORE @ STREAMS-SOURCE-STORE-VALID? 0= IF
        SSSTORE-S-INVALID EXIT
    THEN
    _SSSS-EXPECTED @ 0< IF SSSTORE-S-INVALID EXIT THEN
    _SSSS-STORE @ STREAMS-SOURCE-STORE-BLOCKED? IF
        _SSSS-STORE @ STREAMS-SOURCE-STORE.LAST-STATUS @ EXIT
    THEN
    _SSSS-REGISTRY @ STREAMS-SOURCE-REGISTRY-VALID? 0= IF
        SSSTORE-S-INVALID EXIT
    THEN
    _SSSS-EXPECTED @ 1+ DUP 0> 0= IF DROP SSSTORE-S-CAPACITY EXIT THEN
    _SSSS-REGISTRY @ SSREG.GENERATION @ <> IF SSSTORE-S-INVALID EXIT THEN
    _SSSS-REGISTRY @ _SSSS-EXPECTED @
        _SSSS-STORE @ STREAMS-SOURCE-STORE.CORE VFSNAP-SAVE
        _SSSS-STORE @ _SSSTORE-SYNC ;

\ Private test/golden helper; shared core still constructs the envelope.
