: _STREAMS-SOURCE-STORE-FINI  ( store -- status )
    DUP STREAMS-SOURCE-STORE-VALID? 0= IF DROP SSSTORE-S-INVALID EXIT THEN
    DUP STREAMS-SOURCE-STORE.CORE VFSNAP-FINI
    DUP VFSNAP-S-OK = IF
        DROP STREAMS-SOURCE-STORE-SIZE 0 FILL SSSTORE-S-OK
    ELSE
        NIP _SSSTORE-VFSNAP>STATUS
    THEN ;
' _STREAMS-SOURCE-STORE-FINI CONSTANT _ssstore-fini-xt
: STREAMS-SOURCE-STORE-FINI  ( store -- status )
    DUP _SSSTORE-STORE-SPAN-SAFE? 0= IF DROP SSSTORE-S-INVALID EXIT THEN
    _ssstore-fini-xt _streams-source-store-guard WITH-GUARD ;

