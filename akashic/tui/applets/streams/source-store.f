: _STREAMS-SOURCE-STORE-INIT-AT
  ( target-a target-u vfs store -- status )
    2OVER 2OVER SWAP DROP _SSSTORE-TARGET-SPAN-SAFE? 0= IF
        2DROP 2DROP SSSTORE-S-INVALID EXIT
    THEN
    _SSSI-STORE ! _SSSI-VFS ! _SSSI-TARGET-U ! _SSSI-TARGET-A !
    _SSSI-TARGET-A @ 0= _SSSI-TARGET-U @ 0< OR
    _SSSI-VFS @ 0= OR _SSSI-STORE @ 0= OR IF
        SSSTORE-S-INVALID EXIT
    THEN
    _SSSI-TARGET-A @ _SSSI-TARGET-U @ _SSSI-STORE @
        _SSSTORE-TARGET-SPAN-SAFE? 0= IF
        \ The target may be inside the still-uninitialized descriptor.  Reject
        \ before FILL so even a one-byte overlap cannot erase caller input or
        \ manufacture a partially configured store.
        SSSTORE-S-INVALID EXIT
    THEN
    _SSSI-STORE @ STREAMS-SOURCE-STORE-SIZE 0 FILL
    _SSSTORE-MAGIC _SSSI-STORE @ STREAMS-SOURCE-STORE.MAGIC !
    _SSSI-VFS @ _SSSI-STORE @ STREAMS-SOURCE-STORE.VFS !
    SSSTORE-S-ABSENT _SSSI-STORE @
        STREAMS-SOURCE-STORE.LAST-STATUS !
    _SSSI-VFS @ _SSSI-STORE @ STREAMS-SOURCE-STORE.REPLACE
        VREPL-INIT DUP IF _SSSTORE-VREPL>STATUS EXIT THEN DROP
    _SSSI-TARGET-A @ _SSSI-TARGET-U @
        _SSSI-STORE @ STREAMS-SOURCE-STORE.REPLACE
        VREPL-DERIVE-PATHS! _SSSTORE-VREPL>STATUS ;

