        _SSSTORE-IO-SPAN-SAFE? 0= IF SSSTORE-S-INVALID EXIT THEN
    _SSSS-STORE @ STREAMS-SOURCE-STORE-VALID? 0= IF
        SSSTORE-S-INVALID EXIT
    THEN
    _SSSS-EXPECTED @ 0< IF SSSTORE-S-INVALID EXIT THEN
    _SSSS-STORE @ STREAMS-SOURCE-STORE-BLOCKED? IF
        _SSSS-STORE @ STREAMS-SOURCE-STORE.LAST-STATUS @ EXIT
    THEN
    _SSSS-REGISTRY @ STREAMS-SOURCE-REGISTRY-VALID? 0= IF
        SSSTORE-S-INVALID EXIT
    THEN
    _SSSS-EXPECTED @ 1+ DUP 0> 0= IF
        DROP SSSTORE-S-CAPACITY EXIT
    THEN
    _SSSS-REGISTRY @ SSREG.GENERATION @ <> IF
        SSSTORE-S-INVALID EXIT
    THEN
    _SSSS-STORE @ _STREAMS-SOURCE-STORE-RECOVER DUP IF
        _SSSTORE-SAVE-FAIL EXIT
    THEN DROP
