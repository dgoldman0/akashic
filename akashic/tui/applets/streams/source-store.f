\ =====================================================================
\  source-store.f - Crash-recoverable Streams source-registry snapshot
\ =====================================================================
\  One pointer-free registry snapshot is stored at /streams-sources.bin.
\  The fixed V1 envelope and payload have independent CRC-32 checks.  CRCs
\  detect accidental corruption; they do not authenticate a hostile writer.
\
\  LOAD resolves an interrupted VREPL transaction, validates the complete
\  record, including canonical zeroed source slots outside COUNT, and only
\  then copies to caller memory.  CORRUPT, UNSUPPORTED, I/O,
\  and ambiguous recovery states block later mutation through this descriptor
\  until a fresh lifecycle reconstructs and reloads it.  Evidence is never
\  replaced with a manufactured empty registry.
\
\  SAVE is optimistic.  EXPECTED-GENERATION is zero only for an absent first
\  snapshot; otherwise it must equal the complete current snapshot.  The
\  candidate registry generation must be exactly expected + 1.  Streams must
\  therefore mutate a candidate copy, save it, and publish that candidate to
\  live applet state only after SAVE succeeds.
\
\  Public API:
\    STREAMS-SOURCE-STORE-INIT-AT
\      ( target-a target-u vfs store -- status )
\    STREAMS-SOURCE-STORE-INIT       ( vfs store -- status )
\    STREAMS-SOURCE-STORE-RECOVER    ( store -- status )
\    STREAMS-SOURCE-STORE-LOAD
\      ( destination capacity store -- status )
\    STREAMS-SOURCE-STORE-SAVE
\      ( registry expected-generation store -- status )
\    STREAMS-SOURCE-STORE-BLOCKED?   ( store -- flag )
\    STREAMS-SOURCE-STORE-PATH$      ( store -- a u )
\
\  Public scratch is wiped over its full capacity on every LOAD, SAVE, and
\  RECOVER return or exception path.  VREPL separately wipes its read-back
\  verification buffer.
\ =====================================================================

