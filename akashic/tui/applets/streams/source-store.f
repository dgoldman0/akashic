\ ---------------------------------------------------------------------
\ Public span/alias preflight
\ ---------------------------------------------------------------------
\ Half-open byte spans make exact adjacency safe.  Validate both additions
\ before comparing endpoints so a wrapping address+length is never mistaken
\ for a disjoint range.  LOAD protects the complete caller-declared capacity;
\ SAVE protects the complete fixed registry, not merely its live prefix.

: _SSSTORE-SPAN-VALID?  ( address length -- flag )
    OVER 0= IF 2DROP 0 EXIT THEN
    MSPAN-NONWRAPPING? ;

: _SSSTORE-RANGES-OVERLAP?  ( a1 u1 a2 u2 -- flag )
    2OVER _SSSTORE-SPAN-VALID? 0= IF
        2DROP 2DROP 0 EXIT
    THEN
    2DUP _SSSTORE-SPAN-VALID? 0= IF
        2DROP 2DROP 0 EXIT
    THEN
    \ The shared predicate also owns no addressable scratch; private-range
    \ admission remains below in this store-specific boundary.
    MSPAN-OVERLAP? ;

: _SSSTORE-PRIVATE-ALIASES?  ( address length -- flag )
    _SSSTORE-PRIVATE-RANGE-N @ 0 ?DO
        2DUP
        _SSSTORE-PRIVATE-RANGES I _SSSTORE-PRIVATE-RANGE-SIZE * +
        DUP @ SWAP 8 + @ _SSSTORE-RANGES-OVERLAP? IF
            2DROP -1 UNLOOP EXIT
        THEN
    LOOP
    2DROP 0 ;

: _SSSTORE-STORE-SPAN-SAFE?  ( store -- flag )
    DUP STREAMS-SOURCE-STORE-SIZE _SSSTORE-SPAN-VALID? 0= IF
        DROP 0 EXIT
    THEN
    DUP STREAMS-SOURCE-STORE-SIZE
        _SSSTORE-PRIVATE-ALIASES? 0= NIP ;

: _SSSTORE-IO-ALIASES?  ( address length store -- flag )
    >R
    R@ _SSSTORE-STORE-SPAN-SAFE? 0= IF
        2DROP R> DROP -1 EXIT
    THEN
    2DUP _SSSTORE-SPAN-VALID? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    2DUP R@ STREAMS-SOURCE-STORE-SIZE _SSSTORE-RANGES-OVERLAP? >R
    _SSSTORE-PRIVATE-ALIASES?
    R> OR R> DROP ;

: _SSSTORE-IO-SPAN-SAFE?  ( address length store -- flag )
    >R
    2DUP _SSSTORE-SPAN-VALID?
    R@ _SSSTORE-STORE-SPAN-SAFE? AND
    0= IF 2DROP R> DROP 0 EXIT THEN
    R> _SSSTORE-IO-ALIASES? 0= ;

: _SSSTORE-TARGET-SPAN-SAFE?  ( target-a target-u store -- flag )
    >R
    R@ _SSSTORE-STORE-SPAN-SAFE? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    2DUP _SSSTORE-SPAN-VALID? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    2DUP R@ STREAMS-SOURCE-STORE-SIZE _SSSTORE-RANGES-OVERLAP? >R
    _SSSTORE-PRIVATE-ALIASES?
    R> OR 0= R> DROP ;

