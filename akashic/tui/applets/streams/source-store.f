: _STREAMS-SOURCE-STORE-INIT-AT
  ( target-a target-u vfs store -- status )
    _SSSI-STORE ! _SSSI-VFS ! _SSSI-TARGET-U ! _SSSI-TARGET-A !
    _SSSI-TARGET-A @ 0= _SSSI-TARGET-U @ 0< OR
    _SSSI-VFS @ 0= OR _SSSI-STORE @ 0= OR IF SSSTORE-S-INVALID EXIT THEN
    _SSSI-TARGET-A @ _SSSI-TARGET-U @ _SSSI-STORE @
        _SSSTORE-TARGET-SPAN-SAFE? 0= IF SSSTORE-S-INVALID EXIT THEN
    _SSSI-STORE @ STREAMS-SOURCE-STORE-SIZE 0 FILL
    _SSSI-TARGET-A @ _SSSI-TARGET-U @
        _SSSI-STORE @ STREAMS-SOURCE-STORE.SCRATCH
        STREAMS-SOURCE-STORE-RECORD-MAX
        _SSSI-VFS @ _SSSTORE-SPEC
        _SSSI-STORE @ STREAMS-SOURCE-STORE.CORE VFSNAP-INIT-AT
        _SSSI-STORE @ _SSSTORE-SYNC ;

' _STREAMS-SOURCE-STORE-INIT-AT CONSTANT _ssstore-init-at-xt
: STREAMS-SOURCE-STORE-INIT-AT
  ( target-a target-u vfs store -- status )
    2OVER 2OVER SWAP DROP _SSSTORE-TARGET-SPAN-SAFE? 0= IF
        2DROP 2DROP SSSTORE-S-INVALID EXIT
    THEN
    _ssstore-init-at-xt _streams-source-store-guard WITH-GUARD ;
: STREAMS-SOURCE-STORE-INIT  ( vfs store -- status )
    STREAMS-SOURCE-STORE-TARGET$ 2SWAP STREAMS-SOURCE-STORE-INIT-AT ;

: _STREAMS-SOURCE-STORE-RECOVER  ( store -- status )
    DUP STREAMS-SOURCE-STORE-VALID? 0= IF DROP SSSTORE-S-INVALID EXIT THEN
    DUP >R STREAMS-SOURCE-STORE.CORE VFSNAP-RECOVER R> _SSSTORE-SYNC ;

: _STREAMS-SOURCE-STORE-LOAD  ( destination capacity store -- status )
    _SSSL-STORE ! _SSSL-CAPACITY ! _SSSL-DESTINATION !
    _SSSL-STORE @ STREAMS-SOURCE-STORE-VALID? 0= IF
        SSSTORE-S-INVALID EXIT
    THEN
    _SSSL-DESTINATION @ _SSSL-CAPACITY @
        _SSSL-STORE @ STREAMS-SOURCE-STORE.LOAD-GENERATION
        _SSSL-STORE @ STREAMS-SOURCE-STORE.CORE VFSNAP-LOAD
        _SSSL-STORE @ _SSSTORE-SYNC ;

VARIABLE _SSLT-DESTINATION
VARIABLE _SSLT-CAPACITY
