    _OAIE-STATUS @ ?DUP IF EXIT THEN
    _OAIE-FOUND @ IF OAIEV-S-OK EXIT THEN
    _OAIE-A @ _OAIE-U @ S" error" _OAIE-OBJECT-FIELD
