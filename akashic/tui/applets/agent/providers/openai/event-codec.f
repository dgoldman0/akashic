VARIABLE _OAIEF-IOR
VARIABLE _OAIEF-N

: _OAIE-STRING-FIELD!  ( object key destination bounds -- found status )
    _OAIEF-LEN ! _OAIEF-CAP ! _OAIEF-D !
    _OAIEF-KU ! _OAIEF-KA ! _OAIEF-OU ! _OAIEF-OA !
    _OAIEF-OA @ _OAIEF-OU @ _OAIEF-KA @ _OAIEF-KU @ JSON-FIELD
    _OAIEF-IOR ! _OAIEF-FOUND ! _OAIEF-VU ! _OAIEF-VA !
    _OAIEF-IOR @ IF 0 OAIEV-S-FIELD EXIT THEN
    _OAIEF-FOUND @ 0= IF 0 OAIEV-S-OK EXIT THEN
    _OAIEF-VA @ _OAIEF-VU @ JSON-STRING? 0= IF 0 OAIEV-S-TYPE EXIT THEN
    _OAIEF-VA @ _OAIEF-VU @ JSON-GET-STRING
    _OAIEF-D @ _OAIEF-CAP @ JSON-UNESCAPE _OAIEF-N !
    JSON-OK? 0= IF
        0 JSON-ERR @ JSON-E-OVERFLOW = IF OAIEV-S-CAPACITY ELSE OAIEV-S-TYPE THEN
        EXIT
    THEN
    _OAIEF-N @ _OAIEF-LEN @ !
    -1 OAIEV-S-OK ;

: _OAIE-RAW-FIELD!  ( object key destination bounds -- found status )
    _OAIEF-LEN ! _OAIEF-CAP ! _OAIEF-D !
    _OAIEF-KU ! _OAIEF-KA ! _OAIEF-OU ! _OAIEF-OA !
    _OAIEF-OA @ _OAIEF-OU @ _OAIEF-KA @ _OAIEF-KU @ JSON-FIELD
    _OAIEF-IOR ! _OAIEF-FOUND ! _OAIEF-VU ! _OAIEF-VA !
    _OAIEF-IOR @ IF 0 OAIEV-S-FIELD EXIT THEN
    _OAIEF-FOUND @ 0= IF 0 OAIEV-S-OK EXIT THEN
    _OAIEF-VU @ _OAIEF-CAP @ > IF 0 OAIEV-S-CAPACITY EXIT THEN
    _OAIEF-VA @ _OAIEF-D @ _OAIEF-VU @ CMOVE
    _OAIEF-VU @ _OAIEF-LEN @ !
    -1 OAIEV-S-OK ;

: _OAIE-OBJECT-FIELD  ( object key -- value found status )
    JSON-FIELD _OAIEF-IOR ! _OAIEF-FOUND ! _OAIEF-VU ! _OAIEF-VA !
    _OAIEF-IOR @ IF 0 0 0 OAIEV-S-FIELD EXIT THEN
    _OAIEF-FOUND @ 0= IF 0 0 0 OAIEV-S-OK EXIT THEN
    _OAIEF-VA @ _OAIEF-VU @ JSON-OBJECT? 0= IF
        0 0 0 OAIEV-S-TYPE EXIT
    THEN
    _OAIEF-VA @ _OAIEF-VU @ -1 OAIEV-S-OK ;

VARIABLE _OAIE-TYPE-U
CREATE _OAIE-TYPE-BUF 64 ALLOT
VARIABLE _OAIE-ITEM-TYPE-U
CREATE _OAIE-ITEM-TYPE-BUF 64 ALLOT

: _OAIE-TYPE-KIND  ( addr len -- kind )
    2DUP S" response.created" STR-STR= IF 2DROP OAIEV-K-RESPONSE-CREATED EXIT THEN
    2DUP S" response.output_text.delta" STR-STR= IF 2DROP OAIEV-K-TEXT-DELTA EXIT THEN
    2DUP S" response.output_item.added" STR-STR= IF 2DROP OAIEV-K-OUTPUT-ADDED EXIT THEN
    2DUP S" response.function_call_arguments.delta" STR-STR= IF
        2DROP OAIEV-K-ARGUMENTS-DELTA EXIT
    THEN
    2DUP S" response.function_call_arguments.done" STR-STR= IF
        2DROP OAIEV-K-ARGUMENTS-DONE EXIT
    THEN
    2DUP S" response.output_item.done" STR-STR= IF 2DROP OAIEV-K-OUTPUT-DONE EXIT THEN
    2DUP S" response.completed" STR-STR= IF 2DROP OAIEV-K-RESPONSE-COMPLETED EXIT THEN
    2DUP S" response.failed" STR-STR= IF 2DROP OAIEV-K-RESPONSE-FAILED EXIT THEN
    2DUP S" response.incomplete" STR-STR= IF 2DROP OAIEV-K-RESPONSE-INCOMPLETE EXIT THEN
    2DUP S" response.metadata" STR-STR= IF 2DROP OAIEV-K-RESPONSE-METADATA EXIT THEN
    S" error" STR-STR= IF OAIEV-K-ERROR ELSE OAIEV-K-UNKNOWN THEN ;

