    _OAIE-STATUS @ ?DUP IF EXIT THEN
    _OAIE-FOUND @ 0= IF OAIEV-S-OK EXIT THEN
    _OAIE-RA @ _OAIE-RU @ S" error" _OAIE-OBJECT-FIELD
    _OAIE-STATUS ! _OAIE-FOUND ! _OAIE-EU ! _OAIE-EA !
    _OAIE-STATUS @ ?DUP IF EXIT THEN
    _OAIE-FOUND @ 0= IF OAIEV-S-OK EXIT THEN
    _OAIE-EA @ _OAIE-EU @ S" message"
    _OAIE-E @ OAIEV.MESSAGE-BUF OAIEV-MESSAGE-CAPACITY
    _OAIE-E @ OAIEV.MESSAGE-U _OAIE-STRING-FIELD!
