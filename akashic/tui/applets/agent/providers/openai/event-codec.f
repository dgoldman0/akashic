    _OAIE-A @ _OAIE-U @ S" message"
    _OAIE-E @ OAIEV.MESSAGE-BUF OAIEV-MESSAGE-CAPACITY
    _OAIE-E @ OAIEV.MESSAGE-U _OAIE-STRING-FIELD!
