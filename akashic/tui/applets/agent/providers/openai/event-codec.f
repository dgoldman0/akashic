: _OAIE-TURN-STATE-VALUE!  ( value-a value-u -- status )
    2DUP JSON-STRING? 0= IF
        2DUP JSON-ARRAY? 0= IF 2DROP OAIEV-S-TYPE EXIT THEN
        JSON-ENTER
        2DUP JSON-COUNT 0= IF 2DROP OAIEV-S-OK EXIT THEN
        2DUP JSON-STRING? 0= IF 2DROP OAIEV-S-TYPE EXIT THEN
    THEN
    JSON-GET-STRING
    _OAIE-E @ OAIEV.TURN-STATE-BUF OAIEV-TURN-STATE-CAPACITY
    JSON-UNESCAPE _OAIEF-N !
    JSON-OK? 0= IF
        JSON-ERR @ JSON-E-OVERFLOW = IF OAIEV-S-CAPACITY ELSE OAIEV-S-TYPE THEN
        EXIT
    THEN
    _OAIEF-N @ _OAIE-E @ OAIEV.TURN-STATE-U !
    OAIEV-S-OK ;

: _OAIE-TURN-STATE  ( -- status )
    _OAIE-A @ _OAIE-U @ S" headers" _OAIE-OBJECT-FIELD
    _OAIE-STATUS ! _OAIE-FOUND ! _OAIE-HU ! _OAIE-HA !
    _OAIE-STATUS @ ?DUP IF EXIT THEN
    _OAIE-FOUND @ 0= IF OAIEV-S-OK EXIT THEN
    _OAIE-HA @ _OAIE-HU @ S" x-codex-turn-state" JSON-FIELD
    _OAIE-STATUS ! _OAIE-FOUND ! _OAIE-TU ! _OAIE-TA !
    _OAIE-STATUS @ IF OAIEV-S-FIELD EXIT THEN
    _OAIE-FOUND @ 0= IF OAIEV-S-OK EXIT THEN
    _OAIE-TA @ _OAIE-TU @ _OAIE-TURN-STATE-VALUE! ;

: OAIEV-PARSE  ( json-a json-u event -- status )
    _OAIE-E ! _OAIE-U ! _OAIE-A !
    _OAIE-E @ 0= IF OAIEV-S-INVALID EXIT THEN
    _OAIE-E @ OAIEV-RESET
    _OAIE-A @ _OAIE-U @ JSON-VALID? 0= IF OAIEV-S-INVALID EXIT THEN
    _OAIE-A @ _OAIE-U @ JSON-OBJECT? 0= IF OAIEV-S-TYPE EXIT THEN
    _OAIE-A @ _OAIE-U @ S" type" _OAIE-TYPE-BUF 64 _OAIE-TYPE-U
    _OAIE-STRING-FIELD! _OAIE-STATUS ! _OAIE-FOUND !
    _OAIE-STATUS @ ?DUP IF EXIT THEN
    _OAIE-FOUND @ 0= IF OAIEV-S-FIELD EXIT THEN
    _OAIE-TYPE-BUF _OAIE-TYPE-U @ _OAIE-TYPE-KIND
    DUP _OAIE-KIND ! _OAIE-E @ OAIEV.KIND !
    _OAIE-RESPONSE-ID ?DUP IF EXIT THEN
    _OAIE-OUTPUT-INDEX ?DUP IF EXIT THEN
    _OAIE-KIND @ OAIEV-K-TEXT-DELTA = IF
        _OAIE-A @ _OAIE-U @ S" delta"
        _OAIE-E @ OAIEV.DELTA-BUF OAIEV-DELTA-CAPACITY
        _OAIE-E @ OAIEV.DELTA-U _OAIE-STRING-FIELD!
        _OAIE-STATUS ! _OAIE-FOUND !
        _OAIE-STATUS @ ?DUP IF EXIT THEN
        _OAIE-FOUND @ 0= IF OAIEV-S-FIELD ELSE OAIEV-S-OK THEN EXIT
    THEN
    _OAIE-KIND @ OAIEV-K-ARGUMENTS-DELTA = IF
        _OAIE-A @ _OAIE-U @ S" delta"
        _OAIE-E @ OAIEV.DELTA-BUF OAIEV-DELTA-CAPACITY
        _OAIE-E @ OAIEV.DELTA-U _OAIE-STRING-FIELD!
        _OAIE-STATUS ! _OAIE-FOUND !
        _OAIE-STATUS @ ?DUP IF EXIT THEN
        _OAIE-FOUND @ 0= IF OAIEV-S-FIELD ELSE OAIEV-S-OK THEN EXIT
    THEN
    _OAIE-KIND @ OAIEV-K-ARGUMENTS-DONE = IF
        _OAIE-A @ _OAIE-U @ S" arguments"
        _OAIE-E @ OAIEV.ARGUMENTS-BUF OAIEV-ARGUMENTS-CAPACITY
        _OAIE-E @ OAIEV.ARGUMENTS-U _OAIE-STRING-FIELD!
        _OAIE-STATUS ! _OAIE-FOUND !
        _OAIE-STATUS @ ?DUP IF EXIT THEN
        _OAIE-FOUND @ 0= IF OAIEV-S-FIELD ELSE OAIEV-S-OK THEN EXIT
    THEN
    _OAIE-KIND @ OAIEV-K-OUTPUT-ADDED = IF _OAIE-PARSE-ITEM EXIT THEN
    _OAIE-KIND @ OAIEV-K-OUTPUT-DONE = IF _OAIE-PARSE-ITEM EXIT THEN
    _OAIE-KIND @ OAIEV-K-RESPONSE-METADATA = IF _OAIE-TURN-STATE EXIT THEN
    _OAIE-KIND @ OAIEV-K-RESPONSE-FAILED = IF _OAIE-ERROR-MESSAGE EXIT THEN
    _OAIE-KIND @ OAIEV-K-RESPONSE-INCOMPLETE = IF _OAIE-ERROR-MESSAGE EXIT THEN
    _OAIE-KIND @ OAIEV-K-ERROR = IF _OAIE-ERROR-MESSAGE EXIT THEN
    OAIEV-S-OK ;
