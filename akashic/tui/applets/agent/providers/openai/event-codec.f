: _OAIE-RESPONSE-ID  ( -- status )
    _OAIE-A @ _OAIE-U @ S" response_id"
    _OAIE-E @ OAIEV.RESPONSE-BUF OAIEV-RESPONSE-ID-CAPACITY
    _OAIE-E @ OAIEV.RESPONSE-U _OAIE-STRING-FIELD!
    _OAIE-STATUS ! _OAIE-FOUND !
    _OAIE-STATUS @ ?DUP IF EXIT THEN
    _OAIE-FOUND @ IF OAIEV-S-OK EXIT THEN
    _OAIE-A @ _OAIE-U @ S" response" _OAIE-OBJECT-FIELD
    _OAIE-STATUS ! _OAIE-FOUND ! _OAIE-RU ! _OAIE-RA !
    _OAIE-STATUS @ ?DUP IF EXIT THEN
    _OAIE-FOUND @ 0= IF OAIEV-S-OK EXIT THEN
    _OAIE-RA @ _OAIE-RU @ S" id"
    _OAIE-E @ OAIEV.RESPONSE-BUF OAIEV-RESPONSE-ID-CAPACITY
    _OAIE-E @ OAIEV.RESPONSE-U _OAIE-STRING-FIELD!
    _OAIE-STATUS ! _OAIE-FOUND !
    _OAIE-STATUS @ ?DUP IF EXIT THEN
    OAIEV-S-OK ;

: _OAIE-OUTPUT-INDEX  ( -- status )
    _OAIE-A @ _OAIE-U @ S" output_index" JSON-FIELD
    _OAIEF-IOR ! _OAIEF-FOUND ! _OAIEF-VU ! _OAIEF-VA !
    _OAIEF-IOR @ IF OAIEV-S-FIELD EXIT THEN
    _OAIEF-FOUND @ 0= IF OAIEV-S-OK EXIT THEN
    _OAIEF-VA @ _OAIEF-VU @ JSON-NUMBER? 0= IF OAIEV-S-TYPE EXIT THEN
    _OAIEF-VA @ _OAIEF-VU @ JSON-GET-NUMBER
    _OAIE-E @ OAIEV.OUTPUT-INDEX !
    OAIEV-S-OK ;

: _OAIE-PARSE-ITEM  ( -- status )
    _OAIE-A @ _OAIE-U @ S" item"
    _OAIE-E @ OAIEV.ITEM-BUF OAIEV-ITEM-CAPACITY
    _OAIE-E @ OAIEV.ITEM-U _OAIE-RAW-FIELD!
    _OAIE-STATUS ! _OAIE-FOUND !
    _OAIE-STATUS @ ?DUP IF EXIT THEN
    _OAIE-FOUND @ 0= IF OAIEV-S-FIELD EXIT THEN
    _OAIE-E @ OAIEV-ITEM 2DUP JSON-OBJECT? 0= IF 2DROP OAIEV-S-TYPE EXIT THEN
    S" type" _OAIE-ITEM-TYPE-BUF 64 _OAIE-ITEM-TYPE-U
    _OAIE-STRING-FIELD! _OAIE-STATUS ! _OAIE-FOUND !
    _OAIE-STATUS @ ?DUP IF EXIT THEN
    _OAIE-FOUND @ 0= IF OAIEV-S-FIELD EXIT THEN
    _OAIE-ITEM-TYPE-BUF _OAIE-ITEM-TYPE-U @ S" function_call" STR-STR= 0= IF
        OAIEV-S-OK EXIT
    THEN
    _OAIE-E @ OAIEV-ITEM S" call_id"
    _OAIE-E @ OAIEV.CALL-BUF OAIEV-CALL-ID-CAPACITY
    _OAIE-E @ OAIEV.CALL-U _OAIE-STRING-FIELD!
    _OAIE-STATUS ! _OAIE-FOUND !
    _OAIE-STATUS @ ?DUP IF EXIT THEN
    _OAIE-FOUND @ 0= IF OAIEV-S-FIELD EXIT THEN
    _OAIE-E @ OAIEV-ITEM S" name"
    _OAIE-E @ OAIEV.NAME-BUF OAIEV-NAME-CAPACITY
    _OAIE-E @ OAIEV.NAME-U _OAIE-STRING-FIELD!
    _OAIE-STATUS ! _OAIE-FOUND !
    _OAIE-STATUS @ ?DUP IF EXIT THEN
    _OAIE-FOUND @ 0= IF OAIEV-S-FIELD EXIT THEN
    _OAIE-E @ OAIEV-ITEM S" arguments"
    _OAIE-E @ OAIEV.ARGUMENTS-BUF OAIEV-ARGUMENTS-CAPACITY
    _OAIE-E @ OAIEV.ARGUMENTS-U _OAIE-STRING-FIELD!
    _OAIE-STATUS ! _OAIE-FOUND !
    _OAIE-STATUS @ ?DUP IF EXIT THEN
    _OAIE-FOUND @ 0= IF OAIEV-S-FIELD ELSE OAIEV-S-OK THEN ;

