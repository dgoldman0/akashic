\ =====================================================================
\  source.f - Owned construction environment for the OpenAI provider
\ =====================================================================
\  This source owns the credential adapter and concrete KDOS TLS transport
\  that must outlive an OpenAI Responses provider. It does not provision
\  trust roots, persist credentials, or depend on Desk or any TUI module.
\ =====================================================================

PROVIDED akashic-agent-openai-source

REQUIRE ../../provider-source.f
REQUIRE responses.f
REQUIRE ../../auth/api-key.f
REQUIRE ../../../../../net/transports/kdos-tls.f

0 CONSTANT _OAISRC-SOURCE
_OAISRC-SOURCE AGENT-PROVIDER-SOURCE-SIZE + CONSTANT _OAISRC-CONFIG
_OAISRC-CONFIG OPENAI-CONFIG-SIZE + CONSTANT _OAISRC-CREDENTIAL
_OAISRC-CREDENTIAL CREDENTIAL-SIZE + CONSTANT _OAISRC-AUTH
_OAISRC-AUTH APIKEY-AUTH-SIZE + CONSTANT _OAISRC-TRANSPORT
_OAISRC-TRANSPORT KDOSTLS-SIZE + CONSTANT OPENAI-SOURCE-SIZE

: OPENAI-SOURCE-CONFIG  ( source -- config )
    _OAISRC-CONFIG + ;

: OPENAI-SOURCE-CREDENTIAL  ( source -- credential )
    _OAISRC-CREDENTIAL + ;

: OPENAI-SOURCE-AUTH  ( source -- adapter )
    _OAISRC-AUTH + ;

: OPENAI-SOURCE-TRANSPORT  ( source -- adapter )
    _OAISRC-TRANSPORT + ;

VARIABLE _OAISCFG-SOURCE
VARIABLE _OAISCFG-STATUS

: _OPENAI-SOURCE-CONFIGURE  ( source -- status )
    DUP _OAISCFG-SOURCE !
    OPENAI-SOURCE-CONFIG OAIC-HOST
    _OAISCFG-SOURCE @ OPENAI-SOURCE-CONFIG OAIC.PORT @
    _OAISCFG-SOURCE @ OPENAI-SOURCE-TRANSPORT KDOSTLS-CONFIGURE ;

: _OPENAI-SOURCE-CREATE  ( source -- provider status )
    DUP _OAISCFG-SOURCE ! _OPENAI-SOURCE-CONFIGURE ?DUP IF
        DROP 0 APSOURCE-S-INVALID EXIT
    THEN
    _OAISCFG-SOURCE @ OPENAI-SOURCE-CONFIG
    _OAISCFG-SOURCE @ OPENAI-SOURCE-AUTH APIKEY-AUTH.PORT
    _OAISCFG-SOURCE @ OPENAI-SOURCE-TRANSPORT KDOSTLS.PORT
    OPENAI-PROVIDER-NEW ;

: _OPENAI-SOURCE-CANCEL-TRANSPORT  ( source -- )
    OPENAI-SOURCE-TRANSPORT KDOSTLS.PORT NIO-CANCEL
    NIO-S-CANCELLED <> IF KDOSTLS-E-CLEANUP NEGATE THROW THEN ;

: OPENAI-SOURCE-FREE  ( source -- )
    DUP 0= IF DROP EXIT THEN
    DUP _OPENAI-SOURCE-CANCEL-TRANSPORT
    DUP OPENAI-SOURCE-AUTH APIKEY-AUTH.PORT AAUTH-DESTROY
    DUP OPENAI-SOURCE-CREDENTIAL CRED-CLEAR
    DUP OPENAI-SOURCE-SIZE 0 FILL
    FREE ;

VARIABLE _OAISNEW-SOURCE

: OPENAI-SOURCE-NEW  ( -- source status )
    OPENAI-SOURCE-SIZE ALLOCATE
    DUP IF 2DROP 0 APSOURCE-S-NOMEM EXIT THEN
    DROP DUP _OAISNEW-SOURCE ! OPENAI-SOURCE-SIZE 0 FILL
    _OAISNEW-SOURCE @ APSOURCE-INIT
    _OAISNEW-SOURCE @ OPENAI-SOURCE-CONFIG OAIC-INIT
    _OAISNEW-SOURCE @ OPENAI-SOURCE-CREDENTIAL CRED-INIT
    _OAISNEW-SOURCE @ OPENAI-SOURCE-CREDENTIAL
    _OAISNEW-SOURCE @ OPENAI-SOURCE-AUTH APIKEY-AUTH-INIT
    DROP
    _OAISNEW-SOURCE @ OPENAI-SOURCE-TRANSPORT KDOSTLS-INIT
    _OAISNEW-SOURCE @ _OPENAI-SOURCE-CONFIGURE DUP _OAISCFG-STATUS ! IF
        _OAISNEW-SOURCE @ OPENAI-SOURCE-FREE
        0 _OAISCFG-STATUS @ EXIT
    THEN
    _OAISNEW-SOURCE @ _OAISNEW-SOURCE @ APSOURCE.CONTEXT !
    S" org.akashic.agent.source.openai"
    _OAISNEW-SOURCE @ APSOURCE.ID-U ! _OAISNEW-SOURCE @ APSOURCE.ID-A !
    ['] _OPENAI-SOURCE-CREATE _OAISNEW-SOURCE @ APSOURCE.NEW-XT !
    ['] OPENAI-SOURCE-FREE _OAISNEW-SOURCE @ APSOURCE.FREE-XT !
    _OAISNEW-SOURCE @ APSOURCE-S-OK ;
