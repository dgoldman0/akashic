: _OAIR-POLL-RECEIVING  ( -- status )
    _OAIRPOL-S @ DUP OAIR-S.HTTP
    _OAIRPOL-C @ OAIR-C.PORT @ ROT OAIR-S.RECV
    OAIR-RECV-CAPACITY HSTR-PUMP
    _OAIRPOL-PUMP !
    _OAIRPOL-PUMP @ _OAIRPOL-C @ OAIR-C.LAST-STATUS !
    _OAIRPOL-C @ OAIR-C.STATE @ OAIR-STATE-ERROR = IF
        _OAIRPOL-C @ _OAIR-DROP-SESSION ?DUP IF EXIT THEN
        OAIR-STATE-IDLE _OAIRPOL-C @ OAIR-C.STATE !
        OAIR-S-PROTOCOL EXIT
    THEN
    _OAIRPOL-PUMP @ DUP HSTR-PUMP-IDLE =
    SWAP HSTR-PUMP-PROGRESS = OR IF OAIR-S-OK EXIT THEN
    _OAIRPOL-PUMP @ HSTR-PUMP-DONE <> IF
        _OAIRPOL-PUMP @ HSTR-PUMP-PARSER-ERROR = IF
            S" Responses response parser failed"
        ELSE
            S" Responses transport failed"
        THEN
        _OAIRPOL-C @ _OAIR-MARK-ERROR DROP
        _OAIRPOL-C @ _OAIR-DROP-SESSION ?DUP IF EXIT THEN
        OAIR-STATE-IDLE _OAIRPOL-C @ OAIR-C.STATE !
        _OAIRPOL-PUMP @ HSTR-PUMP-PARSER-ERROR = IF
            OAIR-S-PROTOCOL
        ELSE
            OAIR-S-TRANSPORT
        THEN
        EXIT
    THEN
    _OAIR-POLL-RESPONSE-DONE ;

: _OAIR-POLL  ( queue context -- status )
    _OAIRPOL-C ! _OAIRPOL-Q !
    _OAIRPOL-C @ OAIR-C.SESSION @ DUP 0= IF DROP OAIR-S-OK EXIT THEN
    _OAIRPOL-S !
    _OAIRPOL-C @ OAIR-C.STATE @ OAIR-STATE-OPENING = IF
        _OAIR-POLL-OPENING EXIT
    THEN
    _OAIRPOL-C @ OAIR-C.STATE @ OAIR-STATE-CLOSING = IF
        _OAIR-POLL-CLOSING EXIT
    THEN
    _OAIRPOL-C @ OAIR-C.STATE @ OAIR-STATE-ERROR = IF
        OAIR-S-TRANSPORT EXIT
    THEN
    _OAIRPOL-C @ OAIR-C.STATE @ OAIR-STATE-WAITING-AUTH = IF
        _OAIR-POLL-WAITING-AUTH EXIT
    THEN
    _OAIRPOL-C @ OAIR-C.STATE @ OAIR-STATE-WAITING-TOOL = IF
        _OAIR-POLL-WAITING EXIT
    THEN
    _OAIRPOL-C @ OAIR-C.STATE @ OAIR-STATE-SENDING = IF
        _OAIR-POLL-SENDING EXIT
    THEN
    _OAIRPOL-C @ OAIR-C.STATE @ OAIR-STATE-RECEIVING = IF
        _OAIR-POLL-RECEIVING EXIT
    THEN
    OAIR-S-OK ;

: OPENAI-PROVIDER-CONFIG  ( provider -- config | 0 )
    DUP 0= IF EXIT THEN APROV.CONTEXT @ OAIR-C.CONFIG ;

: OPENAI-PROVIDER-HEADERS!  ( callback callback-context provider -- )
    APROV.CONTEXT @ >R
    R@ OAIR-C.HEADERS-CONTEXT ! R> OAIR-C.HEADERS-XT ! ;

: OPENAI-PROVIDER-FREE  ( provider -- )
    DUP 0= IF DROP EXIT THEN
    DUP APROV.CONTEXT @ ?DUP IF
        DUP OAIR-C.SESSION @ IF
            DUP _OAIR-DROP-SESSION IF 2DROP EXIT THEN
        THEN
        DUP OAIR-C.EVENT AEV-FREE
        DUP OPENAI-PROVIDER-CONTEXT-SIZE 0 FILL FREE
    THEN
    FREE ;

