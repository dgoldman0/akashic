: _CDA-START-REQUEST  ( path-a path-u content-type-a content-type-u context -- status )
    _CDA-BUILD-C ! _CDA-BUILD-TYPE-U ! _CDA-BUILD-TYPE-A !
    _CDA-BUILD-PATH-U ! _CDA-BUILD-PATH-A !
    _CDA-BUILD-C @ CDA.REQUEST HREQ-CLEAR
    S" POST" _CDA-BUILD-PATH-A @ _CDA-BUILD-PATH-U @
    _CDA-BUILD-C @ CDA.REQUEST HREQ-BEGIN IF AAUTH-S-PROTOCOL EXIT THEN
    CODEX-AUTH-HOST _CDA-BUILD-C @ CDA.REQUEST HREQ-HOST
    IF AAUTH-S-PROTOCOL EXIT THEN
    _CDA-BUILD-TYPE-A @ _CDA-BUILD-TYPE-U @
    _CDA-BUILD-C @ CDA.REQUEST HREQ-CONTENT-TYPE
    IF AAUTH-S-PROTOCOL EXIT THEN
    S" application/json" _CDA-BUILD-C @ CDA.REQUEST HREQ-ACCEPT
    IF AAUTH-S-PROTOCOL EXIT THEN
    S" Akashic/1" _CDA-BUILD-C @ CDA.REQUEST HREQ-USER-AGENT
    IF AAUTH-S-PROTOCOL EXIT THEN
    _CDA-BUILD-C @ CDA.REQUEST-BODY
    _CDA-BUILD-C @ CDA.REQUEST-BODY-U @
    _CDA-BUILD-C @ CDA.REQUEST HREQ-BODY
    IF AAUTH-S-CAPACITY EXIT THEN
    _CDA-BUILD-C @ CDA.REQUEST _CDA-BUILD-C @ CDA.PORT @
    _CDA-BUILD-C @ CDA.EXCHANGE HBUF-START
    DUP HBUF-S-PENDING = IF DROP AAUTH-S-PENDING ELSE
        DROP AAUTH-S-TRANSPORT
    THEN ;

: _CDA-BUILD-USER-CODE  ( context -- status )
    _CDA-C !
    JSON-BUILD-RESET _CDA-C @ CDA.REQUEST-BODY CDA-REQUEST-BODY-CAPACITY
    JSON-SET-OUTPUT
    JSON-{ S" client_id" JSON-KEY: CODEX-AUTH-CLIENT-ID JSON-ESTR JSON-}
    JSON-OUTPUT-OK? 0= IF AAUTH-S-CAPACITY EXIT THEN
    JSON-OUTPUT-RESULT NIP _CDA-C @ CDA.REQUEST-BODY-U !
    CODEX-AUTH-USER-CODE-PATH S" application/json" _CDA-C @
    _CDA-START-REQUEST ;

: _CDA-TRANSIENT-CLEAR  ( context -- )
    DUP _CDA-C ! _CDA-WORK-FREE
    0 _CDA-C @ CDA.REQUEST-BODY-U !
    0 _CDA-C @ CDA.DEVICE-ID-U !
    0 _CDA-C @ CDA.SUBSTATE ! ;

: _CDA-BEGIN  ( context -- status )
    DUP _CDA-C ! CDA.AUTH DUP AAUTH.STATE @
    DUP AAUTH-STATE-STARTING = OVER AAUTH-STATE-PENDING = OR
    SWAP AAUTH-STATE-REFRESHING = OR IF DROP AAUTH-S-BUSY EXIT THEN
    DROP
    _CDA-C @ _CDA-WORK-NEW ?DUP IF
        S" Insufficient memory for device login" _CDA-C @ _CDA-FAIL EXIT
    THEN
    _CDA-C @ CDA.TOKENS O2TOK-CLEAR
    _CDA-C @ CDA.AUTH AAUTH-METADATA-CLEAR
    MS@ DUP _CDA-C @ CDA.STARTED-MS !
    CDA-LOGIN-TIMEOUT-MS + _CDA-C @ CDA.AUTH AAUTH.EXPIRES-MS !
    CDA-SUB-USER-CODE _CDA-C @ CDA.SUBSTATE !
    _CDA-C @ _CDA-BUILD-USER-CODE DUP AAUTH-S-PENDING <> IF
        S" Could not start device login" _CDA-C @ _CDA-FAIL EXIT
    THEN
    DROP AAUTH-S-PENDING AAUTH-STATE-STARTING _CDA-C @ _CDA-AUTH-TOUCH ;

