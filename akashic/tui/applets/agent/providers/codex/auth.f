: _CDA-WORK-FREE  ( context -- )
    DUP _CDA-C ! CDA.WORK @ ?DUP IF
        DUP _CDAW-EXCHANGE + HBUF-CANCEL DROP
        DUP _CDAW-REQUEST + HREQ-CLEAR
        DUP CODEX-DEVICE-WORK-SIZE 0 FILL FREE
        0 _CDA-C @ CDA.WORK !
    THEN ;

: _CDA-WORK-NEW  ( context -- status )
    DUP _CDA-C ! CDA.WORK @ IF AAUTH-S-OK EXIT THEN
    CODEX-DEVICE-WORK-SIZE ALLOCATE DUP IF
        2DROP AAUTH-S-CAPACITY EXIT
    THEN
    DROP DUP CODEX-DEVICE-WORK-SIZE 0 FILL
    DUP _CDA-C @ CDA.WORK ! DROP
    _CDA-C @ CDA.WIRE CDA-WIRE-CAPACITY _CDA-C @ CDA.REQUEST HREQ-INIT
    IF _CDA-C @ _CDA-WORK-FREE AAUTH-S-CAPACITY EXIT THEN
    _CDA-C @ CDA.RESPONSE CDA-RESPONSE-CAPACITY
    _CDA-C @ CDA.EXCHANGE HBUF-INIT HBUF-S-OK <> IF
        _CDA-C @ _CDA-WORK-FREE AAUTH-S-INVALID EXIT
    THEN
    -1 _CDA-C @ CDA.EXCHANGE HBUF-PERSISTENT!
    AAUTH-S-OK ;

: _CDA-AUTH-TOUCH  ( status state context -- status )
    _CDA-C ! >R DUP _CDA-C @ CDA.AUTH AAUTH.LAST-STATUS !
    R> _CDA-C @ CDA.AUTH AAUTH.STATE !
    1 _CDA-C @ CDA.AUTH AAUTH.REVISION +! ;

: _CDA-FAIL  ( status message-a message-u context -- status )
    _CDA-C ! _CDA-U ! _CDA-A ! _CDA-STATUS !
    _CDA-A @ _CDA-U @ _CDA-C @ CDA.AUTH AAUTH.ERROR CV-STRING! DROP
    _CDA-STATUS @ AAUTH-STATE-ERROR _CDA-C @ _CDA-AUTH-TOUCH ;

: _CDA-FIELD  ( object-a object-u key-a key-u -- value-a value-u flag )
    JSON-FIELD _CDA-IOR ! _CDA-FOUND ! _CDA-VU ! _CDA-VA !
    _CDA-IOR @ _CDA-FOUND @ 0= OR IF 0 0 0 EXIT THEN
    _CDA-VA @ _CDA-VU @ -1 ;

: _CDA-STRING-FIELD  ( object-a object-u key-a key-u -- value-a value-u flag )
    _CDA-FIELD 0= IF 2DROP 0 0 0 EXIT THEN
    2DUP JSON-TYPE? JSON-T-STRING <> IF 2DROP 0 0 0 EXIT THEN
    JSON-GET-STRING -1 ;

