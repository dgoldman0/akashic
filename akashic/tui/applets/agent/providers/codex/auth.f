: _CDA-HANDLE-USER-CODE  ( context -- status )
    DUP _CDA-C ! CDA.EXCHANGE HBUF.HTTP-CODE @ 200 <> IF
        AAUTH-S-PROTOCOL S" Device login is unavailable" _CDA-C @ _CDA-FAIL EXIT
    THEN
    _CDA-C @ CDA.RESPONSE _CDA-C @ CDA.EXCHANGE HBUF.BODY-U @
    2DUP JSON-VALID? 0= IF 2DROP
        AAUTH-S-PROTOCOL S" Invalid device login response" _CDA-C @ _CDA-FAIL EXIT
    THEN
    2DUP _CDA-OBJ-U ! _CDA-OBJ-A ! 2DROP
    _CDA-OBJ-A @ _CDA-OBJ-U @ S" device_auth_id" _CDA-STRING-FIELD
    0= IF 2DROP AAUTH-S-PROTOCOL S" Missing device authorization ID"
        _CDA-C @ _CDA-FAIL EXIT THEN
    DUP CDA-DEVICE-ID-CAPACITY > IF 2DROP AAUTH-S-CAPACITY
        S" Device authorization ID is too long" _CDA-C @ _CDA-FAIL EXIT THEN
    DUP _CDA-C @ CDA.DEVICE-ID-U !
    _CDA-C @ CDA.DEVICE-ID SWAP CMOVE
    _CDA-OBJ-A @ _CDA-OBJ-U @ S" user_code" _CDA-STRING-FIELD
    0= IF 2DROP AAUTH-S-PROTOCOL S" Missing device user code"
        _CDA-C @ _CDA-FAIL EXIT THEN
    _CDA-C @ CDA.AUTH AAUTH.USER-CODE CV-STRING! IF
        AAUTH-S-CAPACITY S" Device user code is too long" _CDA-C @ _CDA-FAIL EXIT
    THEN
    CODEX-AUTH-VERIFY-URI _CDA-C @ CDA.AUTH AAUTH.VERIFY-URI CV-STRING! DROP
    _CDA-OBJ-A @ _CDA-OBJ-U @ S" interval" _CDA-STRING-FIELD IF
        STR>NUM IF 1000 * ELSE DROP 5000 THEN
    ELSE
        2DROP 5000
    THEN
    1000 MAX _CDA-C @ CDA.AUTH AAUTH.POLL-INTERVAL-MS !
    MS@ _CDA-C @ CDA.NEXT-POLL-MS !
    CDA-SUB-IDLE _CDA-C @ CDA.SUBSTATE !
    AAUTH-S-PENDING AAUTH-STATE-PENDING _CDA-C @ _CDA-AUTH-TOUCH ;

: _CDA-HANDLE-TOKEN-POLL  ( context -- status )
    DUP _CDA-C ! CDA.EXCHANGE HBUF.HTTP-CODE @ DUP 403 = SWAP 404 = OR IF
        MS@ _CDA-C @ CDA.AUTH AAUTH.POLL-INTERVAL-MS @ +
        _CDA-C @ CDA.NEXT-POLL-MS !
        CDA-SUB-IDLE _CDA-C @ CDA.SUBSTATE !
        AAUTH-S-PENDING EXIT
    THEN
    _CDA-C @ CDA.EXCHANGE HBUF.HTTP-CODE @ 200 <> IF
        AAUTH-S-PROTOCOL S" Device authorization was rejected" _CDA-C @ _CDA-FAIL EXIT
    THEN
    _CDA-C @ CDA.RESPONSE _CDA-C @ CDA.EXCHANGE HBUF.BODY-U @
    2DUP S" authorization_code" _CDA-STRING-FIELD 0= IF
        2DROP 2DROP AAUTH-S-PROTOCOL S" Missing authorization code"
        _CDA-C @ _CDA-FAIL EXIT
    THEN
    _CDA-AUTHORIZATION-U ! _CDA-AUTHORIZATION-A !
    S" code_verifier" _CDA-STRING-FIELD 0= IF
        2DROP AAUTH-S-PROTOCOL S" Missing PKCE verifier" _CDA-C @ _CDA-FAIL EXIT
    THEN
    _CDA-AUTHORIZATION-A @ _CDA-AUTHORIZATION-U @
    2SWAP _CDA-C @ _CDA-START-TOKEN-EXCHANGE
    DUP AAUTH-S-PENDING <> IF
        S" Could not exchange device authorization" _CDA-C @ _CDA-FAIL
    THEN ;

: _CDA-HANDLE-TOKEN-EXCHANGE  ( context -- status )
    DUP _CDA-C ! CDA.EXCHANGE HBUF.HTTP-CODE @ 200 <> IF
        AAUTH-S-PROTOCOL S" Token exchange was rejected" _CDA-C @ _CDA-FAIL EXIT
    THEN
    _CDA-C @ CDA.RESPONSE _CDA-C @ CDA.EXCHANGE HBUF.BODY-U @
    2DUP S" id_token" _CDA-STRING-FIELD 0= IF
        2DROP 2DROP AAUTH-S-PROTOCOL S" Missing identity token" _CDA-C @ _CDA-FAIL EXIT
    THEN
    _CDA-ID-U ! _CDA-ID-A !
    2DUP S" access_token" _CDA-STRING-FIELD 0= IF
        2DROP 2DROP AAUTH-S-PROTOCOL S" Missing access token" _CDA-C @ _CDA-FAIL EXIT
    THEN
    _CDA-ACCESS-U ! _CDA-ACCESS-A !
    S" refresh_token" _CDA-STRING-FIELD 0= IF
        2DROP AAUTH-S-PROTOCOL S" Missing refresh token" _CDA-C @ _CDA-FAIL EXIT
    THEN
    _CDA-REFRESH-U ! _CDA-REFRESH-A !
    _CDA-ACCESS-A @ _CDA-ACCESS-U @ _CDA-C @ _CDA-ACCESS-EXPIRY
    _CDA-EXP-MS !
    _CDA-ID-A @ _CDA-ID-U @ _CDA-ACCESS-A @ _CDA-ACCESS-U @
    _CDA-REFRESH-A @ _CDA-REFRESH-U @ _CDA-EXP-MS @ _CDA-C @ CDA.TOKENS
    O2TOK-SET ?DUP IF
        DROP AAUTH-S-CAPACITY S" OAuth tokens exceed native bounds"
        _CDA-C @ _CDA-FAIL EXIT
    THEN
    _CDA-ID-A @ _CDA-ID-U @ _CDA-C @ _CDA-CLAIMS-METADATA ?DUP IF
        S" Identity token lacks Codex account claims" _CDA-C @ _CDA-FAIL EXIT
    THEN
    _CDA-C @ _CDA-WORK-FREE
    _CDA-C @ CDA.AUTH AAUTH.USER-CODE CV-FREE
    _CDA-C @ CDA.AUTH AAUTH.VERIFY-URI CV-FREE
    0 _CDA-C @ CDA.AUTH AAUTH.EXPIRES-MS !
    CDA-SUB-IDLE _CDA-C @ CDA.SUBSTATE !
    AAUTH-S-OK AAUTH-STATE-READY _CDA-C @ _CDA-AUTH-TOUCH ;

: _CDA-HANDLE-REFRESH  ( context -- status )
    DUP _CDA-C ! CDA.EXCHANGE HBUF.HTTP-CODE @ 200 <> IF
        _CDA-C @ CDA.TOKENS O2TOK-CLEAR
        AAUTH-S-PROTOCOL S" Session refresh was rejected" _CDA-C @ _CDA-FAIL EXIT
    THEN
    _CDA-C @ CDA.RESPONSE _CDA-C @ CDA.EXCHANGE HBUF.BODY-U @
    2DUP S" id_token" _CDA-STRING-FIELD IF
        _CDA-ID-U ! _CDA-ID-A !
    ELSE
        2DROP 0 _CDA-ID-A ! 0 _CDA-ID-U !
    THEN
    2DUP S" access_token" _CDA-STRING-FIELD IF
        _CDA-ACCESS-U ! _CDA-ACCESS-A !
    ELSE
        2DROP 0 _CDA-ACCESS-A ! 0 _CDA-ACCESS-U !
    THEN
    S" refresh_token" _CDA-STRING-FIELD IF
        _CDA-REFRESH-U ! _CDA-REFRESH-A !
    ELSE
        2DROP 0 _CDA-REFRESH-A ! 0 _CDA-REFRESH-U !
    THEN
    _CDA-ACCESS-U @ IF
        _CDA-ACCESS-A @ _CDA-ACCESS-U @ _CDA-C @ _CDA-ACCESS-EXPIRY
    ELSE
        0
    THEN _CDA-EXP-MS !
    _CDA-ID-A @ _CDA-ID-U @ _CDA-ACCESS-A @ _CDA-ACCESS-U @
    _CDA-REFRESH-A @ _CDA-REFRESH-U @ _CDA-EXP-MS @ _CDA-C @ CDA.TOKENS
    O2TOK-UPDATE ?DUP IF
        DROP AAUTH-S-CAPACITY S" Refreshed tokens exceed native bounds"
        _CDA-C @ _CDA-FAIL EXIT
    THEN
    _CDA-ID-U @ IF
        _CDA-ID-A @ _CDA-ID-U @ _CDA-C @ _CDA-CLAIMS-METADATA ?DUP IF
            S" Refreshed identity lacks Codex account claims"
            _CDA-C @ _CDA-FAIL EXIT
        THEN
    THEN
    _CDA-C @ _CDA-WORK-FREE
    CDA-SUB-IDLE _CDA-C @ CDA.SUBSTATE !
    AAUTH-S-OK AAUTH-STATE-READY _CDA-C @ _CDA-AUTH-TOUCH ;

: _CDA-POLL  ( context -- status )
    DUP _CDA-POLL-C ! CDA.AUTH DUP AAUTH.STATE @ AAUTH-STATE-PENDING = IF
        MS@ OVER AAUTH.EXPIRES-MS @ >= IF
            DROP AAUTH-S-EXPIRED S" Device login expired" _CDA-POLL-C @
            _CDA-FAIL EXIT
        THEN
        DROP
        _CDA-POLL-C @ CDA.SUBSTATE @ CDA-SUB-IDLE =
        MS@ _CDA-POLL-C @ CDA.NEXT-POLL-MS @ >= AND IF
            CDA-SUB-TOKEN-POLL _CDA-POLL-C @ CDA.SUBSTATE !
            _CDA-POLL-C @ _CDA-BUILD-TOKEN-POLL DUP AAUTH-S-PENDING <> IF
                S" Could not poll device authorization" _CDA-POLL-C @
                _CDA-FAIL EXIT
            THEN DROP
        THEN
    ELSE
        DROP
    THEN
    _CDA-POLL-C @ CDA.SUBSTATE @ CDA-SUB-IDLE = IF AAUTH-S-PENDING EXIT THEN
    _CDA-POLL-C @ CDA.EXCHANGE HBUF-POLL DUP HBUF-S-PENDING = IF
        DROP AAUTH-S-PENDING EXIT
    THEN
    DUP HBUF-S-OK <> IF
        DROP AAUTH-S-TRANSPORT S" Authentication transport failed"
        _CDA-POLL-C @ _CDA-FAIL EXIT
    THEN DROP
    _CDA-POLL-C @ CDA.SUBSTATE @ CASE
        CDA-SUB-USER-CODE OF _CDA-POLL-C @ _CDA-HANDLE-USER-CODE ENDOF
        CDA-SUB-TOKEN-POLL OF _CDA-POLL-C @ _CDA-HANDLE-TOKEN-POLL ENDOF
        CDA-SUB-TOKEN-EXCHANGE OF
            _CDA-POLL-C @ _CDA-HANDLE-TOKEN-EXCHANGE
        ENDOF
        CDA-SUB-REFRESH OF _CDA-POLL-C @ _CDA-HANDLE-REFRESH ENDOF
        AAUTH-S-PROTOCOL
    ENDCASE ;

: _CDA-CANCEL  ( context -- status )
    DUP _CDA-C ! _CDA-TRANSIENT-CLEAR
    _CDA-C @ CDA.AUTH AAUTH.USER-CODE CV-FREE
    _CDA-C @ CDA.AUTH AAUTH.VERIFY-URI CV-FREE
    0 _CDA-C @ CDA.AUTH AAUTH.EXPIRES-MS !
    AAUTH-S-CANCELLED AAUTH-STATE-SIGNED-OUT _CDA-C @ _CDA-AUTH-TOUCH ;

: _CDA-LOGOUT  ( context -- status )
    DUP _CDA-C ! _CDA-TRANSIENT-CLEAR
    _CDA-C @ CDA.TOKENS O2TOK-CLEAR
    _CDA-C @ CDA.AUTH AAUTH-METADATA-CLEAR
    AAUTH-S-OK AAUTH-STATE-SIGNED-OUT _CDA-C @ _CDA-AUTH-TOUCH ;

VARIABLE _CDA-WITH-CB
VARIABLE _CDA-WITH-CONTEXT
VARIABLE _CDA-WITH-C

: _CDA-WITH-ACCESS  ( callback callback-context context -- status )
    _CDA-WITH-C ! _CDA-WITH-CONTEXT ! _CDA-WITH-CB !
    _CDA-WITH-CB @ _CDA-WITH-CONTEXT @ _CDA-WITH-C @ CDA.TOKENS
    O2TOK-WITH-ACCESS DUP O2TOK-S-OK = IF DROP AAUTH-S-OK ELSE
        DROP AAUTH-S-INVALID
    THEN ;

VARIABLE _CDA-REFRESH-C

: _CDA-REFRESH-BODY  ( refresh-a refresh-u context -- status )
    _CDA-REFRESH-C ! _CDA-U ! _CDA-A !
    JSON-BUILD-RESET _CDA-REFRESH-C @ CDA.REQUEST-BODY
    CDA-REQUEST-BODY-CAPACITY JSON-SET-OUTPUT
    JSON-{
    S" client_id" JSON-KEY: CODEX-AUTH-CLIENT-ID JSON-ESTR
    S" grant_type" S" refresh_token" JSON-KV-ESTR
    S" refresh_token" JSON-KEY: _CDA-A @ _CDA-U @ JSON-ESTR
    JSON-}
    JSON-OUTPUT-OK? 0= IF AAUTH-S-CAPACITY EXIT THEN
    JSON-OUTPUT-RESULT NIP _CDA-REFRESH-C @ CDA.REQUEST-BODY-U !
    AAUTH-S-OK ;

: _CDA-REFRESH  ( context -- status )
    DUP _CDA-C ! CDA.AUTH AAUTH.STATE @ AAUTH-STATE-READY <> IF
        AAUTH-S-BUSY EXIT
    THEN
    _CDA-C @ _CDA-WORK-NEW ?DUP IF EXIT THEN
    ['] _CDA-REFRESH-BODY _CDA-C @ _CDA-C @ CDA.TOKENS
    O2TOK-WITH-REFRESH ?DUP IF
        DROP _CDA-C @ _CDA-WORK-FREE AAUTH-S-INVALID EXIT
    THEN
    CDA-SUB-REFRESH _CDA-C @ CDA.SUBSTATE !
    CODEX-AUTH-TOKEN-PATH S" application/json" _CDA-C @
    _CDA-START-REQUEST DUP AAUTH-S-PENDING <> IF
        _CDA-C @ _CDA-WORK-FREE EXIT
    THEN
    DROP AAUTH-S-PENDING AAUTH-STATE-REFRESHING _CDA-C @ _CDA-AUTH-TOUCH ;

: _CDA-DISPOSE  ( context -- )
    DUP _CDA-WORK-FREE
    DUP CDA.TOKENS O2TOK-CLEAR
    DROP ;

