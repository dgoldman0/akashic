: _LIBCR-C.SOURCE           ( context -- a ) _LIBCR-C-SOURCE + ;
: _LIBCR-C.CALLBACK-STATUS  ( context -- a ) _LIBCR-C-CALLBACK-STATUS + ;
: _LIBCR-C.CALLED           ( context -- a ) _LIBCR-C-CALLED + ;
: _LIBCR-C.PAYLOAD-A        ( context -- a ) _LIBCR-C-PAYLOAD-A + ;
: _LIBCR-C.PAYLOAD-U        ( context -- a ) _LIBCR-C-PAYLOAD-U + ;
: _LIBCR-C.TAG              ( context -- a ) _LIBCR-C-TAG + ;
: _LIBCR-C.CONTENT          ( context -- content ) _LIBCR-C-CONTENT + ;
: _LIBCR-C.DIGEST           ( context -- digest ) _LIBCR-C-DIGEST + ;

: _LIBCR-CONTEXT-RESET  ( -- )
    _LIBCR-CONTEXT _LIBCR-CONTEXT-SIZE 0 FILL ;

: _LIBCR-CREC-CAPTURE
  ( context-a context-u payload-a payload-u tag -- context )
    4 PICK _LIBCR-C.TAG !
    3 PICK _LIBCR-C.PAYLOAD-U !
    2 PICK _LIBCR-C.PAYLOAD-A !
    DROP ;

: _LIBCR-CREC-RESULT  ( context library-status -- checked-status )
    OVER _LIBCR-C.CALLBACK-STATUS !
    DUP _LIBCR-C.CALLBACK-STATUS @ LIB-S-OK = IF
        DROP CREC-S-OK
    ELSE
        DROP CREC-S-SEMANTIC
    THEN ;

: _LIBCR-FIXED-TAG?  ( payload-u tag -- flag )
    OVER LIB-CATALOG-PAYLOAD-SIZE = IF
        NIP LIB-CATALOG-RECORD-SIZE = EXIT
    THEN
    OVER LIB-COLLECTION-PAYLOAD-SIZE = IF
        NIP LIB-COLLECTION-RECORD-SIZE = EXIT
    THEN
    2DROP 0 ;

: _LIBCR-FIXED-PAYLOAD-VALID?  ( payload-a payload-u -- flag )
    DUP LIB-CATALOG-PAYLOAD-SIZE = IF
        DROP LIB-ENTRY-VALID? EXIT
    THEN
    DUP LIB-COLLECTION-PAYLOAD-SIZE = IF
        DROP LIB-COLLECTION-VALID? EXIT
    THEN
    2DROP 0 ;

: _LIBCR-FIXED-ENCODE-CALLBACK
  ( context-a context-u payload-a payload-u tag -- checked-status )
    _LIBCR-CREC-CAPTURE
    -1 OVER _LIBCR-C.CALLED !
    DUP _LIBCR-C.PAYLOAD-U @ OVER _LIBCR-C.TAG @
        _LIBCR-FIXED-TAG? 0=
    OVER _LIBCR-C.SOURCE @ 0= OR IF
        LIB-S-INVALID _LIBCR-CREC-RESULT EXIT
    THEN
    DUP _LIBCR-C.SOURCE @ OVER _LIBCR-C.PAYLOAD-A @
        2 PICK _LIBCR-C.PAYLOAD-U @ MOVE
    LIB-S-OK _LIBCR-CREC-RESULT ;

: _LIBCR-FIXED-VALIDATE-CALLBACK
  ( context-a context-u payload-a payload-u tag -- checked-status )
    _LIBCR-CREC-CAPTURE
    -1 OVER _LIBCR-C.CALLED !
    DUP _LIBCR-C.PAYLOAD-U @ OVER _LIBCR-C.TAG @
        _LIBCR-FIXED-TAG? 0= IF
        LIB-S-INVALID _LIBCR-CREC-RESULT EXIT
    THEN
    DUP _LIBCR-C.PAYLOAD-A @ OVER _LIBCR-C.PAYLOAD-U @
        _LIBCR-FIXED-PAYLOAD-VALID? 0= IF
        LIB-S-INVALID _LIBCR-CREC-RESULT EXIT
    THEN
    LIB-S-OK _LIBCR-CREC-RESULT ;

: _LIBCR-CONTENT-ENCODE-SHAPE?  ( context -- flag )
    DUP _LIBCR-C.PAYLOAD-U @ _LIBRC-CONTENT-PREFIX-SIZE < IF
        DROP 0 EXIT
    THEN
    DUP _LIBCR-C.SOURCE @ LIBCT.DATA-U @ _LIBRC-CONTENT-PREFIX-SIZE +
        OVER _LIBCR-C.PAYLOAD-U @ <> IF DROP 0 EXIT THEN
    DUP _LIBCR-C.PAYLOAD-U @ _LIBRC-ALIGN8 CREC-HEADER-SIZE +
        OVER _LIBCR-C.TAG @ <> IF DROP 0 EXIT THEN
    DROP -1 ;

: _LIBCR-CONTENT>PAYLOAD  ( context -- )
    DUP _LIBCR-C.SOURCE @ LIBCT.ID
        OVER _LIBCR-C.PAYLOAD-A @ RID-COPY
    DUP _LIBCR-C.SOURCE @ LIBCT.DOMAIN-REVISION @
        OVER _LIBCR-C.PAYLOAD-A @ _LIBCR-P-DOMAIN-REVISION + !
    DUP _LIBCR-C.SOURCE @ LIBCT.CONTENT-REVISION @
        OVER _LIBCR-C.PAYLOAD-A @ _LIBCR-P-CONTENT-REVISION + !
    DUP _LIBCR-C.SOURCE @ LIBCT.KIND @
        OVER _LIBCR-C.PAYLOAD-A @ _LIBCR-P-KIND + !
    DUP _LIBCR-C.SOURCE @ LIBCT.MEDIA @
        OVER _LIBCR-C.PAYLOAD-A @ _LIBCR-P-MEDIA + !
    DUP _LIBCR-C.SOURCE @ LIBCT.DIGEST
        OVER _LIBCR-C.PAYLOAD-A @ _LIBCR-P-DIGEST +
        LIB-DIGEST-SIZE MOVE
    DUP _LIBCR-C.SOURCE @ LIBCT-DATA$
        2 PICK _LIBCR-C.PAYLOAD-A @ _LIBRC-CONTENT-PREFIX-SIZE +
        SWAP MOVE
    DROP ;

: _LIBCR-CONTENT-ENCODE-CALLBACK
  ( context-a context-u payload-a payload-u tag -- checked-status )
    _LIBCR-CREC-CAPTURE
    -1 OVER _LIBCR-C.CALLED !
    DUP _LIBCR-CONTENT-ENCODE-SHAPE? 0= IF
        LIB-S-INVALID _LIBCR-CREC-RESULT EXIT
    THEN
    DUP _LIBCR-CONTENT>PAYLOAD
    LIB-S-OK _LIBCR-CREC-RESULT ;

: _LIBCR-PAYLOAD>CONTENT  ( context -- )
    DUP _LIBCR-C.CONTENT LIB-CONTENT-INIT
    DUP _LIBCR-C.PAYLOAD-A @
        OVER _LIBCR-C.CONTENT LIBCT.ID RID-COPY
    DUP _LIBCR-C.PAYLOAD-A @ _LIBCR-P-DOMAIN-REVISION + @
        OVER _LIBCR-C.CONTENT LIBCT.DOMAIN-REVISION !
    DUP _LIBCR-C.PAYLOAD-A @ _LIBCR-P-CONTENT-REVISION + @
        OVER _LIBCR-C.CONTENT LIBCT.CONTENT-REVISION !
    DUP _LIBCR-C.PAYLOAD-A @ _LIBCR-P-KIND + @
        OVER _LIBCR-C.CONTENT LIBCT.KIND !
    DUP _LIBCR-C.PAYLOAD-A @ _LIBCR-P-MEDIA + @
        OVER _LIBCR-C.CONTENT LIBCT.MEDIA !
    DUP _LIBCR-C.PAYLOAD-A @ _LIBRC-CONTENT-PREFIX-SIZE +
        OVER _LIBCR-C.CONTENT LIBCT.DATA-A !
    DUP _LIBCR-C.PAYLOAD-U @ _LIBRC-CONTENT-PREFIX-SIZE -
        OVER _LIBCR-C.CONTENT LIBCT.DATA-U !
    DUP _LIBCR-C.PAYLOAD-A @ _LIBCR-P-DIGEST +
        OVER _LIBCR-C.CONTENT LIBCT.DIGEST LIB-DIGEST-SIZE MOVE
    DROP ;

: _LIBCR-CONTENT-VALIDATE-CALLBACK
  ( context-a context-u payload-a payload-u tag -- checked-status )
    _LIBCR-CREC-CAPTURE
    -1 OVER _LIBCR-C.CALLED !
    DUP _LIBCR-C.PAYLOAD-U @ _LIBRC-CONTENT-PREFIX-SIZE <
    OVER _LIBCR-C.PAYLOAD-U @ _LIBRC-CONTENT-PAYLOAD-MAX > OR
    OVER _LIBCR-C.PAYLOAD-U @ _LIBRC-ALIGN8 CREC-HEADER-SIZE +
        2 PICK _LIBCR-C.TAG @ <> OR IF
        LIB-S-INVALID _LIBCR-CREC-RESULT EXIT
    THEN
    DUP _LIBCR-PAYLOAD>CONTENT
    DUP _LIBCR-C.CONTENT LIBCT-DATA$
        2 PICK _LIBCR-C.DIGEST SHA3-256-HASH
    DUP _LIBCR-C.DIGEST
        OVER _LIBCR-C.CONTENT LIBCT.DIGEST SHA3-256-COMPARE 0= IF
        LIB-S-INTEGRITY _LIBCR-CREC-RESULT EXIT
    THEN
    DUP _LIBCR-C.CONTENT LIB-CONTENT-VALID? 0= IF
        LIB-S-INVALID _LIBCR-CREC-RESULT EXIT
    THEN
    LIB-S-OK _LIBCR-CREC-RESULT ;

: _LIBCR-CREC>LIB  ( checked-status -- library-status )
    CASE
        CREC-S-OK OF LIB-S-OK ENDOF
        CREC-S-INVALID OF LIB-S-INVALID ENDOF
        CREC-S-CAPACITY OF LIB-S-CAPACITY ENDOF
        CREC-S-CHECKSUM OF LIB-S-CHECKSUM ENDOF
        CREC-S-UNSUPPORTED OF LIB-S-UNSUPPORTED ENDOF
        CREC-S-CORRUPT OF LIB-S-INVALID ENDOF
        CREC-S-SEMANTIC OF LIB-S-INVALID ENDOF
        LIB-S-INVALID SWAP
    ENDCASE ;

: _LIBCR-CREC-RESULT>LIB  ( checked-status -- library-status )
    DUP CREC-S-SEMANTIC =
    _LIBCR-CONTEXT _LIBCR-C.CALLED @ 0<> AND IF
        DROP _LIBCR-CONTEXT _LIBCR-C.CALLBACK-STATUS @ EXIT
    THEN
    _LIBCR-CREC>LIB ;

: _LIBCR-FIXED-ENCODE
  ( source destination capacity spec payload-u record-u validator -- u status )
    _LIBCR-VALIDATE-XT ! _LIBCR-RECORD-U ! _LIBCR-PAYLOAD-U !
    _LIBCR-SPEC ! _LIBCR-CAP ! _LIBCR-DEST ! _LIBCR-SOURCE !
    _LIBCR-SOURCE @ 0= IF 0 LIB-S-INVALID EXIT THEN
    _LIBCR-SOURCE @ _LIBCR-PAYLOAD-U @ MSPAN-NONWRAPPING? 0= IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBCR-SOURCE @ _LIBCR-VALIDATE-XT @ EXECUTE 0= IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBCR-DEST @ 0= _LIBCR-CAP @ 0< OR IF 0 LIB-S-INVALID EXIT THEN
    _LIBCR-CAP @ _LIBCR-RECORD-U @ < IF 0 LIB-S-CAPACITY EXIT THEN
    _LIBCR-DEST @ _LIBCR-RECORD-U @ MSPAN-NONWRAPPING? 0= IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBCR-SOURCE @ _LIBCR-PAYLOAD-U @
        _LIBCR-DEST @ _LIBCR-RECORD-U @ MSPAN-OVERLAP? IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBCR-CONTEXT-RESET
    _LIBCR-SOURCE @ _LIBCR-CONTEXT _LIBCR-C.SOURCE !
    _LIBCR-CONTEXT _LIBCR-CONTEXT-SIZE _LIBCR-PAYLOAD-U @
        _LIBCR-RECORD-U @ _LIBCR-DEST @ _LIBCR-CAP @
        _LIBCR-SPEC @ _LIBCR-WORK CREC-ENCODE
    _LIBCR-CREC-RESULT>LIB ;

: _LIBCR-FIXED-VALIDATE  ( record length spec -- status )
    _LIBCR-SPEC ! _LIBCR-U ! _LIBCR-REC !
    _LIBCR-CONTEXT-RESET
    _LIBCR-CONTEXT _LIBCR-CONTEXT-SIZE
        _LIBCR-REC @ _LIBCR-U @ _LIBCR-SPEC @ _LIBCR-WORK CREC-VALIDATE
    _LIBCR-CREC-RESULT>LIB ;

: _LIBCR-FIXED-DECODE  ( record length destination spec payload-u -- status )
    _LIBCR-PAYLOAD-U ! _LIBCR-SPEC ! _LIBCR-DEST !
    _LIBCR-U ! _LIBCR-REC !
    _LIBCR-DEST @ 0= IF LIB-S-INVALID EXIT THEN
    _LIBCR-DEST @ _LIBCR-PAYLOAD-U @ MSPAN-NONWRAPPING? 0= IF
        LIB-S-INVALID EXIT
    THEN
    _LIBCR-REC @ 0= IF
        _LIBCR-DEST @ _LIBCR-PAYLOAD-U @ 0 FILL LIB-S-INVALID EXIT
    THEN
    _LIBCR-U @ LIB-FIXED-HEADER-SIZE < IF
        _LIBCR-DEST @ _LIBCR-PAYLOAD-U @ 0 FILL LIB-S-INVALID EXIT
    THEN
    _LIBCR-REC @ _LIBCR-U @ MSPAN-NONWRAPPING? 0= IF
        _LIBCR-DEST @ _LIBCR-PAYLOAD-U @ 0 FILL LIB-S-INVALID EXIT
    THEN
    _LIBCR-REC @ _LIBCR-U @
        _LIBCR-DEST @ _LIBCR-PAYLOAD-U @ MSPAN-OVERLAP? IF
        LIB-S-INVALID EXIT
    THEN
    _LIBCR-DEST @ _LIBCR-PAYLOAD-U @ 0 FILL
    _LIBCR-REC @ _LIBCR-U @ _LIBCR-SPEC @ _LIBCR-FIXED-VALIDATE
        ?DUP IF EXIT THEN
    _LIBCR-WORK CREC-PAYLOAD$ _LIBCR-DEST @ SWAP MOVE
    LIB-S-OK ;

: _LIBCR-CATALOG-VALIDATE  ( record length -- status )
    _LIBCR-CATALOG-SPEC _LIBCR-FIXED-VALIDATE ;
: _LIBCR-COLLECTION-VALIDATE  ( record length -- status )
    _LIBCR-COLLECTION-SPEC _LIBCR-FIXED-VALIDATE ;

: _LIBCR-CATALOG-ENCODE  ( entry destination capacity -- length status )
    _LIBCR-CATALOG-SPEC LIB-CATALOG-PAYLOAD-SIZE
        LIB-CATALOG-RECORD-SIZE ['] LIB-ENTRY-VALID?
        _LIBCR-FIXED-ENCODE ;
: _LIBCR-COLLECTION-ENCODE  ( collection destination capacity -- length status )
    _LIBCR-COLLECTION-SPEC LIB-COLLECTION-PAYLOAD-SIZE
        LIB-COLLECTION-RECORD-SIZE ['] LIB-COLLECTION-VALID?
        _LIBCR-FIXED-ENCODE ;

: _LIBCR-CATALOG-DECODE  ( record length destination -- status )
    _LIBCR-CATALOG-SPEC LIB-CATALOG-PAYLOAD-SIZE _LIBCR-FIXED-DECODE ;
: _LIBCR-COLLECTION-DECODE  ( record length destination -- status )
    _LIBCR-COLLECTION-SPEC LIB-COLLECTION-PAYLOAD-SIZE
        _LIBCR-FIXED-DECODE ;

: _LIBCR-CONTENT-SOURCE?  ( -- flag )
    _LIBCR-SOURCE @ 0= IF 0 EXIT THEN
    _LIBCR-SOURCE @ LIB-CONTENT-SIZE MSPAN-NONWRAPPING? 0= IF 0 EXIT THEN
    _LIBCR-SOURCE @ LIBCT.DATA-U @ DUP 0< IF DROP 0 EXIT THEN
    DUP LIB-CONTENT-MAX > IF DROP 0 EXIT THEN
    DUP 0= IF DROP -1 EXIT THEN
    _LIBCR-SOURCE @ LIBCT.DATA-A @ DUP 0= IF DROP DROP 0 EXIT THEN
    SWAP MSPAN-NONWRAPPING? ;

: _LIBCR-CONTENT-VALIDATE  ( record length -- status )
    _LIBCR-U ! _LIBCR-REC !
    _LIBCR-CONTEXT-RESET
    _LIBCR-CONTEXT _LIBCR-CONTEXT-SIZE _LIBCR-REC @ _LIBCR-U @
        _LIBCR-CONTENT-SPEC _LIBCR-WORK CREC-VALIDATE
    _LIBCR-CREC-RESULT>LIB ;

: _LIBCR-CONTENT-ENCODE  ( content destination capacity -- length status )
    _LIBCR-CAP ! _LIBCR-DEST ! _LIBCR-SOURCE !
    _LIBCR-CONTENT-SOURCE? 0= IF 0 LIB-S-INVALID EXIT THEN
    _LIBCR-SOURCE @ LIB-CONTENT-VALID? 0= IF 0 LIB-S-INVALID EXIT THEN
    _LIBCR-SOURCE @ LIBCT.DATA-U @ _LIBRC-CONTENT-PREFIX-SIZE +
        _LIBCR-PAYLOAD-U !
    _LIBCR-SOURCE @ LIBCT.DATA-U @ LIB-CONTENT-RECORD-SIZE
        _LIBCR-RECORD-U !
    _LIBCR-DEST @ 0= _LIBCR-CAP @ 0< OR IF 0 LIB-S-INVALID EXIT THEN
    _LIBCR-CAP @ _LIBCR-RECORD-U @ < IF 0 LIB-S-CAPACITY EXIT THEN
    _LIBCR-DEST @ _LIBCR-RECORD-U @ MSPAN-NONWRAPPING? 0= IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBCR-SOURCE @ LIB-CONTENT-SIZE
        _LIBCR-DEST @ _LIBCR-RECORD-U @ MSPAN-OVERLAP? IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBCR-SOURCE @ LIBCT-DATA$
        _LIBCR-DEST @ _LIBCR-RECORD-U @ MSPAN-OVERLAP? IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBCR-CONTEXT-RESET
    _LIBCR-SOURCE @ _LIBCR-CONTEXT _LIBCR-C.SOURCE !
    _LIBCR-CONTEXT _LIBCR-CONTEXT-SIZE _LIBCR-PAYLOAD-U @
        _LIBCR-RECORD-U @ _LIBCR-DEST @ _LIBCR-CAP @
        _LIBCR-CONTENT-SPEC _LIBCR-WORK CREC-ENCODE
    _LIBCR-CREC-RESULT>LIB ;

: _LIBCR-CONTENT-DECODE  ( record length destination -- status )
    _LIBCR-DEST ! _LIBCR-U ! _LIBCR-REC !
    _LIBCR-DEST @ 0= IF LIB-S-INVALID EXIT THEN
    _LIBCR-DEST @ LIB-CONTENT-SIZE MSPAN-NONWRAPPING? 0= IF
        LIB-S-INVALID EXIT
    THEN
    _LIBCR-REC @ 0= IF
        _LIBCR-DEST @ LIB-CONTENT-INIT LIB-S-INVALID EXIT
    THEN
    _LIBCR-U @ LIB-CONTENT-HEADER-SIZE < IF
        _LIBCR-DEST @ LIB-CONTENT-INIT LIB-S-INVALID EXIT
    THEN
    _LIBCR-REC @ _LIBCR-U @ MSPAN-NONWRAPPING? 0= IF
        _LIBCR-DEST @ LIB-CONTENT-INIT LIB-S-INVALID EXIT
    THEN
    _LIBCR-REC @ _LIBCR-U @ _LIBCR-DEST @ LIB-CONTENT-SIZE
        MSPAN-OVERLAP? IF LIB-S-INVALID EXIT THEN
    _LIBCR-DEST @ LIB-CONTENT-INIT
    _LIBCR-REC @ _LIBCR-U @ _LIBCR-CONTENT-VALIDATE ?DUP IF EXIT THEN
    _LIBCR-CONTEXT _LIBCR-C.CONTENT _LIBCR-DEST @
        LIB-CONTENT-SIZE MOVE
    LIB-S-OK ;

: _LIBCR-CONTENT-MEASURE  ( header-a header-u -- record-u status )
    _LIBCR-U ! _LIBCR-REC !
    _LIBCR-REC @ 0= _LIBCR-U @ LIB-CONTENT-HEADER-SIZE < OR IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBCR-REC @ _LIBCR-U @ MSPAN-NONWRAPPING? 0= IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBCR-REC @ _LIBCR-U @ _LIBCR-CONTENT-SPEC _LIBCR-WORK
        CREC-INSPECT-HEADER DUP IF
        _LIBCR-CREC>LIB >R 0 R> EXIT
    THEN DROP
    _LIBCR-WORK CREC-TAG@ _LIBCR-WORK CREC-RECORD-U@ <> IF
        0 LIB-S-INVALID EXIT
    THEN
    _LIBCR-WORK CREC-RECORD-U@ LIB-S-OK ;

