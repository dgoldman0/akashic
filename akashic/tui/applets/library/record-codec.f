\ One guarded adapter context carries callback arguments, exact Library
\ status, the decoded transient content view, and digest scratch.  Passing
\ the complete object as CREC context makes the core reject hostile record
\ aliases against every callback-visible byte.

  0 CONSTANT _LIBCR-C-SOURCE
  8 CONSTANT _LIBCR-C-CALLBACK-STATUS
 16 CONSTANT _LIBCR-C-CALLED
 24 CONSTANT _LIBCR-C-PAYLOAD-A
 32 CONSTANT _LIBCR-C-PAYLOAD-U
