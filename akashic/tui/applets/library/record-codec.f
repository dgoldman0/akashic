: LIB-CONTENT-RECORD-SIZE  ( content-u -- record-u|-1 )
    DUP 0< IF DROP -1 EXIT THEN
    DUP LIB-CONTENT-MAX > IF DROP -1 EXIT THEN
    7 + -8 AND LIB-CONTENT-HEADER-SIZE + ;

