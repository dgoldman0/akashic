\ =====================================================================
\  projection-adapter.f - Library resource projection adapter
\ =====================================================================
\  One activation-local owner root retains at most eight Library RIDs.
\  Every acquisition returns a distinct non-durable lease token, even when
\  the RID is already published in RREG.  A slot is never retargeted or
\  evicted: final release unpublishes and destroys its component instance.
\
\  The root borrows one exact Library store, Context, CREG, RREG, and request
\  bus for its complete lifetime.  No operation consults VFS-CUR or discovers
\  a store by path.  Library domain revisions remain separate from CINST
\  revisions.
\
\  L12-DELETION: the adapter's direct LIBRARY-VFS-STORE-* dependency is the
\  temporary fixed-repository seam. L12 retargets the same projection policy
\  to the scalable Library service and deletes the old dependency.
\
\  Public surface:
\    LIBRARY-PROJECTION-OWNER$        ( -- a u )
\    LIBRARY-PROJECTION-CONTRACT$     ( -- a u )
\    LIBRARY-PROJECTION-ROOT-INIT
\      ( store context creg rreg bus root -- status )
\    LIBRARY-PROJECTION-ROOT-VALID?   ( root -- flag )
\    LIBRARY-PROJECTION-ROOT-LIVE@    ( root -- count )
\    LIBRARY-PROJECTION-ROOT-LEASES@  ( root -- count )
\    LIBRARY-PROJECTION-ROOT-FINI     ( root -- status )
\    LIBRARY-PROJECTION-ATTACH
\      ( locator root context rreg binding result -- status )
\
\  Root and attachment outputs are caller-owned buffers.  They must be
\  fully disjoint from dependency-private/global storage that is not
\  represented by a bounded runtime descriptor; the public boundaries also
\  reject every owner-private and reachable borrowed span they can enumerate.
\ =====================================================================

