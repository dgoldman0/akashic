    OVER _LPR-DESCRIBE-VIEW RCONDV.TITLE-A ! 2DROP
    _LPR-KIND @ LIB-KIND-MANAGED-DOCUMENT =
    _LPR-ROOT @ LPR.CONTEXT @ CTX-READONLY? 0= AND
    _LPR-ENTRY LIBE.LIFECYCLE @ LIB-LIFECYCLE-ACTIVE = AND
        _LPR-DESCRIBE-VIEW RCONDV.MUTABLE !
    _LPR-ENTRY LIBE.CONTENT-U @
        _LPR-DESCRIBE-VIEW RCONDV.SIZE !
    LIBRARY-PROJECTION-OWNER$
