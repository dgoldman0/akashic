: _LPR-SNAPSHOT-READ  ( -- status )
    \ Exact-current qualification already loaded and digest-validated these
    \ bytes.  A historical qualification instead proved retained metadata;
    \ go directly to the retained reader rather than issuing a known-conflict
    \ current read first.
    _LPR-QUALIFIED-CONTENT @ IF RACQ-S-OK EXIT THEN
    _LPR-SNAPSHOT-LOCATOR QLOC.DOMAIN-REVISION @
        _LPR-ENTRY LIBE.DOMAIN-REVISION @ < IF
        _LPR-RID-PTR @
            _LPR-SNAPSHOT-LOCATOR QLOC.DOMAIN-REVISION @
            _LPR-BYTES LIB-CONTENT-MAX _LPR-CONTENT
            _LPR-ROOT @ LPR.STORE @
            LIBRARY-VFS-STORE-READ-RETAINED-EXACT
        _LPR-STATUS ! _LPR-N !
        _LPR-STATUS @ _LPR-STORE>RACQ EXIT
    THEN
    _LPR-RID-PTR @
        _LPR-SNAPSHOT-LOCATOR QLOC.DOMAIN-REVISION @
        _LPR-BYTES LIB-CONTENT-MAX _LPR-RESULT-ENTRY _LPR-CONTENT
        _LPR-ROOT @ LPR.STORE @ LIBRARY-VFS-STORE-READ-EXACT
    _LPR-STATUS ! _LPR-N !
    _LPR-STATUS @ _LPR-STORE>RACQ ;

: _LPR-SNAPSHOT-BODY  ( -- status )
    _LPR-SNAPSHOT-OPERANDS! 0= IF CBUS-S-INVALID EXIT THEN
    _LPR-SNAPSHOT-LOCATOR _LPR-ROOT @ _LPR-QUALIFY
    DUP RACQ-S-OK <> IF NIP _LPR-QUALIFY>CBUS EXIT THEN
    DROP _LPR-KIND @ <> IF CBUS-S-FAILED EXIT THEN
    _LPR-SNAPSHOT-READ DUP RACQ-S-OK <> IF
        _LPR-QUALIFY>CBUS EXIT
    THEN DROP
    _LPR-CONTENT LIBCT.DIGEST
        _LPR-SNAPSHOT-LOCATOR QLOC.STATE-DIGEST SHA3-256-COMPARE 0= IF
        CBUS-S-STALE-REVISION EXIT
    THEN
    _LPR-SNAPSHOT-VIEW RCON-SNAPSHOT-VIEW-INIT
    _LPR-SNAPSHOT-LOCATOR _LPR-SNAPSHOT-VIEW RCONSV.LOCATOR !
    _LPR-CONTENT LIBCT.MEDIA @ _LPR-MEDIA$ 0= IF
        2DROP CBUS-S-FAILED EXIT
    THEN
    DUP _LPR-SNAPSHOT-VIEW RCONSV.MEDIA-U !
    OVER _LPR-SNAPSHOT-VIEW RCONSV.MEDIA-A ! 2DROP
    _LPR-CONTENT LIBCT-DATA$
    DUP _LPR-SNAPSHOT-VIEW RCONSV.CONTENT-U !
    OVER _LPR-SNAPSHOT-VIEW RCONSV.CONTENT-A ! 2DROP
    _LPR-CONTENT LIBCT.DIGEST
        _LPR-SNAPSHOT-VIEW RCONSV.CONTENT-DIGEST !
    _LPR-SNAPSHOT-VIEW _LPR-REQUEST @ CBR.RESULT
        _LPR-RESULT-WORKSPACE RCON-SNAPSHOT-RESULT! _LPR-HRESULT ;

: _LPR-REPLACE-BODY  ( -- status )
    _LPR-KIND @ LIB-KIND-MANAGED-DOCUMENT <> IF CBUS-S-DENIED EXIT THEN
    _LPR-ROOT @ LPR.CONTEXT @ CTX-READONLY? IF
        S" Library context is read-only" RACQ-S-UNAVAILABLE
            _LPR-REQUEST @ CBR-ERROR!
        CBUS-S-DENIED EXIT
    THEN
    _LPR-REPLACE-OPERANDS! 0= IF CBUS-S-INVALID EXIT THEN
    _LPR-REPLACE-LOCATOR _LPR-ROOT @ _LPR-QUALIFY
    DUP RACQ-S-OK <> IF NIP _LPR-QUALIFY>CBUS EXIT THEN
    DROP LIB-KIND-MANAGED-DOCUMENT <> IF CBUS-S-FAILED EXIT THEN
    _LPR-ENTRY LIBE.LIFECYCLE @ LIB-LIFECYCLE-ACTIVE <> IF
        CBUS-S-DENIED EXIT
    THEN
    _LPR-ENTRY LIBE.DOMAIN-REVISION @
        _LPR-REPLACE-LOCATOR QLOC.DOMAIN-REVISION @ <> IF
        CBUS-S-STALE-REVISION EXIT
    THEN
    _LPR-REPLACE-OPERANDS RCONRO-CONTENT$ _LPR-B ! _LPR-A !
    _LPR-B @ DUP 0< SWAP LIB-CONTENT-MAX > OR IF
        CBUS-S-INVALID EXIT
    THEN
    _LPR-B @ IF
        _LPR-A @ _LPR-B @ UTF8-VALID? 0= IF CBUS-S-INVALID EXIT THEN
    THEN
    _LPR-ENTRY LIBE.DOMAIN-REVISION @ 1+ _LPR-C !
    \ Allocate and populate the complete result before the durable commit.
    \ Once REPLACE-MANAGED succeeds, no fallible result allocation remains.
    LIBRARY-PROJECTION-OWNER$ _LPR-REF _LPR-C @
        _LPR-REPLACE-OPERANDS RCONRO.CONTENT-DIGEST
        QLOC-DK-PROJECTION-CONTENT
        LIBRARY-PROJECTION-CONTRACT$ _LPR-NEW-LOCATOR QLOC-EXACT!
        QLOC-S-OK <> IF CBUS-S-FAILED EXIT THEN
    _LPR-REPLACE-VIEW RCON-REPLACE-VIEW-INIT
    _LPR-REPLACE-LOCATOR _LPR-REPLACE-VIEW RCONRV.OLD-LOCATOR !
    _LPR-NEW-LOCATOR _LPR-REPLACE-VIEW RCONRV.NEW-LOCATOR !
    _LPR-REPLACE-OPERANDS RCONRO.CONTENT-DIGEST
        _LPR-REPLACE-VIEW RCONRV.CONTENT-DIGEST !
    _LPR-REPLACE-VIEW _LPR-REQUEST @ CBR.RESULT
        _LPR-RESULT-WORKSPACE RCON-REPLACE-RESULT!
    DUP RCON-S-OK <> IF _LPR-HRESULT EXIT THEN DROP
    _LPR-RID-PTR @ _LPR-ENTRY LIBE.DOMAIN-REVISION @
        _LPR-A @ _LPR-B @ _LPR-RESULT-ENTRY
        _LPR-ROOT @ LPR.STORE @ LIBRARY-VFS-STORE-REPLACE-MANAGED
    DUP LIBSTORE-S-OK <> IF _LPR-HSTORE-ERROR EXIT THEN DROP
    _LPR-RESULT-ENTRY LIBE.DOMAIN-REVISION @ _LPR-C @ <>
    _LPR-RESULT-ENTRY LIBE.CONTENT-DIGEST
        _LPR-REPLACE-OPERANDS RCONRO.CONTENT-DIGEST
        SHA3-256-COMPARE 0= OR IF
        CBUS-S-FAILED EXIT
    THEN
    CBUS-S-OK ;

: _LPR-RUN-HANDLER-LOCKED  ( request root rid tag body-xt -- status )
    _LPH-XT ! _LPR-KIND ! _LPR-RID-PTR ! _LPR-ROOT ! _LPR-REQUEST !
    _LPR-REQUEST @ 0= IF CBUS-S-INVALID EXIT THEN
    _LPR-ROOT @ _LPR-ROOT-HEADER? 0= IF CBUS-S-INVALID EXIT THEN
    _LPR-RID-PTR @ DUP 0= IF DROP CBUS-S-INVALID EXIT THEN
    RID-PRESENT? 0= IF CBUS-S-INVALID EXIT THEN
    _LPR-KIND @ _LPR-KIND-VALID? 0= IF CBUS-S-INVALID EXIT THEN
    _LPH-XT @ CATCH DUP IF
        _LPH-THROW !
        _LPR-SCRATCH-WIPE
        S" Library projection handler threw" _LPH-THROW @
            _LPR-REQUEST @ CBR-ERROR!
        CBUS-S-FAILED EXIT
    THEN
    DROP
    _LPR-SCRATCH-WIPE ;

\ The pool pins the component before Library takes its scratch guard.  RID,
\ tag, and owner are snapshotted while only the pool guard can be entered;
\ the inflight pin then keeps those immutable values live through END.  This
\ preserves pool -> Library as the sole nested lock order.
: _LPR-RUN-HANDLER  ( request instance body-xt -- status )
    >R
    2DUP ROPOOL-HANDLER-BEGIN 0= IF
        DROP 2DROP R> DROP CBUS-S-INVALID EXIT
    THEN
    SWAP DROP                         \ request member
    SWAP OVER                         \ member request member
    DUP ROPOOL-MEMBER-OWNER@          \ member request member root
    OVER ROPOOL-MEMBER-RID            \ member request member root rid
    ROT DROP                          \ member request root rid
    3 PICK ROPOOL-MEMBER-TAG@         \ member request root rid tag
    R>
    ['] _LPR-RUN-HANDLER-LOCKED _LPR-GUARD WITH-GUARD
    SWAP ROPOOL-HANDLER-END
    DUP RACQ-S-OK = IF DROP EXIT THEN
    2DROP CBUS-S-FAILED ;

