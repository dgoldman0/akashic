: _LPR-DESCRIBE-BODY  ( -- status )
    _LPR-RID-PTR @ _LPR-ENTRY
        _LPR-ROOT @ LPR.STORE @ LIBRARY-VFS-STORE-READ-IDENTITY
    DUP LIBSTORE-S-OK <> IF _LPR-HSTORE-ERROR EXIT THEN DROP
    _LPR-ENTRY LIBE.ID _LPR-RID-PTR @ RID= 0= IF
        CBUS-S-INVALID EXIT
    THEN
    _LPR-ENTRY LIBE.KIND @ _LPR-KIND @ <> IF
        CBUS-S-FAILED EXIT
    THEN
    _LPR-ENTRY LIBE.LIFECYCLE @ DUP LIB-LIFECYCLE-ACTIVE =
        SWAP LIB-LIFECYCLE-ARCHIVED = OR 0= IF
        CBUS-S-FAILED EXIT
    THEN
    _LPR-DESCRIBE-VIEW RCON-DESCRIBE-VIEW-INIT
    _LPR-RESULT-REFERENCE!
    _LPR-REF _LPR-DESCRIBE-VIEW RCONDV.RESOURCE !
    _LPR-ENTRY LIBE.DOMAIN-REVISION @
        _LPR-DESCRIBE-VIEW RCONDV.DOMAIN-REVISION !
    _LPR-ENTRY LIBE.MEDIA @ _LPR-MEDIA$ 0= IF
        2DROP CBUS-S-FAILED EXIT
    THEN
    DUP _LPR-DESCRIBE-VIEW RCONDV.MEDIA-U !
    OVER _LPR-DESCRIBE-VIEW RCONDV.MEDIA-A ! 2DROP
    _LPR-ENTRY LIBE.KIND @ _LPR-KIND$ 0= IF
        2DROP CBUS-S-FAILED EXIT
    THEN
    DUP _LPR-DESCRIBE-VIEW RCONDV.KIND-U !
    OVER _LPR-DESCRIBE-VIEW RCONDV.KIND-A ! 2DROP
    _LPR-ENTRY LIBE-TITLE$
