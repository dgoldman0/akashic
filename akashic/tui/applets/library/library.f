: _LAPP-ARENA-ID!  ( -- )
    S" org.akashic.library.applet-probe.arena.v1"
        _LAPP-ARENA-ID SHA3-256-HASH ;

: _LAPP-OWNER-OPEN  ( -- status )
    0 _LAPP-READY ! 0 _LAPP-STORE-INITIALIZED !
    VFS-CUR DUP 0= IF DROP LIBSTORE-S-IO _LAPP-SET-STATUS EXIT THEN
    _LAPP-VFS !
    _LAPP-VFS @ _LAPP-STORE LIBRARY-VFS-STORE-INIT
        _LAPP-SET-STATUS
    DUP IF EXIT THEN DROP
    -1 _LAPP-STORE-INITIALIZED !
    _LAPP-STORE LIBRARY-VFS-STORE-LOAD _LAPP-SET-STATUS
    DUP LIBSTORE-S-OK = IF -1 _LAPP-READY ! THEN ;

: _LAPP-ENSURE-PROVISIONED  ( -- status )
    _LAPP-PREVIEW-BUFFER @ 0= IF
        LIBSTORE-S-ALLOCATION _LAPP-SET-STATUS EXIT
    THEN
    _LAPP-READY? IF LIBSTORE-S-OK _LAPP-SET-STATUS EXIT THEN
    _LAPP-STORE-INITIALIZED @ 0= IF
        _LAPP-OWNER-OPEN DROP
        _LAPP-READY? IF LIBSTORE-S-OK _LAPP-SET-STATUS EXIT THEN
        _LAPP-STORE-INITIALIZED @ 0= IF
            _LAPP-LAST-STATUS @ DUP LIBSTORE-S-OK = IF
                DROP LIBSTORE-S-IO _LAPP-SET-STATUS
            THEN
            EXIT
        THEN
    THEN
    \ Last-status is presentation state, not an authority certificate.  A
    \ prepared retry reclassifies the owner before deciding whether first-use
    \ provisioning is safe; only a fresh ABSENT result permits provisioning.
    0 _LAPP-READY !
    _LAPP-STORE LIBRARY-VFS-STORE-LOAD _LAPP-SET-STATUS
    DUP LIBSTORE-S-OK = IF
        -1 _LAPP-READY ! EXIT
    THEN
    DUP LIBSTORE-S-ABSENT <> IF EXIT THEN DROP
    _LAPP-ARENA-ID _LAPP-STORE LIBRARY-VFS-STORE-PROVISION
        _LAPP-SET-STATUS
    DUP LIBSTORE-S-OK = IF -1 _LAPP-READY ! THEN ;

