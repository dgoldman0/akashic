: _LAPP-LOAD-SELECTION  ( -- status )
    _LAPP-ROW-COUNT @ 0= IF
        _LAPP-CLEAR-PREVIEW LIBSTORE-S-OK _LAPP-SET-STATUS EXIT
    THEN
    _LAPP-VIEW @ CASE
        _LAPP-V-COLLECTIONS OF _LAPP-LOAD-COLLECTION-PREVIEW ENDOF
        _LAPP-V-HISTORY OF _LAPP-LOAD-HISTORY-PREVIEW ENDOF
        _LAPP-LOAD-CORPUS-PREVIEW SWAP
    ENDCASE ;

: _LAPP-RUN-CORPUS-PAGE  ( -- status )
    _LAPP-READY? 0= IF
        _LAPP-CLEAR-PAGES _LAPP-CLEAR-PREVIEW
        _LAPP-LAST-STATUS @ EXIT
    THEN
    _LAPP-BUILD-QUERY DUP IF _LAPP-SET-STATUS EXIT THEN DROP
    _LAPP-QUERY _LAPP-CORPUS-PAGE LIBRARY-QUERY-PAGE-MAX _LAPP-STORE
        LIBRARY-VFS-STORE-QUERY-CORPUS
        _LAPP-SET-STATUS >R
    _LAPP-GENERATION ! _LAPP-NEXT-SLOT ! _LAPP-ROW-COUNT !
    R> DUP IF
        _LAPP-CLEAR-PREVIEW EXIT
    THEN DROP
    _LAPP-CLAMP-SELECTION
    _LAPP-LOAD-SELECTION DROP
    _LAPP-LAST-STATUS @ ;

: _LAPP-RUN-COLLECTION-PAGE  ( -- status )
    _LAPP-READY? 0= IF
        _LAPP-CLEAR-PAGES _LAPP-CLEAR-PREVIEW
        _LAPP-LAST-STATUS @ EXIT
    THEN
    _LAPP-GENERATION @
    _LAPP-PAGE-INDEX @ _LAPP-PAGE-START @
    _LAPP-COLLECTION-PAGE LIBRARY-QUERY-PAGE-MAX _LAPP-STORE
        LIBRARY-VFS-STORE-QUERY-COLLECTIONS
        _LAPP-SET-STATUS >R
    _LAPP-GENERATION ! _LAPP-NEXT-SLOT ! _LAPP-ROW-COUNT !
    R> DUP IF _LAPP-CLEAR-PREVIEW EXIT THEN DROP
    _LAPP-CLAMP-SELECTION
    _LAPP-LOAD-SELECTION DROP
    _LAPP-LAST-STATUS @ ;

: _LAPP-RUN-HISTORY  ( -- status )
    _LAPP-READY? 0= IF
        _LAPP-CLEAR-PAGES _LAPP-CLEAR-PREVIEW
        _LAPP-LAST-STATUS @ EXIT
    THEN
    _LAPP-HISTORY-ID _LAPP-HISTORY-CURRENT @
        _LAPP-HISTORY LIB-RETAINED-REVISION-MAX _LAPP-STORE
        LIBRARY-VFS-STORE-LIST-RETAINED-REVISIONS
        _LAPP-SET-STATUS >R
    R> DUP IF
        NIP 0 _LAPP-ROW-COUNT ! _LAPP-CLEAR-PREVIEW EXIT
    THEN DROP
    _LAPP-ROW-COUNT !
    0 _LAPP-PAGE-INDEX ! -1 _LAPP-NEXT-SLOT !
    _LAPP-CLAMP-SELECTION
    _LAPP-LOAD-SELECTION DROP
    _LAPP-LAST-STATUS @ ;

: _LAPP-RUN-PAGE  ( -- status )
    _LAPP-VIEW @ _LAPP-V-HISTORY = IF
        _LAPP-RUN-HISTORY EXIT
    THEN
    _LAPP-VIEW @ _LAPP-V-COLLECTIONS = IF
        _LAPP-RUN-COLLECTION-PAGE
    ELSE
        _LAPP-RUN-CORPUS-PAGE
    THEN ;

: _LAPP-RESET-PAGE  ( -- status )
    _LAPP-CLEAR-PAGES _LAPP-RUN-PAGE ;

: _LAPP-RELOAD  ( -- status )
    _LAPP-PREVIEW-BUFFER @ 0= IF
        LIBSTORE-S-ALLOCATION _LAPP-SET-STATUS EXIT
    THEN
    _LAPP-STORE-INITIALIZED @ 0= IF
        _LAPP-OWNER-OPEN
        DUP LIBSTORE-S-OK = IF DROP _LAPP-RESET-PAGE EXIT THEN
        _LAPP-CLEAR-PAGES _LAPP-CLEAR-PREVIEW EXIT
    THEN
    0 _LAPP-READY !
    _LAPP-STORE LIBRARY-VFS-STORE-LOAD _LAPP-SET-STATUS
    DUP LIBSTORE-S-OK = IF
        -1 _LAPP-READY ! DROP _LAPP-RESET-PAGE EXIT
    THEN
    _LAPP-CLEAR-PAGES _LAPP-CLEAR-PREVIEW ;

: _LAPP-SHOW-STATUS-TOAST  ( -- )
    _LAPP-LAST-STATUS @ _LAPP-STATUS$ 2400 ASHELL-TOAST ;

: _LAPP-REFRESH-AFTER-MUTATION  ( -- )
    _LAPP-TOUCH
    0 _LAPP-GENERATION !
    _LAPP-RESET-PAGE DUP IF DROP _LAPP-SHOW-STATUS-TOAST ELSE DROP THEN
    _LAPP-INVALIDATE ;

