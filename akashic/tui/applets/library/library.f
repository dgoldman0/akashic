        LIB-OPERATION-KEY-SIZE RNG-BYTES
    _LAPP-CREATE-REQUEST LIBMCR.OPERATION-KEY
        _LAPP-CREATE-REQUEST LIBRARY-MANAGED-CREATE-OPERATION-KEY!
        DUP IF EXIT THEN DROP
    _LAPP-STORE LIBRARY-VFS-STORE.GENERATION @
        _LAPP-CREATE-REQUEST LIBMCR.EXPECTED-CATALOG-GENERATION !
    LIB-MEDIA-TEXT-PLAIN _LAPP-CREATE-REQUEST LIBMCR.MEDIA !
    _LAPP-PENDING-TITLE _LAPP-PENDING-TITLE-U @
        _LAPP-CREATE-REQUEST LIBRARY-MANAGED-CREATE-TITLE!
