    _LAPP-PENDING-BODY _LAPP-PENDING-BODY-U @
        _LAPP-CREATE-REQUEST LIBRARY-MANAGED-CREATE-CONTENT!
        DUP IF EXIT THEN DROP
    _LAPP-CREATE-REQUEST LIBRARY-MANAGED-CREATE-REQUEST-VALID? 0= IF
        LIBSTORE-S-INVALID EXIT
    THEN
    _LAPP-CREATE-PREPARED _LAPP-PENDING-CREATE !
