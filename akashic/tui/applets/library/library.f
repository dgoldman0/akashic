        3 _LAPP-PV-COL @ 1+ DRW-TEXT-UNTRUSTED
    244 234 0 DRW-STYLE!
    _LAPP-VIEW @ _LAPP-V-COLLECTIONS = IF
        S" Collection  |  members " 4 _LAPP-PV-COL @ 1+ DRW-TEXT
        _LAPP-COLLECTION-VIEW LIBCV.MEMBER-N @ NUM>STR
            4 _LAPP-PV-COL @ 25 + DRW-TEXT
