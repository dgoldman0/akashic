    _LAPP-CAPTURE-CORPUS-TARGET 0= IF
        LIBSTORE-S-NOT-FOUND _LAPP-SET-STATUS EXIT
    THEN
    _LAPP-TARGET-ID _LAPP-TARGET-REVISION @
        _LAPP-PREVIEW-BYTES LIB-CONTENT-MAX
        _LAPP-ENTRY _LAPP-CONTENT _LAPP-STORE
        LIBRARY-VFS-STORE-READ-EXACT
        _LAPP-SET-STATUS >R
