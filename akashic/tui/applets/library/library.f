    _LAPP-CREATE-REQUEST _LAPP-RESULT-ENTRY _LAPP-STORE
        LIBRARY-VFS-STORE-CREATE-MANAGED _LAPP-SET-STATUS
    DUP LIBSTORE-S-OK = IF
        _LAPP-CLEAR-PENDING
    THEN ;

: _LAPP-DISPATCH-PENDING-CREATE  ( -- status )
    _LAPP-PENDING-CREATE @ _LAPP-CREATE-PREPARED = IF
        _LAPP-ENSURE-PROVISIONED DUP IF EXIT THEN DROP
        \ First-use provisioning advances the catalog generation.  Rebase
        \ only a request that has never reached the mutation API; exact
        \ retries keep every request field and the operation key unchanged.
        _LAPP-STORE LIBRARY-VFS-STORE.GENERATION @
            _LAPP-CREATE-REQUEST LIBMCR.EXPECTED-CATALOG-GENERATION !
    THEN
    _LAPP-CALL-PENDING-CREATE ;

