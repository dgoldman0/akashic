                _LIBMU-TARGET-DOMAIN @ = IF
                I _LIBMU-LOCATOR-FOR-INDEX UNLOOP EXIT
            THEN
        LOOP
        _LIBMU-MISSING-CONTENT-STATUS @ EXIT
