\ =====================================================================
\  service.f - Public Library domain service
\ =====================================================================
\  Commands, exact reads, lifecycle/history, collection operations, and
\  maintenance policy over the Library repository. Query policy is loaded
\  explicitly from query.f; UI and resource projection stay above here.
\
\  L12-DELETION: the temporary fixed-store seam reaches repository-private
\  _LIBAUTH, _LIBCQ, _LIBIX, _LIBLOC, _LIBMA, _LIBMD, _LIBMR, _LIBMU,
\  _LIBPQ, _LIBRARY, _LIBVCF, _LIBVFS, and _LIBVP prefix families. Its owner
\  span also reaches model _LIBM-PRIVATE-BEGIN plus _LIBE-TAGS/_LIBE-LINEAGE,
\  codec _LIBRC-PRIVATE-BEGIN, and format _LIBSF-PRIVATE-BEGIN/END sentinels;
\  repository _LIBVFS-PRIVATE-BEGIN is closed here by _LIBVFS-PRIVATE-END.
\  This also drives private _LIBVFS OPEN/READ-EXACT/SEEK/SYNC and VFSNAP
\  lifecycle operations; repository.f still solely owns paths/topology/durable
\  authority. L12 deletes every grouped reach once the slices cover service.
\ =====================================================================

