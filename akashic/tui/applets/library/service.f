    DUP VFSNAP-S-OK <> IF _LIBVFS-VFSNAP>STATUS EXIT THEN DROP
    _LIBMA-REOPEN-VFS @ _LIBMA-STORE @ _LIBRARY-VFS-STORE-INIT
    DUP IF EXIT THEN DROP
    _LIBMA-REOPEN-FLAGS @
        _LIBMA-STORE @ LIBRARY-VFS-STORE.FLAGS DUP @ ROT OR SWAP !
