: _LIBMU-READ-CONTENT-BODY  ( -- status )
    _LIBMU-RESOLVE-LOCATOR DUP IF EXIT THEN DROP
    _LIBMU-LOCATOR @ _LIBLOC.OFFSET @ _LIBVP-OFFSET !
    _LIBMU-LOCATOR @ _LIBLOC.FRAME-U @ _LIBVP-FRAME-U !
    _LIBVP-OFFSET @ LIB-ARENA-HEADER-SIZE <
    _LIBVP-OFFSET @ LIB-STORE-SECTOR-SIZE MOD 0<> OR
    _LIBVP-FRAME-U @ LIB-CONTENT-HEADER-SIZE < OR
    _LIBVP-FRAME-U @ LIB-CONTENT-FRAME-MAX > OR
    _LIBVP-FRAME-U @ LIB-STORE-SECTOR-SIZE MOD 0<> OR IF
        LIBSTORE-S-CORRUPT EXIT
    THEN
    _LIBVP-FRAME-U @
        _LIBMU-STORE @ LIBRARY-VFS-STORE.BANK
            LIBBF.CONTENT-TAIL @ > IF
        LIBSTORE-S-CORRUPT EXIT
    THEN
    _LIBVP-OFFSET @
        _LIBMU-STORE @ LIBRARY-VFS-STORE.BANK LIBBF.CONTENT-TAIL @
        _LIBVP-FRAME-U @ - > IF
        LIBSTORE-S-CORRUPT EXIT
    THEN
    _LIBVFS-CONTENT-PATH$ LIB-ARENA-SIZE _LIBVP-OPEN-COMMITTED
        DUP IF EXIT THEN DROP
    LIBSTORE-S-IO _LIBVP-THROW-STATUS !
    _LIBVP-OFFSET @ _LIBVP-FD @ _LIBVFS-SEEK
    _LIBVP-FRAME _LIBVP-FRAME-U @
        _LIBVP-FD @ _LIBVFS-READ-EXACT IF LIBSTORE-S-IO EXIT THEN
    _LIBVP-FRAME _LIBVP-FRAME-U @ _LIBMU-FRAME-SHA
        LIB-CONTENT-FRAME-DIGEST _LIBVFS-FORMAT>STATUS
        DUP IF EXIT THEN DROP
    _LIBMU-FRAME-SHA _LIBMU-LOCATOR @ _LIBLOC.FRAME-SHA
        SHA3-256-COMPARE 0= IF LIBSTORE-S-CORRUPT EXIT THEN
    _LIBVP-FRAME LIB-CONTENT-HEADER-SIZE
        LIB-CONTENT-RECORD-MEASURE
    DUP LIB-S-OK <> IF
        _LIBVFS-FORMAT-READ>STATUS >R DROP R> EXIT
    THEN
    DROP _LIBVP-RECORD-U !
    _LIBVP-RECORD-U @ LIB-CONTENT-FRAME-SIZE
        DUP -1 = IF DROP LIBSTORE-S-CORRUPT EXIT THEN
        _LIBVP-FRAME-U @ <> IF LIBSTORE-S-CORRUPT EXIT THEN
    _LIBVP-FRAME _LIBVP-RECORD-U @ +
        _LIBVP-FRAME-U @ _LIBVP-RECORD-U @ -
        _LIBVP-ZERO? 0= IF LIBSTORE-S-CORRUPT EXIT THEN
    _LIBVP-FRAME _LIBVP-RECORD-U @ _LIBMU-CONTENT-READBACK
        LIB-CONTENT-RECORD-DECODE _LIBVFS-FORMAT-READ>STATUS
        DUP IF EXIT THEN DROP
    _LIBMU-CONTENT-TARGET? 0= IF LIBSTORE-S-CORRUPT EXIT THEN
    _LIBMU-CONTENT-MATCHES-ENTRY? 0= IF LIBSTORE-S-CORRUPT EXIT THEN
    _LIBVP-CLOSE-NOW DUP IF EXIT THEN DROP
    1 _LIBPQ-DIRECT-FRAME-READ-N +!
    _LIBVP-FRAME-U @ _LIBPQ-DIRECT-FRAME-BYTES +!
    _LIBMU-CONTENT-READBACK LIBCT.DATA-U @
        _LIBMU-CONTENT-CAP @ > IF
        LIBSTORE-S-OUTPUT-CAPACITY EXIT
    THEN
    LIBSTORE-S-OK ;

