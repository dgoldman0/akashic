    _LIBMA-STORE @ LIBRARY-VFS-STORE-LOADED?
    _LIBMA-STORE @ LIBRARY-VFS-STORE-BLOCKED? 0= AND IF
        LIBSTORE-S-OK
    ELSE
        _LIBMA-REOPEN-AND-LOAD
    THEN ;

