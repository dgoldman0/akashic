\ =====================================================================
\  Public bounded maintenance operations
\ =====================================================================
\  INSPECT is read-only and publishes only a complete staged report.
\  RAW-EXPORT repeats inspection under one VFS transaction and treats the
\  caller report as an optimistic evidence token.  REPAIR makes the same
\  fresh comparison and delegates only the exact planned head replacement
\  cleanup to VREPL; every other health result remains byte-preserved.

: _LIBMA-REPORT-SPAN-SAFE?  ( inspection store -- flag )
    _LIBMU-STORE !
    _LIBMU-STORE @ LIBRARY-VFS-STORE-VALID? 0= IF DROP 0 EXIT THEN
    LIBRARY-INSPECTION-SIZE _LIBMU-OWNER-SPAN-SAFE? ;

: _LIBRARY-VFS-STORE-INSPECT  ( inspection store -- call-status )
    _LIBMA-STORE ! _LIBMA-EXPECTED-REPORT !
    _LIBMA-EXPECTED-REPORT @ _LIBMA-STORE @
        _LIBMA-REPORT-SPAN-SAFE? 0= IF
        LIBSTORE-S-INVALID EXIT
    THEN
    _LIBMA-REPORT-A _LIBMA-STORE @ _LIBMA-INSPECT-INTO
    DUP IF EXIT THEN DROP
    _LIBMA-REPORT-A _LIBMA-EXPECTED-REPORT @
        LIBRARY-INSPECTION-SIZE CMOVE
    LIBSTORE-S-OK ;

' _LIBRARY-VFS-STORE-INSPECT CONSTANT _libvfs-inspect-xt
: LIBRARY-VFS-STORE-INSPECT  ( inspection store -- call-status )
    _libvfs-inspect-xt _library-vfs-store-guard WITH-GUARD ;

: _LIBMA-EXPORT-ARGS?  ( bytes capacity inspection store -- flag )
    _LIBMA-STORE ! _LIBMA-EXPECTED-REPORT !
    _LIBMA-EXPORT-CAP ! _LIBMA-EXPORT-A !
    _LIBMA-EXPECTED-REPORT @ _LIBMA-STORE @
        _LIBMA-REPORT-SPAN-SAFE? 0= IF 0 EXIT THEN
    _LIBMA-EXPORT-CAP @ 0< IF 0 EXIT THEN
    _LIBMA-EXPORT-CAP @ 0= IF -1 EXIT THEN
    _LIBMA-EXPORT-A @ _LIBMA-EXPORT-CAP @
        _LIBMU-OWNER-SPAN-SAFE? 0= IF 0 EXIT THEN
    _LIBMA-EXPORT-A @ _LIBMA-EXPORT-CAP @
        _LIBMA-EXPECTED-REPORT @ LIBRARY-INSPECTION-SIZE
        _VFSNAP-RANGES-OVERLAP? 0= ;

