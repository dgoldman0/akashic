        _LIBMA-CALL-STATUS ! ;

: _LIBMA-DURABILITY-BARRIER  ( -- status )
    -1 _LIBMA-CALL-STATUS !
    ['] _LIBMA-SYNC-CALL CATCH IF
        LIBSTORE-S-IO
    ELSE
        _LIBMA-CALL-STATUS @ IF LIBSTORE-S-IO ELSE LIBSTORE-S-OK THEN
    THEN ;

: _LIBMA-REPAIR-BODY  ( -- status )
    _LIBMA-REPORT-A _LIBMA-STORE @ _LIBMA-INSPECT-INTO
    DUP IF EXIT THEN DROP
    \ A completed repair report is an idempotent receipt only when the fresh
    \ corpus is healthy and its evidence is the exact planned result.
    _LIBMA-REPORT-A LIBINS.HEALTH @ LIBSTORE-S-OK =
    _LIBMA-EXPECTED-REPORT @ LIBINS.REPAIRED-SEAL RID-PRESENT? AND
    _LIBMA-EXPECTED-REPORT @ LIBINS.REPAIRED-SEAL
        _LIBMA-REPORT-A LIBINS.EVIDENCE-SEAL SHA3-256-COMPARE AND IF
        _LIBMA-DURABILITY-BARRIER DUP IF
            _LIBMA-BLOCK-STATUS EXIT
        THEN DROP
        _LIBMA-ENSURE-ACTIVE EXIT
    THEN
    \ A stale observation never authorizes even deterministic cleanup.
    _LIBMA-EXPECTED-REPORT @ LIBINS.EVIDENCE-SEAL
        _LIBMA-REPORT-A LIBINS.EVIDENCE-SEAL
        SHA3-256-COMPARE 0= IF LIBSTORE-S-CONFLICT EXIT THEN
    _LIBMA-REPORT-A LIBINS.REPAIR-MASK @
        LIBRARY-REPAIR-F-HEAD-TRANSACTION <> IF
        _LIBMA-REPORT-A LIBINS.HEALTH @ LIBSTORE-S-OK = IF
            _LIBMA-ENSURE-ACTIVE
        ELSE
            _LIBMA-REPORT-A LIBINS.HEALTH @
        THEN
        EXIT
    THEN
    _LIBMA-REPORT-A LIBINS.HEALTH @ LIBSTORE-S-RECOVERY <> IF
        LIBSTORE-S-RECOVERY EXIT
    THEN
    _LIBMA-REPORT-A LIBINS.REPAIRED-SEAL RID-PRESENT? 0= IF
        LIBSTORE-S-RECOVERY EXIT
    THEN
    _LIBMA-REPORT-A LIBINS.REPAIRED-SEAL _LIBMA-TARGET-HASH
        LIB-DIGEST-SIZE CMOVE
    _LIBMA-RECOVER-HEAD DUP IF _LIBMA-BLOCK-STATUS EXIT THEN DROP
    _LIBMA-REOPEN-AND-LOAD DUP IF EXIT THEN DROP
    _LIBMA-REPORT-A _LIBMA-STORE @ _LIBMA-INSPECT-INTO
    DUP IF _LIBMA-BLOCK-STATUS EXIT THEN DROP
    _LIBMA-REPORT-A LIBINS.HEALTH @ DUP LIBSTORE-S-OK <> IF
        _LIBMA-BLOCK-STATUS EXIT
    THEN DROP
    _LIBMA-TARGET-HASH _LIBMA-REPORT-A LIBINS.EVIDENCE-SEAL
        SHA3-256-COMPARE 0= IF
        LIBSTORE-S-RECOVERY _LIBMA-BLOCK-STATUS EXIT
    THEN
    LIBSTORE-S-OK ;

