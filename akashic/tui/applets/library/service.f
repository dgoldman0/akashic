: _LIBMA-VREPL>STATUS  ( vrepl-status -- status )
    CASE
        VREPL-S-OK OF LIBSTORE-S-OK ENDOF
        VREPL-S-ROLLED-BACK OF LIBSTORE-S-OK ENDOF
        VREPL-S-COMMITTED-CLEANUP OF LIBSTORE-S-OK ENDOF
        VREPL-S-INVALID OF LIBSTORE-S-INVALID ENDOF
        VREPL-S-IO OF LIBSTORE-S-IO ENDOF
        VREPL-S-CONFLICT OF LIBSTORE-S-CONFLICT ENDOF
        VREPL-S-BUSY OF LIBSTORE-S-BUSY ENDOF
        VREPL-S-UNCERTAIN OF LIBSTORE-S-UNCERTAIN ENDOF
        VREPL-S-RECOVERY OF LIBSTORE-S-RECOVERY ENDOF
        VREPL-S-MARKER-CORRUPT OF LIBSTORE-S-RECOVERY ENDOF
