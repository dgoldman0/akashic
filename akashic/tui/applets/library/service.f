  ( bytes capacity inspection store -- required-u status )
    _LIBMA-EXPORT-ARGS? 0= IF 0 LIBSTORE-S-INVALID EXIT THEN
    0 _LIBMA-EXPORT-REQUIRED !
    0 _LIBMA-EXPORT-STARTED !
    _LIBMA-STORE @ _LIBVP-STORE !
    _LIBMA-STORE @ LIBRARY-VFS-STORE.VFS @ _LIBVP-VFS !
    ['] _LIBMA-EXPORT-BODY _LIBVP-RUN
        DUP _LIBMA-CALL-STATUS ! DROP
    _LIBMA-CALL-STATUS @ LIBSTORE-S-OK <>
    _LIBMA-EXPORT-STARTED @ AND IF
        _LIBMA-EXPORT-A @ _LIBMA-EXPORT-REQUIRED @ 0 FILL
    THEN
    _LIBMA-EXPORT-REQUIRED @ _LIBMA-CALL-STATUS @ ;

