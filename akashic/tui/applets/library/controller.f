    _LAPP-SELECTED @ DUP 0< IF DROP 0 EXIT THEN
    DUP _LAPP-ROW-COUNT @ >= IF DROP 0 EXIT THEN
    _LAPP-CORPUS-ROW ;

: _LAPP-SELECTED-COLLECTION  ( -- summary | 0 )
    _LAPP-SELECTED @ DUP 0< IF DROP 0 EXIT THEN
    DUP _LAPP-ROW-COUNT @ >= IF DROP 0 EXIT THEN
    _LAPP-COLLECTION-ROW ;

: _LAPP-SELECTED-HISTORY  ( -- summary | 0 )
    _LAPP-SELECTED @ DUP 0< IF DROP 0 EXIT THEN
    DUP _LAPP-ROW-COUNT @ >= IF DROP 0 EXIT THEN
    _LAPP-HISTORY-ROW ;

: _LAPP-CAPTURE-CORPUS-TARGET  ( -- flag )
    _LAPP-SELECTED-CORPUS ?DUP 0= IF 0 EXIT THEN
    DUP LIBQS.REF RREF.ID _LAPP-TARGET-ID RID-COPY
    LIBQS.DOMAIN-REVISION @ _LAPP-TARGET-REVISION ! -1 ;

: _LAPP-LOAD-CORPUS-PREVIEW  ( -- status )
    _LAPP-CLEAR-PREVIEW
    _LAPP-CAPTURE-CORPUS-TARGET 0= IF
        LIBSTORE-S-NOT-FOUND _LAPP-SET-STATUS EXIT
    THEN
    _LAPP-TARGET-ID _LAPP-TARGET-REVISION @
        _LAPP-PREVIEW-BYTES LIB-CONTENT-MAX
        _LAPP-ENTRY _LAPP-CONTENT _LAPP-STORE
        LIBRARY-VFS-STORE-READ-EXACT
        _LAPP-SET-STATUS >R
    _LAPP-PREVIEW-U !
    R> DUP LIBSTORE-S-OK = IF -1 _LAPP-PREVIEW-READY ! THEN ;

: _LAPP-LOAD-COLLECTION-PREVIEW  ( -- status )
    _LAPP-CLEAR-PREVIEW
    _LAPP-SELECTED-COLLECTION ?DUP 0= IF
        LIBSTORE-S-NOT-FOUND _LAPP-SET-STATUS EXIT
    THEN
    DUP LIBCS.REF RREF.ID _LAPP-TARGET-ID RID-COPY
    LIBCS.REVISION @ DUP _LAPP-TARGET-REVISION !
    _LAPP-TARGET-ID SWAP _LAPP-COLLECTION-VIEW _LAPP-STORE
        LIBRARY-VFS-STORE-READ-COLLECTION-EXACT
        _LAPP-SET-STATUS
    DUP LIBSTORE-S-OK = IF -1 _LAPP-PREVIEW-READY ! THEN ;

