: _LAPP-COPY-ENTRY-METADATA  ( new-title-a new-title-u -- status )
    _LAPP-METADATA LIBRARY-METADATA-INIT
    _LAPP-METADATA LIBRARY-METADATA-TITLE! DUP IF EXIT THEN DROP
    _LAPP-ENTRY LIBE.TAG-N @ 0 ?DO
        I _LAPP-ENTRY LIBE-TAG LIB-TAG$
        I _LAPP-METADATA LIBRARY-METADATA-TAG! DUP IF
