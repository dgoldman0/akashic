            UNLOOP EXIT
        THEN DROP
    LOOP
    LIBSTORE-S-OK ;

: _LAPP-RENAME-NOW  ( a u -- status )
    _LAPP-COPY-ENTRY-METADATA DUP IF _LAPP-SET-STATUS EXIT THEN DROP
    _LAPP-TARGET-ID _LAPP-TARGET-REVISION @ _LAPP-METADATA
        _LAPP-RESULT-ENTRY _LAPP-STORE
        LIBRARY-VFS-STORE-REPLACE-METADATA _LAPP-SET-STATUS ;

: _LAPP-ARCHIVE-NOW  ( -- status )
    _LAPP-CAPTURE-CORPUS-TARGET 0= IF
        LIBSTORE-S-NOT-FOUND _LAPP-SET-STATUS EXIT
    THEN
    _LAPP-TARGET-ID _LAPP-TARGET-REVISION @
        _LAPP-RESULT-ENTRY _LAPP-STORE
        LIBRARY-VFS-STORE-ARCHIVE _LAPP-SET-STATUS ;

: _LAPP-UNARCHIVE-NOW  ( -- status )
    _LAPP-CAPTURE-CORPUS-TARGET 0= IF
        LIBSTORE-S-NOT-FOUND _LAPP-SET-STATUS EXIT
    THEN
    _LAPP-TARGET-ID _LAPP-TARGET-REVISION @
        _LAPP-RESULT-ENTRY _LAPP-STORE
        LIBRARY-VFS-STORE-UNARCHIVE _LAPP-SET-STATUS ;

: _LAPP-OPEN-HISTORY  ( -- status )
    _LAPP-CAPTURE-CORPUS-TARGET 0= IF
        LIBSTORE-S-NOT-FOUND _LAPP-SET-STATUS EXIT
    THEN
    _LAPP-TARGET-ID _LAPP-HISTORY-ID RID-COPY
    _LAPP-TARGET-REVISION @ _LAPP-HISTORY-CURRENT !
    _LAPP-TARGET-ID _LAPP-TARGET-REVISION @
        _LAPP-HISTORY LIB-RETAINED-REVISION-MAX _LAPP-STORE
        LIBRARY-VFS-STORE-LIST-RETAINED-REVISIONS
        _LAPP-SET-STATUS >R
    R> DUP IF NIP EXIT THEN DROP
    _LAPP-ROW-COUNT !
    _LAPP-VIEW @ _LAPP-RETURN-VIEW !
    _LAPP-V-HISTORY _LAPP-VIEW !
    0 _LAPP-PAGE-INDEX ! -1 _LAPP-NEXT-SLOT !
    0 _LAPP-SELECTED ! 0 _LAPP-LIST-SCROLL !
    _LAPP-LOAD-SELECTION ;

: _LAPP-FILTER-COLLECTION  ( -- status )
    _LAPP-SELECTED-COLLECTION ?DUP 0= IF
        LIBSTORE-S-NOT-FOUND _LAPP-SET-STATUS EXIT
    THEN
    LIBCS.REF RREF.ID _LAPP-FILTER-ID RID-COPY
    -1 _LAPP-FILTER-ACTIVE !
    _LAPP-V-ALL _LAPP-VIEW !
    _LAPP-V-ALL _LAPP-RETURN-VIEW !
    _LAPP-RESET-PAGE ;

: _LAPP-BACK  ( -- status )
    _LAPP-VIEW @ _LAPP-V-HISTORY = IF
        _LAPP-RETURN-VIEW @ DUP
        _LAPP-V-ACTIVE < OVER _LAPP-V-ALL > OR IF DROP _LAPP-V-ALL THEN
        _LAPP-VIEW ! _LAPP-RESET-PAGE EXIT
    THEN
    _LAPP-VIEW @ _LAPP-V-COLLECTIONS = IF
        _LAPP-RETURN-VIEW @ DUP
        _LAPP-V-ACTIVE < OVER _LAPP-V-ALL > OR IF DROP _LAPP-V-ALL THEN
        _LAPP-VIEW ! _LAPP-RESET-PAGE EXIT
    THEN
    _LAPP-FILTER-ACTIVE @ IF
        0 _LAPP-FILTER-ACTIVE ! _LAPP-FILTER-ID RID-CLEAR
        _LAPP-RESET-PAGE EXIT
    THEN
    LIBSTORE-S-OK _LAPP-SET-STATUS ;

\ =====================================================================
\  Managed-document create with exact pending retry state
\ =====================================================================

: _LAPP-CONFIGURE-CREATE  ( -- status )
    _LAPP-CREATE-REQUEST LIBRARY-MANAGED-CREATE-REQUEST-INIT
    _LAPP-CREATE-REQUEST LIBMCR.OPERATION-KEY
        LIB-OPERATION-KEY-SIZE RNG-BYTES
    _LAPP-CREATE-REQUEST LIBMCR.OPERATION-KEY
        _LAPP-CREATE-REQUEST LIBRARY-MANAGED-CREATE-OPERATION-KEY!
        DUP IF EXIT THEN DROP
    _LAPP-STORE LIBRARY-VFS-STORE.GENERATION @
        _LAPP-CREATE-REQUEST LIBMCR.EXPECTED-CATALOG-GENERATION !
    LIB-MEDIA-TEXT-PLAIN _LAPP-CREATE-REQUEST LIBMCR.MEDIA !
    _LAPP-PENDING-TITLE _LAPP-PENDING-TITLE-U @
        _LAPP-CREATE-REQUEST LIBRARY-MANAGED-CREATE-TITLE!
        DUP IF EXIT THEN DROP
    _LAPP-PENDING-BODY _LAPP-PENDING-BODY-U @
        _LAPP-CREATE-REQUEST LIBRARY-MANAGED-CREATE-CONTENT!
        DUP IF EXIT THEN DROP
    _LAPP-CREATE-REQUEST LIBRARY-MANAGED-CREATE-REQUEST-VALID? 0= IF
        LIBSTORE-S-INVALID EXIT
    THEN
    _LAPP-CREATE-PREPARED _LAPP-PENDING-CREATE !
    LIBSTORE-S-OK ;

: _LAPP-CALL-PENDING-CREATE  ( -- status )
    _LAPP-PENDING-CREATE @ 0= IF
        LIBSTORE-S-INVALID _LAPP-SET-STATUS EXIT
    THEN
    _LAPP-CREATE-DISPATCHED _LAPP-PENDING-CREATE !
    _LAPP-CREATE-REQUEST _LAPP-RESULT-ENTRY _LAPP-STORE
        LIBRARY-VFS-STORE-CREATE-MANAGED _LAPP-SET-STATUS
    DUP LIBSTORE-S-OK = IF
        _LAPP-CLEAR-PENDING
    THEN ;

: _LAPP-DISPATCH-PENDING-CREATE  ( -- status )
    _LAPP-PENDING-CREATE @ _LAPP-CREATE-PREPARED = IF
        _LAPP-ENSURE-PROVISIONED DUP IF EXIT THEN DROP
        \ First-use provisioning advances the catalog generation.  Rebase
        \ only a request that has never reached the mutation API; exact
        \ retries keep every request field and the operation key unchanged.
        _LAPP-STORE LIBRARY-VFS-STORE.GENERATION @
            _LAPP-CREATE-REQUEST LIBMCR.EXPECTED-CATALOG-GENERATION !
    THEN
    _LAPP-CALL-PENDING-CREATE ;

: _LAPP-CREATE-NOW  ( -- status )
    _LAPP-CONFIGURE-CREATE DUP IF _LAPP-SET-STATUS EXIT THEN DROP
    _LAPP-DISPATCH-PENDING-CREATE ;

