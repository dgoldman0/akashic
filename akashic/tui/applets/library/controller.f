: _LAPP-BUILD-QUERY  ( -- status )
    _LAPP-QUERY LIBRARY-CORPUS-QUERY-REQUEST-INIT
    _LAPP-QUERY-SCOPE!
    _LAPP-TERM _LAPP-TERM-U @ _LAPP-QUERY
        LIBRARY-CORPUS-QUERY-TERM! DUP IF EXIT THEN DROP
    _LAPP-FILTER-ACTIVE @ IF
        _LAPP-FILTER-ID _LAPP-QUERY
            LIBRARY-CORPUS-QUERY-COLLECTION! DUP IF EXIT THEN DROP
    THEN
    _LAPP-GENERATION @ _LAPP-QUERY LIBCQR.EXPECTED-CATALOG-GENERATION !
    _LAPP-PAGE-INDEX @ _LAPP-PAGE-START @
        _LAPP-QUERY LIBCQR.START-SLOT !
    LIBSTORE-S-OK ;

: _LAPP-CLAMP-SELECTION  ( -- )
    _LAPP-ROW-COUNT @ DUP 0= IF
        DROP 0 _LAPP-SELECTED ! 0 _LAPP-LIST-SCROLL ! EXIT
    THEN
    1- _LAPP-SELECTED @ MIN 0 MAX _LAPP-SELECTED !
    _LAPP-LIST-SCROLL @ _LAPP-SELECTED @ > IF
        _LAPP-SELECTED @ _LAPP-LIST-SCROLL !
    THEN ;

