VARIABLE _LIBS-PRIOR-N
VARIABLE _LIBS-CATALOG-N
VARIABLE _LIBS-COLLECTION-N

: LIB-CATALOG-SLOT  ( slot catalog -- entry )
    SWAP LIB-ENTRY-SIZE * + ;

: LIB-COLLECTION-SLOT  ( slot collections -- collection )
    SWAP LIB-COLLECTION-SIZE * + ;

: _LIBS-ENTRY-NOT-SEEN?  ( entry prior-count catalog -- flag )
    _LIBS-CATALOG ! _LIBS-PRIOR-N ! _LIBS-ENTRY !
    _LIBS-PRIOR-N @ 0 ?DO
        _LIBS-ENTRY @ LIBE.ID
            I _LIBS-CATALOG @ LIB-CATALOG-SLOT LIBE.ID RID= IF
            0 UNLOOP EXIT
        THEN
        _LIBS-ENTRY @ LIBE.ID
            I _LIBS-CATALOG @ LIB-CATALOG-SLOT
            LIBE.RECEIPT LIBR.OPERATION-KEY RID= IF
            0 UNLOOP EXIT
        THEN
        _LIBS-ENTRY @ LIBE.RECEIPT
            I _LIBS-CATALOG @ LIB-CATALOG-SLOT LIBE.RECEIPT
            LIB-RECEIPT-KEY= IF
            0 UNLOOP EXIT
        THEN
        _LIBS-ENTRY @ LIBE.RECEIPT LIBR.OPERATION-KEY
            I _LIBS-CATALOG @ LIB-CATALOG-SLOT LIBE.ID RID= IF
            0 UNLOOP EXIT
        THEN
    LOOP
    -1 ;

: _LIBS-CATALOG-VALID?  ( catalog count -- flag )
    _LIBS-N ! _LIBS-CATALOG !
    _LIBS-N @ DUP 0< SWAP LIB-CATALOG-MAX > OR IF 0 EXIT THEN
    _LIBS-N @ 0= IF -1 EXIT THEN
    _LIBS-CATALOG @ 0= IF 0 EXIT THEN
    _LIBS-CATALOG @ _LIBS-N @ LIB-ENTRY-SIZE *
        MSPAN-NONWRAPPING? 0= IF 0 EXIT THEN
    _LIBS-N @ 0 ?DO
        I _LIBS-CATALOG @ LIB-CATALOG-SLOT
        DUP LIB-ENTRY-VALID? 0= IF DROP 0 UNLOOP EXIT THEN
        I _LIBS-CATALOG @ _LIBS-ENTRY-NOT-SEEN? 0= IF
            0 UNLOOP EXIT
        THEN
    LOOP
    -1 ;

: _LIBS-COLLECTION-NOT-SEEN?  ( collection prior-count collections -- flag )
    _LIBS-COLLECTIONS ! _LIBS-PRIOR-N ! _LIBS-COLLECTION !
    _LIBS-PRIOR-N @ 0 ?DO
        _LIBS-COLLECTION @ LIBC.ID
            I _LIBS-COLLECTIONS @ LIB-COLLECTION-SLOT LIBC.ID RID= IF
            0 UNLOOP EXIT
        THEN
        _LIBS-COLLECTION @ LIBC.ID
            I _LIBS-COLLECTIONS @ LIB-COLLECTION-SLOT
            LIBC.OPERATION-KEY RID= IF
            0 UNLOOP EXIT
        THEN
        _LIBS-COLLECTION @ LIBC.OPERATION-KEY
            I _LIBS-COLLECTIONS @ LIB-COLLECTION-SLOT
            LIBC.OPERATION-KEY RID= IF
            0 UNLOOP EXIT
        THEN
        _LIBS-COLLECTION @ LIBC.OPERATION-KEY
            I _LIBS-COLLECTIONS @ LIB-COLLECTION-SLOT LIBC.ID RID= IF
            0 UNLOOP EXIT
        THEN
    LOOP
    -1 ;

: _LIBS-COLLECTION-CATALOG-VALID?  ( collections count -- flag )
    _LIBS-N ! _LIBS-COLLECTIONS !
    _LIBS-N @ DUP 0< SWAP LIB-COLLECTION-MAX > OR IF 0 EXIT THEN
    _LIBS-N @ 0= IF -1 EXIT THEN
    _LIBS-COLLECTIONS @ 0= IF 0 EXIT THEN
    _LIBS-COLLECTIONS @ _LIBS-N @ LIB-COLLECTION-SIZE *
        MSPAN-NONWRAPPING? 0= IF 0 EXIT THEN
    _LIBS-N @ 0 ?DO
        I _LIBS-COLLECTIONS @ LIB-COLLECTION-SLOT
        DUP LIB-COLLECTION-VALID? 0= IF DROP 0 UNLOOP EXIT THEN
        I _LIBS-COLLECTIONS @ _LIBS-COLLECTION-NOT-SEEN? 0= IF
            0 UNLOOP EXIT
        THEN
    LOOP
    -1 ;

: _LIBS-COLLECTION-SLOTS-VALID?  ( collection catalog count -- flag )
    _LIBS-N ! _LIBS-CATALOG ! _LIBS-COLLECTION !
    _LIBS-COLLECTION @ LIB-COLLECTION-VALID? 0= IF 0 EXIT THEN
    _LIBS-CATALOG @ _LIBS-N @ _LIBS-CATALOG-VALID? 0= IF 0 EXIT THEN
    LIB-CATALOG-MAX 0 ?DO
        I _LIBS-COLLECTION @ LIBC-MEMBER? IF
            I _LIBS-N @ >= IF 0 UNLOOP EXIT THEN
            I _LIBS-CATALOG @ LIB-CATALOG-SLOT LIBE.ID
                RID-PRESENT? 0= IF 0 UNLOOP EXIT THEN
        THEN
    LOOP
    -1 ;

: _LIBS-ENTRY-BYTES=  ( old new -- flag )
    SWAP LIB-ENTRY-SIZE ROT LIB-ENTRY-SIZE COMPARE 0= ;

: _LIBS-COLLECTION-BYTES=  ( old new -- flag )
    SWAP LIB-COLLECTION-SIZE ROT LIB-COLLECTION-SIZE COMPARE 0= ;

: _LIBS-CONTENT-IDENTITY=  ( old new -- flag )
    2DUP LIBE.CONTENT-U @ SWAP LIBE.CONTENT-U @ = >R
    LIBE.CONTENT-DIGEST SWAP LIBE.CONTENT-DIGEST
        SHA3-256-COMPARE R> AND ;

: _LIBS-IDENTITIES-PRESERVED?  ( old old-n new new-n -- flag )
    _LIBS-NEW-N ! _LIBS-NEW ! _LIBS-OLD-N ! _LIBS-OLD !
    _LIBS-OLD @ _LIBS-OLD-N @ _LIBS-CATALOG-VALID? 0= IF 0 EXIT THEN
    _LIBS-NEW @ _LIBS-NEW-N @ _LIBS-CATALOG-VALID? 0= IF 0 EXIT THEN
    _LIBS-NEW-N @ _LIBS-OLD-N @ < IF 0 EXIT THEN
    _LIBS-OLD-N @ 0 ?DO
        I _LIBS-OLD @ LIB-CATALOG-SLOT
        I _LIBS-NEW @ LIB-CATALOG-SLOT
        2DUP LIBE.ID SWAP LIBE.ID RID= 0= IF
            2DROP 0 UNLOOP EXIT
        THEN
        2DUP LIBE.KIND @ SWAP LIBE.KIND @ = 0= IF
            2DROP 0 UNLOOP EXIT
        THEN
        DUP LIBE.LIFECYCLE @ LIB-LIFECYCLE-TOMBSTONED <> IF
            2DUP LIBE.MEDIA @ SWAP LIBE.MEDIA @ <> IF
                2DROP 0 UNLOOP EXIT
            THEN
            2DUP SWAP LIBE.CURRENT-CONTENT-REVISION @
                SWAP LIBE.CURRENT-CONTENT-REVISION @ > IF
                2DROP 0 UNLOOP EXIT
            THEN
            2DUP LIBE.CURRENT-CONTENT-REVISION @
                SWAP LIBE.CURRENT-CONTENT-REVISION @ = IF
                2DUP _LIBS-CONTENT-IDENTITY= 0= IF
                    2DROP 0 UNLOOP EXIT
                THEN
            THEN
        THEN
        2DUP LIBE.RECEIPT SWAP LIBE.RECEIPT LIB-RECEIPT= 0= IF
            2DROP 0 UNLOOP EXIT
        THEN
        2DUP SWAP LIBE.DOMAIN-REVISION @
            SWAP LIBE.DOMAIN-REVISION @ > IF
            2DROP 0 UNLOOP EXIT
        THEN
        2DUP SWAP LIBE.MUTATION-SEQUENCE @
            SWAP LIBE.MUTATION-SEQUENCE @ > IF
            2DROP 0 UNLOOP EXIT
        THEN
        2DUP _LIBS-ENTRY-BYTES= 0= IF
            2DUP LIBE.DOMAIN-REVISION @
                SWAP LIBE.DOMAIN-REVISION @ > 0= IF
                2DROP 0 UNLOOP EXIT
            THEN
            2DUP LIBE.MUTATION-SEQUENCE @
                SWAP LIBE.MUTATION-SEQUENCE @ > 0= IF
                2DROP 0 UNLOOP EXIT
            THEN
        THEN
        OVER LIBE.LIFECYCLE @ LIB-LIFECYCLE-TOMBSTONED = IF
            DUP LIBE.LIFECYCLE @ LIB-LIFECYCLE-TOMBSTONED <> IF
                2DROP 0 UNLOOP EXIT
            THEN
            2DUP _LIBS-ENTRY-BYTES= 0= IF
                2DROP 0 UNLOOP EXIT
            THEN
        THEN
        2DROP
    LOOP
    -1 ;

: _LIBS-COLLECTION-IDENTITIES-PRESERVED?  ( old old-n new new-n -- flag )
    _LIBS-NEW-N ! _LIBS-NEW ! _LIBS-OLD-N ! _LIBS-OLD !
    _LIBS-OLD @ _LIBS-OLD-N @ _LIBS-COLLECTION-CATALOG-VALID? 0= IF
        0 EXIT
    THEN
    _LIBS-NEW @ _LIBS-NEW-N @ _LIBS-COLLECTION-CATALOG-VALID? 0= IF
        0 EXIT
    THEN
    _LIBS-NEW-N @ _LIBS-OLD-N @ < IF 0 EXIT THEN
    _LIBS-OLD-N @ 0 ?DO
        I _LIBS-OLD @ LIB-COLLECTION-SLOT
        I _LIBS-NEW @ LIB-COLLECTION-SLOT
        2DUP LIBC.ID SWAP LIBC.ID RID= 0= IF
            2DROP 0 UNLOOP EXIT
        THEN
        2DUP LIBC.OPERATION-KEY SWAP LIBC.OPERATION-KEY RID= 0= IF
            2DROP 0 UNLOOP EXIT
        THEN
        2DUP LIBC.REQUEST-SEAL SWAP LIBC.REQUEST-SEAL
            SHA3-256-COMPARE 0= IF
            2DROP 0 UNLOOP EXIT
        THEN
        2DUP LIBC.EXPECTED-CATALOG-GENERATION @
            SWAP LIBC.EXPECTED-CATALOG-GENERATION @ <> IF
            2DROP 0 UNLOOP EXIT
        THEN
        2DUP SWAP LIBC.REVISION @ SWAP LIBC.REVISION @ > IF
            2DROP 0 UNLOOP EXIT
        THEN
        2DUP SWAP LIBC.MUTATION-SEQUENCE @
            SWAP LIBC.MUTATION-SEQUENCE @ > IF
            2DROP 0 UNLOOP EXIT
        THEN
        2DUP _LIBS-COLLECTION-BYTES= 0= IF
            2DUP LIBC.REVISION @ SWAP LIBC.REVISION @ > 0= IF
                2DROP 0 UNLOOP EXIT
            THEN
            2DUP LIBC.MUTATION-SEQUENCE @
                SWAP LIBC.MUTATION-SEQUENCE @ > 0= IF
                2DROP 0 UNLOOP EXIT
            THEN
        THEN
        2DROP
    LOOP
    -1 ;

: _LIBS-ENTRY-DISJOINT-FROM-COLLECTIONS?
    ( entry collections collection-count -- flag )
    _LIBS-COLLECTION-N ! _LIBS-COLLECTIONS ! _LIBS-ENTRY !
    _LIBS-COLLECTION-N @ 0 ?DO
        _LIBS-ENTRY @ LIBE.ID
            I _LIBS-COLLECTIONS @ LIB-COLLECTION-SLOT LIBC.ID RID= IF
            0 UNLOOP EXIT
        THEN
        _LIBS-ENTRY @ LIBE.ID
            I _LIBS-COLLECTIONS @ LIB-COLLECTION-SLOT
            LIBC.OPERATION-KEY RID= IF
            0 UNLOOP EXIT
        THEN
        _LIBS-ENTRY @ LIBE.RECEIPT LIBR.OPERATION-KEY
            I _LIBS-COLLECTIONS @ LIB-COLLECTION-SLOT
            LIBC.OPERATION-KEY RID= IF
            0 UNLOOP EXIT
        THEN
        _LIBS-ENTRY @ LIBE.RECEIPT LIBR.OPERATION-KEY
            I _LIBS-COLLECTIONS @ LIB-COLLECTION-SLOT LIBC.ID RID= IF
            0 UNLOOP EXIT
        THEN
    LOOP
    -1 ;

: _LIBS-DOMAIN-IDENTITIES-VALID?
    ( catalog catalog-count collections collection-count -- flag )
    _LIBS-COLLECTION-N ! _LIBS-COLLECTIONS !
    _LIBS-CATALOG-N ! _LIBS-CATALOG !
    _LIBS-CATALOG @ _LIBS-CATALOG-N @ _LIBS-CATALOG-VALID? 0= IF
        0 EXIT
    THEN
    _LIBS-COLLECTIONS @ _LIBS-COLLECTION-N @
        _LIBS-COLLECTION-CATALOG-VALID? 0= IF 0 EXIT THEN
    _LIBS-CATALOG-N @ 0 ?DO
        I _LIBS-CATALOG @ LIB-CATALOG-SLOT
        _LIBS-COLLECTIONS @ _LIBS-COLLECTION-N @
        _LIBS-ENTRY-DISJOINT-FROM-COLLECTIONS? 0= IF
            0 UNLOOP EXIT
        THEN
    LOOP
    _LIBS-COLLECTION-N @ 0 ?DO
        I _LIBS-COLLECTIONS @ LIB-COLLECTION-SLOT
        _LIBS-CATALOG @ _LIBS-CATALOG-N @
        _LIBS-COLLECTION-SLOTS-VALID? 0= IF 0 UNLOOP EXIT THEN
    LOOP
    -1 ;

