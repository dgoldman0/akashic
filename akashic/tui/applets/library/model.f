: _LIBM-ZERO?  ( address length -- flag )
    0 ?DO DUP I + C@ IF DROP 0 UNLOOP EXIT THEN LOOP DROP -1 ;

: _LIBM-BYTE-POPCOUNT  ( byte -- count )
    0 SWAP 8 0 DO
        DUP 1 AND ROT + SWAP 1 RSHIFT
    LOOP DROP ;

: _LIBM-MEMBERS-COUNT  ( collection -- count )
    LIBC.MEMBERS 0 SWAP
    LIB-CATALOG-MAX 8 / 0 ?DO
        DUP I + C@ _LIBM-BYTE-POPCOUNT ROT + SWAP
    LOOP DROP ;

: _LIBM-REQUEST-HASH  ( entry destination -- )
    _LIBM-SEAL-DST ! _LIBM-ENTRY !
    SHA3-256-BEGIN
    S" akashic.library.initial-request.v1" SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.RECEIPT LIBR.METHOD 8 SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.KIND 8 SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.RECEIPT LIBR.INITIAL-CONTENT-REVISION
        8 SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.RECEIPT LIBR.INITIAL-CONTENT-U 8 SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.RECEIPT LIBR.INITIAL-MEDIA 8 SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.RECEIPT LIBR.INITIAL-CONTENT-DIGEST
        LIB-DIGEST-SIZE SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.RECEIPT LIBR.IMPORT-CONTRACT-DIGEST
        LIB-DIGEST-SIZE SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.RECEIPT LIBR.EXPECTED-CATALOG-GENERATION
        8 SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.RECEIPT LIBR.SOURCE-OWNER-U 8 SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.RECEIPT LIBR.SOURCE-OWNER
        QLOC-OWNER-MAX SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.TITLE-U 8 SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.TITLE LIB-TITLE-MAX SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.TAG-N 8 SHA3-256-ADD
    _LIBM-ENTRY @ _LIBE-TAGS + LIB-TAG-MAX LIB-TAG-SIZE * SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.ORIGIN LIB-ORIGIN-SIZE SHA3-256-ADD
    _LIBM-ENTRY @ LIBE.LINEAGE-N 8 SHA3-256-ADD
    _LIBM-ENTRY @ _LIBE-LINEAGE + LIB-LINEAGE-MAX LIB-LINEAGE-SIZE *
        SHA3-256-ADD
    _LIBM-SEAL-DST @ SHA3-256-END ;

