: _LIBM-TEXT-SLOT?  ( address capacity length allow-empty? -- flag )
    _LIBM-ALLOW-EMPTY ! _LIBM-U ! _LIBM-MAX ! _LIBM-A !
    _LIBM-U @ DUP 0< SWAP _LIBM-MAX @ > OR IF 0 EXIT THEN
    _LIBM-U @ 0= _LIBM-ALLOW-EMPTY @ 0= AND IF 0 EXIT THEN
    _LIBM-U @ IF
        _LIBM-A @ _LIBM-U @ UTF8-VALID? 0= IF 0 EXIT THEN
    THEN
    _LIBM-A @ _LIBM-U @ + _LIBM-MAX @ _LIBM-U @ - _LIBM-ZERO? ;

: _LIBM-KIND?  ( kind -- flag )
    DUP LIB-KIND-MANAGED-DOCUMENT = SWAP LIB-KIND-CAPTURE = OR ;

: _LIBM-LIFECYCLE?  ( lifecycle -- flag )
    DUP LIB-LIFECYCLE-ACTIVE =
    OVER LIB-LIFECYCLE-ARCHIVED = OR
    SWAP LIB-LIFECYCLE-TOMBSTONED = OR ;

: _LIBM-MEDIA?  ( media -- flag )
    DUP LIB-MEDIA-TEXT-PLAIN =
    OVER LIB-MEDIA-TEXT-MARKDOWN = OR
    SWAP LIB-MEDIA-TEXT-CSV = OR ;

: _LIBM-KIND-MEDIA?  ( kind media -- flag )
    OVER LIB-KIND-MANAGED-DOCUMENT = IF
        NIP DUP LIB-MEDIA-TEXT-PLAIN =
        SWAP LIB-MEDIA-TEXT-MARKDOWN = OR EXIT
    THEN
    SWAP LIB-KIND-CAPTURE = IF _LIBM-MEDIA? ELSE DROP 0 THEN ;

: _LIBM-TIME-REQUIRED?  ( clock value -- flag )
    OVER LIB-CLOCK-UNIX-MS = IF NIP 0>= EXIT THEN
    OVER LIB-CLOCK-MUTATION-SEQUENCE = IF NIP 0> EXIT THEN
    2DROP 0 ;

: _LIBM-TIME-NONE?  ( clock value -- flag )
    SWAP LIB-CLOCK-NONE = SWAP 0= AND ;

: _LIBM-MSEQ-AT-MOST?  ( clock value mutation-sequence -- flag )
    >R OVER LIB-CLOCK-MUTATION-SEQUENCE = IF
        NIP DUP 0> SWAP R@ <= AND R> DROP EXIT
    THEN
    2DROP R> DROP -1 ;

: _LIBM-MSEQ-EXACT?  ( clock value mutation-sequence -- flag )
    >R OVER LIB-CLOCK-MUTATION-SEQUENCE = IF
        NIP R> = EXIT
    THEN
    2DROP R> DROP -1 ;

: _LIBM-VFS-VALID?  ( locator -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP LIB-VFS-LOCATOR-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    _LIBM-VFS !
    _LIBM-VFS @ LIBV.PATH LIB-VFS-PATH-MAX 1+
        _LIBM-VFS @ LIBV.PATH-U @ 0 _LIBM-TEXT-SLOT? 0= IF 0 EXIT THEN
    _LIBM-VFS @ LIBV.PATH-U @ LIB-VFS-PATH-MAX > IF 0 EXIT THEN
    _LIBM-VFS @ LIBV.PATH C@ [CHAR] / <> IF 0 EXIT THEN
    _LIBM-VFS @ LIBV.CONTENT-U @ DUP 0< SWAP LIB-CONTENT-MAX > OR IF
        0 EXIT
    THEN
    _LIBM-VFS @ LIBV.DIGEST-KIND @ QLOC-DK-PROJECTION-CONTENT <> IF
        0 EXIT
    THEN
    _LIBM-VFS @ LIBV.FLAGS @ 0<> IF 0 EXIT THEN
    _LIBM-VFS @ LIBV.CONTENT-DIGEST RID-PRESENT? ;

: _LIBM-SEMANTIC-EXACT?  ( qloc -- flag )
    DUP QLOC-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP QLOC-VALID? 0= IF DROP 0 EXIT THEN
    DUP QLOC.MODE @ QLOC-M-EXACT-DOMAIN =
    OVER QLOC.STATE-DIGEST RID-PRESENT? AND
    SWAP DROP ;

: _LIBM-ORIGIN-VALID?  ( origin -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP LIB-ORIGIN-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP LIBO.KIND @ CASE
        LIB-ORIGIN-NONE OF LIB-ORIGIN-SIZE _LIBM-ZERO? ENDOF
        LIB-ORIGIN-SEMANTIC OF LIBO.SEMANTIC _LIBM-SEMANTIC-EXACT? ENDOF
        LIB-ORIGIN-VFS-SNAPSHOT OF LIBO.VFS _LIBM-VFS-VALID? ENDOF
        NIP 0 SWAP
    ENDCASE ;

: _LIBM-RECEIPT-VALID?  ( receipt -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP LIB-RECEIPT-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    _LIBM-RECEIPT !
    _LIBM-RECEIPT @ LIBR.OPERATION-KEY RID-PRESENT? 0= IF 0 EXIT THEN
    _LIBM-RECEIPT @ LIBR.INITIAL-CONTENT-REVISION @ 1 <> IF 0 EXIT THEN
    _LIBM-RECEIPT @ LIBR.INITIAL-CONTENT-U @ DUP 0<
        SWAP LIB-CONTENT-MAX > OR IF 0 EXIT THEN
    _LIBM-RECEIPT @ LIBR.INITIAL-MEDIA @ _LIBM-MEDIA? 0= IF 0 EXIT THEN
    _LIBM-RECEIPT @ LIBR.INITIAL-CONTENT-DIGEST RID-PRESENT? 0= IF
        0 EXIT
    THEN
    _LIBM-RECEIPT @ LIBR.REQUEST-SEAL RID-PRESENT? 0= IF 0 EXIT THEN
    _LIBM-RECEIPT @ LIBR.FLAGS @ 0<> IF 0 EXIT THEN
    _LIBM-RECEIPT @ LIBR.EXPECTED-CATALOG-GENERATION @ 0< IF 0 EXIT THEN
    _LIBM-RECEIPT @ LIBR.METHOD @ CASE
        LIB-IMPORT-CREATED OF
            _LIBM-RECEIPT @ LIBR.ORIGIN-REVISION @ 0=
            _LIBM-RECEIPT @ LIBR.ORIGIN-DIGEST-KIND @ QLOC-DK-NONE = AND
            _LIBM-RECEIPT @ LIBR.ORIGIN-STATE-DIGEST
                LIB-DIGEST-SIZE 2 * _LIBM-ZERO? AND
            _LIBM-RECEIPT @ LIBR.SOURCE-OWNER-U @ 0= AND
            _LIBM-RECEIPT @ LIBR.SOURCE-OWNER QLOC-OWNER-MAX
                _LIBM-ZERO? AND
            _LIBM-RECEIPT @ LIBR.IMPORT-CONTRACT-DIGEST
                LIB-DIGEST-SIZE _LIBM-ZERO? AND
        ENDOF
        LIB-IMPORT-VFS-SNAPSHOT OF
            _LIBM-RECEIPT @ LIBR.ORIGIN-REVISION @ 0=
            _LIBM-RECEIPT @ LIBR.ORIGIN-DIGEST-KIND @
                QLOC-DK-PROJECTION-CONTENT = AND
            _LIBM-RECEIPT @ LIBR.ORIGIN-STATE-DIGEST RID-PRESENT? AND
            _LIBM-RECEIPT @ LIBR.LOCATOR-DIGEST RID-PRESENT? AND
            _LIBM-RECEIPT @ LIBR.SOURCE-OWNER QLOC-OWNER-MAX
                _LIBM-RECEIPT @ LIBR.SOURCE-OWNER-U @ 0
                _LIBM-TEXT-SLOT? AND
            _LIBM-RECEIPT @ LIBR-SOURCE-OWNER$
                LIB-VFS-SOURCE-OWNER$ COMPARE 0= AND
            _LIBM-RECEIPT @ LIBR.IMPORT-CONTRACT-DIGEST RID-PRESENT? AND
            _LIBM-RECEIPT @ LIBR.INITIAL-CONTENT-DIGEST
                _LIBM-RECEIPT @ LIBR.ORIGIN-STATE-DIGEST
                SHA3-256-COMPARE AND
        ENDOF
        LIB-IMPORT-SEMANTIC-SNAPSHOT OF
            _LIBM-RECEIPT @ LIBR.ORIGIN-REVISION @ 0>
            _LIBM-RECEIPT @ LIBR.ORIGIN-DIGEST-KIND @ DUP
                QLOC-DK-SEMANTIC-STATE =
            SWAP QLOC-DK-PROJECTION-CONTENT = OR AND
            _LIBM-RECEIPT @ LIBR.ORIGIN-STATE-DIGEST RID-PRESENT? AND
            _LIBM-RECEIPT @ LIBR.LOCATOR-DIGEST RID-PRESENT? AND
            _LIBM-RECEIPT @ LIBR.SOURCE-OWNER QLOC-OWNER-MAX
                _LIBM-RECEIPT @ LIBR.SOURCE-OWNER-U @ 0
                _LIBM-TEXT-SLOT? AND
            _LIBM-RECEIPT @ LIBR.IMPORT-CONTRACT-DIGEST RID-PRESENT? AND
            _LIBM-RECEIPT @ LIBR.ORIGIN-DIGEST-KIND @
                QLOC-DK-PROJECTION-CONTENT = IF
                _LIBM-RECEIPT @ LIBR.INITIAL-CONTENT-DIGEST
                _LIBM-RECEIPT @ LIBR.ORIGIN-STATE-DIGEST
                    SHA3-256-COMPARE AND
            THEN
        ENDOF
        0 SWAP
    ENDCASE ;

: _LIBM-TAG-VALID?  ( tag -- flag )
    DUP LIB-TAG.TEXT LIB-TAG-TEXT-MAX
    ROT LIB-TAG.LEN @ 0 _LIBM-TEXT-SLOT? ;

: _LIBM-TAG-AFTER-PREVIOUS?  ( index entry -- flag )
    _LIBM-ENTRY ! _LIBM-I !
    _LIBM-I @ 0= IF -1 EXIT THEN
    _LIBM-I @ 1- _LIBM-ENTRY @ LIBE-TAG LIB-TAG$
    _LIBM-I @ _LIBM-ENTRY @ LIBE-TAG LIB-TAG$
    COMPARE 0< ;

: _LIBM-TAGS-VALID?  ( entry -- flag )
    DUP _LIBM-ENTRY ! LIBE.TAG-N @ DUP 0< SWAP LIB-TAG-MAX > OR IF
        0 EXIT
    THEN
    LIB-TAG-MAX 0 ?DO
        I _LIBM-ENTRY @ LIBE-TAG
        I _LIBM-ENTRY @ LIBE.TAG-N @ < IF
            DUP _LIBM-TAG-VALID? 0= IF DROP 0 UNLOOP EXIT THEN
            DROP
            I _LIBM-ENTRY @ _LIBM-TAG-AFTER-PREVIOUS? 0= IF
                0 UNLOOP EXIT
            THEN
        ELSE
            LIB-TAG-SIZE _LIBM-ZERO? 0= IF 0 UNLOOP EXIT THEN
        THEN
    LOOP
    -1 ;

: _LIBM-LINEAGE-AFTER-PREVIOUS?  ( index entry -- flag )
    _LIBM-ENTRY ! _LIBM-I !
    _LIBM-I @ 0= IF -1 EXIT THEN
    _LIBM-I @ 1- _LIBM-ENTRY @ LIBE-LINEAGE LIBLN.LOCATOR QLOC-SIZE
    _LIBM-I @ _LIBM-ENTRY @ LIBE-LINEAGE LIBLN.LOCATOR QLOC-SIZE
    COMPARE 0< ;

: _LIBM-LINEAGE-VALID?  ( entry -- flag )
    DUP _LIBM-ENTRY ! LIBE.LINEAGE-N @ DUP 0<
        SWAP LIB-LINEAGE-MAX > OR IF 0 EXIT THEN
    LIB-LINEAGE-MAX 0 ?DO
        I _LIBM-ENTRY @ LIBE-LINEAGE
        I _LIBM-ENTRY @ LIBE.LINEAGE-N @ < IF
            DUP LIBLN.RELATION @ LIB-LINEAGE-RELATION-DERIVED-FROM <> IF
                DROP 0 UNLOOP EXIT
            THEN
            LIBLN.LOCATOR _LIBM-SEMANTIC-EXACT? 0= IF 0 UNLOOP EXIT THEN
            I _LIBM-ENTRY @ _LIBM-LINEAGE-AFTER-PREVIOUS? 0= IF
                0 UNLOOP EXIT
            THEN
        ELSE
            LIB-LINEAGE-SIZE _LIBM-ZERO? 0= IF 0 UNLOOP EXIT THEN
        THEN
    LOOP
    -1 ;

: _LIBM-RECEIPT-MATCHES-ORIGIN?  ( entry -- flag )
    _LIBM-ENTRY !
    _LIBM-ENTRY @ LIBE.ORIGIN LIB-ORIGIN-SIZE
        _LIBM-DIGEST SHA3-256-HASH
    _LIBM-DIGEST _LIBM-ENTRY @ LIBE.RECEIPT LIBR.LOCATOR-DIGEST
        SHA3-256-COMPARE 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.ORIGIN LIBO.KIND @ CASE
        LIB-ORIGIN-VFS-SNAPSHOT OF
            _LIBM-ENTRY @ LIBE.RECEIPT LIBR.METHOD @
                LIB-IMPORT-VFS-SNAPSHOT <> IF 0 EXIT THEN
            _LIBM-ENTRY @ LIBE.RECEIPT LIBR.ORIGIN-REVISION @ IF 0 EXIT THEN
            _LIBM-ENTRY @ LIBE.ORIGIN LIBO.VFS LIBV.DIGEST-KIND @
                _LIBM-ENTRY @ LIBE.RECEIPT LIBR.ORIGIN-DIGEST-KIND @
                <> IF 0 EXIT THEN
            _LIBM-ENTRY @ LIBE.ORIGIN LIBO.VFS LIBV.CONTENT-U @
                _LIBM-ENTRY @ LIBE.RECEIPT LIBR.INITIAL-CONTENT-U @
                <> IF 0 EXIT THEN
            _LIBM-ENTRY @ LIBE.ORIGIN LIBO.VFS LIBV.CONTENT-DIGEST
                _LIBM-ENTRY @ LIBE.RECEIPT LIBR.ORIGIN-STATE-DIGEST
                SHA3-256-COMPARE 0= IF 0 EXIT THEN
            _LIBM-ENTRY @ LIBE.RECEIPT LIBR-SOURCE-OWNER$
                LIB-VFS-SOURCE-OWNER$ COMPARE 0=
        ENDOF
        LIB-ORIGIN-SEMANTIC OF
            _LIBM-ENTRY @ LIBE.RECEIPT LIBR.METHOD @
                LIB-IMPORT-SEMANTIC-SNAPSHOT <> IF 0 EXIT THEN
            _LIBM-ENTRY @ LIBE.ORIGIN LIBO.SEMANTIC
            DUP QLOC.DOMAIN-REVISION @
                _LIBM-ENTRY @ LIBE.RECEIPT LIBR.ORIGIN-REVISION @
                <> IF DROP 0 EXIT THEN
            DUP QLOC.DIGEST-KIND @
                _LIBM-ENTRY @ LIBE.RECEIPT LIBR.ORIGIN-DIGEST-KIND @
                <> IF DROP 0 EXIT THEN
            DUP QLOC.STATE-DIGEST
                _LIBM-ENTRY @ LIBE.RECEIPT LIBR.ORIGIN-STATE-DIGEST
                SHA3-256-COMPARE 0= IF DROP 0 EXIT THEN
            DUP QLOC-OWNER$
                _LIBM-ENTRY @ LIBE.RECEIPT LIBR-SOURCE-OWNER$ COMPARE
            IF DROP 0 EXIT THEN
            QLOC-PROJECTION$ _LIBM-DIGEST SHA3-256-HASH
            _LIBM-DIGEST _LIBM-ENTRY @ LIBE.RECEIPT
                LIBR.IMPORT-CONTRACT-DIGEST SHA3-256-COMPARE
        ENDOF
        0 SWAP
    ENDCASE ;

: _LIBM-ENTRY-TOMBSTONE?  ( entry -- flag )
    _LIBM-ENTRY !
    _LIBM-ENTRY @ LIBE.MEDIA @ LIB-MEDIA-NONE =
    _LIBM-ENTRY @ LIBE.CURRENT-CONTENT-REVISION @ 0= AND
    _LIBM-ENTRY @ LIBE.OLDEST-CONTENT-REVISION @ 0= AND
    _LIBM-ENTRY @ LIBE.CONTENT-U @ 0= AND
    _LIBM-ENTRY @ LIBE.CONTENT-DIGEST LIB-DIGEST-SIZE _LIBM-ZERO? AND
    _LIBM-ENTRY @ LIBE.CREATED-CLOCK @
        _LIBM-ENTRY @ LIBE.CREATED-VALUE @ _LIBM-TIME-NONE? AND
    _LIBM-ENTRY @ LIBE.IMPORTED-CLOCK @
        _LIBM-ENTRY @ LIBE.IMPORTED-VALUE @ _LIBM-TIME-NONE? AND
    _LIBM-ENTRY @ LIBE.MODIFIED-CLOCK @
        _LIBM-ENTRY @ LIBE.MODIFIED-VALUE @ _LIBM-TIME-NONE? AND
    _LIBM-ENTRY @ LIBE.DELETED-CLOCK @
        _LIBM-ENTRY @ LIBE.DELETED-VALUE @ _LIBM-TIME-REQUIRED? AND
    _LIBM-ENTRY @ LIBE.DELETED-CLOCK @
        _LIBM-ENTRY @ LIBE.DELETED-VALUE @
        _LIBM-ENTRY @ LIBE.MUTATION-SEQUENCE @ _LIBM-MSEQ-EXACT? AND
    _LIBM-ENTRY @ LIBE.TITLE-U @ 0= AND
    _LIBM-ENTRY @ LIBE.TITLE LIB-TITLE-MAX _LIBM-ZERO? AND
    _LIBM-ENTRY @ LIBE.TAG-N @ 0= AND
    _LIBM-ENTRY @ _LIBE-TAGS + LIB-TAG-MAX LIB-TAG-SIZE *
        _LIBM-ZERO? AND
    _LIBM-ENTRY @ LIBE.ORIGIN LIB-ORIGIN-SIZE _LIBM-ZERO? AND
    _LIBM-ENTRY @ LIBE.LINEAGE-N @ 0= AND
    _LIBM-ENTRY @ _LIBE-LINEAGE +
        LIB-LINEAGE-MAX LIB-LINEAGE-SIZE * _LIBM-ZERO? AND ;

: _LIBM-ENTRY-REVISION-WINDOW?  ( entry -- flag )
    DUP _LIBM-ENTRY ! LIBE.CURRENT-CONTENT-REVISION @ DUP 0> 0= IF
        DROP 0 EXIT
    THEN
    DUP _LIBM-ENTRY @ LIBE.DOMAIN-REVISION @ > IF DROP 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.KIND @ LIB-KIND-CAPTURE = IF
        1 = _LIBM-ENTRY @ LIBE.OLDEST-CONTENT-REVISION @ 1 = AND EXIT
    THEN
    DUP LIB-RETAINED-REVISION-MAX 1- > IF
        LIB-RETAINED-REVISION-MAX 1- -
    ELSE
        DROP 1
    THEN
    _LIBM-ENTRY @ LIBE.OLDEST-CONTENT-REVISION @ = ;

: _LIBM-ENTRY-LIVE?  ( entry -- flag )
    _LIBM-ENTRY !
    _LIBM-ENTRY @ LIBE.KIND @ _LIBM-ENTRY @ LIBE.MEDIA @
        _LIBM-KIND-MEDIA? 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.MEDIA @
        _LIBM-ENTRY @ LIBE.RECEIPT LIBR.INITIAL-MEDIA @ <> IF 0 EXIT THEN
    _LIBM-ENTRY @ _LIBM-ENTRY-REVISION-WINDOW? 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.CONTENT-U @ DUP 0< SWAP LIB-CONTENT-MAX > OR IF
        0 EXIT
    THEN
    _LIBM-ENTRY @ LIBE.CONTENT-DIGEST RID-PRESENT? 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.CREATED-CLOCK @
        _LIBM-ENTRY @ LIBE.CREATED-VALUE @ _LIBM-TIME-REQUIRED? 0= IF
        0 EXIT
    THEN
    _LIBM-ENTRY @ LIBE.MODIFIED-CLOCK @
        _LIBM-ENTRY @ LIBE.MODIFIED-VALUE @ _LIBM-TIME-REQUIRED? 0= IF
        0 EXIT
    THEN
    _LIBM-ENTRY @ LIBE.CREATED-CLOCK @
        _LIBM-ENTRY @ LIBE.CREATED-VALUE @
        _LIBM-ENTRY @ LIBE.MUTATION-SEQUENCE @ _LIBM-MSEQ-AT-MOST? 0= IF
        0 EXIT
    THEN
    _LIBM-ENTRY @ LIBE.IMPORTED-CLOCK @
        _LIBM-ENTRY @ LIBE.IMPORTED-VALUE @
        _LIBM-ENTRY @ LIBE.MUTATION-SEQUENCE @ _LIBM-MSEQ-AT-MOST? 0= IF
        0 EXIT
    THEN
    _LIBM-ENTRY @ LIBE.MODIFIED-CLOCK @
        _LIBM-ENTRY @ LIBE.MODIFIED-VALUE @
        _LIBM-ENTRY @ LIBE.MUTATION-SEQUENCE @ _LIBM-MSEQ-EXACT? 0= IF
        0 EXIT
    THEN
    _LIBM-ENTRY @ LIBE.DELETED-CLOCK @
        _LIBM-ENTRY @ LIBE.DELETED-VALUE @ _LIBM-TIME-NONE? 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.TITLE LIB-TITLE-MAX
        _LIBM-ENTRY @ LIBE.TITLE-U @ 0 _LIBM-TEXT-SLOT? 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ _LIBM-TAGS-VALID? 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ _LIBM-LINEAGE-VALID? 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.KIND @
        _LIBM-ENTRY @ LIBE.RECEIPT LIBR.INITIAL-MEDIA @
        _LIBM-KIND-MEDIA? 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.CURRENT-CONTENT-REVISION @ 1 = IF
        _LIBM-ENTRY @ LIBE.CONTENT-U @
            _LIBM-ENTRY @ LIBE.RECEIPT LIBR.INITIAL-CONTENT-U @ =
        _LIBM-ENTRY @ LIBE.MEDIA @
            _LIBM-ENTRY @ LIBE.RECEIPT LIBR.INITIAL-MEDIA @ = AND
        _LIBM-ENTRY @ LIBE.CONTENT-DIGEST
            _LIBM-ENTRY @ LIBE.RECEIPT LIBR.INITIAL-CONTENT-DIGEST
            SHA3-256-COMPARE AND 0= IF 0 EXIT THEN
    THEN
    _LIBM-ENTRY @ LIBE.DOMAIN-REVISION @ 1 = IF
        _LIBM-ENTRY @ _LIBM-REQUEST-SEAL-MATCHES? 0= IF 0 EXIT THEN
    THEN
    _LIBM-ENTRY @ LIBE.RECEIPT LIBR.METHOD @ LIB-IMPORT-CREATED = IF
        _LIBM-ENTRY @ LIBE.KIND @ LIB-KIND-CAPTURE = IF 0 EXIT THEN
        _LIBM-ENTRY @ LIBE.ORIGIN LIB-ORIGIN-SIZE _LIBM-ZERO? 0= IF
            0 EXIT
        THEN
        _LIBM-ENTRY @ LIBE.IMPORTED-CLOCK @
            _LIBM-ENTRY @ LIBE.IMPORTED-VALUE @ _LIBM-TIME-NONE? EXIT
    THEN
    _LIBM-ENTRY @ LIBE.ORIGIN _LIBM-ORIGIN-VALID? 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.IMPORTED-CLOCK @
        _LIBM-ENTRY @ LIBE.IMPORTED-VALUE @ _LIBM-TIME-REQUIRED? 0= IF
        0 EXIT
    THEN
    _LIBM-ENTRY @ _LIBM-RECEIPT-MATCHES-ORIGIN? ;

: _LIBM-ENTRY-VALID?  ( entry -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP LIB-ENTRY-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    _LIBM-ENTRY !
    _LIBM-ENTRY @ LIBE.ID RID-PRESENT? 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.DOMAIN-REVISION @ 0> 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.KIND @ _LIBM-KIND? 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.LIFECYCLE @ _LIBM-LIFECYCLE? 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.DOMAIN-REVISION @ 1 =
    _LIBM-ENTRY @ LIBE.LIFECYCLE @ LIB-LIFECYCLE-ACTIVE <> AND IF
        0 EXIT
    THEN
    _LIBM-ENTRY @ LIBE.MUTATION-SEQUENCE @ 0> 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.FLAGS @ 0<> IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.RECEIPT _LIBM-RECEIPT-VALID? 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.ID
        _LIBM-ENTRY @ LIBE.RECEIPT LIBR.OPERATION-KEY RID= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.KIND @
        _LIBM-ENTRY @ LIBE.RECEIPT LIBR.INITIAL-MEDIA @
        _LIBM-KIND-MEDIA? 0= IF 0 EXIT THEN
    _LIBM-ENTRY @ LIBE.LIFECYCLE @ LIB-LIFECYCLE-TOMBSTONED = IF
        _LIBM-ENTRY @ _LIBM-ENTRY-TOMBSTONE?
    ELSE
        _LIBM-ENTRY @ _LIBM-ENTRY-LIVE?
    THEN ;

: _LIBM-COLLECTION-VALID?  ( collection -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP LIB-COLLECTION-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    _LIBM-COLLECTION !
    _LIBM-COLLECTION @ LIBC.ID RID-PRESENT? 0= IF 0 EXIT THEN
    _LIBM-COLLECTION @ LIBC.OPERATION-KEY RID-PRESENT? 0= IF 0 EXIT THEN
    _LIBM-COLLECTION @ LIBC.ID
        _LIBM-COLLECTION @ LIBC.OPERATION-KEY RID= IF 0 EXIT THEN
    _LIBM-COLLECTION @ LIBC.REQUEST-SEAL RID-PRESENT? 0= IF 0 EXIT THEN
    _LIBM-COLLECTION @ LIBC.REVISION @ 0> 0= IF 0 EXIT THEN
    _LIBM-COLLECTION @ LIBC.MUTATION-SEQUENCE @ 0> 0= IF 0 EXIT THEN
    _LIBM-COLLECTION @ LIBC.FLAGS @ 0<> IF 0 EXIT THEN
    _LIBM-COLLECTION @ LIBC.TITLE LIB-COLLECTION-TITLE-MAX
        _LIBM-COLLECTION @ LIBC.TITLE-U @ 0 _LIBM-TEXT-SLOT? 0= IF 0 EXIT THEN
    _LIBM-COLLECTION @ LIBC.EXPECTED-CATALOG-GENERATION @ 0< IF 0 EXIT THEN
    _LIBM-COLLECTION @ LIBC.MEMBER-N @ DUP 0<
        SWAP LIB-COLLECTION-MEMBER-MAX > OR IF 0 EXIT THEN
    _LIBM-COLLECTION @ _LIBM-MEMBERS-COUNT
        _LIBM-COLLECTION @ LIBC.MEMBER-N @ <> IF 0 EXIT THEN
    _LIBM-COLLECTION @ LIBC.REVISION @ 1 = IF
        _LIBM-COLLECTION @ _LIBM-COLLECTION-REQUEST-SEAL-MATCHES?
    ELSE
        -1
    THEN ;

: _LIBM-CONTENT-SHAPE?  ( content -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP LIB-CONTENT-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    _LIBM-CONTENT !
    _LIBM-CONTENT @ LIBCT.ID RID-PRESENT? 0= IF 0 EXIT THEN
    _LIBM-CONTENT @ LIBCT.DOMAIN-REVISION @ 0> 0= IF 0 EXIT THEN
    _LIBM-CONTENT @ LIBCT.CONTENT-REVISION @ DUP 0> 0= IF DROP 0 EXIT THEN
    _LIBM-CONTENT @ LIBCT.DOMAIN-REVISION @ > IF 0 EXIT THEN
    _LIBM-CONTENT @ LIBCT.KIND @ _LIBM-CONTENT @ LIBCT.MEDIA @
        _LIBM-KIND-MEDIA? 0= IF 0 EXIT THEN
    _LIBM-CONTENT @ LIBCT.KIND @ LIB-KIND-CAPTURE =
    _LIBM-CONTENT @ LIBCT.CONTENT-REVISION @ 1 <> AND IF 0 EXIT THEN
    _LIBM-CONTENT @ LIBCT.DATA-U @ DUP 0< SWAP LIB-CONTENT-MAX > OR IF
        0 EXIT
    THEN
    _LIBM-CONTENT @ LIBCT.DATA-U @ 0>
    _LIBM-CONTENT @ LIBCT.DATA-A @ 0= AND IF 0 EXIT THEN
    _LIBM-CONTENT @ LIBCT-DATA$ MSPAN-NONWRAPPING? 0= IF 0 EXIT THEN
    _LIBM-CONTENT @ LIBCT.FLAGS @ 0<> IF 0 EXIT THEN
    _LIBM-CONTENT @ LIBCT.RESERVED 8 _LIBM-ZERO? ;

: _LIBM-CONTENT-VALID?  ( content -- flag )
    DUP _LIBM-CONTENT ! _LIBM-CONTENT-SHAPE? 0= IF 0 EXIT THEN
    _LIBM-CONTENT @ LIBCT-DATA$ 2DUP UTF8-VALID? 0= IF 2DROP 0 EXIT THEN
    _LIBM-DIGEST SHA3-256-HASH
    _LIBM-DIGEST _LIBM-CONTENT @ LIBCT.DIGEST SHA3-256-COMPARE ;

: _LIBM-CONTENT-DIGEST!  ( content -- status )
    DUP _LIBM-CONTENT ! _LIBM-CONTENT-SHAPE? 0= IF LIB-S-INVALID EXIT THEN
    _LIBM-CONTENT @ LIBCT-DATA$ 2DUP UTF8-VALID? 0= IF
        2DROP LIB-S-INVALID EXIT
    THEN
    _LIBM-CONTENT @ LIBCT.DIGEST SHA3-256-HASH LIB-S-OK ;

: _LIBM-ENTRY-REQUEST-SEAL!  ( entry -- status )
    DUP 0= IF DROP LIB-S-INVALID EXIT THEN
    DUP LIB-ENTRY-SIZE MSPAN-NONWRAPPING? 0= IF DROP LIB-S-INVALID EXIT THEN
    DUP LIBE.DOMAIN-REVISION @ 1 <> IF DROP LIB-S-INVALID EXIT THEN
    DUP LIBE.LIFECYCLE @ LIB-LIFECYCLE-ACTIVE <> IF
        DROP LIB-S-INVALID EXIT
    THEN
    DUP _LIBM-ENTRY ! DUP LIBE.RECEIPT LIBR.REQUEST-SEAL
        _LIBM-REQUEST-HASH
    _LIBM-ENTRY @ _LIBM-ENTRY-VALID? IF LIB-S-OK EXIT THEN
    _LIBM-ENTRY @ LIBE.RECEIPT LIBR.REQUEST-SEAL LIB-DIGEST-SIZE 0 FILL
    LIB-S-INVALID ;

: _LIBM-COLLECTION-REQUEST-SEAL!  ( initial-collection -- status )
    DUP 0= IF DROP LIB-S-INVALID EXIT THEN
    DUP LIB-COLLECTION-SIZE MSPAN-NONWRAPPING? 0= IF
        DROP LIB-S-INVALID EXIT
    THEN
    DUP LIBC.REVISION @ 1 <> IF DROP LIB-S-INVALID EXIT THEN
    DUP _LIBM-COLLECTION ! DUP LIBC.REQUEST-SEAL
        _LIBM-COLLECTION-REQUEST-HASH
    _LIBM-COLLECTION @ _LIBM-COLLECTION-VALID? IF LIB-S-OK EXIT THEN
    _LIBM-COLLECTION @ LIBC.REQUEST-SEAL LIB-DIGEST-SIZE 0 FILL
    LIB-S-INVALID ;

