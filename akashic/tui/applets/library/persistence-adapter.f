: _LIBPIX-PUT  ( key-a key-u value-a value-u tree-index index-work -- status )
    >R
    DUP 0< OVER _LIBPIX-TREE-COUNT >= OR IF
        DROP _LIBPA-DROP4 R> DROP LIBPA-S-INVALID EXIT
    THEN
    DUP R@ _LIBPIX.PAGE-ID ! DROP
    R@ _LIBPIX.PAGE-ID @ R@ _LIBPIX-ROOT
    R@ _LIBPIX.TEMP-ROOT
    R@ _LIBPIX.PAGE-ID @ R@ _LIBPIX-TREE
    R@ _LIBPIX.BTREE-WORK PBTREE-PUT
    DUP IF _LIBPA-PERSIST>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPIX.BTREE-WORK PBTREE-RETIRED-PAGES$
    DUP IF
        R@ _LIBPIX.RECLAIM-WORK RECLAIM-RELEASE-BATCH
        DUP IF _LIBPA-PERSIST>STATUS R> DROP EXIT THEN DROP
    ELSE
        2DROP
    THEN
    R@ _LIBPIX.TEMP-ROOT
    R@ _LIBPIX.PAGE-ID @ R@ _LIBPIX-ROOT PBTREE-ROOT-COPY
    _LIBPA-PERSIST>STATUS
    R> DROP ;

: _LIBPIX-ENCODE-LIBRARY-CONTENT  ( content index-work -- status )
    >R
    DUP LIB-CONTENT-VALID? 0= IF DROP R> DROP LIBPA-S-INVALID EXIT THEN
    DUP LIBCT.DATA-U @ R@ _LIBPIX.BLOB PBLOB-TOTAL@ <> IF
        DROP R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    R@ _LIBPIX.STAGE DUP LIBPA-CONTENT-DESCRIPTOR-SIZE 0 FILL
    _LIBPA-CONTENT-MAGIC OVER _LIBPAC.MAGIC !
    0 OVER _LIBPAC.FLAGS !
    OVER LIBCT.ID OVER _LIBPAC.RID RID-COPY
    OVER LIBCT.DOMAIN-REVISION @ OVER _LIBPAC.DOMAIN-REVISION !
    OVER LIBCT.CONTENT-REVISION @ OVER _LIBPAC.CONTENT-REVISION !
    OVER LIBCT.KIND @ OVER _LIBPAC.KIND !
    OVER LIBCT.MEDIA @ OVER _LIBPAC.MEDIA !
    OVER LIBCT.DATA-U @ OVER _LIBPAC.DATA-U !
    OVER LIBCT.DIGEST OVER _LIBPAC.DIGEST RID-COPY
    R@ _LIBPIX.BLOB OVER _LIBPAC.BLOB PBLOB-COPY
    DUP IF _LIBPA-PERSIST>STATUS >R 2DROP R> R> DROP EXIT THEN DROP
    DUP LIBPA-CONTENT-DESCRIPTOR-VALID? 0= IF
        2DROP R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    2DROP R> DROP LIBPA-S-OK ;

: _LIBPIX-ENCODE-RAW-CONTENT  ( rid revision index-work -- status )
    >R
    OVER RID-PRESENT? 0= IF 2DROP R> DROP LIBPA-S-INVALID EXIT THEN
    DUP 0> 0= IF 2DROP R> DROP LIBPA-S-INVALID EXIT THEN
    R@ _LIBPIX.BLOB PBLOB-VALID? 0= IF 2DROP R> DROP LIBPA-S-CORRUPT EXIT THEN
    R@ _LIBPIX.STAGE DUP LIBPA-CONTENT-DESCRIPTOR-SIZE 0 FILL
    _LIBPA-CONTENT-MAGIC OVER _LIBPAC.MAGIC !
    LIBPA-CONTENT-F-RAW OVER _LIBPAC.FLAGS !
    2 PICK OVER _LIBPAC.RID RID-COPY
    OVER OVER _LIBPAC.CONTENT-REVISION !
    R@ _LIBPIX.BLOB PBLOB-TOTAL@ OVER _LIBPAC.DATA-U !
    R@ _LIBPIX.BLOB OVER _LIBPAC.BLOB PBLOB-COPY
    DUP IF _LIBPA-PERSIST>STATUS >R 2DROP 2DROP R> R> DROP EXIT THEN DROP
    DUP LIBPA-CONTENT-DESCRIPTOR-VALID? 0= IF
        2DROP DROP R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    2DROP DROP R> DROP LIBPA-S-OK ;

