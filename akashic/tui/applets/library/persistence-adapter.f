: _LIBPIX-KEY-ABSENT-TX  ( key-a key-u tree-index index-work -- status )
    >R
    DUP 0< OVER _LIBPIX-TREE-COUNT >= OR IF
        _LIBPA-DROP3 R> DROP LIBPA-S-INVALID EXIT
    THEN
    DUP R@ _LIBPIX-ROOT
    OVER R@ _LIBPIX-TREE
    ROT DROP
    R@ _LIBPIX.BTREE-WORK PBTREE-GET-TX
    DUP PERSIST-S-NOT-FOUND = IF
        DROP 2DROP R> DROP LIBPA-S-OK EXIT
    THEN
    DUP IF
        _LIBPA-PERSIST>STATUS >R 2DROP R> R> DROP EXIT
    THEN
    DROP 2DROP R> DROP LIBPA-S-CONFLICT ;

