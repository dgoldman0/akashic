    R@ _LIBPIX.PAGE-ID @ R@ _LIBPIX-MEMBERSHIP-FIRST
    DUP LIBPA-S-NOT-FOUND = IF DROP R> DROP LIBPA-S-OK EXIT THEN
    DUP IF R> DROP EXIT THEN DROP
    BEGIN R@ _LIBPIX.RESULT-N @ R@ _LIBPIX.TOTAL-ARG @ < WHILE
        R@ _LIBPIX-MEMBERSHIP-NEXT
        DUP LIBPA-S-NOT-FOUND = IF
            DROP
            R@ _LIBPIX.PAGE R@ _LIBPIX.ENTRY-ARG @
                R@ _LIBPIX.RESULT-N @ RID-SIZE * MOVE
            R> DROP LIBPA-S-OK EXIT
        THEN
        DUP IF R> DROP EXIT THEN DROP
    REPEAT
    R@ _LIBPIX.PAGE R@ _LIBPIX.ENTRY-ARG @
        R@ _LIBPIX.RESULT-N @ RID-SIZE * MOVE
    R> DROP LIBPA-S-OK ;

