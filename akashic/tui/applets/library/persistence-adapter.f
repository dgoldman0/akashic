    R@ _LIBPIX.RID-ARG @ DUP 0= IF
        DROP R> DROP LIBPA-S-INVALID EXIT
    THEN
    DUP RID-SIZE R@ _LIBPIX-SPAN-SAFE? 0= IF
        DROP R> DROP LIBPA-S-INVALID EXIT
    THEN
    RID-PRESENT? 0= IF R> DROP LIBPA-S-INVALID EXIT THEN
    R@ _LIBPIX.OTHER-RID-ARG @ DUP IF
        DUP RID-SIZE R@ _LIBPIX-SPAN-SAFE? 0= IF
            DROP R> DROP LIBPA-S-INVALID EXIT
        THEN
