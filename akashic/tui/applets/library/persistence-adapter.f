\ Scoped operation mechanics
\ ---------------------------------------------------------------------

: _LIBPA-BOUND?  ( adapter work -- flag )
    >R
    R@ LIBPA-WORK-VALID? 0= IF DROP R> DROP 0 EXIT THEN
    DUP LIBPA-VALID? 0= IF DROP R> DROP 0 EXIT THEN
    DUP LIBPA-SIZE R@ LIBPA-WORK-SIZE
        MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    DUP _LIBPA-D.STORE @
    1 PICK LIBPA-SIZE 2 PICK PSTORE-SPAN-DISJOINT? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    R@ LIBPA-WORK-SIZE 2 PICK PSTORE-SPAN-DISJOINT? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    R@ _LIBPA-W.STAGE-A @ R@ _LIBPA-W.STAGE-U @
        2 PICK PSTORE-SPAN-DISJOINT? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    DUP PSTORE-SIZE R@ LIBPA-WORK-SIZE
        MSPAN-OVERLAP? IF 2DROP R> DROP 0 EXIT THEN
    DUP PSTORE-SIZE R@ _LIBPA-W.PSTORE-WORK @ PSTORE-WORK-SIZE
        MSPAN-OVERLAP? IF 2DROP R> DROP 0 EXIT THEN
    PSTORE-SIZE R@ _LIBPA-W.STAGE-A @ R@ _LIBPA-W.STAGE-U @
        MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    DROP R> DROP -1 ;

: _LIBPA-ENTER  ( adapter work -- status )
    2DUP _LIBPA-BOUND? 0= IF 2DROP LIBPA-S-INVALID EXIT THEN
    DUP _LIBPA-W.BUSY @ IF 2DROP LIBPA-S-BUSY EXIT THEN
    -1 OVER _LIBPA-W.BUSY !
    OVER OVER _LIBPA-W.ADAPTER !
    LIBPA-S-INVALID OVER _LIBPA-W.STATUS !
    2DROP LIBPA-S-OK ;

: _LIBPA-LEAVE  ( status adapter work -- status )
    >R
    OVER OVER _LIBPA-D.STATUS !
    OVER R@ _LIBPA-W.STATUS !
    0 R@ _LIBPA-W.BUSY !
    0 R@ _LIBPA-W.ADAPTER !
    DROP R> DROP ;

: _LIBPA-REJECT  ( adapter work status -- status )
    >R
    DUP LIBPA-WORK-VALID? IF R@ OVER _LIBPA-W.STATUS ! THEN
    OVER LIBPA-VALID? IF R@ 2 PICK _LIBPA-D.STATUS ! THEN
    2DROP R> ;

: _LIBPA-WORK-CATCH  ( work body-xt -- status )
    >R DUP R> CATCH
    DUP IF
        _LIBPA-DROP3 LIBPA-S-FAULT
    ELSE
        DROP NIP
    THEN ;

: _LIBPA-ABORT-PRIMARY  ( primary-status work -- final-status )
    >R
    DUP R@ _LIBPA-W.TEMP-STATUS !
    R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-ABORT
    DUP PERSIST-S-OK = IF
        DROP R@ _LIBPA-W.TEMP-STATUS @
    ELSE
        _LIBPA-PERSIST>STATUS
    THEN
    R> DROP ;

\ ---------------------------------------------------------------------
\ Provision, open, create, and bounded read
\ ---------------------------------------------------------------------

: _LIBPA-PROVISION-RUN  ( work -- status )
    DUP _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @
    SWAP _LIBPA-W.PSTORE-WORK @ PSTORE-PROVISION
    _LIBPA-PERSIST>STATUS ;

: LIBPA-PROVISION  ( adapter work -- status )
    2DUP _LIBPA-ENTER DUP IF _LIBPA-REJECT EXIT THEN DROP
    DUP ['] _LIBPA-PROVISION-RUN _LIBPA-WORK-CATCH
    ROT ROT _LIBPA-LEAVE ;

: _LIBPA-OPEN-RUN  ( work -- status )
    >R
    R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-OPEN
    DUP PERSIST-S-ABSENT = IF R> DROP EXIT THEN
    DUP IF _LIBPA-PERSIST>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPA-LOAD-CURRENT R> DROP ;

: LIBPA-OPEN  ( adapter work -- status )
    2DUP _LIBPA-ENTER DUP IF _LIBPA-REJECT EXIT THEN DROP
    DUP ['] _LIBPA-OPEN-RUN _LIBPA-WORK-CATCH
    ROT ROT _LIBPA-LEAVE ;

: _LIBPA-CREATE-RUN  ( work -- status )
    >R
    R@ _LIBPA-CREATE-ARGS? 0= IF R> DROP LIBPA-S-INVALID EXIT THEN
    R@ _LIBPA-W.ENTRY-ARG @ LIBE.ID R@ _LIBPA-W.ROOT-RID RID-COPY

    R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-BEGIN
    DUP IF _LIBPA-PERSIST>STATUS R> DROP EXIT THEN DROP

    R@ _LIBPA-W.CONTENT-ARG @
    R@ _LIBPA-W.STAGE-A @ R@ _LIBPA-W.STAGE-U @
        LIB-CONTENT-RECORD-ENCODE
    DUP IF
        _LIBPA-LIB>STATUS R@ _LIBPA-W.TEMP-STATUS ! DROP
        R@ _LIBPA-W.TEMP-STATUS @ R@ _LIBPA-ABORT-PRIMARY
        R> DROP EXIT
    THEN DROP
    R@ _LIBPA-W.STAGE-A @ SWAP R@ _LIBPA-W.CONTENT-REF
    R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-APPEND-RECORD
    DUP IF
        _LIBPA-PERSIST>STATUS R@ _LIBPA-ABORT-PRIMARY R> DROP EXIT
    THEN DROP

    R@ _LIBPA-W.ENTRY-ARG @
    R@ _LIBPA-W.STAGE-A @ R@ _LIBPA-W.STAGE-U @
        LIB-CATALOG-RECORD-ENCODE
    DUP IF
        _LIBPA-LIB>STATUS R@ _LIBPA-W.TEMP-STATUS ! DROP
        R@ _LIBPA-W.TEMP-STATUS @ R@ _LIBPA-ABORT-PRIMARY
        R> DROP EXIT
    THEN DROP
    R@ _LIBPA-W.STAGE-A @ SWAP R@ _LIBPA-W.ENTRY-REF
    R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-APPEND-RECORD
    DUP IF
        _LIBPA-PERSIST>STATUS R@ _LIBPA-ABORT-PRIMARY R> DROP EXIT
    THEN DROP

    R@ _LIBPA-WORK>ROOT
    R@ _LIBPA-W.STAGE-A @ PERSIST-PAGE-PAYLOAD-SIZE
    R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-APPEND-PAGE
    DUP IF
        _LIBPA-PERSIST>STATUS R@ _LIBPA-W.TEMP-STATUS ! DROP
        R@ _LIBPA-W.TEMP-STATUS @ R@ _LIBPA-ABORT-PRIMARY
        R> DROP EXIT
    THEN DROP
    R@ _LIBPA-W.PAGE-ID !
    R@ _LIBPA-W.PAGE-ID @
    R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-APPLICATION-ROOT!
    DUP IF
        _LIBPA-PERSIST>STATUS R@ _LIBPA-ABORT-PRIMARY R> DROP EXIT
    THEN DROP

    R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-COMMIT
    DUP IF _LIBPA-PERSIST>STATUS R> DROP EXIT THEN DROP

    R@ _LIBPA-LOAD-CURRENT DUP IF R> DROP EXIT THEN DROP
    R@ _LIBPA-W.STAGE-A @ R@ _LIBPA-W.ENTRY-ARG @ LIB-ENTRY-SIZE
        _LIBPA-BYTES= 0= IF R> DROP LIBPA-S-CORRUPT EXIT THEN
    R@ _LIBPA-W.CONTENT-ARG @ R@ _LIBPA-W.CONTENT
        _LIBPA-CONTENT= 0= IF R> DROP LIBPA-S-CORRUPT EXIT THEN
    R> DROP LIBPA-S-OK ;

: LIBPA-CREATE  ( entry content adapter work -- status )
    2DUP _LIBPA-ENTER DUP IF
        _LIBPA-REJECT >R 2DROP R> EXIT
    THEN DROP
    >R
    1 PICK R@ _LIBPA-W.CONTENT-ARG !
    2 PICK R@ _LIBPA-W.ENTRY-ARG !
    R@ ['] _LIBPA-CREATE-RUN _LIBPA-WORK-CATCH
    R@ _LIBPA-W.ADAPTER @ R@ _LIBPA-LEAVE
    >R _LIBPA-DROP3 R> R> DROP ;

: _LIBPA-READ-RUN  ( work -- status )
    >R
    0 R@ _LIBPA-W.RESULT-U !
    R@ _LIBPA-READ-ARGS? 0= IF R> DROP LIBPA-S-INVALID EXIT THEN
    R@ _LIBPA-LOAD-CURRENT DUP IF R> DROP EXIT THEN DROP
    R@ _LIBPA-W.ROOT-RID R@ _LIBPA-W.RID-ARG @ RID= 0= IF
        R> DROP LIBPA-S-NOT-FOUND EXIT
    THEN
    R@ _LIBPA-W.CONTENT LIBCT.DATA-U @
    DUP R@ _LIBPA-W.DATA-CAP @ > IF DROP R> DROP LIBPA-S-CAPACITY EXIT THEN
    DUP R@ _LIBPA-W.RESULT-U ! DROP

    R@ _LIBPA-W.STAGE-A @ R@ _LIBPA-W.ENTRY-ARG @
        LIB-ENTRY-SIZE MOVE
    R@ _LIBPA-W.CONTENT LIBCT-DATA$
    R@ _LIBPA-W.DATA-A @ SWAP MOVE
    R@ _LIBPA-W.CONTENT R@ _LIBPA-W.CONTENT-ARG @
        LIB-CONTENT-SIZE MOVE
    R@ _LIBPA-W.DATA-A @
        R@ _LIBPA-W.CONTENT-ARG @ LIBCT.DATA-A !

    R@ _LIBPA-W.ENTRY-ARG @ R@ _LIBPA-W.CONTENT-ARG @
        _LIBPA-PAIR-VALID? 0= IF R> DROP LIBPA-S-CORRUPT EXIT THEN
    R@ _LIBPA-W.ROOT-RID R@ _LIBPA-W.ENTRY-ARG @ LIBE.ID RID= 0= IF
        R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    R> DROP LIBPA-S-OK ;

: LIBPA-READ
  ( rid entry-dst content-dst data-a data-cap adapter work -- data-u status )
    2DUP _LIBPA-ENTER DUP IF
        _LIBPA-REJECT >R _LIBPA-DROP5 0 R> EXIT
    THEN DROP
    >R
    1 PICK R@ _LIBPA-W.DATA-CAP !
    2 PICK R@ _LIBPA-W.DATA-A !
    3 PICK R@ _LIBPA-W.CONTENT-ARG !
    4 PICK R@ _LIBPA-W.ENTRY-ARG !
    5 PICK R@ _LIBPA-W.RID-ARG !
    R@ ['] _LIBPA-READ-RUN _LIBPA-WORK-CATCH
    R@ _LIBPA-W.ADAPTER @ R@ _LIBPA-LEAVE
    R@ _LIBPA-W.RESULT-U @ SWAP
    >R >R _LIBPA-DROP6 R> R> R> DROP ;
