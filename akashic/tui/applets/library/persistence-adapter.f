    THEN
    DUP LIBPA-CONTENT-RAW? IF DROP R> DROP LIBPA-S-CORRUPT EXIT THEN
    R@ _LIBPA-W.CONTENT DUP LIB-CONTENT-INIT
    OVER _LIBPAC.RID OVER LIBCT.ID RID-COPY
    OVER _LIBPAC.DOMAIN-REVISION @ OVER LIBCT.DOMAIN-REVISION !
    OVER _LIBPAC.CONTENT-REVISION @ OVER LIBCT.CONTENT-REVISION !
    OVER _LIBPAC.KIND @ OVER LIBCT.KIND !
