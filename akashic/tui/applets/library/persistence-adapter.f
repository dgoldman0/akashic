: _LIBPA-PAIR-VALID?  ( entry content -- flag )
    DUP LIB-CONTENT-VALID? 0= IF 2DROP 0 EXIT THEN
    OVER LIB-ENTRY-VALID? 0= IF 2DROP 0 EXIT THEN
    >R
    DUP LIBE.ID R@ LIBCT.ID RID= 0= IF DROP R> DROP 0 EXIT THEN
    DUP LIBE.DOMAIN-REVISION @ R@ LIBCT.DOMAIN-REVISION @ <> IF
        DROP R> DROP 0 EXIT
    THEN
    DUP LIBE.KIND @ R@ LIBCT.KIND @ <> IF DROP R> DROP 0 EXIT THEN
    DUP LIBE.MEDIA @ R@ LIBCT.MEDIA @ <> IF DROP R> DROP 0 EXIT THEN
    DUP LIBE.CURRENT-CONTENT-REVISION @
        R@ LIBCT.CONTENT-REVISION @ <> IF DROP R> DROP 0 EXIT THEN
    DUP LIBE.CONTENT-U @ R@ LIBCT.DATA-U @ <> IF DROP R> DROP 0 EXIT THEN
    LIBE.CONTENT-DIGEST R@ LIBCT.DIGEST SHA3-256-COMPARE
    R> DROP ;

: _LIBPA-CONTENT=  ( a b -- flag )
    DUP LIB-CONTENT-VALID? 0= IF 2DROP 0 EXIT THEN
    OVER LIB-CONTENT-VALID? 0= IF 2DROP 0 EXIT THEN
    >R
    DUP LIBCT.ID R@ LIBCT.ID RID= 0= IF DROP R> DROP 0 EXIT THEN
    DUP LIBCT.DOMAIN-REVISION @ R@ LIBCT.DOMAIN-REVISION @ <> IF
        DROP R> DROP 0 EXIT
    THEN
    DUP LIBCT.CONTENT-REVISION @ R@ LIBCT.CONTENT-REVISION @ <> IF
        DROP R> DROP 0 EXIT
    THEN
    DUP LIBCT.KIND @ R@ LIBCT.KIND @ <> IF DROP R> DROP 0 EXIT THEN
    DUP LIBCT.MEDIA @ R@ LIBCT.MEDIA @ <> IF DROP R> DROP 0 EXIT THEN
    DUP LIBCT.DATA-U @ R@ LIBCT.DATA-U @ <> IF DROP R> DROP 0 EXIT THEN
    DUP LIBCT.DIGEST R@ LIBCT.DIGEST SHA3-256-COMPARE 0= IF
        DROP R> DROP 0 EXIT
    THEN
    DUP LIBCT-DATA$ R@ LIBCT.DATA-A @ SWAP _LIBPA-BYTES= NIP
    R> DROP ;

: _LIBPA-SPAN-SAFE?  ( a u work -- flag )
    >R
    2DUP MSPAN-NONWRAPPING? 0= IF 2DROP R> DROP 0 EXIT THEN
    2DUP R@ LIBPA-WORK-SIZE MSPAN-OVERLAP? IF 2DROP R> DROP 0 EXIT THEN
    2DUP R@ _LIBPA-W.PSTORE-WORK @ PSTORE-WORK-SIZE
        MSPAN-OVERLAP? IF 2DROP R> DROP 0 EXIT THEN
    2DUP R@ _LIBPA-W.STAGE-A @ R@ _LIBPA-W.STAGE-U @
        MSPAN-OVERLAP? IF 2DROP R> DROP 0 EXIT THEN
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-WORK-RECORD-BUFFER$
    2OVER MSPAN-OVERLAP? IF 2DROP R> DROP 0 EXIT THEN
    2DUP R@ _LIBPA-W.ADAPTER @ LIBPA-SIZE
        MSPAN-OVERLAP? IF 2DROP R> DROP 0 EXIT THEN
    2DUP R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @
        PSTORE-SPAN-DISJOINT? 0= IF 2DROP R> DROP 0 EXIT THEN
    2DUP R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @ PSTORE-SIZE
        MSPAN-OVERLAP? IF 2DROP R> DROP 0 EXIT THEN
    2DROP R> DROP -1 ;

: _LIBPA-CREATE-ARGS?  ( work -- flag )
    >R
    R@ _LIBPA-W.ENTRY-ARG @ R@ _LIBPA-W.CONTENT-ARG @
        _LIBPA-PAIR-VALID? 0= IF R> DROP 0 EXIT THEN
    R@ _LIBPA-W.ENTRY-ARG @ LIB-ENTRY-SIZE R@ _LIBPA-SPAN-SAFE? 0= IF
        R> DROP 0 EXIT
    THEN
    R@ _LIBPA-W.CONTENT-ARG @ LIB-CONTENT-SIZE R@ _LIBPA-SPAN-SAFE? 0= IF
        R> DROP 0 EXIT
    THEN
    R@ _LIBPA-W.CONTENT-ARG @ LIBCT-DATA$ R@ _LIBPA-SPAN-SAFE? 0= IF
        R> DROP 0 EXIT
    THEN
    R@ _LIBPA-W.ENTRY-ARG @ LIB-ENTRY-SIZE
    R@ _LIBPA-W.CONTENT-ARG @ LIB-CONTENT-SIZE
        MSPAN-OVERLAP? 0= R> DROP ;

: _LIBPA-READ-ARGS?  ( work -- flag )
    >R
    R@ _LIBPA-W.RID-ARG @ DUP 0= IF DROP R> DROP 0 EXIT THEN
    DUP RID-SIZE MSPAN-NONWRAPPING? 0= IF DROP R> DROP 0 EXIT THEN
    RID-PRESENT? 0= IF R> DROP 0 EXIT THEN
    R@ _LIBPA-W.RID-ARG @ RID-SIZE R@ _LIBPA-SPAN-SAFE? 0= IF
        R> DROP 0 EXIT
    THEN
    R@ _LIBPA-W.ENTRY-ARG @ 0= IF R> DROP 0 EXIT THEN
    R@ _LIBPA-W.ENTRY-ARG @ LIB-ENTRY-SIZE R@ _LIBPA-SPAN-SAFE? 0= IF
        R> DROP 0 EXIT
    THEN
    R@ _LIBPA-W.CONTENT-ARG @ 0= IF R> DROP 0 EXIT THEN
    R@ _LIBPA-W.CONTENT-ARG @ LIB-CONTENT-SIZE R@ _LIBPA-SPAN-SAFE? 0= IF
        R> DROP 0 EXIT
    THEN
    R@ _LIBPA-W.DATA-CAP @ DUP 0< IF DROP R> DROP 0 EXIT THEN
    DUP 0> R@ _LIBPA-W.DATA-A @ 0= AND IF DROP R> DROP 0 EXIT THEN
    R@ _LIBPA-W.DATA-A @ SWAP R@ _LIBPA-SPAN-SAFE? 0= IF
        R> DROP 0 EXIT
    THEN
    R@ _LIBPA-W.ENTRY-ARG @ LIB-ENTRY-SIZE
    R@ _LIBPA-W.CONTENT-ARG @ LIB-CONTENT-SIZE
        MSPAN-OVERLAP? IF R> DROP 0 EXIT THEN
    R@ _LIBPA-W.ENTRY-ARG @ LIB-ENTRY-SIZE
    R@ _LIBPA-W.DATA-A @ R@ _LIBPA-W.DATA-CAP @
        MSPAN-OVERLAP? IF R> DROP 0 EXIT THEN
    R@ _LIBPA-W.CONTENT-ARG @ LIB-CONTENT-SIZE
    R@ _LIBPA-W.DATA-A @ R@ _LIBPA-W.DATA-CAP @
        MSPAN-OVERLAP? 0= R> DROP ;

