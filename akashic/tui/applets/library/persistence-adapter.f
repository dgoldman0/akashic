: _LIBPIX-DOCUMENT-RUN  ( index-work -- status )
    >R
    R@ _LIBPIX.STATE @ _LIBPIX-STATE-ACTIVE <> IF
        R> DROP LIBPA-S-BUSY EXIT
    THEN
    R@ _LIBPIX.ENTRY-ARG @ LIB-ENTRY-SIZE R@ _LIBPIX-SPAN-SAFE? 0= IF
        R> DROP LIBPA-S-INVALID EXIT
    THEN
    R@ _LIBPIX.CONTENT-ARG @ LIB-CONTENT-SIZE R@ _LIBPIX-SPAN-SAFE? 0= IF
        R> DROP LIBPA-S-INVALID EXIT
    THEN
    R@ _LIBPIX.ENTRY-ARG @ R@ _LIBPIX.CONTENT-ARG @
        _LIBPA-PAIR-VALID? 0= IF R> DROP LIBPA-S-INVALID EXIT THEN
    R@ _LIBPIX.CONTENT-ARG @ LIBCT-DATA$ R@ _LIBPIX-SPAN-SAFE? 0= IF
        R> DROP LIBPA-S-INVALID EXIT
    THEN
    \ L11 admission is deliberately create-only.  Re-key/update, lifecycle,
    \ tombstones, and the four-revision/GONE retention contract remain the
    \ named L12 complete-owner seam; accepting an existing RID here would
    \ silently strand old secondary keys.
    R@ _LIBPIX-DOCUMENT-KEYS-ABSENT
    DUP IF R> DROP EXIT THEN DROP

    \ L12 deletion seam: the indexed directory still carries the current
    \ fixed catalog record while the complete owner has not yet cut over.
    R@ _LIBPIX.ENTRY-ARG @ R@ _LIBPIX.STAGE LIB-CATALOG-RECORD-SIZE
        LIB-CATALOG-RECORD-ENCODE
    DUP IF _LIBPA-LIB>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPIX.STAGE SWAP R@ _LIBPIX.RECORD-REF R@ _LIBPIX-APPEND
    DUP IF R> DROP EXIT THEN DROP

    R@ _LIBPIX.CONTENT-ARG @ LIBCT.DATA-U @
    ['] _LIBPIX-MEMORY-SOURCE
    R@ _LIBPIX.CONTENT-ARG @ LIBCT.DATA-A @
    R@ _LIBPIX.BLOB
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPIX.PSTORE-WORK @ R@ _LIBPIX.BLOB-WORK PBLOB-WRITE
    DUP IF _LIBPA-PERSIST>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPIX.CONTENT-ARG @ R@ _LIBPIX-ENCODE-LIBRARY-CONTENT
    DUP IF R> DROP EXIT THEN DROP
    R@ _LIBPIX-CONTENT-APPEND
    DUP IF R> DROP EXIT THEN DROP

    R@ _LIBPIX.ENTRY-ARG @ LIBE.ID R@ _LIBPIX.KEY0 LIBPI-RID-KEY
    DUP IF _LIBPA-LIB>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPIX.KEY0 LIBPI-RID-KEY-SIZE
    R@ _LIBPIX.RECORD-REF PERSIST-REF-SIZE
    _LIBPIX-RID-DIRECTORY R@ _LIBPIX-PUT
    DUP IF R> DROP EXIT THEN DROP

    R@ _LIBPIX.ENTRY-ARG @ LIBE.CREATED-VALUE @
    R@ _LIBPIX.ENTRY-ARG @ LIBE.ID
    R@ _LIBPIX.KEY0 LIBPI-ORDER-KEY
    DUP IF _LIBPA-LIB>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPIX.KEY0 LIBPI-ORDER-KEY-SIZE
    R@ _LIBPIX.ENTRY-ARG @ LIBE.ID RID-SIZE
    _LIBPIX-CREATION-ORDER R@ _LIBPIX-PUT
    DUP IF R> DROP EXIT THEN DROP

    R@ _LIBPIX.ENTRY-ARG @ LIBE-TITLE$
    R@ _LIBPIX.ENTRY-ARG @ LIBE.ID
    R@ _LIBPIX.KEY0 LIBPI-TITLE-KEY
    DUP IF _LIBPA-LIB>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPIX.KEY0 LIBPI-TITLE-KEY-SIZE
    R@ _LIBPIX.ENTRY-ARG @ LIBE.ID RID-SIZE
    _LIBPIX-TITLE-ORDER R@ _LIBPIX-PUT
    DUP IF R> DROP EXIT THEN DROP

    R@ _LIBPIX.ENTRY-ARG @ LIBE.ID
    R@ _LIBPIX.CONTENT-ARG @ LIBCT.CONTENT-REVISION @
    R@ _LIBPIX-ADD-HISTORY
    DUP IF R> DROP EXIT THEN DROP
    1 R@ _LIBPIX.DOCUMENT-N +!
    R> DROP LIBPA-S-OK ;

