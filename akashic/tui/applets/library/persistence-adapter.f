        DROP -1 R@ _LIBPIX.PAGE-ID !
    ELSE
        DROP 0 R@ _LIBPIX.PAGE-ID !
    THEN
    0 R@ _LIBPIX.RESULT-N !
    R@ _LIBPIX-MEMBERSHIP-PREPARE
    DUP IF R> DROP EXIT THEN DROP
    R@ _LIBPIX.TOTAL-ARG @ 0= IF R> DROP LIBPA-S-OK EXIT THEN
    R@ _LIBPIX.KEY0 LIBPI-EDGE-KEY-SIZE 0 FILL
