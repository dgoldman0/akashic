  ( pstore-work stage-a stage-u work -- status )
    3 PICK 3 PICK 3 PICK 3 PICK _LIBPA-WORK-INIT-ARGS? 0= IF
        _LIBPA-DROP3 DROP LIBPA-S-INVALID EXIT
    THEN
    >R
    R@ LIBPA-WORK-SIZE 0 FILL
    _LIBPA-WORK-MAGIC R@ _LIBPA-W.MAGIC !
    R@ R@ _LIBPA-W.SELF !
    LIBPA-S-OK R@ _LIBPA-W.STATUS !
