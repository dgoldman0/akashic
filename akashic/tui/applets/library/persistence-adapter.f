: _LIBPA-BOUND?  ( adapter work -- flag )
    >R
    R@ LIBPA-WORK-VALID? 0= IF DROP R> DROP 0 EXIT THEN
    DUP LIBPA-VALID? 0= IF DROP R> DROP 0 EXIT THEN
    DUP LIBPA-SIZE R@ LIBPA-WORK-SIZE
        MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    DUP LIBPA-SIZE R@ _LIBPA-W.PSTORE-WORK @
        PSTORE-WORK-SPAN-DISJOINT? 0= IF DROP R> DROP 0 EXIT THEN
    R@ LIBPA-WORK-SIZE R@ _LIBPA-W.PSTORE-WORK @
        PSTORE-WORK-SPAN-DISJOINT? 0= IF DROP R> DROP 0 EXIT THEN
    R@ _LIBPA-W.STAGE-A @ R@ _LIBPA-W.STAGE-U @
        R@ _LIBPA-W.PSTORE-WORK @ PSTORE-WORK-SPAN-DISJOINT? 0= IF
        DROP R> DROP 0 EXIT
    THEN
    DUP _LIBPA-D.STORE @
    1 PICK LIBPA-SIZE 2 PICK PSTORE-SPAN-DISJOINT? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    R@ LIBPA-WORK-SIZE 2 PICK PSTORE-SPAN-DISJOINT? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    R@ _LIBPA-W.STAGE-A @ R@ _LIBPA-W.STAGE-U @
        2 PICK PSTORE-SPAN-DISJOINT? 0= IF
        2DROP R> DROP 0 EXIT
    THEN
    DUP PSTORE-SIZE R@ LIBPA-WORK-SIZE
        MSPAN-OVERLAP? IF 2DROP R> DROP 0 EXIT THEN
    DUP PSTORE-SIZE R@ _LIBPA-W.PSTORE-WORK @ PSTORE-WORK-SIZE
        MSPAN-OVERLAP? IF 2DROP R> DROP 0 EXIT THEN
    PSTORE-SIZE R@ _LIBPA-W.STAGE-A @ R@ _LIBPA-W.STAGE-U @
        MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    DROP R> DROP -1 ;

