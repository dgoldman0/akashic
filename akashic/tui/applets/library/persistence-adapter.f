    R@ _LIBPA-W.STAGE-A @ OVER LIBCT.DATA-A !
    OVER _LIBPAC.DATA-U @ OVER LIBCT.DATA-U !
    OVER _LIBPAC.DIGEST OVER LIBCT.DIGEST RID-COPY
    DROP
    R@ _LIBPA-W.CONTENT LIB-CONTENT-VALID? 0= IF
        DROP R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    DROP R> DROP LIBPA-S-OK ;

: _LIBPA-READ-RUN  ( work -- status )
    >R
    0 R@ _LIBPA-W.RESULT-U !
    R@ _LIBPA-READ-ARGS? 0= IF R> DROP LIBPA-S-INVALID EXIT THEN
    R@ _LIBPA-BOUND-INDEX-WORK DUP 0= IF
        DROP R> DROP LIBPA-S-INVALID EXIT
    THEN
    R@ _LIBPA-W.PAGE-ID !
    R@ _LIBPA-W.RID-ARG @ R@ _LIBPA-W.STAGE-A @
    R@ _LIBPA-W.ADAPTER @ R@ _LIBPA-W.PAGE-ID @
        LIBPA-INDEX-RID-LOOKUP
    DUP IF R> DROP EXIT THEN DROP
    R@ _LIBPA-W.STAGE-A @
    R@ _LIBPA-W.PAGE-ID @ _LIBPIX.PAGE LIB-ENTRY-SIZE MOVE
    R@ _LIBPA-W.PAGE-ID @ _LIBPIX.PAGE
        LIBE.CURRENT-CONTENT-REVISION @
    DUP R@ _LIBPA-W.PAGE-ID @ _LIBPIX.REVISION-ARG !
    R@ _LIBPA-W.RID-ARG @
    DUP R@ _LIBPA-W.PAGE-ID @ _LIBPIX.RID-ARG !
    SWAP R@ _LIBPA-W.PAGE-ID @ _LIBPIX-LOAD-HISTORY
    DUP IF R> DROP EXIT THEN DROP
    R@ _LIBPA-W.PAGE-ID @ _LIBPIX.STAGE LIBPA-CONTENT-SIZE@
    DUP 0< IF DROP R> DROP LIBPA-S-CORRUPT EXIT THEN
    DUP R@ _LIBPA-W.DATA-CAP @ > IF
        DROP R> DROP LIBPA-S-CAPACITY EXIT
    THEN
    DUP R@ _LIBPA-W.STAGE-U @ > IF
        DROP R> DROP LIBPA-S-CAPACITY EXIT
    THEN
    R@ _LIBPA-W.TEMP-STATUS !
    R@ _LIBPA-W.PAGE-ID @ _LIBPIX.STAGE
    _LIBPAC.BLOB 0 R@ _LIBPA-W.TEMP-STATUS @
    ['] _LIBPA-READ-SINK R@
    R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPA-W.PSTORE-WORK @
    R@ _LIBPA-W.PAGE-ID @ _LIBPIX.BLOB-WORK PBLOB-READ-RANGE
    DUP IF _LIBPA-PERSIST>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPA-W.PAGE-ID @ _LIBPIX.STAGE R@ _LIBPA-DESCRIPTOR>CONTENT
    DUP IF R> DROP EXIT THEN DROP
    R@ _LIBPA-W.PAGE-ID @ _LIBPIX.PAGE R@ _LIBPA-W.CONTENT
        _LIBPA-PAIR-VALID? 0= IF
        R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    R@ _LIBPA-W.TEMP-STATUS @ IF
        R@ _LIBPA-W.STAGE-A @ R@ _LIBPA-W.DATA-A @
        R@ _LIBPA-W.TEMP-STATUS @ MOVE
    THEN
    R@ _LIBPA-W.DATA-A @ R@ _LIBPA-W.CONTENT LIBCT.DATA-A !
    R@ _LIBPA-W.PAGE-ID @ _LIBPIX.PAGE R@ _LIBPA-W.ENTRY-ARG @
        LIB-ENTRY-SIZE MOVE
    R@ _LIBPA-W.CONTENT R@ _LIBPA-W.CONTENT-ARG @ LIB-CONTENT-SIZE MOVE
    R@ _LIBPA-W.TEMP-STATUS @ R@ _LIBPA-W.RESULT-U !
    R> DROP LIBPA-S-OK ;

