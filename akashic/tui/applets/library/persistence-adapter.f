    DUP _LIBPIX.SELF @ OVER <> IF DROP 0 EXIT THEN
    DUP _LIBPIX.BUSY @ DUP 0= SWAP -1 = OR 0= IF DROP 0 EXIT THEN
    DUP _LIBPIX.STATE @ DUP _LIBPIX-STATE-READY >=
        SWAP _LIBPIX-STATE-REOPEN <= AND 0= IF DROP 0 EXIT THEN
    DUP _LIBPIX.STATUS @ _LIBPIX-STATUS? 0= IF DROP 0 EXIT THEN
    DUP _LIBPIX.ADAPTER @ LIBPA-VALID? 0= IF DROP 0 EXIT THEN
    DUP _LIBPIX.PSTORE-WORK @ PSTORE-WORK-VALID? 0= IF DROP 0 EXIT THEN
    DUP _LIBPIX.RECLAIM RECLAIM-VALID? 0= IF DROP 0 EXIT THEN
    DUP _LIBPIX.RECLAIM-WORK RECLAIM-WORK-VALID? 0= IF DROP 0 EXIT THEN
    DUP _LIBPIX.BTREE-WORK PBTREE-WORK-VALID? 0= IF DROP 0 EXIT THEN
    DUP _LIBPIX.BLOB-WORK PBLOB-WORK-VALID? 0= IF DROP 0 EXIT THEN
    0
    BEGIN DUP _LIBPIX-TREE-COUNT < WHILE
        DUP 2 PICK _LIBPIX-TREE PBTREE-VALID? 0= IF 2DROP 0 EXIT THEN
