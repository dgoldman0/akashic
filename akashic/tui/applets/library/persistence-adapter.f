: _LIBPA-READ-SINK  ( logical-offset payload-a payload-u work -- status )
    >R
    2 PICK DUP 0< IF DROP _LIBPA-DROP3 R> DROP LIBPA-S-INVALID EXIT THEN
    R@ _LIBPA-W.TEMP-STATUS @ > IF
        _LIBPA-DROP3 R> DROP LIBPA-S-CAPACITY EXIT
    THEN
    DUP R@ _LIBPA-W.TEMP-STATUS @ 4 PICK - > IF
        _LIBPA-DROP3 R> DROP LIBPA-S-CAPACITY EXIT
    THEN
    DUP R@ _LIBPA-W.STAGE-U @ 4 PICK - > IF
        _LIBPA-DROP3 R> DROP LIBPA-S-CAPACITY EXIT
    THEN
    OVER R@ _LIBPA-W.STAGE-A @ 4 PICK + 2 PICK MOVE
    _LIBPA-DROP3 R> DROP LIBPA-S-OK ;

