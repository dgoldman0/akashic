: _LIBPA-BOUND-INDEX-WORK  ( work -- index-work|0 )
    DUP _LIBPA-W.INDEX-WORK @ DUP LIBPA-INDEX-WORK-VALID? 0= IF
        2DROP 0 EXIT
    THEN
    DUP _LIBPIX.ADAPTER @ 2 PICK _LIBPA-W.ADAPTER @ <> IF
        2DROP 0 EXIT
    THEN
    NIP ;

: _LIBPA-OPEN-RUN  ( work -- status )
    DUP _LIBPA-BOUND-INDEX-WORK DUP 0= IF
        2DROP LIBPA-S-INVALID EXIT
    THEN
    SWAP _LIBPA-W.ADAPTER @ SWAP LIBPA-INDEX-OPEN ;

: LIBPA-OPEN  ( adapter work -- status )
    2DUP _LIBPA-ENTER DUP IF _LIBPA-REJECT EXIT THEN DROP
    DUP ['] _LIBPA-OPEN-RUN _LIBPA-WORK-CATCH
    ROT ROT _LIBPA-LEAVE ;

: _LIBPA-CREATE-RUN  ( work -- status )
