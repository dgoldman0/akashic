\ =====================================================================
\  persistence-adapter.f - Library vertical slice over neutral PSTORE
\ =====================================================================
\  This is deliberately a Library-owned adapter.  It gives one valid
\  LIB-ENTRY and its matching LIB-CONTENT one atomic physical authority;
\  it does not make the Library applet meaningful outside Desk/TUI and it
\  does not move Library policy into the neutral persistence layer.
\
\  The nested Library record-codec envelopes written here are an explicit
\  L10 bridge scheduled for deletion in L12.  They are not a second durable
\  schema, migration promise, command log, collection store, or index.
\
\  All mutable memory is caller-owned.  A LIBPA descriptor borrows one
\  PSTORE.  A LIBPA work object permanently borrows the PSTORE work used to
\  open and mutate that authority plus a disjoint codec staging buffer.
\  Separate descriptor/work graphs are therefore safely interleavable.
\ =====================================================================

PROVIDED akashic-tui-library-persistence-adapter

\ Preload the two independently useful surfaces.  Neither imports a
\ Library controller, service, repository, applet, or path convention.
REQUIRE ../../../persistence/store.f
REQUIRE record-codec.f

