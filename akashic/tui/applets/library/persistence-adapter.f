    DUP 0> R@ _LIBPIX.CALLBACK-XT @ 0= AND IF
        DROP R> DROP LIBPA-S-INVALID EXIT
    THEN DROP
    R@ _LIBPIX.RID-ARG @ R@ _LIBPIX.REVISION-ARG @
    R@ _LIBPIX.KEY0 LIBPI-HISTORY-KEY
    DUP IF _LIBPA-LIB>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPIX.KEY0 LIBPI-HISTORY-KEY-SIZE _LIBPIX-HISTORY
    R@ _LIBPIX-KEY-ABSENT-TX DUP IF R> DROP EXIT THEN DROP
    R@ _LIBPIX.TOTAL-ARG @
    R@ _LIBPIX.CALLBACK-XT @ R@ _LIBPIX.CALLBACK-CONTEXT @
    R@ _LIBPIX.BLOB
