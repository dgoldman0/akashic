    DUP _LIBPIX.STATE @ _LIBPIX-STATE-ACTIVE <> IF
        DROP LIBPA-S-BUSY EXIT
    THEN
    LIBPA-S-OK SWAP _LIBPIX-ABORT-ACTIVE ;

: LIBPA-INDEX-ABORT  ( adapter index-work -- status )
    2DUP _LIBPIX-ENTER DUP IF >R 2DROP R> EXIT THEN DROP
    DUP ['] _LIBPIX-ABORT-RUN _LIBPIX-WORK-CATCH
    ROT ROT _LIBPIX-LEAVE ;

