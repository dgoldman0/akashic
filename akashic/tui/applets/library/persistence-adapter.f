    THEN
    R@ _LIBPIX.PAGE _LIBPAR-FLAGS + @ IF
        R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    R@ _LIBPIX.PAGE _LIBPAR-RESERVED +
        PERSIST-PAGE-PAYLOAD-SIZE _LIBPAR-RESERVED -
        _LIBPA-ZERO? 0= IF
        R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    0
    BEGIN DUP _LIBPIX-TREE-COUNT < WHILE
        DUP R@ _LIBPIX.PAGE-ID !
        R@ _LIBPIX.PAGE _LIBPAR-ROOTS +
            R@ _LIBPIX.PAGE-ID @ PBTREE-ROOT-SIZE * +
        R@ _LIBPIX.PAGE-ID @ R@ _LIBPIX-ROOT PBTREE-ROOT-COPY
        DUP IF
            _LIBPA-PERSIST>STATUS >R DROP R> R> DROP EXIT
        THEN DROP
        R@ _LIBPIX.PAGE-ID @ R@ _LIBPIX-ROOT
        R@ _LIBPIX.PAGE-ID @ R@ _LIBPIX-TREE
            PBTREE-ROOT-VALID? 0= IF
            DROP R> DROP LIBPA-S-CORRUPT EXIT
        THEN
        R@ _LIBPIX.PAGE-ID @ R@ _LIBPIX-ROOT PBTREE-ROOT-GENERATION@
        R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @ PSTORE-GENERATION@ <> IF
            DROP R> DROP LIBPA-S-CORRUPT EXIT
        THEN
        1+
    REPEAT
    DROP
    R@ _LIBPIX.PAGE _LIBPAR-RECLAIM-STATE + RECLAIM-STATE-SIZE
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @ R@ _LIBPIX.RECLAIM
        RECLAIM-OPEN _LIBPA-PERSIST>STATUS
    DUP IF R> DROP EXIT THEN DROP
    _LIBPIX-STATE-READY R@ _LIBPIX.STATE !
    R> DROP LIBPA-S-OK ;

: _LIBPIX-WORK>ROOT  ( index-work -- status )
    >R
    R@ _LIBPIX.PAGE PERSIST-PAGE-PAYLOAD-SIZE 0 FILL
    _LIBPA-ROOT-MAGIC R@ _LIBPIX.PAGE _LIBPAR-MAGIC + 8 MOVE
    LIBPA-ROOT-FORMAT R@ _LIBPIX.PAGE _LIBPAR-FORMAT + !
    0
    BEGIN DUP _LIBPIX-TREE-COUNT < WHILE
        DUP R@ _LIBPIX-ROOT
        OVER PBTREE-ROOT-SIZE *
            R@ _LIBPIX.PAGE _LIBPAR-ROOTS + +
        PBTREE-ROOT-COPY
        DUP IF
            _LIBPA-PERSIST>STATUS >R DROP R> R> DROP EXIT
        THEN DROP
        1+
    REPEAT
    DROP
    R@ _LIBPIX.PAGE _LIBPAR-RECLAIM-STATE + RECLAIM-STATE-SIZE
    R@ _LIBPIX.RECLAIM-WORK RECLAIM-STATE!
    _LIBPA-PERSIST>STATUS R> DROP ;

: _LIBPIX-LOAD-CURRENT  ( index-work -- status )
    >R
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @ PSTORE-CURRENT-ROOT@
    DUP 0= IF DROP R> DROP LIBPA-S-CORRUPT EXIT THEN
    PROOTV.APPLICATION-ROOT @ DUP 0< IF
        DROP R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    R@ _LIBPIX.PAGE-ID !
    R@ _LIBPIX.PAGE-ID @
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPIX.PSTORE-WORK @ PSTORE-READ-PAGE
    DUP IF _LIBPA-PERSIST>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPIX.PSTORE-WORK @ PSTORE-PAGE-PAYLOAD$
    R> _LIBPIX-ROOT>WORK ;

: _LIBPIX-BIND-EMPTY-RECLAIM  ( index-work -- status )
    >R
    R@ _LIBPIX.PAGE _LIBPAR-RECLAIM-STATE + RECLAIM-STATE-SIZE
        RECLAIM-STATE-INIT
    DUP IF _LIBPA-PERSIST>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPIX.PAGE _LIBPAR-RECLAIM-STATE + RECLAIM-STATE-SIZE
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @ R@ _LIBPIX.RECLAIM
        RECLAIM-OPEN _LIBPA-PERSIST>STATUS
    R> DROP ;

: _LIBPIX-OPEN-EMPTY  ( index-work -- status )
    >R
    R@ _LIBPIX-ROOTS-INIT DUP IF R> DROP EXIT THEN DROP
    R@ _LIBPIX-BIND-EMPTY-RECLAIM DUP IF R> DROP EXIT THEN DROP
    _LIBPIX-STATE-READY R@ _LIBPIX.STATE !
    R> DROP LIBPA-S-ABSENT ;

: _LIBPIX-OPEN-RUN  ( index-work -- status )
    >R
    R@ _LIBPIX.STATE @ _LIBPIX-STATE-ACTIVE = IF
        R> DROP LIBPA-S-BUSY EXIT
    THEN
    _LIBPIX-STATE-REOPEN R@ _LIBPIX.STATE !
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPIX.PSTORE-WORK @ PSTORE-OPEN
    DUP PERSIST-S-ABSENT = IF
        DROP R> _LIBPIX-OPEN-EMPTY EXIT
    THEN
    DUP IF _LIBPA-PERSIST>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @ PSTORE-CURRENT-ROOT@
    DUP 0= IF DROP R> DROP LIBPA-S-CORRUPT EXIT THEN
    PROOTV.APPLICATION-ROOT @ 0< IF
        R> _LIBPIX-OPEN-EMPTY EXIT
    THEN
    R> _LIBPIX-LOAD-CURRENT ;

: LIBPA-INDEX-OPEN  ( adapter index-work -- status )
    2DUP _LIBPIX-ENTER DUP IF
        >R 2DROP R> EXIT
    THEN DROP
    DUP ['] _LIBPIX-OPEN-RUN _LIBPIX-WORK-CATCH
    ROT ROT _LIBPIX-LEAVE ;

: _LIBPIX-ABORT-ACTIVE  ( primary-status index-work -- final-status )
    >R
    DUP R@ _LIBPIX.TEMP-STATUS !
    \ Reclaim abort deliberately follows the page-store abort.  The
    \ reclaimer's proposal-local allocation/discard ledger remains fenced
    \ while PSTORE still advertises an active proposed root.
    R@ _LIBPIX.PSTORE-WORK @ PSTORE-PROPOSED-ROOT@ IF
        R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @
        R@ _LIBPIX.PSTORE-WORK @ PSTORE-ABORT
        DUP PERSIST-S-OK <> IF
            _LIBPA-PERSIST>STATUS R@ _LIBPIX.TEMP-STATUS !
        ELSE
            DROP
        THEN
    THEN
    R@ _LIBPIX.PSTORE-WORK @ PSTORE-PROPOSED-ROOT@ 0= IF
        R@ _LIBPIX.RECLAIM-WORK RECLAIM-ABORT
        DUP PERSIST-S-OK <> IF
            _LIBPA-PERSIST>STATUS R@ _LIBPIX.TEMP-STATUS !
        ELSE
            DROP
        THEN
    THEN
    _LIBPIX-STATE-REOPEN R@ _LIBPIX.STATE !
    DROP R@ _LIBPIX.TEMP-STATUS @ R> DROP ;

: _LIBPIX-FAIL-ACTIVE  ( status index-work -- status )
    >R
    DUP IF
        R@ _LIBPIX.STATE @ _LIBPIX-STATE-ACTIVE = IF
            R@ _LIBPIX-ABORT-ACTIVE
        THEN
    THEN
    R> DROP ;

: _LIBPIX-ABORT-STORE-PRIMARY  ( primary-status index-work -- final-status )
    >R
    DUP R@ _LIBPIX.TEMP-STATUS !
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPIX.PSTORE-WORK @ PSTORE-ABORT
    DUP PERSIST-S-OK <> IF
        _LIBPA-PERSIST>STATUS R@ _LIBPIX.TEMP-STATUS !
    ELSE
        DROP
    THEN
    DROP R@ _LIBPIX.TEMP-STATUS @ R> DROP ;

: _LIBPIX-BEGIN-RUN  ( index-work -- status )
    >R
    R@ _LIBPIX.STATE @ _LIBPIX-STATE-ACTIVE = IF
        R> DROP LIBPA-S-BUSY EXIT
    THEN
    R@ _LIBPIX.STATE @ _LIBPIX-STATE-REOPEN = IF
        R> DROP LIBPA-S-CONFLICT EXIT
    THEN
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @ PSTORE-GENERATION@
        R@ _LIBPIX.BASE-GENERATION !
    R@ _LIBPIX.RECLAIM RECLAIM-STORE@ 0= IF
        R@ _LIBPIX.BASE-GENERATION @ IF
            R> DROP LIBPA-S-CONFLICT EXIT
        THEN
        R@ _LIBPIX-BIND-EMPTY-RECLAIM
        DUP IF R> DROP EXIT THEN DROP
    THEN
    R@ _LIBPIX.BASE-GENERATION @ IF
        R@ _LIBPIX-ROOTS-CURRENT? 0= IF
            R> DROP LIBPA-S-CONFLICT EXIT
        THEN
    THEN
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @ PSTORE-CURRENT-ROOT@
    DUP IF
        PROOTV.APPLICATION-ROOT @
    ELSE
        DROP -1
    THEN
    R@ _LIBPIX.OLD-ROOT-PAGE !
    0 R@ _LIBPIX.RECLAIM-STEP-CALLS !
    0 R@ _LIBPIX.RECLAIM-MOVED !
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPIX.PSTORE-WORK @ PSTORE-BEGIN
    DUP IF _LIBPA-PERSIST>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPIX.PSTORE-WORK @ R@ _LIBPIX.RECLAIM
    R@ _LIBPIX.RECLAIM-WORK RECLAIM-TX-BEGIN
    DUP IF
        _LIBPA-PERSIST>STATUS R@ _LIBPIX-ABORT-STORE-PRIMARY
        R> DROP EXIT
    THEN DROP
    _LIBPIX-STATE-ACTIVE R@ _LIBPIX.STATE !
    R@ _LIBPIX.OLD-ROOT-PAGE @ 0>= IF
        R@ _LIBPIX.OLD-ROOT-PAGE 1 R@ _LIBPIX.RECLAIM-WORK
            RECLAIM-RETIRE-BATCH
        DUP IF
            _LIBPA-PERSIST>STATUS R@ _LIBPIX-ABORT-ACTIVE R> DROP EXIT
        THEN DROP
    THEN
    R@ _LIBPIX.BASE-GENERATION @ 0= IF
        R@ _LIBPIX-ROOTS-INIT DUP IF
            R@ _LIBPIX-ABORT-ACTIVE R> DROP EXIT
        THEN DROP
    THEN
    0 R@ _LIBPIX.DOCUMENT-N !
    0 R@ _LIBPIX.EDGE-N !
    0 R@ _LIBPIX.RAW-N !
    -1 R@ _LIBPIX.APP-ROOT-PAGE !
    R@ _LIBPIX.BLOB PBLOB-SIZE 0 FILL
    R> DROP LIBPA-S-OK ;

: LIBPA-INDEX-BEGIN  ( adapter index-work -- status )
    2DUP _LIBPIX-ENTER DUP IF >R 2DROP R> EXIT THEN DROP
    DUP ['] _LIBPIX-BEGIN-RUN _LIBPIX-WORK-CATCH
    OVER _LIBPIX-FAIL-ACTIVE
    ROT ROT _LIBPIX-LEAVE ;

