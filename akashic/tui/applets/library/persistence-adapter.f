    R@ _LIBPIX.CURSOR R@ _LIBPIX.BTREE-WORK PBTREE-RESUME
    R@ _LIBPIX-CURSOR-RID R> DROP ;

: _LIBPIX-SLICE-LOOP  ( tree-index first? index-work -- status )
    >R
    R@ _LIBPIX.RESULT-N @ R@ _LIBPIX.TOTAL-ARG @ >= IF
        2DROP R> DROP LIBPA-S-OK EXIT
    THEN
    IF
        DUP R@ _LIBPIX-SLICE-NEXT
    ELSE
        DUP
        R@ _LIBPIX.KEY0 R@ _LIBPIX.PAGE-ID @
        ROT R@ _LIBPIX-SLICE-RESUME
    THEN
    DUP LIBPA-S-NOT-FOUND = IF DROP DROP R> DROP LIBPA-S-OK EXIT THEN
    DUP IF NIP R> DROP EXIT THEN DROP
    BEGIN R@ _LIBPIX.RESULT-N @ R@ _LIBPIX.TOTAL-ARG @ < WHILE
        DUP R@ _LIBPIX-SLICE-NEXT
        DUP LIBPA-S-NOT-FOUND = IF DROP DROP R> DROP LIBPA-S-OK EXIT THEN
        DUP IF NIP R> DROP EXIT THEN DROP
    REPEAT
    DROP R> DROP LIBPA-S-OK ;

: _LIBPIX-SLICE-PREPARE  ( tree-index index-work -- status )
    >R
    R@ _LIBPIX-READY? 0= IF DROP R> DROP LIBPA-S-BUSY EXIT THEN
    R@ _LIBPIX.TOTAL-ARG @ DUP 0< OVER LIBPA-INDEX-SLICE-MAX > OR IF
        2DROP R> DROP LIBPA-S-INVALID EXIT
    THEN
    RID-SIZE *
    R@ _LIBPIX.ENTRY-ARG @ SWAP R@ _LIBPIX-SPAN-SAFE? 0= IF
        DROP R> DROP LIBPA-S-INVALID EXIT
    THEN
    DUP R@ _LIBPIX-ROOT OVER R@ _LIBPIX-TREE R@ _LIBPIX.CURSOR
        PBTREE-CURSOR-INIT
    DUP IF _LIBPA-PERSIST>STATUS >R DROP R> R> DROP EXIT THEN DROP
    R@ _LIBPIX.TEMP-STATUS !
    0 R@ _LIBPIX.RESULT-N !
    R> DROP LIBPA-S-OK ;

: _LIBPIX-CREATION-SLICE-RUN  ( index-work -- status )
    >R
    R@ _LIBPIX.REVISION-ARG @ DUP 0< IF
        DROP R> DROP LIBPA-S-INVALID EXIT
    THEN
    DUP 0= IF
        DROP
        R@ _LIBPIX.RID-ARG @ IF R> DROP LIBPA-S-INVALID EXIT THEN
    ELSE
        DROP
        R@ _LIBPIX.RID-ARG @ R@ _LIBPIX-RID-INPUT? 0= IF
            R> DROP LIBPA-S-INVALID EXIT
        THEN
    THEN
    _LIBPIX-CREATION-ORDER R@ _LIBPIX-SLICE-PREPARE
    DUP IF R> DROP EXIT THEN DROP
    R@ _LIBPIX.TOTAL-ARG @ 0= IF R> DROP LIBPA-S-OK EXIT THEN
    R@ _LIBPIX.REVISION-ARG @ 0= IF
        _LIBPIX-CREATION-ORDER -1 R@ _LIBPIX-SLICE-LOOP
    ELSE
        R@ _LIBPIX.REVISION-ARG @ R@ _LIBPIX.RID-ARG @
        R@ _LIBPIX.KEY0 LIBPI-ORDER-KEY
        DUP IF _LIBPA-LIB>STATUS R> DROP EXIT THEN DROP
        LIBPI-ORDER-KEY-SIZE R@ _LIBPIX.PAGE-ID !
        _LIBPIX-CREATION-ORDER 0 R@ _LIBPIX-SLICE-LOOP
    THEN
    DUP IF R> DROP EXIT THEN DROP
    R@ _LIBPIX.PAGE R@ _LIBPIX.ENTRY-ARG @
        R@ _LIBPIX.RESULT-N @ RID-SIZE * MOVE
    R> DROP LIBPA-S-OK ;

