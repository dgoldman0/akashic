\ =====================================================================
\  persistence-adapter.f - Library vertical slices over neutral persistence
\ =====================================================================
\  This is deliberately a Library-owned adapter.  Library records, ordered
\  meanings, history locators, membership edges, and content ownership stay
\  here; the neutral layers see only byte keys, opaque values, checked pages,
\  and immutable blobs.  The adapter does not make Library meaningful outside
\  Desk/TUI and remains a non-authoritative proving surface until L12.
\
\  The catalog record-codec envelope written here is the explicitly named
\  L10-to-L12 codec seam.  It is scheduled for deletion when L12 maps the full
\  owner; it is not a migration promise, compatibility reader, or second root
\  format.  There is exactly one current application-root shape.
\
\  All mutable memory is caller-owned.  A LIBPA descriptor borrows one
\  PSTORE.  A LIBPA work object permanently borrows the PSTORE work used to
\  open and mutate that authority plus a disjoint codec staging buffer.
\  Separate descriptor/work graphs are therefore safely interleavable.
\ =====================================================================

