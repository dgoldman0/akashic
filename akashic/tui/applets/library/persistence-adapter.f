: _LIBPA-ROOT>WORK  ( payload-a payload-u work -- status )
    >R
    DUP PERSIST-PAGE-PAYLOAD-SIZE <> IF
        2DROP R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    OVER _LIBPAR-MAGIC + _LIBPA-ROOT-MAGIC 8 _LIBPA-BYTES= 0= IF
        2DROP R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    OVER _LIBPAR-FORMAT + @ LIBPA-ROOT-FORMAT <> IF
        2DROP R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    OVER _LIBPAR-FLAGS + @ IF 2DROP R> DROP LIBPA-S-CORRUPT EXIT THEN
    OVER _LIBPAR-RID + RID-PRESENT? 0= IF
        2DROP R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    OVER _LIBPAR-CONTENT-REF + PERSIST-REF-VALID? 0= IF
        2DROP R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    OVER _LIBPAR-ENTRY-REF + PERSIST-REF-VALID? 0= IF
        2DROP R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    OVER _LIBPAR-RESERVED +
        PERSIST-PAGE-PAYLOAD-SIZE _LIBPAR-RESERVED -
        _LIBPA-ZERO? 0= IF 2DROP R> DROP LIBPA-S-CORRUPT EXIT THEN
    OVER _LIBPAR-RID + R@ _LIBPA-W.ROOT-RID RID-COPY
    OVER _LIBPAR-CONTENT-REF + R@ _LIBPA-W.CONTENT-REF PERSIST-REF-COPY
    OVER _LIBPAR-ENTRY-REF + R@ _LIBPA-W.ENTRY-REF PERSIST-REF-COPY
    2DROP R> DROP LIBPA-S-OK ;

: _LIBPA-WORK>ROOT  ( work -- )
    >R
    R@ _LIBPA-W.STAGE-A @ DUP PERSIST-PAGE-PAYLOAD-SIZE 0 FILL
    _LIBPA-ROOT-MAGIC OVER _LIBPAR-MAGIC + 8 MOVE
    LIBPA-ROOT-FORMAT OVER _LIBPAR-FORMAT + !
    R@ _LIBPA-W.ROOT-RID OVER _LIBPAR-RID + RID-COPY
    R@ _LIBPA-W.CONTENT-REF OVER _LIBPAR-CONTENT-REF + PERSIST-REF-COPY
    R@ _LIBPA-W.ENTRY-REF OVER _LIBPAR-ENTRY-REF + PERSIST-REF-COPY
    DROP R> DROP ;

\ Read and validate the single current Library authority.  The decoded
\ entry occupies STAGE; decoded content borrows PSTORE's record buffer.
: _LIBPA-LOAD-CURRENT  ( work -- status )
    >R
    R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @ PSTORE-CURRENT-ROOT@
    DUP 0= IF DROP R> DROP LIBPA-S-CORRUPT EXIT THEN
    PROOTV.APPLICATION-ROOT @ DUP 0< IF
        DROP R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    R@ _LIBPA-W.PAGE-ID !
    R@ _LIBPA-W.PAGE-ID @
    R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-READ-PAGE
    DUP IF _LIBPA-PERSIST>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-PAGE-PAYLOAD$
    R@ _LIBPA-ROOT>WORK DUP IF R> DROP EXIT THEN DROP

    R@ _LIBPA-W.ENTRY-REF
    R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-READ-RECORD
    DUP IF _LIBPA-PERSIST>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-RECORD-PAYLOAD$
    R@ _LIBPA-W.STAGE-A @ LIB-CATALOG-RECORD-DECODE
    DUP IF _LIBPA-LIB>STATUS R> DROP EXIT THEN DROP

    R@ _LIBPA-W.CONTENT-REF
    R@ _LIBPA-W.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-READ-RECORD
    DUP IF _LIBPA-PERSIST>STATUS R> DROP EXIT THEN DROP
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-RECORD-PAYLOAD$
    R@ _LIBPA-W.CONTENT LIB-CONTENT-RECORD-DECODE
    DUP IF _LIBPA-LIB>STATUS R> DROP EXIT THEN DROP

    R@ _LIBPA-W.STAGE-A @ R@ _LIBPA-W.CONTENT _LIBPA-PAIR-VALID? 0= IF
        R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    R@ _LIBPA-W.ROOT-RID R@ _LIBPA-W.STAGE-A @ LIBE.ID RID= 0= IF
        R> DROP LIBPA-S-CORRUPT EXIT
    THEN
    R> DROP LIBPA-S-OK ;

