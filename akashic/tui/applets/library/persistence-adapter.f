    2 PICK PSTORE-WORK-VALID? 0= IF _LIBPA-DROP3 0 EXIT THEN
    1 PICK LIBPA-VALID? 0= IF _LIBPA-DROP3 0 EXIT THEN
    DUP LIBPA-INDEX-WORK-SIZE MSPAN-NONWRAPPING? 0= IF
        _LIBPA-DROP3 0 EXIT
    THEN
    DUP LIBPA-INDEX-WORK-SIZE 4 PICK PSTORE-WORK-SIZE
        MSPAN-OVERLAP? IF _LIBPA-DROP3 0 EXIT THEN
    DUP LIBPA-INDEX-WORK-SIZE 4 PICK
        PSTORE-WORK-SPAN-DISJOINT? 0= IF _LIBPA-DROP3 0 EXIT THEN
    DUP LIBPA-INDEX-WORK-SIZE 3 PICK LIBPA-SIZE
        MSPAN-OVERLAP? IF _LIBPA-DROP3 0 EXIT THEN
    1 PICK LIBPA-SIZE 4 PICK
        PSTORE-WORK-SPAN-DISJOINT? 0= IF _LIBPA-DROP3 0 EXIT THEN
    DUP LIBPA-INDEX-WORK-SIZE 3 PICK _LIBPA-D.STORE @
        PSTORE-SPAN-DISJOINT? 0= IF _LIBPA-DROP3 0 EXIT THEN
    2 PICK PSTORE-WORK-RECORD-BUFFER$
    DUP PBLOB-CHUNK-SIZE PERSIST-RECORD-HEADER-SIZE + < IF
        2DROP _LIBPA-DROP3 0 EXIT
    THEN
    2DUP 4 PICK LIBPA-INDEX-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP _LIBPA-DROP3 0 EXIT
    THEN
    2DROP _LIBPA-DROP3 -1 ;

