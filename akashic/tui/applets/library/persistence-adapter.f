: LIBPA-VALID?  ( adapter -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP LIBPA-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _LIBPA-D.MAGIC @ _LIBPA-MAGIC <> IF DROP 0 EXIT THEN
    DUP _LIBPA-D.SELF @ OVER <> IF DROP 0 EXIT THEN
    DUP _LIBPA-D.STATUS @ DUP LIBPA-S-OK <
        SWAP LIBPA-S-FAULT > OR IF DROP 0 EXIT THEN
    DUP _LIBPA-D.STORE @ DUP PSTORE-VALID? 0= IF 2DROP 0 EXIT THEN
    PSTORE-SIZE ROT LIBPA-SIZE MSPAN-OVERLAP? 0= ;

: _LIBPA-WORK-GEOMETRY?  ( work -- flag )
    >R
    R@ _LIBPA-W.PSTORE-WORK @ DUP PSTORE-WORK-VALID? 0= IF
        DROP R> DROP 0 EXIT
    THEN
    DUP PSTORE-WORK-SIZE R@ LIBPA-WORK-SIZE
        MSPAN-OVERLAP? IF DROP R> DROP 0 EXIT THEN
    DUP PSTORE-WORK-RECORD-BUFFER$
    2DUP MSPAN-NONWRAPPING? 0= IF
        2DROP DROP R> DROP 0 EXIT
    THEN
    DUP 0> 0= IF 2DROP DROP R> DROP 0 EXIT THEN
    2DUP R@ LIBPA-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP DROP R> DROP 0 EXIT
    THEN
    2DUP R@ _LIBPA-W.STAGE-A @ R@ _LIBPA-W.STAGE-U @
        MSPAN-OVERLAP? IF 2DROP DROP R> DROP 0 EXIT THEN
    2DROP DROP
    R@ _LIBPA-W.STAGE-A @ R@ _LIBPA-W.STAGE-U @
    2DUP MSPAN-NONWRAPPING? 0= IF 2DROP R> DROP 0 EXIT THEN
    DUP LIB-CONTENT-RECORD-MAX < IF 2DROP R> DROP 0 EXIT THEN
    2DUP R@ LIBPA-WORK-SIZE MSPAN-OVERLAP? IF
        2DROP R> DROP 0 EXIT
    THEN
    R@ _LIBPA-W.PSTORE-WORK @ PSTORE-WORK-SIZE
        MSPAN-OVERLAP? 0= R> DROP ;

: LIBPA-WORK-VALID?  ( work -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP LIBPA-WORK-SIZE MSPAN-NONWRAPPING? 0= IF DROP 0 EXIT THEN
    DUP _LIBPA-W.MAGIC @ _LIBPA-WORK-MAGIC <> IF DROP 0 EXIT THEN
    DUP _LIBPA-W.SELF @ OVER <> IF DROP 0 EXIT THEN
    DUP _LIBPA-W.BUSY @ DUP 0= SWAP -1 = OR 0= IF DROP 0 EXIT THEN
    DUP _LIBPA-W.STATUS @ DUP LIBPA-S-OK <
        SWAP LIBPA-S-FAULT > OR IF DROP 0 EXIT THEN
    _LIBPA-WORK-GEOMETRY? ;

: LIBPA-INIT  ( store adapter -- status )
    >R
    DUP PSTORE-VALID? 0= IF DROP R> DROP LIBPA-S-INVALID EXIT THEN
    R@ 0= IF DROP R> DROP LIBPA-S-INVALID EXIT THEN
    R@ LIBPA-SIZE MSPAN-NONWRAPPING? 0= IF
        DROP R> DROP LIBPA-S-INVALID EXIT
    THEN
    R@ LIBPA-SIZE 2 PICK PSTORE-SPAN-DISJOINT? 0= IF
        DROP R> DROP LIBPA-S-INVALID EXIT
    THEN
    DUP PSTORE-SIZE R@ LIBPA-SIZE MSPAN-OVERLAP? IF
        DROP R> DROP LIBPA-S-INVALID EXIT
    THEN
    R@ LIBPA-SIZE 0 FILL
    _LIBPA-MAGIC R@ _LIBPA-D.MAGIC !
    R@ R@ _LIBPA-D.SELF !
    DUP R@ _LIBPA-D.STORE !
    LIBPA-S-OK R@ _LIBPA-D.STATUS !
    DROP R> DROP LIBPA-S-OK ;

: _LIBPA-INIT-RECORD-BUFFER?
  ( buffer-a buffer-u stage-a stage-u work -- flag )
    >R
    3 PICK 3 PICK MSPAN-NONWRAPPING? 0= IF
        _LIBPA-DROP4 R> DROP 0 EXIT
    THEN
    2 PICK 0> 0= IF _LIBPA-DROP4 R> DROP 0 EXIT THEN
    3 PICK 3 PICK R@ LIBPA-WORK-SIZE MSPAN-OVERLAP? IF
        _LIBPA-DROP4 R> DROP 0 EXIT
    THEN
    3 PICK 3 PICK 3 PICK 3 PICK MSPAN-OVERLAP? IF
        _LIBPA-DROP4 R> DROP 0 EXIT
    THEN
    _LIBPA-DROP4 R> DROP -1 ;

: _LIBPA-WORK-INIT-ARGS?
  ( pstore-work stage-a stage-u work -- flag )
    >R
    R@ 0= IF _LIBPA-DROP3 R> DROP 0 EXIT THEN
    R@ LIBPA-WORK-SIZE MSPAN-NONWRAPPING? 0= IF
        _LIBPA-DROP3 R> DROP 0 EXIT
    THEN
    2 PICK DUP PSTORE-WORK-VALID? 0= IF
        DROP _LIBPA-DROP3 R> DROP 0 EXIT
    THEN
    PSTORE-WORK-SIZE R@ LIBPA-WORK-SIZE MSPAN-OVERLAP? IF
        _LIBPA-DROP3 R> DROP 0 EXIT
    THEN
    1 PICK 1 PICK MSPAN-NONWRAPPING? 0= IF
        _LIBPA-DROP3 R> DROP 0 EXIT
    THEN
    DUP LIB-CONTENT-RECORD-MAX < IF _LIBPA-DROP3 R> DROP 0 EXIT THEN
    1 PICK 1 PICK R@ LIBPA-WORK-SIZE MSPAN-OVERLAP? IF
        _LIBPA-DROP3 R> DROP 0 EXIT
    THEN
    2 PICK PSTORE-WORK-SIZE 3 PICK 3 PICK MSPAN-OVERLAP? IF
        _LIBPA-DROP3 R> DROP 0 EXIT
    THEN
    2 PICK PSTORE-WORK-RECORD-BUFFER$
    3 PICK 3 PICK R@ _LIBPA-INIT-RECORD-BUFFER? 0= IF
        _LIBPA-DROP3 R> DROP 0 EXIT
    THEN
    _LIBPA-DROP3 R> DROP -1 ;

