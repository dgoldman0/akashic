: _LIBPIX-COMMIT-RUN  ( index-work -- status )
    >R
    R@ _LIBPIX.STATE @ _LIBPIX-STATE-ACTIVE <> IF
        R> DROP LIBPA-S-BUSY EXIT
    THEN
    R@ _LIBPIX.DOCUMENT-N @ R@ _LIBPIX.RAW-N @ OR
    R@ _LIBPIX.EDGE-N @ OR 0= IF
        LIBPA-S-INVALID R@ _LIBPIX-ABORT-ACTIVE R> DROP EXIT
    THEN

    R@ _LIBPIX-ROOTS-ADVANCE DUP IF
        R@ _LIBPIX-ABORT-ACTIVE R> DROP EXIT
    THEN DROP

    R@
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPIX.PSTORE-WORK @ _LIBPIX-ALLOCATE
    DUP IF
        _LIBPA-PERSIST>STATUS >R DROP R> R@ _LIBPIX-ABORT-ACTIVE
        R> DROP EXIT
    THEN DROP
    R@ _LIBPIX.APP-ROOT-PAGE !

    \ Claim the allocated application-root slot before reclaim finalization.
    \ A fresh high-water id is thereby reflected in the proposed page count;
    \ a reused id is already excluded by the allocation ledger.  Finalization
    \ can consequently allocate its own metadata without aliasing this slot.
    R@ _LIBPIX.PAGE PERSIST-PAGE-PAYLOAD-SIZE 0 FILL
    R@ _LIBPIX.PAGE PERSIST-PAGE-PAYLOAD-SIZE
    R@ _LIBPIX.APP-ROOT-PAGE @
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPIX.PSTORE-WORK @ PSTORE-WRITE-PAGE-TX
    DUP IF
        _LIBPA-PERSIST>STATUS R@ _LIBPIX-ABORT-ACTIVE R> DROP EXIT
    THEN DROP

    R@ _LIBPIX.RECLAIM-WORK RECLAIM-FINALIZE
    DUP IF
        _LIBPA-PERSIST>STATUS R@ _LIBPIX-ABORT-ACTIVE R> DROP EXIT
    THEN DROP
    R@ _LIBPIX-WORK>ROOT DUP IF
        R@ _LIBPIX-ABORT-ACTIVE R> DROP EXIT
    THEN DROP
    R@ _LIBPIX.PAGE PERSIST-PAGE-PAYLOAD-SIZE
    R@ _LIBPIX.APP-ROOT-PAGE @
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPIX.PSTORE-WORK @ PSTORE-WRITE-PAGE-TX
    DUP IF
        _LIBPA-PERSIST>STATUS R@ _LIBPIX-ABORT-ACTIVE R> DROP EXIT
    THEN DROP
    R@ _LIBPIX.APP-ROOT-PAGE @
    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPIX.PSTORE-WORK @ PSTORE-APPLICATION-ROOT!
    DUP IF
        _LIBPA-PERSIST>STATUS R@ _LIBPIX-ABORT-ACTIVE R> DROP EXIT
    THEN DROP

    R@ _LIBPIX.ADAPTER @ _LIBPA-D.STORE @
    R@ _LIBPIX.PSTORE-WORK @ PSTORE-COMMIT
    _LIBPA-PERSIST>STATUS R@ _LIBPIX.TEMP-STATUS !
    R@ _LIBPIX-COMMIT-DURABLE? IF
        R@ _LIBPIX.RECLAIM-WORK R@ _LIBPIX.RECLAIM RECLAIM-ADOPT
        DUP IF
            _LIBPA-PERSIST>STATUS
            R@ _LIBPIX.TEMP-STATUS @ LIBPA-S-OK = IF
                R@ _LIBPIX.TEMP-STATUS !
            ELSE
                DROP
            THEN
            _LIBPIX-STATE-REOPEN R@ _LIBPIX.STATE !
        ELSE
            DROP
            _LIBPIX-STATE-READY R@ _LIBPIX.STATE !
        THEN
    ELSE
        R@ _LIBPIX.TEMP-STATUS @ R@ _LIBPIX-ABORT-ACTIVE DROP
    THEN
    R@ _LIBPIX.TEMP-STATUS @ R> DROP ;

: _LIBPIX-COMMIT-FINALIZE  ( primary-status index-work -- final-status )
    >R
    DUP R@ _LIBPIX.TEMP-STATUS !
    R@ _LIBPIX.STATE @ _LIBPIX-STATE-ACTIVE <> IF
        R> DROP EXIT
    THEN
    R@ _LIBPIX-COMMIT-DURABLE? IF
        R@ _LIBPIX.RECLAIM-WORK R@ _LIBPIX.RECLAIM RECLAIM-ADOPT
        DUP IF
            _LIBPA-PERSIST>STATUS
            R@ _LIBPIX.TEMP-STATUS @ LIBPA-S-OK = IF
                R@ _LIBPIX.TEMP-STATUS !
            ELSE
                DROP
            THEN
            _LIBPIX-STATE-REOPEN R@ _LIBPIX.STATE !
        ELSE
            DROP
            _LIBPIX-STATE-READY R@ _LIBPIX.STATE !
        THEN
    ELSE
        R@ _LIBPIX-ABORT-ACTIVE
    THEN
    DROP R@ _LIBPIX.TEMP-STATUS @ R> DROP ;

