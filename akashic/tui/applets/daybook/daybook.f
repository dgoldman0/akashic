    _DB-DAY-COUNT NUM>STR _DB-ST-APPEND
    S"  entries  |  " _DB-ST-APPEND
    _DB-DIRTY @ IF S" Unsaved" ELSE S" Saved" THEN _DB-ST-APPEND
    _DB-E-SBAR-STATE @ ?DUP IF
        S" text" _DB-STATUS-BUF _DB-STATUS-U @ UTUI-SET-ATTR
    THEN ;

