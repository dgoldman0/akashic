    _DB-A-N @ _DB-A-ENTRY @ _DB-E-TEXT-U + !
    _DB-A-TEXT-A @ _DB-A-ENTRY @ _DB-E-TEXT + _DB-A-N @ CMOVE
    1 _DB-COUNT +! ;

