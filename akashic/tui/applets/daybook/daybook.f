VARIABLE _DB-N-OK

: _DB-FIXED-NUM  ( addr digits -- n flag )
    _DB-N-U ! _DB-N-A ! 0 _DB-N-ACC ! -1 _DB-N-OK !
    _DB-N-U @ 0 ?DO
        _DB-N-A @ I + C@
        DUP [CHAR] 0 < OVER [CHAR] 9 > OR IF
            DROP 0 _DB-N-OK !
        ELSE
            [CHAR] 0 - _DB-N-ACC @ 10 * + _DB-N-ACC !
        THEN
    LOOP
    _DB-N-ACC @ _DB-N-OK @ ;

