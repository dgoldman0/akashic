: _DB-ADD  ( kind done date minute text-a text-u -- index | -1 )
    _DB-A-TEXT-U ! _DB-A-TEXT-A ! _DB-A-MINUTE !
    _DB-A-DATE ! _DB-A-DONE ! _DB-A-KIND !
    _DB-COUNT @ _DB-MAX-ENTRIES >= IF -1 EXIT THEN
    _DB-COUNT @ DUP _DB-ENTRY _DB-A-ENTRY !
