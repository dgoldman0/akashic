    DUP _DB-PANEL-RGN !
    _DB-PANEL
    30 OVER !
    SWAP OVER 8 + !
    ['] _DB-PANEL-DRAW OVER 16 + !
    ['] _DB-PANEL-HANDLE OVER 24 + !
    WDG-F-VISIBLE WDG-F-DIRTY OR SWAP 32 + ! ;

