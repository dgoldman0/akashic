\ =====================================================================
\  app-catalog.f - Durable installed-applet catalog
\ =====================================================================
\  The catalog is machine state, not live component state.  It owns a
\  bounded, checksummed index at /app-catalog.bin and publishes changes
\  through VREPL.  Package code is resolved lazily through a caller-set
\  hook; this module never EVALUATEs a manifest field or loads an image.
\
\  Persistent mutations are staged in a complete candidate table.  The
\  live table is replaced only after VREPL reports a committed write.
\  Runtime-only descriptor, load-state, error, and slot fields are not
\  encoded.
\
\  Resolver hook:  ( entry context -- desc status )
\
\  Public API:
\    ACAT-NEW                 ( vfs -- catalog status )
\    ACAT-FREE                ( catalog -- )
\    ACAT-ACTIVATE            ( catalog -- status )
\    ACAT-RESOLVER!           ( xt context catalog -- )
\    ACAT-RELEASER!           ( xt context catalog -- )
\    ACAT-BIND-BUILTIN        ( desc default-flags catalog -- status )
\    ACAT-UPSERT-PACKAGE      ( ia iu ta tu va vu ma mu flags cat -- status )
\    ACAT-INSTALL-PACKAGE     ( ia iu ta tu va vu ma mu flags cat -- status )
\    ACAT-SET-FLAGS           ( flags entry catalog -- status )
\    ACAT-QUARANTINE          ( reason entry catalog -- status )
\    ACAT-RESOLVE             ( entry catalog -- desc status )
\    ACAT-ENCODE              ( buffer capacity catalog -- length status )
\    ACAT-DECODE              ( buffer length catalog -- status )
\    ACAT-COUNT / ACAT-NTH / ACAT-FIND-ID
\ =====================================================================

