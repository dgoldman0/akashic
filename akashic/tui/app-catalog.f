    DUP _ACAT-HDR-SIZE < OVER ACAT-FILE-MAX > OR IF
        DROP ACAT-S-INVALID _ACA-STATUS ! EXIT
    THEN DROP
    _ACA-CAT @ ACAT.IO _ACA-SIZE @ _ACA-FD @ VFS-READ-EXACT IF
        ACAT-S-IO _ACA-STATUS ! EXIT
    THEN
    _ACA-CAT @ ACAT.IO _ACA-SIZE @ _ACA-CAT @ ACAT-DECODE
    _ACA-STATUS ! ;

: _ACAT-ACTIVATE-CLEANUP  ( -- failed? )
    0 _ACA-CLEAN-FAILED !
    _ACA-FD @ ?DUP IF
        DROP ['] _ACAT-ACTIVATE-CLOSE CATCH IF -1 _ACA-CLEAN-FAILED ! THEN
        0 _ACA-FD !
    THEN
    _ACA-HAVE-VFS @ IF
        0 _ACA-HAVE-VFS !
        ['] _ACAT-ACTIVATE-RESTORE CATCH IF -1 _ACA-CLEAN-FAILED ! THEN
    THEN _ACA-CLEAN-FAILED @ ;

: ACAT-ACTIVATE  ( c -- status )
    DUP ACAT-VALID? 0= IF DROP ACAT-S-INVALID EXIT THEN _ACA-CAT !
    _ACA-CAT @ ACAT.REPLACE VREPL-RECOVER DUP
    VREPL-S-OK = OVER VREPL-S-ROLLED-BACK = OR
    OVER VREPL-S-COMMITTED-CLEANUP = OR 0= IF
        DROP _ACAT-D-F-ACTIVE _ACAT-D-F-READONLY OR _ACAT-D-F-RECOVERY OR
        _ACA-CAT @ ACAT.FLAGS !
        ACAT-S-RECOVERY DUP _ACA-CAT @ ACAT.LAST-STATUS ! EXIT
    THEN DROP
    0 _ACA-FD ! 0 _ACA-HAVE-VFS ! ACAT-S-IO _ACA-STATUS !
    ['] _ACAT-ACTIVATE-OP CATCH IF ACAT-S-IO _ACA-STATUS ! THEN
    _ACAT-ACTIVATE-CLEANUP IF ACAT-S-IO _ACA-STATUS ! THEN
    _ACA-STATUS @ DUP ACAT-S-OK = OVER ACAT-S-MISSING = OR IF
        _ACAT-D-F-ACTIVE _ACA-CAT @ ACAT.FLAGS !
    ELSE
        _ACAT-D-F-ACTIVE _ACAT-D-F-READONLY OR _ACAT-D-F-RECOVERY OR
        _ACA-CAT @ ACAT.FLAGS !
    THEN
    _ACA-STATUS @ DUP _ACA-CAT @ ACAT.LAST-STATUS ! ;

