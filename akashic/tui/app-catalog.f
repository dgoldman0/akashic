VARIABLE _ACR-CAT
VARIABLE _ACR-DESC
VARIABLE _ACR-STATUS

: _ACAT-RESOLVE-CALL  ( -- )
    _ACR-E @ _ACR-CAT @ ACAT.RESOLVE-CTX @
    _ACR-CAT @ ACAT.RESOLVE-XT @ EXECUTE
    _ACR-STATUS ! _ACR-DESC ! ;

: _ACAT-RESOLVE-DISCARD  ( -- )
    _ACR-DESC @ ?DUP IF
        _ACR-CAT @ _ACAT-RELEASE-DESC
        0 _ACR-DESC !
    THEN ;

: ACAT-RESOLVE  ( entry c -- desc status )
    _ACR-CAT ! _ACR-E !
    _ACR-CAT @ ACAT-VALID? 0= IF 0 ACAT-S-INVALID EXIT THEN
    _ACR-E @ _ACR-CAT @ _ACAT-LIVE-INDEX 0= IF DROP 0 ACAT-S-NOT-FOUND EXIT THEN DROP
    _ACR-E @ ACE-QUARANTINED? IF 0 ACAT-S-QUARANTINED EXIT THEN
    _ACR-E @ ACE-ENABLED? 0= IF 0 ACAT-S-DISABLED EXIT THEN
    _ACR-E @ ACE.DESC @ ?DUP IF ACAT-S-OK EXIT THEN
    _ACR-E @ ACE-BUILTIN? IF 0 ACAT-S-NOT-FOUND EXIT THEN
    _ACR-CAT @ ACAT.RESOLVE-XT @ 0= IF 0 ACAT-S-NO-RESOLVER EXIT THEN
    0 _ACR-DESC ! ACAT-S-RESOLVE _ACR-STATUS !
    ['] _ACAT-RESOLVE-CALL CATCH IF ACAT-S-RESOLVE _ACR-STATUS ! THEN
    _ACR-STATUS @ IF
        _ACAT-RESOLVE-DISCARD
        ACAT-R-FAILED _ACR-E @ ACE.STATE !
        _ACR-STATUS @ _ACR-E @ ACE.ERROR !
        0 _ACR-STATUS @ EXIT
    THEN
    _ACR-DESC @ APP-DESC-VALID? 0= IF
        _ACAT-RESOLVE-DISCARD
        ACAT-R-FAILED _ACR-E @ ACE.STATE !
        ACAT-S-INCOMPATIBLE _ACR-E @ ACE.ERROR !
        0 ACAT-S-INCOMPATIBLE EXIT
    THEN
    _ACR-DESC @ APP.COMP-DESC @ DUP COMP.ID-A @ SWAP COMP.ID-U @
    _ACR-E @ ACE-ID$ STR-STR= 0= IF
        _ACAT-RESOLVE-DISCARD
        ACAT-R-FAILED _ACR-E @ ACE.STATE !
        ACAT-S-INCOMPATIBLE _ACR-E @ ACE.ERROR !
        0 ACAT-S-INCOMPATIBLE EXIT
    THEN
    _ACR-DESC @ _ACR-E @ ACE.DESC !
    ACAT-R-LOADED _ACR-E @ ACE.STATE !
    0 _ACR-E @ ACE.ERROR !
    _ACR-DESC @ ACAT-S-OK ;

