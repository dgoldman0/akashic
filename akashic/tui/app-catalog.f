: _ACAT-ENCODE-BASE  ( base count generation buffer capacity -- length status )
    _ACENC-CAP ! _ACENC-BUF ! _ACENC-GEN ! _ACENC-N ! _ACENC-BASE !
    _ACENC-N @ 0< _ACENC-N @ ACAT-MAX > OR IF 0 ACAT-S-INVALID EXIT THEN
    _ACAT-HDR-SIZE _ACENC-N @ _ACE-PERSIST-SIZE * + DUP _ACENC-LEN !
    _ACENC-CAP @ > IF 0 ACAT-S-FULL EXIT THEN
    _ACENC-N @ 0 ?DO
        _ACENC-BASE @ I ACAT-ENTRY-SIZE * +
        _ACAT-PERSIST-VALID? 0= IF 0 ACAT-S-INVALID UNLOOP EXIT THEN
    LOOP
