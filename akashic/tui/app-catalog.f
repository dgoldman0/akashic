    _ACC-CAT ! _ACC-N !
    _ACC-CAT @ ACAT-READONLY? IF ACAT-S-READONLY EXIT THEN
    _ACC-CAT @ ACAT.CANDIDATE _ACC-N @
    _ACC-CAT @ ACAT.GENERATION @ 1+
    _ACC-CAT @ ACAT.IO ACAT-FILE-MAX _ACAT-ENCODE-BASE
    DUP IF NIP EXIT THEN DROP _ACC-LEN !
    _ACC-CAT @ ACAT.IO _ACC-LEN @ _ACC-CAT @ ACAT.REPLACE VREPL-REPLACE
    DUP VREPL-S-OK = OVER VREPL-S-COMMITTED-CLEANUP = OR IF
        DROP
        _ACC-CAT @ ACAT.CANDIDATE _ACC-CAT @ ACAT.LIVE
        ACAT-MAX ACAT-ENTRY-SIZE * CMOVE
        _ACC-N @ _ACC-CAT @ ACAT.COUNT !
        1 _ACC-CAT @ ACAT.GENERATION +!
        ACAT-S-OK DUP _ACC-CAT @ ACAT.LAST-STATUS ! EXIT
    THEN
    DUP VREPL-S-UNCERTAIN = IF
        _ACC-CAT @ ACAT.FLAGS DUP @
        _ACAT-D-F-READONLY _ACAT-D-F-RECOVERY OR OR SWAP !
        DROP ACAT-S-RECOVERY
    ELSE
        DROP ACAT-S-IO
    THEN DUP _ACC-CAT @ ACAT.LAST-STATUS ! ;

