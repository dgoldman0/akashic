: ACAT-UPSERT-PACKAGE  ( ia iu ta tu va vu ma mu flags c -- status )
    _ACP-CAT ! _ACP-FLAGS ! _ACP-M-U ! _ACP-M-A !
    _ACP-V-U ! _ACP-V-A ! _ACP-T-U ! _ACP-T-A ! _ACP-ID-U ! _ACP-ID-A !
    _ACP-CAT @ ACAT-VALID? 0= IF ACAT-S-INVALID EXIT THEN
    _ACP-ID-U @ 0= _ACP-ID-U @ ACAT-ID-MAX > OR IF ACAT-S-INVALID EXIT THEN
    _ACP-T-U @ ACAT-TITLE-MAX > _ACP-V-U @ ACAT-VERSION-MAX > OR
    _ACP-M-U @ 0= OR _ACP-M-U @ ACAT-PATH-MAX > OR IF ACAT-S-INVALID EXIT THEN
    _ACP-FLAGS @ _ACAT-F-KIND INVERT AND ACAT-F-PACKAGE OR
    _ACAT-FLAGS-VALID? 0= IF ACAT-S-INVALID EXIT THEN
    _ACP-ID-A @ _ACP-ID-U @ _ACP-CAT @ ACAT-FIND-ID DUP _ACP-EXIST ! IF
        _ACP-EXIST @ ACE-PACKAGE? 0= IF ACAT-S-INCOMPATIBLE EXIT THEN
        _ACP-EXIST @ ACE.SLOT @ IF ACAT-S-BUSY EXIT THEN
        _ACP-EXIST @ _ACP-CAT @ _ACAT-LIVE-INDEX 0= IF
            DROP ACAT-S-NOT-FOUND EXIT
        THEN
        _ACP-IDX !
        \ Rebuild metadata without silently changing user policy.  Enabled,
        \ pinned, autostart, quarantine, and its reason survive an update.
        _ACP-EXIST @ ACE.FLAGS @ _ACP-OLD-FLAGS !
        _ACP-EXIST @ ACE.REASON @ _ACP-OLD-REASON !
        _ACP-EXIST @ ACE.DESC @ _ACP-OLD-DESC !
        _ACP-CAT @ _ACAT-COPY-LIVE>CAND
        _ACP-IDX @ _ACP-CAT @ _ACAT-CAND-ENTRY
        _ACAT-FILL-CAND-PACKAGE DUP IF EXIT THEN DROP
        _ACP-OLD-FLAGS @ _ACP-IDX @ _ACP-CAT @ _ACAT-CAND-ENTRY ACE.FLAGS !
        _ACP-OLD-REASON @ _ACP-IDX @ _ACP-CAT @ _ACAT-CAND-ENTRY ACE.REASON !
        _ACP-CAT @ ACAT.COUNT @ _ACP-CAT @ _ACAT-COMMIT-CANDIDATE
        DUP IF EXIT THEN DROP
        _ACP-OLD-DESC @ _ACP-CAT @ _ACAT-RELEASE-DESC
        ACAT-S-OK EXIT
    THEN
    _ACP-CAT @ ACAT.COUNT @ ACAT-MAX >= IF ACAT-S-FULL EXIT THEN
    _ACP-CAT @ _ACAT-COPY-LIVE>CAND
    _ACP-CAT @ ACAT.COUNT @ _ACP-CAT @ _ACAT-CAND-ENTRY
    _ACAT-FILL-CAND-PACKAGE DUP IF EXIT THEN DROP
    _ACP-CAT @ ACAT.COUNT @ 1+ _ACP-CAT @ _ACAT-COMMIT-CANDIDATE ;

