    ACAT-FILE-VERSION _ACENC-BUF @ _ACH-VERSION + !
    _ACENC-GEN @ _ACENC-BUF @ _ACH-GENERATION + !
    _ACENC-N @ _ACENC-BUF @ _ACH-COUNT + !
