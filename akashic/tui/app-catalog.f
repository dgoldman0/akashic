VARIABLE _ACPATH-START
VARIABLE _ACPATH-CH

: _ACAT-PATH-CHAR?  ( ch -- flag )
    _ACPATH-CH !
    _ACPATH-CH @ [CHAR] a >= _ACPATH-CH @ [CHAR] z <= AND
    _ACPATH-CH @ [CHAR] A >= _ACPATH-CH @ [CHAR] Z <= AND OR
    _ACPATH-CH @ [CHAR] 0 >= _ACPATH-CH @ [CHAR] 9 <= AND OR
    _ACPATH-CH @ [CHAR] . = OR _ACPATH-CH @ [CHAR] - = OR
    _ACPATH-CH @ [CHAR] _ = OR _ACPATH-CH @ [CHAR] / = OR ;

: _ACAT-PATH-COMPONENT?  ( end-offset -- flag )
    _ACPATH-START @ - DUP 0= OVER VREPL-NAME-MAX > OR IF
        DROP 0 EXIT
    THEN
    DUP 1 = IF
        _ACPATH-A @ _ACPATH-START @ + C@ [CHAR] . = IF DROP 0 EXIT THEN
    THEN
    DUP 2 = IF
        _ACPATH-A @ _ACPATH-START @ + C@ [CHAR] . =
        _ACPATH-A @ _ACPATH-START @ + 1+ C@ [CHAR] . = AND IF
            DROP 0 EXIT
        THEN
    THEN
    DROP -1 ;

: _ACAT-MANIFEST-PATH-VALID?  ( a u -- flag )
    _ACPATH-U ! _ACPATH-A !
    _ACPATH-U @ 2 < _ACPATH-U @ ACAT-PATH-MAX > OR IF 0 EXIT THEN
    _ACPATH-A @ C@ [CHAR] / <> IF 0 EXIT THEN
    1 _ACPATH-START !
    _ACPATH-U @ 1 DO
        _ACPATH-A @ I + C@ DUP _ACAT-PATH-CHAR? 0= IF
            DROP 0 UNLOOP EXIT
        THEN
        [CHAR] / = IF
            I _ACAT-PATH-COMPONENT? 0= IF 0 UNLOOP EXIT THEN
            I 1+ _ACPATH-START !
        THEN
    LOOP
    _ACPATH-U @ _ACAT-PATH-COMPONENT? ;

