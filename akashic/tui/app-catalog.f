\ Package descriptors returned by the resolver are owned through an explicit
\ companion hook.  Built-in descriptors never pass through these words.
VARIABLE _ACREL-DESC
VARIABLE _ACREL-CAT
VARIABLE _ACREL-ENTRY

: _ACAT-RELEASE-CALL  ( -- )
    _ACREL-DESC @ _ACREL-CAT @ ACAT.RELEASE-CTX @
    _ACREL-CAT @ ACAT.RELEASE-XT @ EXECUTE ;

: _ACAT-RELEASE-DESC  ( desc catalog -- )
    _ACREL-CAT ! _ACREL-DESC !
    _ACREL-DESC @ 0= IF EXIT THEN
    _ACREL-CAT @ ACAT.RELEASE-XT @ 0= IF EXIT THEN
    ['] _ACAT-RELEASE-CALL CATCH DROP ;

: _ACAT-RELEASE-ENTRY  ( entry catalog -- )
    _ACREL-CAT ! _ACREL-ENTRY !
    _ACREL-ENTRY @ ACE-PACKAGE? 0= IF EXIT THEN
    \ A live Desk slot still executes through its descriptor.  Proper Desk
    \ teardown closes every slot first; defensive misuse leaks rather than
    \ introducing a use-after-free.
    _ACREL-ENTRY @ ACE.SLOT @ IF EXIT THEN
    _ACREL-ENTRY @ ACE.DESC @ DUP 0= IF DROP EXIT THEN _ACREL-DESC !
    0 _ACREL-ENTRY @ ACE.DESC !
    ACAT-R-AVAILABLE _ACREL-ENTRY @ ACE.STATE !
    0 _ACREL-ENTRY @ ACE.ERROR !
    _ACREL-DESC @ _ACREL-CAT @ _ACAT-RELEASE-DESC ;

VARIABLE _ACRA-CAT

: _ACAT-RELEASE-ALL  ( catalog -- )
    _ACRA-CAT !
    _ACRA-CAT @ ACAT.COUNT @ 0 ?DO
        I _ACRA-CAT @ _ACAT-LIVE-ENTRY _ACRA-CAT @ _ACAT-RELEASE-ENTRY
    LOOP ;

: _ACAT-HAS-LIVE-SLOTS?  ( catalog -- flag )
    _ACRA-CAT !
    _ACRA-CAT @ ACAT.COUNT @ 0 ?DO
        I _ACRA-CAT @ _ACAT-LIVE-ENTRY ACE.SLOT @ IF -1 UNLOOP EXIT THEN
    LOOP 0 ;

: ACAT-RELEASER!  ( xt context catalog -- )
    >R R@ ACAT-VALID? IF
        R@ ACAT.RELEASE-CTX ! R> ACAT.RELEASE-XT !
    ELSE
        R> DROP 2DROP
    THEN ;

