: _ACAT-DECODE-CANDIDATE  ( a u c -- status )
    _ACDEC-CAT ! _ACDEC-U ! _ACDEC-A !
    _ACDEC-U @ _ACAT-HDR-SIZE < IF ACAT-S-INVALID EXIT THEN
    _ACDEC-A @ _ACH-MAGIC + @ _ACAT-FILE-MAGIC <> IF ACAT-S-INVALID EXIT THEN
    _ACDEC-A @ _ACH-VERSION + @ ACAT-FILE-VERSION <> IF ACAT-S-INVALID EXIT THEN
    _ACDEC-A @ _ACH-COUNT + @ DUP _ACDEC-N !
    DUP 0< SWAP ACAT-MAX > OR IF ACAT-S-INVALID EXIT THEN
    _ACAT-HDR-SIZE _ACDEC-N @ _ACE-PERSIST-SIZE * +
    _ACDEC-U @ <> IF ACAT-S-INVALID EXIT THEN
    _ACDEC-A @ _ACH-RESERVED + 24 _ACAT-ZERO? 0= IF ACAT-S-INVALID EXIT THEN
    _ACDEC-A @ _ACDEC-U @ _ACAT-CRC
    _ACDEC-A @ _ACH-CRC + @ <> IF ACAT-S-INVALID EXIT THEN
    _ACDEC-A @ _ACH-GENERATION + @ _ACDEC-GEN !
    _ACDEC-CAT @ ACAT.CANDIDATE ACAT-MAX ACAT-ENTRY-SIZE * 0 FILL
    _ACDEC-N @ 0 ?DO
        _ACDEC-A @ _ACAT-HDR-SIZE + I _ACE-PERSIST-SIZE * +
        I _ACDEC-CAT @ _ACAT-CAND-ENTRY DUP _ACDEC-E !
        _ACE-PERSIST-SIZE CMOVE
        _ACDEC-E @ _ACAT-PERSIST-VALID? 0= IF ACAT-S-INVALID UNLOOP EXIT THEN
        _ACDEC-E @ I _ACDEC-CAT @ _ACAT-CAND-DUPLICATE? IF
            ACAT-S-DUPLICATE UNLOOP EXIT
        THEN
        _ACDEC-E @ ACE-BUILTIN? IF ACAT-R-UNBOUND ELSE ACAT-R-AVAILABLE THEN
        _ACDEC-E @ ACE.STATE !
    LOOP
    ACAT-S-OK ;

: ACAT-DECODE  ( a u c -- status )
    DUP ACAT-VALID? 0= IF DROP 2DROP ACAT-S-INVALID EXIT THEN
    DUP _ACDEC-CAT !
    _ACAT-DECODE-CANDIDATE DUP IF EXIT THEN DROP
    _ACDEC-CAT @ _ACAT-HAS-LIVE-SLOTS? IF ACAT-S-BUSY EXIT THEN
    _ACDEC-CAT @ _ACAT-RELEASE-ALL
    _ACDEC-CAT @ ACAT.CANDIDATE _ACDEC-CAT @ ACAT.LIVE
    ACAT-MAX ACAT-ENTRY-SIZE * CMOVE
    _ACDEC-N @ _ACDEC-CAT @ ACAT.COUNT !
    _ACDEC-GEN @ _ACDEC-CAT @ ACAT.GENERATION !
    ACAT-S-OK ;

