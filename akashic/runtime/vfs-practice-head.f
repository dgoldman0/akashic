: _PHEADVFS-SELECT-WRITE-PATH  ( slot -- )
    IF _PHEADVFS-PATH-B ELSE _PHEADVFS-PATH-A THEN
    _PHW-PATH-U ! _PHW-PATH-A ! ;

: _PHEADVFS-OPEN-WRITE  ( -- fd | 0 )
    VFS-CUR _PHW-OLD-VFS !
    _PHW-STORE @ PHEADVFS.VFS @ VFS-USE
    _PHW-PATH-A @ _PHW-PATH-U @ VFS-OPEN DUP 0= IF
        DROP
        _PHW-PATH-A @ _PHW-PATH-U @
        _PHW-STORE @ PHEADVFS.VFS @ VFS-CREATE
        DUP 0= IF
            DROP _PHW-OLD-VFS @ VFS-USE 0 EXIT
        THEN
        DROP _PHW-PATH-A @ _PHW-PATH-U @ VFS-OPEN
    THEN
    _PHW-OLD-VFS @ VFS-USE ;

: PHEADVFS-SAVE  ( head store -- status )
    OVER 0= OVER 0= OR IF 2DROP PHEADVFS-S-INVALID EXIT THEN
    DUP PHEADVFS.VFS @ 0= IF 2DROP PHEADVFS-S-INVALID EXIT THEN
    _PHW-STORE ! _PHW-HEAD !
    _PHW-STORE @ PHEADVFS-READONLY? IF PHEADVFS-S-READONLY EXIT THEN
    _PHW-HEAD @ PHEAD-READONLY? IF PHEADVFS-S-READONLY EXIT THEN
    _PHW-HEAD @ PHEAD-VALID? 0= IF PHEADVFS-S-INVALID EXIT THEN
    _PHW-STORE @ PHEADVFS.GENERATION @ 1+
    DUP 0> 0= IF DROP PHEADVFS-S-INVALID EXIT THEN
    _PHW-GENERATION !
    _PHW-STORE @ PHEADVFS.ACTIVE-SLOT @ 0>= _PHW-HAD-ACTIVE !
    _PHW-STORE @ PHEADVFS.ACTIVE-SLOT @ 0= IF 1 ELSE 0 THEN
    DUP _PHW-SLOT ! _PHEADVFS-SELECT-WRITE-PATH
    PHEADVFS-SNAPSHOT-SIZE ALLOCATE DUP IF
        2DROP PHEADVFS-S-NOMEM EXIT
    THEN
    DROP _PHW-BUFFER !
    _PHW-GENERATION @ _PHW-HEAD @ _PHW-BUFFER @
    PHEADVFS-SNAPSHOT-SIZE _PHEADVFS-ENCODE
    _PHW-STATUS ! _PHW-LENGTH !
    _PHW-STATUS @ PHEADVFS-S-OK <> IF
        _PHW-BUFFER @ FREE _PHW-STATUS @ EXIT
    THEN
    _PHEADVFS-OPEN-WRITE DUP _PHW-FD ! 0= IF
        _PHW-BUFFER @ FREE PHEADVFS-S-IO EXIT
    THEN
    _PHW-FD @ VFS-REWIND
    0 _PHW-FD @ VFS-TRUNCATE IF
        _PHW-FD @ VFS-CLOSE _PHW-BUFFER @ FREE
        PHEADVFS-S-IO EXIT
    THEN
    _PHW-BUFFER @ _PHW-LENGTH @ _PHW-FD @ VFS-WRITE _PHW-ACTUAL !
    _PHW-FD @ VFS-CLOSE
    _PHW-BUFFER @ FREE
    _PHW-ACTUAL @ _PHW-LENGTH @ <> IF PHEADVFS-S-IO EXIT THEN
    _PHW-STORE @ PHEADVFS.VFS @ VFS-SYNC IF PHEADVFS-S-IO EXIT THEN
    _PHW-GENERATION @ _PHW-STORE @ PHEADVFS.GENERATION !
    _PHW-SLOT @ _PHW-STORE @ PHEADVFS.ACTIVE-SLOT !
    _PHW-HAD-ACTIVE @ IF
        0 _PHW-STORE @ PHEADVFS.FLAGS !
    ELSE
        PHEADVFS-F-FALLBACK _PHW-STORE @ PHEADVFS.FLAGS !
    THEN
    PHEADVFS-S-OK ;

