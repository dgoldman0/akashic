\ =====================================================================
\  vfs-practice-head.f - Dual-generation VFS Practice-head storage
\ =====================================================================
\  Each slot contains a fixed, checksummed envelope around the pointer-
\  free PHEAD record.  Saves replace only the inactive slot and publish
\  the new in-memory generation after VFS-SYNC succeeds.
\
\  This is a corruption/torn-write boundary, not an authentication
\  boundary.  Practice activation must still authenticate committed
\  roots and enforce schema, manifest, export, and grant policy.
\
\  Pending grants, invocation handles, leases, queue entries, execution
\  tokens, and runtime pointers have no representation in this format.
\
\  Public API:
\    PHEADVFS-INIT          ( vfs store -- status )
\    PHEADVFS-SAVE          ( head store -- status )
\    PHEADVFS-LOAD          ( destination store -- status )
\    PHEADVFS-LOAD-VALIDATED
\      ( destination rejected validator-xt validator-data store -- status )
\    PHEADVFS-REINITIALIZE  ( head recovery-store -- status )
\    PHEADVFS-READONLY? / PHEADVFS-RECOVERY? / PHEADVFS-FALLBACK?
\
\  PHEADVFS-REINITIALIZE is deliberately available only after LOAD has
\  entered recovery.  It replaces both generations with a caller-supplied
\  valid, writable head; it does not silently bless recovered data.
\ =====================================================================

