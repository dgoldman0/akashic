: PHEADVFS.GENERATION   ( store -- a ) _PHSV-GENERATION + ;
: PHEADVFS.ACTIVE-SLOT  ( store -- a ) _PHSV-ACTIVE-SLOT + ;
: PHEADVFS.FLAGS        ( store -- a ) _PHSV-FLAGS + ;
