: _RSS-UNIQUE?  ( index feed -- flag )
    _RSS-U-FEED ! _RSS-U-I !
    _RSS-U-I @ _RSS-U-FEED @ _RSS-FEED-ITEM DUP _RSS-U-ITEM !
    RSS.ITEM.IDENTITY-KIND @ RSS-IDENTITY-GUID = IF
        _RSS-U-ITEM @ RSS.ITEM.GUID
    ELSE _RSS-U-ITEM @ RSS.ITEM.LINK THEN
    _RSS-U-U ! _RSS-U-A !
    _RSS-U-I @ 0 ?DO
        I _RSS-U-FEED @ _RSS-FEED-ITEM DUP RSS.ITEM.IDENTITY-KIND @
            RSS-IDENTITY-GUID = IF RSS.ITEM.GUID ELSE RSS.ITEM.LINK THEN
        _RSS-U-A @ _RSS-U-U @ STR-STR= IF 0 UNLOOP EXIT THEN
    LOOP -1 ;

