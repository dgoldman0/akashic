: _RSS-SCAN-ROOT  ( root-a root-u -- status )
    0 _RSS-CHANNELS ! 0 _RSS-ATOM-CHANNEL !
    MU-ENTER _RSS-ROOT-CUR-U ! _RSS-ROOT-CUR-A !
    BEGIN
        _RSS-ROOT-CUR-A @ _RSS-ROOT-CUR-U @ _RSS-CHILD
        DUP IF >R 2DROP 2DROP DROP R> EXIT THEN DROP
        0= IF
            2DROP 2DROP
            _RSS-CHANNELS @ 1 = IF SYN-S-OK ELSE SYN-S-MISSING THEN
            EXIT
        THEN
        _RSS-NU ! _RSS-NA !
        _RSS-ROOT-CUR-U ! _RSS-ROOT-CUR-A !
        _RSS-NA @ _RSS-NU @ S" channel" STR-STR= IF
            _RSS-ROOT-CUR-A @ _RSS-ROOT-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-CHANNELS @ IF SYN-S-INVALID EXIT THEN
            _RSS-ROOT-CUR-A @ _RSS-CHANNEL-A !
            _RSS-ROOT-CUR-U @ _RSS-CHANNEL-U !
            _RSS-ROOT-CUR-A @ _RSS-ROOT-CUR-U @ _RSS-ATOM-BINDING
            DUP IF NIP EXIT THEN DROP _RSS-ATOM-CHANNEL !
            1 _RSS-CHANNELS +!
        THEN
        _RSS-ROOT-CUR-A @ _RSS-ROOT-CUR-U @ MU-SKIP-ELEMENT
            _RSS-ROOT-CUR-U ! _RSS-ROOT-CUR-A !
    AGAIN ;

: _RSS-ATOM-LINK-NS  ( element-a element-u -- status )
    _RSS-ATOM-BINDING DUP IF NIP EXIT THEN DROP _RSS-ATOM-LOCAL !
    _RSS-ATOM-LOCAL @ 0= IF
        _RSS-ATOM-CHANNEL @ DUP 0= IF DROP _RSS-ATOM-ROOT @ THEN
    ELSE
        _RSS-ATOM-LOCAL @
    THEN
    1 = IF SYN-S-OK ELSE SYN-S-UNSUPPORTED THEN ;

: _RSS-DECODE-INTO  ( document-a document-u feed -- status )
    _RSS-DEST ! _RSS-IN-U ! _RSS-IN-A !
    _RSS-IN-A @ _RSS-IN-U @ SX-VALIDATE ?DUP IF EXIT THEN
    _RSS-IN-A @ _RSS-IN-U @ SX-ROOT
    DUP IF >R 2DROP R> EXIT THEN DROP
    _RSS-ROOT-U ! _RSS-ROOT-A !
    _RSS-ROOT-A @ _RSS-ROOT-U @ MU-GET-TAG-NAME
        _RSS-NU ! _RSS-NA ! 2DROP
    _RSS-NA @ _RSS-NU @ S" rss" STR-STR= 0= IF
        SYN-S-UNSUPPORTED EXIT
    THEN
    _RSS-ROOT-A @ _RSS-ROOT-U @ S" version"
        _RSS-ATTR-TMP 64 _RSS-TMP _RSS-ATTR-TO
        DUP IF NIP EXIT THEN DROP
    0= IF SYN-S-MISSING EXIT THEN
    _RSS-ATTR-TMP _RSS-TMP @ S" 2.0" STR-STR= 0= IF
        SYN-S-UNSUPPORTED EXIT
    THEN
    _RSS-ROOT-A @ _RSS-ROOT-U @ _RSS-ELEMENT-NO-NS ?DUP IF EXIT THEN
    _RSS-ROOT-A @ _RSS-ROOT-U @ _RSS-ATOM-BINDING
        DUP IF NIP EXIT THEN DROP _RSS-ATOM-ROOT !
    _RSS-ROOT-A @ _RSS-ROOT-U @ _RSS-SCAN-ROOT ?DUP IF EXIT THEN
    0 _RSS-FLAGS ! 0 _RSS-COUNT ! 0 _RSS-COUNT2 !
    _RSS-CHANNEL-A @ _RSS-CHANNEL-U @ MU-ENTER
        _RSS-CUR-U ! _RSS-CUR-A !
    BEGIN
        _RSS-CUR-A @ _RSS-CUR-U @ _RSS-CHILD
        DUP IF >R 2DROP 2DROP DROP R> EXIT THEN DROP
        0= IF
            2DROP 2DROP
            _RSS-FLAGS @ 7 AND 7 <> IF SYN-S-MISSING EXIT THEN
            _RSS-COUNT @ _RSS-DEST @ RSS.FEED.COUNT !
            _RSS-COUNT2 @ _RSS-DEST @ RSS.FEED.CATEGORY-COUNT !
            SYN-S-OK EXIT
        THEN
        _RSS-NU ! _RSS-NA ! _RSS-CUR-U ! _RSS-CUR-A !
        _RSS-NA @ _RSS-NU @ S" title" STR-STR= IF
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-FLAGS @ 1 AND IF SYN-S-INVALID EXIT THEN
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-DEST @ _RSS-FEED-TITLE +
                SYN-TITLE-CAP _RSS-DEST @ _RSS-FEED-TITLE-U + _RSS-COPY
                ?DUP IF EXIT THEN 1 _RSS-FLAGS +!
        ELSE _RSS-NA @ _RSS-NU @ S" link" STR-STR= IF
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-FLAGS @ 2 AND IF SYN-S-INVALID EXIT THEN
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-DEST @ _RSS-FEED-LINK +
                SYN-URL-CAP _RSS-DEST @ _RSS-FEED-LINK-U + _RSS-COPY
                ?DUP IF EXIT THEN 2 _RSS-FLAGS +!
        ELSE _RSS-NA @ _RSS-NU @ S" description" STR-STR= IF
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-FLAGS @ 4 AND IF SYN-S-INVALID EXIT THEN
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-DEST @ _RSS-FEED-DESCRIPTION +
                SYN-DESCRIPTION-CAP _RSS-DEST @ _RSS-FEED-DESCRIPTION-U +
                _RSS-COPY ?DUP IF EXIT THEN 4 _RSS-FLAGS +!
        ELSE _RSS-NA @ _RSS-NU @ S" language" STR-STR= IF
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-FLAGS @ 8 AND IF SYN-S-INVALID EXIT THEN
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-DEST @ _RSS-FEED-LANGUAGE + 64
                _RSS-DEST @ _RSS-FEED-LANGUAGE-U + _RSS-COPY
                ?DUP IF EXIT THEN 8 _RSS-FLAGS +!
        ELSE _RSS-NA @ _RSS-NU @ S" lastBuildDate" STR-STR= IF
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-FLAGS @ 16 AND IF SYN-S-INVALID EXIT THEN
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-DEST @ _RSS-FEED-BUILDDATE +
                SYN-DATETIME-CAP _RSS-DEST @ _RSS-FEED-BUILDDATE-U +
                _RSS-COPY ?DUP IF EXIT THEN 16 _RSS-FLAGS +!
        ELSE _RSS-NA @ _RSS-NU @ S" pubDate" STR-STR= IF
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-FLAGS @ 32 AND IF SYN-S-INVALID EXIT THEN
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-DEST @ _RSS-FEED-PUBDATE +
                SYN-DATETIME-CAP _RSS-DEST @ _RSS-FEED-PUBDATE-U +
                _RSS-COPY ?DUP IF EXIT THEN 32 _RSS-FLAGS +!
        ELSE _RSS-NA @ _RSS-NU @ S" generator" STR-STR= IF
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-FLAGS @ 64 AND IF SYN-S-INVALID EXIT THEN
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-DEST @ _RSS-FEED-GENERATOR +
                SYN-TITLE-CAP _RSS-DEST @ _RSS-FEED-GENERATOR-U +
                _RSS-COPY ?DUP IF EXIT THEN 64 _RSS-FLAGS +!
        ELSE _RSS-NA @ _RSS-NU @ S" category" STR-STR= IF
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-COUNT2 @ SYN-MAX-TAGS >= IF SYN-S-CAPACITY EXIT THEN
            _RSS-COUNT2 @ RSS-CATEGORY-SIZE *
                _RSS-DEST @ _RSS-FEED-CATEGORIES + + _RSS-CAT !
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-CAT @ _RSS-DECODE-CATEGORY
                ?DUP IF EXIT THEN 1 _RSS-COUNT2 +!
        ELSE _RSS-NA @ _RSS-NU @ S" atom:link" STR-STR= IF
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-ATOM-LINK-NS
                ?DUP IF EXIT THEN
            _RSS-CUR-A @ _RSS-CUR-U @ S" rel" _RSS-ATTR-TMP 64 _RSS-TMP
                _RSS-ATTR-TO DUP IF NIP EXIT THEN
            DROP
            IF _RSS-ATTR-TMP _RSS-TMP @ S" next" STR-STR= IF
                _RSS-DEST @ _RSS-FEED-NEXT-U + @ IF SYN-S-INVALID EXIT THEN
                _RSS-CUR-A @ _RSS-CUR-U @ S" href"
                    _RSS-DEST @ _RSS-FEED-NEXT + SYN-URL-CAP
                    _RSS-DEST @ _RSS-FEED-NEXT-U + _RSS-ATTR-TO
                    DUP IF NIP EXIT THEN DROP
                    0= IF SYN-S-MISSING EXIT THEN
            THEN THEN
        ELSE _RSS-NA @ _RSS-NU @ S" item" STR-STR= IF
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-COUNT @ SYN-MAX-ENTRIES >= IF SYN-S-CAPACITY EXIT THEN
            _RSS-COUNT @ _RSS-DEST @ _RSS-FEED-ITEM _RSS-ITEM !
            _RSS-CUR-A @ _RSS-CUR-U @ _RSS-ITEM @ _RSS-DECODE-ITEM
                ?DUP IF EXIT THEN
            _RSS-COUNT @ _RSS-DEST @ _RSS-UNIQUE? 0= IF SYN-S-INVALID EXIT THEN
            1 _RSS-COUNT +!
        THEN THEN THEN THEN THEN THEN THEN THEN THEN THEN
        _RSS-CUR-A @ _RSS-CUR-U @ MU-SKIP-ELEMENT
            _RSS-CUR-U ! _RSS-CUR-A !
    AGAIN ;

