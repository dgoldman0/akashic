: _RSS-DECODE-PREFLIGHT
    ( document-a document-u feed scratch scratch-u -- same status )
    3 PICK 0< IF SYN-S-INVALID EXIT THEN
    DUP 0< IF SYN-S-INVALID EXIT THEN
    3 PICK SYN-XML-DOCUMENT-CAP > IF SYN-S-CAPACITY EXIT THEN
    DUP RSS-FEED-SCRATCH-SIZE < IF SYN-S-CAPACITY EXIT THEN
    4 PICK 4 PICK SYN-SPAN-VALID? 0= IF SYN-S-INVALID EXIT THEN
    2 PICK RSS-FEED-SIZE SYN-SPAN-VALID? 0= IF SYN-S-INVALID EXIT THEN
    2DUP SYN-SPAN-VALID? 0= IF SYN-S-INVALID EXIT THEN
    4 PICK 4 PICK 3 PICK 3 PICK
        SYN-RANGES-OVERLAP? IF SYN-S-INVALID EXIT THEN
    2 PICK RSS-FEED-SIZE 3 PICK 3 PICK
        SYN-RANGES-OVERLAP? IF SYN-S-INVALID EXIT THEN
    SYN-S-OK ;

: RSS-FEED-DECODE
    ( document-a document-u feed scratch scratch-u -- status )
    _RSS-DECODE-PREFLIGHT DUP IF
        _SYN-DECODE-ARGS>STATUS EXIT
    THEN DROP
    _RSS-SCRATCH-U ! _RSS-SCRATCH ! _RSS-OUT ! _RSS-DOC-U ! _RSS-DOC-A !
    _RSS-SCRATCH @ RSS-FEED-INIT
    _RSS-DOC-A @ _RSS-DOC-U @ _RSS-SCRATCH @ _RSS-DECODE-INTO
        _RSS-STATUS !
    _RSS-STATUS @ SYN-S-OK = IF
        _RSS-SCRATCH @ _RSS-OUT @ RSS-FEED-SIZE CMOVE
    THEN
    _RSS-SCRATCH @ RSS-FEED-SCRATCH-SIZE 0 FILL _RSS-STATUS @ ;

