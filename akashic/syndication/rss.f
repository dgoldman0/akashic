CREATE _RSS-ATTR-TMP 64 ALLOT

: _RSS-CHILD  ( cursor-a cursor-u -- child-a child-u name-a name-u flag status )
    BEGIN
        MU-SKIP-WS
        DUP 0= IF 2DROP 0 0 0 0 0 SYN-S-INVALID EXIT THEN
        2DUP MU-TAG-TYPE
        DUP MU-T-CLOSE = IF DROP 2DROP 0 0 0 0 0 SYN-S-OK EXIT THEN
        DUP MU-T-COMMENT = IF
            DROP MU-SKIP-COMMENT
        ELSE
            DUP MU-T-OPEN = OVER MU-T-SELF-CLOSE = OR 0= IF
                DROP 2DROP 0 0 0 0 0 SYN-S-INVALID EXIT
            THEN
            DROP 2DUP MU-GET-TAG-NAME _RSS-NU ! _RSS-NA ! 2DROP
            _RSS-NA @ _RSS-NU @ -1 SYN-S-OK EXIT
        THEN
    AGAIN ;

: _RSS-COPY  ( element-a element-u dest cap length-cell -- status )
    SX-ELEMENT-TEXT ;

: _RSS-ATTR-TO
    ( element-a element-u key-a key-u dest cap len-cell -- found status )
    SX-ATTR-COPY ;

: _RSS-ELEMENT-NO-NS  ( element-a element-u -- status )
    S" xmlns" _RSS-ATTR-TMP 64 _RSS-TMP _RSS-ATTR-TO
    DUP IF NIP EXIT THEN DROP
    0= IF SYN-S-OK EXIT THEN
    _RSS-TMP @ 0= IF SYN-S-OK ELSE SYN-S-UNSUPPORTED THEN ;

: _RSS-ATOM-BINDING  ( element-a element-u -- binding status )
    S" xmlns:atom" _RSS-ATTR-TMP 64 _RSS-TMP _RSS-ATTR-TO
    DUP IF NIP 0 SWAP EXIT THEN DROP
    0= IF 0 SYN-S-OK EXIT THEN
    _RSS-ATTR-TMP _RSS-TMP @ S" http://www.w3.org/2005/Atom" STR-STR= IF
        1
    ELSE
        2
    THEN
    SYN-S-OK ;

: _RSS-PARSE-UINT  ( a u -- n status )
    DUP 0= IF 2DROP 0 SYN-S-INVALID EXIT THEN
    0 _RSS-TMP !
    0 ?DO
        DUP I + C@ DUP 48 < OVER 57 > OR IF
            2DROP 0 SYN-S-INVALID UNLOOP EXIT
        THEN
        48 - _RSS-DIGIT !
        _RSS-TMP @ 0x7FFFFFFFFFFFFFFF _RSS-DIGIT @ - 10 / > IF
            DROP 0 SYN-S-CAPACITY UNLOOP EXIT
        THEN
        _RSS-TMP @ 10 * _RSS-DIGIT @ + _RSS-TMP !
    LOOP DROP _RSS-TMP @ SYN-S-OK ;

: _RSS-DECODE-CATEGORY  ( element-a element-u category -- status )
    >R _RSS-ELEM-U ! _RSS-ELEM-A !
    _RSS-ELEM-A @ _RSS-ELEM-U @ R@ _RSS-CAT-VALUE + SYN-TAG-CAP
        R@ _RSS-CAT-VALUE-U + _RSS-COPY ?DUP IF R> DROP EXIT THEN
    R@ _RSS-CAT-VALUE-U + @ 0= IF R> DROP SYN-S-INVALID EXIT THEN
    _RSS-ELEM-A @ _RSS-ELEM-U @ S" domain"
        R@ _RSS-CAT-DOMAIN + 256 R@ _RSS-CAT-DOMAIN-U +
        _RSS-ATTR-TO DUP IF NIP R> DROP EXIT THEN DROP DROP
    R> DROP SYN-S-OK ;

: _RSS-DECODE-ENCLOSURE  ( element-a element-u enclosure -- status )
    >R _RSS-ELEM-U ! _RSS-ELEM-A !
    _RSS-ELEM-A @ _RSS-ELEM-U @ S" url"
        R@ _RSS-ENC-URL + SYN-URL-CAP R@ _RSS-ENC-URL-U +
        _RSS-ATTR-TO DUP IF NIP R> DROP EXIT THEN
    DROP 0= IF R> DROP SYN-S-MISSING EXIT THEN
    _RSS-ELEM-A @ _RSS-ELEM-U @ S" type"
        R@ _RSS-ENC-TYPE + SYN-MIME-CAP R@ _RSS-ENC-TYPE-U +
        _RSS-ATTR-TO DUP IF NIP R> DROP EXIT THEN
    DROP 0= IF R> DROP SYN-S-MISSING EXIT THEN
    _RSS-ELEM-A @ _RSS-ELEM-U @ S" length"
        _RSS-ATTR-TMP 64 _RSS-TMP _RSS-ATTR-TO
        DUP IF NIP R> DROP EXIT THEN
    DROP 0= IF R> DROP SYN-S-MISSING EXIT THEN
    _RSS-ATTR-TMP _RSS-TMP @ _RSS-PARSE-UINT
        DUP IF NIP R> DROP EXIT THEN DROP R@ _RSS-ENC-LENGTH + !
    RSS-ENCLOSURE-HAS-LENGTH R@ _RSS-ENC-FLAGS + !
    R> DROP SYN-S-OK ;

: _RSS-DECODE-GUID  ( element-a element-u item -- status )
    >R _RSS-ELEM-U ! _RSS-ELEM-A !
    _RSS-ELEM-A @ _RSS-ELEM-U @
        R@ _RSS-ITEM-GUID + SYN-ID-CAP R@ _RSS-ITEM-GUID-U +
        _RSS-COPY ?DUP IF R> DROP EXIT THEN
    R@ _RSS-ITEM-GUID-U + @ 0= IF R> DROP SYN-S-INVALID EXIT THEN
    RSS-GUID-IS-PERMALINK R@ _RSS-ITEM-GUID-FLAGS + !
    _RSS-ELEM-A @ _RSS-ELEM-U @ S" isPermaLink"
        _RSS-ATTR-TMP 64 _RSS-TMP _RSS-ATTR-TO
        DUP IF NIP R> DROP EXIT THEN
    DROP
    IF
        _RSS-ATTR-TMP _RSS-TMP @ S" true" STR-STR= IF
            RSS-GUID-IS-PERMALINK R@ _RSS-ITEM-GUID-FLAGS + !
        ELSE _RSS-ATTR-TMP _RSS-TMP @ S" false" STR-STR= IF
            0 R@ _RSS-ITEM-GUID-FLAGS + !
        ELSE R> DROP SYN-S-INVALID EXIT THEN THEN
    THEN
    R> DROP SYN-S-OK ;

: _RSS-DECODE-ITEM  ( element-a element-u item -- status )
    _RSS-ITEM ! 0 _RSS-ITEM-FLAGS ! 0 _RSS-ITEM-COUNT !
    MU-ENTER _RSS-ITEM-CUR-U ! _RSS-ITEM-CUR-A !
    BEGIN
        _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @ _RSS-CHILD
        DUP IF >R 2DROP 2DROP DROP R> EXIT THEN DROP
        0= IF
            2DROP 2DROP
            _RSS-ITEM-COUNT @ _RSS-ITEM @ _RSS-ITEM-CATEGORY-COUNT + !
            _RSS-ITEM @ _RSS-ITEM-GUID-U + @ IF
                RSS-IDENTITY-GUID
                    _RSS-ITEM @ _RSS-ITEM-IDENTITY-KIND + !
            ELSE _RSS-ITEM @ _RSS-ITEM-LINK-U + @ IF
                RSS-IDENTITY-LINK
                    _RSS-ITEM @ _RSS-ITEM-IDENTITY-KIND + !
            ELSE SYN-S-MISSING EXIT THEN THEN
            _RSS-ITEM @ _RSS-ITEM-TITLE-U + @
            _RSS-ITEM @ _RSS-ITEM-DESCRIPTION-U + @ OR 0= IF
                SYN-S-MISSING EXIT
            THEN
            SYN-S-OK EXIT
        THEN
        _RSS-NU ! _RSS-NA ! _RSS-ITEM-CUR-U ! _RSS-ITEM-CUR-A !
        _RSS-NA @ _RSS-NU @ S" guid" STR-STR= IF
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-ITEM-FLAGS @ 1 AND IF SYN-S-INVALID EXIT THEN
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @
                _RSS-ITEM @ _RSS-DECODE-GUID
                ?DUP IF EXIT THEN 1 _RSS-ITEM-FLAGS +!
        ELSE _RSS-NA @ _RSS-NU @ S" title" STR-STR= IF
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-ITEM-FLAGS @ 2 AND IF SYN-S-INVALID EXIT THEN
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @
                _RSS-ITEM @ _RSS-ITEM-TITLE +
                SYN-TITLE-CAP _RSS-ITEM @ _RSS-ITEM-TITLE-U + _RSS-COPY
                ?DUP IF EXIT THEN 2 _RSS-ITEM-FLAGS +!
        ELSE _RSS-NA @ _RSS-NU @ S" link" STR-STR= IF
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-ITEM-FLAGS @ 4 AND IF SYN-S-INVALID EXIT THEN
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @
                _RSS-ITEM @ _RSS-ITEM-LINK +
                SYN-URL-CAP _RSS-ITEM @ _RSS-ITEM-LINK-U + _RSS-COPY
                ?DUP IF EXIT THEN 4 _RSS-ITEM-FLAGS +!
        ELSE _RSS-NA @ _RSS-NU @ S" description" STR-STR= IF
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-ITEM-FLAGS @ 8 AND IF SYN-S-INVALID EXIT THEN
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @
                _RSS-ITEM @ _RSS-ITEM-DESCRIPTION +
                SYN-CONTENT-CAP _RSS-ITEM @ _RSS-ITEM-DESCRIPTION-U +
                _RSS-COPY ?DUP IF EXIT THEN 8 _RSS-ITEM-FLAGS +!
        ELSE _RSS-NA @ _RSS-NU @ S" author" STR-STR= IF
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-ITEM-FLAGS @ 16 AND IF SYN-S-INVALID EXIT THEN
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @
                _RSS-ITEM @ _RSS-ITEM-AUTHOR + 256
                _RSS-ITEM @ _RSS-ITEM-AUTHOR-U + _RSS-COPY
                ?DUP IF EXIT THEN 16 _RSS-ITEM-FLAGS +!
        ELSE _RSS-NA @ _RSS-NU @ S" pubDate" STR-STR= IF
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-ITEM-FLAGS @ 32 AND IF SYN-S-INVALID EXIT THEN
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @
                _RSS-ITEM @ _RSS-ITEM-PUBDATE +
                SYN-DATETIME-CAP _RSS-ITEM @ _RSS-ITEM-PUBDATE-U +
                _RSS-COPY ?DUP IF EXIT THEN 32 _RSS-ITEM-FLAGS +!
        ELSE _RSS-NA @ _RSS-NU @ S" category" STR-STR= IF
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-ITEM-COUNT @ SYN-MAX-TAGS >= IF SYN-S-CAPACITY EXIT THEN
            _RSS-ITEM-COUNT @ RSS-CATEGORY-SIZE *
                _RSS-ITEM @ _RSS-ITEM-CATEGORIES + + _RSS-CAT !
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @
                _RSS-CAT @ _RSS-DECODE-CATEGORY
                ?DUP IF EXIT THEN 1 _RSS-ITEM-COUNT +!
        ELSE _RSS-NA @ _RSS-NU @ S" enclosure" STR-STR= IF
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @ _RSS-ELEMENT-NO-NS
                ?DUP IF EXIT THEN
            _RSS-ITEM @ _RSS-ITEM-HAS-ENCLOSURE + @ IF
                SYN-S-INVALID EXIT
            THEN
            _RSS-ITEM @ _RSS-ITEM-ENCLOSURE + _RSS-ENC !
            _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @
                _RSS-ENC @ _RSS-DECODE-ENCLOSURE
                ?DUP IF EXIT THEN -1 _RSS-ITEM @ _RSS-ITEM-HAS-ENCLOSURE + !
        THEN THEN THEN THEN THEN THEN THEN THEN
        _RSS-ITEM-CUR-A @ _RSS-ITEM-CUR-U @ MU-SKIP-ELEMENT
            _RSS-ITEM-CUR-U ! _RSS-ITEM-CUR-A !
    AGAIN ;

