: _ATOM-DECODE-PERSON  ( element-a element-u person -- status )
    _ATOM-TARGET ! _ATOM-ELEM-U ! _ATOM-ELEM-A ! 0 _ATOM-PERSON-FLAGS !
    _ATOM-ELEM-A @ _ATOM-ELEM-U @ MU-ENTER
        _ATOM-PERSON-CUR-U ! _ATOM-PERSON-CUR-A !
    BEGIN
        _ATOM-PERSON-CUR-A @ _ATOM-PERSON-CUR-U @ _ATOM-CHILD
        DUP IF >R 2DROP 2DROP DROP R> EXIT THEN DROP
        0= IF
            2DROP 2DROP
            _ATOM-PERSON-FLAGS @ 1 AND
                IF SYN-S-OK ELSE SYN-S-MISSING THEN EXIT
        THEN
        _ATOM-NU ! _ATOM-NA !
        _ATOM-PERSON-CUR-U ! _ATOM-PERSON-CUR-A !
        _ATOM-NA @ _ATOM-NU @ S" name" STR-STR= IF
            _ATOM-PERSON-CUR-A @ _ATOM-PERSON-CUR-U @ _ATOM-ELEMENT-NS
                ?DUP IF EXIT THEN
            _ATOM-PERSON-FLAGS @ 1 AND IF SYN-S-INVALID EXIT THEN
            _ATOM-PERSON-CUR-A @ _ATOM-PERSON-CUR-U @
                _ATOM-TARGET @ _ATOM-PERSON-NAME +
                SYN-AUTHOR-NAME-CAP _ATOM-TARGET @ _ATOM-PERSON-NAME-U +
                SX-ELEMENT-TEXT ?DUP IF EXIT THEN 1 _ATOM-PERSON-FLAGS +!
        ELSE _ATOM-NA @ _ATOM-NU @ S" email" STR-STR= IF
            _ATOM-PERSON-CUR-A @ _ATOM-PERSON-CUR-U @ _ATOM-ELEMENT-NS
                ?DUP IF EXIT THEN
            _ATOM-PERSON-FLAGS @ 2 AND IF SYN-S-INVALID EXIT THEN
            _ATOM-PERSON-CUR-A @ _ATOM-PERSON-CUR-U @
                _ATOM-TARGET @ _ATOM-PERSON-EMAIL +
                256 _ATOM-TARGET @ _ATOM-PERSON-EMAIL-U +
                SX-ELEMENT-TEXT ?DUP IF EXIT THEN 2 _ATOM-PERSON-FLAGS +!
        ELSE _ATOM-NA @ _ATOM-NU @ S" uri" STR-STR= IF
            _ATOM-PERSON-CUR-A @ _ATOM-PERSON-CUR-U @ _ATOM-ELEMENT-NS
                ?DUP IF EXIT THEN
            _ATOM-PERSON-FLAGS @ 4 AND IF SYN-S-INVALID EXIT THEN
            _ATOM-PERSON-CUR-A @ _ATOM-PERSON-CUR-U @
                _ATOM-TARGET @ _ATOM-PERSON-URI +
                SYN-URL-CAP _ATOM-TARGET @ _ATOM-PERSON-URI-U +
                SX-ELEMENT-TEXT ?DUP IF EXIT THEN 4 _ATOM-PERSON-FLAGS +!
        THEN THEN THEN
        _ATOM-PERSON-CUR-A @ _ATOM-PERSON-CUR-U @ MU-SKIP-ELEMENT
            _ATOM-PERSON-CUR-U ! _ATOM-PERSON-CUR-A !
    AGAIN ;

