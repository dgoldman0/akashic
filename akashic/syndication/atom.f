CREATE _ATOM-TMP 128 ALLOT

: _ATOM-CHILD  ( cursor-a cursor-u -- child-a child-u name-a name-u flag status )
    BEGIN
        MU-SKIP-WS
        DUP 0= IF 2DROP 0 0 0 0 0 SYN-S-INVALID EXIT THEN
        2DUP MU-TAG-TYPE
        DUP MU-T-CLOSE = IF DROP 2DROP 0 0 0 0 0 SYN-S-OK EXIT THEN
        DUP MU-T-COMMENT = IF
            DROP MU-SKIP-COMMENT
        ELSE
            DUP MU-T-OPEN = OVER MU-T-SELF-CLOSE = OR 0= IF
                DROP 2DROP 0 0 0 0 0 SYN-S-INVALID EXIT
            THEN
            DROP 2DUP MU-GET-TAG-NAME _ATOM-NU ! _ATOM-NA ! 2DROP
            _ATOM-NA @ _ATOM-NU @ -1 SYN-S-OK EXIT
        THEN
    AGAIN ;

: _ATOM-ATTR
    ( element-a element-u key-a key-u dest cap len-cell -- found status )
    SX-ATTR-COPY ;

: _ATOM-OPTIONAL-ATTR
    ( element-a element-u key-a key-u dest cap len-cell -- status )
    _ATOM-ATTR DUP IF NIP EXIT THEN DROP DROP SYN-S-OK ;

: _ATOM-REQUIRED-ATTR
    ( element-a element-u key-a key-u dest cap len-cell -- status )
    _ATOM-ATTR DUP IF NIP EXIT THEN DROP
    IF SYN-S-OK ELSE SYN-S-MISSING THEN ;

: _ATOM-ELEMENT-NS  ( element-a element-u -- status )
    S" xmlns" _ATOM-TMP 128 _ATOM-TMP-U _ATOM-ATTR
    DUP IF NIP EXIT THEN DROP
    0= IF SYN-S-OK EXIT THEN
    _ATOM-TMP _ATOM-TMP-U @ S" http://www.w3.org/2005/Atom" STR-STR= IF
        SYN-S-OK
    ELSE
        SYN-S-UNSUPPORTED
    THEN ;

