\ =====================================================================
\  thread-codec.f - Checksummed native conversation snapshot format
\ =====================================================================
\  Version 1 is a bounded little-endian MegaPad format. It contains no VFS,
\  Desk, TUI, provider, or credential behavior.
\ =====================================================================

PROVIDED akashic-agent-thread-codec

REQUIRE ../conversation.f
REQUIRE ../../math/crc.f
