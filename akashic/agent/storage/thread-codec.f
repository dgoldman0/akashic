REQUIRE ../../utils/string.f

0 CONSTANT ATHREAD-S-OK
1 CONSTANT ATHREAD-S-INVALID
2 CONSTANT ATHREAD-S-CAPACITY
3 CONSTANT ATHREAD-S-CHECKSUM
4 CONSTANT ATHREAD-S-NOMEM

1 CONSTANT ATHREAD-FORMAT-VERSION
80 CONSTANT ATHREAD-HEADER-SIZE
48 CONSTANT ATHREAD-RECORD-SIZE
72 CONSTANT ATHREAD-CONTEXT-RECORD-SIZE
262144 CONSTANT ATHREAD-MAX-SNAPSHOT

CREATE _ATHREAD-MAGIC
65 C, 75 C, 84 C, 72 C, 82 C, 48 C, 48 C, 49 C,

 0 CONSTANT _ATH-H-MAGIC
 8 CONSTANT _ATH-H-VERSION
16 CONSTANT _ATH-H-GENERATION
24 CONSTANT _ATH-H-PAYLOAD-U
32 CONSTANT _ATH-H-MESSAGE-N
40 CONSTANT _ATH-H-CONTEXT-N
48 CONSTANT _ATH-H-CONV-ID
56 CONSTANT _ATH-H-REVISION
64 CONSTANT _ATH-H-CONTEXT-REVISION
72 CONSTANT _ATH-H-CRC32

 0 CONSTANT _ATH-R-ROLE
 8 CONSTANT _ATH-R-STATE
16 CONSTANT _ATH-R-RUN-ID
24 CONSTANT _ATH-R-TIME-MS
32 CONSTANT _ATH-R-FLAGS
40 CONSTANT _ATH-R-TEXT-U

 0 CONSTANT _ATH-C-KIND
 8 CONSTANT _ATH-C-ROLE
16 CONSTANT _ATH-C-RUN-ID
24 CONSTANT _ATH-C-FLAGS
32 CONSTANT _ATH-C-STATUS
40 CONSTANT _ATH-C-SOURCE-U
48 CONSTANT _ATH-C-NAME-U
56 CONSTANT _ATH-C-CALL-U
64 CONSTANT _ATH-C-DATA-U

: _ATHREAD-ALIGN8  ( u -- u' )
    7 + -8 AND ;

VARIABLE _ATHE-GEN
VARIABLE _ATHE-CONV
VARIABLE _ATHE-BUF
VARIABLE _ATHE-CAP
VARIABLE _ATHE-POS
VARIABLE _ATHE-MSG
VARIABLE _ATHE-DST
VARIABLE _ATHE-TEXT-U
VARIABLE _ATHE-PAD-U
VARIABLE _ATHE-ITEM
VARIABLE _ATHE-SOURCE-A
VARIABLE _ATHE-SOURCE-U
VARIABLE _ATHE-NAME-A
VARIABLE _ATHE-NAME-U
VARIABLE _ATHE-CALL-A
VARIABLE _ATHE-CALL-U
VARIABLE _ATHE-DATA-A
VARIABLE _ATHE-DATA-U
VARIABLE _ATHE-STR-POS

: _ATHREAD-ENCODE-MESSAGE  ( message -- status )
    DUP _ATHE-MSG ! AMSG.TEXT-U @ DUP _ATHE-TEXT-U !
    DUP 0< IF DROP ATHREAD-S-INVALID EXIT THEN
    _ATHREAD-ALIGN8 _ATHE-PAD-U !
    _ATHE-POS @ ATHREAD-RECORD-SIZE + _ATHE-PAD-U @ +
    _ATHE-CAP @ ATHREAD-HEADER-SIZE - > IF ATHREAD-S-CAPACITY EXIT THEN
    _ATHE-BUF @ ATHREAD-HEADER-SIZE + _ATHE-POS @ + DUP _ATHE-DST !
    ATHREAD-RECORD-SIZE _ATHE-PAD-U @ + 0 FILL
    _ATHE-MSG @ AMSG.ROLE @ _ATHE-DST @ _ATH-R-ROLE + !
    _ATHE-MSG @ AMSG.STATE @ _ATHE-DST @ _ATH-R-STATE + !
    _ATHE-MSG @ AMSG.RUN-ID @ _ATHE-DST @ _ATH-R-RUN-ID + !
    _ATHE-MSG @ AMSG.TIME-MS @ _ATHE-DST @ _ATH-R-TIME-MS + !
    _ATHE-MSG @ AMSG.FLAGS @ _ATHE-DST @ _ATH-R-FLAGS + !
    _ATHE-TEXT-U @ _ATHE-DST @ _ATH-R-TEXT-U + !
    _ATHE-TEXT-U @ IF
        _ATHE-MSG @ AMSG.TEXT-A @ DUP 0= IF
            DROP ATHREAD-S-INVALID EXIT
        THEN
        DUP _ATHE-TEXT-U @ UTF8-VALID? 0= IF
            DROP ATHREAD-S-INVALID EXIT
        THEN
        _ATHE-DST @ ATHREAD-RECORD-SIZE + _ATHE-TEXT-U @ CMOVE
    THEN
    ATHREAD-RECORD-SIZE _ATHE-PAD-U @ + _ATHE-POS +!
    ATHREAD-S-OK ;

: _ATHREAD-ENCODE-CONTEXT  ( item -- status )
    DUP _ATHE-ITEM ! ACTXI-VALID? 0= IF ATHREAD-S-INVALID EXIT THEN
    _ATHE-ITEM @ ACTXI-SOURCE-TEXT _ATHE-SOURCE-U ! _ATHE-SOURCE-A !
    _ATHE-ITEM @ ACTXI-NAME-TEXT _ATHE-NAME-U ! _ATHE-NAME-A !
    _ATHE-ITEM @ ACTXI-CALL-ID-TEXT _ATHE-CALL-U ! _ATHE-CALL-A !
    _ATHE-ITEM @ ACTXI-DATA-TEXT _ATHE-DATA-U ! _ATHE-DATA-A !
    ATHREAD-CONTEXT-RECORD-SIZE
    _ATHE-SOURCE-U @ + _ATHE-NAME-U @ + _ATHE-CALL-U @ + _ATHE-DATA-U @ +
    _ATHREAD-ALIGN8 DUP _ATHE-PAD-U !
    _ATHE-POS @ + _ATHE-CAP @ ATHREAD-HEADER-SIZE - > IF
        ATHREAD-S-CAPACITY EXIT
    THEN
    _ATHE-BUF @ ATHREAD-HEADER-SIZE + _ATHE-POS @ + DUP _ATHE-DST !
    _ATHE-PAD-U @ 0 FILL
    _ATHE-ITEM @ ACTXI.KIND @ _ATHE-DST @ _ATH-C-KIND + !
    _ATHE-ITEM @ ACTXI.ROLE @ _ATHE-DST @ _ATH-C-ROLE + !
    _ATHE-ITEM @ ACTXI.RUN-ID @ _ATHE-DST @ _ATH-C-RUN-ID + !
    _ATHE-ITEM @ ACTXI.FLAGS @ _ATHE-DST @ _ATH-C-FLAGS + !
    _ATHE-ITEM @ ACTXI.STATUS @ _ATHE-DST @ _ATH-C-STATUS + !
    _ATHE-SOURCE-U @ _ATHE-DST @ _ATH-C-SOURCE-U + !
    _ATHE-NAME-U @ _ATHE-DST @ _ATH-C-NAME-U + !
    _ATHE-CALL-U @ _ATHE-DST @ _ATH-C-CALL-U + !
    _ATHE-DATA-U @ _ATHE-DST @ _ATH-C-DATA-U + !
    ATHREAD-CONTEXT-RECORD-SIZE _ATHE-STR-POS !
    _ATHE-SOURCE-U @ IF
        _ATHE-SOURCE-A @ _ATHE-DST @ _ATHE-STR-POS @ +
        _ATHE-SOURCE-U @ CMOVE _ATHE-SOURCE-U @ _ATHE-STR-POS +!
    THEN
    _ATHE-NAME-U @ IF
        _ATHE-NAME-A @ _ATHE-DST @ _ATHE-STR-POS @ +
        _ATHE-NAME-U @ CMOVE _ATHE-NAME-U @ _ATHE-STR-POS +!
    THEN
    _ATHE-CALL-U @ IF
        _ATHE-CALL-A @ _ATHE-DST @ _ATHE-STR-POS @ +
        _ATHE-CALL-U @ CMOVE _ATHE-CALL-U @ _ATHE-STR-POS +!
    THEN
    _ATHE-DATA-U @ IF
        _ATHE-DATA-A @ _ATHE-DST @ _ATHE-STR-POS @ +
        _ATHE-DATA-U @ CMOVE
    THEN
    _ATHE-PAD-U @ _ATHE-POS +!
    ATHREAD-S-OK ;

: ATHREAD-ENCODE  ( generation conversation buffer capacity -- length status )
    _ATHE-CAP ! _ATHE-BUF ! _ATHE-CONV ! _ATHE-GEN !
    _ATHE-GEN @ 1 < _ATHE-CONV @ 0= OR _ATHE-BUF @ 0= OR IF
        0 ATHREAD-S-INVALID EXIT
    THEN
    _ATHE-CAP @ ATHREAD-HEADER-SIZE <
    _ATHE-CAP @ ATHREAD-MAX-SNAPSHOT > OR IF
        0 ATHREAD-S-CAPACITY EXIT
    THEN
    _ATHE-CONV @ ACONV.COUNT @ DUP 0< SWAP ACONV-MAX-MESSAGES > OR IF
        0 ATHREAD-S-INVALID EXIT
    THEN
    _ATHE-BUF @ ATHREAD-HEADER-SIZE 0 FILL
    0 _ATHE-POS !
    _ATHE-CONV @ ACONV.COUNT @ 0 ?DO
        I _ATHE-CONV @ ACONV-NTH _ATHREAD-ENCODE-MESSAGE
        ?DUP IF 0 SWAP UNLOOP EXIT THEN
    LOOP
    _ATHE-CONV @ ACONV.MODEL-CONTEXT @ ACTX.COUNT @ 0 ?DO
        I _ATHE-CONV @ ACONV.MODEL-CONTEXT @ ACTX-NTH
        _ATHREAD-ENCODE-CONTEXT ?DUP IF 0 SWAP UNLOOP EXIT THEN
    LOOP
    _ATHREAD-MAGIC _ATHE-BUF @ _ATH-H-MAGIC + 8 CMOVE
    ATHREAD-FORMAT-VERSION _ATHE-BUF @ _ATH-H-VERSION + !
    _ATHE-GEN @ _ATHE-BUF @ _ATH-H-GENERATION + !
    _ATHE-POS @ _ATHE-BUF @ _ATH-H-PAYLOAD-U + !
    _ATHE-CONV @ ACONV.COUNT @ _ATHE-BUF @ _ATH-H-MESSAGE-N + !
    _ATHE-CONV @ ACONV.MODEL-CONTEXT @ ACTX.COUNT @
    _ATHE-BUF @ _ATH-H-CONTEXT-N + !
    _ATHE-CONV @ ACONV.ID @ _ATHE-BUF @ _ATH-H-CONV-ID + !
    _ATHE-CONV @ ACONV.REVISION @ _ATHE-BUF @ _ATH-H-REVISION + !
    _ATHE-CONV @ ACONV.MODEL-CONTEXT @ ACTX.REVISION @
    _ATHE-BUF @ _ATH-H-CONTEXT-REVISION + !
    _ATHE-BUF @ ATHREAD-HEADER-SIZE + _ATHE-POS @ CRC32
    _ATHE-BUF @ _ATH-H-CRC32 + !
    ATHREAD-HEADER-SIZE _ATHE-POS @ + ATHREAD-S-OK ;

