: _ATHREAD-DECODE-FAIL  ( status -- conversation generation status )
    0 0 ROT ;

: _ATHREAD-STATE  ( stored-state -- state recovered? )
    DUP AMSG-S-STREAMING = OVER AMSG-S-PENDING = OR
    OVER AMSG-S-APPROVAL = OR IF
        DROP AMSG-S-CANCELLED -1
    ELSE
        0
    THEN ;

: _ATHREAD-VALID-RECORD?  ( record -- flag )
    _ATHD-REC !
    _ATHD-REC @ _ATH-R-ROLE + @ DUP AROLE-USER < SWAP AROLE-SYSTEM > OR IF
        0 EXIT
    THEN
    _ATHD-REC @ _ATH-R-STATE + @ DUP 0< SWAP AMSG-S-CANCELLED > OR IF
        0 EXIT
    THEN
    _ATHD-REC @ _ATH-R-TEXT-U + @ DUP _ATHD-TEXT-U !
    DUP 0< IF DROP 0 EXIT THEN
    _ATHREAD-ALIGN8 DUP _ATHD-PAD-U !
    _ATHD-POS @ ATHREAD-RECORD-SIZE + SWAP +
    _ATHD-PAYLOAD-U @ > IF 0 EXIT THEN
    _ATHD-TEXT-U @ IF
        _ATHD-REC @ ATHREAD-RECORD-SIZE + _ATHD-TEXT-U @ UTF8-VALID? 0= IF
            0 EXIT
        THEN
    THEN
    ATHREAD-RECORD-SIZE _ATHD-PAD-U @ + _ATHD-POS +!
    -1 ;

: _ATHREAD-CONTEXT-STRING-VALID?  ( offset length -- flag )
    DUP 0< IF 2DROP 0 EXIT THEN
    >R _ATHD-CREC @ + R@ IF R@ UTF8-VALID? ELSE DROP -1 THEN
    R> DROP ;

: _ATHREAD-VALID-CONTEXT?  ( record -- flag )
    DUP _ATHD-CREC !
    DUP _ATH-C-KIND + @ DUP ACTX-K-MESSAGE < SWAP ACTX-K-PROVIDER > OR IF
        DROP 0 EXIT
    THEN
    DUP _ATH-C-RUN-ID + @ 0< IF DROP 0 EXIT THEN
    DUP _ATH-C-SOURCE-U + @ DUP _ATHD-CSOURCE-U ! 0< IF DROP 0 EXIT THEN
    DUP _ATH-C-NAME-U + @ DUP _ATHD-CNAME-U ! 0< IF DROP 0 EXIT THEN
    DUP _ATH-C-CALL-U + @ DUP _ATHD-CCALL-U ! 0< IF DROP 0 EXIT THEN
    _ATH-C-DATA-U + @ DUP _ATHD-CDATA-U ! 0< IF 0 EXIT THEN
    ATHREAD-CONTEXT-RECORD-SIZE _ATHD-CSOURCE-U @ +
    _ATHD-CNAME-U @ + _ATHD-CCALL-U @ + _ATHD-CDATA-U @ +
    _ATHREAD-ALIGN8 DUP _ATHD-CTOTAL-U !
    _ATHD-POS @ + _ATHD-PAYLOAD-U @ > IF 0 EXIT THEN
    ATHREAD-CONTEXT-RECORD-SIZE _ATHD-CP !
    _ATHD-CP @ _ATHD-CSOURCE-U @ _ATHREAD-CONTEXT-STRING-VALID? 0= IF
        0 EXIT
    THEN
    _ATHD-CSOURCE-U @ _ATHD-CP +!
    _ATHD-CP @ _ATHD-CNAME-U @ _ATHREAD-CONTEXT-STRING-VALID? 0= IF
        0 EXIT
    THEN
    _ATHD-CNAME-U @ _ATHD-CP +!
    _ATHD-CP @ _ATHD-CCALL-U @ _ATHREAD-CONTEXT-STRING-VALID? 0= IF
        0 EXIT
    THEN
    _ATHD-CCALL-U @ _ATHD-CP +!
    _ATHD-CP @ _ATHD-CDATA-U @ _ATHREAD-CONTEXT-STRING-VALID? 0= IF
        0 EXIT
    THEN
    _ATHD-CTOTAL-U @ _ATHD-POS +!
    -1 ;

: _ATHREAD-VALID?  ( -- status )
    _ATHD-LEN @ ATHREAD-HEADER-SIZE <
    _ATHD-LEN @ ATHREAD-MAX-SNAPSHOT > OR IF ATHREAD-S-INVALID EXIT THEN
    _ATHD-BUF @ 8 _ATHREAD-MAGIC 8 STR-STR= 0= IF
        ATHREAD-S-INVALID EXIT
    THEN
    _ATHD-BUF @ _ATH-H-VERSION + @ ATHREAD-FORMAT-VERSION <> IF
        ATHREAD-S-INVALID EXIT
    THEN
    _ATHD-BUF @ _ATH-H-GENERATION + @ 1 < IF ATHREAD-S-INVALID EXIT THEN
    _ATHD-BUF @ _ATH-H-CONV-ID + @ 1 < IF ATHREAD-S-INVALID EXIT THEN
    _ATHD-BUF @ _ATH-H-REVISION + @ 1 < IF ATHREAD-S-INVALID EXIT THEN
    _ATHD-BUF @ _ATH-H-CONTEXT-REVISION + @ 1 < IF
        ATHREAD-S-INVALID EXIT
    THEN
    _ATHD-BUF @ _ATH-H-PAYLOAD-U + @ DUP _ATHD-PAYLOAD-U !
    DUP 0< IF DROP ATHREAD-S-INVALID EXIT THEN
    ATHREAD-HEADER-SIZE + _ATHD-LEN @ <> IF ATHREAD-S-INVALID EXIT THEN
    _ATHD-BUF @ _ATH-H-MESSAGE-N + @ DUP _ATHD-COUNT !
    DUP 0< SWAP ACONV-MAX-MESSAGES > OR IF ATHREAD-S-INVALID EXIT THEN
    _ATHD-BUF @ _ATH-H-CONTEXT-N + @ DUP _ATHD-CONTEXT-N !
    DUP 0< SWAP ACTX-MAX-ITEMS > OR IF ATHREAD-S-INVALID EXIT THEN
    _ATHD-BUF @ ATHREAD-HEADER-SIZE + _ATHD-PAYLOAD-U @ CRC32
    _ATHD-BUF @ _ATH-H-CRC32 + @ <> IF ATHREAD-S-CHECKSUM EXIT THEN
    0 _ATHD-POS !
    _ATHD-COUNT @ 0 ?DO
        _ATHD-POS @ ATHREAD-RECORD-SIZE + _ATHD-PAYLOAD-U @ > IF
            ATHREAD-S-INVALID UNLOOP EXIT
        THEN
        _ATHD-BUF @ ATHREAD-HEADER-SIZE + _ATHD-POS @ +
        _ATHREAD-VALID-RECORD? 0= IF ATHREAD-S-INVALID UNLOOP EXIT THEN
    LOOP
    _ATHD-CONTEXT-N @ 0 ?DO
        _ATHD-POS @ ATHREAD-CONTEXT-RECORD-SIZE +
        _ATHD-PAYLOAD-U @ > IF ATHREAD-S-INVALID UNLOOP EXIT THEN
        _ATHD-BUF @ ATHREAD-HEADER-SIZE + _ATHD-POS @ +
        _ATHREAD-VALID-CONTEXT? 0= IF ATHREAD-S-INVALID UNLOOP EXIT THEN
    LOOP
    _ATHD-POS @ _ATHD-PAYLOAD-U @ <> IF ATHREAD-S-INVALID EXIT THEN
    ATHREAD-S-OK ;

: _ATHREAD-APPEND-RECOVERY  ( conversation -- )
    DUP ACONV.COUNT @ ACONV-MAX-MESSAGES >= IF DROP EXIT THEN
    DROP
    AROLE-SYSTEM AMSG-S-ERROR 0
    S" Previous agent run was interrupted before completion."
    _ATHD-CONV @ ACONV-APPEND DUP IF 2DROP EXIT THEN
    DROP DUP AMSG-F-AUDIT AMSG-F-RECOVERED OR SWAP AMSG.FLAGS ! DROP ;

: _ATHREAD-DECODE-RECORD  ( record -- status )
    _ATHD-REC !
    _ATHD-REC @ _ATH-R-STATE + @ _ATHREAD-STATE
    DUP _ATHD-THIS-RECOVERED !
    _ATHD-RECOVERED @ OR _ATHD-RECOVERED ! _ATHD-STATE !
    _ATHD-REC @ _ATH-R-ROLE + @ _ATHD-STATE @
    _ATHD-REC @ _ATH-R-RUN-ID + @
    _ATHD-REC @ ATHREAD-RECORD-SIZE +
    _ATHD-REC @ _ATH-R-TEXT-U + @ _ATHD-CONV @ ACONV-APPEND
    DUP IF NIP EXIT THEN
    DROP DUP _ATHD-MSG ! DROP
    _ATHD-REC @ _ATH-R-TIME-MS + @ _ATHD-MSG @ AMSG.TIME-MS !
    _ATHD-REC @ _ATH-R-FLAGS + @
    _ATHD-THIS-RECOVERED @ IF AMSG-F-RECOVERED OR THEN
    _ATHD-MSG @ AMSG.FLAGS !
    _ATHD-THIS-RECOVERED @ IF
        _ATHD-REC @ _ATH-R-RUN-ID + @ _ATHD-RECOVERED-RUN !
    THEN
    ATHREAD-S-OK ;

: _ATHREAD-DECODE-CONTEXT  ( record -- status )
    _ATHD-CREC !
    ATHREAD-CONTEXT-RECORD-SIZE _ATHD-CP !
    _ATHD-CREC @ _ATH-C-KIND + @
    _ATHD-CREC @ _ATH-C-ROLE + @
    _ATHD-CREC @ _ATH-C-RUN-ID + @
    _ATHD-CREC @ _ATH-C-FLAGS + @
    _ATHD-CREC @ _ATH-C-STATUS + @
    _ATHD-CREC @ _ATHD-CP @ +
    _ATHD-CREC @ _ATH-C-SOURCE-U + @ DUP _ATHD-CSOURCE-U !
    DUP _ATHD-CP +!
    _ATHD-CREC @ _ATHD-CP @ +
    _ATHD-CREC @ _ATH-C-NAME-U + @ DUP _ATHD-CNAME-U !
    DUP _ATHD-CP +!
    _ATHD-CREC @ _ATHD-CP @ +
    _ATHD-CREC @ _ATH-C-CALL-U + @ DUP _ATHD-CCALL-U !
    DUP _ATHD-CP +!
    _ATHD-CREC @ _ATHD-CP @ +
    _ATHD-CREC @ _ATH-C-DATA-U + @ DUP _ATHD-CDATA-U !
    _ATHD-CONV @ ACONV.MODEL-CONTEXT @ ACTX-APPEND-RAW
    DUP IF NIP DROP ATHREAD-S-NOMEM EXIT THEN
    2DROP ATHREAD-S-OK ;

: ATHREAD-DECODE  ( buffer length -- conversation generation status )
    _ATHD-LEN ! _ATHD-BUF !
    _ATHD-BUF @ 0= IF ATHREAD-S-INVALID _ATHREAD-DECODE-FAIL EXIT THEN
    _ATHREAD-VALID? ?DUP IF _ATHREAD-DECODE-FAIL EXIT THEN
    ACONV-NEW DUP IF
        2DROP ATHREAD-S-NOMEM _ATHREAD-DECODE-FAIL EXIT
    THEN
    DROP DUP _ATHD-CONV ! DROP
    0 _ATHD-RECOVERED ! 0 _ATHD-RECOVERED-RUN ! 0 _ATHD-POS !
    _ATHD-COUNT @ 0 ?DO
        _ATHD-BUF @ ATHREAD-HEADER-SIZE + _ATHD-POS @ + DUP _ATHD-REC !
        _ATHREAD-DECODE-RECORD ?DUP IF
            _ATHD-CONV @ ACONV-FREE
            ATHREAD-S-NOMEM _ATHREAD-DECODE-FAIL UNLOOP EXIT
        THEN
        _ATHD-REC @ _ATH-R-TEXT-U + @ _ATHREAD-ALIGN8
        ATHREAD-RECORD-SIZE + _ATHD-POS +!
    LOOP
    _ATHD-CONTEXT-N @ 0 ?DO
        _ATHD-BUF @ ATHREAD-HEADER-SIZE + _ATHD-POS @ + DUP _ATHD-CREC !
        _ATHREAD-DECODE-CONTEXT ?DUP IF
            _ATHD-CONV @ ACONV-FREE
            ATHREAD-S-NOMEM _ATHREAD-DECODE-FAIL UNLOOP EXIT
        THEN
        ATHREAD-CONTEXT-RECORD-SIZE
        _ATHD-CREC @ _ATH-C-SOURCE-U + @ +
        _ATHD-CREC @ _ATH-C-NAME-U + @ +
        _ATHD-CREC @ _ATH-C-CALL-U + @ +
        _ATHD-CREC @ _ATH-C-DATA-U + @ +
        _ATHREAD-ALIGN8 _ATHD-POS +!
    LOOP
    _ATHD-BUF @ _ATH-H-CONV-ID + @ _ATHD-CONV @ ACONV.ID !
    _ATHD-BUF @ _ATH-H-REVISION + @ _ATHD-CONV @ ACONV.REVISION !
    _ATHD-BUF @ _ATH-H-CONTEXT-REVISION + @
    _ATHD-CONV @ ACONV.MODEL-CONTEXT @ ACTX.REVISION !
    0 _ATHD-CONV @ ACONV.ACTIVE-RUN !
    _ATHD-RECOVERED-RUN @ ?DUP IF
        _ATHD-CONV @ ACONV.MODEL-CONTEXT @ ACTX-DROP-RUN
    THEN
    _ATHD-RECOVERED @ IF
        _ATHD-CONV @ ACONV.COUNT @ ACONV-MAX-MESSAGES >= IF
            1 _ATHD-CONV @ ACONV.REVISION +!
        ELSE
            _ATHD-CONV @ _ATHREAD-APPEND-RECOVERY
        THEN
    THEN
    _ATHD-CONV @ _ATHD-BUF @ _ATH-H-GENERATION + @ ATHREAD-S-OK ;
