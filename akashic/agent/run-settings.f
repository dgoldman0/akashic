    _ARPN-N ! _ARPN-A ! _ARPN-I !
    _ARPN-I @ 0< _ARPN-I @ _ARPN-N @ >= OR IF 0 EXIT THEN
    _ARPN-A @ _ARPN-I @ 8 * + @ ;

