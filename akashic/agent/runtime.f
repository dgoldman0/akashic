: _ARUNTIME-NEW-CLEANUP  ( -- )
    _ARN-CONV @ ?DUP IF ACONV-FREE THEN
    _ARN-EVENTS @ ?DUP IF AEQ-FREE THEN
    _ARN-RUNTIME @ ?DUP IF FREE THEN
    0 _ARN-CONV ! 0 _ARN-EVENTS ! 0 _ARN-RUNTIME ! ;

: ARUNTIME-NEW  ( provider -- runtime ior )
    _ARN-PROVIDER ! 0 _ARN-RUNTIME ! 0 _ARN-EVENTS ! 0 _ARN-CONV !
    AGENT-RUNTIME-SIZE ALLOCATE DUP IF SWAP DROP 0 SWAP EXIT THEN
    DROP DUP _ARN-RUNTIME ! AGENT-RUNTIME-SIZE 0 FILL
    AEQ-NEW DUP IF 2DROP _ARUNTIME-NEW-CLEANUP 0 1 EXIT THEN
    DROP _ARN-EVENTS !
    ACONV-NEW DUP IF 2DROP _ARUNTIME-NEW-CLEANUP 0 1 EXIT THEN
    DROP _ARN-CONV !
    _ARN-PROVIDER @ _ARN-RUNTIME @ ARUNTIME.PROVIDER !
    _ARN-EVENTS @ _ARN-RUNTIME @ ARUNTIME.EVENTS !
    _ARN-CONV @ _ARN-RUNTIME @ ARUNTIME.CONVERSATION !
    1 _ARN-RUNTIME @ ARUNTIME.NEXT-RUN !
    1 _ARN-RUNTIME @ ARUNTIME.REVISION !
    ARUN-S-OFFLINE _ARN-RUNTIME @ ARUNTIME.STATUS !
    _ARN-EVENTS @ _ARN-PROVIDER @ APROV-CONNECT IF
        ARUN-S-ERROR _ARN-RUNTIME @ ARUNTIME.STATUS !
    THEN
    _ARN-RUNTIME @ 0 ;

: ARUNTIME-FREE  ( runtime -- )
    DUP 0= IF DROP EXIT THEN
    >R
    R@ ARUNTIME.EVENTS @ R@ ARUNTIME.PROVIDER @ APROV-DISCONNECT
    R@ ARUNTIME.EVENT-SCRATCH AEV-FREE
    R@ ARUNTIME.CONVERSATION @ ACONV-FREE
    R@ ARUNTIME.EVENTS @ AEQ-FREE
    R> FREE ;

VARIABLE _ARS-A
VARIABLE _ARS-U
VARIABLE _ARS-R
VARIABLE _ARS-RUN
VARIABLE _ARS-MSG

: ARUNTIME-SEND  ( prompt-a prompt-u runtime -- ior )
    _ARS-R ! _ARS-U ! _ARS-A !
    _ARS-U @ 0= IF 1 EXIT THEN
    _ARS-R @ ARUNTIME.STATUS @ DUP ARUN-S-RUNNING =
    SWAP ARUN-S-APPROVAL = OR IF 2 EXIT THEN
    _ARS-R @ ARUNTIME.NEXT-RUN @ _ARS-RUN !
    1 _ARS-R @ ARUNTIME.NEXT-RUN +!
    AROLE-USER AMSG-S-COMPLETE _ARS-RUN @ _ARS-A @ _ARS-U @
    _ARS-R @ ARUNTIME.CONVERSATION @ ACONV-APPEND
    DUP IF NIP EXIT THEN 2DROP
    AROLE-ASSISTANT AMSG-S-STREAMING _ARS-RUN @ 0 0
    _ARS-R @ ARUNTIME.CONVERSATION @ ACONV-APPEND
    DUP IF NIP EXIT THEN DROP _ARS-MSG !
    _ARS-MSG @ _ARS-R @ ARUNTIME.ASSISTANT-MSG !
    _ARS-RUN @ _ARS-R @ ARUNTIME.ACTIVE-RUN !
    ARUN-S-RUNNING _ARS-R @ ARUNTIME.STATUS !
    _ARS-RUN @ _ARS-R @ ARUNTIME.CONVERSATION @ ACONV.ACTIVE-RUN !
    1 _ARS-R @ ARUNTIME.REVISION +!
    _ARS-A @ _ARS-U @ _ARS-RUN @
    _ARS-R @ ARUNTIME.EVENTS @ _ARS-R @ ARUNTIME.PROVIDER @ APROV-START
    DUP IF
        ARUN-S-ERROR _ARS-R @ ARUNTIME.STATUS !
        AMSG-S-ERROR _ARS-MSG @ AMSG.STATE !
    THEN ;

