: _ARUNTIME-PUMP-AUTH  ( runtime -- changed? )
    DUP >R ARUNTIME.PROVIDER @ APROV-AUTH DUP 0= IF
        DROP R> DROP 0 EXIT
    THEN
    DUP AAUTH-PENDING? 0= IF
        DROP R> DROP 0 EXIT
    THEN
    AAUTH-POLL DUP AAUTH-S-PENDING = IF DROP R> DROP -1 EXIT THEN
    DUP AAUTH-S-OK = IF
        DROP R@ ARUNTIME.PROVIDER @ APROV-AUTH AAUTH-READY? IF
            R@ _ARUNTIME-AUTH-READY DROP
        THEN
        R> DROP -1 EXIT
    THEN
    DROP ARUN-S-ERROR R> ARUNTIME.STATUS ! -1 ;

