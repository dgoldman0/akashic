VARIABLE _ARBS-MAX-RUN

: _ARUNTIME-RESET-TRANSIENT  ( runtime -- )
    DUP _ARBS-R !
    0 OVER ARUNTIME.ASSISTANT-MSG !
    0 OVER ARUNTIME.APPROVAL-MSG !
    0 OVER ARUNTIME.ACTIVE-RUN !
    0 SWAP ARUNTIME.CONVERSATION @ ACONV.ACTIVE-RUN !
    0 _ARBS-MAX-RUN !
    _ARBS-R @ ARUNTIME.CONVERSATION @ DUP ACONV.COUNT @ 0 ?DO
        I OVER ACONV-NTH AMSG.RUN-ID @ _ARBS-MAX-RUN @ MAX
        _ARBS-MAX-RUN !
    LOOP
    DROP _ARBS-MAX-RUN @ 1+ 1 MAX _ARBS-R @ ARUNTIME.NEXT-RUN ! ;

: ARUNTIME-CONVERSATION-STORE!  ( store runtime -- status )
    _ARBS-R ! _ARBS-STORE !
    _ARBS-R @ ARUNTIME.CONVERSATION-STORE @ ?DUP IF ACSTORE-FREE THEN
    _ARBS-STORE @ _ARBS-R @ ARUNTIME.CONVERSATION-STORE !
    0 _ARBS-R @ ARUNTIME.PERSISTED-REVISION !
    0 _ARBS-R @ ARUNTIME.LAST-PERSIST-MS !
    ACSTORE-S-OK _ARBS-R @ ARUNTIME.STORE-STATUS !
    _ARBS-STORE @ ACSTORE-LOAD
    _ARBS-STATUS ! _ARBS-CONV !
    _ARBS-STATUS @ ACSTORE-S-NOT-FOUND = IF
        ACSTORE-S-OK _ARBS-R @ ARUNTIME.STORE-STATUS !
        _ARBS-R @ ARUNTIME-PERSIST-FORCE EXIT
    THEN
    _ARBS-STATUS @ ACSTORE-S-OK <> IF
        _ARBS-STATUS @ _ARBS-R @ ARUNTIME.STORE-STATUS !
        _ARBS-R @ ARUNTIME.CONVERSATION @ ACONV.REVISION @
        _ARBS-R @ ARUNTIME.PERSISTED-REVISION !
        _ARBS-STATUS @ EXIT
    THEN
    _ARBS-R @ ARUNTIME.CONVERSATION @ ACONV-FREE
    _ARBS-CONV @ _ARBS-R @ ARUNTIME.CONVERSATION !
    _ARBS-R @ _ARUNTIME-RESET-TRANSIENT
    1 _ARBS-R @ ARUNTIME.REVISION +!
    ACSTORE-S-OK _ARBS-R @ ARUNTIME.STORE-STATUS !
    _ARBS-R @ ARUNTIME-PERSIST-FORCE ;

: ARUNTIME-FREE  ( runtime -- )
    DUP 0= IF DROP EXIT THEN
    >R
    R@ _ARUNTIME-MANDATE-END DROP
    R@ ARUNTIME-PERSIST-FORCE DROP
    R@ ARUNTIME.EVENTS @ R@ ARUNTIME.PROVIDER @ APROV-DISCONNECT
    R@ ARUNTIME.EVENT-SCRATCH AEV-FREE
    R@ ARUNTIME.CONVERSATION @ ACONV-FREE
    R@ ARUNTIME.EVENTS @ AEQ-FREE
    R@ ARUNTIME.CONVERSATION-STORE @ ?DUP IF ACSTORE-FREE THEN
    R> FREE ;

