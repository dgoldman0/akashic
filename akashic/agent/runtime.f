: _AR-EVENT-ACCEPT?  ( event runtime -- flag )
    _ARAE-R ! DUP _ARAE-E ! AEV.KIND @ DUP _ARAE-KIND !
    \ Once local terminal processing starts, provider events can no longer
    \ change the selected terminal cause.  The gateway completion path alone
    \ releases any queued request and finishes the transition.
    _ARAE-R @ ARUNTIME.FLAGS @ ARUNTIME-F-TERMINATING AND IF
        DROP 0 EXIT
    THEN
    DUP AEV-CONNECTED = OVER AEV-DISCONNECTED = OR IF
        DROP _ARAE-E @ AEV.RUN-ID @ 0=
        _ARAE-R @ ARUNTIME.ACTIVE-RUN @ 0= AND EXIT
    THEN
    DUP AEV-RUN-STARTED < SWAP AEV-CONTEXT-ITEM > OR IF 0 EXIT THEN
    _ARAE-E @ AEV.RUN-ID @ DUP 0= IF
        DROP _ARAE-KIND @ AEV-ERROR = EXIT
    THEN
    DUP 0> SWAP _ARAE-R @ ARUNTIME.ACTIVE-RUN @ = AND
        0= IF 0 EXIT THEN
    -1 ;

: _AR-HANDLE-EVENT  ( event runtime -- )
    _ARH-R ! DUP _ARH-E !
    \ Only connection state and provider-global errors may use run id zero.
    \ All turn, text, tool, approval, context, and terminal events are bound
    \ to the one currently active positive run id.
    _ARH-E @ _ARH-R @ _AR-EVENT-ACCEPT? 0= IF DROP EXIT THEN
    DUP AEV.KIND @ _ARH-R @ ARUNTIME.LAST-EVENT !
    AEV.KIND @ CASE
        AEV-CONNECTED OF
            ARUN-S-IDLE _ARH-R @ ARUNTIME.STATUS !
        ENDOF
        AEV-DISCONNECTED OF
            ARUN-S-OFFLINE _ARH-R @ ARUNTIME.STATUS !
        ENDOF
        AEV-RUN-STARTED OF
            ARUN-S-RUNNING _ARH-R @ ARUNTIME.STATUS !
            _ARH-E @ _ARH-R @ _AR-MARK-REMOTE-RUN
        ENDOF
        AEV-TEXT-DELTA OF
            _ARH-R @ ARUNTIME.ASSISTANT-MSG @ ?DUP IF
                DUP AMSG.STATE @ AMSG-S-STREAMING <> IF
                    DROP _ARH-R @ _AR-FAIL-RUN
                ELSE
                    _ARH-E @ _AR-EVENT-TEXT IF
                        ROT AMSG-APPEND IF _ARH-R @ _AR-FAIL-RUN THEN
                    ELSE
                        2DROP DROP _ARH-R @ _AR-FAIL-RUN
                    THEN
                THEN
            ELSE
                _ARH-R @ _AR-FAIL-RUN
            THEN
        ENDOF
        AEV-MESSAGE-DONE OF
            _ARH-R @ ARUNTIME.ASSISTANT-MSG @ ?DUP IF
                DUP AMSG.STATE @ AMSG-S-STREAMING = IF
                    AMSG-S-COMPLETE OVER AMSG.STATE !
                    _ARH-R @ _AR-CONTEXT-ASSISTANT IF
                        _ARH-R @ _AR-FAIL-RUN
                    THEN
                THEN
                DROP
            ELSE
                _ARH-R @ _AR-FAIL-RUN
            THEN
        ENDOF
        AEV-TOOL-CALL OF
            3 _ARH-R @ ARUNTIME.CONVERSATION @ ACONV-ROOM? 0=
            _ARH-R @ ARUNTIME-MODEL-CONTEXT ACTX.COUNT @
                ACTX-MAX-ITEMS 3 - > OR IF
                _ARH-R @ _AR-FAIL-RUN
            ELSE
                _ARH-E @ _ARH-R @ _AR-APPEND-TOOL-CALL
                DUP 0= IF
                    DROP _ARH-R @ _AR-FAIL-RUN
                ELSE
                    DUP _ARH-R @ ARUNTIME.APPROVAL-MSG !
                    DROP
                    _ARH-E @ _ARH-R @ _AR-CONTEXT-TOOL-CALL DUP IF
                        DROP _ARH-R @ _AR-FAIL-RUN
                    ELSE
                        DROP _ARH-E @ _ARH-R @ _AR-START-TOOL DUP IF
                            _ARH-E @ _ARH-R @ _AR-FAIL-TOOL
                        ELSE
                            DROP ARUN-S-RUNNING _ARH-R @ ARUNTIME.STATUS !
                        THEN
                    THEN
                THEN
            THEN
        ENDOF
        AEV-APPROVAL OF
            2 _ARH-R @ ARUNTIME.CONVERSATION @ ACONV-ROOM? 0= IF
                _ARH-R @ _AR-FAIL-RUN
            ELSE
                AROLE-SYSTEM AMSG-S-APPROVAL _ARH-E @ _ARH-R @
                _AR-APPEND-EVENT-MESSAGE
                DUP IF
                    DUP _ARH-E @ SWAP _AR-MESSAGE-CALL-ID+ IF
                        DROP
                        _ARH-R @ ARUNTIME.CONVERSATION @ ACONV-DROP-LAST
                        _ARH-R @ _AR-FAIL-RUN
                    ELSE
                        DUP _ARH-R @ ARUNTIME.APPROVAL-MSG !
                        DROP ARUN-S-APPROVAL _ARH-R @ ARUNTIME.STATUS !
                    THEN
                ELSE
                    DROP _ARH-R @ _AR-FAIL-RUN
                THEN
            THEN
        ENDOF
        AEV-TOOL-RESULT OF
            AROLE-TOOL AMSG-S-COMPLETE _ARH-E @ _ARH-R @
            _AR-APPEND-EVENT-MESSAGE 0= IF _ARH-R @ _AR-FAIL-RUN THEN
        ENDOF
        AEV-CONTEXT-ITEM OF
            _ARH-E @ _ARH-R @ _AR-CONTEXT-PROVIDER-ITEM IF
                _ARH-R @ _AR-FAIL-RUN
            THEN
        ENDOF
        AEV-RUN-DONE OF
            \ RUN-DONE is valid only after MESSAGE-DONE committed the
            \ assistant item to model context.  Otherwise the transcript and
            \ provider context would diverge permanently.
            _ARH-R @ ARUNTIME.ASSISTANT-MSG @ ?DUP IF
                AMSG.STATE @ AMSG-S-COMPLETE <>
            ELSE
                -1
            THEN IF
                _ARH-R @ _AR-FAIL-RUN
            ELSE
                _ARH-E @ _ARH-R @ _AR-MARK-REMOTE-RUN
                _ARH-R @ ARUNTIME.FLAGS @ ARUNTIME-F-EXPIRED AND IF
                    ARUN-S-EXPIRED -1
                ELSE
                    _ARH-R @ _AR-GATEWAY-PENDING? IF
                        _ARH-E @ AEV.RUN-ID @ _ARH-R @ ARUNTIME.EVENTS @
                        _ARH-R @ ARUNTIME.PROVIDER @ APROV-CANCEL DROP
                        ARUN-S-ERROR -1
                    ELSE
                        ARUN-S-IDLE 0
                    THEN
                THEN
                _ARH-R @ _AR-TERMINAL-BEGIN DROP
            THEN
        ENDOF
        AEV-CANCELLED OF
            _ARH-R @ ARUNTIME.FLAGS @ ARUNTIME-F-EXPIRED AND IF
                ARUN-S-EXPIRED
            ELSE
                ARUN-S-CANCELLED
            THEN -1 _ARH-R @ _AR-TERMINAL-BEGIN DROP
        ENDOF
        AEV-ERROR OF
            AROLE-SYSTEM AMSG-S-ERROR _ARH-E @ _ARH-R @
            _AR-APPEND-EVENT-MESSAGE DROP
            ARUN-S-ERROR -1 _ARH-R @ _AR-TERMINAL-BEGIN DROP
        ENDOF
    ENDCASE
    1 _ARH-R @ ARUNTIME.REVISION +!
    1 _ARH-R @ ARUNTIME.CONVERSATION @ ACONV.REVISION +! ;

