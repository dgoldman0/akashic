            2DROP 0
        ELSE
            DROP _ARH-ROLE @ AROLE-TOOL = IF
                AMSG-F-TOOL OVER AMSG.FLAGS !
            THEN
        THEN
    ELSE
        2DROP 0
    THEN ;

