\ Expiry is fail-closed even when no further tool is attempted.  Authority
\ becomes unusable at the AMRUN deadline; the pump proactively cancels both
\ gateway and provider work and refuses another send until a terminal event.
: _ARUNTIME-PUMP-EXPIRY  ( runtime -- changed? )
    DUP _AREX-R ! ARUNTIME.MANDATE-RUN @ DUP 0= IF DROP 0 EXIT THEN
    AMRUN-ACTIVE? IF 0 EXIT THEN
    _AREX-R @ ARUNTIME.FLAGS @ ARUNTIME-F-EXPIRED AND IF 0 EXIT THEN
    _AREX-R @ ARUNTIME.ACTIVE-RUN @ DUP _AREX-RUN ! 0= IF
        _AREX-R @ _ARUNTIME-MANDATE-END DROP 0 EXIT
    THEN
    _AREX-R @ ARUNTIME.FLAGS DUP @ ARUNTIME-F-EXPIRED OR SWAP !
    _AREX-RUN @ _AREX-R @ ARUNTIME.EVENTS @
        _AREX-R @ ARUNTIME.PROVIDER @ APROV-CANCEL DROP
    ARUN-S-EXPIRED -1 _AREX-R @ _AR-TERMINAL-BEGIN DROP
    1 _AREX-R @ ARUNTIME.REVISION +!
    1 _AREX-R @ ARUNTIME.CONVERSATION @ ACONV.REVISION +!
    -1 ;

: ARUNTIME-PUMP  ( max-events runtime -- count )
    _ARP-R ! _ARP-MAX ! 0 _ARP-N !
    0 _ARP-POLL-RUN !
    _ARP-R @ _ARUNTIME-PUMP-EXPIRY IF 1 _ARP-N +! THEN
    _ARP-R @ _ARUNTIME-PUMP-AUTH IF 1 _ARP-N +! THEN
    _ARP-R @ _ARUNTIME-PUMP-SETTINGS IF 1 _ARP-N +! THEN
    _ARP-R @ _AR-PUMP-GATEWAY IF 1 _ARP-N +! THEN
    _ARP-R @ ARUNTIME.FLAGS @ ARUNTIME-F-TERMINATING AND IF
        _ARP-N @ IF _ARP-R @ ARUNTIME-PERSIST DROP THEN
        _ARP-N @ EXIT
    THEN
    _ARP-R @ ARUNTIME.EVENTS @ _ARP-R @ ARUNTIME.PROVIDER @ APROV-POLL
    IF
        _ARP-R @ ARUNTIME.ACTIVE-RUN @ _ARP-POLL-RUN !
        1 _ARP-N +!
        \ Poll failure is already the selected terminal cause.  Preserve one
        \ exact matching diagnostic, discard every other queued provider event
        \ without dispatch, and only then cancel local/provider work.
        _ARP-R @ _AR-POLL-FAIL-DRAIN 0= IF
            _ARP-R @ _AR-POLL-FAIL-GENERIC
        THEN
        _ARP-POLL-RUN @ IF
            _ARP-R @ _AR-FAIL-RUN
        ELSE
            ARUN-S-ERROR _ARP-R @ ARUNTIME.STATUS !
            1 _ARP-R @ ARUNTIME.REVISION +!
        THEN
    ELSE
        _ARP-MAX @ 0 ?DO
            _ARP-R @ ARUNTIME.EVENT-SCRATCH
            _ARP-R @ ARUNTIME.EVENTS @ AEQ-POP 0= IF LEAVE THEN
            _ARP-R @ ARUNTIME.EVENT-SCRATCH _ARP-R @ _AR-HANDLE-EVENT
            _ARP-R @ ARUNTIME.EVENT-SCRATCH AEV-FREE
            1 _ARP-N +!
        LOOP
    THEN
    _ARP-N @ IF _ARP-R @ ARUNTIME-PERSIST DROP THEN
    _ARP-N @ ;

