: _ARUNTIME-MANDATE-END  ( runtime -- status )
    DUP _ARM-R ! ARUNTIME.MANDATE-RUN @ DUP 0= IF
        DROP CBUS-S-OK EXIT
    THEN
    _ARM-RUN !
    _ARM-R @ ARUNTIME.TOOL-GATEWAY @ ?DUP IF
        DUP ATOOLG.STATE @ ATOOLG-S-COMPLETE = IF
            DUP ATOOLG-CLEAR DROP
        THEN
        DUP ATOOLG.STATE @ ATOOLG-S-IDLE <> IF
            DROP CBUS-S-BUSY EXIT
        THEN
        ATOOLG-MANDATE-CLEAR DROP
    THEN
    _ARM-RUN @ AMRUN-CLOSE
    _ARM-RUN @ AMRUN-FREE
    0 _ARM-R @ ARUNTIME.MANDATE-RUN !
    CBUS-S-OK ;

