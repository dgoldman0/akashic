: _ARUNTIME-CONNECT-PROVIDER  ( runtime -- status )
    DUP _ARPC-R ! ARUNTIME.EVENTS @
    _ARPC-R @ ARUNTIME.PROVIDER @ APROV-CONNECT
    DUP IF ARUN-S-ERROR ELSE ARUN-S-OFFLINE THEN
    _ARPC-R @ ARUNTIME.STATUS ! ;

: _ARUNTIME-REFRESH-SETTINGS  ( runtime -- status )
    DUP _ARPC-R ! ARUNTIME-RUN-SETTINGS DUP 0= IF
        DROP ARSET-S-UNSUPPORTED EXIT
    THEN
    DUP _ARPC-SETTINGS ! ARSET-REFRESH DUP _ARPC-STATUS !
    DUP ARSET-S-PENDING = IF
        ARUN-S-OFFLINE _ARPC-R @ ARUNTIME.STATUS ! EXIT
    THEN
    DUP ARSET-S-OK = IF
        DROP _ARPC-SETTINGS @ ARSET.STATE @ ARSET-STATE-READY = IF
            _ARPC-R @ _ARUNTIME-CONNECT-PROVIDER DROP ARSET-S-OK
        ELSE
            ARUN-S-ERROR _ARPC-R @ ARUNTIME.STATUS ! ARSET-S-PROTOCOL
        THEN
        EXIT
    THEN
    ARUN-S-ERROR _ARPC-R @ ARUNTIME.STATUS ! ;

: _ARUNTIME-INITIAL-PROVIDER  ( runtime -- )
    DUP _ARPC-R ! ARUNTIME-RUN-SETTINGS DUP 0= IF
        DROP _ARPC-R @ _ARUNTIME-CONNECT-PROVIDER DROP EXIT
    THEN
    DUP _ARPC-SETTINGS ! ARSET.STATE @ ARSET-STATE-READY = IF
        _ARPC-R @ _ARUNTIME-CONNECT-PROVIDER DROP EXIT
    THEN
    _ARPC-R @ ARUNTIME.PROVIDER @ APROV-AUTH DUP IF
        AAUTH-READY? 0= IF ARUN-S-OFFLINE _ARPC-R @ ARUNTIME.STATUS ! EXIT THEN
    ELSE
        DROP
    THEN
    _ARPC-R @ _ARUNTIME-REFRESH-SETTINGS DROP ;

: _ARUNTIME-NEW-CLEANUP  ( -- )
    _ARN-CONV @ ?DUP IF ACONV-FREE THEN
    _ARN-EVENTS @ ?DUP IF AEQ-FREE THEN
    _ARN-RUNTIME @ ?DUP IF FREE THEN
    0 _ARN-CONV ! 0 _ARN-EVENTS ! 0 _ARN-RUNTIME ! ;

: ARUNTIME-NEW  ( provider -- runtime ior )
    _ARN-PROVIDER ! 0 _ARN-RUNTIME ! 0 _ARN-EVENTS ! 0 _ARN-CONV !
    AGENT-RUNTIME-SIZE ALLOCATE DUP IF SWAP DROP 0 SWAP EXIT THEN
    DROP DUP _ARN-RUNTIME ! AGENT-RUNTIME-SIZE 0 FILL
    AEQ-NEW DUP IF 2DROP _ARUNTIME-NEW-CLEANUP 0 1 EXIT THEN
    DROP _ARN-EVENTS !
    ACONV-NEW DUP IF 2DROP _ARUNTIME-NEW-CLEANUP 0 1 EXIT THEN
    DROP _ARN-CONV !
    _ARN-PROVIDER @ _ARN-RUNTIME @ ARUNTIME.PROVIDER !
    _ARN-EVENTS @ _ARN-RUNTIME @ ARUNTIME.EVENTS !
    _ARN-CONV @ _ARN-RUNTIME @ ARUNTIME.CONVERSATION !
    1 _ARN-RUNTIME @ ARUNTIME.NEXT-RUN !
    1 _ARN-RUNTIME @ ARUNTIME.REVISION !
    ARUN-S-OFFLINE _ARN-RUNTIME @ ARUNTIME.STATUS !
    _ARN-RUNTIME @ _ARUNTIME-INITIAL-PROVIDER
    _ARN-RUNTIME @ 0 ;

VARIABLE _ARPS-R
VARIABLE _ARPS-STATUS

: ARUNTIME-PERSIST-FORCE  ( runtime -- status )
    DUP _ARPS-R ! ARUNTIME.CONVERSATION-STORE @ DUP 0= IF
        DROP ACSTORE-S-OK EXIT
    THEN
    _ARPS-R @ ARUNTIME.STORE-STATUS @ ACSTORE-S-INVALID = IF
        DROP ACSTORE-S-INVALID EXIT
    THEN
    _ARPS-R @ ARUNTIME.CONVERSATION @ SWAP ACSTORE-SAVE
    DUP _ARPS-STATUS ! _ARPS-R @ ARUNTIME.STORE-STATUS !
    _ARPS-STATUS @ ACSTORE-S-OK = IF
        _ARPS-R @ ARUNTIME.CONVERSATION @ ACONV.REVISION @
        _ARPS-R @ ARUNTIME.PERSISTED-REVISION !
        MS@ _ARPS-R @ ARUNTIME.LAST-PERSIST-MS !
    THEN
    _ARPS-STATUS @ ;

500 CONSTANT ARUNTIME-PERSIST-INTERVAL-MS

: ARUNTIME-PERSIST  ( runtime -- status )
    DUP _ARPS-R ! ARUNTIME.CONVERSATION-STORE @ 0= IF
        ACSTORE-S-OK EXIT
    THEN
    _ARPS-R @ ARUNTIME.CONVERSATION @ ACONV.REVISION @
    _ARPS-R @ ARUNTIME.PERSISTED-REVISION @ = IF ACSTORE-S-OK EXIT THEN
    _ARPS-R @ ARUNTIME.STATUS @ ARUN-S-RUNNING =
    _ARPS-R @ ARUNTIME.LAST-PERSIST-MS @ 0<> AND IF
        MS@ _ARPS-R @ ARUNTIME.LAST-PERSIST-MS @ -
        ARUNTIME-PERSIST-INTERVAL-MS < IF ACSTORE-S-OK EXIT THEN
    THEN
    _ARPS-R @ ARUNTIME-PERSIST-FORCE ;

VARIABLE _ARBS-STORE
