: _AR-APPEND-REQUEST-ERROR  ( request runtime -- )
    _ARGE-R ! _ARGE-REQ !
    _ARGE-REQ @ CBR.ERROR-A @ 0= IF EXIT THEN
    AROLE-SYSTEM AMSG-S-ERROR _ARGE-R @ ARUNTIME.ACTIVE-RUN @
    _ARGE-REQ @ CBR.ERROR-A @ _ARGE-REQ @ CBR.ERROR-U @
    _ARGE-R @ ARUNTIME.CONVERSATION @ ACONV-APPEND 2DROP ;

: _AR-PUMP-GATEWAY  ( runtime -- changed? )
    DUP _ARG-R ! ARUNTIME.TOOL-GATEWAY @ DUP 0= IF DROP 0 EXIT THEN
    DUP _ARG-G ! ATOOLG.STATE @
    DUP ATOOLG-S-APPROVAL = IF
        DROP
        _ARG-R @ ARUNTIME.STATUS @ ARUN-S-APPROVAL = IF 0 EXIT THEN
        _ARG-R @ ARUNTIME.APPROVAL-MSG @ ?DUP IF
            AMSG-S-APPROVAL SWAP AMSG.STATE !
        THEN
        ARUN-S-APPROVAL _ARG-R @ ARUNTIME.STATUS !
        1 _ARG-R @ ARUNTIME.REVISION +!
        1 _ARG-R @ ARUNTIME.CONVERSATION @ ACONV.REVISION +!
        -1 EXIT
    THEN
    ATOOLG-S-COMPLETE <> IF 0 EXIT THEN
    _ARG-G @ ATOOLG.REQUEST @ DUP _ARG-REQ !
    CBR.STATUS @ DUP _ARG-STATUS !
    CBUS-S-CANCELLED = IF
        _ARG-R @ ARUNTIME.APPROVAL-MSG @ ?DUP IF
            AMSG-S-CANCELLED SWAP AMSG.STATE !
        THEN
        _ARG-G @ ATOOLG-CLEAR DROP
        1 _ARG-R @ ARUNTIME.REVISION +!
        -1 EXIT
    THEN
    _ARG-R @ ARUNTIME.APPROVAL-MSG @ ?DUP IF
        _ARG-STATUS @ CBUS-S-OK = IF
            AMSG-S-COMPLETE
        ELSE
            AMSG-S-ERROR
        THEN
        SWAP AMSG.STATE !
    THEN
    _ARG-STATUS @ CBUS-S-OK <> IF
        _ARG-REQ @ _ARG-R @ _AR-APPEND-REQUEST-ERROR
    THEN
    _ARG-G @ ATOOLG.RUN-ID @
    _ARG-G @ ATOOLG.NAME DUP CV-DATA@ SWAP CV-LEN@
    _ARG-REQ @ CBR.RESULT _ARG-STATUS @ _ARG-R @
    _AR-PROVIDER-TOOL-RESULT
    DUP IF
        ARUN-S-ERROR _ARG-R @ ARUNTIME.STATUS !
    ELSE
        ARUN-S-RUNNING _ARG-R @ ARUNTIME.STATUS !
    THEN DROP
    _ARG-G @ ATOOLG-CLEAR DROP
    1 _ARG-R @ ARUNTIME.REVISION +!
    1 _ARG-R @ ARUNTIME.CONVERSATION @ ACONV.REVISION +!
    -1 ;

: ARUNTIME-PUMP  ( max-events runtime -- count )
    _ARP-R ! 0 _ARP-N !
    _ARP-R @ _AR-PUMP-GATEWAY IF 1 _ARP-N +! THEN
    _ARP-R @ ARUNTIME.EVENTS @ _ARP-R @ ARUNTIME.PROVIDER @ APROV-POLL
    IF ARUN-S-ERROR _ARP-R @ ARUNTIME.STATUS ! THEN
    0 ?DO
        _ARP-R @ ARUNTIME.EVENT-SCRATCH
        _ARP-R @ ARUNTIME.EVENTS @ AEQ-POP 0= IF LEAVE THEN
        _ARP-R @ ARUNTIME.EVENT-SCRATCH _ARP-R @ _AR-HANDLE-EVENT
        _ARP-R @ ARUNTIME.EVENT-SCRATCH AEV-FREE
        1 _ARP-N +!
    LOOP
    _ARP-N @ ;

VARIABLE _ARC-R
VARIABLE _ARC-RUN

: ARUNTIME-CANCEL  ( runtime -- ior )
    DUP _ARC-R ! ARUNTIME.ACTIVE-RUN @ DUP 0= IF DROP 0 EXIT THEN
    _ARC-RUN !
    _ARC-R @ ARUNTIME.TOOL-GATEWAY @ ?DUP IF
        ATOOLG-CANCEL DROP
    THEN
    _ARC-RUN @ _ARC-R @ ARUNTIME.EVENTS @
    _ARC-R @ ARUNTIME.PROVIDER @ APROV-CANCEL ;

VARIABLE _ARR-APPROVED
VARIABLE _ARR-R

: ARUNTIME-RESOLVE  ( approved runtime -- ior )
    _ARR-R ! _ARR-APPROVED !
    _ARR-R @ ARUNTIME.STATUS @ ARUN-S-APPROVAL <> IF 1 EXIT THEN
    _ARR-R @ ARUNTIME.TOOL-GATEWAY @ ?DUP IF
        DUP ATOOLG.STATE @ ATOOLG-S-APPROVAL = IF
            _ARR-R @ ARUNTIME.APPROVAL-MSG @ ?DUP IF
                _ARR-APPROVED @ IF AMSG-S-PENDING ELSE AMSG-S-ERROR THEN
                SWAP AMSG.STATE !
            THEN
            _ARR-APPROVED @ SWAP ATOOLG-RESOLVE
            DUP 0= IF
                ARUN-S-RUNNING _ARR-R @ ARUNTIME.STATUS !
                _ARR-R @ _AR-PUMP-GATEWAY DROP
                8 _ARR-R @ ARUNTIME-PUMP DROP
            THEN
            EXIT
        THEN
        DROP
    THEN
    _ARR-R @ ARUNTIME.APPROVAL-MSG @ ?DUP IF
        AMSG-S-COMPLETE SWAP AMSG.STATE !
    THEN
    _ARR-APPROVED @ _ARR-R @ ARUNTIME.ACTIVE-RUN @
    _ARR-R @ ARUNTIME.EVENTS @ _ARR-R @ ARUNTIME.PROVIDER @ APROV-RESOLVE
    DUP 0= IF ARUN-S-RUNNING _ARR-R @ ARUNTIME.STATUS ! THEN ;

: ARUNTIME-CLEAR  ( runtime -- ior )
    DUP ARUNTIME.STATUS @ DUP ARUN-S-RUNNING =
    SWAP ARUN-S-APPROVAL = OR IF DROP 1 EXIT THEN
    DUP ARUNTIME.CONVERSATION @ ACONV-CLEAR
    DUP ARUNTIME.TOOL-GATEWAY @ ?DUP IF ATOOLG-CLEAR DROP THEN
    0 OVER ARUNTIME.ASSISTANT-MSG !
    0 OVER ARUNTIME.APPROVAL-MSG !
    1 SWAP ARUNTIME.REVISION +! 0 ;

: ARUNTIME-RECONNECT  ( runtime -- ior )
    DUP >R ARUNTIME.EVENTS @ R@ ARUNTIME.PROVIDER @ APROV-CONNECT
    DUP IF ARUN-S-ERROR ELSE ARUN-S-OFFLINE THEN
    R@ ARUNTIME.STATUS !
    1 R> ARUNTIME.REVISION +! ;
