    _ARTR-CALL @ ACTXI-CALL-ID-TEXT _ARTR-CU ! _ARTR-CA !
    _ARTR-RUN @ _ARTR-SA @ _ARTR-SU @ _ARTR-NA @ _ARTR-NU @
    _ARTR-CA @ _ARTR-CU @ _ARTR-V @ _ARTR-STATUS @
    _ARTR-R @ ARUNTIME-MODEL-CONTEXT
    ACTX-APPEND-TOOL-RESULT-VALUE NIP ;

: _AR-PROVIDER-TOOL-RESULT  ( run-id name-a name-u value status runtime -- ior )
    _ARTR-R ! _ARTR-STATUS ! _ARTR-V ! _ARTR-NU ! _ARTR-NA ! _ARTR-RUN !
    _ARTR-RUN @ _ARTR-NA @ _ARTR-NU @ _ARTR-V @ _ARTR-STATUS @ _ARTR-R @
    _AR-CONTEXT-TOOL-RESULT ?DUP IF EXIT THEN
    _ARTR-RUN @ _ARTR-NA @ _ARTR-NU @ _ARTR-V @ _ARTR-STATUS @
    _ARTR-R @ ARUNTIME.EVENTS @ _ARTR-R @ ARUNTIME.PROVIDER @
    APROV-TOOL-RESULT ;

: _AR-START-TOOL  ( event runtime -- status )
    _ART-R ! _ART-E !
    _ART-R @ ARUNTIME.TOOL-GATEWAY @ 0= IF
        DROP CBUS-S-NO-HANDLER EXIT
    THEN
    _ART-E @ _AR-EVENT-NAME 0= IF
        2DROP CBUS-S-INVALID EXIT
    THEN
    _ART-E @ AEV.DATA
    _ART-E @ AEV.RUN-ID @
    _ART-E @ _AR-EVENT-CALL-ID 0= IF
        2DROP _ART-R @ ARUNTIME.TOOL-GATEWAY @ ATOOLG-CALL EXIT
    THEN
    _ART-R @ ARUNTIME.TOOL-GATEWAY @ ATOOLG-CALL-WITH-ID ;

: _AR-FAIL-TOOL  ( status event runtime -- )
    _ART-R ! _ART-E ! _ART-STATUS !
    _ART-R @ ARUNTIME.APPROVAL-MSG @ ?DUP IF
        AMSG-S-ERROR SWAP AMSG.STATE !
    THEN
    _ART-E @ _AR-EVENT-NAME IF
        _ART-U ! _ART-A !
        _ART-E @ AEV.RUN-ID @ _ART-A @ _ART-U @
        _ART-E @ AEV.DATA _ART-STATUS @ _ART-R @
        _AR-PROVIDER-TOOL-RESULT DROP
    ELSE
        2DROP ARUN-S-ERROR _ART-R @ ARUNTIME.STATUS !
    THEN ;

: _AR-HANDLE-EVENT  ( event runtime -- )
    _ARH-R ! DUP _ARH-E !
    DUP AEV.KIND @ _ARH-R @ ARUNTIME.LAST-EVENT !
    AEV.KIND @ CASE
        AEV-CONNECTED OF
            ARUN-S-IDLE _ARH-R @ ARUNTIME.STATUS !
        ENDOF
        AEV-DISCONNECTED OF
            ARUN-S-OFFLINE _ARH-R @ ARUNTIME.STATUS !
        ENDOF
        AEV-RUN-STARTED OF
            ARUN-S-RUNNING _ARH-R @ ARUNTIME.STATUS !
        ENDOF
        AEV-TEXT-DELTA OF
            _ARH-R @ ARUNTIME.ASSISTANT-MSG @ ?DUP IF
                _ARH-E @ _AR-EVENT-TEXT IF
                    ROT AMSG-APPEND DROP
                ELSE 2DROP DROP THEN
            THEN
        ENDOF
        AEV-MESSAGE-DONE OF
            _ARH-R @ ARUNTIME.ASSISTANT-MSG @ ?DUP IF
                DUP AMSG.STATE @ AMSG-S-STREAMING = IF
                    AMSG-S-COMPLETE OVER AMSG.STATE !
                    _ARH-R @ _AR-CONTEXT-ASSISTANT IF
                        ARUN-S-ERROR _ARH-R @ ARUNTIME.STATUS !
                    THEN
                THEN
                DROP
            THEN
        ENDOF
        AEV-TOOL-CALL OF
            _ARH-E @ _ARH-R @ _AR-APPEND-TOOL-CALL
            DUP _ARH-R @ ARUNTIME.APPROVAL-MSG !
            DROP
            _ARH-E @ _ARH-R @ _AR-CONTEXT-TOOL-CALL DUP IF
                DROP ARUN-S-ERROR _ARH-R @ ARUNTIME.STATUS !
                _ARH-E @ AEV.RUN-ID @ _ARH-R @ ARUNTIME.EVENTS @
                _ARH-R @ ARUNTIME.PROVIDER @ APROV-CANCEL DROP
            ELSE
                DROP _ARH-E @ _ARH-R @ _AR-START-TOOL DUP IF
                    _ARH-E @ _ARH-R @ _AR-FAIL-TOOL
                ELSE
                    DROP ARUN-S-RUNNING _ARH-R @ ARUNTIME.STATUS !
                THEN
            THEN
        ENDOF
        AEV-APPROVAL OF
            AROLE-SYSTEM AMSG-S-APPROVAL _ARH-E @ _ARH-R @
            _AR-APPEND-EVENT-MESSAGE
            DUP _ARH-R @ ARUNTIME.APPROVAL-MSG !
            DROP ARUN-S-APPROVAL _ARH-R @ ARUNTIME.STATUS !
        ENDOF
        AEV-TOOL-RESULT OF
            AROLE-TOOL AMSG-S-COMPLETE _ARH-E @ _ARH-R @
            _AR-APPEND-EVENT-MESSAGE DROP
        ENDOF
        AEV-CONTEXT-ITEM OF
            _ARH-E @ _ARH-R @ _AR-CONTEXT-PROVIDER-ITEM IF
                ARUN-S-ERROR _ARH-R @ ARUNTIME.STATUS !
            THEN
        ENDOF
        AEV-RUN-DONE OF
            ARUN-S-IDLE _ARH-R @ ARUNTIME.STATUS !
            0 _ARH-R @ ARUNTIME.ACTIVE-RUN !
            0 _ARH-R @ ARUNTIME.CONVERSATION @ ACONV.ACTIVE-RUN !
            _ARH-R @ _ARUNTIME-MANDATE-END DROP
        ENDOF
        AEV-CANCELLED OF
            ARUN-S-CANCELLED _ARH-R @ ARUNTIME.STATUS !
            _ARH-R @ ARUNTIME.ASSISTANT-MSG @ ?DUP IF
                AMSG-S-CANCELLED SWAP AMSG.STATE !
            THEN
            _ARH-E @ AEV.RUN-ID @ ?DUP IF
                _ARH-R @ ARUNTIME-MODEL-CONTEXT ACTX-DROP-RUN
            THEN
            0 _ARH-R @ ARUNTIME.ACTIVE-RUN !
            _ARH-R @ _ARUNTIME-MANDATE-END DROP
        ENDOF
        AEV-ERROR OF
            AROLE-SYSTEM AMSG-S-ERROR _ARH-E @ _ARH-R @
            _AR-APPEND-EVENT-MESSAGE DROP
            _ARH-E @ AEV.RUN-ID @ ?DUP IF
                _ARH-R @ ARUNTIME-MODEL-CONTEXT ACTX-DROP-RUN
            THEN
            ARUN-S-ERROR _ARH-R @ ARUNTIME.STATUS !
            _ARH-R @ _ARUNTIME-MANDATE-END DROP
        ENDOF
    ENDCASE
    1 _ARH-R @ ARUNTIME.REVISION +!
    1 _ARH-R @ ARUNTIME.CONVERSATION @ ACONV.REVISION +! ;

