        0
    THEN ;

\ Complete a terminal transition only after outstanding tool work is no
\ longer queued or awaiting review.  A queued cancellation remains sealed by
\ ACTIVE-RUN and the live AMRUN until the request-bus callback marks it
\ complete; no new turn or access change can race that interval.
: _AR-TERMINAL-FINISH  ( runtime -- status )
    DUP _ARTM-R ! ARUNTIME.FLAGS @ ARUNTIME-F-TERMINATING AND 0= IF
        CBUS-S-OK EXIT
    THEN
    _ARTM-R @ ARUNTIME.TOOL-GATEWAY @ ?DUP IF
        DUP ATOOLG.STATE @ DUP ATOOLG-S-QUEUED =
        SWAP ATOOLG-S-APPROVAL = OR IF
            DROP CBUS-S-BUSY EXIT
        THEN
        ATOOLG-CLEAR ?DUP IF EXIT THEN
    THEN
    _ARTM-R @ _ARUNTIME-MANDATE-END ?DUP IF EXIT THEN
    _ARTM-R @ ARUNTIME.ACTIVE-RUN @ _ARTM-RUN !
    _ARTM-R @ ARUNTIME.FLAGS @ ARUNTIME-F-DROP-RUN AND
    _ARTM-RUN @ 0<> AND IF
        _ARTM-RUN @ _ARTM-R @ ARUNTIME-MODEL-CONTEXT ACTX-DROP-RUN
    THEN
    0 _ARTM-R @ ARUNTIME.ACTIVE-RUN !
    0 _ARTM-R @ ARUNTIME.CONVERSATION @ ACONV.ACTIVE-RUN !
    0 _ARTM-R @ ARUNTIME.ASSISTANT-MSG !
    0 _ARTM-R @ ARUNTIME.APPROVAL-MSG !
    _ARTM-R @ ARUNTIME.FLAGS DUP @
        ARUNTIME-F-TERMINATING ARUNTIME-F-DROP-RUN OR INVERT AND SWAP !
    CBUS-S-OK ;

