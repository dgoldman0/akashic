    _ARPC-SETTINGS @ ARSET-POLL DUP ARSET-S-PENDING = IF DROP 0 EXIT THEN
    ARSET-S-OK = _ARPC-SETTINGS @ ARSET.STATE @ ARSET-STATE-READY = AND IF
        _ARPC-R @ ARUNTIME.PROVIDER @ APROV.STATE @ APROV-S-READY <> IF
            _ARPC-R @ _ARUNTIME-CONNECT-PROVIDER DROP
        THEN
    ELSE
        ARUN-S-ERROR _ARPC-R @ ARUNTIME.STATUS !
    THEN
    1 _ARPC-R @ ARUNTIME.REVISION +! -1 ;

