: _AR-EVENT-TEXT  ( event -- addr len flag )
    AEV.DATA DUP CV-TYPE@ CV-T-STRING = IF
        DUP CV-DATA@ SWAP CV-LEN@ -1
    ELSE
        DROP 0 0 0
    THEN ;

: _AR-APPEND-EVENT-MESSAGE  ( role state event runtime -- message | 0 )
    _ARH-R ! _ARH-E ! _ARH-STATE ! _ARH-ROLE !
    _ARH-E @ _AR-EVENT-TEXT IF
        _ARH-U ! _ARH-A !
        _ARH-ROLE @ _ARH-STATE @ _ARH-E @ AEV.RUN-ID @
        _ARH-A @ _ARH-U @
        _ARH-R @ ARUNTIME.CONVERSATION @ ACONV-APPEND
        DUP IF 2DROP 0 ELSE DROP THEN
    ELSE
        2DROP 0
    THEN ;

VARIABLE _ART-E
VARIABLE _ART-R
VARIABLE _ART-M
VARIABLE _ART-A
VARIABLE _ART-U
VARIABLE _ART-STATUS

: _AR-EVENT-NAME  ( event -- addr len flag )
    AEV.NAME DUP CV-TYPE@ CV-T-STRING = IF
        DUP CV-DATA@ SWAP CV-LEN@ -1
    ELSE
        DROP 0 0 0
    THEN ;

: _AR-APPEND-TOOL-CALL  ( event runtime -- message | 0 )
    _ART-R ! _ART-E !
    _ART-E @ _AR-EVENT-NAME IF
        _ART-U ! _ART-A !
    ELSE
        2DROP S" unnamed.tool" _ART-U ! _ART-A !
    THEN
    AROLE-TOOL AMSG-S-PENDING _ART-E @ AEV.RUN-ID @
    _ART-A @ _ART-U @ _ART-R @ ARUNTIME.CONVERSATION @ ACONV-APPEND
    DUP IF 2DROP 0 EXIT THEN
    DROP _ART-M !
    S" : " _ART-M @ AMSG-APPEND DROP
    _ART-E @ _AR-EVENT-TEXT IF
        _ART-M @ AMSG-APPEND DROP
    ELSE
        2DROP
    THEN
    _ART-M @ ;

: _AR-PROVIDER-TOOL-RESULT  ( run-id name-a name-u value status runtime -- ior )
    _ART-R ! _ART-STATUS ! >R _ART-U ! _ART-A ! _ARH-STATE !
    _ARH-STATE @ _ART-A @ _ART-U @ R> _ART-STATUS @
    _ART-R @ ARUNTIME.EVENTS @ _ART-R @ ARUNTIME.PROVIDER @
    APROV-TOOL-RESULT ;

: _AR-START-TOOL  ( event runtime -- status )
    _ART-R ! _ART-E !
    _ART-R @ ARUNTIME.TOOL-GATEWAY @ 0= IF
        DROP CBUS-S-NO-HANDLER EXIT
    THEN
    _ART-E @ _AR-EVENT-NAME 0= IF
        2DROP CBUS-S-INVALID EXIT
    THEN
    _ART-E @ AEV.DATA
    _ART-E @ AEV.RUN-ID @
    _ART-R @ ARUNTIME.TOOL-GATEWAY @ ATOOLG-CALL ;

: _AR-FAIL-TOOL  ( status event runtime -- )
    _ART-R ! _ART-E ! _ART-STATUS !
    _ART-R @ ARUNTIME.APPROVAL-MSG @ ?DUP IF
        AMSG-S-ERROR SWAP AMSG.STATE !
    THEN
    _ART-E @ _AR-EVENT-NAME IF
        _ART-U ! _ART-A !
        _ART-E @ AEV.RUN-ID @ _ART-A @ _ART-U @
        _ART-E @ AEV.DATA _ART-STATUS @ _ART-R @
        _AR-PROVIDER-TOOL-RESULT DROP
    ELSE
        2DROP ARUN-S-ERROR _ART-R @ ARUNTIME.STATUS !
    THEN ;

: _AR-HANDLE-EVENT  ( event runtime -- )
    _ARH-R ! DUP _ARH-E !
    DUP AEV.KIND @ _ARH-R @ ARUNTIME.LAST-EVENT !
    AEV.KIND @ CASE
        AEV-CONNECTED OF
            ARUN-S-IDLE _ARH-R @ ARUNTIME.STATUS !
        ENDOF
        AEV-DISCONNECTED OF
            ARUN-S-OFFLINE _ARH-R @ ARUNTIME.STATUS !
        ENDOF
        AEV-RUN-STARTED OF
            ARUN-S-RUNNING _ARH-R @ ARUNTIME.STATUS !
        ENDOF
        AEV-TEXT-DELTA OF
            _ARH-R @ ARUNTIME.ASSISTANT-MSG @ ?DUP IF
                _ARH-E @ _AR-EVENT-TEXT IF
                    ROT AMSG-APPEND DROP
                ELSE 2DROP DROP THEN
            THEN
        ENDOF
        AEV-MESSAGE-DONE OF
            _ARH-R @ ARUNTIME.ASSISTANT-MSG @ ?DUP IF
                AMSG-S-COMPLETE SWAP AMSG.STATE !
            THEN
        ENDOF
        AEV-TOOL-CALL OF
            _ARH-E @ _ARH-R @ _AR-APPEND-TOOL-CALL
            DUP _ARH-R @ ARUNTIME.APPROVAL-MSG !
            DROP
            _ARH-E @ _ARH-R @ _AR-START-TOOL DUP IF
                _ARH-E @ _ARH-R @ _AR-FAIL-TOOL
            ELSE
                DROP ARUN-S-RUNNING _ARH-R @ ARUNTIME.STATUS !
            THEN
        ENDOF
        AEV-APPROVAL OF
            AROLE-SYSTEM AMSG-S-APPROVAL _ARH-E @ _ARH-R @
            _AR-APPEND-EVENT-MESSAGE
            DUP _ARH-R @ ARUNTIME.APPROVAL-MSG !
            DROP ARUN-S-APPROVAL _ARH-R @ ARUNTIME.STATUS !
        ENDOF
        AEV-TOOL-RESULT OF
            AROLE-TOOL AMSG-S-COMPLETE _ARH-E @ _ARH-R @
            _AR-APPEND-EVENT-MESSAGE DROP
        ENDOF
        AEV-RUN-DONE OF
            ARUN-S-IDLE _ARH-R @ ARUNTIME.STATUS !
            0 _ARH-R @ ARUNTIME.ACTIVE-RUN !
            0 _ARH-R @ ARUNTIME.CONVERSATION @ ACONV.ACTIVE-RUN !
        ENDOF
        AEV-CANCELLED OF
            ARUN-S-CANCELLED _ARH-R @ ARUNTIME.STATUS !
            _ARH-R @ ARUNTIME.ASSISTANT-MSG @ ?DUP IF
                AMSG-S-CANCELLED SWAP AMSG.STATE !
            THEN
            0 _ARH-R @ ARUNTIME.ACTIVE-RUN !
        ENDOF
        AEV-ERROR OF
            AROLE-SYSTEM AMSG-S-ERROR _ARH-E @ _ARH-R @
            _AR-APPEND-EVENT-MESSAGE DROP
            ARUN-S-ERROR _ARH-R @ ARUNTIME.STATUS !
        ENDOF
    ENDCASE
    1 _ARH-R @ ARUNTIME.REVISION +!
    1 _ARH-R @ ARUNTIME.CONVERSATION @ ACONV.REVISION +! ;

