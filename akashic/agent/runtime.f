: _ARUNTIME-RESET-TRANSIENT  ( runtime -- )
    DUP _ARBS-R !
    0 OVER ARUNTIME.ASSISTANT-MSG !
    0 OVER ARUNTIME.APPROVAL-MSG !
    0 OVER ARUNTIME.ACTIVE-RUN !
    0 SWAP ARUNTIME.CONVERSATION @ ACONV.ACTIVE-RUN !
