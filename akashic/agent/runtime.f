: ARUNTIME-PERSIST-FORCE  ( runtime -- status )
    DUP _ARPS-R ! ARUNTIME.CONVERSATION-STORE @ DUP 0= IF
        DROP ACSTORE-S-OK EXIT
    THEN
    _ARPS-R @ ARUNTIME.STORE-STATUS @ ACSTORE-S-INVALID = IF
        DROP ACSTORE-S-INVALID EXIT
    THEN
    _ARPS-R @ ARUNTIME.CONVERSATION @ SWAP ACSTORE-SAVE
    DUP _ARPS-STATUS ! _ARPS-R @ ARUNTIME.STORE-STATUS !
    _ARPS-STATUS @ ACSTORE-S-OK = IF
        _ARPS-R @ ARUNTIME.CONVERSATION @ ACONV.REVISION @
        _ARPS-R @ ARUNTIME.PERSISTED-REVISION !
        MS@ _ARPS-R @ ARUNTIME.LAST-PERSIST-MS !
    THEN
    _ARPS-STATUS @ ;

500 CONSTANT ARUNTIME-PERSIST-INTERVAL-MS

: ARUNTIME-PERSIST  ( runtime -- status )
    DUP _ARPS-R ! ARUNTIME.CONVERSATION-STORE @ 0= IF
        ACSTORE-S-OK EXIT
    THEN
    _ARPS-R @ ARUNTIME.CONVERSATION @ ACONV.REVISION @
    _ARPS-R @ ARUNTIME.PERSISTED-REVISION @ = IF ACSTORE-S-OK EXIT THEN
    _ARPS-R @ ARUNTIME.STATUS @ ARUN-S-RUNNING =
    _ARPS-R @ ARUNTIME.LAST-PERSIST-MS @ 0<> AND IF
        MS@ _ARPS-R @ ARUNTIME.LAST-PERSIST-MS @ -
        ARUNTIME-PERSIST-INTERVAL-MS < IF ACSTORE-S-OK EXIT THEN
    THEN
    _ARPS-R @ ARUNTIME-PERSIST-FORCE ;

