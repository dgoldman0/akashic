\ Build a provider-facing snapshot from the visible transcript rather than
\ exposing the mixed model ledger.  Only complete prior user/assistant turns
\ survive; tool calls, tool results, provider state, audits, failed runs, and
\ the current prompt are excluded.  Selection proceeds newest-first under
\ both item and byte caps.  A completed run's user/assistant pair is selected
\ atomically into an explicit index list, then copied chronologically for
\ provider use.  No contiguous transcript suffix is trusted.
: _ARUNTIME-BUILD-HISTORY  ( runtime -- bytes status )
    DUP _ARHB-R ! ARUNTIME.DISCLOSURE-CONTEXT @ DUP _ARHB-X ! ACTX-CLEAR
    0 _ARHB-R @ ARUNTIME.LAST-DISCLOSURE-ITEMS !
    0 _ARHB-R @ ARUNTIME.LAST-DISCLOSURE-BYTES !
    _ARHB-R @ ARUNTIME-ACCESS-PROFILE DUP _ARHB-P ! 0= IF
        0 ACTX-S-OK EXIT
    THEN
    _ARHB-P @ AAP.FLAGS @ AAP-F-CHAT-HISTORY AND 0= IF
        0 ACTX-S-OK EXIT
    THEN
    _ARHB-R @ ARUNTIME.CONVERSATION @ DUP _ARHB-C !
    ACONV.COUNT @ _ARHB-I !
    0 _ARHB-N ! 0 _ARHB-PAIRS ! 0 _ARHB-BYTES ! -1 _ARHB-KEEP !
    BEGIN _ARHB-I @ 0> _ARHB-KEEP @ AND WHILE
        -1 _ARHB-I +!
        _ARHB-I @ _ARHB-C @ ACONV-NTH _ARHB-M !
        _ARHB-I @ _ARHB-C @ _AR-HISTORY-ELIGIBLE?
        _ARHB-M @ AMSG.ROLE @ AROLE-ASSISTANT = AND IF
            _ARHB-M @ AMSG.RUN-ID @ _ARHB-I @ _ARHB-C @
                _AR-HISTORY-USER-INDEX DUP _ARHB-USER-I ! 0>= IF
                _ARHB-USER-I @ _ARHB-C @ ACONV-NTH AMSG-TEXT NIP
                _ARHB-M @ AMSG-TEXT NIP + _ARHB-PAIR-BYTES !
                _ARHB-N @ 2 + _ARHB-P @ AAP.HISTORY-ITEMS @ >
                _ARHB-BYTES @ _ARHB-PAIR-BYTES @ +
                    _ARHB-P @ AAP.HISTORY-BYTES @ > OR IF
                    0 _ARHB-KEEP !
                ELSE
                    _ARHB-USER-I @ _ARHB-PAIRS @ _ARHB-USER!
                    _ARHB-I @ _ARHB-PAIRS @ _ARHB-ASSIST!
                    1 _ARHB-PAIRS +!
                    2 _ARHB-N +!
                    _ARHB-PAIR-BYTES @ _ARHB-BYTES +!
                    _ARHB-USER-I @ _ARHB-I !
                THEN
            THEN
        THEN
    REPEAT
    ACTX-S-OK _ARHB-STATUS !
    _ARHB-PAIRS @ _ARHB-J !
    BEGIN _ARHB-J @ 0> _ARHB-STATUS @ 0= AND WHILE
        -1 _ARHB-J +!
        _ARHB-J @ _ARHB-USER@ _ARHB-COPY-INDEX
        _ARHB-STATUS @ 0= IF
            _ARHB-J @ _ARHB-ASSIST@ _ARHB-COPY-INDEX
        THEN
    REPEAT
    _ARHB-STATUS @ IF
        _ARHB-X @ ACTX-CLEAR
        0 _ARHB-R @ ARUNTIME.LAST-DISCLOSURE-ITEMS !
        0 _ARHB-R @ ARUNTIME.LAST-DISCLOSURE-BYTES !
        0 _ARHB-STATUS @ EXIT
    THEN
    _ARHB-X @ ACTX.COUNT @ _ARHB-R @ ARUNTIME.LAST-DISCLOSURE-ITEMS !
    _ARHB-BYTES @ _ARHB-R @ ARUNTIME.LAST-DISCLOSURE-BYTES !
    _ARHB-BYTES @ ACTX-S-OK ;

: _ARUNTIME-PREPARE-TURN  ( prompt-a prompt-u run-id runtime -- turn )
    _ARBT-R ! _ARBT-RUN ! _ARBT-U ! _ARBT-A !
    _ARBT-R @ ARUNTIME.TURN-REQUEST DUP _ARS-TURN ! ATURN-INIT
    _ARBT-R @ ARUNTIME.CONVERSATION @ DUP ACONV.ID @ _ARS-TURN @ ATURN.THREAD-ID !
    ACONV.MODEL-CONTEXT @ DUP _ARS-CONTEXT ! DUP _ARBT-TURN-CONTEXT !
    _ARBT-R @ ARUNTIME.MANDATE-RUN @ IF
        DROP
        _ARBT-R @ ARUNTIME.DISCLOSURE-CONTEXT @
            DUP _ARBT-TURN-CONTEXT !
        DUP ACTX.COUNT @ _ARS-TURN @ ATURN.CONTEXT-N !
        ATURN-F-SCOPED-HISTORY _ARS-TURN @ ATURN.FLAGS !
    ELSE
        DUP ACTX.COUNT @ _ARS-TURN @ ATURN.CONTEXT-N !
    THEN
    DROP
    _ARBT-TURN-CONTEXT @ DUP ACTX.REVISION @
        _ARS-TURN @ ATURN.CONTEXT-REVISION !
    _ARS-TURN @ ATURN.CONTEXT !
    _ARBT-R @ ARUNTIME.TOOL-GATEWAY @ _ARS-TURN @ ATURN.TOOL-GATEWAY !
    _ARBT-RUN @ _ARS-TURN @ ATURN.RUN-ID !
    _ARBT-A @ _ARS-TURN @ ATURN.PROMPT-A !
    _ARBT-U @ _ARS-TURN @ ATURN.PROMPT-U !
    _ARBT-R @ ARUNTIME.MANDATE-RUN @ ?DUP IF
        AMRUN.MANDATE MAND.TOKEN-BUDGET @ _ARS-TURN @ ATURN.MAX-OUTPUT !
    THEN
    _ARS-TURN @ ;

: ARUNTIME-SEND  ( prompt-a prompt-u runtime -- ior )
    _ARS-R ! _ARS-U ! _ARS-A !
    _ARS-U @ 0= IF 1 EXIT THEN
    _ARS-R @ ARUNTIME-RUN-SETTINGS ?DUP IF
        ARSET.STATE @ ARSET-STATE-READY <> IF 4 EXIT THEN
    THEN
    _ARS-R @ ARUNTIME.ACTIVE-RUN @ IF 2 EXIT THEN
    _ARS-R @ ARUNTIME.STATUS @ DUP ARUN-S-RUNNING =
    SWAP ARUN-S-APPROVAL = OR IF 2 EXIT THEN
    2 _ARS-R @ ARUNTIME.CONVERSATION @ ACONV-ROOM? 0= IF 3 EXIT THEN
    _ARS-R @ ARUNTIME-MODEL-CONTEXT ACTX.COUNT @
    ACTX-MAX-ITEMS 2 - > IF 3 EXIT THEN
    0 _ARS-R @ ARUNTIME.ASSISTANT-MSG !
    0 _ARS-R @ ARUNTIME.APPROVAL-MSG !
    _ARS-R @ ARUNTIME.NEXT-RUN @ _ARS-RUN !
    0 _ARS-R @ ARUNTIME.FLAGS !
    _ARS-RUN @ _ARS-R @ _ARUNTIME-MANDATE-PREPARE ?DUP IF EXIT THEN
    _ARS-R @ ARUNTIME.MANDATE-RUN @ DUP IF
        _ARS-MANDATE-RUN !
        _ARS-R @ _ARUNTIME-BUILD-HISTORY
        _ARS-STATUS ! _ARS-CONTEXT !
        _ARS-STATUS @ IF
            _ARS-R @ _ARUNTIME-MANDATE-END DROP _ARS-STATUS @ EXIT
        THEN
        _ARS-CONTEXT @ _ARS-U @ + _ARS-MANDATE-RUN @
            AMRUN-DISCLOSE-BYTES ?DUP IF
            _ARS-R @ _ARUNTIME-MANDATE-END DROP EXIT
        THEN
    ELSE
        DROP
    THEN
    0 _ARS-R @ ARUNTIME.LAST-REMOTE-RUN !
    0 _ARS-R @ ARUNTIME.LAST-REMOTE-MS !
    1 _ARS-R @ ARUNTIME.NEXT-RUN +!
    AROLE-USER AMSG-S-COMPLETE _ARS-RUN @ _ARS-A @ _ARS-U @
    _ARS-R @ ARUNTIME.CONVERSATION @ ACONV-APPEND
    DUP IF
        NIP _ARS-STATUS ! _ARS-R @ _ARUNTIME-MANDATE-END DROP
        _ARS-STATUS @ EXIT
    THEN 2DROP
    AROLE-ASSISTANT AMSG-S-STREAMING _ARS-RUN @ 0 0
    _ARS-R @ ARUNTIME.CONVERSATION @ ACONV-APPEND
    DUP IF
        NIP _ARS-STATUS !
        _ARS-R @ ARUNTIME.CONVERSATION @ ACONV-DROP-LAST
        _ARS-R @ _ARUNTIME-MANDATE-END DROP
        _ARS-STATUS @ EXIT
    THEN DROP _ARS-MSG !
    _ARS-MSG @ _ARS-R @ ARUNTIME.ASSISTANT-MSG !
    _ARS-RUN @ _ARS-R @ ARUNTIME.ACTIVE-RUN !
    ARUN-S-RUNNING _ARS-R @ ARUNTIME.STATUS !
    _ARS-RUN @ _ARS-R @ ARUNTIME.CONVERSATION @ ACONV.ACTIVE-RUN !
    1 _ARS-R @ ARUNTIME.REVISION +!
    _ARS-A @ _ARS-U @ _ARS-RUN @ _ARS-R @ _ARUNTIME-PREPARE-TURN DROP
    AROLE-USER _ARS-RUN @ _ARS-A @ _ARS-U @ _ARS-CONTEXT @
    ACTX-APPEND-MESSAGE _ARS-STATUS ! DROP
    _ARS-STATUS @ IF
        ARUN-S-ERROR _ARS-R @ ARUNTIME.STATUS !
        AMSG-S-ERROR _ARS-MSG @ AMSG.STATE !
        0 _ARS-R @ ARUNTIME.ACTIVE-RUN !
        0 _ARS-R @ ARUNTIME.CONVERSATION @ ACONV.ACTIVE-RUN !
        _ARS-R @ ARUNTIME-PERSIST-FORCE DROP
        _ARS-R @ _ARUNTIME-MANDATE-END DROP
        _ARS-STATUS @ EXIT
    THEN
    _ARS-TURN @ _ARS-R @ ARUNTIME.EVENTS @
    _ARS-R @ ARUNTIME.PROVIDER @ APROV-START
    DUP _ARS-STATUS ! IF
        _ARS-CONTEXT @ ACTX-DROP-LAST
        ARUN-S-ERROR _ARS-R @ ARUNTIME.STATUS !
        AMSG-S-ERROR _ARS-MSG @ AMSG.STATE !
        0 _ARS-R @ ARUNTIME.ACTIVE-RUN !
        0 _ARS-R @ ARUNTIME.CONVERSATION @ ACONV.ACTIVE-RUN !
        _ARS-R @ _ARUNTIME-MANDATE-END DROP
    THEN
    _ARS-R @ ARUNTIME-PERSIST-FORCE DROP
    _ARS-STATUS @ ;

