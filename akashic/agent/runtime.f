: ARUNTIME-RESOLVE  ( approved runtime -- ior )
    _ARR-R ! _ARR-APPROVED !
    0 _ARR-LOCAL ! 0 _ARR-FORCED-DENY !
    _ARR-R @ ARUNTIME.STATUS @ ARUN-S-APPROVAL <> IF 1 EXIT THEN
    \ Never persist an APPROVED audit record for an operand that cannot be
    \ reviewed completely or that no longer matches its owned fingerprint.
    \ Normalize the request to a durable denial before touching authority.
    _ARR-APPROVED @ IF
        _ARR-R @ ARUNTIME.TOOL-GATEWAY @ ?DUP IF
            DUP ATOOLG.STATE @ ATOOLG-S-APPROVAL = IF
                ATOOLG-ARGS-REVIEWABLE? 0= IF
                    0 _ARR-APPROVED ! -1 _ARR-FORCED-DENY !
                THEN
            ELSE
                DROP
            THEN
        THEN
    THEN
    _ARR-APPROVED @ _ARR-R @ _ARUNTIME-AUDIT-DECISION DUP IF
        _ARR-IOR !
        \ No reviewed authority is issued unless the complete audit record
        \ is durable.  Settle the pending request as denied and keep the
        \ failed approval visible as an error.
        _ARR-R @ ARUNTIME.TOOL-GATEWAY @ ?DUP IF
            DUP ATOOLG.STATE @ ATOOLG-S-APPROVAL = IF
                -1 _ARR-LOCAL !
                0 SWAP ATOOLG-RESOLVE DROP
            ELSE
                DROP
            THEN
        THEN
        _ARR-LOCAL @ 0= IF
            0 _ARR-R @ ARUNTIME.ACTIVE-RUN @
            _ARR-R @ ARUNTIME.EVENTS @
            _ARR-R @ ARUNTIME.PROVIDER @ APROV-RESOLVE ?DUP IF
                _ARR-IOR ! _ARR-R @ _AR-FAIL-RUN
                _ARR-R @ ARUNTIME-PERSIST-FORCE DROP
                _ARR-IOR @ EXIT
            THEN
        THEN
        AMSG-S-ERROR _ARR-R @ _AR-APPROVAL-STATE!
        ARUN-S-RUNNING _ARR-R @ ARUNTIME.STATUS !
        _ARR-R @ _AR-PUMP-GATEWAY DROP
        _ARR-IOR @ EXIT
    THEN DROP
    _ARR-R @ ARUNTIME.TOOL-GATEWAY @ ?DUP IF
        DUP ATOOLG.STATE @ ATOOLG-S-APPROVAL = IF
            _ARR-APPROVED @ SWAP ATOOLG-RESOLVE
            DUP 0= IF
                _ARR-APPROVED @ IF AMSG-S-PENDING ELSE AMSG-S-ERROR THEN
                    _ARR-R @ _AR-APPROVAL-STATE!
                ARUN-S-RUNNING _ARR-R @ ARUNTIME.STATUS !
                _ARR-R @ _AR-PUMP-GATEWAY DROP
                8 _ARR-R @ ARUNTIME-PUMP DROP
            ELSE
                \ Grant/post failures must not hide an actionable approval.
                DUP _ARR-IOR ! DROP
                0 _ARR-R @ ARUNTIME.TOOL-GATEWAY @
                    ATOOLG-RESOLVE DROP
                AMSG-S-ERROR _ARR-R @ _AR-APPROVAL-STATE!
                ARUN-S-RUNNING _ARR-R @ ARUNTIME.STATUS !
                _ARR-R @ _AR-PUMP-GATEWAY DROP _ARR-IOR @
            THEN
            _ARR-R @ ARUNTIME-PERSIST-FORCE DROP
            DUP 0= _ARR-FORCED-DENY @ AND IF
                DROP CBUS-S-DENIED
            THEN
            EXIT
        THEN
        DROP
    THEN
    _ARR-APPROVED @ _ARR-R @ ARUNTIME.ACTIVE-RUN @
    _ARR-R @ ARUNTIME.EVENTS @ _ARR-R @ ARUNTIME.PROVIDER @ APROV-RESOLVE
    DUP 0= IF
        AMSG-S-COMPLETE _ARR-R @ _AR-APPROVAL-STATE!
        ARUN-S-RUNNING _ARR-R @ ARUNTIME.STATUS !
    ELSE
        DUP _ARR-IOR ! DROP
        _ARR-R @ _AR-FAIL-RUN
        _ARR-R @ ARUNTIME-PERSIST-FORCE DROP
        _ARR-IOR @
    THEN
    _ARR-R @ ARUNTIME-PERSIST-FORCE DROP ;

: ARUNTIME-CLEAR  ( runtime -- ior )
    DUP ARUNTIME.ACTIVE-RUN @ IF DROP 1 EXIT THEN
    DUP ARUNTIME.STATUS @ DUP ARUN-S-RUNNING =
    SWAP ARUN-S-APPROVAL = OR IF DROP 1 EXIT THEN
    DUP ARUNTIME.CONVERSATION @ ACONV-CLEAR
    DUP _ARUNTIME-MANDATE-END DROP
    DUP ARUNTIME.TOOL-GATEWAY @ ?DUP IF ATOOLG-CLEAR DROP THEN
    DUP ARUNTIME.DISCLOSURE-CONTEXT @ ?DUP IF ACTX-CLEAR THEN
    0 OVER ARUNTIME.ASSISTANT-MSG !
    0 OVER ARUNTIME.APPROVAL-MSG !
    0 OVER ARUNTIME.LAST-DISCLOSURE-ITEMS !
    0 OVER ARUNTIME.LAST-DISCLOSURE-BYTES !
    0 OVER ARUNTIME.LAST-REMOTE-MS !
    0 OVER ARUNTIME.LAST-REMOTE-RUN !
    0 OVER ARUNTIME.FLAGS !
    1 OVER ARUNTIME.REVISION +!
    ARUNTIME-PERSIST-FORCE DROP 0 ;

: ARUNTIME-RECONNECT  ( runtime -- ior )
    DUP ARUNTIME.ACTIVE-RUN @ IF DROP 1 EXIT THEN
    DUP ARUNTIME.MANDATE-RUN @ IF DROP 1 EXIT THEN
    DUP ARUNTIME-RUN-SETTINGS ?DUP IF
        ARSET.STATE @ ARSET-STATE-READY <> IF DROP 1 EXIT THEN
    THEN
    DUP >R ARUNTIME.EVENTS @ R@ ARUNTIME.PROVIDER @ APROV-CONNECT
    DUP IF ARUN-S-ERROR ELSE ARUN-S-OFFLINE THEN
    R@ ARUNTIME.STATUS !
    1 R> ARUNTIME.REVISION +! ;

VARIABLE _ARA-A
VARIABLE _ARA-U
VARIABLE _ARA-R
VARIABLE _ARA-STATUS

: _ARUNTIME-CLEAR-SETTINGS  ( runtime -- )
    ARUNTIME-RUN-SETTINGS ?DUP IF ARSET-CANCEL DROP THEN ;

: ARUNTIME-AUTH  ( runtime -- auth | 0 )
    ARUNTIME.PROVIDER @ APROV-AUTH ;

: ARUNTIME-BUSY?  ( runtime -- flag )
    DUP ARUNTIME.ACTIVE-RUN @ IF DROP -1 EXIT THEN
    ARUNTIME.STATUS @ DUP ARUN-S-RUNNING =
    SWAP ARUN-S-APPROVAL = OR ;

: ARUNTIME-AUTH-SET  ( secret-a secret-u runtime -- status )
    _ARA-R ! _ARA-U ! _ARA-A !
    _ARA-R @ ARUNTIME-BUSY? IF AAUTH-S-BUSY EXIT THEN
    _ARA-A @ _ARA-U @ _ARA-R @ ARUNTIME-AUTH DUP 0= IF
        DROP 2DROP AAUTH-S-UNSUPPORTED EXIT
    THEN
    AAUTH-SECRET-SET
    DUP _ARA-STATUS ! IF _ARA-STATUS @ EXIT THEN
    _ARA-R @ ARUNTIME-RUN-SETTINGS IF
        _ARA-R @ _ARUNTIME-CLEAR-SETTINGS
        _ARA-R @ _ARUNTIME-AUTH-READY DROP
    ELSE
        _ARA-R @ ARUNTIME-RECONNECT DROP
    THEN
    AAUTH-S-OK ;

: ARUNTIME-AUTH-CLEAR  ( runtime -- status )
    DUP _ARA-R ! ARUNTIME-BUSY? IF AAUTH-S-BUSY EXIT THEN
    _ARA-R @ ARUNTIME-AUTH DUP 0= IF DROP AAUTH-S-UNSUPPORTED EXIT THEN
    AAUTH-LOGOUT
    DUP _ARA-STATUS ! IF _ARA-STATUS @ EXIT THEN
    _ARA-R @ ARUNTIME.EVENTS @ _ARA-R @ ARUNTIME.PROVIDER @ APROV-DISCONNECT
    _ARA-R @ _ARUNTIME-CLEAR-SETTINGS
    ARUN-S-OFFLINE _ARA-R @ ARUNTIME.STATUS !
    1 _ARA-R @ ARUNTIME.REVISION +!
    AAUTH-S-OK ;

: ARUNTIME-AUTH-PRESENT?  ( runtime -- flag )
    ARUNTIME-AUTH AAUTH-READY? ;

: ARUNTIME-AUTH-BEGIN  ( runtime -- status )
    DUP _ARA-R ! ARUNTIME-BUSY? IF AAUTH-S-BUSY EXIT THEN
    _ARA-R @ ARUNTIME-AUTH DUP 0= IF DROP AAUTH-S-UNSUPPORTED EXIT THEN
    DUP AAUTH.METHODS @ AAUTH-M-DEVICE AND 0= IF
        DROP AAUTH-S-UNSUPPORTED EXIT
    THEN
    AAUTH-BEGIN DUP _ARA-STATUS !
    _ARA-STATUS @ DUP AAUTH-S-OK = SWAP AAUTH-S-PENDING = OR IF
        _ARA-R @ ARUNTIME.EVENTS @ _ARA-R @ ARUNTIME.PROVIDER @ APROV-DISCONNECT
        _ARA-R @ _ARUNTIME-CLEAR-SETTINGS
        ARUN-S-OFFLINE _ARA-R @ ARUNTIME.STATUS !
        1 _ARA-R @ ARUNTIME.REVISION +!
    THEN
    _ARA-STATUS @ ;

: ARUNTIME-AUTH-CANCEL  ( runtime -- status )
    DUP _ARA-R ! ARUNTIME-AUTH DUP 0= IF DROP AAUTH-S-UNSUPPORTED EXIT THEN
    AAUTH-CANCEL DUP _ARA-STATUS !
    1 _ARA-R @ ARUNTIME.REVISION +!
    _ARA-STATUS @ ;

