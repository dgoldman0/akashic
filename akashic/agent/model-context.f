: _ACTXI-TEXT  ( value -- addr len )
    DUP CV-TYPE@ CV-T-STRING = IF
        DUP CV-DATA@ SWAP CV-LEN@
    ELSE
        DROP 0 0
    THEN ;

: ACTXI-SOURCE-TEXT  ( item -- addr len ) ACTXI.SOURCE _ACTXI-TEXT ;
: ACTXI-NAME-TEXT    ( item -- addr len ) ACTXI.NAME _ACTXI-TEXT ;
: ACTXI-CALL-ID-TEXT ( item -- addr len ) ACTXI.CALL-ID _ACTXI-TEXT ;
: ACTXI-DATA-TEXT    ( item -- addr len ) ACTXI.DATA _ACTXI-TEXT ;

: _ACTXI-STRING?  ( value -- flag )
    DUP CV-TYPE@ CV-T-STRING <> IF DROP 0 EXIT THEN
    DUP CV-LEN@ 0= IF DROP -1 EXIT THEN
    DUP CV-DATA@ SWAP CV-LEN@ UTF8-VALID? ;

: _ACTXI-EMPTY-STRING?  ( value -- flag )
    DUP CV-TYPE@ CV-T-STRING = SWAP CV-LEN@ 0= AND ;

: ACTXI-VALID?  ( item -- flag )
    >R
    R@ ACTXI.RUN-ID @ 0< IF R> DROP 0 EXIT THEN
    R@ ACTXI.KIND @ CASE
        ACTX-K-MESSAGE OF
            R@ ACTXI.ROLE @ DUP AROLE-USER < SWAP AROLE-SYSTEM > OR IF
                R> DROP 0 EXIT
            THEN
            R@ ACTXI.SOURCE _ACTXI-EMPTY-STRING? 0= IF R> DROP 0 EXIT THEN
            R@ ACTXI.NAME _ACTXI-EMPTY-STRING? 0= IF R> DROP 0 EXIT THEN
            R@ ACTXI.CALL-ID _ACTXI-EMPTY-STRING? 0= IF R> DROP 0 EXIT THEN
            R@ ACTXI.DATA DUP CV-TYPE@ CV-T-STRING <> IF
                DROP R> DROP 0 EXIT
            THEN
            DUP CV-LEN@ IF DUP CV-DATA@ SWAP CV-LEN@ UTF8-VALID? ELSE DROP -1 THEN
        ENDOF
        ACTX-K-TOOL-CALL OF
            R@ ACTXI.ROLE @ AROLE-ASSISTANT <> IF R> DROP 0 EXIT THEN
            R@ ACTXI.SOURCE _ACTXI-STRING? 0= IF R> DROP 0 EXIT THEN
            R@ ACTXI.NAME _ACTXI-STRING? 0= IF R> DROP 0 EXIT THEN
            R@ ACTXI.CALL-ID _ACTXI-STRING? 0= IF R> DROP 0 EXIT THEN
            R@ ACTXI.DATA _ACTXI-STRING? 0= IF R> DROP 0 EXIT THEN
            R@ ACTXI-DATA-TEXT JSON-VALID?
        ENDOF
        ACTX-K-TOOL-RESULT OF
            R@ ACTXI.ROLE @ AROLE-TOOL <> IF R> DROP 0 EXIT THEN
            R@ ACTXI.SOURCE _ACTXI-STRING? 0= IF R> DROP 0 EXIT THEN
            R@ ACTXI.NAME _ACTXI-STRING? 0= IF R> DROP 0 EXIT THEN
            R@ ACTXI.CALL-ID _ACTXI-STRING? 0= IF R> DROP 0 EXIT THEN
            R@ ACTXI.DATA _ACTXI-STRING? 0= IF R> DROP 0 EXIT THEN
            R@ ACTXI-DATA-TEXT JSON-VALID?
        ENDOF
        ACTX-K-PROVIDER OF
            R@ ACTXI.ROLE @ 0<> IF R> DROP 0 EXIT THEN
            R@ ACTXI.SOURCE _ACTXI-STRING? 0= IF R> DROP 0 EXIT THEN
            R@ ACTXI.NAME DUP CV-TYPE@ CV-T-STRING <> IF
                DROP R> DROP 0 EXIT
            THEN
            DUP CV-LEN@ IF DUP CV-DATA@ SWAP CV-LEN@ UTF8-VALID? ELSE DROP -1 THEN
            0= IF R> DROP 0 EXIT THEN
            R@ ACTXI.CALL-ID _ACTXI-EMPTY-STRING? 0= IF R> DROP 0 EXIT THEN
            R@ ACTXI.DATA _ACTXI-STRING? 0= IF R> DROP 0 EXIT THEN
            R@ ACTXI-DATA-TEXT 2DUP JSON-VALID? IF JSON-OBJECT? ELSE 2DROP 0 THEN
        ENDOF
        0 SWAP DROP
    ENDCASE
    R> DROP ;

