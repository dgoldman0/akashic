    _ACTXA-STATUS @ _ACTXA-I @ ACTXI.STATUS !
    _ACTXA-SA @ _ACTXA-SU @ _ACTXA-I @ ACTXI.SOURCE CV-STRING!
    IF ACTX-S-NOMEM _ACTXA-FAIL EXIT THEN
    _ACTXA-NA @ _ACTXA-NU @ _ACTXA-I @ ACTXI.NAME CV-STRING!
    IF ACTX-S-NOMEM _ACTXA-FAIL EXIT THEN
    _ACTXA-CA @ _ACTXA-CU @ _ACTXA-I @ ACTXI.CALL-ID CV-STRING!
    IF ACTX-S-NOMEM _ACTXA-FAIL EXIT THEN
    _ACTXA-DA @ _ACTXA-DU @ _ACTXA-I @ ACTXI.DATA CV-STRING!
    IF ACTX-S-NOMEM _ACTXA-FAIL EXIT THEN
    _ACTXA-I @ ACTXI-VALID? 0= IF ACTX-S-INVALID _ACTXA-FAIL EXIT THEN
    1 _ACTXA-C @ ACTX.COUNT +!
    1 _ACTXA-C @ ACTX.REVISION +!
