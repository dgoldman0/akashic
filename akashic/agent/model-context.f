    _ACTXF-C @ ACTX.COUNT @ 0 ?DO I _ACTXF-C @ ACTX-NTH ACTXI-FREE LOOP
    0 _ACTXF-C @ ACTX.COUNT ! 1 _ACTXF-C @ ACTX.REVISION +! ;

