: ACTX-NEW  ( -- context status )
    AGENT-MODEL-CONTEXT-SIZE ALLOCATE
    DUP IF SWAP DROP 0 ACTX-S-NOMEM EXIT THEN
    DROP DUP AGENT-MODEL-CONTEXT-SIZE 0 FILL
    1 OVER ACTX.REVISION ! ACTX-S-OK ;

: ACTX-NTH  ( index context -- item | 0 )
    >R DUP 0< OVER R@ ACTX.COUNT @ >= OR IF DROP R> DROP 0 EXIT THEN
    AGENT-CONTEXT-ITEM-SIZE * R> ACTX.ITEMS + ;

