: _ACTXI-MOVE  ( source destination -- )
    DUP ACTXI-FREE >R DUP R@ AGENT-CONTEXT-ITEM-SIZE CMOVE
    AGENT-CONTEXT-ITEM-SIZE 0 FILL R> DROP ;

: ACTX-DROP-RUN  ( run-id context -- )
    _ACTXD-C ! _ACTXD-RUN ! 0 _ACTXD-W !
    _ACTXD-C @ ACTX.COUNT @ 0 ?DO
        I _ACTXD-C @ ACTX-NTH DUP _ACTXD-ITEM !
        ACTXI.RUN-ID @ _ACTXD-RUN @ = IF
            _ACTXD-ITEM @ ACTXI-FREE
        ELSE
            I _ACTXD-W @ <> IF
                _ACTXD-ITEM @ _ACTXD-W @ _ACTXD-C @ ACTX-NTH _ACTXI-MOVE
            THEN
            1 _ACTXD-W +!
        THEN
    LOOP
    _ACTXD-W @ _ACTXD-C @ ACTX.COUNT @ <> IF
        _ACTXD-W @ _ACTXD-C @ ACTX.COUNT !
        1 _ACTXD-C @ ACTX.REVISION +!
    THEN ;

