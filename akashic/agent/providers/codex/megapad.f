\ =====================================================================
\  megapad.f - Native Codex account environment for physical MegaPad
\ =====================================================================

PROVIDED akashic-agent-codex-megapad

REQUIRE ../../provider-source.f
REQUIRE provider.f
REQUIRE ../../../net/transports/megapad-tls.f

0 CONSTANT _CDMP-SOURCE
_CDMP-SOURCE AGENT-PROVIDER-SOURCE-SIZE + CONSTANT _CDMP-CONFIG
_CDMP-CONFIG OPENAI-CONFIG-SIZE + CONSTANT _CDMP-AUTH
_CDMP-AUTH CODEX-DEVICE-AUTH-SIZE + CONSTANT _CDMP-AUTH-TRANSPORT
_CDMP-AUTH-TRANSPORT MPTLS-SIZE + CONSTANT _CDMP-MODEL-TRANSPORT
_CDMP-MODEL-TRANSPORT MPTLS-SIZE + CONSTANT CODEX-MEGAPAD-SOURCE-SIZE

: CODEX-MEGAPAD-SOURCE-CONFIG  ( source -- config ) _CDMP-CONFIG + ;
: CODEX-MEGAPAD-SOURCE-AUTH    ( source -- auth-context ) _CDMP-AUTH + ;
: CODEX-MEGAPAD-AUTH-TRANSPORT  ( source -- adapter )
    _CDMP-AUTH-TRANSPORT + ;
: CODEX-MEGAPAD-MODEL-TRANSPORT  ( source -- adapter )
    _CDMP-MODEL-TRANSPORT + ;

VARIABLE _CDMPS-S
VARIABLE _CDMPS-STATUS

: _CODEX-MEGAPAD-CONFIGURE  ( source -- status )
    _CDMPS-S !
    CODEX-AUTH-HOST 443
    _CDMPS-S @ CODEX-MEGAPAD-AUTH-TRANSPORT MPTLS-CONFIGURE
    ?DUP IF EXIT THEN
    CODEX-BACKEND-HOST 443
    _CDMPS-S @ CODEX-MEGAPAD-MODEL-TRANSPORT MPTLS-CONFIGURE ;

: _CODEX-MEGAPAD-CREATE  ( source -- provider status )
    DUP _CDMPS-S ! _CODEX-MEGAPAD-CONFIGURE ?DUP IF
        DROP 0 APSOURCE-S-INVALID EXIT
    THEN
    _CDMPS-S @ CODEX-MEGAPAD-SOURCE-CONFIG
    _CDMPS-S @ CODEX-MEGAPAD-SOURCE-AUTH
    _CDMPS-S @ CODEX-MEGAPAD-MODEL-TRANSPORT MPTLS.PORT
    CODEX-PROVIDER-NEW ;

: CODEX-MEGAPAD-SOURCE-FREE  ( source -- )
    DUP 0= IF DROP EXIT THEN
    DUP CODEX-MEGAPAD-AUTH-TRANSPORT MPTLS.PORT NIO-CLOSE
    DUP CODEX-MEGAPAD-MODEL-TRANSPORT MPTLS.PORT NIO-CLOSE
    DUP CODEX-MEGAPAD-SOURCE-AUTH CDA.AUTH AAUTH-DESTROY
    DUP CODEX-MEGAPAD-SOURCE-SIZE 0 FILL
    FREE ;

VARIABLE _CDMPN-S

: CODEX-MEGAPAD-SOURCE-NEW  ( -- source status )
    CODEX-MEGAPAD-SOURCE-SIZE ALLOCATE
    DUP IF 2DROP 0 APSOURCE-S-NOMEM EXIT THEN
    DROP DUP _CDMPN-S ! CODEX-MEGAPAD-SOURCE-SIZE 0 FILL
    _CDMPN-S @ APSOURCE-INIT
    _CDMPN-S @ CODEX-MEGAPAD-SOURCE-CONFIG CODEX-CONFIG-INIT
    ?DUP IF
        DROP _CDMPN-S @ FREE 0 APSOURCE-S-INVALID EXIT
    THEN
    _CDMPN-S @ CODEX-MEGAPAD-AUTH-TRANSPORT MPTLS-INIT
    _CDMPN-S @ CODEX-MEGAPAD-MODEL-TRANSPORT MPTLS-INIT
    _CDMPN-S @ _CODEX-MEGAPAD-CONFIGURE DUP _CDMPS-STATUS ! IF
        _CDMPN-S @ FREE 0 _CDMPS-STATUS @ EXIT
    THEN
    _CDMPN-S @ CODEX-MEGAPAD-AUTH-TRANSPORT MPTLS.PORT
    _CDMPN-S @ CODEX-MEGAPAD-SOURCE-AUTH CODEX-DEVICE-AUTH-INIT
    AAUTH-S-OK <> IF
        _CDMPN-S @ CODEX-MEGAPAD-SOURCE-FREE
        0 APSOURCE-S-INVALID EXIT
    THEN
    _CDMPN-S @ _CDMPN-S @ APSOURCE.CONTEXT !
    S" org.akashic.agent.source.codex.megapad"
    _CDMPN-S @ APSOURCE.ID-U ! _CDMPN-S @ APSOURCE.ID-A !
    ['] _CODEX-MEGAPAD-CREATE _CDMPN-S @ APSOURCE.NEW-XT !
    ['] CODEX-MEGAPAD-SOURCE-FREE _CDMPN-S @ APSOURCE.FREE-XT !
    _CDMPN-S @ APSOURCE-S-OK ;
