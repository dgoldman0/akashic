    _CDSEL-SLOT @ _CDSEL-C @ _CDMC-INSTR-A @
    _CDSEL-SLOT @ _CDSEL-C @ _CDMC-INSTR-U @
    _CDSEL-C @ CDMC.CONFIG @ OAIC.INSTRUCTIONS-BUF
    OAIC-INSTRUCTIONS-CAPACITY JSON-UNESCAPE
    JSON-OK? 0= IF DROP ARSET-S-CAPACITY EXIT THEN
    DUP _CDSEL-C @ CDMC.CONFIG @ OAIC.INSTRUCTIONS-BUF SWAP UTF8-VALID?
    0= IF DROP ARSET-S-PROTOCOL EXIT THEN
    _CDSEL-C @ CDMC.CONFIG @ OAIC.INSTRUCTIONS-U !
