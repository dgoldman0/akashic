: _CDMC-KEY-COPY  ( oa ou ka ku dest cap required? -- len found? status )
    _CDK-REQ ! _CDK-CAP ! _CDK-D ! _CDK-KU ! _CDK-KA !
    _CDK-OU ! _CDK-OA !
    _CDK-OA @ _CDK-OU @ _CDK-KA @ _CDK-KU @ JSON-KEY? 0= IF
        2DROP _CDK-REQ @ IF 0 0 ARSET-S-PROTOCOL ELSE 0 0 ARSET-S-OK THEN
        EXIT
    THEN
    2DUP JSON-NULL? IF
        2DROP _CDK-REQ @ IF 0 0 ARSET-S-PROTOCOL ELSE 0 0 ARSET-S-OK THEN
        EXIT
    THEN
    2DUP JSON-STRING? 0= IF 2DROP 0 0 ARSET-S-PROTOCOL EXIT THEN
    JSON-GET-STRING _CDK-VU ! _CDK-VA !
    _CDK-D @ _CDK-CAP @ 0 FILL JSON-CLEAR-ERR
    _CDK-VA @ _CDK-VU @ _CDK-D @ _CDK-CAP @ JSON-UNESCAPE
    JSON-OK? 0= IF DROP 0 0 ARSET-S-CAPACITY EXIT THEN
    -1 ARSET-S-OK ;

