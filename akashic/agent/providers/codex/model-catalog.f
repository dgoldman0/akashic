: _CDMC-SORT  ( catalog -- )
    _CDSORT-C ! 0 _CDSORT-I !
    BEGIN
        _CDSORT-I @ _CDSORT-C @ CDMC.SETTINGS ARSET.MODELS-N @ 1- <
    WHILE
        0 _CDSORT-J !
        BEGIN
            _CDSORT-J @ _CDSORT-C @ CDMC.SETTINGS ARSET.MODELS-N @ 1- <
        WHILE
            _CDSORT-J @ _CDSORT-C @ _CDMC-MODEL-PTR DUP _CDSORT-P !
            @ _CDSORT-A !
            _CDSORT-P @ 8 + @ _CDSORT-B !
            _CDSORT-A @ ARMODEL.PRIORITY @
            _CDSORT-B @ ARMODEL.PRIORITY @ > IF
                _CDSORT-B @ _CDSORT-P @ !
                _CDSORT-A @ _CDSORT-P @ 8 + !
            THEN
            1 _CDSORT-J +!
        REPEAT
        1 _CDSORT-I +!
    REPEAT ;

