    ELSE 0 0 THEN
    _CDSEL-C @ CDMC.CONFIG @ OAIC-SUMMARY! IF ARSET-S-CAPACITY EXIT THEN
    _CDSEL-M @ ARMODEL.FLAGS @ ARMODEL-F-VERBOSITY AND IF
        _CDSEL-SLOT @ _CDSEL-C @ _CDMC-VERB
        _CDSEL-SLOT @ _CDSEL-C @ _CDMC-VERB-U @
