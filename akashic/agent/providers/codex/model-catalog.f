: _CDMC-PARSE-EFFORTS  ( -- status )
    0 _CDMC-DEFAULT-EFFORT-U !
    S" default_reasoning_level" _CDMC-DEFAULT-EFFORT
    CDMC-CHOICE-ID-CAPACITY 0 _CDMC-MODEL-STRING
    DUP IF NIP EXIT THEN DROP _CDMC-DEFAULT-EFFORT-U !
    _CDPM-MODEL-OA @ _CDPM-MODEL-OU @
    S" supported_reasoning_levels" JSON-KEY?
    0= IF 2DROP 0 _CDPM-M @ ARMODEL.EFFORTS-N ! ARSET-S-OK EXIT THEN
    2DUP JSON-ARRAY? 0= IF 2DROP ARSET-S-PROTOCOL EXIT THEN
    JSON-ENTER 2DUP JSON-COUNT DUP CDMC-MAX-EFFORTS > IF
        DROP 2DROP ARSET-S-CAPACITY EXIT
    THEN
    DUP _CDPM-M @ ARMODEL.EFFORTS-N ! _CDPM-N !
    _CDPM-SLOT @ _CDPM-C @ _CDMC-EFFORT-PTR-A
    _CDPM-M @ ARMODEL.EFFORTS-A !
    _CDPM-N @ 0 ?DO
        2DUP I JSON-NTH JSON-VALUE-SPAN
        2DUP JSON-OBJECT? 0= IF
            2DROP 2DROP ARSET-S-PROTOCOL UNLOOP EXIT
        THEN
        JSON-ENTER _CDPM-ITEM-OU ! _CDPM-ITEM-OA ! I _CDPM-I !
        I _CDPM-SLOT @ _CDPM-C @ _CDMC-EFFORT DUP _CDPM-CHOICE !
        AGENT-RUN-CHOICE-SIZE 0 FILL
        S" effort" I _CDPM-SLOT @ _CDPM-C @ _CDMC-EFFORT-ID
        CDMC-CHOICE-ID-CAPACITY -1 _CDMC-ITEM-STRING
        DUP IF
            _CDPM-STATUS ! DROP 2DROP _CDPM-STATUS @ UNLOOP EXIT
        THEN
        DROP _CDPM-U !
        I _CDPM-SLOT @ _CDPM-C @ _CDMC-EFFORT-ID
        _CDPM-CHOICE @ ARCH.ID-A ! _CDPM-U @ _CDPM-CHOICE @ ARCH.ID-U !
        _CDPM-CHOICE @ ARCH-ID
        _CDPM-CHOICE @ ARCH.LABEL-U ! _CDPM-CHOICE @ ARCH.LABEL-A !
        S" description" I _CDPM-SLOT @ _CDPM-C @ _CDMC-EFFORT-DESC
        CDMC-CHOICE-DESC-CAPACITY 0 _CDMC-ITEM-STRING
        DUP IF
            _CDPM-STATUS ! DROP 2DROP _CDPM-STATUS @ UNLOOP EXIT
        THEN
        DROP _CDPM-U !
        I _CDPM-SLOT @ _CDPM-C @ _CDMC-EFFORT-DESC
        _CDPM-CHOICE @ ARCH.DESC-A ! _CDPM-U @ _CDPM-CHOICE @ ARCH.DESC-U !
        _CDPM-CHOICE @ ARCH-ID _CDMC-DEFAULT-EFFORT
        _CDMC-DEFAULT-EFFORT-U @ STR-STR= IF
            ARCH-F-DEFAULT _CDPM-CHOICE @ ARCH.FLAGS !
        THEN
        _CDPM-CHOICE @ _CDPM-SLOT @ _CDPM-C @ _CDMC-EFFORT-PTR-A
        I 8 * + !
    LOOP
    2DROP
    _CDPM-N @ IF
        0 _CDPM-SLOT @ _CDPM-C @ _CDMC-EFFORT
        _CDPM-CHOICE !
        _CDPM-N @ 0 ?DO
            I _CDPM-SLOT @ _CDPM-C @ _CDMC-EFFORT
            ARCH.FLAGS @ ARCH-F-DEFAULT AND IF ARSET-S-OK UNLOOP EXIT THEN
        LOOP
        ARCH-F-DEFAULT _CDPM-CHOICE @ ARCH.FLAGS !
    THEN
    ARSET-S-OK ;

