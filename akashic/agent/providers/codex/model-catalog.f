: _CDMC-PARSE-MODEL  ( model-a model-u slot catalog -- status )
    _CDPM-C ! _CDPM-SLOT !
    2DUP JSON-OBJECT? 0= IF 2DROP ARSET-S-PROTOCOL EXIT THEN
    JSON-ENTER _CDPM-MODEL-OU ! _CDPM-MODEL-OA !
    _CDPM-SLOT @ _CDPM-C @ _CDMC-MODEL DUP _CDPM-M !
    AGENT-RUN-MODEL-SIZE 0 FILL

    S" slug" _CDPM-SLOT @ _CDPM-C @ _CDMC-SLUG
    CDMC-SLUG-CAPACITY -1 _CDMC-MODEL-STRING
    DUP IF NIP EXIT THEN DROP _CDPM-U !
    _CDPM-SLOT @ _CDPM-C @ _CDMC-SLUG
    _CDPM-M @ ARMODEL.ID-A ! _CDPM-U @ _CDPM-M @ ARMODEL.ID-U !

    S" display_name" _CDPM-SLOT @ _CDPM-C @ _CDMC-LABEL
    CDMC-LABEL-CAPACITY -1 _CDMC-MODEL-STRING
    DUP IF NIP EXIT THEN DROP _CDPM-U !
    _CDPM-SLOT @ _CDPM-C @ _CDMC-LABEL
    _CDPM-M @ ARMODEL.LABEL-A ! _CDPM-U @ _CDPM-M @ ARMODEL.LABEL-U !

    S" description" _CDPM-SLOT @ _CDPM-C @ _CDMC-DESC
    CDMC-DESC-CAPACITY 0 _CDMC-MODEL-STRING
    DUP IF NIP EXIT THEN DROP _CDPM-U !
    _CDPM-SLOT @ _CDPM-C @ _CDMC-DESC
    _CDPM-M @ ARMODEL.DESC-A ! _CDPM-U @ _CDPM-M @ ARMODEL.DESC-U !

    S" priority" 999 _CDMC-MODEL-NUMBER
    DUP IF NIP EXIT THEN DROP _CDPM-M @ ARMODEL.PRIORITY !
    S" context_window" 0 _CDMC-MODEL-NUMBER
    DUP IF NIP EXIT THEN DROP _CDPM-M @ ARMODEL.CONTEXT-WINDOW !
    ARMODEL-F-TEXT ARMODEL-F-IMAGE OR _CDPM-M @ ARMODEL.FLAGS !
    S" use_responses_lite" 0 _CDMC-MODEL-BOOL
    DUP IF NIP EXIT THEN DROP IF
        ARMODEL-F-RESPONSES-LITE _CDPM-M @ ARMODEL.FLAGS +!
    THEN
    S" support_verbosity" 0 _CDMC-MODEL-BOOL
    DUP IF NIP EXIT THEN DROP IF
        ARMODEL-F-VERBOSITY _CDPM-M @ ARMODEL.FLAGS +!
    THEN

    _CDPM-MODEL-OA @ _CDPM-MODEL-OU @ S" base_instructions" JSON-KEY? 0= IF
        2DROP ARSET-S-PROTOCOL EXIT
    THEN
    2DUP JSON-STRING? 0= IF 2DROP ARSET-S-PROTOCOL EXIT THEN
    JSON-GET-STRING
