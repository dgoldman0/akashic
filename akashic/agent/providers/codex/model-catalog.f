: _CDMC-PARSE  ( body-a body-u catalog -- status )
    _CDPARSE-C ! _CDPARSE-U ! _CDPARSE-A !
    _CDPARSE-A @ _CDPARSE-U @ JSON-VALID? 0= IF ARSET-S-PROTOCOL EXIT THEN
    _CDPARSE-A @ _CDPARSE-U @ 2DUP JSON-OBJECT? 0= IF
        2DROP ARSET-S-PROTOCOL EXIT
    THEN
    JSON-ENTER S" models" JSON-KEY?
    0= IF 2DROP ARSET-S-PROTOCOL EXIT THEN
    2DUP JSON-ARRAY? 0= IF 2DROP ARSET-S-PROTOCOL EXIT THEN
    JSON-ENTER 2DUP JSON-COUNT _CDPARSE-N !
    0 _CDPARSE-C @ CDMC.SETTINGS ARSET.MODELS-N !
    _CDPARSE-N @ 0 ?DO
        2DUP I JSON-NTH JSON-VALUE-SPAN
        _CDPARSE-MU ! _CDPARSE-MA !
        _CDPARSE-MA @ _CDPARSE-MU @ _CDMC-LIST-MODEL?
        _CDPARSE-STATUS ! _CDPARSE-VISIBLE !
        _CDPARSE-STATUS @ IF
            2DROP _CDPARSE-STATUS @ UNLOOP EXIT
        THEN
        _CDPARSE-VISIBLE @ IF
            _CDPARSE-C @ CDMC.SETTINGS ARSET.MODELS-N @
            DUP CDMC-MAX-MODELS >= IF
                DROP 2DROP ARSET-S-CAPACITY UNLOOP EXIT
            THEN
            _CDPARSE-MA @ _CDPARSE-MU @ ROT _CDPARSE-C @
            _CDMC-PARSE-MODEL DUP IF
                _CDPARSE-STATUS ! DROP 2DROP
                _CDPARSE-STATUS @ UNLOOP EXIT
            THEN
            DROP
            _CDPARSE-C @ CDMC.SETTINGS ARSET.MODELS-N @ DUP
            _CDPARSE-C @ _CDMC-MODEL
            SWAP _CDPARSE-C @ _CDMC-MODEL-PTR !
            1 _CDPARSE-C @ CDMC.SETTINGS ARSET.MODELS-N +!
        THEN
    LOOP
    2DROP
    _CDPARSE-C @ CDMC.SETTINGS ARSET.MODELS-N @ 0= IF
        ARSET-S-PROTOCOL EXIT
    THEN
    _CDPARSE-C @ _CDMC-SORT
    0 _CDPARSE-C @ CDMC.SETTINGS ARSET-MODEL-NTH
    ARMODEL-F-DEFAULT OVER ARMODEL.FLAGS +! DROP
    0 _CDPARSE-SELECT !
    _CDPARSE-C @ CDMC.SETTINGS ARSET.MODELS-N @ 0 ?DO
        I _CDPARSE-C @ CDMC.SETTINGS ARSET-MODEL-NTH ARMODEL-ID
        _CDPARSE-C @ CDMC.CONFIG @ OAIC-MODEL STR-STR= IF
            I _CDPARSE-SELECT ! LEAVE
        THEN
    LOOP
    _CDPARSE-SELECT @ _CDPARSE-C @ _CDMC-MODEL-INDEX! ;

VARIABLE _CDCW-C

: _CDMC-AUTH-CB  ( secret-a secret-u catalog -- status )
    >R R@ CDMC.REQUEST HREQ-AUTH-BEARER R> DROP ;

: _CDMC-BUILD-WIRE  ( catalog -- status )
    DUP _CDCW-C ! CDMC.REQUEST HREQ-CLEAR
    S" GET" CODEX-MODELS-PATH _CDCW-C @ CDMC.REQUEST HREQ-BEGIN
    IF ARSET-S-PROTOCOL EXIT THEN
    CODEX-BACKEND-HOST _CDCW-C @ CDMC.REQUEST HREQ-HOST
    IF ARSET-S-PROTOCOL EXIT THEN
    S" application/json" _CDCW-C @ CDMC.REQUEST HREQ-ACCEPT
    IF ARSET-S-PROTOCOL EXIT THEN
    S" Akashic/1" _CDCW-C @ CDMC.REQUEST HREQ-USER-AGENT
    IF ARSET-S-PROTOCOL EXIT THEN
    _CDCW-C @ CDMC.REQUEST HREQ-CONNECTION-CLOSE IF ARSET-S-PROTOCOL EXIT THEN
    ['] _CDMC-AUTH-CB _CDCW-C @ _CDCW-C @ CDMC.AUTH @ AAUTH-WITH-ACCESS
    AAUTH-S-OK <> IF ARSET-S-AUTH EXIT THEN
    _CDCW-C @ CDMC.AUTH @ AAUTH.ACCOUNT-ID DUP CV-TYPE@ CV-T-STRING <> IF
        DROP ARSET-S-AUTH EXIT
    THEN
    DUP CV-DATA@ SWAP CV-LEN@ DUP 0= IF 2DROP ARSET-S-AUTH EXIT THEN
    S" ChatGPT-Account-ID" 2SWAP _CDCW-C @ CDMC.REQUEST HREQ-HEADER
    IF ARSET-S-PROTOCOL EXIT THEN
    _CDCW-C @ CDMC.REQUEST HREQ-SEAL IF ARSET-S-PROTOCOL EXIT THEN
    _CDCW-C @ CDMC.REQUEST _CDCW-C @ CDMC.PORT @
    _CDCW-C @ CDMC.EXCHANGE HBUF-START HBUF-S-PENDING <> IF
        ARSET-S-TRANSPORT EXIT
    THEN
    CDMC-SUB-HTTP _CDCW-C @ CDMC.SUBSTATE ! ARSET-S-PENDING ;

: _CDMC-REFRESH  ( catalog -- status )
    DUP _CDCW-C ! CDMC.SETTINGS ARSET-PENDING? IF ARSET-S-BUSY EXIT THEN
    _CDCW-C @ CDMC.CONFIG @ 0= IF ARSET-S-INVALID EXIT THEN
    _CDCW-C @ CDMC.AUTH @ AAUTH-READY? 0= IF ARSET-S-AUTH EXIT THEN
    0 _CDCW-C @ CDMC.AUTH-RETRIED !
    0 _CDCW-C @ CDMC.SETTINGS ARSET.MODELS-N !
    -1 _CDCW-C @ CDMC.SETTINGS ARSET.SELECTED !
    ARSET-STATE-LOADING _CDCW-C @ CDMC.SETTINGS ARSET.STATE !
    0 _CDCW-C @ CDMC.SETTINGS ARSET.ERROR-A !
    0 _CDCW-C @ CDMC.SETTINGS ARSET.ERROR-U !
    1 _CDCW-C @ CDMC.SETTINGS ARSET.REVISION +!
    _CDCW-C @ _CDMC-BUILD-WIRE DUP ARSET-S-PENDING = IF EXIT THEN
    S" Model catalog request could not be started"
    _CDCW-C @ _CDMC-FAIL ;

: _CDMC-WAIT-AUTH  ( catalog -- status )
    DUP _CDCW-C ! CDMC.AUTH @ DUP AAUTH-POLL DROP
    DUP AAUTH-READY? IF
        DROP _CDCW-C @ _CDMC-BUILD-WIRE DUP ARSET-S-PENDING = IF EXIT THEN
        S" Model catalog retry could not be started"
        _CDCW-C @ _CDMC-FAIL EXIT
    THEN
    AAUTH-PENDING? IF ARSET-S-PENDING EXIT THEN
    ARSET-S-AUTH S" Model catalog authentication refresh failed"
    _CDCW-C @ _CDMC-FAIL ;

: _CDMC-UNAUTHORIZED  ( catalog -- status )
    DUP _CDCW-C ! CDMC.AUTH-RETRIED @ IF
        ARSET-S-AUTH S" Model catalog remained unauthorized after refresh"
        _CDCW-C @ _CDMC-FAIL EXIT
    THEN
    -1 _CDCW-C @ CDMC.AUTH-RETRIED !
    CDMC-SUB-WAIT-AUTH _CDCW-C @ CDMC.SUBSTATE !
    _CDCW-C @ CDMC.AUTH @ AAUTH-REFRESH
    DUP AAUTH-S-PENDING = IF DROP ARSET-S-PENDING EXIT THEN
    AAUTH-S-OK = IF _CDCW-C @ _CDMC-WAIT-AUTH EXIT THEN
    ARSET-S-AUTH S" Model catalog credentials could not be refreshed"
    _CDCW-C @ _CDMC-FAIL ;

: _CDMC-POLL  ( catalog -- status )
    DUP _CDCW-C ! CDMC.SUBSTATE @ CDMC-SUB-WAIT-AUTH = IF
        _CDCW-C @ _CDMC-WAIT-AUTH EXIT
    THEN
    _CDCW-C @ CDMC.EXCHANGE HBUF-POLL DUP HBUF-S-PENDING = IF
        DROP _CDCW-C @ CDMC.EXCHANGE HBUF.STATE @ HBUF-STATE-RECEIVING = IF
            _CDCW-C @ CDMC.REQUEST HREQ-CLEAR
        THEN
        ARSET-S-PENDING EXIT
    THEN
    HBUF-S-OK <> IF
        ARSET-S-TRANSPORT S" Model catalog transport failed"
        _CDCW-C @ _CDMC-FAIL EXIT
    THEN
    _CDCW-C @ CDMC.REQUEST HREQ-CLEAR
    _CDCW-C @ CDMC.EXCHANGE HBUF.HTTP-CODE @ DUP 401 = IF
        DROP _CDCW-C @ _CDMC-UNAUTHORIZED EXIT
    THEN
    200 <> IF
        ARSET-S-PROTOCOL S" Model catalog request was rejected"
        _CDCW-C @ _CDMC-FAIL EXIT
    THEN
    _CDCW-C @ CDMC.EXCHANGE HBUF.BODY-A @
    _CDCW-C @ CDMC.EXCHANGE HBUF.BODY-U @ _CDCW-C @ _CDMC-PARSE
    ?DUP IF
        S" Model catalog response is invalid" _CDCW-C @ _CDMC-FAIL EXIT
    THEN
    CDMC-SUB-IDLE _CDCW-C @ CDMC.SUBSTATE !
    ARSET-STATE-READY _CDCW-C @ CDMC.SETTINGS ARSET.STATE !
    1 _CDCW-C @ CDMC.SETTINGS ARSET.REVISION +! ARSET-S-OK ;

: _CDMC-CANCEL  ( catalog -- status )
    DUP _CDCW-C ! CDMC.EXCHANGE HBUF-CANCEL DROP
    _CDCW-C @ CDMC.REQUEST HREQ-CLEAR
    CDMC-SUB-IDLE _CDCW-C @ CDMC.SUBSTATE !
    ARSET-STATE-EMPTY _CDCW-C @ CDMC.SETTINGS ARSET.STATE !
    1 _CDCW-C @ CDMC.SETTINGS ARSET.REVISION +! ARSET-S-CANCELLED ;

: _CDMC-DISPOSE  ( catalog -- )
    DUP CDMC.EXCHANGE HBUF-RESET
    CDMC.REQUEST HREQ-CLEAR ;

: CDMC-CONFIG!  ( config catalog -- )
    CDMC.CONFIG ! ;

