\ =====================================================================
\  source.f - Owned construction environment for the Codex provider
\ =====================================================================
\  The source owns account state plus distinct KDOS TLS transports for
\  authentication and model requests. It contains no hardware or TUI code.
\ =====================================================================

PROVIDED akashic-agent-codex-source

REQUIRE ../../provider-source.f
REQUIRE provider.f
REQUIRE ../../../net/transports/kdos-tls.f

0 CONSTANT _CDSRC-SOURCE
_CDSRC-SOURCE AGENT-PROVIDER-SOURCE-SIZE + CONSTANT _CDSRC-CONFIG
_CDSRC-CONFIG OPENAI-CONFIG-SIZE + CONSTANT _CDSRC-AUTH
_CDSRC-AUTH CODEX-DEVICE-AUTH-SIZE + CONSTANT _CDSRC-AUTH-TRANSPORT
_CDSRC-AUTH-TRANSPORT KDOSTLS-SIZE + CONSTANT _CDSRC-MODEL-TRANSPORT
_CDSRC-MODEL-TRANSPORT KDOSTLS-SIZE + CONSTANT CODEX-SOURCE-SIZE

: CODEX-SOURCE-CONFIG  ( source -- config ) _CDSRC-CONFIG + ;
: CODEX-SOURCE-AUTH    ( source -- auth-context ) _CDSRC-AUTH + ;
: CODEX-SOURCE-AUTH-TRANSPORT  ( source -- adapter )
    _CDSRC-AUTH-TRANSPORT + ;
: CODEX-SOURCE-MODEL-TRANSPORT  ( source -- adapter )
    _CDSRC-MODEL-TRANSPORT + ;

VARIABLE _CDSRCC-S
VARIABLE _CDSRCC-STATUS

: _CODEX-SOURCE-CONFIGURE  ( source -- status )
    _CDSRCC-S !
    CODEX-AUTH-HOST 443
    _CDSRCC-S @ CODEX-SOURCE-AUTH-TRANSPORT KDOSTLS-CONFIGURE
    ?DUP IF EXIT THEN
    CODEX-BACKEND-HOST 443
    _CDSRCC-S @ CODEX-SOURCE-MODEL-TRANSPORT KDOSTLS-CONFIGURE ;

: _CODEX-SOURCE-CREATE  ( source -- provider status )
    DUP _CDSRCC-S ! _CODEX-SOURCE-CONFIGURE ?DUP IF
        DROP 0 APSOURCE-S-INVALID EXIT
    THEN
    _CDSRCC-S @ CODEX-SOURCE-CONFIG
    _CDSRCC-S @ CODEX-SOURCE-AUTH
    _CDSRCC-S @ CODEX-SOURCE-MODEL-TRANSPORT KDOSTLS.PORT
    CODEX-PROVIDER-NEW ;

: CODEX-SOURCE-FREE  ( source -- )
    DUP 0= IF DROP EXIT THEN
    DUP CODEX-SOURCE-AUTH-TRANSPORT KDOSTLS.PORT NIO-CLOSE
    DUP CODEX-SOURCE-MODEL-TRANSPORT KDOSTLS.PORT NIO-CLOSE
    DUP CODEX-SOURCE-AUTH CDA.AUTH AAUTH-DESTROY
    DUP CODEX-SOURCE-SIZE 0 FILL
    FREE ;

VARIABLE _CDSRCN-S

: CODEX-SOURCE-NEW  ( -- source status )
    CODEX-SOURCE-SIZE ALLOCATE
    DUP IF 2DROP 0 APSOURCE-S-NOMEM EXIT THEN
    DROP DUP _CDSRCN-S ! CODEX-SOURCE-SIZE 0 FILL
    _CDSRCN-S @ APSOURCE-INIT
    _CDSRCN-S @ CODEX-SOURCE-CONFIG CODEX-CONFIG-INIT
    ?DUP IF
        DROP _CDSRCN-S @ FREE 0 APSOURCE-S-INVALID EXIT
    THEN
    _CDSRCN-S @ CODEX-SOURCE-AUTH-TRANSPORT KDOSTLS-INIT
    _CDSRCN-S @ CODEX-SOURCE-MODEL-TRANSPORT KDOSTLS-INIT
    _CDSRCN-S @ _CODEX-SOURCE-CONFIGURE DUP _CDSRCC-STATUS ! IF
        _CDSRCN-S @ FREE 0 _CDSRCC-STATUS @ EXIT
    THEN
    _CDSRCN-S @ CODEX-SOURCE-AUTH-TRANSPORT KDOSTLS.PORT
    _CDSRCN-S @ CODEX-SOURCE-AUTH CODEX-DEVICE-AUTH-INIT
    AAUTH-S-OK <> IF
        _CDSRCN-S @ CODEX-SOURCE-FREE
        0 APSOURCE-S-INVALID EXIT
    THEN
    _CDSRCN-S @ _CDSRCN-S @ APSOURCE.CONTEXT !
    S" org.akashic.agent.source.codex"
    _CDSRCN-S @ APSOURCE.ID-U ! _CDSRCN-S @ APSOURCE.ID-A !
    ['] _CODEX-SOURCE-CREATE _CDSRCN-S @ APSOURCE.NEW-XT !
    ['] CODEX-SOURCE-FREE _CDSRCN-S @ APSOURCE.FREE-XT !
    _CDSRCN-S @ APSOURCE-S-OK ;
