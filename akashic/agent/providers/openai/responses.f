: _OAIR-SSE-EVENT-R  ( parser session -- status )
    _OAIRSE-S ! _OAIRSE-P !
    _OAIRSE-S @ OAIR-S.CONTEXT @ _OAIRSE-C !
    _OAIRSE-S @ OAIR-S.OPENAI-EVENT _OAIRSE-E !
    _OAIRSE-P @ SSE-DATA _OAIRSE-E @ OAIEV-PARSE ?DUP IF
        DROP S" Invalid Responses stream event" _OAIRSE-C @ _OAIR-MARK-ERROR
        DROP 1 EXIT
    THEN
    _OAIRSE-E @ OAIEV.KIND @ OAIEV-K-RESPONSE-CREATED = IF
        _OAIRSE-S @ _OAIR-ENSURE-RUN-STARTED IF 1 ELSE 0 THEN EXIT
    THEN
    _OAIRSE-E @ OAIEV.KIND @ OAIEV-K-TEXT-DELTA = IF
        _OAIRSE-S @ _OAIR-ENSURE-RUN-STARTED IF 1 EXIT THEN
        _OAIRSE-E @ OAIEV-DELTA _OAIRSE-U ! _OAIRSE-A !
        _OAIRSE-S @ OAIR-S.OUTPUT-U @ _OAIRSE-U @ +
        _OAIRSE-C @ OAIR-C.CONFIG OAIC.MAX-OUTPUT @ > IF
            S" Response text exceeded its bound" _OAIRSE-C @
            _OAIR-MARK-ERROR DROP 1 EXIT
        THEN
        _OAIRSE-U @ _OAIRSE-S @ OAIR-S.OUTPUT-U +!
        AEV-TEXT-DELTA 0 0 _OAIRSE-A @ _OAIRSE-U @
        _OAIRSE-C @ _OAIR-EMIT-STRING IF 1 ELSE 0 THEN EXIT
    THEN
    _OAIRSE-E @ OAIEV.KIND @ OAIEV-K-OUTPUT-DONE = IF
        _OAIRSE-E @ OAIEV-ITEM _OAIRSE-S @ _OAIR-HISTORY+ IF
            S" Response history exceeded its bound" _OAIRSE-C @
            _OAIR-MARK-ERROR DROP 1 EXIT
        THEN
        _OAIRSE-E @ OAIEV.CALL-U @ IF
            _OAIRSE-S @ OAIR-S.TOOL-PENDING @ IF
                S" Provider received parallel tool calls" _OAIRSE-C @
                _OAIR-MARK-ERROR DROP 1 EXIT
            THEN
            _OAIRSE-E @ OAIEV-NAME _OAIRSE-C @ OAIR-C.GATEWAY @
            OAI-GATEWAY-TOOL-FIND DUP 0= IF
                DROP S" Model requested an unavailable tool" _OAIRSE-C @
                _OAIR-MARK-ERROR DROP 1 EXIT
            THEN
            _OAIRSE-CAP !
            _OAIRSE-S @ OAIR-S.ARGS DUP CV-FREE CV-INIT
            _OAIRSE-E @ OAIEV-ARGUMENTS
            _OAIRSE-CAP @ CAP.IN-SCHEMA @ _OAIRSE-S @ OAIR-S.ARGS
            CAPJSON-DECODE-INPUT IF
                S" Model supplied invalid tool arguments" _OAIRSE-C @
                _OAIR-MARK-ERROR DROP 1 EXIT
            THEN
            _OAIRSE-E @ OAIEV-CALL-ID DUP
            _OAIRSE-S @ OAIR-S.CALL-U !
            _OAIRSE-S @ OAIR-S.CALL-ID SWAP CMOVE
            _OAIRSE-CAP @ _OAIRSE-S @ OAIR-S.CAP !
            -1 _OAIRSE-S @ OAIR-S.TOOL-PENDING !
            _OAIRSE-CAP @ _OAIRSE-S @ _OAIR-EMIT-TOOL IF 1 ELSE 0 THEN EXIT
        THEN
        0 EXIT
    THEN
    _OAIRSE-E @ OAIEV.KIND @ OAIEV-K-RESPONSE-COMPLETED = IF
        -1 _OAIRSE-S @ OAIR-S.RESPONSE-COMPLETE ! 0 EXIT
    THEN
    _OAIRSE-E @ OAIEV.KIND @ DUP OAIEV-K-RESPONSE-FAILED =
    OVER OAIEV-K-RESPONSE-INCOMPLETE = OR SWAP OAIEV-K-ERROR = OR IF
        _OAIRSE-E @ OAIEV-MESSAGE DUP IF
            _OAIRSE-C @ _OAIR-MARK-ERROR
        ELSE
            2DROP S" OpenAI response failed" _OAIRSE-C @ _OAIR-MARK-ERROR
        THEN
        DROP 1 EXIT
    THEN
    0 ;

