    ?DUP IF DROP OAIR-S-PROTOCOL EXIT THEN
    ['] _OAIR-AUTH-CB _OAIRP-S @
    _OAIRP-C @ OAIR-C.CONFIG OAIC.CREDENTIAL @ CRED-WITH
    ?DUP IF DROP OAIR-S-AUTH EXIT THEN
    _OAIRP-S @ OAIR-S.BODY _OAIRP-S @ OAIR-S.BODY-U @
    _OAIRP-S @ OAIR-S.REQUEST HREQ-BODY
    ?DUP IF DROP OAIR-S-CAPACITY EXIT THEN
    _OAIRP-CONTINUE @ IF
        _OAIRP-S @ _OAIR-BUILD-TOOL-HISTORY-ITEM ?DUP IF EXIT THEN
        0 _OAIRP-S @ OAIR-S.TOOL-PENDING !
        0 _OAIRP-S @ OAIR-S.RESULT-READY !
        0 _OAIRP-S @ OAIR-S.CAP !
        _OAIRP-S @ OAIR-S.ARGS DUP CV-FREE CV-INIT
    THEN
    _OAIRP-S @ OAIR-S.BODY OAIR-BODY-CAPACITY 0 FILL
    0 _OAIRP-S @ OAIR-S.BODY-U !
    _OAIRP-S @ _OAIR-RESET-RESPONSE
    OAIR-STATE-SENDING _OAIRP-C @ OAIR-C.STATE !
    OAIR-S-OK ;

VARIABLE _OAIRF-C
VARIABLE _OAIRF-S

: _OAIR-DROP-SESSION  ( context -- )
    DUP _OAIRF-C ! OAIR-C.SESSION @ _OAIRF-S !
    _OAIRF-C @ OAIR-C.PORT @ NIO-CLOSE
    _OAIRF-S @ _OAIR-SESSION-FREE
    0 _OAIRF-C @ OAIR-C.SESSION ! ;

: _OAIR-FINISH-RUN  ( session -- status )
    DUP _OAIRF-S ! OAIR-S.CONTEXT @ _OAIRF-C !
    _OAIRF-S @ _OAIR-ENSURE-RUN-STARTED ?DUP IF EXIT THEN
    AEV-MESSAGE-DONE 0 0 0 0 _OAIRF-C @ _OAIR-EMIT-STRING ?DUP IF EXIT THEN
    AEV-RUN-DONE 0 0 0 0 _OAIRF-C @ _OAIR-EMIT-STRING ?DUP IF EXIT THEN
    _OAIRF-C @ _OAIR-DROP-SESSION
    OAIR-STATE-IDLE _OAIRF-C @ OAIR-C.STATE !
    OAIR-S-OK ;

