        DUP OAIR-C.SESSION @ IF DUP _OAIR-DROP-SESSION THEN
        DUP OAIR-C.EVENT AEV-FREE
        DUP OPENAI-PROVIDER-CONTEXT-SIZE 0 FILL FREE
    THEN
    FREE ;

