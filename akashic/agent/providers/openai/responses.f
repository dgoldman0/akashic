VARIABLE _OAIRC-Q

: _OAIR-CONNECT  ( queue context -- status )
    _OAIRC-C ! _OAIRC-Q !
    _OAIRC-C @ OAIR-C.PORT @ 0= IF OAIR-S-TRANSPORT EXIT THEN
    _OAIRC-C @ OAIR-C.CONFIG OAIC.CREDENTIAL @ DUP 0= IF
        DROP OAIR-S-AUTH EXIT
    THEN
    CRED-PRESENT? 0= IF OAIR-S-AUTH EXIT THEN
    _OAIRC-Q @ _OAIRC-C @ OAIR-C.QUEUE !
    OAIR-STATE-IDLE _OAIRC-C @ OAIR-C.STATE !
    APROV-S-READY _OAIRC-C @ OAIR-C.PROVIDER @ APROV.STATE !
    AEV-CONNECTED 0 0 S" OpenAI Responses provider ready"
    _OAIRC-C @ _OAIR-EMIT-STRING ;

: _OAIR-DISCONNECT  ( queue context -- )
    _OAIRC-C ! _OAIRC-Q !
    _OAIRC-C @ OAIR-C.SESSION @ IF _OAIRC-C @ _OAIR-DROP-SESSION THEN
    OAIR-STATE-OFFLINE _OAIRC-C @ OAIR-C.STATE !
    APROV-S-OFFLINE _OAIRC-C @ OAIR-C.PROVIDER @ APROV.STATE !
    AEV-DISCONNECTED 0 0 S" Provider disconnected"
    _OAIRC-C @ _OAIR-EMIT-STRING DROP ;

