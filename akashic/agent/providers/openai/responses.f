: _OAIR-TOOL-ERROR-JSON  ( status session -- status )
    _OAIRTR-S ! _OAIRTR-STATUS !
    JSON-BUILD-RESET
    _OAIRTR-S @ OAIR-S.TOOL-OUTPUT OAIR-TOOL-OUTPUT-CAPACITY JSON-SET-OUTPUT
    JSON-{
    S" ok" 0 JSON-KV-BOOL
    S" status" _OAIRTR-STATUS @ JSON-KV-NUM
    JSON-}
    JSON-OUTPUT-OK? 0= IF OAIR-S-CAPACITY EXIT THEN
    JSON-OUTPUT-RESULT NIP _OAIRTR-S @ OAIR-S.TOOL-OUTPUT-U !
    OAIR-S-OK ;

: _OAIR-TOOL-RESULT  ( run-id name-a name-u value status queue context -- status )
    _OAIRTR-C ! _OAIRTR-Q ! _OAIRTR-STATUS ! _OAIRTR-V !
    _OAIRTR-NU ! _OAIRTR-NA ! _OAIRTR-RUN !
    _OAIRTR-C @ OAIR-C.SESSION @ DUP 0= IF DROP OAIR-S-INVALID EXIT THEN
    DUP _OAIRTR-S ! OAIR-S.RUN-ID @ _OAIRTR-RUN @ <> IF OAIR-S-INVALID EXIT THEN
    _OAIRTR-S @ OAIR-S.TOOL-PENDING @ 0= IF OAIR-S-INVALID EXIT THEN
    _OAIRTR-S @ OAIR-S.CAP @ CAP-ID
    _OAIRTR-NA @ _OAIRTR-NU @ STR-STR= 0= IF OAIR-S-INVALID EXIT THEN
    _OAIRTR-STATUS @ IF
        _OAIRTR-STATUS @ _OAIRTR-S @ _OAIR-TOOL-ERROR-JSON ?DUP IF EXIT THEN
    ELSE
        _OAIRTR-V @ _OAIRTR-S @ OAIR-S.TOOL-OUTPUT
        OAIR-TOOL-OUTPUT-CAPACITY CAPJSON-ENCODE-OUTPUT
        DUP IF 2DROP OAIR-S-CAPACITY EXIT THEN
        DROP _OAIRTR-S @ OAIR-S.TOOL-OUTPUT-U !
    THEN
    -1 _OAIRTR-S @ OAIR-S.RESULT-READY !
    AEV-TOOL-RESULT 0 0
    _OAIRTR-S @ OAIR-S.TOOL-OUTPUT _OAIRTR-S @ OAIR-S.TOOL-OUTPUT-U @
    _OAIRTR-C @ _OAIR-EMIT-STRING ;

