: _OAIR-SESSION-NEW  ( context queue run-id -- session status )
    >R >R >R
    OPENAI-SESSION-SIZE ALLOCATE
    DUP IF R> DROP R> DROP R> DROP 2DROP 0 OAIR-S-NOMEM EXIT THEN
    DROP DUP _OAIRN-S ! OPENAI-SESSION-SIZE 0 FILL
    R> _OAIRN-S @ OAIR-S.CONTEXT !
    R> _OAIRN-S @ OAIR-S.QUEUE !
    R> _OAIRN-S @ OAIR-S.RUN-ID !
    1 _OAIRN-S @ OAIR-S.SEQUENCE !
    _OAIRN-S @ OAIR-S.WIRE OAIR-WIRE-CAPACITY
    _OAIRN-S @ OAIR-S.REQUEST HREQ-INIT IF
        _OAIRN-S @ FREE 0 OAIR-S-NOMEM EXIT
    THEN
    _OAIRN-S @ OAIR-S.HTTP HSTR-INIT
    ['] _OAIR-HTTP-HEADERS _OAIRN-S @ OAIR-S.HTTP HSTR-ON-HEADERS!
    ['] _OAIR-HTTP-BODY _OAIRN-S @ OAIR-S.HTTP HSTR-ON-BODY!
    _OAIRN-S @ _OAIRN-S @ OAIR-S.HTTP HSTR-CONTEXT!
    _OAIRN-S @ OAIR-S.SSE SSE-INIT
    ['] _OAIR-SSE-EVENT _OAIRN-S @ _OAIRN-S @ OAIR-S.SSE SSE-ON-EVENT!
    _OAIRN-S @ OAIR-S.OPENAI-EVENT OAIEV-INIT
    _OAIRN-S @ OAIR-S.ARGS CV-INIT
    91 _OAIRN-S @ OAIR-S.HISTORY C!
    93 _OAIRN-S @ OAIR-S.HISTORY 1+ C!
    2 _OAIRN-S @ OAIR-S.HISTORY-U !
    _OAIRN-S @ OAIR-S-OK ;

