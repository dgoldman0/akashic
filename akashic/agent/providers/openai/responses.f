: _OAIR-START  ( prompt-a prompt-u run-id queue context -- status )
    _OAIRS-C ! _OAIRS-Q ! _OAIRS-RUN ! _OAIRS-U ! _OAIRS-A !
    _OAIRS-C @ OAIR-C.STATE @ OAIR-STATE-IDLE <> IF OAIR-S-BUSY EXIT THEN
    _OAIRS-U @ 0= _OAIRS-U @ OAIR-PROMPT-CAPACITY > OR IF
        OAIR-S-CAPACITY EXIT
    THEN
    _OAIRS-A @ _OAIRS-U @ UTF8-VALID? 0= IF OAIR-S-INVALID EXIT THEN
    _OAIRS-C @ _OAIRS-Q @ _OAIRS-RUN @ _OAIR-SESSION-NEW
    DUP IF NIP EXIT THEN DROP DUP _OAIRS-S !
    DROP _OAIRS-S @ _OAIRS-C @ OAIR-C.SESSION !
    _OAIRS-A @ _OAIRS-S @ OAIR-S.PROMPT _OAIRS-U @ CMOVE
    _OAIRS-U @ _OAIRS-S @ OAIR-S.PROMPT-U !
    0 _OAIRS-S @ _OAIR-PREPARE-REQUEST DUP IF
        _OAIRS-C @ _OAIR-DROP-SESSION
        OAIR-STATE-IDLE _OAIRS-C @ OAIR-C.STATE !
    THEN ;

