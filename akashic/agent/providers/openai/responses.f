        DUP OAI-GATEWAY-TOOLS-VALID? 0= IF DROP R> DROP OAIR-S-TOOL EXIT THEN
    THEN
    R> OAIR-C.GATEWAY ! OAIR-S-OK ;

VARIABLE _OAIRPOL-Q
VARIABLE _OAIRPOL-C
VARIABLE _OAIRPOL-S
VARIABLE _OAIRPOL-PUMP
VARIABLE _OAIRPOL-STATUS

: _OAIR-POLL-CONTINUE  ( -- status )
    -1 _OAIRPOL-S @ _OAIR-PREPARE-REQUEST DUP IF
        _OAIRPOL-STATUS !
        S" Failed to continue the tool response" _OAIRPOL-C @
        _OAIR-MARK-ERROR DROP
        _OAIRPOL-C @ _OAIR-DROP-SESSION
        OAIR-STATE-IDLE _OAIRPOL-C @ OAIR-C.STATE !
        _OAIRPOL-STATUS @
    THEN ;

: _OAIR-POLL-WAITING  ( -- status )
    _OAIRPOL-S @ OAIR-S.RESULT-READY @ IF
        _OAIR-POLL-CONTINUE
    ELSE
        OAIR-S-OK
    THEN ;

: _OAIR-POLL-SENDING  ( -- status )
    _OAIRPOL-C @ OAIR-C.PORT @ _OAIRPOL-S @ OAIR-S.REQUEST
    HREQ-SEND-STEP DUP HREQ-PUMP-DONE = IF
        DROP _OAIRPOL-S @ OAIR-S.REQUEST HREQ-CLEAR
        OAIR-STATE-RECEIVING _OAIRPOL-C @ OAIR-C.STATE !
        OAIR-S-OK EXIT
    THEN
    DUP HREQ-PUMP-IDLE = SWAP HREQ-PUMP-PROGRESS = OR IF
        OAIR-S-OK EXIT
    THEN
    S" Responses request send failed" _OAIRPOL-C @ _OAIR-MARK-ERROR DROP
    _OAIRPOL-C @ _OAIR-DROP-SESSION
    OAIR-STATE-IDLE _OAIRPOL-C @ OAIR-C.STATE !
    OAIR-S-TRANSPORT ;

: _OAIR-POLL-HTTP-ERROR  ( -- status )
    _OAIRPOL-S @ OAIR-S.ERROR-BODY
    _OAIRPOL-S @ OAIR-S.ERROR-BODY-U @ DUP IF
        2DUP UTF8-VALID? 0= IF 2DROP S" OpenAI HTTP request failed" THEN
    ELSE
        2DROP S" OpenAI HTTP request failed"
    THEN
    _OAIRPOL-C @ _OAIR-MARK-ERROR DROP
    _OAIRPOL-C @ _OAIR-DROP-SESSION
    OAIR-STATE-IDLE _OAIRPOL-C @ OAIR-C.STATE !
    OAIR-S-PROTOCOL ;

: _OAIR-POLL-RESPONSE-DONE  ( -- status )
    _OAIRPOL-S @ OAIR-S.SSE SSE-EOF DROP
    _OAIRPOL-C @ OAIR-C.PORT @ NIO-CLOSE
    _OAIRPOL-S @ OAIR-S.HTTP-CODE @ 200 <> IF
        _OAIR-POLL-HTTP-ERROR EXIT
    THEN
    _OAIRPOL-S @ OAIR-S.RESPONSE-COMPLETE @ 0= IF
        S" Responses stream ended before completion" _OAIRPOL-C @
        _OAIR-MARK-ERROR DROP
        _OAIRPOL-C @ _OAIR-DROP-SESSION
        OAIR-STATE-IDLE _OAIRPOL-C @ OAIR-C.STATE !
        OAIR-S-PROTOCOL EXIT
    THEN
    _OAIRPOL-S @ OAIR-S.TOOL-PENDING @ IF
        OAIR-STATE-WAITING-TOOL _OAIRPOL-C @ OAIR-C.STATE !
        _OAIRPOL-S @ OAIR-S.RESULT-READY @ IF
            _OAIR-POLL-CONTINUE
        ELSE
            OAIR-S-OK
        THEN
        EXIT
    THEN
    _OAIRPOL-S @ _OAIR-FINISH-RUN ;

: _OAIR-POLL-RECEIVING  ( -- status )
    _OAIRPOL-S @ DUP OAIR-S.HTTP
    _OAIRPOL-C @ OAIR-C.PORT @ ROT OAIR-S.RECV
    OAIR-RECV-CAPACITY HSTR-PUMP
    _OAIRPOL-PUMP !
    _OAIRPOL-C @ OAIR-C.STATE @ OAIR-STATE-ERROR = IF
        _OAIRPOL-C @ _OAIR-DROP-SESSION
        OAIR-STATE-IDLE _OAIRPOL-C @ OAIR-C.STATE !
        OAIR-S-PROTOCOL EXIT
    THEN
    _OAIRPOL-PUMP @ DUP HSTR-PUMP-IDLE =
    SWAP HSTR-PUMP-PROGRESS = OR IF OAIR-S-OK EXIT THEN
    _OAIRPOL-PUMP @ HSTR-PUMP-DONE <> IF
        S" Responses transport failed" _OAIRPOL-C @ _OAIR-MARK-ERROR DROP
        _OAIRPOL-C @ _OAIR-DROP-SESSION
        OAIR-STATE-IDLE _OAIRPOL-C @ OAIR-C.STATE !
        OAIR-S-TRANSPORT EXIT
    THEN
    _OAIR-POLL-RESPONSE-DONE ;

: _OAIR-POLL  ( queue context -- status )
    _OAIRPOL-C ! _OAIRPOL-Q !
    _OAIRPOL-C @ OAIR-C.SESSION @ DUP 0= IF DROP OAIR-S-OK EXIT THEN
    _OAIRPOL-S !
    _OAIRPOL-C @ OAIR-C.STATE @ OAIR-STATE-WAITING-TOOL = IF
        _OAIR-POLL-WAITING EXIT
    THEN
    _OAIRPOL-C @ OAIR-C.STATE @ OAIR-STATE-SENDING = IF
        _OAIR-POLL-SENDING EXIT
    THEN
    _OAIRPOL-C @ OAIR-C.STATE @ OAIR-STATE-RECEIVING = IF
        _OAIR-POLL-RECEIVING EXIT
    THEN
    OAIR-S-OK ;

