VARIABLE _OAIT-HASH
VARIABLE _OAIT-A
VARIABLE _OAIT-U
VARIABLE _OAIT-D
VARIABLE _OAIT-DMAX
VARIABLE _OAIT-BASE-U

: _OAI-HASH32  ( addr len -- hash )
    _OAIT-U ! _OAIT-A ! 2166136261 _OAIT-HASH !
    _OAIT-U @ 0 ?DO
        _OAIT-HASH @ _OAIT-A @ I + C@ XOR
        16777619 * 0xFFFFFFFF AND _OAIT-HASH !
    LOOP
    _OAIT-HASH @ ;

: _OAI-TOOL-CHAR?  ( c -- flag )
    DUP 48 >= OVER 57 <= AND IF DROP -1 EXIT THEN
    DUP 65 >= OVER 90 <= AND IF DROP -1 EXIT THEN
    DUP 97 >= OVER 122 <= AND IF DROP -1 EXIT THEN
    DUP 45 = SWAP 95 = OR ;

: _OAI-HEX  ( nibble -- c )
    15 AND DUP 10 < IF 48 + ELSE 87 + THEN ;

: OAI-TOOL-ID-NAME  ( id-a id-u destination capacity -- length status )
    _OAIT-DMAX ! _OAIT-D ! _OAIT-U ! _OAIT-A !
    _OAIT-D @ 0= IF 0 OAIREQ-S-INVALID EXIT THEN
    _OAIT-U @ 0= IF 0 OAIREQ-S-INVALID EXIT THEN
    _OAIT-U @ 55 MIN _OAIT-BASE-U !
    _OAIT-BASE-U @ 9 + _OAIT-DMAX @ > IF 0 OAIREQ-S-CAPACITY EXIT THEN
    _OAIT-BASE-U @ 0 ?DO
        _OAIT-A @ I + C@ DUP _OAI-TOOL-CHAR? 0= IF DROP 95 THEN
        _OAIT-D @ I + C!
    LOOP
    95 _OAIT-D @ _OAIT-BASE-U @ + C!
    _OAIT-A @ _OAIT-U @ _OAI-HASH32 _OAIT-HASH !
    8 0 DO
        _OAIT-HASH @ 28 I 4 * - RSHIFT _OAI-HEX
        _OAIT-D @ _OAIT-BASE-U @ 1+ I + + C!
    LOOP
    _OAIT-BASE-U @ 9 + OAIREQ-S-OK ;

VARIABLE _OAIT-NAME-CAP
VARIABLE _OAIT-NAME-D
VARIABLE _OAIT-NAME-DMAX

: OAI-TOOL-NAME  ( cap destination capacity -- length status )
    _OAIT-NAME-DMAX ! _OAIT-NAME-D ! _OAIT-NAME-CAP !
    _OAIT-NAME-CAP @ 0= IF 0 OAIREQ-S-INVALID EXIT THEN
    _OAIT-NAME-CAP @ CAP-ID _OAIT-NAME-D @ _OAIT-NAME-DMAX @
    OAI-TOOL-ID-NAME ;

CREATE _OAIT-FIND-NAME OAI-TOOL-NAME-CAPACITY ALLOT
CREATE _OAIT-CHECK-A OAI-TOOL-NAME-CAPACITY ALLOT
CREATE _OAIT-CHECK-B OAI-TOOL-NAME-CAPACITY ALLOT
VARIABLE _OAIT-FIND-A
VARIABLE _OAIT-FIND-U
VARIABLE _OAIT-FIND-G
VARIABLE _OAIT-FIND-CAP
VARIABLE _OAIT-FIND-N
VARIABLE _OAIT-CHECK-NA
VARIABLE _OAIT-CHECK-NB
VARIABLE _OAIT-CHECK-I
VARIABLE _OAIT-CHECK-J

: OAI-GATEWAY-TOOL-FIND  ( name-a name-u gateway -- cap | 0 )
    _OAIT-FIND-G ! _OAIT-FIND-U ! _OAIT-FIND-A !
    _OAIT-FIND-G @ 0= IF 0 EXIT THEN
    _OAIT-FIND-G @ ATOOLG-REFRESH IF 0 EXIT THEN
    _OAIT-FIND-G @ ATOOLG-TOOL-N 0 ?DO
        I _OAIT-FIND-G @ ATOOLG-TOOL-NTH DUP _OAIT-FIND-CAP !
        _OAIT-FIND-NAME OAI-TOOL-NAME-CAPACITY OAI-TOOL-NAME
        DUP IF 2DROP ELSE
            DROP DUP _OAIT-FIND-N !
            _OAIT-FIND-NAME SWAP _OAIT-FIND-A @ _OAIT-FIND-U @
            STR-STR= IF _OAIT-FIND-CAP @ UNLOOP EXIT THEN
        THEN
    LOOP
    0 ;

: OAI-GATEWAY-TOOLS-VALID?  ( gateway -- flag )
    DUP 0= IF DROP 0 EXIT THEN
    DUP ATOOLG-REFRESH IF DROP 0 EXIT THEN
    0 _OAIT-CHECK-I !
    BEGIN _OAIT-CHECK-I @ OVER ATOOLG-TOOL-N < WHILE
        _OAIT-CHECK-I @ OVER ATOOLG-TOOL-NTH
        _OAIT-CHECK-A OAI-TOOL-NAME-CAPACITY OAI-TOOL-NAME
        DUP IF 2DROP DROP 0 EXIT THEN
        DROP _OAIT-CHECK-NA !
        _OAIT-CHECK-I @ 1+ _OAIT-CHECK-J !
        BEGIN _OAIT-CHECK-J @ OVER ATOOLG-TOOL-N < WHILE
            _OAIT-CHECK-J @ OVER ATOOLG-TOOL-NTH
            _OAIT-CHECK-B OAI-TOOL-NAME-CAPACITY OAI-TOOL-NAME
            DUP IF 2DROP DROP 0 EXIT THEN
            DROP _OAIT-CHECK-NB !
            _OAIT-CHECK-A _OAIT-CHECK-NA @
            _OAIT-CHECK-B _OAIT-CHECK-NB @ STR-STR= IF
                DROP 0 EXIT
            THEN
            1 _OAIT-CHECK-J +!
        REPEAT
        1 _OAIT-CHECK-I +!
    REPEAT
    DROP -1 ;

VARIABLE _OAIB-CONFIG
VARIABLE _OAIB-GATEWAY
VARIABLE _OAIB-BUFFER
VARIABLE _OAIB-CAPACITY
VARIABLE _OAIB-ERROR
VARIABLE _OAIB-CAP
CREATE _OAIB-TOOL-NAME OAI-TOOL-NAME-CAPACITY ALLOT

: _OAIB-FAIL  ( status -- )
    _OAIB-ERROR @ 0= IF _OAIB-ERROR ! ELSE DROP THEN ;

: _OAIB-TOOL-WRITE  ( cap -- )
    _OAIB-CAP !
    _OAIB-CAP @ _OAIB-TOOL-NAME OAI-TOOL-NAME-CAPACITY OAI-TOOL-NAME
    DUP IF NIP _OAIB-FAIL EXIT THEN DROP
    JSON-{
    S" type" S" function" JSON-KV-ESTR
    S" name" JSON-KEY: _OAIB-TOOL-NAME SWAP JSON-ESTR
    _OAIB-CAP @ CAP.DESC-U @ IF
        S" description" JSON-KEY:
        _OAIB-CAP @ DUP CAP.DESC-A @ SWAP CAP.DESC-U @ JSON-ESTR
    ELSE
        _OAIB-CAP @ CAP.TITLE-U @ IF
            S" description" JSON-KEY:
            _OAIB-CAP @ DUP CAP.TITLE-A @ SWAP CAP.TITLE-U @ JSON-ESTR
        THEN
    THEN
    S" parameters" JSON-KEY:
    _OAIB-CAP @ CAP.IN-SCHEMA @ CSJSON-STRUCTURAL-INPUT-WRITE
    ?DUP IF DROP OAIREQ-S-SCHEMA _OAIB-FAIL THEN
    S" strict" _OAIB-CAP @ CAP.IN-SCHEMA @ CSJSON-STRICT? JSON-KV-BOOL
    JSON-} ;

: _OAIB-TOOLS?  ( -- flag )
    _OAIB-CONFIG @ OAIC.FLAGS @ OAIC-F-TOOLS AND 0<>
    _OAIB-GATEWAY @ 0<> AND
    _OAIB-GATEWAY @ ?DUP IF ATOOLG-TOOL-N 0> ELSE 0 THEN AND ;

: _OAIB-PREPARE-TOOLS  ( -- flag )
    _OAIB-TOOLS? 0= IF 0 EXIT THEN
    _OAIB-GATEWAY @ ATOOLG-REFRESH ?DUP IF
        OAIREQ-S-TOOLS _OAIB-FAIL 0 EXIT
    THEN
    -1 ;

: _OAIB-WRITE-TOOL-ARRAY  ( -- )
    _OAIB-GATEWAY @ ATOOLG-TOOL-N 0 ?DO
        I _OAIB-GATEWAY @ ATOOLG-TOOL-NTH _OAIB-TOOL-WRITE
        _OAIB-ERROR @ IF LEAVE THEN
    LOOP ;

: _OAIB-WRITE-TOOLS  ( -- )
    _OAIB-PREPARE-TOOLS 0= IF EXIT THEN
    S" parallel_tool_calls" 0 JSON-KV-BOOL
    S" tools" JSON-KEY: JSON-[
    _OAIB-WRITE-TOOL-ARRAY
    JSON-] ;

: _OAIB-WRITE-LITE-PREFIX  ( -- )
    _OAIB-CONFIG @ OAIC-RESPONSES-LITE? 0= IF EXIT THEN
    JSON-{
    S" type" S" additional_tools" JSON-KV-ESTR
    S" role" S" developer" JSON-KV-ESTR
    S" tools" JSON-KEY: JSON-[
    _OAIB-PREPARE-TOOLS IF _OAIB-WRITE-TOOL-ARRAY THEN
    JSON-] JSON-}
    _OAIB-CONFIG @ OAIC-INSTRUCTIONS DUP IF
        JSON-{
        S" type" S" message" JSON-KV-ESTR
        S" role" S" developer" JSON-KV-ESTR
        S" content" JSON-KEY: JSON-[ JSON-{
        S" type" S" input_text" JSON-KV-ESTR
        S" text" 2SWAP JSON-KV-ESTR
        JSON-} JSON-] JSON-}
    ELSE
        2DROP
    THEN ;

: _OAIB-WRITE-REASONING  ( -- )
    _OAIB-CONFIG @ OAIC-EFFORT NIP
    _OAIB-CONFIG @ OAIC-SUMMARY NIP OR
    _OAIB-CONFIG @ OAIC-RESPONSES-LITE? OR 0= IF EXIT THEN
    S" reasoning" JSON-KEY: JSON-{
    _OAIB-CONFIG @ OAIC-EFFORT DUP IF
        S" effort" 2SWAP JSON-KV-ESTR
    ELSE
        2DROP
    THEN
    _OAIB-CONFIG @ OAIC-SUMMARY DUP IF
        S" summary" 2SWAP JSON-KV-ESTR
    ELSE
        2DROP
    THEN
    _OAIB-CONFIG @ OAIC-RESPONSES-LITE? IF
        S" context" S" all_turns" JSON-KV-ESTR
    THEN
    JSON-} ;

: _OAIB-WRITE-OPTIONALS  ( -- )
    _OAIB-WRITE-REASONING
    _OAIB-CONFIG @ OAIC-TIER DUP IF
        S" service_tier" 2SWAP JSON-KV-ESTR
    ELSE
        2DROP
    THEN
    _OAIB-CONFIG @ OAIC-VERBOSITY DUP IF
        S" text" JSON-KEY: JSON-{
        S" verbosity" 2SWAP JSON-KV-ESTR JSON-}
    ELSE
        2DROP
    THEN ;

: _OAIB-WRITE-SETTINGS  ( -- )
    _OAIB-CONFIG @ OAIC-RESPONSES-LITE? 0= IF
        _OAIB-CONFIG @ OAIC-INSTRUCTIONS DUP IF
            S" instructions" 2SWAP JSON-KV-ESTR
        ELSE
            2DROP
        THEN
    THEN
    S" stream" -1 JSON-KV-BOOL
    S" store" _OAIB-CONFIG @ OAIC.FLAGS @ OAIC-F-STORE AND 0<>
    JSON-KV-BOOL
    _OAIB-CONFIG @ OAIC.FLAGS @ OAIC-F-STORE AND 0= IF
        S" include" JSON-KEY: JSON-[
        S" reasoning.encrypted_content" JSON-ESTR JSON-]
    THEN
    _OAIB-WRITE-OPTIONALS
    _OAIB-CONFIG @ OAIC-RESPONSES-LITE? IF
        S" parallel_tool_calls" 0 JSON-KV-BOOL
    ELSE
        _OAIB-WRITE-TOOLS
    THEN ;

: _OAIB-BEGIN  ( -- )
    0 _OAIB-ERROR !
    JSON-BUILD-RESET
    _OAIB-BUFFER @ _OAIB-CAPACITY @ JSON-SET-OUTPUT ;

: _OAIB-END  ( -- length status )
    _OAIB-ERROR @ ?DUP IF 0 SWAP EXIT THEN
    JSON-OUTPUT-OK? 0= IF 0 OAIREQ-S-CAPACITY EXIT THEN
    JSON-OUTPUT-RESULT NIP DUP _OAIB-CONFIG @ OAIC.MAX-REQUEST @ > IF
        DROP 0 OAIREQ-S-CAPACITY
    ELSE
        OAIREQ-S-OK
    THEN ;

: _OAIB-WRITE-ARRAY  ( json-a json-u -- )
    JSON-ENTER
    2DUP JSON-COUNT 0 ?DO
        2DUP I JSON-NTH JSON-VALUE-SPAN JSON-RAW
    LOOP
    2DROP ;

VARIABLE _OAITR-TURN
VARIABLE _OAITR-PA
VARIABLE _OAITR-PU
VARIABLE _OAITR-ITEM
VARIABLE _OAITR-WIRE-U
VARIABLE _OAITR-WIRE-STATUS
CREATE _OAITR-WIRE-NAME OAI-TOOL-NAME-CAPACITY ALLOT

: _OAITR-SOURCE?  ( item -- flag )
    ACTXI-SOURCE-TEXT _OAITR-PA @ _OAITR-PU @ STR-STR= ;

: _OAITR-ROLE  ( role -- addr len flag )
    CASE
        AROLE-USER OF S" user" -1 ENDOF
        AROLE-ASSISTANT OF S" assistant" -1 ENDOF
        AROLE-SYSTEM OF S" system" -1 ENDOF
        0 0 0 ROT DROP
    ENDCASE ;

: _OAITR-WRITE-MESSAGE  ( item -- )
    DUP ACTXI.ROLE @ _OAITR-ROLE 0= IF 2DROP DROP EXIT THEN
    >R >R
    JSON-{
    S" role" JSON-KEY: R> R> JSON-ESTR
    S" content" JSON-KEY: ACTXI-DATA-TEXT JSON-ESTR
    JSON-} ;

: _OAITR-WRITE-TOOL-CALL  ( item -- )
    DUP ACTXI-NAME-TEXT
    _OAITR-WIRE-NAME OAI-TOOL-NAME-CAPACITY OAI-TOOL-ID-NAME
    _OAITR-WIRE-STATUS ! _OAITR-WIRE-U !
    _OAITR-WIRE-STATUS @ IF
        DROP _OAITR-WIRE-STATUS @ _OAIB-FAIL EXIT
    THEN
    JSON-{
    S" type" S" function_call" JSON-KV-ESTR
    S" call_id" JSON-KEY: DUP ACTXI-CALL-ID-TEXT JSON-ESTR
    S" name" JSON-KEY: _OAITR-WIRE-NAME _OAITR-WIRE-U @ JSON-ESTR
    S" arguments" JSON-KEY: ACTXI-DATA-TEXT JSON-ESTR
    JSON-} ;

: _OAITR-WRITE-TOOL-RESULT  ( item -- )
    JSON-{
    S" type" S" function_call_output" JSON-KV-ESTR
    S" call_id" JSON-KEY: DUP ACTXI-CALL-ID-TEXT JSON-ESTR
    S" output" JSON-KEY: ACTXI-DATA-TEXT JSON-ESTR
    JSON-} ;

: _OAITR-WRITE-ITEM  ( item -- )
    DUP _OAITR-ITEM ! ACTXI.KIND @ CASE
        ACTX-K-MESSAGE OF _OAITR-ITEM @ _OAITR-WRITE-MESSAGE ENDOF
        ACTX-K-TOOL-CALL OF
            _OAITR-ITEM @ _OAITR-SOURCE? IF
                _OAITR-ITEM @ _OAITR-WRITE-TOOL-CALL
            THEN
        ENDOF
        ACTX-K-TOOL-RESULT OF
            _OAITR-ITEM @ _OAITR-SOURCE? IF
                _OAITR-ITEM @ _OAITR-WRITE-TOOL-RESULT
            THEN
        ENDOF
        ACTX-K-PROVIDER OF
            _OAITR-ITEM @ _OAITR-SOURCE? IF
                _OAITR-ITEM @ ACTXI-DATA-TEXT JSON-RAW
            THEN
        ENDOF
    ENDCASE ;

: OAI-TURN-INPUT-JSON  ( turn provider-a provider-u output-a output-u -- length status )
    _OAIB-CAPACITY ! _OAIB-BUFFER ! _OAITR-PU ! _OAITR-PA ! _OAITR-TURN !
    _OAITR-TURN @ ATURN-VALID? 0=
    _OAITR-PU @ 0= OR _OAIB-BUFFER @ 0= OR _OAIB-CAPACITY @ 0> 0= OR IF
        0 OAIREQ-S-INVALID EXIT
    THEN
    0 _OAIB-ERROR !
    JSON-BUILD-RESET _OAIB-BUFFER @ _OAIB-CAPACITY @ JSON-SET-OUTPUT
    JSON-[
    _OAITR-TURN @ ATURN.CONTEXT-N @ 0 ?DO
        I _OAITR-TURN @ ATURN.CONTEXT @ ACTX-NTH DUP ACTXI-VALID? IF
            _OAITR-WRITE-ITEM
        ELSE
            DROP OAIREQ-S-INVALID _OAIB-FAIL LEAVE
        THEN
    LOOP
    JSON-{
    S" role" S" user" JSON-KV-ESTR
    S" content" JSON-KEY: _OAITR-TURN @ ATURN-PROMPT JSON-ESTR
    JSON-}
    JSON-]
    _OAIB-ERROR @ ?DUP IF 0 SWAP EXIT THEN
    JSON-OUTPUT-OK? 0= IF 0 OAIREQ-S-CAPACITY EXIT THEN
    JSON-OUTPUT-RESULT NIP OAIREQ-S-OK ;

VARIABLE _OAIS-INPUT-A
VARIABLE _OAIS-INPUT-U

: OAI-RESPONSES-START-INPUT-JSON  ( input config gateway output -- length status )
    _OAIB-CAPACITY ! _OAIB-BUFFER ! _OAIB-GATEWAY ! _OAIB-CONFIG !
    _OAIS-INPUT-U ! _OAIS-INPUT-A !
    _OAIB-CONFIG @ 0= _OAIB-BUFFER @ 0= OR _OAIB-CAPACITY @ 0> 0= OR IF
        0 OAIREQ-S-INVALID EXIT
    THEN
    _OAIS-INPUT-U @ 0= IF 0 OAIREQ-S-INVALID EXIT THEN
    _OAIS-INPUT-A @ _OAIS-INPUT-U @ JSON-VALID? 0= IF
        0 OAIREQ-S-INVALID EXIT
    THEN
    _OAIS-INPUT-A @ _OAIS-INPUT-U @ JSON-ARRAY? 0= IF
        0 OAIREQ-S-INVALID EXIT
    THEN
    _OAIB-BEGIN JSON-{
    S" model" JSON-KEY: _OAIB-CONFIG @ OAIC-MODEL JSON-ESTR
    S" input" JSON-KEY: JSON-[
    _OAIB-WRITE-LITE-PREFIX
    _OAIS-INPUT-A @ _OAIS-INPUT-U @ _OAIB-WRITE-ARRAY
    JSON-]
    _OAIB-WRITE-SETTINGS
    JSON-} _OAIB-END ;

