VARIABLE _DFA-S

: _DFA-METADATA  ( source -- status )
    _DFA-S ! _DFA-S @ DFSRC.AUTH AAUTH-METADATA-CLEAR
    S" account-fixture" _DFA-S @ DFSRC.AUTH AAUTH.ACCOUNT-ID CV-STRING! IF
        AAUTH-S-CAPACITY EXIT
    THEN
    S" developer@example.com"
    _DFA-S @ DFSRC.AUTH AAUTH.ACCOUNT-LABEL CV-STRING! IF
        AAUTH-S-CAPACITY EXIT
    THEN
    S" pro" _DFA-S @ DFSRC.AUTH AAUTH.PLAN CV-STRING! IF
        AAUTH-S-CAPACITY EXIT
    THEN
    AAUTH-S-OK ;

: _DFA-BEGIN  ( source -- status )
    DUP _DFA-S ! DFSRC.AUTH AAUTH-PENDING? IF
        AAUTH-S-BUSY EXIT
    THEN
    _DFA-S @ DFSRC.AUTH AAUTH-METADATA-CLEAR
    S" AKASHIC-7H2K" _DFA-S @ DFSRC.AUTH AAUTH.USER-CODE CV-STRING! IF
        AAUTH-S-CAPACITY EXIT
    THEN
    S" https://auth.openai.com/codex/device"
    _DFA-S @ DFSRC.AUTH AAUTH.VERIFY-URI CV-STRING! IF
        AAUTH-S-CAPACITY EXIT
    THEN
    AAUTH-STATE-PENDING _DFA-S @ DFSRC.AUTH AAUTH.STATE !
    1 _DFA-S @ DFSRC.AUTH AAUTH.REVISION +!
    1500 MS@ + _DFA-S @ DFSRC.AUTH-READY-MS !
    AAUTH-S-PENDING ;

: _DFA-POLL  ( source -- status )
    DUP _DFA-S ! DFSRC.AUTH DUP AAUTH.STATE @
    AAUTH-STATE-PENDING <> IF DROP AAUTH-S-OK EXIT THEN
    MS@ _DFA-S @ DFSRC.AUTH-READY-MS @ < IF DROP AAUTH-S-PENDING EXIT THEN
    DROP _DFA-S @ _DFA-METADATA DUP IF EXIT THEN
    AAUTH-STATE-READY _DFA-S @ DFSRC.AUTH AAUTH.STATE !
    1 _DFA-S @ DFSRC.AUTH AAUTH.REVISION +! AAUTH-S-OK ;

: _DFA-CANCEL  ( source -- status )
    DFSRC.AUTH DUP AAUTH-METADATA-CLEAR
    AAUTH-STATE-SIGNED-OUT OVER AAUTH.STATE !
    1 SWAP AAUTH.REVISION +! AAUTH-S-CANCELLED ;

: _DFA-LOGOUT  ( source -- status )
    _DFA-CANCEL DROP AAUTH-S-OK ;

: _DFA-REFRESH  ( source -- status )
    DUP _DFA-S ! DFSRC.AUTH AAUTH-READY? 0= IF AAUTH-S-INVALID EXIT THEN
    _DFA-S @ _DFA-METADATA ;

VARIABLE _DFS-S
VARIABLE _DFS-I
VARIABLE _DFS-M
VARIABLE _DFS-C

: _DFS-MODEL-SELECT  ( index source -- status )
    _DFS-S ! _DFS-I !
    _DFS-I @ 0< _DFS-I @ 2 >= OR IF ARSET-S-INVALID EXIT THEN
    2 0 DO
        I _DFS-S @ _DFS-MODEL DUP ARMODEL.FLAGS @
        ARMODEL-F-SELECTED INVERT AND SWAP ARMODEL.FLAGS !
    LOOP
    _DFS-I @ _DFS-S @ _DFS-MODEL _DFS-M !
    ARMODEL-F-SELECTED _DFS-M @ ARMODEL.FLAGS +!
    _DFS-I @ _DFS-S @ DFSRC.SETTINGS ARSET.SELECTED !
    _DFS-M @ ARMODEL.EFFORTS-N @ 0 ?DO
        I _DFS-M @ ARMODEL-EFFORT-NTH DUP ARCH.FLAGS @
        ARCH-F-SELECTED INVERT AND SWAP ARCH.FLAGS !
    LOOP
    _DFS-M @ ARMODEL.EFFORTS-N @ 0 ?DO
        I _DFS-M @ ARMODEL-EFFORT-NTH DUP ARCH.FLAGS @
        ARCH-F-DEFAULT AND IF ARCH-F-SELECTED SWAP ARCH.FLAGS +! LEAVE THEN
        DROP
    LOOP
    _DFS-M @ ARMODEL.TIERS-N @ 0 ?DO
        I _DFS-M @ ARMODEL-TIER-NTH DUP ARCH.FLAGS @
        ARCH-F-SELECTED INVERT AND SWAP ARCH.FLAGS !
    LOOP
    ARVERB-AUTO _DFS-S @ DFSRC.SETTINGS ARSET.VERBOSITY !
    1 _DFS-S @ DFSRC.SETTINGS ARSET.REVISION +! ARSET-S-OK ;

: _DFS-EFFORT-SELECT  ( index source -- status )
    _DFS-S ! _DFS-I !
    _DFS-S @ DFSRC.SETTINGS ARSET-SELECTED-MODEL DUP 0= IF
        DROP ARSET-S-INVALID EXIT
    THEN
    DUP _DFS-M ! ARMODEL.EFFORTS-N @
    _DFS-I @ <= _DFS-I @ 0< OR IF ARSET-S-INVALID EXIT THEN
    _DFS-M @ ARMODEL.EFFORTS-N @ 0 ?DO
        I _DFS-M @ ARMODEL-EFFORT-NTH DUP ARCH.FLAGS @
        ARCH-F-SELECTED INVERT AND SWAP ARCH.FLAGS !
    LOOP
    _DFS-I @ _DFS-M @ ARMODEL-EFFORT-NTH
    ARCH-F-SELECTED SWAP ARCH.FLAGS +!
    1 _DFS-S @ DFSRC.SETTINGS ARSET.REVISION +! ARSET-S-OK ;

: _DFS-TIER-SELECT  ( index source -- status )
    _DFS-S ! _DFS-I !
    _DFS-S @ DFSRC.SETTINGS ARSET-SELECTED-MODEL DUP 0= IF
        DROP ARSET-S-INVALID EXIT
    THEN
    _DFS-M !
    _DFS-I @ -1 <
    _DFS-I @ _DFS-M @ ARMODEL.TIERS-N @ >= OR IF ARSET-S-INVALID EXIT THEN
    _DFS-M @ ARMODEL.TIERS-N @ 0 ?DO
        I _DFS-M @ ARMODEL-TIER-NTH DUP ARCH.FLAGS @
        ARCH-F-SELECTED INVERT AND SWAP ARCH.FLAGS !
    LOOP
    _DFS-I @ 0>= IF
        _DFS-I @ _DFS-M @ ARMODEL-TIER-NTH
        ARCH-F-SELECTED SWAP ARCH.FLAGS +!
    THEN
    1 _DFS-S @ DFSRC.SETTINGS ARSET.REVISION +! ARSET-S-OK ;

: _DFS-VERBOSITY  ( verbosity source -- status )
    _DFS-S ! _DFS-I !
    _DFS-I @ 0< _DFS-I @ ARVERB-HIGH > OR IF ARSET-S-INVALID EXIT THEN
    _DFS-I @ _DFS-S @ DFSRC.SETTINGS ARSET.VERBOSITY !
    1 _DFS-S @ DFSRC.SETTINGS ARSET.REVISION +! ARSET-S-OK ;

: _DFS-REFRESH  ( source -- status )
    DUP _DFS-S ! DFSRC.AUTH AAUTH-READY? 0= IF ARSET-S-AUTH EXIT THEN
    ARSET-STATE-LOADING _DFS-S @ DFSRC.SETTINGS ARSET.STATE !
    1 _DFS-S @ DFSRC.SETTINGS ARSET.REVISION +!
    800 MS@ + _DFS-S @ DFSRC.SETTINGS-READY-MS ! ARSET-S-PENDING ;

: _DFS-POLL  ( source -- status )
    DUP _DFS-S ! DFSRC.SETTINGS ARSET.STATE @
    ARSET-STATE-LOADING <> IF ARSET-S-INVALID EXIT THEN
    MS@ _DFS-S @ DFSRC.SETTINGS-READY-MS @ < IF ARSET-S-PENDING EXIT THEN
    ARSET-STATE-READY _DFS-S @ DFSRC.SETTINGS ARSET.STATE !
    0 _DFS-S @ _DFS-MODEL-SELECT DROP
    1 _DFS-S @ DFSRC.SETTINGS ARSET.REVISION +! ARSET-S-OK ;

: _DFS-CANCEL  ( source -- status )
    DFSRC.SETTINGS DUP ARSET-STATE-EMPTY SWAP ARSET.STATE !
    DUP -1 SWAP ARSET.SELECTED !
    1 SWAP ARSET.REVISION +! ARSET-S-CANCELLED ;

: _DFS-CHOICE  ( id-a id-u label-a label-u desc-a desc-u flags choice -- )
    >R R@ AGENT-RUN-CHOICE-SIZE 0 FILL
    R@ ARCH.FLAGS !
    R@ ARCH.DESC-U ! R@ ARCH.DESC-A !
    R@ ARCH.LABEL-U ! R@ ARCH.LABEL-A !
    R@ ARCH.ID-U ! R> ARCH.ID-A ! ;

: _DFS-MODEL-DESC  ( id-a id-u label-a label-u desc-a desc-u flags priority model -- )
    >R R@ AGENT-RUN-MODEL-SIZE 0 FILL
    R@ ARMODEL.PRIORITY ! R@ ARMODEL.FLAGS !
    R@ ARMODEL.DESC-U ! R@ ARMODEL.DESC-A !
    R@ ARMODEL.LABEL-U ! R@ ARMODEL.LABEL-A !
    R@ ARMODEL.ID-U ! R> ARMODEL.ID-A ! ;

