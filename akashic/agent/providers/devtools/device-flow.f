    _DFS-S @ DFSRC.TIER-PTRS
    1 _DFS-S @ _DFS-MODEL ARMODEL.TIERS-A !
    1 1 _DFS-S @ _DFS-MODEL ARMODEL.TIERS-N !
    0 _DFS-S @ _DFS-MODEL _DFS-S @ DFSRC.MODEL-PTRS !
    1 _DFS-S @ _DFS-MODEL _DFS-S @ DFSRC.MODEL-PTRS 8 + ! ;

VARIABLE _DFNEW-S
VARIABLE _DFNEW-P
VARIABLE _DFNEW-STATUS

: _DFS-CREATE  ( source -- provider status )
    _DFNEW-S ! SCRIPTED-PROVIDER-NEW
    _DFNEW-STATUS ! _DFNEW-P !
    _DFNEW-STATUS @ IF _DFNEW-P @ _DFNEW-STATUS @ EXIT THEN
    _DFNEW-S @ DFSRC.AUTH _DFNEW-P @ APROV.AUTH !
    _DFNEW-S @ DFSRC.SETTINGS _DFNEW-P @ APROV.RUN-SETTINGS !
    _DFNEW-P @ APROV.FEATURES DUP @ APROV-F-AUTH OR SWAP !
    S" org.akashic.agent.testing.device-flow"
    _DFNEW-P @ APROV.ID-U ! _DFNEW-P @ APROV.ID-A !
    _DFNEW-P @ 0 ;

: DEVFLOW-SOURCE-FREE  ( source -- )
    DUP DFSRC.SETTINGS ARSET-DESTROY
    DUP DFSRC.AUTH AAUTH-DESTROY
    DUP DEVFLOW-SOURCE-SIZE 0 FILL FREE ;

: DEVFLOW-SOURCE-NEW  ( -- source status )
    DEVFLOW-SOURCE-SIZE ALLOCATE
    DUP IF 2DROP 0 APSOURCE-S-NOMEM EXIT THEN
    DROP DUP _DFNEW-S ! DEVFLOW-SOURCE-SIZE 0 FILL
    _DFNEW-S @ APSOURCE-INIT
    _DFNEW-S @ DFSRC.AUTH AAUTH-INIT
    AAUTH-M-DEVICE _DFNEW-S @ DFSRC.AUTH AAUTH.METHODS !
    AAUTH-STATE-SIGNED-OUT _DFNEW-S @ DFSRC.AUTH AAUTH.STATE !
    _DFNEW-S @ _DFNEW-S @ DFSRC.AUTH AAUTH.CONTEXT !
    ['] _DFA-BEGIN _DFNEW-S @ DFSRC.AUTH AAUTH.BEGIN-XT !
    ['] _DFA-POLL _DFNEW-S @ DFSRC.AUTH AAUTH.POLL-XT !
    ['] _DFA-CANCEL _DFNEW-S @ DFSRC.AUTH AAUTH.CANCEL-XT !
    ['] _DFA-LOGOUT _DFNEW-S @ DFSRC.AUTH AAUTH.LOGOUT-XT !
    ['] _DFA-REFRESH _DFNEW-S @ DFSRC.AUTH AAUTH.REFRESH-XT !
    _DFNEW-S @ DFSRC.SETTINGS ARSET-INIT
    _DFNEW-S @ _DFNEW-S @ DFSRC.SETTINGS ARSET.CONTEXT !
    _DFNEW-S @ DFSRC.MODEL-PTRS
    _DFNEW-S @ DFSRC.SETTINGS ARSET.MODELS-A !
    2 _DFNEW-S @ DFSRC.SETTINGS ARSET.MODELS-N !
    ['] _DFS-REFRESH _DFNEW-S @ DFSRC.SETTINGS ARSET.REFRESH-XT !
    ['] _DFS-POLL _DFNEW-S @ DFSRC.SETTINGS ARSET.POLL-XT !
    ['] _DFS-CANCEL _DFNEW-S @ DFSRC.SETTINGS ARSET.CANCEL-XT !
    ['] _DFS-MODEL-SELECT _DFNEW-S @ DFSRC.SETTINGS ARSET.MODEL-XT !
    ['] _DFS-EFFORT-SELECT _DFNEW-S @ DFSRC.SETTINGS ARSET.EFFORT-XT !
    ['] _DFS-TIER-SELECT _DFNEW-S @ DFSRC.SETTINGS ARSET.TIER-XT !
    ['] _DFS-VERBOSITY _DFNEW-S @ DFSRC.SETTINGS ARSET.VERBOSITY-XT !
    _DFNEW-S @ _DFS-INIT-DESCRIPTORS
    _DFNEW-S @ _DFNEW-S @ APSOURCE.CONTEXT !
    S" org.akashic.agent.source.testing.device-flow"
    _DFNEW-S @ APSOURCE.ID-U ! _DFNEW-S @ APSOURCE.ID-A !
    ['] _DFS-CREATE _DFNEW-S @ APSOURCE.NEW-XT !
    ['] DEVFLOW-SOURCE-FREE _DFNEW-S @ APSOURCE.FREE-XT !
    _DFNEW-S @ APSOURCE-S-OK ;
