PROVIDED akashic-agent-event

REQUIRE ../interop/value.f

1  CONSTANT AEV-CONNECTED
2  CONSTANT AEV-DISCONNECTED
3  CONSTANT AEV-RUN-STARTED
4  CONSTANT AEV-TEXT-DELTA
5  CONSTANT AEV-MESSAGE-DONE
6  CONSTANT AEV-TOOL-CALL
7  CONSTANT AEV-TOOL-RESULT
8  CONSTANT AEV-APPROVAL
9  CONSTANT AEV-RUN-DONE
10 CONSTANT AEV-ERROR
11 CONSTANT AEV-CANCELLED
12 CONSTANT AEV-CONTEXT-ITEM

 0 CONSTANT _AEV-KIND
 8 CONSTANT _AEV-RUN-ID
16 CONSTANT _AEV-SEQUENCE
24 CONSTANT _AEV-FLAGS
32 CONSTANT _AEV-NAME
72 CONSTANT _AEV-DATA
112 CONSTANT _AEV-CALL-ID
152 CONSTANT AGENT-EVENT-SIZE

: AEV.KIND      ( event -- a ) _AEV-KIND + ;
: AEV.RUN-ID    ( event -- a ) _AEV-RUN-ID + ;
: AEV.SEQUENCE  ( event -- a ) _AEV-SEQUENCE + ;
: AEV.FLAGS     ( event -- a ) _AEV-FLAGS + ;
: AEV.NAME      ( event -- value ) _AEV-NAME + ;
: AEV.DATA      ( event -- value ) _AEV-DATA + ;
: AEV.CALL-ID   ( event -- value ) _AEV-CALL-ID + ;

: AEV-INIT  ( event -- ) AGENT-EVENT-SIZE 0 FILL ;

: AEV-FREE  ( event -- )
    DUP AEV.NAME CV-FREE
    DUP AEV.DATA CV-FREE
    DUP AEV.CALL-ID CV-FREE
    AEV-INIT ;

: AEV-MOVE  ( source destination -- )
    DUP AEV-FREE
    >R DUP R@ AGENT-EVENT-SIZE CMOVE
    AGENT-EVENT-SIZE 0 FILL
    R> DROP ;

32 CONSTANT AEQ-CAPACITY
 0 CONSTANT _AEQ-HEAD
 8 CONSTANT _AEQ-TAIL
16 CONSTANT _AEQ-COUNT
24 CONSTANT _AEQ-DROPPED
32 CONSTANT _AEQ-RING
_AEQ-RING AEQ-CAPACITY AGENT-EVENT-SIZE * + CONSTANT AEQ-SIZE

: AEQ.HEAD     ( queue -- a ) _AEQ-HEAD + ;
: AEQ.TAIL     ( queue -- a ) _AEQ-TAIL + ;
: AEQ.COUNT    ( queue -- a ) _AEQ-COUNT + ;
: AEQ.DROPPED  ( queue -- a ) _AEQ-DROPPED + ;
: AEQ.RING     ( queue -- a ) _AEQ-RING + ;

: AEQ-NEW  ( -- queue ior )
    AEQ-SIZE ALLOCATE
    DUP IF EXIT THEN
    DROP DUP AEQ-SIZE 0 FILL 0 ;

: AEQ-NTH  ( index queue -- event )
    AEQ.RING SWAP AGENT-EVENT-SIZE * + ;

VARIABLE _AEQF-Q

: AEQ-FREE  ( queue -- )
    DUP 0= IF DROP EXIT THEN
    DUP _AEQF-Q !
    AEQ-CAPACITY 0 ?DO I _AEQF-Q @ AEQ-NTH AEV-FREE LOOP
    FREE ;

VARIABLE _AEQP-EVENT
VARIABLE _AEQP-QUEUE

: AEQ-POST  ( event queue -- ior )
    _AEQP-QUEUE ! _AEQP-EVENT !
    _AEQP-QUEUE @ AEQ.COUNT @ AEQ-CAPACITY >= IF
        1 _AEQP-QUEUE @ AEQ.DROPPED +! 1 EXIT
    THEN
    _AEQP-EVENT @
    _AEQP-QUEUE @ AEQ.HEAD @ _AEQP-QUEUE @ AEQ-NTH AEV-MOVE
    _AEQP-QUEUE @ AEQ.HEAD @ 1+ AEQ-CAPACITY MOD
    _AEQP-QUEUE @ AEQ.HEAD !
    1 _AEQP-QUEUE @ AEQ.COUNT +!
    0 ;

VARIABLE _AEQO-DEST
VARIABLE _AEQO-QUEUE

: AEQ-POP  ( destination queue -- flag )
    _AEQO-QUEUE ! _AEQO-DEST !
    _AEQO-QUEUE @ AEQ.COUNT @ 0= IF 0 EXIT THEN
    _AEQO-QUEUE @ AEQ.TAIL @ _AEQO-QUEUE @ AEQ-NTH
    _AEQO-DEST @ AEV-MOVE
    _AEQO-QUEUE @ AEQ.TAIL @ 1+ AEQ-CAPACITY MOD
    _AEQO-QUEUE @ AEQ.TAIL !
    -1 _AEQO-QUEUE @ AEQ.COUNT +!
    -1 ;
